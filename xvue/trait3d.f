      SUBROUTINE TRAIT3D( NC, XYZ1, XYZ2 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE LE TRAIT 3D DE (X1,Y1,Z1) A (X2,Y2,Z2) AVEC LA COULEUR NC
C -----
C        LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C        LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREES:
C --------
C NC   : NUMERO DE LA COULEUR DU TRAIT A TRACER
C XYZ1 : 1-ERE EXTREMITE DU TRAIT
C XYZ2 : 2-EME EXTREMITE DU TRAIT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS        MAI 1994
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/minint.inc"
      REAL   XYZ1(3), XYZ2(3), AXYZ1(3), AXYZ2(3)
C
C     SI COULEUR NEGATIVE PAS DE TRACE
      IF( NC .LT. 0 ) RETURN
C
C     TRANSFORMATION EN COORDONNEES AXONOMETRIQUES
      CALL XYZAXO( XYZ1, AXYZ1 )
      CALL XYZAXO( XYZ2, AXYZ2 )
C
C     SI UN POINT EST EXTERIEUR AUX 2 PLANS LE TRAIT N'EST PAS TRACE
      IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C        AXOARR ET AXOAVA SONT ACTIFS
         CALL XYZAXO( AXOEIL, AXOOEIL )
C        DISTANCE A L'OEIL
         IF( AXOOEIL(3) - AXYZ1(3) .LT. AXOAVA ) RETURN
         IF( AXOOEIL(3) - AXYZ1(3) .GT. AXOARR ) RETURN
         IF( AXOOEIL(3) - AXYZ2(3) .LT. AXOAVA ) RETURN
         IF( AXOOEIL(3) - AXYZ2(3) .GT. AXOARR ) RETURN
      ENDIF
C
C     TRANSFORMATION EN PIXELS DANS LA FENETRE XV
      NX1 = NUPXEX( AXYZ1(1) )
      NY1 = NUPXEY( AXYZ1(2) )
      NX2 = NUPXEX( AXYZ2(1) )
      NY2 = NUPXEY( AXYZ2(2) )
      IF( NX1 .EQ. MININT .OR. NY1 .EQ. MININT .OR.
     %    NX2 .EQ. MININT .OR. NY2 .EQ. MININT ) RETURN
C
C     TRACE EFFECTIF
      CALL XVCOULEUR( NC )
      CALL XVTRAIT( NX1, NY1, NX2, NY2 )
C
      RETURN
      END
