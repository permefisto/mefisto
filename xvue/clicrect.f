      SUBROUTINE CLICRECT( NOTYEV, PT1, PT2 )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : 2 CLICS SOURIS SUR L'ECRAN AU MIN MAX DU RECTANGLE DE SELECTION
C -----
C
C SORTIES:
C --------
C NOTYEV : NO D'EVENEMENT
C          -1 ABANDON DES CLICS
C           1 2 ou 3 => NUMERO DU BOUTON DE LA SOURIS ET PT1 PT2 ACTIFS
C PT1: POINT INFERIEUR GAUCHE DU RECTANGLE EN COORDONNEES AXONOMETRIQUES
C PT2: POINT SUPERIEUR DROIT  DU RECTANGLE EN COORDONNEES AXONOMETRIQUES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Septembre 2010
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      include"./incl/gsmenu.inc"
C
C     LE NUMERO D'UNITE DU CLAVIER, FENETRE DES AFFICHAGES ET
C     LE PARAMETRE DE NIVEAU D'INTERACTIVITE,DE DOCUMENTATION
C     DU FICHIER FRAPPE OU LES DONNEES SONT SAUVEGARDEES
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NFDOCU,NFFRAP,NUNITE(27)
C
      REAL           R, PT1(3), PT2(3)
C
C     2 clics SOURIS au min MAX du RECTANGLE DE SELECTION
      IF( INTERA .LE. 1 ) GOTO 9000
C
C     UNE SOURIS EST DISPONIBLE
 10   IF( LANGAG .EQ. 0 ) THEN
         CALL INVITR( 'CLIQUER au POINT min du RECTANGLE' )
      ELSE
         CALL INVITR( 'CLICK at the min POINT of the RECTANGLE' )
      ENDIF
      CALL RECTEF( NRLGSA )
C
C     SAISIE D'UN POINT PAR CLIC DE LA SOURIS OU PAR CLAVIER PHYSIQUE
      CALL SAIPTC( NOTYEV, NX1, NY1, NOCHAR )
      IF( NOTYEV .EQ. -1 .AND. CHAR(NOCHAR) .EQ. '@' ) GOTO 9000
      IF( NOTYEV .EQ. 0 ) GOTO 9000
      IF( NOTYEV .LT. 0 ) GOTO 10
C     TRACE DU POINT DESIGNE
      CALL XVCOULEUR( NCJAUN )
      CALL XVTEXTE( 'X', 1, NX1, NY1 )
C
 20   IF( LANGAG .EQ. 0 ) THEN
         CALL INVITR( 'CLIQUER au POINT MAX du RECTANGLE' )
      ELSE
         CALL INVITR( 'CLICK at the MAX POINT of the RECTANGLE' )
      ENDIF
      CALL RECTEF( NRLGSA )
C
C     SAISIE D'UN POINT PAR CLIC DE LA SOURIS OU PAR CLAVIER PHYSIQUE
      CALL SAIPTC( NOTYEV, NX2, NY2, NOCHAR )
C     L'INVITE EST EFFACEE
      CALL RECTEF( NRINVI )
      IF( NOTYEV .EQ. -1 .AND. CHAR(NOCHAR) .EQ. '@' ) GOTO 9000
      IF( NOTYEV .EQ. 0 ) GOTO 9000
      IF( NOTYEV .LT. 0 ) GOTO 20
C     TRACE DU POINT DESIGNE
      CALL XVCOULEUR( NCJAUN )
      CALL XVTEXTE( 'X', 1, NX2, NY2 )
C
      IF( NX1 .EQ. NX2 .OR. NY1 .EQ. NY2 ) GOTO 10
C
C     TRACE DU BORD DU RECTANGLE DE SELECTION
      CALL XVCOULEUR( NCROUG )
      CALL XVBORDRECTANGLE( MIN(NX1,NX2), MIN(NY1,NY2),
     %                      ABS(NX2-NX1), ABS(NY2-NY1) )
C
C     LES COORDONNEES DES POINTS CLIQUES  LES EXTREMA EN COORDONNEES AXONO
      PT1(1) = XOB2PX( NX1 )
      PT2(1) = XOB2PX( NX2 )
      IF( PT1(1) .GT. PT2(1) ) THEN
         R      = PT2(1)
         PT2(1) = PT1(1)
         PT1(1) = R
      ENDIF
C
      PT1(2) = YOB2PX( NY1 )
      PT2(2) = YOB2PX( NY2 )
      IF( PT1(2) .GT. PT2(2) ) THEN
         R      = PT2(2)
         PT2(2) = PT1(2)
         PT1(2) = R
      ENDIF
      RETURN
C
C     ABANDON
 9000 NOTYEV = -1
      RETURN
      END
