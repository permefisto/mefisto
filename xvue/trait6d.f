      SUBROUTINE TRAIT6D( NC, XYZUVW1, XYZUVW2 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE LE TRAIT 6D DE (X1,Y1,Z1,U1,V1,W1) A (X2,Y2,Z2,U2,V2,W2)
C -----   AVEC LA COULEUR NC
C        LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C        LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREES:
C --------
C NC   : NUMERO DE LA COULEUR DU TRAIT A TRACER
C XYZUVW1 : 1-ERE EXTREMITE DU TRAIT
C XYZUVW2 : 2-EME EXTREMITE DU TRAIT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire JL LIONS UPMC Paris Decembre 2006
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/minint.inc"
      REAL   XYZUVW1(6), XYZUVW2(6), AXYZUVW1(6), AXYZUVW2(6)
C
C     SI COULEUR NEGATIVE PAS DE TRACE
      IF( NC .LT. 0 ) RETURN
C
C     TRANSFORMATION EN COORDONNEES AXONOMETRIQUES
      CALL XYZUVWAXO( XYZUVW1, AXYZUVW1 )
      CALL XYZUVWAXO( XYZUVW2, AXYZUVW2 )
C
C     SI UN POINT EST EXTERIEUR AUX 2 PLANS LE TRAIT N'EST PAS TRACE
      IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C        AXOARR ET AXOAVA SONT ACTIFS
         IF( AXYZUVW1(3) .LT. AXOARR ) RETURN
         IF( AXYZUVW1(3) .GT. AXOAVA ) RETURN
         IF( AXYZUVW2(3) .LT. AXOARR ) RETURN
         IF( AXYZUVW2(3) .GT. AXOAVA ) RETURN
      ENDIF
C
C     TRANSFORMATION EN PIXELS DANS LA FENETRE XV
      NX1 = NUPXEX( AXYZUVW1(1) )
      NY1 = NUPXEY( AXYZUVW1(2) )
      NX2 = NUPXEX( AXYZUVW2(1) )
      NY2 = NUPXEY( AXYZUVW2(2) )
      IF( NX1 .EQ. MININT .OR. NY1 .EQ. MININT .OR.
     %    NX2 .EQ. MININT .OR. NY2 .EQ. MININT ) RETURN
C
C     TRACE EFFECTIF
      CALL XVCOULEUR( NC )
      CALL XVTRAIT( NX1, NY1, NX2, NY2 )
      END
