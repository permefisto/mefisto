      SUBROUTINE FENETRE( XUTMIN, XUTMAX, YUTMIN, YUTMAX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  DEFINIR LA MISE A L'ECHELLE POUR VOIR LA FENETRE ACTUELLE
C -----  SELON LES COORDONNEES OBJET 2D DONNES ICI
C        SELON UN REPERE A PRIORI NON ORTHONORME
C        STOCKER LES COEFFICIENTS DE CONVERSION
C
C ENTREES :
C ---------
C XUTMIN : ABSCISSE OBJET DE LA GAUCHE DE LA FENETRE
C XUTMAX : ABSCISSE OBJET DE LA DROITE DE LA FENETRE
C YUTMIN : ORDONNEE OBJET DU    BAS    DE LA FENETRE
C YUTMAX : ORDONNEE OBJET DU    HAUT   DE LA FENETRE
C
C SORTIES :
C ---------
C TOUS LES COEFFICIENTS SONT STOCKES DANS LE COMMON  /TRVAR3 /
C DU FICHIER INCLUDE ~/incl/trvari.inc
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS        MAI 1994
C2345X7..............................................................012
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNIT(29)
C
      XDECAL = 0
      YDECAL = 0
      IF( XUTMIN .GE. XUTMAX ) THEN
         WRITE(IMPRIM,*) 'FENETRE INCORRECTE: X MIN >= X MAX =',XUTMIN
         XDECAL = XUTMAX / 10
         IF( XDECAL .EQ. 0 ) XDECAL = 1
      ENDIF
      IF( YUTMIN .GE. YUTMAX ) THEN
         WRITE(IMPRIM,*) 'FENETRE INCORRECTE: Y MIN >= Y MAX =',YUTMIN
         YDECAL = YUTMAX / 10
         IF( YDECAL .EQ. 0 ) YDECAL = 1
      ENDIF
C
C     FENETRE CORRECTE
      XOBMIN = XUTMIN - XDECAL
      XOBMAX = XUTMAX + XDECAL
      YOBMIN = YUTMIN - YDECAL
      YOBMAX = YUTMAX + YDECAL
C
C     CALCULS DES COEFFICIENTS DE LA TRANSFORMATION x objet => PIXELS X
C     XOB -> X PIXELS     NUPXEX(x) = AXOBPX * x + BXOBPX
      AXOBPX = LAPXFE / ( XOBMAX - XOBMIN )
      BXOBPX = - AXOBPX * XOBMIN
C
C     CALCULS DES COEFFICIENTS DE LA TRANSFORMATION y objet => PIXELS Y
C     YOB -> Y PIXELS     NUPXEY(y) = AYOBPX * y + BYOBPX
      AYOBPX = - LHPXFE / ( YOBMAX - YOBMIN )
      BYOBPX = - AYOBPX * YOBMAX
C
C     MISE A JOUR DE AXOLAR ET AXOHAU
      AXOLAR = ABS( XOBMAX - XOBMIN ) / 2
      AXOHAU = ABS( YOBMAX - YOBMIN ) / 2
C
C     MISE A JOUR DU POINT VU
      IF( NDIMLI .LE. 2 ) THEN
         AXOPTV(1) = ( XOBMIN + XOBMAX ) / 2
         AXOPTV(2) = ( YOBMIN + YOBMAX ) / 2
      ENDIF
C
C     RETOUR
      RETURN
      END
