      SUBROUTINE REEL3D( NC, XYZ, REEL, FRMT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE LES CARACTERES DE LA VALEUR DE REEL AVEC LA COULEUR NC
C -----  A PARTIR DU POINT XYZ EN COORDONNEES OBJET 3D
C        LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C        LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREES:
C --------
C NC     : NUMERO DE LA COULEUR DES CARACTERES DE LA VALEUR DE REEL
C XYZ    : ABSCISSE ORDONNEE COTE DES CARACTERES DE LA VALEUR DE REEL
C REEL   : LE REEL DONT LA VALEUR DOIT ETRE TRACEE
C FRMT   : FORMAT FORTRAN '(E15.6)' ou '(G10.2)' ou '(F5.1)' ...
C          POUR TRACER LES CARACTERES NON BLANCS DE LA VALEUR DU REEL
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS  SEPTEMBRE 1994
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/minint.inc"
      REAL           REEL, XYZ(3), AXYZ(3)
      CHARACTER*(*)  FRMT
      CHARACTER*23   KTXT
      CHARACTER*24   KREEL
C
C     SI COULEUR NEGATIVE PAS DE TRACE
      IF( NC .LT. 0 ) RETURN
C
C     TRANSFORMATION EN COORDONNEES AXONOMETRIQUES
      CALL XYZAXO( XYZ, AXYZ )
C
C     SI LE POINT EST EXTERIEUR AUX 2 PLANS LE TEXTE N'EST PAS TRACE
      IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C        AXOARR ET AXOAVA SONT ACTIFS
         IF( AXYZ(3) .LT. AXOARR ) RETURN
         IF( AXYZ(3) .GT. AXOAVA ) RETURN
      ENDIF
C
C     TRANSFORMATION EN PIXELS DANS LA FENETRE XV
      NX = NUPXEX( AXYZ(1) )
      NY = NUPXEY( AXYZ(2) )
      IF( NX .EQ. MININT .OR. NY .EQ. MININT ) RETURN
C
C     TRANSLATION POUR CENTRER LE .
      NX = NX - NPLACA/2
      NY = NY + 2
C
C     LE NOMBRE DE CARACTERES DE REEL
      KTXT = '    '
      WRITE( KTXT, FRMT ) REEL
      DO 10 I=1,23
         IF( KTXT(I:I) .NE. ' ' ) GOTO 20
 10   CONTINUE
C
 20   KREEL = '.' // KTXT(I:23)
      I     = 25 - I
C     LE TRACE XV DE LA CHAINE DES I CARACTERES
      CALL XVCOULEUR( NC )
      CALL XVTEXTE( KREEL(1:I), I, NX, NY )
      END
