      SUBROUTINE TRAR3D(NC, PREDAR, XYZ1, XYZ2, XYZTG1, XYZTG2 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE L'ARETE 3D DE XYZ1 A XYZ2 DE TANGENTES XYZTG1 ET XYZTG2
C -----  AVEC LA COULEUR NC ET UNE REDUCTION DE PREDAR POUR CENTS
C
C ATTENTION : L'ARETE EST PARAMETREE PAR UN POLYNOME P3
C             POUR CHACUNE DE SES 3 COMPOSANTES
C             XYZ EST EN COORDONNEES OBJET 3D
C             LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C             LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREES:
C --------
C NC     : NUMERO DE LA COULEUR DU TRAIT A TRACER
C PREDAR : POURCENTAGE DE REDUCTION DES ARETES DES LIGNES
C XYZ1   : 1-ERE EXTREMITE DE L'ARETE
C XYZ2   : 2-EME EXTREMITE DU L'ARETE
C XYZTG1 : LES 3 COMPOSANTES DE LA TANGENTE AU SOMMET 1 DE L'ARETE
C          AVEC LA DIRECTION S1 VERS S2
C XYZTG2 : LES 3 COMPOSANTES DE LA TANGENTE AU SOMMET 2 DE L'ARETE
C          AVEC LA DIRECTION S2 VERS S1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS        MAI 1996
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/minint.inc"
      include"./incl/mecoit.inc"
      REAL   XYZ1(3), XYZ2(3), XYZTG1(3), XYZTG2(3), AXYZ1(3), AXYZ2(3)
      REAL   XYZ(3)
C
C     SI COULEUR NEGATIVE PAS DE TRACE
      IF( NC .LT. 0 ) RETURN
C
C     TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DES 2 SOMMETS
      CALL XYZAXO( XYZ1, AXYZ1 )
      CALL XYZAXO( XYZ2, AXYZ2 )
C
C     SI UN POINT EST EXTERIEUR AUX 2 PLANS LE TRAIT N'EST PAS TRACE
      IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C        AXOARR ET AXOAVA SONT ACTIFS
         IF( AXYZ1(3) .LT. AXOARR ) RETURN
         IF( AXYZ1(3) .GT. AXOAVA ) RETURN
         IF( AXYZ2(3) .LT. AXOARR ) RETURN
         IF( AXYZ2(3) .GT. AXOAVA ) RETURN
      ENDIF
C
C     TRANSFORMATION EN PIXELS DANS LA FENETRE DE TRACE
      NX1 = NUPXEX( AXYZ1(1) )
      NY1 = NUPXEY( AXYZ1(2) )
      NX2 = NUPXEX( AXYZ2(1) )
      NY2 = NUPXEY( AXYZ2(2) )
      IF( NX1 .EQ. MININT .OR. NY1 .EQ. MININT .OR.
     %    NX2 .EQ. MININT .OR. NY2 .EQ. MININT ) RETURN
C
C     LE NOMBRE MAXIMAL DE PIXELS
      NBSUBA = MAX( ABS(NX2-NX1) , ABS(NY2-NY1) )
      IF( NBSUBA .EQ. 0 ) RETURN
C
C     LE NOMBRE DE SOUS-ARETES D'UNE ARETE P3
      NBSUBA = MAX( 2, NBSUBA/3, NBSUAR )
C
C     LA COULEUR DE L'ARETE
      CALL XVCOULEUR( NC )
      CALL XVTYPETRAIT( 0 )
      CALL XVEPAISSEUR( NEPARL )
C
C     TRACE DE NPX SOUS-ARETES SOUS LA FORME DE SEGMENTS DE DROITE
C     LE PARAMETRE U DE [0,1]
      H = 1.0 / NBSUBA
C
      IF( PREDAR .LE. 0 ) THEN
C
C        PAS DE REDUCTION DANS LE TRACE DE L'ARETE
C        LE PARAMETRE U DE [0,1]
         H = 1.0 / NBSUBA
         U = 0.0
C
      ELSE
C
C        TRACE AVEC REDUCTION DES SOUS-ARETES
         U = PREDAR * 0.005
C
C        LE POINT INITIAL EN COORDONNEES OBJET 3D
         XYZ(1) = VALP3H( U, XYZ1(1), XYZ2(1), XYZTG1(1), XYZTG2(1) )
         XYZ(2) = VALP3H( U, XYZ1(2), XYZ2(2), XYZTG1(2), XYZTG2(2) )
         XYZ(3) = VALP3H( U, XYZ1(3), XYZ2(3), XYZTG1(3), XYZTG2(3) )
C
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ, AXYZ1 )
C
C        LE POINT INITIAL EN COORDONNEES PIXELS
         NX1 = NUPXEX( AXYZ1(1) )
         NY1 = NUPXEY( AXYZ1(2) )
C
C        LE POINT FINAL EN COORDONNEES OBJET 3D
         XYZ(1) = VALP3H( 1.0-U, XYZ1(1), XYZ2(1), XYZTG1(1), XYZTG2(1))
         XYZ(2) = VALP3H( 1.0-U, XYZ1(2), XYZ2(2), XYZTG1(2), XYZTG2(2))
         XYZ(3) = VALP3H( 1.0-U, XYZ1(3), XYZ2(3), XYZTG1(3), XYZTG2(3))
C
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ, AXYZ2 )
C
C        LE POINT FINAL EN COORDONNEES PIXELS
         NX2 = NUPXEX( AXYZ2(1) )
         NY2 = NUPXEY( AXYZ2(2) )
C
C        LE PARAMETRE U DE [0,1]
         H = ( 1.0 - U - U ) / NBSUBA
      ENDIF
C
      DO 10 I=1,NBSUBA-1
C
C        LE POINT SUIVANT EN COORDONNEES OBJET 3D
         U = U + H
         XYZ(1) = VALP3H( U, XYZ1(1), XYZ2(1), XYZTG1(1), XYZTG2(1) )
         XYZ(2) = VALP3H( U, XYZ1(2), XYZ2(2), XYZTG1(2), XYZTG2(2) )
         XYZ(3) = VALP3H( U, XYZ1(3), XYZ2(3), XYZTG1(3), XYZTG2(3) )
C
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ, AXYZ1 )
C
C        LE POINT SUIVANT EN COORDONNEES PIXELS
         NX = NUPXEX( AXYZ1(1) )
         NY = NUPXEY( AXYZ1(2) )
C
C        LE TRACE DE LA SOUS-ARETE I
         CALL XVTRAIT( NX1, NY1,  NX, NY )
C
C        MISE A JOUR POUR LA SOUS-ARETE SUIVANTE
         NX1 = NX
         NY1 = NY
 10   CONTINUE
C
C     LA DERNIERE SOUS-ARETE DE L'ARETE P3
      CALL XVTRAIT( NX1, NY1,  NX2, NY2 )
C
C     TRACE EVENTUEL DES TANGENTES AUX SOMMETS EN BLANC
      IF( IAVTGA .NE. 0 ) THEN
C
C        LA COULEUR DE TRACE DES TANGENTES
         CALL XVCOULEUR( NCOTGA )
C        TRACE EN  CONTINU(0) ou POINTILLE (1) ou DOUBLE POINTILLE(2)
         CALL XVTYPETRAIT( NTRTGA )
         CALL XVEPAISSEUR( 0 )
C
C        LE SOMMET 1: LES COORDONNEES EN PIXELS SUR L'ECRAN
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ1, AXYZ1 )
C        LE SOMMET EN COORDONNEES PIXELS
         NX1 = NUPXEX( AXYZ1(1) )
         NY1 = NUPXEY( AXYZ1(2) )
C
C        LES COORDONNEES DE LA POINTE DE LA TG
         XYZ(1) = XYZ1(1) + XYZTG1(1)
         XYZ(2) = XYZ1(2) + XYZTG1(2)
         XYZ(3) = XYZ1(3) + XYZTG1(3)
C        LES COORDONNEES EN PIXELS SUR L'ECRAN DE LA POINTE DE LA TG
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ, AXYZ1 )
C        LES COORDONNEES EN PIXELS SUR L'ECRAN DE LA POINTE DE LA TG
         NX2 = NUPXEX( AXYZ1(1) )
         NY2 = NUPXEY( AXYZ1(2) )
C        LE TRACE DE LA TANGENTE AU SOMMET 1
         CALL XVTRAIT( NX1, NY1,  NX2, NY2 )
C        TRACE DU SYMBOLE DE FIN DE TANGENTE
C        TRANSLATION POUR CENTRER LE SYMBOLE EN FONCTION DE LA FONTE
CCC         CALL XVTEXTE( '*', 1, NX2-NPLACA/2+1, NY2+NPHACA/2 )
C
C        LE SOMMET 2: LES COORDONNEES EN PIXELS SUR L'ECRAN
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ2, AXYZ1 )
C        LE SOMMET EN COORDONNEES PIXELS
         NX1 = NUPXEX( AXYZ1(1) )
         NY1 = NUPXEY( AXYZ1(2) )
C
C        LES COORDONNEES DE LA POINTE DE LA TG DU SOMMET 2
         XYZ(1) = XYZ2(1) + XYZTG2(1)
         XYZ(2) = XYZ2(2) + XYZTG2(2)
         XYZ(3) = XYZ2(3) + XYZTG2(3)
C        LES COORDONNEES EN PIXELS SUR L'ECRAN DE LA POINTE DE LA TG
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DU POINT
         CALL XYZAXO( XYZ, AXYZ1 )
C        LES COORDONNEES EN PIXELS SUR L'ECRAN DE LA POINTE DE LA TG
         NX2 = NUPXEX( AXYZ1(1) )
         NY2 = NUPXEY( AXYZ1(2) )
C        LE TRACE DE LA TANGENTE AU SOMMET 2
         CALL XVTRAIT( NX1, NY1,  NX2, NY2 )
C        TRACE DU SYMBOLE DE FIN DE TANGENTE
C        TRANSLATION POUR CENTRER LE SYMBOLE EN FONCTION DE LA FONTE
CCC         CALL XVTEXTE( '*', 1, NX2-NPLACA/2+1, NY2+NPHACA/2 )
C
      ENDIF
C
C     AU DELA LES TRAITS SONT CONTINUS ET DE NEPARL EPAISSEURS
      CALL XVTYPETRAIT( 0 )
      CALL XVEPAISSEUR( 0 )
      RETURN
      END
