      SUBROUTINE FAP13D( NCF0, NCA, POREDF, NBS, X, Y, Z )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE LA FACE DEGRE 1 3D DE SOMMETS RANGEES DANS
C -----  X,Y,Z  (SOMMETS 1 A NBS) ET
C        SELON LES COULEURS NCF POUR LA FACE ET NCA POUR LES ARETES
C
C        ATTENTION (X,Y,Z) EN COORDONNEES OBJET 3D
C        LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C        LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREE :
C --------
C NCF0   : NUMERO DE LA COULEUR DE REMPLISSAGE DE LA FACE A TRACER
C          <0 PAS DE TRACE DE L'INTERIEUR DE LA FACE
C          SERAIT MODIFIE SI LCRITR=0 SANS LA PROTECTION
C NCA    : NUMERO DE LA COULEUR DU CONTOUR     DE LA FACE A TRACER
C          <0 PAS DE TRACE DES ARETES DU CONTOUR DE LA FACE
C POREDF : POURCENTAGE DE REDUCTION DE LA FACE ( 0<=POREDF<100 )
C NBS    : NOMBRE DE SOMMETS ( 3 ou 4 ) DE LA FACE A TRACER
C X      : ABSCISSE OBJET DES NBS SOMMETS
C Y      : ORDONNEE OBJET DES NBS SOMMETS
C Z      : COTE     OBJET DES NBS SOMMETS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS       JUIN 1996
C2345X7..............................................................012
      include"./incl/mecoit.inc"
      include"./incl/trvari.inc"
C
      REAL    X(NBS), Y(NBS), Z(NBS), XYZ(3,4), XYZP(3), CNORFA(3)
C
C     SI FACE et ARETE de COULEUR NEGATIVE => RETOUR
      IF( NCF0 .LT. 0 .AND. NCA .LT. 0 ) RETURN
C
C     VERIFICATION
      IF( NBS .LE. 2 .OR. NBS. GT. 4 ) RETURN

C     PROTECTION DE NCF0 CONTRE UNE MODIFICATION
      NCF = NCF0

C     LES COORDONNEES DES NBS SOMMETS
      DO J=1,NBS
         XYZ(1,J) = X(J)
         XYZ(2,J) = Y(J)
         XYZ(3,J) = Z(J)
      ENDDO
C
      IF( POREDF .GT. 0.0 ) THEN
C
C        REDUCTION DE LA FACE
         REDUCF = POREDF * 0.01
         REDUC1 = 1.0 - REDUCF
C
C        CALCUL DES COORDONNEES XYZP DU BARYCENTRE DE LA FACE
         CALL COBAPO( NBS, XYZ, XYZP )
C
C        L'HOMOTHETIE DE CENTRE LE BARYCENTRE DE LA FACE
         DO J=1,NBS
            XYZ(1,J) = XYZ(1,J) * REDUC1 + XYZP(1) * REDUCF
            XYZ(2,J) = XYZ(2,J) * REDUC1 + XYZP(2) * REDUCF
            XYZ(3,J) = XYZ(3,J) * REDUC1 + XYZP(3) * REDUCF
         ENDDO
C
      ENDIF
C
C     AU DELA LES TRAITS SONT CONTINUS ET DE NEPARF EPAISSEURS
      CALL XVTYPETRAIT( 0 )
      CALL XVEPAISSEUR( NEPARF )
C
      IF( LCRITR .EQ. 0 .AND. NCF .GE. 0 ) THEN
C        PAS DE PRISE EN COMPTE DE LA QUALITE DE LA FACE
C        ECLAIRAGE DE LA FACE SELON SON VECTEUR NORMAL
C        LES 3 COORDONNEES DE LA NORMALE A LA FACE PLANE
         CALL NORFA3( XYZ(1,1), XYZ(1,2), XYZ(1,NBS),
     %                CNORFA, IERR )
C        CALCUL DE LA COULEUR EN CE POINT A PARTIR
C        DE LA NORMALE ET DES ECLAIRAGES
         CALL ECLAIR( CNORFA, RCOUL )
         NCF = NINT( RCOUL )
      ENDIF
C
C     TRACE DE LA FACE P1 SANS TG
      CALL FACE3D( NCF, NCA, NBS, XYZ )
C
C     AU DELA LES TRAITS SONT CONTINUS ET FINS
      CALL XVEPAISSEUR( 0 )

      RETURN
      END
