      SUBROUTINE COUTIP ( INDCOU , TEINTE , PRCTEI , PRCINT , PRCPUR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C  BUT :  DEFINIR UNE COULEUR DANS LE SYSTEME DE MUNSELL ET L'AFFECTER
C  -----  A LA COULEUR INDCOU DE LA PALETTE DES COULEURS
C
C         ( COULEUR PAR SA TEINTE, SON INTENSITE, SA PURETE )
C
C ENTREES:
C --------
C INDCOU : L'INDICE DE CETTE COULEUR
C TEINTE : CHAINE DE CARACTERES, COMPOSEE DE UNE OU DEUX TEINTES
C PRCTEI : INDIQUE LA PLACE DE LA TEINTE A DEFINIR ENTRE LES
C          DEUX TEINTES PRECEDENTES (EN POUR-CENT) :
C          0%  POUR LA PREMIERE ... 100% POUR LA SECONDE
C PRCINT : L'INTENSITE DE LA COULEUR EN POUR-CENT (DE 0% A 100%)
C          0% COULEUR FONCEE NOIRE ... 100% COULEUR CLAIRE
C PRCPUR : LA PURETE DE LA COULEUR EN POUR-CENT (DE 0% A 100%)
C          0% COULEUR GRISE (LAVEE DE BLANC) ... 100% COULEUR PURE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : SVIGA-PERRONNET  ANALYSE NUMERIQUE UPMC  PARIS      MARS 1994
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/xvpalette.inc"
      CHARACTER*(*)  TEINTE
      LOGICAL        CODEE
C
      IF( INDCOU .LT. N1COUL .OR. INDCOU .GT. NDCOUL ) THEN
C        LA COULEUR DEMANDEE N'EXISTE PAS
         RETURN
      ENDIF
C
C     CODAGE DE LA COULEUR SELON LE SYSTEME DE MUNSELL
      CALL CODTEI( TEINTE, PRCTEI, DEGTEI, CODEE )
C
      IF( .NOT. CODEE .OR. PRCINT .LT. 0.0 .OR. PRCINT .GT. 100.0
     &    .OR. PRCPUR .LT. 0.0 .OR. PRCPUR .GT. 100.0 ) THEN
C        LA COULEUR DEMANDEE EST INCORRECTE
         RETURN
      ENDIF
C
C     PASSAGE TEINTE, PRCINTITE, PRCPUR  -->  ROUGE, VERT, BLEU
C     STOCKAGE DANS LA PALETTE EN POSITION INDCOU
      CALL TIPRVB( DEGTEI, PRCINT*0.01, PRCPUR*0.01,
     %             PROUGE(INDCOU), PVERT(INDCOU), PBLEU(INDCOU) )
      END
