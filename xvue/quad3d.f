      SUBROUTINE QUAD3D( NCF, NCA, XYZ )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACE LE QUADRANGLE 3D DE 4 SOMMETS XYZ AVEC
C -----    LA COULEUR NCF POUR LA FACE ET LA COULEUR NCA POUR LES ARETES
C          A PARTIR DU BARYCENTRE ET 6 TRIANGLES POUR RECOUVRIR
C          DANS TOUS LES CAS CROISES LE QUADRANGLE
C
C          ATTENTION (X,Y,Z) EN COORDONNEES OBJET 3D
C          LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C          LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREE :
C --------
C NCF    : NUMERO DE LA COULEUR DE REMPLISSAGE DU QUADRANGLE A TRACER
C NCA    : NUMERO DE LA COULEUR DU CONTOUR     DU QUADRANGLE A TRACER
C          <0 PAS DE TRACE DES ARETES DU CONTOUR DU QUADRANGLE
C XYZ    : ABSCISSE, ORDONNEE, COTE DES 4 SOMMETS DU QUADRANGLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS       JUIN 1999
C2345X7..............................................................012
      REAL  XYZ(3,4), XYZT(3,3)
C
C     SI COULEURS NEGATIVES => RETOUR
      IF( NCF .LT. 0 .AND. NCA .LT. 0 ) RETURN
C
C     LE QUADRANGLE EST DIVISE EN TRIANGLES 4 A PARTIR DU BARYCENTRE B
      DO 10 I=1,3
         XYZT(I,1) = (XYZ(I,1) + XYZ(I,2) + XYZ(I,3) + XYZ(I,4) ) * 0.25
 10   CONTINUE
C
      KM1 = 4
      DO 30 K=1,4
C
C        LES XYZ ET COULEURS DU TRIANGLE B K-1 K
         DO 20 I=1,3
            XYZT(I,2) = XYZ(I,KM1)
            XYZT(I,3) = XYZ(I,K  )
 20      CONTINUE
C
C        TRACE DU TRIANGLE BARYCENTRE-ARETE K
         CALL FACE3D( NCF, NCA, 3, XYZT )
C
C        PASSAGE AU TRIANGLE SUIVANT
         KM1 = K
 30   CONTINUE
C
C     IL FAUT TRACER LES TRIANGLES BARYCENTRE-ARETE24 ET BARYCENTRE-ARETE13
C     POUR ETRE CERTAIN DE RECOUVRIR LE QUADRANGLE CROISE QUELQUESOIT
C     L'ORDRE DES 4 SOMMETS
      DO 50 K=1,2
C
C        LES XYZ ET COULEURS DU TRIANGLE B K K+2
         DO 40 I=1,3
            XYZT(I,2) = XYZ(I,K)
            XYZT(I,3) = XYZ(I,K+2)
 40      CONTINUE
C
C        TRACE DU TRIANGLE
         CALL FACE3D( NCF, NCA, 3, XYZT )
C
 50   CONTINUE
C
      RETURN
      END
