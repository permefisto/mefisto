      SUBROUTINE TEXTE3D( NC, XYZ, TEXTE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE LES CARACTERES DU TEXTE SELON LA COULEUR NC
C -----  A PARTIR DU POINT (X,Y,Z) EN COORDONNEES OBJET 3D
C        LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C        LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREE :
C --------
C NC     : NUMERO DE LA COULEUR DS CARACTERES DU TEXTE
C XYZ    : ABSCISSE ORDONNEE COTE DU COIN INFERIEUR GAUCHE DU TEXTE
C TEXTE  : LES CARACTERES A TRACER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS        MAI 1994
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/minint.inc"
      CHARACTER*(*)  TEXTE
      REAL           XYZ(3), AXYZ(3)
C
C     SI COULEUR NEGATIVE PAS DE TRACE
      IF( NC .LT. 0 ) RETURN
C
C     LE NOMBRE DE CARACTERES DU TEXTE
C     A PARTIR DE LA FIN ARRET AU PREMIER CARACTERE NON BLANC RENCONTRE
      NBCAR = NUDCNB( TEXTE )
      IF( NBCAR .LE. 0 ) RETURN
C
C     TRANSFORMATION EN COORDONNEES AXONOMETRIQUES
      CALL XYZAXO( XYZ, AXYZ )
C
C     SI LE POINT EST EXTERIEUR AUX 2 PLANS LE TEXTE N'EST PAS TRACE
      IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C        AXOARR ET AXOAVA SONT ACTIFS
         IF( AXYZ(3) .LT. AXOARR ) RETURN
         IF( AXYZ(3) .GT. AXOAVA ) RETURN
      ENDIF
C
C     TRANSFORMATION EN PIXELS DANS LA FENETRE XV
      NX = NUPXEX( AXYZ(1) )
      NY = NUPXEY( AXYZ(2) )
      IF( NX .EQ. MININT .OR. NY .EQ. MININT ) RETURN
C
C     TRACE EFFECTIF
      CALL XVCOULEUR( NC )
      CALL XVTEXTE( TEXTE, NBCAR, NX, NY )
      END
