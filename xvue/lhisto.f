C     LA POSITION DU COIN SUPERIEUR GAUCHE DU RECTANGLE HISTORIQUE
      XRECT(NRHIST) = MIN( (LAPXFE-DXRECT(NRHIST))/2,
     %                      XRECT(NRLGSA)-DXRECT(NRHIST)) - ECARRC
      YRECT(NRHIST) = ECARRC
CCC
