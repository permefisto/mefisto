CCC      CALL CHARGEFONTE( NPRECT(NRHIST) )
C
C     LE NUMERO MAXIMAL DU DERNIER CARACTERE NON BLANC DE KHIST
      MDRECT(NRHIST) = MXCANB( NBLGRC(NRHIST), KHIST )
C
C     LA LARGEUR EN PIXELS
      DXRECT(NRHIST) = ECARLR(NRHIST) * 2
     %               + LAMXPXTXT( NBLGRC(NRHIST), KHIST )
