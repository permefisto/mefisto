      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      INTEGER           NAT(1)
C
      IF( INTERA .LT. 3 ) RETURN
C
C     PROTECTION DU NUMERO DE LA FONTE STANDARD
CCC      NUMERF = NOPOCA
C
