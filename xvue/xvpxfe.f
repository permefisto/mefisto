      SUBROUTINE XVPXFE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DEFINIR LE NOMBRE DE PIXELS EN LARGEUR HAUTEUR de la FENETRE
C ----- LE NOMBRE DE PIXELS DES CARACTERES DES MENUS ou
C       REVENIR AUX NOMBRES DE PIXELS INITIAUX
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  TIMS NTU TAIPEI TAIWAN         November 2009
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/xvfontes.inc"
      include"./incl/xvpalette.inc"
      include"./incl/traaxe.inc"
      include"./incl/xyzext.inc"
      COMMON /UNITES/ LECTEU,IMPRIM,INTERA,NUNIT(29)
C
 10   CALL LIMTCL( 'fenetre', NMTCL )
C
C     TRAITEMENT DE L'OPTION NMTCL
      IF( NMTCL .LT.  0 ) GOTO 9000
      GOTO( 100, 200, 300, 400 ), NMTCL+1
C
C     Valeurs INITIALES par DEFAUT
 100  CALL XVINIT
      GOTO 500
C
C     NOMBRE DE PIXELS de la LARGEUR de la FENETRE
 200  CALL INVITE( 143 )
      CALL LIRENT( NCVALS , NBPX )
      IF( NCVALS .LE.   0 ) GOTO 10
      IF( NBPX   .LE. 300 ) GOTO 10
      LAPXFE = NBPX
      GOTO 500
C
C     NOMBRE DE PIXELS de la HAUTEUR de la FENETRE
 300  CALL INVITE( 144 )
      CALL LIRENT( NCVALS , NBPX )
      IF( NCVALS .LE.   0 ) GOTO 10
      IF( NBPX   .LE. 200 ) GOTO 10
      LHPXFE = NBPX
      GOTO 500
C
C     NOMBRE DE PIXELS de la HAUTEUR des CARACTERES des MENUS
 400  CALL INVITE( 145 )
      CALL LIRENT( NCVALS , NBPX )
      IF( NCVALS .LE. 0 ) GOTO 10
      IF( NBPX   .LE. 6 .OR. NBPX .GT. 20 ) GOTO 10
      NPHFCO = NBPX
      CALL CHOIXFONTE( NPHFCO )
C     IMPRESSION DE LA FONTE CHARGEE NOFONT cf incl/xvfontes.inc
      WRITE(IMPRIM,10009) NOFONT, NPHFCO,
     %                    NMFONT(NOFONT)(1:NBCAFO(NOFONT))
10009 FORMAT('xvpxfe: FONTE=',I3,': HAUTEUR en PIXELS=',I3,' NOM=',A)
      GOTO 10
C
C
C     TRAITEMENT DU NOUVEAU NOMBRE DE  PIXELS
C     ---------------------------------------
C     FERMETURE DE X-WINDOW X11
 500  CALL EFFACE
      CALL XVFERMER
C     OUVERTURE DE X-WINDOW X11
      CALL XTINIT
      CALL XVINFO( LAPXFE,LHPXFE,MAXFONTS,
     %             N1CORE,NDCORE,N1COEL,NDCOEL,N1COUL,NDCOUL,NBCOLO,
     %             NMFONT,NBCAFO,NBFONT,NCVISU )
C
C     QUELQUES DIMENSIONS ET CARACTERISTIQUES DE LA FENETRE XVUE
C     NOMBRE DE PIXELS EN LARGEUR ET HAUTEUR DE LA FENETRE ACTUELLE
      CALL XVPXFENETRE(LAPXFE,LHPXFE)
C     DIMENSION EN MM DE LA FENETRE OUVERTE
      XMMMIN=0.
C     LARGEUR EN MM DE LA FENETRE OUVERTE
      XMMMAX=LAPXFE/CXMMPX
      YMMMIN=0.
C     HAUTEUR EN MM DE LA FENETRE OUVERTE
      YMMMAX=LHPXFE/CXMMPX
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000)'PIXELS de la FENETRE',LAPXFE,LHPXFE
         WRITE(IMPRIM,10000)'MM     de la FENETRE',NINT(XMMMAX)
     %                                            ,NINT(YMMMAX)
         WRITE(IMPRIM,*)'En X CONVERSION MM=>PIXELS =',CXMMPX
         WRITE(IMPRIM,*)'En Y CONVERSION MM=>PIXELS =',CYMMPX
      ELSE
         WRITE(IMPRIM,10001)'Window PIXELS',LAPXFE,LHPXFE
         WRITE(IMPRIM,10001)'Window MM    ',NINT(XMMMAX),NINT(YMMMAX)
         WRITE(IMPRIM,*)'X-CONVERSION MM=>PIXELS =',CXMMPX
         WRITE(IMPRIM,*)'Y-CONVERSION MM=>PIXELS =',CYMMPX
      ENDIF
C
C     REGENERATION DE LA PALETTE ACTUELLE DES COULEURS
      CALL PALCDE( NOPALC )
C
C     LA COULEUR ACTUELLE DU FOND EST IMPOSEE
C     THE BACKGROUND COLOR IS IMPOSED
      CALL XVFOND( NCOFON )
C
C     COPIE DE MEMPX DANS FENETRE
      CALL MEMPXFENETRE
C     POUR VIDER LE BUFFER DE X11
      CALL XVVOIR
      GOTO 10
C
 9000 RETURN
10000 FORMAT(1X,A,T22,' LARGEUR=',I4,'  HAUTEUR=',I4)
10001 FORMAT(1X,A,T20,'WIDTH=',I4,'  HEIGHT=',I4)
      END
