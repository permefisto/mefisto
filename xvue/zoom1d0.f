      SUBROUTINE ZOOM1D0( NOTYEV )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    INITIALISATION POUR UN TRACE 1D AVEC TRANSLATION ZOOM
C -----
C SORTIE :
C --------
C NOTYEV : = 0 SI ABANDON DEMANDE PAR CLIC DU BOUTON 2 DE LA SOURIS
C                         OU PAR FRAPPE DE LA TOUCHE ECHAPPEMENT OU @
C          = 1 SI CLIC ENFONCE ET RELACHE D'UN BOUTON DE LA SOURIS
C          =-1 SI CLIC SEULEMENT ENFONCE  D'UN BOUTON DE LA SOURIS
C          =-2 SI LE POINTEUR DE LA SOURIS A BOUGE SANS BOUTON ENFONCE
C          = 2 SI FRAPPE D'UN CARACTERE AU CLAVIER
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR:ALAIN PERRONNET LJLL UPMC PARIS & St Pierre du Perray JUIN 2009
C23456---------------------------------------------------------------012
      include"./incl/trvari.inc"
C
C     POUR EVITER PLUSIEURS SUCCESSIVES EXECUTIONS
      IF( NORBITE .NE. 0 ) RETURN
C
C     EN ATTENTE D'EVENEMENT
      CALL XVSOURIS( NOTYEV, NOBOUTON, NOPX0, NOPY0 )
C     NOTYEV := 0 SI ABANDON DEMANDE PAR CLIC DU BOUTON 2 DE LA SOURIS
C                         OU PAR FRAPPE DE LA TOUCHE ECHAPPEMENT OU @
C             = 1 SI CLIC ENFONCE ET RELACHE D'UN BOUTON DE LA SOURIS => NOPX0,
C             =-1 SI CLIC SEULEMENT ENFONCE  D'UN BOUTON DE LA SOURIS => NOPX0,
C             =-2 SI LE POINTEUR DE LA SOURIS A BOUGE                 => NOPX0,
C             = 2 SI FRAPPE D'UN CARACTERE AU CLAVIER
C
      IF( NOTYEV .EQ. 0 ) THEN
C
C        ABANDON DEMANDE
         NOBOUTON = 2
         RETURN
C
      ENDIF
C
C     FENETRE POUR VOIR L'OBJET
      CALL FENETRE( XOBMIN, XOBMAX, YOBMIN, YOBMAX )
C
      AXOPTV(1) = ( XOBMIN + XOBMAX ) / 2
      AXOPTV(2) = ( YOBMIN + YOBMAX ) / 2
      AXOPTV(3) = 0
C
      NOETATBOUTON = 0
C
C     LA MEMOIRE PIXELS EST EFFACEE
      CALL EFFACEMEMPX
C
C     TEMOIN DE PASSAGE DANS ZOOM2D0
      NORBITE = 1
C
      RETURN
      END
