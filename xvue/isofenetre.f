      SUBROUTINE ISOFENETRE( XOBJMI, XOBJMX, YOBJMI, YOBJMX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  DEFINIR LA MISE A L'ECHELLE POUR VOIR LA FENETRE ACTUELLE
C -----  SELON LES COORDONNEES OBJET DONNES ICI
C        SELON UN REPERE ORTHONORME => LE PLUS GRAND ECART EN X OU Y
C                                      EST AJUSTE A LA FENETRE
C        STOCKER LES COEFFICIENTS DE CONVERSION
C
C ENTREES :
C ---------
C XOBJMI : ABSCISSE OBJET DE LA GAUCHE DE LA FENETRE
C XOBJMX : ABSCISSE OBJET DE LA DROITE DE LA FENETRE
C YOBJMI : ORDONNEE OBJET DU    BAS    DE LA FENETRE
C YOBJMX : ORDONNEE OBJET DU    HAUT   DE LA FENETRE
C
C SORTIES :
C ---------
C TOUS LES COEFFICIENTS SONT STOCKES DANS LE COMMON  /TRVAR3 /
C DU FICHIER INCLUDE ~/incl/trvari.inc
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS       AOUT 1997
C2345X7..............................................................012
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNIT(29)
C
      IF( XOBJMI .GT. XOBJMX ) THEN
         S      = XOBJMI
         XOBJMI = XOBJMX
         XOBJMX = S
      ENDIF
      IF( XOBJMI .EQ. XOBJMX ) THEN
         WRITE(IMPRIM,*)'ISOFENETRE INCORRECTE: X MIN=',XOBJMI,
     %                  ' >= X MAX =',XOBJMX
         WRITE(IMPRIM,*)'ISOFENETRE INCORRECTE: Y MIN=',YOBJMI,
     %                  '    Y MAX =',YOBJMX
         GOTO 9999
      ENDIF
C
      IF( YOBJMI .GE. YOBJMX ) THEN
         S      = YOBJMI
         YOBJMI = YOBJMX
         YOBJMX = S
      ENDIF
      IF( YOBJMI .EQ. YOBJMX ) THEN
         WRITE(IMPRIM,*)'ISOFENETRE INCORRECTE: X MIN=',XOBJMI,
     %                  '    X MAX =',XOBJMX
         WRITE(IMPRIM,*)'ISOFENETRE INCORRECTE: Y MIN=',YOBJMI,
     %                  ' >= Y MAX =',YOBJMX
         GOTO 9999
      ENDIF
C
C     RAPPORT LARGEUR FENETRE / HAUTEUR FENETRE
      R = FLOAT(LAPXFE) / FLOAT(LHPXFE) * CYMMPX / CXMMPX
C
C     RECHERCHE DE LA PLUS GRANDE DES DIMENSIONS
      EX = XOBJMX - XOBJMI
      EY = YOBJMX - YOBJMI
      IF( EX .GE. EY*R ) THEN
C
C        XOB MIN ET MAX REALISENT LA GAUCHE ET LA DROITE DE LA FENETRE
         XUTMIN = XOBJMI
         XUTMAX = XOBJMX
         EY     = EX / R
         YUTMIN = ( YOBJMI + YOBJMX - EY ) * 0.5
         YUTMAX = YUTMIN + EY
C
      ELSE
C
C        YOB MIN ET MAX REALISENT LE BAS ET LE HAUT DE LA FENETRE
         YUTMIN = YOBJMI
         YUTMAX = YOBJMX
         EX     = EY * R
         XUTMIN = ( XOBJMI + XOBJMX - EX ) * 0.5
         XUTMAX = XUTMIN + EX
      ENDIF
C
C     CALCUL DES COEFFICIENTS DES TRANSFORMATIONS OBJET->PIXELS
      CALL FENETRE( XUTMIN, XUTMAX, YUTMIN, YUTMAX )
C
C     RETOUR
 9999 RETURN
      END
