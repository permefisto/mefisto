      SUBROUTINE DEDVSR( MNXYZS, MNXYZT, NOSOEL,
     %                   U, V, DIRECU, DIRECV,
     %                   DERIVE )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES 3 COORDONNEES DE LA DERIVEE DVSR(U,V)(DIRECU,DIRECV)
C -----    SUR LE QUADRANGLE DE SOMMETS ET TANGENTES DE NUMEROS DANS NOSOEL
C
C ENTREES:
C --------
C MNXYZS : ADRESSE MCN DE LA PREMIERE COORDONNEE DU PREMIER SOMMET
C MNXYZT : ADRESSE MCN DE LA PREMIERE COORDONNEE DE LA PREMIERE TG
C NOSOEL : NUMERO DES 4 SOMMETS ET DES 8 TANGENTES DE L'EF
C          QUADRANGLE: NO  SOMMET1, NS2 , NS3, NS4,
C                      NO TANGENTE1(S1S2), NT2(S1S3),   NT3(S2S3), NT4(S2S1),
C                      NO TANGENTE5(S3S4), NT6(S3S2),   NT7(S4S1), NT8(S4S3)
C          CE CHOIX PERMET UNE BOUCLE SUR LES TANGENTES PAR LES SOMMETS
C U,V    : VALEUR DES 2 PARAMETRES DANS [0,1] DE L'EF DE VEUBKE SANDER REDUIT
C DIRECU : ABSCISSE DE LA DIRECTION DE LA DERIVEE A CALCULER
C DIRECV : ORDONNEE DE LA DIRECTION DE LA DERIVEE A CALCULER
C
C SORTIES:
C --------
C DERIVE : LES 3 COMPOSANTES DE LA DERIVEE DVSR(U,V)(DIRECU,DIRECV)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1996
C2345X...............................................................012
      INTEGER        NOSOEL(1:12)
      REAL           DERIVE(3)
      REAL           X(12), Y(12), Z(12), DUDVSR(12), DVDVSR(12)
      include"./incl/pp.inc"
      COMMON         RMCN(MOTMCN)
C
C     CALCUL DES 3 COORDONNEES DES SOMMETS ET TANGENTES DE CET EF DVSR
      CALL XYZSTG( MNXYZS-3, MNXYZT-3, 4, NOSOEL, 1,
     %             X, Y, Z )
C
C     CALCUL DE LA DERIVEE / U AU POINT (U,V) DES FONCTIONS DE BASE
      CALL DDUDVS( U,V, DUDVSR )
C
C     CALCUL DE LA DERIVEE / V AU POINT (U,V) DES FONCTIONS DE BASE
      CALL DDVDVS( U,V, DVDVSR )
C
C     CALCUL DE LA DERIVEE DVSR(U,V)(DIRECU,DIRECV)
      XU = 0
      XV = 0
      YU = 0
      YV = 0
      ZU = 0
      ZV = 0
      DO 10 I=1,12
         XU = XU + DUDVSR(I) * X(I)
         XV = XV + DVDVSR(I) * X(I)
         YU = YU + DUDVSR(I) * Y(I)
         YV = YV + DVDVSR(I) * Y(I)
         ZU = ZU + DUDVSR(I) * Z(I)
         ZV = ZV + DVDVSR(I) * Z(I)
 10   CONTINUE
C
C     PRISE EN COMPTE DE LA DIRECTION
      DERIVE(1) = XU * DIRECU + XV * DIRECV
      DERIVE(2) = YU * DIRECU + YV * DIRECV
      DERIVE(3) = ZU * DIRECU + ZV * DIRECV
      END
