      SUBROUTINE LUOU
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :      RESTAURER DANS LE COMMON / KLANUT / ET / ILANUT /
C -----      LES CONSTANTES ET VARIABLES DU LANGAGE UTILISATEUR
C            A PARTIR DU TMS ADAM>LU
C            CF ~/util/GRAMMAIRE DE DEFINITION DU LANGAGE UTILISATEUR
C            CF ~/incl/lu.inc
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS       JUIN 1994
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      include"./incl/homdir.inc"
      include"./incl/xvfontes.inc"
      include"./incl/nbcamo.inc"
      include"./incl/trvari.inc"
      include"./incl/lu.inc"
      include"./incl/td.inc"
      include"./incl/typobj.inc"
      include"./incl/a_lu.inc"
      include"./incl/gsmenu.inc"
      include"./incl/mecoit.inc"
      include"./incl/nisafr.inc"
      include"./incl/darete.inc"
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      COMMON / MSSFTA /  MSSF(28), NTADAM
      COMMON / UNITES /  LECTEU, IMPRIM, INTERA, NUNITE(29)
      COMMON / LECLXQ /  LECLEX
      CHARACTER*(NBCAMO) CHARX
      INTRINSIC          INT

C     LA HAUTEUR DES LIGNES DANS KTD EST NULLE
      LHKTD = 0

C     ECRAN NOIR ET BLANC OU COULEUR ?   (stocke dans ~/incl/gsmenu.inc)
      IF( NDCOUL .LE. 1 ) THEN
         NOIBLA = 1
      ELSE
         NOIBLA = 0
      ENDIF

C     OUVERTURE DU TABLEAU LU
      CALL LXTSOU( NTADAM , 'LU' , NTLU , MNLU )
      IF( NTLU .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'luou: TABLEAU LU INEXISTANT'
         CALL LEREUR
         GOTO 9999
      ENDIF

C     LES VARIABLES ET TABLEAUX DES COMMONS SONT RESTAURES
      NBCTE  = MCN( MNLU + WNBCTE )
      NBVARU = MCN( MNLU + WBVARU )
      MXCAVA = MCN( MNLU + WXCAVA )
      NBVATS = MCN( MNLU + WBVATS )
      MXCATS = MCN( MNLU + WXCATS )
      NBPACH = MCN( MNLU + WBPACH )
      MXCACH = MCN( MNLU + WXCACH )

      IF( MXCAVA * NBCAMO .NE. NBCAVA ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'SP LUOU:REVOIR NBCAVA INCORRECT'
         CALL LEREUR
         GOTO 9999
      ENDIF

      IF( MXCATS * NBCAMO .NE. NBCATS ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'SP LUOU:REVOIR NBCATS INCORRECT'
         CALL LEREUR
         GOTO 9999
      ENDIF

      IF( MXCACH * NBCAMO .NE. NBCACH ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'SP LUOU:REVOIR NBCACH INCORRECT'
         CALL LEREUR
         GOTO 9999
      ENDIF

C     LES CONSTANTES
      MN   = MNLU + WDCTE
      MOTS = NBCTE*MOTVAR(6)
      CALL TRTATA( MCN(MN) , DCTE , MOTS )

C     LES VALEURS DES VARIABLES UTILISATEUR
      MN   = MN + MOTS
      MOTS = NBVARU * MOTVAR(6)
      CALL TRTATA( MCN(MN) , DVARU , MOTS )

C     LES MOTS DE NBCAMO CARACTERES DES NOMS DE VARIABLE UTILISATEUR
      MN = MN + MOTS
      DO 20 I = 1 , NBVARU
         I1 = 1
         I2 = NBCAMO
         DO 10 MOTS = 1 , MXCAVA
            KVARU(I)(I1:I2) = CHARX( MCN(MN) )
            MN = MN + 1
            I1 = I2 + 1
            I2 = I2 + NBCAMO
 10      CONTINUE
 20   CONTINUE

C     LES MOTS DE NBCAMO CARACTERES DES NOMS DE VARIABLE TMS
      LHTMS = 0
      DO 40 I = 1 , NBVATS
         I1 = 1
         I2 = NBCAMO
         DO 30 MOTS = 1 , MXCATS
            KVATS(I)(I1:I2) = CHARX( MCN(MN) )
            MN = MN + 1
            I1 = I2 + 1
            I2 = I2 + NBCAMO
 30      CONTINUE
 40   CONTINUE

C     LES MOTS DE NBCAMO CARACTERES DES NOMS DE PARAMETRES CHAINES
      DO 60 I = 1 , NBPACH
         I1 = 1
         I2 = NBCAMO
         DO 50 MOPC = 1 , MXCACH
            KPACH(I)(I1:I2) = CHARX( MCN(MN) )
            MN = MN + 1
            I1 = I2 + 1
            I2 = I2 + NBCAMO
 50      CONTINUE
 60   CONTINUE

C     LES MEMOIRES DE LU
      LHKLG  = 0
      KLG(1) = ' '
      NLPTV1 = 0
      NCPTV1 = 0
      NLPTV2 = 0
      NCPTV2 = 0
      NBETIQ = 0
      LUIMPR = 1

C     RECUPERATION de la LONGUEUR MAX des ARETES des MAILLAGES par DEFAUT
C     de la SAUVEGARDE EFFECTUEE en FIN d'EXECUTION PAR CALL LUFE
C     ZERO INDIQUE UNE NON INITIALISATION DE CETTE VALEUR
      IF( NBCTE .GT. 0 .AND. DCTE( NBCTE ) .GT. 0D0 ) THEN
         DARETE = DCTE( NBCTE )
         NBCTE = NBCTE - 1
      ELSE
         DARETE = 0D0
      ENDIF
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10095) DARETE
      ELSE
         WRITE(IMPRIM,20095) DARETE
      ENDIF
10095 FORMAT(G14.6,'=LONGUEUR SOUHAITEE des ARETES des MAILLAGES' )
20095 FORMAT(G14.6,'=WISHED LENGTH of MESH EDGES' )


C     RECUPERATION de NOFOTI le NUMERO de la FONCTION
C     'TAILLE_IDEALE(x,y,z)' ou 'EDGE_LENGTH(x,y,z)'
C     dans le TMS des FONCTIONS CREEES par l'UTILISATEUR
C     et ZERO SI AUCUNE DE CES 2 FONCTIONS N'EST TROUVEE
      NOFOTI = NOFOTIEL()

C     LE NOM DU REPERTOIRE DE MEFISTO EST TRANSMIS AU C
      CALL NOMREPMEFISTO( HOMDIR, LEN(HOMDIR) )

C     LECTURE DES INVITES DU FICHIER $MEFISTO/td/i
      CALL LINVTX

C     TEMP2TRAC: TEMPS UTILISATEUR EN SECONDES ENTRE 2 TRACES
      TEMP2TRAC = 0.D0

C     PAS DE SORTIE DU TRACE DU MAILLAGE EN POSTSCRIPT
      LASOPS = 0

C     LA VISEE N'EST PAS DEFINIE
      NOTYVI = 0

C     INITIALISATION DES RECTANGLES DE DOC , HISTORIQUE , MENU
C                                      LIGNES_LUES , INVITE ,
C                                      LIGNE DE SAISIE

CCCC     LARGEUR ET HAUTEUR EN PIXELS DE L'ECRAN
CCC      CALL XVPXECRAN( LAPXEC, LHPXEC )

C     LE NOMBRE DE PIXELS EN HAUTEUR DE LA FONTE DE CARACTERES DE L'HISTORIQUE
      IF( LHPXFE .LE. 600 ) THEN
C        ECRAN 800x600
         NPHFHI = 10
      ELSE IF( LHPXFE .LE. 900 ) THEN
C        ECRAN 1024x768 ou MAC G4 1280x854
         NPHFHI = 12
      ELSE
C        ECRAN 1280 x 1024 OU PLUS
         NPHFHI = 18
      ENDIF

C     LE CHOIX DES POLICES DE CARACTERES:
C     LA POLICE DE CARACTERES DE L'HISTORIQUE
      CALL CHOIXFONTE( NPHFHI )
C     LE NOMBRE ACTUEL (REEL) DE PIXELS EN HAUTEUR ET LARGEUR DE L'HISTORIQUE
      NPHFHI = NPHACA
      NPLFHI = NPLACA
C     LE NUMERO DE LA POLICE DE CARACTERES DE L'HISTORIQUE
      NOPOHI = NOPOCA

C     CHOIX DE LA FONTE COURANTE TEL QUE LE MENU TRACMAIL TIENNE DANS LA FENETRE
C     IDEM DANS $MEFISTO/xvue/xvinit.f
C     INITIALISATION DE LA VARIABLE NPHFCO EN FONCTION DE LHPXFE
      CALL LAHAFO
      CALL CHOIXFONTE( NPHFCO )
C     NPHACA : HAUTEUR EN PIXELS DES CARACTERES DE CETTE FONTE
C     NPLACA : LARGEUR EN PIXELS DES CARACTERES DE CETTE FONTE
C     RALALH : RAPPORT EN MM ECRAN ENTRE LARGEUR / HAUTEUR D'UN CARACTERE
C     SONT MIS A JOUR DANS LE COMMON / TRVAR3 / DE ~/incl/trvari.inc

C     ECARRC L'ECART EN PIXELS ENTRE 2 RECTANGLES PROCHES
C     LCARLR L'ECARTEMENT EN PIXELS DES CARACTERES PAR RAPPORT AUX BORDS
      IF( LHPXFE .LE. 600 ) THEN
C        ECRAN 800x600
         ECARRC = 4
         LCARLR = 3
      ELSE IF( LHPXFE .LE. 800 ) THEN
C        ECRAN 1024x768 ou MAC G4 1280x854
         ECARRC = 5
         LCARLR = 3
      ELSE
C        ECRAN 1280x1024 OU PLUS
         ECARRC = 8
         LCARLR = 4
      ENDIF

      DO 80 I=1,MXRECT
C        LA LARGEUR ET HAUTEUR DES CARACTERES
         DXKARC(I) = NPLACA
         DYKARC(I) = NPHACA
C        L'ECARTEMENT EN PIXELS DES CARACTERES PAR RAPPORT AUX BORDS
         ECARLR(I) = LCARLR
C        LA HAUTEUR D'UNE LIGNE
         DYLGRC(I) = 2 * ECARLR(I) + DYKARC(I)

C        LA COULEUR DU FOND ET DU FOND DES CARACTERES
         NFRECT(I) = NCNOIR
C        LA COULEUR DE LA LIGNE DU BORD
         NBRECT(I) = NCBLAN
C        LA COULEUR DES CARACTERES
         NKRECT(I) = NCBLAN
C        LE NUMERO DE LA POLICE DES CARACTERES
         NPRECT(I) = NOPOCA
C        LE NOMBRE ACTUEL DE LIGNES DU RECTANGLE
         NBLGRC(I) = 0
C        LE NUMERO DU DERNIER CARACTERE NON BLANC DE LA DERNIERE LIGNE
         MDLGRC(I) = 0
C        LE NUMERO DU DERNIER CARACTERE NON BLANC DES LIGNES DU RECTANGLE
         MDRECT(I) = 0
C        LARGEUR DU RECTANGLE
         DXRECT(I) = 0
C        HAUTEUR DU RECTANGLE
         DYRECT(I) = 0
 80   CONTINUE

C     LE NUMERO DE LA POLICE DES CARACTERES
      NPRECT(NRHIST) = NOPOHI
C     LA LARGEUR EN PIXELS DE LA FONTE DE L'HISTORIQUE
      DXKARC(NRHIST) = NPLFHI
C     LA HAUTEUR EN PIXELS DE LA FONTE DE L'HISTORIQUE
      DYLGRC(NRHIST) = 2 * ECARLR(NRHIST) + NPHFHI
      DYKARC(NRHIST) = NPHFHI
C     LE NUMERO MAXIMAL DU DERNIER CARACTERE NON BLANC DE KHIST
      MDRECT(NRHIST) = 0

C     LE NOMBRE MAXIMAL DE LIGNES DE CHAQUE RECTANGLE
      MXLGRC( NRDOCU ) = MXLGDO
      MXLGRC( NRHIST ) = MXLGHI
      MXLGRC( NRMENU ) = MXLGME
      MXLGRC( NRLGLU ) = MXKLG
      MXLGRC( NRINVI ) = MXLGIN
      MXLGRC( NRLGSA ) = MXLGSA
      MXLGRC( NRERR  ) = MXLGER
      KINVI(1) = ' '
      KLGSA(1) = ' '
      KERR (1) = ' '
      LECLEX   = 0

C     LA DOCUMENTATION
C     ================
C     LE COIN SUPERIEUR GAUCHE
      XRECT ( NRDOCU ) = ECARRC
      YRECT ( NRDOCU ) = ECARRC
C     NBRECT : NUMERO DE LA COULEUR DU BORD DU RECTANGLE
      NBRECT( NRDOCU ) = NCGRIC
C     NKRECT : NUMERO DE LA COULEUR DES CARACTERES DU RECTANGLE
      NKRECT( NRDOCU ) = NCNOIR
C     NFRECT : NUMERO DE LA COULEUR DU FOND DU RECTANGLE ET DES CARACTERES
      NFRECT( NRDOCU ) = NCBLAN

C     L'HISTORIQUE ET LE MENU
C     =======================
C     LA COULEUR DU FOND DU BORD ET DES CARACTERES
      IF( NOIBLA .EQ. 0 ) THEN
C        NBRECT : NUMERO DE LA COULEUR DU BORD DU RECTANGLE
ccc         NBRECT(NRMENU) = NCROSE
         NBRECT(NRMENU) = NCGRIC
C        NKRECT : NUMERO DE LA COULEUR DES CARACTERES DU RECTANGLE
         NKRECT(NRMENU) = NCBLAN
C        NFRECT : NUMERO DE LA COULEUR DU FOND DU RECTANGLE ET DES CARACTERES
         NFRECT(NRMENU) = NCGRIS

         NFRECT(NRHIST) = NCORAN
         NBRECT(NRHIST) = NCGRIC
         NKRECT(NRHIST) = NCBLAN
         YRECT (NRHIST) = LHPXFE
      ELSE
         NFRECT(NRHIST) = NCBLAN
         NBRECT(NRHIST) = NCNOIR
         NKRECT(NRHIST) = NCNOIR
      ENDIF

C     LA LIGNE DE SAISIE
C     ==================
C     LA COULEUR DU FOND DU BORD ET DES CARACTERES
      NFRECT(NRLGSA) = NCBLAN
      IF( NOIBLA .EQ. 0 ) THEN
         NBRECT(NRLGSA) = NCROUG
      ELSE
         NBRECT(NRLGSA) = NCNOIR
      ENDIF
      NKRECT(NRLGSA) = NCNOIR
      XRECT(NRLGSA) = INT( LAPXFE*0.8 )

C     LES LIGNES LUES
C     ===============
      XRECT(NRLGLU) = XRECT(NRLGSA)
      IF( NOIBLA .EQ. 0 ) THEN
         NBRECT(NRLGLU) = NCTURQ
         NKRECT(NRLGLU) = NCNOIR
         NFRECT(NRLGLU) = NCBLAN
      ENDIF

C     L'INVITE
C     ========
C     LA COULEUR DU FOND DU BORD ET DES CARACTERES
      IF( NOIBLA .EQ. 0 ) THEN
         NFRECT(NRINVI) = NCBEIG
         NBRECT(NRINVI) = NCROSE
      ELSE
         NFRECT(NRINVI) = NCBLAN
         NBRECT(NRINVI) = NCNOIR
      ENDIF
      NKRECT(NRINVI) = NCNOIR

C     LES ERREURS
C     ===========
C     LA COULEUR DU FOND DU BORD ET DES CARACTERES
      IF( NOIBLA .EQ. 0 ) THEN
         NFRECT( NRERR  ) = NCROUG
         NFRECT( NRERR0 ) = NCROUG
      ENDIF
      NBRECT( NRERR  ) = NCBLAN
      NKRECT( NRERR  ) = NCBLAN
      NBRECT( NRERR0 ) = NCBLAN
      NKRECT( NRERR0 ) = NCBLAN

C     DECLARATION DU TABLEAU DE LECTURE DES VALEURS DES MOTS CLES
C     ===========================================================
      CALL TNMCDC( 'REEL2' , 8 , MNMTCL )

C     PAS DE TYPE OBJET POUR EVITER LA RECURSIVITE DANS MOTSTD
C     ========================================================
      NETOBR = 0

C     SAUVEGARDE DES DONNEES SUR LE FICHIER FRAPPE
C     ============================================
      NISAFR = 1

C     EXECUTION BATCH?
      IF( INTERA .LE. 0 ) GOTO 9999

C     LA COULEUR DU FOND DE LA FENETRE GRAPHIQUE DE MEFISTO
C     =====================================================
      NCOFON = NCFOND()
      CALL XVFOND( NCOFON )

C     LES PARAMETRES PAR DEFAUT DU TRACE DES PLSVO
C     ============================================
      CALL COUDEF

C     AFFICHAGE DU JOURNAL
C     ====================
      NUIDEN = 1
      KIDENT( NUIDEN ) = 'JOURNAL'
      CALL AFDOCU

C     DECLARATION DES TABLEAUX DES 9 TYPES D'ITEMS TRACABLES
C     ======================================================
      CALL ITEMDC

C     DECLARATION ET MISE A ZERO DES ITEMS VISIBLES SUR L'ECRAN
C     =========================================================
      CALL EFFACE

9999  RETURN
      END
