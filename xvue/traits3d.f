      SUBROUTINE TRAITS3D( NC, NBS, XYZ )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACE LES SEGMENTS DE DROITE (XYZ(I)) (XYZX(I+1))
C -----  POUR I=1 A NBS
C
C        ATTENTION (XYZ) EN COORDONNEES OBJET 3D
C        LA TRANSFORMATION EN PIXELS EST ASSUREE DANS CE SP
C        LES AUTRES CARACTERISTIQUES DU TRACE SONT CELLES ACTUELLES
C
C ENTREE :
C --------
C NC     : NUMERO DE LA COULEUR DES TRAITS
C          <0 PAS DE TRACE DES TRAITS
C NBS    : NOMBRE DE SOMMETS DES TRAITS A TRACER
C XYZ    : ABSCISSE ORDONNEE COTE DES NBS SOMMETS DES TRAITS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC  PARIS        MAI 1994
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/minint.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      PARAMETER        (MXPOIN=512)
      INTEGER*2         XYPX(2,MXPOIN)
      REAL              XYZ(3,NBS), AXYZ(3)
      INTRINSIC         INT2
C
C     SI COULEUR NEGATIVE => RETOUR
      IF( NC .LT. 0 ) RETURN
C
C     VERIFICATION
      IF( NBS .LE. 2 ) RETURN
      IF( NBS .GT. MXPOIN ) THEN
         WRITE(IMPRIM,*) 'AUGMENTER MXPOIN DANS SP TRAITS3D'
         RETURN
      ENDIF
C
      DO 10 I=1,NBS
C        TRANSFORMATION EN COORDONNEES AXONOMETRIQUES
         CALL XYZAXO( XYZ(1,I), AXYZ )
C        SI UN POINT EST EXTERIEUR AUX 2 PLANS LA FACE N'EST PAS TRACEE
         IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C           AXOARR ET AXOAVA SONT ACTIFS
            IF( AXYZ(3) .LT. AXOARR ) RETURN
            IF( AXYZ(3) .GT. AXOAVA ) RETURN
         ENDIF
C        TRANSFORMATION EN PIXELS DANS LA FENETRE XV
         NX = NUPXEX( AXYZ(1) )
         NY = NUPXEY( AXYZ(2) )
C        SI LE NUMERO PIXEL EST INCORRECT ABANDON DU TRACE DE LA FACE
         IF( NX .EQ. MININT .OR. NY .EQ. MININT ) RETURN
         XYPX(1,I) = INT2( NX )
         XYPX(2,I) = INT2( NY )
 10   CONTINUE
C
C     TRACE EFFECTIF DES ARETES DE LA FACE
      CALL XVCOULEUR( NC )
      CALL XVTRAITS( NBS, XYPX )
C
      RETURN
      END
