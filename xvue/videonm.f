      SUBROUTINE VIDEONM( MODECO, CAR4, NOMFIC )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    DEFINIR SELON MODECO LE NOM DU FICHIER POUR LA VIDEO
C -----    NOMFIC=CAR4 // NomModeco
C
C ENTREES:
C --------
C MODECO = 1  CE SONT DES TEMPERATURES
C        = 2  CE SONT DES VECTEURS PROPRES
C        = 3  CE SONT DES PRESSIONS P1 A PARTIR D'UNE INTERPOLATION
C             SOIT P2 SOIT P1+BULLE P3
C        = 4  CE SONT DES ERREURS PONCTUELLES SOLEX-SOLCAL
C             => TRACE DE L'ERREUR ENTRE TEMPERATURE_EXACTE
C                ET LA TEMPERATURE CALCULEE
C        = 5  C'EST LE SUIVI DE PARTICULES SUIVANT L'ECOULEMENT DU FLUIDE
C        = 6  CE SONT DES MAGNITUDES DE VITESSE AUX NOEUDS D'UN MAILLAGE
C        = 7  CE SONT DES SUIVIS DE PARTICULES
C        = 8  CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C             DU MODULE D'UNE ONDE COMPLEXE
C            (CALCUL DU MODULE DE L'ERREUR COMPLEXE A FAIRE ICI)
C        = 9  CE SONT DES PARTIES REELLES     D'UNE ONDE COMPLEXE
C        =10  CE SONT DES PARTIES IMAGINAIRES D'UNE ONDE COMPLEXE
C        =11  CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C             DU MODULE DE LA VITESSE D'UN FLUIDE
C        =12  CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C        =13  CE SONT DES FLECHES REPRESENTANT DES VECTEURS VITESSE
C        =14  CE SONT DES ROTATIONNEL DE VITESSES ou TOURBILLONS ou VORTICITES
C
C CAR4   : 4 CARACTERES DEFINIS PAR LE SOUS-PROGRAMME DE TRACE
C
C SORTIE :
C --------
C NOMFIC : NomModeco.CAR4
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TEXAS A & M UNIVERSITY at QATAR  Janvier 2012
C AJOUTS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Fevrier 2013
C23456---------------------------------------------------------------012
      CHARACTER*4    CAR4
      CHARACTER*(*)  NOMFIC
C
C    NOM DU FICHIER VIDEO SUIVI DU NO DE L'IMAGE AVEC 4 CHIFFRES SELON MODECO
      IF( MODECO .EQ. 1 ) THEN
         NOMFIC = 'Tempera_' // CAR4
      ELSE IF( MODECO .EQ. 2 ) THEN
         NOMFIC = 'EigenVV_' // CAR4
      ELSE IF( MODECO .EQ. 3 ) THEN
         NOMFIC = 'Pressur_' // CAR4
      ELSE IF( MODECO .EQ. 4 ) THEN
         NOMFIC = 'TempErr_' // CAR4
      ELSE IF( MODECO .EQ. 5 ) THEN
         NOMFIC = 'Particl_' // CAR4
      ELSE IF( MODECO .EQ. 6 ) THEN
         NOMFIC = 'VeloMag_' // CAR4
      ELSE IF( MODECO .EQ. 7 ) THEN
         NOMFIC = 'StreamF_' // CAR4
      ELSE IF( MODECO .EQ. 8 ) THEN
         NOMFIC = 'WaveErr_' // CAR4
      ELSE IF( MODECO .EQ. 9 ) THEN
         NOMFIC = 'WavePRe_' // CAR4
      ELSE IF( MODECO .EQ. 10 ) THEN
         NOMFIC = 'WavePIm_' // CAR4
      ELSE IF( MODECO .EQ. 11 ) THEN
         NOMFIC = 'VelMErr_' // CAR4
      ELSE IF( MODECO .EQ. 12 ) THEN
         NOMFIC = 'PresErr_' // CAR4
      ELSE IF( MODECO .EQ. 13 ) THEN
         NOMFIC = 'VeloVec_' // CAR4
      ELSE IF( MODECO .EQ. 14 ) THEN
         NOMFIC = 'RotVelo_' // CAR4
      ELSE
         NOMFIC = 'Video_' // CAR4
      ENDIF
C
      RETURN
      END
