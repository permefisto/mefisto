      SUBROUTINE EM3LAG( NOOBVC, NUMIVO, NUMAVO, LTDEVO,
     &                   NBPOLY, NPI,    POLY,
     &                   F,      POIDEL, MASS,
     &                   NCODSM, MASSEF )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DE LA MATRICE DE MASSE STOCKEE SOUS FORME SYMETRIQUE
C -----    POUR UN EF 3D LAGRANGE ISOPARAMETRIQUE SAUF TETR 3P1D
C
C ENTREES:
C --------
C NOOBVC : NUMERO DE L'OBJET SURFACE DE CET ELEMENT
C NUMIVO : NUMERO MINIMAL DES SURFACES DE L'OBJET
C NUMAVO : NUMERO MAXIMAL DES SURFACES DE L'OBJET
C LTDEVO : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES MASSE
C          DES VOLUMES DE L'OBJET
C
C NBPOLY : NOMBRE DE POLYNOMES DE L'ELEMENT FINI VOLUMIQUE
C NPI    : NOMBRE DE POINTS D INTEGRATION NUMERIQUE DU VOLUME DE L'EF
C POLY   : VALEUR DES POLYNOMES DE BASE AUX POINTS D'INTEGRATION
C
C F      : 3 COORDONNEES DES NPI POINTS D INTEGRATION DE L'EF
C POIDEL : DELTA * POIDS(NPI) DES NPI POINTS D INTEGRATION DE L'EF
C MASS   : DENSITE DE MASSE AUX POINTS D'INTEGRATION DE L'EF
C
C SORTIES:
C --------
C NCODSM : CODE DE STOCKAGE DE LA MATRICE ELEMENTAIRE DE MASSE
C          1 SYMETRIQUE PLEINE
C MASSEF : MATRICE DE MASSE STOCKEE   1
C                                     2 3
C                                     4 5 6
C                                     7 ...
C          RANGEE PAR NOEUDS (U11,U12,U13, U21,U22,U23, U31,U32,U33, ...)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS       AVRIL 1998
C23456---------------------------------------------------------------012
      include"./incl/donela.inc"
      DOUBLE PRECISION  MASS(NPI), MASSEF(1:*), S
      DOUBLE PRECISION  POLY(NBPOLY, NPI),
     %                  F(NPI,3), POIDEL(NPI), XYZPI(3)
      INTEGER           LTDEVO( 1:MXDOEL, NUMIVO:NUMAVO )
C
C     DENSITE DE MASSE AUX POINTS D'INTEGRATION
C     =========================================
      DO 5 L=1,NPI
C
C        RECHERCHE DE LA DENSITE DE MASSE AU POINT D'INTEGRATION L
         XYZPI(1) = F(L,1)
         XYZPI(2) = F(L,2)
         XYZPI(3) = F(L,3)
         CALL REMASS( 4, NOOBVC, 3, XYZPI,
     %                LTDEVO(LPMASS,NOOBVC), MASS(L) )
C
C        MASSE = MASSE * POIDS * DELTA
         MASS(L) = MASS(L) * POIDEL(L)
C
 5    CONTINUE
C
C     LE CALCUL DE LA MATRICE DE MASSE
C     --------------------------------
C     INITIALISATION A ZERO
      L = 3 * NBPOLY
      L = L * ( L + 1 ) / 2
      DO 10 N=1,L
         MASSEF(N) = 0D0
 10   CONTINUE
C
      DO 40 I=1,NBPOLY
C
C        LE TROISIEME DL DU NOEUD I
         I3  = I  * 3
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I3-1=I2
         MI3 = I3 * ( I3 - 1 ) / 2
C        LE SECOND DL DU NOEUD I
         I2  = I3 - 1
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I2-1=I1
         MI2 = MI3 - I2
C        LE PREMIER DL DU NOEUD I
         I1  = I2 - 1
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I1-1
         MI1 = MI2 - I1
C
         DO 30 J=1,I
C
C           LE CALCUL DU COEFFICIENT I,J DE LA MATRICE INTEGRALE (ROT t[P] [P])
            S = 0.D0
            DO 20 L=1,NPI
               S = S + MASS(L) * POLY(J,L) * POLY(I,L)
 20         CONTINUE
C
C           CE COEFFICIENT I,J EST A PLACER POUR SES 2 COMPOSANTES
C           DANS LA RENUMEROTATION PAR NOEUDS DANS LA MATRICE DE MASSE
C           COMPTE TENU DE SON STOCKAGE SYMETRIQUE TRIANGULAIRE INFERIEUR
C           LE TROISIEME DL DU NOEUD J<=I
            J3 =  J * 3
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I3,J3
            N  = J3 + MI3
            MASSEF(N) = S
C
C           LE SECOND DL DU NOEUD J<=I
            J2 = J3 - 1
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I2,J2
            N  = J2 + MI2
            MASSEF(N) = S
C
C           LE PREMIER DL DU NOEUD J<=I
            J1 = J2 - 1
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I1,J1
            N  = J1 + MI1
            MASSEF(N) = S
C
 30      CONTINUE
C
 40   CONTINUE
C
C     CODE MATRICE ELEMENTAIRE DE MASSE SYMETRIQUE
C     STOCKEE SOUS FORME D'UNE MATRICE TRIANGULAIRE INFERIEURE
      NCODSM = 1
      RETURN
      END
