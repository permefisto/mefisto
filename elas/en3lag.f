      SUBROUTINE EN3LAG( NOOBVC, NUMIVO, NUMAVO, LTDEVO,
     &                   NBPOLY, NPI,    POLY,
     &                   F,      POIDEL, COEFDE,
     &                   RAIDEUR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CONTRIBUTION A LA MATRICE DE RAIDEUR DU COEFFICIENT DU DEPLACEMENT
C -----    MATRICE DE RAIDEUR STOCKEE SOUS FORME SYMETRIQUE
C          POUR UN EF 3D LAGRANGE ISOPARAMETRIQUE SAUF TETR 3P1D
C
C ENTREES:
C --------
C NOOBVC : NUMERO DE L'OBJET SURFACE DE CET ELEMENT
C NUMIVO : NUMERO MINIMAL DES SURFACES DE L'OBJET
C NUMAVO : NUMERO MAXIMAL DES SURFACES DE L'OBJET
C LTDEVO : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES COEFFICIENT
C          DES VOLUMES DE L'OBJET
C
C NBPOLY : NOMBRE DE POLYNOMES DE L'ELEMENT FINI VOLUMIQUE
C NPI    : NOMBRE DE POINTS D INTEGRATION NUMERIQUE DU VOLUME DE L'EF
C POLY   : VALEUR DES POLYNOMES DE BASE AUX POINTS D'INTEGRATION
C
C F      : 3 COORDONNEES DES NPI POINTS D INTEGRATION DE L'EF
C POIDEL : DELTA * POIDS(NPI) DES NPI POINTS D INTEGRATION DE L'EF
C
C AUXILIAIRE:
C -----------
C COEFDE : COMPOSANTES DU COEFFICIENT DU DEPLACEMENT AUX NPI POINTS D'INTEGRATIO
C          A DECLARER (3,NPI)
C
C SORTIES:
C --------
C RAIDEUR: MATRICE DE RAIDEUR STOCKEE   1
C                                       2 3
C                                       4 5 6
C                                       7 ...
C          RANGEE PAR NOEUDS (U11,U12,U13, U21,U22,U23, U31,U32,U33, ...)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    DECEMBRE 1999
C23456---------------------------------------------------------------012
      include"./incl/donela.inc"
      DOUBLE PRECISION  COEFDE(3,NPI), RAIDEUR(1:*), S1, S2, S3
      DOUBLE PRECISION  POLY(NBPOLY, NPI),
     %                  F(NPI,3), POIDEL(NPI)
      INTEGER           LTDEVO( 1:MXDOEL, NUMIVO:NUMAVO )
C
C     LES COMPOSANTES DU COEFFICIENT AUX POINTS D'INTEGRATION
      DO 5 L=1,NPI
C
C        RECHERCHE DES COMPOSANTES DU COEFFICIENT AU POINT D'INTEGRATION L
         CALL RECOED( 4, NOOBVC, 3, F(L,1),F(L,2),F(L,3),
     %                LTDEVO(LPCOED,NOOBVC), COEFDE(1,L) )
C
C        COEFFICIENT = COEFFICIENT * POIDS * DELTA
         COEFDE(1,L) = COEFDE(1,L) * POIDEL(L)
         COEFDE(2,L) = COEFDE(2,L) * POIDEL(L)
         COEFDE(3,L) = COEFDE(3,L) * POIDEL(L)
C
 5    CONTINUE
C
C     LA CONTRIBUTION DU COEFFICIENT DU DEPLACEMENT
      DO 40 I=1,NBPOLY
C
C        LE TROISIEME DL DU NOEUD I
         I3  = I  * 3
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I3-1=I2
         MI3 = I3 * ( I3 - 1 ) / 2
C        LE SECOND DL DU NOEUD I
         I2  = I3 - 1
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I2-1=I1
         MI2 = MI3 - I2
C        LE PREMIER DL DU NOEUD I
         I1  = I2 - 1
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I1-1
         MI1 = MI2 - I1
C
         DO 30 J=1,I
C
C           LE CALCUL DU COEFFICIENT I,J DE LA MATRICE INTEGRALE (ROT t[P] [P])
            S1 = 0.D0
            S2 = 0.D0
            S3 = 0.D0
            DO 20 L=1,NPI
               S1 = S1 + COEFDE(1,L) * POLY(J,L) * POLY(I,L)
               S2 = S2 + COEFDE(2,L) * POLY(J,L) * POLY(I,L)
               S3 = S3 + COEFDE(3,L) * POLY(J,L) * POLY(I,L)
 20         CONTINUE
C
C           CE COEFFICIENT I,J EST A PLACER POUR SES 3 COMPOSANTES
C           DANS LA RENUMEROTATION PAR NOEUDS DANS LA MATRICE DE COEFFICIENT
C           COMPTE TENU DE SON STOCKAGE SYMETRIQUE TRIANGULAIRE INFERIEUR
C           LE TROISIEME DL DU NOEUD J<=I
            J3 =  J * 3
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I3,J3
            N  = J3 + MI3
            RAIDEUR(N) = RAIDEUR(N) + S1
C
C           LE SECOND DL DU NOEUD J<=I
            J2 = J3 - 1
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I2,J2
            N  = J2 + MI2
            RAIDEUR(N) = RAIDEUR(N) + S2
C
C           LE PREMIER DL DU NOEUD J<=I
            J1 = J2 - 1
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I1,J1
            N  = J1 + MI1
            RAIDEUR(N) = RAIDEUR(N) + S3
C
 30      CONTINUE
C
 40   CONTINUE
C
      RETURN
      END
