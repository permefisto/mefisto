      SUBROUTINE ELAD2T( KNOMOB, MOREE2, NOAXIS,
     %                   NTLXOB, MNTOPO, NDIM,   NDPGST, MNXYZP, MNXYZN,
     %                   NBCODE, NBTYEL, MNNPEF, NBTTEF,
     %                   MNTPOB, NBDLMX, MOAUX,  MNTAUX,
     %                   NUMIOB, NUMAOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %                   MXTYEL, MXDOEF, MNDOEF,
     %                   IEMASS, IEYOUN, IEDILA, IECOED, IECOIN, IEFOIN,
     %                   IEFIXA, IEFOCL, IEFOPO,
     %                   AMORTM, AMORTK,
     %                   MNELAS, MNFORC, MOTAEL, MNTAEL, MNX,
     %                   MNIP,   MNNODL,
     %                   NORESO, MNLPLK, MNLPCK, NIVEAU, NBRDKG,
     %                   NCODMG, NBRDMG,
     %                   TPSINI, TPSFIN, DT,     NBDEPL, TESTDE, DTSTDE,
     %                   NTVEDE, MNVEDE, NTDLDE, MNUG,
     %                   NOTHEL, NTVETE, MNVETE, NBTEMP, NTDLTE, MNTEM0,
     %                   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ENTRER LES DONNEES DU PROBLEME D'ELASTICITE INSTATIONNAIRE
C -----    D'ORDRE 2 EN TEMPS (PB DE LA DYNAMIQUE)
C          CALCULER LE DEPLACEMENT INITIAL AU TEMPS TPSINI
C          CALCULER LE DEPLACEMENT AU TEMPS TPSINI + DT
C          A PARTIR DE LA VITESSE INITIALE AU TEMPS TPSINI
C
C ENTREE :
C --------
C KNOMOB : NOM DE L'OBJET A TRAITER
C
C SORTIES:
C --------
C MOREE2 : NOMBRE DE MOTS   D'UNE VARIABLE REELLE DOUBLE PRECISION
C NOAXIS : 1 SI PB AXISYMETRIQUE, 0 SINON
C
C NTLXOB : NUMERO DU TMS DU LEXIQUE DE L'OBJET KNOMOB
C MNTOPO : NUMERO DU TMS TOPOLOGIE  DE L'OBJET KNOMOB
C NDIM   : DIMENSION DES COORDONNEES DES POINTS ( 2 OU 3 )
C NDPGST : CODE D'IDENTIFICATION DES SOMMETS POINTS NOEUDS
C MNXYZP : ADRESSE MCN DU TABLEAU XYZPOINT DE L'OBJET KNOMOB
C MNXYZN : ADRESSE MCN DU TABLEAU XYZNOEUD DE L'OBJET KNOMOB
C
C NBCODE : NOMBRE DE COMPOSANTES DU DEPLACEMENT
C NBTYEL : NOMBRE DE TYPES D'EF DU MAILLAGE DE CET OBJET
C MNNPEF : ADRESSE MCN DU TABLEAU DES ADRESSES MCN DES TMS NPEF"TYPE EF
C NBTTEF : NOMBRE TOTAL D'EF DU MAILLAGE
C
C MNTPOB : ADRESSE MCN DU TABLEAU POINTEUR SUR LES TABLEAUX POBA DES EF
C NBDLMX : NOMBRE MAXIMAL DE DEGRES DE LIBERTE (TEMPERATURES) D'UN EF
C MOAUX  : NOMBRE DE MOTS AUXILIAIRES NECESSAIRES AU CALCUL DES EF
C MNTAUX : ADRESSE MCN DU TABLEAU AUXILIAIRE POUR LES EF
C
C NUMIOB : NUMERO MINIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C NUMAOB : NUMERO MAXIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C NBOBIN : NOMBRE DE VOLUMES en 3D, SURFACES en 2D DE L'OBJET
C MNOBIN : ADRESSE MCN DU DEBUT DU TABLEAU NUOBIN (PARTIE DE TOPOLOGIE)
C NBOBCL : NOMBRE DE PLS en 3D, PL en 2D DE L'OBJET
C MNOBCL : ADRESSE MCN DU DEBUT DU TABLEAU NUOBCL (PARTIE DE TOPOLOGIE)
C
C MXTYEL : NOMBRE MAXIMAL DE TYPES D'EF (7)
C MXDOEF : NOMBRE DE MOTS DECLARES DES TABLEAUX D'ADRESSE DANS MNDOEF
C MNDOEF : LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C          TABLEAUX DECRIVANT LES DONNEES ELASTICITE DE L'OBJET COMPLET
C
C IEMASS : NOMBRE DE TMS MASSE            DES SV DE L'OBJET RETROUVES
C IEYOUN : NOMBRE DE TMS YOUNG (+POISSON) DES SV DE L'OBJET RETROUVES
C IEDILA : NOMBRE DE TMS DILATATION       DES SV DE L'OBJET RETROUVES
C IECOED : NOMBRE DE TMS COEFDEPLACEMENT  DES SV DE L'OBJET RETROUVES
C IECOIN : NOMBRE DE TMS CONTRINIT        DES SV DE L'OBJET RETROUVES
C IEFOIN : NOMBRE DE TMS FORCE "INTERNE"  DES SV DE L'OBJET RETROUVES
C
C IEFIXA : NOMBRE DE TMS FIXATION            DES PLS DE L'OBJET RETROUVES
C IEFOCL : NOMBRE DE TMS FORCE "AUX LIMITES" DES PLS DE L'OBJET RETROUVES
C IEFOPO : NOMBRE DE TMS FORCE               DES P   DE L'OBJET RETROUVES
C
C AMORTM : COEFFICIENT D'AMORTISSEMENT DE LA MATRICE DE MASSE
C AMORTK : COEFFICIENT D'AMORTISSEMENT DE LA MATRICE DE RAIDEUR
C
C MNELAS : 128 REELS DOUBLE PRECISION POUR LE COEFFICIENT DE YOUNG AUX PT INTEGR
C MNFORC : ADRESSE MCN DU TABLEAU DES FORCES EXERCEES EN UN POINT
C MOTAEL : NOMBRE DE MOTS DECLARES DU TABLEAU DES TABLEAUX ELEMENTAIRES
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DU TABLEAU DES 3 COORDONNEES DES NOEUDS=POINTS DE L'EF
C MNIP   : NUMERO DES DL PAR COMPOSANTES A PAR NOEUDS
C MNNODL : ADRESSE MCN DU TABLEAU DES NUMEROS DES NOEUDS DE L'EF COURANT
C
C NORESO : CODE RESOLUTION DES SYSTEMES LINEAIRES
C          1 FACTORISATION DE CHOLESKY ET MATRICES PROFILS
C          2 GRADIENT CONJUGUE PRECONDITIONNE PAR NIVEAUX ET MATRICES MORSES
C MNLPLK : ADRESSE MCN DES POINTEURS SUR LES COEFFICIENTS DIAGONAUX DE LA MATRIC
C MNLPCK : ADRESSE MCN DU NUMERO DE COLONNE DES COEFFICIENTS DE LA MATRICE MORSE
C NIVEAU : NIVEAU DE FACTORISATION INCOMPLETE DE LA MATRICE DE PRECONDITIONNEMEN
C          A CHOISIR PARMI 0 1 2
C NBRDKG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE KG DE RAIDEUR
C NCODMG : 1 SI MATRICE GLOBALE DE MASSE SYMETRIQUE
C          0 SI MATRICE GLOBALE DE MASSE DIAGONALE
C         -1 SI MATRICE GLOBALE DE MASSE NON SYMETRIQUE
C NBRDMG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE MG DE MASSE
C
C TPSINI : TEMPS INITIAL DU CALCUL
C TPSFIN : TEMPS FINAL   DU CALCUL
C DT     : PAS CONSTANT DU TEMPS
C NBDEPL : NUMERO DU DERNIER VECTEUR TEMPERATURE STOCKE = VECTEUR A TPSINI
C TESTDE : TEMPS DU PROCHAIN VECTEUR"DEPLACEMENT A STOCKER
C DTSTDE : PAS CONSTANT DU TEMPS ENTRE 2 STOCKAGES DU VECTEUR"DEPLACT
C
C NTVEDE : NUMERO      DU TMS VECTEUR"DEPLACT DE L'OBJET
C MNVEDE : ADRESSE MCN DU TMS VECTEUR"DEPLACT DE L'OBJET
C NTDLDE : NOMBRE TOTAL DE D.L. DU DEPLACEMENT SUR LE MAILLAGE
C MNUG   : ADRESSE MCN DES 3 TMS DES VECTEURS DEPLACEMENT AUX TEMPS n-1 n n+1
C          A L'INSTANT INITIAL ET INITIAL + DT
C
C NOTHEL : 1  POUR LA RESOLUTION DE L'ELASTICITE SEULE
C          2  POUR LA RESOLUTION DE LA THERMO-ELASTICITE
C EN THERMO-ELASTICITE NOTHEL=2 LES VARIABLES SUIVANTES SONT >0 ET =0 SINON
C NTVETE : NUMERO      DU TMS DES VECTEUR"TEMPERATURE
C MNVETE : ADRESSE MCN DU TMS DES VECTEUR"TEMPERATURE
C NBTEMP : NUMERO      DU VECTEUR"TEMPERATURE UTILISE AU TEMPS TPSINI
C NTDLTE : NOMBRE DE D.L. EN TEMPERATURE
C MNTEM0 : ADRESSE MCN DU VECTEUR TEMPERATURE CALCULE AU TEMPS TPSINI
C          CE VECTEUR TEMPERATURE EST UNE INTERPOLATION LINEAIRE ENTRE
C          LES TEMPERATURES AUX 2 TEMPS QUI ENCADRENT LE TEMPS TPSINI
C          =0 SI PAS DE THERMO-ELASTICITE
C
C IERR   : =0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS       AVRIL 1999
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/donela.inc"
      include"./incl/donele.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a_objet__definition.inc"
      include"./incl/a___arete.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___young.inc"
      include"./incl/a___dilatation.inc"
      include"./incl/a___coefdeplacement.inc"
      include"./incl/a___force.inc"
      include"./incl/a___fixation.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___deplactinit.inc"
      include"./incl/a___vitesseinit.inc"
      include"./incl/a___tableau1r.inc"
      include"./incl/msvaau.inc"
      include"./incl/ponoel.inc"
      include"./incl/ctemps.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE       (MCN(1), RMCN(1), DMCN(1))
C
      EXTERNAL          EETAEL
      DOUBLE PRECISION  DT0, DT1, DT
      DOUBLE PRECISION  XPOIN,  YPOIN, ZPOIN
C
      INTEGER           NOOBSF(6), NOOBLA(12), NOOBPS(8)
      INTEGER           NUMIOB(4), NUMAOB(4),  MNDOEF(4), MXDOEF(4)
      CHARACTER*(*)     KNOMOB
C
      DOUBLE PRECISION  DEPLA0(3), VITES0(3), AMORTM, AMORTK
      CHARACTER*80      NOMTS, KNOMTD
      INTEGER           NOTYOB(3,MXNOEL)
      INTRINSIC         REAL
C
C     QUELQUES INITIALISATIONS DE VARIABLES ET ADRESSES MCN
C     =====================================================
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) KNOMOB
      ELSE
         WRITE(IMPRIM,20000) KNOMOB
      ENDIF
10000 FORMAT(/' DONNEES de l''ELASTICITE INSTATIONNAIRE de l''OBJET ',A/
     %)
20000 FORMAT(/' DATA of the UNSTEADY ELASTICITY of the OBJECT ',A/)
      IERR = 0
C
C     A PRIORI LE TEMPS INITIAL DE CALCUL
      TEMPS  = 0.0
C
C     PROTECTION DES ADRESSES POUR EVITER DES PROBLEMES LORS
C     DE LA DESTRUCTION DES TABLEAUX
      MNTIME = 0
      MNTHET = 0
      MNTHDL = 0
C     POUR LES TEMPERATURES EN THERMO-ELASTICITE
      NTVETE = 0
      MNVETE = 0
      NTDLTE = 0
      NBTEMP = 0
      MXTEMP = 0
      MNTEMP = 0
      MNTETE = 0
      MNLPLK = 0
      MNLPCK = 0
      NCODSK = 1
      NBDEPL = 0
C
C     AFFICHAGE ET VERIFICATION DE L'OBJET
C     ====================================
C     RECHERCHE DU NOM DE L'OBJET DANS LE LEXIQUE DES OBJETS
      CALL LXLXOU( NTOBJE, KNOMOB, NTLXOB, MNLXOB )
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET ' // KNOMOB
            KERR(2) = 'OBJET INCONNU'
         ELSE
            KERR(1) = 'ERROR: OBJECT ' // KNOMOB
            KERR(2) = 'IS UNKNOWN'
         ENDIF
         CALL LEREUR
         CALL LXIM( NTOBJE )
         IERR = 1
         RETURN
      ENDIF
C
C     RECHERCHE DU TABLEAU DEFINITION DE L'OBJET
C     ==========================================
      CALL LXTSOU( NTLXOB, 'DEFINITION', NTDFOB, MNDFOB )
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
      IF( NTDFOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'OBJET ' // KNOMOB
            KERR(2) = 'de DEFINITION INCONNUE'
         ELSE
            KERR(1) = 'The OBJECT ' // KNOMOB
            KERR(2) = 'is NOT DEFINED'
         ENDIF
         CALL LEREUR
         RETURN
      ENDIF
C
C     RECHERCHE DES TABLEAUX XYZSOMMET XYZNOEUD XYZPOINT NPEF"
C     ASSOCIES A L'OBJET
C     ========================================================
      CALL MIMAOB( 1,      NTLXOB, MXDOEL, NTTOPO, MNTOPO,
     %             NTXYZP, MNXYZP, NTXYZN, MNXYZN,
     %             MXTYEL, NBTYEL, MTNPEF, MNNPEF,
     %             NUMIOB, NUMAOB,
     %             NDPGST, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %             MXDOEF, MNDOEF, IERR )
      IF( IERR .NE. 0 ) RETURN
C     ICI NUMIOB CONTIENT LES 4 NUMEROS MINIMA DES OBJETS
C         NUMAOB          LES 4 NUMEROS MAXIMA DES OBJETS
C     MNDOEF LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C     TABLEAUX DECRIVANT L'ELASTICITE DE L'OBJET COMPLET
C
C     NDIM LA DIMENSION 2 OU 3 DE L'ESPACE DES COORDONNEES
      CALL DIMCOO( MCN(MNXYZP+WNBPOI), MCN(MNXYZP+WYZPOI), NDIM )
C     NBCODE LE NOMBRE DE COMPOSANTES DU DEPLACEMENT EN UN NOEUD
      NBCODE = NDIM
C     NTDLDE LE NOMBRE TOTAL DE DEGRES DE LIBERTE DU VECTEUR DEPLACEMENT
      NBNOEU = MCN( MNXYZN + WNBNOE )
      NTDLDE = NBCODE * NBNOEU
C
C     CHOISIR THERMO-ELASTICITE OU ELASTICITE SEULE
C     =============================================
      CALL LIMTCL( 'therelas', NOTHEL )
C     NOTHEL = 1  POUR LA RESOLUTION DE L'ELASTICITE SEULE
C              2  POUR LA RESOLUTION DE LA THERMO-ELASTICITE
      IF( NOTHEL .LE. 0 ) RETURN
      IF( LANGAG .EQ. 0 ) THEN
         IF( NOTHEL .EQ. 2 ) THEN
            WRITE(IMPRIM,*) 'THERMO-ELASTICITE DEMANDEE'
         ELSE
            WRITE(IMPRIM,*) 'SEULEMENT ELASTICITE SANS THERMIQUE'
         ENDIF
      ELSE
         IF( NOTHEL .EQ. 2 ) THEN
            WRITE(IMPRIM,*) 'THERMO-ELASTICITY REQUIRED'
         ELSE
            WRITE(IMPRIM,*) 'ONLY ELASTICITY WITHOUT HEAT TRANSFER'
         ENDIF
      ENDIF
C
C     ENTREE DU COEFFICENT D'AMORTISSEMENT DE LA MATRICE DE MASSE : AMORTM
C     ====================================================================
      CALL INVITE( 8 )
      NCVALS = 0
      CALL LIRRDP( NCVALS, AMORTM )
      IF( NCVALS .LT. 0 ) THEN
C        ABANDON DE LA LECTURE DES DONNEES
         IERR = 2
         RETURN
      ENDIF
C
C     ENTREE DU COEFFICIENT D'AMORTISSEMENT DE LA MATRICE RIGIDITE : AMORTK
C     ====================================================================
      CALL INVITE( 9 )
      NCVALS = 0
      CALL LIRRDP( NCVALS, AMORTK )
      IF( NCVALS .LT. 0 ) THEN
C        ABANDON DE LA LECTURE DES DONNEES
         IERR = 2
         RETURN
      ENDIF
C
C     ENTREE DU TEMPS INITIAL: TPSINI
C     ===============================
      CALL INVITE( 95 )
      NCVALS = 0
      CALL LIRRSP( NCVALS, TPSINI )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 2
        RETURN
      ENDIF
      TEMPS = TPSINI
C
C     ENTREE DU MODE DE CONSTRUCTION DU DEPLACEMENT INITIAL
C     =====================================================
      L1     = NUDCNB(KNOMOB)
      NOMTS  = '~>OBJET>' // KNOMOB(1:L1) // '>DEPLACTINIT'
      KNOMTD = '~>>>DEPLACTINIT'
      L1 = NUDCNB(NOMTS)
      CALL MOTSTD( KNOMTD, NOMTS(1:L1), IERR )
      IF( IERR .NE. 0 ) THEN
        NBLGRC(NRERR) = 1
        IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'DEPLACEMENT INITIAL NON DISPONIBLE, NON CALCULE?'
        ELSE
           KERR(1) = 'INITIAL DISPLACEMENT UNKNOWN, NOT COMPUTED?'
        ENDIF
        CALL LEREUR
        RETURN
      ENDIF
C     AFFICHAGE DU TMS
      CALL AFTSTD( NOMTS )
C
C     CODE DE CALCUL DU DEPLACEMENT INITIAL U0 AU TEMPS TPSINI
      CALL LXTSOU( NTLXOB,'DEPLACTINIT', NTDEIN, MNDEIN )
      LTDEP0 = MCN( MNDEIN + WTDEP0 )
C
      IF( LTDEP0 .NE. 3 ) THEN
C
C        ENTREE DE LA VITESSE INITIALE V0: LECTURE DU TMS VITESSEINIT
C        ============================================================
         L1     = NUDCNB(KNOMOB)
         NOMTS  = '~>OBJET>' // KNOMOB(1:L1) // '>VITESSEINIT'
         KNOMTD = '~>>>VITESSEINIT'
         L1 = NUDCNB(NOMTS)
         CALL MOTSTD( KNOMTD, NOMTS(1:L1), IERR )
         IF( IERR .NE. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'ERREUR: VITESSE INITIALE NON RECUPERABLE'
            ELSE
               KERR(1) = 'ERROR: INITIAL SPEED UNKNOWN'
            ENDIF
            CALL LEREUR
            RETURN
         ENDIF
C        AFFICHAGE DU TMS
         CALL AFTSTD( NOMTS )
C
      ELSE
C
C        LE TEMPS INITIAL DOIT ETRE UN TEMPS DE STOCKAGE DU VECTEUR"DEPLACT
         CALL LXTSOU( NTLXOB, 'VECTEUR"DEPLACT', NTVEDE, MNVEDE )
         IF( MNVEDE .LE. 0 ) THEN
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'ERREUR: PAS DE VECTEUR"DEPLACT'
               KERR(2) = 'AU TEMPS INITIAL DONNE'
            ELSE
               KERR(1) = 'ERROR: NO VECTEUR"DEPLACT'
               KERR(2) = 'at GIVEN INITIAL TIME'
            ENDIF
            CALL LEREUR
            IERR = 1
            RETURN
         ENDIF
C        NOMBRE DE VECTEURS STOCKES
         NBVECT = MCN(MNVEDE+WBVECT)
C
C        RECHERCHE DU TEMPS INITIAL TPSINI COMME PLUS PROCHE TEMPS
C        PARMI LES TEMPS STOCKES DU CALCUL PRECEDENT EN ELASTICITE
C        NOMBRE DE TEMPS STOCKES
         NBTIME = MCN( MNVEDE + WBCPIN )
         IF( NBTIME .GT. 0 ) THEN
            MNTIME = MNVEDE + WECTEU + MOREE2*NTDLDE*NBVECT - 1
C           NBDEPL LE NUMERO DU VECTEUR STOCKE AU TEMPS LE PLUS PROCHE DE TPSINI
            R = RINFO( 'GRAND' )
            DO 7 I=1,NBTIME
               RR = ABS( RMCN(MNTIME+I) - TPSINI )
               IF( RR .LT. R ) THEN
                  NBDEPL = I
                  R      = RR
               ENDIF
 7          CONTINUE
C
C           AFFICHAGE DU NOUVEAU TEMPS INITIAL RETENU
            TPSINI = RMCN(MNTIME+NBDEPL)
            TEMPS  = TPSINI
C
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,*) 'DEPLACEMENT INITIAL au TEMPS INITIAL=',
     %                          TEMPS
            ELSE
               WRITE(IMPRIM,*) 'INITIAL DISPLACEMENT at INITIAL TIME=',
     %                          TEMPS
            ENDIF
C
         ENDIF
C
      ENDIF
C
C     ENTREE DU TEMPS FINAL: TPSFIN
C     =============================
      CALL INVITE( 94 )
      NCVALS = 0
      CALL LIRRSP( NCVALS, TPSFIN )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 2
        RETURN
      ENDIF
      IF( TPSFIN .LE. TPSINI ) THEN
        NBLGRC(NRERR) = 1
        IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'TEMPS FINAL INCORRECT <= TEMPS INITIAL'
        ELSE
           KERR(1) = 'INCORRECT FINAL TIME <= INITIAL TIME'
        ENDIF
        CALL LEREUR
        IERR = 1
        RETURN
      ENDIF
C
C     ENTREE DU PAS DE TEMPS CONSTANT: DT
C     ===================================
      CALL INVITE( 86 )
      NCVALS = 0
      CALL LIRRDP( NCVALS, DT )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 2
        RETURN
      ENDIF
      IF( DT .LE. 0 ) THEN
        NBLGRC(NRERR) = 1
        IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'PAS DE TEMPS <=0 INCORRECT'
        ELSE
           KERR(1) = 'INCORRECT STEP of TIME <=0'
        ENDIF
        CALL LEREUR
        IERR = 1
        RETURN
      ENDIF
      PasTemps = REAL( DT )
C
C     ENTREE DU NOMBRE MAXIMAL DE VECTEURS SOLUTIONS A STOCKER
C     ========================================================
      CALL INVITE( 74 )
      NCVALS = 0
      CALL LIRENT( NCVALS, MXDEPL )
      IF( NCVALS .LT. 0 ) THEN
C        ABANDON DE LA LECTURE DES DONNEES
         IERR = 2
         RETURN
      ENDIF
      IF( MXDEPL .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'STOCKAGE des DEPLACEMENTS au TEMPS=T0 et TMAX'
         ELSE
            KERR(1) = 'STORAGE of DISPLACEMENTS at TIME=T0 and TMAX'
         ENDIF
         CALL LEREUR
         MXDEPL = 1
         RETURN
      ENDIF
C
C     NOMBRE DE PAS DE TEMPS A CALCULER LE DEPLACEMENT
      NBPATE = NINT( ( TPSFIN - TPSINI ) / DT )
      IF( MXDEPL .GT. NBPATE ) MXDEPL = NBPATE
C
C     LE PAS DE TEMPS POUR LES STOCKAGES DES NOUVEAUX VECTEUR"DEPLACT
      DTSTDE = ( TPSFIN - TPSINI ) / MXDEPL
C
C     CHOISIR LA METHODE DE RESOLUTION DU SYSTEME LINEAIRE
C     ====================================================
      CALL LIMTCL( 'methreso', NORESO )
      IF( NORESO .LE. 0 ) RETURN
      IF( NORESO .GE. 3 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'METHODE DE RESOLUTION NON PROGRAMMEE'
         ELSE
            KERR(1) = 'METHOD of COMPUTATION NOT PROGRAMMED'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
      IF( NORESO .EQ. 2 ) THEN
C        LECTURE DU NIVEAU DE FACTORISATION INCOMPLETE
         CALL INVITE( 36 )
         NCVALS = 0
         NIVEAU = 0
         CALL LIRENT( NCVALS, NIVEAU )
      ENDIF
C
C     CHOISIR LE SCHEMA D'INTEGRATION EN TEMPS
C     ========================================
      CALL LIMTCL( 'schedyna', NOSCHM )
      IF( NOSCHM .LE. 0 ) RETURN
 11   IF( NOSCHM .EQ. 1 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*)'SCHEMA de NEWMARK IMPLICITE en TEMPS'
         ELSE
            WRITE(IMPRIM,*)'IMPLICIT in TIME NEWMARK SCHEME'
         ENDIF
      ELSE IF( NOSCHM .EQ. 2 ) THEN
         IF( NBCODE .NE. 1 ) THEN
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'SCHEMA NON PROGRAMME'
               KERR(2) = 'SCHEMA DE NEWMARK IMPOSE'
            ELSE
               KERR(1) = 'NOT PROGRAMMED SCHEME'
               KERR(2) = 'NEWMARK SCHEME IMPOSED'
            ENDIF
            CALL LEREUR
            NOSCHM = 1
            GOTO 11
         ELSE
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,*)'SCHEMA DECENTRE IMPLICITE EN TEMPS'
            ELSE
               WRITE(IMPRIM,*)'IMPLICIT in TIME DECENTRED SCHEME'
            ENDIF
         ENDIF
      ELSE
         IF( NBCODE .NE. 1 ) THEN
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'SCHEMA NON PROGRAMME'
               KERR(2) = 'SCHEMA DE NEWMARK IMPOSE'
            ELSE
               KERR(1) = 'NOT PROGRAMMED SCHEME'
               KERR(2) = 'NEWMARK SCHEME IMPOSED'
            ENDIF
            CALL LEREUR
            NOSCHM = 1
            GOTO 11
         ELSE
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,*)'SCHEMA EXPLICITE CONDITIONNELLEMENT STABL
     %E dt/dx<C'
          ELSE
               WRITE(IMPRIM,*)'EXPLICIT SCHEME STABLE UNDER CONDITION dt
     %/dx<C'
          ENDIF
         ENDIF
      ENDIF
C
C     RETROUVER LES ADRESSES MCN DES DONNEES ELASTICITE
C     DES   SV "OBJETS INTERNES"    DE L'OBJET
C     DES PLS  "OBJETS AUX LIMITES" DE L'OBJET
C     =================================================
      CALL ELADON( NUMIOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL, MNDOEF,
     %             IEMASS, IEYOUN, IEDILA, IECOED, IECOIN, IEFOIN,
     %             IEFIXA, IEFOCL, IEFOPO,
     %             IEDEIN, IEVIIN, IERR )
      IF( IEMASS .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='UN VOLUME SANS MASSE'
             ELSE
                KERR(1)='UNE SURFACE SANS MASSE'
             ENDIF
             KERR(2)='=> PROBLEME SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='1 VOLUME WITHOUT MASSE'
             ELSE
                KERR(1)='1 SURFACE WITHOUT MASSE'
             ENDIF
             KERR(2)='=> PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 11
      ENDIF
      IF( IEYOUN .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='UN VOLUME SANS YOUNG'
             ELSE
                KERR(1)='UNE SURFACE SANS YOUNG'
             ENDIF
             KERR(2)='=> PROBLEME SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='1 VOLUME WITHOUT YOUNG'
             ELSE
                KERR(1)='1 SURFACE WITHOUT YOUNG'
             ENDIF
             KERR(2)='=> PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 12
      ENDIF
      IF( IERR .NE. 0 ) RETURN
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10010) NDIM, NBNOEU, NTDLDE, TPSINI, TPSFIN, DT
      ELSE
         WRITE(IMPRIM,20010) NDIM, NBNOEU, NTDLDE, TPSINI, TPSFIN, DT
      ENDIF
10010 FORMAT(/' DIMENSION 2 OU 3 DE L''ESPACE' ,T41,'=',I7,/
     %' NOMBRE DE NOEUDS'                      ,T41,'=',I7,/
     %' NOMBRE DE DEGRES DE LIBERTE'           ,T41,'=',I7,/
     %' TEMPS INITIAL DE L''ETUDE'             ,T41,'=',G14.6,/
     %' TEMPS FINAL   DE L''ETUDE'             ,T41,'=',G14.6,/
     %' VALEUR DU PAS DE TEMPS CONSTANT'       ,T41,'=',G14.6/)
20010 FORMAT(/' SPACE DIMENSION (2 or 3)' ,T32,'=',I7,/
     %' NUMBER of NODES'                  ,T32,'=',I7,/
     %' NUMBER of DEGREES of FREEDOM'     ,T32,'=',I7,/
     %' INITIAL TIME of the study'        ,T32,'=',G14.6,/
     %' FINAL   TIME of the study'        ,T32,'=',G14.6,/
     %' CONSTANT VALUE STEP of TIME'      ,T32,'=',G14.6/)
C
C     RECHERCHE DES VECTEUR"TEMPERATURE POUR LA THERMO-ELASTICITE
C     ===========================================================
      IF( NOTHEL .EQ. 2 ) THEN
C
C        THERMO-ELASTICITE: LA TEMPERATURE A T ELLE ETE CALCULEE?
         CALL LXTSOU( NTLXOB, 'VECTEUR"TEMPERATURE', NTVETE, MNVETE )
         IF( MNVETE .LE. 0 ) THEN
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'ERREUR: THERMO-ELASTICITE DEMANDEE'
               KERR(2) = 'MAIS PAS de STOCKAGE des TEMPERATURES'
               KERR(3) = 'EXECUTER THERMICER AUPARAVANT'
            ELSE
               KERR(1) = 'ERROR: REQUEST of THERMO-ELASTICITY'
               KERR(2) = 'BUT NO STORAGE of TEMPERATURES'
               KERR(3) = 'EXECUTE THERMICER PREVIOUSLY'
            ENDIF
            CALL LEREUR
            IERR = 10
            RETURN
         ENDIF
C
C        OUI: NOMBRE DE VECTEUR"TEMPERATURE STOCKES
         MXTEMP = MCN( MNVETE + WBVECT )
C        NOMBRE DE DL DU PROBLEME THERMIQUE
         NTDLTE = MCN( MNVETE + WBCOVE )
C
C        ADRESSE DU VECTEUR DES TEMPERATURES AUX TEMPS INITIAL DE L'ELASTICITE
         CALL TNMCDC( 'REEL2', NTDLTE, MNTEM0 )
C
C        RECHERCHE DU TEMPS TPSINI PARMI LES VALEURS DU TEMPS
C        DE STOCKAGE DES VECTEUR"TEMPERATURE
C        NOMBRE DE TEMPS STOCKES POUR LA TEMPERATURE
         N = MCN( MNVETE + WBCPIN )
         IF( N .EQ. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
          KERR(1) = 'ERREUR: PAS de STOCKAGE des TEMPS durant THERMICER'
            ELSE
               KERR(1) = 'ERROR: NO STORAGE of TIMES during THERMICER'
            ENDIF
            CALL LEREUR
            IERR = 11
            RETURN
         ENDIF
C
C        RECHERCHE DU TEMPS LE PLUS PROCHE DE TPSINI PARMI CEUX STOCKES
C        POUR LA TEMPERATURE
         MNTETE = MNVETE + WECTEU + MOREE2 * NTDLTE * MXTEMP - 1
C        NBTEMP LE NUMERO DU VECTEUR STOCKE AU TEMPS LE PLUS PROCHE DE TPSINI
         IF( LTDEP0 .EQ. 3 ) THEN
            TPSINI = REAL( TPSINI - DT )
         ENDIF
         NBTEMP = 1
         R      = RINFO( 'GRAND' )
         DO 15 I=1,N
            RR = ABS( RMCN(MNTETE+I) - TPSINI )
            IF( RR .LT. R ) THEN
               NBTEMP = I
               R      = RR
            ENDIF
 15      CONTINUE
C
C        LE TEMPS DE STOCKAGE DE LA TEMPERATURE
         MNTETE = MNTETE + NBTEMP
         TEMPT0 = RMCN(MNTETE)
         IF( ABS(TPSINI-TEMPT0) .LE. 1E-4*TPSINI ) THEN
C
C           TRANSFERT DE LA TEMPERATURE INITIALE NBTEMP DANS LE TABLEAU MNTEM0
            MNTEMP = MNVETE + WECTEU + MOREE2 * NTDLTE * (NBTEMP-1)
            CALL TRTATD( MCN(MNTEMP), MCN(MNTEM0), NTDLTE )
C
         ELSE IF( TPSINI .GT. TEMPT0 ) THEN
C
C           TPSINI EST ENTRE LE VECTEUR NBTEMP ET NBTEMP+1
            IF( NBTEMP .GE. MXTEMP ) THEN
               NBLGRC(NRERR) = 2
               IF( LANGAG .EQ. 0 ) THEN
                  KERR(1) = 'ERREUR: TEMPS INITIAL NON DANS LES TEMPS'
                  KERR(2) = 'DES VECTEURS TEMPERATURES CALCULES'
               ELSE
                  KERR(1) = 'ERROR: INITIAL TIME NOT in TIMES'
                  KERR(2) = 'of STORED COMPUTED TEMPERATURE VECTORS'
               ENDIF
               CALL LEREUR
               IERR = 12
               RETURN
            ENDIF
            MNTEMP = MNVETE + WECTEU + MOREE2 * NTDLTE * (NBTEMP-1)
            TEMPT1 = RMCN(MNTETE+1)
            DT0    = (TEMPT1 - TPSINI) / ( TEMPT1 - TEMPT0 )
            DT1    = (TPSINI - TEMPT0) / ( TEMPT1 - TEMPT0 )
C           LES TEMPERATURES AU TEMPS TPSINI OBTENUES PAR INTERPOLATION LINEAIRE
            CALL CL2VED( NTDLTE, DT0, MCN(MNTEMP),
     %                           DT1, MCN(MNTEMP+MOREE2*NTDLTE),
     %                           MCN(MNTEM0) )
         ELSE
C
C           TPSINI EST ENTRE LE VECTEUR NBTEMP-1 ET NBTEMP
            IF( NBTEMP .LE. 1 ) THEN
               NBLGRC(NRERR) = 2
               IF( LANGAG .EQ. 0 ) THEN
                  KERR(1) = 'ERREUR: TEMPS INITIAL NON DANS LES TEMPS'
                  KERR(2) = 'DES VECTEURS TEMPERATURES CALCULEES'
               ELSE
                  KERR(1) = 'ERROR: INITIAL TIME NOT in TIMES'
                  KERR(2) = 'of STORED COMPUTED TEMPERATURE VECTORS'
               ENDIF
               CALL LEREUR
               IERR = 13
               RETURN
            ENDIF
C           LE TEMPS DE STOCKAGE DE LA TEMPERATURE
            NBTEMP = NBTEMP - 1
            MNTEMP = MNVETE + WECTEU + MOREE2 * NTDLTE * (NBTEMP-1)
            MNTETE = MNTETE - 1
            TEMPT0 = RMCN(MNTETE)
            TEMPT1 = RMCN(MNTETE+1)
            DT0    = (TEMPT1 - TPSINI) / ( TEMPT1 - TEMPT0 )
            DT1    = (TPSINI - TEMPT0) / ( TEMPT1 - TEMPT0 )
C           LES TEMPERATURES AU TEMPS TPSINI OBTENUES PAR INTERPOLATION LINEAIRE
            CALL CL2VED( NTDLTE, DT0, MCN(MNTEMP),
     %                           DT1, MCN(MNTEMP+MOREE2*NTDLTE),
     %                           MCN(MNTEM0) )
         ENDIF
      ENDIF
C
C     RECUPERATION DES TABLEAUX POBA NECESSAIRES A LA
C     CONSTRUCTION DES TABLEAUX ELEMENTAIRES
C     ===============================================
      CALL TAPOBA( NBTYEL, MNNPEF, EETAEL,
     %             MNTPOB, NBDLMX, MOAUX, NBTTEF, NOAXIS, NCODMG, IERR )
      IF( IERR .NE. 0 ) RETURN
C
C     DECLARATION DES 3 VECTEURS GLOBAUX DES DEPLACEMENTS EN 2 TEMPS SUCCESSIFS
C     =========================================================================
      CALL TNMCDC( 'REEL2', NTDLDE*3, MNUG )
      MNUG0 = MNUG
      MNUG1 = MNUG0 + MOREE2 * NTDLDE
C
C     ADRESSAGE DES TABLEAUX AUXILIAIRES ET ELEMENTAIRES
C     ===================================================
      CALL TNMCDC( 'REEL2', MOAUX, MNTAUX )
C
C     MOTAEL = 2 MATRICES ELEMENTAIRES (DE MASSE + DE RAIDEUR)
C              ET MXDEPL SECOND MEMBRES
      MOTAEL = NBDLMX * (NBDLMX+1) + NBDLMX * (1+MXDEPL)
      CALL TNMCDC( 'REEL2', MOTAEL, MNTAEL )
C
C     LE TABLEAU DE LA PERMUTATION DES DL PAR COMPOSANTES A CELLE PAR NOEUDS
      MNIP = 0
      CALL TNMCDC( 'ENTIER', NBDLMX, MNIP )
C
C     LE TABLEAU DU NUMERO DES DEGRES DE LIBERTE D'UN ELEMENT FINI
      MNNODL = 0
      CALL TNMCDC( 'ENTIER', NBDLMX, MNNODL )
C
C     YOUNG AUX POINTS D'INTEGRATION NUMERIQUE DE L'EF
      MNELAS = 0
      CALL TNMCDC( 'REEL2', 128, MNELAS )
C
C     CONTRAINTES INITIALES OU FORCES EN UN POINT
      MNFORC = 0
      CALL TNMCDC( 'REEL2', 6, MNFORC )
C
C     LE TABLEAU DES 3 COORDONNEES DES NBDLMX NOEUDS DE L'EF MAXIMAL
      MNX = 0
      CALL TNMCDC( 'REEL', NBDLMX*3, MNX )
C
C     DECLARATION DU VECTEUR DEPLACEMENT LOCAL SUR UN ELEMENT FINI
      CALL TNMCDC( 'REEL2', NBDLMX, MNTHDL )
C
C     ===========================================================
C     CALCUL DES MATRICES DE MASSE [M] ET RAIDEUR [C]
C     SUPPOSEES INDEPENDANTES DU TEMPS ET DU DEPLACEMENT VERTICAL
C     ===========================================================
C
C     PREPARATION DE LA MATRICE PROFIL OU MORSE
C     =========================================
      IF( NORESO .EQ. 1 ) THEN
C
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10015)
         ELSE
            WRITE(IMPRIM,20015)
         ENDIF
10015    FORMAT(' RESOLUTION DIRECTE PAR FACTORISATION DE CHOLESKY')
20015    FORMAT(' DIRECT COMPUTATION by CHOLESKY FACTORIZATION')
C
C        STOCKAGE PROFIL ET FACTORISATION DE CHOLESKY
C        --------------------------------------------
C        CALCUL DU PROFIL DE LA MATRICE
C        (LA MATRICE PROFIL EST ICI SYMETRIQUE)
         CALL TNMCDC( 'ENTIER', NTDLDE+1     , MNLPLK )
         CALL PRPRMC( MNTOPO, MCN(MNNPEF), MNXYZN,
     %                NBCODE, NCODSK,
     %                MCN(MNLPLK), IERR )
         IF( IERR .NE. 0 ) RETURN
C
C        DECLARATION INITIALISATION DE LA MATRICE PROFIL SYMETRIQUE
         NBRDKG = MCN( MNLPLK + NTDLDE )
         MOTMAT = NTDLDE + MOREE2 * NBRDKG
         IF( NCODMG .NE. 0 ) THEN
            MOTMAT = MOTMAT + MOREE2 * NBRDKG
         ELSE
            MOTMAT = MOTMAT + MOREE2 * NTDLDE
         ENDIF
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10016) MOTMAT
         ELSE
            WRITE(IMPRIM,20016) MOTMAT
         ENDIF
10016  FORMAT(' STOCKAGE MATRICES [M]&[K] PROFIL =',I15,' MOTS MEMOIRE')
20016  FORMAT(' SKYLINE MATRIX STORAGE of [M]&[K]=',I15,' MEMORY WORDS')
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        POINTEURS DU STOCKAGE MORSE POUR METHODE DU GRADIENT CONJUGUE
C        -------------------------------------------------------------
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10018)
         ELSE
            WRITE(IMPRIM,20018)
         ENDIF
10018    FORMAT(' RESOLUTION ITERATIVE PAR GRADIENT CONJUGUE')
20018    FORMAT(' ITERATIVE COMPUTATION by PRECONDITIONATED CONJUGATE GR
     %ADIENT')
C
C        CALCUL DU SQUELETTE DE LA MATRICE
C        (LA MATRICE EST ICI SYMETRIQUE)
         CALL PRGCMC( MNTOPO, MCN(MNNPEF), MNXYZN,
     %                NBCODE, NCODSK,
     %                MNLPLK, MNLPCK, IERR )
         IF( IERR .NE. 0 ) RETURN
C
C        DECLARATION INITIALISATION DE LA MATRICE MORSE SYMETRIQUE
         NBRDKG = MCN( MNLPLK + NTDLDE )
         MOTMAT = NTDLDE + (1+MOREE2) * NBRDKG
         IF( NCODMG .NE. 0 ) THEN
            MOTMAT = MOTMAT + MOREE2 * NBRDKG
         ELSE
            MOTMAT = MOTMAT + MOREE2 * NTDLDE
         ENDIF
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10020) MOTMAT
         ELSE
            WRITE(IMPRIM,20020) MOTMAT
         ENDIF
10020    FORMAT(' STOCKAGE DES MATRICES [M]&[K] MORSE=',I15,' MOTS MEMOI
     %RE' )
20020    FORMAT(' SPARSE MATRIX STORAGES [M]&[K]=',I15,' MEMORY WORDS' )
      ENDIF
C
C     DECLARATION DE LA MATRICE DE MASSE
      IF( NCODMG .NE. 0 ) THEN
         NBRDMG = NBRDKG
      ELSE
         NBRDMG = NTDLDE
      ENDIF
C
      WRITE(IMPRIM,*) 'MATRICES [M] [K] CONSTANTES CALCULEES'
C
C     INITIALISATION DU VECTEUR UG0 DU DEPLACEMENT INITIAL
C     AUX NOEUDS DU MAILLAGE ET A L'INSTANT  TEMPS INITIAL
C     ====================================================
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10040) TEMPS
      ELSE
         WRITE(IMPRIM,20040) TEMPS
      ENDIF
10040 FORMAT(/' Au TEMPS',G14.6,' DEBUT du CALCUL du DEPLACEMENT')
20040 FORMAT(/' At TIME',G14.6,' START of DISPLACEMENT COMPUTATION')
C
      CALL LXNMNO( NTOBJE, KNOMOB, NOOB, I )
      DT0 = 0D0
      IF( LTDEP0 .GE. 2 ) THEN
C
C        LE DEPLACEMENT INITIAL EST DEJA CALCULE DANS VECTEUR"DEPLACT
C        ------------------------------------------------------------
         CALL LXTSOU( NTLXOB, 'VECTEUR"DEPLACT', NTVEDE, MNVEDE )
         IF( MNVEDE .LE. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'PAS DE VECTEUR"DEPLACT INITIAL'
            ELSE
               KERR(1) = 'NO INITIAL VECTEUR"DEPLACT'
            ENDIF
            CALL LEREUR
            IERR = 1
            RETURN
         ENDIF
         IF( NTDLDE .NE. MCN(MNVEDE+WBCOVE) ) THEN
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'NOMBRE DE COMPOSANTES DU VECTEUR"DEPLACT'
               WRITE(KERR(5)(1:8),'(I8)') NTDLDE
               KERR(2) = 'DIFFERENT DE ' // KERR(5)(1:8) // ' NOEUDS'
            ELSE
               KERR(1) = 'NUMBER of COMPONENTS of the VECTEUR"DEPLACT'
               WRITE(KERR(5)(1:8),'(I8)') NTDLDE
               KERR(2) = 'DIFFERENT of ' // KERR(5)(1:8) // ' NODES'
            ENDIF
            CALL LEREUR
            IERR = 1
            RETURN
         ENDIF
C
C        RECHERCHE DU TEMPS INITIAL TPSINI COMME PLUS PROCHE TEMPS
C        PARMI LES TEMPS STOCKES DU CALCUL PRECEDENT
         MNTIME = MNVEDE + WECTEU + MOREE2*NTDLDE*MCN(MNVEDE+WBVECT)-1
C        NOMBRE DE TEMPS STOCKES
         NBTIME = MCN( MNVEDE + WBCPIN )
         IF( NBTIME .LE. 0 ) THEN
C           ADRESSE MCN DU VECTEUR DEPLACEMENT STOCKE NBDEPL
            MNDEPL = MNVEDE + WECTEU
            CALL TRTATD( MCN(MNDEPL), MCN(MNUG0), NTDLDE )
            NBDEPL = 1
            GOTO 64
         ENDIF
C
C        IL EXISTE DES VECTEURS DEPLACEMENTS DEJA CALCULES
         R = RINFO( 'GRAND' )
         DO 45 I=1,NBTIME
            RR = ABS( RMCN(MNTIME+I) - TPSINI )
            IF( RR .LT. R ) THEN
C              NBDEPL LE NUMERO DU VECTEUR STOCKE AU TEMPS LE PLUS PROCHE DE TPS
               NBDEPL = I
               R      = RR
            ENDIF
 45      CONTINUE
         IF( ABS(TPSINI-RMCN(MNTIME+NBDEPL)) .LE. 1E-4*TPSINI ) THEN
            RMCN(MNTIME+NBDEPL) = TPSINI
         ENDIF
         IF( TPSINI .LT. RMCN(MNTIME+NBDEPL) ) THEN
C           TPSINI EST ENTRE NBDEPL et < NBDEPL+1
            NBDEPL = MAX( 1, NBDEPL - 1 )
         ENDIF
C        ADRESSE MCN DU VECTEUR DEPLACEMENT STOCKE NBDEPL
         MNDEPL = MNVEDE + WECTEU + MOREE2 * NTDLDE * (NBDEPL-1)
         MNDEP1 = MNDEPL + MOREE2 * NTDLDE
C        LE PAS DE TEMPS DT0 DE L'ETUDE PRECEDENTE EN ELASTICITE
         TPSDE0 = RMCN(MNTIME+NBDEPL)
         TPSDE1 = RMCN(MNTIME+NBDEPL+1)
         DT0 = (TPSDE1 - TPSINI) / ( TPSDE1 - TPSDE0 )
         DT1 = (TPSINI - TPSDE0) / ( TPSDE1 - TPSDE0 )
C
C        UG0 A TPSINI EST INTERPOLE LINEAIREMENT ENTRE LES DEPLACEMENTS
C        STOCKES AU TEMPS TPSDE0 (NBDEPL) et TPSDE1 (NBDEPL+1)
         CALL CL2VED( NTDLDE, DT0, MCN(MNDEPL),
     %                        DT1, MCN(MNDEP1), MCN(MNUG0) )
C
         IF( LTDEP0 .EQ. 3 ) THEN
C
C           UG1 A TPSINI+DT EST INTERPOLE LINEAIREMENT ENTRE LES
C           DEPLACEMENTS AU TEMPS TPSINI (UG0) et TPSDE1 (NBDEPL+1)
            DT0 = (TPSDE1 - (TPSINI+DT)) / ( TPSDE1 - TPSINI )
            DT1 = ((TPSINI+DT) - TPSINI) / ( TPSDE1 - TPSINI )
            IF( ABS(DT0) .LE. 1D-5 ) THEN
C              ERREUR D'ARRONDI RESORBEE
               DT0 = 0D0
               DT1 = 1D0
            ENDIF
            CALL CL2VED( NTDLDE, DT0, MCN(MNUG0),
     %                           DT1, MCN(MNDEP1), MCN(MNUG1) )
            NBDEPL = NBDEPL + 1
            GOTO 80
         ENDIF
C
      ELSE
C
C        LTDEP0<2
C        TRAITEMENT DU DEPLACEMENT INITIAL A PARTIR DU TMS 'DEPLACTINIT'
C        ---------------------------------------------------------------
         IF( LTDEP0 .EQ. 1 ) THEN
C
C           LE DEPLACEMENT UG0 EST OBTENU PAR CONSTANTE
C           -------------------------------------------
            DEPLA0(1) = RMCN( MNDEIN + WADEP0 )
            MN = (MNUG0-1) / 2
            DO 47 I=1,NTDLDE
C              LE VECTEUR UG0 EST INITIALISE
               DMCN( MN+I ) = DEPLA0(1)
 47         CONTINUE
C
         ELSE IF( LTDEP0 .EQ. -1 ) THEN
C
C           LE DEPLACEMENT UG0 EST OBTENU PAR FONCTION
C           ------------------------------------------
            MN = (MNUG0-1) / 2
            MM = MNXYZN + WYZNOE - 3
            DO 50 I=1,NBNOEU
               MM    = MM + 3
               XPOIN = RMCN( MM )
               YPOIN = RMCN( MM + 1 )
               ZPOIN = RMCN( MM + 2 )
               CALL REDEIN( 5,NOOB,NBCODE,XPOIN,YPOIN,ZPOIN,MNDEIN,
     %                      DEPLA0 )
C              LE VECTEUR UG0 EST INITIALISE
               DO 48 J=1,NBCODE
                  DMCN( MN+J ) = DEPLA0(J)
 48            CONTINUE
               MN = MN + NBCODE
 50         CONTINUE
         ENDIF
C
C        TRAITEMENT DU DEPLACEMENT INITIAL PARTICULARISE SUR DES PLSV
C        ------------------------------------------------------------
         IF( IEDEIN .LE. 0 ) GOTO 64
C        AU MOINS UN PLSV SUPPORTE UN TMS 'DEPLACTINIT'
C
C        LA BOUCLE SUR LES TYPES D'ELEMENTS FINIS DU MAILLAGE DE L'OBJET
         MM  = MNXYZN + WYZNOE - 3
         MN0 = (MNUG0-1) / MOREE2 - NBCODE
         DO 60 NOTYEL = 1, NBTYEL
C
C           L'ADRESSE DU TABLEAU NPEF"No TYPE EF
            MNELE = MCN( MNNPEF - 1 + NOTYEL )
C           LE NUMERO DU TYPE DE L'ELEMENT FINI
            NUTYEL = MCN( MNELE + WUTYEL )
C           LE NOMBRE D'ELEMENTS DE CE TYPE
            NBELEM = MCN( MNELE + WBELEM )
C
C           L'ADRESSE MCN DES NOEUDS ET POINTS GEOMETRIQUES DES EF
            MNNDEL = MNELE + WUNDEL
            MNPGEL = MNNDEL
            IF( NDPGST .GE. 2 ) THEN
               MNPGEL = MNPGEL + NBELEM * MCN(MNELE+WBNDEL)
            ENDIF
C
C           LES CARACTERISTIQUES DE L'ELEMENT FINI
C           ON TROUVE: NBPOE, NBNOE, NARET
            CALL ELTYCA( NUTYEL )
C
C           LA BOUCLE SUR LES ELEMENTS FINIS DE CE TYPE NUTYEL
            DO 58 NUELEM = 1, NBELEM
C
C              LES NOEUDS DE L'ELEMENT FINI
C              LE NUMERO DE VOLUME  DE L'EF
C              LE NUMERO DE SURFACE DES FACES   DE L'EF
C              LE NUMERO DE LIGNE   DES ARETES  DE L'EF
C              LE NUMERO DE POINT   DES SOMMETS DE L'EF
               CALL EFPLSV( MNELE , NUELEM,
     %                      NOVCEL, NOSFEL, NOLAEL, NOPSEL,
     %                      NOOBVC, NOOBSF, NOOBLA, NOOBPS )
C
C              LE CALCUL DU TYPE OBJET DE CHAQUE NOEUD DE L'ELEMENT FINI
C              EN COMMENCANT PAR LE VOLUME, PUIS LES FACES, PUIS LES ARETES
C              PUIS LES SOMMETS CE QUI ASSURE LA PRIORITE DES POINTS SUR
C              LES LIGNES, SURFACES ET VOLUMES
               CALL EFTNND( NOOBVC, NOOBSF, NOOBLA, NOOBPS,
     %                      NUMIOB, MNDOEF,
     %                     'DEPLACTINIT', MXDOEL, LPDEIN,
     %                      NOTYOB )
C
C              LE RECENSEMENT DU DEPLACEMENT INITIAL
C              AUX NBNOE NOEUDS DE CET ELEMENT FINI
               DO 55 J=1,NBNOE
C
C                 LE NUMERO DU NOEUD DANS LE MAILLAGE DE L'OBJET
                  NONOE = MCN( MNNDEL-1 + NUELEM + NBELEM*(J-1) )
C
C                 L'ADRESSE MCN DU TABLEAU DEPLACTINIT
                  MN2 = NOTYOB(3,J)
C
C                 EXISTE-T-IL UN "DEPLACTINIT" EN CE NOEUD ?
                  IF( MN2 .GT. 0 ) THEN
C
C                    CALCUL DU DEPLACEMENT INITIAL EN CE NOEUD
C                    LE TYPE OBJET DU NOEUD J DE L'EF
                     NTYOB = NOTYOB(1,J)
                     NOOB  = NOTYOB(2,J)
                     N     = MM + 3 * NONOE
                     XPOIN = RMCN(N)
                     YPOIN = RMCN(N+1)
                     ZPOIN = RMCN(N+2)
                     CALL REDEIN( NTYOB,NOOB,NBCODE,XPOIN,YPOIN,ZPOIN,
     %                            MNDEIN, DEPLA0 )
C                    LE VECTEUR UG0 EST INITIALISE EN CE NOEUD
                     L = MN0 + NBCODE*NONOE
                     DO 53 K=1,NBCODE
                        DMCN( L+K ) = DEPLA0( K )
 53                  CONTINUE
C
                  ENDIF
C
 55            CONTINUE
 58         CONTINUE
 60      CONTINUE
      ENDIF
C     ICI LE VECTEUR UG0 EST INITIALISE
C
C     CONSTRUCTION DU VECTEUR UG1 A l'INSTANT T + DT
C     ==============================================
C     OUVERTURE DU TMS VITESSEINIT DE L'OBJET
C     ---------------------------------------
 64   CALL LXTSOU( NTLXOB, 'VITESSEINIT', NTVIIN, MNVIIN )
      LTVIT0=MCN(MNVIIN+WTVIT0)
C     L'ADRESSE DMCN DE UG0
      MN = (MNUG0-1) / 2
C     L'ADRESSE DMCN DE UG1
      L  = (MNUG1-1) / 2
      MM = MNXYZN + WYZNOE - 3
      DO 66 I=1,NBNOEU
         MM    = MM + 3
         XPOIN = RMCN( MM     )
         YPOIN = RMCN( MM + 1 )
         ZPOIN = RMCN( MM + 2 )
         CALL REVIIN( 5,NOOB,NBCODE,XPOIN,YPOIN,ZPOIN,MNVIIN, VITES0)
         DO 65 J=1,NBCODE
C           LE VECTEUR UG1 AU TEMPS TPSINI+DT EST INITIALISE
            MN = MN + 1
            L  = L  + 1
            DMCN( L ) = DMCN( MN ) + DT * VITES0(J)
 65      CONTINUE
 66   CONTINUE
C
C     TRAITEMENT DE LA VITESSE INITIALE PARTICULARISEE SUR DES PLSV
C     -------------------------------------------------------------
      IF( IEVIIN .GT. 0 ) THEN
C        AU MOINS UN PLSV SUPPORTE UN TMS 'VITESSEINIT'
         CALL DEPLT1( MOREE2, MNXYZN, NDPGST, MXDOEL, LPVIIN, MNVIIN,
     %                NBCODE, NBTYEL, MNNPEF, NUMIOB, MNDOEF,
     %                DT,     MNUG0,  MNUG1 )
      ENDIF
C     ICI LE VECTEUR UG1 EST INITIALISE
C
C     ADRESSAGE INITIALISATION DU VECTEUR"DEPLACT SUR LE MAILLAGE TOTAL
C     =================================================================
 80   CALL LXTSOU( NTLXOB, 'VECTEUR"DEPLACT', NTVEDE, MNVEDE )
      IF( NTVEDE .GT. 0 ) THEN
C
         IF( NBDEPL .LE. 0 ) THEN
C
C           LE VECTEUR EST DETRUIT POUR ETRE REDECLARE
            CALL LXTSDS( NTLXOB, 'VECTEUR"DEPLACT' )
C
         ELSE
C
C           LE VECTEUR EXISTE DEJA. MXDEPL NOUVEAUX VECTEURS LUI
C           SONT AJOUTES A PARTIR DU VECTEUR NBDEPL QUI EST VECTEUR INITIAL
            NBVECT = MCN(MNVEDE+WBVECT)
            MN     = MNVEDE + WECTEU + MOREE2 * NTDLDE * NBVECT
            NBTIME = MCN( MNVEDE + WBCPIN )
            MXDEPL = NBDEPL + MXDEPL
            IF( MXDEPL .GT. MCN(MNVEDE+WBVECT) ) THEN
C              LE TMS EST AUGMENTE
               L = WECTEU + NTDLDE * MXDEPL * MOREE2 + MXDEPL
               CALL TAMSAU( NTVEDE, L )
               CALL LXTSOU( NTLXOB, 'VECTEUR"DEPLACT',
     %                      NTVEDE, MNVEDE )
            ENDIF
            MNDEPL = MNVEDE + WECTEU
            MNTIME = MNDEPL + MOREE2 * NTDLDE * MXDEPL
C           COPIE DES NBDEPL-1 PREMIERS TEMPS EXISTANTS DES DEPLACEMENTS
            IF( NBTIME .LE. 0 ) GOTO 95
            DO 93 I=0,MIN(NBTIME,NBDEPL-1)
               RMCN( MNTIME+I ) = RMCN( MN+I )
 93         CONTINUE
            GOTO 95
         ENDIF
C
      ENDIF
C
C     LE VECTEUR N'EXISTE PAS. IL EST CREE AVEC 1+MXDEPL VECTEURS A STOCKER
      NBDEPL = 1
      MXDEPL = 1 + MXDEPL
      L      = WECTEU + NTDLDE * MXDEPL * MOREE2 + MXDEPL
      CALL LXTNDC( NTLXOB, 'VECTEUR"DEPLACT', 'MOTS', L )
      CALL LXTSOU( NTLXOB, 'VECTEUR"DEPLACT', NTVEDE, MNVEDE )
C
C     LE NOMBRE DE VECTEUR"DEPLACT A STOCKER
 95   MCN( MNVEDE + WBVECT ) = MXDEPL
C
C     LE TEMPS
      TEMPS  = TPSINI
C
C     STOCKAGE DU VECTEUR UG0 DES DEPLACEMENTS INITIAUX DANS MNDEPL
      IF( NBDEPL .LE. 0 ) THEN
         NBDEPL = 1
      ENDIF
      IF( LTDEP0 .EQ. 3 ) THEN
         NBDEPL = NBDEPL - 1
      ENDIF
      MNDEPL = MNVEDE + WECTEU + MOREE2 * NTDLDE * (NBDEPL-1)
      CALL TRTATD( MCN(MNUG0), MCN(MNDEPL), NTDLDE )
C
C     LE TEMPS DU VECTEUR DEPLACEMENT INITIAL STOCKE
      MNTIME = MNVEDE + WECTEU + MOREE2 * NTDLDE * MXDEPL - 1
      RMCN(MNTIME+NBDEPL) = TEMPS
C
C     INITIALISATION DES TEMPS DE STOCKAGE DES VECTEUR"
C     LE PROCHAIN TEMPS DE STOCKAGE DU VECTEUR"DEPLACEMENT
      TESTDE = TEMPS + DTSTDE
C     AFFICHAGE DU STOCKAGE DU VECTEUR INITIAL
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10095) TEMPS, NTDLDE, NBDEPL,MXDEPL
      ELSE
         WRITE(IMPRIM,20095) TEMPS, NTDLDE, NBDEPL,MXDEPL
      ENDIF
10095 FORMAT(' Au TEMPS',G14.6,' STOCKAGE de',I7,' DEPLACEMENTS',
     %   ' COLONNE',I5,'/',I5,' du TMS VECTEUR"DEPLACT')
20095 FORMAT(' At TIME',G14.6,' STORAGE of',I7,' DISPLACEMENTS',
     %   ' COLUMN',I5,'/',I5,' of TMS VECTEUR"DEPLACT')
C
C     ==========================================
C     ACTUALISER LE TEMPS 1 = TEMPS INITIAL + DT
C     ==========================================
      TEMPS = REAL( TEMPS + DT )
      IF( TEMPS .GE. TESTDE*0.9999 ) THEN
C        STOCKAGE DU DEPLACEMENT A CET INSTANT TEMPS 1
         MNDEPL = MNDEPL + MOREE2 * NTDLDE
         CALL TRTATD( MCN(MNUG1), MCN(MNDEPL), NTDLDE )
C        LE NOMBRE DE VECTEURS DEPLACEMENT STOCKES
         NBDEPL = NBDEPL + 1
C        LE TEMPS DE STOCKAGE
         RMCN(MNTIME+NBDEPL) = TEMPS
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10095) TEMPS,NTDLDE,NBDEPL,MXDEPL
         ELSE
            WRITE(IMPRIM,20095) TEMPS,NTDLDE,NBDEPL,MXDEPL
         ENDIF
C        LE PROCHAIN TEMPS DE STOCKAGE DU PROCHAIN VECTEUR DEPLACEMENT
         TESTDE = TESTDE + DTSTDE
      ENDIF
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10040) TEMPS
         WRITE(IMPRIM,10099) DT, NBPATE, MXDEPL, DTSTDE
      ELSE
         WRITE(IMPRIM,20040) TEMPS
         WRITE(IMPRIM,20099) DT, NBPATE, MXDEPL, DTSTDE
      ENDIF
C
10099 FORMAT(' PAS DE TEMPS',                      T41,'=',G15.7,/
     %       ' NOMBRE de PAS de TEMPS du CALCUL',  T41,'=',I6,/
     %       ' NOMBRE de STOCKAGES du DEPLACEMENT',T41,'=',I6,/
     %       ' PAS DE TEMPS ENTRE 2 STOCKAGES',    T41,'=',G15.7)
20099 FORMAT(' STEP OF TIME',                      T41,'=',G15.7,/
     %       ' NUMBER of STEPS of TIME ',          T41,'=',I6,/
     %       ' NUMBER of STORAGES of DISPLACEMENT',T41,'=',I6,/
     %       ' STEP OF TIME BETWEEN 2 STORAGES',   T41,'=',G15.7)
C
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10010) NDIM, NBNOEU, NTDLDE, TPSINI, TPSFIN, DT
      ELSE
         WRITE(IMPRIM,20010) NDIM, NBNOEU, NTDLDE, TPSINI, TPSFIN, DT
      ENDIF
C
      RETURN
      END
