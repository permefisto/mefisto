      SUBROUTINE E43LAG ( X,      NDSM,   NBPOLY, NBPOF,  NOPOF,
     &                   POLYF,  DPOLYF, NPIF,   POIDSF,
     &                   NOOBSF, NUMISU, NUMASU, LTDESU,
     &                   FORCE,
     &                   BE )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   CONTRIBUTION D UNE FACE AUX SECONDS MEMBRES ELEMENTAIRES
C -----   D UN ELEMENT TRIDIMENSIONNEL LAGRANGE DEGRE 1 2 ISOPARAMETRIQUE
C         EN ELASTICITE LINEAIRE SAUF TETR 3P1D
C
C ENTREES:
C --------
C X      : COORDONNEES DES POINTS DE L ELEMENT FINI (NBPOLY,3)
C NDSM   : NOMBRE DE SECONDS MEMBRES
C
C NBPOLY : NOMBRE DE POINTS DE L ELEMENT FINI TRIDIMENSIONNEL COMPLET
C NBPOF  : NOMBRE DE POINTS DEFINISSANT L APPLICATION G DE LA FACE
C NOPOF  : NO DANS L ELEMENT FINI DES NBPOF POINTS DE LA FACE
C
C POLYF  : POLYF(I,J)=VALEUR DU I-EME POLYNOME AU J-EME POINT D INTEGRAT
C DPOLYF : DPOLYF(I,J,L)=VALEUR DE LA I-EME DERIVEE DU J-EME
C                        POLYNOME AU L-EME POINT D INTEGRATION DE LA FACE
C NPIF   : NOMBRE DE POINTS D INTEGRATION SUR CETTE FACE
C POIDSF : LES NPIF POIDS DE LA FORMULE D INTEGRATION DE LA FACE
C
C NOOBSF : NUMERO DE SURFACE DE LA FACE DE L'EF
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES THERMIQUES
C          DES OBJETS SURFACES
C
C FORCE  : PRESSION FORCE(NDSM,3) TABLEAU REMPLI DANS CE SP
C
C SORTIES:
C --------
C BE     : LES NDSM SECONDS MEMBRES ELEMENTAIRES
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS       AVRIL 1999
C23456---------------------------------------------------------------012
      include"./incl/donela.inc"
      REAL              X(NBPOLY,3)
      INTEGER           NOPOF(NBPOF)
      INTEGER           LTDESU(1:MXDOEL,NUMISU:NUMASU)
      DOUBLE PRECISION  POLYF(NBPOF,NPIF),
     &                  DPOLYF(2,NBPOF,NPIF),
     &                  POIDSF(NPIF),
     &                  FORCE(NDSM,3),
     &                  BE(NDSM,*)
C
      DOUBLE PRECISION  DELTA, D,
     &                  GL(3),VN(3),
     &                  DGL(2,3), DGLN
C
C     BOUCLE SUR LES NPIF POINTS D INTEGRATION DE LA FACE
C     ---------------------------------------------------
      DO 100 L=1,NPIF
C
C        RECHERCHE DES COORDONNEES DU POINT ET DU JACOBIEN DE G
         CALL E23LAG( NBPOLY, NBPOF, NOPOF,
     &               POLYF(1,L), DPOLYF(1,1,L), X,
     &               GL, DGL, DELTA )
C        EN SORTIE:  GL=LES 3 COORDONNEES DU POINT D'INTEGRATION L
C                   DGL=LES 3 COMPOSANTES DES 2 VECTEURS TANGENTS
C
C        CALCUL DU VECTEUR NORMAL A LA FACE AU POINT D'INTEGRATION L
         CALL VECNOR( DGL, DGLN, VN )
C
C        CALCUL DES FORCES EN CE POINT D'INTEGRATION DE LA FACE
         CALL REFORC( 3, NOOBSF, 3,
     &                GL(1), GL(2), GL(3), VN(1), VN(2), VN(3),
     &                LTDESU(LPFORC,NOOBSF), FORCE )
C
C        CONTRIBUTION DU POIDS DE LA FORMULE D'INTEGRATION
         DELTA = DELTA * POIDSF(L)
C
         DO 30 I=1,NBPOF
C
C           PRODUIT PAR LA VALEUR DU POLYNOME AU POINT D'INTEGRATION
            D = DELTA * POLYF(I,L)
C
C           NUMERO ELEMENTAIRE DU NOEUD
            NU = NOPOF(I)
C
            DO 25 K=1,3
C
C              NUMERO DU DL
               II = 3 * NU - 3 + K
C
               DO 20 N=1,NDSM
                  BE(N,II) = BE(N,II) + D * FORCE(N,K)
 20            CONTINUE
C
 25         CONTINUE
 30      CONTINUE
C
100   CONTINUE
C
      RETURN
      END
