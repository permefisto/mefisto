      SUBROUTINE MXCON2( NBELFI , NBPIEF , NDIMES , COPIEF , COIN   ,
     %                   CONTR  , POIDS  , CONTMN , CONTMX ,
     %                   NTDL   , MISTRE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES VALEURS DES CONTRAINTES MIN ET MAX , SUIVANT
C -----    LE CRITERE DE VON MISES OU CELUI DE TRESCA , PUIS
C          L'HEXAEDRE ENGLOBANT TOUS LES POINTS
C
C ENTREES :
C ---------
C NBELFI : NOMBRE D'ELEMENTS FINIS DE CE TYPE
C NBPIEF : NOMBRE DE POINTS DE CALCUL SUR CHAQUE ELEMENT FINI
C NDIMES : DIMENSION DE L'ESPACE
C COPIEF : COORDONNEES DES POINTS DES ELEMENTS FINIS
C CONPRI : LES CONTRAINTES PRINCIPALES EN TOUS LES POINTS DE TOUS LES EF
C NCAS   : NUMERO DU CAS A TRAITER
C NTDL   : NOMBRE D'INCONNUES
C MISTRE : 1 POUR VON MISES , 2 POUR TRESCA
C
C MODIFIES :
C ----------
C COIN   : MIN ET MAX DES COORDONNEES DES POINTS OU SONT CALCULEES
C          LES CONTRAINTES
C CONTR  : LE TABLEAU DES CONTRAINTES SUIVANT MISTRE
C POIDS  : TABLEAU DE PONDERATION
C CONTMN : VALEUR DE LA CONTRAINTE MINIMALE
C CONTMX : VALEUR DE LA CONTRAINTE MAXIMALE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS   NOVEMBRE 1989
C          PASCAL JOLY     ANALYSE NUMERIQUE UPMC PARIS   DECEMBRE 1990
C23456---------------------------------------------------------------012
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      REAL              COPIEF(1:NBELFI,1:NBPIEF,1:NDIMES),COIN(6,2)
      DOUBLE PRECISION  CONTR(1:NTDL),POIDS(1:NTDL)
      INTRINSIC         REAL
C
      IF (MISTRE.EQ.1) THEN
C
C     LE CRITERE DE VON MISES
C     -----------------------
C     VON MISES = SQRT( (S1-S2)**2 + (S2-S3)**2 + ( S3-S1)**2 )
C     LE PARCOURS DES COORDONNEES DES POINTS ST DES CONTRAINTES
      DO 30 K=1,NBELFI
         DO 20 J=1,NBPIEF
            DO 10 I=1,NDIMES
C              LE MINIMUM DES COORDONNEES
               COIN(I,1) = MIN( COIN(I,1) , COPIEF(K,J,I) )
C              LE MAXIMUM DES COORDONNEES
               COIN(I,2) = MAX( COIN(I,2) , COPIEF(K,J,I) )
C              CALCUL DES CONTRAINTES AUX NOEUDS
 10         CONTINUE
 20      CONTINUE
 30   CONTINUE
C
      ELSE
C
C     LE CRITERE DE TRESCA
C     --------------------
C     TRESCA    = MAX (I .NE. J) ABS( SI - SJ )
C     LE PARCOURS DES COORDONNEES DES POINTS ST DES CONTRAINTES
      DO 31 K=1,NBELFI
         DO 21 J=1,NBPIEF
            DO 11 I=1,NDIMES
C              LE MINIMUM DES COORDONNEES
               COIN(I,1) = MIN( COIN(I,1) , COPIEF(K,J,I) )
C              LE MAXIMUM DES COORDONNEES
               COIN(I,2) = MAX( COIN(I,2) , COPIEF(K,J,I) )
C              CALCUL DES CONTRAINTES AUX NOEUDS
 11         CONTINUE
 21      CONTINUE
 31   CONTINUE
C
      ENDIF
c
C
C     CALCUL DES EXTREMES
C     -------------------
      CONTMN = RINFO( 'GRAND' )
      CONTMX = -CONTMN
C
      DO 40 N=1,NTDL
         CONTR(N) = CONTR(N) / POIDS(N)
         CONT     = REAL( CONTR(N) )
         CONTMN = MIN( CONTMN , CONT )
         CONTMX = MAX( CONTMX , CONT )
 40   CONTINUE
C
      RETURN
      END
