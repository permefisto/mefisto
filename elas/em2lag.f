      SUBROUTINE EM2LAG( D2PI,   NOAXIS,
     &                   NOOBSF, NUMISU, NUMASU, LTDESU,
     &                   NBPOLY, NPI,    POLY,
     &                   F1, F2, POIDEL, MASS,
     &                   NCODSM, MASSEF )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DE LA MATRICE DE MASSE STOCKEE SOUS FORME SYMETRIQUE
C -----    POUR UN EF 2D LAGRANGE ISOPARAMETRIQUE (TRIANGLE OU QUADRANGLE)
C
C ENTREES:
C --------
C D2PI   : 2 FOIS PI
C NOAXIS : 1 SI PROBLEME AXISYMETRIQUE, 0 SINON
C
C NOOBSF : NUMERO DE L'OBJET SURFACE DE CET ELEMENT
C NUMISU : NUMERO MINIMAL DES SURFACES DE L'OBJET
C NUMASU : NUMERO MAXIMAL DES SURFACES DE L'OBJET
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES MASSE
C          DES SURFACES DE L'OBJET
C
C NBPOLY : NOMBRE DE POLYNOMES DE L'ELEMENT SURFACE
C NPI    : NOMBRE DE POINTS D INTEGRATION NUMERIQUE SUR LA SURFACE
C POLY   : VALEUR DES POLYNOMES DE BASE AUX POINTS D'INTEGRATION
C          POLY(I,L)= P(I) (XL)
C
C F1     : COORDONNEES XX DES NPI POINTS D INTEGRATION DE L'EF
C F2     : COORDONNEES YY DES NPI POINTS D INTEGRATION DE L'EF
C POIDEL : DELTA * POIDS(NPI) DES NPI POINTS D INTEGRATION
C
C AUXILIAIRE:
C -----------
C MASS   : DENSITE SURFACIQUE DE MASSE AUX NPI POINTS D'INTEGRATION
C
C SORTIES:
C --------
C NCODSM : CODE DE STOCKAGE DE LA MATRICE ELEMENTAIRE DE MASSE
C          1 SYMETRIQUE PLEINE
C MASSEF : MATRICE DE MASSE STOCKEE   1
C                                     2 3
C                                     4 5 6
C                                     7 ...
C          RANGEE PAR NOEUDS ( U1S1,U2S1, U1S2,U2S2, U1S3,U2S3, ... )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    DECEMBRE 1997
C23456---------------------------------------------------------------012
      include"./incl/donela.inc"
      DOUBLE PRECISION  D2PI, DELTA, MASS(NPI), MASSEF(1:*), S
      DOUBLE PRECISION  POLY(NBPOLY, NPI),
     %                  F1(NPI), F2(NPI), POIDEL(NPI), XYZPI(3)
      INTEGER           LTDESU( 1:MXDOEL, NUMISU:NUMASU )
C
C     DENSITE DE MASSE AUX POINTS D'INTEGRATION
C     =========================================
      DO 5 L=1,NPI
C
C        RECHERCHE DE LA DENSITE DE MASSE AU POINT D'INTEGRATION L
         XYZPI(1) = F1(L)
         XYZPI(2) = F2(L)
         XYZPI(3) = 0D0
         CALL REMASS( 3, NOOBSF, 3, XYZPI,
     %                LTDESU(LPMASS,NOOBSF), MASS(L) )
C
         IF( NOAXIS .NE. 0 ) THEN
C           EF AXISYMETRIQUE
            DELTA = POIDEL(L) * D2PI * F1(L)
         ELSE
C           EF NON AXISYMETRIQUE
            DELTA = POIDEL(L)
         ENDIF
C
C        MASSE = MASSE * POIDS * DELTA
         MASS(L) = MASS(L) * DELTA
C
 5    CONTINUE
C
C     LE CALCUL DE LA MATRICE DE MASSE
C     --------------------------------
C     INITIALISATION A ZERO
      DO 10 N = 1, NBPOLY * ( 2 * NBPOLY + 1 )
         MASSEF(N) = 0D0
 10   CONTINUE
C
      DO 40 I=1,NBPOLY
C
C        LE SECOND DL DU NOEUD I
         I2  = I  * 2
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I2-1=I1
         MI2 = I2 * ( I2 - 1 ) / 2
C        LE PREMIER DL DU NOEUD I
         I1  = I2 - 1
C        L'INDICE DU COEFFICIENT DIAGONAL DE LA LIGNE I1-1
         MI1 = MI2 - I1
C
         DO 30 J=1,I
C
C           LE CALCUL DU COEFFICIENT I,J DE LA MATRICE INTEGRALE (ROT t[P] [P])
            S = 0.D0
            DO 20 L=1,NPI
               S = S + MASS(L) * POLY(J,L) * POLY(I,L)
 20         CONTINUE
C
C           CE COEFFICIENT I,J EST A PLACER POUR SES 2 COMPOSANTES
C           DANS LA RENUMEROTATION PAR NOEUDS DANS LA MATRICE DE MASSE
C           COMPTE TENU DE SON STOCKAGE SYMETRIQUE TRIANGULAIRE INFERIEUR
C           LE SECOND DL DU NOEUD J<=I
            J2 =  J * 2
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I2,J2
            N  = J2 + MI2
            MASSEF(N) = S
C
C           LE PREMIER DL DU NOEUD J<=I
            J1 = J2 - 1
C           L'INDICE DANS [M] SYMETRIQUE DU COEFFICIENT I1,J1
            N  = J1 + MI1
            MASSEF(N) = S
C
 30      CONTINUE
C
 40   CONTINUE
C
C     CODE MATRICE ELEMENTAIRE DE MASSE SYMETRIQUE
C     STOCKEE SOUS FORME D'UNE MATRICE TRIANGULAIRE INFERIEURE
      NCODSM = 1
      RETURN
      END
