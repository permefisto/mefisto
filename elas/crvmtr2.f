      SUBROUTINE CRVMTR2( MISTRE, NBELFI, NBPIEF, NDIMES, NBCAS,
     %                    COPIEX, CONPRI, NCAS,
     %                    CONTR,  A,      CONTMN, CONTMX, NBPIEX,
     %                    NOPIEX, NOINTI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES VALEURS DU CRITERE DE VON MISES OU DE TRESCA
C -----    AUX SOMMETS DE L'EF AVEC INTERPOLATION DE DEGRE 1
C ENTREES:
C --------
C MISTRE : 1 POUR VON MISES , 2 POUR TRESCA
C NBELFI : NOMBRE D'ELEMENTS FINIS DE CE TYPE
C NBPIEF : NOMBRE DE POINTS DE CALCUL SUR CHAQUE ELEMENT FINI
C NDIMES : DIMENSION DE L'ESPACE
C NBCAS  : NOMBRE DE CAS
C COPIEX : COORDONNEES DES POINTS D'INTEGRATION DES ELEMENTS FINIS
C CONPRI : LES CONTRAINTES PRINCIPALES EN TOUS LES POINTS DE TOUS LES EF
C NCAS   : NUMERO DU CAS A TRAITER
C A      : TABLEAU UTILISE POUR LA RESOLUTION DU SYSTEME LINEAIRE
C NBNOIN : NBRE DE NOEUDS SUR LESQUELS ON INTERPOLE
C NOPIEX : NUMEROS DES POINTS D'INTEGRATION UTILISES
C NOINTI : NO D INTERPOLATION POUR LE CRITERE DE VON MISES TRESCA
C
C MODIFIES:
C ---------
C CONTR  : LE TABLEAU DU CRITERE DE VON MISES OU TRESCA
C CONTMN : CONTRAINTE MIN
C CONTMX : CONTRAINTE MAX
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE UPMC PARIS   NOVEMBRE 1989
C          GUILLAUME GERBER ANALYSE NUMERIQUE UPMC PARIS    JANVIER 2000
C AUTEUR : Alain PERRONNET Laboratoire J-L. LIONS UPMC PARIS    MAI 2007
C23456---------------------------------------------------------------012
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      DOUBLE PRECISION  COPIEX(3,8)
      DOUBLE PRECISION  CONPRI(1:NBELFI,1:NBPIEF,1:NDIMES,1:NBCAS)
      DOUBLE PRECISION  CONTR(NBPIEX,NBELFI)
      DOUBLE PRECISION  SIGMA(3),XPI,YPI,ZPI
      DOUBLE PRECISION  FBASE(30),A(NBPIEX,NBPIEX+NBELFI)
      REAL              CONTMN, CONTMX, CONT
      INTEGER           NOPIEX(30)
      INTRINSIC         REAL
C
C     CALCUL DES VALEURS DES FONCTIONS DE BASE AU NIVEAU DES POINTS
C     D'INTEGRATION ET REMPLISSAGE DE LA MATRICE A DU SYSTEME D'EXTRAPOLATION
C     SOMME sur i POLYBASEi(PT INTEGRATIONj) COEFi = CRITERE(PT INTEGRATIONj)
C     INVERSER LE SYSTEME => VALEUR DU CRITERE AU SOMMET i = COEFi
      DO 15 I=1,NBPIEX
         XPI = COPIEX(1,I)
         YPI = COPIEX(2,I)
         ZPI = COPIEX(3,I)
         CALL INTERP( NOINTI, XPI, YPI, ZPI, NBN, FBASE )
         DO 10 J=1,NBPIEX
            A(I,J) = FBASE(J)
   10    CONTINUE
   15 CONTINUE
C
C     CALCUL DU CRITERE DE VON MISES OU DE TRESCA
C     AU NIVEAU DES POINTS D'INTEGRATION
C     REMPLISSAGE DES SECONDS MEMBRES DU SYSTEME LINEAIRE
C     POUR EXTRAPOLER LES CONTRAINTES AUX SOMMETS DE L'EF
      DO 40 K=1,NBELFI
         DO 30 I=1,NBPIEX
            DO 20 J=1,NDIMES
               SIGMA(J) = CONPRI(K,NOPIEX(I),J,NCAS)
   20       CONTINUE
            IF( NDIMES .EQ. 2 ) THEN
C              VON MISES et TRESCA
               A(I,NBPIEX+K) = ABS( SIGMA(1) - SIGMA(2) )
            ELSE
               IF( MISTRE .EQ. 1 ) THEN
C                 VON MISES
                  A(I,NBPIEX+K) = SQRT( ( SIGMA(1) - SIGMA(2) )**2
     %                                + ( SIGMA(2) - SIGMA(3) )**2
     %                                + ( SIGMA(3) - SIGMA(1) )**2 )
               ELSE
C                 TRESCA
                  A(I,NBPIEX+K) = MAX( ABS( SIGMA(1) - SIGMA(2) ),
     %                                 ABS( SIGMA(2) - SIGMA(3) ),
     %                                 ABS( SIGMA(3) - SIGMA(1) ) )
               ENDIF
            ENDIF
   30    CONTINUE
   40 CONTINUE
C
C     EXTRAPOLATION AUX SOMMETS DU CRITERE DES CONTRAINTES PAR
C     RESOLUTION DE 1 SYSTEME LINEAIRE SUR EF DE REFERENCE
C     ET NBELFI SECONDS MEMBRES
      CALL GAUSPT( NBPIEX, NBELFI, A, IERR )
C
C     REMPLISSAGE DU TABLEAU CRITERE
      DO 70 K=1,NBELFI
         DO 60 J=1,NBPIEX
            CONTR(J,K) = A(J,NBPIEX+K)
   60    CONTINUE
   70 CONTINUE
C
C     CALCUL DES CRITERES EXTREMES
C     ----------------------------
      DO 90 N=1,NBELFI
         DO 80 M=1,NBPIEX
            CONT   = REAL( CONTR(M,N) )
            CONTMN = MIN( CONTMN , CONT )
            CONTMX = MAX( CONTMX , CONT )
   80    CONTINUE
   90 CONTINUE
C
      END
