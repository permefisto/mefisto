      SUBROUTINE TRDE32( MOFACE, MXFACE, LFACES, NBFAFR, NBFATR,
     %                   MOARFR, MXARFR, LAREFR, NBARFR,
     %                   NUFAAR, XYZPOI,
     %                   NCAS,   NTDL,   NBVECT, DEPLAC, AMPLID )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER EN BLANC LES ARETES FRONTALIERES ET EN COULEURS
C ----     LES FACES FRONTIERES DEFORMEES AVEC AMPLIFICATION
C          SELON L'ALGORITHME DU PEINTRE
C
C ENTREES:
C --------
C MOFACE : NOMBRE DE MOTS PAR FACE DU TABLEAU NFACE DES FACES
C MXFACE : NOMBRE DE FACES DU TABLEAU NFACE
C LFACES : TABLEAU NUMERO DES FACES, LIEN, NO DES CUBES
C          LFACES(1,I)= NO DU 1-ER  SOMMET DE LA FACE
C          LFACES(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LFACES(3,I)= NO DU 3-EME SOMMET DE LA FACE
C          LFACES(4,I)= NO DU 4-EME SOMMET DE LA FACE, 0 SI TRIANGLE
C NBFAFR : NOMBRE DE FACES FRONTALIERES
C NBFATR : NOMBRE DE FACES A TRACER (MULTIPLE DE NBFAFR)
C
C MOARFR : NOMBRE DE MOTS PAR ARETE FRONTALIERE DU TABLEAU LAREFR
C MXARFR : NOMBRE DE FACES DU TABLEAU LAREFR
C LAREFR : TABLEAU NUMERO DES 2 SOMMETS ET LIEN DES ARETES FRONTALIERES
C          LAREFR(1,I)= NO DU 1-ER  SOMMET DE L'ARETE FRONTALIERE
C          LAREFR(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LAREFR(3,I)= 0 OU NUMERO DE L'ARETE FRONTALIERE SUIVANTE
C                       DANS LE HACHAGE
C NBARFR : NOMBRE D'ARETES FRONTALIERES A TRACER SANS DEFORMATION
C
C NUFAAR : NUMERO DES FACES ET ARETES SELON LEUR DISTANCE DECROISSANTE
C          A L'OEIL (RESULTAT DU TRI)
C XYZPOI : COORDONNEES DES POINTS=NOEUDS DE L'OBJET
C
C NCAS   : NUMERO DU CAS A TRACER
C NTDL   : NOMBRE TOTAL DE DEGRES DE LIBERTE
C NBVECT : NOMBRE TOTAL DE VECTEURS DEPLACEMENTS
C DEPLAC : LES COMPOSANTES DES DEPLACEMENTS
C AMPLID : FACTEUR D'AMPLIFICATION DES DEPLACEMENTS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS        MARS 1997
C2345X7..............................................................012
      PARAMETER   ( LIGCON=0, LIGTIR=1 )
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/gsmenu.inc"
      INTEGER           LFACES(1:MOFACE,1:MXFACE)
      INTEGER           NUFAAR(1:NBFATR+NBARFR)
      INTEGER           LAREFR(1:MOARFR,1:MXARFR)
      REAL              XYZPOI(1:3,1:*)
      DOUBLE PRECISION  DEPLAC(NTDL,NBVECT), PI
C
      REAL              XYZFFD(1:3,1:4)
      REAL              CNORFA(1:3)
      INTRINSIC         REAL
C
      PI = ATAN( 1D0 ) * 4D0
C
C     LA DIRECTION DE VISEE PTV-OEIL
      DIREVI(1) = AXOEIL(1) - AXOPTV(1)
      DIREVI(2) = AXOEIL(2) - AXOPTV(2)
      DIREVI(3) = AXOEIL(3) - AXOPTV(3)
C
C     LA DIRECTION DE VISEE NORMALISEE A 1.
      CALL NORMER( 3, DIREVI, IERR )
      IF( IERR .NE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'OEIL et POINT VU sont IDENTIQUES'
            KERR(2) = 'LES MODIFIER'
         ELSE
            KERR(1) = 'EYE and AIMED POINT are EQUAL'
            KERR(2) = 'MODIFY THEM'
         ENDIF
         CALL LEREUR
         RETURN
      ENDIF
C
C     LE NOMBRE DE COULEURS DISPONIBLES
      NBCOUL = NDCOUL - N1COUL + 1
C
C     LA PALETTE DES GRIS
CCC      CALL PALCDE(10)
CCC      CALL EFFACE
C
C     LES ARETES ONT UNE EPAISSEUR 1
      CALL XVEPAISSEUR( 1 )
C
C     LE TRACE DES FACES EN COMMENCANT PAR LES PLUS ELOIGNEES
C     =======================================================
      MXFAC1 = MXFACE + 1
      NBVUES = NBFATR / NBFAFR - 1
      DO 50 NF = 1, NBFATR + NBARFR
C
C        LE NUMERO DE LA FACE OU ARETE LA PLUS ELOIGNEE NON ENCORE TRACEE
         NAOF = NUFAAR( NF )
C
         IF( NAOF .GT. 0 ) THEN
C
C           TRACE DE LA FACE NAOF FRONTALIERE DEFORMEE
C           ------------------------------------------
            IF( NAOF .LE. MXFACE ) THEN
               AMPLDE = AMPLID
            ELSE
C              PLUSIEURS VUES POUR TRACER LA VIBRATION D'UNE FREQUENCE PROPRE
               NOVUE = NAOF / MXFAC1
               NAOF  = NAOF - NOVUE * MXFAC1
C              FACTEUR DE DEFORMATION POUR LA FREQUENCE PROPRE DE CETTE VUE
ccc               AMPLDE = AMPLID * COS( ( PI * NOVUE ) / NBVUES )
               AMPLDE = AMPLID
            ENDIF
C
C           LE NOMBRE DE SOMMETS DE LA FACE
            IF( LFACES(4,NAOF) .EQ. 0 ) THEN
               NSF = 3
            ELSE
               NSF = 4
            ENDIF
C
C           CONSTRUCTION DU TABLEAU DES 3 COORDONNEES DES NSF SOMMETS DE LA FACE
            DO 10 I=1,NSF
C              LE NUMERO DU SOMMET I DE LA FACE NAOF DU TABLEAU LFACES
               NS = LFACES( I, NAOF )
               ND = 3 * NS - 3
               DO 5 K=1,3
C                 LA COORDONNEE K AVEC AMPLIFICATION DU DEPLACEMENT
                  XYZFFD(K,I) = REAL( XYZPOI(K,NS)
     %                              + AMPLDE * DEPLAC(ND+K,NCAS) )
  5            CONTINUE
 10         CONTINUE
C
C           CNORFA LES COORDONNEES DE LA NORMALE A LA FACE (NORME=1)
            M = 0
            CALL NORMTQ( NSF, XYZFFD,  CNORFA, M )
            IF( M .NE. 0 ) GOTO 50
C
C           LE PRODUIT SCALAIRE DIRECTION VISEE ET NORMALE
            R = PROSCR( DIREVI, CNORFA, 3 )
            R = 1.0 - ABS(R)
C
C           LA COULEUR DE TRACE DE LA FACE
            NC = NINT( NBCOUL * R + N1COUL )
C
C           LE TRACE DE LA FACE DEFORMEE
            CALL XVTYPETRAIT( LIGCON )
            CALL FACE3D( NC, NCOUAD, NSF, XYZFFD )
C
         ELSE
C
C           TRACE DE L' ARETE NAOF FRONTALIERE NON DEFORMEE
C           -----------------------------------------------
C           LE NUMERO DE L'ARETE DANS LE TABLEAU LAREFR
            NAOF = -NAOF
            CALL XVTYPETRAIT( NTLAFR )
            CALL TRAIT3D( NCOUAF, XYZPOI(1,LAREFR(1,NAOF)),
     %                            XYZPOI(1,LAREFR(2,NAOF)) )
         ENDIF
C
 50   CONTINUE
C
C     RETOUR AU TRACE CONTINU DES LIGNES
      CALL XVTYPETRAIT( LIGCON )
      CALL XVEPAISSEUR( 1 )
C
      RETURN
      END
