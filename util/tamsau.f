      SUBROUTINE TAMSAU( NOTAM1 , NBVAR2 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : AUGMENTER LE TABLEAU MS NOTAM1 AFIN QU'IL AIT EN SORTIE
C ----- NBVAR2 VARIABLES DU TYPE DEJA DECLARE
C       SI LE TYPE EST CARACTERE ,NBVAR2 EST LE PRODUIT DU NOMBRE DE
C       CHAINES PAR LE NOMBRE DE CARACTERES PAR CHAINE.
C***********************************************************************
C ATTENTION : SOUS-PROGRAMME DEPENDANT MACHINE PAR LA DECLARATION DE
C =========== MCK ET MCG
C***********************************************************************
C ENTREE :
C --------
C NOTAM1 : NUMERO DU TABLEAU MS A AUGMENTER
C          CE NUMERO EST LE MEME EN ENTREE ET SORTIE
C          MAIS L'ADRESSE DU TABLEAU EN MCN OU MCK EST DIFFERENTE
C          => LA FERMETURE DU TABLEAU EN SORTIE POUR FORCER
C             L'UTILISATEUR A OUVRIR LE TABLEAU => NOUVELLE ADRESSE
C NBVAR2 : NOMBRE DE VARIABLES > NOMBRE DE VARIABLES DE SA DECLARATION
C
C ATTENTION : EN SORTIE LE TABLEAU DE NBVAR2 VARIABLES N'EST PAS OUVERT.
C =========== AFIN D' OBTENIR SON ADRESSE EN MC FAIRE
C             CALL TAMSOU( NOTAM1 , MCTAM1 )
C
C ATTENTION : LES 2 TABLEAUX AVANT ET APRES DOIVENT TENIR SIMULTANEMENT
C =========== DANS MCN OU MCK
C
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1985
C.......................................................................
      include"./incl/ppmck.inc"
      include"./incl/motmcg.inc"
      include"./incl/msvaau.inc"
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      COMMON / MSSFTA /  MSV1(10),M1TAMS,NSV2(13),MGZLMN,NSV3(4)
      COMMON / UNITES /  LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSIMTA /  NOIMPR
C
      CHARACTER*9        KTYPE
      CHARACTER*16       ERREUR
      DATA               ERREUR / 'ERREUR TAMSAU :' /
C
C     LES CARACTERISTIQUES DU TABLEAU D ENTREE  NOTAM1
      CALL TAMSTV( NOTAM1, KTYPE, NBVAR1 )
CCC      WRITE(IMPRIM,*) 'ENTREE TAMSAU: NOTAM1=',NOTAM1,
CCC     % ' NBVAR1=',NBVAR1,' NBVAR2=',NBVAR2,' KTYPE=',KTYPE
C
C     VERIFICATION DES TYPES , NOMBRES DE VARIABLES
C     =============================================
      IF( NBVAR2 .LT. NBVAR1 ) THEN
         WRITE(IMPRIM,10000) ERREUR,NOTAM1,NBVAR1,NBVAR2
10000 FORMAT(A16,' LE TABLEAU MS',I10,' DE',I10,' VARIABLES NE PEUT ETRE
     % ALLONGE A',I10,' VARIABLES.PAS DE MODIFICATION.VOIR SP TAMSRA'/)
C
      ELSE IF( NBVAR2 .EQ. NBVAR1 ) THEN
         RETURN
C
      ELSE IF( NBVAR2 .LE. 0 ) THEN
         WRITE(IMPRIM,10005) ERREUR,NOTAM1,NBVAR2
10005 FORMAT(A16,' LE TABLEAU MS',I8,' NE PEUT AVOIR',I8,' VARIABLES'/)
         CALL ARRET( 100 )
      ENDIF
C
C     OUVERTURE DU TABLEAU NOTAM1
      CALL TAMSOU( NOTAM1 , MCTAM1 )
C
C     DECLARATION DU TABLEAU AUGMENTE
C     ===============================
      CALL TNMSDC( KTYPE , NBVAR2 , NOTAM2 )
C
C     OUVERTURE DU TABLEAU AUGMENTE
      CALL TAMSOU( NOTAM2 , MCTAM2 )
C
C     NOMBRE DE MOTS DE L'ANCIEN TABLEAU
      I = NUMTYP( KTYPE )
      MOT = MOTTAB( I , NBVAR1 )
C
C     COPIE DU PREMIER DANS LE SECOND TABLEAU MS
      IF( I .EQ. NOTYPK ) THEN
C        TABLEAU DE CARACTERES
         CALL TRTKTK( MCK(MCTAM1) , MCK(MCTAM2) , MOT )
      ELSE
C        TABLEAU NUMERIQUE
         CALL TRTATA( MCN(MCTAM1) , MCN(MCTAM2) , MOT )
      ENDIF
C     WRITE(IMPRIM,*) ' COPIE DE',NOTAM1,' ADRESSE',MCTAM1,
C    %                ' DANS',NOTAM2,' ADRESSE',MCTAM2,' ET PERMUTATION'
C
C     PERMUTATION DES DESCRIPTEURS DES 2 TABLEAUX TAMS
C     PERMISE CAR ILS SONT TOUS LES 2 OUVERTS
C     ================================================
C     DECLARATION OUVERTURE D'UN TABLEAU MC POUR LA PERMUTATION
C     CAR L'ADRESSE DU MG.. PEUT VARIER ENTRE 2 APPELS DE TAMSRE
      CALL TAMCDC( 'ENTIER' , M1TAMS , MCG(MGZLMN) , MNTAMC )
C
C     L'ADRESSE DU DESCRIPTEUR DU TABLEAU NOTAM1
      CALL TAMSRE( NOTAM1 , MGTAM1 )
C
C     COPIE DE CE DESCRIPTEUR DANS LE TABLEAU AUXILIAIRE
      CALL TRTATA( MCG(MGTAM1) , MCN(MNTAMC) , M1TAMS )
C
C     L'ADRESSE DU DESCRIPTEUR DU TABLEAU NOTAM2
      CALL TAMSRE( NOTAM2 , MGTAM2 )
C
C     PERMUTATION DU DESCRIPTEUR DU 2ME TABLEAU ET
C     DE LA COPIE DU DESCRIPTEUR DU 1-ER TABLEAU
      DO 10 I = 0 , M1TAMS-1
         MN = MCN( MNTAMC + I)
         MCN( MNTAMC + I ) = MCG( MGTAM2 + I )
         MCG( MGTAM2 + I ) = MN
 10   CONTINUE
C
C     L'ADRESSE DU DESCRIPTEUR DU TABLEAU NOTAM1
      CALL TAMSRE( NOTAM1 , MGTAM1 )
C
C     LA COPIE DU DESCRIPTEUR DU NOUVEAU DANS L'ANCIEN
      CALL TRTATA( MCN(MNTAMC) , MCG(MGTAM1) , M1TAMS )
C
C     DESTRUCTION DU TABLEAU MC AUXILIAIRE
      CALL TAMCDS( 'ENTIER' , M1TAMS , MCG(MGZLMN) , MNTAMC )
C
C     DESTRUCTION DE L'ANCIEN TABLEAU MS EN POSITION NOTAM2
      CALL TAMSDS( NOTAM2 )
C
C     FERMETURE DU TABLEAU NOTAM1
      CALL TAMSFE( NOTAM1)
C
      IF( NOIMPR .NE. 0 ) WRITE(IMPRIM,19900) NOTAM1,NBVAR1,
     %                    NBVAR2,KTYPE,MCTAM1,MCTAM2
19900 FORMAT(' TAMSAU:LE TABLEAU MS',I9,' DE',I9,
     %' VARIABLES PASSE A',I9,' VARIABLES DE TYPE ',A9/
     %' SON ADRESSE',I8,' DEVIENT',I8/)
C
      END
