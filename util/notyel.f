      INTEGER FUNCTION NOTYEL( NCOGEL, NOINTE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETOURNER LE NUMERO DE L'ELEMENT FINI POUR
C -----    LE CODE GEOMETRIQUE NCOGEL ET L'INTERPOLATION NOINTE
C
C ENTREES :
C ---------
C NCOGEL : CODE GEOMETRIQUE 1 POINT , 2 SEGMENT , 3 TRIANGLE ,
C          4 QUADRANGLE, 5 TETRAEDRE, 6 PENTAEDRE, 7 HEXAEDRE,
C          8 6-CUBE, 9 PYRAMIDE
C NOINTE : NUMERO DE L'INTERPOLATION CHOISIE  (CF DFTOPO)
C          1  'AXISYMETRIQUE_DEGRE_1',
C          2  'AXISYMETRIQUE_DEGRE_2',
C          3  'LAGRANGE_DEGRE_1',
C          4  'LAGRANGE_DEGRE_2'
C
C SORTIE :
C --------
C NOTYEL : =0 SI ERREUR
C          >0 SI LE NO RETOURNE DU TYPE DE L'EF EST CORRECT
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE UPMC PARIS    OCTOBRE 1989
C2345X7..............................................................012
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
C
C     AIGUILLAGE SELON L'INTERPOLATION
      GOTO( 100, 200, 300, 400 ), NOINTE
C
C     AXISYMETRIQUE_DEGRE_1
 100  GOTO( 9000, 9000, 130, 140, 9000, 9000, 9000, 9000, 9000 ), NCOGEL
C     TRIANGLE AP1D
 130  NOTYEL = 1
      GOTO 8000
C     QUADRANGLE AQ1C
 140  NOTYEL = 3
      GOTO 8000
C
C     AXISYMETRIQUE_DEGRE_2
 200  GOTO( 9000, 9000, 230, 240, 9000, 9000, 9000, 9000, 9000 ), NCOGEL
C     TRIANGLE AP2C
 230  NOTYEL = 2
      GOTO 8000
C     QUADRANGLE AQ2C
 240  NOTYEL = 4
      GOTO 8000
C
C     LAGRANGE_DEGRE_1
 300  GOTO( 310, 320, 330, 340, 350, 360, 370, 380, 390, 9000 ), NCOGEL
C     NOEUD SOMM
 310  NOTYEL = 27
      GOTO 8000
C     SEGMENT 1P1D
 320  NOTYEL = 28
      GOTO 8000
C     TRIANGLE 2P1D
 330  NOTYEL = 13
      GOTO 8000
C     QUADRANGLE 2Q1C
 340  NOTYEL = 16
      GOTO 8000
C     TETRAEDRE 3P1D
 350  NOTYEL = 19
      GOTO 8000
C     PENTAEDRE 3R1C
 360  NOTYEL = 21
      GOTO 8000
C     HEXAEDRE 3Q1C
 370  NOTYEL = 23
      GOTO 8000
C     6-CUBE 6Q1C
 380  NOTYEL = 30
      GOTO 8000
C     PYRAMIDE 3PY1
 390  NOTYEL = 31
      GOTO 8000
C
C     LAGRANGE_DEGRE_2
 400  GOTO(9000, 420, 430, 440, 450, 460, 470, 9000, 490, 9000 ),NCOGEL
C     SEGMENT 1P2D
 420  NOTYEL = 33
      GOTO 8000
C     TRIANGLE 2P2C
 430  NOTYEL = 15
      GOTO 8000
C     QUADRANGLE 2Q2C
 440  NOTYEL = 18
      GOTO 8000
C     TETRAEDRE 3P2C
 450  NOTYEL = 20
      GOTO 8000
C     PENTAEDRE 3R2C
 460  NOTYEL = 22
      GOTO 8000
C     HEXAEDRE 3Q2C
 470  NOTYEL = 24
      GOTO 8000
C     PYRAMIDE 3PY2
 490  NOTYEL = 32
      GOTO 8000
C
 8000 RETURN
C
C     ERREUR
 9000 NBLGRC(NRERR) = 2
      WRITE(KERR(MXLGER)(1:4),'(I4)') NOINTE
      KERR(1) =  'ERREUR NOTYEL:INTERPOLATION '//KERR(MXLGER)(1:4)
      WRITE(KERR(MXLGER)(1:4),'(I4)') NCOGEL
      KERR(2) =  'CODE GEOMETRIQUE '//KERR(MXLGER)(1:4)//
     %           ' SANS ELEMENT FINI PROGRAMME'
      CALL LEREUR
      NOTYEL = 0
      END
