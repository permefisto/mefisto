      SUBROUTINE TKMSDC( NBCACH , NBCHAI , NOTAMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER UN TABLEAU MS DE NBCHAI CHAINES DE NBCACH CARACTERES
C -----
C ENTREES :
C ---------
C NBCACH : NOMBRE DE CARACTERES PAR CHAINE
C NBCHAI : NOMBRE DE CHAINES DE NBCACH CARACTERES
C
C SORTIE  :
C ---------
C NOTAMS : NUMERO DU TABLEAU MS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
C
C     LE NOMBRE DE CARACTERES REELS DU TABLEAU MS
      NBC = NBCATA( NBCACH , NBCHAI )
C
C     LA DECLARATION PROPREMENT DITE DU TABLEAU MS
      CALL TNMSDC( 'CARACTERE' , NBC , NOTAMS )
C
      RETURN
      END
