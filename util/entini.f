      SUBROUTINE ENTINI( COINMX , DISMIN , BASE , ECHEL , IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CALCUL DE LA BASE ET ECHELLE POUR TRANSFORMER
C -----     DES COORDONNEES REELLES EN COORDONNEES ENTIERES
C           PUIS EFFECTUER LES CALCULS SUR DES ENTIERS MULTI-MOTS
C
C ENTREES :
C ---------
C COINMX  : 3 COORDONNEES DU MINIMUM ET MAXIMUM DES POINTS DU NUAGE
C           COINMX(.,1)=MINIMUM   COINMX(.,2)=MAXIMUM
C DISMIN  : DISTANCE MINIMALE ENTRE 2 POINTS DU NUAGE
C
C SORTIES :
C ---------
C BASE    : BASE DE CALCUL POUR LA SUBDIVISION EN MOTS
C
C           ATTENTION : BASE DOIT VERIFIER
C           =========== BASE**2 < AU MAXIMUM DOUBLE PRECISION
C                      2*BASE-2 < AU MAXIMUM ENTIER SUR UN MOT
C                       MIN(NA,NB) + 1 < BASE
C
C ECHEL   : COEFFICIENT DE MISE A L'ECHELLE DES COORDONNEES
C           RELLES EN ENTIERES
C           ELLE EST ISOMETRIQUE DANS LES 3 DIRECTIONS
C IERR    : 0 SI PAS D'ERREUR
C           1 SI 2 POINTS DU NUAGE SONT IDENTIQUES
C           2 SI BASE N'EST PAS CALCULABLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   FEVRIER 1988
C23456...............................................................012
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      REAL              COINMX(3,2),DISMIN,ECHEL
      INTEGER           BASE,IERR
      INTRINSIC         INT
C
      BASE = 2 ** 26
C
C     RESULTATS DE DEPM/FRT/PBASE2.FTN SUR APOLLO
C
C 2** 25=    0.33554432000E+08 AU CARRE=    0.112589990684262400E+16
C 2** 26=    0.67108864000E+08 AU CARRE=    0.450359962737049600E+16
C 2** 27=    0.13421772800E+09 AU CARRE=    0.180143985094819800E+17
C 2** 28=    0.26843545600E+09 AU CARRE=    0.720575940379279400E+17
C 2** 29=    0.53687091200E+09 AU CARRE=    0.288230376151711700E+18
C 2** 30=    0.10737418240E+10 AU CARRE=    0.115292150460684700E+19
C 2** 31=   -0.21474836480E+10 AU CARRE=    0.461168601842738800E+19
C
C BASE= 0.9000000100000000E+08 AU CARRE=    0.810000018000000100E+16
C
      IERR = 0
C
      IF( DISMIN .EQ. 0. ) THEN
         WRITE(IMPRIM,*) 'ENTINI:2 POINTS IDENTIQUES'
         IERR = 1
         RETURN
      ENDIF
C
C     RECHERCHE DE LA DIFFERENCE MAXIMALE DES COORDONNEES
      DIFMAX = 0.
      DO 10 I=1,3
         DIF = COINMX(I,2) - COINMX(I,1)
         IF( DIF .GT. DIFMAX ) DIFMAX = DIF
 10   CONTINUE
C
C     NOMBRE D'INTERVALLES DE LONGUEUR DISMIN DANS DIFMAX
      NBI = INT( DIFMAX / DISMIN + 1 )
C
C     IMPRESSIONS
      WRITE(IMPRIM,19000) BASE,DISMIN,DIFMAX,NBI
19000 FORMAT(' BASE DES MULTI-ENTIERS          ',I14/
     %       ' DISTANCE MINIMALE ENTRE 2 POINTS',G14.7/
     %       ' ECART MAXIMAL DES COORDONNEES   ',G14.7/
     %       ' NOMBRE DE SOUS-INTERVALLES      ',I14/)
C
C     L'ENTIER MAXIMAL POUR SEPARER PAR 1024 LES 2 POINTS LES PLUS
C     PROCHES DANS LE NUAGE DE POINTS
C     INTMAX = NBI * 1024 DOIT ETRE =< BASE-1
C     ET GARE AU DEBORDEMENT DE CETTE MULTIPLICATION
C
      IF( NBI .GT. (BASE-1)/1024 ) THEN
C        TRAITEMENT IMPOSSIBLE
         WRITE(IMPRIM,10020) BASE,DISMIN,COINMX
10020 FORMAT(' BASE',I10,' TROP FAIBLE POUR SEPARER',
     %   'CORRECTEMENT LA DISTANCE MINIMALE ENTRE 2 POINTS',G15.7/
     %   'MINIMUM ET MAXIMUM',(T30,3G15.7/))
         IERR = 2
         RETURN
      ENDIF
C
C     L'ECHELLE ISOMETRIQUE DANS LES 3 DIRECTIONS
      ECHEL = ( BASE - 1 ) / DIFMAX
      END
