      SUBROUTINE EFVFAS( MNNPEF, NBVVEF, NBSFEF, NBLAEF, NBPSEF,
     %                           MNVVEF, MNSFEF, MNLAEF, MNPSEF, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  CONSTRUIRE LES TABLEAUX
C -----  NUVVEF DU NUMERO DE VOLUME DE CHAQUE ELEMENT FINI
C        NUSFEF DU NUMERO DE SURFACE DES FACES   DE CHAQUE EF
C        NULAEF DU NUMERO DE LIGNE   DES ARETES  DE CHAQUE EF
C        NUPSEF DU NUMERO DE POINT   DES SOMMETS DE CHAQUE EF

C ENTREES :
C ---------
C MNNPEF : ADRESSE MCN DU TABLEAU 'NPEF"NOM_ELEMENT_FINI'
C NBVVEF : NOMBRE DE VOLUME  POUR 1 EF DE CE TYPE
C NBSFEF : NOMBRE DE FACES   POUR 1 EF DE CE TYPE
C NBLAEF : NOMBRE D'ARETES   POUR 1 EF DE CE TYPE
C NBPSEF : NOMBRE DE SOMMETS POUR 1 EF DE CE TYPE
C
C SORTIES :
C ---------
C MNVVEF : ADRESSE MCN DU TABLEAU DU NUMERO DE VOLUME DE CHAQUE ELEMENT FINI
C MNSFEF : ADRESSE MCN DU TABLEAU DU NUMERO DE SURFACE DES FACES   DE CHAQUE EF
C MNLAEF : ADRESSE MCN DU TABLEAU DU NUMERO DE LIGNE   DES ARETES  DE CHAQUE EF
C MNPSEF : ADRESSE MCN DU TABLEAU DU NUMERO DE POINT   DES SOMMETS DE CHAQUE EF
C IERR   : =0 SI PAS D'ERREUR
C          =1 SI AU MOINS LA DECLARATION D'UN TABLEAU A SATURE MCN
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2012
C23456---------------------------------------------------------------012
      include"./incl/a___npef.inc"
      include"./incl/pp.inc"
      COMMON         MCN(MOTMCN)
      INTEGER        NOOBVC, NOOBSF(6), NOOBLA(12), NOOBPS(8)

C     LE NOMBRE D'ELEMENTS FINIS
      NBELEM = MCN( MNNPEF + WBELEM )

      PRINT*,'efvfas:CONSTRUCTION des TABLEAUX des PLSV des SAFV des EF'

C     DECLARATION DES TABLEAUX 
C     en 3D: NUVVEF(NBELEM), NUSUEF(NBSFEF,NBELEM), 
C                            NULIEF(NBLAEF,NBELEM), NUPOEF(NBPSEF,NBELEM)
C     en 2D: NUSUEF(NBELEM), NULIEF(NBLAEF,NBELEM), NUPOEF(NBPSEF,NBELEM)
C     en 1D:                 NULIEF(NBELEM),        NUPOEF(NBPSEF,NBELEM)

      IF( NBVVEF .GT. 0 ) THEN
         PRINT*,'No VOLUME NUVVEF(',NBELEM,')'
         CALL TNMCDC( 'ENTIER', NBELEM*NBVVEF, MNVVEF )
         IF( MNVVEF .LE. 0 ) GOTO 9900
      ELSE
         MNVVEF = 0
      ENDIF

      IF( NBSFEF .GT. 0 ) THEN
         PRINT*,'No SURFACE des FACES NUSUEF(',NBSFEF,',',NBELEM,')'
         CALL TNMCDC( 'ENTIER', NBELEM*NBSFEF, MNSFEF )
         IF( MNSFEF .LE. 0 ) GOTO 9900
      ELSE
         MNSFEF = 0
      ENDIF

      IF( NBLAEF .GT. 0 ) THEN
         PRINT*,'No LIGNE des ARETES  NULIEF(',NBLAEF,',',NBELEM,')'
         CALL TNMCDC( 'ENTIER', NBELEM*NBLAEF, MNLAEF )
         IF( MNLAEF .LE. 0 ) GOTO 9900
      ELSE
         MNLAEF = 0
      ENDIF

      IF( NBPSEF .GT. 0 ) THEN
         PRINT*,'No POINT des SOMMETS NUPOEF(',NBPSEF,',',NBELEM,')'
         CALL TNMCDC( 'ENTIER', NBELEM*NBPSEF, MNPSEF )
         IF( MNPSEF .LE. 0 ) GOTO 9900
      ELSE
         MNPSEF = 0
      ENDIF

C     BOUCLE SUR LES EF DE CE TYPE
      MNV = MNVVEF
      MNF = MNSFEF
      MNA = MNLAEF
      MNS = MNPSEF

      DO NUELEM = 1, NBELEM

C        NO DE  POINTS  LIGNES SURFACES VOLUMES
C           DES SOMMETS ARETES FACES    VOLUME de l'EF NUELEM
         CALL EFPLSV( MNNPEF, NUELEM,
     %                NOVCEL, NOSFEL, NOLAEL, NOPSEL,
     %                NOOBVC, NOOBSF, NOOBLA, NOOBPS )

C        LE NUMERO DE VOLUME DE L'EF
         DO K=1,NBVVEF
            MCN( MNV ) = NOOBVC
            MNV = MNV + 1
         ENDDO

C        LE NUMERO DE SURFACE DES FACES DE L'EF
         DO K=1,NBSFEF
            MCN( MNF ) = NOOBSF(K)
            MNF = MNF + 1
         ENDDO

C        LE NUMERO DE LIGNE DES ARETES DE L'EF
         DO K=1,NBLAEF
            MCN( MNA ) = NOOBLA(K)
            MNA = MNA + 1
         ENDDO

C        LE NUMERO DE POINT DES SOMMETS DE L'EF
         DO K=1,NBPSEF
            MCN( MNA ) = NOOBPS(K)
            MNS = MNS + 1
         ENDDO

      ENDDO

      IERR = 0
      RETURN

C     SATURATION DES TABLEAUX DANS MCN
 9900 PRINT*,'efvfas: SATURATION DU SUPER-TABLEAU MCN. AUGMENTER MOTMCN'
      IERR = 1
      RETURN
      END
