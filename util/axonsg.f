      SUBROUTINE AXONSG( NC, X3,Y3,Z3, X4,Y4,Z4 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : TRACER LE SEGMENT EN AXONOMETRIE DANS LA
C ----- FENETRE ECRAN (XCM1,YCM1) (XCM2,YCM2)
C       ET LE CADRE DE SAISIE CADRSA
C
C ENTREES :
C ---------
C NC        : NUMERO DE LA COULEUR DE TRACE DU SEGMENT
C X3,Y3,Z3  : LES 3 COORDONNEES DU 1-ER  SOMMET DU SEGMENT A TRACER
C X4,Y4,Z4  : LES 4 COORDONNEES DU 2-EME SOMMET DU SEGMENT A TRACER
C XAXMIN,XAXMAX : X MIN MAX DANS LE PLAN D'AXONOMETRIE
C YAXMIN,YAXMAX : Y MIN MAX DANS LE PLAN D'AXONOMETRIE
C PV        : POINT VISE
C PTAXE3    : LES 3 AXES DUS A L'AXONOMETRIE
C XCM1,XCM2 : X MIN ET MAX EN CENTIMETRES DE LA FENETRE ECRAN
C YCM1,YCM2 : Y MIN ET MAX EN CENTIMETRES DE LA FENETRE ECRAN
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS        MAI 1990
C23456---------------------------------------------------------------012
      include"./incl/cm4vue.inc"
C
C     PASSAGE AUX COORDONNEES DE L'AXONOMETRIE
      CALL AXOYZ( X3,Y3,Z3, R3,S3 )
      CALL AXOYZ( X4,Y4,Z4, R4,S4 )
C
C     CLIPPAGE DANS LE PLAN D'AXONOMETRIE
      CALL CLIPSG( NC, R3,S3, R4,S4, YAXMIN,YAXMAX, ZAXMIN,ZAXMAX,
     %             AXX1,AXX2, AXY1,AXY2 )
      END
