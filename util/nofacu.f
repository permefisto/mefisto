          SUBROUTINE NOFACU( NBSF,   NCOGEL, NUSTCU,
     %                       NUFACU, LESENS, MINNUS )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER LE NUMERO DE LA FACE DU CUBE DE SOMMETS NUSTFA
C -----    RANGES SELON L'ORDRE NECESSAIRE AU HACHAGE
C ENTREES:
C --------
C NBSF   : NOMBRE DE SOMMETS DE LA FACE (3 OU 4)
C NCOGEL : CODE GEOMETRIQUE DE L ELEMENT  FINI VOLUMIQUE
C          5:TETRAEDRE 6:PENTAEDRE 7:HEXAEDRE 8:6-CUBE >9:ERREUR
C NUSTCU : NUMERO DES AU PLUS 8 SOMMETS DE L'EF VOLUMIQUE
C
C SORTIES:
C --------
C NUFACU : >0 NUMERO DE LA FACE DANS LE CUBE
C          =0 FACE NON RETROUVEE
C LESENS : SI SOMMET 2 < SOMMET NBSF ALORS FACE AVEC LESENS=+1
C          SI SOMMET 2 > SOMMET NBSF ALORS FACE AVEC LESENS=-1
C          ET PERMUTATION POUR QUE NUSTF1(2)<NUSTF1(NBSF)
C MINNUS : INDICE DANS NUSTFC1 DU PLUS PETIT NUMERO DE SOMMET
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS    DECEMBRE 1996
C2345X7..............................................................012
      include"./incl/sotgfc.inc"
      INTEGER  NUSTCU(8)
      INTEGER  NUSTFC0(4), NUSTFC1(4)
C
C     BOUCLE SUR LES FACES DU CUBE
      DO 50 NF=1, NBFACE( NCOGEL )
C
C        LES SOMMETS DE LA FACE NF DE L'EF VOLUMIQUE
         NBSF = NBSTFA( NF, NCOGEL )
         DO 10 I=1, NBSF
            NUSTFC0( I ) = NUSTCU( NOSTFA(I,NF,NCOGEL) )
 10      CONTINUE
C
C        REARRANGEMENT POUR LE HACHAGE
         CALL NSFAHA( NBSF, NUSTFC0, LESENS, NUSTFC1, MINNUS )
C
C        COMPARAISON DIRECTE CAR DANS LE MEME ORDRE
         DO 30 I=1,NBSF
            IF( NUSTFC0(I) .NE. NUSTFC1(I) ) GOTO 50
 30      CONTINUE
C
C        FACE RETROUVEE
         NUFACU = NF
         RETURN
 50   CONTINUE
C
C     ERREUR
      NUFACU = 0
      RETURN
      END
