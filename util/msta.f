      SUBROUTINE MSTA( MXTAMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : AUGMENTER LE NOMBRE DE TABLEAUX MS DE LA MEMOIRE SECONDAIRE
C -----
C ENTREE :
C --------
C MXTAMS : NOMBRE MAXIMAL DE TABLEAUX MS DECLARABLES
C          CETTE VALEUR DOIT ETRE SUPERIEURE A CELLE DU CALL MSDC()
C          POUR ETRE EFFICACE.SINON RETOUR SANS MODIFICATION
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/ppmck.inc"
      include"./incl/motmcg.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      include"./incl/msvaau.inc"
      CHARACTER*16      ERREUR
      DATA              ERREUR / 'ERREUR MSTA   :' /
C
C     MXTAMS EST IL CORRECT ?
      IF( MXTAMS .LE. M2TAMS ) THEN
         WRITE(IMPRIM,10000) MXTAMS,M2TAMS
10000 FORMAT(' LE NOUVEAU NOMBRE DE TABLEAUX MS=',I10,' EST =< A L ANCIE
     %N=',I10/' PAS DE MODIFICATION'/)
         RETURN
      ENDIF
C
C     LE NOMBRE DE PAGES AVANT (NPTAMS) ET APRES (NPTAM1) DE RETAMS
      NPTAM1 = ( MXTAMS - 1 ) / NBCTMS + 1
C     LE NOMBRE DE PAGES DU NOUVEAU TABLEAU NPSFTA
      NPSNP1 = ( NPTAM1 - 1 ) / MOPASF + 1
C     LE NOMBRE DE PAGES SUPPLEMENTAIRES POUR NPSFTA ET RETAMS
      NEWPAG = NPTAM1 - NPTAMS + NPSNP1 - NPSNPS
      IF( NSFLIB-1+NEWPAG .GT. NBPASF ) THEN
         WRITE(IMPRIM,10001) ERREUR,NBPASF,NSFLIB+NEWPAG
10001 FORMAT(A16,' LE NOMBRE DE PAGES DU SF=',I10,' EST INSUFFISANT.LE S
     %F DOIT AVOIR AU MOINS',I10,' PAGES'/)
         CALL ARRET(100)
      ENDIF
C
C     COMME LE NOMBRE DE PAGES DU TABLEAU NPSFTA A AUGMENTE
C     CE TABLEAU EST DUPLIQUE , MIS A JOUR ET SAUVEGARDE SUR LE SF
      MGNPS1 = 0
      CALL TAMCDC( 'ENTIER ' , NPTAM1 , MCG(MGZLMG) , MGNPS1 )
C
      L  = NSFNPS
      IG = MGNPS1 - 1
         DO 5 I=1,NPTAM1
C        L ADRESSE MCG DU NO DE PAGE SF DE LA I-EME PAGE DE RETAMS
         IG = IG + 1
         IF( I .LE. NPTAMS ) THEN
C           L ANCIENNE VALEUR
            MCG( IG ) = MCG( MGNPSF - 1 + I )
         ELSEIF( I .LE. NPTAMS + NPSNPS ) THEN
C           UNE PAGE OCCUPEE AUPARAVANT PAR LE TABLEAU ANCIEN NPSFTA
            MCG( IG ) = L
            L         = L + 1
         ELSE
C           UNE PAGE DERRIERE TOUTES CELLES OCCUPEES DU SUPER-FICHIER
            MCG( IG ) = NSFLIB
            NSFLIB    = NSFLIB + 1
         ENDIF
C
 5       CONTINUE
C
C     SAUVEGARDE DE CE TABLEAU SUR LE SF.NSFLIB EST INCREMENTE.
      NSFNPS = NSFLIB
      CALL TAMCEC( NOFISF , NSFLIB , MOPASF , NPTAM1 , MCG(MGNPS1) )
C
C     DESTRUCTION DE L ANCIEN TABLEAU NPSFTA SI BESOIN EST
      CALL TAMCDS( 'ENTIER ' , NPTAMS , MCG(MGZLMG) , MGNPSF )
C
C     MISE A JOUR DE L ADRESSE DU TABLEAU NPSFTA
C                 ET DE SON NOMBRE DE PAGES
      MGNPSF = MGNPS1
      NPSNPS = NPSNP1
C
C     LA MISE A JOUR DU CHAINAGE DU DERNIER TABLEAU LIBRE DE RETAMS
      J = LLTAMS
C
 9    IF( J .NE. 0 ) THEN
C        PASSAGE AU TABLEAU SUIVANT
         CALL TAMSRE( J , MGTAMS )
         J = MCG( MGTAMS + 5 )
      GOTO 9
C
      ELSE
C        C EST LE DERNIER TABLEAU LIBRE.IL EST CHAINE AVEC LE 1-ER AJOUT
         L                 = M2TAMS + 1
         MCG( MGTAMS + 5 ) = L
      ENDIF
C
C     MISE A JOUR DU MAXIMUM DE TABLEAUX MS
      M2TAMS = NPTAM1 * NBCTMS
C     L INITIALISATION DES PAGES SUPPLEMENTAIRES DE RETAMS
C     LA BOUCLE SUR LES PAGES SUPPLEMENTAIRES
      DO 20 I=NPTAMS+1 , NPTAM1
         IG = MGBUSF - 1
C        LA BOUCLE SUR LES TABLEAUX MS DECRITS DANS UNE PAGE
         DO 15 J=1,NBCTMS
C           LES 5 PREMIERS MOTS SONT ANNULES
            DO 10 K=1,5
               MCG( IG + K ) = 0
 10         CONTINUE
C           LE CHAINAGE SUR LE SUIVANT
            L = L + 1
            K = L
            IF( L .GT. M2TAMS ) K = 0
            MCG( IG + 6 ) = K
C           PASSAGE AU TABLEAU MS SUIVANT
            IG = IG + M1TAMS
 15      CONTINUE
C        SAUVEGARDE DU DESCRIPTEUR DE CES NBCTMS TABLEAUX MS
         CALL FIPAEC( NOFISF , MCG(MGNPSF-1+I) , MOPASF , MCG(MGBUSF) )
 20   CONTINUE
C
C     MISE A JOUR DU NOMBRE DE PAGES DE RETAMS
      NPTAMS = NPTAM1
C
      RETURN
      END
