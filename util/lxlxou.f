      SUBROUTINE LXLXOU( NTLX , KNOM , NTLXLX , MNLXLX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    OUVRIR LE NOM KNOM LEXIQUE FILS DU LEXIQUE NTLX PERE
C -----
C
C ENTREES :
C ---------
C NTLX   : NUMERO DU LEXIQUE PERE CONTENANT LE LEXIQUE FILS
C KNOM   : CHAINE DE CARACTERES NOM A RETROUVER DANS LE LEXIQUE PERE
C
C SORTIES :
C ---------
C NTLXLX : NUMERO DE CE LEXIQUE FILS
C          0 SI KNOM N'A PAS ETE RETROUVE DANS LE LEXIQUE ET RETOUR
C          ==> NECESSITE DE FAIRE UN TEST EN RETOUR
C MNLXLX : ADRESSE EN MEMOIRE CENTRALE MCN OU MCK DE CE LEXIQUE FILS
C          0 SI KNOM N'A PAS ETE RETROUVE DANS LE LEXIQUE ET RETOUR
C          ==> NECESSITE DE FAIRE UN TEST EN RETOUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   OCTOBRE 1985
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      CHARACTER*(*)     KNOM
      CHARACTER*4       KTYPE
C
C     OUVERTURE DU LEXIQUE FILS DE NOM KNOM
      CALL LXNMOU( NTLX , KNOM , KTYPE , NTLXLX , MNLXLX )
      END
