      SUBROUTINE LXTNDC( NTLX, KNOM, KTYPVA, NBVARI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER UN NOM KNOM DANS LE LEXIQUE NTLX
C ----- CE NOM A NBVARI VARIABLES DE TYPE KTYPVA
C       NUMERIQUE C'EST A DIRE NON CARACTERES
C       SI LE NOM N'EXISTE PAS DANS LE LEXIQUE, IL EST DECLARE
C       SINON SI LE TYPE OU LES ATTRIBUTS SONT DIFFERENTS
C             ALORS 'ERREUR' ET ARRET
C             SINON RETOUR
C
C ENTREES :
C ---------
C NTLX   : NUMERO DU TABLEAU MS CONTENANT LE LEXIQUE
C KNOM   : CHAINE DE CARACTERES NOM A RETROUVER DANS LE LEXIQUE
C KTYPVA : 'LOGIQUE'  'ENTIER/2' 'ENTIER' 'REEL' 'REEL2' 'REEL4'
C          'COMPLEXE' 'COMPLEXE2'
C NBVARI : NOMBRE DE VARIABLES DE TYPE KTYPVA
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS     OCTOBRE 1984
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      CHARACTER*(*) KNOM, KTYPVA
C
C     DECLARATION DU TABLEAU MEMOIRE SECONDAIRE 'TAMS'
      CALL LXNMDC( NTLX, KNOM, 'TAMS', KTYPVA, NBVARI, NRETOU )
C
      RETURN
      END
