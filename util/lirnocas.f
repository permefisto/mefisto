      SUBROUTINE LIRNOCAS( NBVECT, NCAS0, NCAS1, NCVALS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:    LECTURE D'UN NUMERO NCAS DE CAS ET SON AFFICHAGE
C ----    SI    NCAS<=0 ou NCAS>NBVECT
C         ALORS TRAITEMENT DE TOUS LES CAS SI C'EST POSSIBLE
C         SINON TRAITEMENT DU SEUL CAS NCAS LU
C
C ENTREE :
C --------
C NBVECT : NUMERO MAXIMAL A LIRE POUR LES CAS TRAITES
C
C SORTIES:
C --------
C NCAS0  : NUMERO DU PREMIER CAS A TRAITER
C NCAS1  : NUMERO DU DERNIER CAS A TRAITER
C NCVALS : <0 POUR ABANDONNER LA LECTURE
C          =0 SI LECTURE CORRECTE ET SORTIE DE NCAS0 et NCAS1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & ST PIERRE DU PERRAY  OCTOBRE 2010
C23456---------------------------------------------------------------012
      NCVALS = 4
      CALL INVITE( 84 )
      CALL LIRENT( NCVALS, NCAS )
      IF( NCVALS .EQ. -1 ) GOTO 9900
      NCVALS = 0
C
C     CONVENTION:
C     SI    NCAS<=0 ou NCAS>NBVECT
C     ALORS TRAITEMENT DE TOUS LES CAS SI C'EST POSSIBLE
C     SINON TRAITEMENT DU SEUL CAS NCAS
      IF( NCAS .LE. 0 .OR. NCAS .GT. NBVECT ) THEN
         NCAS0 = 1
         NCAS1 = NBVECT
      ELSE
         NCAS0 = NCAS
         NCAS1 = NCAS
      ENDIF
C
 9900 RETURN
      END
