      SUBROUTINE LIRTRD( NCVALS, NBVRDP, DREEL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : LIRE LES NBVRDP VARIABLES DOUBLE PRECISION DU TABLEAU DREEL
C -----
C       LA DEMANDE EST TOUJOURS SATISFAITE CAR PAS DE RETOUR TANT QUE
C       LA VALEUR DU DREEL EST INCORRECTE SAUF @ DEMANDANT L'ABANDON
C
C       CF ~/td/g/grammaire_lu DE DEFINITION DU LANGAGE UTILISATEUR
C
C ENTREE :
C --------
C NBVRDP : LE NOMBRE DE VARIABLES REELLES DOUBLE PRECISION A LIRE
C
C ENTREE ET SORTIE :
C ------------------
C NCVALS : EN ENTREE :  6 LE TABLEAU REEL EST INITIALISE EN ENTREE
C                       0 SINON
C          EN SORTIE :  1 LE TABLEAU EST INITIALISE
C                      -1 ABANDON DE LA LECTURE PAR ENTREE DE @
C SORTIE :
C --------
C DREEL   : LES VARIABLES LUES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS  SEPTEMBRE 1990
C23456---------------------------------------------------------------012
      DOUBLE PRECISION  DREEL(NBVRDP)
C
      DO 10 I=1,NBVRDP
C        LE CODE D'INITIALISATION OU NON DE LA VARIABLE
         NCVALS = 0
         CALL LIRRDP( NCVALS , DREEL(I) )
C        SI @ RETOUR IMMEDIAT
         IF( NCVALS .EQ. -1 ) RETURN
 10   CONTINUE
C
      RETURN
      END
