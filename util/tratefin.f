      SUBROUTINE TRATEFIN( LARETE, XYZCENTR, NCARETE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     TRACER AVEC LA COULEUR NCARETE LES 6 ARETES DU TETRAEDRE
C -----     EQUILATERAL DE BARYCENTRE DE COORDONNEES XYZCENTR et
C           DE LONGUEUR D'ARETE LARETE

C ENTREES:
C --------
C LARETE  : LONGUEUR DE L'ARETE
C XYZCENTR: XYZ DU POINT BARYCENTRE DU TETRAEDRE
C NCARETE : NUMERO DE LA COULEUR DES ARETES du TETRAEDRE EQUILATERAL
C           DE BARYCENTRE XYZCENTR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint PIERRE du PERRAY         Decembre 2020
C2345X7..............................................................012
      REAL      LARETE, XYZCENTR(3), XYZSOM(3,4), HAUTEURS4, SQRT3
      INTEGER   NCARETE, NOSOTE(4)
      DATA      NOSOTE/ 1, 2, 3, 4 /

C     CALCUL DES XYZ DES 4 SOMMETS DU TETRAEDRE EQUILATERAL
      SQRT3 = SQRT(3.)
      HAUTEURS4 = SIN( ACOS( 1.0 / SQRT3 ) ) * LARETE / 4

      XYZSOM(1,1) = XYZCENTR(1) + LARETE * SQRT3/6.
      XYZSOM(2,1) = XYZCENTR(2) - LARETE / 2
      XYZSOM(3,1) = XYZCENTR(3) + HAUTEURS4

      XYZSOM(1,2) = XYZCENTR(1) + LARETE * SQRT3/6.
      XYZSOM(2,2) = XYZCENTR(2) + LARETE /2
      XYZSOM(3,2) = XYZCENTR(3) + HAUTEURS4

      XYZSOM(1,3) = XYZCENTR(1) - LARETE * SQRT3/3.
      XYZSOM(2,3) = XYZCENTR(2)
      XYZSOM(3,3) = XYZCENTR(3) + HAUTEURS4

      XYZSOM(1,4) = XYZCENTR(1)
      XYZSOM(2,4) = XYZCENTR(2)
      XYZSOM(3,4) = XYZCENTR(3) - HAUTEURS4 * 3.

C     TRACE DES 6 ARETES D'UN TETRAEDRE DEFINI PAR LE NUMERO
C     DE SES 4 SOMMETS DANS XYZSOM
      CALL TRATETRA( NCARETE, NOSOTE, XYZSOM )

C     TRACE D'UNE * AU BARYCENTRE DU TETRAEDRE
      CALL SYMBOLE3D( NCARETE, XYZCENTR, '*' )

      RETURN
      END

