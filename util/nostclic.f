      SUBROUTINE NOSTCLIC( NX, NY, NDIM, NBSOM, XYZSOM, NBTQ, NOSOTQ,
     %                     NUMTQ, XYZPT, NUMST )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  RETOURNER LE NUMERO DU SOMMET VISIBLE LE PLUS PROCHE DU POINT
C -----  CLIQUE EN NX, NY DANS UN MAILLAGE 2D ou 3D

C ATTENTION: POUR CONNAITRE LA DISTANCE A L'OEIL DU POINT, IL EST
C            NECESSAIRE DE TROUVER LE TRIANGLE OU LE QUADRANGLE VISIBLE
C            LE CONTENANT
C            SI LE POINT CLIQUE N'EST DANS AUCUN TRIANGLE ou QUADRANGLE
C            ALORS, RECHERCHE DANS LE PLAN AXONOMETRIQUE DU PLUS PROCHE
C                   SOMMET  =>  RISQUE DE TROUVER UN SOMMET NON VISIBLE!
C
C ENTREES:
C --------
C NX     : ABSCISSE ECRAN DU POINT CLIQUE
C NY     : ORDONNEE ECRAN DU POINT CLIQUE
C NDIM   : DIMENSION (2 ou 3) DE L'ESPACE DE LA SURFACE
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C XYZSOM : XYZ 3 COORDONNEES CARTESIENNES DES SOMMETS DU MAILLAGE
C NBTQ   : NOMBRE DE TRIANGLES OU QUADRANGLES DU MAILLAGE
C NOSOTQ : NO DES 4 SOMMETS DES TRIANGLES ET/OU QUADRANGLES DU MAILLAGE
C
C SORTIES:
C --------
C NUMTQ  : >0 NUMERO DU TRIANGLE ou QUADRANGLE CONTENANT LE POINT CLIQUE
C          =0 SI LE POINT CLIQUE EST HORS MAILLAGE
C XYZPT  : 3 COORDONNEES CARTESIENNES DU POINT CLIQUE SI INTERNE A
C          un TRIANGLE ou un QUADRANGLE
C          SI POINT EXTERNE les 3 COORDONNEES DU SOMMET NUMST LE PLUS
C          PROCHE PROJETE DANS LE PLAN AXONOMETRIQUE
C NUMST  : >0 NUMERO DU SOMMET VISIBLE LE PLUS PROCHE DU POINT CLIQUE
C          DANS LE PLAN AXONOMETRIQUE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : Alain PERRONNET LJLL UPMC & StPIERRE du PERRAY SEPTEMBRE 2010
C2345X7..............................................................012
      include"./incl/langue.inc"
      REAL    XYZSOM(3,NBSOM)
      INTEGER NOSOTQ(4,NBTQ)
      REAL    XYZAX(3,4), XYZAPT(3), XYZPT(3)
      EQUIVALENCE (XYZAX(1,1),X1),(XYZAX(2,1),Y1),
     %            (XYZAX(1,2),X2),(XYZAX(2,2),Y2),
     %            (XYZAX(1,3),X3),(XYZAX(2,3),Y3),
     %            (XYZAX(1,4),X4),(XYZAX(2,4),Y4)
      EQUIVALENCE (XYZAPT(1),XAPT), (XYZAPT(2),YAPT), (XYZAPT(3),ZAPT)
C
      NUMST = 0
C
C     COORDONNEES AXONOMETRIQUES DU POINT CLIQUE EN NX NY PIXELS
C     PAS D'INFORMATION DE LA 3-EME COORDONNEE DANS LA DIRECTION DE VISEE
      XAPT = XOB2PX( NX )
      YAPT = YOB2PX( NY )
C
C     RECHERCHE DU TRIANGLE ou QUADRANGLE CONTENANT LE POINT CLIQUE
C     -------------------------------------------------------------
      CALL TQPTCLIC( NX, NY, NDIM, XYZSOM, NBTQ, NOSOTQ,  NUMTQ )
      IF( NUMTQ .EQ. 0 ) THEN
C
C        LE POINT CLIQUE N'EST DANS AUCUN TRIANGLE ou QUADRANGLE
C        -------------------------------------------------------
C        RECHERCHE DANS LE PLAN AXONOMETRIQUE DU PLUS PROCHE SOMMET
C        ATTENTION: RISQUE DE TROUVER UN SOMMET NON VISIBLE!
         DMIN = 1E28
         DO NUST=1,NBSOM
            IF( NDIM .EQ. 2 ) THEN
C              LES 2 COORDONNEES XYZPT DU SOMMET NUST
               XYZPT(1) = XYZSOM(1,NUST)
               XYZPT(2) = XYZSOM(2,NUST)
            ELSE
C              LES 3 COORDONNEES XYZPT AXONOMETRIQUES DU SOMMET NUST
               CALL XYZAXO( XYZSOM(1,NUST), XYZPT )
            ENDIF
            D = ( XAPT - XYZPT(1) ) ** 2 + ( YAPT - XYZPT(2) ) ** 2
            IF( D .LT. DMIN ) THEN
               DMIN  = D
               NUMST = NUST
            ENDIF
         ENDDO
C
C        3 COORDONNEES DU SOMMET NUMST LE PLUS PROCHE DU POINT CLIQUE
         DO NS=1,3
            XYZPT(NS) = XYZSOM(NS,NUMST)
         ENDDO
         GOTO 9999
C
      ENDIF
C
C     LE POINT CLIQUE EST DANS LE TRIANGLE ou QUADRANGLE NUMTQ
C     --------------------------------------------------------
      IF( NOSOTQ(4,NUMTQ) .EQ. 0 ) THEN
C        NUMTQ EST UN TRIANGLE
         NBS = 3
      ELSE
C        NUMTQ EST UN QUADRANGLE
         NBS = 4
      ENDIF
C
C     LES COORDONNEES AXONO DES SOMMETS DU TRIANGLE ou QUADRANGLE NUMTQ
      DO NS=1,NBS
C        NUMERO DU SOMMET NS DU TQ NUMTQ
         NUST = NOSOTQ(NS,NUMTQ)
         IF( NDIM .EQ. 2 ) THEN
C           CALCUL DES 3 COORDONNEES DU SOMMET NS
C           A PARTIR DES 3 COORDONNEES XYZSOM DU SOMMET N
            XYZAX(1,NS) = XYZSOM(1,NUST)
            XYZAX(2,NS) = XYZSOM(2,NUST)
            XYZAX(3,NS) = XYZSOM(3,NUST)
          ELSE
C           CALCUL DES 3 COORDONNEES AXONOMETRIQUES XYZAX
C           A PARTIR DES 3 COORDONNEES XYZSOM DU SOMMET N
            CALL XYZAXO( XYZSOM(1,NUST), XYZAX(1,NS) )
         ENDIF
      ENDDO
C
C     LE POINT (NX,NY) EST DANS LE TRIANGLE ou QUADRANGLE NUMTQ
C     CALCUL DES 3 COORDONNEES BARYCENTRIQUES DU POINT (XAPT,YAPT)
C     DANS LE TRIANGLE NUMTQ ou LE TRIANGLE 123 DU QUADRANGLE NUMTQ
      D   =    (X2-X1 )*(Y3-Y1 )  -  (X3-X1 )*(Y2-Y1)
      CB1 = ( (X2-XAPT)*(Y3-YAPT) - (X3-XAPT)*(Y2-YAPT) ) / D
      CB2 = ( (X3-XAPT)*(Y1-YAPT) - (X1-XAPT)*(Y3-YAPT) ) / D
      CB3 = ( (X1-XAPT)*(Y2-YAPT) - (X2-XAPT)*(Y1-YAPT) ) / D
C
      IF( NBS .EQ. 3 ) THEN
C
C        NUMTQ EST UN TRIANGLE
C        3-EME COORDONNEE AXONOMETRIQUE DU POINT CLIQUE DANS L'EF
         ZAPT = CB1 * XYZAX(3,1) + CB2 * XYZAX(3,2) + CB3 * XYZAX(3,3)
C
      ELSE
C
C        NUMTQ EST UN QUADRANGLE
         IF( CB1 .LT. 0.0 .OR. CB1 .GT. 1.0 .OR.
     %       CB2 .LT. 0.0 .OR. CB2 .GT. 1.0 .OR.
     %       CB3 .LT. 0.0 .OR. CB3 .GT. 1.0 ) THEN
C           LE POINT (XAPT,YAPT) N'EST PAS DANS LE TRIANGLE 123
C           DU QUADRANGLE NUMTQ
C           CALCUL DES 3 COORDONNEES BARYCENTRIQUES DU POINT (XAPT,YAPT)
C           DANS LE TRIANGLE 1 3 4 DU QUADRANGLE NUMTQ
            D   =    (X3-X1 )*(Y4-Y1)   -  (X4-X1 )*(Y3-Y1)
            CB1 = ( (X3-XAPT)*(Y4-YAPT) - (X4-XAPT)*(Y3-YAPT) ) / D
            CB2 = ( (X4-XAPT)*(Y1-YAPT) - (X1-XAPT)*(Y4-YAPT) ) / D
            CB3 = ( (X1-XAPT)*(Y3-YAPT) - (X3-XAPT)*(Y1-YAPT) ) / D
C
C           3-EME COORDONNEE AXONOMETRIQUE DU POINT CLIQUE DANS L'EF
            ZAPT = CB1*XYZAX(3,1) + CB2*XYZAX(3,3) + CB3*XYZAX(3,4)
C
         ENDIF
C
      ENDIF
C
C     3 COORDONNEES CARTESIENNES DU POINT CLIQUE DANS LE TRIANGLE OU QUADRANGLE
      IF( NDIM .EQ. 2 ) THEN
         XYZPT(1) = XAPT
         XYZPT(2) = YAPT
         XYZPT(3) = 0
      ELSE
         CALL AXOXYZ( XYZAPT, XYZPT )
      ENDIF
C
C     RECHERCHE DU PLUS PROCHE SOMMET du TRIANGLE ou QUADRANGLE
C     AU POINT, LE TOUT EN COORDONNEES CARTESIENNES
      DMIN = 1E28
      DO NS=1,NBS
         NUST = NOSOTQ(NS,NUMTQ)
         D = ( XYZSOM(1,NUST) - XYZPT(1) ) ** 2
     %     + ( XYZSOM(2,NUST) - XYZPT(2) ) ** 2
     %     + ( XYZSOM(3,NUST) - XYZPT(3) ) ** 2
         IF( D .LT. DMIN ) THEN
            DMIN  = D
            NUMST = NUST
         ENDIF
      ENDDO

 9999 IF( NUMST .GT. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'nostclic: SOMMET CLIQUE: No',NUMST,' X=',
     %      XYZSOM(1,NUMST), ' Y=',XYZSOM(2,NUMST),' Z=',XYZSOM(3,NUMST)
         ELSE
            PRINT*,'nostclic: CLICKED VERTEX: No',NUMST,' X=',
     %      XYZSOM(1,NUMST), ' Y=',XYZSOM(2,NUMST),' Z=',XYZSOM(3,NUMST)
         ENDIF
      ENDIF

      RETURN
      END
