      SUBROUTINE HAC2AF( NMOBJT, NBMOTS, NTARSU, MNARSU, IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   CREER LE TABLEAU DES ARETES D'UNE SURFACE OU
C -----   D'UN MAILLAGE D'UN OBJET 2D A PARTIR DE SA TOPOLOGIE NPEF"...
C         UNE EVENTUELLE ANCIENNE VERSION EST DETRUITE
C         CHAINAGE DES ARETES DE CHAQUE LIGNE DE L'OBJET
C         CHAINAGE DES ARETES APPARTENANT A UN SEUL EF (FRONTALIERES)
C         CHAINAGE DES ARETES DANS 2 EF DE 2 SURFACES DIFFERENTES
C         CES 2 DERNIERS TYPES D'ARETES NE SONT PAS SUR DES LIGNES
C         COMME FAISANT PARTIE DE L'OBJET (O CONDITION AUX LIMITES)
C
C ENTREES:
C --------
C NMOBJT : NOM DE L'OBJET
C NBMOTS : NOMBRE DE MOTS D'INFORMATIONS A STOCKER EN PLUS PAR ARETE(>=3)
C
C SORTIES:
C --------
C NTARSU : NUMERO DU TMS DES ARETES DE LA SURFACE
C          0 SI NON CREE PAR CAUSE D'ERREUR
C MNARSU : ADRESSE MCN DU TABLEAU DES ARETES DE L'OBJET
C          cf ~/td/d/a___arete
C IERR   : =0 SI PAS D'ERREUR
C          =1 SI PAS D'EF SURFACIQUES
C          >0 SI ERREUR
C
C EN SORTIE: LE TABLEAU LARETE <=> MCN(MNARET) OU
C MNARET = MNARSU + W1LGFR + MCN(MNARSU+WUMXLF) - MCN(MNARSU+WUMILF) + 1
C CONTIENT
C LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C LARETE(4,I)= NUMERO DU 1-ER TOUQ CONTENANT CETTE ARETE
C              + NO TYPE EF (1 a NBTYEF) * 100 000 000
C              >0 SI ARETE   DIRECTE DANS CE TOUQ
C              <0 SI ARETE INDIRECTE DANS CE TOUQ
C SI L'ARETE APPARTIENT A 2 TOUQ ALORS
C LARETE(5,I)= NUMERO DU 2-EME TOUQ CONTENANT CETTE ARETE
C              + NO TYPE EF (1 a NBTYEF) * 100 000 000
C              >0 SI ARETE   DIRECTE DANS CE TOUQ
C              <0 SI ARETE INDIRECTE DANS CE TOUQ
C              =0 SI ARETE APPARTENANT A UN SEUL TOUQ
C LARETE(6,I)= NUMERO DANS LARETE DE L'ARETE SUIVANTE
C              SOIT DANS LE CHAINAGE D'UNE LIGNE J ENTRE NUMILF ET NUMXLF
C              SOIT DANS LE CHAINAGE DES ARETES FRONTALIERES
C              0 SI C'EST LA DERNIERE
C
C L1LGFR(J)=NUMERO DE LA PREMIERE ARETE CHAINEE DE LA LIGNE J DE L'OBJET
C           POUR J COMPRIS ENTRE NUMILF ET NUMXLF
C L1ARFB = NUMERO DE LA PREMIERE ARETE CHAINEE FRONTALIERE DANS LARETE
C          UNE TELLE ARETE APPARTIENT A UN SEUL EF ET NON SUR UNE LIGNE
C L1ARIN = NUMERO DE LA PREMIERE ARETE CHAINEE INTERFACE   DANS LARETE
C          UNE TELLE ARETE APPARTIENT A 2 EF DE 2 SURFACES DIFFERENTES
C          CES 2 DERNIERS TYPES D'ARETES NE SONT PAS SUR DES LIGNES
C          COMME FAISANT PARTIE DE L'OBJET (O CONDITION AUX LIMITES)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS     JANVIER 1995
C ...................................................................012
      IMPLICIT INTEGER (W)
      include"./incl/langue.inc"
      include"./incl/nctyef.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___arete.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/ponoel.inc"
      include"./incl/ntmnlt.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      CHARACTER*(*)     NMOBJT
      INTEGER           NTELEM(9), MNELEM(9),
     %                  NOOBVC, NOOBSF(6), NOOBLA(12), NOOBPS(8)
      INTEGER           NOSOEL(9), NGS(2)
      EQUIVALENCE       (NGS(1),NS1),(NGS(2),NS2)
CCC      LOGICAL           AVANT
C
      IERR = 0
C
C     L'OBJET EST RECHERCHE
C     =====================
C     LE TABLEAU LEXIQUE DE CET OBJET
      CALL LXLXOU( NTOBJE, NMOBJT, NTLXOB, MN )
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = NMOBJT
         IF( LANGAG .EQ. 0 ) THEN
            KERR(2) = 'OBJET INCONNU'
         ELSE
            KERR(2) = 'UNKNOWN OBJECT'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     RECHERCHE DES ADRESSES DES TABLEAUX DE LA TOPOLOGIE DE L'OBJET
C     ==============================================================
C     NTTOPO : NUMERO      DU TMS 'TOPOLOGIE' DE L'OBJET
C     MNTOPO : ADRESSE MCN DU TMS 'TOPOLOGIE' DE L'OBJET
C     NTPOGE : NUMERO      DU TMS 'XYZPOINT'  DE L'OBJET
C     MNPOGE : ADRESSE MCN DU TMS 'XYZPOINT'  DE L'OBJET
C     NTNOEU : NUMERO      DU TMS 'XYZNOEUD'  DE L'OBJET
C     MNNOEU : ADRESSE MCN DU TMS 'XYZNOEUD'  DE L'OBJET
C     NBTYEL : NOMBRE DE TYPES D'EF DU MAILLAGE
C     NTELEM : NUMERO      DU TMS 'NPEF' DES NBTYEL TYPES D'EF
C     MNELEM : ADRESSE MCN DU TMS 'NPEF' DES NBTYEL TYPES D'EF
      CALL NDPGEL( NTLXOB, NTTOPO, MNTOPO ,
     %             NTPOGE, MNPOGE, NTNOEU, MNNOEU ,
     %             NBTYEL, NTELEM, MNELEM, IERR )
C
C     LE NOMBRE TOTAL DE POINTS DE L'OBJET
      NBPOI = MCN( MNPOGE + WNBPOI )
C
C     LE TABLEAU 'ARETE' DE CET OBJET
      CALL LXTSOU( NTLXOB, 'ARETE', NTARSU, MNARSU )
      IF( NTARSU .GT. 0 ) THEN
CCCC        LES ARETES SONT ELLES ANTERIEURES A LA TOPOLOGIE DE L'OBJET?
CCC         IF( AVANT( MCN(MNTOPO), MCN(MNARSU) ) ) THEN
CCCC           LES SOMMETS SONT ANTERIEURS AUX ARETES
CCCC           LE TABLEAU ARETE EST ACCEPTE  => RETOUR SANS RECALCUL
CCC            RETURN
CCC         ENDIF
C        LE TABLEAU ARETE EST DETRUIT POUR ETRE ENSUITE REGENERE
         CALL LXTSDS( NTLXOB, 'ARETE' )
         NTARSU = 0
         MNARSU = 0
      ENDIF
C
      NDPGST = MCN( MNTOPO + WDPGST )
      NBOBIN = MCN( MNTOPO + WBOBIN )
      NBOBCL = MCN( MNTOPO + WBOBCL )
C
C     RECHERCHE DU MIN ET MAX DES NUMEROS DES LIGNES FRONTIERES
C     =========================================================
      NUMILF = IINFO( 'GRAND' )
      NUMXLF = 0
C
C     L'ADRESSE MCN DU DEBUT DU TABLEAU NUOBCL
      MNOBCL = MNTOPO + WMTYEL + NBTYEL + MOTVAR(13) * NBOBIN
      MN     = MNOBCL - 2
      NBLGFR = 0
      DO 10 I=1,NBOBCL
C        LE TYPE DE L'OBJET AUX LIMITES
         MN   = MN + 2
         NYOB = MCN( MN )
         NUOB = MCN( MN + 1 )
         IF( NYOB .EQ. 2 ) THEN
            NBLGFR = NBLGFR + 1
C           LE MINIMUM DES NUMEROS DE LIGNES
            NUMILF = MIN( NUMILF, NUOB )
C           LE MAXIMUM DES NUMEROS D'OBJETS
            NUMXLF = MAX( NUMXLF, NUOB )
         ENDIF
 10   CONTINUE
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) NBLGFR,' LIGNES FRONTALIERES DE ',NUMILF,
     %                 ' A ',NUMXLF,' POUR L''OBJET ',NMOBJT
      ELSE
         WRITE(IMPRIM,*) NBLGFR,' BOUNDARY LINES from ',NUMILF,
     %                 ' to ',NUMXLF,' for the OBJECT ',NMOBJT
      ENDIF
C
C     LE PSEUDO NUMERO DE LIGNE DES ARETES INTERFACE
C     APPARTENANT A 2 EF DE NUMEROS DE SURFACE DIFFERENTS
C     MAIS NON DECLAREE LIGNE DANS L'OBJET ( 0 CL DESSUS )
      NULINT = NUMXLF + 1
C
C     RECHERCHE DU NOMBRE DES EF SURFACIQUES DE L'OBJET
      NBTRIA = 0
      NBQUAD = 0
      NBTYEL = MCN(MNTOPO+WBTYEL)
      DO 20 I=1,NBTYEL
         NUTYEL = MCN(MNELEM(I)+WUTYEL)
         CALL ELNUCG( NUTYEL, NCOGEL )
         GOTO ( 20, 20, 15, 16, 20, 20, 20 ), NCOGEL
C        TRIANGLES
 15      NBTRIA = NBTRIA + MCN( MNELEM(I) + WBELEM )
         GOTO 20
C        QUADRANGLES
 16      NBQUAD = NBQUAD + MCN( MNELEM(I) + WBELEM )
 20   CONTINUE
C
      N = NBTRIA + NBQUAD
      IF( N .LE. 0 ) THEN
C        PAS DE SURFACE => PAS D'ARETES FRONTALIERES
         NTARSU = 0
         MNARSU = 0
         IERR = 1
         RETURN
      ENDIF
C
C     NOMBRE D'ENTIERS POUR UNE ARETE
      MOARET = 3 + NBMOTS
C
C     MAJORATION DU NOMBRE D'ARETES : FORMULE A MODIFIER EVENTUELLEMENT
      IF( N .LT. 500 ) THEN
         MXARET = 3 * NBTRIA + 4 * NBQUAD
      ELSE
C        FORMULE EXACTE (3*NBTRIA+4*NBQUA+NBARFB)/2  MAIS NBARFB INCONNU
         MXARET = INT( ( 3 * NBTRIA + 4 * NBQUAD ) * 0.61 )
      ENDIF
C
C     ADRESSAGE ET OUVERTURE DU TABLEAU ARETE
C     ---------------------------------------
      IF( NUMXLF .GT. 0 ) THEN
         MXLGFR = NUMXLF - NUMILF + 1
      ELSE
         MXLGFR = 0
      ENDIF
      WARETE = W1LGFR + MXLGFR
      CALL LXTNDC( NTLXOB , 'ARETE' , 'ENTIER' , WARETE + MOARET*MXARET)
      CALL LXTSOU( NTLXOB , 'ARETE' , NTARSU , MNARSU )
C
C     INITIALISATION DES NUMEROS DES LIGNES FRONTALIERES OU INTERFACES
C     DEFINIES DANS L'OBJET
      MN1LGF = MNARSU + W1LGFR - NUMILF
C     PAR DEFAUT: MISE A ZERO POUR INDIQUER L'ABSENCE DE LA LIGNE AVEC CE NUMERO
      DO 30 I=NUMILF, NUMXLF
         MCN(MN1LGF+I) = 0
 30   CONTINUE
C
      MN = MNOBCL - 2
      DO 35 I=1,NBOBCL
C        LE TYPE DE L'OBJET AUX LIMITES
         MN   = MN + 2
         NYOB = MCN( MN )
         NUOB = MCN( MN + 1 )
         IF( NYOB .EQ. 2 ) THEN
C           LE NUMERO DE LIGNE EST AJOUTE
            MCN(MN1LGF+NUOB) = -NUOB
         ENDIF
 35   CONTINUE
C
C     LE TABLEAU DES ARETES EST INITIALISE A ZERO
      MNARET = MNARSU + WARETE
      CALL AZEROI( MOARET*MXARET, MCN(MNARET) )
C
C     LA 1-ERE ARET LIBRE
      LIBREF = MXARET
C
      DO 2000 NTYEF=1,NBTYEL
C
C        ADRESSE MCN DU TABLEAU NPEF"TYPE_EF
         MNELE = MNELEM(NTYEF)
C        LE NUMERO DE TYPE EF DES EF
         NUTYEL = MCN(MNELE+WUTYEL)
C        LES CARACTERISTIQUES DES EF DE CE TYPE
         CALL ELTYCA( NUTYEL )
C        LE CODE GEOMETRIQUE DES EF
         CALL ELNUCG( NUTYEL, NCOGEL )
         GOTO ( 2000, 2000, 40, 40, 2000, 2000, 2000 ), NCOGEL
C
C        LE NOMBRE D'EF DE CE TYPE
 40      NBELEM = MCN( MNELE + WBELEM )
C
C        LE CODAGE DU NUMERO DE TYPE D'EF POUR LE NO D'EF
C        DANS LE TABLEAU LARETE DES ARETES DU MAILLAGE
         NUCYEF = NCTYEF * NTYEF
C
         DO 1000 NUELEM=1,NBELEM
C
C           LES NOEUDS DE L'ELEMENT FINI NUELEM
C           -----------------------------------
            CALL EFNOEU( MNELE, NUELEM, NBNDEL, NOSOEL )
C
C           LE NUMERO DE SURFACE DE L'EF
C           LE NUMERO DE SURFACE DES FACES   DE L'EF
C           LE NUMERO DE LIGNE   DES ARETES  DE L'EF
C           LE NUMERO DE POINT   DES SOMMETS DE L'EF
C           ----------------------------------------
            CALL EFPLSV( MNELE  , NUELEM ,
     %                   NOVCEL , NOSFEL , NOLAEL , NOPSEL ,
     %                   NOOBVC , NOOBSF , NOOBLA , NOOBPS )
C
C           BOUCLE SUR LES NCOGEL ARETES DU TOUQ
C           ------------------------------------
            DO 200 NOA=1,NCOGEL
C              LE NUMERO LOCAL DES SOMMETS
               IF( NOA .LT. NCOGEL ) THEN
                  NS2 = NOA + 1
               ELSE
                  NS2 = 1
               ENDIF
C              LE NUMERO GLOBAL DES SOMMETS DE L'ARETE
               NS1 = NOSOEL(NOA)
               NS2 = NOSOEL(NS2)
               IF( NS1 .GT. NS2 ) THEN
                  L     = NS1
                  NS1   = NS2
                  NS2   = L
                  ISENS = -1
               ELSE
                  ISENS = 1
               ENDIF
C
C              1:NO LE PLUS FAIBLE DES SOMMETS DE L'ARETE
C              2:NO LE PLUS GRAND  DES SOMMETS DE L'ARETE
C              SI MIN = SOMMET 2  ARETE DIRECTE   DANS CETTE FACE
C                       SOMMET NS ARETE INDIRECTE DANS CETTE FACE
C
C              RECHERCHE OU ADJONCTION DE L'ARETE
C              ----------------------------------
               CALL HACHAG( 2, NGS, MOARET, MXARET, MCN(MNARET), 3,
     &                      LIBREF, NOARE)
               MN = MNARET + MOARET * ( ABS(NOARE) - 1 )
C
C              STOCKAGE ADRESSE DE L ELEMENT DANS ARETES(4,...;NOARE)
C              -----------------------------------------------------
               K = 2
 140           K = K + 1
               IF( K .LT. MOARET ) THEN
C
C                 RECHERCHE D'UN NUMERO NUL D'ARETE
                  IF( MCN(MN + K) .NE. 0) GOTO 140
C
C                 NO ELEMENT > 0 SI L'ARETE EST   DIRECTE DANS L EF
C                            < 0 SI L'ARETE EST INDIRECTE DANS L EF
                  MCN( MN + K ) = ( NUCYEF + NUELEM ) * ISENS
C
                  IF( NOOBLA(NOA) .GT. 0 ) THEN
C
C                    CETTE ARETE APPARTIENT A UNE LIGNE UTILISATEUR
C                    SAUVEGARDE DU NUMERO DE CETTE LIGNE EN POSITION 6 DE LARETE
C                    ATTENTION: SI INTERFACE (LIGNE COMMUNE A 2 MATERIAUX)
C                               LE NUMERO LIGNE PRIS EN COMPTE EST LE SECOND
C                               QUI EN TOUTE LOGIQUE DEVRAIT ETRE LE MEME !
                     MCN(MN+5) = NOOBLA(NOA)
C
                  ELSE
C
C                    CETTE ARETE N'APPARTIENT PAS A UNE LIGNE UTILISATEUR
                     IF( MCN(MN+5) .EQ. 0 ) THEN
C
C                       ARETE VUE POUR LA PREMIERE FOIS
C                        => -NO DE LA SURFACE DE L'EF
                        MCN(MN+5) = - NOOBSF(1)
C
                     ELSE IF( MCN(MN+5) .LT. 0 ) THEN
C
C                       ARETE VUE POUR LA SECONDE FOIS
C                       LE NUMERO DE SURFACE EST IL EGAL AU PRECEDENT ?
                        IF( ABS(MCN(MN+5)) .EQ. NOOBSF(1) ) THEN
C                          ARETE COMMUNE A 2 EF D'UNE MEME SURFACE
C                          => ARETE INTERNE A UN MATERIAU NON CHAINEE
                           MCN(MN+5) = 0
                        ELSE
C                          ARETE APPARTENANT A 2 EF DE 2 SURFACES DIFFERENTES
C                           => ARETE INTERFACE NON DECLAREE
C                             (PAS DE CONDITIONS AUX LIMITES ASSOCIEES)
                           MCN(MN+5) = NULINT
                        ENDIF
C
                     ENDIF
                  ENDIF
C
               ELSE
C
C                 ERREUR IL Y A PLUS DE NBMOTS FACES CONTENANT CETTE ARETE
                  NBLGRC(NRERR) = 1
                  IF( LANGAG .EQ. 0 ) THEN
                     KERR(1) = 'TROP D''EF CONTIENNENT CETTE ARETE'
                  ELSE
                     KERR(1) = 'TOO MANY FE CONTAIN THIS EDGE'
                  ENDIF
                  CALL LEREUR
                  IF( LANGAG .EQ. 0 ) THEN
                     WRITE(IMPRIM,10200) NUELEM,(NOSOEL(L),L=1,NCOGEL)
                     WRITE(IMPRIM,10201) NOA,(NGS(L),L=1,2),NBMOTS
                  ELSE
                     WRITE(IMPRIM,20200) NUELEM,(NOSOEL(L),L=1,NCOGEL)
                     WRITE(IMPRIM,20201) NOA,(NGS(L),L=1,2),NBMOTS
                  ENDIF
C
               ENDIF
 200        CONTINUE
 1000    CONTINUE
 2000 CONTINUE
C
10200 FORMAT(/' HAC2AF: EF:',I4,' SOMMETS:',T25,4I6)
20200 FORMAT(/' HAC2AF: FE:',I4,' VERTICES:',T25,4I6)
10201 FORMAT( ' L''ARETE',I2,' DE SOMMETS:',T25,2I6,
     %        ' APPARTIENT A PLUS DE',I3,' ELEMENTS FINIS' )
20201 FORMAT( ' The EDGE',I2,' of VERTICES:',T25,2I6,
     %        ' BELONGS to MORE ',I3,' FINITE ELEMENTS' )
C
C     CHAINAGE DES ARETES FRONTALIERES C-A-D
C     SOIT APPARTENANT A UNE SEULE FACE ET A AUCUNE LIGNE
C     SOIT APPARTENANT A UNE LIGNE (FRONTIERE OU INTERFACE)
C     =====================================================
      MN     = MNARET
      L1ARFB = 0
      NBARFB = 0
      L1ARIN = 0
      NBARIN = 0
      DO 3000 N=1,MXARET
C
C        ELIMINATION DES ARETES NON UTILISEES DANS MNARET
         IF( MCN( MN ) .EQ. 0 ) GOTO 2999
C
         NL = MCN(MN+5)
         IF( NL .GT. 0 ) THEN
C
C           L'ARETE EST SUR LA LIGNE UTILISATEUR NL:
C           L'ARETE APPARTIENT ELLE A UNE LIGNE UTILISATEUR OU LIGNE INTERFACE?
            IF( NL .NE. NULINT ) THEN
C
C              NL EST LE NUMERO D'UNE VRAIE LIGNE UTILISATEUR
               IF( MCN(MN1LGF+NL) .EQ. -NL ) THEN
C                 PAS DE TETE DE CHAINAGE => PAS D'ARETE SUIVANTE
                  MCN(MN+5) = 0
               ELSE
C                 CHAINAGE DE CETTE ARETE EN TETE
C                 => ARETE SUIVANTE=TETE ACTUELLE
                  MCN(MN+5) = MCN(MN1LGF+NL)
               ENDIF
C              LA NOUVELLE ARETE TETE DU CHAINAGE DES ARETES DE CETTE LIGNE
               MCN(MN1LGF+NL) = N
C
            ELSE
C
C              NL EST LE NUMERO DE LA LIGNE INTERFACE (NON UTILISATEUR)
C              ELLE EST CHAINEE AVEC LA PRECEDENTE
               IF( L1ARIN .EQ. 0 ) THEN
C                 PAS DE TETE DE CHAINAGE => PAS D'ARETE SUIVANTE
                  MCN(MN+5) = 0
               ELSE
C                 CHAINAGE DE CETTE ARETE EN TETE
C                 => ARETE SUIVANTE=TETE ACTUELLE
                  MCN(MN+5) = L1ARIN
               ENDIF
               L1ARIN = N
               NBARIN = NBARIN + 1
C
            ENDIF
            GOTO 2999
         ENDIF
C
C        ELIMINATION DES ARETES APPARTENANT A 2 EF DONC INTERNES
         IF( MCN(MN+4) .NE. 0 ) GOTO 2999
C
C        ARETE APPARTENANT A UN SEUL EF NON SUR UNE LIGNE DE L'OBJET
C        => ARETE FRONTALIERE CHAINEE AVEC LA PRECEDENTE ARETE FRONTALIERE
         IF( L1ARFB .EQ. 0 ) THEN
C           PAS DE TETE DE CHAINAGE => PAS D'ARETE SUIVANTE
            MCN(MN+5) = 0
         ELSE
C           CHAINAGE DE CETTE ARETE EN TETE => ARETE SUIVANTE=TETE ACTUELLE
            MCN(MN+5) = L1ARFB
         ENDIF
         L1ARFB = N
         NBARFB = NBARFB + 1
C
C        PASSAGE A L'ARETE SUIVANTE
 2999    MN = MN + MOARET
 3000 CONTINUE
C
C     LE TABLEAU 'ARETE' EST COMPLETE
C     LE NOMBRE D'ENTIERS PAR ARETE
      MCN( MNARSU + WOARET ) = MOARET
C     LA MAJORATION DU NOMBRE D'ARETES
      MCN( MNARSU + WXARET ) = MXARET
C     LE NOMBRE D'ARETES FRONTALIERES NON SUR LIGNES UTILISATEUR
      MCN( MNARSU + WBARFB ) = NBARFB
C     LE NUMERO DE LA PREMIERE ARETE FRONTALIERE NON LIGNE UTILISATEUR
      MCN( MNARSU + W1ARFB ) = L1ARFB
C     LE NOMBRE D'ARETES INTERFACES   NON SUR LIGNES UTILISATEUR
      MCN( MNARSU + WBARIN ) = NBARIN
C     LE NUMERO DE LA PREMIERE ARETE INTERFACE NON LIGNE UTILISATEUR
      MCN( MNARSU + W1ARIN ) = L1ARIN
C     LE NOMBRE D'ARETES AVEC TANGENTES
      MCN( MNARSU + WBARTG ) = 0
C     LE NUMERO MINIMAL DE LIGNE DE L'OBJET
      MCN( MNARSU + WUMILF ) = NUMILF
C     LE NUMERO MAXIMAL DE LIGNE DE L'OBJET
      MCN( MNARSU + WUMXLF ) = NUMXLF
C
C     AJOUT DE LA DATE
      CALL ECDATE( MCN(MNARSU) )
C     AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNARSU + MOTVAR(6) ) = NONMTD( '~>>>ARETE' )
C
      RETURN
      END
