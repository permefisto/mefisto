      REAL FUNCTION ANGINT( ANGLE , ANGMIN , ANGMAX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RAMENER LA VALEUR DEGRES  DE ANGLE DANS L'INTERVALLE
C ----- (ANGMIN,ANGMAX) COMPTE TENU DE LA PERIODE ANGMAX-ANGMIN
C       CONVERTIR L ANGLE RESULTANT EN RADIANS
C
C ENTREES :
C ---------
C ANGLE  : ANGLE EN DEGRES A RAMENER DANS L'INTERVALLE ANGMIN ANGMAX
C ANGMIN : VALEUR INFERIEURE DE L'INTERVALLE
C ANGMAX : VALEUR SUPERIEURE DE L'INTERVALLE
C
C SORTIE  :
C ---------
C ANGINT : ANGLE EN RADIANS APRES TRAITEMENT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  OCTOBRE 1984
C.......................................................................
      INTRINSIC  REAL
C
C     LA PERIODE DE CALCUL
      PERIOD = ANGMAX - ANGMIN
C
C     PROJECTION DE L'ANGLE DANS CET INTERVALLE ANGMIN ANGMAX
C     PAR ADDITION DE OU SOUSTRACTION DE LA PERIODE
      ANGINT = ANGLE
 10   IF( ANGINT .LT. ANGMIN ) THEN
         ANGINT = ANGINT + PERIOD
         GOTO 10
      ENDIF
 20   IF( ANGINT .GT. ANGMAX ) THEN
         ANGINT = ANGINT - PERIOD
         GOTO 20
      ENDIF
C
C     CONVERSION ANGLE EN DEGRES => RADIANS
      ANGINT = REAL( ANGINT * 3.14159265358979312D0 / 180.D0 )
C
      RETURN
      END
