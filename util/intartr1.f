      SUBROUTINE INTARTR1( S1, S2, P1, P2, P3, LINTER, PT, CBT, CBAPT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES 3 COORDONNEES DU POINT PT, INTERSECTION DU
C -----    SEGMENT S1-S2 ET DU TRIANGLE DEFINI PAR SES SOMMETS P1, P2, P3

C          SP DIFFERENT de INTARTR2 PAR LA POSITION du POINT PT SUR S1S2
C          SOUHAITEE AU DELA DE S1 VERS S2

C ENTREES:
C --------
C S1,S2   : LES 2 POINTS QUI DEFINISSENT L'ARETE
C P1,P2,P3: LES 3 POINTS QUI DEFINISSENT LE TRIANGLE

C SORTIES:
C --------
C LINTER : -2 SI S1=S2 PAS DE CALCUL DE PT
C          -1 SI S1-S2 PARALLELE AU PLAN DU TRIANGLE
C           0 SI S1-S2 N'INTERSECTE PAS LE TRIANGLE ENTRE CES 3 SOMMETS
C                      ou AVANT S1 SUR LE SEGMENT S1-S2
C           1 SI S1-S2 INTERSECTE LE TRIANGLE ENTRE CES 3 SOMMETS
C                      EN UN POINT AU DELA DE S1 VERS S2
C           PT EST DANS LE TRIANGLE OU SUR UNE DE SES 3 ARETES
C PT     : LES 3 COORDONNEES DU POINT D'INTERSECTION SI LINTER=1
C CBT    : LES 3 COORDONNEES BARYCENTRIQUES DE PT DANS LE TRIANGLE P1P2P3
C CBAPT  : LA    COORDONNEE  BARYCENTRIQUE2 DE PT DANS LE SEGMENT S1S2
C          =0 SI PT=S1                 ...S1=PT...S2...>
C          =1 SI PT=S2                 ...S1...S2=PT...>
C          >1 SI PT EST AU DELA DE S2  ...S1...S2...PT...>
C          <0 SI PT EST AU DELA DE S1  ...PT...S1...S2...>
C          >0 et <1                    ...S1...PT...S2...>
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET Saint Pierre du Perray            Octobre 2020
C23456...............................................................012
      IMPLICIT  NONE
      DOUBLE PRECISION  S1(3), S2(3), P1(3), P2(3), P3(3), PT(3),
     %                  CBT(3), CBA(3), CBAPT, S1S2, S1PT
      INTEGER           LINTER, K, N

      CALL INTARTR0( S1, S2, P1, P2, P3, LINTER, PT, CBT )

C LINTER : -2 SI S1=S2 PAS DE CALCUL DE PT
C          -1 SI S1-S2 PARALLELE AU PLAN DU TRIANGLE
C           0 SI S1-S2 N'INTERSECTE PAS LE TRIANGLE ENTRE CES 3 SOMMETS
C           1 SI S1-S2   INTERSECTE     LE TRIANGLE ENTRE CES 3 SOMMETS
C             C-A-D  PT EST INTERNE AU TRIANGLE OU SUR UNE DE SES 3 ARETES
C PT     : LES 3 COORDONNEES DU POINT D'INTERSECTION SI LINTER=1
C CBT    : LES 3 COORDONNEES BARYCENTRIQUES DE PT DANS LE TRIANGLE P1P2P3

      IF( LINTER .NE. 1 ) GOTO 9999

C     COORDONNEE BARYCENTRIQUE 2 DE PT SUR L'ARETE S1-S2
C     CALCULEE PAR SA MOYENNE SUR SES 3 COORDONNES XYZ
C     AJOUT 29/09/2014 DU TEST SUR PT INTERIEUR A S1-S2
      N = 0
      S1S2 = 0D0
      S1PT = 0D0
      DO K=1,3
         S1S2 = S1S2 + ( S2(K) - S1(K) ) ** 2
         S1PT = S1PT + ( PT(K) - S1(K) ) ** 2
         IF( S2(K) .NE. S1(K) ) THEN
            CBA(K) = ( PT(K) - S1(K) ) / ( S2(K) - S1(K) )
            N = N + 1
         ELSE
            CBA(K) = 0D0
         ENDIF
      ENDDO
      CBAPT = ( CBA(1) + CBA(2) + CBA(3) ) / N

      IF( S1PT .EQ. 0D0 ) THEN
C        PT=S1
         CBAPT = 0D0
         LINTER = 1
         GOTO 9999
      ENDIF

      IF( S1S2 .EQ. 0D0 ) THEN
C        S1=S2  NON PT
         GOTO 10
      ENDIF

      IF( CBAPT .LT. 0D0 ) THEN
         CBAPT = -SQRT( S1PT / S1S2 )
      ELSE
         CBAPT =  SQRT( S1PT / S1S2 )
      ENDIF

 10   IF( CBAPT .GE. 0D0 ) THEN
C        LE POINT EST DANS LE TRIANGLE OU SUR UNE DE SES 3 ARETES
C        ET SUR LE SEGMENT S1S2 AU DELA DE S1
         LINTER = 1
         GOTO 9999
      ENDIF

C     La DROITE S1-S2 N'INTERSECTE PAS LE TRIANGLE ENTRE CES 3 SOMMETS
C     ou BIEN LE POINT D'INTERSECTION N'EST PAS AU DELA DE S1 VERS S2
C     SUR LE SEGMENT S1S2 DANS LA DIRECTION VERS S2
      LINTER = 0

 9999 RETURN
      END
