      SUBROUTINE ARSOLI( NULIGN , NTLXLI , NOGRLI ,
     %                   LIGNOU , NBSOLI , NOSOLI ,
     %                   NTARLI , MNARLI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  GENERER LE TABLEAU 'ARETE' A PARTIR DU TABLEAU 'SOMMET' D'UNE LIGNE
C -----
C ENTREES:
C --------
C NULIGN : NUMERO DE LA LIGNE DANS LE LEXIQUE LIGNES
C NTLXLI : NUMERO TS DU LEXIQUE DE CETTE LIGNE
C NOGRLI : NUMERO DE GROUPE DE LA LIGNE
C LIGNOU : 1 SI LA LIGNE EST OUVERTE ( DEBUT ET FIN DIFFERENT  )
C          0 SI LA LIGNE EST FERMEE  ( DEBUT ET FIN COINCIDENT )
C NBSOLI : NOMBRE DE  SOMMETS DE LA LIGNE
C NOSOLI : NUMERO DES SOMMETS DE LA LIGNE
C
C SORTIES:
C --------
C NTARLI : NUMERO TS   DU TABLEAU DES ARETES DE LA LIGNE
C MNARLI : ADRESSE MCN DU TABLEAU DES ARETES DE LA LIGNE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS  AVRIL 1987
C...............................................................................
      include"./incl/pp.inc"
      COMMON  MCN(MOTMCN)
      INTEGER NOSOLI(1:NBSOLI),INFOAR(-1:2)
C
C     NUMERO DE GROUPE DES ARETES
      INFOAR(-1) = NOGRLI
C     NUMERO DE LIGNE  DES ARETES
      INFOAR( 0) = NULIGN + 2 000 000
C
C     CONSTRUCTION DU TABLEAU 'ARETE'
C     --------------------------------
C     NBARET NOMBRE D'ARETES
      IF( LIGNOU .EQ. 0 ) THEN
C        LIGNE FERMEE . AUTANT D'ARETES QUE DE SOMMETS
         NBARET = NBSOLI
      ELSE
C        LIGNE OUVERTE . UNE ARETE DE MOINS QUE DE SOMMETS
         NBARET = NBSOLI - 1
      ENDIF
C
      CALL LXTNDC( NTLXLI , 'ARETE' , 'ENTIER' , 3 + NBARET )
      CALL LXTSOU( NTLXLI , 'ARETE' ,  NTARLI  , MNARLI )
C
C     NB NOMBRE D'ARETES
      MCN( MNARLI + 2 ) = NBARET
C
C     LE NUMERO DU SOMMET INITIAL DE LA LIGNE
      N1 = NOSOLI(1)
      DO 20 I=1,NBARET
C
C        N1 N2 LES 2 SOMMETS DE L'ARETE I
         IF( I .LT. NBSOLI ) THEN
C           IL EXISTE UN SOMMET SUIVANT
            N2 = NOSOLI(I+1)
         ELSE
C           ICI I=NBSOLI=NBARET : LE SOMMET SUIVANT EST LE PREMIER
            N2 = NOSOLI(1)
         ENDIF
C
C        LE NUMERO DES 2 SOMMETS DE L'ARETE
         INFOAR( 1 ) = N1
         INFOAR( 2 ) = N2
         CALL PAFCEC( 2 , INFOAR , MCN(MNARLI+2+I) )
         N1 = N2
 20   CONTINUE
C
C     AJOUT DE LA DATE
      CALL ECDATE( MCN(MNARLI) )
      END
