      SUBROUTINE LXTTDC( NTLX , KNOM , MXTATA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER UN NOM KNOM TABLEAU DE TABLEAUX DANS LE LEXIQUE NTLX
C -----
C       SI LE NOM N'EXISTE PAS DANS LE LEXIQUE , IL EST DECLARE
C       SINON SI LES ATTRIBUTS SONT DIFFERENTS
C             ALORS 'ERREUR' ET ARRET
C             SINON RETOUR
C
C ENTREES :
C ---------
C NTLX   : NUMERO DU TABLEAU MS CONTENANT LE LEXIQUE
C KNOM   : CHAINE DE CARACTERES NOM A RETROUVER DANS LE LEXIQUE
C MXTATA : NOMBRE MAXIMAL DE TABLEAUX DU TABLEAU DE TABLEAUX
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   OCTOBRE 1984
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      include"./incl/pp.inc"
      COMMON        MCN(MOTMCN)
      CHARACTER*(*) KNOM
C
C     DECLARATION DU TABLEAU DE TABLEAUX MEMOIRE SECONDAIRE 'TATA'
      CALL LXNMDC( NTLX,KNOM,'TATA','ENTIER',MXTATA+4,NRETOU )
C
C     SI LE TABLEAU DE TABLEAUX EXISTE DEJA  ALORS RETOUR
      IF( NRETOU .GT. 0 ) RETURN
C
C     L'INITIALISATION A ZERO DU TABLEAU DES NUMEROS DES TABLEAUX
C     DU TABLEAU DE TABLEAUX (TATA)
      CALL LXTTOU( NTLX , KNOM , NTTATA , MNTATA )
      DO 10 I=0,MXTATA+3
         MCN(MNTATA+I) = 0
 10   CONTINUE
C
C     LE MAXIMUM DE TABLEAUX EST MIS A JOUR
      MCN( MNTATA+3 ) = MXTATA
      END
