      SUBROUTINE ENTMIS( ECHEL , COOMIN , XYZ , IXYZ )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRANSFORMATION DES COORDONNEES REELLES EN COORDONNEES ENTIERES
C -----
C
C ENTREES :
C ---------
C ECHEL   : COEFFICIENT DE MISE A L'ECHELLE DES COORDONNEES
C           RELLES EN ENTIERES ( TRANSFORMATION ISOMETRIQUE )
C COOMIN  : 3 COORDONNEES DU MINIMUM DES POINTS DU NUAGE
C XYZ     : 3 COORDONNEES REELLES
C
C SORTIES :
C ---------
C IXYZ    : 3 COORDONNEES ENTIERES CHACUNE SUR UN MOT ENTIER MACHINE
C
C POUR PLUS DE PRECISION VOIR LE SOUS-PROGRAMME ENTINI
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   FEVRIER 1988
C23456...............................................................012
      REAL    XYZ(3),COOMIN(3),ECHEL
      INTEGER IXYZ(3)
C
      IXYZ(1) = NINT( ( XYZ(1) - COOMIN(1) ) * ECHEL )
      IXYZ(2) = NINT( ( XYZ(2) - COOMIN(2) ) * ECHEL )
      IXYZ(3) = NINT( ( XYZ(3) - COOMIN(3) ) * ECHEL )
      END
