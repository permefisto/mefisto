      SUBROUTINE LXXXOU( NTRACI , KNOM , NOTAMS , MCTAMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : OUVRIR LE LEXIQUE OU TABLEAU MS OU TABLEAU DE TABLEAUX
C ----- DEFINI PAR SON NOM KNOM ET DE RACINE LE LEXIQUE DEFINI
C       PAR LE TABLEAU MS NTRACI
C
C ENTREES :
C ---------
C NTRACI : NUMERO DU TABLEAU MS RACINE (NTADAM POUR LA RACINE TOTALE)
C KNOM   : LE NOM A OUVRIR.IL EST DEFINI PAR PLUSIEURS MOTS SEPARES
C          PAR LE CARACTERE > . PAR EXEMPLE   LIGNES>DEFINITION
C          SI NTRACI = NTADAM ALORS ADAM>LIGNE>DEFINITION SERA OUVERT
C
C SORTIES :
C ---------
C NOTAMS : NUMERO DU TABLEAU MS DEFINI PAR KNOM
C MCTAMS : SON ADRESSE DANS MCN OU MCK SELON SON TYPE CARACTERES OU NON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS  OCTOBRE 1985
C.......................................................................
      CHARACTER*(*) KNOM
      CHARACTER*4   KTYPE
      CHARACTER*1   SEPARE
      DATA          SEPARE/ '>' /
C
C     LE LEXIQUE DE DEPART,LA 1-ERE ET DERNIERE LETTRE
      NT = NTRACI
      N1 = 1
      L  = LEN( KNOM )
C
C     BOUCLE SUR LES DIFFERENTS MOTS DE KNOM SEPARES PAR '>'
      DO 100 N=1,100
C        RECHERCHE DU 1-ER CARACTERE > RENCONTRE
         N2 = INDEX( KNOM(N1:L) , SEPARE )
         IF( N1 .EQ. N2 ) THEN
C           '>>' NON SEPARE . PASSAGE AU SUIVANT
            N1 = N1 + 1
            GOTO 100
         ENDIF
         IF( N2 .EQ. 0 ) THEN
C           IL N'Y A PLUS DE '>'
            IF( N1.LE. L ) THEN
C              IL Y A ENCORE UN NOM APRES LE DERNIER '>'
C              OUVERTURE DE CE NOM
               CALL LXNMOU( NT , KNOM(N1:L),KTYPE,NOTAMS,MCTAMS)
            ENDIF
            RETURN
         ENDIF
C
C        IL RESTE UN '>' . OUVERTURE DU LEXIQUE
         CALL LXLXOU( NT , KNOM(N1:N2-1) , NOTAMS , MCTAMS )
         NT = NOTAMS
         N1 = N2 + 1
 100  CONTINUE
      END
