      FUNCTION NBELFIN( NBTYEL, MNNPEF )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE NOMBRE D'ELEMENTS FINIS DU MAILLAGE 'NPEF'
C -----    D'UN OBJET
C
C ENTREES:
C --------
C      EST SUPPOSE DEJA EXECUTE:
C      ADRESSAGE DES ADRESSES DES TABLEAUX ELEMENTS DE CET OBJET
C      CALL TNMCDC( 'ENTIER', 2*MXTYEL, MNNPEF )
C      MNTELE = MNNPEF + MXTYEL
C      CALL NDPGEL( NTLXOB, NTTOPO, MNTOPO,
C     %             NTPOGE, MNPOGE, NTNOEU, MNNOEU,
C     %             NBTYEL, MCN(MNTELE), MCN(MNNPEF), IERR )
C     NTTOPO : NUMERO      DU TMS 'TOPOLOGIE' DE L'OBJET
C     MNTOPO : ADRESSE MCN DU TMS 'TOPOLOGIE' DE L'OBJET
C     NTPOGE : NUMERO      DU TMS 'XYZPOINT'  DE L'OBJET
C     MNPOGE : ADRESSE MCN DU TMS 'XYZPOINT'  DE L'OBJET
C     NTNOEU : NUMERO      DU TMS 'XYZNOEUD'  DE L'OBJET
C     MNNOEU : ADRESSE MCN DU TMS 'XYZNOEUD'  DE L'OBJET
C     NBTYEL : NOMBRE DE TYPES D'ELEMENTS FINIS DU MAILLAGE
C     NTELEM : NUMERO      DU TMS DES NBTYEL TYPES D'ELEMENTS FINIS
C     MNNPEF : ADRESSE MCN DU TMS DES NBTYEL TYPES D'ELEMENTS FINIS
C MNNPEF : ADRESSE MCN DES TABLEAUX ELEMENTS DE CET OBJET
C NBTYEL : NOMBRE DE TYPES D'ELEMENTS FINIS
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire J-L. LIONS UPMC PARIS    MAI 2007
C....................................................................012
      include"./incl/a___npef.inc"
      include"./incl/pp.inc"
      COMMON    MCN(MOTMCN)
C
      NBELFIN = 0
      DO 10 I = 0, NBTYEL-1
C
C        L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF DE CE TYPE D'EF
         MNELE = MCN( MNNPEF + I )
C
C        LE NOMBRE DE TELS ELEMENTS FINIS
         NBELEM = MCN(MNELE + WBELEM )
C
         NBELFIN = NBELFIN + NBELEM
 10   CONTINUE
C
      RETURN
      END
