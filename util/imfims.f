      SUBROUTINE IMFIMS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : IMPRIMER LE CONTENU DU TABLEAU REFIMS QUI GERE LES FICHIERS
C ----- DE LA MEMOIRE SECONDAIRE
C
C REFIMS : REPERTOIRE DES FICHIERS DE LA MEMOIRE SECONDAIRE
C          POUR N=1 A M2FIMS
C          REFIMS(1,N):NUMERO LOGIQUE DU FICHIER POUR LE SYSTEME
C          REFIMS(2,N):NOMBRE DE PAGES DE CE FICHIER DE LA MS
C          REFIMS(3,N):NOMBRE DE MOTS PAR PAGE DE CE FICHIER
C          REFIMS(4,N):ADRESSE DANS LA MEMOIRE DE GESTION MCG DE L UNIQU
C                      BUFFER DE CE FICHIER
C          REFIMS(5,N):NUMERO DE LA PAGE DE SON REPERTOIRE DES PAGES
C                      (RED1FI) ACTUELLEMENT EN MEMOIRE CENTRALE
C          REFIMS(6,N):NUMERO DE LA PAGE SF QUI PRECEDE LA PREMIERE
C                      PAGE DU REPERTOIRE(RED1FI) DE CE FICHIER N
C          REFIMS(7,N):NUMERO DE LA PREMIERE PAGE LIBRE DE RED1FI
C                      CHAINAGE DES PAGES LIBRES DE CE FICHIER N
C          LE SUPER-FICHIER EST LE PREMIER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/langue.inc"
      include"./incl/motmcg.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      CHARACTER*48      FICHNM,NMFICH
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000)
      ELSE
         WRITE(IMPRIM,20000)
      ENDIF
C
      IG = MGFIMS
      DO 10 J=1,M2FIMS
         IF( MCG(IG) .EQ. 0 ) THEN
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10002) J
            ELSE
               WRITE(IMPRIM,20002) J
            ENDIF
         ELSEIF( MCG(IG) .GT. 0 ) THEN
            NMFICH = FICHNM(J)
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10010) J,NMFICH,(MCG(IG+I),I=0,6)
            ELSE
               WRITE(IMPRIM,20010) J,NMFICH,(MCG(IG+I),I=0,6)
            ENDIF
         ENDIF
         IG = IG + M1FIMS
 10   CONTINUE
C
10000 FORMAT(' TABLEAU REFIMS REPERTOIRE DES FICHIERS DE LA MS'/)
20000 FORMAT(' REFIMS Array: DIRECTORY of MS Files'/)
C
10002 FORMAT(' LE FICHIER MS',I5,' A ETE DETRUIT')
20002 FORMAT(' THE FILE MS',I5,' HAS BEEN DELETED')
C
10010 FORMAT(' FICHIER MS=',I9,' NOM=',A48/
     %       ' NUMERO FICHIER',T46,1H:,I9/
     %       ' NOMBRE PAGES',T46,1H:,I9/
     %       ' MOTS PAR PAGE',T46,1H:,I9/
     %       ' ADRESSE MCG DU BUFFER',T46,1H:,I9/
     %       ' NO PAGE DANS BUFFER',T46,1H:,I9/
     %       ' NO PREMIERE PAGE SF DE RED1FI',T46,1H:,I9/
     %       ' NO PREMIERE PAGE LIBRE DU FICHIER',T46,1H:,I9/)
20010 FORMAT(' FILE MS=',I9,' NAME=',A48/
     %       ' FILE NUMBER',T46,1H:,I9/
     %       ' PAGE NUMBER',T46,1H:,I9/
     %       ' WORDS BY PAGE',T46,1H:,I9/
     %       ' ADRESS MCG of BUFFER',T46,1H:,I9/
     %       ' NO PAGE in BUFFER',T46,1H:,I9/
     %       ' FIRST PAGE NUMBER in SF of RED1FI',T46,1H:,I9/
     %       ' FIRST FREE PAGE NUMBER of FILE',T46,1H:,I9/)
      END
