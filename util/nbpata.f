      SUBROUTINE NBPATA( MOTABL , MOPAGE ,  NBPAGE , MOPERD )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETOURNER LE NOMBRE DE PAGES OCCUPES PAR LE TABLEAU DE MOTABL
C ----- MOTS ET LE NOMBRE DE MOTS PERDUS DE LA DERNIERE PAGE
C ENTREE :
C --------
C MOTABL : NOMBRE DE MOTS DU TABLEAU
C MOPAGE : NOMBRE DE MOTS DE LA PAGE
C
C SORTIE :
C --------
C NBPAGE : NOMBRE DE PAGES NECESSAIRES POUR STOCKER LES MOTABL MOTS
C MOPERD : NOMBRE DE MOTS PERDUS DANS LA DERNIERE PAGE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1983
C.......................................................................
C     LE NOMBRE DE PAGES
      NBPAGE = MOTABL / MOPAGE
C     LE NOMBRE DE MOTS DE LA DERNIERE PAGE
      MOPERD = MOTABL - NBPAGE * MOPAGE
C     SI LE RESTE EST NON NUL IL FAUT AJOUTER UNE PAGE
      IF( MOPERD .GT. 0 ) THEN
         NBPAGE = NBPAGE + 1
C        ET FAIRE LE COMPLEMENT DE LA PAGE POUR TROUVER LE RESTE PERDU
         MOPERD = MOPAGE - MOPERD
      ENDIF
C
      RETURN
      END
