      SUBROUTINE NUQTADAR( NA,     NTR, NOSTQT, L1ARET, L2ARET, LARETE,
     %                     NOARET, NQTAD )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER LE NUMERO NOARET DANS LE TABLEAU LARETE DE
C -----    L'ARETE NA DU QTANGLE NTR DANS LE TABLEAU NOSTQT

C ENTREES:
C --------
C NA     : NUMERO 1 A 3 DE L'ARETE DU QTANGLE NTR
C NTR    : NUMERO DU QTANGLE DANS LE TABLEAU NOSTQT
C NOSTQT : NUMERO XYZSOM DES 3 SOMMETS ET 0 POUR CHACUN DES QTANGLES
C L1ARET : NOMBRE DE MOTS PAR ARETE DU TABLEAU NOARET
C L2ARET : NOMBRE DE ARETES DU TABLEAU NOARET
C LARETE : TABLEAU DES ARETES DU MAILLAGE
C          LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C          LARETE(4,I)= NUMERO DU 1-ER QTANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 1-ER  QTANGLE
C          LARETE(5,I)= NUMERO DU 2-EME QTANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 2-EME QTANGLE

C SORTIES:
C --------
C NOARET : >0 SI L'ARETE NA DE NTR EST L'ARETE NOARET DANS LARETE
C          =0 SI L'ARETE NA DE NTR N'EST PAS DANS LARETE
C          <0 SI L'ARETE NA DE NTR A ETE AJOUTEE DANS LE TABLEAU LARETE
C NQTAD  :    SI L'ARETE APPARTIENT A 2 QTANGLES ALORS
C          >0 NUMERO DANS NOSTQT DU QTANGLE ADJACENT A NTR PAR L'ARETE NA
C          =0 SI L'ARETE N'EST PAS RETROUVEE ou
C             SI L'ARETE APPARTIENT A UN SEUL QTANGLE ou a >2 QTANGLES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET  Saint Pierre du Perray              Mars 2020
C....................................................................012
      INTEGER  NOSTQT(4,*), LARETE(L1ARET,L2ARET), NOSOAR(2), NOQTAR(16)

C     NOMBRE DE SOMMETS DU QTANGLE NTR
      IF( NOSTQT( 4, NTR ) .EQ. 0 ) THEN
         NBS = 3
      ELSE
         NBS = 4
      ENDIF

C     LES 2 SOMMETS DE L'ARETE NA DU QTANGLE NTR
      NS1 = NOSTQT( NA, NTR )
      IF( NA .EQ. NBS ) THEN
         N2 = 1
      ELSE
         N2 = NA+1
      ENDIF
      NS2 = NOSTQT( N2, NTR )
 
C     RECHERCHE DU QTANGLE ADJACENT PAR L'ARETE NS1 NS2
      IF( NS1 .LT. NS2 ) THEN
         NOSOAR(1) = NS1
         NOSOAR(2) = NS2
      ELSE
         NOSOAR(1) = NS2
         NOSOAR(2) = NS1
      ENDIF

C     NAR LE NUMERO DE L'ARETE NS1 NS2 DANS LARETE
      CALL HACHAR( 2, NOSOAR, L1ARET, L2ARET, LARETE, 3, NOARET )

      IF( NOARET .LE. 0 ) THEN
C        NOARET =0 SI LE TABLEAU NOSOAR N'A PAS ETE RETROUVE
C               >0 SI LE TABLEAU NOSOAR   A     ETE RETROUVE
         NQTAD = 0
         GOTO 9999
      ENDIF

C     QUEL EST LE NOMBRE DE QT CONTENANT CETTE ARETE?
      NBQTAR = 0
      DO K = 4, L1ARET
         NOQT = ABS( LARETE(K,NOARET) )
         IF( NOQT .NE. 0 ) THEN
            NBQTAR = NBQTAR + 1
            NOQTAR( NBQTAR ) = NOQT
         ENDIF
      ENDDO

      IF( NBQTAR .EQ. 2 ) THEN
C        NQTAD EST LE QTANGLE ADJACENT AU QTANGLE NTR PAR L'ARETE NOARET
         IF( NTR .EQ. NOQTAR(1) ) THEN
            NQTAD = NOQTAR(2)
         ELSE
            NQTAD = NOQTAR(1)
         ENDIF
      ELSE
C        ARETE APPARTENANT a 1 QT ou a >2 QTANGLES
         NQTAD = 0
      ENDIF

 9999 RETURN
      END
