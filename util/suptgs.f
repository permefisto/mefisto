      SUBROUTINE SUPTGS( NTSOMM, MNSOMM, NTNSEF, MNNSEF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    SUPPRIMER LES COMPOSANTES DES TANGENTES DANS LE TMS XYZSOMMET
C -----    SUPPRIMER LE NUMERO DES TANGENTES DANS CHAQUE EF DU TMS NSEF
C
C ENTREES:
C --------
C NTSOMM : NUMERO      DU TABLEAU 'XYZSOMMET' DE L'OBJET
C MNSOMM : ADRESSE MCN DU TABLEAU 'XYZSOMMET' DE L'OBJET
C NTNSEF : NUMERO      DU TMS 'NSEF' DES NUMEROS DES EF
C MNNSEF : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES EF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    DECEMBRE 1995
C23456---------------------------------------------------------------012
      include"./incl/a___xyzsommet.inc"
C     include"./incl/a___nsef.inc"
      include"./incl/pp.inc"
      COMMON         MCN(MOTMCN)
C
      IF( MCN(MNSOMM+WNBTGS) .GT. 0 ) THEN
C
C        LE NOMBRE DE TANGENTES STOCKEES DANS LE TMS XYZSOMMET DEVIENT NUL
         MCN( MNSOMM + WNBTGS ) = 0
C
C        REDUCTION DE LA TAILLE DU TMS XYZSOMMET
         CALL TAMSRA( NTSOMM, WYZSOM+3*MCN(MNSOMM+WNBSOM) )
C
      ENDIF
C
C     COMME LES TANGENTES SONT SUPPRIMEES DANS XYZSOMMET MAIS
C     SONT PEUT ETRE PRESENTES DANS NSEF
C     ALORS LE TABLEAU NSEF EST REMIS A JOUR EN SUPPRIMANT LES
C     NUMEROS DES TANGENTES DE CHAQUE EF
      IF( MNNSEF .GT. 0 ) CALL NSEFT0( NTNSEF, MNNSEF )
C
      RETURN
      END
