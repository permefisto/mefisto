      SUBROUTINE TAMCDS( KTYPE, NBVARI, REZLMC, MCTABL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : FERMER ET DETRUIRE UN TABLEAU MC DE NBVARI VARIABLES DE TYPE
C ----- KTYPE . LES ZONES LIBRES DE MC SONT DECLAREES DANS LE TABLEAU
C       REZLMC
C
C ENTREE :
C --------
C KTYPE  : NOM DU TYPE DES VARIABLES DU TABLEAU SELON
C          LOGIQUE  CARACTERE  ENTIER/2  ENTIER  REEL REEL2 REEL4
C          COMPLEXE COMPLEXE2
C NBVARI : NOMBRE DE VARIABLES DU TABLEAU
C          ATTENTION:CE N EST PAS LE NOMBRE DE MOTS
C                    SI COMPLEXE2 LE NOMBRE DE MOTS=4*NBVARI
C REZLMC : TABLEAU REPERTOIRE DES ZONES LIBRES DANS MC
C          REZLMC(1,0)=VALEUR MAXIMALE DU 2-EME INDICE DU TABLEAU REZLMC
C          REZLMC(2,0)=VALEUR DU 2-EME INDICE DE REZLMC DESIGNANT LA
C                      1-ERE ZONE LIBRE.DEPART DU CHAINAGE ZONES LIBRES
C          REZLMC(3,0)=NUMERO DE LA 1-ERE COLONNE DE REZLMC NON OCCUPEE
C                      PAR UNE ZONE LIBRE.CHAINAGE DES COLONNES NON
C                      OCCUPEES DE REZLMC
C          REZLMC(1,N)=ADRESSE  MOT        DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(2,N)=NOMBRE   DE MOTS    DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(3,N)=CHAINAGE SUCCESSEUR DE LA N-EME ZONE LIBRE DE MC
C
C ENTREE ET SORTIE :
C ------------------
C MCTABL : EN ENTREE ADRESSE EN MOTS DU 1-ER MOT DU TABLEAU
C          EN SORTIE = 0 SI TOUT C EST BIEN PASSE
C                    > 0 VALEUR D ENTREE EN CAS D ERREUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR      : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1983
C MODIFICATION: PERRONNET ALAIN                          NOVEMBRE 1987
C.......................................................................
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/pp.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSIMTA / NOIMPR
      CHARACTER*(*)     KTYPE
      INTEGER           REZLMC(3,0:*)
C
C     PROTECTIONS
      IF( MCTABL .LE. 0 .OR. MCTABL .GT. MOTMCN ) THEN
         NBLGRC(NRERR) = 2
         WRITE(KERR(MXLGER)(1:16),'(I16)') MCTABL
         WRITE(KERR(MXLGER-1)(1:16),'(I16)') MOTMCN
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'TAMCDS: ADRESSE INCORRECTE='//KERR(MXLGER)(1:16)
            KERR(2) = 'NON COMPRISE ENTRE 1 ET ' // KERR(MXLGER-1)(1:16)
         ELSE
            KERR(1) = 'TAMCDS: NOT VALUABLE ADRESS='//KERR(MXLGER)(1:16)
            KERR(2) = 'NOT BETWEEN 1 and ' // KERR(MXLGER-1)(1:16)
         ENDIF
         CALL LEREUR
         RETURN
      ELSEIF( NBVARI .LE. 0 .OR. NBVARI .GT. MOTMCN ) THEN
         NBLGRC(NRERR) = 2
         WRITE(KERR(MXLGER)(1:16),'(I16)') NBVARI
         WRITE(KERR(MXLGER-1)(1:16),'(I16)') MOTMCN
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'TAMCDS: NOMBRE INCORRECT DE VARIABLES '
     %             // KERR(MXLGER)(1:16)
            KERR(2) = 'NON COMPRIS ENTRE 1 ET ' // KERR(MXLGER-1)(1:16)
         ELSE
            KERR(1) = 'TAMCDS: NOT VALUABLE VARIABLES NUMBER '
     %             // KERR(MXLGER)(1:16)
            KERR(2) = 'NOT BETWEEN 1 and ' // KERR(MXLGER-1)(1:16)
         ENDIF
         CALL LEREUR
         RETURN
      ENDIF
C
C     LE NOMBRE DE MOTS DU TABLEAU
      NUMTYV = NUMTYP( KTYPE )
      MOTABL = MOTTAB( NUMTYV , NBVARI )
C
C     CE NOMBRE EST RENDU PAIR POUR L'ALIGNEMENT
C     DES VARIABLES DOUBLE PRECISION
      MOTABL =(MOTABL+1)/2
      MOTABL = MOTABL * 2
C
C     AVANT DESTRUCTION VERIFICATION QUE LE TABLEAU NE CHEVAUCHE PAS
C     DE ZONE LIBRE OU N'EST ENGLOBE PAR UNE ZONE LIBRE
C     ==============================================================
      MCMOTB = MCTABL - 1 + MOTABL
      NZONE  = REZLMC(2,0)
C
C     PARCOURS DES ZONES LIBRES
 5    IF( NZONE .GT. 0 ) THEN
         MCZL   = REZLMC(1,NZONE)
         MOZL   = REZLMC(2,NZONE)
         MCMOZL = MCZL - 1 + MOZL
C
         IF( ( MCZL   .LE. MCTABL .AND. MCTABL .LE. MCMOZL ) .OR.
     %       ( MCZL   .LE. MCMOTB .AND. MCMOTB .LE. MCMOZL ) ) THEN
C            LA ZONE LIBRE CHEVAUCHE OU ENGLOBE LE TABLEAU A DETRUIRE
             NBLGRC(NRERR) = 5
             WRITE(KERR(MXLGER)(1:16),'(I16)') NBVARI
             WRITE(KERR(MXLGER)(17:32),'(I16)') MCTABL
             IF( LANGAG .EQ. 0 ) THEN
                KERR(1) = 'TAMCDS:LE TABLEAU DE TYPE ' // KTYPE
                KERR(2) = 'NOMBRE DE VARIABLES='//KERR(MXLGER)(1:16)
                KERR(3) = 'CHEVAUCHE UNE ZONE INTER-TABLEAUX'
                KERR(4) = 'ADRESSE DEBUT MCTABL=' // KERR(MXLGER)(17:32)
                KERR(5) = 'A CORRIGER AVANT DE RELANCER'
             ELSE
                KERR(1) = 'TAMCDS:The ARRAY of TYPE ' // KTYPE
                KERR(2) = 'VARIABLES NUMBER='//KERR(MXLGER)(1:16)
                KERR(3) = 'OVERLAPS a BETWEEN-ARRAY ZONE'
                KERR(4) = 'STARTING ADRESS MCTABL='//KERR(MXLGER)(17:32)
                KERR(5) = 'CORRECT IT BEFORE START AGAIN'
             ENDIF
             CALL LEREUR
             IF( LANGAG .EQ. 0 ) THEN
                WRITE(IMPRIM,10005) MCTABL,MCMOTB,MCZL,MCMOZL
             ELSE
                WRITE(IMPRIM,20005) MCTABL,MCMOTB,MCZL,MCMOZL
             ENDIF
10005 FORMAT('TABLEAU MAL POSITIONNE PAR RAPPORT AUX ZONES LIBRES'/
     %       'LE TABLEAU A DETRUIRE  OCCUPE LES PLACES ',I16,' A',I16/
     %       'LA ZONE INTER-TABLEAUX OCCUPE LES PLACES ',I16,' A',I16/)
20005 FORMAT('The ARRAY is INCORRECTLY STORED on FREE ZONES'/
     %       'It is BETWEEN THE WORDS ',I16,' to',I16/
     %       'and the BETWEEN-ARRAY FREE ZONE is BETWEEN the WORDS ',
     %        I16,' to',I16/)
             RETURN
         ENDIF
C
C        PAS D'ERREUR DETECTEE. PASSAGE A LA ZONE SUIVANTE
         NZONE = REZLMC(3,NZONE)
         GOTO 5
      ENDIF
C
C     RECHERCHE DES ZONES LIBRES DEVANT ET DERRIERE
C     =============================================
C     NLIMSV DESIGNE LE NUMERO DE LA ZONE LIBRE PLACEE DEVANT LE
C            TABLEAU A DETRUIRE
C     DEVANT LE TABLEAU A DETRUIRE
      NLIMSV = 0
C
C     LE CHAINAGE SUR LES ZONES LIBRES
      NZONE  = REZLMC(2,0)
C
C     EXISTE-T-IL UNE ZONE LIBRE DEVANT LE TABLEAU A DETRUIRE?
C     ========================================================
 10   IF( NZONE .GT. 0 ) THEN
C
C        MCZL ADRESSE DERRIERE LA ZONE LIBRE NZONE
         MCZL = REZLMC(1,NZONE) + REZLMC(2,NZONE)
C
         IF( MCZL .NE. MCTABL ) THEN
C           PASSAGE A LA ZONE LIBRE SUIVANTE
            NZONE  = REZLMC(3,NZONE)
            GOTO 10
C
         ELSE
C
C           IL Y A UNE ZONE LIBRE DEVANT LE TABLEAU A DETRUIRE
C           CETTE ZONE LIBRE EST AUGMENTEE.
C           --------------------------------------------------
            REZLMC(2,NZONE) = REZLMC(2,NZONE) + MOTABL
C           LE NUMERO DE CETTE ZONE LIBRE
            NLIMSV = NZONE
         ENDIF
      ENDIF
C
C     EXISTE-T-IL UNE ZONE LIBRE DERRIERE CE TABLEAU A DETRUIRE?
C     ==========================================================
      NAVANT = 0
      NZONE  = REZLMC(2,0)
C     MCTAFI : ADRESSE DU PREMIER MOT DERRIERE LE TABLEAU A DETRUIRE
      MCTAFI = MCTABL + MOTABL
C
 30   IF( NZONE .GT. 0 ) THEN
         IF( REZLMC(1,NZONE) .NE. MCTAFI ) THEN
C
C           PASSAGE A LA ZONE LIBRE SUIVANTE
            NAVANT = NZONE
            NZONE  = REZLMC(3,NZONE)
            GOTO 30
C
         ELSE
C
C           IL EXISTE UNE ZONE LIBRE DERRIERE LE TABLEAU
            IF( NLIMSV .EQ. 0 ) THEN
C
C              IL Y A UNE ZONE LIBRE DERRIERE MAIS PAS DEVANT LE TABLEAU
               REZLMC(1,NZONE) = MCTABL
               REZLMC(2,NZONE) = REZLMC(2,NZONE) + MOTABL
C
            ELSE
C
C              IL Y A UNE ZONE LIBRE DEVANT ET DERRIERE LE TABLEAU A TUER
C              LA ZONE LIBRE DEVANT A DEJA ETE TRAITEE MAIS
C              ELLE EST ENCORE ALLONGEE DE LA ZONE LIBRE DE DERRIERE
               REZLMC(2,NLIMSV) = REZLMC(2,NLIMSV) + REZLMC(2,NZONE)
C
C              LA ZONE LIBRE DERRIERE LE TABLEAU EST SUPPRIMEE
               IF( NAVANT .EQ. 0 ) THEN
C
C                 LA ZONE LIBRE EST LA PREMIERE
                  REZLMC(2,0) = REZLMC(3,NZONE)
C
               ELSE
C
C                 LA ZONE LIBRE N EST PAS LA PREMIERE
                  REZLMC(3,NAVANT) = REZLMC(3,NZONE)
               ENDIF
C
C              LA COLONNE CORRESPONDANTE NZONE REDEVIENT LIBRE
               REZLMC(3,NZONE) = REZLMC(3,0)
               REZLMC(3,0    ) = NZONE
            ENDIF
            GOTO 9999
         ENDIF
      ENDIF
C
C     FIN DE BOUCLE SUR LES ZONES LIBRES DERRIERE LE TABLEAU
C     ======================================================
C
      IF( NLIMSV .LE. 0 ) THEN
C
C        LE TABLEAU A DETRUIRE N'EST NI PRECEDE NI SUIVI D UNE ZONE
C        LIBRE.CE TABLEAU DEVIENT LA PREMIERE ZONE LIBRE
         L = REZLMC(3,0)
         IF( L .EQ. 0 ) THEN
C           IL N Y A PLUS DE COLONNE LIBRE DANS REZLMC
            NBLGRC(NRERR) = 2
            WRITE(KERR(MXLGER)(1:16),'(I16)') REZLMC(1,0)
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1)='NOMBRE MAXIMAL DE ZONES LIBRES='//
     %                    KERR(MXLGER)(1:16) //' SATURE'
               KERR(2)='AUGMENTER M2ZLMN DANS UTIL/OUVRMS ET RECOMPILER'
            ELSE
               KERR(1)='MAX NUMBER of FREE ZONES='//
     %                 KERR(MXLGER)(1:16) //' are SATURED'
               KERR(2)='AUGMENT M2ZLMN in UTIL/OUVRMS and COMPILE AGAIN'
            ENDIF
            CALL LEREUR
CCC            CALL XVPAUSE
            RETURN
         ELSE
C
C           LA ZONE LIBRE L EST INITIALISEE
C           SON ADRESSE
            REZLMC(1,L) = MCTABL
C           SON NOMBRE DE MOTS
            REZLMC(2,L) = MOTABL
C           LA PREMIERE COLONNE LIBRE EST LA SUIVANTE DE CELLE QUI
C           VIENT D ETRE EMPLOYEE
            REZLMC(3,0) = REZLMC(3,L)
C           CHAINAGE SUR L ANCIENNE PREMIERE ZONE LIBRE
            REZLMC(3,L) = REZLMC(2,0)
C           LA PREMIERE ZONE LIBRE EST L MAINTENANT
            REZLMC(2,0) = L
         ENDIF
      ENDIF
C
C     FIN NORMALE0
C     ============
 9999 IF( NOIMPR .NE. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19999) NBVARI,KTYPE,MCTABL
         ELSE
            WRITE(IMPRIM,29999) NBVARI,KTYPE,MCTABL
         ENDIF
      ENDIF
C
19999 FORMAT(' DESTRUCTION TABLEAU MC DE',I16,' VARIABLES',A16,
     %' D  ADRESSE MC',I16)
29999 FORMAT(' DELETION of ARRAY MC of',I16,' VARIABLES',A16,
     %' at ADRESS MC',I16)
      MCTABL = 0
      RETURN
      END
