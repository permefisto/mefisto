      SUBROUTINE TAMCRA( KTYPE , NBVARE , REZLMC , MCTABL , NBVARS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RACOURCIR LE NOMBRE DE VARIABLES NBVARE DU TABLEAU A UN NOMBRE
C ----- INFERIEUR NBVARS.(NBVARS=<NBVARE)
C       EN CAS D ERREUR NBVARS = 0 EN SORTIE
C
C ENTREE :
C --------
C KTYPE  : NOM DU TYPE DES VARIABLES DU TABLEAU SELON
C          LOGIQUE  CARACTERE  ENTIER/2  ENTIER  REEL REEL2 REEL4
C          COMPLEXE  COMPLEXE2
C NBVARE : NOMBRE DE VARIABLES DU TABLEAU EN ENTREE
C          ATTENTION:CE N EST PAS LE NOMBRE DE MOTS
C                    SI COMPLEXE2 LE NOMBRE DE MOTS=4*NBVARE
C REZLMC : TABLEAU REPERTOIRE DES ZONES LIBRES DANS MC
C          REZLMC(1,0)=VALEUR MAXIMALE DU 2-EME INDICE DU TABLEAU REZLMC
C          REZLMC(2,0)=VALEUR DU 2-EME INDICE DE REZLMC DESIGNANT LA
C                      1-ERE ZONE LIBRE.DEPART DU CHAINAGE ZONES LIBRES
C          REZLMC(3,0)=NUMERO DE LA 1-ERE COLONNE DE REZLMC NON OCCUPEE
C                      PAR UNE ZONE LIBRE.CHAINAGE DES COLONNES NON
C                      OCCUPEES DE REZLMC
C          REZLMC(1,N)=ADRESSE MOT         DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(2,N)=NOMBRE  DE MOTS     DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(3,N)=CHAINAGE SUCCESSEUR DE LA N-EME ZONE LIBRE DE MC
C MCTABL : ADRESSE DU PREMIER MOT DU TABLEAU DANS LE SUPER-TABLEAU
C
C ENTREE ET SORTIE :
C ------------------
C NBVARS : EN ENTREE NOMBRE DE VARIABLES APRES LA REDUCTION
C          EN SORTIE = NOMBRE PRECEDENT SI PAS D ERREUR
C                    = 0                SI ERREUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1983
C.......................................................................
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSIMTA / NOIMPR
      INTEGER           REZLMC(3,0:*)
      CHARACTER*(*)     KTYPE
C
C     PROTECTIONS
      IF( NBVARE .LT. NBVARS .OR. NBVARE .LE. 0 .OR. NBVARS .LE. 0 )THEN
         NBLGRC(NRERR) = 3
         WRITE(KERR(MXLGER)(1:10),'(I10)') NBVARE
         WRITE(KERR(MXLGER-1)(1:10),'(I10)') NBVARS
         IF( LANGAG .EQ. 0 ) THEN
         KERR(1) = 'NOMBRE INITIAL DE VARIABLES='//KERR(MXLGER)(1:10)
         KERR(2) = 'NOMBRE FINAL   DE VARIABLES='//KERR(MXLGER-1)(1:10)
         KERR(3) = 'VALEURS INCORRECTES POUR REDUIRE LE TABLEAU'
         ELSE
         KERR(1) = 'INITIAL NUMBER of VARIABLES='//KERR(MXLGER)(1:10)
         KERR(2) = 'FINAL   NUMBER of VARIABLES='//KERR(MXLGER-1)(1:10)
         KERR(3) = 'INCORRECT VALUES to REDUCE the ARRAY'
         ENDIF
         CALL LEREUR
         NBVARS = 0
         RETURN
      ELSEIF( MCTABL .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:10),'(I10)') MCTABL
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) ='TAMCRA: ADRESSE ERRONEE='//KERR(MXLGER)(1:10)
         ELSE
            KERR(1) ='TAMCRA: INCORRECT ADRESS='//KERR(MXLGER)(1:10)
         ENDIF
         NBVARS = 0
         RETURN
      ENDIF
C
C     LE NOMBRE DE MOTS DU TABLEAU AVANT ET APRES
      NUMTYV = NUMTYP( KTYPE  )
      MOTAVA = MOTTAB( NUMTYV , NBVARE )
C     CE NOMBRE EST RENDU PAIR POUR L'ALIGNEMENT
C     DES VARIABLES DOUBLE PRECISION
      MOTAVA =(MOTAVA+1)/2
      MOTAVA = MOTAVA * 2
C
      MOTAPR = MOTTAB( NUMTYV , NBVARS )
C     CE NOMBRE EST RENDU PAIR POUR L'ALIGNEMENT
C     DES VARIABLES DOUBLE PRECISION
      MOTAPR =(MOTAPR+1)/2
      MOTAPR = MOTAPR * 2
C
      IF( NBVARE .EQ. NBVARS ) GOTO 9999
C
C     EXISTE-T-IL UNE ZONE LIBRE DERRIERE LE TABLEAU INITIAL?
C     =======================================================
C     ADRESSE DU MOT DERRIERE LE TABLEAU INITIAL
      MCTF = MCTABL + MOTAVA
C
C     LE CHAINAGE DES ZONES LIBRES
      NZONE  = REZLMC(2,0)
C
C     LA BOUCLE SUR LES ZONES LIBRES
 30   IF( NZONE .EQ. 0 ) GOTO 40
      MCZL = REZLMC(1,NZONE)
      IF( MCZL .NE. MCTF ) THEN
C
C        PASSAGE A LA ZONE LIBRE SUIVANTE
         NZONE  = REZLMC(3,NZONE)
         GOTO 30
      ELSE
C
C        LE TABLEAU EST SUIVI D UNE ZONE LIBRE.
C        ELLE EST ALLONGEE DE LA DIFFERENCE DE MOTS
C        ------------------------------------------
         REZLMC(1,NZONE) = MCTABL + MOTAPR
         REZLMC(2,NZONE) = REZLMC(2,NZONE) + MOTAVA - MOTAPR
         GOTO 9999
      ENDIF
C
C     IL N EXISTE PAS DE ZONE LIBRE DERRIERE LE TABLEAU
C     UNE ZONE LIBRE EST CREEE
C     -------------------------------------------------
 40   L = REZLMC(3,0)
      IF( L .EQ. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:4),'(I4)') REZLMC(1,0)
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'NOMBRE MAXIMAL DE ZONES LIBRES ATTEINT='
     %            //KERR(MXLGER)(1:10)
         ELSE
            KERR(1) = 'MAX of FREE ZONES ATTAINED='
     %            //KERR(MXLGER)(1:10)
         ENDIF
         CALL LEREUR
C        LA ZONE EST PERDUE.POURSUITE DU TRAITEMENT.
      ELSE
C
C        LA COLONNE LIBRE SUIVANTE
         REZLMC(3,0) = REZLMC(3,L)
C
C        LA COLONNE=LA ZONE EST INITIALISEE
C        SON ADRESSE
         REZLMC(1,L) = MCTABL + MOTAPR
C        SON NOMBRE DE MOTS
         REZLMC(2,L) = MOTAVA - MOTAPR
C
C        LE CHAINAGE . L DEVIENT LA PREMIERE ZONE LIBRE
         REZLMC(3,L) = REZLMC(2,0)
         REZLMC(2,0) = L
      ENDIF
C
 9999 IF( NOIMPR .NE. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19999) NBVARE,NUMTYV,NBVARS
         ELSE
            WRITE(IMPRIM,29999) NBVARE,NUMTYV,NBVARS
         ENDIF
      ENDIF
19999 FORMAT(' REDUCTION   TABLEAU MC DE',I9,' VARIABLES DE TYPE',I3,
     %' A',I9,' VARIABLES')
29999 FORMAT(' REDUCTION SIZE of ARRAY MC of',I9,' VARIABLES of TYPE',
     %I3,' to',I9,' VARIABLES')
      RETURN
      END
