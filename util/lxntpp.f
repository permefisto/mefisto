      SUBROUTINE LXNTPP( KNOM , NTLXNM , MNLXNM , N1 , L )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETOURNER LE NUMERO DU TABLEAU MS DU LEXIQUE PERE DU TABLEAU MS
C ----- DE NOM KNOM
C
C       DIFFERENCE AVEC LE SP LXNTPN : CE SP DECLARE LES LEXIQUES
C                                      INTERMEDIARES INEXISTANTS
C
C ENTREE  :
C ---------
C KNOM    : NOM DU TABLEAU MS DONT ON VEUT LE NUMERO DU LEXIQUE PERE
C
C SORTIES :
C ---------
C NTLXNM  : NUMERO DU TABLEAU MS LEXIQUE PERE DE KNOM
C           =0 SI KNOM EST INCORRECT
C MNLXNM  : ADRESSE MCN DE CE LEXIQUE QUI EST OUVERT EN SORTIE
C N1      : POSITION DANS KNOM DU 1-ER CARACTERE DU DERNIER CHAMP
C L       : POSITION DANS KNOM DU CARACTERE QUI PRECEDE LE 1-ER BLANC
C           OU SANS BLANC LA LONGUEUR DE LA CHAINE KNOM
C
C ATTENTION : LES LEXIQUES INTERMEDIAIRES SONT OUVERTS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS SEPTEMBRE 1988
C23456---------------------------------------------------------------012
      include"./incl/gsmenu.inc"
      COMMON / MSSFTA /  MSSF(28),NTADAM
      COMMON / UNITES /  LECTEU,IMPRIM,NUNITE(30)
      CHARACTER*(*)      KNOM
      CHARACTER*1        SEPARE
      DATA               SEPARE/ '>' /
C
C     LE NOMBRE DE CARACTERES DE KNOM
      L  = LEN( KNOM )
C
C     RECHERCHE DU 1-ER BLANC
      N1 = INDEX( KNOM , ' ' )
      IF( N1 .GT. 0 ) L = N1-1
      IF( L .LE. 0 ) GOTO 9000
C
C     LE LEXIQUE DE DEPART EST ADAM
      NTLXNM = NTADAM
      N1     = INDEX( KNOM , SEPARE )
      IF( N1 .LE. 0 ) THEN
C        PAS DE > DONC LEXIQUE ADAM
         CALL TAMSOU( NTLXNM , MNLXNM )
         N1 = 1
         RETURN
      ENDIF
C
C     LA 1-ERE ET DERNIERE LETTRE APRES >
      N1 = N1 + 1
C
C     BOUCLE SUR LES DIFFERENTS MOTS DE KNOM SEPARES PAR '>'
C     RECHERCHE DU 1-ER CARACTERE > RENCONTRE
 10   N2 = INDEX( KNOM(N1:L) , SEPARE )
C
      IF( N2 .GT. 0 ) THEN
C        N1:N2-1 EST LE NOM DU LEXIQUE INTERMEDIAIRE
C        OUVERTURE DE CE LEXIQUE A PARTIR DU NOM
         N2 = N1 + N2 - 1
 20      CALL LXLXOU( NTLXNM , KNOM(N1:N2-1) , NOTAMS , MCTAMS )
C
C        SI LE LEXIQUE N'EXISTE PAS , ALORS IL EST CREE
         IF( NOTAMS .LE. 0 ) THEN
            CALL LXLXDC( NTLXNM , KNOM(N1:N2-1) , 24 , 8 )
            GOTO 20
         ENDIF
C
         NTLXNM = NOTAMS
         MNLXNM = MCTAMS
         IF( NOTAMS .LE. 0 ) GOTO 9000
C        PASSAGE AU CHAMP SUIVANT
         N1     = N2 + 1
         GOTO 10
      ENDIF
C
C     N1:L EST LE NOM TERMINAL
      RETURN
C
C     ERREUR
 9000 NBLGRC(NRERR) = 2
      KERR(1) = KNOM
      KERR(1) ='LXNTPP:NOM INCORRECT'
      CALL LEREUR
      NTLXNM = 0
      MNLXNM = 0
      N1     = 0
      L      = 0
      END
