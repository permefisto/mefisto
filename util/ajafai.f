      SUBROUTINE AJAFAI( KNOMOB, NTLXOB,
     %                   MOARET, MXARET, NBARFB, NBARIN, L1ARFB, L1ARIN,
     %                   LARETE,
     %                   NLARFB, NLARIN, IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   CREER EVENTUELLEMENT LA LIGNE "FRONTIERE_SANS_CL"
C -----   CREER EVENTUELLEMENT LA LIGNE "INTERFACE_SANS_CL"
C         AJOUTER CES 2 LIGNES A LA DEFINITION DE L'OBJET 2D
C         AJOUTER LE NUMERO DE LIGNE DES ARETES CONCERNEES DANS NPEF"
C
C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET
C NTLXOB : NUMERO DU TMS DU LEXIQUE DE L'OBJET 2D
C MOARET : NOMBRE DE MOTS PAR ARETE DU MAILLAGE DANS LARETE
C MXARET : NOMBRE MAXIMAL D'ARETES DECLARABLES DANS LARETE
C NBARFB : NOMBRE D'ARETES FRONTALIERES SANS CL
C NBARIN : NOMBRE D'ARETES INTERFACES SANS CL
C L1ARFB : NUMERO DANS LARETE DE LA 1-ERE ARETE FRONTIERE
C L1ARIN : NUMERO DANS LARETE DE LA 1-ERE ARETE INTERFACE
C LARETE : TABLEAU DE HACHAGE DES ARETES DU MAILLAGE DE L'OBJET
C          LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C          LARETE(4,I)= NUMERO DU 1-ER  TOUQ CONTENANT CETTE ARETE
C                       >0 SI ARETE   DIRECTE DANS CE TOUQ
C                       <0 SI ARETE INDIRECTE DANS CE TOUQ
C          SI L'ARETE APPARTIENT A 2 TOUQ ALORS
C          LARETE(5,I)= NUMERO DU 2-EME TOUQ CONTENANT CETTE ARETE
C                       >0 SI ARETE   DIRECTE DANS CE TOUQ
C                       <0 SI ARETE INDIRECTE DANS CE TOUQ
C                       =0 SI ARETE APPARTENANT A UN SEUL TOUQ
C          LARETE(6,I)= NUMERO DANS LARETE DE L'ARETE SUIVANTE
C                       SOIT DANS LE CHAINAGE D'UNE LIGNE J ENTRE NUMILF ET NUMX
C                       SOIT DANS LE CHAINAGE DES ARETES FRONTALIERES
C                       0 SI C'EST LA DERNIERE
C
C          L1ARFB = NUMERO DE LA PREMIERE ARETE CHAINEE FRONTALIERE DANS LARETE
C          L1ARIN = NUMERO DE LA PREMIERE ARETE CHAINEE INTERFACE   DANS LARETE
C          L1LGFR(J)  = NUMERO DE LA PREMIERE ARETE CHAINEE DE LA LIGNE J
C                       POUR J COMPRIS ENTRE NUMILF ET NUMXLF
C NLARFB : NUMERO DE LA LIGNE "FRONTIERE_SANS_CL"
C          0 SI ELLE N'A PAS ETE AJOUTEE
C NLARIN : NUMERO DE LA LIGNE "INTERFACE_SANS_CL"
C          0 SI ELLE N'A PAS ETE AJOUTEE
C
C SORTIES:
C --------
C IERR   : 0 SI AUCUN PROBLEME RENCONTRE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS     FEVRIER 1995
C ...................................................................012
      PARAMETER     (MXTYEL=7)
      include"./incl/ponoel.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___arete.inc"
      include"./incl/a_ligne__definition.inc"
      include"./incl/a_objet__definition.inc"
      include"./incl/a_objet__topologie.inc"
C
      include"./incl/pp.inc"
      COMMON        MCN(MOTMCN)
      CHARACTER*(*) KNOMOB
      CHARACTER*4   NOM4, CHARX
      CHARACTER*24  KLG
      INTEGER       LARETE(MOARET,MXARET)
C
      NLARFB = 0
      NLARIN = 0
      IF( NBARFB .LE. 0 .AND. NBARIN .LE. 0 ) RETURN
C
C     NOMBRE DE CARACTERES DU NOM DE L'OBJET
      NBC = NUDCNB( KNOMOB )
C     5 CARACTERES RESERVES POUR .F0CL OU .I0CL
      NBC = MIN( NBC, 19)
C
C     DECLARATION OUVERTURE ET NUMERO DE LA LIGNE "FRONTIERE_SANS_CL"
C     ===============================================================
      IF( NBARFB .GT. 0 ) THEN
         KLG = KNOMOB(1:NBC) // '"F0CL'
         CALL LXLXDC( NTLIGN, KLG, 24, 16 )
         CALL LXLXOU( NTLIGN, KLG, NTLXAF, MNLXAF )
         IF( NTLXAF .LE. 0 ) RETURN
C
C        LE TABLEAU DEFINITION DE CETTE LIGNE
         CALL LXTNDC( NTLXAF, 'DEFINITION', 'MOTS', WUTSSL+1 )
         CALL LXTSOU( NTLXAF, 'DEFINITION', NTAFDF, MNAFDF )
         IF( NTAFDF .LE. 0 ) RETURN
C
C        LE TABLEAU DEFINITION EST CONSTRUIT COMME LECTURRE DE XYZSOMMET ET NSEF
C        LA TRANSFORMATION I
         MCN( MNAFDF + WTYTRL ) = 1
C        LE TYPE DE LA LIGNE
         MCN( MNAFDF + WUTYLI ) = 10
C        LE NUMERO DU TABLEAU XYZSOMMET INEXISTANT POUR L'INSTANT
         MCN( MNAFDF + WUTSOL ) = 0
C        LE NUMERO DU TABLEAU NSEF INEXISTANT POUR L'INSTANT
         MCN( MNAFDF + WUTSSL ) = 0
C
C        LA DATE DE CREATION
         CALL ECDATE( MCN(MNAFDF) )
C
C        LE NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MNAFDF + MOTVAR(6) ) = NONMTD( '~>LIGNE>>DEFINITION' )
C
C        LE NUMERO DE LA LIGNE
         CALL NUOBNM( 'LIGNE', KLG, NLARFB )
         IF( NLARFB .LE. 0 ) RETURN
      ENDIF
C
C     DECLARATION OUVERTURE ET NUMERO DE LA LIGNE "INTERFACE_SANS_CL"
C     ===============================================================
      IF( NBARIN .GT. 0 ) THEN
         KLG = KNOMOB(1:NBC) // '"I0CL'
         CALL LXLXDC( NTLIGN, KLG, 24, 16 )
         CALL LXLXOU( NTLIGN, KLG, NTLXAI, MNLXAI )
         IF( NTLXAI .LE. 0 ) RETURN
C
C        LE TABLEAU DEFINITION DE CETTE LIGNE
         CALL LXTNDC( NTLXAI, 'DEFINITION', 'MOTS', WUTSSL+1 )
         CALL LXTSOU( NTLXAI, 'DEFINITION', NTAIDF, MNAIDF )
         IF( NTAIDF .LE. 0 ) RETURN
C
C        LE TABLEAU DEFINITION EST CONSTRUIT COMME LECTURRE DE XYZSOMMET ET NSEF
C        LA TRANSFORMATION I
         MCN( MNAIDF + WTYTRL ) = 1
C        LE TYPE DE LA LIGNE
         MCN( MNAIDF + WUTYLI ) = 10
C        LE NUMERO DU TABLEAU XYZSOMMET INEXISTANT POUR L'INSTANT
         MCN( MNAIDF + WUTSOL ) = 0
C        LE NUMERO DU TABLEAU NSEF INEXISTANT POUR L'INSTANT
         MCN( MNAIDF + WUTSSL ) = 0
C
C        LA DATE DE CREATION
         CALL ECDATE( MCN(MNAIDF) )
C
C        LE NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MNAIDF + MOTVAR(6) ) = NONMTD( '~>LIGNE>>DEFINITION' )
C
C        LE NUMERO DE LA LIGNE
         CALL NUOBNM( 'LIGNE', KLG, NLARIN )
         IF( NLARIN .LE. 0 ) RETURN
      ENDIF
C
C     RECHERCHE DES TABLEAUX TOPOLOGIE SOMMETS NOEUDS POINTS ASSOCIES A L'OBJET
C     ADRESSAGE DES ADRESSES DES TABLEAUX NPEF".... DE CET OBJET
C     =========================================================================
      CALL TNMCDC( 'ENTIER' , 2*MXTYEL , MNELEM )
      MNTELE = MNELEM + MXTYEL
      CALL NDPGEL( NTLXOB , NTTOPO , MNTOPO ,
     %             NTPOGE , MNPOGE , NTNOEU , MNNOEU ,
     %             NBTYEL , MCN(MNTELE) , MCN(MNELEM) , IERR )
C     NTTOPO : NUMERO      DU TMS 'TOPOLOGIE' DE L'OBJET
C     MNTOPO : ADRESSE MCN DU TMS 'TOPOLOGIE' DE L'OBJET
C     NTPOGE : NUMERO      DU TMS 'XYZPOINT'    DE L'OBJET
C     MNPOGE : ADRESSE MCN DU TMS 'XYZPOINT'    DE L'OBJET
C     NTNOEU : NUMERO      DU TMS 'XYZNOEUD'    DE L'OBJET
C     MNNOEU : ADRESSE MCN DU TMS 'XYZNOEUD'    DE L'OBJET
C     NBTYEL : NOMBRE DE TYPES D'ELEMENTS DU MAILLAGE
C     NTELEM : NUMERO      DU TMS DES NBTYEL TYPES D'ELEMENTS
C     MNELEM : ADRESSE MCN DU TMS DES NBTYEL TYPES D'ELEMENTS
      IF( IERR .NE. 0 ) RETURN
C
C     NDPGST : CODE TRAITEMENT DES XYZ DES SOMMETS POINTS NOEUDS DU MAILLAGE
C              0 : NOEUDS=POINTS=SOMMETS
C              1 : NOEUDS=POINTS#SOMMETS
C              2 : NOEUDS#POINTS=SOMMETS
C              3 : NOEUDS#POINTS#SOMMETS
      NDPGST = MCN( MNTOPO + WDPGST )
C
C     BOUCLE SUR LES TABLEAUX NPEF"  POUR LEUR MISE A JOUR
C     C-A-D AJOUT DU NUMERO DES ARETES FRONTALIERES ET INTERFACES
C     ============================================================
      DO 50 I = 0, NBTYEL-1
C
C        LE NOM DU TABLEAU NPEF"
         NOM4 = CHARX( MCN(MNTOPO+WMTYEL+I) )
C
C        L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF DE CE TYPE D'EF
         MNNPEF = MCN( MNELEM + I )
C
C        LE NUMERO DU TYPE DES ELEMENTS FINIS
         NUTYEL = MCN( MNNPEF + WUTYEL )
C        LE NOMBRE DE TELS ELEMENTS FINIS
         NBELEM = MCN( MNNPEF + WBELEM )
C        NBNDEL nombre de noeuds d''un EF
         NBNDEL = MCN( MNNPEF + WBNDEL )
C        NBPGEL nombre de points d'un EF (0 si points=noeuds)
         NBPGEL = MCN( MNNPEF + WBPGEL )
C        NBPSEL nombre de SOMMETS d'EF sur un point utilisateur
         NBPSEL = MCN( MNNPEF + WBPSEL )
C        MOPSEL nombre de variables des tableaux NLPSEL et NEPSEL
         MOPSEL = MCN( MNNPEF + WOPSEL )
C        NBLAEL nombre d'ARETES d'EF sur une ligne utilisateur
         NBLAEL = MCN( MNNPEF + WBLAEL )
C        MOLAEL nombre de variables des tableaux NLLAEL et NELAEL
         MOLAEL = MCN( MNNPEF + WOLAEL )
C        NBSFEL nombre de FACES d''EF sur une surface utilisateur
         NBSFEL = MCN( MNNPEF + WBSFEL )
C        MOSFEL nombre de variables des tableaux NLSFEL et NESFEL
         MOSFEL = MCN( MNNPEF + WOSFEL )
C        NBVCEL nombre d'EF dans un volume utilisateur
         NBVCEL = MCN( MNNPEF + WBVCEL )
C        MOVCEL nombre de variables des tableaux NLVCEL et NEVCEL
         MOVCEL = MCN( MNNPEF + WOVCEL )
C
C        LES CARACTERISTIQUES DE L'ELEMENT FINI
         CALL ELTYCA( NUTYEL )
C
C        L'ADRESSE MCN DU TABLEAU 'NPEF' POUR CE TYPE D'EF
         MNPGEL = MNNPEF + WUNDEL
         IF( NDPGST .GE. 2 ) THEN
            MNPGEL = MNPGEL + NBELEM * NBNOE
         ENDIF
C
C        AJOUT DES NUMEROS DE LIGNES NLARFB ET NLARIN AUX ARETES DES EF
         MLLAEL = MNPGEL + NBELEM * NBPOE + MCN(MNNPEF+WBPSEL) +
     %            MCN(MNNPEF+WOPSEL) * 2
         NBLAEL = MCN(MNNPEF+WBLAEL)
         MOLAEL = MCN(MNNPEF+WOLAEL)
         MULAEL = MLLAEL + NBLAEL
         MELAEL = MULAEL + MOLAEL
C
C        LE TABLEAU DE REMPLACEMENT
         CALL TNMCDC( 'ENTIER', NBLAEL + NBARFB + NBARIN, M1LAEL )
         K = MOLAEL + NBARFB + NBARIN
         CALL TNMCDC( 'ENTIER', 2*K, M2LAEL )
         M3LAEL = M2LAEL + K
C
         CALL AJAFA1( MOARET, MXARET, L1ARFB, L1ARIN,
     %                LARETE, NLARFB, NLARIN,
     %                NUTYEL, NBELEM, MNNPEF,
     %                NBLAEL, MCN(MLLAEL), MCN(MULAEL), MCN(MELAEL),
     %                NBLAE1, MCN(M1LAEL), MCN(M2LAEL), MCN(M3LAEL),
     %                IERR )
         IF( IERR .NE. 0 ) RETURN
C
C        REMPLACEMENT DE L'ANCIEN TABLEAU NPEF PAR LE NOUVEAU
C        ----------------------------------------------------
         L  = WUNDEL + NBELEM * (NBNDEL+NBPGEL) + NBPSEL + MOPSEL*2
         L0 = NBLAE1 * 3
         L1 = NBSFEL + MOSFEL*2
         CALL LXTNDC( NTLXOB, 'NPEF2', 'ENTIER', L+L0+L1 )
         CALL LXTSOU( NTLXOB, 'NPEF2',  NTNPE2,  MNNPE2 )
         CALL TRTATA( MCN(MNNPEF), MCN(MNNPE2), L )
         CALL TRTATA( MCN(M1LAEL), MCN(MNNPE2+L),          NBLAE1 )
         CALL TRTATA( MCN(M2LAEL), MCN(MNNPE2+L+NBLAE1),   NBLAE1 )
         CALL TRTATA( MCN(M3LAEL), MCN(MNNPE2+L+NBLAE1*2), NBLAE1 )
         CALL TRTATA( MCN(MNNPEF+L+NBLAEL+MOLAEL*2),
     %                MCN(MNNPE2+L+NBLAE1*3), L1 )
C        DESTRUCTION DE L'ANCIEN TMS NPEF"
         CALL LXTSDS( NTLXOB, 'NPEF"'//NOM4 )
C        CHANGEMENT DU NOM NPEF2 EN NPEF"
         CALL LXNMNM( NTLXOB, 'NPEF2', 'NPEF"'//NOM4 )
C        MISE A JOUR DES TAILLES DES TABLEAUX
         MCN(MNNPE2+WBLAEL) = NBLAE1
         MCN(MNNPE2+WOLAEL) = NBLAE1
C
C        DESTRUTION DES TABLEAUX MCN INUTILES
         CALL TNMCDS( 'ENTIER', NBLAEL + NBARFB + NBARIN, M1LAEL )
         CALL TNMCDS( 'ENTIER', 2*K, M2LAEL )
 50   CONTINUE
C
C     DESTRUCTION DU TABLEAU DEVENU INUTILE
      CALL TNMCDS( 'ENTIER', 2*MXTYEL , MNELEM )
C
C     AJOUT DES 2 LIGNES DANS LA DEFINITION DE LA TOPOLOGIE DE L'OBJET
C     ================================================================
      NBOBIN = MCN( MNTOPO + WBOBIN )
      NBOBCL = MCN( MNTOPO + WBOBCL )
C
C     LE NOMBRE DE MOTS DU TABLEAU TOPOLOGIE
C     AVANT ET APRES AJOUT DES 2 LIGNES
      L0 = WMTYEL + NBTYEL + MOTVAR(13) * (NBOBIN + NBOBCL)
      L1 = L0
      IF( NBARFB .GT. 0 ) L1 = L1 + MOTVAR(13)
      IF( NBARIN .GT. 0 ) L1 = L1 + MOTVAR(13)
C
C     LE NOUVEAU TABLEAU TOPOLOGIE DE CET OBJET
      CALL LXTNDC( NTLXOB, 'TOPO', 'MOTS', L1 )
      CALL LXTSOU( NTLXOB, 'TOPO', NT1, MN1 )
C     COPIE DE L'ANCIEN DANS LE NOUVEAU
      CALL TRTATA( MCN(MNTOPO), MCN(MN1), L0 )
C
C     L'ADRESSE MCN DU DEBUT DU TABLEAU NUOBCL
      MNOBCL = MN1 + L0
C     AJOUT DE LA LIGNE "FRONTIERE_SANS_CL"
      IF( NBARFB .GT. 0 ) THEN
C        MISE A JOUR DU NOMBRE D'OBJETS A LA LIMITE
         NBOBCL = NBOBCL + 1
C        LE TYPE LIGNE
         MCN(MNOBCL) = 2
C        LE NUMERO DE LA LIGNE
         MCN(MNOBCL+1) = NLARFB
         MNOBCL = MNOBCL + 2
      ENDIF
C
C     AJOUT DE LA LIGNE "INTERFACE_SANS_CL"
      IF( NBARIN .GT. 0 ) THEN
C        MISE A JOUR DU NOMBRE D'OBJETS A LA LIMITE
         NBOBCL = NBOBCL + 1
C        LE TYPE LIGNE
         MCN(MNOBCL) = 2
C        LE NUMERO DE LA LIGNE
         MCN(MNOBCL+1) = NLARIN
      ENDIF
      MCN( MN1 + WBOBCL ) = NBOBCL
C
C     DESTRUCTION DE L'ANCIEN TABLEAU TOPOLOGIE
      CALL LXTSDS( NTLXOB, 'TOPOLOGIE' )
C
C     LE NOUVEAU TABLEAU TOPOLOGIE EST RENOMME
      CALL LXNMNM( NTLXOB, 'TOPO', 'TOPOLOGIE' )
C
C     MODIFICATION DU TABLEAU OBJET>KNOMOB>DEFINITION
C     ===============================================
      CALL LXTSOU( NTLXOB, 'DEFINITION', NT1, MN1 )
C     LE NOMBRE DE PLSVO AVANT
      NB1 = MCN(MN1+WBDOBJ)
C     LE NOMBRE DE MOTS DU TABLEAU DEFINITION DE L'OBJET
      L0  = WTYOBJ + MOTVAR(13) * NB1
C     LE NOMBRE DE PLSVO APRES
      IF( NBARFB .GT. 0 ) NB1 = NB1 + 1
      IF( NBARIN .GT. 0 ) NB1 = NB1 + 1
C     LE NOUVEAU TABLEAU DEFINITION DE CET OBJET
      L1 = WTYOBJ + MOTVAR(13) * NB1
      CALL LXTNDC( NTLXOB, 'DEFY', 'MOTS', L1 )
      CALL LXTSOU( NTLXOB, 'DEFY', NT, MN )
      CALL TRTATA( MCN(MN1), MCN(MN), L0 )
C     MISE A JOUR DU NOMBRE DE PLSV
      MCN(MN+WBDOBJ) = NB1
C
      IF( NLARFB .GT. 0 ) THEN
C        AJOUT DE LA LIGNE NLARFB  ( TYPE 2 LIGNE ET NUMERO )
         MCN( MN + L0 ) = 2
         MCN( MN + L0 + 1) = NLARFB
         MN = MN + 2
      ENDIF
C
      IF( NLARIN .GT. 0 ) THEN
C        AJOUT DE LA LIGNE NLARIN  ( TYPE 2 LIGNE ET NUMERO )
         MCN( MN + L0 ) = 2
         MCN( MN + L0 + 1) = NLARIN
      ENDIF
C
C     DESTRUCTION DE L'ANCIEN
      CALL LXTSDS( NTLXOB, 'DEFINITION' )
C
C     LE NOUVEAU NOM DU TABLEAU DEFINITION DE L'OBJET
      CALL LXNMNM( NTLXOB, 'DEFY', 'DEFINITION' )
      END
