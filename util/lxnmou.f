      SUBROUTINE LXNMOU( NTLX, KNOM, KTYPE, NOTAMS, MCTAMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    OUVRIR LE NOM KNOM DU LEXIQUE NTLX
C -----
C
C ENTREES :
C ---------
C NTLX   : NUMERO DU TABLEAU MS CONTENANT LE LEXIQUE
C KNOM   : CHAINE DE CARACTERES NOM A RETROUVER DANS LE LEXIQUE
C
C SORTIES :
C ---------
C KTYPE  : NOM DU TYPE 'LEXI' OU 'TAMS' OU 'TATA'
C          DANS CES CAS LE TABLEAU MS ASSOCIE A LES CARACTERISTIQUES
C NOTAMS : NUMERO DE CE TABLEAU MS
C          0 SI KNOM N'A PAS ETE RETROUVE DANS LE LEXIQUE ET RETOUR
C          ==> NECESSITE DE FAIRE UN TEST EN RETOUR
C MCTAMS : ADRESSE EN MEMOIRE CENTRALE MCN OU MCK DE CE TABLEAU MS
C          0 SI KNOM N'A PAS ETE RETROUVE DANS LE LEXIQUE ET RETOUR
C          ==> NECESSITE DE FAIRE UN TEST EN RETOUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   OCTOBRE 1984
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      include"./incl/gsmenu.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      CHARACTER*(*)     KNOM
      CHARACTER*4       KTYPE,CHARX
C
C     OUVERTURE DU TABLEAU MS = LEXIQUE NTLX
C     RECHERCHE DU NOM KNOM PARMI LES NOMS DU LEXIQUE NTLX
      CALL LXNMNO( NTLX , KNOM , NO , MNLX )
C
C     LE NOM A T IL ETE RETROUVE ?
      IF( NO .LE. 0 ) THEN
C
C        NON
C        ===
         NOTAMS = 0
         MCTAMS = 0
         GOTO 9999
C
      ENDIF
C
C     OUI
C     ===
C     ADRESSE DANS MCN DU NOM DANS LE LEXIQUE
      MN = MNLX + MCN( MNLX ) * NO + MCN( MNLX+2 ) + 1
C     LE TYPE DU NOM 'LEXI' 'TAMS' 'TATA' 'RELA'
      KTYPE = CHARX( MCN(MN) )
C     LE NUMERO DU TAMS ET SON OUVERTURE
      NOTAMS = MCN( MN + 1 )
      CALL TAMSOU( NOTAMS , MCTAMS )
C
 9999 RETURN
      END
