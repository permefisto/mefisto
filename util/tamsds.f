      SUBROUTINE TAMSDS( NOTAMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DETRUIRE LE TABLEAU MS NOTAMS
C ----- DU FICHIER QUI LE SAUVEGARDE
C       EVENTUELLEMENT DE LA MEMOIRE CENTRALE S'IL Y RESIDE ENCORE
C
C ENTREE :
C --------
C NOTAMS : NUMERO DU TABLEAU MS A DETRUIRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/ppmck.inc"
      include"./incl/motmcg.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/msvaau.inc"
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      COMMON / MSIMTA / NOIMPR
      CHARACTER*9       TYPNUM
C
C     RECHERCHE DU DESCRIPTEUR RETAMS(1..6,NOTAMS)
      CALL TAMSRE( NOTAMS , MGTAMS )
C     RETAMS(1)=NTTAMS NO DU TYPE DU TABLEAU MS
C                      0 SI LE TABLEAU N EST PAS DECLARE
C     RETAMS(2)=NVTAMS NOMBRE DE VARIABLES DU TABLEAU MS
C     RETAMS(3)=NFTAMS NUMERO DU FICHIER MS (1 A M2FIMS) DU TABLEAU MS
C                      0 SI NON DECLARE
C     RETAMS(4)=N1TAMS NUMERO DE LA PREMIERE PAGE DU TABLEAU SUR
C                      LE FICHIER NFTAMS QUI LE SAUVEGARDE
C     RETAMS(5)=MCTAMS ADRESSE MC DU PREMIER MOT DU TABLEAU MS
C                      >0 LE TABLEAU EST OUVERT( VERROUILLE ) EN MC
C                         IL EST PRET A ETRE UTILISE
C                         IL NE PEUT ETRE SAUVEGARDE SUR SON FICHIER
C                         TANT QU IL N EST PAS FERME
C                      =0 LE TABLEAU N EST PAS EN MC
C                         IL EST DECLARE MAIS PAS OUVERT
C                      <0 LE TABLEAU EST FERME,SUSCEPTIBLE
C                         D ETRE SAUVEGARDE SUR SON FICHIER MS
C                         IL EST UTILISABLE SEULEMENT S IL EST REOUVERT
C     RETAMS(6)=LSTAMS CHAINAGE AVAL DES TABLEAUX MS LIBRES OU FERMES
C
C     LE TABLEAU EST-IL EN MEMOIRE CENTRALE MC ?
      MCTAMS = MCG( MGTAMS + 4 )
C     SAUVEGARDE DE CETTE ADRESSE DANS MSTAMS
      MSTAMS = MCTAMS
      IF( MCTAMS .NE. 0 ) THEN
C
C        IL EXISTE EN MC.IL ETAIT OUVERT OU FERME.IL EST DETRUIT.
C        =============== -----------------------------------------
         IF( MCG(MGTAMS) .EQ. NOTYPK ) THEN
C           LE TABLEAU EST UN TABLEAU DE CARACTERES
            MGZL = MGZLMK
         ELSE
C           LE TABLEAU EST UN TABLEAU NUMERIQUE
            MGZL = MGZLMN
         ENDIF
C        DESTRUCTION DU TABLEAU
         MCTAMS = ABS( MCTAMS )
         CALL TAMCDS(TYPNUM(MCG(MGTAMS)),MCG(MGTAMS+1),MCG(MGZL),MCTAMS)
      ENDIF
C
C     LE NUMERO DU PREMIER TABLEAU MS LIBRE DANS RETAMS
C     =================================================
C
C     EST REMPLACE PAR CELUI DETRUIT ET CHAINE COMME SUIVANT
      LSTAMS = MCG( MGTAMS + 5 )
      MCG( MGTAMS + 5 ) = LLTAMS
      LLTAMS = NOTAMS
C
C     LES PAGES OCCUPEES PAR CE TABLEAU MS SUR SON FICHIER
C      REDEVIENNENT LIBRES
C     ====================================================
C     LA PREMIERE PAGE DU TABLEAU NOTAMS
      N1PAGE = MCG( MGTAMS + 3 )
C
C     LA PREMIERE PAGE LIBRE DU FICHIER NOFICH DE CE TABLEAU NOTAMS
      NOFICH = MCG( MGTAMS + 2 )
      IG = MGFIMS + M1FIMS * ( NOFICH - 1 )
      L1PLFI = MCG( IG + 6 )
C
C     EST REMPLACEE PAR CELLE DU TABLEAU NOTAMS
      MCG( IG + 6 ) = N1PAGE
C
C     LA DERNIERE PAGE DU TABLEAU NOTAMS EST RECHERCHEE
      NP1 = N1PAGE
C
 10   CALL FIPASU( NP1 , MCG(IG) , MGNOPS )
C
C     LA PAGE NP1 EST-ELLE LA DERNIERE DU TABLEAU ?
C     ---------------------------------------------
      IF( MCG(MGNOPS) .NE. 0 ) THEN
C
C        NON: LA PAGE SUIVANTE EST RECHERCHEE
C        ----
         NP1 = MCG(MGNOPS)
         GOTO 10
      ELSE
C
C        OUI:NP1 EST LA DERNIERE PAGE DU TABLEAU
C        ---- SA VALEUR DEVIENT CELLE DE L'ANCIENNE PREMIERE PAGE LIBRE
         MCG(MGNOPS) = L1PLFI
      ENDIF
C
C     LE TABLEAU RETAMS(NOTAMS) SAUF LE CHAINAGE DES TABLEAUX LIBRES
C     EST MIS A JOUR
      DO 50 NOFICH = 0,4
         MCG( MGTAMS + NOFICH ) = 0
 50   CONTINUE
C
C     SI LE TABLEAU MS DETRUIT ETAIT FERME SON CHAINAGE EST SUPPRIME
      IF( MSTAMS .LT. 0 ) THEN
         NOP0 = 0
         NOP1 = LFTAMS
C
 60      IF( NOP1 .NE. NOTAMS ) THEN
C           CE N EST PAS LE TABLEAU QUI PRECEDE CELUI A SUPPRIMER DES FE
            CALL TAMSRE( NOP1 , MGTAMS )
            NOP0 = NOP1
            NOP1 = MCG( MGTAMS + 5 )
            GOTO 60
C
         ELSE
C           C EST LE TABLEAU FERME QUI PRECEDE CELUI A SUPPRIMER
C           EST-IL LE PREMIER ?
            IF( NOP0 .EQ. 0 ) THEN
C              OUI.LE SUIVANT FERME DU TABLEAU NOTAMS DEVIENT LE PREMIER
               LFTAMS = LSTAMS
C
            ELSE
C              NON.LE PRECEDENT EST RELIE AU SUIVANT
               MCG( MGTAMS + 5 ) = LSTAMS
            ENDIF
         ENDIF
      ENDIF
C
      IF( NOIMPR .NE. 0 ) WRITE(IMPRIM,19000) NOTAMS
19000 FORMAT(' DESTRUCTION TABLEAU MS NO',I9)
      RETURN
      END
