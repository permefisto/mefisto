      SUBROUTINE GESTFR( L1ARET, L2ARET, LARETE, MXSOMM,
     %                   IESTFR, NOSTFR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LE TABLEAU NOSTFR POUR SAVOIR SI UN SOMMET
C -----    APPARTIENT A UNE ARETE APPARTENANT A UN SEUL TRIANGLE
C          => LE SOMMET EST DECLARE FRONTALIER
C
C ENTREES:
C --------
C L1ARET : NOMBRE DE MOTS PAR ARETE DU TABLEAU NARET
C L2ARET : NOMBRE DE ARETES DU TABLEAU NARET
C LARETE : TABLEAU DES ARETES DU MAILLAGE
C          LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C          LARETE(4,I)= NUMERO DU 1-ER TRIANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 1-ER  TRIANGLE
C          LARETE(5,I)= NUMERO DU 2-EME TRIANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 2-EME TRIANGLE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DE LA TRIANGULATION
C
C SORTIES :
C ---------
C IESTFR : EXISTENCE(>0) OU NON(=0) DE SOMMETS APPARTENANT A UNE ARETE
C          0=SURFACE FERMEE=>TOUTE ARETE APPARTIENT A 2 TRIANGLES
C NOSTFR : NUMERO DES SOMMETS FRONTALIERS
C          NOSTFR( NST ) = 1 SI LE SOMMET NST APPARTIENT A UNE ARETRE
C                               APPARTENANT A UN SEUL TRIANGLE
C                        = 0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    OCTOBRE 1993
C....................................................................012
      INTEGER  LARETE(L1ARET,L2ARET), NOSTFR(MXSOMM)

C     MISE A ZERO
      DO J=1,MXSOMM
         NOSTFR(J) = 0
      ENDDO

C     PARCOURS DES ARETES
      IESTFR = 0
      DO J=1,L2ARET
         IF( LARETE(1,J) .NE. 0 ) THEN
C           L'ARETE J EXISTE
            IF( LARETE(5,J) .EQ. 0 .AND. LARETE(4,J) .NE. 0 ) THEN
C              ARETE APPARTENANT A UN SEUL TRIANGLE
               NOSTFR( LARETE(1,J) ) = 1
               NOSTFR( LARETE(2,J) ) = 1
               IESTFR = IESTFR + 1
            ENDIF
         ENDIF
      ENDDO
ccc      PRINT *,'GESTFR: NOMBRE D''ARETES APPARTENANT A UN SEUL TRIANGLE='
ccc     %       ,IESTFR

C     NOMBRE DE SOMMETS FRONTALIERS
C     UN SOMMET EST VU 2 FOIS S'IL APPARTIENT A UN POLYGONE FERME
C     AUCUN SOMMET FRONTALIER SI LA SURFACE EST FERMEE
ccc      IESTFR = IESTFR / 2

      RETURN
      END
