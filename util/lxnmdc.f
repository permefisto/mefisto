      SUBROUTINE LXNMDC( NTLX, KNOM, KTYPE, KTYPVA, NBVARI, NRETOU )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER UN NOM KNOM DANS LE LEXIQUE NTLX
C ----- CE NOM EST DE TYPE KTYPE ET A NBVARI VARIABLES DE TYPE KTYPVA
C       SI LE NOM N'EXISTE PAS DANS LE LEXIQUE, IL EST DECLARE
C       SINON SI LE TYPE OU LES ATTRIBUTS SONT DIFFERENTS
C             ALORS 'ERREUR' ET ARRET
C             SINON RETOUR
C
C       SI LE NOMBRE DE NOMS EST SATURE IL EST AUTOMATIQUEMENT AUGMENTE
C
C ENTREES :
C ---------
C NTLX   : NUMERO DU TABLEAU MS CONTENANT LE LEXIQUE
C KNOM   : CHAINE DE CARACTERES NOM A RETROUVER DANS LE LEXIQUE
C KTYPE  : NOM DU TYPE 'LEXI' OU 'TAMS' OU 'TATA' OU 'RELA'
C KTYPVA : SI KTYPE N EST PAS 'LEXI' ALORS KTYPE VAUT
C          'LOGIQUE'     'CARACTERE'   'ENTIER/2'    'ENTIER'
C          'REEL'        'REEL2'       'REEL4'       'COMPLEXE'
C          'COMPLEXE2'   'MOTS'        '^LEXIQUE'    'XYZ'
C          'TYPEOBJET'
C          DANS LE CAS KTYPE='LEXI' KTYPVA EST LE NOMBRE DE CARACTERES
C          D'UN NOM DU LEXIQUE CONVERTI EN 4 CARACTERES (=> <10000)
C NBVARI : NOMBRE DE VARIABLES DE TYPE KTYPVA
C
C SORTIE :
C --------
C NRETOU : -1 TYPE INCORRECT
C          -2 TAMS DU LEXIQUE DE NUMERO NTLX<=0
C          -3 SI LE TAMS NTLX NE PEUT ETRE OUVERT
C           0 LE NOM A ETE AJOUTE
C          >0 SI LE NOM A ETE RETROUVE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   OCTOBRE 1984
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      include"./incl/motmcg.inc"
      include"./incl/gsmenu.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSIMTA / NOIMPR
      INTEGER           INOM(18)
      CHARACTER*(*)     KNOM
      CHARACTER*9       KTYPVB
      CHARACTER*(*)     KTYPVA
      CHARACTER*4       KTYPE,CHARX
C
C     VERIFICATION SUR NTLX
      IF( NTLX .LE. 0 ) THEN
         WRITE(IMPRIM,*) 'ERREUR LXNMDC: NTLX=',NTLX
         CALL ARRET( 100 )
         NRETOU = -2
         GOTO 9999
      ENDIF
C
C     SI LE TYPE EST INCORRECT ARRET
      IF( KTYPE .NE. 'LEXI' .AND. KTYPE .NE. 'TAMS' .AND.
     %    KTYPE .NE. 'TATA' .AND. KTYPE .NE. 'RELA'      ) THEN
          NBLGRC(NRERR) = 1
          KERR(1) = 'ERREUR LXNMDC: TYPE ' // KTYPE //
     %              ' INCONNU PARMI: TAMS TATA RELA LEXI'
          CALL LEREUR
          GOTO 9900
      ENDIF
C
      NRETOU = 0
C
C     OUVERTURE DU TABLEAU MS = LEXIQUE NTLX
 5    CALL TAMSOU( NTLX, MNLX )
C     MNLX   : ADRESSE MCN DU LEXIQUE
      IF( MNLX .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(2)(1:10), '(I10)') NTLX
         KERR(1) = 'LXNMDC: TMS NTLX=' // KERR(2)(1:10) //
     %             ' NON OUVRABLE'
         CALL LEREUR
         NRETOU = -3
         GOTO 9999
      ENDIF
C     M1LX   : NOMBRE D ENTIERS PAR NOM ET ATTRIBUTS DU LEXIQUE
      M1LX   = MCN( MNLX )
C     NBENNM : NOMBRE D'ENTIERS POUR STOCKER LES CARACTERES D'UN NOM
      NBENNM = MCN( MNLX + 2 )
C
C     CONVERSION DE KNOM EN ENTIERS DE INOM
      CALL NOMENT( NBENNM, KNOM, INOM )
C
C     DEBUT DU CHAINAGE DES NOMS OCCUPES
      NONOM1 = MCN( MNLX + 5 )
C
C     LA BOUCLE SUR LES NOMS OCCUPES
 10   IF( NONOM1 .GT. 0 ) THEN
C
C        IL EXISTE ENCORE UN NOM. EST IL KNOM ?
         MN = MNLX + M1LX * NONOM1
C        TENTATIVE D'ACCELERATION PAR NON PASSAGE FREQUENT
C        SUR LA BOUCLE
         IF( INOM(1) .NE. MCN( MN ) ) GOTO 30
         DO 20 I=2,NBENNM
            IF( INOM(I) .NE. MCN( MN + I - 1) ) GOTO 30
 20      CONTINUE
C
C        LE NOM EST RETROUVE.
C        ====================
C        SES CARACTERISTIQUES SONT-ELLES IDENTIQUES?
C
C        LE TYPE
         I = MCN( MN + 1 + NBENNM )
         IF( ICHARX( KTYPE ) .NE. I ) THEN
C           NON.DIAGNOSTIC ET ARRET
            NBLGRC(NRERR) = 1
            KERR(1) = 'LES TYPES DECLARE '//KTYPE//' ET STOCKE '
     %                //CHARX(I) //' NON EGAUX'
            CALL LEREUR
            GOTO 9900
         ENDIF
C
C        SI DECLARATION D UN LEXIQUE DEJA EXISTANT ALORS RETOUR
         IF( KTYPE .EQ. 'LEXI' ) GOTO 25
C
C        TYPES IDENTIQUES.LES TYPES DE VARIABLES SONT ILS IDENTIQUES?
         CALL TAMSTV( MCN(MN+NBENNM+2), KTYPVB, J )
         IF( KTYPVB .NE. KTYPVA ) THEN
C           NON.DIAGNOSTIC ET ARRET
            NBLGRC(NRERR) = 1
            KERR(1) = 'TYPE DECLARE '//KTYPVA//' ET STOCKE '
     %                //KTYPVB //' NON EGAUX'
            CALL LEREUR
            GOTO 9900
         ENDIF
C
C        LE NOMBRE DE VARIABLES EST IL LE MEME?
         IF( NBVARI .NE. J ) THEN
            NBLGRC(NRERR) = 2
            WRITE(KERR(MXLGER)( 1:12),'(I12)') NBVARI
            WRITE(KERR(MXLGER)(21:32),'(I12)') J
            KERR(1) = 'LXNMDC: NOMBRE DE VARIABLES DECLARE'
     %              //KERR(MXLGER)(1:12)
            KERR(2) = 'ET STOCKE'//KERR(MXLGER)(21:32)//' NON EGAUX'
            CALL LEREUR
         ENDIF
C
C        TOUS LES ATTRIBUTS SONT IDENTIQUES.RETOUR SANS RIEN FAIRE
 25      NRETOU = 1
         GOTO 9999
C
C        LE NOM NONOM1 ACTUEL N'EST PAS IDENTIQUE A KNOM
C        ===============================================
C        PASSAGE AU SUIVANT SELON LE CHAINAGE AVAL
 30      NONOM1 = MCN( MN + NBENNM )
         GOTO 10
      ENDIF
C
C     LE NOM NE FAIT PAS PARTIE DU LEXIQUE
C     DECLARATION DU NOUVEAU LEXIQUE OU DU TAMS (SI SATURATION PAS DE RETOUR)
      IF( KTYPE .NE. 'LEXI' ) THEN
C        DECLARATION DU TABLEAU MS
         CALL TNMSDC( KTYPVA, NBVARI, NTNOM )
      ELSE
C        DECLARATION ET OUVERTURE DU LEXIQUE DE LEXIQUE
C        KTYPVA EST CONVERTI EN UN ENTIER DE 4 CHIFFRES AU PLUS
         READ(KTYPVA,'(I4)') I
         CALL LXDC( I, NBVARI, NTNOM )
      ENDIF
C
C     RECHERCHE D'UN NOM LIBRE DANS LE CHAINAGE DES NOMS LIBRES
      NONOM1 = MCN( MNLX + 6 )
      IF( NONOM1 .LE. 0 ) THEN
C        AUCUN NOM LIBRE . LE NOMBRE DE NOMS EST AUGMENTE DE 16
         CALL LXAU( NTLX, 16 )
         GOTO 5
      ENDIF
C
C     CHAINAGE INITIAL DES NOMS LIBRES SUR LE SUIVANT LIBRE
      MN = MNLX + NONOM1 * MCN( MNLX )
C     LE NOUVEAU PREMIER NOM LIBRE
      MCN( MNLX + 6 ) = MCN( MN + NBENNM )
C
C     COPIE DU NOM TRANSFORME EN ENTIERS
      DO 40 I=1,NBENNM
         MCN(MN-1+I) = INOM(I)
 40   CONTINUE
C
C     LE SUIVANT DE NONOM1 DANS LE LEXIQUE EST LE PREMIER NOM OCCUPE
      MN = MN + NBENNM
      MCN( MN ) = MCN( MNLX + 5 )
C     TYPE DU TMS 'TAMS' ou 'LEXI'
      MCN( MN + 1 ) = ICHARX( KTYPE )
C     NUMERO GLOBAL DU TMS
      MCN( MN + 2 ) = NTNOM
C     LE PREMIER OCCUPE EST NONOM1
      MCN( MNLX + 5 ) = NONOM1
C
C     IMPRESSION EVENTUELLE
      IF(NOIMPR.NE.0) WRITE(IMPRIM,*) 'LXNMDC: NOM DECLARE: ',KNOM
      GOTO 9999
C
C     ARRET APRES IMPRESSION DU DIAGNOSTIC
 9900 NRETOU = -1
C
 9999 RETURN
      END
