      SUBROUTINE FICHDC( NOFIMS, REFIMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    DECLARER UN NOUVEAU FICHIER NOFIMS DE LA MEMOIRE SECONDAIRE
C -----
C ENTREES ET SORTIES :
C --------------------
C NOFIMS : NUMERO DU FICHIER DE LA MS A AJOUTER
C          =0 EN SORTIE EN CAS D ERREUR RENCONTREE
C REFIMS : REPERTOIRE DES FICHIERS DE LA MEMOIRE SECONDAIRE
C          REFIMS(1) : NUMERO LOGIQUE DU FICHIER POUR LE SYSTEME
C          REFIMS(2) : NOMBRE DE PAGES DE CE FICHIER DE LA MS
C          REFIMS(3) : NOMBRE DE MOTS PAR PAGE DE CE FICHIER
C          REFIMS(4) : ADRESSE DANS LA MEMOIRE DE GESTION MCG DE L UNIQU
C                      BUFFER DE CE FICHIER
C          REFIMS(5) : NUMERO DE LA PAGE DE SON REPERTOIRE DES PAGES
C                      (RED1FI) ACTUELLEMENT EN MEMOIRE CENTRALE
C          REFIMS(6) : NUMERO DE LA PAGE SF QUI PRECEDE LA PREMIERE
C                      PAGE DU REPERTOIRE(RED1FI) DE CE FICHIER
C          REFIMS(7) : NUMERO DE LA PREMIERE PAGE LIBRE DE RED1FI
C                      CHAINAGE DES PAGES LIBRES DE CE FICHIER N
C REZLMG : TABLEAU REPERTOIRE DES ZONES LIBRES DANS MCG
C          REZLMG(1,0)=VALEUR MAXIMALE DU 2-EME INDICE DU TABLEAU REZLMG
C                      OU NOMBRE MAXIMAL DE ZONES LIBRES PERMISES
C                      DANS LE SUPER-TABLEAU
C          REZLMG(2,0)=VALEUR DU 2-EME INDICE DE REZLMG DESIGNANT LA
C                      1-ERE ZONE LIBRE.DEPART DU CHAINAGE ZONES LIBRES
C          REZLMG(3,0)=NUMERO DE LA 1-ERE COLONNE DE REZLMG NON OCCUPEE
C                      PAR UNE ZONE LIBRE.CHAINAGE DES COLONNES NON
C                      OCCUPEES DE REZLMG
C          POUR N COMPRIS ENTRE 1 ET REZLMG(1,0)
C          REZLMG(1,N)=ADRESSE MOT         DE LA N-EME ZONE LIBRE DE MCG
C          REZLMG(2,N)=NOMBRE  DE MOTS     DE LA N-EME ZONE LIBRE DE MCG
C          REZLMG(3,N)=CHAINAGE SUCCESSEUR DE LA N-EME ZONE LIBRE DE MCG
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/msvaau.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
C
      INTEGER           REFIMS(7)
CCC   INTEGER           REZLMG(3,0:*)
      CHARACTER*48      FICHNM,NOMFIC
C
C     VERIFICATIONS DES DONNEES
C     =========================
C     LE NUMERO FICHIER SYSTEME DU FICHIER MS DECRIT DANS REFIMS
      NOFISY = REFIMS(1)
      IF( NOFISY .GE. 0 ) THEN
C        LE FICHIER EST DEJA DECLARE OU DETRUIT
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:4),'(I4)') NOFIMS
         KERR(1) = 'LE FICHIER '//KERR(MXLGER)(1:4)//
     %             ' EST DEJA DECLARE'
         CALL LEREUR
         GOTO 9900
      ELSEIF( NOFIMS .LE. 0 .OR. NOFIMS .GT. M2FIMS ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:4),'(I4)') NOFIMS
         KERR(1) = 'NUMERO INCORRECT DU FICHIER '//KERR(MXLGER)(1:4)
         CALL LEREUR
      ELSEIF( REFIMS(2) .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:4),'(I4)') REFIMS(2)
         KERR(1) = KERR(MXLGER)(1:4) //
     %           ' NOMBRE INCORRECT DE PAGES DU FICHIER'
         CALL LEREUR
         GOTO 9900
      ELSEIF( REFIMS(3) .LE. 0 ) THEN
C        LA PAGE A UN NOMBRE DE MOTS =<0
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:8),'(I8)') REFIMS(3)
         KERR(1) = KERR(MXLGER)(1:8) //
     %           ' NOMBRE INCORRECT DE MOTS D''UNE PAGE DU FICHIER'
         CALL LEREUR
         GOTO 9900
      ENDIF
C
C     LE NUMERO DE FICHIER EST RENDU POSITIF
      NOFISY    = - NOFISY
      REFIMS(1) =   NOFISY
C
C     APPEL VERSION: 4 CARACTERES PAR MOT ET NOM DE 4 CARACTERES
C     **********************************************************
      NOMFIC = FICHNM(NOFIMS)
      OPEN( UNIT=NOFISY , ERR=9900 , STATUS='NEW' ,
     %      FILE=NOMFIC , ACCESS='DIRECT' ,
     %      FORM='UNFORMATTED' , IOSTAT=IOERR ,
     %      RECL=REFIMS(3) * NBCHMO )
      GOTO 9999
C
C     TRAITEMENT DES ERREURS
 9900 NBLGRC(NRERR) = 1
      WRITE(KERR(MXLGER)(1:4),'(I4)') NOFIMS
      KERR(1) = 'LE FICHIER'//KERR(MXLGER)(1:4)//
     %          'DE LA MS NE PEUT ETRE DECLARE'
      CALL LEREUR
      CALL IMFIMS
      REFIMS(1) = - ABS( NOFISY )
C
 9999 RETURN
      END
