      INTEGER FUNCTION MODECA( NUMTYP , MCTABL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETOURNER LE DECALAGE EN MOTS POUR ALIGNER LE TABLEAU DE
C ----- NUMERO DE TYPE NUMTYP SUR UNE FRONTIERE DE MOTS OU DOUBLE OU
C       QUADRUPLE MOTS
C ENTREE :
C --------
C NUMTYP : NUMERO COMPRIS ENTRE 1 ET 9 AVEC LA CORRESPONDANCE
C          LOGIQUE  <= 1 CARACTERE<= 2 ENTIER/2 <= 3 ENTIER   <= 4
C          REEL     <= 5 REEL2    <= 6 REEL4    <= 7 COMPLEXE <= 8
C          COMPLEXE2<= 9
C MCTABL : ADRESSE EN MOTS DU 1-ER MOT DU TABLEAU DE TYPE NUMTYP
C
C SORTIE :
C --------
C MODECA : NOMBRE DE MOTS DONT LE TABLEAU DOIT ETRE DECALE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1983
C.......................................................................
C     LE TYPE MOTS EST TRAITE COMME LE PLUS LONG TYPE
C     POUR STOCKER N'IMPORTE QUEL TYPE DE VARIABLES
      MOT1VA = MOTVAR( NUMTYP )
      IF( NUMTYP .EQ. 10 ) THEN
C        TYPE COMPLEXE DOUBLE PRECISION 4 MOTS
         MOT1VA = 4
      ENDIF
C
C     LE DECALAGE PROPREMENT DIT
      IF( MOT1VA .EQ. 1 ) THEN
         MODECA = 0
      ELSE
         MODECA = ( MCTABL - 1 ) / MOT1VA
         MODECA = ( MCTABL - 1 ) - MODECA * MOT1VA
      ENDIF
      END
