      SUBROUTINE LXTKDC( NTLX , KNOM , NBCACH , NBCHAI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER UN NOM KNOM DANS LE LEXIQUE NTLX
C ----- CE NOM EST DE TYPE NBCHAI CHAINES DE NBCACH CARACTERES
C       SI LE NOM N'EXISTE PAS DANS LE LEXIQUE , IL EST DECLARE
C       SINON SI LE TYPE OU LES ATTRIBUTS SONT DIFFERENTS
C             ALORS 'ERREUR' ET ARRET
C             SINON RETOUR
C
C ENTREES :
C ---------
C NTLX   : NUMERO DU TABLEAU MS CONTENANT LE LEXIQUE
C KNOM   : CHAINE DE CARACTERES NOM A RETROUVER DANS LE LEXIQUE
C NBCACH : NOMBRE DE CARACTERES PAR CHAINE
C NBCHAI : NOMBRE DE CHAINES DE NBCACH CARACTERES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   OCTOBRE 1985
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      CHARACTER*(*) KNOM
C
C     LE NOMBRE TOTAL DE CARACTERES DES CHAINES
      NBC = NBCATA( NBCACH , NBCHAI )
C
C     DECLARATION DU TABLEAU MEMOIRE SECONDAIRE 'TAMS'
      CALL LXNMDC( NTLX , KNOM , 'TAMS' , 'CARACTERE' , NBC , NRETOU )
      END
