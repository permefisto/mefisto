      SUBROUTINE INARTRNS( PTXYZD, NSA1, NSA2, NST1, NST2, NST3,
     %                     LINTER, PTINT, CBTR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    L'ARETE NSA1-NSA2 INTERSECTE T ELLE LE TRIANGLE DE SOMMETS
C -----    NST1-NST2-NST3 (SOMMETS PTXYZD)  ET COMMENT?
C          POUR UNE INTERSECTION FRANCHE LES 3 COORDONNEES DU POINT PTINT
C          ET SES COORDONNEES BARYCENTRIQUES DANS LE TRIANGLE SONT CALCULEES

C ENTREES:
C --------
C NSA1,NSA2     : LES 2 NUMEROS DES POINTS SOMMETS DE L'ARETE
C NST1,NST2,NST3: LES 3 NUMEROS DES POINTS SOMMETS DU TRIANGLE
C                 DE VECTEUR NORMAL ST1ST2 x ST1ST3 DIRIGE VERS
C                 L'INTERIEUR DE L'ETOILE

C SORTIES:
C --------
C LINTER : -3 SI NSA1-NSA2 EST UNE ARETE DU TRIANGLE
C          -2 SI NSA1=NSA2 PAS DE CALCUL DE PTINT
C          -1 SI NSA1-NSA2 PARALLELE AU PLAN DU TRIANGLE
C           0 SI NSA1-NSA2 N'INTERSECTE PAS LE TRIANGLE ENTRE CES 3 SOMMETS
C                               ET ENTRE NSA1-NSA2
C           1 SI NSA1-NSA2   INTERSECTE     LE TRIANGLE ENTRE CES 3 SOMMETS
C                               ET ENTRE NSA1-NSA2
C             PTINT EST DANS LE TRIANGLE OU SUR UNE DE SES 3 ARETES
C           2 LE SOMMET NSA1 EST UN SOMMET DU TRIANGLE ET
C             LE SOMMET NSA2 VOIT LE TRIANGLE PAR DERRIERE (VOLUME<0)
C             ou
C             LE SOMMET NSA2 EST UN SOMMET DU TRIANGLE ET
C             LE SOMMET NSA1 VOIT LE TRIANGLE PAR DERRIERE (VOLUME<0)
C PTINT  : LES 3 COORDONNEES DU POINT D'INTERSECTION SI LINTER=1
C CBTR   : LES 3 COORDONNEES BARYCENTRIQUES DE PTINT DANS LE TRIANGLE SI LINTER=1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: : ALAIN PERRONNET Saint Pierre du Perray          Janvier 2020
C23456...............................................................012
      DOUBLE PRECISION  PTXYZD(4,*), PTINT(3), CBTR(3), VOLTET, VTE

C     NSA1 EST IL UN SOMMET DU TRIANGLE?
      NSA1DT = 0
      IF( NSA1.EQ.NST1 .OR. NSA1.EQ.NST2 .OR. NSA1.EQ.NST3 ) THEN
C        OUI
         NSA1DT = 1
      ENDIF

C     NSA2 EST IL UN SOMMET DU TRIANGLE?
      NSA2DT = 0
      IF( NSA2.EQ.NST1 .OR. NSA2.EQ.NST2 .OR. NSA2.EQ.NST3 ) THEN
C        OUI
         NSA2DT = 1
      ENDIF

      IF( NSA1DT .EQ. 1 .AND. NSA2DT .EQ. 1 ) THEN
C         NSA1-NSA2 EST UNE ARETE DU TRIANGLE
         LINTER = -3
         GOTO 9999
      ENDIF

      IF( NSA1DT .EQ. 1 .AND. NSA2DT .EQ. 0 ) THEN
C        LE VOLUME DU TETRAEDRE TRIANGLE ORIENTE-SOMMET NSA2 EST IL
C        NEGATIF?
         VTE = VOLTET( PTXYZD(1,NST1), PTXYZD(1,NST2), PTXYZD(1,NST3),
     %                 PTXYZD(1,NSA2) )
         IF( VTE .LT. 0D0 ) THEN
C           LE SOMMET NSA2 VOIT LE TRIANGLE PAR DERRIERE
C           ENGENDRANT UN TETRAEDRE DE VOLUME NEGATIF
            LINTER = 2
            GOTO 9999
         ENDIF
      ENDIF


      IF( NSA1DT .EQ. 0 .AND. NSA2DT .EQ. 1 ) THEN
C        LE VOLUME DU TETRAEDRE TRIANGLE ORIENTE-SOMMET NSA1 EST IL
C        NEGATIF?
         VTE = VOLTET( PTXYZD(1,NST1), PTXYZD(1,NST2), PTXYZD(1,NST3),
     %                 PTXYZD(1,NSA1) )
         IF( VTE .LT. 0D0 ) THEN
C           LE SOMMET NSA1 VOIT LE TRIANGLE PAR DERRIERE
C           ENGENDRANT UN TETRAEDRE DE VOLUME NEGATIF
            LINTER = 2
            GOTO 9999
         ENDIF
      ENDIF

C     RECHERCHE DU POINT D'INTERSECTION ARETE-TRIANGLE
      CALL INARTR( PTXYZD(1,NSA1), PTXYZD(1,NSA2),
     %             PTXYZD(1,NST1), PTXYZD(1,NST2), PTXYZD(1,NST3),
     %             LINTER, PTINT, CBTR )

 9999 RETURN
      END
