      SUBROUTINE TNMCMX( KTYPE , MAXVAR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETOURNER EN FONCTION DU TYPE LE NOMBRE MAXIMAL DE VARIABLES
C ----- NUMERIQUES (NON CARACTERES) DE LA PLUS GRANDE ZONE LIBRE
C       DU SUPER-TABLEAU MCN
C
C ENTREE :
C --------
C KTYPE  : NOM DU TYPE DES VARIABLES DU TABLEAU SELON
C          LOGIQUE  ENTIER/2  ENTIER  REEL REEL2 REEL4 COMPLEXE
C          COMPLEXE2
C
C SORTIE :
C --------
C MAXVAR : NOMBRE DE VARIABLES DE TYPE KTYPE CONTIGUES DANS LA PLUS
C          GRANDE ZONE LIBRE DU SUPER-TABLEAU MCN
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1983
C.......................................................................
      include"./incl/motmcg.inc"
      CHARACTER*(*)     KTYPE
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
C
C     RECHERCHE DU MAXIMUM
C     VARIABLES NON CARACTERES
      CALL TAMCMX( KTYPE , MCG(MGZLMN) , MAXVAR )
      RETURN
      END
