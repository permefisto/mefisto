      SUBROUTINE MSFI( NOFIMS , NBPAFI , MOPAFI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : AJOUTER UN FICHIER D ACCES DIRECT A LA MEMOIRE SECONDAIRE
C ----- DECLARATION ET OUVERTURE
C
C ENTREES:
C --------
C NOFIMS : NUMERO DU FICHIER DE LA MS A AJOUTER
C NBPAFI : NOMBRE DE PAGES DE CE NOUVEAU FICHIER
C MOPAFI : NOMBRE DE MOTS DE CHAQUE PAGE DE CE FICHIER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS    NOVEMBRE 1983
C.......................................................................
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/motmcg.inc"
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      COMMON / MSIMTA / NOIMPR
      include"./incl/msvaau.inc"
      CHARACTER*16      ERREUR
      DATA              ERREUR / 'ERREUR MSFI   :' /
C
C     NOFIMS EST-IL CORRECT ?
      N  = 0
      IG = 0
      IF( NOFIMS .LE. 1 .OR. NOFIMS .GT. M2FIMS ) THEN
         WRITE(IMPRIM,10000) ERREUR,NOFIMS,M2FIMS
         GOTO 9900
      ELSEIF( NBPAFI .LE. 0 .OR. MOPAFI .LE. 0 ) THEN
         WRITE(IMPRIM,10001) ERREUR,NOFIMS,NBPAFI,MOPAFI
         GOTO 9900
      ENDIF
10000 FORMAT(A16,' NO FICHIER MS=',I5,' NON COMPRIS ENTRE 1 ET',I5/)
10001 FORMAT(A16,' NO FICHIER MS=',I5,' NOMBRE DE PAGES=',I9,
     %' MOTS PAR PAGE=',I9/' REVOIR CES VALEURS'/)
C
C     DECLARATION DU FICHIER NOFIMS DE LA MEMOIRE SECONDAIRE (MS)
      IG     = MGFIMS + ( NOFIMS - 1 ) * M1FIMS
      N      = MCG(IG)
C
C     LE FICHIER EST-IL DEJA DECLARE ?
      IF( N .GT. 0 ) THEN
         WRITE(IMPRIM,10002) ERREUR,NOFIMS
10002 FORMAT(A16,'LE FICHIER MS',I3,' EST DEJA DECLARE.LE FICHIER N EST
     %PAS MODIFIE'/)
         CALL IMFIMS
         CALL ARRET(100)
      ENDIF
C
      MCG(IG+1) = NBPAFI
      MCG(IG+2) = MOPAFI
      CALL FICHDC( NOFIMS , MCG(IG) )
CCC   CALL FICHDC( NOFIMS , MCG(IG) , MCG(MGZLMG) )
C
C     DECLARATION DU BUFFER ASSOCIE A CE FICHIER DANS MCG
      MGBUFI = 0
      CALL TAMCDC( 'ENTIER ' , MOPASF , MCG(MGZLMG) , MGBUFI )
      MCG( IG + 3 ) = MGBUFI
      IF( MGBUFI .LE. 0 ) GOTO 9900
C
C     LE NUMERO DE LA PREMIERE PAGE SF DU CHAINAGE DES PAGES DU FICHIER
      MCG( IG + 5 ) = NSFLIB
C
C     LE NUMERO DE LA PREMIERE PAGE LIBRE DU REPERTOIRE RED1FI
      MCG( IG + 6 ) = 1
C
C     LE NOMBRE DE PAGES NECESSAIRES AU STOCKAGE DE CE REPERTOIRE RED1FI
      NBP = ( NBPAFI - 1 ) / MOPASF + 1
C
C     Y A T IL ASSEZ DE PAGES SF ?
      IF( NSFLIB - 1 + NBP .GT. NBPASF ) THEN
C
C     PROTECTION DE DEPASSEMENT DE LA TAILLE DU SUPER-FICHIER
         WRITE(IMPRIM,10010) ERREUR,NBPASF
         GOTO 9900
      ENDIF
10010 FORMAT(A16,'MAXIMUM PAGES DU SUPER-FICHIER=',I5,' DEPASSE'/)
C
C     INITIALISATION DES PAGES SF DU REPERTOIRE DE CE FICHIER (RED1FI)
C     TABLEAU DU CHAINAGE DES PAGES (LIBRES ET OCCUPEES) DE CE FICHIER
      L  = 1
      LL = MGBUFI - 1
      DO 20 I=1,NBP
         DO 10 J=1,MOPASF
            L = L + 1
            IF( L .LE. NBPAFI ) THEN
               MCG( LL + J ) = L
            ELSE
               MCG( LL + J ) = 0
            ENDIF
 10      CONTINUE
C
C        ECRITURE SUR LE SF DE LA PAGE I DE RED1FI
         CALL FIPAEC( NOFISF , NSFLIB , MOPASF , MCG(MGBUFI) )
         NSFLIB = NSFLIB + 1
 20   CONTINUE
C
C     LE NUMERO DE LA PAGE DANS LE BUFFER
      MCG( IG + 4 ) = NBP
C
C     MISE A JOUR DE LPFIMS 1-ER FICHIER MS NON DECLARE
      IG = MGFIMS
      DO 30 I=1,M2FIMS
         IF( MCG(IG) .GE. 0 ) THEN
C           FICHIER DECLARE OU DETRUIT
            IG = IG + M1FIMS
         ELSE
C           1-ER FICHIER NON DECLARE
            LPFIMS = I
            GOTO 9999
         ENDIF
 30   CONTINUE
C     IL N Y PLUS DE FICHIERS NON DECLARES
      LPFIMS = 0
      GOTO 9999
C
C     TRAITEMENT DES ERREURS
 9900 MCG( IG ) = N
      DO 9910 I=2,M1FIMS
         MCG( IG - 1 + I ) = 0
 9910 CONTINUE
      WRITE(IMPRIM,19900) ERREUR,NOFIMS
19900 FORMAT(A16,'LE FICHIER',I5,' DE LA MS NE PEUT ETRE DECLARE'/)
      CALL IMFIMS
      CALL ARRET(100)
C
 9999 WRITE(IMPRIM,19999) NOFIMS,NBPAFI,MOPAFI
19999 FORMAT(' ADDITION DU FICHIER MS',I3,':',I8,' PAGES DE',I8,
     %' MOTS')
      RETURN
      END
