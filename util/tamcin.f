      SUBROUTINE TAMCIN( MCG , MOTSMG , MOLIMC , M2ZLMC ,  MGZLMC )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : INITIALISER LE REPERTOIRE DES ZONES LIBRES ENTRE LES TABLEAUX
C ----- STOCKES D UN SUPER-TABLEAU
C       SI PAS ASSEZ DE PLACE DANS MCG => DIAGNOSTIC ET MGZLMC = 0
C
C ENTREE :
C --------
C MCG    : SUPER-TABLEAU DANS LEQUEL EST STOCKE LE REPERTOIRE
C MOTSMG : NOMBRE DE MOTS LIBRES DU TABLEAU MCG
C MOLIMC : NOMBRE MAXIMAL DE MOTS LIBRES DU SUPER-TABLEAU MC
C M2ZLMC : NOMBRE MAXIMAL DE ZONES LIBRES PERMISES ENTRE LES TABLEAUX
C          CONTENUS PLUS TARD DANS LE SUPER-TABLEAU MC
C MGZLMC : ADRESSE DANS MCG DU 1-ER MOT DU REPERTOIRE REZLMC
C
C SORTIE :
C --------
C MCG    : LES MOTS DE MCG AU DELA DE MGZLMC ONT ETE INITIALISES
C          EN FAIT 3 * M2ZLMC + 3 REPRESENTANT LE TABLEAU REZLMC
C          REZLMC(1,0)=VALEUR MAXIMALE DU 2-EME INDICE DU TABLEAU REZLMC
C          REZLMC(2,0)=VALEUR DU 2-EME INDICE DE REZLMC DESIGNANT LA
C                      1-ERE ZONE LIBRE.DEPART DU CHAINAGE ZONES LIBRES
C          REZLMC(3,0)=NUMERO DE LA 1-ERE COLONNE DE REZLMC NON OCCUPEE
C                      PAR UNE ZONE LIBRE.CHAINAGE DES COLONNES NON
C                      OCCUPEES DE REZLMC
C          REZLMC(1,N)=ADRESSE MOT         DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(2,N)=NOMBRE  DE MOTS     DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(3,N)=CHAINAGE SUCCESSEUR DE LA N-EME ZONE LIBRE DE MC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1983
C.......................................................................
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      INTEGER      MCG(*)
C
      IF( M2ZLMC .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:10),'(I10)') M2ZLMC
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'TAMCIN: NOMBRE DE ZONES LIBRES='//
     %              KERR(MXLGER)(1:10)//' AU LIEU DE >0'
         ELSE
            KERR(1) = 'TAMCIN: FORBIDDEN FREE ZONES NUMBER='//
     %              KERR(MXLGER)(1:10)//' <=0'
         ENDIF
         CALL LEREUR
         MGZLMC = 0
         RETURN
      ENDIF
C
C     LE REPERTOIRE EST IL STOCKABLE DANS MCG
      IF( MOTSMG .LT. MGZLMC + 3 * M2ZLMC + 2 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:10),'(I10)') MOTSMG
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'TAMCIN: AUGMENTER LE NOMBRE DE MOTS DE MCG='
     %            //KERR(MXLGER)(1:10)
         ELSE
            KERR(1) = 'TAMCIN: AUGMENT the WORD NUMBER of MCG='
     %            //KERR(MXLGER)(1:10)
         ENDIF
         CALL LEREUR
         MGZLMC = 0
         RETURN
      ENDIF
C
C     LE NOMBRE MAXIMAL DE ZONES LIBRES ENTRE LES TABLEAUX
      MCG( MGZLMC ) = M2ZLMC
C
C     LE NUMERO DE COLONNE DE LA 1-ERE ZONE LIBRE DANS LE
C     REPERTOIRE REZLMC CHAINAGE AVAL DES ZONES LIBRES
      MCG( MGZLMC + 1 ) = 1
C
C     LE NUMERO DE LA 1-ERE COLONNE NON OCCUPEE DANS REZLMC(3,0:M2ZLMC)
C     PAR UNE ZONE LIBRE CHAINAGE AVAL DES COLONNES LIBRES REZLMC
      MCG( MGZLMC + 2 ) = 2
C
C     L ADRESSE DANS LE REPERTOIRE DE LA 1-ERE ET UNIQUE ZONE LIBRE
      MCG( MGZLMC + 3 ) = 1
C
C     LA 1-ERE ET UNIQUE ZONE LIBRE ACTUELLE
      MCG( MGZLMC + 4 ) = MOLIMC
C
C     LE CHAINAGE AVAL : C EST LA DERNIERE ZONE LIBRE => 0
      MCG( MGZLMC + 5 ) = 0
C
C     LE CHAINAGE DES COLONNES DU REPERTOIRE
      M = MGZLMC + 5
      DO 10 I=2,M2ZLMC
         M = M + 3
         MCG( M ) = I + 1
 10   CONTINUE
C
C     LA DERNIERE COLONNE LIBRE DU REPERTOIRE
      MCG( M ) = 0
C
      RETURN
      END
