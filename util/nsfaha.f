      SUBROUTINE NSFAHA( NBSF, NUSTFA0, LESENS, NUSTFA1, MINNUS )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    FOURNIR LES NUMEROS DES NBSF SOMMETS D'UNE FACE
C -----    SELON L'ORDRE NECESSAIRE AU HACHAGE
C
C  LE PLUS PETIT NUMERO DE SOMMET EST MIS EN PREMIERE POSITION DE NUSTFA1
C  PUIS LE PLUS PETIT SOMMET PARMI LE SECOND ET LE NBSF-EME
C
C ENTREES:
C --------
C NBSF   : NOMBRE DE SOMMETS DE LA FACE (3 OU 4)
C NUSTFA0: NUMERO DES NBSF SOMMETS DE LA FACE AVANT REARRANGEMENT
C
C SORTIES:
C --------
C LESENS : SI SOMMET 2 < SOMMET NBSF ALORS FACE AVEC LESENS=+1
C          SI SOMMET 2 > SOMMET NBSF ALORS FACE AVEC LESENS=-1
C          ET PERMUTATION POUR QUE NUSTF1(2)<NUSTF1(NBSF)
C NUSTFA1: NUMERO DES NBSF SOMMETS DE LA FACE APRES REARRANGEMENT
C MINNUS : INDICE DANS NUSTFA0 DU PLUS PETIT NUMERO DE SOMMET
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS    DECEMBRE 1996
C2345X7..............................................................012
      INTEGER  NUSTFA0(NBSF), NUSTFA1(NBSF)
C
C     RECHERCHE DU PLUS PETIT NUMERO DE SOMMET PARMI LES NBSF SOMMETS
      MINNUS = 1
      DO 10 I=2,NBSF
         IF( NUSTFA0(I) .LT. NUSTFA0(MINNUS) ) THEN
C           MINNUS = INDICE DANBSF NUSTFA0 DU PLUS PETIT NUMERO
C                    DE SOMMET DE LA FACE
            MINNUS = I
         ENDIF
 10   CONTINUE
C
C     PERMUTATION CIRCULAIRE DES SOMMETS POUR AMENER NUSTFA0(I)
C     LE PLUS PETIT NO EN PREMIERE POSITION DE NUSTFA1
      L = MINNUS
      DO 20 I=1,NBSF
         NUSTFA1(I) = NUSTFA0(L)
         L = L + 1
         IF( L .GT. NBSF ) L = 1
 20   CONTINUE
C
C     LA NUMEROTATION DES SOMMETS EST MISE A JOUR POUR
C     QUE LA 1-ERE ARETE DE LA FACE SOIT
C     1: NO LE PLUS FAIBLE DES SOMMETS DE LA FACE
C     2: NO MIN(SOMMET2, SOMMET NBSF)
C     SI SOMMET 2 < SOMMET NBSF ALORS FACE AVEC LESENS=+1
C     SI SOMMET 2 > SOMMET NBSF ALORS FACE AVEC LESENS=-1
C
      L = NUSTFA1( 2 )
      IF( L .GT. NUSTFA1(NBSF) ) THEN
         LESENS = -1
         NUSTFA1(  2 ) = NUSTFA1( NBSF )
         NUSTFA1(NBSF) = L
      ELSE
         LESENS = 1
      ENDIF

      RETURN
      END
