      SUBROUTINE CNTSTD( NFFICA , NRETOU )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : CONVERTIR TOUS LES TABLEAUX TMS DU FICHIER DE CARACTERES
C ----- A L'AIDE DES TABLEAUX DESCRIPTEURS EN TMS NUMERIQUES
C       POUR DES TRANSFERTS ENTRE ORDINATEURS HETEROGENES
C
C ATTENTION LE FICHIER EST SUPPOSE OUVERT EN ENTREE AFIN DE CONCATENER
C           LE FICHIER N'EST PAS FERME EN SORTIE
C ENTREE :
C --------
C NFFICA : NUMERO D'UNITE LOGIQUE DU FICHIER DES CARACTERES
C NRETOU : 0 SI PAS D'ERREUR , >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS  OCTOBRE 1988
C23456---------------------------------------------------------------012
      PARAMETER (NF=91)
C.......................................................................
      include"./incl/td.inc"
      include"./incl/msvaau.inc"
      include"./incl/gsmenu.inc"
C.......................................................................
      CHARACTER*80      KNOMTS
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
C
C     TETE DE LECTURE MISE AU DEBUT DU FICHIER
      REWIND( UNIT=NFFICA , IOSTAT = NRETOU )
      IF( NRETOU .NE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'CNTSTD: REMBOBINAGE FICHIER IMPOSSIBLE'
         CALL LEREUR
         RETURN
      ENDIF
C
C     LE POINTEUR SUR LE DERNIER CARACTERE STOCKE DANS KFICA
C     LA LIGNE BUFFER DU FICHIER CARACTERE
C     LCFICA > NCFICA FORCE A LIRE LE 1-ER ENREGISTREMENT DU FICHIER
      LCFICA = NCFICA + 1
C
C     LE TABLEAU TMS EST PRECEDE DE SON NOM
 10   CALL FICACK( NFFICA , KNOMTS , NRETOU )
      IF( NRETOU .EQ. 1001 ) THEN
C        LE FICHIER A ETE TOTALEMENT EXPLOITE
         NBLGRC(NRERR) = 1
         KERR(1) = 'CNTSTD: FIN CONVERSION FICHIER CARACTERES'
      CALL LEREUR
         NRETOU = 0
         RETURN
      ELSE IF( NRETOU .NE. 0 ) THEN
C        UNE ERREUR A ETE RENCONTREE AUTRE QUE FIN DE FICHIER
         RETURN
      ENDIF
C
C     CONVERSION DE CE TABLEAU LU EN CARACTERES EN TMS NUMERIQUE
      LIRECR = 1
      CALL NCTSTD( LIRECR , KNOMTS , NFFICA , NRETOU )
      IF( NRETOU .NE. 0 ) RETURN
C
C     PASSAGE AU TABLEAU TMS SUIVANT
      GOTO 10
      END
