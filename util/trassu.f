      SUBROUTINE TRASSU( KNOM, NUSURF )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER EN GRIS    TOUTES LES ARETES APPARTENANT A  2 FACES
C -----           EN ROUGE   TOUTES LES ARETES APPARTENANT A >2 FACES
C                 EN MAGENTA TOUTES LES ARETES APPARTENANT A  1 FACE
C          D'UNE SURFACE 2D ou 3D
C ENTREE :
C --------
C KNOM   : LE NOM DE LA SURFACE A TRAITER
C
C SORTIE :
C --------
C NUSURF : NUMERO DE LA SURFACE DANS LE LEXIQUE DES SURFACES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Labo ANALYSE NUMERIQUE UPMC PARIS   MARS 1997
C AJOUTS : ALAIN PERRONNET LJLL UPMC & St PIERRE DU PERRAY NOVEMBRE 2015
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      CHARACTER*(*)     KNOM
      INTEGER           NBARXF(3)

      MNARFA = 0
      L1ARFA = 0
      L2ARFA = 0
      MNAR1F = 0
      MNSA1F = 0
      MNNVST = 0
      MNDVOI = 0

C     LE LEXIQUE DE LA SURFACE
      CALL NUOBNM( 'SURFACE', KNOM, NUSURF )
      CALL LXNLOU( NTSURF, NUSURF, NTLXOB, MNLXOB )
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  'SURFACE INCONNUE:' // KNOM
         CALL LEREUR
         RETURN
      ENDIF

C     LE TABLEAU 'NSEF' DE LA SURFACE
      CALL LXTSOU( NTLXOB, 'NSEF', NTTSMA, MNTSMA )
      IF( NTTSMA .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'SURFACE NON MAILLEE ' // KNOM
         CALL LEREUR
         RETURN
      ENDIF

C     LE TABLEAU 'XYZSOMMET' DE LA SURFACE
      CALL LXTSOU( NTLXOB, 'XYZSOMMET', NTSOMM, MNSOMM )
      IF( NTSOMM .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'SURFACE SANS XYZSOMMET '// KNOM
         CALL LEREUR
         RETURN
      ENDIF
      NBSOM = MCN( MNSOMM + WNBSOM )
      CALL DIMCOO( NBSOM, MCN(MNSOMM+WYZSOM), NDIMLI )
      CALL MAJEXT( MNSOMM )
      MNXYZS = MNSOMM + WYZSOM

C     LE TYPE DE FERMETURE DU MAILLAGE DE LA SURFACE
      NUTFMA = MCN( MNTSMA + WUTFMA )
      IF( NUTFMA .EQ. 1 ) THEN
C        LA SURFACE A DEJA ETE TESTEE COMME ETANT FERME
C        => PAS D'ARETES SIMPLES
         NBLGRC(NRERR) = 1
         KERR(1) = 'PAS D''ARETE SIMPLE. SURFACE MAILLEE FERMEE'
         CALL LERESU
         IERR = 0
         GOTO 9900
      ENDIF
C
C     LE TYPE DU MAILLAGE
      NUTYMA = MCN( MNTSMA + WUTYMA )
      IF( NUTYMA .NE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'PAS DE TRACE DES ARETES SIMPLES'
            KERR(2) = 'SI LE MAILLAGE EST STRUCTURE (PAS d''ERREUR)'
         ELSE
            KERR(1) = 'NO DRAWING OF SIMPLE EDGES'
            KERR(2) = 'WITH A STRUCTURED MESH BECAUSE NO ERROR'
         ENDIF
         CALL LEREUR
         RETURN
      ENDIF

C     NOMBRE DE SOMMETS ET TANGENTES PAR EF
      NBSOEF = MCN( MNTSMA + WBSOEF )

C     NOMBRE DE ARFA
      NBEFOB = MCN( MNTSMA + WBEFOB )

C     LE TABLEAU DES ARETES DES FACES DE LA SURFACE
      MXFAAR = 4
      CALL GEARFA( MCN(MNSOMM+WYZSOM), NBSOEF, NBEFOB,
     %             MCN(MNTSMA+WUSOEF), MXFAAR,
     %             L1ARFA, L2ARFA, MNARFA, NBARXF, IERR )
      IF( IERR .NE. 0 ) GOTO 9900
      IF( NBARXF(1) .EQ. 0 .AND. NBARXF(3) .EQ. 0 ) GOTO 1000

C     A PARTIR DU TABLEAU ARFA DES ARETES DES FACES D'UNE SURFACE
C     CONSTRUIRE LA TABLEAU AR1F DU NUMERO NARFA DES ARETES SIMPLES CAD
C     DES ARETES APPARTENANT A UNE SEULE FACE (QUADRANGLE ou TRIANGLE)
      CALL LAR1F( NBSOM, RMCN(MNXYZS), L1ARFA, L2ARFA, MCN(MNARFA),
     %            MXAR1F, NBARXF(1), MNAR1F, IERR )
      IF( IERR .NE. 0 ) GOTO 1000

C     A PARTIR DU TABLEAU AR1F
C     CONSTRUIRE LE TABLEAU SA1F DES SOMMETS DES ARETES SIMPLES
      MXSA1F = NBSOM
      CALL TNMCDC( 'ENTIER', MXSA1F, MNSA1F )
      IF( MNSA1F .LE. 0 ) THEN
         CALL TNMCDS( 'ENTIER', MXAR1F, MNAR1F )
         IERR = 1
         GOTO 1000
      ENDIF
      CALL LAR1FST( L1ARFA, L2ARFA, MCN(MNARFA), NBARXF(1), MCN(MNAR1F),
     %              MXSA1F, NBSA1F, MCN(MNSA1F) )

C     BILAN DES TYPES D'ARETES DU MAILLAGE
      WRITE(IMPRIM,*) 'SURFACE ',KNOM
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) NBSA1F,' SOMMETS des',NBARXF(1),
     %                 ' ARETES dans 1 FACE'
      ELSE
         WRITE(IMPRIM,*) NBSA1F,' VERTICES of',NBARXF(1),
     %                 ' EDGES in 1 FACE'
      ENDIF

C     A PARTIR DU TABLEAU DES SOMMETS SA1F DES ARETES SIMPLES D'UNE SURFACE
C     CONSTRUIRE LE TABLEAU DES SOMMETS VOISINS DES ARETES SIMPLES
C     LES PLUS PROCHES ET LEUR DISTANCE
C     POUR CHAQUE SOMMET DU SOMMET DE NUSA1F NO DE SON PLUS PROCHE VOISIN
      MXNVST = NBSA1F
      CALL TNMCDC( 'ENTIER', MXNVST, MNNVST )
      IF( MNNVST .LE. 0 ) THEN
         IERR = 1
         GOTO 1000
      ENDIF

C     DISTANCE ENTRE LE SOMMET SIMPLE ET SON VOISIN SA1F LE PLUS PROCHE
      MXDVOI = NBSA1F
      CALL TNMCDC( 'REEL', MXDVOI, MNDVOI )
      IF( MNDVOI .LE. 0 ) THEN
         IERR = 2
         GOTO 1000
      ENDIF
      CALL LAR1FVD( RMCN(MNXYZS), NBSA1F, MCN(MNSA1F),
     %              MCN(MNNVST),  MCN(MNDVOI) )

C     SI LE TABLEAU TRACE DE CET OBJET EXISTE ALORS LES OPTIONS DE TRACE
C     SONT INITIALISEES AVEC SES VALEURS
 1000 CALL COUOBJ( 3, KNOM, MNTRAC )
C
C     LA VISEE DE L'OBJET SELON LES COOEXT DU COMMON / XYZEXT /
      CALL VISEE0

C     TRACER EN MAGENTA TOUTES LES ARETES APPARTENANT A  1 FACE
C            EN GRIS    TOUTES LES ARETES APPARTENANT A  2 FACES
C            EN ROUGE   TOUTES LES ARETES APPARTENANT A >2 FACES
      CALL TRARFASU( NDIMLI, NBSOM, RMCN(MNXYZS), MCN(MNTSMA+WUSOEF),
     %               L1ARFA, L2ARFA, MCN(MNARFA), NBARXF )

C     DESTRUCTION DES TABLEAUX DEVENUS INUTILES
 9900 IF( MNDVOI .GT. 0 ) CALL TNMCDS( 'REEL',   MXDVOI, MNDVOI )
      IF( MNNVST .GT. 0 ) CALL TNMCDS( 'ENTIER', MXNVST, MNNVST )
      IF( MNSA1F .GT. 0 ) CALL TNMCDS( 'ENTIER', NBSOM,  MNSA1F )
      IF( MNAR1F .GT. 0 ) CALL TNMCDS( 'ENTIER', MXAR1F, MNAR1F )
      IF( MNARFA .GT. 0 ) CALL TNMCDS('ENTIER',  L1ARFA*L2ARFA, MNARFA )

      RETURN
      END
