      GOTO (110, 120, 130, 140, 150, 160, 170, 180, 190, 200,
     &      210, 220, 110, 240, 120, 130, 270, 140, 290, 300,
     &      310, 320, 330, 340, 160, 160, 105, 380, 110, 600,
     &      410, 420, 430, 440 )   , NO
C     ******************************************************************
C
  105 NBLGRC(NRERR) = 2
      KERR(1) = 'ELPONO: TYPE D''EF NON PROGRAMME'
      WRITE( KERR(3)(1:10), '(I10)' ) NO
