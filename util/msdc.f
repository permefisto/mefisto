      SUBROUTINE MSDC( MXFIMS , MXTAMS , NBFIMS , LDFIMS , NMPRMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER LA MEMOIRE SECONDAIRE
C ----- DECLARER ET INITIALISER LE SUPER-FICHIER(10) DE GESTION DE LA MS
C       DECLARER LES FICHIERS DECRITS PAR LE TABLEAU LDFIMS
C
C ENTREES :
C ---------
C MXFIMS : NOMBRE MAXIMAL DE FICHIERS DE LA MS
C MXTAMS : NOMBRE MAXIMAL DE TABLEAUX DECLARABLES DANS LA MS
C NBFIMS : NOMBRE DE FICHIERS INITIAUX DE LA MS ( >1)
C          LE SUPER-FICHIER EST OBLIGATOIREMENT LE PREMIER D ENTRE EUX
C          LE NUMERO SYSTEME ATTRIBUE EST 10 POUR LE SUPER-FICHIER(S.F.)
C           9 + I POUR LE I-1 EME FICHIER SUPPORT DES TABLEAUX
C           NUMERIQUES ET CARACTERES DE LA MS
C LDFIMS : LES DESCRIPTEURS DES FICHIERS DE LA MS
C          LDFIMS(1,N)=NOMBRE DE PAGES DU N-EME FICHIER
C          LDFIMS(2,N)=NOMBRE DE MOTS DE CHAQUE PAGE DU N-EME FICHIER
C          N VARIE DE 1 (SF) A NBFIMS (=<MXFIMS)
C          ATTENTION : LDFIMS(2,1) DOIT ETRE SUPERIEUR A 29.
C          ---------
C NMPRMS : NOM DU PROJET NECESSITANT CETTE MS ( 72 CARACTERES )
C
C SORTIE :
C --------
C          LE SUPER-FICHIER=1-ER FICHIER DECLARE EST INITIALISE
C          CF LA BROCHURE DESCRIPTION DU SUPER-FICHIER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/ppmck.inc"
      include"./incl/langue.inc"
      include"./incl/motmcg.inc"
      include"./incl/msvaau.inc"
      include"./incl/nmproj.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      COMMON / MSIMTA / NOIMPR
      CHARACTER*(*)     NMPRMS
      INTEGER           PAGE1(29)
      EQUIVALENCE       (PAGE1(1),NOFISF)
      CHARACTER*48      FICHNM,NOMFIC
      INTEGER           LDFIMS(2,NBFIMS)
      CHARACTER*16      ERREUR
      DATA              ERREUR / ' ERREUR MSDC   :' /
C
C     INITIALISATION DES VARIABLES DEPENDANT MACHINE
C     ==============================================
      CALL MSINIT
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) NBFIMS-1
      ELSE
         WRITE(IMPRIM,20000) NBFIMS-1
      ENDIF
10000 FORMAT(/'DECLARATION DE LA MEMOIRE SECONDAIRE AVEC 1 SUPER-FICHIER
     % ET',I4,' FICHIERS NUMERIQUES'/90('='))
20000 FORMAT(/'SECONDARY MEMORY DECLARATION WITH 1 SUPER-FILE AND',
     % I4,' NUMERICAL FILES'/80('='))
C
C     TRANSFERT DES VARIABLES D'ENTREE DANS LE COMMON / MSSFTA /
C     ==========================================================
      M1FIMS = 7
      M1TAMS = 6
      M2FIMS = MXFIMS
C
C     IMPRESSION DES ADRESSAGES : AUCUNE
      NOIMPR = 0
C
C     ATTENTION : MOTSMG ET MOTSMK DOIVENT CORRESPONDRE A LA DECLARATION
C     =========== DU NOMBRE DE MOTS DES TABLEAUX MCG ET MCK DES
C                 COMMON / MSMCG / ET / MSMCK /
C
      MOTSMG = MOTMCG
C
C     INITIALISATION DU REPERTOIRE DES ZONES LIBRES DANS MCG
C     =====================================================
C
C     LE NOMBRE MAXIMAL DE ZONES LIBRES PERMISES ENTRE LES TABLEAUX
C     STOCKES DANS MCG
      M2ZLMG = 10 + ABS( NBFIMS )
C
C     LE NOMBRE DE MOTS DU TABLEAU REZLMG
      MOZLMG = 3 * M2ZLMG + 3
C
C     LE NOMBRE DE MOTS RESTANTS DANS MCG
      MOLIMG = MOTSMG - MOZLMG
C
C     LE TABLEAU REZLMG EST DECLARE A LA FIN DE MCG
      MGZLMG = MOLIMG + 1
C
C     L INITIALISATION
      CALL TAMCIN( MCG,MOTSMG,MOLIMG,M2ZLMG,MGZLMG )
C     MGZLMG EST L ADRESSE DANS MCG DU 1-ER MOT DU TABLEAU REZLMG
C
C     DECLARATION ET INITIALISATION DU TABLEAU REFIMS(M1FIMS,M2FIMS)
C     ==============================================================
C
C     SON NOMBRE DE VARIABLES
      MOFIMS = MOTTAB( NOTYPI , M1FIMS * M2FIMS )
C
C     SA DECLARATION DANS MCG
      MGFIMS = 0
      CALL TAMCDC( 'ENTIER ' , M1FIMS * M2FIMS , MCG(MGZLMG) , MGFIMS )
C     MGFIMS EST L ADRESSE DANS MCG DU PREMIER MOT DE REFIMS
C
C     INITIALISATION DES VARIABLES DU SUPER-FICHIER (SF)
C     LE NUMERO SYSTEME DU SF
      NOFISF = 10
C     LE NOMBRE DE PAGES DU SF
      NBPASF = LDFIMS(1,1)
C     LE NOMBRE DE MOTS PAR PAGE DU SF
      MOPASF = LDFIMS(2,1)
C     VERIFICATIONS
      IF( NBPASF .LE. 0 .OR. MOPASF .LE. 0 ) THEN
         IF( LANGAG .EQ. 0 ) WRITE(IMPRIM,10010) ERREUR,1,NBPASF,MOPASF
         GOTO 9990
      ENDIF
10010 FORMAT(A16,' FICHIER',I5,' DE',I9,' PAGES DE',I9,' MOTS'/)
C     I INITIALISATION DU REPERTOIRE REFIMS
      IG     = MGFIMS
      DO 20 I=2,M2FIMS+1
C
C        LE NUMERO SYSTEME DU FICHIER I ( <0 CAR NON ENCORE DECLARE )
         MCG( IG     ) = - ( 9 + I )
C
C        LE NOMBRE DE SES PAGES ET LE NOMBRE DE MOTS PAR PAGE
         IF( I .LE. NBFIMS ) THEN
C
C           INITIALISATION SELON L UTILISATEUR
            IF( LDFIMS(1,I) .LE. 0 .OR. LDFIMS(2,I) .LE. 0 ) THEN
C              ERREUR
               WRITE(IMPRIM,10010)ERREUR,I,LDFIMS(1,I),LDFIMS(2,I)
               GOTO 9990
            ELSE
               MCG( IG + 1 ) = LDFIMS(1,I)
               MCG( IG + 2 ) = LDFIMS(2,I)
            ENDIF
         ELSE
C           INITIALISATION PAR DEFAUT
            MCG( IG + 1 ) = 0
            MCG( IG + 2 ) = 0
         ENDIF
C
C        LE RESTE EST MOMENTANEMENT ANNULE
         DO 10 J=3,M1FIMS-1
            MCG( IG + J ) = 0
 10      CONTINUE
C
C        PASSAGE AU FICHIER SUIVANT
         IG = IG + M1FIMS
 20   CONTINUE
C
C     INITIALISATION DU NOM DU PROJET
C     ===============================
      NMPROJ = NMPRMS
C
C     DECLARATION ET OUVERTURE DU SUPER-FICHIER SF
C     ============================================
C
C     LA DECLARATION ET L'OUVERTURE DU SF
C
C     APPEL VERSION 4 CARACTERES PAR MOT ET NOM DE 4 CARACTERES
      NOMFIC  = FICHNM(0)
      MOTRECL = MOPASF * NBCHMO
      L       = NUDCNB( NOMFIC )
      print *,'MSDC: Nom du Super Fichier=', NOMFIC(1:L)
      print *,'MOTRECL=',MOTRECL
      OPEN( UNIT=NOFISF, ERR=9900, STATUS='NEW',
     %      FILE=NOMFIC(1:L), ACCESS='DIRECT',
     %      FORM='UNFORMATTED', IOSTAT=IOERR,
     %      RECL=MOTRECL )
C
C     L'OUVERTURE DU BUFFER => L'ADRESSE MCG MGBUSF
      MGBUSF = 0
      CALL TAMCDC( 'ENTIER' , MOPASF , MCG(MGZLMG) , MGBUSF )
C
C     LE TRAITEMENT DES TABLEAUX DU SUPER-FICHIER
C     ===========================================
C
C     LE NOMBRE DE COLONNES DU REPERTOIRE RETAMS PAR PAGE DU SF
      NBCTMS = MOPASF / M1TAMS
C
C     NPTAMS:LE NOMBRE DE PAGES DU SF NECESSAIRE POUR STOCKER RETAMS
      NPTAMS = ( MXTAMS - 1 ) / NBCTMS + 1
C     AJUSTEMENT AU MAXIMUM DU NOMBRE DE TABLEAUX MS
      M2TAMS = NPTAMS * NBCTMS
C
C     LE NOMBRE DE PAGES SF NECESSAIRE AU STOCKAGE DU REPERTOIRE REFIMS
      CALL NBPATA( MOFIMS,MOPASF,NPFIMS,I )
C
C     LE NOMBRE DE MOTS NECESSAIRE AU STOCKAGE DU TABLEAU NMPROJ
      MONMPR = MOTTAB( NOTYPK , 72 ) + MOTTAB( 4 , 1 )
C
C     LE NOMBRE DE MOTS OCCUPES PAR LES PREMIERES VARIABLES DU SF
      I = MAX( 29 , MONMPR )
C
C     LE NOMBRE DE MOTS PAR PAGE DU SF EST-IL SUFFISANT ?
      IF( I .GT. MOPASF ) THEN
C        ERREUR:PAGE TROP COURTE
         WRITE(IMPRIM,10030) ERREUR,MOPASF,I
         GOTO 9990
      ENDIF
10030 FORMAT(A16,'LE NOMBRE DE MOTS D UNE PAGE DU 1-ER FICHIER=',I6,
     %' DOIT ETRE SUPERIEUR A',I6/' REVOYEZ LA VARIABLE LDFIMS(2,1)'/)
C
C     ECRITURE DU NOM DU PROJET PAGE 2 DU SUPER-FICHIER
C     =================================================
      NPA    = 2
      NUEXEC = 0
      WRITE(UNIT=NOFISF,REC=NPA) NUEXEC,NMPROJ
C
C     NOMBRE DE PAGES SF POUR STOCKER LE NUMERO DE LA PAGE SF
C     DES PAGES DU REPERTOIRE RETAMS : TABLEAU NPSFTA
      CALL NBPATA( NPTAMS,MOPASF,NPSNPS,I )
C
C     NSFIMS:NUMERO DE LA 1-RE PAGE SF DU TABLEAU REFIMS
      NSFIMS = 3
C
C     L'INITIALISATION DES VARIABLES 6 ET 7 DU TABLEAU REFIMS
      IG     = MGFIMS
      NPA    = 3 + NPFIMS
      DO 40 I=2,M2FIMS+1
C
C        LE NOMBRE DE PAGES SF NECESSAIRES POUR STOCKER LE TABLEAU RED1F
         IF( I .LE. NBFIMS ) THEN
C           LE FICHIER EST DECLARE DANS CE SP
            NPSFFI = ( MCG(IG+1) - 1 ) / MOPASF + 1
C
C           REFIMS(6,I):LE NUMERO DE LA 1-RE PAGE SF DU REPERTOIRE
C                       RED1FI DE CE FICHIER I (CHAINAGE DES PAGES)
            MCG( IG + 5 ) = NPA
            NPA = NPA + NPSFFI
         ELSE
C           LE FICHIER NE SERA PAS DECLARE DANS CE SP
            MCG( IG + 5 ) = 0
         ENDIF
C
C        LE NUMERO DE LA 1-RE PAGE LIBRE DE RED1FI
C        CHAINAGE DES PAGES LIBRES
         MCG( IG + 6 ) = 1
         IG = IG + M1FIMS
 40   CONTINUE
C
C     NSFNPS:NUMERO DE LA 1-RE PAGE SF DU TABLEAU NPSFTA
      NSFNPS = NPA
C
C     DECLARATION DES NBFIMS FICHIERS.INITIALISATION DE RED1FI
C     REPERTOIRE DES PAGES LIBRES ET OCCUPEES DE CHAQUE FICHIER 2 A NBFI
C     ==================================================================
C
C
C     LE BUFFER DU SF SERT A L INITIALISATION DU CHAINAGE DES PAGES
C     DE RED1FI
      IG = MGFIMS
      DO 80 I=2,NBFIMS
C
C        LE NOMBRE DE PAGES DU FICHIER I
         NPAFI  = MCG( IG + 1 )
C
C        LE NOMBRE DE PAGES SF POUR STOCKER RED1FI
         NPSFFI = ( NPAFI - 1 ) / MOPASF + 1
C
C        LE NUMERO DE LA 1-ERE PAGE SF DU REPERTOIRE RED1FI
         NPA    = MCG( IG + 5 ) - 1
         N      = 1
         DO 70 J=1,NPSFFI
            IGB = MGBUSF - 1
            DO 60 K=1,MOPASF
               N = N + 1
C              EXISTE-T-IL UNE PAGE SUIVANTE ?
               IF( N .LE. NPAFI ) THEN
C              OUI:LA SUIVANTE EST N
                  MCG( IGB + K ) = N
               ELSE
C                 NON:PAS DE PAGE SUIVANTE
                  MCG( IGB + K ) = 0
               ENDIF
 60         CONTINUE
C
C           ECRITURE SUR LE SF DE CETTE PAGE J
            CALL FIPAEC( NOFISF , NPA + J , MOPASF , MCG(MGBUSF) )
 70      CONTINUE
C
C        LA DECLARATION DU FICHIER MS NUMERO I
C        --------------------------------------
         CALL FICHDC( I-1 , MCG(IG) )
CCC      CALL FICHDC( I-1 , MCG(IG) , MCG(MGZLMG) )
         IG = IG + M1FIMS
 80   CONTINUE
C
C     ECRITURE DU TABLEAU REFIMS
C     ==========================
      NPA = 3
      CALL TAMCEC( NOFISF , NPA , MOPASF , MOFIMS , MCG(MGFIMS) )
      CALL IMFIMS
C
C     LA DECLARATION DU TABLEAU NPSFTA ET SON INITIALISATION
C     ======================================================
      MGNPSF = 0
      CALL TAMCDC( 'ENTIER ' , NPTAMS , MCG(MGZLMG) , MGNPSF )
      IG  = MGNPSF - 1
      NPA = NSFNPS + NPSNPS - 1
      DO 50 I=1,NPTAMS
         MCG( IG + I ) = NPA + I
 50   CONTINUE
C
C     NSFLIB:NUMERO DE LA 1-RE PAGE LIBRE DU SF DERRIERE CELLES OCCUPEES
      NSFLIB = NSFNPS + NPSNPS + NPTAMS
C
C     LE NOMBRE DE PAGES SF (NBPASF+10 POUR PREVOIR L AVENIR)
C     EST-IL SUPERIEUR A NPA NOMBRE DE PAGES OCCUPEES ?
      IF( NBPASF .LE. NSFLIB + 10 ) THEN
C
C        ERREUR:LE NOMBRE DE PAGES SF EST INSUFFISANT
         WRITE(IMPRIM,10050) ERREUR,NSFLIB+10
         GOTO 9990
      ENDIF
10050 FORMAT(A16,'LE NOMBRE DE PAGES DU 1-ER FICHIER DOIT ETRE SUPERIEUR
     % OU EGAL A',I6/)
C
C     ECRITURE SUR LE SF DU TABLEAU NPSFTA
C     ====================================
C
C     LE NUMERO DE LA 1-RE PAGE SF DE NPSFTA
      NPA    = NSFNPS
      CALL TAMCEC( NOFISF , NPA , MOPASF , NPTAMS , MCG(MGNPSF) )
C
C     DECLARATION DU REPERTOIRE PAGINE RETAMS
C     INITIALISATION ET SAUVEGARDE SUR LE SF
C     =======================================
C
C     LE NUMERO - 1 DU TABLEAU LIBRE SUIVANT
      N = 1
      DO 120 I=1,NPTAMS
C
C        LA BOUCLE SUR LES PAGES DE RETAMS
C        L'ADRESSE DE SON BUFFER
         IG = MGBUSF - 1
         DO 110 J=1,NBCTMS
C           LA BOUCLE SUR LES COLONNES DANS LA PAGE SF
            DO 100 K=1,5
               MCG( IG + K ) = 0
 100        CONTINUE
C           LE NUMERO DU TABLEAU LIBRE SUIVANT
            N = N + 1
C           EXISTE-T-IL UN TABLEAU LIBRE SUIVANT ?
            IF( N .LE. M2TAMS ) THEN
C              OUI:LA COLONNE SUIVANTE EST N
               MCG( IG + 6 ) = N
            ELSE
C              NON:LA COLONNE SUIVANTE EST 0
               MCG( IG + 6 ) = 0
            ENDIF
C
C           PASSAGE A LA COLONNE SUIVANTE
            IG = IG + M1TAMS
 110     CONTINUE
C
C        SAUVEGARDE DU BUFFER DANS LA PAGE NPA DU SF
         NPA = MCG( MGNPSF - 1 + I )
         CALL FIPAEC( NOFISF , NPA , MOPASF , MCG(MGBUSF) )
 120  CONTINUE
C
C     DESTRUCTION DE TOUS LES TABLEAUX MCG
C     ===================================
C     LE TABLEAU NPSFTA
      CALL TAMCDS( 'ENTIER ' , NPTAMS , MCG(MGZLMG) , MGNPSF )
C     LE TABLEAU BUFFER DU SF
      CALL TAMCDS( 'ENTIER ' , MOPASF , MCG(MGZLMG) , MGBUSF )
C     LE TABLEAU REFIMS
      CALL TAMCDS( 'ENTIER ' , M1FIMS * M2FIMS , MCG(MGZLMG) , MGFIMS )
C
C     INITIALISATION DES VARIABLES MANQUANTES DE LA 1-RE PAGE
C     =======================================================
      LPFIMS = NBFIMS
      MGBUTA = 0
      NBBUTA = 0
      NATAMS = 0
      LLTAMS = 1
      LFTAMS = 0
      MGZLMN = 0
      MGZLMG = 0
      MGZLMK = 0
C
C     ECRITURE DE LA 1-RE PAGE = COMMON / MSSFTA /
C     ============================================
      CALL FIPAEC( NOFISF , 1 , 29 , PAGE1 )
C
C     FERMETURE DU SF
C     ===============
      CLOSE( UNIT=NOFISF , STATUS='KEEP' , ERR=9920 , IOSTAT=IOERR )
      GOTO 9999
C
C     ERREUR A LA DECLARATION-OUVERTURE DU SF
C     =======================================
 9900 WRITE(IMPRIM,19900) ERREUR, NOMFIC, IOERR
19900 FORMAT(A16,'LE SUPER-FICHIER 10 ',A,' NE PEUT ETRE OUVERT'/
     %' VERIFIEZ LE NOM DU FICHIER OU LDFIMS(1,1) ET LDFIMS(2,1)'/
     %' CODE ERREUR A L''OUVERTURE DU FICHIER=',I12/)
      GOTO 9990
C
 9920 WRITE(IMPRIM,19920) ERREUR,IOERR
19920 FORMAT(A16,'LE SUPER-FICHIER 10 NE PEUT ETRE FERME.CODE ERREUR
     %SYSTEME=',I12/)
C
C     ERREUR DETECTEE.ARRET SANS FERMETURE DE LA MS
 9990 CALL ARRET(-1)
C
 9999 RETURN
      END
