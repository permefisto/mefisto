ccc         DO 80 L=1,NDIM
ccc            PRINT 10001, (I,L,J,PDP(I,L,J),I=1,NBPOL)
ccc 80      CONTINUE
ccc 90   CONTINUE
ccc10001 FORMAT(2(3I3,1X,D25.17,4X))
C
      RETURN
      END
