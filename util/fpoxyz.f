      SUBROUTINE FPOXYZ ( NDIM, NPO, NOINTF, COPOE, XREF, PREF, C )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES COORDONNEES DU POINT C DE L ELEMENT COURANT
C -----    A PARTIR DU POINT XREF DE L ELEMENT DE REFERENCE
C
C PARAMETRES D ENTREE :
C ---------------------
C NDIM   : DIMENSION DE L ESPACE DE TRAVAIL (2 OU 3)
C NPO    : NOMBRE DE POINTS DE L ELEMENT FINI
C NOINTF : NO DE L INTERPOLATION COMMUNE AUX NDIM COMPOSANTES DE L
C          APPLICATION : ELEMENT FINI REFERENCE -> ELEMENT FINI COURANT
C COPOE  : COORDONNEES DES POINTS DE L ELEMENT COURANT
C          COPOE(NPO,NDIM) REEL DOUBLE PRECISION
C XREF   : COORDONNEES DU POINT DE L ELEMENT FINI DE REFERENCE
C
C PARAMETRES RESULTATS:
C ----------------------
C PREF   : VALEUR DES FONCTIONS DE BASE EN CE POINT DE L ELEMENT DE
C          REFERENCE  POUR L INTERPOLATION NOINTF
C C      : COORDONNEES DU POINT SUR L ELEMENT COURANT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS   NOVEMBRE 1990
C23456---------------------------------------------------------------012
      DOUBLE PRECISION COPOE(NPO,NDIM),XREF(3),PREF(30),PROSCD
      REAL             C(NDIM)
      INTRINSIC        REAL
C
C     VALEUR DES FONCTIONS DE BASE AU POINT XREF DE L ELEMENT REFERENCE
C     ==================================================================
      CALL INTERP ( NOINTF , XREF(1) , XREF(2) , XREF(3) , NPO , PREF )
C
C     CALCUL DES COORDONNEES DU POINT SUR L ELEMENT COURANT
C     =====================================================
      DO I=1,NDIM
         C( I ) = REAL( PROSCD( PREF , COPOE(1,I) , NPO ) )
      ENDDO
C
      RETURN
      END
