      SUBROUTINE LXDC( NBCANM, MXNOMS, NTLX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     DECLARER UN LEXIQUE
C -----
C
C ENTREES:
C --------
C NBCANM : NOMBRE DE CARACTERES PAR NOM DU LEXIQUE
C MXNOMS : NOMBRE MAXIMAL DE NOMS DANS LE LEXIQUE
C
C SORTIE :
C --------
C NTLX   : NUMERO DU TABLEAU MS CONTENANT LE LEXIQUE DECLARE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   OCTOBRE 1984
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/msvaau.inc"
      include"./incl/pp.inc"
      COMMON         MCN(MOTMCN)
      INTEGER        INOM(18)
C
C     LE NOMBRE D ENTIERS OCCUPES NBCANM CARACTERES
      NBENNM = ( NBCANM - 1 ) / NBCHMO + 1
C
C     NOMBRE D ENTIERS POUR UN NOM ET SES ATTRIBUTS DANS LE LEXIQUE
      M1LX   = MAX( 7 , NBENNM+3 )
      IF( M1LX .GT. 16 ) THEN
C         PROTECTION DE DEBORDEMENT
          M1LX   = 16
          NBENNM = M1LX - 3
      ENDIF
C
C     DECLARATION DU TABLEAU MX = LEXIQUE
      CALL TNMSDC( 'ENTIER' , M1LX*(MXNOMS+1) , NTLX )
C
C     OUVERTURE DU TABLEAU MS = LEXIQUE
      CALL TAMSOU( NTLX , MNLX )
      IF( MNLX .LE. 0 ) THEN
         WRITE(IMPRIM,10000)
10000 FORMAT('LXDC: REPERTOIRE DES TABLEAUX DE LA MS SATURE'/)
         CALL ARRET(100)
      ENDIF
C
C     INITIALISATION DU LEXIQUE D ADRESSE MNLX DANS LE SUPER TABLEAU MCN
C     NOMBRE D ENTIERS POUR UN NOM ET SES ATTRIBUTS DANS LE LEXIQUE
      MCN( MNLX     ) = M1LX
C     LE NOMBRE MAXIMAL DE NOMS DU LEXIQUE
      MCN( MNLX + 1 ) = MXNOMS
C     NOMBRE D ENTIERS NECESSAIRES AU STOCKAGE D UN NOM DU LEXIQUE
      MCN( MNLX + 2 ) = NBENNM
C     NOMBRE DE CARACTERES D'UN NOM DU LEXIQUE
      MCN( MNLX + 3 ) = NBCANM
C     NUMERO DU TAMS DU LEXIQUE PERE (ICI 0)
      MCN( MNLX + 4 ) = 0
C     NUMERO DU PREMIER NOM OCCUPE   (ICI 0)
      MCN( MNLX + 5 ) = 0
C     NUMERO DU PREMIER NOM LIBRE    (ICI 1)
      MCN( MNLX + 6  ) = 1
C
C     INITIALISATION A 'V DE' DES NOMS DU LEXIQUE
      CALL NOMENT( NBENNM , 'V DE' , INOM )
      IVIDE = ICHARX( 'V DE' )
      MN    = MNLX
      MN1   = 0
C
      DO I=1,MXNOMS
C
C        INCREMENTATION DE M1LX ENTIERS
         MN = MN + M1LX
C
C        LE NOM ACTUEL EST 'V DE'
         DO J=1,NBENNM
            MCN( MN - 1 + J ) = INOM( J )
         ENDDO
C
C        LE CHAINAGE SUR LES NOMS VIDES
         MN1 = MN + NBENNM
         MCN( MN1 ) = I + 1
C
C        LE TYPE EST 'V DE'
         MCN( MN1 + 1 ) = IVIDE
C
C        LE NUMERO DU TABLEAU EST 0
         MCN( MN1 + 2 ) = 0
C
      ENDDO
C
C     LE DERNIER NOM VIDE
      MCN( MN1 ) = 0
C
C     LE LEXIQUE EST OUVERT
      RETURN
      END
