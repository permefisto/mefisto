      SUBROUTINE ITEMAU( MNITEM )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   DOUBLER LE NOMBRE D'ITEMS VISIBLES STOCKABLES DANS LE
C -----   TABLEAU DES ITEMS

C ENTREE ET SORTIE :
C ------------------
C MNITEM : ADRESSE MCN DU TABLEAU DES ITEMS   ( TABLEAU MC )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS NOVEMBRE 1988
C....................................................................
      include"./incl/pp.inc"
      COMMON         MCN(MOTMCN)

C     LA VALEUR MAXIMALE DES 2 INDICES DU TABLEAU DES ITEMS
C     NOMBRE DE MOTS PAR ITEM
      N1ITEM = MCN( MNITEM )

C     NOMBRE MAXIMAL D'ITEMS STOCKABLES DANS CE TABLEAU MCN(MNITEM)
      N2ITEM = MCN( MNITEM + 1 )

C     LE NOUVEAU NOMBRE D'ITEMS STOCKABLES
      N22 = 2 * N2ITEM + 1

C     LE NOMBRE TOTAL DES VARIABLES DU TABLEAU AVANT DOUBLEMENT
      MXV1 = N1ITEM * N2ITEM + N1ITEM

C     LE NOMBRE TOTAL DES VARIABLES DU TABLEAU APRES DOUBLEMENT
      MXV2 = N1ITEM * N22    + N1ITEM

C     DECLARATION DU TABLEAU DOUBLE
      MNITE2 = 0
      CALL TNMCDC( 'ENTIER', MXV2, MNITE2 )

C     COPIE DE L'ANCIEN TABLEAU DANS LE NOUVEAU
      CALL TRTATA( MCN(MNITEM), MCN(MNITE2), MXV1 )

C     MISE A JOUR DU TABLEAU DOUBLE D'ADRESSE MNITE2
      MCN( MNITE2 + 1 ) = N22

C     DESTRUCTION DE L'ANCIEN TABLEAU
      CALL TNMCDS( 'ENTIER', MXV1, MNITEM )

C     LE TABLEAU DOUBLE DEVIENT LE TABLEAU DES ITEMS VISIBLES
      MNITEM = MNITE2

      RETURN
      END
