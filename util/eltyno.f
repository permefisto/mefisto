      SUBROUTINE ELTYNO(NOMPB,NO,NBNOE,NOTYNO)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETOURNER SELON LE PROBLEME TRAITE , LE NOM DE L EF
C ----- LE NOMBRE DE SES NOEUDS ET POUR CHACUN LEUR NO DE TYPE
C
C PARAMETRES D ENTREE :
C ---------------------
C NOMPB  : NOM DE LA CLASSE DU PROBLEME TRAITE
C NO     : NO  DE L EF SI INCORRECT EN ENTREE IL EST MIS A JOUR
C
C PARAMETRES RESULTATS :
C ----------------------
C NBNOE  : NOMBRE DE NOEUDS DE L EF
C NOTYNO : NO DU TYPE DE CHACUN DES NBNOE NOEUDS
C          1:VALEUUR AU NOEUD  2:GRADIENT NORMAL ( CF SP NOEUDL )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS   MARS 1987
C ......................................................................
      include"./incl/gsmenu.inc"
      CHARACTER*(*)      NOMPB
      CHARACTER*4        LISTE(3),NOMELE(2)
      INTEGER            NOTYNO(1)
      COMMON / UNITES /  LECTEU,IMPRIM,NUNITE(30)
      DATA LLISTE,LISTE /3,'THER','ELAS','FLUI'/
10005 FORMAT('ERREUR ELTYNO : LA CLASSE ',A4,' NON RETROUVEE PARMI'/
     &20(1X,A4))
10010 FORMAT('ERREUR ELTYNO:PROBLEME:',A4,' EF:',2(1X,A4),
     &' NON DEFINI'/)
C
C     CALCUL DE K NO DE LA CLASSE DE PROBLEME TRAITE
C     ----------------------------------------------
      DO 5 I=1,LLISTE
         K = I
         IF( NOMPB .EQ. LISTE(I) ) GOTO 8
    5 CONTINUE
C
C     CLASSE DE TRAVAIL NON RETROUVEE
C     -------------------------------
      NBLGRC(NRERR) = 1
      KERR(1) = 'ERREUR ELTYNO : LA CLASSE '//NOMPB//' INCONNUE'
      CALL LEREUR
      WRITE(IMPRIM,10005) NOMPB,LISTE
      RETURN
C
C     ******************************************************************
    8 GOTO ( 1000 , 10 , 10 ) , K
C     ******************************************************************
 10   NBLGRC(NRERR) = 2
      CALL ELNUNM( NO , NOMELE )
      KERR(1) = 'ELTYNO:EF INCONNU'
      KERR(2) = NOMELE(1) // ' ' // NOMELE(2)
      CALL LEREUR
      WRITE(IMPRIM,10010) NOMPB,NOMELE
      RETURN
C
C     ==================================================================
C     LE PROBLEME THERMIQUE
C     ==================================================================
C
C     ******************************************************************
 1000 GOTO ( 1010, 1020, 1030, 1040, 1050, 1060, 1070, 1080, 1090, 1100,
     &       1110, 1120, 1010, 1020, 1020, 1030, 1040, 1040, 1030, 1200,
     &       1020, 1220, 1040, 1240, 1020, 1020,  10,  1280, 1010, 1300,
     &       1310, 1320, 1330, 1340 ) ,NO
C     ******************************************************************
C
C     ==================================================================
C     TRIA AP1D ET TRIA 2P1D ET TRIA 2P1C
C     ==================================================================
 1010 NBNOE  = 3
C
 1015 DO 1018 I=1,NBNOE
         NOTYNO(I) = 1
 1018 CONTINUE
      RETURN
C
C     ==================================================================
C     TRIA AP2C ET TRIA 2P2C ET PENT 3R1C ET TRIA HD06 ET TRIA EQ06
C     ==================================================================
 1020 NBNOE  = 6
      GOTO 1015
C
C     ==================================================================
C     QUAD AQ1C ET QUAD 2Q1C ET TETR 3P1D
C     ==================================================================
 1030 NBNOE  = 4
      GOTO 1015
C
C     ==================================================================
C     QUAD AQ2C ET QUAD 2Q2C ET HEXA 3Q1C
C     ==================================================================
 1040 NBNOE  = 8
      GOTO 1015
C
C     ==================================================================
C     TRIA MT10
C     ==================================================================
 1050 NBNOE  = 3
C
 1055 DO 1058 I=1,NBNOE
         NOTYNO(I) = 2
 1058 CONTINUE
      RETURN
C
C     ==================================================================
C     TRIA MT21
C     ==================================================================
 1060 NBNOE  = 6
      GOTO 1055
C
C     ==================================================================
C     QUAD MQ10
C     ==================================================================
 1070 NBNOE  = 4
      GOTO 1055
C
C     ==================================================================
C     QUAD MQ21
C     ==================================================================
 1080 NBNOE  = 8
      GOTO 1055
C
C     ==================================================================
C     TETR M3T1
C     ==================================================================
 1090 NBNOE  = 4
      GOTO 1055
C
C     ==================================================================
C     TETR M3T2
C     ==================================================================
 1100 NBNOE  = 12
      GOTO 1055
C
C     ==================================================================
C     HEXA M3H1
C     ==================================================================
 1110 NBNOE  = 6
      GOTO 1055
C
C     ==================================================================
C     HEXA M3H2
C     ==================================================================
 1120 NBNOE  = 24
      GOTO 1055
C
C     ==================================================================
C     TETR 3P2C
C     ==================================================================
 1200 NBNOE  = 10
      GOTO 1015
C
C     ==================================================================
C     PENT 3R2C
C     ==================================================================
 1220 NBNOE  = 15
      GOTO 1015
C
C     ==================================================================
C     HEXA 3Q2C
C     ==================================================================
 1240 NBNOE  = 20
      GOTO 1015
C
C     ==================================================================
C     6CUB 6Q1C
C     ==================================================================
 1300 NBNOE  = 64
      GOTO 1015
C
C     ==================================================================
C     PYRA 3PY1
C     ==================================================================
 1310 NBNOE = 5
      GOTO 1015
C
C     ==================================================================
C     PYRA 3PY2
C     ==================================================================
 1320 NBNOE = 13
      GOTO 1015
C
C     ==================================================================
C     SEGM 1P1D  LAGRANGE DEGRE 1
C     ==================================================================
 1280 NBNOE = 2
      GOTO 1015
C
C     ==================================================================
C     SEGM 1P2D  LAGRANGE DEGRE 2
C     ==================================================================
 1330 NBNOE = 3
      GOTO 1015
C
C     ==================================================================
C     SEGM 1P3D  HERMITE DEGRE 3    VALEUR ET DERIVEE AUX 2 NOEUDS
C     ==================================================================
 1340 NBNOE = 2
      DO 1345 I=1,NBNOE
         NOTYNO(I) = 3
 1345 CONTINUE
C
      RETURN
      END
