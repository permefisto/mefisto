      SUBROUTINE MSOU( MOPDSF , M2ZLMG , M2ZLMK , M2ZLMN , MOTSMCN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : OUVRIR LA MEMOIRE SECONDAIRE
C ----- EN FAIT DECLARER LES REPERTOIRES EN MCG ET LES LIRE SUR
C       LE SUPER-FICHIER (S.F.) . LE MAXIMUM DE BUFFERS SONT ALLOUES
C       POUR LE REPERTOIRE PAGINE RETAMS DES TABLEAUX GERES PAR LA MS
C***********************************************************************
C ATTENTION: SOUS-PROGRAMME DEPENDANT MACHINE ICI VERSION IBM
C***********************************************************************
C ENTREES :
C ---------
C MOPDSF : NOMBRE DE MOTS DE CHAQUE PAGE DU SUPER-FICHIER
C          C EST A DIRE LDFIMS(2,1) DU CALL MSDC()
C M2ZLMG : NOMBRE MAXIMAL DE ZONES LIBRES ENTRE LES TABLEAUX DECLARES
C          PAR LA MS DANS LE SUPER-TABLEAU DE GESTION MCG
C M2ZLMK : NOMBRE MAXIMAL DE ZONES LIBRES ENTRE LES TABLEAUX DECLARES
C          PAR LA MS DANS LE SUPER-TABLEAU CARACTERE MCK
C M2ZLMN : NOMBRE MAXIMAL DE ZONES LIBRES ENTRE LES TABLEAUX DECLARES
C          PAR LA MS DANS LE SUPER-TABLEAU NUMERIQUE MCN
C MOTSMCN: NOMBRE DE MOTS DISPONIBLES DE LA MEMOIRE DE GESTION MCN
C
C SORTIES DANS LE COMMON / MSSFTA / :
C -----------------------------------
C TOUTES LES VARIABLES SONT INITIALISEES.CF BROCHURE SF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/ppmck.inc"
      include"./incl/motmcg.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      include"./incl/msvaau.inc"
      include"./incl/nmproj.inc"
C
      COMMON / MSIMTA / NOIMPR
      CHARACTER*16      ERREUR
      INTEGER           PAGE1(29)
      EQUIVALENCE       (PAGE1(1),NOFISF)
      CHARACTER*48      FICHNM,NOMFIC
CCC      CHARACTER*(NBCNFI)NOMFIC
      DATA              ERREUR / ' ERREUR MSOU   :' /
C
C     INITIALISATION DES VARIABLES DEPENDANT MACHINE
C     ==============================================
      CALL MSINIT
C
C     IMPRESSION DE L OUVERTURE DE LA MEMOIRE SECONDAIRE
CCC      WRITE(IMPRIM,10000)
C     IMPRESSION DES ADRESSAGES : AUCUNE
      NOIMPR = 0
CCC10000 FORMAT(/'OUVERTURE DE LA MEMOIRE SECONDAIRE')
C
C     OUVERTURE DU SUPER-FICHIER SF
C     =============================
C     ATTENTION: SOUS-PROGRAMME DEPENDANT MACHINE ICI VERSION IBM
C     ***********************************************************
      NOFISF = 10
      NOMFIC = FICHNM(0)
      OPEN( UNIT=NOFISF , ERR=9990 , STATUS='OLD' ,
     %      FILE=NOMFIC , ACCESS='DIRECT' ,
     %      FORM='UNFORMATTED' , IOSTAT=IOERR ,
     %      RECL=MOPDSF * NBCHMO )
C
C     LA LECTURE DE LA 1-RE PAGE DANS SON COMMON / MSSFTA /
C     =====================================================
      NPA = 1
      CALL FIPALE( NOFISF , NPA , 29 , PAGE1 )
C
C     LECTURE DU NOM DU PROJET DE LA MS
C     =================================
      NPA = 2
      READ( UNIT=NOFISF , REC=NPA ) NUEXEC,NMPROJ
C
C     VERIFICATION DU NOMBRE DE MOTS PAR PAGE DU SUPER-FICHIER
C     ========================================================
      IF( MOPDSF .NE. MOPASF ) THEN
         WRITE(IMPRIM,10005) ERREUR,MOPDSF,MOPASF
10005 FORMAT(A16,' MOTS PAR PAGE EN ENTREE=',I10,' SUR LE SUPER-FICHIER=
     %=',I10/' REVOYEZ L APPEL DE MSOU'/)
         CALL ARRET(-1)
      ENDIF
C
C     TRANSFERT DE MOTSMCN DANS LE COMMON / MSSFTA /
C     ==============================================
      MOTSMN = MOTSMCN
C
C     ATTENTION : MOTSMG ET MOTSMK DOIVENT CORRESPONDRE A LA DECLARATION
C     =========== DU NOMBRE DE MOTS DES TABLEAUX MCG ET MCK DES
C                 COMMON / MSMCG / ET / MSMCK /
C
      MOTSMG = MOTMCG
      MOTSMK = MOTMCK
C
C     INITIALISATION DU REPERTOIRE REZLMG DES ZONES LIBRES DANS MCG
C     INITIALISATION DU REPERTOIRE REZLMK DES ZONES LIBRES DANS MCK
C     INITIALISATION DU REPERTOIRE REZLMN DES ZONES LIBRES DANS MCN
C     =============================================================
C
C     LE NOMBRE DE MOTS DU REPERTOIRE REZLMN REZLMG ET REZLMK
      MOZLMG = 3 * M2ZLMG + 3
      MOZLMK = 3 * M2ZLMK + 3
      MOZLMN = 3 * M2ZLMN + 3
C     Y A-T-IL ASSEZ DE PLACE EN MCG ?
      I = MOZLMN + MOZLMG + MOZLMK + 10 * MOPDSF
      IF( I .GT. MOTSMG ) THEN
C        NON:ERREUR
         WRITE(IMPRIM,10006) ERREUR,MOTSMG,I
10006 FORMAT(A16,'LE NOMBRE DE MOTS DE MCG=',I12,' DOIT ETRE SUPERIEUR A
     %',I12/)
         STOP
      ENDIF
C
C     LES TABLEAUX REZLMN,G ET K SONT AU BOUT DU SUPER-TABLEAU MCG
      MGZLMG = MOTSMG - MOZLMG - MOZLMN - MOZLMK + 1
      CALL TAMCIN( MCG,MOTSMG,MOTSMG-MOZLMN-MOZLMG-MOZLMK,M2ZLMG,MGZLMG)
      MGZLMK = MGZLMG + MOZLMG
      CALL TAMCIN( MCG , MOTSMG , MOTSMK , M2ZLMK , MGZLMK )
      MGZLMN = MGZLMK + MOZLMK
      CALL TAMCIN( MCG , MOTSMG , MOTSMN , M2ZLMN , MGZLMN )
C
C     LA DECLARATION DU BUFFER DU SUPER-FICHIER
C     =========================================
      MGBUSF = 0
      CALL TAMCDC( 'ENTIER ' , MOPASF , MCG(MGZLMG) , MGBUSF )
C
C     LA DECLARATION ET LA LECTURE DU REPERTOIRE REFIMS
C     =================================================
      MGFIMS = 0
      CALL TAMCDC( 'ENTIER ' , M1FIMS * M2FIMS , MCG(MGZLMG) , MGFIMS )
      NPA = 3
      CALL TAMCLE( NOFISF , NPA , MOPASF , M1FIMS*M2FIMS , MCG(MGFIMS) )
C
C
C     L'OUVERTURE DES FICHIERS NUMERIQUES
C     ===================================
      IG = MGFIMS
      DO 10 I=1,M2FIMS
         IF( MCG( IG ) .LE. 0 ) GOTO 7
C        LE FICHIER A DEJA ETE DECLARE.IL EST OUVERT
         CALL FICHOU( I , MCG(IG) , MCG(MGZLMG) )
C        PASSAGE AU FICHIER SUIVANT
 7       IG = IG + M1FIMS
 10   CONTINUE
C
C     LA DECLARATION DU TABLEAU NPSFTA( NPTAMS )
C     ==========================================
      MGNPSF = 0
      CALL TAMCDC( 'ENTIER ' , NPTAMS , MCG(MGZLMG) , MGNPSF )
C
C     LA LECTURE DU TABLEAU NUMERO DES PAGES SF DE RETAMS
C     ===================================================
      NPA = NSFNPS
      CALL TAMCLE( NOFISF , NPA , MOPASF , NPTAMS , MCG(MGNPSF) )
C
C     RECHERCHE DU NOMBRE MAXIMAL DE BUFFERS RETAMS LOGEABLES DANS MCG
C     ================================================================
      CALL TAMCMX( 'ENTIER ' , MCG(MGZLMG) , I )
      NBBUTA = MIN( NPTAMS , I / ( 3 + MOPASF) ) - 1
C     UNE PAGE EST MISE EN RESERVE POUR AUGMENTER EVENTUELLEMENT
C     D'UN FICHIER LA MS
CCC      WRITE(IMPRIM,10010) NBBUTA,NPTAMS
CCC10010 FORMAT(' NOMBRE DE PAGES EN MCG=',I6,' POUR LES',I6,' PAGES DES DE
CCC     %SCRIPTEURS DE TABLEAUX MS')
      IF( NBBUTA .LE. 0 ) THEN
         WRITE(IMPRIM,10011)
10011 FORMAT(A16,' LE SUPER-TABLEAU MCG N EST PAS ASSEZ GRAND.IL DOIT ET
     %RE AUGMENTE D AU MOINS UNE PAGE SUPER-FICHIER'/)
         CALL ARRET(-1)
      ENDIF
C
C     LA DECLARATION DU TABLEAU MCGBUTA(3 , NBBUTA)
C     ============================================
      MGBUTA = 0
      CALL TAMCDC( 'ENTIER ' , 3 * NBBUTA , MCG(MGZLMG) , MGBUTA )
C
C     DECLARATION DES NBBUTA PAGES(BUFFERS) DE RETAMS EN MCG
C     =====================================================
      IG = MGBUTA
      DO 20 I=1,NBBUTA
C        DECLARATION MCG DE LA PAGE I
         MCG(IG) = 0
         CALL TAMCDC( 'ENTIER ' , MOPASF , MCG(MGZLMG) , MCG(IG) )
C        MCG(IG  ):ADRESSE MCG DU BUFFER I
C        MCG(IG+1):NUMERO DE PAGE SF ACTUELLEMENT DANS LE BUFFER I
         MCG(IG+1) = 0
C        MCG(IG+2):NOMBRE D'APPELS A CE BUFFER I
         MCG(IG+2) = 0
         IG       = IG + 3
 20   CONTINUE
      GOTO 9999
C
C     ERREUR A L'OUVERTURE DU SUPER-FICHIER
C     =====================================
 9990 WRITE(IMPRIM,19990) ERREUR,NOFISF
19990 FORMAT(A16,'LE SUPER-FICHIER',I6,' NE PEUT ETRE OUVERT'/)
      CALL ARRET(-1)
C
 9999 RETURN
      END
