      SUBROUTINE INTARTRPL( S1, S2, P1, P2, P3, NBPTI, PTI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     DETERMINER LES AU PLUS 3 POINTS D'INTERSECTION
C -----     DU SEGMENT S1-S2 AVEC LE TRIANGLE P1 P2 P3 ET
C           INTERNES A S1S2 ET INTERNE A L'ARETE DU TRIANGLE
C           DANS LE CAS OU L'ARETE S1S2 ET LE TRIANGLE P1P2P3 SONT
C           TOUS LES DEUX DANS UN MEME PLAN DE R3
C
C ENTREES:
C --------
C S1,S2   : LES 2 POINTS QUI DEFINISSENT L'ARETE
C P1,P2,P3: LES 3 POINTS QUI DEFINISSENT LE TRIANGLE
C
C SORTIES:
C --------
C NBPTI  : NBPTI(K)=NOMBRE (0 ou 1) DE POINT INTERNE INTERSECTION DE S1S2
C                   ET DE L'ARETE K (EXTREMITES INCLUSES)
C          =0 LE POINT D'INTERSECTION EST EXTERNE A
C             S1S2 ET/OU L'ARETE K DE P1P2P3
C          =1 LE POINT D'INTERSECTION EST INTERNE A
C             S1S2 ET A L'ARETE K DE P1P2P3 (SOMMETS EXTREMITES INCLUS)
C PTI    : LES 3 COORDONNEES DES EVENTUELS POINTS D'INTERSECTION INTERNES
C          DU SEGMENT S1S2 AVEC LES 3 ARETES DU TRIANGLE P1P2P3
C          LES ARETES AVEC INTERSECTION SONT RETROUVABLES PAR NBPTI(K)=1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: PERRONNET Alain LJLL UPMC & St Pierre du Perray SEPTEMBRE 2011
C2345X7..............................................................012
      DOUBLE PRECISION  S1(3), S2(3), P1(3), P2(3), P3(3), PTI(3,3), CBP
      INTEGER           NBPTI(3)
C
C     RECHERCHE DU POINT D'INTERSECTION S1S2 AVEC CHAQUE ARETE DU TRIANGLE
C     ET INTERNE A S1S2 ET A L'ARETE DU TRIANGLE
      CALL INTARAR3( S1, S2, P1, P2, NBPTI(1), PTI(1,1), CBP )
      CALL INTARAR3( S1, S2, P2, P3, NBPTI(2), PTI(1,2), CBP )
      CALL INTARAR3( S1, S2, P3, P1, NBPTI(3), PTI(1,3), CBP )
C
C     NBPTI: LE NOMBRE DE POINT D'INTERSECTION DES ARETES S1S2 ET P1P2
C            =0 SI LE POINT D'INTERSECTION N'EXISTE PAS OU EST EXTERNE
C               STRICTEMENT A L'UNE DES 2 ARETES
C            =1 SI LE POINT D'INTERSECTION EXISTE ET EST INTERNE AUX 2
C               ARETES (EXTREMITES INCLUSES)
C
      NBPTIT = NBPTI(1) + NBPTI(2) + NBPTI(3)
      IF( NBPTIT .EQ. 3 ) THEN
         print *,'INTARTRPL nbptit=3 est un CAS IMPOSSIBLE!...'
         print *,'S1=',S1
         print *,'S2=',S2
         print *,'P1=',P1
         print *,'P2=',P2
         print *,'P3=',P3
      ENDIF
C
      RETURN
      END
