      SUBROUTINE TNDECL( KNOM , KTYPE , KTYPVA , NBVARI, NRETOU  )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    DECLARER LE TABLEAU MS DE TYPE KTYPE DEFINI PAR SON NOM KNOM
C -----    PAR EXEMPLE KNOM = ADAM>LIGNE>DEFINITION
C
C ENTREES :
C ---------
C KNOM   : LE NOM A DECLARER.IL EST DEFINI PAR PLUSIEURS MOTS SEPARES
C          PAR LE CARACTERE > . PAR EXEMPLE  ADAM>LIGNE
C KTYPE  : NOM DU TYPE 'LEXI' OU 'TAMS' OU 'TATA' OU 'RELA'
C KTYPVA : SI KTYPE N EST PAS 'LEXI' ALORS KTYPE VAUT
C          'LOGIQUE'     'CARACTERE'   'ENTIER/2'    'ENTIER'
C          'REEL'        'REEL2'       'REEL4'       'COMPLEXE'
C          'COMPLEXE2'
C          DANS LE CAS KTYPE='LEXI' KTYPVA EST LE NOMBRE DE CARACTERES
C          D'UN NOM DU LEXIQUE CONVERTI EN 4 CARACTERES (=> <10000)
C NBVARI : NOMBRE DE VARIABLES DE TYPE KTYPVA
C
C SORTIE :
C --------
C NRETOU : 0 SINON LE NOM A ETE AJOUTE
C          >0 SI LE NOM A ETE RETROUVE OU KNOM EST INCORRECT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS SEPTEMBRE 1988
C23456---------------------------------------------------------------012
      CHARACTER*(*) KNOM,KTYPE,KTYPVA
C
C     LE LEXIQUE PERE DE CE KNOM
      CALL LXNTPN( KNOM , NTLX , MNLX , N1 , L )
      IF( N1 .LE. 0 ) THEN
         NRETOU = 1
         RETURN
      ENDIF
C
C     KNOM(N1:L) EST LE NOM TERMINAL DANS LE LEXIQUE NTLX
C     DECLARATION COMME TABLEAUX MS
      CALL LXNMDC( NTLX , KNOM(N1:L) , KTYPE , KTYPVA , NBVARI ,
     %             NRETOU)
      END
