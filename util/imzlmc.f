      SUBROUTINE IMZLMC( KMC, REZLMC )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  IMPRIMER LE CONTENU DU TABLEAU REZLMC QUI GERE LES ZONES LIBRES
C -----  ENTRE LES TABLEAUX DU SUPER-TABLEAU DE NOM KMC ('MCN' OU 'MCG')

C ENTREES :
C ---------
C KMC    : NOM DU SUPER-TABLEAU ('MCN' OU 'MCG')
C REZLMC : TABLEAU REPERTOIRE DES ZONES LIBRES DANS MC
C          REZLMC(1,0)=VALEUR MAXIMALE DU 2-EME INDICE DU TABLEAU REZLMC
C                      OU NOMBRE MAXIMAL DE ZONES LIBRES PERMISES
C                      DANS LE SUPER-TABLEAU
C          REZLMC(2,0)=VALEUR DU 2-EME INDICE DE REZLMC DESIGNANT LA
C                      1-ERE ZONE LIBRE.DEPART DU CHAINAGE ZONES LIBRES
C          REZLMC(3,0)=NUMERO DE LA 1-ERE COLONNE DE REZLMC NON OCCUPEE
C                      PAR UNE ZONE LIBRE.CHAINAGE DES COLONNES NON
C                      OCCUPEES DE REZLMC
C          POUR N COMPRIS ENTRE 1 ET REZLMC(1,0) :
C          REZLMC(1,N)=ADRESSE MOT         DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(2,N)=NOMBRE  DE MOTS     DE LA N-EME ZONE LIBRE DE MC
C          REZLMC(3,N)=CHAINAGE SUCCESSEUR DE LA N-EME ZONE LIBRE DE MC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS    DECEMBRE 1983
C.......................................................................
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      INTEGER           REZLMC(3,0:*)
      CHARACTER*3       KMC

C     IMPRESSION DU TABLEAU REZLMC
      WRITE(IMPRIM,10000)KMC,KMC,(REZLMC(I,0),I=1,3)
10000 FORMAT(/'TABLEAU REZLMC ',A3,' REPERTOIRE DES ZONES LIBRES ENTRE L
     %ES TABLEAUX ',A3,/1X,78(1H-)/
     %' MAX INDICE1=3 MAX INDICE2=',I6/' CHAINAGE 1-ERE ZONE LIBRE='
     %,I7,' CHAINAGE 1-ERE COLONNE LIBRE=',I7)
10010 FORMAT(
     %' ZONE',I6,' : ADRESSE=',I12,' MOTS LIBRES=',I12,' SUIVANT=',I12)

      L0   = REZLMC(2,0)
      NBZL = 0
      DO J=1,REZLMC(1,0)
         IF( L0 .EQ. 0 ) GOTO 9000
C        IL Y A UNE ZONE LIBRE. SES CARACTERISTIQUES SONT IMPRIMEES
         WRITE(IMPRIM,10010) L0,(REZLMC(I,L0),I=1,3)
C        LA ZONE SUIVANTE
         NBZL = NBZL + 1
         L0 = REZLMC(3,L0)
      ENDDO
C
 9000 WRITE(IMPRIM,19000) NBZL,REZLMC(1,0)
19000 FORMAT(I6,' ZONES LIBRES UTILISEES SUR',I6,' DISPONIBLES'/)

      RETURN
      END
