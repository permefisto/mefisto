      SUBROUTINE REFORC( NYOBJT, NUOBJT, NDIM,
     %                   XPI,YPI,ZPI, XNPI,YNPI,ZNPI, MNFORC,
     %                   FORCES)
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++012
C BUT :    REMPLIR LE TABLEAU DES FORCES EXERCEES SUR L'OBJET
C -----    AU TEMPS TEMPS ET AU POINT XPI,YPI,ZPI
C
C ENTREES :
C ---------
C NYOBJT : NUMERO DU TYPE DE L'OBJET (1:POINT, 2:LIGNE, 3:SURFACE, ... )
C NUOBJT : NUMERO DE L'OBJET DANS SON LEXIQUE
C NDIM   : 2 OU 3 DIMENSION DE L'ESPACE  ( 2 SI PB AXISYMETRIQUE )
C          ou NOMBRE DE COMPOSANTES DES FORCES
C
C XPI    : ABSCISSE DU POINT D'INTEGRATION NUMERIQUE
C YPI    : ORDONNEE DU POINT D'INTEGRATION NUMERIQUE
C ZPI    : COTE     DU POINT D'INTEGRATION NUMERIQUE
C
C XNPI   : ABSCISSE DU VECTEUR NORMAL UNITAIRE AU POINT D'INTEGRATION
C YNPI   : ORDONNEE DU VECTEUR NORMAL UNITAIRE AU POINT D'INTEGRATION
C ZNPI   : COTE     DU VECTEUR NORMAL UNITAIRE AU POINT D'INTEGRATION
C
C REMARQUE: CES 3 VALEURS SONT ACTIVES POUR UNE SURFACE D'UN OBJET 3D
C                                      POUR UNE LIGNE   D'UN OBJET 2D
C           DANS LES AUTRES CAS (VOLUME, POINT, LIGNE EN 3D)
C           LES 3 VALEURS SONT A INITIALISER NULLES
C
C MNFORC : ADRESSE MCN DU TABLEAU 'FORCES'
C
C SORTIE :
C --------
C FORCES : LE TABLEAU (NDIM) DES FORCES EXERCEES SUR L'OBJET
C          UNE COMPOSANTE NON INITIALISEE EST MISE A ZERO
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++012
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     JUILLET 1989
C....6...............................................................012
      include"./incl/donela.inc"
      include"./incl/a___force.inc"
      include"./incl/ctemps.inc"
      DOUBLE PRECISION XPI,YPI,ZPI,XNPI,YNPI,ZNPI,XYZ(10)
      DOUBLE PRECISION FORCES(NDIM)
      include"./incl/pp.inc"
      COMMON           MCN(MOTMCN)
      REAL             RMCN(1)
      EQUIVALENCE     (MCN(1),RMCN(1))
C
C     MISE A ZERO DU TABLEAU FORCES
C     -----------------------------
      CALL AZEROD( NDIM, FORCES )
C
C     LE NOMBRE DE COMPOSANTES DE LA FORCE
      NBCOFO = MCN( MNFORC + WBCOFO )
C
C     REMPLISSAGE SELON LE TYPE DES DONNEES DES FORCES
      IF( MCN(MNFORC+WTFORC) .EQ. 1 ) THEN
C
C        VALEURS CONSTANTES POUR CET OBJET
C        =================================
         MN = MNFORC + WUCOFO + NBCOFO
         DO 30 J=0,NBCOFO-1
C
C           LE NUMERO DE LA COMPOSANTE
            NUCOFO = MCN( MNFORC + WUCOFO + J )
C
C           LA FORCE EST MISE DANS LA BONNE COMPOSANTE
            FORCES( NUCOFO ) = RMCN( MN + J )
C
 30      CONTINUE
C
      ELSE
C
C        FONCTION UTILISATEUR
C        ====================
C        ADRESSE DU NUMERO DE LA FONCTION
         MN     = MNFORC + WUCOFO + NBCOFO
         XYZ(1) = TEMPS
         XYZ(2) = XPI
         XYZ(3) = YPI
         XYZ(4) = ZPI
C
C        TYPE DU PLSV ET SON NUMERO
         XYZ(5) = NYOBJT
         XYZ(6) = NUOBJT
C
C        AJOUT DE LA NORMALE UNITAIRE VN A LA FRONTIERE LE 2/12/99
C        POUR FACILITER L'ENTREE PAR L'UTILISATEUR DE FORCES NORMALES
C        ATTENTION: POUR NLSE VN EST REMPLACE PAR
C                   LA VALEUR DE L'ONDE COMPLEXE (PReel,PImaginaire,0)
         XYZ( 8) = XNPI
         XYZ( 9) = YNPI
         XYZ(10) = ZNPI
C
         DO 50 J=0,NBCOFO-1
C
C           LE NUMERO DE LA COMPOSANTE DE LA FORCE A FIXER
            NUCOFO = MCN( MNFORC + WUCOFO + J )
            XYZ(7) = NUCOFO
C
            CALL FONVAL( MCN(MN), 10, XYZ,
     %                   NCODEV, FORCES( NUCOFO ) )
C
 50      CONTINUE
      ENDIF
      END
