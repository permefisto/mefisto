      SUBROUTINE DEPKLG( NLMIN  , NCMIN  , NLMAX , NCMAX , NBCARA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DEPLACER EVENTUELLEMENT LES CARACTERES QUI SUIVENT
C ----- KLG(NLMIN)(NCMIN:...) AFIN QUE KLG(NLMIN)(NCMIN:NCMIN+NBCARA-1)
C       SOIENT LIBRES
C
C ENTREES :
C ---------
C NBCARA : NOMBRE DE CARACTERES BLANCS A INSERER
C
C MODIFIES :
C ---------
C NLMIN,NCMIN : POSITION DANS KLG DU PREMIER CARACTERE A DEPLACER
C               NLMIN=0 SI PAS ASSEZ DE LIGNES DANS KLG
C NLMAX,NCMAX : POSITION DANS KLG DU DERNIER CARACTERE A TRAITER
C
C SORTIES :
C ---------
C NRETOU :  0 SI PAS D'ERREUR
C          >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS    FEVRIER 1990
C23456---------------------------------------------------------------012
      include"./incl/lu.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
C
C     RECHERCHE DU DERNIER CARACTERE NON BLANC DE LA LIGNE NL
      NL = NLMIN
      DO 10 NC=NBCALI,NCMIN,-1
         IF( KLG(NL)(NC:NC) .NE. ' ' ) GOTO 20
 10   CONTINUE
      NC = NCMIN - 1
C
C     LE NOMBRE DE CARACTERES LIBRES
 20   NBCALB = NBCALI - NC
      IF( NBCARA .LE. NBCALB ) THEN
C        IL Y A SUFFISAMMENT DE PLACE LIBRE
C        LES CARACTERES SONT DECALES VERS NBCALI
         DO  30 I=NBCALI-NBCARA,NCMIN,-1
            KLG(NLMIN)(I+NBCARA:I+NBCARA) = KLG(NLMIN)(I:I)
 30      CONTINUE
         KLG(NLMIN)(NCMIN:NCMIN+NBCARA-1) = ' '
C        LE DECALAGE A PORTE SUR NBCARA CARACTERE
         NBCATR = NBCARA
C        MISE A JOUR EVENTUELLE DE NLMAX,NCMAX
         IF( NLMAX .EQ. NLMIN ) THEN
            NCMAX = NCMAX + NBCATR
         ENDIF
         GOTO 8000
      ENDIF
C
C     LA LIGNE NLMIN NE SUFFIT PAS.
C     UNE LIGNE BLANCHE EST INTERCALEE
      IF( LHKLG .GE. MXKLG) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'LU: SATURATION DES LIGNES DE KLG'
         KERR(2) = 'LU: AUGMENTER MXKLG DANS LU.INC'
         CALL LEREUR
         NLMIN = 0
         RETURN
      ENDIF
      DO 40 NL = LHKLG,NLMIN+1,-1
         KLG(NL+1) = KLG(NL)
 40   CONTINUE
C     UNE LIGNE DE KLG EST OCCUPEE EN PLUS
      LHKLG = LHKLG + 1
C     LA LIGNE NLMIN+ 1 EST BLANCHIE
      KLG(NLMIN+1) = ' '
C     LA LIGNE MAX A ETE DECALEE
      NLMAX = NLMAX + 1
C     LA FIN DE LA LIGNE RECOIT LA FIN DE LA LIGNE NLMIN
      I = NCMIN + NBCALB
      KLG(NLMIN+1)(I:NBCALI) = KLG(NLMIN)(NCMIN:NBCALI)
C     LE NOMBRE DE CARACTERES TRANSLATES
      NBCATR = NBCALB
      KLG(NLMIN)(NCMIN:NBCALI) = ' '
C     MISE A JOUR DE NCMAX
      IF( NLMAX .EQ. NLMIN+1 ) NCMAX = NCMAX + NBCALB
C     LA PLACE LIBRE SUR LA LIGNE NLMIN
      NBCALB = NBCALI - NCMIN + 1
      IF( NBCARA .LE. NBCALB ) GOTO 8000
      NL1    = NLMIN + 1
C
C     LA PLACE LIBRE SUR LA LIGNE NLMIN + 1
      NBCALB = I - 1
C     Y A T IL ASSEZ DE PLACE AU DEBUT DE LA LIGNE NLMIN+1
 45   IF( NBCARA .LE. NBCALB ) THEN
         NLMIN = NL1
         NCMIN = 1
         GOTO 8000
      ENDIF
C
C     PAS ASSEZ DE PLACE . UNE LIGNE BLANCHE EST AJOUTEE
      IF( LHKLG .GE. MXKLG) THEN
         NBLGRC(NRERR) = 2
         KERR(1) =  'LU: SATURATION DES LIGNES DE KLG'
         KERR(2) =  'LU: AUGMENTER MXKLG DANS LU.INC'
         CALL LEREUR
         NLMIN = 0
         RETURN
      ENDIF
      DO 50 NL = LHKLG,NL1,-1
         KLG(NL+1) = KLG(NL)
 50   CONTINUE
      LHKLG = LHKLG + 1
      NLMAX = NLMAX + 1
      KLG(NL1) = ' '
      NBCALB = NBCALB + NBCALI
      GOTO 45
C
C     DEPLACEMENT EVENTUEL DU DERNIER CARACTERE TRAITE
 8000 IF( NLMAX .GT. NLPTV2 .OR.
     %  ( NLMAX .EQ. NLPTV2 .AND. NCMAX .LT. NCPTV2 ) ) THEN
C        LE DERNIER CARACTERE LU DOIT ETRE DEPLACE
         NLPTV2 = NLMAX
         NCPTV2 = NCMAX + NBCATR
      ENDIF
      END
