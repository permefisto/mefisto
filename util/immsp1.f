      SUBROUTINE IMMSP1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : IMPRIMER LES VARIABLES DU COMMON / MSSFTA / C'EST A DIRE
C ----- LES VARIABLES DE LA PREMIERE PAGE DU SUPER-FICHIER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS    DECEMBRE 1983
C.......................................................................
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSSFTA / PAGE1(29)
      INTEGER           PAGE1
C
C     IMPRESSION DE LA 1-RE PAGE
      WRITE(IMPRIM,10100) (PAGE1(I),I=1,12)
