10100 FORMAT('0GENERALITES DE LA MS :'/1X,76(1H=)/
     %' NO SYSTEME DU SUPER-FICHIER',T61,'NOFISF :',I9/
     %' NOMBRE DE PAGES DU SUPER-FICHIER',T61,'NBPASF :',I9/
     %' NOMBRE DE MOTS PAR PAGE DU SF',T61,'MOPASF :',I9/
     %' ADRESSE MCG DU BUFFER DU SF',T61,'MGBUSF :',I9/
     %' NUMERO DE LA PREMIERE PAGE SF LIBRE',T61,'NSFLIB :',I9/
     %' MAXIMUM DU 1-ER INDICE REPERTOIRE REFIMS',T61,'M1FIMS :',I9/
     %' MAXIMUM DE FICHIERS NUMERIQUES DECLARES (SF NON COMPRIS)',T61,
     %  'M2FIMS :',I9/
     %' ADRESSE MCG DE REFIMS',T61,'MGFIMS :',I9/
     %' NUMERO 1-RE PAGE SF DE REFIMS',T61,'NSFIMS :',I9/
     %' NUMERO 1-ER FICHIER NON ENCORE DECLARE',T61,'LPFIMS :',I9/
     %' MAXIMUM DU 1-ER INDICE DE REPERTOIRE PAGINE RETAMS',T61,'M1TAMS
     %:',I9/
     %' MAXIMUM DE TABLEAUX MS DECLARABLES',T61,'M2TAMS :',I9)
10101 FORMAT(' ADRESSE MCG DU REPERTOIRE DES BUFFERS DE RETAMS',T61,
     %'MGBUTA :',I9/
     %' NOMBRE BUFFERS OU PAGES SF EN MCG DE RETAMS',T61,'NBBUTA :',I9/
     %' NOMBRE DE PAGES SF DU REPERTOIRE RETAMS',T61,'NPTAMS :',I9/
     %' NOMBRE D APPELS DE TABLEAUX MS',T61,'NATAMS :',I9/
     %' NOMBRE DESCRIPTEURS DE TABLEAUX MS PAR PAGE SF',T61,'NBCTMS :',
     %I9/
     %' DEBUT CHAINAGE DES TABLEAUX MS LIBRES',T61,'LLTAMS :',I9/
     %' DEBUT CHAINAGE DES TABLEAUX MS FERMES',T61,'LFTAMS :',I9/
     %' ADRESSE MCG DU REPERTOIRE DES PAGES SUIVANTES DES FICHIERS',T61,
     %  'MGNPSF :',I9/
     %' NUMERO 1-RE PAGE SF DU TABLEAU NPSFTA',T61,'NSFNPS :',I9/
     %' NOMBRE DE PAGES SF DU TABLEAU NPSFTA',T61,'NPSNPS :',I9/
     %' ADRESSE MCG DU REPERTOIRE ZONES LIBRES DE MCG',T61,'MGZLMG :',I9
     %/' ADRESSE MCG DU REPERTOIRE ZONES LIBRES DE MCK',T61,'MGZLMK :'
     %,I9/' ADRESSE MCG DU REPERTOIRE ZONES LIBRES DE MN',T61,'MGZLMN :'
     %,I9/' NOMBRE DE MOTS DU SUPER-TABLEAU MCN',T61,'MOTSMN :',I9/
     %' NOMBRE DE MOTS DU SUPER-TABLEAU MCG',T61,'MOTSMG :',I9/
     %' NOMBRE DE MOTS DU SUPER-TABLEAU MCK',T61,'MOTSMK :',I9/
     %' NUMERO DU TABLEAU MS DU LEXIQUE ADAM',T61,'NTADAM :',I9/)
C
      RETURN
      END
