      SUBROUTINE FIPASU( NO , REFIMS , MGNOPS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETROUVER L'ADRESSE MCG DE RED1FI(NO) C EST A DIRE
C ----- L ADRESSE DE LA PAGE SUIVANTE DE LA PAGE NO
C       UTILISATION DU CHAINAGE DES PAGES SELON RED1FI
C       REPERTOIRE DU CHAINAGE DES PAGES OCCUPEES OU LIBRES D'UN FICHIER
C
C ENTREES :
C ---------
C NO     : NUMERO DE LA PAGE DONT LE NUMERO DE LA SUIVANTE EST DEMANDE
C
C ENTREES ET SORTIES :
C --------------------
C REFIMS : REPERTOIRE DU FICHIER ICI TRAITE DE LA MEMOIRE SECONDAIRE
C          REFIMS(1) : NUMERO LOGIQUE DU FICHIER POUR LE SYSTEME
C          REFIMS(2) : NOMBRE DE PAGES DE CE FICHIER DE LA MS
C          REFIMS(3) : NOMBRE DE MOTS PAR PAGE DE CE FICHIER
C          REFIMS(4) : ADRESSE DANS LA MEMOIRE DE GESTION MCG DE L UNIQU
C                      BUFFER DE CE FICHIER
C          REFIMS(5) : NUMERO DE LA PAGE DE SON REPERTOIRE DES PAGES
C                      (RED1FI) ACTUELLEMENT EN MEMOIRE CENTRALE
C          REFIMS(6) : NUMERO DE LA PAGE SF QUI PRECEDE LA PREMIERE
C                      PAGE DU REPERTOIRE(RED1FI) DE CE FICHIER
C          REFIMS(7) : NUMERO DE LA PREMIERE PAGE LIBRE DE RED1FI
C                      CHAINAGE DES PAGES LIBRES DE CE FICHIER N
C
C SORTIE :
C --------
C MGNOPS : ADRESSE MCG DU NUMERO DE LA PAGE SUIVANTE DE LA PAGE NO
C          DU FICHIER MS DECRIT PAR LE TABLEAU REFIMS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS    DECEMBRE 1983
C.......................................................................
      include"./incl/motmcg.inc"
      include"./incl/gsmenu.inc"
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      INTEGER           REFIMS(7)
C
C     LE NUMERO NO EST IL COMPRIS ENTRE 1 ET LE MAXIMUM DE PAGES
C     DU FICHIER ?
      IF( NO .LE. 0 .OR. NO .GT. REFIMS(2) ) THEN
         NBLGRC(NRERR) = 3
         WRITE(KERR(MXLGER)(1:10),'(I10)') NO
         KERR(1) = 'NUMERO '//KERR(MXLGER)(1:10)//
     %              ' INCORRECT DE PAGE'
         WRITE(KERR(MXLGER)(1:4),'(I4)') REFIMS(1)
         KERR(2) = 'SUR LE FICHIER '//KERR(MXLGER)(1:4)
         WRITE(KERR(MXLGER)(1:10),'(I10)') REFIMS(2)
         KERR(3) = 'NON COMPRIS ENTRE 1 ET '//KERR(MXLGER)(1:10)
         CALL LEREUR
         CALL IMFIMS
         CALL ARRET( 100 )
      ENDIF
C
C     MOPASF : NOMBRE DE MOTS DE LA PAGE DU SUPER-FICHIER (SF)
C     LE NUMERO DE LA PAGE CONTENANT LA LIGNE NO DE RED1FI
      NOPAG  = NO / MOPASF
C
C     LE DECALAGE DANS LA PAGE
      NDECAL = NO - NOPAG * MOPASF
C
C     LA CORRECTION SI NO EST UN MULTIPLE DE MOPASF OU NON
      IF( NDECAL .EQ. 0 ) THEN
         NDECAL = MOPASF
      ELSE
         NOPAG  = NOPAG + 1
      ENDIF
C
C     LA PAGE ACTUELLE DANS LE BUFFER EST-ELLE CELLE RECHERCHEE ?
      NPBUFI = REFIMS(5)
      IF( NPBUFI .NE. NOPAG ) THEN
C
C        NON . LA PAGE ACTUELLE EST SAUVEGARDEE SUR LE FICHIER
         IF( NPBUFI .GT. 0 )
     %   CALL FIPAEC( NOFISF , REFIMS(6) + NPBUFI - 1 ,
     %                MOPASF , MCG( REFIMS(4) ) )
C
C        LA PAGE INTERESSANTE EST LUE SUR LE FICHIER
         CALL FIPALE( NOFISF , REFIMS(6) + NOPAG  - 1 ,
     %                MOPASF , MCG( REFIMS(4) ) )
C
C        MISE A JOUR DU NO DE PAGE ACTUELLEMENT DANS LE BUFFER
         REFIMS(5) = NOPAG
      ENDIF
C
C     L ADRESSE MCG DU NO DE PAGE SUIVANTE RED1FI( NO )
      MGNOPS = REFIMS(4) - 1 + NDECAL
C
      RETURN
      END
