      SUBROUTINE SETACLIC( XYZSOM, L1ARET, L2ARET, LARETE,
     %                     NXCL,   NYCL,   NOARETCL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   RECHERCHER PARMI TOUTES LES ARETES LAQUELLE EST LA PLUS PROCHE
C -----   DU POINT CLIQUE (NXCL,NYITCL) SUR LA FENETRE AXONOMETRIQUE PIXELS

C ENTREES:
C --------
C XYZSOM : XYZ les 3 COORDONNEES CARTESIENNES DES ARETES DU MAILLAGE
C L1ARET : NOMBRE DE MOTS PAR ARET DU TABLEAU LARETE
C L2ARET : NOMBRE DE FACES DU TABLEAU LARETE
C LARETE : LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR L'ARETE SUIVANTE
C          LARETE(4:3+MXFAAR,I)= NO NOSOFA DE LA FACE CONTENANT L'ARETE
C          SI UNE ARETE APPARTIENT A PLUS DE MXFAAR FACES, 
C          LE NUMERO DE FACE MXFAAR EST RENDU NEGATIF POUR INDIQUER
C          QUE LA LISTE DES FACES EST INCOMPLETE

C NXCL   : ABSCISSE PIXELS DU POINT CLIQUE
C NYCL   : ORDONNEE PIXELS DU POINT CLIQUE

C SORTIE :
C --------
C NOARETCL: >0 NUMERO DANS LARETE DE L'ARETE LA PLUS PROCHE DU CLIC
C           =0 SI PAS D'ARETE PROCHE ( DistancePX > 40 PIXELS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : Alain PERRONNET  Saint PIERRE du PERRAY          Janvier 2021
C2345X7..............................................................012
      include"./incl/trvari.inc"
      INTEGER        LARETE(L1ARET,L2ARET)
      REAL           XYZSOM(3,*)
      REAL           BARY(3), BARYAXO(3)

C     DISTANCE PTV-OEIL POUR CETTE VISEE
C     ----------------------------------
      DPTVOEIL = SQRT( ( AXOEIL(1) - AXOPTV(1) ) **2
     %               + ( AXOEIL(2) - AXOPTV(2) ) **2
     %               + ( AXOEIL(3) - AXOPTV(3) ) **2 )

C     RECHERCHE DE L'ARETE VISIBLE LE PLUS PROCHE DU CLIC NXCL NYCL
      NOARETCL  = 0
      DISTPXMIN = 1E28
      DISTMOEIL = 1E28

C     PARCOURS DES ARETES
C     -------------------
      DO 50 NAR = 1, L2ARET

C        LE NUMERO DES 2 SOMMETS DE L'ARETE NAR
         NS1 = LARETE(1,NAR)
         IF( NS1 .EQ. 0 ) GOTO 50
C        L'ARETE NAR EST INITIALISEE
         NS2 = LARETE(2,NAR)

C        XYZ du BARYCENTRE DE L'ARETE NS1-NS2
         DO K=1,3
            BARY(K) = ( XYZSOM(K,NS1) + XYZSOM(K,NS2) ) / 2
         ENDDO

C        LES COORDONNEES PIXELS de l'ARETE NAR
         IF( NDIMLI .EQ. 2 ) THEN
C           ABSCISSE ORDONNEE PIXELS DE L'ARET
            NXAR = NUPXEX( BARY(1) )
            NYAR = NUPXEY( BARY(2) )
         ELSE
C           COORDONNEES AXONOMETRIQUES de l'ARETE NAR
            CALL XYZAXO( BARY, BARYAXO )
C           ABSCISSE ORDONNEE PIXELS DE L'ARETE
            NXAR = NUPXEX( BARYAXO(1) )
            NYAR = NUPXEY( BARYAXO(2) )
         ENDIF

C        DISTANCE**2 PIXELS ENTRE (NXCL,NYCL) et (NXAR,NYAR) de l'ARETE NAR
         DISTPXAR = (NXCL-NXAR) ** 2 + (NYCL-NYAR) ** 2
         IF( DISTPXAR .GT. 1600 ) THEN
C           CLIC NON DANS LE CERCLE DE RAYON 40 PIXELS
C           POINT CLIQUE TROP ELOIGNE DE L'ARETE NAR DANS LE PLAN des PIXELS
            GOTO 50
         ENDIF

C        L'ARETE NAR EST DANS LE CERCLE DE RAYON 40 PIXELS DE CENTRE NXCL NYCL
         IF( DISTPXAR .LT. DISTPXMIN ) THEN

C           L'ARETE NAR EST LE PLUS PROCHE EN PIXELS DU POINT CLIQUE
            IF( NDIMLI .EQ. 3 ) THEN

C              EN 3D: DISTANCE A L'OEIL DU BARYCENTRE DE L'ARETE NAR
               DISTAROE = DPTVOEIL - BARYAXO(3)

C              L'ARETE NAR EST IL TRES ELOIGNE DE L'OEIL?
C              ET DERRIERE LA PRECEDENTE ARETE PROCHE DU CLIC?
               IF( NOARETCL .GT. 0 ) THEN
C                 IL EXISTE UN ARETE NOARETCL PRECEDENT POINT PROCHE DU CLIC
                  IF( DISTAROE .GE. DISTMOEIL ) THEN
C                    L'ARETE NAR EST TROP LOIN DE L'OEIL
                     GOTO 50
                  ENDIF
               ENDIF

            ENDIF

C           NAR EST LA NOUVELLE ARETE LE PLUS PROCHE DU POINT CLIQUE (NXCL,NYCL)
            NOARETCL  = NAR
            DISTPXMIN = DISTPXAR
            DISTMOEIL = DISTAROE

         ENDIF

 50   ENDDO

      RETURN
      END
