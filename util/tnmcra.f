      SUBROUTINE TNMCRA( KTYPE , NBVAR0 , NBVAR1 , MNTABL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RACOURCIR UN TABLEAU MC DE NBVAR0 VARIABLES DE NUMERO DE TYPE
C ----- KTYPE A NBVAR1 VARIABLES
C
C ENTREES :
C ---------
C KTYPE  : NOM DU TYPE DES VARIABLES DU TABLEAU
C          LOGIQUE  ENTIER/2  ENTIER  REEL REEL2 REEL4
C          COMPLEXE  COMPLEXE2
C NBVAR0 : NOMBRE DE VARIABLES DU TABLEAU MC AVANT
C NBVAR1 : NOMBRE DE VARIABLES DU TABLEAU MC APRES
C MNTABL : ADRESSE MC DU 1-ER MOT DU TABLEAU MC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/motmcg.inc"
      CHARACTER*(*)     KTYPE
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
C
C     LA DIMINUTION PROPREMENT DITE DU TABLEAU
      CALL TAMCRA( KTYPE , NBVAR0 , MCG(MGZLMN) , MNTABL , NBVAR1 )
C
      RETURN
      END
