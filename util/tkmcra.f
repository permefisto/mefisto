      SUBROUTINE TKMCRA( NBCACH , NBCHA0 , NBCHA1 , MKTABL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RACOURCIR UN TABLEAU DE NBCHA0 CHAINES DE NBCACH CARACTERES A
C ----- NBCHA1 CHAINES DE NBCACH CARACTERES
C
C ENTREES :
C ---------
C NBCACH : NOMBRE DE CARACTERES PAR CHAINE
C NBCHA0 : NOMBRE DE CHAINES DE NBCACH CARACTERES DU TABLEAU EN ENTREE
C NBCHA1 : NOMBRE DE CHAINES DE NBCACH CARACTERES DU TABLEAU EN SORTIE
C
C SORTIE  :
C ---------
C MKTABL : ADRESSE MCK DU 1-ER MOT DU TABLEAU MC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/motmcg.inc"
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
C
C     LE NOMBRE DE CARACTERES REELS DU TABLEAU
      NBC0 = NBCATA( NBCACH , NBCHA0 )
      NBC1 = NBCATA( NBCACH , NBCHA1 )
C
C     LA DIMINUTION PROPREMENT DITE DU TABLEAU
      CALL TAMCRA( 'CARACTERE' , NBC0 ,  MCG(MGZLMK) ,  MKTABL , NBC1 )
      END
