      SUBROUTINE OUVRMS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : OUVRIR LA MEMOIRE SECONDAIRE
C -----       LE LEXIQUE ADAM 'A'  ( NOTRE PERE A TOUS |)
C             LE NOM 'POINTS       ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C             LE NOM 'LIGNES       ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C             LE NOM 'SURFACES     ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C             LE NOM 'VOLUMES      ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C             LE NOM 'OBJETS       ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C             LE NOM 'FONCTIONS    ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C             LE NOM 'TRANSFO      ' DANS LE LEXIQUE ADAM ( LEXIQUE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS        MARS 1990
C2345X7..............................................................012
      include"./incl/pp.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/msvaau.inc"
      include"./incl/xyzext.inc"
      REAL              RMCN(MOTMCN)
      COMMON             MCN(MOTMCN)
      EQUIVALENCE       (MCN(1),RMCN(1))
      COMMON / MSSFTA / MSSF(28),NTADAM
C
C     OUVERTURE DE LA MEMOIRE SECONDAIRE DECLAREE AUPARAVANT
C     ======================================================
C     NOMBRE MAXIMAL DE ZONES LIBRES ENTRE LES TABLEAUX DE MCG MCK MCN
      M2ZLMG =  96
      M2ZLMK =  32
      M2ZLMN = 1024
      MOPASF = 128
      CALL MSOU( MOPASF, M2ZLMG, M2ZLMK, M2ZLMN, MOTMCN )
C
C     OUVERTURE DU FICHIER SAUVEGARDE DE LA FRAPPE
      CALL FRAPPE
C
C     PROTECTION EN CAS D'ERREUR DES LEXIQUES QUI SUIVENT
C     POUR LES DECLARER AU FOND DU SUPER-TABLEAU MCN
      N = MOTMCN / 2**16
      CALL TNMCDC( 'MOTS', (N-1)*(2**16), MCAUX )
C
C     OUVERTURE DU LEXIQUE ADAM
      CALL TAMSOU( NTADAM, MNADAM )
C
C     OUVERTURE DES POINTS
      CALL LXLXOU( NTADAM, 'POINT', NTPOIN, MNPOIN )
C
C     OUVERTURE DES LIGNES
      CALL LXLXOU( NTADAM, 'LIGNE', NTLIGN, MNLIGN )
C
C     OUVERTURE DES SURFACES
      CALL LXLXOU( NTADAM, 'SURFACE', NTSURF, MNSURF )
C
C     OUVERTURE DES VOLUMES
      CALL LXLXOU( NTADAM, 'VOLUME', NTVOLU, MNVOLU )
C
C     OUVERTURE DES OBJETS
      CALL LXLXOU( NTADAM, 'OBJET', NTOBJE, MNOBJE )
C
C     OUVERTURE DES FONCTIONS
      CALL LXLXOU( NTADAM, 'FONCTION', NTFONC, MNFONC )
C
C     OUVERTURE DES TRANSFORMATIONS
      CALL LXLXOU( NTADAM, 'TRANSFO', NTTRAN, MNTRAN )
C
C     OUVERTURE DES COORDONNEES EXTREMALES
C     COMMON / XYZEXT / COOEXT(6,2), XYZAMPLI(4), INIEXT, MOAXYZ
      CALL LXTSOU( NTADAM, 'MIN_MAX_SOMMETS', NTCOEX, MNCOEX )
      CALL TRTATA( RMCN(MNCOEX), COOEXT, 6*2+2+4 )
C
C     FIN DE LA PROTECTION EN CAS D'ERREUR DES LEXIQUES QUI SUIVENT
      CALL TNMCDS( 'MOTS', (N-1)*(2**16), MCAUX )
C
      RETURN
      END
