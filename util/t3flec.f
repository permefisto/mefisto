      SUBROUTINE T3FLEC( NOCOUL, XYZ, DISTCM, DXYZCM )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACE UNE FLECHE PARTANT DE XYZ DANS LA DIRECTION DXYZCM
C -----    DE LONGUEUR DISTCM AVEC UN TRIANGLE EQUILATERAL DANS LE
C          SENS USUEL SI DISTCM > 0 , DANS L AUTRE SENS SI DISTCM < 0
C
C ENTREES :
C ---------
C NOCOUL : NUMERO DE LA COULEUR DE LA FLECHE A TRACER
C XYZ    : COORDONNEES OBJET DU POINT DE DEPART DE LA FLECHE
C DISTCM : LONGUEUR DE LA FLECHE . SON SIGNE DETERMINE LE SENS DU
C          TRIANGLE DE LA FLECHE
C DXYZCM : X Y Z EN CM DE LA DIRECTION DE LA FLECHE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS         MAI 1999
C23456---------------------------------------------------------------012
      include"./incl/minint.inc"
      include"./incl/mecoit.inc"
      include"./incl/trvari.inc"
      REAL       XYZ(3), DXYZCM(3), XYZP(3), AXYZ(3), AXYZP(3)
      REAL       XS(3),YS(3)
      INTEGER*2  XYSF(2,3)
      INTRINSIC  INT2, NINT
C
C     SI LA TAILLE DE LA FLECHE EST < 0.1 CM ELLE N EST PAS TRACEE
      IF( ABS( DISTCM ) .LT. 0.1 ) RETURN
C
C     LE NOMBRE DE CM DE LA LARGEUR DE LA FENETRE
      R = LAPXFE * 0.1 / CXMMPX
C
C     1 CM DE LARGEUR SUR L'ECRAN REPRESENTE R UNITES OBJETS
      R = ( XOBMAX - XOBMIN ) / R
C
C     TRANSFORMATION DES CM EN COORDONNEES OBJET
C     ET CALCUL DE XYZ DE LA POINTE DE LA FLECHE
      DO 10 I=1,3
         XYZP(I) = XYZ(I) + DXYZCM(I) * R
 10   CONTINUE
C
C     TRANSFORMATION EN COORDONNEES AXONOMETRIQUES DES 2 SOMMETS
      CALL XYZAXO( XYZ , AXYZ  )
      CALL XYZAXO( XYZP, AXYZP )
C
C     SI LE POINT EST EXTERIEUR AUX 2 PLANS IL N'EST PAS TRACE
      IF( AXOARR .NE. 0 .OR. AXOAVA .NE. 0 ) THEN
C        AXOARR ET AXOAVA SONT ACTIFS
         IF( AXYZ(3) .LT. AXOARR ) RETURN
         IF( AXYZ(3) .GT. AXOAVA ) RETURN
      ENDIF
C
C     TRANSFORMATION EN PIXELS DANS LA FENETRE XV
      NX = NUPXEX( AXYZ(1) )
      NY = NUPXEY( AXYZ(2) )
C     SI LE NUMERO PIXEL EST INCORRECT ABANDON DU TRACE DE LA FACE
      IF( NX .EQ. MININT .OR. NY .EQ. MININT ) RETURN
C
C     LA POINTE DE LA FLECHE
      NXP = NUPXEX( AXYZP(1) )
      NYP = NUPXEY( AXYZP(2) )
C     SI LE NUMERO PIXEL EST INCORRECT ABANDON DU TRACE DE LA FACE
      IF( NXP .EQ. MININT .OR. NYP .EQ. MININT ) RETURN
C
C     LE VECTEUR FLECHE EN COORDONNEES PIXELS MAIS AVEC DES REELS
      X  = NXP - NX
      Y  = NYP - NY
      D  = SQRT( X*X + Y*Y )
      ND = NINT( D )
      IF( ND .LE. 0 ) RETURN
C
C     TRACE DE LA FLECHE SANS LE TRIANGLE TERMINAL
      CALL XVEPAISSEUR( NEPFLE )
      CALL XVCOULEUR( NOCOUL )
      CALL XVTRAIT( NX, NY, NXP, NYP )
C
C     VECTEUR NORMAL A LA FLECHE EN COORDONNEES PIXELS
      XN = - Y / D
      YN =   X / D
C
C     POINT INTERSECTION DE LA FLECHE ET DU TRIANGLE
      XS(1) = NX + X * 0.75
      YS(1) = NY + Y * 0.75
C
C     DEMI-LONGUEUR DU COTE DU TRIANGLE EQUILATERAL DE LA FLECHE
      D = D * 0.06
      IF( NEPFLE .GT. 1 ) D = D * NEPFLE * 0.75
C
C     LE TRIANGLE DE LA FLECHE EST AIGUISE
      D = D * 0.75

C     TRACE DU TRIANGLE TERMINAL DE LA FLECHE
      IF( DISTCM .GT. 0 ) THEN
C
C        TRACTION ELONGATION ---|> COULEUR NOCOUL
         XS(2) = XS(1) + XN * D
         YS(2) = YS(1) + YN * D
C
         XS(3) = XS(1) - XN * D
         YS(3) = YS(1) - YN * D
C
C        TRACE DU TRIANGLE TERMINAL
         XYSF(1,1) = INT2( NXP )
         XYSF(2,1) = INT2( NYP )
         XYSF(1,2) = INT2( NINT( XS(2) ) )
         XYSF(2,2) = INT2( NINT( YS(2) ) )
         XYSF(1,3) = INT2( NINT( XS(3) ) )
         XYSF(2,3) = INT2( NINT( YS(3) ) )
         CALL XVFACE( 3, XYSF )

         CALL XVCOULEUR( NCNOIR )
         CALL XVEPAISSEUR( 0 )
         CALL XVTRAIT( NXP,   NYP, NINT(XS(2)),NINT(YS(2)) )
         CALL XVTRAIT( NINT(XS(2)),NINT(YS(2)),NINT(XS(3)),NINT(YS(3)) )
         CALL XVTRAIT( NINT(XS(3)),NINT(YS(3)),NXP,   NYP   )
C
      ELSE
C
C        COMPRESSION ---<| EN ROUGE
         XS(2) = NXP + XN * D
         YS(2) = NYP + YN * D
C
         XS(3) = NXP - XN * D
         YS(3) = NYP - YN * D
C
C        TRACE DU TRIANGLE TERMINAL
         CALL XVCOULEUR( NCROUG )
         XYSF(1,1) = INT2( NINT( XS(1) ) )
         XYSF(2,1) = INT2( NINT( YS(1) ) )
         XYSF(1,2) = INT2( NINT( XS(2) ) )
         XYSF(2,2) = INT2( NINT( YS(2) ) )
         XYSF(1,3) = INT2( NINT( XS(3) ) )
         XYSF(2,3) = INT2( NINT( YS(3) ) )
         CALL XVFACE( 3, XYSF )
C
ccc         CALL XVCOULEUR( NCGRIS )
         CALL XVCOULEUR( NCNOIR )
         CALL XVEPAISSEUR( 0 )
         CALL XVTRAIT( NINT(XS(2)),NINT(YS(2)),NINT(XS(3)),NINT(YS(3)) )
         CALL XVTRAIT( NINT(XS(3)),NINT(YS(3)),NINT(XS(1)),NINT(YS(1)) )
         CALL XVTRAIT( NINT(XS(1)),NINT(YS(1)),NINT(XS(2)),NINT(YS(2)) )
C
      ENDIF
C
      RETURN
      END
