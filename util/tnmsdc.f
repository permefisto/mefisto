      SUBROUTINE TNMSDC( KTYPE , NBVARI , NOTAMS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : DECLARER UN TABLEAU MS DE NUMERO DE TYPE KTYPE
C ----- ET DE NBVARI VARIABLES
C
C ENTREES :
C ---------
C KTYPE  : NOM DU TYPE DES VARIABLES DU TABLEAU
C          LOGIQUE  CARACTERE  ENTIER/2  ENTIER  REEL  REEL2  REEL4
C          COMPLEXE  COMPLEXE2
C NBVARI : NOMBRE DE VARIABLES DU TABLEAU MS A DECLARER
C
C SORTIE :
C --------
C NOTAMS : NUMERO DU TABLEAU MS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  DECEMBRE 1983
C.......................................................................
      include"./incl/motmcg.inc"
      include"./incl/msvaau.inc"
      include"./incl/ppmck.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / MSSFTA / NOFISF,NBPASF,MOPASF,MGBUSF,NSFLIB,
     %                  M1FIMS,M2FIMS,MGFIMS,NSFIMS,LPFIMS,
     %                  M1TAMS,M2TAMS,MGBUTA,NBBUTA,NPTAMS,NATAMS,
     %                  NBCTMS,LLTAMS,LFTAMS,MGNPSF,NSFNPS,NPSNPS,
     %                  MGZLMG,MGZLMK,MGZLMN,MOTSMG,MOTSMK,MOTSMN,NTADAM
      COMMON / MSIMTA / NOIMPR
      CHARACTER*(*)     KTYPE
      INTEGER           PILE(2,30)
      CHARACTER*16      ERREUR
      DATA              ERREUR / 'ERREUR TNMSDC :' /
C
C     LE PREMIER TABLEAU LIBRE DU REPERTOIRE A POUR NUMERO LLTAMS
C     LE REPERTOIRE RETAMS EST-IL SATURE?
      IF( LLTAMS .LE. 0 ) THEN
C
C     OUI:ERREUR.ARRET
         WRITE(IMPRIM,10000) ERREUR
10000 FORMAT(A16,'REPERTOIRE DES TABLEAUX DE LA MS SATURE'/)
         CALL ARRET(100)
      ENDIF
C
C     NON:MISE A JOUR DU NUMERO DU PREMIER TABLEAU LIBRE=>SUIVANT
      NOTAMS = LLTAMS
      CALL TAMSRE( NOTAMS , MGTAMS )
C
C     MCG(MGTAMS+0..5) = RETAMS(1..6) SOIT PLUS PRECISEMENT
C     RETAMS(1)=NTTAMS NO DU TYPE DU TABLEAU MS
C                      0 SI LE TABLEAU N EST PAS DECLARE
C     RETAMS(2)=NVTAMS NOMBRE DE VARIABLES DU TABLEAU MS
C     RETAMS(3)=NFTAMS NUMERO DU FICHIER MS (1 A M2FIMS) DU TABLEAU MS
C                      0 SI NON DECLARE
C     RETAMS(4)=N1TAMS NUMERO DE LA PREMIERE PAGE DU TABLEAU SUR
C                      LE FICHIER NFTAMS QUI LE SAUVEGARDE
C     RETAMS(5)=MCTAMS ADRESSE MC DU PREMIER MOT DU TABLEAU MS
C                      >0 LE TABLEAU EST OUVERT( VERROUILLE ) EN MC
C                         IL EST PRET A ETRE UTILISE
C                         IL NE PEUT ETRE SAUVEGARDE SUR SON FICHIER
C                         TANT QU IL N EST PAS FERME
C                      =0 LE TABLEAU N EST PAS EN MC
C                         IL EST DECLARE MAIS PAS OUVERT
C                      <0 LE TABLEAU EST FERME,SUSCEPTIBLE
C                         D ETRE SAUVEGARDE SUR SON FICHIER MS
C                         IL EST UTILISABLE SEULEMENT S IL EST REOUVERT
C     RETAMS(6)=LSTAMS CHAINAGE AVAL DES TABLEAUX MS LIBRES OU FERMES
C
      LLTAMS = MCG(MGTAMS+5)
C
C     RECHERCHE DU FICHIER DEVANT CONTENIR CE TABLEAU SELON L'ALGORITHME
C     FICHIER DONT LA TAILLE DES PAGES MINIMISE LE NOMBRE DE PAGES
C     NECESSAIRES POUR STOCKER LE TABLEAU ET EN CAS D'EGALITE CELUI
C     DE PLUS FAIBLE RESTE DANS LA DERNIERE PAGE
C     ==================================================================
C
C     LE NOMBRE DE MOTS DU TABLEAU
      NUMTYV = NUMTYP( KTYPE  )
      MOTABL = MOTTAB( NUMTYV , NBVARI )
C
C     SI LE FICHIER SE SATURE EN COURS D ADDITION DE CE TABLEAU
C     ALORS CE FICHIER EST EMPILE
C
      LPILE  = 0
 20   MINFIL = 0
      MINPAG = IINFO( 'GRAND' )
      MINPER = MINPAG
      IGF    = MGFIMS
      DO 30 I=1,M2FIMS
C
C        LE FICHIER EST-IL DECLARE?
         IF( MCG( IGF ) .LE. 0 ) GOTO 25
C
C        OUI:LE FICHIER EST-IL SATURE?
         IF( MCG(IGF+6) .EQ. 0 ) GOTO 25
C
C        NON:CALCUL DU NOMBRE DE PAGES OCCUPEES ET DU RESTE PERDU
         CALL NBPATA(MOTABL , MCG(IGF+2) , NBPAG , MOPERD)
C
C        LE NOMBRE DE PAGES POUR STOCKER LE TABLEAU EST-IL MINIMAL?
         IF( NBPAG .LT. MINPAG ) THEN
C           OUI:CE FICHIER REMPLACE LE PRECEDENT
            MINPAG = NBPAG
            MINFIL = I
            MINPER = MOPERD
C        NON:MAIS EST-IL EGAL AU MINIMUM ?
         ELSEIF( NBPAG .EQ. MINPAG ) THEN
C           OUI:.SI LE RESTE EST PLUS PETIT CE FICHIER REMPLACE
C                LE PRECEDENT
            IF( MOPERD .LT. MINPER ) THEN
               MINFIL = I
               MINPER = MOPERD
            ENDIF
         ENDIF
 25      IGF = IGF + M1FIMS
 30   CONTINUE
C
C     LES FICHIERS SONT ILS TOUS SATURES?
C     -----------------------------------
      IF( MINFIL .EQ. 0 ) THEN
C        OUI:ERREUR ET ARRET
         WRITE(IMPRIM,10030) ERREUR
         CALL ARRET(100)
10030 FORMAT(A16,' TOUS LES FICHIERS DE LA MS SONT SATURES'/)
      ENDIF
C
C     NON:REALISATION DU CHAINAGE ENTRE LES PAGES DU TABLEAU
C         POUR CELA LE REPERTOIRE DES PAGES LIBRES OU OCCUPEES DE CE
C         FICHIER EST EXAMINE (RED1FI)
      IGFMIN = MGFIMS + M1FIMS * ( MINFIL - 1 )
      L1     = MCG( IGFMIN + 6 )
      N1TAMS = L1
C
      DO 40 I=1,MINPAG
C
C        SI L1=0 LE FICHIER S EST SATURE AVEC CE TABLEAU
         IF( L1 .EQ. 0 ) THEN
C           WRITE(IMPRIM,10040) MINFIL
C10040 FORMAT(' FICHIER MS',I5,' SATURE'/)
C           LE FICHIER EST EMPILE
            LPILE = LPILE + 1
C           PROTECTION DU NUMERO DE FICHIER MS
            PILE(1,LPILE) = MINFIL
C           PROTECTION DU NO FICHIER SYSTEME
            PILE(2,LPILE) = MCG( IGFMIN )
C           LE NUMERO DU FICHIER SYSTEME EST ECRASE
            MCG( IGFMIN ) = 0
            GOTO 20
C
         ELSE
C
C           PASSAGE A LA PAGE SUIVANTE
            CALL FIPASU( L1 , MCG(IGFMIN) , IGS )
            L1 = MCG( IGS )
         ENDIF
 40   CONTINUE
C
C     LE FICHIER OPTIMAL EST TROUVE
C     SA PREMIERE PAGE LIBRE EST MISE A JOUR
      MCG( IGFMIN + 6 ) = L1
C
C     C'EST LA DERNIERE DES PAGES DU TABLEAU ( RED1FI( L ) <- 0 )
      MCG( IGS ) = 0
C
C     MISE A JOUR DU NUMERO DES FICHIERS SYSTEMES MOMENTANEMENT SATURES
      DO 60 I=1,LPILE
         IGF = MGFIMS + M1FIMS * ( PILE(1,I) - 1 )
         MCG( IGF ) = PILE( 2 , I )
 60   CONTINUE
C
C     LE REPERTOIRE DU TABLEAU MS EST MIS A JOUR
C     LE NUMERO DU TYPE EST NEGATIF POUR INDIQUER QUE LE TABLEAU NE
C     DOIT PAS ETRE LU SUR LE FICHIER A SA PREMIERE OUVERTURE
      MCG(MGTAMS    ) = - NUMTYV
      MCG(MGTAMS + 1) = NBVARI
      MCG(MGTAMS + 2) = MINFIL
      MCG(MGTAMS + 3) = N1TAMS
      MCG(MGTAMS + 4) = 0
      MCG(MGTAMS + 5) = 0
C
      IF( NOIMPR .NE. 0 ) WRITE(IMPRIM,19000) NBVARI,KTYPE,NOTAMS
19000 FORMAT(' DECLARATION TABLEAU MS DE',I9,A10,
     %' => NUMERO',I13)
C
      RETURN
      END
