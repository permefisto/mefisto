      SUBROUTINE QUADCXD( XYZP1, XYZP2, XYZP3, XYZP4, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :      LE QUADRANGLE XYZP1 XYZP2 XYZP3 XYZP4 EST IL CONVEXE ?
C -----      LES 2 DECOUPAGES EN 2 TRIANGLES SONT ILS POSSIBLES?

C ENTREES:
C --------
C XYZP1,XYZP2,XYZP3,XYZP4 : 3 COORDONNEES DES 4 SOMMETS DU QUADRANGLE

C SORTIE :
C --------
C NONOUI : 1 LE QUADRANGLE EST CONVEXE
C          0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS UPMC     FEVRIER 1993
C2345X7..............................................................012
      DOUBLE PRECISION  XYZP1(3), XYZP2(3), XYZP3(3), XYZP4(3)
      DOUBLE PRECISION  SURTRD, S0, S1, S01, S2, S3, S23, SMIN

C     LES 2 CHOIX POSSIBLES DE TRIANGULATIONS ONT ELLES DES
C     SURFACES EGALES ?
      S0  = SURTRD( XYZP1, XYZP3, XYZP2 )
      S1  = SURTRD( XYZP1, XYZP3, XYZP4 )
      S01 = S0 + S1

      S2  = SURTRD( XYZP2, XYZP4, XYZP1 )
      S3  = SURTRD( XYZP2, XYZP4, XYZP3 )
      S23 = S2 + S3

C     UN DOUTE SUBSISTE SI L'UN DES TRIANGLES EST DE SURFACE FAIBLE
      SMIN = MIN( S0, S1, S2, S3 )
ccc      IF( SMIN .LT. (S01+S23) * 0.5D-3 ) GOTO 9000    2020/03/03
      IF( SMIN .LT. (S01+S23) * 1D-3 ) GOTO 9000

C     LE TRIANGLE DE SURFACE MINIMALE N'EST PAS DEGENERE
C     SI QUADRANGLE NON CONVEXE ALORS ABS(S23-S01) = 2 MIN(S0,S1,S2,S3)
      IF( ABS( S23 - S01 ) .GT. SMIN ) GOTO 9000

C     QUADRANGLE CONVEXE
      NONOUI = 1
      GOTO 9999

C     QUADRANGLE NON CONVEXE
 9000 NONOUI = 0

 9999 RETURN
      END
