      SUBROUTINE FIMOEC( NOFISY , NOPAGE , MOPAGE , NBMOTS , PAGE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : ECRIRE LES NBMOTS PREMIERS MOTS DU TABLEAU PAGE DANS LA PAGE
C ----- NOPAGE DU FICHIER NOFISY
C
C ENTREES :
C ---------
C NOFISY : NUMERO SYSTEME DU FICHIER D ACCES DIRECT
C NOPAGE : NUMERO DE LA PAGE A LIRE SUR CE FICHIER
C MOPAGE : NOMBRE DE MOTS PAR PAGE DE CE FICHIER
C PAGE   : TABLEAU ECRIT DANS LA PAGE NOPAGE DU FICHIER NOFISY
C NBMOTS : NOMBRE DE MOTS A ECRIRE SUR LE FICHIER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS  NOVEMBRE 1989
C.......................................................................
      INTEGER PAGE(MOPAGE)
C
C     ECRITURE SUR LE FICHIER NOFISY DE LA PAGE NOPAGE
      WRITE( UNIT=NOFISY,REC=NOPAGE ) (PAGE(I),I=1,NBMOTS)
C
      RETURN
      END
