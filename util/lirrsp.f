      SUBROUTINE LIRRSP( NCVALS , REEL )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : LIRE UNE CHAINE DE CARACTERES REPRESENTANT UN REEL TERMINE PAR ;
C ----- RETOURNER LA VALEUR DU REEL EN SIMPLE PRECISION
C
C       LA DEMANDE EST TOUJOURS SATISFAITE CAR PAS DE RETOUR TANT QUE
C       LA VALEUR DU REEL EST INCORRECTE  SAUF @ DEMANDANT L'ABANDON
C
C       CF ~/td/g/grammaire_lu DE DEFINITION DU LANGAGE UTILISATEUR
C
C ENTREE ET SORTIE :
C ------------------
C NCVALS : EN ENTREE :  5 LE REEL EST INITIALISE EN ENTREE
C                       0 SINON
C          EN SORTIE :  1 LE REEL EST INITIALISE
C                      -1 ABANDON DE LA LECTURE PAR ENTREE DE @
C
C SORTIE :
C --------
C REEL   : LA VALEUR DU REEL LU
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS    JANVIER 1990
C23456---------------------------------------------------------------012
      include"./incl/td.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      DOUBLE PRECISION  D
      CHARACTER*1       KCHAIN
      INTRINSIC         REAL
C
C     DEMANDE DE LECTURE D'UNE CHAINE DE CARACTERES
      IF( NCVALS .NE. 0 ) THEN
         NOTYP = 5
         D     = REEL
      ELSE
         NOTYP = 0
      ENDIF
C
CCC 1    IF( INTERA .GE. 3 ) THEN
CCCC
CCCC        LE TEXTE EN BOUT DE LA DERNIERE LIGNE
CCC         IF( NBLGRC(NRINVI) .LT. MXLGIN ) THEN
CCC            NBLGRC(NRINVI) = NBLGRC(NRINVI) + 1
CCC         ENDIF
CCC         IF( NUIDEN .GT. 0 .AND. NUIDEN .LE. MXIDEN ) THEN
CCCC           AJOUT DE L'IDENTIFICATEUR DE VALEUR DEMANDEE
CCC            I = NUDCNB( KIDENT(NUIDEN) )
CCC            KINVI(NBLGRC(NRINVI)) = 'LE REEL SIMPLE ' //
CCC     %                               KIDENT(NUIDEN)(1:I) // '=?'
CCC         ELSE
CCC            KINVI(NBLGRC(NRINVI)) = 'LE REEL SIMPLE=?'
CCC         ENDIF
CCC         MDLGRC(NRINVI) = MXCANB( NBLGRC(NRINVI) , KINVI )
CCC         MDRECT(NRINVI) = MDLGRC(NRINVI)
CCC      ENDIF
C
C     LECTURE OU INTERPRETATION DIRECTE
 10   CALL DONNMF( NOTYP , 5 , NCVALS , D , KCHAIN )
      IF( NCVALS .EQ. -1 ) GOTO 9000
      IF( NCVALS .NE.  1 ) GOTO 10
C
C     TRANSFORMATION DU REEL DOUBLE PRECISION EN REEL SIMPLE
      REEL = REAL( D )
C
C     INVITE EFFACEE
 9000 NBLGRC( NRINVI ) = 0
C
CCC 8000 CALL CLAVIS
      RETURN
      END
