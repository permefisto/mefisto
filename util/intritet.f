      SUBROUTINE INTRITET( S1TR, S2TR, S3TR,  S1TE, S2TE, S3TE, S4TE,
     %                     LINTER )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    L'INTERSECTION D'UN TRIANGLE ET D'UN TETRAEDRE EST ELLE VIDE?
C -----  I.E. AUCUNE ARETE DU TRIANGLE INTERSECTE LES FACES DU TETRAEDRE
C          +  AUCUNE ARETE DU TETRAEDRE INTERSECTE LE TRIANGLE?
C          +  AUCUN DES 3 SOMMETS DU TRIANGLE EST INTERNE AU TETRAEDRE?
C          VERSION XYZ SOMMETS EN DOUBLE PRECISION
C ENTREES:
C --------
C S1TR, S2TR, S3TR       : 3 COORDONNEES DES 3 SOMMETS DU TRIANGLE
C S1TE, S2TE, S3TE, S4TE : 3 COORDONNEES DES 4 SOMMETS DU TETRAEDRE
C
C SORTIE :
C --------
C LINTER : 0 PAS D'INTERSECTION
C          1 SI UNE ARETE DU TRIANGLE INTERSECTE UNE FACE DU TETRAEDRE
C          2 SI LE TRIANGLE EST INTERSECTE PAR UNE ARETE DU TETRAEDRE
C          3 SI UN DES SOMMETS DU TRIANGLE EST STRICTEMENT INTERNE
C            AU TETRAEDRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY  DECEMBRE 2014
C23456...............................................................012
      DOUBLE PRECISION  S1TR(3), S2TR(3), S3TR(3),
     %                  S1TE(3), S2TE(3), S3TE(3), S4TE(3),
     %                  STR(3,3), STE(3,4)

C     LES 3 COORDONNEES DES 3 SOMMETS DU TRIANGLE
      DO L=1,3
         STR(L,1) = S1TR(L)
         STR(L,2) = S2TR(L)
         STR(L,3) = S3TR(L)
      ENDDO

C     LES 3 COORDONNEES DES 4 SOMMETS DU TETRAEDRE
      DO L=1,3
         STE(L,1) = S1TE(L)
         STE(L,2) = S2TE(L)
         STE(L,3) = S3TE(L)
         STE(L,4) = S4TE(L)
      ENDDO

C     INTERSECTION TRIANGLE NF2-TETRAEDRE?
      CALL INTRTE( STR, STE, LINTER )

      RETURN
      END
