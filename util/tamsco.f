      SUBROUTINE TAMSCO( NOTAMS , NOTACO , MCTACO )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    COPIER UN TABLEAU MS DANS UN AUTRE ET OUVRIR CE DERNIER
C -----
C
C ENTREE :
C --------
C NOTAMS : NUMERO DU TABLEAU A COPIER
C          SI LE TABLEAU EST OUVERT EN ENTREE IL RESTE OUVERT EN SORTIE
C          SI LE TABLEAU EST FERME  EN ENTREE IL RESTE FERME  EN SORTIE
C
C SORTIES :
C ---------
C NOTACO : NUMERO DU TABLEAU COPIE ET OUVERT
C MCTACO : ADRESSE MC DU TABLEAU NOTACO APRES COPIE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR : PERRONNET ALAIN ANALYSE NUMERIQUE PARIS DECEMBRE 1983
C.......................................................................
      include"./incl/ppmck.inc"
      include"./incl/motmcg.inc"
      include"./incl/msvaau.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      COMMON / MSIMTA / NOIMPR
      CHARACTER*9       TYPNUM
C
C     RECHERCHE DU DESCRIPTEUR DU TABLEAU A COPIER
      CALL TAMSRE( NOTAMS , MGTAMS )
C
C     MCTAM0 ADRESSE INITIALE DE CE TABLEAU EN MC => SON ETAT
C            >0 TABLEAU OUVERT; <0 FERME ; =0 NON EN MC
      MCTAM0 = MCG( MGTAMS + 4 )
C
C     OUVERTURE DU TABLEAU A COPIER
      CALL TAMSOU( NOTAMS , MCTAMS )
C
C     MGTAMS ADRESSE MCG DE SON DESCRIPTEUR
      CALL TAMSRE( NOTAMS , MGTAMS )
C
C     SON TYPE ET LE NOMBRE DE SES VARIABLES
      NUMTYV = ABS( MCG(MGTAMS) )
      NBVARI = MCG( MGTAMS + 1  )
C
C     DECLARATION DU TABLEAU MS RECEVEUR DE LA COPIE
      CALL TNMSDC( TYPNUM( NUMTYV ) , NBVARI , NOTACO )
C
C     OUVERTURE DE CE TABLEAU
      CALL TAMSOU( NOTACO , MCTACO )
C
C     LE NOMBRE DE MOTS DU TABLEAU
      MOT = MOTTAB( NUMTYV , NBVARI )
C
C     COPIE EN MC SELON LE TYPE CARACTERE OU NON
      IF( NUMTYV .EQ. NOTYPK ) THEN
C        TABLEAU DE CARACTERES
         CALL TRTKTK( MCK(MCTAMS) , MCK(MCTACO) , MOT )
      ELSE
C        TABLEAU NUMERIQUE
         CALL TRTATA( MCN(MCTAMS) , MCN(MCTACO) , MOT )
      ENDIF
C
C     SI LE TABLEAU A COPIER ETAIT INITIALEMENT FERME OU NON EN MC
C     ALORS IL EST FERME
      IF( MCTAM0 .LE. 0 ) CALL TAMSFE( NOTAMS )
C
      IF(NOIMPR .NE. 0) WRITE(IMPRIM,19999) NOTAMS,NBVARI,NUMTYV,NOTACO
19999 FORMAT(' COPIE DU TABLEAU MS',I9,' DE',I9,' VARIABLES DE TYPE',I3,
     %' DANS LE TABLEAU MS OUVERT',I9)
      RETURN
      END
