      SUBROUTINE TRISOT( NDIM,   KNOMOB, MODECO0,
     %                   NBTYEL, MNELEM, MNPOGE, NDPGST,
     %                   NCAS0,  NCAS1,  NTDL,
     %                   NTYP,   TEMPER, dptemp,
     %                   TMIN,   NOEMIN, NCAMIN, TMAX, NOEMAX, NCAMAX,
     %                   TIMES )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES COURBES OU SURFACES ISOTHERMES DANS UN OBJET
C -----    2D ou 2D AXISYMETRIQUE OU 3D
C
C ENTREES:
C --------
C NOPROJ  : TYPE DE PROJECTION 0 CI-DESSOUS FIXE LA COORDONNEE A ZERO
C          <=0 PAS DE PROJECTION TRAITEMENT en XYZ NORMAL
C           1 : 'X Y Z 0 0 0'
C           2 : 'X Y 0 U 0 0'
C           3 : 'X 0 0 U V 0'
C           4 : '0 0 0 U V W'
C NDIM   : DIMENSION DE L'ESPACE DE L'OBJET (1 OU 2 OU 3)
C KNOMOB : NOM DE L'OBJET
C MODECO : MODE DE TRACE DES VECTEURS DU TMS D'ADRESSE MNDEPL
C         =-11 CE SONT DES TEMPERATURES MAIS PAS DE LECTURE DE MOTS CLES
C              POUR UN TRACE A CHAQUE PAS DE TEMPS DU CACUL
C         =  1 CE SONT DES TEMPERATURES
C         =  2 CE SONT DES MODES PROPRES
C         =  3 CE SONT DES PRESSIONS P1 A PARTIR D'UNE INTERPOLATION
C              SOIT en 2D P2 SOIT P1+BULLE P3 OU en 3D P1 OU en 3D P2
C         =  4 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C         =  5 CAS PARTICULIER DU 1D TRACE DES VECTEURS TEMPERATURES
C         =  6 CE SONT LES NORMES D'UNE VITESSE
C         =  7 FONCTION COURANT D'UN FLUIDE
C         =  8 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C              DU MODULE D'UNE ONDE COMPLEXE
C             (CALCUL DU MODULE DE L'ERREUR COMPLEXE A FAIRE ICI)
C         =  9 CE SONT DES PARTIES REELLES     D'UNE ONDE COMPLEXE
C         = 10 CE SONT DES PARTIES IMAGINAIRES D'UNE ONDE COMPLEXE
C         = 11 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C              DU MODULE DE LA VITESSE D'UN FLUIDE
C         = 12 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C
C NBTYEL : LE NOMBRE DE TYPES D'ELEMENTS FINIS DANS CETTE TOPOLOGIE
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE
C MNELEM : ADRESSE MCN DES TABLEAUX ELEMENTS DE CETTE TOPOLOGIE
C MNPOGE : ADRESSE MCN DU TABLEAU POINTS GEOMETRIQUES DE L'OBJET
C NDPGST : CODE TRAITEMENT DES SOMMETS POINTS NOEUDS DU MAILLAGE
C          0 : NOEUDS=POINTS=SOMMETS
C          1 : NOEUDS=POINTS#SOMMETS
C          2 : NOEUDS#POINTS=SOMMETS
C          3 : NOEUDS#POINTS#SOMMETS

C NCAS0  : NUMERO DU PREMIER CAS A TRACER PARMI LES CAS
C NCAS1  : NUMERO DU DERNIER CAS A TRACER PARMI LES CAS
C NTDL   : NOMBRE TOTAL DE DEGRES DE LIBERTE
C NTYP   : =0 EMPLOI du TABLEAU TEMPER
C          =1 EMPLOI du TABLEAU de POINTEURS dptemp
C TEMPER : TABLEAU DES NTDL * NCAS0:NCAS1 TEMPERATURES
C dptemp : TABLEAU DES NCAS0:NCAS1 TABLEAU(NTDL) TEMPERATURES
C TMIN   : TEMPERATURE MINIMALE DES CAS NCAS0:NCAS1
C NOEMIN : NUMERO DU NOEUD OU LA TEMPERATURE EST MINIMALE
C NCAMIN : NO DU CAS OU LA TEMPERATURE EST MINIMALE
C TMAX   : TEMPERATURE MAXIMALE DES CAS NCAS0:NCAS1
C NOEMAX : NUMERO DU NOEUD OU LA TEMPERATURE EST MAXIMALE
C NCAMAX : NUMERO DU CAS   OU LA TEMPERATURE EST MAXIMALE
C TIMES  : TEMPS DU CALCUL DES VECTEURS TEMPER NCAS0:NCAS1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1994
C MODIFS : ALAIN PERRONNET LJLL UPMC & ST PIERRE DU PERRAY  OCTOBRE 2010
C MODIFS : ALAIN PERRONNET Saint PIERRE du PERRAY              MARS 2021
C MODIFS : ALAIN PERRONNET Saint PIERRE du PERRAY          DECEMBRE 2022
C23456---------------------------------------------------------------012
      PARAMETER     (LIGCON=0, TRIANG=1E25)
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___face.inc"
      include"./incl/a___aretefr.inc"
      include"./incl/ponoel.inc"
      include"./incl/donele.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/ctemps.inc"
      include"./incl/xyzext.inc"
      include"./incl/traaxe.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)

      CHARACTER*(*)     KNOMOB
      CHARACTER*90      KNOM
      CHARACTER*24      NOMFGIF
      REAL              TIMES( NCAS0:NCAS1 )
      DOUBLE PRECISION  TEMPER( NTDL, NCAS0:NCAS1 )
      DOUBLE PRECISION  TMINCAS, TMAXCAS, V
      type typ_dptab
         DOUBLE PRECISION, dimension(:), pointer :: dptab
      end type typ_dptab
      type( typ_dptab ),dimension(NCAS0:NCAS1) :: dptemp

      IF( INTERA .LE. 0 ) RETURN
C     INTERA=0:BATCH      PAS d' ECRAN GRAPHIQUE PAS de CLAVIER PAS de SOURIS
C            1:INTERACTIF AVEC   ECRAN GRAPHIQUE PAS de CLAVIER PAS de SOURIS
C            3:INTERACTIF AVEC   ECRAN GRAPHIQUE AVEC   CLAVIER AVEC   SOURIS

      IF( MODECO0 .EQ. -11 ) THEN
C        DES TEMPERATURES A TRACER SANS LECTURE DE MOTS-CLES
         MODECO = 1
      ELSE
         MODECO = MODECO0
      ENDIF

C     NOM DU FICHIER VIDEO  SELON MODECO // 'isov'
      CALL VIDEONM( MODECO, 'isov', NOMFGIF )

      MOREE2 = MOTVAR(6)
      MNSOLE = 0
      MNPILE = 0
      MOCOPT = 0
      MNCOPT = 0
      MNXYZI = 0
      MNNISO = 0
      MNBARY = 0
      MNNUFA = 0
      NBF    = 0

      IF( NDIM .GE. 3 ) THEN
         NBISO = 5
      ELSE
         NBISO = 10
      ENDIF
      MXISO  = NBISO

      MNVISO = 0
      MNTISO = 0
C
C     NBCOOR = NOMBRE DE COORDONNEES DES POINTS
      NBCOOR = MCN(MNPOGE+WBCOOP )
C
C     NBPOI    NOMBRE DE POINTS DU MAILLAGE DE L'OBJET
      NBPOI  = MCN(MNPOGE+WNBPOI)
C
C     RECTIFICATION DE TMIN EN CAS DE VALEUR 1E-16 et TMAX D'ORDRE>1
C     SINON => PB DE CONVERGENCE DE LA DICHOTOMIE POUR CALCULER LES ISO
      TEMIN = TMIN
      IF( TMAX .GT. 0 .AND. ABS(TMIN) .LT. 1E-6 * TMAX ) TEMIN = 0
C     IDEM SUR LE MAX
      TEMAX = TMAX
      IF( TMIN .LT. 0 .AND. ABS(TMAX) .LT. 1E-6 * ABS(TMIN) ) TEMAX = 0
C
C     LECTURE DES DONNEES
C     ===================
 100  IF( MODECO0 .EQ. -11 ) GOTO 200
      CALL LIMTCL( 'valisoth', NMTCL )
      IF( NMTCL .LT.  0 ) GOTO 9900
      IF( NMTCL .EQ. 19 ) GOTO  190
      GOTO( 110, 120, 130, 140, 150, 160, 170, 180 ) , NMTCL
C
C     NOMBRE D'ISOTHERMES
C     ...................
 110  IF( MNVISO .GT. 0 ) CALL TNMCDS( 'REEL', MXISO+1, MNVISO )
C     DESTRUCTION DU TABLEAU DES VALEURS DES ISOTHERMES
      IF( MNTISO .GT. 0 ) CALL TNMCDS( 'ENTIER', MXISO, MNTISO )
      CALL INVITE( 79 )
      NCVALS = 4
      CALL LIRENT( NCVALS, NBISO )
      IF( NCVALS .LE. 0 ) GOTO 100
      NBISO = ABS( NBISO )
C     AU MOINS UNE ISOVALEUR
      NBISO  = MAX( 1, NBISO )
C     EN 3D AU PLUS 15 ISOVALEURS
      IF( NDIM .EQ. 3 ) NBISO = MIN( 15, NBISO )
      MXISO  = NBISO
      IF( MNVISO .EQ. 0 ) CALL TNMCDC( 'REEL', MXISO+1, MNVISO )
      IF( MNVISO .LE. 0 ) GOTO 9000
      IF( MNTISO .EQ. 0 ) CALL TNMCDC( 'ENTIER', MXISO, MNTISO )
      IF( MNTISO .LE. 0 ) GOTO 9000
C     PAR DEFAUT ISOTHERMES REGULIERES
C
C     REGULIERES
C     ..........
 120  X = ( TEMAX - TEMIN ) / MAX( 1, NBISO )
      IF( X .EQ. 0 ) X = 1.0
CCC      TEMIN = TEMIN + X * 0.5  VALEUR NON INTUITIVE
CCC      TEMAX = TEMAX - X * 0.5
      GOTO 135
C
C     INTERVALLE_MIN_MAX_DES_ISOS
C     ...........................
 130  NCVALS = 0
      CALL INVITE( 32 )
      CALL LIRRSP( NCVALS, TEMIN )
      IF( NCVALS .LE. 0 ) GOTO 100
      CALL INVITE( 30 )
      NCVALS = 0
      CALL LIRRSP( NCVALS, TEMAX )
      IF( NCVALS .LE. 0    ) GOTO 100
      IF( TEMIN  .GT. TEMAX ) GOTO 130
C
 135  NBISO = MAX( NBISO, 1 )
      IF( NBISO .EQ. 1 ) THEN
C        LE MILIEU DE L'INTERVALLE EST CHOISI
         RMCN(MNVISO) = (TEMIN + TEMAX) * 0.5
         GOTO 139
      ENDIF
C
cccC     SI TEMIN EST LA TEMPERATURE MINIMALE OU
cccC     SI TEMAX EST LA TEMPERATURE MAXIMALE
cccC     DEUX ISO-VALEURS SONT AJOUTEES
cccC     POUR EVITER DE TRACE UNE ISO REDUITE A UN POINT NON VISIBLE
ccc      IF( TEMIN .EQ. TMIN .OR. TEMAX .EQ. TMAX ) NBISO = NBISO + 2
C
C     ECART ENTRE ISOS
      X = ( TEMAX - TEMIN ) / ( NBISO - 1 )
      F = TEMIN

      DO I=1,NBISO-1
         RMCN( MNVISO - 1 + I ) = F
         F                      = F + X
      ENDDO
      RMCN( MNVISO - 1 + NBISO ) = TEMAX
C
 139  IF( NMTCL .EQ. 1 ) GOTO 100
      GOTO 215
C
C     VALEURS_DES_ISOS
C     ................
 140  IF( LANGAG .EQ. 0 ) THEN
         KNOM = 'VALEUR de l''ISOTHERME '
      ELSE
         KNOM = 'ISO-VALUE             '
      ENDIF
      DO 148 I=1,NBISO
         WRITE( KNOM(23:28), '(I6)' ) I
         CALL INVITD( KNOM )
         NCVALS = 0
         CALL LIRRSP( NCVALS, RMCN(MNVISO-1+I) )
         IF( NCVALS .LE. 0 ) GOTO 100
 148  ENDDO
      CALL TRIREE( NBISO, RMCN(MNVISO) )
      GOTO 200
C
C     COULEUR des ARETES du MAILLAGE
C     ..............................
 150  CALL LIMTCL( 'couleur0' , I )
      IF( I .EQ. -1 ) THEN
         GOTO 100
      ELSE IF( I .EQ. -2 ) THEN
         NCOUAF = -2
      ELSE IF( I .EQ. 0 ) THEN
C        COULEUR NOIRE
         NCOUAF = 0
      ELSE
C        COULEUR RESERVEE
         NCOUAF = N1COEL + I
      ENDIF
      GOTO 100
C
C     TYPE du TRAIT des ARETES du MAILLAGE
C     ....................................
 160  CALL LIMTCL( 'typtrait' , I )
      IF( I .EQ. -1 ) GOTO 100
      NTLAFR = I
      GOTO 100
C
C     COULEUR des ARETES sur les SURFACES ISOTHERMES
C     ..............................................
 170  CALL LIMTCL( 'couleur0' , I )
      IF( I .EQ. -1 ) GOTO 100
      IF( I .EQ. -2 ) THEN
C        COULEUR INVISIBLE A NE PAS TRACER
         NCOAPL = -2
      ELSE IF( I .EQ. 0 ) THEN
C        LA COULEUR NOIRE
         NCOAPL = 0
      ELSE
         NCOAPL = N1COEL + I
      ENDIF
      GOTO 100
C
C     TYPE du TRAIT des ARETES des SURFACES ISOTHERMES
C     ................................................
 180  CALL LIMTCL( 'typtrait' , I )
      IF( I .EQ. -1 ) GOTO 100
      NTLAPL = I
      GOTO 100
C
C     POURCENTAGE de REDUCTION des FACES
C     ..................................
 190  CALL INVITE( 130 )
      CALL LIRRSP( NCVALS , R )
      IF( NCVALS .LE. 0 ) GOTO 100
      PREDUF = R
      PREDUF = MIN( 100.0 , PREDUF )
      PREDUF = MAX(   0.0 , PREDUF )
      NBLGRC(NRERR) = 1
      WRITE(KERR(MXLGER)(1:10),'(G10.2)' ) PREDUF
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1) = '% de REDUCTION des FACES= ' // KERR(MXLGER)(1:10)
      ELSE
         KERR(1) = '% of REDUCTION of FACES= ' // KERR(MXLGER)(1:10)
      ENDIF
      CALL LERESU
      GOTO 100


C     EXECUTION DU TRACE DES ISOTHERMES
C     =================================
 200  IF( MNVISO .EQ. 0 ) THEN
C        INITIALISATIONS PAR DEFAUT DES VALEURS DES ISO
         NBISO = ABS( NBISO )
         NBISO = MAX( 2, NBISO )
         MXISO = NBISO
         IF( MNVISO .EQ. 0 ) CALL TNMCDC( 'REEL', MXISO+1, MNVISO )
         IF( MNTISO .EQ. 0 ) CALL TNMCDC( 'ENTIER', MXISO, MNTISO )
C        DECALAGE POUR EVITER LES ISOS TEMIN et TEMAX
         X = ( TEMAX - TEMIN ) / ( NBISO + 1 )
         F = TEMIN + X
         DO I=1,NBISO
            RMCN( MNVISO - 1 + I ) = F
            F                      = F + X
         ENDDO
         IF( NBISO .EQ. 1 ) RMCN(MNVISO) = (TEMIN + TEMAX) * 0.5
      ENDIF
C
 215  IF( LANGAG .EQ. 0 ) THEN
         PRINT 10210, (I,RMCN(MNVISO-1+I),I=1,NBISO)
      ELSE
         PRINT 20210, (I,RMCN(MNVISO-1+I),I=1,NBISO)
      ENDIF
10210 FORMAT('Trace des LIGNES ou SURFACES ISOVALEURS'/
     % 5(I4,' : ',G13.5))
20210 FORMAT('Drawing of ISOVALUES LINES or SURFACES'/
     % 5(I4,' : ',G13.5))
C
C     PALETTE ARC EN CIEL
      IF( NDIM .LE. 2 ) THEN
C        COULEURS PURES AVEC AJOUT DE BLANC
         CALL PALCDE( 12 )
      ELSE
C        COULEURS PURES AVEC AJOUT DE NOIR
         CALL PALCDE( 13 )
      ENDIF
C
      IF( MODECO .EQ.  4 .OR.
     %    MODECO .EQ.  8
ccc     %    MODECO .EQ. 11 .OR.    iso trop jaunes...
ccc     %    MODECO .EQ. 12
     %  ) THEN
C        LA PALETTE ORANGE ROUGE POUR TRACER LES ERREURS
         CALL PALCDE( 5 )
cccC        IAVELO sans eloignement
ccc         IAVELO = 0
      ENDIF
C
C     ===============================================
C     TRACE SELON LA DIMENSION DE L'ESPACE DE L'OBJET
C     ===============================================
      NOUVIS = 0
      IF( NDIM .GT. 2 ) GOTO 400
C
C     ********
C     OBJET 2D
C     ********
C     OPTIONS DE LA VISEE POUR VOIR L'OBJET ET SES ISOTHERMES
 300  IF( MODECO0 .EQ. -11 ) THEN
         NMTCL = 90
         CALL VISEE1
      ELSE
         CALL VISE2D( NMTCL )
      ENDIF
      IF( NMTCL .LT. 0 ) GOTO 100
C
C     INITIALISATION DE TRANSLATION ZOOM
      IF( LORBITE .NE. 0 ) THEN
         CALL ZOOM2D0( NOTYEV )
         IF( NOTYEV .EQ. 0 ) GOTO 300
      ENDIF
C
C     PARAMETRES DES TAILLES DES TABLEAUX. A MODIFIER EVENTUELLEMENT
C     MXSOUI : MAXIMUM D INTERVALLES DU SEGMENT UNITE
C              MXSOUI ** 2 SOUS-TRIANGLES  CREES AU PLUS DANS UN TRIANGLE
 310  MXSOUI = 4
C
C     MXPILE : MAXIMUM DE SOUS-TRIANGLES GENERES DANS L EF REFERENCE
      MXPILE = 2 * MXSOUI * MXSOUI + 16
C
C     NOPOTR(3,NBISO) NOMBRE NO DES EXTREMITES DES ISOS SUR LE TRIANGLE
      L1 = 3 * NBISO
C
C     NPILE(3,MXPILE) PILE DES 3 SOMMETS DE CHAQUE SOUS-TRIANGLE
      L2 = 3 * MXPILE
C
C     ADRESSAGES DANS UN SEUL TABLEAU EN MCN
      MOPILE = L1 + L2
      CALL TNMCDC( 'ENTIER', MOPILE, MNPILE )
      MNNOPO = MNPILE + L2
C
C     SOLEL (MXNOEL) SOLUTION AUX NOEUDS DE L ELEMENT FINI DU MAILLAGE
C     FBASE (MXNOEL) VALEUR DES FONCTIONS DE BASE EN UN POINT
C     COPOE (MXPOEL,3) COORDONNEES DES POINTS DE L ELEMENT
      MOSOLE = MXNOEL + MXNOEL + MXPOEL*3
      CALL TNMCDC( 'REEL2', MOSOLE, MNSOLE )
      MNFBAS = MNSOLE + MOREE2 * MXNOEL
      MNCOPO = MNFBAS + MOREE2 * MXNOEL
C
C     MXPOIS : MAXIMUM D INTERSECTIONS DES ISO ET ARETES DU SOUS-TRIANGLE
      MXPOIS = 3 * (NBISO + 1)
      L      = 2 * MXPOIS
C     COPOT(2,MXPOIS) COORDONNEES DES POINTS D INTERSECTION ISO-ARETE
C
C     MXSOMM : MAXIMUM DE SOMMETS DES SOUS-TRIANGLES DE L EF REFERENCE
      MXSOMM = ( MXSOUI + 1 ) ** 2 + 16
C     VALST (3,MXSOMM) COORDONNEES ET VALEUR DE LA SOLUTION
C     AUX SOMMETS DES SOUS-TRIANGLES
      IF( MNCOPT .GT. 0 .AND. MOCOPT .GT. 0 )
     %    CALL TNMCDS( 'REEL', MOCOPT, MNCOPT )
      L1     = 3 * MXSOMM
      MOCOPT = L + L1
      CALL TNMCDC( 'REEL', MOCOPT, MNCOPT )
      MNVALS = MNCOPT + L
C
C     BOUCLE SUR LES CAS A TRACER EN 2D
C     ---------------------------------------------------------------
      DO NCAS = NCAS0, NCAS1
C
C        L'ECRAN PIXELS EST EFFACE (PAS DE SCINTILLEMENT)
         CALL EFFACEMEMPX

C        TRACE DES AXES 2D
         NETAXE = 0
         CALL TRAXE2
C
C        TEMPS DE CALCUL DU VECTEUR NCAS
         TEMPS = TIMES( NCAS )
C
C        LE NOMBRE DE TRACES DE SEGMENTS DE CHAQUE ISO EST MIS A ZERO
C        POUR AFFICHAGE PERIODIQUE DU NUMERO DE L'ISOVALEUR
         CALL AZEROI( MXISO, MCN(MNTISO) )
C
C        BOUCLE SUR LES DIFFERENTS TYPES D'ELEMENTS FINIS DU MAILLAGE
         DO 320 I = 0, NBTYEL-1
C
C           L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF DE CE TYPE D'EF
            MNELE = MCN( MNELEM + I )
C
C           LE NUMERO DU TYPE DES ELEMENTS FINIS
            NUTYEL = MCN( MNELE + WUTYEL )
C
C           LE NOMBRE DE TELS ELEMENTS
            NBELEM = MCN( MNELE + WBELEM )
C
C           LES CARACTERISTIQUES DE L'ELEMENT FINI
            CALL ELTYCA( NUTYEL )
C
C           L'ADRESSE MCN DU TABLEAU 'NPEF' POUR CE TYPE D'EF
            MNPGEL = MNELE + WUNDEL
            IF( NDPGST .GE. 2 ) THEN
               MNPGEL = MNPGEL + MCN(MNELE+WBELEM)*MCN(MNELE+WBNDEL)
            ENDIF
C
C           TRACE DES COURBES ISOVALEURS EN 2D
C           PASSAGE PAR UN SP AFIN DE BENEFICIER DES INDICES FORTRAN EN CLAIR
            CALL TRISO2( NBISO , RMCN(MNVISO), MCN(MNTISO),
     %                   NDIM  , NCAS, NCAS0, NCAS1, NTDL,
     %                   NTYP  , TEMPER,  dptemp,
     %                   NUTYEL, NBELEM ,
     %                   NBNOE , MCN(MNELE+WUNDEL), NBPOE, MCN(MNPGEL),
     %                   NBCOOR, NBPOI , MCN(MNPOGE+WYZPOI),
     %                   MXSOMM, MXPOIS, MCN(MNSOLE), MCN(MNCOPO),
     %                   MXPILE, MCN(MNPILE), MCN(MNNOPO),
     %                   MCN(MNCOPT), MCN(MNVALS), MCN(MNFBAS) )
 320     ENDDO
C
C        AFFICHAGE DES VALEURS EXTREMES
         CALL XVTYPETRAIT( LIGCON )
         IF( NCAS .EQ. NCAMIN ) THEN
            MN = MNPOGE+WYZPOI-NBCOOR+NBCOOR*NOEMIN
            CALL SYMBOLE2D( NCNOIR, RMCN(MN), RMCN(MN+1), '.Min' )
         ENDIF
         IF( NCAS .EQ. NCAMAX ) THEN
            MN = MNPOGE+WYZPOI-NBCOOR+NBCOOR*NOEMAX
            CALL SYMBOLE2D( NCNOIR, RMCN(MN), RMCN(MN+1), '.Max' )
         ENDIF

C        CALCUL DU MIN ET MAX DU CAS NCAS
         TMINCAS = 1D100
         TMAXCAS =-1D100
         IF( NTYP .EQ. 0 ) THEN
            DO K = 1,NTDL
               V = TEMPER( K, NCAS )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ELSE
            DO K = 1,NTDL
               V = dptemp( NCAS )%dptab( K )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ENDIF

         TMINC = REAL( TMINCAS )
         TMAXC = REAL( TMAXCAS )

C        TRACE LA LEGENDE DU TRACE DES ISOTHERMES
         CALL LEGISOTH( KNOMOB, NCAS, TMINC, TMAXC, NBISO, MNVISO,
     %                  MODECO )
C
C        MISE SUR FICHIER NomfgifBoImage.xwd puis NomfgifNoImage.jpg
C        DE LA PIXMAP de la FENETRE X11 ACTUELLE
         CALL VIDEO1( NOMFGIF, NCAS )
C
C        ATTENDRE POUR LIRE LE TRACE
         CALL ATTENDSEC( TEMP2TRAC )
C
C        FIN DE LA BOUCLE SUR LES CAS  ------------------------
      ENDDO
C
C     CONSTRUIRE le FICHIER VIDEO Nomfic.gif A PARTIR DES FICHIERS
C     CONSTRUITS de NOMS NomfgifNoImag.jpg
C     ------------------------------------------------------------
      CALL VIDEOFIN( NOMFGIF )
C
C     RETOUR POUR UNE NOUVELLE VISEE
C     ------------------------------
      IF( MODECO0 .EQ. -11 ) GOTO 9900
      NOUVIS = 1
      IF( LORBITE .NE. 0 ) THEN
         IF( NCAS0 .EQ. NCAS1 ) THEN
C           ZOOM  BOUTON ENFONCE et DEPLACE
            CALL ZOOM2D1( NOTYEV )
         ELSE
C           ZOOM  BOUTON ENFONCE et DEPLACE et RELACHE
            CALL ZOOM2D3( NOTYEV )
         ENDIF
         IF( NOTYEV .EQ. 0 ) GOTO 300
         GOTO 310
      ELSE
         CALL CLICSO
      ENDIF
      GOTO 300


C     ======================
C     OBJET DE DIMENSION >=3
C     ======================
 400  IF( NBCOOR .EQ. 6 ) THEN
C        ISOTHERMES EN 6D NON ENCORE PROGRAMME
         RETURN
      ENDIF
C
C     ********
C     OBJET 3D
C     ********
      IF( NOUVIS .NE. 0 ) GOTO 450
C     CREATION OU REDECOUVERTE DU TMS OBJET>>>FACE
      CALL HACHOB( KNOMOB , 4 , NTFAOB , MNFAOB , IERR )
      IF( IERR .NE. 0 ) GOTO 9900
C
C     CREATION DU HACHAGE DES ARETES DES FACES FRONTALIERES DE L'OBJET
      CALL HACHAF( KNOMOB, 0, NTFAOB, MNFAOB,
     %             NTAFOB, MNAFOB, I )
      IF( I .NE. 0 ) GOTO 9900
C
C     CREATION DU TABLEAU DES XYZ DES SOMMETS DES ISOVALEURS
      IF( MNXYZI .GT. 0 ) CALL TNMCDS( 'REEL', MOXYZI,  MNXYZI )
ccc      MXFISO = NBPOI/3 * NBISO
      MXFISO = NBPOI * NBISO
 410  MOXYZI = 12 * MXFISO
      IF( MOXYZI .LE. 0 ) THEN
         MXFISO = MXFISO / 2
         GOTO 410
      ENDIF

      CALL TNMCDC( 'REEL', MOXYZI, MNXYZI )
      IF( MNXYZI .LE. 0 ) THEN
         MXFISO = MXFISO / 2
         GOTO 410
      ENDIF

      CALL TNMCDC( 'ENTIER', MXFISO, MNNISO )
      IF( MNNISO .LE. 0 ) GOTO 9000
C
C     PARAMETRES DES TAILLES DES TABLEAUX. A MODIFIER EVENTUELLEMENT
C     MXSOUI : MAXIMUM D INTERVALLES DU SEGMENT UNITE
C              MXSOUI ** 3 SOUS-TETRAEDRES CREES AU PLUS DANS UN TETRAEDRE
      MXSOUI = 4
C
C     MXPILE : MAXIMUM DE SOUS-TETRAEDRES GENERES DANS L EF REFERENCE
      MXPILE = 6 * MXSOUI * MXSOUI * MXSOUI
C     NPILE(4,MXPILE) PILE DES 4 SOMMETS DE CHAQUE SOUS-TETRAEDRE
      MOPILE = 4 * MXPILE
      CALL TNMCDC( 'ENTIER', MOPILE, MNPILE )
      IF( MNPILE .LE. 0 ) GOTO 9000
C
C     SOLEL (MXNOEL) SOLUTION AUX NOEUDS DE L ELEMENT FINI DU MAILLAGE
C     FBASE (MXNOEL) VALEUR DES FONCTIONS DE BASE EN UN POINT
C     COPOE (MXPOEL,3) COORDONNEES DES POINTS DE L EF
      MOSOLE = MXNOEL+MXNOEL+MXPOEL*3
      CALL TNMCDC( 'REEL2', MOSOLE, MNSOLE )
      MNFBAS = MNSOLE + MOREE2 * MXNOEL
      MNCOPO = MNFBAS + MOREE2 * MXNOEL
C
C     MXSOMM : MAXIMUM DE SOMMETS DES SOUS-TETRAEDRES DE L EF REFERENCE
      MXSOMM = ( MXSOUI + 1 ) ** 3
C
C     VALST (NDIM+1,MXSOMM) COORDONNEES ET VALEUR DE LA SOLUTION
C                           AUX SOMMETS DES SOUS-TETRAEDRES
      IF( MNCOPT .GT. 0 .AND. MOCOPT .GT. 0 )
     %    CALL TNMCDS( 'REEL', MOCOPT, MNCOPT )
      MOCOPT = 4 * MXSOMM
      CALL TNMCDC( 'REEL', MOCOPT, MNCOPT )
      IF( MNCOPT .LE. 0 ) GOTO 9000
      MNVALS = MNCOPT
C
C     OPTIONS DE LA VISEE POUR VOIR L'OBJET ET SES ISOTHERMES
C     -------------------------------------------------------
 450  IF( MODECO0 .EQ. -11 ) THEN
C        LES PARAMETRES DE LA VISEE ACTUELLE
         CALL VISEE1
         NMTCL = 90
      ELSE
         CALL VISE3D( NMTCL )
      ENDIF
      IF( NMTCL .LT. 0 ) GOTO 100

C     LE NOMBRE D'ENTIERS PAR ARETE FRONTALIERE
      MOARFR = MCN( MNAFOB + WOARFR )
C     LA MAJORATION DU NOMBRE DES ARETES FRONTALIERES
      MXARFR = MCN( MNAFOB + WXARFR )
C     LE NUMERO DANS LAREFR DE LA PREMIERE ARETE FRONTALIERE
      L1ARFR = MCN( MNAFOB + W1ARFR )
C     LE NOMBRE D'ARETES FRONTALIERES DANS LE CHAINAGE
      NBARFR = MCN( MNAFOB + WBARFR )
C
C     INITIALISATION DE L'ORBITE ZOOM DEPLACEMENT
      IF( LORBITE .NE. 0 ) THEN
         CALL ORBITE0( NOTYEV )
         IF( NOTYEV .EQ. 0 ) GOTO 450
      ENDIF
C
C     BOUCLE SUR LES CAS A TRACER EN 3D
C     ---------------------------------------------------------------
 500  DO NCAS = NCAS0, NCAS1
C
C        TEMPS DE CALCUL DU VECTEUR NCAS
         TEMPS = TIMES( NCAS )
C
C        BOUCLE SUR LES DIFFERENTS TYPES D'ELEMENTS FINIS DU MAILLAGE
         NBFISO = 0
         DO 520 I = 0, NBTYEL-1
C
C           L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF DE CE TYPE D'EF
            MNELE = MCN( MNELEM + I )
C
C           LE NUMERO DU TYPE DES ELEMENTS FINIS
            NUTYEL = MCN( MNELE + WUTYEL )
C
C           LE NOMBRE DE TELS ELEMENTS
            NBELEM = MCN(MNELE + WBELEM )
C
C           LES CARACTERISTIQUES DE L'ELEMENT FINI
            CALL ELTYCA( NUTYEL )
            IF( NUTYEL .EQ. 30 ) GOTO 520
C           PAS DE TRACE DES 6CUBES, SEULEMENT DES 3Q1C EXTRAITS
C
C           L'ADRESSE MCN DU TABLEAU 'NPEF' POUR CE TYPE D'EF
            MNPGEL = MNELE + WUNDEL
            IF( NDPGST .GE. 2 ) THEN
               MNPGEL = MNPGEL + MCN(MNELE+WBELEM)*MCN(MNELE+WBNDEL)
            ENDIF
C
C           CALCUL DES FACES DES SURFACES ISOVALEURS
            CALL TRISO3( NBISO,  RMCN(MNVISO),
     %                   NDIM,   NCAS, NCAS0, NCAS1, NTDL,
     %                   NTYP,   TEMPER,  dptemp,
     %                   NUTYEL, NBELEM ,
     %                   NBNOE , MCN(MNELE+WUNDEL), NBPOE, MCN(MNPGEL),
     %                   NBCOOR, NBPOI, MCN(MNPOGE+WYZPOI),
     %                   MXSOMM, MCN(MNSOLE), MCN(MNCOPO),
     %                   MXPILE, MCN(MNPILE),
     %                   MCN(MNVALS), MCN(MNFBAS),
     %                   TRIANG, MXFISO, NBFISO,
     %                   RMCN(MNXYZI), MCN(MNNISO) )
 520     ENDDO
C
         IF( MNBARY .GT. 0 ) CALL TNMCDS( 'REEL',   NBF, MNBARY )
         IF( MNNUFA .GT. 0 ) CALL TNMCDS( 'ENTIER', NBF, MNNUFA )
         MNBARY = 0
         MNNUFA = 0
         NBF = NBFISO + NBARFR
         CALL TNMCDC( 'REEL', NBF, MNBARY )
         IF( MNBARY .LE. 0 ) GOTO 9000
         CALL TNMCDC( 'ENTIER', NBF, MNNUFA )
         IF( MNNUFA .LE. 0 ) GOTO 9000
C
C        FORMATION DES TABLEAUX NUMERO DES FACES ET DISTANCE AXONOMETRIQUE
C        POUR LES FACES ISOVALEURS
         CALL TRBFIS( TRIANG, NBFISO, 3, 0, RMCN(MNXYZI),
     %                MCN(MNNUFA), RMCN(MNBARY) )
C
C        FORMATION DES TABLEAUX NUMERO DES FACES ET DISTANCE AXONOMETRIQUE
C        POUR LES ARETES FRONTALIERES DE L'OBJET
C        ATTENTION LES ARETES FRONTALIERES DOIVENT OBLIGATOIREMENT ETRE APRES
C                  LES FACES ISOVALEURS
         CALL TRBARF( MOARFR, MXARFR, L1ARFR, MCN(MNAFOB+WAREFR),
     %                MCN(MNPOGE+WBCOOP), RMCN(MNPOGE+WYZPOI),
     %                NBFISO, MCN(MNNUFA), RMCN(MNBARY))
C
C        LE TRI PAR TAS DE CETTE DISTANCE
C        LA FACE LA PLUS PROCHE EST LA PREMIERE
         CALL TRITRP( NBF, RMCN(MNBARY), MCN(MNNUFA) )
C
C        L'ECRAN PIXELS EST EFFACE (PAS DE SCINTILLEMENT)
         CALL EFFACEMEMPX
C
C        TRACE DES AXES 3D
         NETAXE = 0
         CALL TRAXE3
C
C        TRACE EFFECTIF DES ARETES FRONTALIERES ET DES FACES ISOVALEURS
C        --------------------------------------------------------------
         CALL T3AFIS( NBISO,  NBARFR, NBFISO, MCN(MNNUFA),
     %                MOARFR, MXARFR, MCN(MNAFOB+WAREFR),
     %                MCN(MNPOGE+WBCOOP), RMCN(MNPOGE+WYZPOI),
     %                TRIANG, RMCN(MNXYZI), MCN(MNNISO) )
C
C        TRACE DU POINT DE TEMPERATURE MINIMALE ET MAXIMALE
         CALL XVTYPETRAIT( LIGCON )
         IF( NCAS .EQ. NCAMIN ) THEN
            MN = MNPOGE + WYZPOI + NBCOOR*NOEMIN -NBCOOR
            CALL SYMBOLE3D( NCNOIR, RMCN(MN), '.Min' )
         ENDIF
         IF( NCAS .EQ. NCAMAX ) THEN
            MN = MNPOGE + WYZPOI + NBCOOR*NOEMAX -NBCOOR
            CALL SYMBOLE3D( NCNOIR, RMCN(MN), '.Max' )
         ENDIF

C        CALCUL DU MIN ET MAX DU CAS NCAS
         TMINCAS = 1D100
         TMAXCAS =-1D100
         IF( NTYP .EQ. 0 ) THEN
            DO K = 1,NTDL
               V = TEMPER( K, NCAS )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ELSE
            DO K = 1,NTDL
               V = dptemp( NCAS )%dptab( K )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ENDIF

         TMINC = REAL( TMINCAS )
         TMAXC = REAL( TMAXCAS )

C        TRACE LA LEGENDE DU TRACE DES ISOTHERMES
         CALL LEGISOTH( KNOMOB, NCAS, TMINC, TMAXC, NBISO, MNVISO,
     %                  MODECO )
C
C        MISE SUR FICHIER NomfgifBoImage.xwd puis NomfgifNoImage.jpg
C        DE LA PIXMAP de la FENETRE X11 ACTUELLE
         CALL VIDEO1( NOMFGIF, NCAS )
C
C        ATTENDRE POUR LIRE LE TRACE
         CALL ATTENDSEC( TEMP2TRAC )
C
C        FIN DE LA BOUCLE SUR LES CAS --------------------------------
      ENDDO
C
C     CONSTRUIRE le FICHIER VIDEO Nomfic.gif A PARTIR DES FICHIERS
C     CONSTRUITS de NOMS NomfgifNoImag.jpg
C     ------------------------------------------------------------
      CALL VIDEOFIN( NOMFGIF )
C
C     RETOUR POUR UNE NOUVELLE VISEE
C     ------------------------------
      IF( MODECO0 .EQ. -11 ) GOTO 9900
      NOUVIS = 1
      IF( LORBITE .NE. 0 ) THEN
         IF( NCAS0 .EQ. NCAS1 ) THEN
C           ORBITE BOUTON ENFONCE et DEPLACE
            CALL ORBITE1( NOTYEV )
         ELSE
C           ORBITE BOUTON ENFONCE et DEPLACE et RELACHE
            CALL ORBITE3( NOTYEV )
         ENDIF
         IF( NOTYEV .EQ. 0 ) GOTO 450
         GOTO 500
      ELSE
C        POUR LIRE LE TRACE AVANT D'AFFICHER UN MENU
         CALL CLICSO
      ENDIF
      GOTO 450


C     SUPER TABLEAU MCN SATURE
C     ========================
 9000 NBLGRC(NRERR) = 2
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1) ='PAS ASSEZ de MOTS pour le SUPER-TABLEAU MCN'
         KERR(2) ='AUGMENTER ce NOMBRE dans $MEFISTO/incl.pp.inc'
      ELSE
         KERR(1) ='NO SUFFICIENT WORD NUMBER of SUPER-ARRAY MCN'
         KERR(2) ='AUGMENT IT in $MEFISTO/incl/pp.inc'
      ENDIF
      CALL LEREUR


C     SORTIE DU TRACE DES ISOTHERMES
C     ==============================
 9900 IF( MNVISO .GT. 0 ) CALL TNMCDS( 'REEL'  , MXISO+1, MNVISO )
      IF( MNTISO .GT. 0 ) CALL TNMCDS( 'ENTIER', MXISO  , MNTISO )
      IF( MNBARY .GT. 0 ) CALL TNMCDS( 'REEL',   NBF,     MNBARY )
      IF( MNNUFA .GT. 0 ) CALL TNMCDS( 'ENTIER', NBF,     MNNUFA )
      IF( MNSOLE .GT. 0 ) CALL TNMCDS( 'REEL2' , MOSOLE,  MNSOLE )
      IF( MNPILE .GT. 0 ) CALL TNMCDS( 'ENTIER', MOPILE,  MNPILE )
      IF( MNCOPT .GT. 0 ) CALL TNMCDS( 'REEL'  , MOCOPT,  MNCOPT )
      IF( MNXYZI .GT. 0 ) CALL TNMCDS( 'REEL'  , MOXYZI,  MNXYZI )
      IF( MNNISO .GT. 0 ) CALL TNMCDS( 'ENTIER', MXFISO,  MNNISO )
      IF( MNVISO .GT. 0 ) CALL TNMCDS( 'REEL',   MXISO+1, MNVISO )
      IF( MNTISO .GT. 0 ) CALL TNMCDS( 'ENTIER', MXISO,   MNTISO )

      RETURN
      END
