      SUBROUTINE NLSEVOU( NDIM,   NBNOMA, MNTOPO,
     %                    NBTYEL, MNNPEF, NDPGST,
     %                    MNTPOB, MNTAUX,
     %                    MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                    MNTHER, MNTAEL, MNX,    MNVENL,
     %                    IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %                    IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %                    NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %                    NBDLIB, MNDLIB,
     %                    RC,     NBVAPR, VALPR,  MXROOT, RAVANT,
     %                    NBHOMO, DETC,   NBAVRC, NBOURC )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE NOMBRE DE VALEURS PROPRES OUBLIEES AVANT RC
C -----    C-A-D CALCULER LE NOMBRE DE COEFFICIENTS DIAGONAUX NEGATIFS
C          DE LA MATRICE FACTORISEE  A - RC B = L D tL
C
C ENTREES:
C --------
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 1 ou 2 ou 3 ou 6
C          (SI AXISYMETRIE NDIM=2 X => R>=0 et Y=>Z et Z=0)
C NBNOMA : NOMBRE DE NOEUDS DU MAILLAGE DE L'OBJET ou
C          NOMBRE TOTAL DE DEGRES DE LIBERTE DU MAILLAGE DE L'OBJET ou
C          NOMBRE DE LIGNES ET COLONNES DES MATRICES DE KG ET MG
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE DE L'OBJET
C NBTYEL : NOMBRE DE TYPES D'EF DE L'OBJET
C MNNPEF : ADRESSE MCN DU TMC DES ADRESSES MCN DES TABLEAUX NPEF"TYPE EF
C NDPGST : CODE DE STOCKAGE DES SOMMETS POINTS NOEUDS
C MNTPOB : ADRESSE MCN DES TABLEAUX POLYNOMES DE BASE DES TYPES D'EF
C MXPLBA : NOMBRE MAXIMAL DE TABLEAUX POBA PAR TYPE D'EF
C MNTAUX : ADRESSE MCN DES TABLEAUX AUXILAIRES
C MNXYZP : ADRESSE MCN DE TMS XYZSOMMET DE L'OBJET
C NUMIOB : NUMERO MINIMAL DES OBJETS
C NUMAOB : NUMERO MAXIMAL DES OBJETS
C MNDOEL : ADRESSE MCN DES TMS DES DONNEES THERMIQUES DE L'OBJET
C MNTHER : ADRESSE MCN DU TABLEAU DES DONNEES THERMIQUES INTERNES
C          AU MOINS NPIMAX(=128) REELS DOUBLE PRECISION
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DES NBCOOR COORDONNEES DES POINTS DE L'EF COURANT
C MNVENL : ADRESSE MCN DU VECTEUR SERVANT A DEFINIR LA NON LINEARITE
C
C NCODSM : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CAPACITE
C          0 SI MATRICE DIAGONALE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDMG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE MG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NBNOMA SI MATRICE DIAGONALE
C MNMUMG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C MG     : MATRICE PROFIL GLOBALE DE CAPACITE CALCULEE
C
C NCODSK : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CONDUCTIVITE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDKG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE KG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NBNOMA SI MATRICE DIAGONALE
C MNMUKG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C KG     : MATRICE PROFIL GLOBALE DE CONDUCTIVITE CALCULEE
C
C NBDLIB : NOMBRE DE DEGRES DE LIBERTE LIBRES
C NBRDLX : NOMBRE DE DL SUPPORT D'UN DEGRE DE LIBERTE FIXE
C MONDLX : NOMBRE DE MOTS DECLARES DU TABLEAU MC NO DES DL FIXES
C MNNDLX : ADRESSE MCN DU TABLEAU MC DES NUMEROS DES DL FIXES, 0 SINON
C MNVDLX : ADRESSE MCN DU TABLEAU MC DES VALEURS DES DL FIXES, 0 SINON
C MNDLIB : MCN(MNDLIB-1+I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NBNOMA) LIBRE
C                          -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          0  SI PAS DE DL BLOQUES
C
C RC     : REEL AU DESSOUS DUQUEL LES VALEURS PROPRES SONT A COMPTER
C          -RC EST AUSSI LA TRANSLATION  KG - RC MG => KG
C NBNOMA   : NOMBRE DE DEGRES DE LIBERTE
C NBVAPR : NOMBRE DE VALEURS PROPRES DEJA CALCULEES
C VALPR  : NBVAPR VALEURS PROPRES
C NCODSK : CODE DE STOCKAGE DE LA MATRICE PROFIL KG
C MNMUKG : ADRESSE MCN DU TABLEAU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C KG     : MATRICE PROFIL DE RAIDEUR
C NCODSM : CODE DE STOCKAGE DE LA MATRICE PROFIL MG
C MNMUMG : ADRESSE MCN DU TABLEAU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C MG     : MATRICE PROFIL DE MASSE
C MXROOT : NOMBRE MAJORANT DES VALEURS PROPRES >NBROOT AFIN DE PRENDRE EN
C          CONSIDERATION LES VALEURS MULTIPLES OU LES CALCULS DES VALEURS
C          SUPERIEURES A CELLES DEMANDEES

C MODIFIE:
C --------
C RAVANT : RAVANT(I,1)=VALEUR MINIMALE AYANT I VALEURS PROPRES AVANT ELLE
C          1D111 indique une valeur non initialisee
C          RAVANT(I,2)=VALEUR MAXIMALE AYANT I VALEURS PROPRES AVANT ELLE
C          -1D111 indique une valeur non initialisee

C SORTIES:
C --------
C NBHOMO : NOMBRE D'HOMOTHETIES DE SCALE DU DETERMINANT DE KG - RC MG
C DETC   : DETERMINANT DE (KG - RC MG) * SCALE ** NBHOMO DEFLATIONNE
C NBAVRC : NOMBRE DE VALEURS PROPRES DE KG - RC MG INFERIEURES A RC
C NBOURC : NOMBRE DE VALEURS PROPRES OUBLIEES DANS VALPR AVANT RC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TEXAS A & M UNIVERSITY at QATAR  Fevrier 2011
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2014
C2345X7..............................................................012
      PARAMETER (NENTRE=1)
      include"./incl/epsvvp.inc"
      include"./incl/langue.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      INTEGER           NUMIOB(4), NUMAOB(4),  MNDOEL(4)
C
      DOUBLE PRECISION  RC, VALPR(NBVAPR), KG(NBRDKG), MG(NBRDMG), DETC,
     %                  RAVANT(0:MXROOT*2,2)
C
cccC     RESTAURATION DE LA MATRICE A A PARTIR DE RG
ccc 10   CALL TRTATA( RG, KG, MOTSRG )

      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) RC
10000    FORMAT('DEBUT NLSEVOU avec R=',G15.7)
      ELSE
         WRITE(IMPRIM,20000) RC
20000    FORMAT('NLSEVOU START with R=',G15.7)
      ENDIF
C
C     CALCUL DE LA MATRICE KG(V**2)
      IEMG = 0
      IEKG = 1
 10   CALL NLSEMK( NDIM,   NBNOMA, MNTOPO,
     %             NBTYEL, MNNPEF, NDPGST,
     %             MNTPOB, MNTAUX,
     %             MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %             MNTHER, MNTAEL, MNX,    MNVENL,
     %             IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %             IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %             NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %             NBDLIB, MNDLIB, IERR  )
C
C     KG - RC MG => KG
      CALL MUA2PD( NBDLIB, 1.D0, NCODSK, MCN(MNMUKG), KG,
     %                      -RC, NCODSM, MCN(MNMUMG), MG,
     %                                   MCN(MNMUKG), KG )
C
C     KG - RC MG = L D tL DANS KG
      CALL CRMC1D( MCN(MNMUKG), KG, NBDLIB, EPS, NENTRE, KG, NRETOU )
      IF( NRETOU .NE. 0 ) THEN
         RC = RC * ( 1D0 + FAST )
         GOTO 10
      ENDIF

C     MISE A ZERO DES COEFFICIENTS INFERIEURS A 1D-20
      DO I = 1, NBRDKG
         IF( ABS(KG(I)) .LT. 1D-20 ) KG(I) = 0D0
      ENDDO

C     DETERMINANT DE LA MATRICE KG - RC MG
      CALL DETETD( KG, MCN(MNMUKG), NBDLIB, NBAVRC, NBHOMO, DETC )
C
C     NOMBRE NB DE VALPR(I) < RC
      NBOURC = 0
      DO I = 1, NBVAPR
C        DEFLATION DU POLYNOME CARACTERISTIQUE
         DETC = DETC / (RC-VALPR(I))
C
C        NOMBRE DE VALEURS PROPRES INFERIEURES A RC
         IF( VALPR(I) .LT. RC ) NBOURC = NBOURC + 1
      ENDDO
C
C     NOMBRE DE VALEURS PROPRES OUBLIEES AVANT RC
      NBOURC = MAX( 0, NBAVRC - NBOURC )

C     AU DELA DE MXROOT*2, NBAVRC EST PROJETE EN MXROOT*2
C     POUR AVOIR UNE LIMITE DANS RAVANT
      NBAVRC = MIN( MXROOT*2, NBAVRC )

      IF( NBAVRC .GE. 0 ) THEN

C        MISE A JOUR DE LA VALEUR MINIMALE AYANT NBAVRC VALEURS PROPRES AVANT
         IF( RC .LT. RAVANT(NBAVRC,1) ) THEN
            RAVANT(NBAVRC,1) = RC
         ENDIF

C        MISE A JOUR DE LA VALEUR MAXIMALE AYANT NBAVRC VALEURS PROPRES AVANT
         IF( RC .GT. RAVANT(NBAVRC,2) ) THEN
            RAVANT(NBAVRC,2) = RC
         ENDIF

      ENDIF

ccc      DO j=1,2
ccc         print 10039,(i,j,ravant(i,j),i=0,MXROOT*2)
ccc      enddo
ccc10039 FORMAT(5(' ravant(',I3,',',I3,')=',G15.7))

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10020) NBAVRC, NBOURC, RC
      ELSE
         WRITE(IMPRIM,20020) NBAVRC, NBOURC, RC
      ENDIF
10020 FORMAT('FIN NLSEVOU: Il y a',I7,' VALEURS PROPRES AVANT et',
     %       I7,' VALEURS PROPRES OUBLIEES AVANT',G15.7/)
20020 FORMAT('NLSEVOU END: There are',I7,' SMALLER EIGENVALUES and',
     %       I7,' FORGOTTEN EIGENVALUES BEFORE',  G15.7/)
C
      RETURN
      END
