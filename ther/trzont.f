      SUBROUTINE TRZONT( NOPROJ, NDIM,   KNOMOB, MODECO0,
     %                   NBTYEL, MNELEM, MNPOGE, MNNOEU, NDPGST,
     %                   NCAS0,  NCAS1,  NTDL,
     %                   NTYP,   TEMPER, dptemp,
     %                   TMIN,   NOEMIN, NCAMIN,
     %                   TMAX,   NOEMAX, NCAMAX,
     %                   TIMES )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER DE LA TEMPERATURE PAR ZONES DE COULEURS
C -----    EN 2D DANS LES EF
C          EN 3D SUR LES FACES FRONTALIERES DE L'OBJET

C ENTREES:
C --------
C NOPROJ : TYPE DE PROJECTION 0 CI-DESSOUS FIXE LA COORDONNEE A ZERO
C          -1 PAS DE PROJECTION TRAITEMENT en XYZ NORMAL
C           1 : 'X Y Z 0 0 0'
C           2 : 'X Y 0 U 0 0'
C           3 : 'X 0 0 U V 0'
C           4 : '0 0 0 U V W'
C NDIM   : DIMENSION DE L'ESPACE DE L'OBJET (1 OU 2 OU 3)
C KNOMOB : NOM DE L'OBJET
C MODECO0: MODE DE TRACE DES VECTEURS DU TMS D'ADRESSE MNDEPL
C         =-11 CE SONT DES TEMPERATURES MAIS PAS DE LECTURE DE MOTS CLES
C              POUR UN TRACE A CHAQUE PAS DE TEMPS DU CACUL
C         =1  CE SONT DES TEMPERATURES
C         =2  CE SONT DES VECTEURS PROPRES
C         =3  CE SONT DES PRESSIONS P1 A PARTIR D'UNE INTERPOLATION
C             SOIT P2 SOIT P1+BULLE P3
C         =4  CE SONT DES ERREURS PONCTUELLES SOLEX-SOLCAL
C             => TRACE DE L'ERREUR ENTRE TEMPERATURE_EXACTE
C                ET LA TEMPERATURE CALCULEE
C         =6  CE SONT DES NORMES DE VITESSE AUX NOEUDS D'UN MAILLAGE
C         =8  CE SONT DES MODULES D'UNE ONDE COMPLEXE NLSE
C             OU DES ERREURS AUX NOEUDS DU MODULE D'UNE ONDE COMPLEXE
C            (CALCUL DU MODULE DE L'ERREUR COMPLEXE A FAIRE ICI)
C         =9  PARTIE REELLE     D'UNE ONDE COMPLEXE NLSE
C         =10 PARTIE IMAGINAIRE D'UNE ONDE COMPLEXE NLSE
C         =11 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C             DU MODULE DE LA VITESSE D'UN FLUIDE
C            (CALCUL DE L'ERREUR(VITESSE EXACTE - CALCULEE) DEJA CALCULE)
C         =12 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C             DE LA PRESSION P1 DANS UN FLUIDE
C            (CALCUL DE L'ERREUR(PRESSION EXACTE-CALCULEE) DEJA CALCULE)
C         =13 FLECHES REPRESENTANT DES VECTEURS VITESSE
C         =14 ROTATIONNEL DE VITESSES ou TOURBILLONS ou VORTICITES
C
C NBTYEL : LE NOMBRE DE TYPES D'ELEMENTS DANS CETTE TOPOLOGIE
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE
C MNELEM : ADRESSE MCN DES TABLEAUX ELEMENTS FINIS DE CETTE TOPOLOGIE
C MNPOGE : ADRESSE MCN DU TABLEAU POINTS GEOMETRIQUES DE L'OBJET
C MNNOEU : ADRESSE MCN DU TABLEAU NOEUDS D'INTERPOLATION DU MAILLAGE
C NDPGST : CODE TRAITEMENT DES SOMMETS POINTS NOEUDS DU MAILLAGE
C          0 : NOEUDS=POINTS=SOMMETS
C          1 : NOEUDS=POINTS#SOMMETS
C          2 : NOEUDS#POINTS=SOMMETS
C          3 : NOEUDS#POINTS#SOMMETS

C NCAS0  : NUMERO DU PREMIER CAS A TRACER PARMI LES NCAS0:NCAS1 CAS
C NCAS1  : NUMERO DU DERNIER CAS A TRACER PARMI LES NCAS0:NCAS1 CAS
C NTDL   : NOMBRE TOTAL DE DEGRES DE LIBERTE D'UN VECTEUR TEMPER
C NTYP   : =0 EMPLOI de TEMPER
C          =1 EMPLOI de dptemp
C TEMPER : TABLEAU DES NTDL * NCAS0:NCAS1 TEMPERATURES
C dptemp : TABLEAU DES NCAS0:NCAS1 TABLEAU(NTDL) TEMPERATURES
C TMIN   : TEMPERATURE MINIMALE DES NCAS0:NCAS1 CAS
C NOEMIN : NUMERO DU NOEUD OU LA TEMPERATURE EST MINIMALE
C NCAMIN : NO DU CAS DE NCAS0:NCAS1 OU LA TEMPERATURE EST MINIMALE
C TMAX   : TEMPERATURE MAXIMALE DES NCAS0:NCAS1 CAS
C NOEMAX : NUMERO DU NOEUD OU LA TEMPERATURE EST MAXIMALE
C NCAMAX : NO DU CAS DE NCAS0:NCAS1 OU LA TEMPERATURE EST MAXIMALE
C TIMES  : TEMPS DU CALCUL DES NCAS0:NCAS1 VECTEURS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1994
C MODIFS : ALAIN PERRONNET LJLL UPMC & ST PIERRE DU PERRAY  OCTOBRE 2010
C MODIFS : ALAIN PERRONNET Saint PIERRE du PERRAY          DECEMBRE 2022
C MODIFS : ALAIN PERRONNET Saint PIERRE du PERRAY              MARS 2023
C23456---------------------------------------------------------------012
      PARAMETER     (LIGCON=0)
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___face.inc"
      include"./incl/ponoel.inc"
      include"./incl/donele.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/ctemps.inc"
      include"./incl/xyzext.inc"
      include"./incl/traaxe.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)

      CHARACTER*(*)     KNOMOB
      CHARACTER*24      NOMFGIF
      REAL              TIMES(NCAS0:NCAS1)
      DOUBLE PRECISION  TEMPER(NTDL,NCAS0:NCAS1),
     %                  TMINCAS, TMAXCAS, V
      type typ_dptab
         DOUBLE PRECISION, dimension(:), pointer :: dptab
      end type typ_dptab
      type( typ_dptab ),dimension(NCAS0:NCAS1) :: dptemp

      IF( INTERA .LE. 0 ) RETURN
C     INTERA=0:BATCH      PAS d' ECRAN GRAPHIQUE PAS de CLAVIER PAS de SOURIS
C            1:INTERACTIF AVEC   ECRAN GRAPHIQUE PAS de CLAVIER PAS de SOURIS
C            3:INTERACTIF AVEC   ECRAN GRAPHIQUE AVEC   CLAVIER AVEC   SOURIS

      IF( MODECO0 .EQ. -11 ) THEN
C        DES TEMPERATURES A TRACER SANS LECTURE DE MOTS-CLES
         MODECO = 1
      ELSE
         MODECO = MODECO0
      ENDIF

      MOREE2 = MOTVAR(6)
      MNSOLE = 0
      MNPIL3 = 0
      MNVALS = 0
      MNTRFF = 0
      MNBAQ2 = 0
      MNDTFF = 0
      MNNUTR = 0
      NBFAFR = 0
      MNXYZS = 0
      MNFBAS = 0
      MNCOPO = 0
C
C     NOM DU FICHIER VIDEO  SELON MODECO // 'zone'
      CALL VIDEONM( MODECO, 'zone', NOMFGIF )
C
C     NOMBRE DE POINTS=NOEUDS=DL DE L'OBJET
      NBPOI  = MCN(MNPOGE+WNBPOI)
C
C     NOMBRE DE COORDONNEES D'UN POINT
      NBCOOR = MCN(MNPOGE+WBCOOP)
C
      IF( MODECO .EQ. 9 ) THEN
C        LA PALETTE BLEU BLANC ROUGE POUR TRACER LA PARTIE REELLE DE L'ONDE
         CALL PALCDE( 16 )
      ELSE IF( MODECO .EQ. 10 ) THEN
C        LA PALETTE PALETTE ARC EN CIEL CLAIRE SANS VERT POUR LA PARTIE IMAGINAIRE
         CALL PALCDE( 11 )
      ELSE IF( MODECO .EQ.  4 .OR.
     %         MODECO .EQ. 11 .OR. MODECO .EQ. 12 ) THEN
C        LA PALETTE ORANGE ROUGE POUR TRACER L'ERREUR
         CALL PALCDE( 5 )
C        IAVELO sans eloignement
         IAVELO = 0
      ELSE
C        LA PALETTE ARC EN CIEL POUR TRACER LA TEMPERATURE
         CALL PALCDE( 14 )
      ENDIF
C
C     LECTURE DES DONNEES
C     ===================
 100  IF( MODECO0 .EQ. -11 ) GOTO 200
      CALL LIMTCL( 'valzone', NMTCL )
      IF( NMTCL .LT.  0 ) GOTO 9900
      IF( NMTCL .EQ. 90 ) GOTO  200
      GOTO( 200, 100, 100, 100, 150, 160 ) , NMTCL
C
C     COULEUR des ARETES du MAILLAGE
C     ..............................
 150  CALL LIMTCL( 'couleur0' , I )
      IF( I .EQ. -1 ) THEN
         GOTO 100
      ELSE IF( I .EQ. -2 ) THEN
         NCOUAF = -2
      ELSE IF( I .EQ. 0 ) THEN
C        COULEUR NOIRE
         NCOUAF = 0
      ELSE
C        COULEUR RESERVEE
         NCOUAF = N1COEL + I
      ENDIF
      GOTO 100
C
C     TYPE du TRAIT des ARETES du MAILLAGE
C     ....................................
 160  CALL LIMTCL( 'typtrait' , I )
      IF( I .EQ. -1 ) GOTO 100
      NTLAFR = I
      GOTO 100
C
C     TRACE DES ZONES DE COULEURS
C     ...........................
 200  NOUVIS = 0
C
 300  IF( NDIM .GT. 2 ) GOTO 400
C
C     ********
C     OBJET 2D
C     ********
      IF( NOUVIS .NE. 0 ) GOTO 310
C     PARAMETRES DES TAILLES DES TABLEAUX.A MODIFIER EVENTUELLEMENT
C     MXSOUI : MAXIMUM D INTERVALLES DU SEGMENT UNITE
C              MXSOUI ** 2 SOUS-TRIANGLES CREES AU PLUS DANS UN TRIANGLE
      MXSOUI = 4
C     MXPIL3 : MAXIMUM DE SOUS-TRIANGLES GENERES DANS L ELEMENT REFERENCE
      MXPIL3 = 2 * MXSOUI * MXSOUI + 16
C     MAXDLE : MAXIMUM DE D.L. D UNE INTERPOLATION (CF SP INTERP)
      MAXDLE = 30
C
C     NPIL3 (3,MXPIL3) PILE DES 3 SOMMETS DE CHAQUE SOUS-TRIANGLE
      MOPIL3 = 3 * MXPIL3
      CALL TNMCDC( 'ENTIER', MOPIL3, MNPIL3 )
C
C     SOLEL (MXNOEL) SOLUTION AUX NOEUDS DE L ELEMENT DU MAILLAGE
C     FBASE (MAXDLE) VALEUR DES FONCTIONS DE BASE EN UN POINT
C     COPOE (MXPOEL,3) COORDONNEES DES POINTS DE L ELEMENT
      MOSOLE = MXNOEL+MAXDLE+MXPOEL*3
      CALL TNMCDC( 'REEL2', MOSOLE, MNSOLE )
      MNFBAS = MNSOLE + MOREE2 * MXNOEL
      MNCOPO = MNFBAS + MOREE2 * MAXDLE
C
C     MXSOMM : MAXIMUM DE SOMMETS DES SOUS-TRIANGLES DE L EF REFERENCE
      MXSOMM = ( MXSOUI + 1 ) ** 2 + 16
C     VALST (3,MXSOMM) COORDONNEES ET VALEUR DE LA SOLUTION
C     XYZSOM(3,MXSOMM) COORDONNEES DES SOMMETS DES SOUS-TRIANGLES
C     AUX SOMMETS DES SOUS-TRIANGLES (DANS R2 OU R3)
      IF( MNVALS .GT. 0 ) CALL TNMCDS( 'REEL', MOVALS, MNVALS )
      MOVALS = 6 * MXSOMM
      CALL TNMCDC( 'REEL', MOVALS, MNVALS )
      MNXYZS = MNVALS + 3 * MXSOMM
C
C     OPTIONS DE LA VISEE POUR VOIR L'OBJET ET SES ZONES DE COULEURS
 310  IF( MODECO0 .EQ. -11 ) THEN
         NMTCL = 90
         CALL VISEE1
      ELSE
         CALL VISE2D( NMTCL )
      ENDIF
      IF( NMTCL .LT. 0 ) GOTO 100
C
C     INITIALISATION DE TRANSLATION ZOOM
      IF( LORBITE .NE. 0 ) THEN
         CALL ZOOM2D0( NOTYEV )
         IF( NOTYEV .EQ. 0 ) GOTO 310
      ENDIF
C
C     BOUCLE SUR LES CAS A TRACER EN 2D
C     ---------------------------------------------------------------
 315  DO NCAS = NCAS0, NCAS1
C
C        L'ECRAN PIXELS EST EFFACE (PAS DE SCINTILLEMENT)
         CALL EFFACEMEMPX

C        TRACE EFFECTIF DES AXES
         NETAXE = 0
         CALL TRAXE2
C
C        TEMPS DE CALCUL DU VECTEUR NCAS
         TEMPS = TIMES( NCAS )
C
C        BOUCLE SUR LES DIFFERENTS TYPES D'EF DU MAILLAGE
         DO 320 I = 0, NBTYEL-1
C
C           L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF
            MNELE = MCN( MNELEM + I )
C
C           LE NUMERO DU TYPE DES ELEMENTS FINIS
            NUTYEL = MCN( MNELE + WUTYEL )

            IF( NUTYEL .EQ. 30 ) GOTO 320
C           PAS DE TRACE DES 6CUBES, SEULEMENT DES 3Q1C EXTRAITS
C
C           LE NOMBRE DE TELS ELEMENTS
            NBELEM = MCN( MNELE + WBELEM )
C
C           LES CARACTERISTIQUES DE L'ELEMENT FINI
            CALL ELTYCA( NUTYEL )
C
C           L'ADRESSE MCN DU TABLEAU 'NPEF' POUR CE TYPE D'EF
            MNPGEL = MNELE + WUNDEL
            IF( NDPGST .GE. 2 ) THEN
               MNPGEL = MNPGEL + MCN(MNELE+WBELEM) * MCN(MNELE+WBNDEL)
            ENDIF
C
C           TRACE 2D EFFECTIF DES ZONES ISOTHERMES DE TEMPERATURE
C           PASSAGE PAR UN SP AFIN DE BENEFICIER DES INDICES FORTRAN EN CLAIR
            CALL TRZON2( TMIN,   TMAX,  NDIM,
     %                   NCAS,   NCAS0, NCAS1, NTDL,
     %                   NTYP,   TEMPER, dptemp,
     %                   NUTYEL, NBELEM,
     %                   NBNOE , MCN(MNELE+WUNDEL), NBPOE, MCN(MNPGEL),
     %                   NBCOOR, MCN(MNNOEU+WNBNOE), MCN(MNNOEU+WYZNOE),
     %                   NBPOI , MCN(MNPOGE+WYZPOI),
     %                   MXSOMM, MCN(MNSOLE), MCN(MNCOPO),
     %                   MXPIL3, MCN(MNPIL3),
     %                   MCN(MNVALS), MCN(MNFBAS), MCN(MNXYZS) )

 320     CONTINUE
C
C        AFFICHAGE DES VALEURS EXTREMES
         CALL XVTYPETRAIT( LIGCON )
         IF( NCAS .EQ. NCAMIN ) THEN
            MN = MNPOGE+WYZPOI-NBCOOR+NBCOOR*NOEMIN
            CALL SYMBOLE2D( NCNOIR, RMCN(MN), RMCN(MN+1), '.Min' )
         ENDIF
         IF( NCAS .EQ. NCAMAX ) THEN
            MN = MNPOGE+WYZPOI-NBCOOR+NBCOOR*NOEMAX
            CALL SYMBOLE2D( NCNOIR, RMCN(MN), RMCN(MN+1), '.Max' )
         ENDIF

C        CALCUL DU MIN ET MAX DU CAS NCAS
         TMINCAS = 1D100
         TMAXCAS =-1D100
         IF( NTYP .EQ. 0 ) THEN
            DO K = 1,NTDL
               V = TEMPER( K, NCAS )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ELSE
            DO K = 1,NTDL
               V = dptemp( NCAS )%dptab( K )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ENDIF

         TMINC = REAL( TMINCAS )
         TMAXC = REAL( TMAXCAS )
C
C        TRACE LA LEGENDE DU TRACE DES ZONES
         CALL LEGZONTH( KNOMOB, NCAS, NOPROJ, MODECO,
     %                  TMIN, TMAX, TMINC, TMAXC )
C
C        MISE SUR FICHIER NomfgifBoImage.xwd puis NomfgifNoImage.jpg
C        DE LA PIXMAP de la FENETRE X11 ACTUELLE
         CALL VIDEO1( NOMFGIF, NCAS )
C
C        ATTENDRE POUR LIRE LE TRACE
         CALL ATTENDSEC( TEMP2TRAC )
C
C        FIN DE LA BOUCLE SUR LES CAS  ------------------------
      ENDDO
C
C     CONSTRUIRE le FICHIER VIDEO Nomfic.gif A PARTIR DES FICHIERS
C     CONSTRUITS de NOMS NomfgifNoImag.jpg
C     ------------------------------------------------------------
      CALL VIDEOFIN( NOMFGIF )
C
C     RETOUR POUR UNE NOUVELLE VISEE
C     ------------------------------
      IF( MODECO0 .EQ. -11 ) GOTO 9900
      NOUVIS = 1
      IF( LORBITE .NE. 0 ) THEN
         IF( NCAS0 .EQ. NCAS1 ) THEN
C           ZOOM  BOUTON ENFONCE et DEPLACE
            CALL ZOOM2D1( NOTYEV )
         ELSE
C           ZOOM  BOUTON ENFONCE et DEPLACE et RELACHE
            CALL ZOOM2D3( NOTYEV )
         ENDIF
         IF( NOTYEV .EQ. 0 ) GOTO 300
         GOTO 315
      ELSE
         CALL CLICSO
      ENDIF
      GOTO 300
C
C     ********
C     OBJET 3D
C     ********
 400  IF( NOUVIS .NE. 0 ) GOTO 450
C
C     CREATION OU REDECOUVERTE DU TMS OBJET>>>FACE
      CALL HACHOB( KNOMOB, 4, NTFAOB, MNFAOB, IERR )
      IF( IERR .NE. 0 ) GOTO 9900
C
C     LE NOMBRE DE FACES FRONTALIERES
      NBFAFR = MCN( MNFAOB + WBFAFR )
C
C     LE QUADRANGLE DEGRE 2 DONNE AU PLUS 8 TRIANGLES
      MXTRFF = NBFAFR * 8
C
      IF( MNTRFF .GT. 0 ) CALL TNMCDS( 'ENTIER', 3*MXTRFF, MNTRFF )
      IF( MNBAQ2 .GT. 0 ) CALL TNMCDS( 'REEL',   4*NBFAFR, MNBAQ2 )
      IF( MNNUTR .GT. 0 ) CALL TNMCDS( 'ENTIER', MXTRFF,   MNNUTR )
      IF( MNDTFF .GT. 0 ) CALL TNMCDS( 'REEL',   MXTRFF,   MNDTFF )
C
C     ADRESSAGE ET OUVERTURE DU TABLEAU NSTRFF
      CALL TNMCDC( 'ENTIER', 3*MXTRFF, MNTRFF )
      CALL TNMCDC( 'REEL',   4*NBFAFR, MNBAQ2 )
      CALL TNMCDC( 'REEL',   MXTRFF,   MNDTFF )
      CALL TNMCDC( 'ENTIER', MXTRFF,   MNNUTR )
C
C     OPTIONS DE LA VISEE POUR VOIR L'OBJET ET SES ZONES THERMIQUES
 450  IF( MODECO0 .EQ. -11 ) THEN
C        LES PARAMETRES DE LA VISEE ACTUELLE
         CALL VISEE1
         NMTCL = 90
      ELSE
         CALL VISE3D( NMTCL )
      ENDIF
      IF( NMTCL .LT. 0 ) GOTO 100
C
C     INITIALISATION DE L'ORBITE ZOOM DEPLACEMENT
      IF( LORBITE .NE. 0 ) THEN
         CALL ORBITE0( NOTYEV )
         IF( NOTYEV .EQ. 0 ) GOTO 450
      ENDIF
C
C     BOUCLE SUR LES CAS A TRACER EN 3D
C     ---------------------------------------------------------------
 500  DO NCAS = NCAS0, NCAS1
C
C        TEMPS DE CALCUL DU VECTEUR NCAS
         TEMPS = TIMES( NCAS )
C
C        CREATION DU TABLEAU DES TRIANGLES DES FACES FRONTALIERES
         CALL PITRFF( NBPOI , NCAS0,  NCAS1,  NCAS,
     %                NTYP,   TEMPER, dptemp,
     %                MNELEM, MNPOGE, MNFAOB, NDPGST,
     %                MNTRFF, NBTRFF, MNBAQ2, NBBAQ2 )
C
C        CREATION DU TABLEAU DES DISTANCES A L'OEIL DU BARYCENTRE
C        DES TRIANGLES DES FACES FRONTALIERES DE L'OBJET
         CALL DIPITR( NBCOOR, NBPOI , RMCN(MNPOGE+WYZPOI),
     %                NBTRFF, MCN(MNTRFF), RMCN(MNBAQ2),
     %                RMCN(MNDTFF), MCN(MNNUTR) )
C
C        LE TRI PAR TAS DE CETTE DISTANCE
C        LA FACE LA PLUS PROCHE EST LA PREMIERE
         CALL TRITRP( NBTRFF, RMCN(MNDTFF), MCN(MNNUTR) )
C
C        EFFACE L'ECRAN PIXELS
         CALL EFFACEMEMPX

C        TRACE EFFECTIF DES AXES
         NETAXE = 0
         CALL TRAXE3
C
C        TRACE DES TRIANGLES FRONTALIERS EN PALETTE ARC EN CIEL
C        ET SELON LA TEMPERATURE ET L'ALGORITHME DU PEINTRE
         CALL T3TFZC( NBCOOR, NTDL, RMCN(MNPOGE+WYZPOI),
     %                NBTRFF, MCN(MNTRFF), RMCN(MNBAQ2),
     %                NCAS0,  NCAS1, NCAS,
     %                NTYP,   TEMPER, dptemp,
     %                TMIN,   TMAX, MCN(MNNUTR) )
C
C        TRACE DU POINT DE TEMPERATURE MINIMALE ET MAXIMALE
         CALL XVTYPETRAIT( LIGCON )
         IF( NCAS .EQ. NCAMIN ) THEN
            MN = MNPOGE + WYZPOI + NBCOOR*NOEMIN -NBCOOR
            CALL SYMBOLE3D( NCNOIR, RMCN(MN), '.Min' )
         ENDIF
         IF( NCAS .EQ. NCAMAX ) THEN
            MN = MNPOGE + WYZPOI + NBCOOR*NOEMAX -NBCOOR
            CALL SYMBOLE3D( NCNOIR, RMCN(MN), '.Max' )
         ENDIF

C        CALCUL DU MIN ET MAX DU CAS NCAS
         TMINCAS = 1D100
         TMAXCAS =-1D100
         IF( NTYP .EQ. 0 ) THEN
            DO K = 1,NTDL
               V = TEMPER( K, NCAS )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ELSE
            DO K = 1,NTDL
               V = dptemp( NCAS )%dptab( K )
               IF( V .LT. TMINCAS ) TMINCAS = V
               IF( V .GT. TMAXCAS ) TMAXCAS = V
            ENDDO
         ENDIF

         TMINC = REAL( TMINCAS )
         TMAXC = REAL( TMAXCAS )
C
C        TRACE LA LEGENDE DU TRACE DES ZONES DE COULEURS
         CALL LEGZONTH( KNOMOB, NCAS, NOPROJ, MODECO,
     %                  TMIN,   TMAX, TMINC,  TMAXC )
C
C        MISE SUR FICHIER NomfgifBoImage.xwd puis NomfgifNoImage.jpg
C        DE LA PIXMAP de la FENETRE X11 ACTUELLE
         CALL VIDEO1( NOMFGIF, NCAS )
C
C        ATTENDRE POUR LIRE LE TRACE
         CALL ATTENDSEC( TEMP2TRAC )
C
C        FIN DE LA BOUCLE SUR LES CAS ------------------------------
      ENDDO
C
C     CONSTRUIRE le FICHIER VIDEO Nomfic.gif A PARTIR DES FICHIERS
C     CONSTRUITS de NOMS NomfgifNoImag.jpg
C     ------------------------------------------------------------
      CALL VIDEOFIN( NOMFGIF )
C
C     RETOUR POUR UNE NOUVELLE VISEE
C     ------------------------------
      IF( MODECO0 .EQ. -11 ) GOTO 9900
      NOUVIS = 1
      IF( LORBITE .NE. 0 ) THEN
         IF( NCAS0 .EQ. NCAS1 ) THEN
C           ORBITE BOUTON ENFONCE et DEPLACE
            CALL ORBITE1( NOTYEV )
         ELSE
C           ORBITE BOUTON ENFONCE et DEPLACE et RELACHE
            CALL ORBITE3( NOTYEV )
         ENDIF
         IF( NOTYEV .EQ. 0 ) GOTO 450
         GOTO 500
      ELSE
C        POUR LIRE LE TRACE AVANT D'AFFICHER UN MENU
         CALL CLICSO
      ENDIF
      GOTO 450
C
C     DESTRUCTION DES TABLEAUX MC DEVENUS INUTILES
 9900 IF( MNSOLE .GT. 0 ) CALL TNMCDS( 'REEL2' , MOSOLE,   MNSOLE )
      IF( MNPIL3 .GT. 0 ) CALL TNMCDS( 'ENTIER', MOPIL3,   MNPIL3 )
      IF( MNVALS .GT. 0 ) CALL TNMCDS( 'REEL'  , MOVALS,   MNVALS )
      IF( MNTRFF .GT. 0 ) CALL TNMCDS( 'ENTIER', 3*MXTRFF, MNTRFF )
      IF( MNBAQ2 .GT. 0 ) CALL TNMCDS( 'REEL'  , 4*NBFAFR, MNBAQ2 )
      IF( MNNUTR .GT. 0 ) CALL TNMCDS( 'ENTIER', MXTRFF,   MNNUTR )
      IF( MNDTFF .GT. 0 ) CALL TNMCDS( 'REEL',   MXTRFF,   MNDTFF )
C
      RETURN
      END
