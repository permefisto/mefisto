      SUBROUTINE TE2LAG( D2PI,   NOAXIS, X,      NDSM,
     %                   NBELEM, NUELEM,
     %                   NTDL,   NUNDEL, NBPIFN,
     %                   NBPOLA, NPIA,   POIDSA, POLYA,  DPOLYA,
     %                   NBCOTE, NOOBLA, NUMILI, NUMALI, LTDELI,
     %                   NBPOLY, NPI,    POLY,
     %                   NOOBSF, NUMISU, NUMASU, LTDESU,
     %                   POIDEL, FOMEGA, COND,   DCOND,
     %                   F1,     F2,     DP,     DDP,
     %                   IDPARE, DPARET, TEMP,   FLUXPT, FGAMMA,
     %                   COPNFN, FLUXNP, FLUXTO,
     %                   NUEF,   NBTTEF, NBPTAR,
     %                   MOARET, MXARET, LARETE,
     %                   CPNFAF, SFLUAF, ERTHEF, H1TEEF,
     %                   REELVI, ERTHAF )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  EF LAGRANGE ISOPARAMETRIQUE EN 2D (TRIANGLE P2 QUADRANGLE Q1 Q2)
C -----  CALCUL DU FLUX NORMAL SUR CHAQUE ARETE FRONTIERE
C        CALCUL DU SAUT DU FLUX NORMAL SUR CHAQUE ARETE INTERNE
C        CALCUL DE L'ESTIMATEUR D'ERREUR (BERNARDI, METIVET, VERFURTH)
C        CALCUL DE LA NORME H1 DE LA TEMPERATURE SUR CHAQUE EF
C
C ENTREES :
C ---------
C D2PI   : 2 FOIS PI
C NOAXIS : 1 SI PROBLEME AXISYMETRIQUE, 0 SINON
C X      : COORDONNEES RAYON ET COTE DES NBPOLY POINTS DE L ELEMENT
C          OU X Y DES NBPOLY POINTS DE L'ELEMENT
C NDSM   : NOMBRE DE SECONDS MEMBRES
C NBELEM : NOMBRE D'ELEMENTS FINIS DE CE TYPE
C NUELEM : NUMERO DE L'ELEMENT FINI PARMI CEUX DE CE TYPE
C NTDL   : NOMBRE TOTAL DE DL DE TEMPERATURE POUR TOUT LE MAILLAGE
C NBPOLY : NOMBRE DE POLYNOMES = NOMBRE DE NOEUDS DE L'EF
C NUNDEL : NUMERO DES NOEUDS DES ELEMENTS FINIS
C
C NBPIFN : NOMBRE TOTAL DE POINTS SUR LES ARETES DE L'EF OU SONT CALCULES
C          LES FLUX NORMAUX DE CHALEUR   (=NPIA*NBCOTE)
C NBPOLA : NOMBRE DE POLYNOMES DE BASE SUR UNE ARETE DE L'EF
C NPIA   : NOMBRE DE POINTS D INTEGRATION SUR UNE ARETE DE L'EF
C POIDSA : POIDS DES POINTS D INTEGRATION SUR UNE ARETE DE L'EF
C POLYA  : VALEUR DES POLYNOMES DE BASE AUX POINTS D INTEGRATION D'UNE ARETE
C DPOLYA : VALEUR DU GRADIENT DE CES MEMES POLYNOMES AUX POINTS ...
C
C NBCOTE : NOMBRE DES ARETES DE L'EF
C NOOBLA : NUMERO DES LIGNES DES ARETES DE L'EF
C NUMILI : NUMERO MINIMAL DES OBJETS LIGNES
C NUMALI : NUMERO MAXIMAL DES OBJETS LIGNES
C LTDELI : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DE THERMIQUE
C          DES OBJETS LIGNES
C
C NOOBSF : NUMERO DE L'OBJET SURFACE DE CET EF
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DE THERMIQUE
C          DES OBJETS SURFACES
C
C IDPARE : IDPARE(K) ADRESSE DANS DPARET DE LA 1-ERE VARIABLE DE
C          L'ARETE K DANS DPARET
C DPARET : DPARET(I,J,L,K)=DPARET(I,J,IDPARE(K)+L)
C          DERIVEE / XI DE LA J-EME FONCTION DE BASE AU L-EME POINT
C          DE LA K-EME ARETE DE L EF DE REFERENCE SURFACE
C
C TEMP   : TEMPERATURE EN TOUS LES NOEUDS DU MAILLAGE
C FLUXPT : TABLEAU AUXILIAIRE DE NBPOLY * NPIA REELS DOUBLE PRECISION
C
C NUEF   : NUMERO DE L'EF ACTUEL PARMI LA TOTALITE DES EF DE L'OBJET
C NBTTEF : NOMBRE TOTAL D'EF DE L'OBJET
C NBPTAR : NOMBRE MAXIMAL DE POINTS D'INTEGRATION PAR ARETE (MAX=3)
C
C MOARET : NOMBRE D'ENTIERS PAR ARETE DU TABLEAU LARETES
C MXARET : NOMBRE D'ARETES DECLAREES POUR LE HACHAGE ET LE TABLEAU ARETES
C LARETE : TABLEAU DE HACHAGE DES ARETES DU MAILLAGE DE L'OBJET
C          LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C          LARETE(4,I)= NUMERO DU 1-ER  TOUQ CONTENANT CETTE ARETE
C                       >0 SI ARETE   DIRECTE DANS CE TOUQ
C                       <0 SI ARETE INDIRECTE DANS CE TOUQ
C          SI L'ARETE APPARTIENT A 2 TOUQ ALORS
C          LARETE(5,I)= NUMERO DU 2-EME TOUQ CONTENANT CETTE ARETE
C                       >0 SI ARETE   DIRECTE DANS CE TOUQ
C                       <0 SI ARETE INDIRECTE DANS CE TOUQ
C                       =0 SI ARETE APPARTENANT A UN SEUL TOUQ
C          LARETE(6,I)= NUMERO DANS LARETE DE L'ARETE SUIVANTE
C                       SOIT DANS LE CHAINAGE D'UNE LIGNE J ENTRE NUMILF ET NUMX
C                       SOIT DANS LE CHAINAGE DES ARETES FRONTALIERES
C                       0 SI C'EST LA DERNIERE
C
C          L1ARFR = NUMERO DE LA PREMIERE ARETE CHAINEE FRONTALIERE DANS LARETE
C          L1ARIN = NUMERO DE LA PREMIERE ARETE CHAINEE INTERFACE   DANS LARETE
C          L1LGFR(J)  = NUMERO DE LA PREMIERE ARETE CHAINEE DE LA LIGNE J
C                       POUR J COMPRIS ENTRE NUMILF ET NUMXLF
C REELVI : IDENTIFICATEUR POUR INDIQUER LA NON INITIALISATION D'UN REEL
C
C NOOBSF : NUMERO DE L'OBJET SURFACE DE CET ELEMENT
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES CONDUCTIVITE
C          DES OBJETS SURFACES
C F1     : COORDONNEES XX DES NPI POINTS D INTEGRATION DE L ELEMENT
C F2     : COORDONNEES YY DES NPI POINTS D INTEGRATION DE L ELEMENT
C POIDEL : DELTA * POIDS(NPI) DES NPI POINTS D INTEGRATION
C DP     : GRADIENT DES POLYNOMES DE BASE SUR L ELEMENT COURANT
C DDP    : HESSIEN  DES POLYNOMES DE BASE SUR L ELEMENT COURANT
C          DANS L'ORDRE DDXX, DDYX, DDYY
C
C SORTIES:
C --------
C COPNFN : 2 COORDONNEES DES NBPIFN POINTS ET DE LA NORMALE SUR LES
C          ARETES DES EF OU SONT CALCULES LES FLUX NORMAUX DE TEMPERATURE
C FLUXNP : FLUX NORMAL EN LES NBPIFN POINTS DES ARETES DES EF DE CE TYPE
C FLUXTO : FLUX NORMAL SOMME SUR CHAQUE LIGNE DE L'OBJET
C CPNFAF : 3 COORDONNEES DES POINTS D'INTEGRATION ET NORMALES SUR LES ARETES
C SFLUAF : 2 FLUX NORMAUX EN LES POINTS D'INTEGRATION SUR LES ARETES
C ERTHEF : ERREUR L2 A POSTERIORI SUR CHAQUE EF
C H1TEEF : NORME H1 DE LA TEMPERATURE SUR CHAQUE EF ET POUR CHAQUE CAS
C REELVI : TEMOIN DE NON UTILISATION D'UNE VARIABLE DE ERTHAF
C ERTHAF : ERREUR L2 A POSTERIORI SUR CHAQUE ARETE DES EF
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS         MAI 1995
C23456---------------------------------------------------------------012
      include"./incl/donthe.inc"
      include"./incl/cthet.inc"
      include"./incl/cnonlin.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      DOUBLE PRECISION  D2PI, REELVI
      DOUBLE PRECISION  POIDSA(NPIA),
     %                  POLYA(NBPOLA,NPIA),
     %                  DPOLYA(NBPOLA,NPIA)
      DOUBLE PRECISION  FLUXPT(NBPOLY,NPIA)
      DOUBLE PRECISION  POLY(NBPOLY,NPI),
     %                  F1(NPI),F2(NPI),
     %                  POIDEL(NPI),
     %                  DP (2, NBPOLY, NPI),
     %                  DDP(3, NBPOLY, NPI)
      DOUBLE PRECISION  TEMP(NTDL,NDSM),
     %                  FOMEGA(NDSM),
     %                  COND(6),
     %                  DCOND(3,6),
     %                  DREM(9),
     %                  TPPTDP(9,9),
     %                  FGAMMA(NDSM,NPIA),
     %                  ECHANG(9)
      DOUBLE PRECISION  DF(2,2), DF1(2,2), DN(2), DPARET(*), EC, EF
      DOUBLE PRECISION  GL(3), DGL(2), DELTA, H, XYZ(3)
      DOUBLE PRECISION  D(2), DD(3), S, DGLN, HAR, VITEFL(2)
      DOUBLE PRECISION  FLUXTO(NUMILI:NUMALI,1:NDSM),
     %                  FLUXNP(NBPIFN,NBELEM,NDSM),
     %                  SFLUAF(1:2,1:NBPTAR,1:MXARET,1:NDSM),
     %                  ERTHAF(1:MXARET,1:NDSM),
     %                  ERTHEF(1:NBTTEF,1:NDSM),
     %                  H1TEEF(1:NBTTEF,1:NDSM)
C
      REAL              X(NBPOLY,2),
     %                  COPNFN(3,NBPIFN,NBELEM,2),
     %                  CPNFAF(1:3,1:NBPTAR,1:MXARET,1:2)
C
      INTEGER           LARETE(1:MOARET,1:MXARET)
      INTEGER           NOOBLA(NBCOTE)
      INTEGER           LTDESU(1:MXDOTH,NUMISU:NUMASU)
      INTEGER           LTDELI(1:MXDOTH,NUMILI:NUMALI)
      INTEGER           NOPOAR(3), NUNDEL(NBELEM,NBPOLY)
      INTEGER           IDPARE(NBCOTE)
      INTEGER           NOSA(2)
C
C     CONTRIBUTION DES ARETES DE CET EF NUEF A L'ESTIMATEUR D'ERREUR
C     ==============================================================
      H      = 0D0
      LIBREF = MXARET
      NUPTF0 = 0
      DO 100 K=1,NBCOTE
C
C        L'ARETE EST ELLE SUR UNE LIGNE UTILISATEUR?
         NL     = NOOBLA( K )
C        A PRIORI PAS DE F GAMMA OU FLUX NORMAUX EN DONNEE
         LIFGAM = 0
C        A PRIORI PAS DE COEFFICIENT D'ECHANGE EN DONNEE
         LIECHA = 0
CCCC        A PRIORI PAS DE CONTACT (TEMPERATURE IMPOSEE) SUR CETTE ARETE
CCC         LICONT = 0
CCCC
CCCC        L'ARETE A T ELLE SA TEMPERATURE IMPOSEE?
CCC         IF( NL .GT. 0 ) THEN
CCC            IF( LTDELI(LPCONT,NOOBLA(K)) .GT. 0 ) THEN
CCCC              UN TABLEAU CONTACT EXISTE POUR CETTE LIGNE
CCCC              LE FLUX SUR CETTE ARETE NE DOIT PAS ETRE CALCULE
CCCC              POUR L'ESTIMATEUR D'ERREUR
CCC               LICONT = 1
CCC            ENDIF
CCC         ENDIF
C
C        LE NUMERO DES POINTS DU COTE
         NOPOAR(1) = K
         IF( K .NE. NBCOTE ) THEN
            NOPOAR(2) = K+1
         ELSE
            NOPOAR(2) = 1
         ENDIF
C        LE NUMERO DU POINT MILIEU
         NOPOAR(3) = K + NBCOTE
C
C        RECHERCHE DU NUMERO DE CETTE ARETE DANS LE TABLEAU LARETE
         NOSA(1) = NUNDEL( NUELEM, NOPOAR(1) )
         NOSA(2) = NUNDEL( NUELEM, NOPOAR(2) )
         IF( NOSA(1) .GT. NOSA(2) ) THEN
            L       = NOSA(1)
            NOSA(1) = NOSA(2)
            NOSA(2) = L
         ENDIF
         CALL HACHAG( 2, NOSA, MOARET, MXARET, LARETE, 3,
     &                LIBREF, NOARE )
         IF( NOARE .LE. 0 ) THEN
C           ARETE NON RETROUVEE
            WRITE(IMPRIM,*) 'TE2LAG: ARETE ',NOSA,
     %                      ' NON RETROUVEE DANS LARETE'
            IERR = 1
            RETURN
         ENDIF
C
C        NOMBRE D'EF CONTENANT CETTE ARETE
         IF( LARETE(5,NOARE) .NE. 0 ) THEN
            NBEFAR = 2
         ELSE IF( LARETE(4,NOARE) .NE. 0 ) THEN
            NBEFAR = 1
         ELSE
C           ARETE NON RETROUVEE
            WRITE(IMPRIM,*) 'TE2LAG: ARETE ',NOSA,' DANS AUCUN EF'
            IERR = 1
            RETURN
         ENDIF
C
C        CETTE ARETE A T ELLE ETE VUE ?
         IF( ERTHAF(NOARE,1) .EQ. REELVI ) THEN
C           NON: ARETE VUE POUR LA PREMIERE FOIS
            NBVUAR = 0
         ELSE
            NBVUAR = 1
         ENDIF
C
C        LE DEBUT DU TABLEAU DES DERIVEES DES POLYNOMES AUX POINTS
C        D'INTEGRATION NUMERIQUE DE L'ARETE K
         MNDPA = IDPARE( K ) + NBPOLY * NPIA
C
C        MISE A ZERO DU TABLEAU DREM
         CALL AZEROD( NBPOLY, DREM )
C
C        LA LONGUEUR DE L'ARETE K DE CET EF
         HAR = 0D0
C
         DO 20 L=1,NPIA
C
C           CALCUL DES COORDONNEES DU POINT D INTEGRATION L
C           ET DU JACOBIEN
            CALL E22LAG ( NBPOLY, NBPOLA, NOPOAR ,
     %                   POLYA(1,L), DPOLYA(1,L),
     %                   X, GL, DGL, DELTA )
C           EN SORTIE GL=LES 2 COORDONNEES DU POINT D'INTEGRATION SUR E
C
C           RECHERCHE DU TENSEUR SYMETRIQUE DE CONDUCTIVITE AU POINT
C           D'INTEGRATION L DE L'ARETE
            GL(3) = 0D0
            CALL RECOND( 3, NOOBSF, 3, GL,
     %                   LTDESU(LPCOND,NOOBSF), COND )
C
C           LES COORDONNEES DU POINT OU EST CALCULE LE FLUX NORMAL
            NUPTFN = NUPTF0 + L
            COPNFN( 1, NUPTFN, NUELEM, 1 ) = REAL( GL(1) )
            COPNFN( 2, NUPTFN, NUELEM, 1 ) = REAL( GL(2) )
            COPNFN( 3, NUPTFN, NUELEM, 1 ) = 0
C
C           CALCUL DE LA NORMALE: -PRODUIT VECTORIEL(DG/DX1,DG/DX2)
            DGLN  = SQRT( DGL(1) ** 2 + DGL(2) ** 2 )
            DN(1) =  DGL(2) / DGLN
            DN(2) = -DGL(1) / DGLN
C
C           LA CONTRIBUTION A LA LONGUEUR DE L'ARETE
            HAR = HAR + POIDSA(L) * DGLN
C
C           LES COMPOSANTES DU VECTEUR NORMAL OU EST CALCULE LE FLUX NORMAL
            COPNFN( 1, NUPTFN, NUELEM, 2 ) = REAL( DN(1) )
            COPNFN( 2, NUPTFN, NUELEM, 2 ) = REAL( DN(2) )
            COPNFN( 3, NUPTFN, NUELEM, 2 ) = 0
C
            IF( NBVUAR .EQ. 0 ) THEN
C              ARETE VUE POUR LA PREMIERE FOIS
C              INITIALISATION DES COORDONNEES DES POINTS D'INTEGRATION
               CPNFAF( 1, L, NOARE, 1 ) = REAL( GL(1) )
               CPNFAF( 2, L, NOARE, 1 ) = REAL( GL(2) )
               CPNFAF( 3, L, NOARE, 1 ) = 0
C
C              LES COMPOSANTES DU VECTEUR NORMAL OU EST CALCULE LE FLUX NORMAL
               CPNFAF( 1, L, NOARE, 2 ) = REAL( DN(1) )
               CPNFAF( 2, L, NOARE, 2 ) = REAL( DN(2) )
C              PROTECTION DE DGLN
               CPNFAF( 3, L, NOARE, 2 ) = REAL( DGLN )
C
C              CONTRIBUTION DE F GAMA SUR L'ARETE
               IF( NL .GT. 0 ) THEN
                  XYZ(1) = GL(1)
                  XYZ(2) = GL(2)
                  XYZ(3) = 0D0
C
                  IF( LTDELI(LPSOUR,NOOBLA(K)) .GT. 0 ) THEN
C                    UN TABLEAU SOURCE (FLUX NORMAL) EXISTE POUR CETTE LIGNE
                     LIFGAM = 1
C                    RECHERCHE DU FLUX NORMAL AU POINT D'INTEGRATION NUMERIQUE
                     CALL RESOUR( 2,NOOBLA(K), 3, XYZ,
     %                            LTDELI(LPSOUR,NOOBLA(K)), FGAMMA(1,L))
                  ENDIF
C
                  IF( LTDELI(LPECHA,NOOBLA(K)) .GT. 0 ) THEN
C                    UN TABLEAU ECHANGE EXISTE POUR CETTE LIGNE
                     LIECHA = 1
C                    CALCUL DU COEFFICIENT D'ECHANGE SUR CETTE ARETE
                     CALL REECHA( 2,NOOBLA(K), 3, XYZ,
     %                            LTDELI(LPECHA,NOOBLA(K)), ECHANG(L) )
                  ENDIF
               ENDIF
            ENDIF
C
C           CALCUL DE DF(BL) AU POINT D INTEGRATION DE L ARETE REFERENCE
            CALL MATJAC( 2, NBPOLY, X, DPARET(MNDPA), DF )
C
C           CALCUL DE DF(BL) ** -1
            CALL DFM1R2( DF, DELTA, DF1 )
C
C           D = TRANSPOSE(DN) * COND
            CALL TABSZD( 1, 2, DN, COND, D )
C
C           DD = D * ((DF) ** -1)
            CALL AB0D( 1, 2, 2, D, DF1, DD )
C
C           FLUXPT = DD * DP ARETE AU POINT
            CALL AB0D( 1, 2, NBPOLY, DD, DPARET(MNDPA), FLUXPT(1,L) )
C
            IF( NL .GT. 0 ) THEN
C              CONTRIBUTION A L'INTEGRALE NUMERIQUE DU FLUX NORMAL SUR L'ARETE
C              LE POIDS * NORME DU VECTEUR NORMAL A DGL
               S = POIDSA(L) * DGLN
C              SI ELEMENT AXISYMETRIQUE PRODUIT PAR 2 * PI * R
               IF( NOAXIS .NE. 0 ) THEN
                  S = S * D2PI  * GL( 1 )
               ENDIF
               DO 10 I=1,NBPOLY
                  DREM(I) = DREM(I) + S * FLUXPT(I,L)
 10            CONTINUE
            ENDIF
C
C           PASSAGE AU POINT D'INTEGRATION NUMERIQUE SUIVANT
            MNDPA = MNDPA + 2 * NBPOLY
 20      CONTINUE
C
C        LA LONGUEUR DE L'ARETE MAXIMALE
         H = MAX( H, HAR )
C
         DO 90 N=1,NDSM
C
C           LE FLUX NORMAL AUX POINTS D'INTEGRATION NUMERIQUE DE CETTE ARETE
C           TEMOIN DE PASSAGE SUR CETTE ARETE (REELVI EST REMPLACE)
            ERTHAF(NOARE,N) = 0D0
C
            DO 40 L=1,NPIA
               S = 0
               DO 30 I=1,NBPOLY
                  S = S - FLUXPT(I,L) * TEMP( NUNDEL(NUELEM,I), N )
 30            CONTINUE
               NUPTFN = NUPTF0 + L
               FLUXNP( NUPTFN, NUELEM, N ) = S
C
C              FLUX NORMAL AU POINT L DE L'ARETE NOARE POUR LE CAS N
               IF( NBVUAR .EQ. 0 ) THEN
C                 VUE POUR LA PREMIERE FOIS DANS LE SENS DE L'ARETE
                  NUPIAR = L
               ELSE
C                 VUE POUR LA SECONDE FOIS DANS LE SENS INVERSE DE L'ARETE
                  NUPIAR = NPIA + 1 - L
               ENDIF
               SFLUAF( 1+NBVUAR, NUPIAR, NOARE, N ) = S
C
               IF( NBEFAR .EQ. 2 .AND. NBVUAR .EQ. 1 ) THEN
C                 ARETE COMMUNE A 2 EF VUE POUR LA SECONDE FOIS
C                 LE POIDS * NORME DU VECTEUR NORMAL A DGL
                  S = POIDSA(L) *  CPNFAF( 3, NUPIAR, NOARE, 2 )
C                 SI ELEMENT AXISYMETRIQUE PRODUIT PAR 2 * PI * R
                  IF( NOAXIS .NE. 0 ) THEN
                     S = S * D2PI  * CPNFAF( 1, NUPIAR, NOARE, 1 )
                  ENDIF
C                 LE SAUT EST ( ATTENTION: + CAR NORMALES DANS DES SENS OPPOSES
                  S = S * ( SFLUAF(2,NUPIAR,NOARE,N)
     %                    + SFLUAF(1,NUPIAR,NOARE,N) ) ** 2
C                 ET LA CONTRIBUTION A L'ERREUR L2 DE L'ARETE
                  ERTHAF(NOARE,N) =  ERTHAF(NOARE,N) + S
               ENDIF
C
               IF( NBEFAR .EQ. 1 ) THEN
C                 ARETE FRONTALIERE
                  EC = 0D0
                  IF( LIECHA .NE. 0 ) THEN
C                    CALCUL DE ECHANGE(BL) * [PA(BL)] * TEMPERATURE(ARETE)
                     DO 32 I=1,NBPOLA
                        EC = EC + POLYA(I,L) *
     %                            TEMP( NUNDEL(NUELEM,NOPOAR(I)), N )
 32                  CONTINUE
                     EC = EC * ECHANG(L)
                  ENDIF
C
                  EF = 0D0
                  IF( LIFGAM .NE. 0 ) THEN
C                    F GAMMA AU POINT D'INTEGRATION L ET POUR LE CAS N
                     EF = FGAMMA(N,L)
                  ENDIF
C
C                 || F GAMMA - G TEMP - D COND D TEMP || L2(ARETE)
C                 LE POIDS * NORME DU VECTEUR NORMAL A DGL
                  S = POIDSA(L) *  CPNFAF( 3, L, NOARE, 2 )
C                 SI ELEMENT AXISYMETRIQUE PRODUIT PAR 2 * PI * R
                  IF( NOAXIS .NE. 0 ) THEN
                     S = S * D2PI  * ABS( CPNFAF( 1, L, NOARE, 1 ) )
                  ENDIF
                  ERTHAF(NOARE,N) = ERTHAF(NOARE,N) + S *
     %               ( EF - EC + SFLUAF( 1+NBVUAR, L, NOARE, N ) ) ** 2
               ENDIF
 40         CONTINUE
C
C           LA CONTRIBUTION A L'ERREUR L2 DE L'ARETE * (H ** 1/2)
            ERTHAF(NOARE,N) =  SQRT( ERTHAF(NOARE,N) * HAR )
C
C           LA CONTRIBUTION AUX FLUX NORMAUX DE L'ARETE K DE L'EF
            IF( NL .GT. 0 ) THEN
               S = 0
               DO 80 I=1,NBPOLY
                  S = S + DREM(I) * TEMP( NUNDEL(NUELEM,I), N )
 80            CONTINUE
C              SOMMATION DES FLUX POUR LA LIGNE FRONTIERE NL
               FLUXTO( NL, N ) = FLUXTO( NL, N ) + S
            ENDIF
C
 90      CONTINUE
C
C        MISE A JOUR DU NUMERO DU DERNIER POINT OU EST CALCULE LE FLUX NORMAL
         NUPTF0 = NUPTF0 + NPIA
 100  CONTINUE
C
C     CONTRIBUTION DE LA SURFACE DE CET EF NUEF A L'ESTIMATEUR D'ERREUR
C     =================================================================
C     INITIALISATION DE L'ESTIMATEUR D'ERREUR SUR CET EF POUR LES NDSM CAS
      DO 105 N=1,NDSM
         ERTHEF(NUEF,N) = 0D0
 105  CONTINUE
C
C     MISE A ZERO DU TABLEAU TPPTDP
      DO 120 I=1,NBPOLY
         DO 110 J=1,NBPOLY
            TPPTDP(I,J) = 0D0
 110     CONTINUE
 120  CONTINUE
      LIFOME = 0
C
      DO 180 L=1,NPI
C
         IF( LTDESU(LPSOUR,NOOBSF) .GT. 0 ) THEN
C           IL EXISTE UN TABLEAU SOURCE POUR CETTE SURFACE
            LIFOME = 1
C           LA VALEUR DES SOURCES SURFACIQUES EN CE POINT
            XYZ(1) = F1(L)
            XYZ(2) = F2(L)
            XYZ(3) = 0D0
            CALL RESOUR( 3, NOOBSF, 3, XYZ,
     %                   LTDESU(LPSOUR,NOOBSF),FOMEGA)
         ENDIF
C
C        CALCUL DU "LAPLACIEN" DE LA TEMPERATURE AU POINT GL
C        RECHERCHE DU TENSEUR SYMETRIQUE DE CONDUCTIVITE AU POINT
C        D'INTEGRATION L
         GL(1) = F1(L)
         GL(2) = F2(L)
         GL(3) = 0D0
         CALL RECOND( 3, NOOBSF, 3, GL,
     %                LTDESU(LPCOND,NOOBSF),COND )
C        PRISE EN COMPTE DE LA SYMETRIE DU TENSEUR 2 2 DE CONDUCTIVITE
         COND(2) = 2 * COND(2)
C
C        [ C11, 2*C21, C22] * [DDP] => DREM
         CALL AB0D( 1, 3, NBPOLY, COND, DDP(1,1,L), DREM )
C
C        RECHERCHE DU GRADIENT DU TENSEUR SYMETRIQUE DE CONDUCTIVITE
C        AU POINT D'INTEGRATION L
         IF( LTDESU(LPDCON,NOOBSF) .GT. 0 ) THEN
            XYZ(1) = F1(L)
            XYZ(2) = F2(L)
            XYZ(3) = 0D0
            CALL REDCON( 3, NOOBSF, 3, XYZ,
     %                   LTDESU(LPDCON,NOOBSF),DCOND )
C           DCOND(i,j) = D/DXi CONDUCTIVITE j  1:11, 2:21, 3:22, ...
            DD(1) = DCOND(1,1) + DCOND(2,2)
            DD(2) = DCOND(1,2) + DCOND(2,3)
            CALL AB1D( 1, 2, NBPOLY, DD, DP(1,1,L), DREM )
         ENDIF
C
         DO 140 N=1,NDSM
            S = 0D0
C
C           LE LAPLACIEN DE LA TEMPERATURE AU POINT D'INTEGRATION L
            DO 130 I=1,NBPOLY
               S = S + DREM(I) * TEMP( NUNDEL(NUELEM,I), N )
 130        CONTINUE
C
C           LA CONTRIBUTION DE FOMEGA (SOURCE SURFACIQUE) ET DU LAPLACIEN
C           A L'ESTIMATEUR D'ERREUR EN CE POINT D'INTEGRATION
            IF( LIFOME .NE. 0 ) S = S + FOMEGA(N)
C
C           LA CONTRIBUTION EVENTUELLE DU TERME: -VITESSE * GRADIENT TEMPERATURE
C           ACTUELLEMENT CE TERME EST SOUSTRAIT DU SECOND MEMBRE + POINT FIXE
            IF( LTDESU(LPVIFL,NOOBSF) .GT. 0 ) THEN
C              LA TEMPERATURE AU POINT D'INTEGRATION L
               TEMPEL = 0D0
               DO 135 I=1,NBPOLY
                  TEMPEL = TEMPEL + POLY(I,L) * TEMP(NUNDEL(NUELEM,I),N)
 135           CONTINUE
C              LES 2 COMPOSANTES DE LA VITESSE AU POINT D'INTEGRATION L
               XYZ(1) = F1(L)
               XYZ(2) = F2(L)
               XYZ(3) = 0D0
               CALL REVIFL( 3, NOOBSF, 2, 3, XYZ,
     %                      LTDESU(LPVIFL,NOOBSF), VITEFL )
               DO 138 J=1,2
                  DD(J) = 0D0
                  DO 137 I=1,NBPOLY
                     DD(J) = DD(J) + DP(J,I,L)*TEMP(NUNDEL(NUELEM,I),N)
 137              CONTINUE
 138           CONTINUE
               S = S - ( VITEFL(1) * DD(1) + VITEFL(2) * DD(2) )
            ENDIF
C
C           LA CONTRIBUTION AU CARRE DU RESIDU DU POINT D'INTEGRATION L
            ERTHEF(NUEF,N)= ERTHEF(NUEF,N) + POIDEL(L) *  (S**2)
C
 140     CONTINUE
C
C        LE CALCUL DU TABLEAU INTEGRALE( TP P + TDP DP ) POUR LA NORME H1
C        DE LA TEMPERATURE
         DO 160 I=1,NBPOLY
            DO 150 J=1,NBPOLY
               TPPTDP(I,J) = TPPTDP(I,J) + POIDEL(L) *
     %         ( POLY(I,L) * POLY(J,L) +
     %           DP(1,I,L) * DP(1,J,L) + DP(2,I,L) * DP(2,J,L) )
 150        CONTINUE
 160     CONTINUE
C
 180  CONTINUE
C
C     LE PRODUIT PAR H LE DIAMETRE DE L'EF ET LA RACINE CARREE
      DO 190 N=1,NDSM
         ERTHEF(NUEF,N) = SQRT( ERTHEF(NUEF,N) ) * H
 190  CONTINUE
C
C     LA NORME H1 DE LA TEMPERATURE SUR TOUT LE MAILLAGE
      DO 240 N=1,NDSM
         S = 0D0
         DO 230 I=1,NBPOLY
            DO 220 J=1,NBPOLY
               S = S + TPPTDP(I,J) * TEMP( NUNDEL(NUELEM,I), N ) *
     %                               TEMP( NUNDEL(NUELEM,J), N )
 220        CONTINUE
 230     CONTINUE
C        LA CONTRIBUTION DE CET EF A LA NORME H1 DE LA TEMPERATURE
         H1TEEF(NUEF,N) = SQRT( S )
 240  CONTINUE
      END
