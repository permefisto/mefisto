      SUBROUTINE DLFXVG( NBRDLX, NUDLFX, ONDEFX, TGV, NTDL,  ONDTGV )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    PRISE EN COMPTE SUR LE VECTEUR GLOBAL DES COMPOSANTES
C -----    DES VITESSES FIXEES
C
C ENTREES:
C --------
C NBRDLX : NOMBRE DE DL DE L'ONDE FIXES
C          ERREUR SI NBRDLX=0 CAR PAS DE CONDITION AUX LIMITES!
C NUDLFX : TABLEAU MC DES NUMEROS DES DL FIXES
C ONDEFX : TABLEAU MC DES VALEURS DES DL FIXES
C TGV    : VALEUR MULTIPLICATIVE  DES DL FIXES
C NTDL   : NOMBRE DE COMPOSANTES OU DL DE L'ONDE
C
C MODIFIE:
C --------
C ONDTGV : VALEUR DES COEFFICIENTS DU VECTEUR GLOBAL(2,NTDL/2)
C          DES DL DE L'ONDE COMPLEXE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TEXAS A & M University at QATAR     MARS 2011
C23456---------------------------------------------------------------012
      INTEGER           NUDLFX(NBRDLX)
      DOUBLE PRECISION  ONDEFX(NBRDLX), TGV, ONDTGV(NTDL)
C
      DO K=1,NBRDLX
C
C        NUMERO DU DL FIXE DE 1 A NTDL POUR UN VECTEUR(2,NBNOMA)
         NDL = NUDLFX( K )
C
C        VALEUR FIXEE DE LA VITESSE
         ONDTGV( NDL ) = ONDEFX(K) * TGV
C
      ENDDO
C
      RETURN
      END
