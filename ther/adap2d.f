      SUBROUTINE ADAP2D( KNOMOB, NBISO, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ADAPTER LE MAILLAGE D'UN OBJET 2D EN FONCTION DES ISOVALEURS
C -----
C
C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET
C NBISO  : NOMBRE D'ISOTHERMES AVANT L'ADAPTATION DU MAILLAGE
C
C SORTIES:
C --------
C NBISO  : NOMBRE D'ISOTHERMES APRES L'ADAPTATION DU MAILLAGE
C IERR   :  0 SI PAS D'ERREUR
C          >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS         MAI 1995
C23456---------------------------------------------------------------012
      IMPLICIT INTEGER (W)
      PARAMETER     (LIGCON=0)
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___face.inc"
      include"./incl/a___arete.inc"
      include"./incl/ponoel.inc"
      include"./incl/donele.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/a_objet__erreurth.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1),DD
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
C
      CHARACTER*(*)     KNOMOB
      CHARACTER*160     KNOM
      CHARACTER*4       KNUADP
      CHARACTER*8       KNM
      REAL              COMXMI(2,2)
      DOUBLE PRECISION  DTMIN, DTMAX
C
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
C     TRACE OU NON DES TRIANGLES GENERES DANS LA TRIANGULATION
      TRATRI = .TRUE.
C
C     LECTURE DE L'ERREUR SOUHAITEE EN %
      CALL INVITE( 21 )
      NCVALS = 5
C     VALEUR PAR DEFAUT 5%
      R      = 0.05
      CALL LIRRSP( NCVALS , R )
      IF( NCVALS .LE. 0 ) RETURN
      ESTERS = R
      IF( ESTERS .LE. 0 ) THEN
C        SI ERREUR D'ENTREE DE ESTERS ALORS ESTIMATEUR RELATIF SOUHAITE = 5 %
         ESTERS = 0.05
      ENDIF
C
C     TARMAX : TAILLE DE L'ARETE MAXIMALE PERMISE SUR UNE FRONTIERE
      CALL INVITE( 93 )
 2    NCVALS = 0
      CALL LIRRSP( NCVALS , TARMAX )
      IF( NCVALS .LE. 0 ) RETURN
      IF( TARMAX .LE. 0 ) THEN
         GOTO 2
      ENDIF
C
C     NOMBRE DE MOTS POUR UN REEL DOUBLE PRECISION
      MOREE2 = MOTVAR(6)
C
C     DECLARATION DES TABLEAUX
      MNELEM = 0
      MNVISO = 0
      MNVALS = 0
      MNFBAS = 0
      MNCDPE = 0
      MNSOLE = 0
      MNPILT = 0
      MNPILA = 0
      MNVISO = 0
      MNNXYZ = 0
      MNXYZS = 0
      MNPLIS = 0
      MNNVTR = 0
      MNARCH = 0
      MNL1FR = 0
      NCAS   = 1
      MNPXYD = 0
      MNTRIA = 0
      MNNSCT = 0
      MNTRSO = 0
C
      CALL LXLXOU( NTOBJE , KNOMOB , NTLXOB , MNLXOB )
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'ERREUR: OBJET INCONNU ' // KNOMOB
         CALL LEREUR
         IERR = 10
         GOTO 9999
      ENDIF
C
C     RECHERCHE DU NUMERO D'ITERATION DE L'ADAPTATION
      NUITER = 0
C
 3    IF( NUITER .LT. 10 ) THEN
          NCITER = 3
          WRITE(KNUADP(3:3),'(I1)') NUITER
          KNUADP(4:4) = ' '
      ELSE IF( NUITER .LT. 100 ) THEN
          NCITER = 4
          WRITE(KNUADP(3:4),'(I2)') NUITER
      ELSE
         NBLGRC(NRERR) = 1
         KERR(1) = 'TROP D''ITERATIONS D''ADAPTATION'
         CALL LEREUR
         IERR = NUITER
         RETURN
      ENDIF
      KNUADP(1:2) = '.A'
      KNM = 'NSEF' // KNUADP
      CALL LXLXOU( NTLXOB, KNM, NT, MN )
      IF( NT .GT. 0 ) THEN
         NUITER = NUITER + 1
         GOTO 3
      ENDIF
C
C     S'IL S'AGIT DE LA PREMIERE ITERATION D'ADAPTATION ALORS
C          AJOUT DES 2 LIGNES NOM_OBJET.F0CL POUR "FRONTIERE_SANS_CL"
C                             NOM_OBJET.I0CL POUR "INTERFACE_SANS_CL"
      IF( NUITER .EQ. 0 ) THEN
C
C        CALCUL PAR HACHAGE DES ARETES DE L'OBJET A PARTIR DE TOPO+NPEF"
         CALL HAC2AF( KNOMOB, 3, NTARSU, MNARSU, IERR )
         IF( IERR .NE. 0 ) GOTO 9000
C
C        LE NOMBRE D'ENTIERS PAR ARETE
         MOARET = MCN( MNARSU + WOARET )
C        LA MAJORATION DU NOMBRE D'ARETES
         MXARET = MCN( MNARSU + WXARET )
C        LE NOMBRE D'ARETES FRONTALIERES
         NBARFB = MCN( MNARSU + WBARFB )
C        LE NOMBRE D'ARETES INTERFACES
         NBARIN = MCN( MNARSU + WBARIN )
C        LE NUMERO MINIMAL DE LIGNE DE L'OBJET
         NUMILF = MCN( MNARSU + WUMILF )
C        LE NUMERO MAXIMAL DE LIGNE DE L'OBJET
         NUMXLF = MCN( MNARSU + WUMXLF )
C        LE NUMERO DE LA PREMIERE ARETE FRONTALIERE
         L1ARFB = MCN( MNARSU + W1ARFB )
C        LE NUMERO DE LA PREMIERE ARETE INTERFACE
         L1ARIN = MCN( MNARSU + W1ARIN )
C        ADRESSE MCN DU 1-ER MOT DU TABLEAU LARETE
         MNARET = MNARSU + W1LGFR + NUMXLF - NUMILF + 1
C
C        CREATION EFFECTIVE DES 2 LIGNES
C        LIGNE  NOMOBJET"F0CL  ET  NOMOBJET"I0CL
C        AJOUT DE CES 2 LIGNES DANS LA DEFINITION DE L'OBJET
C        AJOUT DU NUMERO DE CES 2 LIGNES SUR LES ARETES DES EF DE L'OBJET
C        (MODIFICATION DES TABLEAUX NULAEL, NLLAEL, NELAEL DES TABLEAUX NPEF" )
         CALL AJAFAI( KNOMOB, NTLXOB,
     %                MOARET, MXARET, NBARFB, NBARIN, L1ARFB, L1ARIN,
     %                MCN(MNARET),
     %                NLARFB, NLARIN, IERR )
         IF( IERR .NE. 0 ) GOTO 9000
      ENDIF
C
C     RECHERCHE DES TABLEAUX TOPOLOGIE SOMMETS NOEUDS POINTS ASSOCIES A L'OBJET
C     ADRESSAGE DES ADRESSES DES TABLEAUX NPEF".... DE CET OBJET
C     =================================================================
      MXTYEL = 7
      CALL TNMCDC( 'ENTIER' , 2*MXTYEL , MNELEM )
      MNTELE = MNELEM + MXTYEL
      CALL NDPGEL( NTLXOB , NTTOPO , MNTOPO ,
     %             NTPOGE , MNPOGE , NTNOEU , MNNOEU ,
     %             NBTYEL , MCN(MNTELE) , MCN(MNELEM) , IERR )
C     NTTOPO : NUMERO      DU TMS 'TOPOLOGIE' DE L'OBJET
C     MNTOPO : ADRESSE MCN DU TMS 'TOPOLOGIE' DE L'OBJET
C     NTPOGE : NUMERO      DU TMS 'XYZPOINT'    DE L'OBJET
C     MNPOGE : ADRESSE MCN DU TMS 'XYZPOINT'    DE L'OBJET
C     NTNOEU : NUMERO      DU TMS 'XYZNOEUD'    DE L'OBJET
C     MNNOEU : ADRESSE MCN DU TMS 'XYZNOEUD'    DE L'OBJET
C     NBTYEL : NOMBRE DE TYPES D'ELEMENTS DU MAILLAGE
C     NTELEM : NUMERO      DU TMS DES NBTYEL TYPES D'ELEMENTS
C     MNELEM : ADRESSE MCN DU TMS DES NBTYEL TYPES D'ELEMENTS
      IF( IERR .NE. 0 ) GOTO 9999
C
C     NDPGST : CODE TRAITEMENT DES XYZ DES SOMMETS POINTS NOEUDS DU MAILLAGE
C              0 : NOEUDS=POINTS=SOMMETS
C              1 : NOEUDS=POINTS#SOMMETS
C              2 : NOEUDS#POINTS=SOMMETS
C              3 : NOEUDS#POINTS#SOMMETS
      NDPGST = MCN( MNTOPO + WDPGST )
      IF( NDPGST .EQ. 0 ) THEN
C        NOEUDS=POINTS=SOMMETS => DEGRE 1 => PAS DE SUBDIVISION
         NBAEFU = 1
      ELSE
C        NOEUDS=POINTS#SOMMETS => DEGRE 2 => SUBDIVISION EN 4 SOUS-TRIANGLES
         NBAEFU = 2
C        SI P3 PROGRAMME ALORS DISCERNER POUR QUE NBAEFU=2 DEVIENNE NBAEFU=3
      ENDIF
C
C     NDIM DIMENSION EFFECTIVE DE L'ESPACE DES COORDONNEES
C     NBPOI  NOMBRE DE POINTS DU MAILLAGE DE L'OBJET
      NBPOI = MCN(MNPOGE+WNBPOI)
      CALL DIMCOO( NBPOI, MCN(MNPOGE+WYZPOI), NDIM )
C
      IF( NDIM .NE. 2 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'ADAP2D: ADAPTATION PERMISE SEULEMENT EN DIMENSION 2'
         CALL LEREUR
         IERR = 11
         RETURN
      ENDIF
C
C     RECHERCHE DES POINTS EXTREMES EN X ET Y DU DOMAINE A TRIANGULER
      R = RINFO( 'GRAND' )
      DO 7 I=1,2
         COMXMI(I,1) =  R
         COMXMI(I,2) = -R
 7    CONTINUE
      MN = MNPOGE + WYZPOI - 1
      DO 9 K=1,NBPOI
         DO 8 I=1,2
            R = RMCN( MN + I )
            COMXMI(I,1) = MIN( R, COMXMI(I,1) )
            COMXMI(I,2) = MAX( R, COMXMI(I,2) )
 8       CONTINUE
         MN = MN + 3
 9    CONTINUE
C
C     CALCUL DU NOMBRE TOTAL D'EF DU MAILLAGE POUR VERIFICATION
      NBTTEF = 0
      DO 11 I = 0, NBTYEL-1
C        L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF DE CE TYPE D'EF
         MNNPEF = MCN( MNELEM + I )
C        LE NOMBRE DE TELS ELEMENTS
         NBELEM = MCN( MNNPEF + WBELEM )
         NBTTEF = NBTTEF + NBELEM
 11   CONTINUE
C
C     NOMBRE D'OBJETS INTERNES ET AUX LIMITES DE L'OBJET
      NBOBIN = MCN( MNTOPO + WBOBIN )
      NBOBCL = MCN( MNTOPO + WBOBCL )
C
C     RECHERCHE DU MIN ET MAX DES NUMEROS DES LIGNES FRONTIERES
C     =========================================================
C     NUMILF : NUMERO MINIMAL DES LIGNES FRONTIERES DE L'OBJET
C     NUMXLF : NUMERO MAXIMAL DES LIGNES FRONTIERES DE L'OBJET
C     L1LGFR : POUR CHAQUE LIGNE DE NUMILF A NUMXLF POINTE SUR LA PREMIERE
C              ARETE CHAINEE DE LA LIGNE
C
      NUMILF = IINFO( 'GRAND' )
      NUMXLF = 0
C
C     L'ADRESSE MCN DU DEBUT DU TABLEAU NUOBCL
      MNOBCL = MNTOPO + WMTYEL + NBTYEL + MOTVAR(13) * NBOBIN
      MN     = MNOBCL - 2
      DO 10 I=1,NBOBCL
C        LE TYPE DE L'OBJET AUX LIMITES
         MN   = MN + 2
         NYOB = MCN( MN )
         NUOB = MCN( MN + 1 )
         IF( NYOB .EQ. 2 ) THEN
C           LE MINIMUM DES NUMEROS DE LIGNES
            NUMILF = MIN( NUMILF, NUOB )
C           LE MAXIMUM DES NUMEROS D'OBJETS
            NUMXLF = MAX( NUMXLF, NUOB )
         ENDIF
 10   CONTINUE
      WRITE(IMPRIM,*) 'LIGNES FRONTALIERES POUR L''OBJET ', KNOMOB
      WRITE(IMPRIM,*) 'DE NUMERO MIN ',NUMILF,' AU NUMERO MAX ',NUMXLF
      CALL TNMCDC( 'ENTIER', NUMXLF-NUMILF+1, MNL1FR )
      CALL AZEROI( NUMXLF-NUMILF+1, MCN(MNL1FR) )
C
C     RECHERCHE DE LA SOLUTION NCAS DE LA TEMPERATURE
C     ===============================================
C     RECHERCHE DES TEMPERATURES DE L'OBJET
      KNOM = 'VECTEUR"TEMPERATURE'
      CALL  LXLXOU( NTLXOB, 'VECTEUR"TEMPERATURE', NTTEMP, MNTEMP )
      IF( NTTEMP .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'ERREUR : OBJET ' // KNOMOB
         KERR(2) = 'SANS TEMPERATURES CALCULEES'
         CALL LEREUR
         IERR = 1
         GOTO 9000
      ENDIF
C
C     NOMBRE DE DEGRES DE LIBERTE
      NTDL = MCN( MNTEMP + WBCOVE )
C     NOMBRE DE CAS
      NDSM = MCN( MNTEMP + WBVECT )
C
C     CALCUL DES TEMPERATURES MIN ET MAX ET DE LEURS NOEUDS
C     TMIN   : TEMPERATURE MINIMALE DU CAS NCAS
C     NOEMIN : NUMERO DU NOEUD OU LA TEMPERATURE EST MINIMALE
C     TMAX   : TEMPERATURE MAXIMALE DU CAS NCAS
C     NOEMAX : NUMERO DU NOEUD OU LA TEMPERATURE EST MAXIMALE
      CALL MXVECT( NTDL, NDSM, MCN(MNTEMP+WECTEU),
     %             DTMIN, NOEMIN, NCAMIN, DTMAX, NOEMAX, NCAMAX )
      TMIN = REAL( DTMIN )
      TMAX = REAL( DTMAX )
C
C     LE TMS OBJET>>ERREURTH
C     ======================
      CALL LXTSOU( NTLXOB , 'ERREURTH' , NTERTH, MNERTH )
      IF( NTERTH .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'PAS D''ESTIMATEUR D''ERREUR POUR CET OBJET'
         KERR(2) = 'RESOUDRE LE PB THERMIQUE AVANT'
         CALL LEREUR
         IERR = 9
         GOTO 9000
      ENDIF
      NBPTAF = MCN( MNERTH + WBPTAF )
CCC      NDSM   = MCN( MNERTH + WBCAAF )
CCC      NBTTEF = MCN( MNERTH + WBTTEF )
      IF( NBTTEF .NE. MCN( MNERTH + WBTTEF ) ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'MAILLAGE DIFFERENT DE CELUI DE L''ESTIMATEUR'
         KERR(2) = 'RESOUDRE LE PB THERMIQUE AVANT'
         CALL LEREUR
         IERR = 9
         GOTO 9000
      ENDIF
C     LA REPARTITION INTERNE DES SOUS-TABLEAUX ET LEUR ADRESSE MCN
      MNSFLU = MNERTH + WFLUAF
      MNERAF = MNSFLU + MOREE2 * 2*NBPTAF*MXARET*NDSM
      MNEREF = MNERAF + MOREE2 * MXARET*NDSM
      MNH1EF = MNEREF + MOREE2 * NBTTEF*NDSM
      MNEETH = MNH1EF + MOREE2 * NBTTEF*NDSM
      MNH1TE = MNEETH + MOREE2 * NDSM
CCC      MNEEH1 = MNH1TE + MOREE2 * NDSM
CCC      MNXYZC = MNEEH1 + MOREE2 * NDSM
C     L'ESTIMATEUR D'ERREUR POUR LE CAS NCAS
C     ESTERR :   NORME L2 DES ESTIMATEURS D'ERREUR SUR LES EF
C              / NORME H1 DE LA TEMPERATURE SUR LES EF
      DD = DMCN((MNH1TE-1)/2+NCAS)
      IF( DD .LE. 0D0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'NORME H1 DE LA TEMPERATURE INCORRECTE'
         KERR(2) = 'RESOUDRE LE PB THERMIQUE AVANT'
         CALL LEREUR
         IERR = 9
         GOTO 9000
      ENDIF
      ESTERR = REAL( DMCN((MNEETH-1)/2+NCAS) / DD )
C
C     LE NOMBRE DES ISOVALEURS EST CALCULE
C     ====================================
      IF( NBISO .EQ. 0 ) THEN
C        CALCUL EMPIRIQUE NBISO = RACINE CARREE DU NOMBRE DES EF
         R     = NBTTEF
         NBISO = NINT( SQRT( R ) )
      ENDIF
C     NBISO <= NBISO * ( ESTERR / ESTERR SOUHAITEE ) ** (1/DEGRE POLYNOME)
      R = ESTERR / ESTERS
      IF( NBAEFU .EQ. 2 ) R = SQRT( R )
      NBISO = NINT( NBISO * R )
      IF( MOD(NBISO,2) .EQ. 0 ) NBISO = NBISO + 1
      R = NBISO
      L = NINT( SQRT( R ) )
C     CALCUL DU PLUS PETIT NOMBRE PREMIER SUPERIEUR A NBISO
 13   DO 14 I=3,L,2
         IF( MOD(NBISO,I) .EQ. 0 ) THEN
            NBISO = NBISO + 2
            GOTO 13
         ENDIF
 14   CONTINUE
C
C     EXECUTION DE L'ADAPTATION
C     =========================
C     CALCUL DES VALEURS DES ISOVALEURS
      IF( MNVISO .EQ. 0 ) THEN
         CALL TNMCDC( 'REEL', NBISO+1, MNVISO )
         DFMXIS = ( TMAX - TMIN ) / NBISO
         F      = TMIN + DFMXIS * 0.5
         DO 20 I=1,NBISO
            RMCN( MNVISO - 1 + I ) = F
            F                      = F + DFMXIS
 20      CONTINUE
      ENDIF
C
      WRITE(IMPRIM,10020) NCAS, (I,RMCN(MNVISO-1+I),I=1,NBISO)
10020 FORMAT(/'CAS ',I3,' : VALEURS DES ISOTHERMES POUR L''ADAPTATION'/
     % 5(I4,' : ',G13.5))
C
C     PARAMETRES DES TAILLES DES TABLEAUX. A MODIFIER EVENTUELLEMENT
C     MXAEFU : MAXIMUM D INTERVALLES DU SEGMENT UNITE
C              MXAEFU ** 2 SOUS-TRIANGLES  CREES AU PLUS DANS UN TRIANGLE
CCC      MXAEFU = 3
      MXAEFU = NBAEFU
C
C     MXPILE : MAXIMUM DE SOUS-TRIANGLES GENERES DANS L EF REFERENCE
      MXPILE = 2 * MXAEFU * MXAEFU
C     DECLARATION DES 2 PILES SUR LES SOUS-TRIANGLES DE L'EF UNITE
      MOPILE = 6
      CALL TNMCDC( 'ENTIER', MOPILE*MXPILE, MNPILT )
      MNPILA = MNPILT + 3 * MXPILE
C
C     MXSOMM : MAXIMUM DE SOMMETS DES SOUS-TRIANGLES DE L EF UNITE
      MXSOMM = (MXAEFU+1) * (MXAEFU+2)
C     VALST (3,MXSOMM) COORDONNEES X Y SUR L'EF UNITE ET VALEUR DE LA SOLUTION
C     AUX SOMMETS DES SOUS-TRIANGLES
      CALL TNMCDC( 'REEL', 3*MXSOMM, MNVALS )
C
C     LE NUMERO DES SOMMETS DES SOUS-TRIANGLES APRES IDENTIFICATION
      CALL TNMCDC( 'ENTIER', MXSOMM, MNNXYZ )
C
C     VALEUR DES FONCTIONS DE BASE AUX NOEUDS
      CALL TNMCDC( 'REEL2', 30, MNFBAS )
C
C     LA SOLUTION SUR L'EF COURANT
      CALL TNMCDC( 'REEL2', MXNOEL, MNSOLE )
C
C     LES COORDONNEES DES POINTS SUR L'EF
      CALL TNMCDC( 'REEL2', MXPOEL, MNCDPE )
C
C     LE NOMBRE MAXIMAL DECLARABLES DE SOMMETS DU NOUVEAU MAILLAGE
      MXYZST = MXAEFU * MXAEFU * NBPOI * NBISO + 512
      NBXYZ  = 0
      CALL TNMCDC( 'REEL', 3*MXYZST, MNXYZS )
C
C     NUPLIS: NUMERO DE POINT OU LIGNE DE CHACUN DES SOMMETS
C             -NP       SI NP EST LE NUMERO DU POINT UTILISATEUR DE CE SOMMET
C             -1234567  SI LE SOMMET APPARTIENT A 2 LIGNES (SOMMET INITIAL)
C              NU LIGNE SI LE SOMMET EST SUR UNE LIGNE UTILISATEUR
C              0        SINON
      MXPLIS = MXYZST
      CALL TNMCDC( 'ENTIER', MXPLIS, MNPLIS )
      CALL AZEROI(  MXPLIS, MCN(MNPLIS) )
C
C     NUISOP : NUMERO DE L'ISO DE CHACUN DES POINTS. PAR DEFAUT 0
      CALL TNMCDC( 'ENTIER', MXPLIS, MNNUIS )
      CALL AZEROI(  MXPLIS, MCN(MNNUIS) )
C
C     LE NOMBRE MAXIMAL DE TRIANGLES DANS LE NOUVEAU MAILLAGE
      MXNVTR = 2 * MXYZST
      NBNVTR = 0
      CALL TNMCDC( 'ENTIER', 4*MXNVTR, MNNVTR )
C
C     LE NOMBRE MAXIMAL D'ARETES CHAINEES DES LIGNES DE L'OBJET
      MXARCH = MXYZST
      NBARCH = 0
      CALL TNMCDC( 'ENTIER', 4*MXARCH, MNARCH )
C     CHAINAGE DES ARETES VIDES
      MN = MNARCH - 4
      DO 30 I=1,MXARCH
         MN = MN + 4
C        LE NUMERO DE SOMMET EST MIS A ZERO
         MCN(MN) = 0
C        L'ARETE SUIVANTE EST MISE EN POSITION 4
         MCN(MN+3) = I+1
 30   CONTINUE
      MCN(MN+3) = 0
      N1ARCH = 1
C
C     BOUCLE SUR LES DIFFERENTS TYPES D'ELEMENTS FINIS DU MAILLAGE
C     ============================================================
      DO 50 I = 0, NBTYEL-1
C
C        L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF DE CE TYPE D'EF
         MNNPEF = MCN( MNELEM + I )
C
C        LE NUMERO DU TYPE DES ELEMENTS FINIS
         NUTYEL = MCN( MNNPEF + WUTYEL )
C
C        LE NOMBRE DE TELS ELEMENTS
         NBELEM = MCN( MNNPEF + WBELEM )
C
C        LES CARACTERISTIQUES DE L'ELEMENT FINI
         CALL ELTYCA( NUTYEL )
C
C        L'ADRESSE MCN DU TABLEAU 'NPEF' POUR CE TYPE D'EF
         MNPGEL = MNNPEF + WUNDEL
         IF( NDPGST .GE. 2 ) THEN
            MNPGEL = MNPGEL + MCN(MNNPEF+WBELEM) * MCN(MNNPEF+WBNDEL)
         ENDIF
C
CCCC        LE NOMBRE DE SUBDIVISIONS EN SOUS-ARETES D'UN COTE DE L'EF UNITE
CCC         IF( NBNOE .LE. 4 ) THEN
CCC            NBAEFU = 1
CCC         ELSE
CCCC           IL EST POSSIBLE DE CHOISIR ICI NBAEFU = MXAEFU > 2
CCC            NBAEFU = 2
CCC         ENDIF
C
C        RETRIANGULATION SELON LES ISOVALEURS
C        ------------------------------------
         CALL ADAP21( NBISO , RMCN(MNVISO), DFMXIS,
     %                NCAS,   NDSM,   NTDL, MCN(MNTEMP+WECTEU),
     %                NUTYEL, NBELEM, MNNPEF,
     %                NBNOE,  MCN(MNNPEF+WUNDEL),
     %                NBPOE,  MCN(MNPGEL),
     %                NBPOI , MCN(MNPOGE+WYZPOI),
     %                MXYZST, NBXYZ,  MCN(MNXYZS), MCN(MNNXYZ),
     %                MXNVTR, NBNVTR, MCN(MNNVTR),
     %                MCN(MNPLIS), MCN(MNNUIS),
     %                NUMILF, NUMXLF, MCN(MNL1FR),
     %                MXARCH, NBARCH, N1ARCH, MCN(MNARCH),
     %                NBAEFU, MXSOMM, MCN(MNSOLE), MCN(MNCDPE),
     %                MXPILE, MCN(MNPILT), MCN(MNPILA),
     %                MCN(MNVALS), MCN(MNFBAS), IERR )
C        ATTENTION: LE SENS DIRECT DES SOMMETS DES TRIANGLES N'EST PAS ASSURE
         IF( IERR .NE. 0 ) THEN
            NBLGRC(NRERR) = 2
            KERR(1) = 'SUITE A UN PROBLEME INDIQUE AVANT'
            KERR(2) = 'ADAPTATION NON REALISEE'
            CALL LEREUR
            GOTO 9000
         ENDIF
 50   CONTINUE
C
      IF( TRATRI ) THEN
C        LES TRACES SONT DEMANDES
         CALL EFFACE
C        LE CADRE OBJET GLOBAL EN UNITES UTILISATEUR
         XE = COMXMI(1,2) - COMXMI(1,1)
         YE = COMXMI(2,2) - COMXMI(2,1)
         CALL ISOFENETRE( COMXMI(1,1)-XE/20, COMXMI(1,2)+XE/20,
     %                    COMXMI(2,1)-YE/20, COMXMI(2,2)+YE/20 )
      ENDIF
C
C     GENERATION DES TABLEAUX NOTRIA, NOTRSO, PXYD
C     --------------------------------------------
C     RESERVATION DES TABLEAUX
      MXTRIA = NBXYZ * 3
      MXPXYD = MXTRIA / 2
      IF( MXPXYD .GT.  MXPLIS ) THEN
C        AUGMENTATION DU TABLEAU NUPLIS
         CALL TNMCAU( 'ENTIER', MXPLIS, MXPXYD, NBXYZ, MNPLIS )
C        MISE A ZERO AU DELA DE NBXYZ
         CALL AZEROI( MXPXYD-NBXYZ, MCN(MNPLIS-1+NBXYZ) )
C        AUGMENTATION DU TABLEAU NUISOP
         CALL TNMCAU( 'ENTIER', MXPLIS, MXPXYD, NBXYZ, MNNUIS )
C        MISE A ZERO AU DELA DE NBXYZ
         CALL AZEROI( MXPXYD-NBXYZ, MCN(MNNUIS-1+NBXYZ) )
         MXPLIS = MXPXYD
      ENDIF
      CALL TNMCDC( 'REEL2',  3*MXPXYD, MNPXYD )
      CALL TNMCDC( 'ENTIER', 6*MXTRIA, MNTRIA )
      CALL TNMCDC( 'ENTIER',   MXTRIA, MNNSCT )
      CALL TNMCDC( 'ENTIER',   MXPXYD, MNTRSO )
      CALL AJTRVO( NBXYZ,  MCN(MNXYZS), NBNVTR, MCN(MNNVTR),
     %             MXPXYD, MXTRIA,
     %             RMCN(MNPXYD), MCN(MNTRSO), N1TRVI, MCN(MNTRIA),
     %             MCN(MNNSCT), IERR )
      IF( IERR .NE. 0 ) GOTO 9000
C
C     DESTRUCTION DES TABLEAUX DEVENUS INUTILES
      IF( MNXYZS .GT. 0 ) CALL TNMCDS( 'REEL',   3*MXYZST, MNXYZS )
      IF( MNNVTR .GT. 0 ) CALL TNMCDS( 'ENTIER', 4*MXNVTR, MNNVTR )
C
C     DESTRUCTION DES SOMMETS "MILIEUX" ET DES ARETES TROP PETITES
C     ------------------------------------------------------------
      CALL ADSUAM( TARMAX, MXPXYD, NBXYZ,  RMCN(MNPXYD),
     %             MXTRIA, NBNVTR, N1TRVI, MCN(MNTRIA), MCN(MNTRSO),
     %             MCN(MNNSCT), MCN(MNPLIS), MCN(MNNUIS),
     %             NUMILF, NUMXLF, MCN(MNL1FR),
     %             MXARCH, NBARCH, N1ARCH, MCN(MNARCH),
     %             IERR )
      IF( IERR .NE. 0 ) GOTO 9000
C
C     GENERATION DES TABLEAUX NSEF + XYZSOM DES SURFACES ADAPTEES
C     -----------------------------------------------------------
C     L'ADRESSE MCN DU DEBUT DU TABLEAU NUOBIN
      MNOBIN = MNTOPO + WMTYEL + NBTYEL
      MN     = MNOBIN - 2
      DO 90 I=1,NBOBIN
C        LE TYPE DE L'OBJET INTERNE
         MN   = MN + 2
         NYOB = MCN( MN )
         NUOB = MCN( MN + 1 )
         IF( NYOB .EQ. 3 ) THEN
C           NUOB EST UNE SURFACE DONC RETRIANGULEE
            CALL GSXYEF( KNUADP, NUOB,
     %                   MXYZST, NBXYZ,  RMCN(MNPXYD),
     %                   MXNVTR, NBNVTR, MCN(MNTRIA), MCN(MNNSCT),
     %                   MCN(MNPLIS),
     %                   NTXYZ,  MNXYZ,  NTNSEF, MNSEF, IERR )
            IF( IERR .NE. 0 ) GOTO 9000
         ENDIF
 90   CONTINUE
C
C     GENERATION DES TABLEAUX NSEF + XYZSOM DES LIGNES ADAPTEES
C     ---------------------------------------------------------
C     L'ADRESSE MCN DU DEBUT DU TABLEAU NUOBCL
C     MNOBCL = MNTOPO + WMTYEL + NBTYEL + MOTVAR(13) * NBOBIN
      MN     = MNOBCL - 2
      DO 100 I=1,NBOBCL
C        LE TYPE DE L'OBJET AUX LIMITES
         MN   = MN + 2
         NYOB = MCN( MN )
         NUOB = MCN( MN + 1 )
         IF( NYOB .EQ. 2 ) THEN
C           NUOB EST UNE LIGNE ADAPTEE
            CALL GLXYEF( KNUADP, NUOB,
     %                   MXYZST, NBXYZ,  RMCN(MNPXYD),
     %                   NUMILF, NUMXLF, MCN(MNL1FR),
     %                   MXARCH, MCN(MNARCH),
     %                   MCN(MNPLIS),
     %                   NTXYZ,  MNXYZ,  NTNSEF, MNSEF, IERR )
            IF( IERR .NE. 0 ) GOTO 9000
         ENDIF
 100  CONTINUE
C
C     SORTIE DE L'ADAPTATION DU MAILLAGE DE L'OBJET
C     =============================================
 9000 IF( MNVISO .GT. 0 ) CALL TNMCDS( 'REEL' ,  NBISO+1,  MNVISO )
      IF( MNPILT .GT. 0 ) CALL TNMCDS( 'ENTIER', MOPILE*MXPILE, MNPILT )
      IF( MNL1FR .GT. 0 ) CALL TNMCDS( 'ENTIER',NUMXLF-NUMILF+1,MNL1FR )
      IF( MNVALS .GT. 0 ) CALL TNMCDS( 'REEL',   3*MXSOMM, MNVALS )
      IF( MNFBAS .GT. 0 ) CALL TNMCDS( 'REEL2',  30,       MNFBAS )
      IF( MNSOLE .GT. 0 ) CALL TNMCDS( 'REEL2',  MXNOEL,   MNSOLE )
      IF( MNCDPE .GT. 0 ) CALL TNMCDS( 'REEL2',  MXPOEL,   MNCDPE )
      IF( MNPLIS .GT. 0 ) CALL TNMCDS( 'ENTIER', MXPLIS,   MNPLIS )
      IF( MNNUIS .GT. 0 ) CALL TNMCDS( 'ENTIER', MXPLIS,   MNNUIS )
      IF( MNNXYZ .GT. 0 ) CALL TNMCDS( 'ENTIER', MXSOMM,   MNNXYZ )
      IF( MNXYZS .GT. 0 ) CALL TNMCDS( 'REEL',   3*MXYZST, MNXYZS )
      IF( MNNVTR .GT. 0 ) CALL TNMCDS( 'ENTIER', 4*MXNVTR, MNNVTR )
      IF( MNARCH .GT. 0 ) CALL TNMCDS( 'ENTIER', 4*MXARCH, MNARCH )
      IF( MNPXYD .GT. 0 ) CALL TNMCDS( 'REEL2', 3*MXPXYD,  MNPXYD )
      IF( MNNSCT .GT. 0 ) CALL TNMCDS( 'ENTIER',   MXTRIA, MNNSCT )
      IF( MNTRIA .GT. 0 ) CALL TNMCDS( 'ENTIER', 6*MXTRIA, MNTRIA )
      IF( MNTRSO .GT. 0 ) CALL TNMCDS( 'ENTIER', MXPXYD,   MNTRSO )
C
 9999 IF( MNELEM .GT. 0 ) CALL TNMCDS( 'ENTIER' , 2*MXTYEL , MNELEM )
C
C     RETOUR AUX PARAMETRES INITIAUX
      CALL XVEPAISSEUR( 1 )
      CALL XVTYPETRAIT( LIGCON )
      END
