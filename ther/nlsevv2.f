      SUBROUTINE NLSEVV2( NDIM,   NTDL,   MNTOPO,
     %                    NBTYEL, MNNPEF, NDPGST, MNTPOB, MNTAUX,
     %                    MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                    MNTHER, MNTAEL, MNX,    MNVENL,
     %                    IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %                    IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %                    NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %                    NBDLIB, MNDLIB,
     %                    MXROOT, NOVALP, NUINVP, DIAGKG, DIAGMG,
     %                    NBVAPR, VALPR,  VECPR,  MGVECPR,
     %                    RAVANT, NINV,   NBFACTKG,
     %                    R1,     NBAVR1, NBOUR1,
     %                    R2,     NBAVR2, NBOUR2,
     %                    VP,     NBAVVP, NBOUVP, NBHVP,  DETVP,  
     %                    V,      W,      IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ITERATIONS INVERSES POUR AFFINER LA VALEUR PROPRE R1 EN VP
C -----    ET CALCULER LE VECTEUR PROPRE V ASSOCIE A VP
C
C ENTREES:
C --------
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 1 ou 2 ou 3 ou 6
C          (SI AXISYMETRIE NDIM=2 X => R>=0 et Y=>Z et Z=0)
C NTDL   : NOMBRE DE DEGRES DE LIBERTE
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE DE L'OBJET
C NBTYEL : NOMBRE DE TYPES D'EF DE L'OBJET
C MNNPEF : ADRESSE MCN DU TMC DES ADRESSES MCN DES TABLEAUX NPEF"TYPE EF
C NDPGST : CODE DE STOCKAGE DES SOMMETS POINTS NOEUDS
C MNTPOB : ADRESSE MCN DES TABLEAUX POLYNOMES DE BASE DES TYPES D'EF
C MXPLBA : NOMBRE MAXIMAL DE TABLEAUX POBA PAR TYPE D'EF
C MNTAUX : ADRESSE MCN DES TABLEAUX AUXILAIRES
C MNXYZP : ADRESSE MCN DE TMS XYZSOMMET DE L'OBJET
C NUMIOB : NUMERO MINIMAL DES OBJETS
C NUMAOB : NUMERO MAXIMAL DES OBJETS
C MNDOEL : ADRESSE MCN DES TMS DES DONNEES THERMIQUES DE L'OBJET
C MNTHER : ADRESSE MCN DU TABLEAU DES DONNEES THERMIQUES INTERNES
C          AU MOINS NPIMAX(=128) REELS DOUBLE PRECISION
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DES NBCOOR COORDONNEES DES POINTS DE L'EF COURANT
C MNVENL : ADRESSE MCN DU VECTEUR SERVANT A DEFINIR LA NON LINEARITE
C
C NCODSM : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CAPACITE
C          0 SI MATRICE DIAGONALE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDMG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE MG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NTDL SI MATRICE DIAGONALE
C MNMUMG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C MG     : MATRICE PROFIL GLOBALE DE CAPACITE CALCULEE
C
C NCODSK : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CONDUCTIVITE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDKG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE KG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NTDL SI MATRICE DIAGONALE
C MNMUKG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C KG     : MATRICE PROFIL GLOBALE (KG - R1 MG)**-1 = L D tL FACTORISEE
C
C NBDLIB : NOMBRE DE DEGRES DE LIBERTE LIBRES
C NBRDLX : NOMBRE DE DL SUPPORT D'UN DEGRE DE LIBERTE FIXE
C MONDLX : NOMBRE DE MOTS DECLARES DU TABLEAU MC NO DES DL FIXES
C MNNDLX : ADRESSE MCN DU TABLEAU MC DES NUMEROS DES DL FIXES, 0 SINON
C MNVDLX : ADRESSE MCN DU TABLEAU MC DES VALEURS DES DL FIXES, 0 SINON
C MNDLIB : MCN(MNDLIB-1+I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NTDL) LIBRE
C                          -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          0  SI PAS DE DL BLOQUES
C
C MXROOT : NOMBRE MAXIMAL DE VALEURS ET VECTEURS PROPRES A CALCULER
C NOVALP : NUMERO DE LA VALEUR PROPRE A CALCULER
C NUINVP : NUMERO D'INITIALISATION DU VECTEUR DE 1 A 9
C DIAGKG : DIAGONALE DE LA MATRICE PROFIL KG
C DIAGMG : DIAGONALE DE LA MATRICE PROFIL MG
C NINVMX : NOMBRE MAXIMAL D'ITERATIONS INVERSES
C
C MODIFIES:
C ---------
C NBVAPR : NOMBRE DE VALEURS PROPRES STOCKES DANS VALPR, VECPR, MGVECPR
C VALPR  : NBVAPR VALEURS PROPRES DEJA CALCULEES
C VECPR  : VECTEURS PROPRES DEJA CALCULES
C MGVECPR: MG * VECPR VECTEURS PROPRES DEJA CALCULES

C RAVANT : RAVANT(I,1)=VALEUR MINIMALE AYANT I VALEURS PROPRES AVANT ELLE
C                     +1D111 indique une valeur non initialisee
C          RAVANT(I,2)=VALEUR MAXIMALE AYANT I VALEURS PROPRES AVANT ELLE
C                     -1D111 indique une valeur non initialisee
C NINV   : NOMBRE D'ITERATIONS INVERSES CALCULEES POUR CHAQUE VALEUR PROPRE
C NBFACTKG:NOMBRE DE FACTORISATIONS L D tL de KG FAITES AVANT ET APRES
C R1     : LA PLUS GRANDE VALEUR AVEC 0 VALEUR PROPRE OUBLIEE AVANT
C NBAVR1 : NOMBRE DE VALEURS PROPRES INFERIEURES A R1
C NBOUR1 : NOMBRE DE VALEURS PROPRES OUBLIEES DANS VALPR AVANT R1
C R2     : LA VALEUR LA PLUS PETITE  AVEC AU MOINS UNE VALEUR PROPRE OUBLIEE
C NBAVR2 : NOMBRE DE VALEURS PROPRES INFERIEURES A R2
C NBOUR2 : NOMBRE DE VALEURS PROPRES OUBLIEES DANS VALPR AVANT R2
C
C SORTIES:
C --------
C VP     : VALEUR PROPRE AFFINEE PAR ITERATIONS INVERSES = R1 + QR
C          QR EST LA TRANSLATION PAR RAPPORT A R1
C NBAVVP : NOMBRE DE VALEURS PROPRES INFERIEURES A VP
C NBOUVP : NOMBRE DE VALEURS PROPRES OUBLIEES DANS VALPR AVANT VP
C NBHVP  : NOMBRE D'HOMOTHETIES DE SCALE DU DETERMINANT DE KG - VP MG
C DETVP  : DETERMINANT DE KG - VP MG  * SCALE ** NBHVP
C V      : VECTEUR PROPRE ASSOCIE A LA VALEUR PROPRE VP
C W      : PRODUIT DE MG AVEC V
C IERR   : =0 PAS D'ERREUR RENCONTREE
C          >0 ERREUR RENCONTREE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray    Avril 2014
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2014
C2345X7..............................................................012
      include"./incl/langue.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      INTEGER           NUMIOB(4), NUMAOB(4),  MNDOEL(4)
C
      INTEGER           NINV(MXROOT)
      DOUBLE PRECISION  KG(NBRDKG), MG(NBRDMG), V(NBDLIB),
     %                  W(NBDLIB), DIAGKG(NBDLIB), DIAGMG(NBDLIB),
     %                  VALPR(MXROOT), VECPR(NBDLIB,MXROOT),
     %                  MGVECPR(NBDLIB,MXROOT), RAVANT(0:MXROOT*2,2)
      DOUBLE PRECISION  VP0, VP, R1, R2, DETVP, QR0, QR, TVKGV, TVMGV
      INTRINSIC         SQRT, ABS
C
      IEMG   = 0
      IEKG   = 1

      MXITINV=100
C     MXITINV  NOMBRE MAXIMAL D ITERATIONS INVERSES A FAIRE AVANT REPRISE

      VP0 = VP

C     INITIALISATION DU VECTEUR PROPRE V POUR L'ITERATION INVERSE
C     DE (KG -VP MG)=LDtL
C     MG ORTHOGONALISE AUX VECTEURS PROPRES DEJA CALCULES
C     -----------------------------------------------------------
 10   CALL INITVV0( NUINVP, NBDLIB, DIAGKG,  VP0, DIAGMG,
     %              NBVAPR, VECPR,  MGVECPR,
     %              NCODSM, NBRDMG, MCN(MNMUMG), MG, V, W, IERR )
C     Wk = MG Vk    fait dans initvv0
      IF( IERR .NE. 0 ) THEN
         WRITE(IMPRIM,*) 'NLSEVV2: PAS ASSEZ DE MODES D''INITIALISATION 
     %DE V0'
         WRITE(IMPRIM,*) ' => AMELIORER le SP initvv0.f'
         GOTO 9999
      ENDIF
C
      WRITE(IMPRIM,10010) NUINVP,R1,R2,VP0
10010 FORMAT(/'NLSEVV2: INITIALISATION',I3,' DU VECTEUR V0 POUR ITERATIO
     %N INVERSE avec R1=',G14.6,' R2=',G14.6,' VP0=',G14.6)

      QR0 = 1D100
      QR  = 0D0

      VP0 = VP
      VP  = ( R1 + R2 ) / 2
      NBCAVP = 0
      NBDICO = 0
      GOTO 40
C
C     UN CALCUL DE VP DE PLUS A PARTIR DE (KG -VP0 MG)=LDtL
C     =====================================================
 15   NBCAVP = NBCAVP + 1

      WRITE(IMPRIM,10015) NBCAVP,R1,NBOUR1,R2,NBOUR2,VP0,NBOUVP
10015 FORMAT('NLSEVV2: Calcul',I3,' de VP avec R1=',G14.6,
     %' NBOUR1=',I3, '  R2=',G14.6,' NBOUR2=',I3,
     %'  VP0=',G14.6,' NBOUVP0=',I3)
C
C     NOMBRE D'ITERATIONS INVERSES AVEC LA MEME VALEUR DE R1
      NBITQR = 0
C
C     UNE ITERATION INVERSE DE PLUS DE VP0 A VP
C     -----------------------------------------
 20   IF( NINV(NOVALP) .GT. MXITINV ) THEN
C        MAXIMUM D'ITERATIONS INVERSES ATTEINT
         WRITE(IMPRIM,10020) MXITINV, VP
10020 FORMAT(130('|')/
     %'NLSEVV2: PROBLEME NON CONVERGENCE APRES',I5,' MAXIMUM DES ITERATI
     %ONS INVERSES'/
     %' POURSUITE DES CALCULS AVEC DERNIER VP=',G14.6/130('|'))
         GOTO 40
      ENDIF
C
C     UNE ITERATION INVERSE CALCULEE DE PLUS
      NINV(NOVALP) = NINV(NOVALP) + 1
      NBITQR       = NBITQR       + 1

C     QR0 ANCIEN QUOTIENT DE RAYLEIGH
      QR0 = QR
C
C     MG-ORTHONORMALISATION DE V AUX NBVAPR VECTEURS PROPRES DEJA CALCULES
C     ET Wk = MG Vk
      CALL MORTHO( NBDLIB, NBVAPR, VECPR, MGVECPR,
     %             NCODSM, MCN(MNMUMG), MG, V, W )
C
C     CALCUL DE QR QUOTIENT DE RAYLEIGH POUR (KG -VP MG)**-1 et 
C     V = (KG - VP0 MG)**-1 MG V0 = (L D tL)**-1 W
      CALL NLSEQR( NBDLIB,
     %             NBVAPR, VECPR, MGVECPR,
     %             NCODSM, MCN(MNMUMG), MG,
     %             NCODSK, MCN(MNMUKG), KG,
     %             W,   V, W, TVMGV, TVKGV, QR, IERR )
C     V et W SONT MG orthonormalises en sortie
C
C     QR QUOTIENT DE RAYLEIGH SUR (KG -VP0 MG)**-1 => VP
      VP = VP0 + QR

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10025) NINV(NOVALP),VP0,NUINVP,TVMGV,TVKGV,QR,VP
      ELSE
         WRITE(IMPRIM,20025) NINV(NOVALP),VP0,NUINVP,TVMGV,TVKGV,QR,VP
      ENDIF
10025 FORMAT('NLSEVV2: ITERATION INVERSE',I4,' VP0=',G14.6,
     %' INIT V=',I2,' TVMGV=',G14.6,' TVKGV=',G14.6,
     %' QR RAYLEIGHT=',G14.6,' => VP=',G14.6)
20025 FORMAT('NLSEVV2: INVERSE ITERATION',I4,' VP0=',G14.6,
     %' INIT V=',I2,' TVMGV=',G14.6,' TVKGV=',G14.6,
     %' QR RAYLEIGHT=',G14.6,' => VP=',G14.6)

C     TESTS DE CONVERGENCE
C     QUOTIENT DE RAYLEIGHT SUFFISAMMENT PETIT ou CONVERGE?
C     -----------------------------------------------------
      IF( NBITQR .LE. 1 ) GOTO 20
      IF( VP .LT. R1 .OR. VP .GT. R2 ) GOTO 35
      IF( ABS(QR)     .LT. ABS(VP0)*1D-4 ) GOTO 70
      IF( ABS(QR-QR0) .GT. ABS(QR) *1D-3 ) GOTO 20

C     ICI: CONVERGENCE DES ITERATIONS INVERSES POUR CETTE VALEUR VP0
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10030) NBITQR, VP0, VP
      ELSE
         WRITE(IMPRIM,20030) NBITQR, VP0, VP
      ENDIF

10030 FORMAT('NLSEVV2: CONVERGENCE ',I3,
     %' ITERATIONS INVERSES avec (KG-',G14.6,' MG)**-1 -> VP=',G14.6)
20030 FORMAT('NLSEVV2: CONVERGENCE ',I3,
     %' INVERSE ITERATIONS on (KG-',G14.6,' MG)**-1 -> VP=',G14.6)

C     DICHOTOMIE ENTRE LES 2 VALEURS R1 R2 ENCADRANT UNE VALEUR PROPRE et VP
C     ======================================================================
 35   NBDICO = NBDICO + 1
      IF( VP .LT. R1  .OR.  VP .GT. R2 ) THEN
         VP = (R1 + R2) / 2D0
      ENDIF

 40   IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10040) R1, NBOUR1, R2, NBOUR2, VP
      ELSE
         WRITE(IMPRIM,20040) R1, NBOUR1, R2, NBOUR2, VP
      ENDIF
10040 FORMAT('NLSEVV2: DICHOTOMIE entre R1=',G14.6,' NBOUR1=',I3,
     %' R2=',G14.6,' NBOUR2=',I3,' => VP=',G14.6)
20040 FORMAT('NLSEVV2: DICHOTOMY between R1=',G14.6,' NBOUR1=',I3,
     %' R2=',G14.6,' NBOUR2=',I3,' => VP=',G14.6)

C     Factorisation KG(|V|**2) - VP MG = L D tL ET NOMBRE VALEURS PROPRES AVANT VP
C     ----------------------------------------------------------------------------
      CALL CPSIVV( 1, NTDL, NBDLIB, MCN(MNDLIB), NBRDLX, MCN(MNVDLX),
     %             V, MCN(MNVENL) )
      CALL NLSEVOU( NDIM,   NTDL,   MNTOPO,
     %              NBTYEL, MNNPEF, NDPGST,
     %              MNTPOB, MNTAUX,
     %              MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %              MNTHER, MNTAEL, MNX,    MNVENL,
     %              IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %              IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %              NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %              NBDLIB, MNDLIB,
     %              VP,     NBVAPR, VALPR,  MXROOT, RAVANT,
     %              NBHVP,  DETVP,  NBAVVP, NBOUVP )
C     UNE FACTORISATION DE CROUT L D tL = KG - VP MG  DE PLUS
      NBFACTKG = NBFACTKG + 1
C
C     R1 EST LA PLUS GRANDE VALEUR CALCULEE AVEC 0 VALEUR OUBLIEE
      IF( NBOUVP .EQ. 0 .AND. VP .GT. R1 ) THEN
         R1     = VP
         NBAVR1 = NBAVVP
         NBOUR1 = NBOUVP
      ENDIF
C
C     R2 EST LA PLUS PETITE VALEUR CALCULEE AVEC AU MOINS 1 VALEUR OUBLIEE
      IF( NBOUVP .GE. 1 .AND. VP .LT. R2 ) THEN
         R2     = VP
         NBAVR2 = NBAVVP
         NBOUR2 = NBOUVP
      ENDIF

      WRITE(IMPRIM,10050) R1,NBOUR1, R2,NBOUR2, VP,NBOUVP
10050 FORMAT('NLSEVV2: R1=',G14.6,' NBOUR1=',I3,
     %'  R2=',G14.6,' NBOUR2=',I3,
     %'  VP=',G14.6,' NBOUVP=',I3)

C     ENCADREMENT SUFFISAMMENT PETIT DE LA VALEUR PROPRE?
      IF( NBDICO .LE. 1 .OR. ABS(R2-R1) .GT. ABS(R2)*1D-3 ) THEN

C        NON CONVERGENCE POUR CE CALCUL DE VP
         IF( NBCAVP .LT. 32 ) THEN
C           CHANGEMENT DE VP0 POUR ACTUALISER [KG(|V|**2)]
            VP0 = VP
            QR  = 0D0
            MXITINV = MXITINV + 100
            GOTO 15
         ENDIF

C        NOMBRE DE CALCUL DE VP DEPASSE => ABANDON
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10060) NOVALP, VP, R1, NBOUR1, R2, NBOUR2,
     %                          NBCAVP, NINV(NOVALP), NBFACTKG, VP0
         ELSE
            WRITE(IMPRIM,20060) NOVALP, VP, R1, NBOUR1, R2, NBOUR2,
     %                          NBCAVP, NINV(NOVALP), NBFACTKG, VP0
         ENDIF

10060 FORMAT(130('&')/'NLSEVV2: ATTENTION, VALEUR PROPRE',I4,
     %'  NON  CONVERGEE =',G14.6,
     %' R1=',G14.6,' NBOUR1=',I3,'  R2=',G14.6,' NBOUR2=',I3/
     %' APRES',I4,' CALCUL de R',I4,' ITERATIONS INVERSES',I4,
     %' FACTORISATIONS LDtL=KG - R MG'/
     %' POURSUITE DES CALCULS AVEC VP=',G14.6/130('&'))

20060 FORMAT(130('&')/'NLSEVV2: ATTENTION, NOT CONVERGED EIGENVALUE',
     %I4,' =',G14.6,
     %' R1=',G14.6,' NBOUR1=',I3,'  R2=',G14.6,' NBOUR2=',I3/
     %' after',I4,' COMPUTATION of R',I4,' INVERSE ITERATIONS',I4,
     %' FACTORIZATIONS LDtL=KG - R MG'/
     %' COMPUTATIONS CONTINUE WITH VP=',G14.6/130('&'))

C        ABANDON AVEC CETTE DERNIERE VALEUR CALCULEE
         VP = VP0
         GOTO 75

      ENDIF
C
C     ---------------------------------------------------
C     VP EST LA VALEUR PROPRE ET V EST SON VECTEUR PROPRE
C     ---------------------------------------------------
 70   IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10090) NOVALP, VP,  R1, NBOUR1, R2, NBOUR2,
     %                       NBCAVP, NINV(NOVALP), NBFACTKG
      ELSE
         WRITE(IMPRIM,20090) NOVALP, VP,  R1, NBOUR1, R2, NBOUR2,
     %                       NBCAVP, NINV(NOVALP), NBFACTKG
      ENDIF

10090 FORMAT(/'NLSEVV2: VALEUR PROPRE',I4,' CONVERGEE =',G14.6,
     %' R1=',G14.6,' NBOUR1=',I3,'  R2=',G14.6,' NBOUR2=',I3/
     %' APRES',I4,' CALCULS de R',I4,' ITERATIONS INVERSES',I4,
     %' FACTORISATIONS LDtL=KG - R MG')

20090 FORMAT('NLSEVV2: FINAL CONVERGED EIGENVALUE',I4,' =',G14.6,
     %' R1=',G14.6,' NBOUR1=',I3,'  R2=',G14.6,' NBOUR2=',I3/
     %' after',I4,' COMPUTATIONS of R',I4,' INVERSE ITERATIONS',I4,
     %' FACTORIZATIONS LDtL=KG - R MG')
C
C     STOCKAGE DE LA VALEUR PROPRE VP ET DU VECTEUR PROPRE V
C     ------------------------------------------------------
C     VP EST LA VALEUR PROPRE NOVALP DEFINITIVE
 75   VALPR(NOVALP) = VP
C
C     MG-ORTHOGONALISATION DE V AUX NBVAPR VECTEURS PROPRES
C     DEJA CALCULES
      CALL BORTHO( NBDLIB, NBVAPR, VECPR, MGVECPR, V )
C
C     MISE A ZERO DES VALEURS TRES PETITES
      DO I = 1, NBDLIB
         IF( ABS( V(I) ) .LT. 1D-20 ) V(I) = 0D0
      ENDDO
C
C     V SON VECTEUR PROPRE ASSOCIE
C     W = [MG] V  LE PRODUIT DE [MG] PAR V
      CALL MAPRVE( 0, 1D0, NBDLIB, NCODSM, MCN(MNMUMG), MG, V,  W )
C
C     MG ORTHONORMALISATION DU VECTEUR PROPRE V ET DE W=[MG] V
C     TVMGV = tV MG V
      TVMGV = 0D0
      DO I = 1, NBDLIB
         TVMGV = TVMGV + V(I) * W(I)
      ENDDO
C
C     V EST IL LE VECTEUR NUL?
      IF( ABS(TVMGV) .LE. 1D-3 ) THEN
C        OUI: RECHERCHE D'UN AUTRE VECTEUR V ORTHOGONAL A CEUX CALCULES
         WRITE(IMPRIM,*) 'NLSEVV2: VECTEUR PROPRE NUL ABS(TVMGV)=',TVMGV
         NUINVP = MOD( NUINVP, 9 ) + 1
         GOTO 10
      ENDIF
C
C     STOCKAGE ET NORMALISATION DU NOUVEAU VECTEUR PROPRE V ET W=[MG] V
      TVMGV = SQRT( ABS(TVMGV) )
      DO I = 1, NBDLIB
C        DIVISION PAR TVMGV POUR AVOIR FINALEMENT tV MG V = 1
         W(I) = W(I) / TVMGV
         MGVECPR(I,NOVALP) = W(I)
         V(I) = V(I) / TVMGV
         VECPR(I,NOVALP) = V(I)
      ENDDO

C     LE NOMBRE DE VALEURS ET VECTEURS PROPRES STOCKES
      NBVAPR = NOVALP
C
 9999 RETURN
      END
