      SUBROUTINE NLSEMK( NDIM,   NBNOMA, MNTOPO,
     %                   NBTYEL, MNNPEF, NDPGST, MNTPOB, MNTAUX,
     %                   MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                   MNTHER, MNTAEL, MNX,    MNVENL,
     %                   IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %                   IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %                   NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %                   NBDLIB, MNDLIB, IERR  )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    NLSE: CALCUL DE LA MATRICE GLOBALE DE CAPACITE [MG(Masse)]
C -----    ET/OU CONDUCTIVITE [KG]=[(tGrad Alfa Grad) + N(u)]
C          DES ELEMENTS FINIS LAGRANGE 1D OU 2D OU 3D DE DEGRE 1 OU 2
C
C          NLSE PROBLEM:
C          COMPUTATION OF THE CAPACITY GLOBAL MATRIX [MG(Masse)]
C                         THE CONDUCTIVITY GLOBAL MATRIX [KG]
C                         [KG]=[KG(tGrad Alfa Grad) + MG(N(u**2))]
C          FROM ISOPARAMETRIC LAGRANGE FINITE ELEMENT 1D or 2D or 3D
C          WITH POLYNOMIALS OF DEGREE 1 or 2 ON A REFERENCE FINITE ELEMENT
C
C ENTREES:
C --------
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 1 ou 2 ou 3 ou 6
C          (SI AXISYMETRIE NDIM=2 X => R>=0 et Y=>Z et Z=0)
C NBNOMA : NOMBRE DE NOEUDS DU MAILLAGE DE L'OBJET
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE DE L'OBJET
C NBTYEL : NOMBRE DE TYPES D'EF DE L'OBJET
C MNNPEF : ADRESSE MCN DU TMC DES ADRESSES MCN DES TABLEAUX NPEF"TYPE EF
C NDPGST : CODE DE STOCKAGE DES SOMMETS POINTS NOEUDS
C MNTPOB : ADRESSE MCN DES TABLEAUX POLYNOMES DE BASE DES TYPES D'EF
C MNTAUX : ADRESSE MCN DES TABLEAUX AUXILAIRES
C MNXYZP : ADRESSE MCN DE TMS XYZSOMMET DE L'OBJET
C NUMIOB : NUMERO MINIMAL DES OBJETS
C NUMAOB : NUMERO MAXIMAL DES OBJETS
C MNDOEL : ADRESSE MCN DES TMS DES DONNEES THERMIQUES DE L'OBJET
C MNTHER : ADRESSE MCN DU TABLEAU DES DONNEES THERMIQUES INTERNES
C          AU MOINS NPIMAX(=128) REELS DOUBLE PRECISION
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DES NBCOOR COORDONNEES DES POINTS DE L'EF COURANT
C
C MNVENL : ADRESSE MCN DU VECTEUR SERVANT A DEFINIR LA NON LINEARITE
C IEMG   : 1 POUR CALCULER MG, 0 POUR NE PAS CALCULER MG
C NCODSM : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CAPACITE
C          0 SI MATRICE DIAGONALE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDMG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE MG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NBNOMA SI MATRICE DIAGONALE
C MNMUMG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C
C IEKG   : 1 POUR CALCULER KG, 0 POUR NE PAS CALCULER KG
C NCODSK : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CONDUCTIVITE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDKG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE KG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NBNOMA SI MATRICE DIAGONALE
C MNMUKG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C
C SORTIES:
C --------
C MG     : MATRICE PROFIL GLOBALE DE CAPACITE CALCULEE
C KG     : MATRICE PROFIL GLOBALE DE CONDUCTIVITE CALCULEE
C NBDLIB : NOMBRE DE DEGRES DE LIBERTE LIBRES
C NBRDLX : NOMBRE DE DL SUPPORT D'UN DEGRE DE LIBERTE FIXE
C MONDLX : NOMBRE DE MOTS DECLARES DU TABLEAU MC NO DES DL FIXES
C MNNDLX : ADRESSE MCN DU TABLEAU MC DES NUMEROS DES DL FIXES, 0 SINON
C MNVDLX : ADRESSE MCN DU TABLEAU MC DES VALEURS DES DL FIXES, 0 SINON
C MNDLIB : MCN(MNDLIB-1+I)=NUMERO DE 1 A NBDLIB DU DL I (1 A NBNOMA) LIBRE
C                         -(INDICE DANS NODLFX) SI DL I EST FIXE
C          0  SI PAS DE DL BLOQUES
C IERR   : 0 SI PAS D'ERREUR, 1 SI EF NON PROGRAMME, 2 SI EF DEGENERE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TEXAS A&M UNIVERSITY at QATAR    Fevrier 2011
C23456---------------------------------------------------------------012
      DOUBLE PRECISION   PENALI
      PARAMETER         (PENALI=0D0)
      include"./incl/langue.inc"
      include"./incl/donthe.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/ponoel.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ctemps.inc"
      include"./incl/cnonlin.inc"
      include"./incl/cthet.inc"
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
C
      INTEGER           NUMIOB(4), NUMAOB(4),  MNDOEL(4)
      DOUBLE PRECISION  MG(NBRDMG), KG(NBRDKG)
      DOUBLE PRECISION  COMIMG, COMXMG, COMIKG, COMXKG
      DOUBLE PRECISION  D2PI
C     TABLEAU NON UTILISE (VITESSE D'UN FLUIDE INEXISTANT ICI)
      DOUBLE PRECISION  VITEGt(5,3)
C
C     CALCUL DU PROFIL DES MATRICES ICI SYMETRIQUES NON DIAGONALES
      CALL PRPRMC( MNTOPO, MCN(MNNPEF), MNXYZP, 1, NCODSK,
     %             MCN(MNMUKG), IERR )
      NBRDKG = MCN( MNMUKG + NBNOMA )
C
C     COPIE DU TABLEAU POINTEUR
C     (ENSUITE IL PEUT ETRE MODIFIE PAR LES CL DE DIRICHLET)
      IF( IEMG .NE. 0 ) THEN
         CALL TRTATA( MCN(MNMUKG), MCN(MNMUMG), 1+NBNOMA )
         NBRDMG = MCN( MNMUMG + NBNOMA )
      ENDIF
C
C     CALCUL DES MATRICES DE CAPACITE [MG(Masse)] et
C     CONDUCTIVITE [KG]=[tGrad Alfa Grad + N(u**2)]
C     a PARTIR de la capacite Masse, de la conductivite Alfa
C     et de la Fonction N(u**2) du CoefTemperature
C     ------------------------------------------------------
      NBJEUX = 1
      IEBG   = 0
      MNBG   = 0
      D2PI   = ATAN(1D0) * 8D0
      NORESO = 1
      MNLPCO = 0
      MNTHET = MNVENL
      CALL THEMKB( NBJEUX, IEMG,   IEKG,   IEBG,  PENALI,
     %             D2PI,   NDIM,   NBNOMA, VITEGt,
     %             NBTYEL, MNNPEF, NDPGST,
     %             MNTPOB, MXPOBA, MNTAUX,
     %             MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %             MNTHER, MNTAEL, MNX,    MNNODL,
     %             NORESO, MNMUKG, MNLPCO,
     %             NBRDMG, MG,     NBRDKG, KG,    MNBG,
     %             NCODSM, NCODSK, NBPTAF,  IERR )
      IF( IERR .NE. 0 ) GOTO 9999
C
      IF( LANGAG .EQ. 0 ) THEN
         IF( IEMG .GT. 0 ) THEN
            WRITE(IMPRIM,10001)
10001       FORMAT('NLSEMK: MATRICE [MG(Masse)] CALCULEE')
         ENDIF
         IF( IEKG .GT. 0 ) THEN
            WRITE(IMPRIM,10002)
10002 FORMAT('NLSEMK: MATRICE [KG]=[tGrad Alfa Grad + N(u**2)] CALCULEE'
     %)
         ENDIF
      ELSE
         IF( IEMG .GT. 0 ) THEN
            WRITE(IMPRIM,10003)
10003        FORMAT('NLSEMK: MATRIX [MG(Mass)] COMPUTED')
         ENDIF
         IF( IEKG .GT. 0 ) THEN
            WRITE(IMPRIM,10004)
10004       FORMAT('NLSEMK: MATRIX [KG]=[tGrad Alfa Grad + N(u**2)] COMP
     %UTED')
         ENDIF
      ENDIF
C
C     CONSTRUCTION DES TABLEAUX DU NUMERO ET VALEUR DES DL FIXES
C     A PARTIR DU TMS a___fixation QUI PREND EN COMPTE LES 2 PARTIES
C     --------------------------------------------------------------
      CALL DLFXLI( 0,      NBNOMA, NDIM,
     %             NBTYEL, MNNPEF, NDPGST,
     %             MNXYZP, NUMIOB, MNDOEL, RELMIN,
     %             NBRDLX, MONDLX, MNNDLX, MNVDLX, IERR )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     RENUMEROTATION DES DEGRES DE LIBERTE LIBRES NODLIB
C     --------------------------------------------------
C     NODLIB(I) = NUMERO DE 1 A NBNOMA DU DL S'IL EST VRAIMENT LIBRE
C                -INDICE DANS LA LISTE DES DL BLOQUES S'IL EST BLOQUE
C     NBDLIB    = NOMBRE TOTAL DE DEGRES DE LIBERTE LIBRES
      CALL REDLIB( NBRDLX, MCN(MNNDLX), NBNOMA,  NBDLIB, MNDLIB )
C
C     SUPPRESSION DES LIGNES ET COLONNES DES DL FIXES BLOQUES
C     DES MATRICES GLOBALES KG ET MG
C     -------------------------------------------------------
      IF( NBDLIB .LT. NBNOMA ) THEN
C
         IF( IEKG .NE. 0 ) THEN
            CALL PCDLID( NBNOMA, MCN(MNDLIB), NCODSK, MCN(MNMUKG), KG )
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10005) '[KG]',NBRDLX, NBNOMA
            ELSE
               WRITE(IMPRIM,20005) '[KG]',NBRDLX, NBNOMA
            ENDIF
         ENDIF
C
         IF( IEMG .NE. 0 ) THEN
            CALL PCDLID( NBNOMA, MCN(MNDLIB), NCODSM, MCN(MNMUMG), MG )
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10005) '[MG]',NBRDLX, NBNOMA
            ELSE
               WRITE(IMPRIM,20005) '[MG]',NBRDLX, NBNOMA
            ENDIF
         ENDIF
      ENDIF
10005 FORMAT('NLSEMK: ',A,' SUPPRESSION DES',I10,
     %' LIGNES et COLONNES des DL FIXES a ZERO parmi les',I10,' DL')
20005 FORMAT('NLSEMK: ',A,' SUPPRESSION of',I10,' LINES and COLUMNS of D
     %oF FIXED at NULL among the',I10,' DoF')
C
C     CALCUL DU COEFFICIENT MAXIMAL DES 2 MATRICES PROFILS REDUITES
C     -------------------------------------------------------------
      IF( IEKG .NE. 0 ) THEN
         CALL PRCOMIMX( MCN(MNMUKG+NBDLIB), KG, COMIKG, COMXKG )
ccc         CALL PRCOMX( NBDLIB, NCODSK, MCN(MNMUKG), KG, COMXKG )
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10008) '[KG]',COMIKG, '[KG]',COMXKG
         ELSE
            WRITE(IMPRIM,20008) '[KG]',COMIKG, '[KG]',COMXKG
         ENDIF
      ENDIF
C
      IF( IEMG .NE. 0 ) THEN
         CALL PRCOMIMX( MCN(MNMUMG+NBDLIB), MG, COMIMG, COMXMG )
ccc         CALL PRCOMX( NBDLIB, NCODSM, MCN(MNMUMG), MG, COMXMG )
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10008) '[MG]', COMIMG, '[MG]',COMXMG
         ELSE
            WRITE(IMPRIM,20008) '[MG]', COMIMG, '[MG]',COMXMG
         ENDIF
      ENDIF
10008 FORMAT('NLSEMK: COEFFICIENT MINIMAL de la MATRICE ',A,' =',G14.6/
     %       'NLSEMK: COEFFICIENT MAXIMAL de la MATRICE ',A,' =',G14.6)
20008 FORMAT('NLSEMK: MINIMUM COEFFICIENT of the MATRIX ',A,' =',G14.6/
     %       'NLSEMK: MAXIMUM COEFFICIENT of the MATRIX ',A,' =',G14.6)
C
C     AFFICHAGE DES RESULTATS
C     -----------------------
ccc      IF( IEMG .NE. 0 ) THEN
ccc       WRITE(IMPRIM,*)'NLSEMK: [MG] ',MCN(MNMUMG+NBDLIB),' COEFFICIENTS'
ccc      ENDIF
ccc      IF( IEKG .NE. 0 ) THEN
ccc       WRITE(IMPRIM,*)'NLSEMK: [KG] ',MCN(MNMUKG+NBDLIB),' COEFFICIENTS'
ccc      ENDIF
C
 9999 RETURN
      END
