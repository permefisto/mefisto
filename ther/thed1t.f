      SUBROUTINE THED1T( KNOMOB, MOREE2, NOAXIS,
     %                   NTLXOB, MNTOPO, NDIM,   NDPGST, MNXYZP, MNXYZN,
     %                   NBTYEL, MNNPEF, NBTTEF,
     %                   MNTPOB, NBDLMX, MOAUX,  MNTAUX,
     %                   NUMIOB, NUMAOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %                   MXTYEL, MXDOEL, MNDOEL,
     %                   IEMAST, IECHMA, IECOND, IEDILA, IEVIFL, IECOET,
     %                   IESOIN, IECONT, IEECHA, IESOCL, IESOPO,
     %                   MNTHER, MOTAEL, MNTAEL, MNX,
     %                   NORESO, MNLPLI, MNLPCO, NIVEAU, NBRDKG,
     %                   NCODSK, NCODSM, MNUG0,
     %                   DT,     DTSTOC, TPSINI, TPSFIN, NBVECT, MXVECT,
     %                   NTDL,   NTVECT, MNVECT,
     %                   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ENTRER LES DONNEES DU PROBLEME THERMIQUE INSTATIONNAIRE
C -----    D'ORDRE 1 EN TEMPS (PB PARABOLIQUE AVEC OPERATEUR -DIV(A GRAD U)
C          CALCULER LA TEMPERATURE INITIALE ET LE SECOND MEMBRE INITIAL
C
C SI TESTNL=0 A 4   LE PROBLEME EST CELUI DE LA CHALEUR AVEC 1 INCONNUE
C SI TESTNL=5,6,7,8 LE PROBLEME EST CELUI DE L'EQUATION COMPLEXE
C                   NON LINEAIRE DE SCHRODINGER AVEC 2 INCONNUES
C                   LA TEMPERATURE EST REMPLACEE PAR LA FONCTION D'ONDE
C                   COMPLEXE QUI SE DECLINE EN PARTIE REELLE ET IMAGINAIRE
C    TESTNL=5  NLSE Non Linear SCHRODINGER Equations
C              CALCUL DES VALEURS ET VECTEURS PROPRES(1 SEULE INCONNUE)
C              CE SP thed1t.f N'EST PAS APPELE DANS CE CAS Cf nlsevvp.f
C    TESTNL=6  NLSE Non Linear SCHRODINGER Equation  (2 INCONNUES)
C              INTEGRATION SEMI-IMPLICITE MODIFIEE AVEC MATRICE GLOBALE nxn
C    TESTNL=7  NLSE Non Linear SCHRODINGER Equations (2 INCONNUES)
C              INTEGRATION IMPLICITE AVEC MATRICE GLOBALE 2nx2n
C    TESTNL=8  NLSE Non Linear SCHRODINGER Equations (2 INCONNUES)
C              INTEGRATION EN TEMPS EXPLICITE SANS MATRICE GLOBALE
C    TESTNL=9  SCHEMA SEMI-IMPLICITE MODIFIE en TEMPS avec Matrice GLOBALE nxn
C              pour l'equation de GROSS-PITAEVSKI
C
C ENTREE :
C --------
C KNOMOB : NOM DE L'OBJET A TRAITER
C
C SORTIES:
C --------
C MOREE2 : NOMBRE DE MOTS   D'UNE VARIABLE REELLE DOUBLE PRECISION
C NOAXIS : 1 SI PB AXISYMETRIQUE, 0 SINON
C NTLXOB : NUMERO DU TMS DU LEXIQUE DE L'OBJET KNOMOB
C MNTOPO : NUMERO DU TMS TOPOLOGIE  DE L'OBJET KNOMOB
C NDIM   : DIMENSION DES COORDONNEES DES POINTS ( 2 OU 3 )
C NDPGST : CODE D'IDENTIFICATION DES SOMMETS POINTS NOEUDS
C MNXYZP : ADRESSE MCN DU TABLEAU XYZPOINT DE L'OBJET KNOMOB
C MNXYZN : ADRESSE MCN DU TABLEAU XYZNOEUD DE L'OBJET KNOMOB
C
C NBTYEL : NOMBRE DE TYPES D'EF DU MAILLAGE DE CET OBJET
C MNNPEF : ADRESSE MCN DU TABLEAU DES ADRESSES MCN DES TMS NPEF"TYPE EF
C NBTTEF : NOMBRE TOTAL D'EF DU MAILLAGE
C MNTPOB : ADRESSE MCN DU TABLEAU POINTEUR SUR LES TABLEAUX POBA DES EF
C NBDLMX : NOMBRE MAXIMAL DE DEGRES DE LIBERTE (TEMPERATURES) D'UN EF
C MOAUX  : NOMBRE DE OTS AUXILIAIRES NECESSAIRES AU CALCUL DES EF
C MNTAUX : ADRESSE MCN DU TABLEAU AUXILIAIRE POUR LES EF
C
C NUMIOB : NUMERO MINIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C NUMAOB : NUMERO MAXIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C NBOBIN : NOMBRE DE VOLUMES en 3D, SURFACES en 2D DE L'OBJET
C MNOBIN : ADRESSE MCN DU DEBUT DU TABLEAU NUOBIN (PARTIE DE TOPOLOGIE)
C NBOBCL : NOMBRE DE PLS en 3D, PL en 2D DE L'OBJET
C MNOBCL : ADRESSE MCN DU DEBUT DU TABLEAU NUOBCL (PARTIE DE TOPOLOGIE)
C MXTYEL : NOMBRE MAXIMAL DE TYPES D'EF (7)
C MXDOEL : NOMBRE DE MOTS DECLARES DES TABLEAUX D'ADRESSE DANS MNDOEL
C MNDOEL : LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C          TABLEAUX DECRIVANT LES DONNEES THERMIQUES DE L'OBJET COMPLET
C
C IEMAST : NOMBRE DE TMS MASSE            DES SV DE L'OBJET RETROUVES
C IECHMA : NOMBRE DE TMS CHALEURMASSIQUE  DES SV DE L'OBJET RETROUVES
C IECOND : NOMBRE DE TMS CONDUCTIVITE     DES SV DE L'OBJET RETROUVES
C IEDILA : NOMBRE DE TMS DILATATION       DES SV DE L'OBJET RETROUVES
C IEVIFL : NOMBRE DE TMS VITESSEFLUIDE    DES SV DE L'OBJET RETROUVES
C IECOET : NOMBRE DE TMS COEFTEMPERATURE  DES SV DE L'OBJET RETROUVES
C IESOIN : NOMBRE DE TMS SOURCE "INTERNE" DES SV DE L'OBJET RETROUVES
C
C IECONT : NOMBRE DE TMS CONTACT              DES PLS DE L'OBJET RETROUVES
C IEECHA : NOMBRE DE TMS ECHANGE              DES PLS DE L'OBJET RETROUVES
C IESOCL : NOMBRE DE TMS SOURCE "AUX LIMITES" DES PLS DE L'OBJET RETROUVES
C IESOPO : NOMBRE DE TMS SOURCE               DES P   DE L'OBJET RETROUVES
C
C MNTHER : 128 REELS DOUBLE PRECISION POUR LA MATRICE DE CONDUCTIVITE
C MOTAEL : NOMBRE DE MOTS DECLARES DU TABLEAU DES TABLEAUX ELEMENTAIRES
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DU TABLEAU DES NBCOOR COORDONNEES DES NOEUDS=POINTS DE L'
C
C NORESO : CODE RESOLUTION DES SYSTEMES LINEAIRES
C          1 FACTORISATION DE CHOLESKY ET MATRICES PROFILS
C          2 GRADIENT CONJUGUE SIMPLE AVEC MATRICES MORSES
C MNLPLI : ADRESSE MCN DES POINTEURS SUR LES COEFFICIENTS DIAGONAUX DE LA MATRIC
C MNLPCO : ADRESSE MCN DU NUMERO DE COLONNE DES COEFFICIENTS DE LA MATRICE MORSE
C NIVEAU : NIVEAU DE FACTORISATION INCOMPLETE DE LA MATRICE DE PRECONDITIONNEMEN
C          A CHOISIR PARMI 0 1 2
C NBRDKG : NOMBRE DE REELS DOUBLE PRECISION DES MATRICES M ET K
C NCODSK : 1 SI MATRICE DE CONDUCTIVITE SYMETRIQUE
C          0 SI MATRICE DE CONDUCTIVITE DIAGONALE
C         -1 SI MATRICE DE CONDUCTIVITE NON SYMETRIQUE
C NCODSM : 1 SI MATRICE DE CAPACITE SYMETRIQUE
C          0 SI MATRICE DE CAPACITE DIAGONALE
C         -1 SI MATRICE DE CAPACITE NON SYMETRIQUE
C MNUG0  : ADRESSE MCN DE U0 TEMPERATURE A L'INSTANT INITIAL
C
C DT     : PAS CONSTANT DU TEMPS
C DTSTOC : PAS CONSTANT DU TEMPS ENTRE 2 STOCKAGES DU VECTEUR"TEMPERATURE
C TPSINI : TEMPS INITIAL DU CALCUL
C TPSFIN : TEMPS FINAL   DU CALCUL
C NBVECT : NUMERO DU DERNIER VECTEUR TEMPERATURE STOCKE = VECTEUR A TPSINI
C MXVECT : NOMBRE DE VECTEUR"TEMPERATURE A STOCKER
C NTDL   : NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET
C NTVECT : NUMERO      DU TMS VECTEUR"TEMPERATURE DE L'OBJET
C MNVECT : ADRESSE MCN DU TMS VECTEUR"TEMPERATURE DE L'OBJET
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEURS: ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS        MARS 1999
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/donthe.inc"
      include"./incl/donele.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a_objet__definition.inc"
      include"./incl/a_objet__erreurth.inc"
      include"./incl/a___arete.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___profil.inc"
      include"./incl/a___morse.inc"
      include"./incl/a___fluxpt.inc"
      include"./incl/a___dtemperature.inc"
      include"./incl/a___temperinit.inc"
      include"./incl/a___ondeinit.inc"
      include"./incl/msvaau.inc"
      include"./incl/ponoel.inc"
      include"./incl/ctemps.inc"
      include"./incl/cthet.inc"
      include"./incl/cnonlin.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(MOTMCN)
      DOUBLE PRECISION  DMCN(MOTMCN/2)
      EQUIVALENCE      (MCN(1), RMCN(1), DMCN(1))
C
      EXTERNAL          ETTAEL
      INTEGER           NUMIOB(4), NUMAOB(4), MNDOEL(4), MXDOEL(4)
      CHARACTER*(*)     KNOMOB
      CHARACTER*80      NOMTS, KNOMTD
      CHARACTER*20      KNMVEC
C
      DOUBLE PRECISION  WCAMIN(2), WCAMAX(2),WEXMIN(2), WEXMAX(2),
     %                  ERRMAX(2)
      INTEGER           NOFOWE(2)
C
C     QUELQUES INITIALISATIONS
C     ========================
      IF( TESTNL .LT. 5 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10000) KNOMOB
         ELSE
            WRITE(IMPRIM,20000) KNOMOB
         ENDIF
      ELSE
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10001) KNOMOB
         ELSE
            WRITE(IMPRIM,20001) KNOMOB
         ENDIF
      ENDIF
10000 FORMAT(/'DONNEES DE LA THERMIQUE INSTATIONNAIRE DE L''OBJET ',A/)
20000 FORMAT(/'DATA of UNSTEADY HEAT TRANSFER of the OBJECT ',A/)
10001 FORMAT(/'DONNEES de l''EQUATION NON LINEAIRE de SCHRODINGER de l'
     %'OBJET ',A/)
20001 FORMAT(/'DATA of NONLINEAR SCHRODINGER EQUATION of the OBJECT ',
     %A/)
C
      IERR   = 0
      TEMPS  = 0
      TPSINI   = TEMPS
      TEMPSINI = TPSINI
      TEMPSFIN = TPSINI
C
C     PROTECTION DES ADRESSES POUR EVITER DES PROBLEMES LORS
C     DE LA DESTRUCTION DES TABLEAUX
      NBDLMXEF = 0
      NBJEUX = 1
      MNTPOB = 0
      MNTAUX = 0
      MNTAEL = 0
      MNTHER = 0
      MNNODL = 0
      MONODL = 0
      MNX    = 0
      MNBDIR = 0
      MNLPLI = 0
      MNLPCO = 0
      MNTAUX = 0
      MNNPEF = 0
      MNUG0  = 0
      MNTHET = 0
      MNTHDL = 0
      DO I=1,4
         MNDOEL(I) = 0
         MXDOEL(I) = 0
      ENDDO
      TEMPEL = 0D0
      MOTSGC = 0
C
C     AFFICHAGE ET VERIFICATION DU NOM_DE_L'OBJET
C     ===========================================
C     RECHERCHE DU NOM DE L'OBJET DANS LE LEXIQUE DES OBJETS
      CALL LXLXOU( NTOBJE, KNOMOB, NTLXOB, MNLXOB )
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET ' // KNOMOB
            KERR(2) = 'OBJET INCONNU'
         ELSE
            KERR(1) = 'ERROR: OBJECT ' // KNOMOB
            KERR(2) = 'IS UNKNOWN'
         ENDIF
         CALL LEREUR
         CALL LXIM( NTOBJE )
         IERR = 1
         RETURN
      ENDIF
C
C     RECHERCHE DU TABLEAU DEFINITION DE L'OBJET
      CALL LXTSOU( NTLXOB, 'DEFINITION', NTDFOB, MNDFOB )
C
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
      IF( NTDFOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET ' // KNOMOB
            KERR(2) = 'DEFINITION INCONNUE'
         ELSE
            KERR(1) = 'ERROR: OBJECT ' // KNOMOB
            KERR(2) = 'DEFINITION UNKNOWN'
         ENDIF
         CALL LEREUR
         RETURN
      ENDIF
C
C     ENTREE DU TEMPS INITIAL : TPSINI
C     ================================
      CALL INVITE( 95 )
      NCVALS = 0
      CALL LIRRSP( NCVALS, TPSINI )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 1
        RETURN
      ENDIF
C     LE TEMPS INITIAL POUR L'ENTREE DES DONNEES
      TEMPS    = TPSINI
      TEMPSINI = TPSINI
C
      IF( TESTNL .LT. 5 ) THEN
C
C        ENTREE DE LA TEMPERATURE INITIALE DE L'OBJET:
C        CONSTRUCTION DU TMS '~>OBJET>NomObjet>TEMPERINIT'
C        =================================================
         L1 = NUDCNB(KNOMOB)
         NOMTS  = '~>OBJET>' // KNOMOB(1:L1) // '>TEMPERINIT'
         KNOMTD = '~>>>TEMPERINIT'
         L1 = NUDCNB(NOMTS)
         CALL MOTSTD( KNOMTD, NOMTS(1:L1), IERR )
         IF( IERR .NE. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
         KERR(1)='IMPOSSIBLE RECUPERER TEMPERATURE INITIALE de l''OBJET'
            ELSE
       KERR(1)='IMPOSSIBLE to RECOVER INITIAL TEMPERATURE of the OBJECT'
            ENDIF
            CALL LEREUR
            IERR = 1
            RETURN
         ENDIF
C
      ELSE
C
C        ENTREE DE L'ONDE INITIALE COMPLEXE U0: LECTURE DU TMS ONDEINIT
C        DEMANDEE AVEC SES 2 COMPOSANTES: PARTIE REELLE ET IMAGINAIRE
C        ==============================================================
         L1     = NUDCNB(KNOMOB)
         NOMTS  = '~>OBJET>' // KNOMOB(1:L1) // '>ONDEINIT'
         KNOMTD = '~>>>ONDEINIT'
         L1 = NUDCNB(NOMTS)
         CALL MOTSTD( KNOMTD, NOMTS(1:L1), IERR )
         IF( IERR .NE. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'ERREUR: ONDE INITIALE COMPLEXE INCONNUE'
            ELSE
               KERR(1) = 'ERROR: UNKNOWN INITIAL COMPLEX WAVE'
            ENDIF
            CALL LEREUR
            RETURN
         ENDIF
C        AFFICHAGE DU TMS
         CALL AFTSTD( NOMTS )
C
      ENDIF
C
C     ENTREE DU TEMPS FINAL : TPSFIN
C     ==============================
      CALL INVITE( 94 )
      NCVALS = 0
      CALL LIRRSP( NCVALS, TPSFIN )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 1
        RETURN
      ENDIF
      IF( TPSFIN .LE. TPSINI ) THEN
        NBLGRC(NRERR) = 3
        WRITE(KERR(5)(1:15),'(G15.7)') TPSINI
        WRITE(KERR(6)(1:15),'(G15.7)') TPSFIN
        IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'ERREUR: TEMPS FINAL INCORRECT'
           KERR(2) = 'TEMPS INITIAL ' //  KERR(5)(1:15)
           KERR(3) = 'TEMPS FINAL   ' //  KERR(6)(1:15)
        ELSE
           KERR(1) = 'ERROR: INCORRECT FINAL TIME'
           KERR(2) = 'INITIAL TIME ' //  KERR(5)(1:15)
           KERR(3) = 'FINAL   TIME ' //  KERR(6)(1:15)
        ENDIF
        CALL LEREUR
        IERR = 1
        RETURN
      ENDIF
      TEMPSFIN = TPSFIN
C
C     ENTREE DU PAS DE TEMPS CONSTANT: DT
C     ===================================
      CALL INVITE( 86 )
      NCVALS = 0
      CALL LIRRSP( NCVALS, DT )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 1
        RETURN
      ENDIF
      IF( DT .LE. 0 ) THEN
        NBLGRC(NRERR) = 2
        WRITE(KERR(5)(1:15),'(G15.7)') DT
        IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'ERREUR: PAS DE TEMPS <=0 INCORRECT'
           KERR(2) = 'PAS DE TEMPS ' // KERR(5)(1:15)
        ELSE
           KERR(1) = 'ERROR: STEP of TIME <=0 INCORRECT'
           KERR(2) = 'STEP of TIME ' // KERR(5)(1:15)
        ENDIF
        CALL LEREUR
        IERR = 1
        RETURN
      ENDIF
      PasTemps = DT
C
C     ENTREE DU NOMBRE MAXIMAL DE VECTEUR"TEMPERATURE A STOCKER
C     =========================================================
      CALL INVITE( 76 )
      MXVECT = 5
      NCVALS = 4
      CALL LIRENT( NCVALS, MXVECT )
      IF( NCVALS .LT. 0 ) THEN
C       ABANDON DE LA LECTURE DES DONNEES
        IERR = 1
        RETURN
      ENDIF
      IF( MXVECT .LE. 1 ) THEN
        NBLGRC(NRERR) = 2
        IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'STOCKAGE MINIMAL DES TEMPERATURES'
           KERR(2) = 'AU TEMPS MINIMAL ET MAXIMAL'
        ELSE
           KERR(1) = 'MINIMAL STORAGE of TEMPERATURES'
           KERR(2) = 'at MINIMUM and MAXIMAL TIME'
        ENDIF
        CALL LEREUR
        MXVECT = 1
        RETURN
      ENDIF
C
C     NOMBRE DE PAS DE TEMPS A CALCULER LA TEMPERATURE
      J = NINT( ( TPSFIN - TPSINI ) / DT )
      IF( MXVECT .GT. J ) MXVECT = J
C
C     LE PAS DE TEMPS POUR LES STOCKAGES DES VECTEURS TEMPERATURES
      DTSTOC = ( TPSFIN - TPSINI ) / MXVECT
C
C     L'OBJET EST-IL STRUCTURE CLASSIQUE, SOUS-DOMAINES OU JOINTS?
      NDOUNO = MCN(MNDFOB+WDOUNO)
      IF( NDOUNO .GT. 0 ) THEN
C
C        RESOLUTION PAR LA METHODE DES SOUS-DOMAINES OU JOINTS NON PROGRAMMEE
         IERR = 4
         RETURN
C
      ENDIF
C
C     RECHERCHE DES TABLEAUX XYZSOMMET XYZNOEUD XYZPOINT NPEF"
C     ASSOCIES A L'OBJET
C     ========================================================
      CALL MIMAOB( NBJEUX, NTLXOB, MXDOTH, NTTOPO, MNTOPO,
     %             NTXYZP, MNXYZP, NTXYZN, MNXYZN,
     %             MXTYEL, NBTYEL, MTNPEF, MNNPEF,
     %             NUMIOB, NUMAOB,
     %             NDPGST, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %             MXDOEL, MNDOEL, IERR )
      IF( IERR .NE. 0 ) GOTO 9999
C     ICI NUMIOB CONTIENT LES 4 NUMEROS MINIMA DES OBJETS
C         NUMAOB          LES 4 NUMEROS MAXIMA DES OBJETS
C     MNDOEL LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C     TABLEAUX DECRIVANT LA THERMIQUE DE L'OBJET COMPLET
C
C     LE NOMBRE DE POINTS DU MAILLAGE DE L'OBJET
      NBPOI = MCN( MNXYZP + WNBPOI )
C
C     NDIM LA DIMENSION 2 OU 3 DE L'ESPACE DES COORDONNEES
      CALL DIMCOO( NBPOI, MCN(MNXYZP+WYZPOI), NDIM )
C
C     RETROUVER LES ADRESSES MCN DES DONNEES THERMIQUES
C     DES   SV "OBJETS INTERNES"    DE L'OBJET
C     DES PLS  "OBJETS AUX LIMITES" DE L'OBJET
C     =================================================
      CALL THEDON( NUMIOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %             NBJEUX, MNDOEL,
     %             IEMAST, IECHMA, IECOND, IEDILA, IEVIFL, IECOET,
     %             IESOIN, IECONT, IEECHA, IESOCL, IESOPO,
     %             IETEIN, IEVIIN, IEVIANT,IECOBO, IERR )
C
      IF( IEMAST .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='Au MOINS UN VOLUME SANS DENSITE de MASSE'
             ELSE IF( NDIM .EQ. 2 ) THEN
           KERR(1)='Au MOINS UNE SURFACE SANS DENSITE de MASSE'
             ELSE IF( NDIM .EQ. 1 ) THEN
           KERR(1)='Au MOINS UNE LIGNE SANS DENSITE de MASSE'
             ENDIF
             KERR(2)='=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='At LEAST 1 VOLUME WITHOUT MASS DENSITY'
             ELSE IF( NDIM .EQ. 2 ) THEN
            KERR(1)='At LEAST 1 SURFACE WITHOUT MASS DENSITY'
             ELSE IF( NDIM .EQ. 1 ) THEN
            KERR(1)='At LEAST 1 LINE WITHOUT MASS DENSITY'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 10
      ENDIF
C
      IF( IECHMA .NE. NBOBIN .AND. TESTNL .LT. 5 ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='Au MOINS UN VOLUME SANS CHALEUR MASSIQUE'
             ELSE IF( NDIM .EQ. 2 ) THEN
           KERR(1)='Au MOINS UNE SURFACE SANS CHALEUR MASSIQUE'
             ELSE IF( NDIM .EQ. 1 ) THEN
           KERR(1)='Au MOINS UNE LIGNE SANS CHALEUR MASSIQUE'
             ENDIF
             KERR(2)='=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='At LEAST 1 VOLUME WITHOUT MASSIC HEAT'
             ELSE IF( NDIM .EQ. 2 ) THEN
            KERR(1)='At LEAST 1 SURFACE WITHOUT MASSIC HEAT'
             ELSE IF( NDIM .EQ. 1 ) THEN
            KERR(1)='At LEAST 1 LINE WITHOUT MASSIC HEAT'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 10
      ENDIF
C
      IF( IECOND .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='AU MOINS UN VOLUME SANS CONDUCTIVITE'
             ELSE IF( NDIM .EQ. 2 ) THEN
                KERR(1)='AU MOINS UNE SURFACE SANS CONDUCTIVITE'
             ELSE IF( NDIM .EQ. 1 ) THEN
                KERR(1)='AU MOINS UNE LIGNE SANS CONDUCTIVITE'
             ENDIF
             KERR(2) = '=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='At LEAST 1 VOLUME WITHOUT CONDUCTIVITY'
             ELSE IF( NDIM .EQ. 2 ) THEN
                KERR(1)='At LEAST 1 SURFACE WITHOUT CONDUCTIVITY'
             ELSE IF( NDIM .EQ. 1 ) THEN
                KERR(1)='At LEAST 1 LINE WITHOUT CONDUCTIVITY'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 11
      ENDIF
      IF( IERR .NE. 0 ) GOTO 9999
C
C     INITIALISATIONS DE VARIABLES ET AFFICHAGES
C     ==========================================
C     LE NOMBRE DE NOEUDS DU MAILLAGE DE L'OBJET EST EGAL ICI
C     AU NOMBRE TOTAL DE DEGRES DE LIBERTE (CAS CONFORME)
      NBNOEU = MCN(MNXYZN+WNBNOE)
      IF( TESTNL .LT. 5 ) THEN
C        1 INCONNUE PAR NOEUD
         NBVAND = 1
C        AUTANT DE DL QUE DE NOEUDS
         NTDL   = NBNOEU
      ELSE
C        NOMBRE DE VARIABLES PAR NOEUD Partie Reelle et Imaginaire
         NBVAND = 2
C        NOMBRE DE DL = 2 DL PAR NOEUD
         NTDL   = NBNOEU * NBVAND
      ENDIF
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10010) NDIM,   NBNOEU, NBVAND, NTDL,
     %                       TPSINI, TPSFIN, DT
      ELSE
         WRITE(IMPRIM,20010) NDIM,   NBNOEU, NBVAND, NTDL,
     %                       TPSINI, TPSFIN, DT
      ENDIF
10010 FORMAT(/
     %' DIMENSION 2 ou 3 de l''ESPACE   =',I12/
     %' NOMBRE de NOEUDS               =',I12/
     %' NOMBRE DE D.L. PAR NOEUD       =',I12/
     %' NOMBRE de DEGRES de LIBERTE    =',I12/
     %' TEMPS INITIAL de l''ETUDE       =',G14.6/
     %' TEMPS FINAL   de l''ETUDE       =',G14.6/
     %' VALEUR du PAS de TEMPS CONSTANT =',G14.6/)
20010 FORMAT(/
     %' SPACE DIMENSION (2 or 3)     =',I12/
     %' NUMBER of NODES              =',I12/
     %' NUMBER of DoF per NODE       =',I12/
     %' NUMBER of DEGREES of FREEDOM =',I12/
     %' INITIAL TIME of the STUDY    =',G14.6/
     %' FINAL   TIME of the STUDY    =',G14.6/
     %' CONSTANT STEP VALUE of TIME  =',G14.6/)
C
C     RECUPERATION DES TABLEAUX POBA NECESSAIRES A LA
C     CONSTRUCTION DES TABLEAUX ELEMENTAIRES THERMIQUES
C     =================================================
      CALL TAPOBA( NBTYEL, MNNPEF, ETTAEL,
     %             MNTPOB, NBDLMX, MOAUX, NBTTEF, NOAXIS, NCODSM, IERR )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     ADRESSAGE DES TABLEAUX AUXILIAIRES ET ELEMENTAIRES
C     ===================================================
C     LES TABLEAUX AUXILIAIRES POUR LES ELEMENTS FINIS
      CALL TNMCDC( 'REEL2', MOAUX, MNTAUX )
C
C     MOTAEL = 2 MATRICES ELEMENTAIRES (DE CAPACITE + DE CONDUCTIVITE)
C              ET MXVECT SECOND MEMBRES
      MOTAEL = NBDLMX * (NBDLMX+1) + NBDLMX * (1+MXVECT)
      CALL TNMCDC( 'REEL2', MOTAEL, MNTAEL )
C
C     MNTHER : CONDUCTIVITE AUX POINTS D'INTEGRATION NUMERIQUE DE L'EF
      CALL TNMCDC( 'REEL2', 128, MNTHER )
C
C     LE TABLEAU DU NUMERO DES DEGRES DE LIBERTE D'UN ELEMENT FINI
      MONODL = NBDLMX * 2
      CALL TNMCDC( 'ENTIER', MONODL, MNNODL)
C
C     LE TABLEAU DES 3 COORDONNEES DES NBDLMX POINTS DE L'EF MAXIMAL
      MNX = 0
      NBCOOR = MCN(MNXYZN + WBCOON)
      CALL TNMCDC( 'REEL', NBDLMX*NBCOOR, MNX )
C     LE TABLEAU DES 3 COORDONNEES DES NBDLMX POINTS DE L'EF MAXIMAL
C
C     CHOISIR LA METHODE DE RESOLUTION DU SYSTEME LINEAIRE
C     ====================================================
 11   CALL LIMTCL( 'methreso', NORESO )
      IF( NORESO .LE. 0 ) THEN
         IERR = 1
         GO TO 9999
      ENDIF
C
C     NORESO=1 POUR FACTORISATION DE CHOLESKY ou CROUT SUR MATRICE PROFIL
C            2 POUR GRADIENT CONJUGUE SIMPLE SUR MATRICE MORSE
C
      IF( NORESO .EQ. 2 ) THEN
C
C        LECTURE DU NIVEAU DE FACTORISATION INCOMPLETE=0,1, ...
         CALL INVITE( 36 )
         NIVEAU = 0
         NCVALS = 4
         CALL LIRENT( NCVALS, NIVEAU )
         IF( NCVALS .LT. 0 ) THEN
            IERR = 1
            GO TO 9999
         ENDIF
C
      ELSE IF( NORESO .GE. 3 ) THEN
C
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: METHODE DE RESOLUTION NON PROGRAMMEE'
         ELSE
            KERR(1) = 'ERROR: METHOD of COMPUTATION NOT PROGRAMMED'
         ENDIF
         CALL LEREUR
         IF( INTERA .GE. 3 ) GOTO 11
         CALL ARRET( 100 )
         RETURN
      ENDIF
C
C     CAS PARTICULIER DU PROBLEME DES NLSE
      IF( TESTNL .EQ. 5 ) THEN
C        ACTUELLEMENT LA MATRICE PROFIL EST IMPOSEE PLUS TARD
C        POUR LE CALCUL DES VVP DE NLSE. PAS DE POINTEURS CONSTRUITS ICI
         NORESO = 0
         GOTO 30
ccc      ELSE IF( TESTNL .EQ. 6 ) THEN
cccC      NORESO EST CHOISI PAR L'UTILISATEUR
      ELSE IF( TESTNL .EQ. 7 ) THEN
C        LA MATRICE PROFIL 2nx2n EST IMPOSEE POUR NLSE SCHEMA IMPLICITE
         NORESO = 1
      ELSE IF( TESTNL .EQ. 8 ) THEN
C        RESOLUTION NLSE EXPLICITE
C        PAS DE POINTEURS CONSTRUITS ICI CAR PAS DE MATRICE GLOBALE
         NORESO = 0
         GOTO 30
ccc      ELSE IF( TESTNL .EQ. 9 ) THEN
cccC      NORESO EST CHOISI PAR L'UTILISATEUR
      ENDIF
C
C     PREPARATION DE LA MATRICE PROFIL OU MORSE
C     =========================================
      NCODSK = 1
      NBLIGK = NTDL
      IF( TESTNL .EQ. 6 .OR. TESTNL .EQ. 9 ) THEN
C        KG matrice nxn ou n=nombre de noeuds du maillage
         NBLIGK = NTDL / 2
C        MATRICE SUR UNE SEULE PART REELLE ou IMAGINAIRE de l'ONDE
C        => CONDITION DE DIRICHLET POUR LES 2 PARTIES AUX MEMES NOEUDS
         NBVAND = 1
      ENDIF
C
      IF ( NORESO .EQ. 1 ) THEN
C
         IF( TESTNL .LT. 5 ) THEN
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10015) 'CHOLESKY'
            ELSE
               WRITE(IMPRIM,20015) 'CHOLESKY'
            ENDIF
         ELSE
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10015) 'CROUT'
            ELSE
               WRITE(IMPRIM,20015) 'CROUT'
            ENDIF
         ENDIF
10015    FORMAT(' RESOLUTION DIRECTE PAR FACTORISATION DE ',A)
20015    FORMAT(' DIRECT COMPUTATION by ',A,' FACTORIZATION')
C
C        STOCKAGE PROFIL ET FACTORISATION DE CHOLESKY
C        --------------------------------------------
C        CALCUL DU PROFIL DE LA MATRICE
C        (LA MATRICE PROFIL EST ICI SYMETRIQUE)
         MNLPLI = 0
         CALL TNMCDC( 'ENTIER', NBLIGK+1, MNLPLI )
         CALL PRPRMC( MNTOPO  , MCN(MNNPEF), MNXYZN, NBVAND, NCODSK,
     %                MCN(MNLPLI), IERR )
         IF( IERR .NE. 0 ) GOTO 9999
C
C        DECLARATION INITIALISATION DE LA MATRICE PROFIL SYMETRIQUE
         NBRDKG = MCN( MNLPLI + NBLIGK )
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10016) NBLIGK+MOREE2*NBRDKG
         ELSE
            WRITE(IMPRIM,20016) NBLIGK+MOREE2*NBRDKG
         ENDIF
10016    FORMAT(' STOCKAGE MATRICE PROFIL =',I15,' MOTS MEMOIRE',/ )
20016    FORMAT(' SKYLINE MATRIX STORAGE =',I15,' MEMORY WORDS',/ )
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        POINTEURS DU STOCKAGE MORSE POUR METHODE DU GRADIENT CONJUGUE
C        -------------------------------------------------------------
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10018)
         ELSE
            WRITE(IMPRIM,20018)
         ENDIF
10018    FORMAT(' RESOLUTION ITERATIVE PAR GRADIENT CONJUGUE')
20018    FORMAT(' ITERATIVE COMPUTATION by PRECONDITIONATED CONJUGATE GR
     %ADIENT')
C
C        CALCUL DU SQUELETTE DE LA MATRICE
C        (LA MATRICE EST ICI SYMETRIQUE)
         MNLPLI = 0
         MNLPCO = 0
         CALL PRGCMC( MNTOPO, MCN(MNNPEF), MNXYZN,
     %                NBVAND, NCODSK,
     %                MNLPLI, MNLPCO, IERR )
         IF( IERR .NE. 0 ) GOTO 9999
C
C        DECLARATION INITIALISATION DE LA MATRICE MORSE SYMETRIQUE
         NBRDKG = MCN(MNLPLI+NBLIGK)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10020) NBLIGK+(1+MOREE2)*NBRDKG
         ELSE
            WRITE(IMPRIM,20020) NBLIGK+(1+MOREE2)*NBRDKG
         ENDIF
10020    FORMAT(' STOCKAGE MATRICE MORSE =',I15,' MOTS MEMOIRE',/ )
20020    FORMAT(' CONDENSED MATRIX STORAGE =',I15,' MEMORY WORDS',/ )
C
      ENDIF
C
 30   IF( TESTNL .LT. 5 ) THEN
C
C        INITIALISATION DU VECTEUR UG0 DES TEMPERATURES INITIALES
C        AUX NOEUDS DU MAILLAGE ET A L'INSTANT TEMPS INITIAL
C        ========================================================
C        UG0 EST LE VECTEUR GLOBAL DES TEMPERATURES INITIALES
         CALL TEMINI( KNOMOB, NTLXOB, MOREE2, NTDL, TPSINI, IETEIN,
     %                NBTYEL, MNNPEF, NDPGST,
     %                MNXYZN, NUMIOB, MNDOEL,
     %                NTVECT, MNVECT, NBVECT, MNUG0, IERR )
C
      ELSE
C
C        INITIALISATION DU VECTEUR UG0 DE L'ONDE NLSE INITIALE
C        AUX NOEUDS DU MAILLAGE ET A L'INSTANT TEMPS INITIAL
C        =====================================================
C        UG0 EST LE VECTEUR GLOBAL DE L'ONDE NLSE INITIALE
         CALL ONDINI( KNOMOB, NTLXOB, MOREE2, NTDL, TPSINI, IETEIN,
     %                NBTYEL, MNNPEF, NDPGST,
     %                MNXYZN, NUMIOB, MNDOEL,
     %                NTVECT, MNVECT, NBVECT, MNUG0, IERR )
C
C        AFFICHAGE DE LA SOLUTION INITIALE AUX 10 NOEUDS MEDIANS
         CALL AFNLSE( 10,  1, MNXYZN, NBNOEU, 1, MCN(MNUG0),
     %                WCAMIN, WCAMAX, NOFOWE, WEXMIN, WEXMAX, ERRMAX )
C
      ENDIF
      IF( IERR .NE. 0 ) GOTO 9999
C
C     ADRESSAGE INITIALISATION DU VECTEUR"TEMPERATURE SUR LE MAILLAGE TOTAL
C     =====================================================================
      MNTIMES = 0
      IF( TESTNL .LT. 5 ) THEN
         KNMVEC = 'VECTEUR"TEMPERATURE'
      ELSE
         KNMVEC = 'VECTEUR"ONDENLSE'
      ENDIF
      CALL LXTSOU( NTLXOB, KNMVEC, NTVECT, MNVECT )
      IF( NTVECT .GT. 0 ) THEN
C
         IF( NBVECT .LE. 0 ) THEN
C
C           DESTRUCTION DU TMS VECTEUR"...
            CALL LXTSDS( NTLXOB, KNMVEC )
C
         ELSE
C
C           LE VECTEUR EXISTE DEJA. MXVECT NOUVEAUX VECTEURS LUI
C           SONT AJOUTES A PARTIR DU VECTEUR NBVECT QUI EST VECTEUR INITIAL
C
C           PROTECTION DES NBVECT PREMIERS TEMPS
            CALL LESTEMPS( KNOMOB, MNVECT, NBVECT, MNTIMES, IERR )
C
C           AUGMENTATION DE LA TAILLE DU TMS VECTEUR"...
            MXVECT = NBVECT + MXVECT
            IF( MXVECT .GT. MCN(MNVECT+WBVECT) ) THEN
C              LE TMS EST AUGMENTE
               L = WECTEU + NTDL * MXVECT * MOREE2 + MXVECT
               CALL TAMSAU( NTVECT, L )
               CALL LXTSOU( NTLXOB, KNMVEC,
     %                      NTVECT, MNVECT )
            ENDIF
            MNTIME = MNVECT + WECTEU + MOREE2 * NTDL * MXVECT
C           COPIE DES NBVECT PREMIERS TEMPS EXISTANTS
            DO I=0,NBVECT-1
               RMCN( MNTIME+I ) = RMCN( MNTIMES+I )
            ENDDO
C           PROTECTION DE LA BONNE VALEUR (+1 APRES)
            NBVECT = NBVECT - 1
            GOTO 60
         ENDIF
C
      ENDIF
C
C     ICI LE VECTEUR"... N'EXISTE PAS
C     IL EST CREE AVEC 1+MXVECT VECTEURS A STOCKER
      MXVECT = 1 + MXVECT
      L      = WECTEU + NTDL * MXVECT * MOREE2 + MXVECT
      CALL LXTNDC( NTLXOB, KNMVEC, 'MOTS', L )
      CALL LXTSOU( NTLXOB, KNMVEC, NTVECT, MNVECT )
C
C     L'ADRESSE-1 DE STOCKAGE DU TEMPS POUR CHAQUE VECTEUR STOCKE
 60   MNTIME = MNVECT + WECTEU + MOREE2 * NTDL * MXVECT - 1
C
C     STOCKAGE DU VECTEUR UG0 DES TEMPERATURES INITIALES DANS MNTEMP
      MNTEMP = MNVECT + WECTEU + MOREE2 * NTDL * NBVECT
      CALL TRTATD( MCN(MNUG0), MCN(MNTEMP), NTDL )
C
C     L'ADRESSE MCN DU VECTEUR TEMPERATURE INITIALE
      MNTHET = MNTEMP
C
C     LE TEMPS DU VECTEUR TEMPERATURE INITIAL STOCKE
      NBVECT = NBVECT + 1
      RMCN( MNTIME+NBVECT ) = TPSINI
      TEMPS = TPSINI
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10060) TEMPS, NTDL, NBVECT, KNMVEC
      ELSE
         WRITE(IMPRIM,20060) TEMPS, NTDL, NBVECT, KNMVEC
      ENDIF
10060 FORMAT(' AU TEMPS INITIAL',G14.6,' STOCKAGE DE',I12,' DL',
     %       ' EN COLONNE',I5,' DU TMS ',A,/ )
20060 FORMAT(' At INITIAL TIME',G14.6,' STORAGE of',I12,' DoF',
     %       ' in COLUMN',I5,' of TMS ',A,/ )
C
C     LES ADRESSES MCN SUIVANTES SONT STOCKEES DANS LE ./incl/cthet.inc
C     COMMON / PBTHNL / TEMPEL, ONDEPI,...,  MNTHET,MNTHDL,MNNODL,MNTIME
C     DECLARATION DU VECTEUR TEMPERATURE LOCALE SUR UN ELEMENT FINI
      CALL NLDATADC( NBDLMX )
C
 9999 RETURN
      END
