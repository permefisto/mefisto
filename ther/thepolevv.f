      SUBROUTINE THEPOLEVV( KNOMOB, NBGAME, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES VALEURS Lambda ET VECTEURS PROPRES DEFINIS PAR
C -----    UN POLYNOME  P(Lambda)=0 avec L'OPERATEUR DE LA CHALEUR POUR
C          DES ELEMENTS FINIS LAGRANGE DE DEGRE 1 OU 2 EN 2D OU 3D OU 6D
C          ATTENTION: LES CONDITIONS AUX LIMITES SE FONT AVEC LE JEU 1
C                     (=> CONTACT=0 IMPOSE    DANS LE JEU 1
C                     LES FLUX SONT CALCULES AVEC  LE JEU 1
C                     (=> CONDUCTIVITE DONNEE DANS LE JEU 1)
C
C          COMPUTATION OF EIGENVALUES Lambda and EIGENVECTORS DEFINED BY
C          A POLYNOMIAL P(Lambda)=0 with the OPERATOR -DIV( A GRAD . )
C          FOR LAGRANGE FINITE ELEMENTS of DEGREE 1 or 2 in 1D or 2D or 3D
C          ATTENTION: THE BOUNDARY CONDITIONS ARE DONE WITH THE INPUT DATA
C                     OF THE FIRST GAME OF THERMAL INPUT DATA
C                     THE FLUXES ARE COMPUTED WITH THE CONDUCTIVITY
C                     OF THE FIRST GAME OF THERMAL INPUT DATA
C ENTREE :
C --------
C KNOMOB : NOM DE L'OBJET DE VALEURS ET VECTEURS PROPRES A CALCULER
C          NAME OF THE OBJECT TO COMPUTE THE EIGENVALUES ET VECTORS
C NBGAME : NOMBRE DE JEUX DE DONNEES = DEGRE+1 DU POLYNOME P(Lambda)
C          >1 ICI POUR AVOIR UN POLYNOME DE DEGRE>=2
C          NUMBER OF INPUT DATA GAMES = DEGREE + 1 OF THE POLYNOMIAL P
C
C SORTIE :
C --------
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C          O WITHOUT ERROR, >0 ELSE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TIMS NTU TAIPEI TAIWAN          DECEMBRE 2009
C23456---------------------------------------------------------------012
      DOUBLE PRECISION   PENALI
      PARAMETER         (PENALI=0D0)
      PARAMETER         (MXTYEL=7)
      PARAMETER         (MOPAGE=512)
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/donthe.inc"
      include"./incl/donele.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a_objet__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___contact.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___profil.inc"
      include"./incl/a___fluxpt.inc"
      include"./incl/a___tableau1r.inc"
      include"./incl/msvaau.inc"
      include"./incl/ponoel.inc"
      include"./incl/cthet.inc"
      include"./incl/ctemps.inc"
      include"./incl/cnonlin.inc"
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
C
      EXTERNAL          ETTAEL
      DOUBLE PRECISION  TARGETR, TARGETI
      INTEGER           RESTRICT
      DOUBLE PRECISION  RELMIN, D2PI
      DOUBLE PRECISION  DINFO,  DCPU, DPREP, DEIGV, DISP, DMAT, DBLE
      INTEGER           NUMIOB(4), NUMAOB(4), MNDOEL(4), MXDOEL(4)
C
      CHARACTER*(*)     KNOMOB
      DATA              RELMIN/-1D28/
C     TABLEAU NON UTILISE (VITESSE D'UN FLUIDE NON ICI CALCULE)
      DOUBLE PRECISION  VITEGt(5,3)
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) NBGAME-1, KNOMOB
      ELSE
         WRITE(IMPRIM,20000) NBGAME-1, KNOMOB
      ENDIF
10000 FORMAT(/1X,80('=')/
     %' POLYNOME(Lambda)=0 de DEGRE',I2,
     %' de VALEURS PROPRES Lambda de l''OBJET: ',A/1X,80('='))
20000 FORMAT(/1X,80('=')/
     %' POLYNOM(Lambda)=0 of DEGREE',I2,
     %' of EIGENVALUES Lambda of the OBJECT: ',A/1X,80('='))
C
C     LE DEGRE DU POLYNOME P(Lambda) DOIT ETRE >=1 => NBGAME>=2
      IF( NBGAME .LE. 1 ) RETURN
C
C     QUELQUES INITIALISATIONS
      TESTNL = 0
      DCPU   = DINFO( 'CPU' )
      DPREP  = 0D0
      DMODR  = 0D0
C     TEMPS or TIME
      TEMPS  = 0.0
      IERR   = 0
C     2 PI
      D2PI   = ATAN( 1D0 ) * 8D0
      NBCOOR = 0
C     LE NOMBRE DE MOTS D'UNE VARIABLE REEL2
C     NUMBER OF WORDS FOR A REAL DOUBLE PRECISION
      MOREE2 = MOTVAR(6)
C
C     PROTECTION DES ADRESSES POUR EVITER DES PROBLEMES LORS
C     DE LA DESTRUCTION DES TABLEAUX
C     PROTECTION OF ADDRESSES TO AVOID PROBLEMS DURING DELETION OF ARRAYS
      NTVVPR = 0
      MNVVPR = 0
      MNEIGV = 0
      MNNPEF = 0
      MNTPOB = 0
      MNTAUX = 0
      MNTAEL = 0
      MNNODL = 0
      MNTHER = 0
      MNX    = 0
      MNLPLI = 0
      MNLPCO = 0
      MNAG   = 0
      MNMG   = 0
      DO 1 I=1,4
         MNDOEL(I) = 0
         MXDOEL(I) = 0
 1    CONTINUE
      NBTYEL = 0
      MOAUX  = 0
      MOTAEL = 0
      NBDLMX = 0
      NTDL   = 0
      MOFLTO = 0
      MOFLPT = 0
      MONDLX = 0
      MNNDLX = 0
      MNVDLX = 0
      NDSM   = 1
C
C     AFFICHAGE ET VERIFICATION DU NOM_DE_L'OBJET
C     DISPLAY and VERIFICATION ABOUT THE OBJECT
C     ===========================================
C     RECHERCHE DU NOM DE L'OBJET DANS LE LEXIQUE DES OBJETS
C     RESEARCH OF THE OBJECT NAME IN THE DIRECTORY OF OBJECTS
      CALL LXLXOU( NTOBJE, KNOMOB, NTLXOB, MNLXOB )
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
C     IF IT DOES NOT EXIST, RETURN
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET INCONNU ' // KNOMOB
         ELSE
            KERR(1) = 'ERROR: UNKNOWN OBJECT ' // KNOMOB
         ENDIF
         CALL LEREUR
         CALL LXIM( NTOBJE )
         IERR = 1
         RETURN
      ENDIF
C
C     RECHERCHE DU TMS TABLEAU DEFINITION
C     SEARCH THE TMS DEFINITION
      CALL LXTSOU( NTLXOB,'DEFINITION', NTDFOB, MNDFOB )
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
      IF( NTDFOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) ='ERREUR: DEFINITION INCONNUE de l''OBJET ' //KNOMOB
         ELSE
            KERR(1) ='ERROR: UNKNOWN DEFINITION for the OBJECT '//KNOMOB
         ENDIF
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C
C     LECTURE DES DONNEES SUR LES VALEURS PROPRES DEMANDEES
C     READING OF DATA ON WHICH EIGENVALUES MUST BE COMPUTED
 2    CALL LIMTCL( 'eigvvpol', NMTCL )
      IF( NMTCL .LT.  0 ) GOTO 9999
      GOTO( 4, 6, 8, 2, 2, 2, 2, 2, 10 ), NMTCL
C
C     EIGENVALUE TARGET COMPLEX => 2 VARIABLES
C                TARGETR (real part) and TARGETI (imginary part)
 4    NCVALS = 0
      CALL INVITE( 147 )
      CALL LIRRDP( NCVALS, TARGETR )
      IF( NCVALS .EQ. -1 ) GOTO 2
      NCVALS = 0
      CALL INVITE( 148 )
      CALL LIRRDP( NCVALS, TARGETI )
      GOTO 2
C
C     NBEIGV NUMBER OF EIGENVALUES
 6    NCVALS = 4
      NB     = 1
      CALL INVITE( 149 )
      CALL LIRENT( NCVALS, NB )
      IF( NCVALS .EQ. -1 ) GOTO 2
      IF( NB .LE. 0 ) THEN
         NBEIGV = 1
      ELSE
         NBEIGV = NB
      ENDIF
      GOTO 2
C
C     RESTRICT = -1 at LEFT, 0 CENTERED, 1 at RIGHT
 8    CALL LIMTCL( 'eigvvloc', NMTCL )
      IF( NMTCL .LT.  0 ) GOTO 2
      RESTRICT = NMTCL-2
      GOTO 2
C
C     RECHERCHE TABLEAUX XYZSOMMET XYZNOEUD XYZPOINT NPEF" de l'OBJET
C     SEARCH THE TMS ASSOCIATED TO THIS OBJECT
 10   CALL MIMAOB( NBGAME, NTLXOB, MXDOTH, NTTOPO, MNTOPO,
     %             NTXYZP, MNXYZP, NTXYZN, MNXYZN,
     %             MXTYEL, NBTYEL, MTNPEF, MNNPEF,
     %             NUMIOB, NUMAOB,
     %             NDPGST, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %             MXDOEL, MNDOEL, IERR )
      IF( IERR .NE. 0 ) GOTO 9950
C     ICI NUMIOB CONTIENT LES 4 NUMEROS MINIMA DES PLSV de l'OBJET
C         NUMAOB          LES 4 NUMEROS MAXIMA DES PLSV de l'OBJET
C     MNDOEL LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C     TABLEAUX DECRIVANT les DONNEES THERMIQUES DE L'OBJET COMPLET
C     HERE NUMIOB CONTAINS THE 4 NUMBERS MINIMA of PLSV of the OBJECT
C          NUMAOB CONTAINS THE 4 NUMBERS MAXIMA of PLSV of the OBJECT
C     MNDOEL the 4 MCN ADRESSES of ARRAYS of ADDRESSES of THERMAL INPUT DATA
C
C     OUVERTURE DES TABLEAUX DES DONNEES THERMIQUES DES PLSV DE L'OBJET
C     OPENING of TMS of THERMAL INPUT DATA OF PLSV of THE OBJECT
C     =================================================================
      CALL THEDON( NUMIOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %             NBGAME, MNDOEL,
     %             IEMAST, IECHMA, IECOND, IEDILA, IEVIFL, IECOET,
     %             IESOIN, IECONT, IEECHA, IESOCL, IESOPO,
     %             IETEIN, IEVIIN, IEVIANT,IECOBO,
     %             IERR )
      IF( IERR .GT. 0 ) THEN
         IERR = 4
         GOTO 9950
      ENDIF
C
C     INITIALISATIONS DE VARIABLES ET AFFICHAGES
C     INITIALIZATIONS of VARIABLES and DISPLAY
C     ==========================================
C     NBCOOR : NOMBRE DE COORDONNEES DES NOEUDS=POINTS (3 ou 6)
C              NUMBER OF COORDINATES of  NODES =POINTS
      NBCOOR = MCN( MNXYZP + WBCOOP )
C     LE NOMBRE DE POINTS DU MAILLAGE DE L'OBJET
C     POINT NUMBER of the OBJECT MESH
      NBPOI = MCN( MNXYZP + WNBPOI )
C     NDIM DIMENSION 1 OU 2 OU 3 OU 6 DE L'ESPACE DES COORDONNEES
C          DIMENSION 1 or 2 or 3 or 6 of the SPACE of POINTS or NODES
      CALL DIMCOO( NBPOI, MCN(MNXYZP+WYZPOI), NDIM )
C     PARTICULARITE DES 6-CUBES (3Q1C NON A TRAITER EN THERMIQUE)
      IF( NBCOOR .EQ. 6 ) THEN
         NBTYEL = 1
         NDIM   = NBCOOR
      ENDIF
C     NOMBRE NOEUDS DU MAILLAGE DE L'OBJET=NOMBRE TOTAL DEGRES DE LIBERTE
C     TOTAL NUMBER of DEGREES OF FREEDOM = NODE NUMBER of THE OBJECT MESH
      NTDL = MCN( MNXYZN + WNBNOE )
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10210) NDIM,NTDL,NBGAME
      ELSE
         WRITE(IMPRIM,20210) NDIM,NTDL,NBGAME
      ENDIF
10210 FORMAT(/' DIMENSION 2 OU 3 OU 6 DE L''ESPACE',T42,'=',I6/
     %' NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR PROPRE',T42,'=',I6/
     %' NOMBRE DE JEUX DE DONNEES THERMIQUES',      T42,'=',I6)
20210 FORMAT(/' SPACE DIMENSION (2 or 3 or 6)',T40,'=',I6/
     %' NUMBER of COMPONENTS of each EIGENVECTOR',  T40,'=',I6/
     %' NUMBER of THERMAL INPUT DATA GAMES',   T40,'=',I6)
C
C     RECUPERATION DES TABLEAUX POBA NECESSAIRES A LA CONSTRUCTION DES
C     TABLEAUX ELEMENTAIRES
C     RETRIEVE THE ARRAYS OF POLYNOMIALS VALUES AT QUADRATURE FORMULA
C     POINTS WHICH ARE NECESSARY TO COMPUTE THE ELEMENT ARRAYS
C     ================================================================
      CALL TAPOBA( NBTYEL, MNNPEF, ETTAEL,
     %             MNTPOB, NBDLMX, MOAUX, NBTTEF, NOAXIS, NCODSM, IERR )
      NCODSK = 1
      IF( IERR .NE. 0 ) GOTO 9950
C
C     ADRESSAGE DES TABLEAUX AUXILIAIRES ET ELEMENTAIRES
C     ADDRESSING of AUXILIARY and ELEMENT TMC ARRAYS
C     ==================================================
      CALL TNMCDC( 'REEL2', MOAUX, MNTAUX )
C
C     LES 2 MATRICES ELEMENTAIRES ET LES NDSM VECTEURS ELEMENTAIRES
C     The 2 ELEMENT MATRICES and VECTOR
      MOTAEL = NBDLMX * (NBDLMX+1) + NBDLMX * NDSM
      CALL TNMCDC( 'REEL2', MOTAEL, MNTAEL )
C
C     LE NUMERO DES DEGRES DE LIBERTE GLOBAUX DES DL D'UN EF
C     DOF GLOBAL NUMBER of the ELEMENT DDEGREES of FREEDOM
      CALL TNMCDC( 'ENTIER', NBDLMX, MNNODL )
C
C     LE TENSEUR DE CONDUCTIVITE ou COEFFICIENT TEMPERATURE(Pts INTEGRATION)
C     CONDUCTIVITY TENSOR at QUADRATURE FORMULA POINTS
      CALL TNMCDC( 'REEL2', 128, MNTHER )
C
C     LE TABLEAU DES NBCOOR COORDONNEES DES NBDLMX NOEUDS D'UN ELEMENT FINI
C     NBCOOR COORDINATES of  NBDLMX NODES of AN FINITE ELEMENT
      CALL TNMCDC( 'REEL', NBDLMX*NBCOOR, MNX )
C
C     DECLARATION DES NBGAME MATRICES GLOBALES MORSES CONDENSEES
C     DECLARATION of the NBGAME GLOBAL CONDENSED MATRICES
C     ==========================================================
C     CALCUL DU SQUELETTE DE LA MATRICE (LA MATRICE EST ICI SYMETRIQUE)
C     CONSTRUCTION of POINTERS on THE DIAGONAL COEFFICIENT of EVERY LINE
      CALL PRGCMC( MNTOPO, MCN(MNNPEF), MNXYZN, 1, NCODSK,
     %             MNLPLI, MNLPCO, IERR )
      IF( IERR .NE. 0 ) GOTO 9950
C     DECLARATION INITIALISATION des NBGAME MATRICES MORSES SYMETRIQUES
      NBRDAG = MCN(MNLPLI+NTDL)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10020) NBGAME, NTDL+NBRDAG*(1+MOREE2*NBGAME)
      ELSE
         WRITE(IMPRIM,20020) NBGAME, NTDL+NBRDAG*(1+MOREE2*NBGAME)
      ENDIF
10020 FORMAT(' STOCKAGE DES',I2,' MATRICES MORSES dans',I11,
     %' MOTS MEMOIRE')
20020 FORMAT(I2,' MORSE MATRICES STORAGE in',I11,' MEMORY WORDS' )
C
C     TEST DE LA PLACE MEMOIRE DISPONIBLE
C     TEST ON THE SIZE OF FREE AVAILABLE MEMORY
      CALL TNMCMX( 'REEL2', MAXVAR )
      DISP = DBLE( MAXVAR )
C
C     ATTENTION: SI NBRDAG tel que NBGAME*NBRDAG>2**31 => NBGAME*NBRDAG<0!...
C                d'ou le test ci-dessous en double precision ...
      DMAT = NBGAME * DBLE( NBRDAG )
      IF( DISP .LT. DMAT ) THEN
C         PLACE MEMOIRE INSUFFISANTE
          NBLGRC(NRERR) = 4
          WRITE(KERR(MXLGER  )(1:25),'(G25.0)') DISP
          WRITE(KERR(MXLGER-1)(1:25),'(G25.0)') DMAT
          WRITE(KERR(MXLGER-2)(1:2),'(I2)') NBGAME
          IF( LANGAG .EQ. 0 ) THEN
             KERR(1) ='ERREUR: PLACE MEMOIRE INSUFFISANTE pour '
     %                // KERR(MXLGER-2)(1:2) // ' MATRICES MORSE'
             KERR(2) = KERR(MXLGER  )(1:25)//' MOTS MEMOIRE DISPONIBLES'
             KERR(3) = KERR(MXLGER-1)(1:25) // ' MOTS NECESSAIRES pour '
     %              // KERR(MXLGER-2)(1:2)  // ' MATRICES MORSE'
             KERR(4) = 'REDUIRE le MAILLAGE'
          ELSE
             KERR(1) ='ERROR: NOT ENOUGH MEMORY or SWAP for '
     %               // KERR(MXLGER-2)(1:2) // ' CONDENSED MATRICES'
             KERR(2) = KERR(MXLGER  )(1:25) // ' AVAILABLE MEMORY WORDS'
             KERR(3) = KERR(MXLGER-1)(1:25) //
     %               ' NECESSARY WORDS to store '
     %               // KERR(MXLGER-2)(1:2) // ' CONDENSED MATRICES'
             KERR(4) ='REDUCE the MESH'
          ENDIF
          CALL LEREUR
          IF( INTERA .LE. 1 ) CALL ARRET( 100 )
          GOTO 9950
      ENDIF
C
C     ADRESSAGE du TMC des NBGAME AG SYMETRIQUE NON DIAGONALE SYMETRIQUE
C     ADDRESSING of TMC of NBGAME SYMMETRIC CONDENSED MATRICES
      MNAG   = 0
      NVGAAG = NBGAME * NBRDAG
      CALL TNMCDC( 'REEL2', NVGAAG, MNAG )
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'ADRESSE ',MNAG, ' des ',NVGAAG,' DOUBLE REELS'
      ELSE
         WRITE(IMPRIM,*) 'ADRESS ' ,MNAG, ' of ' ,NVGAAG,' DOUBLE REALS'
      ENDIF
      IF( MNAG .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*) 'ERREUR: MANQUE DE MEMOIRE CENTRALE'
         ELSE
            WRITE(IMPRIM,*) 'ERROR: NOT ENOUGH MAIN MEMORY'
         ENDIF
         IERR = 11
         GOTO 9950
      ENDIF
C
C     ICI PAS DE MG.  HERE NO MASS MATRIX
      NCODSM = 0
      MNMG   = 0
      NBRDMG = 0
C
C     ICI PAS DE SECOND MEMBRE GLOBAL
C     HERE NO SECOND MEMBER VECTOR
      MNBG = 0
C
C     CALCUL DES NBGAME MATRICES DE CONDUCTIVITE SUPPOSEES
C     INDEPENDANTES DU TEMPS
C     COMPUTATION OF the NBGAME CONDUCTIVITY MATRICES
C     SUPPOSED INDEPENDENT OF TIME
C     ----------------------------------------------------
      IEMG = 0
      IEAG = 1
      IEBG = 0
      CALL THEMKB( NBGAME, IEMG,   IEAG,   IEBG,   PENALI,
     &             D2PI,   NDIM,   NTDL,   VITEGt,
     &             NBTYEL, MNNPEF, NDPGST,
     &             MNTPOB, MXPOBA, MNTAUX,
     &             MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     &             MNTHER, MNTAEL, MNX,    MNNODL,
     &             2,      MNLPLI, MNLPCO,
     &             0,      MNMG,   NBRDAG, MNAG,   MNBG,
     &             NCODSM, NCODSK, NBPTAF, IERR )
      IF( IERR .NE. 0 ) GOTO 9950
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) NBGAME,
     %      ' MATRICES CONDENSEES ont ete CALCULEES'
      ELSE
         WRITE(IMPRIM,*) NBGAME,
     %      ' CONDENSED MATRICES have been COMPUTED'
      ENDIF
C
C     POUR LE JEU 1 DES DONNEES THERMIQUES
C     CONSTRUCTION DES TABLEAUX DU NUMERO ET VALEUR DES DL FIXES
C     A PARTIR DU PREMIER JEU DE DONNEES (COEFFICIENT 0 du POLYNOME)
C     FROM THE FIRST GAME OF THERMAL INPUT DATA CONSTRUCTION OF THE
C     LIST OF THE DEGREE OF FREEDOM IMPOSED TO ZERO
C     --------------------------------------------------------------
      CALL THDLFX( 0,      NTDL,   NDIM,
     &             NBTYEL, MNNPEF, NDPGST,
     &             MNXYZN, NUMIOB, MNDOEL, RELMIN,
     &             NBDLFX, MONDLX, MNNDLX, MNVDLX, IERR )
      IF( IERR .NE. 0 ) GOTO 9950
C
      IF( NBDLFX .GT. 0 ) THEN
C
C        RENUMEROTATION DES DEGRES DE LIBERTE LIBRES NODLIB=MCN(MNDLIB)
C        RENUMBERING of DOF REALLY FREE
C        NODLIB(I) = NUMERO DU DEGRE DE LIBERTE S'IL EST LIBRE
C                   -INDICE DANS LA LISTE DES DL BLOQUES S'IL EST BLOQUE
C        NBDLIB    = NOMBRE TOTAL DE DEGRES DE LIBERTE LIBRES
C                    TOTAL NUMBER OF FREE DOF
         CALL REDLIB( NBDLFX, MCN(MNNDLX), NTDL,  NBDLIB, MNDLIB )
C
C        PRISE EN COMPTE DES TEMPERATURES IMPOSEES (DIRICHLET NON PENALISEE)
C        TAKE IN ACCOUNT THE IMPOSED TEMPERATURES  (DIRICHLET NOT PENALISED)
C        SUPPRESSION LIGNE ET COLONNE DES DEGRES DE LIBERTE IMPOSE A ZERO
C        SUPPRESSION OF LINE AND COLUMN OF DOF IMPOSED TO ZERO IN THE MATRICES
C        ---------------------------------------------------------------------
         CALL BLDL0GC( NTDL, MCN(MNLPLI+NTDL), NBGAME,
     %                 MCN(MNLPLI), MCN(MNLPCO), MCN(MNAG), MCN(MNDLIB))
C
C        TRANSLATION DES NBGAME-1 DERNIERES MATRICES
         NBRDAGL = MCN( MNLPLI + NBDLIB )
         MN0 = MNAG
         MN1 = MNAG
         DO 82 JEU=2,NBGAME
            MN0 = MN0 + NBRDAG  * MOREE2
            MN1 = MN1 + NBRDAGL * MOREE2
            CALL TRTATA( MCN(MN0), MCN(MN1), NBRDAGL*MOREE2 )
 82      CONTINUE
C
C        REDUCTION DU STOCKAGE DES MATRICES
C        REDUCTION of THE MATRICES STORAGE
         NVGAAGL = NBGAME * NBRDAGL
         CALL TNMCRA( 'REEL2', NVGAAG, NVGAAGL, MNAG )
         NVGAAG = NVGAAGL
C
      ELSE
C        PAS DE DL FIXE
         NBDLIB = NTDL
      ENDIF
C
C     NOMBRE DE COEFFICIENTS D'UNE MATRICE MORSE APRES SUPPRESSION DES DL FIXES
C     COEEFICIENT NUMBER of ONE CONDENSED MATRICE AFTER FIXED DOF DELETION
      NBRDAGL = MCN(MNLPLI+NBDLIB)
C
C     AFFICHAGE DES NBGAME DE MATRICES MORSE ou CONDENSEES APRES SUPPRESSION
C     DISPLAY OF THE NBGAME CONDENSED MATRICES AFTER LINES COLUMNS SUPPRESSION
      MN1 = MNAG
      DO 85 JEU=1,NBGAME
         CALL AFMORSE( 50, NBDLIB, MCN(MNLPLI), MCN(MNLPCO), MCN(MN1) )
         MN1 = MN1 + NBRDAGL*MOREE2
 85   CONTINUE
C
C     TEMPS CALCUL pour CONSTRUIRE les NBGAME MATRICES GLOBALES MORSES
C     CPU TIME TO CONSTRUCT THE NBGAME GLOBAL MATRICES CONDENSED
      DPREP = DINFO( 'DELTA CPU' )
C
C     CALL SOLVER POLYNOMIAL OF EIGENVALUES
C     CONSTRUCTION OF THE TMS 'VECTEUR"VALEURPROPRE' of
C     EIGENVALUES and EIGENVECTORS
C     DECLARATION DES VALEURS et VECTEURS PROPRES CALCULEES
      MXEIGV = 3 * NBEIGV
C     RESERVATION DES VECTEURS POUR L'APRES DECOMPRESSION DES DL
C     RESERVATION FOR THE AFTER DECOMPRESSION OF THE DOF
      MOTEVV = MXEIGV * 2 * ( 1 + NTDL ) * MOREE2
      CALL TNMCDC( 'REEL2', MOTEVV, MNEIGV )
      IF( MNEIGV .LE. 0 ) THEN
C        TRAITEMENT DES ERREURS
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'PAS ASSEZ DE MEMOIRE LIBRE'
         ELSE
            KERR(1) = 'NOT ENOUGH MEMORY'
         ENDIF
         CALL LEREUR
         GOTO 9950
      ENDIF
C     ADRESSES DES TABLEAUX DES VALEURS ET VECTEURS PROPRES
C     ADDRESSES OF EIGENVALUES AND VECTORS
      MNEVAR = MNEIGV
      MNEVAI = MNEVAR + MXEIGV * MOREE2
      MNEVER = MNEVAI + MXEIGV * MOREE2
      MNEVEI = MNEVER + MXEIGV * NTDL * MOREE2
      CALL POLEVV( NBGAME, NBDLIB, NBRDAGL, MCN(MNLPLI), MCN(MNLPCO),
     %             MCN(MNAG), NBEIGV, TARGETR, TARGETI, RESTRICT,
     %             MXEIGV, NBEIGVC, MCN(MNEVAR), MCN(MNEVAI),
     %             MCN(MNEVER), MCN(MNEVEI), IERR )
C     TARGET = COMPLEX EIGENVALUE TARGET
C     NBEIGV = WISHED NUMBER OF COMPUTED EIGENVALUES
C     RESTRICT = -1 at LEFT, 0 CENTERED, 1 at RIGHT of THE TARGET
C     NBEIGVC = NUMBER OF REALLY COMPUTED EIGENVALUES
C     EIGVALR = REAL      PART OF THE EIGENVALUES
C     EIGVALI = IMAGINARY PART OF THE EIGENVALUES
C     EIGVECR = REAL      PART OF THE EIGENVECTOR COEFFICIENTS
C     EIGVECI = IMAGINARY PART OF THE EIGENVECTOR COEFFICIENTS
C     SUBROUTINE POLEVV( NBGAME, NBROWS, NBCOEFA, PDIAGCA, COLUMN, A,
C     %                  NBEIGV, TARGETR, TARGETI, RESTRICT,
C     %                  PARMETERPJD,
C     %                  MXEIGV, NBEIGVC, EIGVALR, EIGVALI,
C     %                  EIGVECR, EIGVECI, IERR )
C     INTEGER NBGAME, NBROWS, NBCOEFA, PDIAGCA(0:NBROWS),COLUMN(NBCOEFA)
C                            (NBCOEFA = PDIAGCA(NBROWS))
C     DOUBLE  PRECISION A(1:NBCOEFA,1:NBGAME)
C     INTEGER NBEIGV,  RESTRICT
C     DOUBLE PRECISION TARGETR, TARGETI
C     DOUBLE PRECISION EIGVALR(MXEIGV), EIGVALI(MXEIGV)
C     DOUBLE PRECISION EIGVECR(NBROWS,MXEIGV), EIGVECI(NBROWS,MXEIGV)
C
C     DESTRUCTION DES MATRICES POUR REDONNER DE LA PLACE EN MC
C     DELETION OF MATRICES TO RECUPERATE THE MEMORY FOR MCN ARRAYS
C     ============================================================
      IF( MNAG .GT. 0 ) CALL TNMCDS( 'REEL2', NVGAAG, MNAG )
      IF( IERR .GT. 0 ) GOTO 9950
C
C     RETOUR A LA NUMEROTATION DE 1 A NTDL DES COMPOSANTES
C     DES NBEIGVC VECTEURS PROPRES GRACE AU TABLEAU NUDLIB(1:NTDL)
C     RETURN TO THE NUMBERING OF INITIAL DOF FROM 1 TO NTDL
C     ===========================================================
      IF( NBDLFX .GT. 0 ) THEN
         CALL RENTDL( NBDLIB, NTDL, MCN(MNDLIB), NBEIGVC,
     %                MCN(MNEVER),  MCN(MNEVER) )
         CALL RENTDL( NBDLIB, NTDL, MCN(MNDLIB), NBEIGVC,
     %                MCN(MNEVEI),  MCN(MNEVEI) )
      ENDIF
C
C     CONSTRUCTION DU TMS 'VECTEUR"VALEURPROPRE' PARTIE REELLE
C     ========================================================
      CALL LXTSOU( NTLXOB, 'VECTEUR"VALEURPROPRE', NTVVPR, MNVVPR )
      IF( NTVVPR .GT. 0 ) THEN
C        LE VECTEUR EST DETRUIT POUR ETRE REDECLARE
         CALL LXTSDS( NTLXOB, 'VECTEUR"VALEURPROPRE' )
      ENDIF
      L = WECTEU + NBEIGVC * NTDL * MOREE2 + NBEIGVC
      CALL LXTNDC( NTLXOB, 'VECTEUR"VALEURPROPRE', 'MOTS', L )
      CALL LXTSOU( NTLXOB, 'VECTEUR"VALEURPROPRE', NTVVPR, MNVVPR )
C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR PROPRE
      MCN( MNVVPR + WBCOVE ) = NTDL
C     NOMBRE DE VECTEURS
      MCN( MNVVPR + WBVECT ) = NBEIGVC
C     NOMBRE DE VALEURS PROPRES REELLE SIMPLE PRECISION
      MCN( MNVVPR + WBCPIN ) = NBEIGVC
C     COPIE DES VECTEURS PROPRES
      MNVECP = MNVVPR + WECTEU
      CALL TRTATA( MCN(MNEVER), MCN(MNVECP), NBEIGVC * NTDL * MOREE2 )
C     COPIE DES VALEURS PROPRES EN SIMPLE PRECISION
      MNVALP =  MNVECP + NBEIGVC * NTDL * MOREE2 - 1
      MN1    = (MNEVAR-1) / 2
      DO 950 I=1,NBEIGVC
         RMCN( MNVALP + I ) = REAL( DMCN( MN1 + I ) )
 950  CONTINUE
C
C     COUT CALCUL DES VALEURS ET VECTEURS PROPRES
C     COST of THE EIGENVALUES AND VECTORS COMPUTATION
C     ===============================================
      DEIGV = DINFO( 'DELTA CPU' )
C
C **************************************************************************
C --------------------------------------------------------------------------
C **************************************************************************
C
C     ATTENTION: SELEMENT POUR LE JEU 1 DES DONNEES
C     CALCUL DES FLUX EN CHAQUE POINT D INTEGRATION DES FACES DE
C     CHAQUE ELEMENT FINI DE CHAQUE TYPE D'EF DE L'OBJET
C     LA CONDUCTIVITE DES MATERIAUX EST SUPPOSEE DANS LE JEU 1 DES DONNEES
C     THE FLUXES ARE COMPUTED WITH THE CONDUCTIVITY OF THE FIRST GAME
C     OF THE NBGAME THERMAL INPUT DATA
C     ====================================================================
      IF( NBCOOR .LE. 3 ) THEN
         CALL THEFLU( KNOMOB, NTLXOB, MNTOPO, NOAXIS, D2PI,
     %                NDIM,   MOREE2, NBEIGVC, NTDL,
     %                NBTYEL, MNNPEF, NDPGST, MNTPOB,
     %                MNTAUX, MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                MOTAEL, MNTAEL, MNX,    MNVVPR )
      ENDIF
C
C     DESTRUCTION DES TABLEAUX TEMPORAIRES
C     DESTRUCTION OF USELESS TEMPORARY ARRAYS
C     =======================================
 9950 IF( MNNPEF .GT. 0 ) CALL TNMCDS( 'ENTIER',2*MXTYEL, MNNPEF )
      DO 11000 I=1,4
         IF( MNDOEL(I) .GT. 0 .AND. MXDOEL(I) .GT. 0 ) THEN
            CALL TNMCDS( 'ENTIER', MXDOEL(I), MNDOEL(I) )
         ENDIF
11000 CONTINUE
      IF( MNTPOB .GT. 0 ) CALL TNMCDS( 'ENTIER', NBTYEL*MXPOBA,MNTPOB )
      IF( MNTAUX .GT. 0 ) CALL TNMCDS( 'REEL2' , MOAUX , MNTAUX )
      IF( MNTAEL .GT. 0 ) CALL TNMCDS( 'REEL2' , MOTAEL, MNTAEL )
      IF( MNNODL .GT. 0 ) CALL TNMCDS( 'ENTIER', NBDLMX, MNNODL )
      IF( MNTHER .GT. 0 ) CALL TNMCDS( 'REEL2' , 128   , MNTHER )
      IF( MNX    .GT. 0 ) CALL TNMCDS( 'REEL',   NBDLMX*NBCOOR, MNX )
      IF( MNNDLX .GT. 0 ) CALL TNMCDS( 'ENTIER', MONDLX, MNNDLX )
      IF( MNVDLX .GT. 0 ) CALL TNMCDS( 'REEL2',  MONDLX, MNVDLX )
      IF( MNLPLI .GT. 0 ) CALL TNMCDS( 'ENTIER', NTDL+1, MNLPLI )
      IF( MNLPCO .GT. 0 ) CALL TNMCDS( 'ENTIER', NBRDAG, MNLPCO )
      IF( MNEIGV .GT. 0 ) CALL TNMCDS( 'REEL2',  MOTEVV, MNEIGV )
      IF( IERR .NE. 0 ) RETURN
C
C     COUT CALCUL DES FLUX
      DCPU = DINFO( 'DELTA CPU' )
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,12001) DPREP,DEIGV,DCPU,DPREP+DEIGV+DCPU
      ELSE
         WRITE(IMPRIM,22001) DPREP,DEIGV,DCPU,DPREP+DEIGV+DCPU
      ENDIF
12001 FORMAT(/
     %' TEMPS CALCUL DE LA PREPARATION   =',F12.2,' SECONDES CPU'/
     %' TEMPS CALCUL DES VALEURS PROPRES =',F12.2,' SECONDES CPU'/
     %' TEMPS CALCUL DES FLUX            =',F12.2,' SECONDES CPU'/
     %' TEMPS CALCUL TOTAL               =',F12.2,' SECONDES CPU')
22001 FORMAT(/
     %' PREPARATION TIME =',F12.2,' CPU SECONDS'/
     %' EIGENVALUES TIME =',F12.2,' CPU SECONDS'/
     %' NORMAL FLUX TIME =',F12.2,' CPU SECONDS'/
     %' TOTAL       TIME =',F12.2,' CPU SECONDS')
C
 9999 RETURN
      END
