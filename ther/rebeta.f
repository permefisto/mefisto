      SUBROUTINE REBETA( XYZD, NOOBSF, NUMISU, NUMASU, LTDESU, BETA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER BETA COEFFICIENT DU TERME NON LINEAIRE U3 DANS UN
C -----    PROBLEME NLSE et SUPPOSE DONNE COMME LE COEFFICIENT DEVANT
C          LA TEMPERATURE
C
C ENTREES:
C --------
C XYZD   : 3 COORDONNEES REELLES DOUBLE PRECISION DU POINT OU CALCULER BETA
C NOOBSF : NUMERO DE L'OBJET SURFACE DE CET ELEMENT FINI
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES THERMIQUES
C          DES OBJETS SURFACES
C SORTIE :
C --------
C BETA   : COEFFICIENT  DU TERME NON LINEAIRE U3 DANS UN PROBLEME NLSE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Texas A & M University at QATAR  Fevrier 2011
C23456---------------------------------------------------------------012
      include"./incl/donthe.inc"
      INTEGER           LTDESU(1:MXDOTH, NUMISU:NUMASU)
      DOUBLE PRECISION  XYZD(3), BETA
C
C     RECHERCHE DU COEFFICIENT DE LA TEMPERATURE AU POINT XYZD
      CALL RECOET( 3, NOOBSF, 3, XYZD,
     %             LTDESU(LPCOET,NOOBSF), BETA )
C
      RETURN
      END
