      SUBROUTINE MXGRAD( NTLXOB, NBTYEL, MNNPEF,  HEXSEC, GRADMX, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE VECTEUR GRADIENT DE TEMPERATURE MAXIMAL ET
C -----    L'HEXAEDRE ENGLOBANT TOUS LES POINTS ET POUR TOUS LES CAS
C          CALCULES ET STOCKES DANS LES TMS "DTEMPERATURE"//...
C
C ENTREES :
C ---------
C NTLXOB : NUMERO DU TMS LEXIQUE DE L'OBJET A TRAITER
C NBTYEL : LE NOMBRE DE TYPES D'ELEMENTS DANS CETTE TOPOLOGIE
C MNNPEF : ADRESSE MCN DU TABLEAU(NBTYEL) DES ADRESSES MCN DES TMS 'NPEF'
C
C RESULTATS :
C -----------
C HEXSEC : MIN ET MAX DES COORDONNEES DES POINTS OU SONT CALCULEES
C          LES GRADIENTS
C GRADMX : VALEUR ABSOLUE DU GRADIENT MAXIMAL
C IERR   : 0 SI PAS D'ERREUR >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1994
C23456---------------------------------------------------------------012
      include"./incl/gsmenu.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___dtemperature.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              GRADMX
      CHARACTER*160     KNOM
      CHARACTER*4       NOMELE(2)
      REAL              HEXSEC(6,2)
C
C     INITIALISATIONS
      RMAX = RINFO( 'GRAND' )
      DO 10 I=1,3
C        LE MINIMUM
         HEXSEC(I,1) =  RMAX
C        LE MAXIMUM
         HEXSEC(I,2) = -RMAX
 10   CONTINUE
C
C     BOUCLE SUR LES DIFFERENTS TYPES D'ELEMENTS FINIS DU MAILLAGE
      GRADMX = 0
      DO 20 I = 0, NBTYEL-1
C        L'ADRESSE MCN DU DEBUT DU TABLEAU NPEF
         MNELE = MCN( MNNPEF + I )
C        LE NOM DU TABLEAU GRADIENT ASSOCIE
         NUTYEL = MCN( MNELE + WUTYEL )
C        LES CARACTERISTIQUES DE L'ELEMENT FINI
         CALL ELNUNM( NUTYEL, NOMELE )
         KNOM = 'DTEMPERATURE"' // NOMELE(2)
C        OUVERTURE DU TABLEAU
         CALL LXTSOU( NTLXOB, KNOM, NTGRAD, MNGRAD )
         IF( NTGRAD .LE. 0 ) THEN
            NBLGRC(NRERR) = 1
            KERR(1) = 'OBJET SANS GRADIENT DE TEMPERATURE'
            CALL LEREUR
            IERR = 1
            GOTO 20
         ENDIF
C
C        RECHERCHE DU MIN MAX DES COORDONNEES ET DES GRADIENTS
         NBELFD = MCN(MNGRAD+WBELFD)
         NDIMED = MCN(MNGRAD+WDIMED)
         NBJECD = MCN(MNGRAD+WBJECD)
         NBPEFD = MCN(MNGRAD+WBPEFD)
         LDCOPG = WTEMPE + MOTVAR(6) * NBELFD * NDIMED * NBPEFD * NBJECD
         CALL MXGRA1( NBELFD, NBPEFD, NDIMED, NBJECD ,
     %                MCN(MNGRAD+LDCOPG), MCN(MNGRAD+WTEMPE),
     %                HEXSEC, GRADMX )
 20   CONTINUE
C
C     RECHERCHE DE LA DIMENSION MAXIMALE ECAMAX DE L'OBJET
      ECAMAX = 0.
      DO 50  I=1,NDIMLI
         ECAMAX = MAX( ECAMAX, HEXSEC(I,2) - HEXSEC(I,1) )
 50   CONTINUE
      IF( ECAMAX .LE. 0. ) ECAMAX = 1.
C     UNE MARGE DE 3%
      ECAMAX = ECAMAX * 0.53
C
      DO 60 I=1,NDIMLI
         RMAX = ( HEXSEC(I,1) + HEXSEC(I,2) ) * 0.5
         HEXSEC(I,1) = RMAX - ECAMAX
         HEXSEC(I,2) = RMAX + ECAMAX
 60   CONTINUE
C
      DO 70 I=NDIMLI+1,6
         HEXSEC(I,1) = 0
         HEXSEC(I,2) = 0
 70   CONTINUE
C
      RETURN
      END
