      SUBROUTINE NLDATA1( NBPOLY, POLY )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  POUR UN PROBLEME NON LINEAIRE, RECUPERER AU TEMPS ACTUEL
C -----  TEMPEL LA TEMPERATURE AU TEMPS tn+1 EN   UN POINT D'INTEGRATION DE L'EF
C        TEMPEL LA PARTIE REELLE DE L'ONDE A tn+1 AU POINT D'INTEGRATION DE L'EF
C
C        SI TESTNL>=6: RECUPERER AUSSI
C        ONDEPI  LA PARTIE IMAGINAIRE AU POINT D'INTEGRATION AU TEMPS tn+1
C        TEMPELn LA PARTIE REELLE     AU POINT D'INTEGRATION AU TEMPS tn
C        ONDEPIn LA PARTIE IMAGINAIRE AU POINT D'INTEGRATION AU TEMPS tn
C        TEMPEL0 LA PARTIE REELLE     AU POINT D'INTEGRATION AU TEMPS t0
C        ONDEPI0 LA PARTIE IMAGINAIRE AU POINT D'INTEGRATION AU TEMPS t0
C
C ATTENTION: TOUT PASSE PAR LES VALEURS CONTENUES DANS
C            ./incl/cthet.inc et ./incl/cnonlin.inc
C
C ENTREES:
C --------
C NBPOLY : NOMBRE DE POLYNOMES DE BASE DE L'INTERPOLATION SUR L'EF
C POLY   : VALEURS DES NBPOLY POLYNOMES DE BASE AU POINT D'INTEGRATION
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & Saint Pierre du Perray  Aout 2011
C23456---------------------------------------------------------------012
      include"./incl/cthet.inc"
      include"./incl/cnonlin.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1), RMCN(1), DMCN(1))
      DOUBLE PRECISION  POLY(1:NBPOLY), PROSCD
C
C     PB NON LINEAIRE:
C     CALCUL DE LA TEMPERATURE ACTUELLE AU POINT D'INTEGRATION L ou
C     CALCUL DE LA PARTIE REELLE ACTUELLE AU POINT D'INTEGRATION L de L'ONDE
      TEMPEL = PROSCD( POLY, MCN(MNTHDL), NBPOLY )
      ONDEPI = 0D0
C
      IF( TESTNL .GE. 6 ) THEN
C
C        ONDE COMPLEXE NLSE :
C        NOMBRE DE MOTS D'UN REEL DOUBLE PRECISION
         MOREE2 = MOTVAR(6)

C        CALCUL PARTIE IMAGINAIRE A tn+1 AU POINT D'INTEGRATION L de L'ONDE
         ONDEPI = PROSCD( POLY, MCN(MNTHDL+MOREE2*NBPOLY), NBPOLY )
C
C        CALCUL PARTIE REELLE     A tn   AU POINT D'INTEGRATION L de L'ONDE
         TEMPELn = PROSCD( POLY, MCN(MNTHDLn), NBPOLY )
C
C        CALCUL PARTIE IMAGINAIRE A tn   AU POINT D'INTEGRATION L de L'ONDE
         ONDEPIn = PROSCD( POLY, MCN(MNTHDLn+MOREE2*NBPOLY), NBPOLY )
C
C        CALCUL PARTIE REELLE     A t0   AU POINT D'INTEGRATION L de L'ONDE
         TEMPEL0 = PROSCD( POLY, MCN(MNTHDL0), NBPOLY )
C
C        CALCUL PARTIE IMAGINAIRE A t0   AU POINT D'INTEGRATION L de L'ONDE
         ONDEPI0 = PROSCD( POLY, MCN(MNTHDL0+MOREE2*NBPOLY), NBPOLY )
C
      ENDIF
C
      RETURN
      END
