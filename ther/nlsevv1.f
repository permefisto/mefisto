      SUBROUTINE NLSEVV1(NDIM,   NBNOMA, MNTOPO, NBTYEL, MNNPEF, NDPGST,
     %                   MNTPOB, MNTAUX, MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                   MNTHER, MNTAEL, MNX,    MNVENL,
     %                   NCODSM, NBRDMG, MNMUMG, MG,
     %                   NCODSK, NBRDKG, MNMUKG, KG,
     %                   NBRDLX, MONDLX, MNNDLX, MNVDLX, NBDLIB, MNDLIB,
     %                   NBROOT, MXROOT, RAVANT, DIAGKG, DIAGMG, V, W,
     %                   NBVAPR, VECPR,  MGVECPR,VALPR,  NINV,
     %                   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DES NBROOT PLUS PETITES VALEURS ET VECTEURS PROPRES DU
C -----    PROBLEME   Alfa LAPLACIEN u + Beta u**2 u = E u   c-a-d
C          [KG(Alfa,Beta u**2)] u = E [MG(Masse)] u
C          METHODE DES ITERATIONS INVERSES AVEC TRANSLATION MODIFIEE
C          POUR RESOUDRE LE PROBLEME NON LINEAIRE EN VALEURS PROPRES
C
C ENTREES:
C --------
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 1 ou 2 ou 3 ou 6
C          (SI AXISYMETRIE NDIM=2 X => R>=0 et Y=>Z et Z=0)
C NBNOMA : NOMBRE DE NOEUDS DU MAILLAGE DE L'OBJET ou
C          NOMBRE TOTAL DE DEGRES DE LIBERTE DU MAILLAGE DE L'OBJET ou
C          NOMBRE DE LIGNES ET COLONNES DES MATRICES DE KG ET MG
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE DE L'OBJET
C NBTYEL : NOMBRE DE TYPES D'EF DE L'OBJET
C MNNPEF : ADRESSE MCN DU TMC DES ADRESSES MCN DES TABLEAUX NPEF"TYPE EF
C NDPGST : CODE DE STOCKAGE DES SOMMETS POINTS NOEUDS
C MNTPOB : ADRESSE MCN DES TABLEAUX POLYNOMES DE BASE DES TYPES D'EF
C MXPLBA : NOMBRE MAXIMAL DE TABLEAUX POBA PAR TYPE D'EF
C MNTAUX : ADRESSE MCN DES TABLEAUX AUXILAIRES
C MNXYZP : ADRESSE MCN DE TMS XYZSOMMET DE L'OBJET
C NUMIOB : NUMERO MINIMAL DES OBJETS
C NUMAOB : NUMERO MAXIMAL DES OBJETS
C MNDOEL : ADRESSE MCN DES TMS DES DONNEES THERMIQUES DE L'OBJET
C MNTHER : ADRESSE MCN DU TABLEAU DES DONNEES THERMIQUES INTERNES
C          AU MOINS NPIMAX(=128) REELS DOUBLE PRECISION
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DES NBCOOR COORDONNEES DES POINTS DE L'EF COURANT
C MNVENL : ADRESSE MCN DU VECTEUR SERVANT A DEFINIR LA NON LINEARITE
C
C NCODSM : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CAPACITE
C          0 SI MATRICE DIAGONALE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDMG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE MG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NBNOMA SI MATRICE DIAGONALE
C MNMUMG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C MG     : MATRICE PROFIL GLOBALE DE CAPACITE CALCULEE
C
C NCODSK : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CONDUCTIVITE
C          1 SI MATRICE SYMETRIQUE
C         -1 SI MATRICE NON SYMETRIQUE
C NBRDKG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE GLOBALE KG
C          = NBRDKG SI MATRICE NON DIAGONALE, =NBNOMA SI MATRICE DIAGONALE
C MNMUKG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C KG     : KG(-Alfa) MATRICE PROFIL GLOBALE DE CONDUCTIVITE CALCULEE
C
C NBDLIB : NOMBRE DE DEGRES DE LIBERTE LIBRES
C NBRDLX : NOMBRE DE DL SUPPORT D'UN DEGRE DE LIBERTE FIXE
C MONDLX : NOMBRE DE MOTS DECLARES DU TABLEAU MC NO DES DL FIXES
C MNNDLX : ADRESSE MCN DU TABLEAU MC DES NUMEROS DES DL FIXES, 0 SINON
C MNVDLX : ADRESSE MCN DU TABLEAU MC DES VALEURS DES DL FIXES, 0 SINON
C MNDLIB : MCN(MNDLIB-1+I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NBNOMA) LIBRE
C                          -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          0  SI PAS DE DL BLOQUES
C
C NBROOT : NOMBRE DES PLUS PETITES VALEURS ET VECTEURS PROPRES A CALCULER
C MXROOT : NOMBRE MAJORANT DES VALEURS PROPRES >NBROOT AFIN DE PRENDRE EN
C          CONSIDERATION LES VALEURS MULTIPLES OU LES CALCULS DES VALEURS
C          SUPERIEURES A CELLES DEMANDEES
C RAVANT : RAVANT(I,1)=VALEUR MINIMALE AYANT I VALEURS PROPRES AVANT ELLE
C                     +1D111 indique une valeur non initialisee
C          RAVANT(I,2)=VALEUR MAXIMALE AYANT I VALEURS PROPRES AVANT ELLE
C                     -1D111 indique une valeur non initialisee
C DIAGKG : TABLEAU  AUXILIAIRE  DE TAILLE N REELS DOUBLE PRECISION
C DIAGMG : TABLEAU  AUXILIAIRE  DE TAILLE N REELS DOUBLE PRECISION
C
C MODIFIES:
C ---------
C V      : PREMIERE APPROXIMATION DU PLUS PETIT VECTEUR PROPRE
C          PUIS, CONTIENT CE PLUS PETIT VECTEUR PROPRE AU COURS DES CALCULS
C          ATTENTION: SON ADRESSE MCN DOIT AUSSI MNTHET DE cthet.inc
C W      : TABLEAU AUXILIAIRE DE TAILLE NBNOMA REELS DOUBLE PRECISION
C NBVAPR : NOMBRE DE VALEURS PROPRES DEJA CALCULEES ET STOCKES DANS
C          LES TABLEAUX VALPR, VECPR, MGVECPR
C VECPR  : MGVECPR(N,MXROOT) TABLEAU DES NBVAPR VECTEURS PROPRES CALCULES
C MGVECPR: MGVECPR(N,MXROOT) TABLEAU DE MG FOIS LES VECTEURS PROPRES
C NINV   : NOMBRE D ITERATIONS INVERSES PAR VALEUR PROPRE
C
C SORTIES:
C --------
C NBVAPR : NOMBRE DES PLUS PETITES VALEURS ET VECTEURS PROPRES CALCULES
C VECPR  : VECPR(NBNOMA,NBVAPR) TABLEAU DES NBVAPR VECTEURS PROPRES CALCULES
C VALPR  : VALPR(NBVAPR) TABLEAU DES NBVAPR VALEURS PROPRES CALCULEES
C IERR   : 0 SI PAS D'ERREUR RENCONTREE, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TEXAS A & M UNIVERSITY at QATAR  FEVRIER 2011
C MODIFS : ALAIN PERRONNET LJLL UPMC Paris& St Pierre du Perray Mai 2014
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/epsvvp.inc"
      include"./incl/donthe.inc"
      include"./incl/cthet.inc"
      include"./incl/cnonlin.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
C
      INTEGER          NUMIOB(4), NUMAOB(4),  MNDOEL(4)
      INTEGER          NINV(MXROOT)
      DOUBLE PRECISION KG(NBRDKG), MG(NBRDMG),
     %                 DIAGKG(NBDLIB), DIAGMG(NBDLIB),
     %                 V(NBDLIB), W(NBDLIB),
     %                 VECPR(NBDLIB,MXROOT), MGVECPR(NBDLIB,MXROOT),
     %                 VALPR(MXROOT+1), RAVANT(0:MXROOT*2,2)
      DOUBLE PRECISION RQ0, RQ, R, R1, R2, VP0, VP, TVMGV, TVKGV,
     %                 DETR1, DETR2, DETVP
C
      WRITE(IMPRIM,10000) NBVAPR
10000 FORMAT(/'NLSEVV1: INITIAL NBVAPR=',I5, 2X, 80('#'))

C     INITIALISATIONS DES SEUILS ET AUTRES CONSTANTES
C     -----------------------------------------------
C     NINVMX NOMBRE D ITERATIONS INVERSES MAX POUR LE CALCUL
C     D'UNE VALEUR PROPRE
      NINVMX = 100
C
C     NUMERO D'INITIALISATION DU VECTEUR PROPRE POUR L'ITERATION INVERSE
      NUINVP = 0
C
C     NOMBRE MAXIMAL DE REPRISES APRES OUBLI DE VALEURS PROPRES
      MXREPR = 12
C
C     LE NOMBRE D'ITERATIONS INVERSES PAR VALEUR PROPRE CALCULEE
      DO I=1,MXROOT
         NINV(I) = 0
      ENDDO

C     VALEUR DE NON INITIALISATION DE LA VALEUR MIN et MAX
C     AYANT I VALEURS PROPRES PLACEES AVANT
      DO I=0,MXROOT*2
C        MINIMUM
         RAVANT(I,1) =  1D111
C        MAXIMUM
         RAVANT(I,2) = -1D111
      ENDDO
C
C     ****************************************************************
C     RECHERCHE D'UNE APPROXIMATION R1 DE LA PLUS PETITE VALEUR PROPRE
C     A PARTIR DE LA VALEUR 0D0
C     ****************************************************************
C     FACTORISATION KG = L D TL  ( => PERTE DE KG INITIALE )
      CALL CRMC1D( MCN(MNMUKG), KG, NBDLIB, EPS, 0, KG, NRETOU )
      IF( NRETOU .NE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1)='[KG(tGrad Alfa Grad +N(u**2))] NON INVERSIBLE'
         ELSE
            KERR(1)='[KG(tGrad Alfa Grad +N(u**2))] NOT INVERSIBLE'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF

C     NOMBRE DE FACTORISATIONS L D tL de KG
      NBFACTKG = 1
C
C     NOVALP NUMERO DE LA VALEUR PROPRE QUE L ON CALCULE
      NOVALP = 1

C     NBVAPR EST LE NOMBRE DE VALEURS PROPRES DEJA CALCULEES
C     0D0 EST LA PREMIERE APPROXIMATION DE LA VALEUR PROPRE
      R1 = 0D0
      R2 = 0D0

C     DETERMINANT DE KG + 0D0 MG = L D tL = DETERMINANT DE D
      CALL DETETD( KG, MCN(MNMUKG), NBDLIB, NBAVR1, NBHR1, DETR1 )
C     NBAVR1 EST LE NOMBRE DE PIVOTS NEGATIFS DONC
C     DE VALEURS PROPRES AVANT 0D0

C     NOMBRE DE VALEURS PROPRES OUBLIEES AVANT R1
      NBOUR1 = NBAVR1 - NBVAPR

      IF( NBAVR1 .GE. 0 .AND. NBAVR1 .LE. MXROOT*2 ) THEN
C        MISE A JOUR DE LA VALEUR MINIMALE AYANT NBAVR1 VALEURS PROPRES AVANT
         IF( R1 .LT. RAVANT(NBAVR1,1) ) THEN
            RAVANT(NBAVR1,1) = 0D0
         ENDIF
C        MISE A JOUR DE LA VALEUR MAXIMALE AYANT NBAVR1 VALEURS PROPRES AVANT
         IF( R1 .GT. RAVANT(NBAVR1,2) ) THEN
            RAVANT(NBAVR1,2) = 0D0
         ENDIF
      ENDIF
C
cccC     CONSTRUCTION DE DIAGKG=DIAGONALE(KG) AVANT FACTORISATION
cccC               ET DE DIAGMG=DIAGONALE(MG)
ccc      DO I=1,NBDLIB
ccc         DIAGKG(I) = KG( MCN(MNMUKG+I) )
ccc         DIAGMG(I) = MG( MCN(MNMUMG+I) )
ccc      ENDDO
C
cccC     INITIALISATION DU QUOTIENT DE RAYLEIGH AVEC V0=MG DIAGKG
cccC     V0=W=MG DIAGKG
ccc      CALL MAPRVE( 0, 1D0, NBDLIB, NCODSM, MCN(MNMUMG), MG, DIAGKG, W )
C
C     V EST INITIALISE A 1 DANS nlsevvp.f, CL + MG-ORTHONORMALISE
ccc      CALL MAPRVE( 0, 1D0, NBDLIB, NCODSM, MCN(MNMUMG), MG, V, W )
C
C     VALEUR PRECEDENTE DE RQ
      RQ0 =  0D0
      VP0 = -1D28

C     ITER NOMBRE D ITERATIONS POUR CALCULER LA VALEUR R1
      ITER = 0
C
C     ITERATIONS DE CALCUL DU QUOTIENT DE RAYLEIGHT AVEC LA TRANSLATION R1 MG
C     =======================================================================
 5    ITER = ITER + 1
C
C     W  = MG Vk
C     V  = Vk+1 = (KG - R1 MG)**-1 (MG Vk)
C     TVKGV = tVk+1 (KG - R1 MG) Vk+1 = NUMERATEUR DU QUOTIENT DE RAYLEIGHT
C           = tVk+1 (KG - R1 MG) (KG - R1 MG)**-1 (MG Vk)
C           = tVk+1 (MG Vk)
C     Wk+1  = MG Vk+1
C     TVMGV = tVk+1 MG Vk+1 = DENOMINATEUR DU QUOTIENT DE RAYLEIGHT
C     RQ = tVk+1 (KG - R1 MG) Vk+1 / tVk+1 MG Vk+1
C     EST LE QUOTIENT DE RAYLEIGHT k+1
      CALL NLSEQR( NBDLIB, NBVAPR, VECPR, MGVECPR,
     %             NCODSM, MCN(MNMUMG), MG,
     %             NCODSK, MCN(MNMUKG), KG,
     %             W,   V, W, TVMGV, TVKGV, RQ, IERR )
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,10005) ITER, R1, RQ
      ELSE
         WRITE (IMPRIM,20005) ITER, R1, RQ
      ENDIF
10005 FORMAT('NLSEVV1: DEPART ITERATION ',I4,' AVEC R=',G14.6,
     %' QUOTIENT RAYLEIGHT=',G14.6)
20005 FORMAT('NLSEVV1: BEGIN ITERATION ', I4,' WITH R=',G14.6,
     %' RAYLEIGHT QUOTIENT=',G14.6)
C
      IF( ABS(RQ-RQ0) .GE. 1D-3*ABS(RQ) ) THEN
C        PAS DE CONVERGENCE DU QUOTIENT DE RAYLEIGHT
C        PASSAGE A L'ITERATION SUIVANTE?
         RQ0 = RQ
         IF( ITER .LT. NINVMX ) GOTO 5

C        NOMBRE MAXIMAL D'ITERATIONS ATTEINT
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*) 'NLSEVV1:',ITER,' ITERATIONS => MAXIMUM ATTE
     %INT. POURSUITE DU CALCUL avec R=',RQ
         ELSE
           WRITE(IMPRIM,*) 'NLSEVV1:',ITER,' ITERATIONS => MAXIMUM ATTAI
     %NED. COMPUTATION CONTINUE with R=',RQ
         ENDIF
C
      ENDIF
C
C     CONVERGENCE DU QUOTIENT DE RAYLEIGHT OU ABANDON APRES MAX ITERATIONS
C     R1 = 0 + RQ  EST LE DERNIER QUOTIENT DE RAYLEIGHT CALCULE
C     --------------------------------------------------------------------
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10008) RQ, ITER
      ELSE
         WRITE(IMPRIM,20008) RQ, ITER
      ENDIF
10008 FORMAT('NLSEVV1: VALEUR PROPRE APPROCHEE RQ=',G14.6,
     %       ' APRES',I4,' ITERATIONS INVERSES A PARTIR DU VECTEUR 1D0')
20008 FORMAT('NLSEVV1: APPROACHED EIGENVALUE RQ=',G14.6,
     %       ' after',I4,' INVERSE ITERATIONS FROM 1D0 VECTOR')
C
C     RECALCUL DE LA MATRICE GLOBALE KG - R1 MG
C     FACTORISATION L D tL = KG - R1 MG
C     CALCUL DU NOMBRE DE VALEURS PROPRES INFERIEURES A R1
C     PASSAGES POUR CALCULER UNE VALEUR DE R1 SANS VALEUR PROPRE AVANT
C     ----------------------------------------------------------------
      IEMG   = 0
      IEKG   = 1

      R2 = RQ
      NBAVR2 = 0
      NBOUR2 = 0

      R1 = RQ

C     NOMBRE DE PASSAGE POUR CALCULER CETTE VALEUR MAXIMALE DE R1
C     SANS VALEUR PROPRE AVANT
      NBPASR1 = 0

C     V(NBDLIB) -> VENL(NBNOMA)
      IF( MNDLIB .LE. 0 ) THEN
         MNDLB = 1
      ELSE
         MNDLB = MNDLIB
      ENDIF
 20   CALL CPSIVV( 1, NBNOMA, NBDLIB, MCN(MNDLB), NBRDLX, MCN(MNVDLX),
     %             V, MCN(MNVENL) )

C     DETR1 = DETERMINANT DE KG - R1 MG
      CALL NLSEVOU( NDIM,   NBNOMA, MNTOPO,
     %              NBTYEL, MNNPEF, NDPGST,
     %              MNTPOB, MNTAUX,
     %              MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %              MNTHER, MNTAEL, MNX,    MNVENL,
     %              IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %              IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %              NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %              NBDLIB, MNDLIB,
     %              R1,     NBVAPR, VALPR,  MXROOT, RAVANT,
     %              NBHR1,  DETR1,  NBAVR1, NBOUR1 )

      NBFACTKG = NBFACTKG + 1
C
      IF( NBOUR1 .GT. 0 ) THEN
C        RECHERCHE DE R1 TEL QU'AUCUNE VALEUR PROPRE NE SOIT AVANT
         R2 = R1
         NBAVR2 = NBAVR1
         NBOUR2 = NBOUR1
         IF( R1 .GT. 0D0 ) THEN
            R1 = R1 / 2
            NBPASR1 = NBPASR1 + 1
            IF( NBPASR1 .GT. 10 ) THEN
C              R1 --> 0 . Pour accelerer, R1 EST CHOISIE <0
               R1 = -1D0
               NBPASR1 = 0
            ENDIF
         ELSE IF( R1 .LT. 0D0 ) THEN
            R1 = R1 * 2
         ELSE
            R1 = 0.01D0
         ENDIF
         GOTO 20
      ENDIF
C
C     TROUVER R1 UNE APPROXIMATION INFERIEURE D'UNE VALEUR PROPRE
C     A AFFINER SA VALEUR EN R2 PAR ITERATIONS INVERSES
C     TOUT EN CALCULANT LE VECTEUR PROPRE ASSOCIE
C     ===========================================================

C     ENCADREMENT [R1,R2] DE LA NBVAPR+1 EME VALEUR PROPRE
C     ----------------------------------------------------
C     R1 VALEUR MAXIMALE AYANT 0 VALEUR PROPRE OUBLIEE AVANT ELLE
 30   DO NBAVR1 = NBVAPR, 0, -1
         R1 = RAVANT(NBAVR1,2)
         IF( R1 .NE. -1D111 ) GOTO 32
      ENDDO
      R1     = RAVANT(0,1)
      NBAVR1 = 0

 32   NBOUR1 = 0
      DO I=1,NBVAPR
         IF( VALPR(I) .LE. R1 ) NBOUR1 = NBOUR1 + 1
      ENDDO
      NBOUR1 = MAX( 0, NBAVR1 - NBOUR1 )
      VP     = R1
      NBAVVP = NBAVR1
      NBOUVP = NBOUR1

C     R2 VALEUR MINIMALE AYANT AU MOINS UNE VALEUR PROPRE AVANT ELLE
      DO NBAVR2 = NBVAPR+1, MXROOT*2
C        LA VALEUR MIN APRES LA NBVAPR+1 VALEUR PROPRE
         R2 = RAVANT(NBAVR2,1)
         IF( R2 .NE. 1D111 ) THEN
            IF( NBAVR2 .EQ. NBVAPR+1 ) THEN
               R2 = R2 * 1.04D0
               GOTO 35
            ELSE
               R2 = R2 * 1.5D0
               GOTO 34
            ENDIF
         ENDIF
      ENDDO

C     EN ABSENCE DE VALEUR MAJORANTE R2 ESSAI AVEC 3*R1
      R2 = 3 * R1

 34   CALL CPSIVV( 1, NBNOMA, NBDLIB, MCN(MNDLB), NBRDLX, MCN(MNVDLX),
     %             V, MCN(MNVENL) )
      CALL NLSEVOU( NDIM,   NBNOMA, MNTOPO,
     %              NBTYEL, MNNPEF, NDPGST,
     %              MNTPOB, MNTAUX,
     %              MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %              MNTHER, MNTAEL, MNX,    MNVENL,
     %              IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %              IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %              NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %              NBDLIB, MNDLIB,
     %              R2,     NBVAPR, VALPR,  MXROOT, RAVANT,
     %              NBHR2,  DETR2,  NBAVR2, NBOUR2 )
      VP = R2
      NBAVVP = NBAVR2
      NBOUVP = NBOUR2

      IF( NBOUR2 .LE. 0 ) THEN
         R2 = 3 * R2
         GOTO 34
      ENDIF

 35   NBOUR2 = 0
      DO I=1,NBVAPR
         IF( VALPR(I) .LE. R2 ) NBOUR2 = NBOUR2 + 1
      ENDDO
      NBOUR2 = MAX( 0, NBAVR2 - NBOUR2 )

      IF( R1 .GT. R2 ) THEN
         R  = R1
         R1 = R2
         R2 = R
      ENDIF

ccc      VP = (R1+R2) / 2
cccC     KG(|V|**2) - VP MG = L D tL ET NOMBRE VALEURS PROPRES AVANT VP
ccc         CALL CPSIVV( 1,NBNOMA,NBDLIB,MCN(MNDLIB),NBRDLX,MCN(MNVDLX),
ccc     %                V, MCN(MNVENL) )
ccc         CALL NLSEVOU( NDIM,   NBNOMA, MNTOPO,
ccc     %                 NBTYEL, MNNPEF, NDPGST,
ccc     %                 MNTPOB, MNTAUX,
ccc     %                 MNXYZP, NUMIOB, NUMAOB, MNDOEL,
ccc     %                 MNTHER, MNTAEL, MNX,    MNVENL,
ccc     %                 IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
ccc     %                 IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
ccc     %                 NBRDLX, MONDLX, MNNDLX, MNVDLX,
ccc     %                 NBDLIB, MNDLIB,
ccc     %                 VP,     NBVAPR, VALPR,  MXROOT, RAVANT,
ccc     %                 NBHVP,  DETVP,  NBAVVP, NBOUVP )

C     AFFICHAGES DES VALEURS ET VECTEURS PROPRES ACTUELS
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,10030) NOVALP
         WRITE (IMPRIM,10031) (KK,VALPR(KK),KK=1,NBVAPR)
         WRITE (IMPRIM,10032) 'R1 ',NBAVR1, R1, NBOUR1
         WRITE (IMPRIM,10032) 'R2 ',NBAVR2, R2, NBOUR2
         WRITE (IMPRIM,10032) 'VP ',NBAVVP, VP, NBOUVP
      ELSE
         WRITE (IMPRIM,20030) NOVALP
         WRITE (IMPRIM,20031) (KK,VALPR(KK),KK=1,NBVAPR)
         WRITE (IMPRIM,20032) 'R1 ',NBAVR1, R1, NBOUR1
         WRITE (IMPRIM,20032) 'R2 ',NBAVR2, R2, NBOUR2
         WRITE (IMPRIM,20032) 'VP ',NBAVVP, VP, NBOUVP
      ENDIF
10030 FORMAT(/'NLSEVV1: CALCUL DE LA VALEUR PROPRE',I4,'  ',100('*'))
10031 FORMAT('VALEURS PROPRES DEJA CALCULEES:'/5(I5,':',G14.6))
10032 FORMAT(A,I5,' VALEURS PROPRES AVANT',G14.6, ' et',
     %         I5,' VALEURS PROPRES OUBLIEES')
C
20030 FORMAT(/'NLSEVV1: COMPUTATION of the EIGENVALUE',I4,'  ',100('*'))
20031 FORMAT('ALREADY COMPUTED EIGENVALUES:'/5(I5,':',G14.6))
20032 FORMAT(A,I5,' EIGENVALUES BEFORE',G14.6,' and',
     %         I5,' FORGOTTEN EIGENVALUES')

      print 10038,NOVALP,VP
10038 FORMAT('NLSEVV1: NOVALP=',I4,' VP=',G14.6)
      do j=1,2
         print 10039,(i,j,ravant(i,j),i=0,MXROOT*2)
      enddo
10039 FORMAT(5(' ravant(',I3,',',I3,')=',G14.6))
C
C     ITERATIONS INVERSES POUR AFFINER LA VALEUR R1 EN VP
C     ET CALCULER LE VECTEUR PROPRE V ASSOCIE A VP
      CALL NLSEVV2( NDIM,   NBNOMA, MNTOPO,
     %              NBTYEL, MNNPEF, NDPGST, MNTPOB, MNTAUX,
     %              MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %              MNTHER, MNTAEL, MNX,    MNVENL,
     %              IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %              IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %              NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %              NBDLIB, MNDLIB,
     %              MXROOT, NOVALP, NUINVP, DIAGKG, DIAGMG,
     %              NBVAPR, VALPR,  VECPR,  MGVECPR,
     %              RAVANT, NINV,   NBFACTKG,
     %              R1,     NBAVR1, NBOUR1,
     %              R2,     NBAVR2, NBOUR2,
     %              VP,     NBAVVP, NBOUVP, NBHVP, DETVP,
     %              V,      W,      IERR  )
      IF( IERR .NE. 0 ) GOTO 80
C     ARRET AVEC LES VALEURS PROPRES DEJA CALCULEES SANS GARANTIE...
C
C     EN SORTIE:
C     VP EST LA VALEUR PROPRE DEFINITIVE RANGEE DANS VALPR(NOVALP)
C     V SON VECTEUR PROPRE ASSOCIE RANGE DANS VECPR(*,NOVALP)
C     W = [MG] V LE PRODUIT DE [MG] PAR V RANGE DANS MGVECPR(*,NOVALP)
      IF( ABS(VP-VP0) .LE. ABS(VP0*1D-4) ) THEN
         print *
         print *,'ATTENTION: VP0=',VP0,' is FOUND AGAIN VP=',VP
         print *,'VP EST UNE VALEUR PROPRE MULTIPLE'
         print *,'VERIFIER QUE LES VECTEURS PROPRES ASSOCIES NE SONT PAS
     % IDENTIQUES'
         print *
      ENDIF
      VP0 = VP

      IF( NBVAPR .GE. NBROOT ) GOTO 80
C     LE NOMBRE DE VALEURS PROPRES CALCULEES EST CELUI DEMANDE SANS OUBLI

C     LA VALEUR PROPRE SUIVANTE EST A CALCULER
C     ----------------------------------------
      NOVALP = NOVALP + 1
C     RETOUR A L'ITERATION INVERSE ET
C     ON FAIT VARIER L'INITIALISATION DU VECTEUR PROPRE
      NUINVP = MOD( NUINVP, 9 ) + 1
      GOTO 30

C     *****************************************************************
C     LES NBVAPR     VALEURS ET VECTEURS PROPRES ONT ETE CALCULES
C     CLASSEMENT DES VALEURS ET VECTEURS PROPRES DANS L ORDRE CROISSANT
C     *****************************************************************
 80   IF( NBVAPR .GT. 1 ) THEN
C        TRI CROISSANT DES VALEURS PROPRES
 85      IS = 0
         DO I=1,NBVAPR-1
            IF( VALPR(I+1) .LT. VALPR(I) ) THEN
               IS        = IS + 1
               VP        = VALPR(I+1)
               VALPR(I+1)= VALPR(I)
               VALPR(I)  = VP
               K         = NINV(I+1)
               NINV(I+1) = NINV(I)
               NINV(I  ) = K
               DO K=1,NBDLIB
                  VP           = VECPR(K,I+1)
                  VECPR(K,I+1) = VECPR(K,I)
                  VECPR(K,I)   = VP
C                 EN CAS DE REPRISE A FAIRE
                  VP             = MGVECPR(K,I+1)
                  MGVECPR(K,I+1) = MGVECPR(K,I)
                  MGVECPR(K,I)   = VP
               ENDDO
            ENDIF
         ENDDO
         IF( IS .GT. 0 ) GOTO 85
      ENDIF
C
C     AFFICHAGE DES VALEURS PROPRES ACTUELLES
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,10086) (K,VALPR(K),K=1,NBVAPR)
      ELSE
         WRITE (IMPRIM,20086) (K,VALPR(K),K=1,NBVAPR)
      ENDIF
10086 FORMAT('VALEURS PROPRES CONVERGEES:'/5(I5,':',G14.6))
20086 FORMAT('CONVERGED EIGENVALUES:'/5(I5,':',G14.6))

      IF( IERR .NE. 0 ) GOTO 9000
C
C     ----------------------------------------------------
C     VERIFICATION: Y A T IL DES VALEURS PROPRES OUBLIEES?
C     ----------------------------------------------------
C     RECHERCHE DE LA DERNIERE VALEUR PROPRE SANS OUBLI AVANT
      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*)
     %  'RECHERCHE DE LA DERNIERE VALEUR PROPRE SANS OUBLI AVANT'
      ELSE
         WRITE(IMPRIM,*)'SEARCH of the LAST EIGENVALUE without FORGOTTEN
     % EIGENVALUE BEFORE'
      ENDIF
C
      DO 90 K = NBVAPR, 1, -1

C        LA VALEUR PROPRE K TESTEE
         R2 = VALPR( K ) * 1.0001D0
C
C        NOMBRE DE VALEURS PROPRES OUBLIEES AVANT R2 ET MISE A JOUR DE R1
c        attention: ici il faut recalculer KG((valpr(K)*1.0001)**2)!
         DO I=1,NBDLIB
            V(I) = VECPR(I,K)
         ENDDO
         CALL CPSIVV( 1,NBNOMA,NBDLIB,MCN(MNDLIB),NBRDLX,MCN(MNVDLX),
     %                V, MCN(MNVENL) )
         CALL NLSEVOU( NDIM,   NBNOMA, MNTOPO,
     %                 NBTYEL, MNNPEF, NDPGST,
     %                 MNTPOB, MNTAUX,
     %                 MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                 MNTHER, MNTAEL, MNX,    MNVENL,
     %                 IEMG,   NCODSM, NBRDMG, MNMUMG, MG,
     %                 IEKG,   NCODSK, NBRDKG, MNMUKG, KG,
     %                 NBRDLX, MONDLX, MNNDLX, MNVDLX,
     %                 NBDLIB, MNDLIB,
     %                 R2,     NBVAPR, VALPR,  MXROOT, RAVANT,
     %                 NBHR2,  DETR2,  NBAVR2, NBOUR2 )

         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10089) NBOUR2, R2
         ELSE
            WRITE(IMPRIM,20089) NBOUR2, R2
         ENDIF

         IF( NBOUR2 .LE. 0 ) THEN
            IF( K .EQ. NBVAPR ) THEN
C              AUCUNE VALEUR PROPRE OUBLIEE
               GOTO 9000
            ELSE
C              REPRISE AVEC K VALEURS PROPRES CALCULEES SANS OUBLI
               NBVAPR = K
               GOTO 92
            ENDIF
         ENDIF

 90   CONTINUE

10089 FORMAT(/'TEST FINAL:',I5,' VALEURS PROPRES OUBLIEES AVANT',G14.6)
20089 FORMAT(/'FINAL TEST:',I5,' FORGOTTEN EIGENVALUES BEFORE',G14.6)
C
C     LA VALEUR PROPRE VALPR(K)*1.0001D0 N'A PAS DE VALEUR PROPRE OUBLIEE AVANT
C     MAIS IL MANQUE AU MOINS UNE VALEUR PROPRE AVANT VALPR(K+1)*1.0001D0
C     -------------------------------------------------------------------------
 92   IF( MXREPR .LE. 0 ) THEN
C        NOMBRE MAXIMAL DE REPRISES ATTEINT
         NBVAPR = K
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10092) NBVAPR
         ELSE
            WRITE(IMPRIM,20092) NBVAPR
         ENDIF
10092    FORMAT('MAXIMUM DE REPRISES ATTEINT: ABANDON AVEC',I5,
     %   ' VALEURS PROPRES CALCULEES'/)
20092    FORMAT('MAXIMUM of RETAKING REACHED: EXIT with',I5,
     %   ' COMPUTED EIGENVALUES'/)
         GOTO 9000
      ENDIF
C
C     UNE NOUVELLE REPRISE DU CALCUL
      MXREPR = MXREPR - 1
      NUINVP = MOD( NUINVP, 9 ) + 1
      IF( NBAVR2 .GT. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10095) NBOUR2, R2
         ELSE
            WRITE(IMPRIM,20095) NBOUR2, R2
         ENDIF
      ENDIF

10095 FORMAT(/'ATTENTION:',I4,' VALEURS PROPRES OUBLIEES AVANT',
     %         G14.6,' => REPRISE NECESSAIRE'/1X,100('&')//)
20095 FORMAT(/'WARNING:',I4,'  FORGOTTEN EIGENVALUES BEFORE',
     %         G14.6,' => NECESSARY RETAKING'/1X,100('&')//)
C
C     LE NOMBRE DE VALEURS PROPRES ACCEPTEES
      NOVALP = K
C
C     LE NOMBRE DES ITERATIONS EST REMIS A ZERO
      NOVALP = NOVALP + 1
      DO J=NOVALP,MXROOT
         NINV(J) = 0
      ENDDO

      GOTO 30
C
C     PAS DE VALEUR PROPRE OUBLIEE => FIN DU CALCUL
C     =============================================
 9000 IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,19000) (NINV(J),J=1,NBVAPR)
      ELSE
         WRITE (IMPRIM,29000) (NINV(J),J=1,NBVAPR)
      ENDIF
19000 FORMAT(
     %/'NOMBRE D''ITERATIONS INVERSES POUR CHAQUE VALEUR PROPRE:'
     %/(1X,10I8))
29000 FORMAT(
     %/'NUMBER of INVERSE ITERATIONS for EACH EIGENVALUE:'
     %/(1X,10I8))
C
ccc 9999 WRITE(IMPRIM,*) 'SORTIE NLSEVV1'
      WRITE(IMPRIM,*) 'SORTIE NLSEVV1'
      RETURN
      END
