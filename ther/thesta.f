         IF( NORMDF .GT. EPSITE * NORMUM .OR.
     %     (IEVIFL.GT.0 .AND. ITERPF .LE. 1) ) THEN
CCC     %    .OR.  (TESTNL.EQ.3 .AND. ITERPF .LT. 10) )THEN
