            ENDIF
11532       FORMAT(' ITERATION POINT FIXE',I3,' MAX|U(N)|=',G15.6,
     %             ' ||Un||=',G15.6,' ||Un-Un-1||=',G15.6,
     %            '  ||Un-Un-1||/||Un||=',G12.4)
21532       FORMAT(' FIX POINT ITERATION',I3,' MAX|U(N)|=',G15.6,
     %             ' ||Un||=',G15.6,' ||Un-Un-1||=',G15.6,
     %            '  ||Un-Un-1||/||Un||=',G12.4)
C
         ENDIF
