               NORMDF = ABS( ENERGI-ENERG0 )
               NORMUM = ABS( ENERGI )
            ENDIF
            DIFE = ABS( (NORH1U-NORH10) / NORH1U )
            IF( DIFE .GT. NORMDF/NORMUM ) THEN
