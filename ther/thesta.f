C           LES IMPRESSIONS
            MNXYMX = MNXYZN + WYZNOE + 3*MNXYMX - 3
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,11519) NORMXP*FACECH,(RMCN(MNXYMX+I),I=0,2)
