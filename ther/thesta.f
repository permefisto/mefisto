            ELSE
               WRITE(IMPRIM,21519) NORMXP*FACECH,(RMCN(MNXYMX+I),I=0,2)
               WRITE(IMPRIM,21520) NORMUM*FACECH, NORMDF/NORMUM,
     %                             NORH1U,ABS((NORH1U-NORH10)/NORH1U),
