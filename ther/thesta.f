     %               G15.6,',',G15.6,',',G15.6,' )')
21519       FORMAT(' MAX|U(N)|=',G15.6,'   at NODE XYZ=(',
     %               G15.6,',',G15.6,',',G15.6,' )')
C
11520       FORMAT(' ||Un||   =',G15.6,'   ||Un-Un-1||/||Un||=',G15.6/
     %             ' | Un |H1 =',G15.6,'   | Hn-Hn-1 |/| Hn |=',G15.6/
     %             ' J=ENERGIE=',G15.6,'   ||En-En-1||/||En||=',G15.6/
