         IF( TESTNL .EQ. 2 ) THEN
            WRITE(IMPRIM,22000) NORMUM, NORMDF,
     %                          NORMDF/NORMUM, EPSITE
22000       FORMAT(' Norm UM  =',G15.6,
     %            '   Norm DIFF=',G15.6,
     %            '  NormDIFF/NormUM=',G15.6,
     %            '  EpsIter=',G15.6)
         ENDIF
C
ccc         print *,'Test iter:',normdf,epsite,ievifl,iterpf,testnl
ccc     %          ,NORMDF,NORMUM
