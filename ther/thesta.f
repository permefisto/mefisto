     %                    MCN(MNRG) )
            RESIDU = -1D0
            MNR    = ( MNRG - 1 ) / 2
            DO 1522 I=1,NTDL
               IF( ABS(DMCN(MNR+I)) .GT. RESIDU )
     %             RESIDU = ABS(DMCN(MNR+I))
 1522       CONTINUE
