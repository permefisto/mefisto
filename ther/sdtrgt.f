      SUBROUTINE SDTRGT( NTLXOB , NBTRFL , NBSD , KNOMCO ,
     %                   NBTYEL , MNTOPO , MNELEM , MNPOGE ,
     %                   NCAS   , NOPT   , CMFLEC , CMPGRA ,
     %                   GRADMX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES FLECHES REPRESENTANT LES GRADIENTS DE TEMPERATURE
C -----    AUX POINTS D'INTEGRATION NUMERIQUE
C
C ENTREES :
C ---------
C KNOMOB : NOM DE L'OBJET
C NTLXOB : NUMERO DU TMS LEXIQUE DE L'OBJET A TRAITER
C KNOMCO : NOM DE L'OBJET COMPLET
C NBTRFL : LE NUMERO DE CE SOUS-DOMAINE
C NBSD   : LE NOMBRE TOTAL DE SOUS-DOMAINES
C NBTYEL : LE NOMBRE DE TYPES D'ELEMENTS DANS CETTE TOPOLOGIE
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE
C MNELEM : ADRESSE MCN DES TABLEAUX ELEMENTS DE CETTE TOPOLOGIE
C MNPOGE : ADRESSE MCN DU TABLEAU POINTS GEOMETRIQUES DE L'OBJET
C
C MODIFIES EVENTUELLEMENT :
C -------------------------
C NCAS   : NUMERO DU CAS A TRAITER
C NOPT   : NUMERO DE L'OPTION DE TRAITEMENT
C CMFLEC : NOMBRE DE CM DU DE LA FLECHE MAXIMALE
C CMPGRA : NOMBRE DE CM POUR L'UNITE DE GRADIENT
C GRADMX : LE GRADIENT MAXIMUM
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PASCAL JOLY   ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1990
C23456---------------------------------------------------------------012
      PARAMETER     (LIGCON=0)
      include"./incl/gsmenu.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___dtemperature.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/ponoel.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/a___fluxpt.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              GRADMX
      CHARACTER*(*)     KNOMCO
      CHARACTER*160     KNOM
      CHARACTER*4       NOMELE(2)
C
      MOREE2 = MOTVAR(6)
C
      IF (NBTRFL .NE. 1 ) GOTO  200
C
C     LECTURE DES DONNEES
 100  CALL LIMTCL( 'tracgrad' , NMTCL )
      IF( NMTCL .LE.  0 ) GOTO 9000
C
      GOTO( 110, 120, 100, 100, 150, 160, 170 ) , NMTCL
C
C     FLECHE MAXIMALE TRACEE EN CM
 110  NOPT = 1
      NCVALS = 0
      CALL INVITE( 66 )
      CALL LIRRSP(NCVALS,CMFLEC)
      IF( NCVALS .EQ. -1 ) GOTO 100
      IF( CMFLEC .LT. 0. ) CMFLEC = -CMFLEC
      GOTO 135
C
C     1CM  VAUT EN GRADIENT
 120  NOPT = 2
      NCVALS = 0
      CALL INVITE( 103 )
      CALL LIRRSP(NCVALS,GRADMX)
      IF( NCVALS .EQ. -1 ) GOTO 100
 135  IF( GRADMX .EQ. 0. ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'GRADIENT NUL A CORRIGER'
         CALL LEREUR
         GOTO 100
      ENDIF
      GRADMX = ABS( GRADMX )
      GOTO 300
C
C     COULEUR des ARETES du MAILLAGE
 150  CALL LIMTCL( 'couleur0' , I )
      IF( I .EQ. -1 ) THEN
         GOTO 9000
      ELSE IF( I .EQ. -2 ) THEN
         NCOUAF = -2
      ELSE IF( I .EQ. 0 ) THEN
C        COULEUR NOIRE
         NCOUAF = 0
      ELSE
C        COULEUR RESERVEE
         NCOUAF = N1COEL + I
      ENDIF
      GOTO 100
C
C     TYPE du TRAIT des ARETES du MAILLAGE
 160  CALL LIMTCL( 'typtrait' , I )
      IF( I .EQ. -1 ) GOTO 100
      NTLAFR = I
      GOTO 100
C
C     COULEUR des FLECHES
 170  CALL LIMTCL( 'couleurs' , I )
      IF( I .LT. 0 ) THEN
         GOTO 9000
      ELSE IF( I .EQ. 0 ) THEN
C        COULEUR NOIRE
         NCOUFL = 0
      ELSE
         NCOUFL = N1COEL + I
      ENDIF
      GOTO 100
C
C     EXECUTION DU TRACE DES GRADIENTS
 300  IF( NOPT .EQ. 1 .AND. CMFLEC .LE. 0. ) GOTO 9999
      IF( NOPT .EQ. 2 .AND. GRADMX .LE. 0. ) GOTO 9999
      IF( NOPT .EQ. 3 .AND. CMPGRA .LE. 0. ) GOTO 9999
      WRITE(IMPRIM,10310) NCAS
10310 FORMAT(' CAS DE CHARGE VISUALISE =',I5/)
C
C     MISE A JOUR DE L'ECHELLE
      IF( NOPT .EQ. 1 ) THEN
         CMPGRA = CMFLEC / GRADMX
      ELSE IF( NOPT .EQ. 2 ) THEN
         CMPGRA = 1. / GRADMX
      ENDIF
C
C     LA FENETRE EST EFFACEE
      CALL EFFACE
C     VALEURS PAR DEFAUT DU TRACE
 320  CALL VISEE( NMTCL )
C     LA PREPARATION DU TRACE
C      CALL VISEE1
C
      IF( NDIM  .EQ. 2 ) THEN
C        INITIALISATION DU ZOOM DEPLACEMENT
         CALL ZOOM2D0( NOTYEV )
      ELSE
C        INITIALISATION DE L'ORBITE ZOOM DEPLACEMENT
         CALL ORBITE0( NOTYEV )
      ENDIF
      IF( NOTYEV .EQ. 0 ) GOTO 320
      GOTO 200
C
C     EN 2D: ZOOM OU TRANSLATION ACTIFS
 322  CALL ZOOM2D1( NOTYEV )
      IF( NOTYEV .EQ. 0 ) GOTO 320
      GOTO 200
C
C     EN 3D: ORBITE OU ZOOM OU TRANSLATION ACTIFS
 323  CALL ORBITE1( NOTYEV )
      IF( NOTYEV .EQ. 0 ) GOTO 320
C
C     LE CODE DE TRAITEMENT DE L'OBJET
C     NDPGST : CODE TRAITEMENT DE L ELEMENT
C                 0 : NOEUDS=POINTS=SOMMETS
C                 1 : NOEUDS=POINTS#SOMMETS
C                 2 : NOEUDS#POINTS=SOMMETS
C                 3 : NOEUDS#POINTS#SOMMETS
 200  NDPGST = MCN( MNTOPO + WDPGST )
C
C     BOUCLE SUR LES DIFFERENTS TYPES D'ELEMENTS DU MAILLAGE
      DO 420 I = 0 , NBTYEL-1
C        L'ADRESSE MCN DU DEBUT DU TABLEAU ELEMENTS
         MNELE = MCN( MNELEM + I )
C        LE NOM DU TABLEAU GRADIENTS ASSOCIE
         NUTYEL = MCN( MNELE + WUTYEL )
C        LES CARACTERISTIQUES DE L'ELEMENT FINI
         CALL ELNUNM( NUTYEL , NOMELE )
         KNOM = 'DTEMPERATURE"' // NOMELE(2)
C        OUVERTURE DU TABLEAU
         CALL LXTSOU( NTLXOB , KNOM , NTGRAD , MNGRAD )
         IF( NTGRAD .LE. 0 ) THEN
            NBLGRC(NRERR) = 1
            KERR(1) = 'OBJET SANS GRADIENT DE TEMPERATURE'
            CALL LEREUR
            GOTO 420
         ENDIF
C        LES CARACTERISTIQUES DE L'ELEMENT
         CALL ELTYCA( NUTYEL )
C
C        TRACE EFFECTIF DES FLECHES
C        LES DIFFERENTS TABLEAUX ET VARIABLES DE 'DTEMPERATURE'
C        AFIN DE BENEFICIER DES INDICES FORTRAN EN CLAIR
         NBELFI = MCN(MNGRAD+WBELFD)
         NBPIEF = MCN(MNGRAD+WBPEFD)
         NBJECA = MCN(MNGRAD+WBJECD)
         NDIMES = MCN(MNGRAD+WDIMED)
         MNPGEL = MNELE + WUNDEL
         IF( NDPGST .GE. 2 ) THEN
            MNPGEL = MNPGEL + MCN(MNELE+WBELEM) * MCN(MNELE+WBNDEL)
         ENDIF
C
C        TRACE EFFECTIF DES ARETES DES EF DE CE TYPE
C        -------------------------------------------
         NBELFI = MCN(MNGRAD+WBELFD)
         NDIMES = MCN(MNGRAD+WDIMED)
         CALL XVTYPETRAIT( NTLAFR )
         CALL XVEPAISSEUR( 1 )
         CALL TRAREF( NDIM,   MCN(MNPOGE+WNBPOI), MCN(MNPOGE+WYZPOI),
     %                NBELFI, MCN(MNPGEL) )
C
C        TRACE EFFECTIF DES FLECHES DU GRADIENT DE TEMPERATURE
C        -----------------------------------------------------
C        LIGNES EPAISSIES
         CALL XVEPAISSEUR( 2 )
         CALL XVTYPETRAIT( LIGCON )
C
C        TRACE EFFECTIF DES FLECHES DU GRADIENT DE TEMPERATURE
C        -----------------------------------------------------
C        LES DIFFERENTS TABLEAUX ET VARIABLES DE 'DTEMPERATURE'
C        AFIN DE BENEFICIER DES INDICES FORTRAN EN CLAIR
         NBPIEF = MCN(MNGRAD+WBPEFD)
         NBJECA = MCN(MNGRAD+WBJECD)
         MNCOPG = MNGRAD + WTEMPE + MOREE2*NBELFI*NDIMES*NBPIEF*NBJECA
         CALL TRGR123( NBELFI, NBPIEF, NDIMES, NBJECA,
     %                 MCN(MNCOPG), MCN(MNGRAD+WTEMPE),
     %                 NCAS, CMPGRA )
C
 420  CONTINUE
C
C     RETOUR AUX PARAMETRES INITIAUX
      CALL XVEPAISSEUR( 1 )
C
C     LE TRACE DU TITRE
      IF ( NBTRFL .NE. NBSD ) RETURN
C
C     LE NOMBRE DE CM DE LA LARGEUR DE LA FENETRE
      R = 0.1 * LAPXFE / CXMMPX
C
C     1 CM DE LARGEUR SUR L'ECRAN REPRESENTE R UNITES OBJETS
      R = ( XOBMAX - XOBMIN ) / R
C
      KNOM = 'OBJET: ' // KNOMCO
      I    = NUDCNB( KNOM )
      CALL XVCOULEUR( NCBLAN )
      CALL XVTEXTE( KNOM(1:I), I, 50, 35 )
C
      KNOM = '1CM DE FLECHE= '
      WRITE( KNOM(15:27), '(G13.5)' )  1./CMPGRA
      KNOM(28:72) = 'UNITES de GRADIENTS de TEMPERATURE '
      I    = NUDCNB( KNOM )
      CALL XVTEXTE( KNOM(1:I), I, 50, 60 )
C     FIN DU TRACE
      CALL TRFINS( 'GRADIENT DE TEMPERATURE' )
C
      IF( LORBITE .EQ. 0 ) GOTO 9000
      IF( NDIM .EQ. 2 ) THEN
         GOTO 322
      ELSE
         GOTO 323
      ENDIF
C
C     SORTIE SANS ERREUR
C     ==================
 9000 RETURN
C
C     ERREUR
 9999 WRITE(KERR(MXLGER-3)(1:13),'(I13)')   NOPT
      WRITE(KERR(MXLGER-2)(1:13),'(G13.5)') CMFLEC
      WRITE(KERR(MXLGER-1)(1:13),'(G13.5)') GRADMX
      WRITE(KERR(MXLGER)(1:13),'(G13.5)')   CMPGRA
      NBLGRC(NRERR) = 5
      KERR(1) = 'ERREUR :  OPTION '
     &           // KERR(MXLGER-3)(1:13)
      KERR(2) = 'FLECHE MAXIMALE EN CM '
     &           // KERR(MXLGER-2)(1:13)
      KERR(3) = 'GRADIENT D''UN CM DE TRACE '
     &           // KERR(MXLGER-1)(1:13)
      KERR(4) = 'CM / GRADIENT '
     &           // KERR(MXLGER)(1:13)
      CALL LEREUR
      GOTO 100
      END
