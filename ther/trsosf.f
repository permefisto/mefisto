      SUBROUTINE TRSOSF( NOPROJ, KNOMOB, MODECO, MNPOGE,
     %                   NBCOMP, NCAS0,  NCAS1,
     %                   NTYP,   SOLUTION, dptemp,
     %                   SOLMIN, SOLMAX, TIMES )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     TRACER DE LA SOLUTION SCALAIRE NCAS EN CHAQUE NOEUD
C -----    (TEMPERATURE ou PRESSION ou ...) SUR LES SURFACES 3D
C           FRONTALIERES DE L'OBJET
C
C ENTREES:
C --------
C NOPROJ  : TYPE DE PROJECTION 0 CI-DESSOUS FIXE LA COORDONNEE A ZERO
C          -1 PAS DE PROJECTION TRAITEMENT en XYZ NORMAL
C           1 : 'X Y Z 0 0 0'
C           2 : 'X Y 0 U 0 0'
C           3 : 'X 0 0 U V 0'
C           4 : '0 0 0 U V W'
C KNOMOB : NOM DE L'OBJET
C MODECO : MODE DE TRACE DES VECTEUR SOLUTION(NBCOMP,NCAS0:NCAS1)
C          =1 CE SONT DES TEMPERATURES
C          =2 CE SONT DES MODES PROPRES
C          =3 CE SONT DES PRESSIONS P1 A PARTIR D'UNE INTERPOLATION
C             SOIT en 2D P2 SOIT P1+BULLE P3 OU en 3D P1 OU en 3D P2
C          =4 CE SONT DES ERREURS AUX NOEUDS D'UN MAILLAGE
C NBTYEL : LE NOMBRE DE TYPES D'ELEMENTS DANS CETTE TOPOLOGIE
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE
C MNPOGE : ADRESSE MCN DU TABLEAU POINTS GEOMETRIQUES DE L'OBJET

C NBCOMP : NOMBRE DE COMPOSANTE D'UN VECTEUR SOLUTION
C NCAS0  : NUMERO DU PREMIER CAS A TRAITER
C NCAS1  : NUMERO DU DERNIER CAS A TRAITER
C NBCOMP : NOMBRE DE COMPOSANTE D'UN VECTEUR SOLUTION
C NTYP   : =0 EMPLOI de SOLUTION
C          =1 EMPLOI de dptemp
C SOLUTION : VECTEURS SOLUTION(NBCOMP,NBCAS0:NCAS1)
C dptemp : TABLEAU DES NCAS0:NCAS1 TABLEAU(NTDL) SOLUTIONS
C SOLMIN : SOLUTION MINIMALE DU CAS NCAS
C SOLMAX : SOLUTION MAXIMALE DU CAS NCAS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC & Saint PIERRE du PERRAY   AOUT 2010
C23456---------------------------------------------------------------012
      PARAMETER   ( LIGCON=0 )
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a_objet__definition.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___face.inc"
      include"./incl/a___aretefr.inc"
      include"./incl/ponoel.inc"
      include"./incl/donele.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/ctemps.inc"
C
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      CHARACTER*(*)     KNOMOB
      CHARACTER*90      KNOM
      REAL              TIMES(NCAS0:NCAS1)
      DOUBLE PRECISION  SOLUTION(NBCOMP,NCAS0:NCAS1)
      type typ_dptab
         DOUBLE PRECISION, dimension(:), pointer :: dptab
      end type typ_dptab
      type( typ_dptab ),dimension(NCAS0:NCAS1) :: dptemp
C
      MNBARY = 0
      MNNUFA = 0
C
C     NBPOI  NOMBRE DE POINTS DU MAILLAGE DE L'OBJET
      NBPOI  = MCN( MNPOGE + WNBPOI )
C
C     NBCOOR : NOMBRE DE COORDONNEES DES NOEUDS=POINTS (3 ou 6)
      NBCOOR = MCN( MNPOGE + WBCOOP )
C
C     MIN ET MAX DES COORDONNEES DES POINTS DU MAILLAGE DE L'OBJET
      CALL MAJEXT( MNPOGE )
C
C     CREATION OU REDECOUVERTE DU TMS OBJET>>>FACE
      CALL HACHOB( KNOMOB, 4, NTFAOB, MNFAOB, IERR )
C
C     CREATION DU HACHAGE DES ARETES DES FACES FRONTALIERES DE L'OBJET
      CALL HACHAF( KNOMOB, 0, NTFAOB, MNFAOB,
     %             NTAFOB, MNAFOB, IERR )
      IF( IERR .NE. 0 ) RETURN
C
C     LE NOMBRE D'ENTIERS PAR FACE
      MOFACE = MCN( MNFAOB + WOFACE )
C     LA MAJORATION DU NOMBRE DE FACES DU TABLEAU LFACES
      MXFACE = MCN( MNFAOB + WXFACE )
C     LE NUMERO DE LA PREMIERE FACE FRONTALIERE (DANS UN SEUL EF)
      L1FAFR = MCN( MNFAOB + W1FAFR )
C     LE NOMBRE DE FACES FRONTALIERES
      NBFAFR = MCN( MNFAOB + WBFAFR )
C     ADRESSE MCN DU 1-ER MOT DU TABLEAU LFACES
      MNLFAC = MNFAOB + WFACES
C
C     LE NOMBRE D'ENTIERS PAR ARETE FRONTALIERE
      MOARFR = MCN( MNAFOB + WOARFR )
C     LA MAJORATION DU NOMBRE DES ARETES FRONTALIERES
      MXARFR = MCN( MNAFOB + WXARFR )
C     LE NUMERO DANS LAREFR DE LA PREMIERE ARETE FRONTALIERE
      L1ARFR = MCN( MNAFOB + W1ARFR )
C     LE NOMBRE D'ARETES FRONTALIERES DANS LE CHAINAGE
      NBARFR = MCN( MNAFOB + WBARFR )
C     ADRESSE MCN DU 1-ER MOT DU TABLEAU ARETFR
      MNLARE = MNAFOB + WAREFR
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'NOMBRE DE FACES FRONTALIERES=',NBFAFR
         WRITE(IMPRIM,*) 'NOMBRE D''ARETES FRONTALIERES=',NBARFR
      ELSE
         WRITE(IMPRIM,*) 'NUMBER of BOUNDARY FACES=',NBFAFR
         WRITE(IMPRIM,*) 'NUMBER of BOUNDARY EDGES=',NBARFR
      ENDIF
C
C     TABLEAU DES BARYCENTRES ET NO DES FACES ET ARETES FRONTALIERES
      NBAF = NBFAFR + NBARFR
      IF( MNBARY .GT. 0 ) CALL TNMCDS( 'REEL',   NBAF, MNBARY )
      IF( MNNUFA .GT. 0 ) CALL TNMCDS( 'ENTIER', NBAF, MNNUFA )
      CALL TNMCDC( 'REEL',   NBAF, MNBARY )
      CALL TNMCDC( 'ENTIER', NBAF, MNNUFA )
C
C     ------------------------------------------------------------
C     OPTIONS DE VISEE POUR VOIR LA SOLUTION SUR LES SURFACES
C     ------------------------------------------------------------
 10   CALL VISE3D( NMTCL )
      IF( NMTCL .LT. 0 ) GOTO 9000
C
C     INITIALISATION DE L'ORBITE ZOOM DEPLACEMENT
      IF( LORBITE .NE. 0 ) THEN
         CALL ORBITE0( NOTYEV )
         IF( NOTYEV .EQ. 0 ) GOTO 10
      ENDIF
C
C     FORMATION DES TABLEAUX NUMERO DES FACES ET DISTANCE AXONOMETRIQUE
C     POUR LES FACES FRONTALIERES DE L'OBJET
 50   CALL TRBFFR( MOFACE, MXFACE, MCN(MNLFAC), RMCN(MNPOGE+WYZPOI),
     %             L1FAFR, 0, NBFAFR,
     %             MCN(MNNUFA), RMCN(MNBARY) )
C
C     FORMATION DES TABLEAUX NUMERO DES FACES ET DISTANCE AXONOMETRIQUE
C     POUR LES ARETES FRONTALIERES DE L'OBJET
      CALL TRBARF( MOARFR, MXARFR, L1ARFR, MCN(MNLARE),
     %             MCN(MNPOGE+WBCOOP), RMCN(MNPOGE+WYZPOI),
     %             NBFAFR, MCN(MNNUFA), RMCN(MNBARY))
C
C     LE TRI PAR TAS DE CETTE DISTANCE
C     LA FACE OU ARETE LA PLUS PROCHE EST LA PREMIERE
      CALL TRITRP( NBAF, RMCN(MNBARY), MCN(MNNUFA) )
C
C     LES DIFFERENTS CAS
      DO NCAS = NCAS0, NCAS1
C
C        L'ECRAN PIXELS EST EFFACE (PAS DE SCINTILLEMENT)
         CALL EFFACEMEMPX
C
C        TRACE DES AXES 3D
         CALL TRAXE3
C
C        TRACE DES ARETES FRONTALIERES ET DE LA SOLUTION SUR
C        LES FACES FRONTALIERES
         CALL TRSOSF3( NBARFR, NBFAFR, MCN(MNNUFA),
     %                 MOFACE, MXFACE, MCN(MNLFAC),
     %                 MOARFR, MXARFR, MCN(MNLARE),
     %                 NBCOOR, RMCN(MNPOGE+WYZPOI),
     %                 NBCOMP, NCAS0,  NCAS1,
     %                 NTYP,   SOLUTION, dptemp, NCAS,
     %                 SOLMIN, SOLMAX )
C
C        EFFACEMENT DE LA LEGENDE DU TRACE POSTSCRIPT
         CALL EFLEGPOS
C
C        LE TRACE DU TITRE FINAL
         KNOM = 'OBJET: ' // KNOMOB
         I    = NUDCNB( KNOM )
         CALL XVCOULEUR( NCNOIR )
         CALL XVTEXTE( KNOM(1:I), I, 50, 30 )
C
C        LE TRACE DE LA LEGENDE : COULEURS => VALEURS
         NBCOUL = NDCOUL - N1COUL
         NCPAS  = NBCOUL / 10
         TPAS   = (SOLMAX-SOLMIN) / 10
         T      = SOLMIN
C
C        TRACE DE 11 VALEURS
         NCOUL = N1COUL
         NX    = LAPXFE - 160
         NY    = LHPXFE - 30
         DO 60 I=0,10
            CALL XVCOULEUR( NCOUL )
            CALL XVRECTANGLE( NX, NY, 30, 10 )
            WRITE( KNOM(1:10), '(G10.3)' ) T
            CALL XVTEXTE( KNOM(1:10), 10, NX+40, NY+10 )
            NCOUL = NCOUL + NCPAS
            T     = T  + TPAS
            NY    = NY - 15
 60      CONTINUE
C
C        RETOUR AU TRACE NORMAL POUR POSTSCRIPT
         IF ( LASOPS .NE. 0 ) THEN
            LASOPS = LASOPS - 10
            CALL XVPOSTSCRIPT(LASOPS)
         ENDIF
C
C        RETOUR AUX PARAMETRES INITIAUX
         CALL XVEPAISSEUR( 1 )
         CALL XVTYPETRAIT( LIGCON )
C
C        DEFINITION DU TITRE ET FIN DU TRACE
         TEMPS = TIMES( NCAS )
         CALL LETITR( NOPROJ, MODECO, NCAS, TEMPS, KNOM )
C
C        ATTENDRE POUR LIRE LE TRACE
         CALL ATTENDSEC( TEMP2TRAC )
C
C        FIN DE LA BOUCLE SUR LES CAS
      ENDDO
C
C     RETOUR POUR UNE NOUVELLE VISEE
C     ------------------------------
      IF( LORBITE .NE. 0 ) THEN
         IF( NCAS0 .EQ. NCAS1 ) THEN
C           ORBITE BOUTON ENFONCE et DEPLACE
            CALL ORBITE1( NOTYEV )
         ELSE
C           ORBITE BOUTON ENFONCE et DEPLACE et RELACHE
            CALL ORBITE3( NOTYEV )
         ENDIF
         IF( NOTYEV .EQ. 0 ) GOTO 10
         GOTO 50
      ELSE
C        POUR LIRE LE TRACE AVANT D'AFFICHER UN MENU
         CALL CLICSO
      ENDIF
      GOTO 10
C
C     SORTIE DU TRACE DES ARETES ET FACES
C     ===================================
 9000 IF( MNBARY .GT. 0 ) CALL TNMCDS( 'REEL',   NBAF, MNBARY )
      IF( MNNUFA .GT. 0 ) CALL TNMCDS( 'ENTIER', NBAF, MNNUFA )
      RETURN
      END
