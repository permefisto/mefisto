C        ASSEMBLAGE DES SOURCES PONCTUELLES
         CALL ASFONO( NTDL, 1, NBFNFX,MCN(MNNFNX),MCN(MNVFNX),RELMIN,
     %                MCN(MNFG1) )
C
      ENDIF
