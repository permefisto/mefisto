C     ##                                                          ##
C     ##  LA BOUCLE EN TEMPS AVEC DES PAS DE TEMPS CONSTANTS = DT ##
C     ##                                                          ##
C     ##############################################################
C
C     MNFG0  CONTIENT  F(t0) SECOND MEMBRE A L'INSTANT INITIAL
C     MNUG0  CONTIENT  U0    TEMPERATURE   A L'INSTANT INITIAL
