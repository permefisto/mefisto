      SUBROUTINE VERIFVP( NTLXOB, NDIM,   NBNOMA,   MNTOPO,
     %                    NBTYEL, MNNPEF, NDPGST,
     %                    MNTPOB, MXPOBA, MNTAUX,
     %                    MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                    MNTHER, MNTAEL, MNX,
     %                    NORESO, MNMUKG, NCODSM, MG, NCODSK, KG,
     %                    IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : VERIFIER QUE LE VECTEUR KG Vp - Vap MG Vp a DE PETITES COMPOSANTES
C ----- CALCULER tVp KG Vp et tVp MG Vp de CHAQUE VECTEUR PROPRE
C
C ENTREES:
C --------
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 1 ou 2 ou 3 ou 6
C          (SI AXISYMETRIE NDIM=2 X => R>=0 et Y=>Z et Z=0)
C NBNOMA : NOMBRE DE NOEUDS DU MAILLAGE DE L'OBJET ou
C          NOMBRE TOTAL DE DEGRES DE LIBERTE DU MAILLAGE DE L'OBJET
C MNTOPO : ADRESSE MCN DU TABLEAU TOPOLOGIE DE L'OBJET
C NBTYEL : NOMBRE DE TYPES D'EF DE L'OBJET
C MNNPEF : ADRESSE MCN DU TMC DES ADRESSES MCN DES TABLEAUX NPEF"TYPE EF
C NDPGST : CODE DE STOCKAGE DES SOMMETS POINTS NOEUDS
C MNTPOB : ADRESSE MCN DES TABLEAUX POLYNOMES DE BASE DES TYPES D'EF
C MNTAUX : ADRESSE MCN DES TABLEAUX AUXILAIRES
C MNXYZP : ADRESSE MCN DE TMS XYZSOMMET DE L'OBJET
C NUMIOB : NUMERO MINIMAL DES OBJETS
C NUMAOB : NUMERO MAXIMAL DES OBJETS
C MNDOEL : ADRESSE MCN DES TMS DES DONNEES THERMIQUES DE L'OBJET
C MNTHER : ADRESSE MCN DU TABLEAU DES DONNEES THERMIQUES INTERNES
C          AU MOINS NPIMAX(=128) REELS DOUBLE PRECISION
C MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
C MNX    : ADRESSE MCN DES NBCOOR COORDONNEES DES POINTS DE L'EF COURANT
C
C NORESO : 1 STOCKAGE PROFIL DES MATRICES
C MNMUKG : ADRESSE DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG PROFIL
C NCODSM : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CAPACITE
C          1 ICI CAR MATRICE SYMETRIQUE
C MG     : MATRICE PROFIL
C NCODSK : CODE DE STOCKAGE DE LA MATRICE GLOBALE DE CAPACITE
C          1 ICI CAR MATRICE SYMETRIQUE
C KG     : MATRICE PROFIL
C
C SORTIE :
C --------
C IERR   : 0 SI PAS D'ERREUR, 1 SI EF NON PROGRAMME, 2 SI EF DEGENERE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET TEXAS A&M UNIVERSITY at QATAR    Fevrier 2011
C MODIFS : ALAIN PERRONNET LJLL UPMC Paris  St Pierre du Perray Mai 2014
C23456---------------------------------------------------------------012
      include"./incl/a___vecteur.inc"
      include"./incl/ctemps.inc"
      include"./incl/cnonlin.inc"
      include"./incl/cthet.inc"
      include"./incl/langue.inc"
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
C
      DOUBLE PRECISION  PENALI
      PARAMETER        (PENALI=0D0)
      DOUBLE PRECISION  D2PI, MINDIF, MAXDIF, MOYDIF, tVMGV, tVKGV
      DOUBLE PRECISION  VaPk, PROSCD, DIF
C     TABLEAU NON UTILISE (VITESSE D'UN FLUIDE INEXISTANT ICI)
      DOUBLE PRECISION  VITEGt(5,3)
C
      D2PI = ATAN(1D0) * 8D0
C
C     LE NOMBRE DE MOTS D'UNE VARIABLE REEL2
      MOREE2 = MOTVAR(6)
C
C     OUVERTURE DU TMS 'VECTEUR"VALEURPROPRE'
      CALL LXTSOU( NTLXOB, 'VECTEUR"VALEURPROPRE', NTVVPR, MNVVPR )
C
C     LE NOMBRE DE VALEURS PROPRES
      NBVECT = MCN(MNVVPR+WBVECT)
C     LE NOMBRE DE COMPOSANTES D'UN VECTEUR PROPRE
      NBNOMA = MCN(MNVVPR+WBCOVE)
C
C     ADRESSE MCN DU PREMIER VECTEUR PROPRE CALCULE
      MNVEC1 = MNVVPR + WECTEU
C
C     ADRESSE MCN AVANT LES VALEURS PROPRES
      MNTIME = MNVVPR + WECTEU + NBNOMA * NBVECT * MOREE2 - 1
C
C     DECLARATIONS DE 2 VECTEURS AUXILIAIRES
      MNKGVk = 0
      CALL TNMCDC( 'REEL2', 2*NBNOMA, MNKGVk )
      MNMGVk = MNKGVk + MOREE2 * NBNOMA
C
C     CALCUL DU PROFIL DES MATRICES ICI SYMETRIQUES NON DIAGONALES
      CALL PRPRMC( MNTOPO, MCN(MNNPEF), MNXYZP, 1, NCODSK,
     %             MCN(MNMUKG), IERR )
      NBRDKG = MCN( MNMUKG + NBNOMA )
C
C     AFFICHAGE
      WRITE(IMPRIM,*)
      WRITE(IMPRIM,10000)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10001)
      ELSE
         WRITE(IMPRIM,20001)
      ENDIF
10001 FORMAT('PRECISION DES VALEURS ET VECTEURS PROPRES ',
     % ' CALCUL de |KG VecteurP - ValeurP MG VecteurP|(Node)')
20001 FORMAT('ACCURACY of EIGENVALUES AND VECTORS',
     % ' Computation of |KG VectorE - ValueE MG VectorE|(Node)')

      WRITE(IMPRIM,10000)
10000 FORMAT(100('='))
C
C     INITIALISATIONS POUR LA BOUCLE DES VECTEURS PROPRES
      NBJEUX = 1
      IEMG   = 1
      IEKG   = 1
      IEBG   = 0
      MNBG   = 0
      NORESO = 1
      MNLPCO = 0
ccc      MODIFS = 0

C     ADRESSE DU VECTEUR PROPRE A TRAITER
      MNVECK = MNVEC1
C
C     BOUCLE SUR LES VECTEURS PROPRES
C     ===============================
      DO K = 1, NBVECT
C
C        VALEUR PROPRE K
         VaPk = RMCN( MNTIME + K )

         WRITE(IMPRIM,*)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*) 'VALEUR PROPRE',K,': ', VaPk
         ELSE
            WRITE(IMPRIM,*) 'EIGENVALUE',K,': ', VaPk
         ENDIF
C
C        CALCUL DES 2 MATRICES KG(Alfa,Beta VP**2) et MG(Masse)
         MNTHET = MNVECK
         CALL THEMKB( NBJEUX, IEMG,   IEKG,   IEBG,  PENALI,
     %                D2PI,   NDIM,   NBNOMA, VITEGt,
     %                NBTYEL, MNNPEF, NDPGST,
     %                MNTPOB, MXPOBA, MNTAUX,
     %                MNXYZP, NUMIOB, NUMAOB, MNDOEL,
     %                MNTHER, MNTAEL, MNX,    MNNODL,
     %                NORESO, MNMUKG, MNLPCO,
     %                NBRDKG, MG,     NBRDKG, KG,    MNBG,
     %                NCODSM, NCODSK, NBPTAF, IERR )
         IF( IERR .NE. 0 ) GOTO 9999
C
C        CALCUL DE [KG(Alfa,Beta VPk**2)] {VPk}
         CALL MAPRVE( 0,1D0, NBNOMA, NCODSK,MCN(MNMUKG),KG, MCN(MNVECK),
     %                MCN(MNKGVk) )
C        tVk KG Vk
         tVKGV = PROSCD( MCN(MNVECK), MCN(MNKGVk), NBNOMA )

C        CALCUL DE [MG] {VPk}
         CALL MAPRVE( 0,1D0, NBNOMA, NCODSK,MCN(MNMUKG),MG, MCN(MNVECK),
     %                MCN(MNMGVk) )

C        tVk MG Vk
         tVMGV = PROSCD( MCN(MNVECK), MCN(MNMGVk), NBNOMA )
C
C        VECTEUR DIFFERENCE [KG(VPk**2)] {VPk} - VaPk [MG] {VPk}
         CALL CL2VED( NBNOMA,  1D0, MCN(MNKGVk),
     %                       -VaPk, MCN(MNMGVk),  MCN(MNKGVk) )
C
C        NORMES DU VECTEUR DIFFERENCE ENTRE LES 2 VECTEURS = KG Vp - Va MG Vp
         MNDIF  = ( MNKGVk - 1 ) / MOREE2
         MINDIF = 1D100
         MAXDIF =-1D100
         MOYDIF = 0D0
         DO I = 1, NBNOMA
            DIF    = DMCN( MNDIF + I )
            MOYDIF = MOYDIF + DIF
            IF( DIF .LT. MINDIF ) MINDIF=DIF
            IF( DIF .GT. MAXDIF ) MAXDIF=DIF
         ENDDO
C
C        AFFICHAGES
         WRITE(IMPRIM,*)'tVp KG Vp =',  tVKGV,
     %           '       tVp MG Vp  =', tVMGV
         WRITE(IMPRIM,*)'MINIMUM Coef( KG Vp -',VaPk,' MG Vp )=',MINDIF
         WRITE(IMPRIM,*)'MAXIMUM Coef( KG Vp -',VaPk,' MG Vp )=',MAXDIF
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*)'MOYENNE Coef( KG Vp -',VaPk,' MG Vp )=',
     %                      MOYDIF/NBNOMA
         ELSE
            WRITE(IMPRIM,*)'MEAN    Coef( KG Vp -',VaPk,' MG Vp )=',
     %                      MOYDIF/NBNOMA
         ENDIF

         IF( ABS(VaPk-tVKGV) .GT. ABS(VaPk*1D-4) ) THEN

C           LA VALEUR PROPRE K est RECTIFIEE avec tVKGV/tVMGV
ccc            MODIFS = MODIFS + 1
ccc            RMCN( MNTIME + K ) = REAL( tVKGV / tVMGV )

            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,19000) K, VaPk, tVKGV / tVMGV
            ELSE
               WRITE(IMPRIM,29000) K, VaPk, tVKGV / tVMGV
            ENDIF

         ENDIF
19000    FORMAT('ATTENTION: La VALEUR PROPRE',i5,'=',G15.7,
     %  ' DIFFERENTE de tV KG V/tV MG V=',G15.7)
29000    FORMAT('ATTENTION: The EIGENALUE',i5,'=',G15.7,
     %  ' IS DIFFERENT of tV KG V/tV MG V=',G15.7)
C
C        PASSAGE AU VECTEUR PROPRE SUIVANT
         MNVECK = MNVECK + MOREE2 * NBNOMA
C
      ENDDO
C
ccc      IF( MODIFS .GT. 0 ) THEN
cccC        TRI CROISSANT DES VALEURS PROPRES ET VECTEURS ASSOCIES
ccc         CALL TRIVVP( NBVECT, MCN(MNTIME+1), NBNOMA, MCN(MNVEC1) )
ccc      ENDIF
C
C     DESTRUCTION DES TABLEAUX INUTILES
 9999 CALL TNMCDS( 'REEL2', 2*NBNOMA, MNKGVk )
C
      RETURN
      END
