      SUBROUTINE TREEAR( NCAS,   NDSM,   MOARET, MXARET, LARETE,
     %                   NBPTAF, SFLUAF, CPNFAF, CMPFLU )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES ESTIMATEURS D'ERREUR DES EF ET LES SAUTS DU
C -----    FLUX NORMAL DE TEMPERATURE SUR LES ARETES (OBJET 2D)
C
C ENTREES :
C ---------
C NCAS   : NUMERO DU JEU DE TEMPERATURE A TRAITER
C NDSM   : NOMBRE TOTAL DE CAS TRAITES
C MOARET : NOMBRE DE MOTS STOCKES POUR CHAQUE ARETE DU TABLEAU LARETE
C MXARET : NOMBRE MAXIMAL D'ARETES DECLARABLES DANS LARETE
C LARETE : LE TABLEAU DES ARETES SELON LE HACHAGE
C NBPTAF : NOMBRE DE POINTS PAR ARETE OU LE FLUX EST CALCULE
C ERTHAF : ERREUR L2 A POSTERIORI SUR CHAQUE ARETE DES EF
C SFLUAF : LE FLUX DES 2 COTES DE CHAQUE ARETE DU MAILLAGE
C CPNFAF : 3 COORDONNEES DES POINTS D'INTEGRATION ET NORMALES SUR LES ARETES
C CMPFLU : NOMBRE DE CM POUR L'UNITE DE FLUX
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS         MAI 1995
C23456---------------------------------------------------------------012
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/ponoel.inc"
C
      DOUBLE PRECISION  SFLUAF(1:2,1:NBPTAF,1:MXARET,1:NDSM)
      INTEGER           LARETE(1:MOARET,1:MXARET)
      REAL              CPNFAF(1:3,1:NBPTAF,1:MXARET,1:2)
      REAL              CXYZCM(3)
      EQUIVALENCE      (CXYZCM(1),CONXCM), (CXYZCM(2),CONYCM),
     %                 (CXYZCM(3),CONZCM)
C
C     TRACE DES SAUTS DU FLUX NORMAL AUX POINTS D'INTEGRATION DES ARETES
C     ------------------------------------------------------------------
      DO 10 N=1,MXARET
         IF( LARETE(1,N) .GT. 0 ) THEN
            DO 5 L=1,NBPTAF
C              LA DIRECTION DU VECTEUR
C              L'INTENSITE DU FLUX NORMAL EN CE POINT RAMENE EN CM
               FL = REAL((SFLUAF(1,L,N,NCAS)-SFLUAF(2,L,N,NCAS))*CMPFLU)
C              LONGUEUR CM DE LA FLECHE DANS LA DIRECTION NORMALE
               CONXCM = CPNFAF( 1, L, N, 2 ) * FL
               CONYCM = CPNFAF( 2, L, N, 2 ) * FL
               CONCM  = SQRT( CONXCM * CONXCM + CONYCM * CONYCM )
C              LE TRACE DE LA FLECHE DU SAUT DU FLUX NORMAL
               CALL T2FLEC( NCOUFL, CPNFAF(1,L,N,1), CPNFAF(2,L,N,1),
     %                      CONCM,  CONXCM, CONYCM )
 5          CONTINUE
         ENDIF
 10   CONTINUE
C
      RETURN
      END
