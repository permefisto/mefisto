      SUBROUTINE REALFA( XYZD, NOOBSF, NUMISU, NUMASU, LTDESU, ALFA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RETROUVER ALFA COEFFICIENT DU LAPLACIEN DANS UN PROBLEME NLSE
C ----- SUPPOSE DONNE COMME LA CONDUCTIVITE HOMOGENE ISOTROPE DU MATERIAU
C
C ENTREES:
C --------
C XYZD   : 3 COORDONNEES REELLES DOUBLE PRECISION DU POINT OU CALCULER ALFA
C NOOBSF : NUMERO DE L'OBJET SURFACE DE CET ELEMENT FINI
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES CONDUCTIVITE
C          DES OBJETS SURFACES
C SORTIE :
C --------
C ALFA   : COEFFICIENT DU LAPLACIEN DANS UN PROBLEME NLSE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Texas A & M University at QATAR  Fevrier 2011
C23456---------------------------------------------------------------012
      include"./incl/donthe.inc"
      INTEGER           LTDESU(1:MXDOTH, NUMISU:NUMASU)
      DOUBLE PRECISION  XYZD(3), ALFA, CONDUC(6)
C
C     RECHERCHE DE LA CONDUCTIVITE AU POINT XYZD
      CALL RECOND( 3, NOOBSF, 3, XYZD,
     %             LTDESU(LPCOND,NOOBSF), CONDUC )
C
C     ALFA EST LA CONDUCTIVITE ISOTROPE HOMOGENE
      ALFA = CONDUC(1)
C
      RETURN
      END
