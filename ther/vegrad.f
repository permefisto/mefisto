      SUBROUTINE VEGRAD( NCAS,   CMPGRA,
     %                   NEF,    NBELFI, NBPIEF, NDIMES, NBJECA,
     %                   COPIEF, GRATEM,
     %                   NBFLEC, MXFLEC, XYZDFL, CMMXFL,
     %                   NBAREF, MXAREF, NOAREF, NOPTEF, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LISTER LES VECTEURS GRADIENTS DE L'EF 3D NEF
C -----
C
C ENTREES:
C --------
C NCAS   : NUMERO DU CAS A TRAITER
C NBCAS  : NOMBRE DE CAS TRAITES
C CMPGRA : NOMBRE DE CM POUR L'UNITE DE GRADIENT
C NBPIEF : NOMBRE DE POINTS DE CALCUL DES GRADIENTS POUR 1 EF DE CE TYPE
C NEF    : NUMERO DE L'EF 3D
C NUTYEF : NUMERO DU TYPE DE L'EF de 1 A NBELFI
C COPIEF : LES NDIMES COORDONNEES DES POINTS DE CALCUL DES GRADIENTS
C GRATEM : LES GRADIENTS DE TEMPERATURE AUX POINTS D'INTEGRATION
C
C SORTIES:
C --------
C NBFLEC : NOMBRE DE FLECHES GRADIENTS
C MXFLEC : NOMBRE MAXIMAL DE FLECHES GRADIENTS DECLARABLES DANS XYZDFL
C XYZDFL : 1:3 XYZ DU POINT DE DEPART DE LA FLECHE
C          4:6 XYZ DE LA DIRECTION DE LA FLECHE
C          7   DIMENSION EN CM DU TRACE
C              SON SIGNE DETERMINE LE SENS DU TRIANGLE DE LA FLECHE
C CMMXFL : CM DE LA FLECHE MAXIMALE
C NBAREF : NOMBRE D'ARETES DES EF SECTIONNES
C MXAREF : MAXIMUM D4ARETES DES EF SECTIONNES A DECLARER DANS NOAREF
C NOAREF : NO DES 2 SOMMETS DES ARETES DES EF SECTIONNES
C NOPTEF : NO DES POINTS DE L'EF
C IERR   : 1 SI TABLEAU XYZDFL SATURE, 0 SINON
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire JL LIONS UPMC Paris Decembre 2006
C....................................................................012
      include"./incl/ponoel.inc"
      REAL              COPIEF(1:NBELFI,1:NBPIEF,1:NDIMES)
      DOUBLE PRECISION  GRATEM(1:NBELFI,1:NDIMES,1:NBPIEF,1:NBJECA)
      REAL              XYZDFL( 7, MXFLEC )
      INTEGER           NOAREF(2,MXAREF), NOPTEF(64)
C
      IERR   = 0
      CMMXFL = 0
C
C     LISTAGE DES VECTEURS GRADIENTS
      DO 10 L=1,NBPIEF
C
C        CONSTRUCTION DU VECTEUR GRADIENT
         IF( NBFLEC .GE. MXFLEC ) THEN
            IERR = 1
            RETURN
         ENDIF
         NBFLEC = NBFLEC + 1
C
C        XYZ DU DEBUT DE LA FLECHE
         XYZDFL(1,NBFLEC) = COPIEF(NEF,L,1)
         XYZDFL(2,NBFLEC) = COPIEF(NEF,L,2)
         XYZDFL(3,NBFLEC) = COPIEF(NEF,L,3)
C
C        LONGUEUR CM DU CORPS DE LA FLECHE
         CONXCM = REAL( GRATEM(NEF,1,L,NCAS) * CMPGRA )
         CONYCM = REAL( GRATEM(NEF,2,L,NCAS) * CMPGRA )
         CONZCM = REAL( GRATEM(NEF,3,L,NCAS) * CMPGRA )
         XYZDFL(7,NBFLEC) = SQRT( CONXCM**2 + CONYCM**2 + CONZCM**2 )
         CMMXFL = MAX( CMMXFL, XYZDFL(7,NBFLEC) )
C
C        DIRECTION DE LA FLECHE
         XYZDFL(4,NBFLEC) = CONXCM
         XYZDFL(5,NBFLEC) = CONYCM
         XYZDFL(6,NBFLEC) = CONZCM
 10   CONTINUE
C
C     AJOUT DES ARETES DE L'EF SECTIONNE
      IF( NBAREF+NARET .GT. MXAREF ) THEN
         IERR = 2
         RETURN
      ENDIF
      DO 20 K=1,NARET
         NBAREF = NBAREF + 1
C        LE NO DES 2 SOMMETS DE L'ARETE K DE L'EF
         NOAREF(1,NBAREF) = NOPTEF( NOSOAR(1,K) )
         NOAREF(2,NBAREF) = NOPTEF( NOSOAR(2,K) )
 20   CONTINUE
C
      RETURN
      END
