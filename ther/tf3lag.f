      SUBROUTINE TF3LAG( X,      NDSM,   NOEF,   NUELEM, NBELEM,
     %                   NTDL,   NUNDEL, NBPIFN,
     %                   NBPOTR, NPITR,  POIDTR, POLYTR, DPOLTR,
     %                   NBPOQU, NPIQU,  POIDQU, POLYQU, DPOLQU,
     %                   NBFACE, NOOBSF, NUMISU, NUMASU, NBPOLY,
     %                   NOOBVO, NUMIVO, NUMAVO, LTDEVO,
     %                   IDPFAC, DPFACE, TEMP,   FLUXPT,
     %                   COPNFN, FLUXNP, FLUXTO )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CALCUL DU FLUX NORMAL DE CHALEUR A CHAQUE FACE DE L'EF 3D
C -----     LAGRANGE ISOPARAMETRIQUE
C
C ENTREES :
C ---------
C X      : COORDONNEES X Y Z DES NBPOLY POINTS DE L'EF VOLUMIQUE
C NDSM   : NOMBRE DE SECONDS MEMBRES
C NOEF   : NUMERO DU TYPE DE L'EF
C NUELEM : NUMERO DE L'ELEMENT FINI PARMI CEUX DE CE TYPE(DANS NPEF"TYPE)
C NBELEM : NOMBRE D'ELEMENTS FINIS DE CE TYPE
C NTDL   : NOMBRE TOTAL DE DL DE TEMPERATURE POUR TOUT LE MAILLAGE
C NUNDEL : NUMERO DES NOEUDS DES ELEMENTS FINIS
C
C NBPIFN : NOMBRE TOTAL DE POINTS SUR LES FACES DE L'EF OU SONT CALCULES
C          LES FLUX NORMAUX DE CHALEUR
C( TETRA=>NPITR*NBFACE OU HEXA=>NPIQU*NBFACE OU PENTA=>NPITR*2 + NPIQU*3 )
C NBPOTR : NOMBRE DE POLYNOMES DE BASE POUR UNE FACE TRIANGULAIRE DE L'EF
C NPITR  : NOMBRE DE POINTS D INTEGRATION POUR UNE FACE TRIANGULAIRE
C POIDTR : POIDS DES POINTS D INTEGRATION  POUR UNE FACE TRIANGULAIRE
C POLYTR : VALEUR DES POLYNOMES DE BASE AUX POINTS D INTEGRATION
C          D'UNE FACE TRIANGULAIRE
C DPOLTR : VALEUR DU GRADIENT DE CES MEMES POLYNOMES AUX POINTS ...
C
C NBPOQU : NOMBRE DE POLYNOMES DE BASE POUR UNE FACE QUADRANGULAIRE
C NPIQU  : NOMBRE DE POINTS D INTEGRATION POUR UNE FACE QUADRANGULAIRE
C POIDQU : POIDS DES POINTS D INTEGRATION  POUR UNE FACE QUADRANGULAIRE
C POLYQU : VALEUR DES POLYNOMES DE BASE AUX POINTS D INTEGRATION
C          D'UNE FACE QUADRANGULAIRE
C DPOLQU : VALEUR DU GRADIENT DE CES MEMES POLYNOMES AUX POINTS ...
C
C NBFACE : NOMBRE DES FACES DE L'EF
C NOOBSF : NUMERO DE LA SURFACE DE CHAQUE FACE DE L'EF
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C
C NBPOLY : NOMBRE DE POLYNOMES = NOMBRE DE NOEUDS DE L'EF
C NOOBVO : NUMERO DE L'OBJET VOLUME DE CET EF
C NUMIVO : NUMERO MINIMAL DES OBJETS VOLUMES
C NUMAVO : NUMERO MAXIMAL DES OBJETS VOLUMES
C LTDEVO : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES CONDUCTIVITE
C          DES OBJETS VOLUMES
C
C IDPFAC : IDPFAC(K) ADRESSE DANS DPFACE DE LA 1-ERE VARIABLE DE
C          LA FACE K
C DPFACE : DPFACE(I,J,L,K)=DPFACE(I,J,IDPFAC(K)+L)
C          DERIVEE / XI DE LA J-EME FONCTION DE BASE AU L-EME POINT
C          DE LA K-EME ARETE DE L EF DE REFERENCE SURFACE
C TEMP   : TEMPERATURE EN TOUS LES NOEUDS DU MAILLAGE
C FLUXPT : TABLEAU AUXILIAIRE DE NBPOLY * (MAX(NPITR,NPIQU) REELS DOUBLE
C
C SORTIES:
C --------
C COPNFN : 2 COORDONNEES DES NBPIFN POINTS ET DE LA NORMALE SUR LES
C          ARETES DES EF OU SONT CALCULES LES FLUX NORMAUX DE TEMPERATURE
C FLUXNP : FLUX NORMAL EN LES NBPIFN POINTS DES FACES DES EF DE CE TYPE
C FLUXTO : FLUX NORMAL SOMME SUR CHAQUE SURFACE DE L'OBJET
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     OCTOBRE 1994
C23456---------------------------------------------------------------012
      include"./incl/donthe.inc"
      include"./incl/ponoel.inc"
      include"./incl/cthet.inc"
      include"./incl/cnonlin.inc"
C
      DOUBLE PRECISION  POIDTR(NPITR),
     %                  POLYTR(NBPOTR,NPITR),
     %                  DPOLTR(2,NBPOTR,NPITR)
      DOUBLE PRECISION  POIDQU(NPIQU),
     %                  POLYQU(NBPOQU,NPIQU),
     %                  DPOLQU(2,NBPOQU,NPIQU)
      REAL              X(NBPOLY,3),
     %                  COPNFN( 3, NBPIFN, NBELEM, 2 )
      INTEGER           LTDEVO( 1:MXDOTH , NUMIVO:NUMAVO )
      INTEGER           NONOFK(9),
     %                  NOOBSF(1:6)
      DOUBLE PRECISION  DREM(20),
     %                  FLUXPT(NBPOLY,NPIQU),
     %                  TEMP(NTDL,NDSM),
     %                  COND(6)
      DOUBLE PRECISION  FLUXTO( NUMISU:NUMASU , 1:NDSM ),
     %                  FLUXNP( NBPIFN, NBELEM, NDSM )
      DOUBLE PRECISION  DPFACE(*)
      INTEGER           NUNDEL( NBELEM, NBPOLY )
      INTEGER           IDPFAC( NBFACE )
C
C     CONTRIBUTION DES FACES
C     ======================
      NUPTF0 = 0
      DO 100 K=1,NBFACE
C
C        LE NUMERO EVENTUEL DE LA SURFACE DE CETTE FACE
         NS = NOOBSF(K)
C
C        LA FACE EST SUR UNE SURFACE UTILISATEUR
C        RECHERCHE DU NUMERO DES POINTS=NOEUDS DE LA FACE K
         CALL ELNOFA( NOEF, K, NBNOFK, NONOFK )
C        NONOFK(I) LES NUMEROS DES NOEUDS DE LA FACE K
C
         IF( NBSOFA(K) .EQ. 3 ) THEN
C
C           FACE TRIANGULAIRE
            NPIF = NPITR
C           LE DEBUT DU TABLEAU DES DERIVEES DES POLYNOMES AUX POINTS
C           D'INTEGRATION NUMERIQUE DE LA FACE K
            MNDPF = IDPFAC( K ) + NBPOLY * NPITR
C           CALCUL DES FLUX NORMAUX AUX POINTS D'INTEGRATION DE LA FACE
            CALL T53LAG( NUELEM, NUNDEL, X,      NBPOLY,
     &                   NS,     NUMISU, NUMASU, NBNOFK, NONOFK,
     &                   POLYTR, DPOLTR, NPITR,  POIDTR,
     &                   NOOBVO, NUMIVO, NUMAVO, LTDEVO,
     &                   COND, DPFACE(MNDPF), NTDL, TEMP, FLUXPT, DREM,
     &                   NBPIFN, NBELEM, NDSM, NUPTF0, COPNFN,
     &                   FLUXNP, FLUXTO )
C
         ELSE
C
C           FACE QUADRANGULAIRE
            NPIF = NPIQU
C           LE DEBUT DU TABLEAU DES DERIVEES DES POLYNOMES AUX POINTS
C           D'INTEGRATION NUMERIQUE DE LA FACE K
            MNDPF = IDPFAC( K ) + NBPOLY * NPIQU
C           CALCUL DES FLUX NORMAUX AUX POINTS D'INTEGRATION DE LA FACE
            CALL T53LAG( NUELEM, NUNDEL, X,      NBPOLY,
     &                   NS,     NUMISU, NUMASU, NBNOFK, NONOFK,
     &                   POLYQU, DPOLQU, NPIQU,  POIDQU,
     &                   NOOBVO, NUMIVO, NUMAVO, LTDEVO,
     &                   COND, DPFACE(MNDPF), NTDL, TEMP, FLUXPT, DREM,
     &                   NBPIFN, NBELEM, NDSM, NUPTF0, COPNFN,
     &                   FLUXNP, FLUXTO )
         ENDIF
C
C        MISE A JOUR DU NUMERO DU DERNIER POINT OU EST CALCULE LE FLUX NORMAL
         NUPTF0 = NUPTF0 + NPIF
 100  CONTINUE
      END
