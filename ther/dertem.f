      SUBROUTINE DERTEM( NBELEM, NUELEM, NDSM,   NDIM,  NBPOLY, NPI, DP,
     &                   NTDL,   NUNDEL, TEMPER, GRATEM )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  CALCULER LE GRADIENT DE LA TEMPERATURE AUX POINTS D INTEGRATION
C -----  NUMERIQUE DE L ELEMENT FINI NUELEM DU TABLEAU NPEF"TYPE_EF
C
C ENTREES:
C --------
C NBELEM : NOMBRE D'ELEMENTS FINIS DE CE TYPE
C NUELEM : NUMERO DE L'ELEMENT FINI
C NDSM   : NOMBRE DE CAS OU SOLUTION EN CHAQUE D.L.
C NDIM   : NOMBRE DE COORDONNEES DE CHAQUE POINT
C NBPOLY : NOMBRE DE D.L. DE L ELEMENT
C NPI    : NOMBRE DE POINTS D INTEGRATION SUR LE VOLUME
C DP     : DP(I,J,K) DERIVEE / X(I) DU J-EME POLYNOME AU K-EME POINT
C          D INTEGRATION
C NTDL   : NOMBRE TOTAL DE DEGRES DE LIBERTE OU NOEUDS DE TEMPERATURE
C NUNDEL : NUMEROS DES NBPOLY NOEUDS DES ELEMENTS FINIS
C TEMPER : TEMPER(I,J) I-EME SOLUTION AU J-EME D.L. DE L ELEMENT
C
C SORTIES:
C --------
C GRATEM : GRATEM(NUELEM,J,L,N) DERIVEE / X(J) DU N-EME CAS
C          AU L-EME POINT D'INTEGRATION NUMERIQUE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     OCTOBRE 1994
C23456---------------------------------------------------------------012
      DOUBLE PRECISION  DP(NDIM,NBPOLY,NPI),
     &                  TEMPER(NTDL,NDSM),
     &                  GRATEM(NBELEM,NDIM,NPI,NDSM), S
      INTEGER           NUNDEL(1:NBELEM,1:NBPOLY)
C
      DO 40 L=1,NPI
         DO 30 J=1,NDIM
            DO 20 N=1,NDSM
               S = 0D0
               DO 10 I=1,NBPOLY
                  S = S + DP(J,I,L) * TEMPER( NUNDEL(NUELEM,I) , N )
   10          CONTINUE
C              GRATEM(NUELEM,J,L,N) DERIVEE / X(J) DU N-EME CAS
C              AU L-EME POINT D'INTEGRATION NUMERIQUE
               GRATEM( NUELEM, J, L, N ) = S
   20       CONTINUE
   30    CONTINUE
   40 CONTINUE
C
      RETURN
      END
