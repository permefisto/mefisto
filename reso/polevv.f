      SUBROUTINE POLEVV( NBGAME, NBROWS,  NBCOEFA, PDIAGCA, COLUMN, A,
     %                   NBEIGV, TARGETR, TARGETI, RESTRICT,
C    %                   PARAMETERPJD,
     %                   MXEIGV, NBEIGVC, EIGVALR, EIGVALI,
     %                   EIGVECR, EIGVECI, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  COMPUTATION OF EIGENVALUES Lambda and EIGENVECTORS DEFINED BY
C -----  A POLYNOMIAL P(Lambda)=0 with the OPERATOR -DIV( A GRAD . )
C        FOR LAGRANGE FINITE ELEMENTS of DEGREE 1 or 2 in 1D or 2D or 3D
C
C INPUT :
C -------
C NBGAME : NUMBER OF INPUT DATA GAMES
C NBROWS : NUMBER OF ROWS OF THE MATRIX A
C NBCOEFA: = PDIAGCA(NBROWS) NUMBER OF NOT ZERO COEFFICIENTS OF A
C PDIAGCA: POINTEUR ON EVERY DIAGONAL COEFFICIENT OF A
C           PDIAGCA(0)=0, PDIAGCA(I)=COEFFICIENT NUMBER OF A(I,I)
C COLUMN : NUMBER OF THE COLUMN
C A      : A(NBCOEFA,NBGAME) COEFFICIENTS OF THE NBGAME MATRICES
C NBEIGV : WISHED NUMBER OF COMPUTED EIGENVALUES
C TARGETR: EIGENVALUE TARGET REAL PART
C TARGETI: EIGENVALUE TARGET IMAGINARY PART
C RESTRICT: LOCATION OFEIGENVALUES FROM THE TARGET
C          -1 at LEFT, 0 CENTERED, 1 at RIGHT
C
C PARAMETERPJD: PARAMETERS TO ADD EVENTUALLY
C
C OUTPUT :
C --------
C NBEIGVC: NUMBER OF EIGENVALUES COMPUTED
C EIGVALR: REAL      PART OF THE NBEIGVC COMPUTED EIGENVALUES
C EIGVALI: IMAGINARY PART OF THE NBEIGVC COMPUTED EIGENVALUES
C EIGVECR: REAL      PART OF THE EIGENVECTOR COMPONENTS
C EIGVECI: IMAGINARY PART OF THE EIGENVECTOR COMPONENTS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEURS: SHENG-HONG LAI  TIMS NTU TAIPEI TAIWAN          DECEMBRE 2009
C          ZIH-HAO WEI     TIMS NTU TAIPEI TAIWAN          DECEMBRE 2009
C          WEICHUNG WANG   TIMS NTU TAIPEI TAIWAN          DECEMBRE 2009
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
C
C     INPUT DATA:
      INTEGER           NBGAME, NBROWS, NBCOEFA
      INTEGER           PDIAGCA(0:NBROWS), COLUMN(NBCOEFA)
      DOUBLE PRECISION  A(1:NBCOEFA,1:NBGAME)
      INTEGER           NBEIGV,  RESTRICT
      DOUBLE PRECISION  TARGETR, TARGETI
C
C     OUTPUT DATA:
      INTEGER           NBEIGVC
      DOUBLE PRECISION  EIGVALR(MXEIGV), EIGVALI(MXEIGV)
      DOUBLE PRECISION  EIGVECR(NBROWS,MXEIGV), EIGVECI(NBROWS,MXEIGV)
C
C     COMPUTATIONS
      NBEIGVC = 1
      EIGVALR(1) = 1D0
      EIGVALI(1) = 0D0
      EIGVECR(1,1) = 1D0
      EIGVECI(1,1) = 0D0
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*)
         WRITE(IMPRIM,*)'Subroutine polevv.f est en construction'
         WRITE(IMPRIM,*)'Installation du logiciel PETSc est necessaire'
       WRITE(IMPRIM,*)'Actuellement, polevv.f ne donne pas de resultats'
      ELSE
         WRITE(IMPRIM,*)
         WRITE(IMPRIM,*)'Subroutine polevv.f is constructing'
         WRITE(IMPRIM,*)'Installation of PETSc is NECESSARY'
         WRITE(IMPRIM,*)'Today, polevv.f returns NO RESULTS'
      ENDIF
C
      print *, pdiagca(0), column(1), A(1,1), nbeigv,  restrict,
     %         targetr, targeti
      IERR = 1
      RETURN
      END
