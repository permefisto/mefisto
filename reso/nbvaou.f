      SUBROUTINE NBVAOU( RC,     NTDL, NBVAPR, VALPR,
     %                   NCODSK, MUKG, KG,     MOTSRG, RG,
     %                   NCODSM, MUMG, MG,
     %                   NBHC,   DETC, NBAVRC, NBVPOU )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE NOMBRE DE VALEURS PROPRES OUBLIEES AVANT RC
C -----    C-A-D CALCULER LE NOMBRE DE COEFFICIENTS DIAGONAUX NEGATIFS
C          DE LA MATRICE FACTORISEE  A - RC B = L D tL
C
C ENTREES:
C --------
C RC     : REEL AU DESSOUS DUQUEL LES VALEURS PROPRES SONT A COMPTER
C          -RC EST AUSSI LA TRANSLATION  KG - RC MG => KG
C NTDL   : NOMBRE DE DEGRES DE LIBERTE
C NBVAPR : NOMBRE DE VALEURS PROPRES CALCULEES
C VALPR  : NBVAPR VALEURS PROPRES
C NCODSK : CODE DE STOCKAGE DE LA MATRICE PROFIL KG
C MUKG   : TABLEAU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C KG     : MATRICE PROFIL DE RAIDEUR
C MOTSRG : NOMBRE DE MOTS DE LA MATRICE PROFIL RG
C RG     : SAUVEGARDE DE LA MATRICE KG AVANT FACTORISATION L D tL
C NCODSM : CODE DE STOCKAGE DE LA MATRICE PROFIL MG
C MUMG   : TABLEAU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C MG     : MATRICE PROFIL DE MASSE
C
C SORTIES:
C --------
C NBHC   : NOMBRE D'HOMOTHETIES DE SCALE DU DETERMINANT DE KG - RC MG
C DETC   : DETERMINANT DE (KG - RC MG) * SCALE ** NBHC
C NBAVRC : NOMBRE DE VALEURS PROPRES DE KG - RC MG INFERIEURES A RC
C NBVPOU : NOMBRE DE VALEURS PROPRES OUBLIEES DANS VALPR AVANT RC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET UPMC ANALYSE NUMERIQUE PARIS     DECEMBRE 1998
C2345X7..............................................................012
      PARAMETER (NENTRE=1)
      include"./incl/epsvvp.inc"
      include"./incl/langue.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      DOUBLE PRECISION  RC, VALPR(NBVAPR), KG(*), RG(*), MG(*), DETC
C
C     RESTAURATION DE LA MATRICE A A PARTIR DE RG
 10   CALL TRTATA( RG, KG, MOTSRG )
C
C     KG - RC MG => KG
      CALL MUA2PD( NTDL, 1.D0, NCODSK, MUKG, KG,
     %                    -RC, NCODSM, MUMG, MG,
     %                                 MUKG, KG )
C
C     KG - RC MG = L D tL DANS KG
      CALL CRMC1D( MUKG, KG, NTDL, EPS, NENTRE, KG, NRETOU )
      IF(NRETOU.NE.0) THEN
         RC = RC * ( 1D0 + FAST )
         GOTO 10
      ENDIF
C
C     DETERMINANT DE LA MATRICE KG - RC MG
      CALL DETETD( KG, MUKG, NTDL, NBAVRC, NBHC, DETC )
C
C     NOMBRE NB DE VALPR(I) < RC
      NBVPOU = 0
      DO 20 I=1,NBVAPR
C
C        DEFLATION DU POLYNOME CARACTERISTIQUE
         DETC = DETC / (RC-VALPR(I))
C
C        NOMBRE DE VALEURS PROPRES INFERIEURES A RC
         IF( VALPR(I) .LT. RC ) NBVPOU = NBVPOU + 1
C
 20   CONTINUE
C
C     NOMBRE DE VALEURS PROPRES OUBLIEES AVANT RC
      NBVPOU  = NBAVRC - NBVPOU
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10020) NBVPOU, RC
      ELSE
         WRITE(IMPRIM,20020) NBVPOU, RC
      ENDIF
10020 FORMAT('Il y a',I5,' VALEURS PROPRES OUBLIEES AVANT',G15.7)
20020 FORMAT('There are',I5,' FORGOTTEN EIGENVALUES before',G15.7)
      RETURN
      END
