      SUBROUTINE COPIEL( NBELEM,NBPIEF,NDIMES,NUELEM,
     %                   COPTEF,COPIEF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : RANGER LES COORDONNEES DES POINTS D'INTEGRATION NUMERIQUE
C ----- DE L'ELEMENT NUELEM
C
C ENTREES:
C --------
C NBELEM : NOMBRE D'ELEMENTS
C NBPIEF : NOMBRE DE POINTS D'INTEGRATION NUMERIQUE DE CE TYPE D'ELEMENT
C NDIMES : DIMENSION DE L'ESPACE ICI 2
C NUELEM : NUMERO DE L'ELEMENT TRAITE
C COPTEF : LES COORDONNEES DES POINTS D'INTEGRATION NUMERIQUE
C          DE L'ELEMENT
C
C MODIFIES:
C ---------
C COPIEF : LES COORDONNEES DES POINTS D'INTEGRATION NUMERIQUE
C          DES ELEMENTS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS  OCTOBRE 1989
C23456---------------------------------------------------------------012
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      REAL              COPTEF(NDIMES,NBPIEF),
     %                  COPIEF(NBELEM,NBPIEF,NDIMES)
C
C     LE TRANSFERT
      DO 20 L=1,NBPIEF
         DO 10 I=1,NDIMES
            COPIEF(NUELEM,L,I) = COPTEF(I,L)
 10      CONTINUE
 20   CONTINUE
      END
