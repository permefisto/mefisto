      SUBROUTINE CALPNT( NTDL,   NIVEAU, NCODSA, MNLPLI, MNLPCO,
     %                   MNLPLC, MNLPCC, MNLPLU, COMP,   IERR  )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CE SP CALCULE LES POINTEURS DE LA MATRICE DE
C ----     PRECONDITIONNEMENT ASSOCIEE A LA FACTORISATION
C          INCOMPLETE DE A SUIVANT LA NOTION DE NIVEAU
C
C ENTREES:
C --------
C NTDL   : LE RANG DU SYSTEME
C NIVEAU : PARAMETRE DE FACTORISATION INCOMPLETE
C NCODSA : MODE DE STOCKAGE DE LA MATRICE
C          1 SI SYMETRIQUE NON DIAGONALE
C         -1 SI NON SYMETRIQUE
C MNLPLI : ADRESSE MCN DU POINTEUR LPDIAG (AG)
C MNLPCO : ADRESSE MCN DU POINTEUR LPCOLO (AG)
C
C SORTIES:
C --------
C MNLPLC : ADRESSE MCN DU POINTEUR LPDIAG (AGC)
C MNLPCC : ADRESSE MCN DU POINTEUR LPCOLO (AGC)
C MNLPLU : ADRESSE MCN DU POINTEUR LPDILU (AGC)
C          0 SI SYMETRIQUE, MAIS NECESSAIRE SI MATRICE NON SYMETRIQUE
C COMP   : LOGIQUE INDICATEUR DE FACTORISATION COMPLETE
C IERR   : CODE D'ERREUR >0 SI ERREUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PASCAL JOLY ANALYSE NUMERIQUE UPMC PARIS         OCTOBRE 1989
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      LOGICAL           COMP
C
C     ADRESSAGE DES TABLEAUX
C     ----------------------
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,1000) NIVEAU
      ELSE
         WRITE (IMPRIM,1001) NIVEAU
      ENDIF
      COMP = .FALSE.
C
C     LES POINTEURS DE LA MATRICE DE CONDITIONNEMENT
      MNLPLC = 0
      CALL TNMCDC( 'ENTIER', NTDL+1, MNLPLC )
      MNLPLU = 0
      CALL TNMCDC( 'ENTIER', NTDL, MNLPLU )
      CALL TNMCMX( 'ENTIER', MAXVAR )
      LONGLP = MAXVAR / 2 - NTDL * 2 - 1
      MNLPCC = 0
      CALL TNMCDC( 'ENTIER', LONGLP, MNLPCC )
C
C     LES TABLEAUX AUXILIAIRES
      LAUX   =  NTDL + 1 + LONGLP + NTDL
      IAUX   = 0
      CALL TNMCDC( 'ENTIER', LAUX, IAUX )
      IA1 = IAUX
      IA2 = IA1 + NTDL + 1
      IA3 = IA2 + LONGLP
C
C     INITIALISATION DES POINTEURS
C     ----------------------------
C     CALL AFMOR1( NTDL, MCN(MNLPLI), MCN(MNLPCO) )
      CALL CALPN1( NTDL, NCODSA, MCN(MNLPLI), MCN(MNLPCO),
     &             MCN(MNLPLC), MCN(MNLPCC), MCN(IA1) )
      CALL CALPN2( NTDL, MCN(MNLPLC), MCN(MNLPCC), MCN(MNLPLU) )
C
C     BOUCLE SUR LE NIVEAU
C     --------------------
      DO 1 NIV=1, NIVEAU
         LOLPC0 = MCN(MNLPLC+NTDL)
         CALL CALPN3( NTDL, MCN(MNLPLC), MCN(MNLPCC),
     &        MCN(MNLPLU), MCN(IA1), MCN(IA2), MCN(IA3), LONGLP, IERR)
         IF (IERR.NE.0) GO TO 90
         CALL CALPN2(NTDL, MCN(MNLPLC), MCN(MNLPCC), MCN(MNLPLU))
         LOLPC1 = MCN(MNLPLC+NTDL)
         IF (LOLPC1.EQ.LOLPC0) THEN
            NIVO = NIV - 1
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,2000) NIVO
            ELSE
               WRITE(IMPRIM,2001) NIVO
            ENDIF
            COMP = .TRUE.
            GO TO 2
         ENDIF
 1    CONTINUE
 2    IF (NCODSA.GE.0) THEN
         CALL CALPN4( NTDL, MCN(MNLPLC), MCN(MNLPCC), MCN(MNLPLU) )
      ENDIF
C     CALL AFMOR1( NTDL, MCN(MNLPLC), MCN(MNLPCC) )
C
C     IMPRESSIONS
C     -----------
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,3000) MCN(MNLPLI+NTDL), MCN(MNLPLC+NTDL)
      ELSE
         WRITE(IMPRIM,3001) MCN(MNLPLI+NTDL), MCN(MNLPLC+NTDL)
      ENDIF
C
C     MISE A JOUR DE LA MEMOIRE
C     -------------------------
C     LES TABLEAUX AUXILIAIRES
 90   CALL TNMCDS( 'ENTIER', LAUX, IAUX )
C     MISE A JOUR DE LA LONGUEUR DE LPCOLO
      LOLPCO = MCN(MNLPLC+NTDL)
      CALL TNMCRA( 'ENTIER', LONGLP, LOLPCO, MNLPCC )
C     DESTRUCTION DU POINTEUR LPDILU DANS LE CAS SYMETRIQUE
      IF( NCODSA .GE. 0 ) THEN
         CALL TNMCDS( 'ENTIER', NTDL, MNLPLU )
         MNLPLU = 0
      ENDIF
      RETURN
 1000 FORMAT(' FACTORISATION INCOMPLETE',/,' NIVEAU',T40,I12)
 1001 FORMAT(' INCOMPLETE FACTORIZATION',/,' LEVEL',T40,I12)
 2000 FORMAT(' FACTORISATION COMPLETE au NIVEAU',T40,I12)
 2001 FORMAT(' COMPLETE FACTORIZATION at LEVEL',T40,I12)
 3000 FORMAT(' NOMBRE DE COEFFICIENTS NON NULS DE AG',T40,I12/,
     &       ' NOMBRE DE COEFFICIENTS NON NULS DE AGC',T40,I12)
 3001 FORMAT(' NUMBER of NO NULL COEFFICIENTS of AG',T40,I12/,
     &       ' NUMBER of NO NULL COEFFICIENTS of AGC',T40,I12)
      END
