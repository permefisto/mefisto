      SUBROUTINE SECINV( NTLXOB,
     +                   NTDL,   NBDLFX, MNVDLX, NBDLIB, MNDLIB,
     +                   NCODSK, MNMUKG, MNKG,
     +                   NCODSM, MNMUMG, MNMG,
     +                   TETA,   MORAID, MNRAID,
     +                   NTVVPR, MNVVPR, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES VALEURS EIGV ET VECTEURS PROPRES VP TELS QUE
C -----    ( K + EIGV M ) VP = 0   ET   tM VP M = Id
C          PAR LA METHODE DU DETERMINANT AVEC CALCUL DES ZEROS DU
C          POLYNOME CARACTERISTIQUE PAR LA METHODE DE LA SECANTE
C
C ENTREES:
C --------
C NTLXOB : NUMERO DU TMS LEXIQUE DE L'OBJET
C NTDL   : NOMBRE DE LIGNES ET COLONNES DES MATRICES K ET M EN ENTREE
C          NOMBRE TOTAL DE DL BLOQUES+LIBRES
C NBDLFX : NOMBRE DE DEGRES DE LIBERTE BLOQUES
C MNVDLX : ADRESSE MCN DES VALEURS DES NODLFX DEGRES FIXES
C NBDLIB : NOMBRE DE DEGRES DE LIBERTE LIBRES
C MNDLIB : MCN(MNDLIB-1+I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NTDL) LIBRE
C                          -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          0  SI PAS DE DL BLOQUES
C
C NCODSK : CODE DE STOCKAGE DE LA MATRICE PROFIL K
C          =0 SI DIAGONALE
C          >0 SI PROFIL SYMETRIQUE
c          <0 SI PROFIL NON SYMETRIQUE
C MNMUK  : ADRESSE MCN DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE K
C MNKG   : ADRESSE MCN DE LA MATRICE PROFIL REELLE DOUBLE PRECISION
C
C NCODSM : CODE DE STOCKAGE DE LA MATRICE PROFIL M
C          =0 SI DIAGONALE
C          >0 SI PROFIL SYMETRIQUE
C          <0 SI PROFIL NON SYMETRIQUE
C MNMUM  : ADRESSE MCN DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE M
C MNMG   : ACRESSE MCN DE LA MATRICE PROFIL REELLE DOUBLE PRECISION
C TETA   : COEFFICIENT DE TRANSLATION TETA TEL QUE K <-- K + TETA M
C MORAID : NOMBRE DE MOTS DE LA MATRICE MNKG
C MNRAID : ADRESSE MCN DE LA SAUVEGARDE DE LA MATRICE MNKG
C
C SORTIES:
C --------
C NTVVPR : NUMERO      DU TMS DES VALEURS ET VECTEURS PROPRES
C MNVVPR : ADRESSE MCN DU TMS DES VALEURS ET VECTEURS PROPRES
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET      ANALYSE NUMERIQUE UPMC PARIS    AOUT 1998
C23456---------------------------------------------------------------012
      include"./incl/a___vecteur.inc"
      include"./incl/epsvvp.inc"
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
C
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      DOUBLE PRECISION  EIGMAX,TETA,DTETA,DET1
      INTRINSIC         REAL
C
C     SEUILS INDEPENDANTS DE LA METHODE CHOISIE
C     -----------------------------------------
C     SCALE BASE POUR L EVALUATION DU DETERMINANT POUR LES HOMOTHETIES
      SCALE = 1.D70
C     FAST FACTEUR PAR LEQUEL ON TRANSLATE LEGEREMENT MU POUR PRESERVER LA FACTO
      FAST  = 5.E-5
C     SEUIL DES FACTORISATIONS CORRECTES L D tL DES MATRICES PROFIL
      EPS   = 1.E-12
C
C     LE NOMBRE DE MOTS D'UNE VARIABLE REEL2
      MOREE2 = MOTVAR(6)
C
C     ENTREE DU NOMBRE DE MODES PROPRES A CALCULER
C     --------------------------------------------
C     NBROOT: > 0 CALCUL DES NBROOT PLUS PETITES VALEURS ET VECTEURS PROPRES
C             = 0 CALCUL DES VALEURS PROPRES POSITIVES INFERIEURES A D
C                 EN SORTIE NBROOT NOMBRE EFFECTIVEMENT CALCULE
C     EIGMAX: BORNE DES VALEURS PROPRES A DONNER LORSQUE NBROOT = 0
      CALL INVITE( 68 )
      NCVALS = 0
      CALL LIRENT( NCVALS, NBROOT )
      IF( NCVALS .LT. 0 ) THEN
C        ABANDON DE LA LECTURE DES DONNEES
         IERR = 2
         RETURN
      ENDIF
C
      IF( NBROOT .GT. 0 ) THEN
         WRITE(IMPRIM,12000) NBROOT
12000    FORMAT(/' NOMBRE DE MODES PROPRES A CALCULER ',T37,'=',I6)
      ELSE
C        ENTREE DE LA BORNE SUPERIEURE DES VALEURS PROPRES A CALCULER
C        ------------------------------------------------------------
         CALL INVITE( 7 )
         NCVALS = 0
         CALL LIRRDP( NCVALS, EIGMAX )
         IF( NCVALS .LT. 0 ) THEN
C           ABANDON DE LA LECTURE DES DONNEES
            IERR = 2
            RETURN
         ENDIF
         NBROOT = 0
         WRITE(IMPRIM,12010) EIGMAX
12010    FORMAT(/' BORNE SUPERIEURE DES VALEURS PROPRES A CALCULER =',
     %   G15.7)
      ENDIF
C
C     SAUVEGARDE DE LA MATRICE DE MASSE SUR UN FICHIER
C     SCRATCH SEQUENTIEL TEMPORAIRE A CREER et OUVRIR
      CALL TRUNIT( NFPCMG )
      OPEN( UNIT=NFPCMG, ERR=9999, STATUS='SCRATCH',
     %      ACCESS='SEQUENTIAL', FORM='UNFORMATTED')
C     2 MOTS POUR LES REELS DOUBLE PRECISION SUR ADRESSE PAIRE
      WRITE(NFPCMG) MORAID,NCVALS,(MCN(MNMG-1+I),I=1,MORAID)
C
      IF ( NBROOT .LE. 0 ) THEN
C
C        RECHERCHE DU NOMBRE NBROOT DE VALEURS PROPRES INFERIEURES A EIGMAX
C        ------------------------------------------------------------------
         ISC   = 1000
         DTETA = EIGMAX
C
 2100    DTETA = DTETA*1.01D0
C        COMBINAISON LINEAIRE DES 2 MATRICES PROFILS K=K-DTETA M
         CALL MUA2PD( NBDLIB,   1D0, NCODSK, MCN(MNMUKG), MCN(MNKG),
     &                       -DTETA, NCODSM, MCN(MNMUMG), MCN(MNMG),
     &                                       MCN(MNMUKG), MCN(MNKG) )
C
C        FACTORISATION DE GAUSS SYMETRIQUE
         CALL CRMC1D( MCN(MNMUKG),MCN(MNKG),NBDLIB,EPS,0,
     &                MCN(MNKG),NRETOU )
         IF( NRETOU .EQ. 0 ) THEN
C           CALCUL DU DETERMINANT DE LA MATRICE
            CALL DETETD( MCN(MNKG),  MCN(MNMUKG), NBDLIB,
     &                   NBROOT, ISC, DET1 )
         ELSE
            CALL TRTATA( MCN(MNRAID), MCN(MNKG), MORAID )
            GOTO 2100
         ENDIF
C
         WRITE (IMPRIM,170) NBROOT,DTETA
         IF ( NBROOT .LE. 0 ) THEN
            WRITE (IMPRIM,180) DTETA
            RETURN
         ENDIF
      ENDIF
  170 FORMAT(' IL Y A ',I6,' VALEURS PROPRES ENTRE 0. ET ',G15.7/)
  180 FORMAT(' ATTENTION ELAVVP: 0 VALEUR PROPRE EN DESSOUS DE',G15.7/)
C
C     MXROOT: MAJORANT DU NOMBRE DE VALEURS PROPRES >NBROOT A CAUSE DES
C             VALEURS MULTIPLES OU DES VALEURS SUPERIEURES A CELLES CHERCHEES
      MXROOT = MIN( 2*NBROOT, NBROOT+8, NBDLIB/2 )
C
C     CREATION DES TABLEAUX DE TRAVAIL ET DE RESULTAT
C     -----------------------------------------------
C     LES VECTEURS PROPRES ET AUXILIAIRES
      MNV  = 0
      CALL TNMCDC( 'REEL2', NBDLIB * ( 4 + MXROOT*2 ), MNV )
      MNV0 = MNV  + MOREE2 * NBDLIB
      MNV1 = MNV0 + MOREE2 * NBDLIB
      MNW  = MNV1 + MOREE2 * NBDLIB
      MNWW = MNW  + MOREE2 * NBDLIB
      MNVV = MNWW + MOREE2 * NBDLIB * MXROOT
C
C     LES VALEURS PROPRES ET BORNES D'ERREUR
      MNEIGV = 0
      CALL TNMCDC( 'REEL2', MXROOT, MNEIGV )
C
C     LE NOMBRE D'ITERATIONS POUR CHAQUE VALEUR PROPRE
      MNNITE = 0
      CALL TNMCDC( 'ENTIER', 2 * MXROOT, MNNITE )
      MNNINV = MNNITE + MXROOT
C
C     METHODE DU DETERMINANT AVEC RECHERCHE DES ZEROS PAR
C     LA METHODE DE LA SECANTE ET ITERATIONS INVERSES
C     ---------------------------------------------------
      CALL SECINW( NBROOT, MXROOT, NBDLIB, TETA,
     +             MCN(MNMUKG), MCN(MNKG), NCODSK,
     +             MCN(MNMUMG), MNMG, MCN(MNMG), NCODSM,
     +             MCN(MNV0), MCN(MNV1), MCN(MNV), MCN(MNW),
     +             MCN(MNVV), MCN(MNWW), MCN(MNEIGV),
     +             MCN(MNNITE), MCN(MNNINV),
     +             MORAID, MCN(MNRAID), NFPCMG,
     +             NVALPO,  IERR )
      CLOSE( NFPCMG )
C
C     ==============================================
C     ADRESSAGE INITIALISATION DU VECTEUR"MODEPROPRE
C     ==============================================
      CALL LXTSOU( NTLXOB, 'VECTEUR"MODEPROPRE', NTVVPR, MNVVPR )
      IF( NTVVPR .GT. 0 ) THEN
C        LE VECTEUR EST DETRUIT POUR ETRE REDECLARE
         CALL LXTSDS( NTLXOB, 'VECTEUR"MODEPROPRE' )
      ENDIF
      L =  WECTEU + NBROOT * NTDL * MOREE2 + NBROOT
      CALL LXTNDC( NTLXOB, 'VECTEUR"MODEPROPRE', 'MOTS', L )
      CALL LXTSOU( NTLXOB, 'VECTEUR"MODEPROPRE', NTVVPR, MNVVPR )
C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR PROPRE
      MCN( MNVVPR + WBCOVE ) = NTDL
C     NOMBRE DE VECTEURS
      MCN( MNVVPR + WBVECT ) = NBROOT
C     NOMBRE DE VALEURS PROPRES REELLE SIMPLE PRECISION
      MCN( MNVVPR + WBCPIN ) = NBROOT
C
C     COPIE DES NC MODES PROPRES A PARTIR DU TABLEAU VV
C     ET EN AJOUTANT LES VALEURS DES DL FIXES
      CALL CPSIVV( NBROOT, NTDL, NBDLIB, MCN(MNDLIB),
     +             NBDLFX, MCN(MNVDLX),
     +             MCN(MNVV), MCN(MNVVPR+WECTEU) )
C
C     COPIE DES MXROOT VALEURS PROPRES EN SIMPLE PRECISION
      MNR = MNVVPR + WECTEU + MOREE2*NTDL*NBROOT
      MND = (MNEIGV+1) / MOREE2
      DO 799 I=1,NBROOT
C        TRANSLATION INVERSE DES VALEURS PROPRES
         RMCN(MNR) = REAL( DMCN(MND) - TETA )
         MNR = MNR + 1
         MND = MND + 1
 799  CONTINUE
C     LA DATE
      CALL ECDATE( MCN(MNVVPR) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNVVPR + MOREE2 ) = NONMTD( '~>>>VECTEUR' )
C
C     DESTRUCTION DES TABLEAUX MC INUTILES
      CALL TNMCDS( 'REEL2', NBDLIB * ( 4 + MXROOT*2 ), MNV )
      CALL TNMCDS( 'REEL2',    MXROOT, MNEIGV )
      CALL TNMCDS( 'ENTIER', 2*MXROOT, MNNITE )
      RETURN
C
 9999 WRITE(IMPRIM,*) 'AUCUNE UNITE OUVRABLE POUR LE FICHIER DE MG'
      IERR = 10
      RETURN
      END
