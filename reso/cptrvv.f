      SUBROUTINE CPTRVV( NBVECTP, NC,     NTDL,   NBDLIB, NODLIB,
     +                   NBDLFX,  NODLFX, VADLFX,
     +                   VPSS,    VECTPR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRANSPOSER LES NBVECTP PREMIERS VECTEURS PROPRES VPSS(NC,NBDLIB)
C -----    DANS LE TABLEAU VECTPR(NTDL,NBVECTP) ET AJOUTER LES DL FIXES
C
C ENTREES:
C --------
C NBVECTP: NOMBRE DE VECTEURS PROPRES A RETENIR
C NC     : NOMBRE DE VECTEURS PROPRES
C NTDL   : NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR VECTPR
C NBDLIB : NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR VPSS
C NODLIB : NODLIB(I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NTDL) LIBRE
C                    -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          PAS DE TABLEAU SI NBDLIB=NTDL
C NBDLFX : NOMBRE DE DEGRES DE LIBERTE BLOQUES
C NODLFX : NUMEROS DE 1 A NTDL DES NBDLFX DEGRES FIXES
C VADLFX : TABLEAU DES VALEURS DES DEGRES DE LIBERTE FIXES
C VPSS   : TABLEAU(NC,NBDLIB) LES COMPOSANTES DES VECTEURS PROPRES
C          AVANT AJOUT DES DEGRES DE LIBERTE BLOQUES
C
C SORTIES:
C --------
C VECTPR : LES COMPOSANTES DES VECTEURS PROPRES
C          APRES AJOUT DES DEGRES DE LIBERTE BLOQUES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET      ANALYSE NUMERIQUE UPMC PARIS    AOUT 1998
C23456---------------------------------------------------------------012
      DOUBLE PRECISION  VADLFX(NBDLFX), VPSS(NC,NBDLIB),
     +                  VECTPR(NTDL,NBVECTP)
      INTEGER           NODLFX(NBDLFX), NODLIB(NTDL)
C
      IF( NBDLFX . EQ. 0 ) THEN
C
C        PAS DE DEGRE DE LIBERTE FIXE : TRANSPOSITION SIMPLE
         DO 20 J=1,NBDLIB
            DO 10 I=1,NBVECTP
               VECTPR( J, I ) = VPSS( I, J )
 10         CONTINUE
 20      CONTINUE
C
      ELSE
C
C        EXISTENCE DE DEGRES DE LIBERTE FIXES
C        EXPANSION DES NBDLIB DL EN NTDL DL
         DO 50 J=1,NBDLIB
C
C           RECHERCHE DANS NODLIB DU DL J
            DO 30 N=1,NTDL
               IF( NODLIB(N) .EQ. J ) GOTO 35
 30         CONTINUE
C
C           LE DL J DANS VPSS EST LE DL N DANS VECTPR
 35         DO 40 I=1,NBVECTP
               VECTPR( N, I ) = VPSS( I, J )
 40         CONTINUE
 50      CONTINUE
C
C        AJOUT DES VALEURS DES DL FIXES
         DO 70 J=1,NBDLFX
C           LE NUMERO DANS 1 A NTDL DU DL FIXE J
            N = NODLFX(J)
            DO 60 I=1,NBVECTP
               VECTPR( N, I ) = VADLFX( J )
 60         CONTINUE
 70      CONTINUE
C
      ENDIF
      END
