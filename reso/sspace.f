      SUBROUTINE SSPACE( NOMPB,  NTLXOB, NTDL,  INIDONN, EIGMIN, EIGMAX,
     %                   NBDLFX, MNDLFX, MNVDLX, NBDLIB, MNDLIB,
     %                   NCODSK, MNMUKG, KG,
     %                   NCODSM, MNMUMG, MG, COMXMG,
     %                   MOTSRG, RG,
     %                   NTVVPR, MNVVPR, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES VALEURS VALP ET VECTEURS PROPRES VP TELS QUE
C -----    ( K + VALP M ) VP = 0   ET   tM VP M = Id
C          PAR LA METHODE DES ITERATIONS PAR SOUS-ESPACES
C
C ENTREES:
C --------
C NOMPB  : NOM EN 4 CARACTERES DE LA CLASSE DU PROBLEME TRAITE
C          'THER' ou 'ELAS' ou 'FLUI'
C NTLXOB : NUMERO DU TMS LEXIQUE DE L'OBJET
C NTDL   : NOMBRE DE LIGNES ET COLONNES DES MATRICES KG ET MG EN ENTREE
C          NOMBRE TOTAL DE DL BLOQUES+LIBRES
C
C INIDONN: =0 DONNEES A DEFINIR PAR LECTURE DES DONNEES DE L'UTILISATEUR
C          =1 EIGMIN et EIGMAX SONT DEJA INITIALISEES
C EIGMIN : BORNE INFERIEURE DES VALEURS PROPRES A CALCULER
C EIGMAX : BORNE SUPERIEURE DES VALEURS PROPRES A CALCULER
C
C NBDLFX : NOMBRE DE DEGRES DE LIBERTE BLOQUES
C MNDLFX : ADRESSE MCN DES NUMEROS DE 1 A NTDL DES NODLFX DEGRES FIXES
C          DE LIBERTE BLOQUES
C MNVDLX : ADRESSE MCN DES VALEURS DES NODLFX DEGRES FIXES
C NBDLIB : NOMBRE DE DEGRES DE LIBERTE LIBRES
C MNDLIB : MCN(MNDLIB-1+I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NTDL) LIBRE
C                          -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          0  SI PAS DE DL BLOQUES
C
C NCODSK : CODE DE STOCKAGE DE LA MATRICE PROFIL KG
C          =0 SI DIAGONALE
C          >0 SI PROFIL SYMETRIQUE
c          <0 SI PROFIL NON SYMETRIQUE
C MNMUK  : ADRESSE MCN DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE KG
C KG     : MATRICE PROFIL REELLE DOUBLE PRECISION
C
C NCODSM : CODE DE STOCKAGE DE LA MATRICE PROFIL MG
C          =0 SI DIAGONALE
C          >0 SI PROFIL SYMETRIQUE
C          <0 SI PROFIL NON SYMETRIQUE
C MNMUM  : ADRESSE MCN DU POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE MG
C MG     : MATRICE PROFIL REELLE DOUBLE PRECISION
C COMXMG : MAXIMUM ACTUEL DES COEFFICIENTS DE LA MATRICE MG
C
C MOTSRG : NOMBRE DE MOTS DE LA MATRICE RG (EN DOUBLE PRECISION)
C RG     : MATRICE DE SAUVEGARDE DE LA MATRICE KG
C
C SORTIES:
C --------
C NTVVPR : NUMERO      DU TMS DES VALEURS ET VECTEURS PROPRES
C MNVVPR : ADRESSE MCN DU TMS DES VALEURS ET VECTEURS PROPRES
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET      ANALYSE NUMERIQUE UPMC PARIS    AOUT 1998
C MODIF : ALAIN PERRONNET      TEXAS A & M UNIVERSITY       JUILLET 2003
C23456---------------------------------------------------------------012
      PARAMETER (MXREPRI=4)
      include"./incl/a___vecteur.inc"
      include"./incl/epsvvp.inc"
      include"./incl/langue.inc"
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
C
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      INTEGER           NROO(2)
      DOUBLE PRECISION  MG(*), KG(*), RG(*)
      DOUBLE PRECISION  EIG(2), EIGMIN, EIGMAX, EIGMAX0, VALPES,
     %                  TETATO, TRA, DET1, D, COMXMG, S, D2PI
      CHARACTER*(*)     NOMPB
      CHARACTER*24      NOMVEC
      INTRINSIC         REAL
C
C     SEUILS INDEPENDANTS DE LA METHODE CHOISIE
C     -----------------------------------------
C     SCALE BASE POUR L EVALUATION DU DETERMINANT POUR LES HOMOTHETIES
      SCALE = 1.D70
C     PRECIS : SEUIL AU DESSOUS DUQUEL LA NORME D UN VECTEUR EST SUPPOSE NUL
C              LORS D'UNE M-ORTHONORMALISATION AUX VP PRECEDENTS
ccc      PRECIS = 1D-10  3/3/2009
      PRECIS = 1D-8
C     FAST FACTEUR PAR LEQUEL ON TRANSLATE LEGEREMENT MU POUR PRESERVER
C     LA FACTORISATION DE K = L D tL D'UN COEFFICIENT DIAGONAL NUL
      FAST  = 5.E-5
C     SEUIL DES FACTORISATIONS CORRECTES L D tL DES MATRICES PROFIL
      EPS   = 1.E-12
C     LE NOMBRE DE MOTS D'UNE VARIABLE REEL2
      MOREE2  = MOTVAR(6)
C
C     CONSTANTE 2 Pi
      D2PI = ATAN( 1D0 ) * 8D0
C
      MNMANA  = 0
      NBVALP0 = 0
      MNVALP0 = 0
      MNVECP0 = 0
      NBREPRI = 0
C
C     A PRIORI PAS DE REPRISE DES VALEURS ET VECTEURS PROPRES DANS VVPR
      INITX0 = 1
C
      IF( INIDONN .GT. 0 ) GOTO 1
C
C     LECTURE DES BORNES DES VALEURS PROPRES A CALCULER
C     -------------------------------------------------
      CALL INVITE( 6 )
      NCVALS = 0
      CALL LIRRDP( NCVALS, EIGMIN )
      IF( NCVALS .LT. 0 ) THEN
C        ABANDON DE LA LECTURE DES DONNEES
         IERR = 2
         RETURN
      ENDIF
      CALL INVITE( 7 )
      NCVALS = 0
      CALL LIRRDP( NCVALS, EIGMAX )
      IF( NCVALS .LT. 0 ) THEN
C        ABANDON DE LA LECTURE DES DONNEES
         IERR = 2
         RETURN
      ENDIF
C
 1    IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) EIGMIN, EIGMAX
      ELSE
         WRITE(IMPRIM,20000) EIGMIN, EIGMAX
      ENDIF
10000 FORMAT('SSPACE: CALCUL DES VALEURS PROPRES COMPRISES ENTRE',G15.7,
     %' ET ',G15.7)
20000 FORMAT('SSPACE: COMPUTATION of EIGENVALUES from ',G15.7,
     %' to ',G15.7)
C
C     RECHERCHE DU NOMBRE NBROOT DE VALEURS PROPRES COMPRISES
C     DANS L'INTERVALLE FERME [ EIGMIN, EIGMAX ]
C     -------------------------------------------------------
      TRA    = 1.01D0
      EIG(1) = EIGMAX
      EIG(2) = EIGMIN
C
      DO 8 K=1,2
C
         TETATO = EIG(K)
C        TETATO<0 NE POSE PAS DE PROBLEME cf TAMU 7/7/2003
C
 5       IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10005) -TETATO
         ELSE
            WRITE(IMPRIM,20005) -TETATO
         ENDIF
10005    FORMAT(/'FACTORISATION L*D*tL DE K +',G15.7,' M')
20005    FORMAT(/'L*D*tL FACTORIZATION of K +',G15.7,' M')
C
         CALL TRTATA( RG, KG, MOTSRG )
         CALL MUA2PD( NBDLIB,
     %                   1D0, NCODSK, MCN(MNMUKG), KG,
     %               -TETATO, NCODSM, MCN(MNMUMG), MG,
     %                                MCN(MNMUKG), KG )
         CALL CRMC1D( MCN(MNMUKG), KG, NBDLIB, EPS, 0, KG,  NRETOU )
         IF( NRETOU .NE. 0 ) THEN
            TETATO = TETATO * TRA
            GOTO 5
         ENDIF
         CALL DETETD( KG, MCN(MNMUKG), NBDLIB,  NROO(K), NBHOMO, DET1 )
         IFACTO = 1
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10007) NROO(K),TETATO
         ELSE
            WRITE(IMPRIM,20007) NROO(K),TETATO
         ENDIF
10007    FORMAT(I5,' VALEURS PROPRES AVANT',G15.7)
20007    FORMAT(I5,' EIGENVALUES BEFORE',G15.7)
C
         IF( K .EQ. 1 .AND. NROO(1) .LE. 0 ) THEN
C           PAS DE VALEUR PROPRE AVANT LA PLUS GRANDE VALEUR MAX
            NROO(2) = 0
            NBROOT = 0
            GOTO 180
         ENDIF
C
         EIG(K) = TETATO
         TRA = 0.99D0
 8    CONTINUE
C
      NBROOT = NROO(1) - NROO(2)
      EIGMAX = EIG(1)
      EIGMIN = EIG(2)
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10180) NBROOT,EIGMIN,EIGMAX
      ELSE
         WRITE(IMPRIM,20180) NBROOT,EIGMIN,EIGMAX
      ENDIF
10180 FORMAT(/'Il y a ',I5,' VALEURS PROPRES de ',G15.7,' a ',G15.7)
20180 FORMAT(/'There are ',I5,' EIGENVALUES from ',G15.7,' to ',G15.7)
C
 180  IF( NBROOT .LE. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE (IMPRIM,10190)
         ELSE
            WRITE (IMPRIM,20190)
         ENDIF
         NVALOU = 0
         IERR   = 5
         RETURN
      ENDIF
10190 FORMAT('SOUS-ESPACE: PAS de VALEUR PROPRE a CALCULER ')
20190 FORMAT('SUB-SPACE: NO EIGENVALUE to COMPUTE')
C
10200 FORMAT('Il y a',I6,' VALEURS PROPRES OUBLIEES entre ',
     %        G15.7,' et ',G15.7)
20200 FORMAT('There are',I6,' FORGOTTEN EIGENVALUES from ',G15.7,
     %' until ', G15.7)
C
C     LE NOMBRE DE VALEURS PROPRES AVANT EIGMIN ET EIGMAX
      NBVAMIN = NROO(2)
      NBVAMAX = NROO(1)
      EIGMAX0 = EIGMAX
C
C     NOMBRE DE VALEURS DECLAREES DANS VALP0 ET VECP0
      NCVALP0 = NBVAMAX - NBVAMIN + 5
C
C     RECHERCHE DU TABLEAU DES VALEURS ET VECTEURS PROPRES DE REPRISE
C     ===============================================================
      IF( NOMPB(1:4) .EQ. 'THER' ) THEN
         NOMVEC = 'VECTEUR"VALEURPROPRE'
      ELSE IF( NOMPB(1:4) .EQ. 'ELAS' ) THEN
         NOMVEC = 'VECTEUR"MODEPROPRE'
      ENDIF
      CALL LXTSOU( NTLXOB, NOMVEC, NTVVPR, MNVVPR )
      NBVALP0 = 0
      IF( NTVVPR .LE. 0 ) GOTO 11
C
C     TOUTES LES VALEURS ET VECTEURS PROPRES CALCULES SONT RECUPEREES
C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR PROPRE
      IF( MCN( MNVVPR + WBCOVE ) .NE. NTDL ) GOTO 11
C     NOMBRE DE VECTEURS PROPRES
      NBVALP0 = MCN( MNVVPR + WBVECT )
C     NOMBRE DE VALEURS PROPRES REELLE SIMPLE PRECISION
C     LES VALEURS PROPRES SONT RANGEES DERRIERE LES VECTEURS PROPRES
      IF( NBVALP0 .NE. MCN( MNVVPR + WBCPIN ) ) THEN
         NBVALP0 = 0
         GOTO 11
      ENDIF
C
C     ADRESSE MCN DES NBVALP0 VECTEURS PROPRES
      MNVECP0 = MNVVPR + WECTEU
C     ADRESSE MCN DES NBVALP0 VALEURS PROPRES REELLES SIMPLE PRECISION
      MNVALP0 = MNVECP0 + NBVALP0 * NTDL * MOREE2
C
C     SI PROBLEME EN ELASTICITE => LES FREQUENCES REDEVIENNENT LES VALEURS PROPR
C     --------------------------------------------------------------------------
      IF( NOMPB(1:4) .EQ. 'ELAS' ) THEN
         MN     = MNVALP0
         DO 22 I=1,NBVALP0
C           Hz = sqrt( VP ) / 2Pi  =>  VP = ( Hz x 2Pi )**2
            S = RMCN(MN)
            RMCN(MN) = REAL( ( S * D2PI ) ** 2 )
            MN = MN + 1
 22      CONTINUE
      ENDIF
C
C     NOMBRE DE VALEURS PROPRES DEJA CALCULEES ENTRE EIGMIN ET EIGMAX
      NBV = 0
      DO 25 I=1,NBVALP0
         D = RMCN(MNVALP0-1+I)
         IF( EIGMIN .LE. D .AND. D .LE. EIGMAX ) NBV = NBV + 1
 25   CONTINUE
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'REPRISE avec',NBV,
     %' VALEURS PROPRES retrouvees entre',EIGMIN,' et ',EIGMAX
      ELSE
         WRITE(IMPRIM,*) 'RETAKING with',
     %   NBV,' EIGENVALUES recovered from ',EIGMIN,' to ',EIGMAX
      ENDIF
C
C     DECLARATION DES TABLEAUX
      MNVALP2 = 0
      CALL TNMCDC( 'REEL2', NCVALP0, MNVALP2 )
      MNVECP2 = 0
      CALL TNMCDC( 'REEL2', NCVALP0*NBDLIB, MNVECP2 )
C
C     LES VECTEURS PROPRES VP AVEC NBDLIB COMPOSANTES
C     COMPRESSION DES DEGRES DE LIBERTES BLOQUES
C     DE VALEUR PROPRE COMPRISE ENTRE EIGMIN ET EIGMAX
C     GESTION DE L'ADRESSE DU TABLEAU DLIB SI PAS DE CONDITIONS AUX LIMITES
      IF( MNDLIB .LE. 0 ) THEN
         MNDLB = 1
      ELSE
         MNDLB = MNDLIB
      ENDIF
      CALL CPCLVV( NTDL,    NBDLIB, MCN(MNDLB),
     %             EIGMIN,  EIGMAX,
     %             NBVALP0, MCN(MNVALP0), MCN(MNVECP0),
     %             NCVALP0, NBV, MCN(MNVALP2), MCN(MNVECP2) )
      NBVALP0 = NBV
      MNVALP0 = MNVALP2
      MNVECP0 = MNVECP2
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,19000) NBV,(I,DMCN((MNVALP2-1)/2+I),I=1,NBV)
      ELSE
         WRITE(IMPRIM,29000) NBV,(I,DMCN((MNVALP2-1)/2+I),I=1,NBV)
      ENDIF
19000 FORMAT(' REPRISE avec ',I5,' VALEURS PROPRES INITIALES'/
     %5(I5,' :',G15.7))
29000 FORMAT(' RETAKING with ',I5,' INITIAL EIGENVALUES'/
     %5(I5,' :',G15.7))
C
C     NOMBRE DE VALEURS PROPRES OUBLIEES
      IF( NBROOT - NBVALP0 .LE. 0 ) THEN
C        VVPR CONTIENT DEJA LE RESULTAT => ABANDON
         CALL TNMCDS( 'REEL2', NCVALP0,        MNVALP0 )
         CALL TNMCDS( 'REEL2', NCVALP0*NBDLIB, MNVECP0 )
         RETURN
      ENDIF
C
C     DECLARATION DE VALP0 ET VECP0
 11   IF( NBVALP0 .EQ. 0 ) THEN
         MNVALP0 = 0
         CALL TNMCDC( 'REEL2', NCVALP0, MNVALP0 )
         MNVECP0 = 0
         CALL TNMCDC( 'REEL2', NCVALP0*NBDLIB, MNVECP0 )
      ENDIF
C
      NVALOU = NBROOT - NBVALP0
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) NVALOU,' VALEURS PROPRES a CALCULER'
         WRITE(IMPRIM,*) NCVALP0,' VECTEURS RESERVES et',
     %                   NBVALP0,' VALEURS PROPRES INITIALES'
      ELSE
         WRITE(IMPRIM,*) NVALOU,' EIGENVALUES to COMPUTE'
         WRITE(IMPRIM,*) NCVALP0,' RESERVED VECTORS and',
     %                   NBVALP0,' INITIAL EIGENVALUES'
      ENDIF
C
C     STRATEGIE DES ITERATIONS DE SOUS-ESPACES
C     ----------------------------------------
C     NOSTRA : OPTION DE STRATEGIE DES ITERATIONS DEMANDEE , 0 AUTOMATIQUE
C              > 0 INDIQUE LE NOMBRE MAXIMAL D ITERATIONS
C     NMANA  : NOM DU TABLEAU GERE DANS M QUI DECRIT CHAQUE ITERATION SELON
C              MANA(I, I=1,MOMANA)
      MOMANA = 64
      NOSTRA = 0
      CALL TNMCDC( 'ENTIER', MOMANA, MNMANA )
C
C     PREPARATION DES ITERATIONS EN CAS D'OUBLI DE VALEURS PROPRES
C     ============================================================
      NITER  = 0
C
C     ======================================================================
C     LES ITERATIONS SUR L APPEL DE SSPAC1 EN CAS D'OUBLI DE VALEURS PROPRES
C     ======================================================================
 20   NITER = NITER + 1
C
C     MAJORATION DU NOMBRE DE VALEURS ET VECTEURS PROPRES A CALCULER
C     POUR MINIMISER LES RISQUES D'OUBLIER UNE VALEUR PROPRE
      NC = NVALOU + 5
C
C     LES VALEURS ET VECTEURS PROPRES POUR CHAQUE ITERATION
      MNVALP1 = 0
      CALL TNMCDC( 'REEL2', NC,        MNVALP1 )
      MNVECP1 = 0
      CALL TNMCDC( 'REEL2', NC*NBDLIB, MNVECP1 )
C
C     CREATION DES TABLEAUX DE TRAVAIL ET DE RESULTAT
C     -----------------------------------------------
C     TABLEAUX DE TRAVAIL: V,W,R,DD,VEC,AR,BR,C,E,EE,MUTA
C     DECLARATION A LA TAILLE MAXIMALE NTDL ET NON NBDLIB
      MNV    = 0
      MOV    = MOREE2 * ( NBDLIB*2 + 3*NC*NC + 2*NC )
      CALL TNMCDC( 'REEL2', MOV/MOREE2, MNV )
      MNW    = MNV   + MOREE2 * NBDLIB
      MND    = MNW   + MOREE2 * NBDLIB
      MNVEC  = MND   + MOREE2 * NC
      MNAR   = MNVEC + MOREE2 * NC * NC
      MNBR   = MNAR  + MOREE2 * NC * NC
      MNDAUX = MNBR  + MOREE2 * NC * NC
C
      MNR    = 0
      LAR    = MOREE2 * NC * NTDL
      CALL TNMCDC( 'REEL2', LAR, MNR )
C
      MNMUTA = 0
      CALL TNMCDC( 'ENTIER', NC, MNMUTA )
C
C     CALCUL DES VALEURS ET VECTEURS PROPRES PAR JACOBI SUR SOUS-ESPACES
C     ------------------------------------------------------------------
      CALL SSPAC1( NOSTRA, MOMANA, MCN(MNMANA),
     %             NC, NBDLIB, INITX0, IFACTO, TETATO,
     %             MOTSRG, RG,
     %             NCODSK, MCN(MNMUKG), KG,
     %             NCODSM, MCN(MNMUMG), MG,COMXMG,
     %             NCVALP0, NBVALP0, MCN(MNVECP0),
     %             MCN(MNR), MCN(MND), MCN(MNVEC),
     %             MCN(MNAR), MCN(MNBR), MCN(MNW), MCN(MNV),
     %             MCN(MNMUTA), MCN(MNDAUX),
     %             NBVALP1, MCN(MNVALP1), MCN(MNVECP1), VALPES, IERR )
C     VALPES = ESTIMATION DE LA PROCHAINE VALEUR PROPRE
C            = VALEUR CALCULEE SUIVANT LA DERNIERE VAP CONVERGEE
      IF( VALPES .GT. EIGMAX0 ) VALPES = EIGMAX0
C
C     DESTRUCTION DES TABLEAUX AUXILIAIRES
      CALL TNMCDS( 'ENTIER', NC,  MNMUTA )
      CALL TNMCDS( 'REEL2',  LAR, MNR    )
C
C     FUSION EVENTUELLE DES TABLEAUX VALP0 ET VALP, VECP0 ET VECP
C     -----------------------------------------------------------
      CALL FUSVVP( NBDLIB,  EIGMIN,  EIGMAX,
     %             NCVALP0, NBVALP0, MCN(MNVALP0), MCN(MNVECP0),
     %             NC,      NBVALP1, MCN(MNVALP1), MCN(MNVECP1) )
C     NBVALP0 DONNE LE NOMBRE DE VALEURS PROPRES CALCULEES ENTRE
C     EIGMIN ET EIGMAX DEMANDES
C
C     ORTHONORMALISATION POUR ELIMINER LES VALEURS DOUBLEES DANS LA FUSION
      CALL ORTVVP( NBDLIB,  NCODSM,  MCN(MNMUMG),  MG, COMXMG,
     %             NCVALP0, NBVALP0, MCN(MNVALP0), MCN(MNVECP0),
     %             MCN(MNV),MCN(MNW) )
      CALL TNMCDS( 'REEL2', MOV/MOREE2, MNV )
C
      NVALOU = NBVAMAX - NBVAMIN - NBVALP0
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10200) NVALOU,EIGMIN,EIGMAX
      ELSE
         WRITE(IMPRIM,20200) NVALOU,EIGMIN,EIGMAX
      ENDIF
C
CCCC     EN CAS DE NON CONVERGENCE DANS SSPAC1: SAUVEGARDE DES VVP ACTUELS
CCC      IF( IERR .NE. 0 ) GOTO 35
C
      IF( NVALOU .GT. 0 ) THEN
C
C        IL EXISTE DES VALEURS PROPRES OUBLIEES
         IF( NBREPRI .GE. MXREPRI ) GOTO 35
C
C        RECHERCHE DE L'INTERVALLE [TETATO,EIGMAX] CONTENANT LES VALEURS
C        PROPRES MANQUANTES ET CALCUL DE LEUR NOMBRE
C        ---------------------------------------------------------------
         CALL SSPAC2( NBREPRI, NBDLIB, NROO, EIGMIN, EIGMAX0,
     %                MOTSRG,  RG,
     %                NCODSK,  MCN(MNMUKG), KG,
     %                NCODSM,  MCN(MNMUMG), MG,
     %                NCVALP0, NBVALP0, MCN(MNVALP0), VALPES,
     %                NVALOU,  IFACTO, TETATO )
C        NVALOU!=0 NOMBRE DE VALEURS PROPRES OUBLIEES ENTRE EIGMIN ET TETATO
ccc         IF( NVALOU .GT. 25 ) GOTO 35   30/1/2009
         IF( NVALOU .GT. 4 ) GOTO 35
         IF( NVALOU .GT. 0 ) THEN
C           UNE ITERATION DE PLUS A FAIRE
C           LES VALEURS ET VECTEURS PROPRES POUR CHAQUE ITERATION
            CALL TNMCDS( 'REEL2', NC,        MNVALP1 )
            CALL TNMCDS( 'REEL2', NC*NBDLIB, MNVECP1 )
C           INITX0 : NO DE L INITIALISATION DU SOUS ESPACE X0 A FAIRE ENSUITE
            INITX0 = INITX0 + 1
            IF( INITX0 .GT. 6 ) INITX0 = 1
            GOTO 20
         ENDIF
C
      ELSE IF( NVALOU .LT. 0 ) THEN
C
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10030) -NVALOU
         ELSE
            WRITE(IMPRIM,20030) -NVALOU
         ENDIF
10030    FORMAT('SSPACE:',I5,
     %          ' VALEURS PROPRES CALCULEES EN TROP. A VERIFIER!')
20030    FORMAT('SSPACE:',I5,
     %          ' TOO MUCH EIGENVALUES. VERIFY THEM')
C
      ENDIF
C
C     VERIFICATION tV MG V = 1 POUR TOUS LES VECTEURS PROPRES CALCULES
      MNVAUX = 0
      CALL TNMCDC( 'REEL2', NBDLIB*2, MNVAUX )
      CALL TVMV( NBDLIB, NCODSM, MCN(MNMUMG), MG,
     %           NCVALP0, NBVALP0, MCN(MNVECP0), MCN(MNVAUX) )
      CALL TNMCDS( 'REEL2', NBDLIB*2, MNVAUX )
C
C     ==============================================
C     ADRESSAGE INITIALISATION DU VECTEUR"...PROPRE
C     ==============================================
 35   IF( NTVVPR .GT. 0 ) THEN
C        LE TMS VECTEUR EST DETRUIT POUR ETRE REDECLARE
         CALL LXTSDS( NTLXOB, NOMVEC )
      ENDIF
C
C     TOUTES LES VALEURS ET VECTEURS PROPRES CALCULES SONT STOCKES
      L = WECTEU + NBVALP0 * NTDL * MOREE2 + NBVALP0
      CALL LXTNDC( NTLXOB, NOMVEC, 'MOTS', L )
      CALL LXTSOU( NTLXOB, NOMVEC, NTVVPR, MNVVPR )
C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR PROPRE
      MCN( MNVVPR + WBCOVE ) = NTDL
C     NOMBRE DE VECTEURS
      MCN( MNVVPR + WBVECT ) = NBVALP0
C     NOMBRE DE VALEURS PROPRES REELLE SIMPLE PRECISION
C     LES VALEURS PROPRES SONT RANGEES DERRIERE LES VECTEURS PROPRES
      MCN( MNVVPR + WBCPIN ) = NBVALP0
C
C     GESTION DE L'ADRESSE DU TABLEAU DLIB SI PAS DE CONDITIONS AUX LIMITES
      IF( MNDLIB .LE. 0 ) THEN
          MNDLB = 1
      ELSE
          MNDLB = MNDLIB
      ENDIF
C     COPIE DES NBVALP0 VALEURS PROPRES EN TRANSPOSANT LE TABLEAU VECP
C     ET EN AJOUTANT LES VALEURS DES DL FIXES
      CALL CPTRVV( NBVALP0, NCVALP0, NTDL, NBDLIB, MCN(MNDLB),
     %             NBDLFX,  MCN(MNDLFX), MCN(MNVDLX),
     %             MCN(MNVECP0), MCN(MNVVPR+WECTEU) )
C
C     COPIE DES NBVALP0 VALEURS PROPRES EN SIMPLE PRECISION !
      MNR = MNVVPR + WECTEU + MOREE2*NTDL*NBVALP0
      MND = (MNVALP0+1) / MOREE2
      DO 40 I=1,NBVALP0
C        TRADUCTION EN REEL SIMPLE
         RMCN(MNR) = REAL( DMCN(MND) )
         MNR = MNR + 1
         MND = MND + 1
 40   CONTINUE
C     LA DATE
      CALL ECDATE( MCN(MNVVPR) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNVVPR + MOREE2 ) = NONMTD( '~>>>VECTEUR' )
C
C     DESTRUCTION DES TABLEAUX MC INUTILES
      IF( NCVALP0 .GT. 0 ) THEN
         CALL TNMCDS( 'REEL2',  NCVALP0,        MNVALP0 )
         CALL TNMCDS( 'REEL2',  NCVALP0*NBDLIB, MNVECP0 )
      ENDIF
      CALL TNMCDS( 'REEL2', NC,        MNVALP1 )
      CALL TNMCDS( 'REEL2', NC*NBDLIB, MNVECP1 )
      IF(MNMANA .GT. 0) CALL TNMCDS( 'ENTIER', MOMANA, MNMANA )
C
C     ON INDIQUE SI LES VVP STOCKES SONT CORRECTS OU NON
C     NOMBRE DE VALEURS PROPRES OUBLIEES
      NVALOU = NBVAMAX - NBVAMIN - NBVALP0
      IF( NVALOU .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10050) EIGMIN, EIGMAX
         ELSE
            WRITE(IMPRIM,20050) EIGMIN, EIGMAX
         ENDIF
10050 FORMAT('AUCUNE VALEUR PROPRE OUBLIEE ENTRE',G15.7,' ET ',G15.7/
     %       'SORTIE de SSPACE avec VALEURS VECTEURS PROPRES CORRECTS'/)
20050 FORMAT('NO FORGOTTEN EIGENVALUE from',G15.7,' until ',G15.7/
     %       'EXIT of SSPACE with CORRECT EIGENSOLUTIONS'/)
      ELSE
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19999) NVALOU, EIGMIN, EIGMAX
         ELSE
            WRITE(IMPRIM,29999) NVALOU, EIGMIN, EIGMAX
         ENDIF
      ENDIF
19999 FORMAT(/'SORTIE DE SSPACE AVEC VVPR INCORRECT CAR ',
     %I5,' VALEURS PROPRES OUBLIEES ENTRE ',G15.7,' ET ',G15.7/)
29999 FORMAT(/'EXIT of SSPACE with INCORRECT EIGENSOLUTIONS: ',
     %I5,' FORGOTTEN EIGENVALUES from ',G15.7,' until ',G15.7/)
C
C     SI PAS DE VALEURS PROPRES OUBLIEES ALORS PAS D'ERREUR!
      IF( NVALOU .EQ. 0 ) IERR = 0
C
      RETURN
      END
