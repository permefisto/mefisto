      SUBROUTINE SDRES6( TABLE,  TRACE,  NBNOIN,
     &                   NBNOSD, NUNOIN, NUNOSD, NBINCO )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : EXTRACTION DES VALEURS SUR L'INTERFACE
C -----

C ENTREES :
C ---------
C NBNOIN : NOMBRE TOTAL DE NOEUDS DE L'INTERFACE
C NBNOSD : NOMBRE DE NOEUDS DE L'INTERFACE
C NUNOIN : LES NUMEROS DE CES NOEUDS DANS L'INTERFACE
C NUNOSD : LES NUMEROS DE CES NOEUDS DANS LE SOUS-DOMAINE
C NBOBNO : LE NOMBRE DE SOUS-DOMAINES CONTENANT CE NOEUD
C NBINCO : LE  NOMBRE D'INCONNUES EN CHAQUE NOEUD
C TABLE  : LE TABLEAU A EXTRAIRE SUR L'INTERFACE

C SORTIES :
C ---------
C TRACE  : LA TRACE EXTRAITE DU TABLEAU TABLE SUR L'INTERFACE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PASCAL JOLY     ANALYSE NUMERIQUE UPMC PARIS  MARS 1993
C23456---------------------------------------------------------------012
      DIMENSION        NUNOIN(NBNOSD),NUNOSD(NBNOSD)
      DOUBLE PRECISION TABLE(NTDL,NDSM),TRACE(NBNOIN*NBINCO,NDSM)
      COMMON / SOUSDO / NORESO,NTDL,NDSM,NDIM,NPIMAX

C     LE TRANSFERT
C     ============
      DO NB = 1 , NBNOSD
         NUNOK = NBINCO * ( NUNOSD(NB) - 1 )
         NBK   = NBINCO * ( NUNOIN(NB) - 1 )
         DO K  = 1 , NBINCO
            DO NS = 1 , NDSM
               TRACE(NBK+K,NS) = TABLE(NUNOK+K,NS)
            ENDDO
         ENDDO
      ENDDO

      RETURN
      END
