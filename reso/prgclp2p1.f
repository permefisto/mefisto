      SUBROUTINE PRGCLP2P1( NBSOM,   NBNOVI,  NONOSO,
     %                      NCODSA,  LP2LIGN, LP2COLO,
     %                      LP1LIGN, IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LE TABLEAU POINTEUR SUR LE COEFFICIENT DIAGONAL
C -----    DU STOCKAGE CONDENSE DE LA MATRICE DU MAILLAGE POUR
C          LES SOMMETS (INTERPOLATION P1) CONNAISSANT CELUI
C          POUR LES NOEUDS P2 A PARTIR D'UN MAILLAGE P2

C ENTREES:
C --------
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBNOVI : NOMBRE DE NOEUDS  DU MAILLAGE
C NONOSO : NONOSO(I)=NO SOMMET SI SOMMET, 0 SI MILIEU D'ARETE
C NCODSA : CODE DE STOCKAGE DE LA MATRICE MORSE
C           0 : MATRICE DIAGONALE
C          -1 : MATRICE NON SYMETRIQUE
C           1 : MATRICE SYMETRIQUE
C LP2LIGN: POINTEUR SUR LES COEFFICIENTS DIAGONAUX      DE LA MATRICE P2
C LP2COLO: NUMERO DES COLONNES DES COEFFICIENTS STOCKES DE LA MATRICE P2

C SORTIES:
C --------
C LP1LIGN: POINTEUR SUR LES COEFFICIENTS DIAGONAUX
C          DE LA MATRICE P1 DU SYSTEME LINEAIRE POUR LES NBSOM SOMMETS
C          LE NOMBRE DE COLONNES DES SOMMETS VOISINS = LP1LIGN(NBSOM)
C IERR   : 0 SI PAS D'ERREUR, 1 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY     Juin 2012
C MODIFS : ALAIN PERRONNET             St PIERRE du PERRAY     Mars 2021
C MODIFS : ALAIN PERRONNET             St PIERRE du PERRAY    Avril 2023
C23456---------------------------------------------------------------012
      include"./incl/pp.inc"
      COMMON         MCN(MOTMCN)
      INTEGER        NONOSO(1:NBNOVI), LP2LIGN(0:NBNOVI), LP2COLO(1:*),
     %               LP1LIGN(0:NBSOM)

      IERR = 0
      IF( NCODSA .EQ. 0 ) THEN
         IERR = 1
         GOTO 9000
      ENDIF

C     PARCOURS DES NUMEROS DE COLONNES DES NOEUDS
C     POUR CALCULER LE POINTEUR DIAGONAL DES SOMMETS P1
C     -------------------------------------------------
      LP1LIGN( 0 ) = 0
      NBS = 0

      DO NOE = 1, NBNOVI

C        NUMERO DE SOMMET DU NOEUD NOE
         NUSOM = NONOSO(NOE)
         IF( NUSOM .GT. 0 ) THEN

C           NUSOM EST UN SOMMET A TRAITER
            NBS = NBS + 1

C           CALCUL DU NOMBRE DE SOMMETS VOISINS DU SOMMET NUSOM
            NBCOLS  = 0
            DO LC = LP2LIGN(NOE-1)+1, LP2LIGN(NOE)

C              NUMERO COLONNE D'UN NOEUD P2
               NCL = LP2COLO( LC )

C              NUMERO SOMMET P1 DE CE NOEUD P2
               NCS = NONOSO( NCL )

               IF( NCS .GT. 0 ) THEN
C                 CE NOEUD EST UN SOMMET: COEFFICIENT EXISTANT
                  NBCOLS = NBCOLS + 1
               ENDIF

            ENDDO

C           POINTEUR DIAGONAL
            LP1LIGN( NUSOM ) = NBCOLS

         ENDIF

      ENDDO
      PRINT *,'prgclp2p2: Nombre de SOMMETS RETROUVES=',NBS,
     %        ' NBSOM=',NBSOM,' DOIVENT ETRE EGAUX'

C     SOMMATION DES NOMBRES DE SOMMETS VOISINS
      DO NCS = 1, NBS
         LP1LIGN( NCS ) = LP1LIGN( NCS-1 ) + LP1LIGN( NCS )
      ENDDO

 9000 RETURN
      END
