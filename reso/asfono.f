      SUBROUTINE ASFONO( NTDL, NDSM, NBFNFX, NODLFN, VADLFN, RELMIN, B )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:     AJOUTER LES FORCES NODALES AUX SECONDS MEMBRES DEJA ASSEMBLES
C ----
C ENTREES:
C --------
C NTDL   : NOMBRE TOTAL DE DEGRES DE LIBERTE D'UN VECTEUR B
C NDSM   : NOMBRE DE CAS OU SECONDS MEMBRES OU VECTEURS B
C NBFNFX : NOMBRE DE FORCES NODALES A ASSEMBLER
C NODLFN : LE NUMERO DES DL DES NBFNFX FORCES NODALES
C VADLFN : LA VALEUR DES NBFNFX FORCES NODALES
C RELMIN : LA VALEUR TEST DE NON INITIALISATION D UNE FORCE NODALE
C
C MODIFIE:
C --------
C B      : TABLEAU(NTDL,NDSM) AVANT ET APRES ASSEMBLAGE DES FORCES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: A.PERRONNET LABORATOIRE D'ANALYSE NUMERIQUE PARIS  MAI 1989
C2345X7..............................................................012
      DOUBLE PRECISION B(NTDL,NDSM), VADLFN(NDSM,NBFNFX), RELMIN, FN
      INTEGER          NODLFN(NBFNFX)
C
C     LA BOUCLE SUR LES FORCES NODALES
      DO 20 J=1,NBFNFX
C
C        LE NUMERO DU DL
         NODL = NODLFN( J )
C
         DO 10 I = 1, NDSM
C
C           SI    LA VALEUR DE LA FORCE NODALE N A PAS ETE INITIALISEE
C           ALORS ELLE EST RAMENEE A 0
            FN = VADLFN( I , J )
            IF( FN .EQ. RELMIN ) FN = 0.D0
C
C           AJOUT DE LA FORCE NODALE FN
            B( NODL , I ) = B( NODL , I ) + FN

 10      CONTINUE
C
 20   CONTINUE

      RETURN
      END
