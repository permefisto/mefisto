      SUBROUTINE SDRES7(TABLE,TRACE,NBNOIN,
     &                  NBNOSD,NUNOIN,NUNOSD,POIDL,POIDS,NBINCO)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : GESTION DU TABLEAU TABLE SUR L'INTERFACE : PONDERATION DES
C ----- VALEURS DE PART ET D'AUTRE DE L'INTERFACE
C
C       CE SP EST EQUIVALENT AU SP SDRES5 , AVEC UN POIDS DIFFERENT
C
C ENTREES :
C ---------
C NBNOIN : NOMBRE TOTAL DE NOEUDS DE L'INTERFACE
C NBNOSD : NOMBRE DE NOEUDS DE L'INTERFACE
C NUNOIN : LES NUMEROS DE CES NOEUDS DANS L'INTERFACE
C NUNOSD : LES NUMEROS DE CES NOEUDS DANS LE SOUS-DOMAINE
C POIDL  : LE POIDS CE NOEUD DANS LE SOUS-DOMAINE
C POIDS  : LE POIDS CE NOEUD DANS LE DOMAINE COMPLET
C NBINCO : LE  NOMBRE D'INCONNUES EN CHAQUE NOEUD
C TABLE  : LE TABLEAU A PONDERER SUR L'INTERFACE

C SORTIES :
C ---------
C TRACE  : LA TRACE PONDEREE DU TABLEAU TABLE SUR L'INTERFACE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PASCAL JOLY     ANALYSE NUMERIQUE UPMC PARIS  MAI 1990
C23456---------------------------------------------------------------012
      DIMENSION NUNOIN(NBNOSD),NUNOSD(NBNOSD)
      DOUBLE PRECISION TABLE(NTDL,NDSM),TRACE(NBNOIN*NBINCO,NDSM)
      DOUBLE PRECISION POIDL(NBNOIN),POIDS(NBNOIN)
      COMMON / SOUSDO / NORESO,NTDL,NDSM,NDIM,NPIMAX

C     LE TRANSFERT
C     ============
      DO NB = 1 , NBNOSD
         NUNOK = NBINCO * ( NUNOSD(NB) - 1 )
         NBK   = NBINCO * ( NUNOIN(NB) - 1 )
         DO K  = 1 , NBINCO
            DO NS = 1 , NDSM
               TRACE(NBK+K,NS) = TRACE(NBK+K,NS) + TABLE(NUNOK+K,NS)
     &                         * POIDL(NUNOIN(NB)) / POIDS(NUNOIN(NB))
            ENDDO
         ENDDO
      ENDDO

      RETURN
      END
