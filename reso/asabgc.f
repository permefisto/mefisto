      SUBROUTINE ASABGC (NTDL, NDSM, NBDL, NODL, AES, AE, BE, NCODSA,
     %                   LPLIGN, LPCOLO, AG, BG)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : ASSEMBLER LA MATRICE ELEMENTAIRE EN LA MATRICE GLOBALE
C ----- MORSE SYMETRIQUE OU NON TOUTE EN MEMOIRE CENTRALE
C       ASSEMBLER LE SECOND MEMBRE ELEMENTAIRE EN LE VECTEUR GLOBAL

C PARAMETRES D ENTREE :
C ---------------------
C NDSM   : NOMBRE DE CAS DE CHARGE
C NBDL   : NOMBRE DE DEGRES DE LIBERTE DE L ELEMENT
C NODL   : NO DES NBDL DEGRES DE LIBERTE DE L ELEMENT
C AES    : MATRICE ELEMENTAIRE SYMETRIQUE (NBDL * (NBDL+1) / 2 )
C AE     : MATRICE ELEMENTAIRE  (NBDL,NBDL)
C BE     : SECOND MEMBRE ELEMENTAIRE  (NBDL)
C NCODSA : CODE DE STOCKAGE DE LA MATRICE PROFIL
C          -1 : MATRICE NON SYMETRIQUE
C           1 : MATRICE SYMETRIQUE
C LPLIGN : TABLEAU POINTEUR SUR LES COEFFICIENTS DIAGONAUX
C LPCOLO : TABLEAU DES NUMEROS DE COLONNES

C PARAMETRES RESULTATS :
C ----------------------
C AG     : MATRICE MORSE TOUTE EN MEMOIRE CENTRALE
C BG     : SECOND MEMBRE ELEMENTAIRE TOUT EN MEMOIRE CENTRALE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : A. PERRONNET LABORATOIRE D'ANALYSE NUMERIQUE PARIS Mai   1989
C MODIFS : A. PERRONNET             St Pierre du Perray       Avril 2023
C ......................................................................
      INTEGER          NODL(NBDL), LPLIGN(0:NTDL), LPCOLO(*)
      DOUBLE PRECISION AES(*), AE(NBDL,NBDL), BE(NDSM,NBDL), 
     %                 AG(1), BG(NTDL,NDSM)

C     ASSEMBLAGE de BE dans BG
      DO I=1,NBDL

C        LE NO GLOBAL DU I-EME DEGRE DE LIBERTE LOCAL
         IEG = NODL( I )

C        ASSEMBLAGE DE LA I-EME COMPOSANTE DE BE DANS BG
         DO J=1,NDSM
            BG( IEG , J ) = BG( IEG , J ) + BE( J , I )
         ENDDO

      ENDDO

C     ASSEMBLAGE de AES ou AE dans AG
      CALL ASMEGC( NBDL,   NODL,   NCODSA, AES, AE,
     %             NCODSA, LPLIGN, LPCOLO, AG )

      RETURN
      END
