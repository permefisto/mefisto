      SUBROUTINE ITEIN1( NBROOT, MXROOT, N,
     %                   MUA,    A,      NCODSA,
     %                   MUB,    B,      NCODSB,
     %                   DIAGA,  DIAGB,  V,      W,
     %                   VECPR,  BVECPR, VALPR,
     %                   NINV,   MOTSRG, RG,
     %                   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   CALCUL DES NBROOT PLUS PETITES VALEURS ET VECTEURS PROPRES DU
C -----   PROBLEME  (A-L*B) * X = 0  (L VALEUR X VECTEUR PROPRE)
C         A MATRICE DE RAIDEUR   B MATRICE DE MASSE
C         METHODE DES ITERATIONS INVERSES AVEC TRANSLATION
C
C ENTREES:
C --------
C NBROOT : NOMBRE DES PLUS PETITES VALEURS ET VECTEURS PROPRES A CALCULER
C MXROOT : NOMBRE MAJORANT DES VALEURS PROPRES >NBROOT AFIN DE PRENDRE EN
C         CONSIDERATION LES VALEURS MULTIPLES OU LES CALCULS DES VALEURS
C         SUPERIEURES A CELLES DEMANDEES
C N     : NOMBRE DE LIGNES ET COLONNES  DES MATRICES DE A ET B
C
C MUA, A, NCODSA : POINTEUR, MATRICE, CODE ( RAIDEUR )
C MUB, B, NCODSB : POINTEUR, MATRICE, CODE ( MASSE )
C
C DIAGA : TABLEAU  AUXILIAIRE  DE TAILLE N REELS DOUBLE PRECISION
C DIAGB : TABLEAU  AUXILIAIRE  DE TAILLE N REELS DOUBLE PRECISION
C V,W   : TABLEAUX AUXILIAIRES DE TAILLE N REELS DOUBLE PRECISION
C VECPR : VECPR(N,MXROOT) TABLEAU DES VECTEURS PROPRES
C BVECPR: BVECPR(N,MXROOT) TABLEAU DE B FOIS LES VECTEURS PROPRES
C VALPR : VALPR(MXROOT) VALEURS PROPRES
C
C NINV  : NOMBRE D ITERATIONS INVERSES PAR VALEUR PROPRE
C MOTSRG: NOMBRE DE MOTS DE LA MATRICE DE SAUVEGARDE DE A
C RG    : MATRICE DE SAUVEGARDE DE A
C
C SORTIE:
C -------
C NBROOT : NOMBRE DES PLUS PETITES VALEURS ET VECTEURS PROPRES CALCULES
C IERR  : 0 SI PAS D'ERREUR RENCONTREE, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET UPMC ANALYSE NUMERIQUE PARIS     DECEMBRE 1998
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/epsvvp.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
C
      INTEGER          MUA(*),MUB(*),NINV(MXROOT)
      DOUBLE PRECISION RG(*),A(*),B(*),DIAGA(N),DIAGB(N),V(N),W(N),
     %                 VECPR(N,MXROOT),BVECPR(N,MXROOT),VALPR(MXROOT+1)
      DOUBLE PRECISION RQ0,RQ,XX,R0,RD,R1,VP,RN,R2,
     %                 DETR0,DETR1,DETR2,DETVP
C
C     INITIALISATIONS DES SEUILS ET AUTRES CONSTANTES
C     -----------------------------------------------
      IERR = 0
      RQ0  = 0D0
C
C     FAST FACTEUR PAR LEQUEL ON TRANSLATE LEGEREMENT MU POUR PRESERVER LA
C     NITEMX NOMBRE D ITERATIONS MAX POUR CALCULER VALEURS ET VECTEURS PROPRES
      NITEMX = 30
C     NINVMX NOMBRE D ITERATIONS INVERSES MAX POUR LE CALCUL D'UNE VALEUR PROPRE
      NINVMX = 100
C     NINVT NOMBRE D ITERATIONS INVERSES SANS TEST DE CONVERGENCE
      NINVT  = 7
C     NMAXII NOMBRE MAX D ITERATIONS INVERSES APRES LESQUELLES LA SECANTE REPART
      NMAXII = 2*NINVT
C     NSK VAUT 0 SAUF SI L ON A OUBLIE PLUSIEURS VALEURS PROPRES LORS DE L
C     ITERATION SECANTE OU S IL ON A CONVERGE VERS LE VECTEUR PROPRE SUIVANT
C     LORS DE L ITERATION INVERSE ET ALORS NSK VAUT 1
      NSK = 0
C     NUMERO D'INITIALISATION DU VECTEUR PROPRE POUR L'ITERATION INVERSE
      NUINVP = 1
C     NOMBRE MAXIMAL DE REPRISES APRES OUBLI DE VALEURS PROPRES
      MXREPR = 12
C
C     NOMBRE DE VALEURS PROPRES DEMANDEES
      NBROOT0 = NBROOT
      NBROOT1 = NBROOT + 1
C
      DO 1 I=1,MXROOT
         NINV(I) = 0
 1    CONTINUE
C
C     ****************************************************************
C     RECHERCHE D'UNE APPROXIMATION R1 DE LA PLUS PETITE VALEUR PROPRE
C     ****************************************************************
C     NURAC NUMERO DE LA RACINE QUE L ON CALCULE
      NURAC = 1
      R0    = 0D0
      CALL TRTATA( RG, A, MOTSRG )
C
C     FACTORISATION A = L D TL
      CALL CRMC1D( MUA,A,N,EPS,0, A,NRETOU )
      IF( NRETOU .NE. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10000)
         ELSE
            WRITE(IMPRIM,20000)
         ENDIF
10000    FORMAT('ERREUR ITEIN1: MATRICE RAIDEUR NON INVERSIBLE')
20000    FORMAT('ERROR ITEIN1: STIFFNESS MATRICE NOT INVERSIBLE')
         IERR = 1
         RETURN
      ENDIF
C
C     DETERMINANT DE A = L D tL = DETERMINANT DE D
      CALL DETETD( A, MUA, N, NOVR0, NBHR0, DETR0 )
C     NOVR0 EST LE NOMBRE DE PIVOTS NEGATIFS DONC
C     DE VALEURS PROPRES AVANT R0=0D0
C
C     CALCUL DE DIAGA=DIAGONALE(A)  DIAGB=DIAGONALE(B)
      DO 2 I=1,N
         XX = A( MUA(I+1) )
CCC         IF( XX .LT. 1D-20 ) XX=1D0       3/7/2003
         DIAGA(I) = XX
         DIAGB(I) = B( MUB(I+1) )
 2    CONTINUE
C
C     ITER NOMBRE D ITERATIONS POUR CALCULER LA VALEUR R1
      ITER = 0
C
C     INITIALISATION DU QUOTIENT DE RAYLEIGH
C     W=W0=B DIAGA
      CALL MAPRVE( 0, 1D0, N, NCODSB, MUB, B, DIAGA,  W )
C
C     ITERATIONS SUR LE QUOTIENT DE RAYLEIGHT POUR CALCULER R1
C     --------------------------------------------------------
 5    ITER = ITER + 1
C
C     V = Vk+1 = (A - R0 B)**-1 Wk
      CALL DRCRPR( N, NCODSA, MUA, A, W, 3,  V, IERR )
C
C     RN = tVk+1 (A - R0 B) Vk+1 = NUMERATEUR DU QUOTIENT DE RAYLEIGHT
C        = t(B Vk) (A - R0 B)-1 (B Vk)
C        = t(B Vk) Vk+1 = tVk+1 (B Vk)
C        = tVk+1 (A - R0 B) Vk+1
      RN = 0D0
      DO 6 I=1,N
         RN = RN + W(I) * V(I)
 6    CONTINUE
C
C     Wk+1 = B Vk+1
      CALL MAPRVE( 0, 1D0, N, NCODSB, MUB, B, V,  W )
C
C     RD = t Vk+1 B Vk+1 = DENOMINATEUR DU QUOTIENT DE RAYLEIGHT
      RD = 0D0
      DO 7 I=1,N
         RD = RD + W(I) * V(I)
 7    CONTINUE
C
C     LE QUOTIENT DE RAYLEIGHT k+1 = tVk+1 (A - R0 B) Vk+1 / Vk+1 B Vk+1
      RQ = RN / RD
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,10007) ITER,RQ
      ELSE
         WRITE (IMPRIM,20007) ITER,RQ
      ENDIF
10007 FORMAT('DEPART ITERATION ',I4,' QUOTIENT RAYLEIGHT=',G15.7)
20007 FORMAT('START ITERATION ',I4,' RAYLEIGHT QUOTIENT=',G15.7)
C
      IF( ABS(RQ-RQ0) .GE. 1D-3*ABS(RQ) ) THEN
C
C        PAS DE CONVERGENCE DU QUOTIENT DE RAYLEIGHT
C        -------------------------------------------
C        B-NORMALISATION DU VECTEUR Wk+1 AVEC RD  = ( tVk+1 B Vk+1 )**1/2
         RD = SQRT( ABS(RD) )
         DO 8 I=1,N
            W(I) = W(I) / RD
 8       CONTINUE
C
C        PASSAGE A L'ITERATION SUIVANTE
         RQ0 = RQ
         IF( ITER .LT. NINVMX ) THEN
C
C           MAX DES ITERATIONS NON ATTEINT
            GOTO 5
C
         ENDIF
C
      ENDIF
C
C     CONVERGENCE DU QUOTIENT DE RAYLEIGHT OU ABANDON APRES MAX ITERATIONS
C     --------------------------------------------------------------------
      R1 = RQ
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10008) 1, R1, ITER
      ELSE
         WRITE(IMPRIM,20008) 1, R1, ITER
      ENDIF
10008 FORMAT('VALEUR PROPRE',I4,' =',G15.7,
     %' APRES',I4,' ITERATIONS INVERSES')
20008 FORMAT('EIGENVALUE',I4,' =',G15.7,
     %' after',I4,' INVERSE ITERATIONS')
C
C     PREPARATION DES ITERATIONS INVERSES POUR LA PREMIERE VALEUR PROPRE
C     ------------------------------------------------------------------
C     A EST REGENERE
      CALL TRTATA( RG, A, MOTSRG )
C     TRANSLATION: A - R1 B => A
      CALL MUA2PD( N, 1.D00, NCODSA, MUA, A, -R1, NCODSB, MUB,B, MUA,A )
C     A - R1 B => L D tL
      CALL CRMC1D( MUA, A, N, EPS, 1, A, NRETOU )
C     DETR1 = DETERMINANT DE A - R1 B
      CALL DETETD( A, MUA, N, NOVR1, NBHR1, DETR1 )
C
C     R1=R2 EST UNE VALEUR PROPRE A AFFINER PAR ITERATIONS INVERSES
      R2     = R1
      NBHR2  = NBHR1
      DETR2  = DETR1
      NOVR2  = NOVR1
C
C     **************************************************************************
C     ITERATION INVERSE POUR AFFINER LA VALEUR PROPRE ET CALCULER LE VECTEUR PRO
C     **************************************************************************
 30   IF( NURAC .GT. MXROOT )  THEN
C        NOMBRE MAXIMAL ATTEINT
         NBROOT = MXROOT
         GOTO 80
      ENDIF
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,10030) NURAC
         WRITE (IMPRIM,10031) (KK,VALPR(KK),KK=1,NURAC-1)
         WRITE (IMPRIM,10032) NOVR2, R2
      ELSE
         WRITE (IMPRIM,20030) NURAC
         WRITE (IMPRIM,20031) (KK,VALPR(KK),KK=1,NURAC-1)
         WRITE (IMPRIM,20032) NOVR2, R2
      ENDIF
10030 FORMAT(/'CALCUL DE LA VALEUR PROPRE',I4)
10031 FORMAT('VALEURS PROPRES DEJA CALCULEES:'/5(I5,':',G15.7))
10032 FORMAT(I5,' VALEURS PROPRES AVANT',G15.7)
C
20030 FORMAT(/'COMPUTATION of the EIGENVALUE',I4)
20031 FORMAT('ALREADY COMPUTED EIGENVALUES:'/5(I5,':',G15.7))
20032 FORMAT(I5,' EIGENVALUES BEFORE',G15.7)
C
      CALL ITEIN2( N, MXROOT,  NURAC,  NUINVP,
     %             NCODSA, MUA, A, DIAGA, MOTSRG, RG,
     %             NCODSB, MUB, B, DIAGB,
     %             NINVMX, NINV,
     %             R0,
     %             R2,     NBHR2, DETR2, NBVIR2, NOVR2,
     %             VALPR,  VECPR, BVECPR,
     %             VP,     V,     W  )
C     EN SORTIE:
C     VP EST LA NOUVELLE VALEUR PROPRE NURAC et DEFINITIVE
C     V SON VECTEUR PROPRE ASSOCIE
C     W=[B] V  LE PRODUIT DE [B] PAR V
      VALPR(NURAC) = VP
C
C     B ORTHONORMALISATION DU VECTEUR PROPRE V ET DE W=[B] V
C     RD = tV B V
      RD = 0D0
      DO 32 I=1,N
         RD = RD + V(I) * W(I)
 32   CONTINUE
C
C     STOCKAGE ET NORMALISATION DU VECTEUR PROPRE V ET W=[B] V
      RD = SQRT( ABS(RD) )
      DO 34 I=1,N
C        DIVISION PAR RD POUR AVOIR FINALEMENT tV B V = 1
         W(I) = W(I) / RD
         BVECPR(I,NURAC) = W(I)
         V(I) = V(I) / RD
         VECPR(I,NURAC) = V(I)
 34   CONTINUE
C
C     **************************************************************
C     STRATEGIE POUR CALCULER LA VALEUR ET LE VECTEUR PROPRE SUIVANT
C     **************************************************************
      VP = VP * 1.01D0
C     NOMBRE DE VALEURS PROPRES OUBLIEES AVANT VP
      CALL NBVAOU( VP,     N,     NURAC,  VALPR,
     %             NCODSA, MUA,   A,      MOTSRG, RG,
     %             NCODSB, MUB,   B,
     %             NBHVP,  DETVP, NBVIVP, NOVVP )
      IF( NBVIVP .GE. NBROOT0 .AND. NOVVP .EQ. 0 ) THEN
C        LE NOMBRE DE VALEURS PROPRES CALCULEES
         NBROOT = NURAC
         GOTO 80
      ENDIF
C
C     R0 EST LA PLUS GRANDE VALEUR CALCULEE AVEC 0 VALEUR OUBLIEE
      IF( NOVVP .EQ. 0 .AND. (VP .GT. R0 .OR. R0.EQ.0D0) ) THEN
         R0    = VP
         NOVR0 = NOVVP
      ENDIF
C
      IF( NOVVP .LE. 0 ) THEN
C
C        PAS D'OUBLI DE VALEUR PROPRE
C        REDEPART EN ITERATIONS INVERSES AVEC VP ACTUEL
C        ----------------------------------------------
         R2    = VP
         DETR2 = DETVP
         NBHR2 = NBHVP
         NOVR2 = NOVVP
C
      ELSE
C
C        OUBLI D'AU MOINS UNE VALEUR PROPRE
C        ----------------------------------
C        ON FAIT VARIER L'INITIALISATION DU VECTEUR PROPRE
         NUINVP = MOD( NUINVP, 9 ) + 1
         IF( NOVR0 .GT. 0 ) THEN
C           IL Y A AU MOINS UNE VALEUR PROPRE OUBLIEE NEGATIVE
CCC            R2 = R0 - 10.D0 ** NOVR0 * VP
            R2 = -1D0
         ELSE
C           REDEPART AVEC LA PLUS GRANDE VALEUR CALCULEE SANS OUBLI
            R2 = R0
        ENDIF
C
C        NOMBRE DE VALEURS PROPRES OUBLIEES AVANT R2
         CALL NBVAOU( R2,     N,     NURAC,  VALPR,
     %                NCODSA, MUA,   A,      MOTSRG, RG,
     %                NCODSB, MUB,   B,
     %                NBHR2,  DETR2, NBVIR2, NOVR2 )
         IF( NOVR2 .LE. 0 .AND. NOVR0 .GT. 0 ) THEN
C           R0 EST UNE VALEUR SANS VALEUR PROPRE OUBLIEE AVANT
            NOVR0 = 0
            R0    = R2
         ENDIF
C
      ENDIF
C
C     LA RACINE SUIVANTE A CALCULER
      NURAC = NURAC + 1
C     RETOUR A L'ITERATION INVERSE
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10080) R2,R0
      ELSE
         WRITE(IMPRIM,20080) R2,R0
      ENDIF
10080 FORMAT('RETOUR vers ITERATION INVERSE avec R2=',G15.7,
     %' R0=',G15.7)
20080 FORMAT('RETURN to INVERSE ITERATION with R2=',G15.7,
     %' R0=',G15.7)
      GOTO 30
C
C     *****************************************************************
C     LES NBROOT      VALEURS ET VECTEURS PROPRES ONT ETE CALCULES
C     CLASSEMENT DES VALEURS ET VECTEURS PROPRES DANS L ORDRE CROISSANT
C     *****************************************************************
 80   IF( NBROOT .EQ. NBROOT0 ) RETURN
      IF( NBROOT .GT. 1 ) THEN
C        TRI CROISSANT DES VALEURS PROPRES
 82      IS = 0
         DO 86 I=1,NBROOT-1
            IF( VALPR(I+1) .LT. VALPR(I) ) THEN
               IS        = IS + 1
               VP        = VALPR(I+1)
               VALPR(I+1)= VALPR(I)
               VALPR(I)  = VP
               K         = NINV(I+1)
               NINV(I+1) = NINV(I)
               NINV(I  ) = K
               DO 84 K=1,N
                  VP           = VECPR(K,I+1)
                  VECPR(K,I+1) = VECPR(K,I)
                  VECPR(K,I)   = VP
C                 EN CAS DE REPRISE A FAIRE
                  VP            = BVECPR(K,I+1)
                  BVECPR(K,I+1) = BVECPR(K,I)
                  BVECPR(K,I)   = VP
 84            CONTINUE
            ENDIF
 86      CONTINUE
         IF( IS .GT. 0 ) GOTO 82
      ENDIF
C
C     AFFICHAGE DES VALEURS PROPRES ACTUELLES
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,10086) (K,VALPR(K),K=1,NURAC)
      ELSE
         WRITE (IMPRIM,20086) (K,VALPR(K),K=1,NURAC)
      ENDIF
10086 FORMAT('VALEURS PROPRES CONVERGEES:'/5(I5,':',G15.7))
20086 FORMAT('CONVERGED EIGENVALUES:'/5(I5,':',G15.7))
C
C     VERIFICATION: Y A T IL DES VALEURS PROPRES OUBLIEES?
C     ----------------------------------------------------
      IF( R0 .LE. VALPR(1) ) THEN
C        IL EXISTE UNE VALEUR PROPRE OUBLIEE AVANT LA PREMIERE CALCULEE
C        REPRISE AVEC LA PLUS PETITE VALEUR PROPRE CALCULEE DIMINUEE
         R2 = VALPR(1) * 0.99
         GOTO 92
      ENDIF
C
C     RECHERCHE DE LA DERNIERE VALEUR PROPRE SANS OUBLI AVANT
      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*)
     %'RECHERCHE DE LA DERNIERE VALEUR PROPRE SANS OUBLI AVANT'
      ELSE
         WRITE(IMPRIM,*)'SEARCH of the LAST EIGENVALUE without FORGOTTEN
     % EIGENVALUE BEFORE'
      ENDIF
C
      R1 = ABS( VALPR(NBROOT) ) * 2
      DO 90 K=NBROOT,1,-1
C
ccc         IF( R0 .LT. VALPR(K) ) GOTO 90
ccc         16/2/2011 et ligne au dessous ajoutee
C
         IF( VALPR(K) .LE. R0 ) GOTO 92
C        SI LA VALEUR PROPRE K EST INFERIEURE A R0 PLUS GRANDE VALEUR SANS
C        OUBLI IL N'EST PAS NECESSAIRE DE FAIRE D'AUTRES VERIFICATIONS
C
C        LA VALEUR PROPRE TESTEE
         R2 = VALPR( K ) * 1.01D0
         IF( ABS( R2 - R1 ) .LE. 0.01D0 * ABS(R2) ) GOTO 90
C
C        NOMBRE DE VALEURS PROPRES OUBLIEES AVANT R0 ET MISE A JOUR DETR0
         CALL NBVAOU( R2,     N,     NBROOT, VALPR,
     %                NCODSA, MUA,   A,      MOTSRG, RG,
     %                NCODSB, MUB,   B,
     %                NBHR2,  DETR2, NBVIR2, NOVR2 )
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10089) NOVR2, R2
         ELSE
            WRITE(IMPRIM,20089) NOVR2, R2
         ENDIF
C        R0 EST LA PLUS GRANDE VALEUR CALCULEE AVEC 0 VALEUR OUBLIEE
         IF( NOVR2 .EQ. 0 .AND. R2 .GT. R0 ) THEN
            R0 = R2
            IF( LANGAG .EQ. 0 ) THEN
               WRITE(IMPRIM,10088) R0
            ELSE
               WRITE(IMPRIM,20088) R0
            ENDIF
         ENDIF
C
         IF( NOVR2 .LE. 0 ) THEN
            IF( K .GE. NBROOT0 ) GOTO 9000
C           REPRISE DES ITERATIONS INVERSES AVEC R2
            GOTO 92
         ENDIF
C
C        PASSAGE AU TEST SUR LA VALEUR PROPRE INFERIEURE
         R1 = R2
C
 90   CONTINUE
C
10088 FORMAT('NOUVEAU R0=',G15.7)
20088 FORMAT('NEW R0=',G15.7)
10089 FORMAT(/'TEST FINAL:',I5,' VALEURS PROPRES OUBLIEES AVANT',G15.7)
20089 FORMAT(/'FINAL TEST:',I5,' FORGOTTEN EIGENVALUES BEFORE',G15.7)
10090 FORMAT(/'ATTENTION:',I4,' VALEURS PROPRES OUBLIEES AVANT',
     %G15.7,' => REPRISE NECESSAIRE'/1X,100('&')//)
20090 FORMAT(/'WARNING:',I4,'  FORGOTTEN EIGENVALUES BEFORE',
     %G15.7,' => NECESSARY RETAKING'/1X,100('&')//)
C
C     LA VALEUR PROPRE VALPR(K)*1.01D0 N'A PAS DE VALEUR PROPRE OUBLIEE AVANT
C     MAIS IL MANQUE AU MOINS UNE VALEUR PROPRE AVANT VALPR(K+1)*1.01D0
C     -----------------------------------------------------------------------
 92   IF( MXREPR .LE. 0 ) THEN
C        NOMBRE MAXIMAL DE REPRISES ATTEINT
         NBROOT = K
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10092) NBROOT
         ELSE
            WRITE(IMPRIM,20092) NBROOT
         ENDIF
10092    FORMAT('MAXIMUM DE REPRISES ATTEINT: ABANDON AVEC',I5,
     %   ' VALEURS PROPRES CALCULEES'/)
20092    FORMAT('MAXIMUM of RETAKING REACHED: EXIT with',I5,
     %   ' COMPUTED EIGENVALUES'/)
         GOTO 9000
      ENDIF
C
C     UNE NOUVELLE REPRISE DU CALCUL
      MXREPR = MXREPR - 1
      NUINVP = MOD( NUINVP, 9 ) + 1
      IF( NOVR2 .GT. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10090) NOVR2, R2
         ELSE
            WRITE(IMPRIM,20090) NOVR2, R2
         ENDIF
      ENDIF
C
C     LE NOMBRE DE VALEURS PROPRES ACCEPTEES
      NURAC = K
C
C     LE NOMBRE DES ITERATIONS EST REMIS A ZERO
      NURAC = NURAC + 1
      DO 96 J=NURAC,MXROOT
         NINV(J) = 0
 96   CONTINUE
      GOTO 30
C
C     PAS DE VALEUR PROPRE OUBLIEE
C     ----------------------------
 9000 NBROOT = MIN( NBROOT, NBROOT0 )
      IF( LANGAG .EQ. 0 ) THEN
         WRITE (IMPRIM,19000) (NINV(J),J=1,NBROOT)
      ELSE
         WRITE (IMPRIM,29000) (NINV(J),J=1,NBROOT)
      ENDIF
19000 FORMAT(
     %/'NOMBRE D''ITERATIONS INVERSES POUR CHAQUE VALEUR PROPRE:'
     %/(1X,10I8))
29000 FORMAT(
     %/'NUMBER of INVERSE ITERATIONS for EACH EIGENVALUE:'
     %/(1X,10I8))
C
      RETURN
      END
