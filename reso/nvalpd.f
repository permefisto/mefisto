      SUBROUTINE NVALPD( NTDL,   BORNE,  EPS, TRA,
     +                   MORAID, RAID,
     +                   NCODSA, MUA,    A,
     +                   NCODSB, MUB,    B,
     +                   NVALPI, NBHOMO, DET )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C  BUT : CALCUL DU NOMBRE NVALPI DE VALEURS PROPRES INFERIEURES A BORNE
C  ---   METHODE DU RECENSEMENT DES COEFFICIENTS DIAGONAUX NEGATIFS
C
C ENTREES:
C --------
C NTDL   : NOMBRE DE LIGNES DE LA MATRICE A
C BORNE  : BORNE AU DESSOUS DE LAQUELLE ON CALCULE
C          LE NOMBRE DE VALEURS PROPRES
C EPS    : SEUIL DE PRECISION DE LA FACTORISATION
C TRA    : FACTEUR D HOMOTHETIE DE BORNE EN CAS DE FACTORISATION
C          AU DESSOUS DU SEUIL DE PRECISION
C MORAID : NOMBRE DE MOTS DE LA MATRICE RAID
C RAID   : MATRICE PROFIL AUXILIAIRE
C NCODSA : CODE DE STOCKAGE DE LA MATRICE PROFIL A
C MUA    : POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE A
C A      : MATRICE PROFIL REELLE DOUBLE PRECISION
C NCODSB : CODE DE STOCKAGE DE LA MATRICE PROFIL B
C MUB    : POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE B
C B      : MATRICE PROFIL REELLE DOUBLE PRECISION
C
C SORTIES:
C --------
C NVALPI : NOMBRE DE VALEURS PROPRES INFERIEURES A BORNE
C NBHOMO : NOMBRE D HOMOTHETIES LORS DU CALCUL DU DETERMINANT
C DET    : DETERMINANT DE A - BORNE * B
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C PROGRAMMEUR  : A.PERRONNET ANALYSE NUMERIQUE PARIS        JANVIER 1979
C MODIFICATION : A.PERRONNET ANALYSE NUMERIQUE PARIS ET INRIA  JUIN 1989
C....67..............................................................012
      include"./incl/langue.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      DOUBLE PRECISION  A(*),B(*),DET,TRA,BORNE
      INTEGER           MUA(*),MUB(*)
C
C     COPIE DE LA MATRICE INITIALE DANS A
    1 CALL TRTATA( RAID, A, MORAID )
C
C     A => A - BORNE * B
      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'L*D*tL FACTORISATION de K +',-BORNE,' M'
      ELSE
         WRITE(IMPRIM,*) 'L*D*Lt FACTORIZATION of K +',-BORNE,' M'
      ENDIF
      CALL MUA2PD( NTDL,   1D0, NCODSA, MUA, A,
     +                  -BORNE, NCODSB, MUB, B,
     +                                  MUA, A )
C
C     A = L * D * TL
      CALL CRMC1D( MUA,A,NTDL,EPS,0,A, NRETOU )
      IF( NRETOU .NE. 0 ) THEN
          BORNE = BORNE * TRA
          GOTO 1
      ENDIF
C
C     CALCUL DU DETERMINANT ET DU NOMBRE DE VALEURS PROPRES < BORNE
      CALL DETETD( A,MUA,NTDL, NVALPI,NBHOMO,DET )
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) NVALPI,' VALEURS PROPRES avant',BORNE
      ELSE
         WRITE(IMPRIM,*) NVALPI,' EIGENVALUES before',BORNE
      ENDIF
      END
