      SUBROUTINE DIAGNE( NTDL, PTDIAG, AG,  NBDINE, DIAMIN, DIAMAX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE NOMBRE DE COEFFICIENTS DIAGONAUX NEGATIFS
C -----    D'UNE MATRICE EF PROFIL OU MORSE
C
C ENTREES:
C --------
C NTDL   : NOMBRE DE DEGRES DE LIBERTE OU LIGNES DE LA MATRICE
C PTDIAG : POINTEUR SUR LE NO DES COEFFICIENTS DIAGONAUX
C AG     : MATRICE STOCKEE SOUS FORME PROFIL ou MORSE
C
C SORTIE :
C --------
C NBDINE : NOMBRE DE COEFFICIENTS NEGATIFS
C DIAMIN : COEFFICIENT DIAGONAL MINIMUM
C DIAMAX : COEFFICIENT DIAGONAL MAXIMUM
C23456---------------------------------------------------------------012
C MODIFS: ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Decembre 2008
C23456---------------------------------------------------------------012
      INTEGER           PTDIAG(0:NTDL)
      DOUBLE PRECISION  AG(*), D, DIAMAX, DIAMIN
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
C
      NBDINE = 0
      DIAMAX = -1D100
      DIAMIN = 1D100
      WRITE(IMPRIM,*)
      WRITE(IMPRIM,*) 'SP DIAGNE: DIAGONALE DE AG'
C
      DO 10 I=1,NTDL
C        VALEUR DU I-eme COEFFICIENT DIAGONAL
         D = AG( PTDIAG(I) )
         IF( D .LT. 0D0 ) THEN
            WRITE(IMPRIM,*)'SP DIAGNE: AG(',I,',',I,')=AG(',
     %                      PTDIAG(I),')=',D,' <0...'
            NBDINE = NBDINE + 1
         ENDIF
         DIAMIN = MIN( DIAMIN, D )
         DIAMAX = MAX( DIAMAX, D )
 10   CONTINUE
C
      WRITE(IMPRIM,*) 'NOMBRE COEFFICIENTS DIAGONAUX<0 NBDINE=',NBDINE
      WRITE(IMPRIM,*) 'MIN COEF DIAG=',DIAMIN, '  MAX COEF DIAG=',DIAMAX
      WRITE(IMPRIM,*)
C
      RETURN
      END
