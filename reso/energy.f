      SUBROUTINE ENERGY( NTDL, NDSM, NORESO, NCODSK, LPLI, LPCO, AG,
     %                   VECT, AUX,  ENERGI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES NDSM ENERGIES par ( [AG] * VECTEUR, VECTEUR )
C -----
C
C ENTREE :
C --------
C NTDL   : NOMBRE DE DL DU VECTEUR TEMPERATURE
C NDSM   : NOMBRE DE VECTEURS TEMPERATURES
C NORESO : CODE RESOLUTION ET MODE DE STOCKAGE DU SYSTEME LINEAIRE
C          1 FACTORISATION DE CROUT SUR UNE MATRICE AG PROFIL
C          2 GRADIENT CONJUGUE SIMPLE STOCKAGE MATRICE AG MORSE

C LPLI   : POINTEUR SUR LA DIAGONALE DE LA MATRICE PROFIL AG
C MNLPCO : NUMERO DE COLONNE DES COEFFICIENTS DE LA MATRICE MORSE AG
C AG     : MATRICE PROFIL
C VECT   : VECTEURS( NTDL, NDSM )
C AUX    : TABLEAU AUXILIAIRE DE NTDL DOUBLE PRECISION
C
C SORTIE :
C --------
C ENERGI : ENERGI( NDSM )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET  ANALYSE NUMERIQUE UPMC PARIS         MAI 2006
C MODIFS: ALAIN PERRONNET  LJLL UPMC & St PIERRE du PERRAY NOVEMBRE 2013
C23456---------------------------------------------------------------012
      DOUBLE PRECISION AG(*), VECT(NTDL,NDSM), ENERGI(NDSM), AUX(NTDL)
      DOUBLE PRECISION PROSCD
      INTEGER          LPLI(0:NTDL), LPCO(1:*)

      DO J=1,NDSM

         IF( NORESO .EQ. 1 ) THEN

C           PRODUIT DE LA MATRICE PROFIL AG PAR LE VECTEUR VECT
            CALL MAPRVE( 0, 1D0, NTDL,
     %                   NCODSK, LPLI,  AG, VECT(1,J),
     %                   AUX )

         ELSE

C           PRODUIT DE LA MATRICE MORSE AG PAR LE VECTEUR VECT
            CALL MAGCVE( 0, 1D0, NTDL,
     %                   NCODSK, LPLI, LPCO, AG, VECT(1,J),
     %                   AUX )

         ENDIF

C        PRODUIT SCALAIRE ( [AG] VECTEUR, VECTEUR )
         ENERGI( J ) = PROSCD( AUX, VECT(1,J), NTDL )
C
      ENDDO
C
      RETURN
      END
