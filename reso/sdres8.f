      SUBROUTINE SDRES8(A,MU,NBNOSD,NUNOIN,NUNOSD,POIDL,POIDS,NBINCO)
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : CALCUL DES POIDS DE NOEUDS DE L'INTERFACE
C -----
C
C ENTREES :
C ---------
C A      : LA MATRICE ASSOCIEE AU SOUS-DOMAINE
C MU     : SON POINTEUR EN STRUCTURE PROFIL OU CONDENSEE
C NBNOSD : NOMBRE DE NOEUDS DE L'INTERFACE
C NUNOIN : LES NUMEROS DE CES NOEUDS DANS L'INTERFACE
C NUNOSD : LES NUMEROS DE CES NOEUDS DANS LE SOUS-DOMAINE
C NBOBNO : LE NOMBRE DE SOUS-DOMAINES CONTENANT CE NOEUD
C NBINCO : LE  NOMBRE D'INCONNUES EN CHAQUE NOEUD
C
C SORTIES :
C ---------
C POIDL  : LE POIDS D'UN NOEUD DE L'INTERFACE DANS LE SOUS-DOMAINE
C POIDS  : LE POIDS D'UN NOEUD DE L'INTERFACE DANS LE DOMAINE COMPLET
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PASCAL JOLY     ANALYSE NUMERIQUE UPMC PARIS  MAI 1990
C23456---------------------------------------------------------------012
C
      DIMENSION NUNOIN(NBNOSD),NUNOSD(NBNOSD),MU(0:NTDL)
      DOUBLE PRECISION A(1),POIDL(1),POIDS(1)
      COMMON / SOUSDO / NORESO,NTDL,NDSM,NDIM,NPIMAX
C
C     CONTRIBUTION DES NOEUDS DU SOUS-DOMAINE
C     =======================================
C
      DO 1 NB = 1 , NBNOSD
         NUNOK = NBINCO * ( NUNOSD(NB) - 1 )
         NBK   = NBINCO * ( NUNOIN(NB) - 1 )
         DO 2 K  = 1 , NBINCO
            POIDL(NBK+K) = A(MU(NUNOK+K))
            POIDS(NBK+K) = POIDS(NBK+K) +  POIDL(NBK+K)
 2       CONTINUE
 1    CONTINUE
C
      RETURN
      END
