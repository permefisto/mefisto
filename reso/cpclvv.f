      SUBROUTINE CPCLVV( NTDL, NBDLIB, NODLIB,
     %                   EIGMIN, EIGMAX,
     %                   NC0, VAL0, VECT0,
     %                   NC1, NB, VAL1, VECT1 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRANSPOSER LES NC0 PREMIERS VECTEURS PROPRES VECT0(NTDL,NC0)
C -----    DE VALEURS PROPRES COMPRISES ENTRE EIGMIN ET EIGMAX
C          DANS LE TABLEAU VECT1(NC1,NTDL) ET SUPPRIMER LES DL FIXES
C
C ENTREES:
C --------
C NTDL   : NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR VECT0
C NBDLIB : NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR VECT1
C NODLIB : NODLIB(I) = NUMERO DE 1 A NBDLIB DU DL I (1 A NTDL) LIBRE
C                    -(INDICE DANS NODLFX) SI LE DL I EST BLOQUE
C          PAS DE TABLEAU SI NBDLIB=NTDL
C
C EIGMIN : BORNE INFERIEURE DES VALEURS PROPRES A CALCULER
C EIGMAX : BORNE SUPERIEURE DES VALEURS PROPRES A CALCULER
C
C NC0    : NOMBRE DE VECTEURS PROPRES DE VAL0 ET VECT0
C VAL0   : NC0 VALEURS PROPRES
C VECT0  : TABLEAU(NTDL,NC0) LES COMPOSANTES DES VECTEURS PROPRES
C          AVANT SUPPRESSION DES DEGRES DE LIBERTE BLOQUES
C NC1    : NOMBRE DE VECTEURS PROPRES DECLARES DANS VAL1 ET VECT1
C
C SORTIES:
C --------
C NB     : LE NOMBRE DE VALEURS PROPRES COMPRISES ENTRE EIGMIN ET EIGMAX
C          ET DE VECTEURS INITIALISES
C VAL1   : LES VALEURS PROPRES COMPRISES ENTRE EIGMIN ET EIGMAX
C VECT1  : LES COMPOSANTES DES VECTEURS PROPRES
C          APRES SUPPRESSION DES DEGRES DE LIBERTE BLOQUES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET  ANALYSE NUMERIQUE UPMC PARIS     JUILLET 2003
C23456---------------------------------------------------------------012
      REAL              VAL0(NC0)
      DOUBLE PRECISION             VECT0(NTDL,NC0),
     +                  VAL1(NC1), VECT1(NC1,NBDLIB)
      DOUBLE PRECISION  EIGMIN, EIGMAX
      INTEGER           NODLIB(NTDL)
C
      NB = 0
      IF( NTDL .EQ. NBDLIB ) THEN
C
C        PAS DE DEGRE DE LIBERTE FIXE: TRANSPOSITION SIMPLE
         DO 20 J=1,NC0
            IF( EIGMIN .LE. VAL0(J) .AND. VAL0(J) .LE. EIGMAX ) THEN
               NB = NB + 1
               VAL1( NB ) = VAL0( J )
               DO 10 N=1,NBDLIB
                  VECT1( NB, N ) = VECT0( N, J )
 10            CONTINUE
            ENDIF
 20      CONTINUE
C
      ELSE
C
C        SUPPRESSION DES NBDLFX DL BLOQUES EN NBDLIB DL LIBRES
         DO 50 J=1,NC0
            IF( EIGMIN .LE. VAL0(J) .AND. VAL0(J) .LE. EIGMAX ) THEN
               NB = NB + 1
               VAL1( NB ) = VAL0( J )
               DO 40 N = 1, NTDL
C                 LE NUMERO DU DL LIBRE OU BLOQUE
                  NODL = NODLIB( N )
                  IF( NODL .GT. 0 ) THEN
C                    LE DL LIBRE N DANS VECT0 EST LE DEGRE NODL DANS VECT1
                     VECT1( NB, NODL ) = VECT0( N, J )
                  ENDIF
 40            CONTINUE
            ENDIF
 50      CONTINUE
C
      ENDIF
      END
