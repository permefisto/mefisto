      SUBROUTINE REDOFL( NBNOEF, NDIM,   XYZEF,
     %                   NOOBVC, NUMIVO, NUMAVO, LTDEVO,
     %                   Rho,    Mhu,    CoGrPr, CoBOUS )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RECUPERER LES DONNEES DU FLUIDE SUPPOSEES CONSTANTES
C -----

C ENTREES:
C --------
C NYOBJT : NUMERO DU TYPE D'OBJET (1:POINT, 2:LIGNE, 3:SURFACE, 4:VOLUME )
C NBNOEF : NOMBRE DE SOMMETS D'UN EF
C XYZEF  : 3 COORDONNEES DES NBNOEF SOMMETS DE L'EF
C NOOBVC : NUMERO DE VOLUME DU TETRAEDRE
C NUMIVO : NUMERO MINIMAL DES VOLUMES DE L'OBJET
C NUMAVO : NUMERO MAXIMAL DES VOLUMES DE L'OBJET
C LTDEVO : ADRESSE MCN DES TABLEAUX DES DONNEES DU FLUIDE

C SORTIES:
C --------
C Rho    : DENSITE VOLUMIQUE DE MASSE DU FLUIDE
C Mhu    : VISCOSITE DYNAMIQUE DU FLUIDE
C CoGrPr : COEFFICIENT du GRADIENT de PRESSION  (=1 PAR DEFAUT)
C CoBOUS : COEFFICIENT DE PROPORTIONNALITE DE LA DIFFERENCE DE
C          TEMPERATURE A LA VARIATION DE LA MASSE VOLUMIQUE
C          de l'APPROXIMATION de BOUSSINESQ
C          Rho0-Rho = Rho CoBOUS (T-T0)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray Novembre 2010
C MODIFS : ALAIN PERRONNET St Pierre du Perray                Avril 2022
C23456---------------------------------------------------------------012
      include"./incl/donflu.inc"
      INTEGER           LTDEVO(1:MXDOFL,NUMIVO:NUMAVO)
      REAL              XYZEF(NBNOEF,NDIM)
      DOUBLE PRECISION  Rho, Mhu, CoGrPr, CoBOUS, XYZD(1:3), S

C     NYOBJT : NUMERO DU TYPE D'OBJET (1:POINT, 2:LIGNE, 3:SURFACE, 4:VOLUME )
      NYOBJT = NDIM + 1

C     BARYCENTRE DE L'EF
      DO K=1,NDIM
         S = 0D0
         DO N=1,NBNOEF
            S = S + XYZEF(N,K)
         ENDDO
         XYZD(K) = S / NBNOEF
      ENDDO
      IF( NDIM .LT. 3 )  XYZD(3) = 0D0

C     RECHERCHE DE LA DENSITE DE MASSE Rho AU BARYCENTRE DE L'EF
      CALL REMASS2( NYOBJT, NOOBVC, XYZD(1), XYZD(2), XYZD(3),
     %              LTDEVO(LPMASS,NOOBVC), Rho )

C     RECHERCHE DE LA VISCOSITE Mhu AU BARYCENTRE DU TETRAEDRE
      CALL REVISC( NYOBJT, NOOBVC, XYZD(1), XYZD(2), XYZD(3),
     %             LTDEVO(LPVISC,NOOBVC), Mhu )

C     RECHERCHE DU COEFFICIENT SUR LE GRADIENT DE LA PRESSION
      IF( LTDEVO(LPCPRE,NOOBVC) .GT. 0 ) THEN
C        IL A ETE DECLARE UN COEFFICIENT DEVANT LA PRESSION
         CALL RECPRE( NYOBJT, NOOBVC, XYZD(1), XYZD(2), XYZD(3),
     %                LTDEVO(LPCPRE,NOOBVC), CoGrPr )
      ELSE
C        PAS DE COEFFICIENT DEVANT LE GRADIENT DE LA PRESSION => =1
         CoGrPr = 1D0
      ENDIF

C     RECHERCHE DU COEFFICIENT DE PROPORTIONNALITE de BOUSSINESQ
      IF( LTDEVO(LPCBOU,NOOBVC) .GT. 0 ) THEN
C        IL EXISTE UN COEFFICIENT
         CALL RECBOU( NTYOBJ, NOOBVC, XYZD(1), XYZD(2), XYZD(3),
     %                LTDEVO(LPCBOU,NOOBVC), CoBOUS )
      ELSE
C        IL N'EXISTE PAS DE COEFFICIENT
         CoBOUS = 0D0
      ENDIF

      RETURN
      END
