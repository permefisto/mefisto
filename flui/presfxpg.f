      SUBROUTINE PRESFXPG( NBRDLX, NDLX, NTDLPR, NBCOPG, LPLIGN,  PG )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    PRISE EN COMPTE SUR LA MATRICE GLOBALE DE PRESSION DES DL
C -----    DE PRESSION FIXES ( 1 SUR LA DIAGONALE et 0 sur la ligne )
C
C ENTREES:
C --------
C NBRDLX : NOMBRE DE DL PRESSION FIXEES
C          ERREUR SI NBRDLX=0 CAR PAS DE CONDITION AUX LIMITES!
C NDLX   : TABLEAU MC DES NUMEROS DES DL FIXES
C
C NTDLPR : NOMBRE TOTAL DE DEGRES DE LIBERTE DES PRESSIONS
C NBCOPG : NOMBRE DE COEFFICIENTS DE LA MATRICE GLOBALE PG
C LPLIGN : POINTEUR SUR LES COEFFICIENTS DIAGONAUX DE PG
C
C MODIFIES:
C ---------
C PG     : VALEUR DES COEFFICIENTS DE LA MATRICE GLOBALE DES PRESSIONS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Novembre 2010
C23456---------------------------------------------------------------012
      INTEGER           NDLX(NBRDLX), LPLIGN(0:NTDLPR)
      DOUBLE PRECISION  PG(1:NBCOPG)
C
      DO K=1,NBRDLX
C
C        NUMERO DU DL FIXE
         NDL = NDLX( K )
C
         DO J = LPLIGN(NDL-1)+1, LPLIGN(NDL)
            PG(J) = 0D0
         ENDDO
C
C        IDENTITE SUR LE COEFFICIENT DIAGONAL
         PG( LPLIGN(NDL) ) = 1D0
C
      ENDDO
C
      RETURN
      END
