      SUBROUTINE VITEFXBG( NBVIFX, NDLFX, VITFIX, TGV,
     %                     VITCON, NDIM,  NBNOVI,   BG )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    PRISE EN COMPTE SUR LE VECTEUR GLOBAL DES COMPOSANTES
C -----    DES VITESSES FIXEES
C
C ENTREES:
C --------
C NBVIFX : NOMBRE DE DL VITESSE FIXEES
C          ERREUR SI NBVIFX=0 CAR PAS DE CONDITION AUX LIMITES!
C NDLFX  : TABLEAU MC DES NUMEROS DES DL FIXES
C VITFIX : TABLEAU MC DES VALEURS DES VITESSES FIXEES
C TGV    : VALEUR MULTIPLICATIVE  DES VITESSES FIXEES
C VITCON : VITESSE CONVECTEE AUX NBNOVI NOEUDS DU MAILLAGE
C
C NDIM   : NOMBRE DE COMPOSANTES DE LA VITESSE
C NBNOVI : NOMBRE DE NOEUDS DE LA VITESSE DU MAILLAGE
C
C MODIFIE:
C --------
C BG     : VALEUR DES COEFFICIENTS DU VECTEUR GLOBAL VITESSE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Decembre 2012
C23456---------------------------------------------------------------012
      INTEGER           NDLFX(NBVIFX)
      DOUBLE PRECISION  VITFIX(NBVIFX), TGV,
     %                  VITCON(NBNOVI*NDIM), BG(NBNOVI*NDIM), VITFX

      DO K=1,NBVIFX

C        NUMERO DU DL FIXE DE 1 A NBNOVI*NDIM
         NDL = NDLFX( K )

C        DL DE VITESSE CONVECTEE (VITFX=1D222 VALEUR TEST)?
         VITFX = VITFIX(K)
         IF( VITFX .NE. 1D222 ) THEN

C           NON: VALEUR FIXEE DE LA VITESSE
            BG( NDL ) = VITFX * TGV

         ELSE

C           VITESSE CONVECTEE DEFINIE DANS VITCON
            BG( NDL ) = VITCON( NDL ) * TGV

         ENDIF

      ENDDO

      RETURN

cccC        NUMERO DE LA COMPOSANTE DE LA VITESSE
ccc         NUCOMP = 1
ccc 10      IF( NDL .GT. NBNOVI ) THEN
ccc            NUCOMP = NUCOMP + 1
ccc            NDL = NDL - NBNOVI
ccc            GOTO 10
ccc         ENDIF

      END
