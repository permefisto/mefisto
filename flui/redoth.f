     %                LTDEVO(LPCOBO,NOOBVC), CoBOUS )
      ELSE
C        IL N'EXISTE PAS DE DONNEE du COEFFICIENT CoBOUS
         CoBOUS = 0D0
      ENDIF


C     PAS de RECHERCHE DU TYPE de la VITESSE de TRANSPORT de la TEMPERATURE
