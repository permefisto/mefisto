         IF( NUTYEL .EQ. 15 ) THEN
C           TRIANGLE TAYLOR-HOOD
            CALL P12DAGPR( UnSRho, NBSOM,  MCN(MNXYZN+WYZNOE),
     %                     NBNOEF, NBELEM, MCN(MNELE+WUNDEL),
     %                     MCN(MNNOSO), NBCMPG, MCN(MNPTDP), PG )
         ELSE
C           TETRAEDRE TAYLOR-HOOD
            CALL P13DAGPR( UnSRho, NBSOM,  MCN(MNXYZN+WYZNOE),
     %                     NBNOEF, NBELEM, MCN(MNELE+WUNDEL),
     %                     MCN(MNNOSO), NBCMPG, MCN(MNPTDP), PG )
         ENDIF
ccc
ccc         call affvect( 'PG PROFIL ASSEMBLEE avec arrondis', 20, PG )
C
cccC        SUPPRESSION DES COEFFICIENTS D'ERREURS D'ARRONDIS
cccC        et MULTIPLICATION PAR  dt UnSRho
ccc         CALL PRARMU( 1D-4, S, NBCMPG, PG )
ccc         call affvect( 'PG PROFIL ASSEMBLE corrige', 20, PG )
C
C        PRISE EN COMPTE DES CONDITIONS AUX LIMITES DES PRESSIONS FIXEES
         DO I = 1, NBPRFX
C           LE NO GLOBAL DU DL FIXE
            NDL = MCN( MNDLPRFX - 1 + I )
C           LE NO DU COEFFICIENT DIAGONAL NDL
            NDIAG = MCN( MNPTDP + NDL )
C           MODIFICATION DU COEFFICIENT DIAGONAL
C           CETTE VALEUR PEUT SERVIR DE MARQUEUR D'UN DL FIXE POUR PG
            PG( NDIAG ) = STGV
         ENDDO
ccc         call affvect( 'PG PROFIL AVEC CL', 20, PG )
C
C        FACTORISATION COMPLETE DE CHOLESKY  PG = L tL
         CALL CHOLPR( NBSOM, NCODSP, MCN(MNPTDP), PG,  PG, IERR )
ccc         call affvect( 'PG=L tL PROFIL', 20, PG )
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        ASSEMBLAGE MORSE UnSRho ( GRAD p, GRAD q ) INTERPOLATION P1
C        DE LA MATRICE GLOBALE DE LA PRESSION
C        RESOLUTION PAR GRADIENT CONJUGUE SIMPLE SANS PRECONDITIONNEMENT
C        ---------------------------------------------------------------
         IF( NUTYEL .EQ. 15 ) THEN
C           TRIANGLE TAYLOR-HOOD
            CALL P12DAGGC( UnSRho, NBSOM,  MCN(MNXYZN+WYZNOE),
     %                   NBNOEF, NBELEM, MCN(MNELE+WUNDEL), MCN(MNNOSO),
     %                   NBCMPG, MCN(MNPTDP), MCN(MNLPCP), PG )
         ELSE
C           TETRAEDRE TAYLOR-HOOD
            CALL P13DAGGC( UnSRho, NBSOM,  MCN(MNXYZN+WYZNOE),
     %                   NBNOEF, NBELEM, MCN(MNELE+WUNDEL), MCN(MNNOSO),
     %                   NBCMPG, MCN(MNPTDP), MCN(MNLPCP), PG )
         ENDIF
C
C        CONSTRUCTION DU TABLEAU des DL PRESSION FIXES 1 OU NON 0
         CALL TNMCDC( 'ENTIER', NBSOM, MNNDPFX )
C        A PRIORI TOUS LES DL SONT LIBRES => 0
         CALL AZEROI( NBSOM, MCN(MNNDPFX) )
C
C        MISE AU NUMERO DU DL FIXE DANS LES TABLEAUX
C        DES DL FIXES DE LA PRESSION
         DO I = 1, NBPRFX
C           LE NO GLOBAL DU DL PRESSION FIXEE
            NDL = MCN( MNDLPRFX - 1 + I )
C           MISE A I DU DL PRESSION FIXEE NDL
            MCN( MNNDPFX-1 + NDL ) = I
C           MODIFICATION DU COEFFICIENT DIAGONAL
C           LE NO DU COEFFICIENT DIAGONAL NDL
            NDIAG = MCN( MNPTDP + NDL )
C           CETTE VALEUR PEUT SERVIR DE MARQUEUR D'UN DL FIXE POUR PG
            PG( NDIAG ) = STGV
         ENDDO
ccc         call affvect( 'PG MORSE AVEC CL', 20, PG )
C
C        LES 4 TABLEAUX AUXILIAIRES du SOUS PROGRAMME Gcaxb
C        MNAUX1, MNAUX2, MNAUX3 : ADRESSES DE TABLEAUX AUXILIAIRES
         LOAUX = NBNOVI * 4
         CALL TNMCDC( 'REEL2', LOAUX, MNAUX1 )
         IF( MNAUX1 .LE. 0 ) GOTO 9990
         MNAUX2 = MNAUX1 + NBNOVI * MOREE2
         MNAUX3 = MNAUX2 + NBNOVI * MOREE2
         MNPRPR = MNAUX3 + NBNOVI * MOREE2
C
      ELSE IF( NORESO .EQ. 3 ) THEN
C
C        ASSEMBLAGE MORSE UnSRho ( GRAD p, GRAD q ) INTERPOLATION P1
C        RESOLUTION PAR GRADIENT CONJUGUE AVEC PRECONDITIONNEMENT
C        -----------------------------------------------------------
C        TETRAEDRE TAYLOR-HOOD
         CALL P13DAGGC( UnSRho, NBSOM,  MCN(MNXYZN+WYZNOE),
     %                  NBNOEF, NBELEM, MCN(MNELE+WUNDEL), MCN(MNNOSO),
     %                  NBCMPG, MCN(MNPTDP), MCN(MNLPCP), PG )
C
C        CONSTRUCTION DU TABLEAU des DL FIXES OU NON POUR LA PRESSION
         CALL TNMCDC( 'ENTIER', NBSOM, MNNDPFX )
C        A PRIORI TOUS LES DL SONT LIBRES => 0
         CALL AZEROI( NBSOM, MCN(MNNDPFX) )
C        MISE A 1 DES DL FIXES
         DO I = 1, NBPRFX
C           LE NO GLOBAL DU DL PRESSION FIXE
            NDL = MCN( MNDLPRFX - 1 + I )
C           MISE A I DU DL PRESSION FIXE NDL
            MCN( MNNDPFX-1 + NDL ) = I
         ENDDO
C
C        CALCUL DE LA MATRICE DE PRECONDITIONNEMENT DE [PG] STOCKAGE MORSE
         CALL MAPRCRGC( NBSOM,  NIVOGC, MNNDPFX,
     %                  NCODSP, MNPTDP, MNLPCV, PG,
     %                  MNLPLC, MNLPCC, MNLPLU, LOLPCC, MNPGC,
     %                  COMP,   IERR )
         IF( IERR .NE. 0 ) GOTO 9999
C
C        LES TABLEAUX AUXILIAIRES DE GCPRCR
C        MNAUX1, MNAUX2, MNAUX3 : ADRESSES DE TABLEAUX AUXILIAIRES
         LOAUX = NBNOVI * 4
         CALL TNMCDC( 'REEL2', LOAUX, MNAUX1 )
         IF( MNAUX1 .LE. 0 ) GOTO 9990
         MNAUX2 = MNAUX1 + NBNOVI * MOREE2
         MNAUX3 = MNAUX2 + NBNOVI * MOREE2
         MNPRPR = MNAUX3 + NBNOVI * MOREE2
C
C        DECLARATION DES TABLEAUX AUXILIAIRES DES DIRECTIONS DU GC
C        NBDIR  : NOMBRE DE DIRECTIONS >=1 POUR STABILISER LE GC
C        LODIR  : NOMBRE DE MOTS DES TABLEAUX POUR STOCKER LES DIRECTIONS
         NBDIR = 3
C        NOMBRE DE DIRECTIONS >=1 POUR STABILISER LE GC
         LODIR = (NBNOVI+1) * NBDIR * 2
         CALL TNMCDC( 'REEL2', LODIR, MNBDIR )
         IF( MNBDIR .LE. 0 ) GOTO 9990
C        MNBDIR, MNDIR, MNADIR, MNDAD, MNBET: ADRESSES DE TABLEAUX AUXILIAIRES
         MNDIR  = MNBDIR
         MNADIR = MNDIR  + NBNOVI * NBDIR * MOREE2
         MNDAD  = MNADIR + NBNOVI * NBDIR * MOREE2
         MNBET  = MNDAD  + NBDIR * MOREE2
C
C        NOMBRE TOTAL DE MOTS STOCKES POUR LE GC PRECONDITIONNE
         MOTSGC = NBNOVI + NBCMPG + (LOAUX+LODIR) * MOREE2
C
      ENDIF
C
C     ==============================================================
C     DECLARATION DE LA MATRICE VG D'UNE COMPOSANTE P2 DE LA VITESSE
C     MATRICE SYMETRIQUE NON DIAGONALE
C     Integrale 1/dt P2 P2 + Mhu/Rho grad P2 grad P2 dX
C     ==============================================================
      NCODSV = 1
C     NBCMVG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE VG
C     VERSION ALLOCATION de la MATRICE VG en LANGAGE FORTRAN 90
      WRITE(IMPRIM,*)
      WRITE(IMPRIM,*) 'ALLOCATION DEMAND  of',NBCMVG,
     %                ' DOUBLE PRECISION of the [VG] MATRIX'
      ALLOCATE ( VG(1:NBCMVG), STAT=IERVGALLOC )
      IF( IERVGALLOC .NE. 0 ) THEN
       WRITE(IMPRIM,*)'ALLOCATION ERROR   of',NBCMVG,
     %                ' DOUBLE PRECISION of the [VG] MATRIX'
         IERR = IERVGALLOC
         GOTO 9990
      ENDIF
      WRITE(IMPRIM,*) 'ALLOCATION CORRECT of',NBCMVG,
     %                ' DOUBLE PRECISION of the [VG] MATRIX'
      WRITE(IMPRIM,*)
C
C     CONSTRUCTION DE LA MATRICE D'UNE COMPOSANTE DE LA VITESSE
C     Integrale  ( 1/dt P2 P2 + Mhu/Rho Grad P2 Grad P2 ) dX
C     avec INTEGRATION EXACTE SUR L'ELEMENT FINI
C     =========================================================
      CALL AZEROD( NBCMVG, VG )
C
C     RECUPERATION DES TABLEAUX POBA NECESSAIRES A LA
C     CONSTRUCTION DES TABLEAUX ELEMENTAIRES
C     RETRIEVE the ARRAYS of BASIS POLYNOMIAL VALUES at
C     NUMERICAL QUADRATURE FORMULA POINTS
      CALL TAPOBA( NBTYEL, MNNPEF, ETTAEL,
     %             MNTPOB, NBDLM0, MOAUX, NBTTEF, NOAXIS, I, IERR )
      IF( IERR .NE. 0 ) GOTO 9990
C
C     RECHERCHE DU TABLEAU DE POBA ET PARTAGE EN P ET DP
      L = MNTPOB
C
C     SAUT DU TABLEAU POUR LES EF 2D DE SURFACE POUR CONDITIONS AUX LIMITES
C     LES VALEURS DES POLYNOMES DE L'EF DE DIMENSION TOTALE C-A-D
C     EN 3D sur le TETRAEDRE DE REFERENCE RECTANGLE UNITE
      L  = L + 1
      IA = MCN( L )
C     DIMENSION DE L ESPACE
C     NDIM   = MCN( IA )
C     NOMBRE DE POLYNOMES D INTERPOLATION
      NBPOLY = MCN( IA + 1 )
C     NOMBRE DE POINTS D INTEGRATION NUMERIQUE
      NPI    = MCN( IA + 2 )
C     ADRESSE DES POIDS DES POINTS DE LA FORMULE D INTEGRATION
      MNPOID = IA + 8
C     ADRESSE DES VALEURS DES POLYNOMES AUX POINTS D INTEGRATION
      MNPOL  = MNPOID + MCN( IA + 3 ) * MOREE2 * NPI
C     ADRESSE DES VALEURS DES DERIVEES DES POLYNOMES AUX POINTS D'INTEGRATION
      MNDPOL = MNPOL + MCN( IA + 4 ) * MOREE2 * NBPOLY * NPI
C
C     CALCUL DE L'INTEGRALE P2 DP2 dx dy dz
C     POUR L'EF de TAYLOR-HOOD en FLUIDE de REFERENCE
      CALL TNMCDC( 'REEL2', 10*3*10, MNP2DP2 )
      CALL INTPDP( NPI, MCN(MNPOID), NDIM, NBPOLY,
     %             MCN(MNPOL), MCN(MNDPOL),  MCN(MNP2DP2))
C
C     CALCUL DE L'INTEGRALE DP2 DP2 dx dy dz
C     POUR L'EF de TAYLOR-HOOD en FLUIDE de REFERENCE
      CALL TNMCDC( 'REEL2', 3*10*3*10, MNDP2DP2 )
      CALL INTDPDP( NPI, MCN(MNPOID), NDIM, NBPOLY, MCN(MNDPOL),
     %              MCN(MNDP2DP2))
C
C     CONSTRUCTION DE LA MATRICE VG D'UNE COMPOSANTE DE LA VITESSE
C     Integrale  ( P2 P2 / dt + Mhu / Rho Grad P2 Grad P2 ) dX
C     avec INTEGRATION EXACTE SUR L'ELEMENT FINI
C     ============================================================
      CALL AGVITTH( UnSdt, MhuSRho, MCN(MNXYZN+WYZNOE),
     %              NUTYEL, NBNOEF, NBELEM, MCN(MNELE+WUNDEL),
     %              NORESO, NBNOVI, NBCMVG, MCN(MNPTDV),MCN(MNLPCV), VG)

C     ==========================================================================
C     PRISE EN COMPTE DES CONDITIONS AUX LIMITES DES VITESSES FIXEES SUR VG
C     ATTENTION: CETTE PROGRAMMATION SUPPOSE QUE TOUS LES DL EN UN NOEUD
C                SONT FIXES ET LES NOEUDS FIXES RESTENT LES MEMES POUR TOUT TEMP
C                MAIS PAS FORCEMENT LA VITESSE A FIXER QUI ELLE DEPEND du TEMPS
C     ==========================================================================
C     LISTE DES DL VITESSES FIXEES EN TOUT TEMPS
      CALL VITEFX( RELMIN, NTDLVI, NDIM,   MNXYZN,
     %             NBTYEL, MNNPEF, NUMIOB, MNDOEL,
     %             NBVCFX, NBVIFX, MNDLVIFX, MNVAVIFX, VITFXMAX )
C
      IF( NORESO .EQ. 2 ) THEN
C
C        CONSTRUCTION DU TABLEAU des DL 1 VITESSE FIXE I OU NON 0
C        --------------------------------------------------------
         CALL TNMCDC( 'ENTIER', NBNOVI, MNNDVFX )
C        A PRIORI TOUS LES DL SONT LIBRES => 0
         CALL AZEROI( NBNOVI, MCN(MNNDVFX) )
C        MISE AU NUMERO DU DL FIXE DANS LES TABLEAUX VIFX
C        DES DL FIXES DE LA 1-ERE COMPOSANTE DE LA VITESSE
         DO I=1, NBVIFX/NDIM
C           LE NO GLOBAL DU DL 1VITESSE FIXEE
            NDL = MCN( MNDLVIFX - 1 + I )
C           MISE A I DU DL 1VITESSE FIXEE NDL
            MCN( MNNDVFX-1 + NDL ) = I
         ENDDO
C
      ENDIF
C
C     PRISE EN COMPTE DE LA VITESSE FIXEE SUR LA MATRICE VG
C     -----------------------------------------------------
      DO I = 1, NBVIFX/NDIM
C        LE NO GLOBAL DU DL FIXE DE LA PREMIERE COMPOSANTE DE LA VITESSE
         NDL = MCN( MNDLVIFX - 1 + I )
C        LE NO DU COEFFICIENT DIAGONAL NDL
         NDIAG = MCN( MNPTDV + NDL )
C        MODIFICATION DU COEFFICIENT DIAGONAL
C        CETTE VALEUR PEUT SERVIR DE MARQUEUR D'UN DL FIXE POUR VG
         VG( NDIAG ) = STGV
      ENDDO
C
      IF( NORESO .EQ. 1 ) THEN
C
C        FACTORISATION COMPLETE DE CHOLESKY  VG = L tL SUR ELLE-MEME
C        -----------------------------------------------------------
         CALL CHOLPR( NBNOVI, NCODSV, MCN(MNPTDV), VG,  VG, IERR )
C
      ENDIF
C
C     TEMPS CALCUL DE FORMATION DES MATRICES GLOBALES ET FACTORISATIONS
      DFACTO = DINFO( 'DELTA CPU' )
C
C     DECLARATIONS DES VECTEURS GLOBAUX
C     =================================
C     DECOMPOSITION DU VECTEUR V3P0 SOLUTION AU TEMPS 0 EN 4 VECTEURS
C     VXYZP0 => VITX0, VITY0, VITZ0, PRES0
C     LE VECTEUR V3P0 SOLUTION AU TEMPS n+1,m (VX,VY,VZ,Pr)
      NBDV3P0 = (NDIM+1) * NBNOVI * 3
      CALL TNMCDC( 'REEL2', NBDV3P0, MNV3P00 )
      MNV3P0  = MNV3P00
      MNVITX0 = MNV3P0
      MNVITY0 = MNVITX0 + MOREE2 * NBNOVI
      MNVITZ0 = MNVITY0 + MOREE2 * NBNOVI
      MNPRES0 = MNVITX0 + MOREE2 * NBNOVI * NDIM
C
C     LE VECTEUR V3P1 SOLUTION AU TEMPS n+1,m+1 (VX,VY,VZ,Pr)
      MNV3P1  = MNV3P0 + MOREE2 * NBNOVI * (NDIM+1)
C
C     LE VECTEUR V3tn SOLUTION AU TEMPS n (VX,VY,VZ,Pr)
      MNV3Ptn = MNV3P1 + MOREE2 * NBNOVI * (NDIM+1)
C
C     MNVXYZP0 = en chaque NOEUD VX,VY,VZ + si MILIEU Pr
C     MNVITX0  = VX(NOEUDS), VY(NOEUDS), VZ(NOEUDS), Pr(SOMMETS)
C     SI TAYLOR-HOOD LA PRESSION AU MILIEU DES ARETES EST CALCULEE
C     EN FAIT DANS V3P1 PUIS SUPPRIME DANS LES INSTRUCTIONS SUIVANTES
C     LE DEBORDEMENT AU DELA DES NTDLVP DL EST AINSI CONTROLE
      CALL VXVYVZPR( NDIM, NUTYEL, NBELEM, NBNOEF, MCN(MNELE+WUNDEL),
     %               MCN(MNNDDL), NTDLVP, 1, MCN(MNVXYZP0),
     %               NBNOVI, MCN(MNVITX0), MCN(MNVITY0), MCN(MNVITZ0),
     %               NBNOVI, MCN(MNV3P1) )
C
C     LA PRESSION0 EST REORDONNEE EN LES SEULS SOMMETS DE 1 A NBSOM
      MNV = ( MNV3P1  - 1 ) / MOREE2
      MNP = ( MNPRES0 - 1 ) / MOREE2
      DO I = 1, NBSOM
C        LE NUMERO N DE NOEUD DU SOMMET I
         N = MCN(MNSONO-1+I)
C        LA PRESSION0 AU NOEUD N EST PLACE AU SOMMET I
         DMCN( MNP + I ) = DMCN( MNV + N )
      ENDDO
C
C     TABLEAU AUXILIAIRE POUR LE GRADIENT CONJUGUE
      IF( NORESO .GT. 1 ) THEN
         CALL TNMCDC( 'REEL2', NTDLVI, MNV3P2 )
         IF( MNV3P2 .LE. 0 ) GOTO 9990
      ENDIF
C
C     L'ADRESSE DE LA VITESSEPRESSION A L'ITERATION 0 DU POINT FIXE
C     NON UTILISE ICI PUISQUE PROBLEME LINEAIRE
C     LA NON LINEARITE EST PRISE EN COMPTE PAR L'INTEGRATION RETROGRADE
C     EN TEMPS LE LONG DES CARACTERISTIQUES
ccc      MNVITI = MNVXYZP0
C
C     FLUX DE LA VITESSE SUR LES SURFACES DE L'OBJET POUR UN TEMPS DONNE
      MOSFOB = NUMAOB(3) - NUMIOB(3) + 1
      CALL TNMCDC( 'REEL2', MOSFOB, MNFVSF )
C
C     BILAN DE L'INSTANT INITIAL
C     ==========================
C     LE PROCHAIN TEMPS POUR STOCKER LE VECTEUR VITESSEPRESSION
      TSTOC  = TPSINI + DTSTOC
      MNFLUI = MNVECT + WECTEU + MOREE2 * NTDLVP * (NBFLUI-1)
      MNTIME = MNVECT + WECTEU + MOREE2 * NTDLVP * MXFLUI - 1
C     LE DEBUT DU VECTEUR VITESSEPRESSION INITIALE
ccc      MNVITI = MNFLUI
C
C     CREATION OU AGMENTATION DU TMS 'VECTEUR"MAXVITESSEPRESS'
C     |VITESSE|Moyen, |VITESSE|Max, Max-MinPression Temps SONT STOCKES
C     A CHAQUE PAS DE TEMPS CALCULE pour TRACE des COURBES(Temps)
C     ----------------------------------------------------------------
      MXPAST = INT( ( TPSFIN - TPSINI ) / DT ) + 2
      NBPAST0= 0
      NBMXVP = 3
      NBPAST = MXPAST
      CALL LXTSOU( NTLXOB, 'VECTEUR"MAXVITESSEPRESS', NTMXVP, MNMXVP )
C
      IF( NTMXVP .GT. 0 ) THEN
C        NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR =? 3 MOYVIT, MAXVIT, MX-MIPRESS
         NBMXVP0 = MCN(MNMXVP+WBCOVE)
C        NOMBRE DE VECTEURS MAX VITESSE PRESSION = NOMBRE DE PAS DE TEMPS CALCUL
         NBPAST0 = MCN(MNMXVP+WBVECT)
         IF( NBMXVP0 .NE. NBMXVP .OR. NBFLUI .LE. 1 ) THEN
C           DESTRUCTION DU TMS
            CALL LXTSDS( NTLXOB, 'VECTEUR"MAXVITESSEPRESS' )
            NBPAST0 = 0
         ELSE
C           CE TABLEAU EST AUGMENTE DE MXPAST VECTEURS
            NBPAST = NBPAST0 + MXPAST
            MOTSVP = WECTEU + MOREE2 * NBMXVP * NBPAST + NBPAST
            CALL TAMSAU( NTMXVP, MOTSVP )
C           IL EST REOUVERT CAR ADRESSE MCN MODIFIEE
            GOTO 13
         ENDIF
      ENDIF
C
C     CONSTRUCTION DU TMS 'VECTEUR"MAXVITESSEPRESS'
      MOTSVP = WECTEU + MOREE2 * NBMXVP * NBPAST + NBPAST
      CALL LXTNDC( NTLXOB, 'VECTEUR"MAXVITESSEPRESS', 'REEL', MOTSVP )
C
 13   CALL LXTSOU( NTLXOB, 'VECTEUR"MAXVITESSEPRESS', NTMXVP, MNMXVP )
C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR = 3 MOYVIT, MAXVIT, MX-MIPRESSION
      MCN(MNMXVP+WBCOVE) = NBMXVP
C     NOMBRE DE VECTEURS MAX VITESSE PRESSION = NOMBRE DE PAS DE TEMPS
      MCN(MNMXVP+WBVECT) = NBPAST
C     NOMBRE DE TEMPS A LA FIN DES VECTEURS
      MCN(MNMXVP+WBCPIN) = NBPAST
C     ADRESSE MCN DES 3 VECTEURS
      MNMAXVIT = MNMXVP   + WECTEU
      MNTIMXVP = MNMAXVIT + MOREE2 * NBMXVP * NBPAST
      IF( NBPAST0 .GT. 0 ) THEN
C        LE STOCKAGE DU TEMPS POUR LE TMS VECTEUR"MAXVITESSEPRESS EST DECALE
         MN = MNMXVP + WECTEU + MOREE2 * NBMXVP0 * NBPAST0
         CALL TRTATA( RMCN(MN), RMCN(MNTIMXVP), NBPAST0 )
      ENDIF
C
C     AFFICHAGE DU VECTEUR"VITESSEPRESSION AU TEMPS INITIAL
C     -----------------------------------------------------
      CALL AFVIPR( NUTYEL, NDIM, NBNOVI, MNXYZN, MCN(MNNDDL),
     %             TEMPS,  NTDLVP, MIN(10,NBNOVI), MCN(MNVXYZP0),
     %             VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )
C
C     STOCKAGE EN POSITION NBPAS DE VITMOY, VITMAX, PREMAX-PREMIN, TEMPS
      NBPAS = NBPAST0 + 1
      MN = ( MNMAXVIT - 1 ) / MOREE2 + NBMXVP * (NBPAS-1)
      DMCN( MN+1 ) = VITMOY
      DMCN( MN+2 ) = VITMAX
      DMCN( MN+3 ) = PREMAX-PREMIN
      RMCN( MNTIMXVP-1+NBPAS ) = TEMPS
C
C     TEMPS CPU DE FABRICATION DES VECTEURS GLOBAUX
      DFABG = DINFO( 'DELTA CPU' )
C
C
C     ############################################################
C     ##                                                        ##
C     ##  LA BOUCLE EN TEMPS AVEC UN PAS DE TEMPS CONSTANT = DT ##
C     ##                                                        ##
C     ############################################################
C
C     EF de TAYLOR-HOOD P2 EN VITESSE et P1 EN PRESSION
C     BOUCLE SUR LES ELEMENTS FINIS POUR CONSTRUIRE U(tn+1)
C     VITESSE EN INTEGRATION SEMI IMPLICITE EN TEMPS
C     VG U(tn+1) = (Rho - dt Mhu Laplacien ) U(tn+1) =
C      = Rho U(tn,X(tn+1,x) - dt UnSRho GRAD P(tn) + dt Force(tn+1)
C     -------------------------------------------------------------
C     LES RESULTATS SONT MAINTENANT AU TEMPS + DT
C     .............................................................
 100  TEMPS   = TEMPS   + DT
      NBPASDT = NBPASDT + 1
C
C     mIter NOMBRE D'ITERATIONS DE POINT FIXE POUR TRAITER LE COUPLAGE
C     ET LE TERME NON LINEAIRE DE TRANSPORT
      mIter   = 0
      NORVIM0 = 0
      NORPRM0 = 0
C
C     Vn+1,0 = Vn  dans  MNV3P0 = MNVITX0, MNVITY0, MNVITZ0, MNPRES0
C                                 VITtn+1,m
C
      CALL TRTATD( MCN(MNV3P0), MCN(MNV3Ptn), NBNOVI * (NDIM+1) )
C                  dans  MNV3Ptn = MNVITXtn, MNVITYtn, MNVITZtn, MNPREStn
C     LES ADRESSES MCN INTERMEDIAIRES DES VECTEURS INDEPENDANTS
C     NDIM COMPOSANTES DE LA VITESSE ET PRESSION AU TEMPS tn
      MNVITXtn = MNV3Ptn
      MNVITYtn = MNVITXtn + MOREE2 * NBNOVI
      MNVITZtn = MNVITYtn + MOREE2 * NBNOVI
      MNPREStn = MNVITXtn + MOREE2 * NBNOVI * NDIM
C
C     Les iterations m pour obtenir un point fixe du probleme non lineaire
C     --------------------------------------------------------------------
 111  mIter = mIter + 1
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10111) TEMPS, mIter
      ELSE
         WRITE(IMPRIM,20111) TEMPS, mIter
      ENDIF
10111 FORMAT(/'fpisonlth: CALCUL VITESSE et PRESSION au TEMPS ',G14.6,
     %' m=',I4,'  Methode PISO: ',70('*'))
20111 FORMAT(/'fpisonlth: VELOCITY PRESSURE COMPUTATION at TIME ',G14.6,
     %' m=',I4,'  PISO Method: ',70('*'))
C
C     NDIM COMPOSANTES DE LA VITESSE ET PRESSION AU TEMPS tn+1,m
      MNVITX0 = MNV3P0
      MNVITY0 = MNVITX0 + MOREE2 * NBNOVI
      MNVITZ0 = MNVITY0 + MOREE2 * NBNOVI
      MNPRES0 = MNVITX0 + MOREE2 * NBNOVI * NDIM
C
C     NDIM COMPOSANTES DE LA VITESSE ET PRESSION AU TEMPS tn+1,m+1
      MNVITX1 = MNV3P1
      MNVITY1 = MNVITX1 + MOREE2 * NBNOVI
      MNVITZ1 = MNVITY1 + MOREE2 * NBNOVI
      MNPRES1 = MNVITX1 + MOREE2 * NBNOVI * NDIM
C
C     =====================================================================
C     TETRAEDRE TAYLOR-HOOD EN VITESSE et P1 EN PRESSION
C     BOUCLE SUR LES ELEMENTS FINIS POUR CONSTRUIRE Vm+1
