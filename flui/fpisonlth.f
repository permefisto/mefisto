C             =  1/dt V(tn)
C              - (Vm . Grad) Vm
C              + 1/Rho Force(tn+1)
C              - 1/Rho Grad P(tn)
C     C. L.  Vm+1 = V(tn+1) SUR LA FRONTIERE
C
C     => CALCUL de
C     Integrale  tP2 1/dt P2 dX V(tn,Se)
C   - Integrale  tP2 P2 Vem(Se) . Grad P2 Vem(Se) dX
C   + Integrale  tP2 P2 Force(tn+1,Se) dX / Rho
C   - Integrale  tP2 Grad P1 Pm(Se) )  dX / Rho
C     =====================================================================
C     MISE A ZERO DE V3P1
      CALL AZEROD( NTDLVP, DMCN((MNV3P1+1)/MOREE2) )
C
C     BOUCLE SUR LES EF
      DO NUELEM = 1, NBELEM
C
C        NO DES NOEUDS DE L'ELEMENT FINI NUELEM
         CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOTE )
C
C        NO DE  POINTS  LIGNES SURFACES VOLUMES
C           DES SOMMETS ARETES FACES    VOLUME DU TETRAEDRE NUELEM
         CALL EFPLSV( MNELE , NUELEM,
     %                NOVCEL, NOSFEL, NOLAEL, NOPSEL,
     %                NOOBVC, NOOBSF, NOOBLA, NOOBPS )
C
C        INTERPOLATION DU FLUIDE avec F: e reference->e  P1 ndim
C        COORDONNEES DES NBNOEF SOMMETS=POINTS=NOEUDS DE L'EF
         CALL EFXYZP( NDIM, MNXYZN, NBELEM, NUELEM, MNPGEL, NBNOEF,
     %                XYZEF )
C
         IF( NUTYEL .EQ. 15 ) THEN
C
C           TRIANGLE TAYLOR-HOOD
            CALL F2BVETH( UnSdt,  UnSRho, 1D0, 1D0,
     %                    XYZEF,  NONOTE, NBNOVI, NBSOM, MCN(MNNOSO),
     %            NOOBLA, NUMIOB(2), NUMAOB(2), MCN(MNDOEL(2)), IEFOCL,
     %            NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)), IEFOIN,
     %                    MCN(MNV3Ptn), MCN(MNV3P0), MCN(MNPRES0),
     %                    AE )
         ELSE
C
C           TETRAEDRE TAYLOR-HOOD
            CALL F3BVETH( UnSdt,  UnSRho, 1D0, 1D0,
     %                    XYZEF,  NONOTE, NBNOVI, NBSOM, MCN(MNNOSO),
     %            NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)), IEFOCL,
     %            NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)), IEFOIN,
     %                    MCN(MNV3Ptn), MCN(MNV3P0), MCN(MNPRES0),
     %                    AE )
         ENDIF
C
ccc         IF( NUELEM .LE. 3 ) THEN
ccc            call affvect( 'Sortie F23BVETH', 30, AE )
ccc         ENDIF
C
C        ASSEMBLAGE DE AE dans V3P1 = VITX1, VITY1, VITZ1
         CALL ASMEEX1( NDIM, NBNOEF, NONOTE, AE, NBNOVI, MCN(MNV3P1) )
C
      ENDDO
C     FIN DE LA BOUCLE SUR LES EF DE LA CONSTRUCTION DE V3P1
ccc      CALL AFTABLE( 'BG Vitesses ', NBNOVI, 3, MCN(MNV3P1),
ccc     %               NBNOVI-10, NBNOVI, 1, 3 )
C
C     LISTE DES DL VITESSES FIXEES AU TEMPS tn+1,m+1
C     ----------------------------------------------
      CALL VITEFX( RELMIN, NTDLVI, NDIM,   MNXYZN,
     %             NBTYEL, MNNPEF, NUMIOB, MNDOEL,
     %             NBVCFX, NBVIFX, MNDLVIFX, MNVAVIFX, VITFXMAX )
C
C     BLOCAGE DES COMPOSANTES FIXEES DE LA VITESSE AU TEMPS tn+1,m+1 SUR V3P1
      CALL VITEFXVG( NBVIFX, MCN(MNDLVIFX), MCN(MNVAVIFX), STGV,
     %               NDIM, NBNOVI, MCN(MNV3P1) )
ccc      CALL AFTABLE( 'BG Vitesses+CL ', NBNOVI, 3, MCN(MNV3P1),
ccc     %               NBNOVI-10, NBNOVI, 1, 3 )
C
C     RESOLUTION EN VITESSES: i=1,...,NDIM
C     Integrale  1/dt  tP2 P2 + Mhu/Rho tGrad P2 Grad P2 dx  V*i
C   = Integrale  1/dt  tP2 Vi(tn,X(tn;tn+1,bl) dX
C   - Integrale  1/Rho tP2 Grad P(tn) dX
C   + Integrale  1/Rho tP2 Force(tn)  dX
C     =======================================================================
      IF( NORESO .EQ. 1 ) THEN
C
C        DESCENTE et REMONTEE DU PROFIL DE LA MATRICE VITESSE
C        L tL COMPLETE DES NDIM COMPOSANTES DE LA VITESSE U*(tn+1)
         MNV1 = MNV3P1
         DO K = 1, NDIM
            CALL DRCHPR( NBNOVI, NCODSV, MCN(MNPTDV), VG, MCN(MNV1), 3,
     %                   MCN(MNV1) )
ccc            call affvect( 'Apres DRCHPR: VITESSE*i', 30, MCN(MNV1) )
            MNV1 = MNV1 + MOREE2 * NBNOVI
         ENDDO
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        RESOLUTION PAR GC SIMPLE SANS PRECONDITIONNEMENT DES
C        NDIM COMPOSANTES DE LA VITESSE U*(tn+1)
C
C        LE VECTEUR INITIAL DU GC EST LE DERNIER VECTEUR CALCULE
C        PROTECTION CAR B DOIT ETRE DIFFERENT DE X DANS LE GC
         CALL TRTATD( MCN(MNV3P1), MCN(MNV3P2), NDIM*NBNOVI )
         MNVB = MNV3P2
         MNV0 = MNV3P0
         MNV1 = MNV3P1
C        EN SORTIE DE GCAXB LA SOLUTION EST DANS MNV1=MNV3P1
         DO K = 1, NDIM
            CALL GCAXB( NBNOVI,       MCN(MNNDVFX),
     %                   MCN(MNPTDV),  MCN(MNLPCV),  VG,
     %                   MCN(MNVB),    MCN(MNV0),
     %                   MCN(MNAUX1),  MCN(MNAUX2),  MCN(MNAUX3),
     %                   MCN(MNV1),     IERR )
ccc            call affvect( 'Apres GCAXB: VITESSE*i', 30, MCN(MNV1) )
C
C           VERIFICATION DE LA CONVERGENCE DU GC
            IF( IERR .NE. 0 ) THEN
C              IL N'Y A PAS EU CONVERGENCE DU GC
C              LA METHODE DU GC NE CONVERGE PAS => ABANDON DES CALCULS
               WRITE(KERR(MXLGER)(1:10),'(I10)') NBNOVI
               NBLGRC(NRERR) = 3
               IF( LANGAG .EQ. 0 ) THEN
                  KERR(1) = 'NON CONVERGENCE APRES '//KERR(MXLGER)(1:10)
     %                   // ' ITERATIONS DU GC SIMPLE'
                  KERR(2) = 'ABANDON DE LA METHODE DU GRADIENT CONJUGUE'
                  KERR(3) = 'ESSAYER UNE AUTRE METHODE DE RESOLUTION'
               ELSE
                  KERR(1) = 'NO CONVERGENCE after '// KERR(MXLGER)(1:10)
     %                   // ' ITERATIONS of SIMPLE CG'
                  KERR(2) = 'EXIT of the CONJUGATE GRADIENT METHOD'
                  KERR(3) = 'TRY an OTHER METHOD'
               ENDIF
               CALL LEREUR
               IERR = 9
               GOTO 9999
            ENDIF
C
ccc            call affvect( 'Apres Gcaxb Vitesse*i=', 30, MCN(MNV1) )
            MNV0 = MNV0 + MOREE2 * NBNOVI
            MNV1 = MNV1 + MOREE2 * NBNOVI
            MNVB = MNVB + MOREE2 * NBNOVI
         ENDDO
C
      ENDIF
ccc      CALL AFTABLE( 'Vitesses ', NBNOVI, 3, MCN(MNV3P1),
ccc     %               NBNOVI-10, NBNOVI, 1, 3 )
C
C     ========================================================================
C     PISO  CALCUL DE LA PRESSION P(tn+1,m+1) PAR LA RESOLUTION DE
C (2) - 1/Rho Laplacien Pm+1(X) = Div { -Fg(tn+1)/Rho
C                                     + Vm+1 . Grad Vm+1 } dans Omega
C     Condition aux limites
C            -1/Rho    dPm+1/dn = n . ( -Fg(tn+1)/Rho
C                                     +  Vm+1 . Grad Vm+1 } sur Gamma
C     or Pn+1,m+1(Gamma) = P(tn+1,Gamma) pour Gamma point sur la frontiere
C
C     REMARQUE: TOUTE LA SURFACE FRONTIERE DOIT FAIRE PARTIE DE L'OBJET
C               POUR PRENDRE EN COMPTE LA NORMALE SUR LE TERME NON LINEAIRE
C     ========================================================================
C     CALCUL DU SECOND MEMBRE BG avec la
C     Version 1 de PISO: Bg avec Div reportee sur la Pression
      IF( NUTYEL .EQ. 15 ) THEN
C        TRIANGLE TAYLOR-HOOD
         CALL F2BPGTH( UnSRho,
     %                 NBSOM,  NBNOVI, MNXYZN, MNPGEL, MCN(MNNOSO),
     %                 MNELE,  NBELEM,
     %                 NOOBLA,
     %                 NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                 MCN(MNV3P1),  MCN(MNP2DP2),   MCN(MNPRES1) )
      ELSE
C        TETRAEDRE TAYLOR-HOOD
         CALL F3BPGTH( UnSRho,
     %                 NBSOM,  NBNOVI, MNXYZN, MNPGEL, MCN(MNNOSO),
     %                 MNELE,  NBELEM,
     %                 NOOBSF,
     %                 NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                 MCN(MNV3P1),  MCN(MNP2DP2),   MCN(MNPRES1) )
      ENDIF
C
ccc      call affvect('Sd Membre Pression )',    20, MCN(MNPRES1) )
ccc      call afl1ve( 'Sd Membre Pression )', NBSOM, MCN(MNPRES1) )
C
C     CONSTRUCTION DES TABLEAUX NO ET VALEURS DES PRESSIONS FIXEES a tn+1
C     -------------------------------------------------------------------
      IF( IEBLPR .GT. 0 ) THEN
         CALL PRESFXST( RELMIN, NBSOM,    NDIM,   MNXYZN, MNNOSO,
     %                  NBTYEL, MNNPEF,   NUMIOB, MNDOEL,
     %                  NBPRFX, MNDLPRFX, MNVAPRFX )
      ELSE
         NBPRFX = 0
      ENDIF
C
C     PRISE EN COMPTE DES DEGRES DE LIBERTE PRESSION FIXES P(TN+1)
C     ------------------------------------------------------------
      IF( NORESO .EQ. 1 ) THEN
         MNP1 = ( MNPRES1 - 1 ) / MOREE2
         MM   = MNPRES1
      ELSE
C        COPIE DE MNPRES1 DANS MNPRPR POUR LE GC
         CALL TRTATD( MCN(MNPRES1), MCN(MNPRPR), NBSOM )
         MNP1 = ( MNPRPR - 1 ) / MOREE2
         MM   = MNPRPR
      ENDIF
      MNV = ( MNVAPRFX - 1 ) / MOREE2
      DO I=1, NBPRFX
C        LE NO SOMMET GLOBAL DU DL FIXE
         NDL = MCN( MNDLPRFX - 1 + I )
C        VALEUR FIXEE DE LA PRESSION  P(TN+1)
         DMCN(MNP1+NDL) = STGV * DMCN(MNV+I)
      ENDDO
C
ccc      call affvect( 'Apres CL Pression(tn+1,m+1)=', 20, MCN(mm) )
      IF( NORESO .EQ. 1 ) THEN
C
C        ------------------------------------------------------------------
C        RESOLUTION de P(tn+1,m+1) = [L tL]-1 Div(-FOmega+Rho U.Grad U)
C        PAR DESCENTE REMONTEE DE LA MATRICE FACTORISEE PG = L tL PROFIL
C        ------------------------------------------------------------------
         CALL DRCHPR( NBSOM, NCODSP, MCN(MNPTDP), PG, MCN(MNPRES1), 3,
     %                MCN(MNPRES1) )
ccc         call affvect( 'Apres DRCHPR PR(tn+1,m+1)=',   20, MCN(MNPRES1))
ccc         call afl1ve(  'Apres DRCHPR Pr(tn+1,m+1)=',NBSOM, MCN(MNPRES1))
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        ----------------------------------------------------------------
C        RESOLUTION de P(tn+1,m+1) = [PG]-1 Div(-FOmega+Rho U.Grad U)
C        PAR GRADIENT CONJUGUE SIMPLE AVEC MATRICE SYMETRIQUE MORSE
C        ----------------------------------------------------------------
C        X0 DU GC EST P(tn+1) CALCULE AUPARAVANT
         CALL GCAXB( NBSOM,        MCN(MNNDPFX),
     %                MCN(MNPTDP),  MCN(MNLPCP),  PG,
     %                MCN(MNPRPR),  MCN(MNPRES0),
     %                MCN(MNAUX1),  MCN(MNAUX2),  MCN(MNAUX3),
     %                MCN(MNPRES1), IERR )
ccc         call affvect( 'Apres Gcaxb PR(tn+1,m+1)=',   20, MCN(MNPRES1) )
ccc         call afl1ve(  'Apres Gcaxb Pr(tn+1,m+1)=',NBSOM, MCN(MNPRES1) )
C
C        VERIFICATION DE LA CONVERGENCE DU GC
         IF( IERR .NE. 0 ) THEN
C           IL N'Y A PAS EU CONVERGENCE DU GC
C           LA METHODE DU GC NE CONVERGE PAS => ABANDON DES CALCULS
            WRITE(KERR(MXLGER)(1:10),'(I10)') NBSOM
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'NON CONVERGENCE APRES '//KERR(MXLGER)(1:10)
     %                   // ' ITERATIONS DU GC SIMPLE'
               KERR(2) = 'ABANDON DE LA METHODE DU GRADIENT CONJUGUE'
               KERR(3) = 'ESSAYER UNE AUTRE METHODE DE RESOLUTION'
            ELSE
               KERR(1) = 'NO CONVERGENCE after '// KERR(MXLGER)(1:10)
     %                   // ' ITERATIONS of SIMPLE CG'
               KERR(2) = 'EXIT of the CONJUGATE GRADIENT METHOD'
               KERR(3) = 'TRY an OTHER METHOD'
            ENDIF
            CALL LEREUR
            IERR = 9
            GOTO 9999
         ENDIF
C
      ELSE IF( NORESO .EQ. 3 ) THEN
C
C        ----------------------------------------------------------------
C        RESOLUTION DU SYSTEME PAR GRADIENT CONJUGUE AVEC CROUT INCOMPLET
C        ----------------------------------------------------------------
C
C        PRISE EN COMPTE DES DEGRES DE LIBERTE PRESSION FIXES SUR BG
C        -----------------------------------------------------------
         CALL PRESFXBG( NBPRFX, MCN(MNDLPRFX), MCN(MNVAPRFX),
     %                  NBSOM, MCN(MNPRES1) )
C
C        MISE A ZERO DU TABLEAU AUXILIAIRE
         CALL AZEROD( LOAUX, DMCN((MNAUX1+1)/MOREE2) )
C        MISE A ZERO DU TABLEAU DES DIRECTIONS
         CALL AZEROD( LODIR, DMCN((MNBDIR+1)/MOREE2) )
C        RESOLUTION GC PRECONDITIONNE PAR CROUT INCOMPLET
C        LE VECTEUR INITIAL DU GC EST LE DERNIER VECTEUR CALCULE
         CALL GCPRCR( NBSOM,       1,           NBDIR,   MCN(MNNDPFX),
     %                MCN(MNPTDP), MCN(MNLPCV), PG,      MCN(MNPRES1),
     %                MCN(MNLPLC), MCN(MNLPCC), MCN(MNPGC),
     %                MCN(MNPRES0),MCN(MNAUX1), MCN(MNAUX2),MCN(MNAUX3),
     %                MCN(MNDIR),  MCN(MNADIR), MCN(MNDAD),  MCN(MNBET),
     %                MCN(MNPRES1), IERR )
ccc         call affvect( 'Apres GCPRCR PRESSION1=', 20, MCN(MNPRES1) )
C
C        VERIFICATION DE LA CONVERGENCE DU GC
         IF( IERR .NE. 0 ) THEN
C           IL N'Y A PAS EU CONVERGENCE DU GC
C           LA METHODE DU GC NE CONVERGE PAS => ABANDON DES CALCULS
            WRITE(KERR(MXLGER)(1:8),'(I8)') NIVOGC
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'NON CONVERGENCE AU NIVEAU '//KERR(MXLGER)(1:8)
               KERR(2) = 'ABANDON DE LA METHODE DU GRADIENT CONJUGUE'
               KERR(3) = 'ESSAYER FACTORISATION COMPLETE DE CHOLESKY MAT
     %RICE PROFIL'
            ELSE
               KERR(1) = 'NO CONVERGENCE at LEVEL '// KERR(MXLGER)(1:8)
               KERR(2) = 'EXIT of the CONJUGATE GRADIENT METHOD'
          KERR(3)='TRY FULL CHOLESKY FACTORIZATION with SKYLINE STORAGE'
            ENDIF
            CALL LEREUR
            IERR = 9
            GOTO 9999
         ENDIF
C
      ENDIF
C
ccc      call affvect(
ccc     %'Solution PR of -UnSRho LAPLACIEN P = Div(-FOmega+Rho U.Grad U)',
ccc     %              20, MCN(MNPRES1) )
ccc      call afl1ve(
ccc     %'Solution Pr of -UnSRho LAPLACIEN P = Div(-FOmega+Rho U.Grad U)',
ccc     %              NBSOM, MCN(MNPRES1))
C
C     PASSAGE DE VITX,VITY,VITZ,PRESSION(tn+1,m+1) PAR COMPOSANTES
C     A VXYZP(tn+1,m+1) PAR NOEUDS
C     ============================================================
      CALL VXYZPRE(NDIM, NUTYEL, MCN(MNNDDL),
     %             1, NBNOVI, MCN(MNVITX1), MCN(MNVITY1), MCN(MNVITZ1),
     %             NBSOM,  MCN(MNNOSO), MCN(MNPRES1),
     %             NTDLVP, MCN(MNVXYZP0) )
C
C     CONVERGENCE DES ITERATIONS mIter ?
C     ==================================
      CALL NORMDIF( 3*NBNOVI, MCN(MNV3P0), MCN(MNV3P1), DIFVIM, NORVIM )
      CALL NORMDIF( NBSOM,  MCN(MNPRES0), MCN(MNPRES1), DIFPRM, NORPRM )
      IF( NORVIM0 .GT. 0D0 ) THEN
         DIFMAX = DIFVIM / NORVIM0
      ELSE
         DIFMAX  = DIFVIM
         NORVIM0 = NORVIM
      ENDIF
      IF( NORPRM0 .GT. 0D0 ) THEN
         DIFMAX = MAX( DIFMAX, DIFPRM/NORPRM0 )
      ELSE
         DIFMAX  = MAX( DIFMAX, DIFPRM )
         NORPRM0 = NORPRM
      ENDIF
C
      WRITE(IMPRIM,10300) TEMPS, mIter, DIFVIM, NORVIM, DIFPRM, NORPRM,
     %                    DIFMAX
10300 FORMAT(/'TEMPS',G13.5,' Iter m=',I3,
     % ' ||Vn+1,m+1-Vn+1,m||/NbV=',G15.7,' ||Vn+1,m||/NbV=',G15.7,
     %'  ||Pn+1,m+1-Pn+1,m||/NbP=',G15.7,' ||Pn+1,m||/NbP=',G15.7,
     %' Test=',G13.5)
C
      IF( DIFMAX .LT. 1D15 ) THEN
         IF( DIFMAX .GT. 0.001D0 ) THEN
C           UNE ITERATION mIter A FAIRE DE PLUS
            IF( mIter .LE. mIterMax ) THEN
               MN     = MNV3P0
               MNV3P0 = MNV3P1
               MNV3P1 = MN
               GOTO 111
            ELSE
               GOTO 301
            ENDIF
         ENDIF
         GOTO 303
      ENDIF
C
C     DIVERGENCE OU NOMBRE D'ITERATIONS MAX ATTEINT
 301  WRITE(IMPRIM,10301) TEMPS, mIterMax
10301 FORMAT(/'Time',G13.5,' m=Nb Iteration MAX=',I3,
     %        ' ATTAINED => DETECTED DIVERGENCE. STOP'//)
      IERR = 10
      GOTO 305
C
C     ICI CONVERGENCE DES ITERATIONS mIter de POINT FIXE
C     --------------------------------------------------
 303  WRITE(IMPRIM,10303) TEMPS, mIter
10303 FORMAT('+++++ TEMPS',G13.5,' Iteration m=',I3,' => CONVERGENCE')
C
C     AFFICHAGE DU VECTEUR"VITESSEPRESSION(tn+1,m+1) NBFLUI AU TEMPS
C     --------------------------------------------------------------
 305  CALL AFVIPR( NUTYEL, NDIM, NBNOVI, MNXYZN, MCN(MNNDDL),
     %             TEMPS,  NTDLVP, MIN(10,NBNOVI), MCN(MNVXYZP0),
     %             VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )
C
C     STOCKAGE EN POSITION NBPAS DE VITMOY, VITMAX, PREMAX-PREMIN, TEMPS
      NBPAS = NBPAS + 1
      MN    = ( MNMAXVIT - 1 ) / MOREE2 + NBMXVP * (NBPAS-1)
      DMCN( MN+1 ) = VITMOY
      DMCN( MN+2 ) = VITMAX
      DMCN( MN+3 ) = PREMAX-PREMIN
      RMCN( MNTIMXVP-1+NBPAS ) = TEMPS
C
C     FLUX DE LA VITESSE A TRAVERS LES FACES DES SURFACES DE L'OBJET
C     --------------------------------------------------------------
C     MNELE=MCN( MNNPEF ): ADRESSE DU TABLEAU NPEF"TYPE EF
      CALL FLUVITSF( MNXYZN, MCN(MNNPEF), MCN(MNNDDL), MCN(MNVXYZP0),
     %               NUMIOB(3), NUMAOB(3),
     %               FLUNEG, FLUPOS, MCN(MNFVSF) )
C
C     STOCKAGE DES RESULTATS AU NBPASR
      NBPASR = NBPASR + 1
      RESULT(1,NBPASR) = TEMPS
      RESULT(2,NBPASR) = VITMOY
      RESULT(3,NBPASR) = VITMAX
      RESULT(4,NBPASR) = PREMOY
      RESULT(5,NBPASR) = PREMAX - PREMIN
      RESULT(6,NBPASR) = FLUNEG
      RESULT(7,NBPASR) = FLUPOS
      RESULT(8,NBPASR) = NBPASDT
C
C     STOCKAGE OU PAS DU VECTEUR SOLUTION?
      IF( TEMPS .GE. TSTOC*0.9999 ) THEN
C        STOCKAGE DE LA VITESSEPRESSION A CET INSTANT TEMPS
         MNFLUI = MNVECT + WECTEU + MOREE2 * NTDLVP * NBFLUI
C        LE NOMBRE DE VECTEURS VITESSEPRESSION STOCKES
         NBFLUI = NBFLUI + 1
         CALL TRTATD( MCN(MNVXYZP0), MCN(MNFLUI), NTDLVP )

C        STOCKAGE DE LA VITESSEPRESSION NBFLUI A CET INSTANT TEMPS
C        SUR FICHIER DU REPERTOIRE DU PROJET
         NAVIER = 3
         CALL ECFIVIPR( KNOMOB, TEMPS,  NBFLUI,
     %                  NAVIER, NORESO, NIVOGC, NBPASDT,
     %                  NBNOVI, NBSOM,
     %                  NTDLVP, MCN(MNVXYZP0),
     %                  KNOMFIC, IERR )

C        LE TEMPS DE STOCKAGE
         MNTIME = MNVECT + WECTEU + MOREE2 * NTDLVP * MXFLUI - 1
         RMCN(MNTIME+NBFLUI) = TEMPS
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10350) TEMPS, NTDLVP, NBFLUI
         ELSE
            WRITE(IMPRIM,20350) TEMPS, NTDLVP, NBFLUI
         ENDIF
C        LE PROCHAIN TEMPS DE STOCKAGE
         TSTOC = TSTOC + DTSTOC
      ENDIF
C
10350 FORMAT(' Au TEMPS',G14.6,' STOCKAGE de',I7,' VITESSEPRESSION',
     %   ' en COLONNE',I5,' du TMS VECTEUR"VITESSEPRESSION')
20350 FORMAT(' At TIME',G14.6,' STORAGE of',I7,' VELOCITY PRESSURE',
     %   ' in COLUMN',I5,' of TMS VECTEUR"VITESSEPRESSION')
C
C     PERMUTATION DE MNV3P0 (tn+1,m) ET MNV3P1 (tn+1,m+1)
C     ---------------------------------------------------
      MN     = MNV3P0
      MNV3P0 = MNV3P1
      MNV3P1 = MN
      IF( IERR .NE. 0 ) GOTO 360
C
      IF( TEMPS + DT .LT. TPSFIN*1.00001 ) GOTO 100
C
C    ##############################################################
C    ##                                                          ##
C    ##                FIN DE LA BOUCLE EN TEMPS                 ##
C    ##                                                          ##
C    ##############################################################
C
C
 360  IF( RMCN(MNTIME+NBFLUI) .NE. TEMPS ) THEN
C        STOCKAGE DES VITESSEPRESSION A CET INSTANT
         MNFLUI = MNVECT + WECTEU + MOREE2 * NTDLVP * NBFLUI
         CALL TRTATD( MCN(MNVXYZP0), MCN(MNFLUI), NTDLVP )
C        LE NOMBRE DE VECTEURS VITESSEPRESSION STOCKES
         NBFLUI = NBFLUI + 1
C        LE TEMPS DE STOCKAGE
         MNTIME = MNVECT + WECTEU + MOREE2 * NTDLVP * MXFLUI - 1
         RMCN(MNTIME+NBFLUI) = TEMPS
         WRITE(IMPRIM,10350) TEMPS, NTDLVP, NBFLUI
      ENDIF
C
C     MISE A JOUR DU TMS 'VECTEUR"VITESSEPRESSION'
C     ============================================
      MCN( MNVECT + WBCOVE ) = NTDLVP
      MCN( MNVECT + WBVECT ) = NBFLUI
      MCN( MNVECT + WBCPIN ) = NBFLUI
      IF( NBFLUI .LT. MXFLUI ) THEN
C        LE TMS EST RACOURCI
         L  = MNVECT + WECTEU + NTDLVP * MXFLUI * MOREE2 - 1
         L1 = MNVECT + WECTEU + NTDLVP * NBFLUI * MOREE2 - 1
         DO 200 I=1,NBFLUI
            RMCN(L1+I) = RMCN(L+I)
 200     CONTINUE
         CALL TAMSRA( NTVECT, WECTEU+NTDLVP*NBFLUI*MOREE2+NBFLUI )
      ENDIF
C     LA DATE
      CALL ECDATE( MCN(MNVECT) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNVECT + MOREE2 ) = NONMTD( '~>>>VECTEUR' )
C
C     L'ADRESSE-1 DU PREMIER TEMPS STOCKE DERRRIERE LES MXFLUI
C     VECTEURS VITESSEPRESSION
      MNTIME = MNVECT + WECTEU + NTDLVP * MXFLUI * MOREE2 -1
C
C     AFFICHAGE DU TABLEAU DES RESULTATS GLOBAUX sur V(t,r)
C     =====================================================
      WRITE(IMPRIM,*)
      WRITE(IMPRIM,19991)
      WRITE(IMPRIM,*)
     %'ARRAY of V(t,r) RESULT at every TIME STEP during the INTERVAL of
     %TIME (',TPSINI, TPSFIN,' )'
      WRITE(IMPRIM,19991)
19991 FORMAT(180('='))
      VMOYMOY = 0D0
      VMAXMAX = RESULT(3,1)
      PMOYMOY = 0D0
      PMXIMXI = RESULT(5,1)
      DO K=1,NBPASR
         WRITE(IMPRIM,19990) (RESULT(I,K),I=1,7),RESULT(6,K)+RESULT(7,K)
         VMOYMOY = VMOYMOY + RESULT(2,K)
         IF( RESULT(3,K) .GT. VMAXMAX ) VMAXMAX = RESULT(3,K)
         PMOYMOY = PMOYMOY + RESULT(4,K)
         IF( RESULT(5,K) .GT. PMXIMXI ) PMXIMXI = RESULT(5,K)
      ENDDO
19990 FORMAT('Time=',G12.5,' VelocityMean=',G14.6,' VelocityMax=',G14.6,
     %' PressureMean=',G14.6,' PressMax-Min=',G14.6,
     %' Flux-=',G14.6,' Flux+=',G14.6,' Flux-+ =',G14.6)
C
C     TOTAUX
      VMOYMOY = VMOYMOY / NBPASR
      PMOYMOY = PMOYMOY / NBPASR
      WRITE(IMPRIM,19991)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*)
     %'Vitesse MOYENNE MAXIMA, PRESSION Max-Min sur l''INTERVALLE de TEM
     %PS [',TPSINI, TPSFIN,' ]'
      ELSE
         WRITE(IMPRIM,*)
     %'MEANS and MAXIMA of Velocity during the INTERVAL of TIME (',
     %TPSINI, TPSFIN,' )'
      ENDIF
      WRITE(IMPRIM,19990) RESULT(1,NBPASR),VMOYMOY, VMAXMAX,
     %                    PMOYMOY, PMXIMXI
      WRITE(IMPRIM,19991)
C
C     AFFICHAGE DU VECTEUR"VITESSEPRESSION V(NBFLUI) AU TEMPS FINAL
C     -------------------------------------------------------------
      MN = MNVECT+WECTEU + MOREE2 * (NBFLUI-1) * NTDLVP
      CALL AFVIPR( NUTYEL, NDIM,   NBNOVI, MNXYZN, MCN(MNNDDL),
     %             TEMPS,  NTDLVP, MIN(32,NBNOVI), MCN(MN),
     %             VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )
C
C     STOCKAGE EN POSITION NBPAS DE VITMOY, VITMAX, PREMAX-PREMIN, TEMPS
ccc      NBPAS = NBPAS + 1
      MN    = ( MNMAXVIT - 1 ) / MOREE2 + NBMXVP * (NBPAS-1)
      DMCN( MN+1 ) = VITMOY
      DMCN( MN+2 ) = VITMAX
      DMCN( MN+3 ) = PREMAX-PREMIN
      RMCN( MNTIMXVP-1+NBPAS ) = TEMPS
C
C     MISE A JOUR TMS VECTEUR"MAXVITESSEPRESS
C    (NBPAS PEUT ETRE DIFFERENT DE NBPAST)
C     ---------------------------------------
C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR = 3 MOYVIT, MAXVIT, MX-MIPRESSION
      MCN(MNMXVP+WBCOVE) = NBMXVP
C     NOMBRE DE VECTEURS MAX VITESSE PRESSION = NOMBRE DE PAS DE TEMPS CALCULES
      MCN(MNMXVP+WBVECT) = NBPAS
C     NOMBRE DE TEMPS A LA FIN DES VECTEURS
      MCN(MNMXVP+WBCPIN) = NBPAS
C     LE STOCKAGE DU TEMPS POUR LE TMS VECTEUR"MAXVITESSEPRESS
      MN = MNMXVP + WECTEU + MOREE2 * NBMXVP * NBPAS
      CALL TRTATA( RMCN(MNTIMXVP), RMCN(MN), NBPAS )
C
C     LA DATE DU TMS VECTEUR"MAXVITESSEPRESS
      CALL ECDATE( MCN(MNMXVP) )
C
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNMXVP + MOREE2 ) = NONMTD( '~>>>VECTEUR' )
C
      MOTSVP1 = WECTEU + NBMXVP * NBPAS * MOREE2 + NBPAS
      IF( MOTSVP1 .LT. MOTSVP ) CALL TAMSRA( NTMXVP, MOTSVP1 )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     LA DATE
      CALL ECDATE( MCN(MNVECT) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNVECT + MOREE2 ) = NONMTD( '~>>>VECTEUR' )
C
C     FLUX DE LA VITESSE FINALE A TRAVERS LES FACES DES SURFACES DE L'OBJET
C     ---------------------------------------------------------------------
      MNFLUI = MNVECT + WECTEU + MOREE2 * NTDLVP *(NBFLUI-1)
      CALL FLUVITSF( MNXYZN, MNELE, MCN(MNNDDL), MCN(MNFLUI),
     %               NUMIOB(3), NUMAOB(3),
     %               FLUNEG, FLUPOS, MCN(MNFVSF) )
C
C     EXTRACTION DES PRESSIONS AUX NOEUDS DU MAILLAGE
C     --------------------------------------------------------------------
C     RECHERCHE DU TABLEAU DEFINITION DE L'OBJET
      CALL LXTSOU( NTLXOB, 'DEFINITION', NTDFOB, MNDFOB )
C     INTERPOLATION de la PRESSION = P1 POUR BREZZI-FORTIN
C                                  = P2 POUR TAYLOR-YOUNG
C     INTERPP et NBNOPR
      INTERPP = 2
      NBNOPR  = NBNOVI
C
C     ADRESSE MCN des NBFLUI VECTEUR PRESSIONS AUX SOMMETS DU MAILLAGE
      CALL TNMCDC( 'REEL2', NBFLUI*NBNOPR, MNPRES )
      IF( MNPRES .EQ. 0 ) GOTO 9990
C
C     EXTRACTION DES PRESSIONS AUX NOEUDS DU MAILLAGE
      CALL VECPRES( NDIM,   NUTYEL, NBELEM, NBNOEF, MCN(MNELE+WUNDEL),
     %              NBNOVI, MCN(MNNDDL), NTDLVP, NBFLUI, MCN(MNFLUI),
     %              NBNOPR, MCN(MNPRES) )
C
C     CALCUL DE L'INTEGRALE DE LA PRESSION d'INTERPOLATION P1 SUR TOUTES
C     LES SURFACES DE L'OBJET POUR TOUS LES VECTEURS STOCKES 1 A NBFLUI
C     et AUSSI CALCUL DU COUPLE EXERCE TOUTES LES SURFACES
C     ------------------------------------------------------------------
      WRITE(IMPRIM,*)
      WRITE(IMPRIM,*)'PRESSURE INTEGRAL OVER ALL OBJECT SURFACES:'
      WRITE(IMPRIM,19991)
      IF( LANGAG .EQ. 0 ) THEN
         NMSOLU = 'PRESSION '
      ELSE
         NMSOLU = 'PRESSURE '
      ENDIF
C     A METTRE A JOUR.....
      NCAS0 = 1
      NCAS1 = 1
      CALL INSOSF( 0,     INTERPP, NTLXOB, MNDFOB,
     %             NCAS0, NCAS1, MCN(MNPRES), NMSOLU,
     %             RMCN(MNTIME+1)  )
C
      CALL TNMCDS( 'REEL2', NBFLUI*NBNOPR, MNPRES )
C
      GOTO 9999
C
C
C     PLACE MEMOIRE INSUFFISANTE
C     **************************
 9990 NBLGRC(NRERR) = 2
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1) = 'ERREUR: PLACE MEMOIRE INSUFFISANTE'
         KERR(2) = 'REDUIRE LE MAILLAGE'
      ELSE
         KERR(1) = 'ERROR: NOT ENOUGH LARGE MEMORY'
         KERR(2) = 'REDUCE the MESH'
      ENDIF
      CALL LEREUR
      IERR = 20
C
C     DESTRUCTION DES TMC DEVENUS INUTILES
C     ====================================
C     FREE MEMORY USED BY ARRAYS PG VG
 9999 IF( IERVGALLOC .EQ. 0 ) DEALLOCATE( VG )
      IF( IERPGALLOC .EQ. 0 ) DEALLOCATE( PG )
      IF( MNDP2DP2 .GT. 0 ) CALL TNMCDS( 'REEL2', 3*10*3*10, MNDP2DP2)
      IF( MNP2DP2  .GT. 0 ) CALL TNMCDS( 'REEL2', 10*3*10,   MNP2DP2 )
      IF( MNPTDV   .GT. 0 ) CALL TNMCDS( 'ENTIER', NBNOVI+1, MNPTDV  )
      IF( MNV3P2   .GT. 0 ) CALL TNMCDS( 'REEL2',  NTDLVI,   MNV3P2  )
      IF( MNV3P00  .GT. 0 ) CALL TNMCDS( 'REEL2',  NBDV3P0,  MNV3P00 )
      IF( NBPRFX   .GT. 0 ) CALL TNMCDS( 'ENTIER', NBPRFX,   MNDLPRFX)
      IF( NBPRFX   .GT. 0 ) CALL TNMCDS( 'REEL2',  NBPRFX,   MNVAPRFX)
      IF( NBVIFX   .GT. 0 ) CALL TNMCDS( 'ENTIER', NBVIFX,   MNDLVIFX)
      IF( NBVIFX   .GT. 0 ) CALL TNMCDS( 'REEL2',  NBVIFX,   MNVAVIFX)
      IF( MNNDLFX  .GT. 0 ) CALL TNMCDS( 'ENTIER', NBDLFX,   MNNDLFX )
      IF( MNVDLFX  .GT. 0 ) CALL TNMCDS( 'REEL2',  NBDLFX,   MNVDLFX )
      IF( MNFVSF   .GT. 0 ) CALL TNMCDS( 'REEL2',  MOSFOB,   MNFVSF  )
      IF( MNNDPFX  .GT. 0 ) CALL TNMCDS( 'ENTIER', NBSOM,    MNNDPFX )
      IF( MNNDVFX  .GT. 0 ) CALL TNMCDS( 'ENTIER', NBNOVI,   MNNDVFX )
      IF( MNSONO   .GT. 0 ) CALL TNMCDS( 'ENTIER', NBSOM,    MNSONO  )
      IF( MNNOSO   .GT. 0 ) CALL TNMCDS( 'ENTIER', NBNOVI,   MNNOSO  )
      IF( MNPTDP   .GT. 0 ) CALL TNMCDS( 'ENTIER', 1+NBSOM,  MNPTDP  )
C
      DMOTSTO = 901.D0+NBNOVI*2+NBPRFX+NBVIFX+NBDLFX+NBSOM*3
     %        + MOREE2*( NBNOVI+NTDLVI+NTDLVP*3
     %                  +NBPRFX+NBVIFX+NBDLFX+ MOSFOB+NBCMVG+NBCMPG )
     %        + NBFLUI + MOREE2 *NTDLVP*NBFLUI
C
      IF( NORESO .GE. 2 ) THEN
C        GRADIENT CONJUGUE STOCKAGE MORSE
C        DESTRUCTION DES TABLEAUX AUXILIAIRES
         IF( MNAUX1 .GT. 0 ) CALL TNMCDS( 'REEL2',  LOAUX,   MNAUX1 )
         IF( MNBDIR .GT. 0 ) CALL TNMCDS( 'REEL2',  LODIR,   MNBDIR )
         IF( MNLPCV .GT. 0 ) CALL TNMCDS( 'ENTIER', NBCMVG,  MNLPCV )
         IF( MNLPLC .GT. 0 ) CALL TNMCDS( 'ENTIER', NBNOVI+1,MNLPLC )
         IF( MNLPCC .GT. 0 ) CALL TNMCDS( 'ENTIER', LOLPCC,  MNLPCC )
         IF( MNPGC  .GT. 0 ) CALL TNMCDS( 'REEL2',  LOLPCC,  MNPGC  )
         IF( MNLPCP .GT. 0 ) CALL TNMCDS( 'ENTIER', NBCMPG,  MNLPCP )
         DMOTSTO = DMOTSTO + NBNOVI+1+LOLPCC+NBCMPG+NBCMVG
     %           + MOREE2*(LOAUX+LODIR+LOLPCC)
      ENDIF
C
C     BILAN SUR LA PLACE MEMOIRE CENTRALE OCCUPEE
C     ===========================================
      IF( NORESO .EQ. 1 ) THEN
C
C        FACTORISATION COMPLETE CHOLESKY STOCKAGE PROFIL
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19002) DMOTSTO
         ELSE
            WRITE(IMPRIM,29002) DMOTSTO
         ENDIF
19002 FORMAT(/'STOCKAGE PROFIL des MATRICES et VECTEURS=',G25.17,
     %' MOTS MEMOIRE'/)
29002 FORMAT(/'SKYLINE STORAGE of MATRICES and VECTORS=', G25.17,
     %' MEMORY WORDS'/)
C
      ELSE IF( NORESO .GE. 3 ) THEN
C
C        GRADIENT CONJUGUE PRECONDITIONNE PAR CROUT INCOMPLET STOCKAGE MORSE
         MOTPRE  = 1 + NBNOVI + LOLPCC * (1+MOREE2)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19004) DMOTSTO, DBLE(MOTPRE), DBLE(MOTSGC),
     %                          DMOTSTO+MOTPRE+MOTSGC
         ELSE
            WRITE(IMPRIM,29004) DMOTSTO, DBLE(MOTPRE), DBLE(MOTSGC),
     %                          DMOTSTO+MOTPRE+MOTSGC
         ENDIF
19004    FORMAT(/' MATRICES MORSE GC ',         T30,G25.17,' MOTS'/
     %           ' MATRICE PRECONDITIONNEMENT', T30,G25.17,' MOTS'/
     %           ' TABLEAUX SUPPLEMENTAIRES GC',T30,G25.17,' MOTS'/
     %           ' AU TOTAL LE GC DEMANDE',     T30,G25.17,' MOTS'/)
C
29004    FORMAT(/' CG CONDENSED MATRICES',      T30,G25.17,' WORDS'/
     %           ' PRECONDITIONED MATRIX',      T30,G25.17,' WORDS'/
     %           ' AUXILIARY ARRAYS',           T30,G25.17,' WORDS'/
     %           ' TOTAL for the CG',           T30,G25.17,' WORDS'/)
      ENDIF
C
C     TOTAL DES MOTS NECESSAIRES A LA RESOLUTION
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,19005) DMOTSTO
      ELSE
         WRITE(IMPRIM,29005) DMOTSTO
      ENDIF
19005 FORMAT(/'STOCKAGE TOTAL des MATRICES et VECTEURS=',G25.17,
     %' MOTS MEMOIRE'/)
29005 FORMAT(/'TOTAL STORAGE of MATRICES and VECTORS=', G25.17,
     %' MEMORY WORDS'/)
C
cccC     RESTAURATION DE EPZERO INITIAL
cccC     ------------------------------
ccc      EPZERO = EPZ
C
C     COUT CALCUL DES VITESSES PRESSIONS
C     ==================================
      DVITPR = DINFO( 'DELTA CPU' )
C
      RETURN
      END
