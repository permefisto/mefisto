      SUBROUTINE FPISONLTH( KNOMOB, NTLXOB, MOREE2, RELMIN, KNOMFIC,
     %                      NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                      NBTYEL, MNNPEF, NBTTEF, NUTYEL, 
     %                      NUMIOB, NUMAOB, MNDOEL,
     %                      IEFOIN, IEFOCL, IEBLPR,
     %                      NORESO, MNPTDV, MNLPCV, NIVOGC, NBCMVG,
     %                      NBDLFX, MNNDLFX,MNVDLFX,
     %                      DT,     DTSTOC, TPSINI, TPSFIN,
     %                      NBFLUI, MXFLUI, NTDLVP, MNNDDL, MNVXYZP0,
     %                      NTVECT, MNVECT, VITMAX, VITMOY,
     %                      MXPAST, RESULT,
     %                      DFABG,  DFACTO, DVITPR, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : CALCULER LES VITESSES PRESSION DANS UN DOMAINE 3D
C ----- DU PROBLEME DE NAVIER-STOKES INSTATIONNAIRE
C       SELON UNE METHODE PISO SEMI IMPLICITE AVEC PAS DE TEMPS CONSTANT
C       POUR DES TETRAEDRES DE TAYLOR-HOOD AVEC PRISE EN COMPTE DIRECTE
C       DU TERME NON LINEAIRE DE TRANSPORT (V.Grad)V
C
C       DENSITE DE MASSE Rho, VISCOSITE, Mhu SONT INDEPENDANTS DU TEMPS
C       SI FORCE INTERNE DE GRAVITE (-Rho g z) => A ENTRER EN DONNEES
C
C       FORCE EXTERNE SUR LA FRONTIERE
C       BLOCAGE DE LA VITESSE  IMPOSEE
C       BLOCAGE DE LA PRESSION IMPOSEE   PEUVENT DEPENDRE DU TEMPS
C
C***********************************************************************
C       NS Option8: METHODE PISO (Pressure Implicit with Split Operator)
C       AVEC LE TERME NON LINEAIRE DE TRANSPORT (V.Grad)V :
C
C       A L'ETAPE n=0,  V0 = V(t0),  P0 = P(t0)=P0
C
C       A L'ETAPE n+1, LE PROBLEME CONSISTE A TROUVER
C       {V(tn+1), P(tn+1)} = lim {Vm+1, Pm+1} quand m->Infini
C       SOLUTION de
C
C (0)    m=0; Vm=V(tn);  Pm=P(tn);  (calculees juste avant)
C
C       Calcul de la Vitesse 
C (1)   (1/dt - Mhu/Rho Laplacien) {Vm+1,i(X)} = Fg(tn+1)/Rho
C         + 1/dt {V(tn)} - (Vm.Grad) {Vm,i(X)} - 1/Rho Gradi Pm(X)
C       Condition aux limites
C       Vm+1,i(tn+1,X) = Vi(tn+1,X)     avec X sur Gamma
C       -Mhu/Rho dVm+1,i(tn+1,X)/dn = 0   avec X sur Frontiere-Gamma
C       pour  i=1,...,NDIM
C
C       Calcul de la Pression
C (2)   - 1/Rho Laplacien Pm+1(X) =
C       Div { -Fg(tn+1)/Rho + (Vn+1,m+1(X).Grad) Vn+1,m+1(X) }
C                                       X dans le domaine
C       Condition aux limites
C       - 1/Rho dPm+1(Gamma)/dn =
C       n . { -Fg(tn+1)/Rho + (Vn+1,m+1(gamma).Grad) Vn+1,m+1(gamma) }
C       or Pn+1,m+1(Gamma) = P(tn+1,Gamma) pour Gamma sur la frontiere
C
C       Si ||Vi,n+1,m+1 - Vi,n+1,m|| > Eps ||Vi,n+1,m+1||
C       ou ||P  n+1,m+1 - P  n+1,m|| > Eps ||P  n+1,m+1|| aller en (1)
C       Sinon
C          Si T0 + n dt < Tfinal alors n=n+1; tn+1=tn+dt; goto (0)
C
C***************************************************************************
C
C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET A TRAITER
C NTLXOB : NUMERO DU TMS DU LEXIQUE DE L'OBJET KNOMOB
C MOREE2 : NOMBRE DE MOTS   D'UNE VARIABLE REELLE DOUBLE PRECISION
C RELMIN : PLUS PETIT REEL SERVANT DE MARQUEUR DE NON UTILISATION
C KNOMFIC: NOM DU FICHIER SUPPORT DU VECTEUR VITESSE+PRESSION
C NDIM   : DIMENSION DES COORDONNEES DES POINTS ( 2 OU 3 )
C MNXYZN : ADRESSE MCN DU TABLEAU XYZNOEUD DE L'OBJET KNOMOB
C          XYZ des SOMMETS+BARYCENTRES    DES EF BREZZI-FORTIN
C          XYZ des SOMMETS+MILIEUX ARETES DES EF TAYLOR-HOOD
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBNOVI : NOMBRE DE NOEUDS DU MAILLAGE (SOMMETS ET MILIEUX DES ARETES)
C
C NBTYEL : NOMBRE DE TYPES D'EF DU MAILLAGE DE CET OBJET
C MNNPEF : ADRESSE MCN DU TABLEAU DES ADRESSES MCN DES TMS NPEF"TYPE EF
C NBTTEF : NOMBRE TOTAL D'EF DU MAILLAGE
C
C NUMIOB : NUMERO MINIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C NUMAOB : NUMERO MAXIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C MNDOEL : LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C          TABLEAUX DECRIVANT LES DONNEES DU FLUIDE DE L'OBJET COMPLET
C IEFOCL :=0 SI PAS DE FORCES DONNEES SUR LES FRONTIERES
C         >0 NOMBRE DE TMS FORCE "AUX LIMITES" DES PLS DE L'OBJET RETROUVES
C IEFOCL :=0 SI PAS DE FORCES DONNEES SUR LES FRONTIERES
C         >0 NOMBRE DE TMS FORCE "AUX LIMITES" DES PLS DE L'OBJET RETROUVES
C IEBLPR : NOMBRE DE TMS BLPRESSION       DES PLS DE L'OBJET TROUVE
C
C NORESO : CODE RESOLUTION DES SYSTEMES LINEAIRES
C          1 FACTORISATION COMPLETE DE CHOLESKY ET MATRICE PROFIL
C          2 GRADIENT CONJUGUE SIMPLE ET STOCKAGE MORSE DE LA MATRICE
C          3 GRADIENT CONJUGUE PRECONDITIONNE PAR NIVEAUX ET STOCKAGE MORSE DES
C            NON PROGRAMME
C MNPTDV : ADRESSE MCN DES POINTEURS SUR LES COEFFICIENTS DIAGONAUX DE VG
C MNLPCV : ADRESSE MCN DU NUMERO DE COLONNE DES COEFFICIENTS DE VG MORSE
C NIVOGC : NIVEAU DE FACTORISATION INCOMPLETE DE LA MATRICE DE PRECONDITIONNEMEN
C          A CHOISIR PARMI 0 1 2
C NBCMVG : NOMBRE DE COEFFICIENTS STOCKES D'UNE MATRICE GLOBALE VITESSE  VG
C          NON DIAGONALE
C
C NBDLFX : NOMBRE DE DL VITESSE ET PRESSION FIXES
C MNNDLFX: ADRESSE MCN DU TABLEAU DU NO GLOBAL DE CHAQUE DL FIXE
C MNVDLFX: ADRESSE MCN DU TABLEAU DE LA VALEUR DE CHAQUE DL FIXE
C
C DT     : PAS CONSTANT DU TEMPS
C DTSTOC : PAS CONSTANT DU TEMPS ENTRE 2 STOCKAGES DU VECTEUR"VITESSEPRESSION
C TPSINI : TEMPS INITIAL DU CALCUL
C TPSFIN : TEMPS FINAL   DU CALCUL
C NBFLUI : NUMERO DU DERNIER VECTEUR VITESSEPRESSION STOCKE
C MXFLUI : NOMBRE DE VECTEUR"VITESSEPRESSION A STOCKER
C
C NTDLVP : NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET AVEC BARYCENTRES SI BF
C          DL VITESSES et PRESSION
C MNNDDL : NUMERO DU DERNIER DL VZ ou Pr DE CHAQUE NOEUD VITESSE (0:NBNOVI)
C          A UTILISER POUR RETROUVER LES DL PRESSION DES VECTEURS VXYZP
C MNVXYZP0: ADRESSE MCN DE VITESSEXYZ+PRESSION AUX NOEUDS A L'INSTANT INITIAL
C
C MODIFIES:
C ---------
C VITMAX : NORME MAXIMALE INITIALE PUIS FINALE DE LA VITESSE AUX NOEUDS
C VITMOY : NORME MOYENNE  INITIALE PUIS FINALE DE LA VITESSE AUX NOEUDS
C
C SORTIES:
C --------
C RESULT : (TEMPS, VitesseMoyenne, VitesseMax,
C           PressionMoyenne, Pression Max-Min,
C           FLUX-, FLUX+) a chaque temps calcule
C NTVECT : NUMERO      DU TMS VECTEUR"VITESSEPRESSION DE L'OBJET
C MNVECT : ADRESSE MCN DU TMS VECTEUR"VITESSEPRESSION DE L'OBJET
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Texas A&M University at QATAR       Mars 2012
C23456---------------------------------------------------------------012
C     VALEUR A METTRE SUR LA DIAGONALE DE LA MATRICE PROFIL
C     POUR TRAITER LES DL FIXES
      DOUBLE PRECISION  TGV
      PARAMETER        (TGV = 1D30)
      PARAMETER        (mIterMax = 16)
C
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/donflu.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a_objet__definition.inc"
      include"./incl/a___arete.inc"
      include"./incl/a___face.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
      include"./incl/a___profil.inc"
      include"./incl/a___morse.inc"
      include"./incl/a___temperinit.inc"
      include"./incl/msvaau.inc"
      include"./incl/ponoel.inc"
      include"./incl/ctemps.inc"
      include"./incl/cthet.inc"
      include"./incl/cnonlin.inc"
      include"./incl/nctyef.inc"
      include"./incl/p2p22d.inc"
      include"./incl/p2p23d.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1), RMCN(1), DMCN(1))
ccc      COMMON / EPSSSS / EPZERO, EPSXYZ
C
      LOGICAL           COMP
      CHARACTER*(*)     KNOMOB, KNOMFIC
      CHARACTER*4       NOMELE(2)
      CHARACTER*12      NMSOLU
      INTEGER           NUMIOB(4), NUMAOB(4), MNDOEL(4)
      INTEGER           NOOBVC(1), NOOBSF(6), NOOBLA(12), NOOBPS(8)
      INTEGER           NOSOEF(4), NONOTE(10)
      REAL              DT, DTSTOC, TPSINI, TPSFIN, XYZEF(30)
      DOUBLE PRECISION  RELMIN, CoBOUS
      DOUBLE PRECISION  DELTAT, Rho, Mhu, UnSRho, UnSdt, MhuSRho, CoGrPr
      DOUBLE PRECISION  AE(55)
      DOUBLE PRECISION  VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY,
     %                  FLUNEG, FLUPOS, DIFVIM, NORVIM, NORVIM0,
     %                  DIFPRM, NORPRM, NORPRM0, DIFMAX, VITFXMAX
      DOUBLE PRECISION  RESULT(8,MXPAST),
     %                  VMAXMAX, VMOYMOY, PMXIMXI, PMOYMOY
      DOUBLE PRECISION  STGV, DINFO, DFABG, DFACTO, DVITPR, DMOTSTO
      INTRINSIC         DBLE, ABS
C
      DOUBLE PRECISION, allocatable, dimension(:) :: PG, VG
      INTRINSIC         ALLOCATED
      EXTERNAL          ETTAEL
C
      WRITE(IMPRIM,*) 'Start of PISO+(V.Grad)V IMPLICIT NAVIER-STOKES SO
     %LVER on the OBJECT ',KNOMOB
C
cccC     LAISSE A L'APPRECIATION DE L'UTILISATEUR Option 20; 2; EPZERO
cccC     PROTECTION DE EPZERO INITIAL
ccc      EPZ     = EPZERO
ccc
      NIVMAX  = 10
      MNTPOB  = 0
      MNLPLC  = 0
      MNLPCC  = 0
      MNLPLU  = 0
      MNPGC   = 0
      MNAUX1  = 0
      MNLPCP  = 0
      MNLGMM  = 0
      MNPRE   = 0
      MNBDIR  = 0
      LODIR   = 0
      MNNDPFX = 0
      MNNDVFX = 0
      NBPRFX  = 0
      MNVAPRFX= 0
      MNDLPRFX= 0
      NBVIFX  = 0
      MNDLVIFX= 0
      MNVAVIFX= 0
      MNFVSF  = 0
      MNV3P00 = 0
      MNV3P1  = 0
      MNV3P2  = 0
      IERPGALLOC = 1
      IERVGALLOC = 1
      MNLGAU  = 0
      MOSFOB  = 0
      LOAUX   = 0
      LODIR   = 0
      LOLPCC  = 0
      MOTSGC  = 0
      MOTPRE  = 0
      MNNOSO  = 0
      MNSONO  = 0
      MNPTDP  = 0
      MNP2DP2 = 0
      MNDP2DP2= 0
      NBCMPG  = 0
      NBMXVP0 = 0
      MNPRPR = 0
      MNAUX1 = 0
      MNAUX2 = 0
      MNAUX3 = 0
      MNDIR  = 0
      MNDAD  = 0
      MNBET  = 0
      MNADIR = 0
      MOTSGC = 0
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000)
      ELSE
         WRITE(IMPRIM,20000)
      ENDIF
10000 FORMAT(/' MASSE VOLUMIQUE, VISCOSITE sont INDEPENDANT du TEMPS et
     %de la VITESSE')
20000 FORMAT(/' DENSITY of MASS, VISCOSITY ARE INDEPENDENT of TIME and
     %VELOCITY')
C
C     STOCKAGE DES RESULTATS
      NBPASDT = 0
      NBPASR  = 1
      RESULT(1,NBPASR) = TEMPS
      RESULT(2,NBPASR) = VITMOY
      RESULT(3,NBPASR) = VITMAX
      RESULT(4,NBPASR) = 0D0
      RESULT(5,NBPASR) = 0D0
      RESULT(6,NBPASR) = 0D0
      RESULT(7,NBPASR) = 0D0
      RESULT(8,NBPASR) = NBPASDT
C
C     LE PAS DE TEMPS EN DOUBLE PRECISION
      DELTAT = DT
      UnSdt = 1D0 / DELTAT
C
C     LE TYPE DES EF DU MAILLAGE EST IL TETRAEDRE TAYLOR-HOOD
C     -------------------------------------------------------
C     MNELE : ADRESSE DU TABLEAU NPEF"TYPE EF (TRIA 2P2C ou TETR 3P2C)
      MNELE  = MCN( MNNPEF )
C
C     NOMBRE D'ELEMENTS FINIS DE CE TYPE
      NBELEM = MCN( MNELE + WBELEM )
C
C     LE NUMERO DU TYPE DE L'ELEMENT FINI (TRIA 2P2C ou TETR 3P2C)
      NUTYEL = MCN( MNELE + WUTYEL )
C
C     MNPGEL ADRESSE MCN DES NUMEROS NOEUDS ET POINTS GEOMETRIQUES DES EF
      MNPGEL = MNELE + WUNDEL
C
C     LES CARACTERISTIQUES DE L'ELEMENT FINI
      CALL ELNUNM( NUTYEL, NOMELE )
      CALL ELTYCA( NUTYEL )
      IF( NUTYEL .EQ. 15 ) THEN
C        TRIANGLE DE TAYLOR-HOOD
         NDIM   = 2
C        NOMBRE DE NO DE SOMMETS D'UN EF DANS NPEF
         NBNOEF = 6
      ELSE IF( NUTYEL .EQ. 20 ) THEN
C        TETRAEDRE DE TAYLOR-HOOD
         NDIM   = 3
C        NOMBRE DE NO DE SOMMETS D'UN EF DANS NPEF
         NBNOEF = 10
      ELSE
C        MAILLAGE D'EF NON TETRAEDRE DE TAYLOR-HOOD
         IERR = 1
         GOTO 9999
      ENDIF
C
C     NOMBRE DE DL DE LA VITESSE
      NTDLVI = NDIM * NBNOVI
C
C     NBSOM EST LE NOMBRE DE DL DE LA PRESSION
C
C     NOMBRE TOTAL DE DL VITESSE+PRESSION
      NTDLVP = NTDLVI + NBSOM
C
C     RECUPERATION DES DONNEES SUPPOSEES CONSTANTES DANS LE FLUIDE
C     SUR LE PREMIER ELEMENT FINI DU MAILLAGE: Rho, Mhu, CoGrPr
C     ------------------------------------------------------------
C     NO DES NOEUDS DE L'ELEMENT FINI 1
      NUELEM = 1
      CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOTE )
C     NO DU BARYCENTRE
      NONOTE(NBNOEF+1) = NBSOM + NUELEM
C
C     NO DES POINTS LIGNES SURFACES VOLUMES DES SOMMETS ARETES FACES VOLUME
      CALL EFPLSV( MNELE , NUELEM,
     %             NOVCEL, NOSFEL, NOLAEL, NOPSEL,
     %             NOOBVC, NOOBSF, NOOBLA, NOOBPS )
C
C     INTERPOLATION DU FLUIDE avec F: echapeau->e  P1 ndim
C     COORDONNEES XYZEF(NBNOEF,3) DES SOMMETS=POINTS DE L'EF
      CALL EFXYZP( NDIM, MNXYZN, NBELEM, NUELEM, MNPGEL, NBNOEF,
     %             XYZEF )
C
C     RECHERCHE DE LA DENSITE DE MASSE Rho, VISCOSITE DYNAMIQUE Mhu,
C     COEFFICIENT DE LA PRESSION CoGrPr AU BARYCENTRE DE L'EF 1
      IF( NDIM .EQ. 2 ) THEN
         CALL REDOFL( NBNOEF, NDIM, XYZEF,
     %                NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                Rho, Mhu, CoGrPr, CoBOUS )
      ELSE
         CALL REDOFL( NBNOEF, NDIM, XYZEF,
     %                NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                Rho, Mhu, CoGrPr, CoBOUS )
      ENDIF
C
C     COEFFICIENTS INTERNES A EOL (DIVISION PAR Rho DES EQUATIONS)
C     COEFFICIENT DEVANT LA PRESSION
      UnSRho  = 1D0 / Rho
C     VISCOSITE CINEMATIQUE DU FLUIDE
      MhuSRho = Mhu / Rho
C
C     REMARQUE: CoGrPr n'est pas utilise ici.
C               IMPIVITEMENT IL VAUT 1/Rho
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10010) DELTAT, Rho, Mhu, MhuSRho
      ELSE
         WRITE(IMPRIM,20010) DELTAT, Rho, Mhu, MhuSRho
      ENDIF
10010 FORMAT(/' Pas de TEMPS dt=',G15.7,'  Rho=',G15.7,'  Mhu=',G15.7,
     %'  Mhu/Rho=',G15.7/)
20010 FORMAT(/' Time STEP dt=',G15.7,'  Rho=',G15.7,'  Mhu=',G15.7,
     %'  Mhu/Rho=',G15.7/)
C
C     CONSTRUCTION DU TABLEAU NO SOMMET => NO NOEUD
C     CONSTRUCTION DU TABLEAU NO NOEUD  => NO SOMMET
C     ==============================================
      CALL TNMCDC( 'ENTIER', NBSOM,  MNSONO )
      CALL TNMCDC( 'ENTIER', NBNOVI, MNNOSO )
C
      CALL AZEROI( NBNOVI,  MCN(MNNOSO) )
C     NOMBRE DE SOMMETS DE l'EF
      NBS = NDIM + 1
      DO NUELEM = 1, NBELEM
         CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOTE )
         DO I = 1, NBS
            MCN( MNNOSO-1+NONOTE(I) ) = 1
         ENDDO
      ENDDO
C
C     NUMEROTATION DES SOMMETS DANS L'ORDRE DES NOEUDS (VITESSE)
      NBSOM = 0
      DO I = 1, NBNOVI
         IF( MCN(MNNOSO-1+I ) .GT. 0 ) THEN
            NBSOM = NBSOM + 1
            MCN( MNNOSO-1+I     ) = NBSOM
            MCN( MNSONO-1+NBSOM ) = I
         ENDIF
      ENDDO
C
C     ================================================================
C     CONSTRUCTION DE LA MATRICE GLOBALE DE PRESSION [PG(UnSRho)]
C     UnSRho Integrale grad P1 grad P1 dX avec INTERPOLATION P1
C     ================================================================
C     MATRICE PRESSION SYMETRIQUE NON DIAGONALE
      NCODSP = 1
C
C     CONSTRUCTION DU POINTEUR SUR LA DIAGONALE DE PG
      CALL TNMCDC( 'ENTIER', 1+NBSOM, MNPTDP )
C
      IF( NORESO .EQ. 1 ) THEN
C
C        CHOLESKY PROFIL POUR LA PRESSION
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10001)
         ELSE
            WRITE(IMPRIM,20001)
         ENDIF
C
C        MAX POUR CALCULER LE MIN DES VOISINS D'UN SOMMET
         DO N = 1, NBSOM
            MCN( MNPTDP+N ) = NBSOM
         ENDDO
C
         DO NUELEM = 1, NBELEM
C           LES NUMEROS DES NOEUDS DE L'EF
            CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOTE )
C           RECHERCHE DU NUMERO MINIMAL DES SOMMETS DE l'EF
            NOSOEF(1) = MCN( MNNOSO-1+NONOTE(1) )
            NOMIN     = NOSOEF(1)
            DO I = 2, NBS
C              NUMERO DU SOMMET I
               NOSOEF(I) = MCN( MNNOSO-1+NONOTE(I) )
C              NUMERO MINIMAL DES SOMMETS
               NOMIN = MIN( NOMIN, NOSOEF(I) )
            ENDDO
            DO I = 1, NBS
C              NUMERO DU SOMMET I
               N = NOSOEF(I)
C              NUMERO MINIMUM DES VOISINS DU SOMMET N
               MCN( MNPTDP+N ) = MIN( NOMIN, MCN( MNPTDP+N ) )
            ENDDO
         ENDDO
C
C        POINTEUR SUR LE COEFFICIENT DIAGONAL DE LA MATRICE PRESSION
         MCN( MNPTDP ) = 0
         DO N = 1, NBSOM
            M = MNPTDP+N
C           NPMBRE DE COEFFICIENTS ENTRE LE MIN ET DIAGONAL
            MCN( M ) = MCN( M-1 ) + N - MCN( M ) +1
         ENDDO
C        NOMBRE DE COEFFICIENTS DE LA MATRICE PROFIL DE PRESSION
         NBCMPG = MCN( MNPTDP+NBSOM )
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        GRADIENT CONJUGUE SIMPLE SUR MATRICE MORSE DE LA PRESSION
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10002)
         ELSE
            WRITE(IMPRIM,20002)
         ENDIF
C
cccC        REDUCTION DU SEUIL POUR STOPPER LES ITERATIONS DU GC
ccc         EPZERO = 1E-4
cccC
cccC        CALCUL DES POINTEURS DE LA MATRICE MORSE PRESSION P1 PAR SOMMETS
cccC        ALORS QUE LE MAILLAGE EST P2
ccc         CALL PRGCMCST( NBSOM, MCN(MNNOSO), MNTOPO, MCN(MNNPEF), MNXYZN,
ccc     %                  1, NCODSP, MNPTDP, MNLPCP, IERR )
C
C        CALCUL   DES POINTEURS DE LA MATRICE MORSE PRESSION P1 PAR SOMMETS
C        A PARTIR DES POINTEURS DE LA MATRICE MORSE VITESSE  P2 PAR NOEUDS
         CALL PRGCVP( NBSOM,NBNOVI, MCN(MNNOSO), NCODSP, MNPTDV, MNLPCV,
     %                MNPTDP, MNLPCP, IERR )
         IF( IERR .NE. 0 ) GOTO 9990
C
C        NOMBRE DE COEFFICIENTS DE LA MATRICE MORSE PRESSION AUX SOMMETS
         NBCMPG = MCN( MNPTDP + NBSOM )
C
      ELSE IF( NORESO .EQ. 3 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10003)
         ELSE
            WRITE(IMPRIM,20003)
         ENDIF
C
      ELSE
C        ERREUR SUR NORESO
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10004) NORESO
         ELSE
            WRITE(IMPRIM,20004) NORESO
         ENDIF
         IERR = 1
         GOTO 9999
      ENDIF
10001 FORMAT('CONSTRUCTION de la MATRICE PROFIL [PG] de PRESSION'/
     %'RESOLUTION par FACTORISATION COMPLETE L tL de CHOLESKY')
20001 FORMAT(' CONSTRUCTION of the PRESSURE SKYLINE MATRIX [PG]'/
     %'SOLUTION by COMPLETE CHOLESKY FACTORIZATION L Lt')
10002 FORMAT('CONSTRUCTION de la MATRICE MORSE [PG] de PRESSION'/
     %'RESOLUTION par GRADIENT CONJUGUE SIMPLE')
20002 FORMAT(' CONSTRUCTION of the PRESSURE CONDENSED MATRIX [PG]'/
     %'SOLUTION by CONDENSED MATRIX SIMPLE CONJUGATE GRADIENT')
10003 FORMAT('CONSTRUCTION de la MATRICE MORSE [PG] de PRESSION'/
     %'RESOLUTION par GRADIENT CONJUGUE PRECONDITIONNE')
20003 FORMAT(' CONSTRUCTION of the PRESSURE CONDENSED MATRIX [PG]'/
     %'SOLUTION by CONDENSED MATRIX PRECONDITIONED CONJUGATE GRADIENT')
10004 FORMAT('CODE DE RESOLUTION INCONNU  NORESO=',I5/)
20004 FORMAT('UNKNOWN SOLUTION CODE  NORESO=',I5/)
C
C     MODE DE PRISE EN COMPTE DES CONDITIONS AUX LIMITES DE DIRICHLET
C     SUR LES SYSTEMES LINEAIRES avec LES MATRICES VG PG
C     ---------------------------------------------------------------
      IF( NORESO .EQ. 1 ) THEN
C        MATRICE PROFIL
         STGV = TGV
      ELSE
C        MATRICE MORSE
         STGV = 1D0
      ENDIF
C
C     MATRICE DE PRESSION P1 SYMETRIQUE NON DIAGONALE
C     ===============================================
      NCODSP = 1
C     NBCMPG : NOMBRE DE REELS DOUBLE PRECISION DE LA MATRICE PG
