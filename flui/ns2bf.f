      CALL VITEFXBG( NBVIFX, MCN(MNDLVIFX), MCN(MNVAVIFX), STGV,
     %               VXVYVZPR(1,NVITC), NDIM, NBNOVI, VXVYVZPR(1,NVP2) )


C     RESOLUTION EN VITESSES: i=1,...,NDIM
C     Integrale  Rho tP1B P1B + dt Mhu tGrad P1B Grad P1B dx U*i(tn+1)
C   = Integrale  Rho tP1B Ui(tn,X(tn;tn+1,bl) dX
C   - Integrale  dt CoGrPr tP1B Grad P(tn) dX
C   + Integrale  dt        tP1B Force(tn)  dX     => U*(tn+1)
C     ================================================================
C     COPIE ESSENTIELLE POUR GARDER BG AUX BARYCENTRES ET REMONTER
      CALL TRTATD( VXVYVZPR(1,NVP2), VXVYVZPR(1,NVP1), NDIM*NBNOVI )

      N1 = 1
      IF( NORESO .EQ. 1 ) THEN

