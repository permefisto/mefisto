         CALL CHOLPR( NBSOM, NCODSV, LP1LIGN, VG,  VG, IERR )
         call affvect( 'VG=L tL PROFIL', 20, VG )
         call afl1ve(  'VG=L tL PROFIL', NBCVPG, VG )
      ENDIF

C     TEMPS CALCUL DE FORMATION DES MATRICES GLOBALES ET FACTORISATION
      DFACTO = DINFO( 'DELTA CPU' )

C     DECLARATIONS DES 4 VECTEURS GLOBAUX DANS LE VECTEUR VXVYVZPR
