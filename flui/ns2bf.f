C     DECLARATION DU VECTEUR V3P0 SOLUTION AU TEMPS 0 EN 4 VECTEURS VX VY VZ PR
C     DECLARATION DU VECTEUR V3P1 SOLUTION AU TEMPS 1 EN 4 VECTEURS VX VY VZ PR
C     DECLARATION DU VECTEUR V3P2 AUXILIAIRE          EN 4 VECTEURS VX VY VZ PR
C     DECLARATION DU VECTEUR VITC VITESSE CONVECTEE   EN 4 VECTEURS VX VY VZ PR
      IF( LANGAG .EQ. 0 ) THEN

         ALLOCATE ( VXVYVZPR( 1:NTDLVP, 0:3 ), STAT=IERVXVYVZPR )
         IF( IERVXVYVZPR .NE. 0 ) THEN
         PRINT*,'fluidens: ERREUR ALLOCATION VXVYVZPR(1:',NTDLVP,
     %'0:3) DREELS'
            IERR = IERVXVYVZPR
            GOTO 9993
         ENDIF
       PRINT*,'fluidens: CORRECTE ALLOCATION VXVYVZPR(1:',NTDLVP,
     %',0:3) DREELS'

      ELSE

         ALLOCATE ( VXVYVZPR( 1:NTDLVP, 0:3 ), STAT=IERVXVYVZPR )
         IF( IERVXVYVZPR .NE. 0 ) THEN
          PRINT*,'fluidens: ALLOCATION ERROR VXVYVZPR(1:',NTDLVP,
     %',0:3) DREALS'
            IERR = IERVXVYVZPR
            GOTO 9993
         ENDIF
        PRINT*,'fluidens: CORRECT ALLOCATION VXVYVZPR(1:',NTDLVP,
     %',0:3) DREALS'

      ENDIF

C     NUMERO DES VECTEURS VITESSE+PRESSION DANS VXVYVZPR
C     POUR EVITER DES PERMUTATIONS COUTEUSES...
      NVP0 = 0
      NVP1 = 1
      NVP2 = 2
      NVITC= 3

C     NO DU PREMIER VITX VITY VITZ PRESSION DANS VXVYVZPR
      N1X = 1
      N1Y = N1X + NBNOVI
      N1Z = N1Y + NBNOVI
      N1P = N1X + NBNOVI * NDIM
      N0P = N1P - 1

C     VXVYVZPR( 1;NTDLVP, NVITC=3 ) EST LE TABLEAU AUXILIAIRE DE LA
C     VITESSE CONVECTEE AUX SEULS SOMMETS DU MAILLAGE
C     ZERO AUX BARYCENTRES DES EF CAR NON CONVECTES
C    (SEULEMENT AUX SOMMETS)
      CALL AZEROD( NTDLVP, VXVYVZPR(1,NVITC) )

C     DECOMPOSITION DU VECTEUR V3P0 SOLUTION AU TEMPS 0 EN 4 VECTEURS
C     MN VXYZP0 => VITX0, VITY0, VITZ0, PRES0
      CALL VPNOVXVYVZPR( NDIM,  NUTYEL, NBELEM,NBS1EF,MCN(MNELE+WUNDEL),
     %                   NDDLNO, NTDLVP, VXYZP0,
     % NBNOVI, VXVYVZPR(N1X,NVP0),VXVYVZPR(N1Y,NVP0),VXVYVZPR(N1Z,NVP0),
     % NBSOM,  VXVYVZPR(N1P,NVP0) )

C     MNVITI NORMALEMENT L'ADRESSE DE LA VITESSEPRESSION A L'ITERATION 0
C     DU POINT FIXE NON UTILISE ICI PUISQUE PROBLEME LINEAIRE CAR
C     LA NON LINEARITE EST PRISE EN COMPTE PAR L'INTEGRATION RETROGRADE
C     EN TEMPS LE LONG DES CARACTERISTIQUES
ccc      MNVITI = 0

      IF( NDIM .EQ. 3 ) THEN
C        FLUX DE LA VITESSE SUR LES SURFACES DE L'OBJET POUR UN TEMPS DONNE
         MOSFOB = NUMAOB(3) - NUMIOB(3) + 1
      ELSE
C        FLUX DE LA VITESSE SUR LES ARETES DE L'OBJET POUR UN TEMPS DONNE
         MOSFOB = NUMAOB(2) - NUMIOB(2) + 1
      ENDIF
      CALL TNMCDC( 'REEL2', MOSFOB, MNFVSF )

C     BILAN DE L'INSTANT INITIAL
