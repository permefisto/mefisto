C        DES NDIM COMPOSANTES DE LA VITESSE U*(tn+1)
         DO K = 1, NDIM
           CALL DRCHPR( NBSOM, NCODSV, LP1LIGN, VG, VXVYVZPR(N1,NVP2),3,
     %                  VXVYVZPR(N1,NVP1) )

