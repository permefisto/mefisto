      CALL DLGABF( NDIM,   NBELEM, NBS1EF, MCN(MNELE+WUNDEL),
     %             NOBARY, AGGAUSS, NBSOM, NBNOVI, VXVYVZPR(1,NVP1) )
ccc      call afl1ve( 'Apres DLGABF U* V3P1=', NBNOVI, VXVYVZPR(1,NVP1) )


