         VG( NDIAG ) = STGV
      ENDDO
      call affvect( 'VG apres CL', 20, VG )
      call afl1ve(  'VG apres CL', NBCVPG, VG )

      IF( NORESO .EQ. 1 ) THEN

C        FACTORISATION COMPLETE DE CHOLESKY  VG = L tL
