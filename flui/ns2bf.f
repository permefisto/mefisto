ccc     %      ' sin(Pi*(t/2.5 + 4./3))=',sin( Pi*(temps/2.5 + 4./3.) )

ccc      call affvect( 'V3P0',  20, VXVYVZPR(1,NVP0) )
ccc      call affvect( 'PRES0', 20, VXVYVZPR(N1P,NVP0) )

C     CALCUL DES VITESSES0 CONVECTEES U(tn,X(tn;tn+1,SOMMETS))
C     --------------------------------------------------------
      IF( NBVCFX .GT. 0 ) THEN
         CALL CONVECBF( tn, tn1, NDIM,  NBSOM, MCN(MNXYZN+WYZNOE),
     %                  NBS1EF, NBELEM, MCN(MNELE+WUNDEL), NO1EFN,
     %                  MOARET, MXARET, MNLARE,
     %                  MOFACE, MXFACE, MCN(MNLFAC),
     %                  NDDLNO, NTDLHB, VXYZP0, VITMAX0,
     %                  NBNOVI, VXVYVZPR(1,NVITC), IERR )
      ENDIF

C     EF BREZZI-FORTIN P1BULLE EN VITESSE et P1 EN PRESSION
