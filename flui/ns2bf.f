      IF( NORESO .EQ. 1 ) THEN
C        MATRICE PROFIL
         STGV = TGV
      ELSE
C        MATRICE MORSE
         STGV = 1D0
      ENDIF

      DO I = 1, NBVIFX/NDIM
C        LE NO GLOBAL DU DL FIXE DE LA PREMIERE COMPOSANTE DE LA VITESSE
         NDL = MCN( MNDLVIFX - 1 + I )
C        LE NO DU COEFFICIENT DIAGONAL NDL
         NDIAG = LP1LIGN( NDL )
