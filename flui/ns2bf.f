         CALL DRCHPR( NBSOM, NCODSP, LP1LIGN, PG, VXVYVZPR(N1P,NVP2), 3,
     %                VXVYVZPR(N1P,NVP1) )
ccc         call affvect( 'Apres DRCHPR PR1-PR0=', 20,    VXVYVZPR(N1P,NVP1) )
ccc         call afl1ve(  'Apres DRCHPR PR1-PR0=', NBSOM, VXVYVZPR(N1P,NVP1) )

      ELSE

C        ---------------------------------------------------
C        RESOLUTION de P(tn+1)-P(tn) = [PG]-1 (-Rho div u*)
C        PAR GRADIENT CONJUGUE AVEC MATRICE SYMETRIQUE MORSE
C        ---------------------------------------------------
C        X0 DU GC EST MIS A ZERO CAR P(tn+1)-P(tn) EST PROCHE DE ZERO
         CALL AZEROD( NBSOM, VXVYVZPR(N1P,NVP1) )
C
C        RESOLUTION PAR GC SUR LA MATRICE PG
C        LE VECTEUR INITIAL DU GC EST LE DERNIER VECTEUR CALCULE
         CALL GCAXBK( NBSOM, NODPFX, LP1LIGN, LP1COLO, PG,
     %                VXVYVZPR(N1P,NVP2),  VXVYVZPR(N1P,NVP1),
     %                DAUX1, DAUX2, DAUX3,
     %                VXVYVZPR(N1P,NVP1),  KGCPRE )
ccc         call affvect( 'Apres Gcaxbk PR1-PR0=', 20,    VXVYVZPR(N1P,NVP1) )
ccc         call afl1ve(  'Apres Gcaxbk PR1-PR0=', NBSOM, VXVYVZPR(N1P,NVP1) )

      ENDIF


C     ==========================================================================
C     (Rho-DeltaT Mhu Laplacien)(ui(tn+1)-u*i)=- dt CoGrPr gradi(p(tn+1)- p(tn))
C     VITCi = - dt CoGrPr gradi (p(tn+1)- p(tn))  i=1,...,3
C     ==========================================================================
      CALL BGGRABF( DELTAT, CoGrPr, NDIM,  NBSOM, NBNOVI,
     %              MCN(MNXYZN+WYZNOE),
     %              NUTYEL, NBS1EF, NBELEM, MCN(MNELE+WUNDEL),
     %              VXVYVZPR(N1P,NVP1), NOBARY, AGGAUSS,
     %              VXVYVZPR(1,NVITC) )
ccc      call affvect( 'BGGRABF BG=', 20,          VXVYVZPR(1,NVITC) )
ccc      call afl1ve(  'BGGRABF BG=', NBNOVI*NDIM, VXVYVZPR(1,NVITC) )

C     BLOCAGE DES DL VITESSES U(tn+1)-U* FIXEES A ZERO
C     CAR U*=U(tn+1) sur la FRONTIERE DIRICHLET DU PROBLEME
C     -----------------------------------------------------
      DO I=1, NBVIFX
C        LE NO GLOBAL DU DL FIXE
         NDL = MCN( MNDLVIFX - 1 + I )
C        VALEUR FIXEE DE LA VITESSE  ( Ui(tn+1)-Ui* )=0
         VXVYVZPR( NDL, NVITC ) = 0D0
      ENDDO

C     RESOLUTION des NDIM COMPOSANTES DE LA VITESSE Ui(tn+1)-U*i: i=1,...,NDIM
C     Integrale Rho tP1B P1B + dt Mhu tGrad P1B Grad P1B dx (Ui(tn+1)-U*i)
C  = -Integrale dt CoGrPr tP1B Grad (P(tn+1)-P(tn)) dX   =>  Ui(tn+1)-U*i
C     ------------------------------------------------------------------------
C     COPIE ESSENTIELLE POUR GARDER BG AUX BARYCENTRES ET REMONTER
      CALL TRTATD( VXVYVZPR(1,NVITC), VXVYVZPR(1,NVP2), NDIM*NBNOVI )

      N1 = 1
      IF( NORESO .EQ. 1 ) THEN

C        DESCENTE et REMONTEE DU PROFIL DE LA MATRICE L tL COMPLETE
C        DES NDIM COMPOSANTES DE LA VITESSE Ui(tn+1)-Ui*
         DO K = 1, NDIM
            CALL DRCHPR( NBSOM, NCODSV, LP1LIGN,VG,VXVYVZPR(N1,NVITC),3,
     %                   VXVYVZPR(N1,NVP2) )
ccc            print *,'Composante ',K,' de (U(tn+1)-U*)'
ccc            call affvect('Apres DRCHPR: (U(tn+1)-U*)i',   20, VXVYVZPR(N1,NVP2))
ccc            call afl1ve( 'Apres DRCHPR: (U(tn+1)-U*)i',NBSOM, VXVYVZPR(N1,NVP2))
            N1 = N1 + NBNOVI
         ENDDO

      ELSE

C        RESOLUTION PAR GC DES NDIM COMPOSANTES DE LA VITESSE U(tn+1)-U*
C        EN SORTIE DE GCAXBK LA SOLUTION EST DANS V3P2
         DO K = 1, NDIM
C           ATTENTION: LE VECTEUR(1:NBSOM) INITIAL DU GC EST MIS A ZERO DANS VITC
C           CAR Ui(tn+1)-U*i) EST PROCHE DE ZERO
C           MAIS PAS AUX BARYCENTRES des EF, VALEURS REMONTEES PAR GAUSS ENSUITE
            CALL AZEROD( NBSOM, VXVYVZPR(N1,NVP2) )
C
            CALL GCAXBK( NBSOM, NODVFX, LP1LIGN, LP1COLO, VG,
     %                   VXVYVZPR(N1,NVITC), VXVYVZPR(N1,NVP2),
     %                   DAUX1, DAUX2, DAUX3,
     %                   VXVYVZPR(N1,NVP2), KGCVIT(K) )
ccc            print *,'Composante ',K,' de (U(tn+1)-U*)'
ccc            call affvect('Apres Gcaxbk: (U(tn+1)-U*)i',   20, VXVYVZPR(N1,NVP2))
ccc            call afl1ve( 'Apres Gcaxbk: (U(tn+1)-U*)i',NBSOM, VXVYVZPR(N1,NVP2))
            N1 = N1 + NBNOVI
         ENDDO

      ENDIF

C     REMONTEE DES NDIM COMPOSANTES DE LA VITESSE U(tn+1)-U* AUX BARYCENTRES
C     DES EF, DEGRES DE LIBERTE ELIMINES PAR LA METHODE DE GAUSS
C     ----------------------------------------------------------------------
      CALL DLGABF( NDIM,   NBELEM, NBS1EF, MCN(MNELE+WUNDEL),
     %             NOBARY, AGGAUSS, NBSOM, NBNOVI, VXVYVZPR(1,NVP2) )
ccc      call afl1ve( 'Apres DLGABF U(tn+1)-U* V3P2=',
ccc     %              NBNOVI*NDIM,  VXVYVZPR(1,NVP2) )

C     U(tn+1) = U* + (U(tn+1)-U*)  =>  U(tn+1)=V3P1
C     ===========================
      CALL SOM2VED( NTDLVI, VXVYVZPR(1,NVP1), VXVYVZPR(1,NVP2),
     %                      VXVYVZPR(1,NVP1) )
ccc      call affvect( 'U(tn+1) = U* + (U(tn+1)-U*)', 20,VXVYVZPR(1,NVP1) )
ccc      call afl1ve(  'U(tn+1) = U* + (U(tn+1)-U*)',NTDLVI,VXVYVZPR(1,NVP1) )


C     CALCUL de P(tn+1) = P(tn) + (P(tn+1)-P(tn))  =>  P(tn+1)=PRES1
C     ===========================================
      CALL SOM2VED( NBSOM, VXVYVZPR(N1P,NVP0), VXVYVZPR(N1P,NVP1),
     %                     VXVYVZPR(N1P,NVP1) )
ccc      call affvect( 'P(tn+1)=P(tn) +(P(tn+1)-P(tn))', 20,VXVYVZPR(N1P,NVP1) )
ccc      call afl1ve(  'P(tn+1)=P(tn) +(P(tn+1)-P(tn))',
ccc     %               NBSOM, VXVYVZPR(N1P,NVP1) )

      IF ( NBPRFX .GT. 0 ) THEN

C        PRISE EN COMPTE DES DEGRES DE LIBERTE PRESSION FIXES
C        ----------------------------------------------------
         MN = ( MNVAPRFX - 1 ) / MOREE2
         DO I=1, NBPRFX
C           LE NO GLOBAL DU DL FIXE
            NDL = MCN( MNDLPRFX - 1 + I )
C           VALEUR FIXEE DE LA PRESSION
            VXVYVZPR(N0P+NDL,NVP1) = DMCN(MN+I)
         ENDDO

      ELSE

C        CALCUL DE LA MOYENNE VOLUMIQUE DE LA PRESSION
C        POUR LUI IMPOSER UNE MOYENNE VOLUMIQUE NULLE SUR LE MAILLAGE
C        ------------------------------------------------------------
         IF( NUTYEL .EQ. 13 ) THEN
C           TRIANGLE BREZZI-FORTIN
            CALL MOYP12D( NBNOVI, RMCN(MNXYZN+WYZNOE),
     %                    NBELEM, NBS1EF, MCN(MNELE+WUNDEL), MCN(1),
     %                    NBSOM,  VXVYVZPR(N1P,NVP1), VOLUME, INTPRES )
         ELSE
C           TETRAEDRE BREZZI-FORTIN
            CALL MOYP13D( NBNOVI, RMCN(MNXYZN+WYZNOE),
     %                    NBELEM, NBS1EF, MCN(MNELE+WUNDEL), MCN(1),
     %                    NBSOM,  VXVYVZPR(N1P,NVP1), VOLUME, INTPRES )
         ENDIF

C        TRANSLATION POUR IMPOSER UNE MOYENNE VOLUMIQUE NULLE DE LA PRESSION
         INTPRES = INTPRES / VOLUME

C///////////////////////////////////////////////////////////////////////
C$OMP PARALLEL PRIVATE(NNN)
C$OMP DO SCHEDULE( STATIC, NBSOM/NBTHREADS )
         DO NNN = 1, NBSOM
            VXVYVZPR(N0P+NNN,NVP1) = VXVYVZPR(N0P+NNN,NVP1) - INTPRES
         ENDDO
C$OMP END DO
C$OMP END PARALLEL
C///////////////////////////////////////////////////////////////////////

cccC        PAS DE CONDITION AUX LIMITES SUR LA PRESSION
cccC        => ELLE EST DEFINIE A UNE CONSTANTE PRES
cccC        RECHERCHE DE LA PLUS BASSE PRESSION POUR SERVIR DE TRANSLATION
cccC        ELLE EST SOUSTRAITE DES AUTRES PRESSIONS ET DEVIENT ZERO
cccC        --------------------------------------------------------------
ccc         PREMIN = 1D100
ccc         DO N=1,NBSOM
ccc            IF( VXVYVZPR(N0P+N,NVP1) .LT. PREMIN ) THEN
ccc               PREMIN = VXVYVZPR(N0P+N,NVP1)
ccc            ENDIF
ccc         ENDDO
cccC        TRANSLATION DE PREMIN A ZERO
ccc         DO N=1,NBSOM
ccc            VXVYVZPR(N0P+N,NVP1) = VXVYVZPR(N0P+N,NVP1) - PREMIN
ccc         ENDDO
ccc         IF( LANGAG .EQ. 0 ) THEN
ccc            WRITE(IMPRIM,*) 'Au TEMPS',TEMPS,' PRESSION MIN',PREMIN,
