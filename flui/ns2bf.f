C        Integrale (-Rho) * tP1 ( Div VITXYZ - Integrale Div VITXYZ / VOLUME )
         CALL BGDIVBF2( NBSOM,  MCN(MNXYZN+WYZNOE),
     %                  NBS1EF, NBELEM, MCN(MNELE+WUNDEL),
     %                  S,    NBNOVI, VXVYVZPR(1,NVP1), VOLUME, INTDIVV,
     %                  VXVYVZPR(N1P,NVP2) )
      ELSE
C        TETRAEDRE BREZZI-FORTIN
C        CALCUL DE Integrale Div VITXY dX et Integrale dX sur le MAILLAGE
         CALL MOYDIVBF3D( NBSOM, RMCN(MNXYZN+WYZNOE),
     %                    NBELEM, MCN(MNELE+WUNDEL), NBNOVI,
