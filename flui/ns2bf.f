      CALL BGVITBF( tn, tn1, Rho, CoGrPr, NDIM, NBSOM, NBNOVI,
     %              NDDLNO, RMCN(MNXYZN+WYZNOE),
     %              NUTYEL, NBS1EF, NBELEM, MCN(MNELE+WUNDEL), NTDLHB,
     %              NBVVEF, NBSFEF, NBLAEF,
     %              MCN(MNVVEF), MCN(MNSFEF), MCN(MNLAEF),
