C     =======================================================
      ALLOCATE ( NO1EFN(1:NBSOM), STAT=IERNO1EFN )
      IF( IERNO1EFN .NE. 0 ) GOTO 9993
      CALL CO1EFN( NBS1EF, NBELEM, MCN(MNELE+WUNDEL), NBSOM,  NO1EFN )

C     ================================================================
