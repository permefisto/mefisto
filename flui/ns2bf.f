C        EN SORTIE DE GCAXBK LA SOLUTION EST DANS V3P1
         DO K = 1, NDIM
            CALL GCAXBK( NBSOM, NODVFX, LP1LIGN, LP1COLO, VG,
     %                   VXVYVZPR(N1,NVP2), VXVYVZPR(N1,NVP0),
     %                   DAUX1, DAUX2, DAUX3,
     %                   VXVYVZPR(N1,NVP1), KGCUET(K) )

ccc            print *,'Composante ',K,' de U*   ------------------'
ccc            call affvect( 'Apres Gcaxbk: Vitesse U*i',  20, VXVYVZPR(N1,NVP1) )
ccc            call afl1ve(  'Apres Gcaxbk: Vitesse U*i',NBSOM,VXVYVZPR(N1,NVP1) )
            N1 = N1 + NBNOVI
         ENDDO

      ENDIF

