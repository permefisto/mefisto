      IF( NORESO .EQ. 1 ) THEN

C        FACTORISATION CHOLESKY SUR LA MATRICE PROFIL PG et VG
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10001)
         ELSE
            WRITE(IMPRIM,20001)
         ENDIF

      ELSE IF( NORESO .EQ. 2 ) THEN

C        GRADIENT CONJUGUE SIMPLE SUR LA MATRICE MORSE PG et VG
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10002)
         ELSE
            WRITE(IMPRIM,20002)
         ENDIF

      ELSE

C        ERREUR SUR NORESO
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10004) NORESO, NIVOGC
         ELSE
            WRITE(IMPRIM,20004) NORESO, NIVOGC
         ENDIF
         IERR = 1
         GOTO 9999

      ENDIF

10001 FORMAT('CONSTRUCTION de la MATRICE PROFIL [PG] de PRESSION'/
     %'RESOLUTION par FACTORISATION COMPLETE L tL de CHOLESKY')
