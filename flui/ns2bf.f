      CALL AGVITBF( DELTAT, Rho, Mhu, MCN(MNXYZN+WYZNOE),
     %              NUTYEL, NBS1EF, NBELEM, MCN(MNELE+WUNDEL), NOBARY,
     %              NORESO, NBSOM,  NBCVPG, LP1LIGN, LP1COLO,
     %              VG,     AGGAUSS )
ccc      call affvect( 'VG avant CL', 20, VG )
ccc      call afl1ve(  'VG avant CL', NBCVPG, VG )
      call affvect('AGVITBF: MATRICE VITESSE AG GAUSS', 20, AGGAUSS )
      call afl1ve('AGVITBF: MATRICE VITESSE AG GAUSS',MOAGGAUSS,AGGAUSS)

