         ALLOCATE ( DAUX1(1:NBSOM), STAT=IERDAUX1 )
         IF( IERDAUX1 .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ALLOCATION ERROR   of',NBSOM,
     %                  ' DOUBLE PRECISION REALS of DAUX1'
