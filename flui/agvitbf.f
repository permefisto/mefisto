      SUBROUTINE AGVITBF( DELTAT, Rho, Mhu, XYZNOE,
     %                    NUTYEL, NBNOEF, NBELEM, NUNOEF, NOBARY,
     %                    NORESO, NBSOM,  NBCVG,  LPLIGN, LPCOLO,
     %                    VG, AGGAUSS )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:     CONSTRUIRE ET ASSEMBLER LA MATRICE GLOBALE DU PROBLEME
C ----     VG=( Rho - dt Mhu Laplacien ) = Integrale Rho P1B P1B
C                                        + deltat Mhu grad P1B grad P1B dX
C          avec INTEGRATION EXACTE SUR L'ELEMENT FINI

C ENTREES:
C --------
C DELTAT : PAS DE TEMPS DU SCHEMA
C Rho    : COEFFICIENT DE LA MATRICE DE MASSE      P1B  P1B
C Mhu    : COEFFICIENT DE LA MATRICE DE VISCOSITE DP1B DP1B
C XYZNOE : XYZNOE(3,NBSOM)  3 COORDONNEES DES SOMMETS DU MAILLAGE
C NUTYEL : NUMERO DU TYPE D'EF ( 13 TRIANGLE, 19 TETRAEDRE BREZZI-FORTIN )
C NBNOEF : NOMBRE DE SOMMETS D'UN EF DE CE TYPE
C          ( 3 POUR LE TRIANGLE, 4 POUR LE TETRAEDRE)
C NBELEM : NOMBRE D'EF DU MAILLAGE
C NUNOEF : NUNOEF(NBELEM,NBNOEF) NO DES SOMMETS DES NBELEM EF
C NOBARY : NUMERO DU DU DL BARYCENTRE = NBNOEF + 1 = NBSOMMET 1 EF + 1
C
C NORESO : CODE RESOLUTION DES SYSTEMES LINEAIRES AVEC LA MATRICE VG
C          1 FACTORISATION COMPLETE DE CHOLESKY ET MATRICE PROFIL VG
C          2 GRADIENT CONJUGUE AVEC UN STOCKAGE MORSE DE VG
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBCVG  : NOMBRE DE COEFFICIENTS DE VG
C          (SI NORESO=1   NBCVG=0
C          (SI NORESO=2   NBCVG=NOMBRE DE COLONNES STOCKEES)
C LPLIGN : POINTEURS SUR LES COEFFICIENTS DIAGONAUX DE VG
C LPCOLO : NUMERO DE COLONNE DES COEFFICIENTS DE LA MATRICE MORSE VG

C SORTIE :
C --------
C VG     : MATRICE GLOBALE ASSEMBLEE
C AGGAUSS: COEFFICIENTS DE LA MATRICE ELEMENTAIRE ELIMINES PAR GAUSS
C          POUR LES NBELEM ELEMENTS FINIS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2012
C23456---------------------------------------------------------------012
      IMPLICIT NONE
      INTEGER           NORESO, NBSOM, NBCVG, LPLIGN(0:NBSOM),
     %                  LPCOLO(NBCVG), NUTYEL, NBNOEF, NBELEM,
     %                  NUNOEF(NBELEM,NBNOEF)
      DOUBLE PRECISION  VG(NBCVG), AGGAUSS(NOBARY,NBELEM),
     %                  DELTAT, Rho, Mhu
      REAL              XYZNOE(3,NBSOM)

      INTEGER           M, NUELEM, NONOEF(5), NOBARY
      DOUBLE PRECISION  AE(15)

C     MISE A ZERO DE LA MATRICE VG
      CALL AZEROD( NBCVG, VG )

C     LE STOCKAGE DES LIGNES ELIMINEES PAR GAUSS AUX BARYCENTRES
      DO NUELEM = 1, NBELEM

C        NO DES NOEUDS DE L'ELEMENT FINI NUELEM
         DO M = 1, NBNOEF
            NONOEF(M) = NUNOEF(NUELEM,M)
         ENDDO

C        CALCUL DE LA MATRICE avec INTEGRATION EXACTE SUR L'EF de
C        Integrale ( Rho P1B P1B + dt Mhu Grad P1B Grad P1B ) dx
C        ELIMINATION DE GAUSS DU DL VITESSE DU BARYCENTRE (1 COMPOSANTE)
C        SUR LA MATRICE ELEMENTAIRE
         IF( NUTYEL .EQ. 13 ) THEN
C           TRIANGLE BF 2D
            CALL AEBFV2( Rho, DELTAT*Mhu, NONOEF, NBSOM, XYZNOE,
     %                   AE, AGGAUSS(1,NUELEM) )
         ELSE
C           TETRAEDRE BF 3D
            CALL AEBFV3( Rho, DELTAT*Mhu, NONOEF, NBSOM, XYZNOE,
     %                   AE, AGGAUSS(1,NUELEM) )
         ENDIF

C        ASSEMBLAGE DE LA MATRICE ELEMENTAIRE DANS LA MATRICE GLOBALE
         IF( NORESO .EQ. 1 ) THEN
C           MATRICE SYMETRIQUE PROFIL
            CALL ASMEPC( NBNOEF, NONOEF, 1, AE, AE, 1,
     %                   LPLIGN, VG )
         ELSE
C           MATRICE SYMETRIQUE MORSE
            CALL ASMEGC( NBNOEF, NONOEF, 1, AE, AE, 1,
     %                   LPLIGN, LPCOLO, VG )
         ENDIF

      ENDDO
C     FIN DE LA BOUCLE SUR LES EF DE LA CONSTRUCTION DE VG

ccc      call affvect( 'AGVITBF: MATRICE VITESSE VG COMPOSANTE', 20, VG )
ccc      call affvect( 'AGVITBF: MATRICE VITESSE AG GAUSS', 20, AGGAUSS )

      RETURN
      END
