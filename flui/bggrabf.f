      SUBROUTINE BGGRABF( DELTAT, CoGrPr, NDIM,   NBSOM,  NBNOVI,
     %                    XYZSOM, NUTYEL, NBNOEF, NBELEM, NUNOEF,
     %                    PRESP1, NOBARY, AGGAUSS,   BG )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT: CONSTRUIRE ET ASSEMBLER LE SECOND MEMBRE GLOBAL BG DU PROBLEME
C ---- (Rho - dt Mhu Laplacien){u(tn+1)-u*} = -dt CoGrPr GRAD(p(tn+1)-p(tn))
C      BG=Integrale sur e  - dt CoGrPr tV(x) Grad PRESP1(x) dx

C ENTREES:
C --------
C DELTAT : PAS CONSTANT DU TEMPS DOUBLE PRECISION
C CoGrPr : COEFFICIENT DU GRADIENT DE PRESSION DANS LES EDP
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 2 ou 3
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBNOVI : NOMBRE DE NOEUDS VITESSE SOMMETS et MILIEUX DES ARETES DES EF
C NTDLVI : NOMBRE TOTAL DE DL VITESSE (NDIM*NBNOVI)
C XYZSOM : XYZSOM(3,NBNOVI)  3 COORDONNEES DES NOEUDS DU MAILLAGE
C NUTYEL : NUMERO DU TYPE D'EF ( 13 TRIANGLE, 19 TETRAEDRE BREZZI-FORTIN )
C NBNOEF : NOMBRE DE SOMMETS D'UN EF DE CE TYPE
C          ( 3 POUR LE TRIANGLE, 4 POUR LE TETRAEDRE)
C NBELEM : NOMBRE D'EF DU MAILLAGE
C NUNOEF : NUNOEF(NBELEM,NBNOEF) NO DES NOEUDS DES NBELEM EF
C NOBARY : NUMERO DU DL BARYCENTRE DANS UN EF
C PRESP1 : DL DE LA PRESSION P1 AUX SOMMETS DU MAILLAGE

C SORTIE :
C --------
C BG     : VECTEUR GLOBAL SECOND MEMBRE ASSEMBLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2012
C23456---------------------------------------------------------------012
      IMPLICIT NONE
      DOUBLE PRECISION  PIVOT, DELTAT, CoGrPr,
     %                  BG(NBNOVI,NDIM), AGGAUSS(NOBARY,NBELEM)
      INTEGER           NUNOEF(NBELEM,NBNOEF)
      REAL              XYZSOM(3,NBSOM)
      INTEGER           NUTYEL, NDIM, NBNOEF, NBELEM, NOBARY,
     %                  NBSOM, NBNOVI
      INTEGER           M, NUELEM, NONOEF(5), K, N
      DOUBLE PRECISION  BE(19)
      DOUBLE PRECISION  PRESP1(NBSOM)

C     MISE A ZERO DE BG
      CALL AZEROD( NBNOVI*NDIM, BG )

C     BOUCLE SUR LES EF de BREZZI-FORTIN
      DO NUELEM = 1, NBELEM

C        NO DES NOEUDS DE L'ELEMENT FINI NUELEM
         DO M = 1, NBNOEF
            NONOEF(M) = NUNOEF(NUELEM,M)
         ENDDO
C        NO DU BARYCENTRE
         NONOEF(NOBARY) = NBSOM + NUELEM

C        Integrale sur ef  - dt CoGrPr tV(x) Grad PRESP1(x) dx
         IF( NUTYEL .EQ. 13 ) THEN

C           TRIANGLE BREZZI_FORTIN
            CALL F2EX4P1BP1GRAD( XYZSOM, NONOEF, DELTAT, CoGrPr,
     %                           NBSOM,  PRESP1, BE )

C           ASSEMBLAGE DES DEGRES DE LIBERTE 4 ET 8 DE BE DANS BG
C           COMPRESSION DE 8 A 6 DEGRES DE LIBERTE DE BE
            CALL GAS2P1BP1( NBSOM, NUELEM, BE, NBNOVI, BG )

         ELSE
C
C           TETRAEDRE BREZZI_FORTIN
            CALL F3EX4P1BP1GRAD( XYZSOM, NONOEF, DELTAT, CoGrPr,
     %                           NBSOM,  PRESP1, BE )

C           ASSEMBLAGE  DES DEGRES DE LIBERTE 5 10 15 DE BE DANS BG
C           COMPRESSION DES DEGRES DE LIBERTE 5 10 15 DE BE
            CALL GAS3P1BP1( NBSOM, NUELEM, BE, NBNOVI, BG )

         ENDIF

CCC         IF( NUELEM .EQ. 5 ) THEN
CCC            call affvect(
CCC     %      'BGGRABF: BE=-dt CoGrPr tV(x) Grad (p(tn+1,x)- p(tn,x))',
CCC     %       NDIM*NBNOEF, BE )
CCC         ENDIF

C        ASSEMBLAGE de BE dans BG
         CALL ASMEEX1( NDIM, NBNOEF, NONOEF, BE, NBNOVI, BG )

C     FIN DE LA BOUCLE SUR LES EF DE LA CONSTRUCTION DE BG
      ENDDO


C     LA BOUCLE SUR LES ELEMENTS FINIS BREZZI-FORTIN POUR FAIRE GAUSS
C     SUR LE SECOND MEMBRE DU SYSTEME LINEAIRE A RESOUDRE
C     ---------------------------------------------------------------
      DO NUELEM = 1, NBELEM

C        TRIANGLE  BREZZI-FORTIN P1BULLEP3 EN VITESSE et P1 EN PRESSION
C        TETRAEDRE BREZZI-FORTIN P1BULLEP4 EN VITESSE et P1 EN PRESSION
C        --------------------------------------------------------------
C        METHODE DE GAUSS SUR LES NDIM DL DU BARYCENTRE SUR LE SECOND
C        MEMBRE DU SYSTEME A RESOUDRE
C        CALL GAR3P1BP1( NBSOM, NUELEM, NONOEF, AGGAUSS(1,NUELEM), NBNOVI, BG )

C        TRIANGULATION DE GAUSS SUR LA LIGNE NOBARY DU VECTEUR
         DO K = 1, NDIM
            PIVOT = BG( NBSOM+NUELEM, K ) / AGGAUSS(NOBARY,NUELEM)
            DO M = 1, NBNOEF
               N = NUNOEF(NUELEM,M)
               BG(N,K) = BG(N,K) - AGGAUSS(M,NUELEM) * PIVOT
            ENDDO
         ENDDO

C     FIN DE LA BOUCLE DES EF POUR ASSEMBLER BG
      ENDDO

CCC      call affvect( 'BGGRABF BG=', 20,          BG )
CCC      call afl1ve(  'BGGRABF BG=', NBNOVI*NDIM, BG )

      RETURN
      END
