      SUBROUTINE PTDLFL( MNELE, NDIM,   NBELEM, NBNOHB, NUTYEL,
     %                   NBSOM, NBNOVI, NDDLNO, IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LE POINTEUR SUR LE DERNIER DEGRE DE LIBERTE
C -----    DE CHAQUE NOEUD DU MAILLAGE D'UN FLUIDE
C          LES NDIM COMPOSANTES DE LA VITESSE EN TOUT NOEUD
C          ( SOMMETS + BARYCENTRE pour EF BREZZI-FORTIN
C            SOMMETS + MILIEUX DES ARETES pour EF TAYLOR_HOOD)
C          + LA PRESSION EN CHAQUE SOMMET
C ENTREES :
C ---------
C MNELE   : ADRESSE DU TABLEAU DES TYPES D'ELEMENTS FINIS
C NDIM    : DIMENSION DE L'ESPACE DU FLUIDE
C NBELEM  : NOMBRE TOTAL D'ELEMENTS FINIS
C NBNOHB  : NOMBRE TOTAL DE NOEUDS DU MAILLAGE HORS BARYCENTRES
C NUTYEL  : NUMERO DU TYPE D'EF UTILISE
C           13 ou 19 : BREZZI FORTIN <=> TRIA 2P1D ou TETR 3P1D
C           15 ou 20 : TAYLOR HOOD   <=> TRIA 2P2D ou TRIA 2P2C
C NBSOM   : NOMBRE INITIAL DE SOMMETS DU MAILLAGE
C NBNOVI  : NOMBRE INITIAL DE NOEUDS SUPPORT DE LA VITESSE

C SORTIES :
C ---------
C NBSOM   : NOMBRE FINAL DE SOMMETS DU MAILLAGE
C NBNOVI  : NOMBRE FINAL DE NOEUDS SUPPORT DE LA VITESSE
C NDDLNO  : TABLEAU DES POINTEURS SUR LE DERNIER DL DE CHAQUE NOEUD
C           NDDLNO(I)= NO DERNIER DL DU NOEUD
C                      (SOMMET ou MILIEU ou BARYCENTRE)
C           CE TABLEAU EST DIMENSIONNE A 1+NBNOVI  cf plus bas
C IERR    : 0 SI PAS D'ERREUR
C           1 SI UN TABLEAU 'NPEF"TYPE_EF' EST INCONNU
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR:SOFIANE BENHAMADOUCHE ANALYSE NUMERIQUE UPMC PARIS Janvier 2000
C MODIFS: ALAIN PERRONNET Laboratoire J-L.LIONS UPMC Paris     Juin 2007
C MODIFS: ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Novembre 2008
C23456---------------------------------------------------------------012
      COMMON /UNITES/ LECTEU,IMPRIM,NUNITE(30)
      INTEGER   NDDLNO(0:NBNOVI)
      INTEGER   NONOEF(27)

      IERR   = 0
      NBNOVI = 0

      IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C        EF BREZZI-FORTIN
         IF( NBNOHB .LT. NBELEM ) THEN
C           LE NOMBRE DE NOEUDS SUPPORT DE LA VITESSE
C           LES BARYCENTRES N'ONT PAS ETE PRIS EN COMPTE
            NBNOVI = NBNOHB + NBELEM
C           NOMBRE DE SOMMETS DU MAILLAGE
            NBSOM = NBNOHB
         ELSE
C           LE NOMBRE DE NOEUDS SUPPORT DE LA VITESSE
C           LES BARYCENTRES ONT ETE PRIS EN COMPTE
            NBNOVI = NBNOHB
C           NOMBRE DE SOMMETS DU MAILLAGE
            NBSOM = NBNOHB - NBELEM
         ENDIF
      ELSE IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN
C        EF TAYLOR-HOOD
C        NOMBRE DE NOEUDS SUPPORT DE LA VITESSE
         NBNOVI = NBNOHB
      ENDIF

C     DETERMINATION DE LA NATURE DES NOEUDS
C     1 : SI VITESSE + PRESSION
C     2 : SI VITESSE SEULE

      DO NUELEM = 1, NBELEM

C        LES NOEUDS DE L'ELEMENT FINI NUELEM
         CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOEF )

C        LES SOMMETS SONT MARQUES PAR 1, LES AUTRES NOEUDS PAR 2
         DO I=1,NDIM+1
C           SOMMET
            NDDLNO( NONOEF(I) ) = 1
         ENDDO

         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN

C           BARYCENTRE DE L'EF NUELEM BREZZI-FORTIN
            NDDLNO( NBSOM + NUELEM ) = 2

         ELSE IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN

C           MILIEU D'ARETE DU TRIANGLE ou DU TETRAEDRE TAYLOR HOOD
            DO I=NDIM+2,NBNOEF
               NDDLNO( NONOEF(I) ) = 2
            ENDDO

         ENDIF

      ENDDO

C     CONSTRUCTION DU TABLEAU NDDLNO(0:NBNOVI)
C     NO DU DERNIER DL DE CHAQUE NOEUD
C     ========================================
C     NOMBRE DE SOMMETS DU MAILLAGE
      NBSOM = 0
C     SOMMATION POUR POINTER SUR LE DERNIER DL DE CHAQUE NOEUD
      NDDLNO(0) = 0
      DO I = 1, NBNOVI
         IF( NDDLNO(I) .EQ. 1 ) THEN
C           SOMMET => NDIM + 1  DL: V1 V2 ... Vndim Pression
            NDDLNO(I) = NDDLNO(I-1) + NDIM + 1
C           NOMBRE DE SOMMETS DU MAILLAGE
            NBSOM = NBSOM + 1
         ELSE IF( NDDLNO(I) .EQ. 2 ) THEN
C           MILIEU D'UN COTE ou BARYCENTRE => NDIM DL: V1 V2 ... Vndim
            NDDLNO(I) = NDDLNO(I-1) + NDIM
         ELSE
            PRINT*,'ptdlfl: NDDLNO(',I,')=',NDDLNO(I),' => IERR=1'
            IERR = 1
         ENDIF
      ENDDO

      PRINT*,'ptdlfl: NOMBRE de SOMMETS NBSOM=',NBSOM,
     %              ' NOMBRE de NOEUDS VITESSE NBNOVI=',NBNOVI

      RETURN
      END
