         ENDIF
19004    FORMAT(/' MATRICES MORSE GC ',         T30,G25.17,' MOTS'/
     %           ' MATRICE PRECONDITIONNEMENT', T30,G25.17,' MOTS'/
     %           ' TABLEAUX SUPPLEMENTAIRES GC',T30,G25.17,' MOTS'/
     %           ' AU TOTAL LE GC DEMANDE',     T30,G25.17,' MOTS'/)
C
29004    FORMAT(/' CG CONDENSED MATRICES',      T30,G25.17,' WORDS'/
     %           ' PRECONDITIONED MATRIX',      T30,G25.17,' WORDS'/
     %           ' AUXILIARY ARRAYS',           T30,G25.17,' WORDS'/
     %           ' TOTAL for the CG',           T30,G25.17,' WORDS'/)
      ENDIF
C
C     TOTAL DES MOTS NECESSAIRES A LA RESOLUTION
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,19005) DMOTSTO
      ELSE
         WRITE(IMPRIM,29005) DMOTSTO
      ENDIF
19005 FORMAT(/'STOCKAGE TOTAL des MATRICES et VECTEURS=',G25.17,
     %' MOTS MEMOIRE'/)
29005 FORMAT(/'TOTAL STORAGE of MATRICES and VECTORS=', G25.17,
     %' MEMORY WORDS'/)
C
C     CHAINAGE DES FACES FRONTALIERES C-A-D APPARTENANT A UN SEUL EF
C     A PARTIR DU ZERO EN POSITION 7 DE LFACES
      IF( MNLFAC .GT. 0 ) THEN
         CALL MACFAF( MOFACE, MXFACE, MCN(MNLFAC),  L1FAFR, NBFAFR )
C        MISE A JOUR DU NUMERO DE LA PREMIERE FACE FRONTALIERE (DANS UN SEUL EF)
         MCN( MNFAOB + W1FAFR ) = L1FAFR
      ENDIF
