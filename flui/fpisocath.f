      DMOTSTO = 901.D0+NBNOVI*2+NBPRFX+NBVIFX+NBDLFX+NBSOM*3
     %        + MOREE2*( NBNOVI+NTDLVI+NTDLVP*3
     %                  +NBPRFX+NBVIFX+NBDLFX+ MOSFOB+NBCMVG+NBCMPG )
     %        + NBFLUI + MOREE2 *NTDLVP*NBFLUI
C
      IF( NORESO .GE. 2 ) THEN
C        GRADIENT CONJUGUE STOCKAGE MORSE
C        DESTRUCTION DES TABLEAUX AUXILIAIRES
         IF( MNAUX1 .GT. 0 ) CALL TNMCDS( 'REEL2',  LOAUX,   MNAUX1 )
         IF( MNBDIR .GT. 0 ) CALL TNMCDS( 'REEL2',  LODIR,   MNBDIR )
         IF( MNLPCV .GT. 0 ) CALL TNMCDS( 'ENTIER', NBCMVG,  MNLPCV )
         IF( MNLPLC .GT. 0 ) CALL TNMCDS( 'ENTIER', NBNOVI+1,MNLPLC )
         IF( MNLPCC .GT. 0 ) CALL TNMCDS( 'ENTIER', LOLPCC,  MNLPCC )
         IF( MNPGC  .GT. 0 ) CALL TNMCDS( 'REEL2',  LOLPCC,  MNPGC  )
         IF( MNLPCP .GT. 0 ) CALL TNMCDS( 'ENTIER', NBCMPG,  MNLPCP )
         DMOTSTO = DMOTSTO + NBNOVI+1+LOLPCC+NBCMPG+NBCMVG
     %           + MOREE2*(LOAUX+LODIR+LOLPCC)
      ENDIF
