      SUBROUTINE BeT3P1BP1( NEF1,    NBEF,    NBNOEF, NUSOTE,
     %                      MOFACE,  MXFACE,  LFACES,
     %                      tn, tn1, Coeftr,  XYZPOI, NDDLNO,
     %                      NTDLHB,  VXYZPN0, VITMXtn,
     %                      Be,      IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : CALCUL DU VECTEUR TRANSPORT NAVIER STOKES DU TETRAEDRE BREZZI-FORTIN
C ----- POLYNOME LAGRANGE DE DEGRE 1 + BULLE P4 POUR LA VITESSE
C       POLYNOME LAGRANGE DE DEGRE 1 POUR LA PRESSION
C       TETRAEDRE ELEMENT FINI: e ref -> e EST P1 POUR CHAQUE COORDONNEE

C       INTEGRATION EN 15 POINTS DU TETRAEDRE P5-EXACTE
C       Coeftr Integrale tP1B ui(tn,X(tn;tn+1,x)) dx
C       avec INTEGRATION RETROGRADE DES CARACTERISTIQUES cf O. PIRONNEAU

C ENTREES:
C --------
C NEF1   : NUMERO DE L'EF DU TERME DE TRANSPORT A TRAITER POUR CALCULER Be
C NBEF   : NOMBRE DE TETRAEDRES DU MAILLAGE
C NBNOEF : =4 NOMBRE DE NOEUDS D'UN TETRAEDRE BF = 4 SOMMETS
C NUSOTE : NUMERO DES 4 SOMMETS DES TETRAEDRES
C MOFACE : NOMBRE DE MOTS DE CHAQUE FACE DU TABLEAU LFACES
C MXFACE : NOMBRE MAXIMAL de FACES DU TABLEAU LFACES
C LFACES : LFACES(1,I)= NO DU 1-ER  SOMMET DE LA FACE
C          LFACES(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LFACES(3,I)= NO DU 3-EME SOMMET DE LA FACE
C          LFACES(4,I)= NO DU 4-EME SOMMET DE LA FACE
C                       0 SI TRIANGLE
C          LFACES(5,I)= CHAINAGE HACHAGE SUR FACE SUIVANTE
C          SI SOMMET 2 < DERNIER SOMMET  => FACE   DIRECTE DANS L'ELEMENT FINI
C                      >                 => FACE INDIRECTE
C          UNE FACE DIRECTE EST VUE DE L EXTERIEUR DE L'ELEMENT FINI
C          SOUS LA FORME DIRECTE
C          LFACES(6,I)= NUMERO DU 1-ER ELEMENT FINI CONTENANT CETTE FACE
C                       >0 SI FACE   DIRECTE DANS CET ELEMENT FINI
C                       <0 SI FACE INDIRECTE DANS CET ELEMENT FINI

C          SI LA FACE APPARTIENT A 2 ELEMENTS FINIS ALORS
C          LFACES(7,I)= NUMERO DU 2-EME ELEMENT FINI CONTENANT CETTE FACE
C                       >0 SI FACE   DIRECTE DANS CET ELEMENT FINI
C                       <0 SI FACE INDIRECTE DANS CET ELEMENT FINI
C          SINON
C          LFACES(7,I)= NUMERO DE LA FACE FRONTALIERE SUIVANTE
C                       0 SI C'EST LA DERNIERE
C          L1FAFR = NUMERO DE LA PREMIERE FACE FRONTALIERE DANS LFACES
C                   CHAINAGE SUIVANT DANS LFACES(7,I)
C          L1FA2M = NUMERO DE LA PREMIERE FACE INTERFACE 2 MATERIAUX DANS LFACES
C                   CHAINAGE SUIVANT DANS LFACES(8,I)
C          SI VOLUME MULTI-MATERIAUX
C          LFACES(8,I)= NO DE LA FACE INTERFACE ENTRE 2 MATERIAUX SUIVANTE
C                       0 SI FACE SANS INFORMATION SUPPLEMENTAIRE
C          SINON VOLUME MONO-MATERIAU
C          LFACES(8,I)= NUMERO DE LA FACE A TANGENTE
C                       0 SI FACE SANS TANGENTE

C tn, tn1: INTERVALLE DE TEMPS de l'INTEGRATION
C DT     : LE PAS CONSTANT DE TEMPS ENTRE tn ET tn+1
C CoefTr : COEFFICIENT DE LA VITESSE TRANSPORTEE
C XYZPOI : 3 COORDONNEES DES SOMMETS=POINTS DES TETRAEDRES
C NDDLNO : TABLEAU DES POINTEURS SUR LE DERNIER DL DE CHAQUE NOEUD FLUIDE
C          CE TABLEAU EST DIMENSIONNE 0:NBNOEU
C NTDLHB : NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET HORS BARYCENTRES
C          (NDIM+1) NBNOHB (SANS BARYCENTRES)
C VXYZPN0: VECTEUR GLOBAL des DL VITESSES-PRESSIONS (1:NTDLHB) au TEMPS tn
C          SUIVI DES 3 VITESSES AU BARYCENTRE DES TETRAEDRES
C VITMXtn: NORME DE LA VITESSE MAXIMALE AU TEMPS tn

C SORTIES:
C --------
C Be     : VECTEUR ELEMENTAIRE DE 3x5 COEFFICIENTS (DEBORDE A 19)
C IERR   : CODE D'ERREUR 0 PAS D'ERREUR, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray     Mars 2010
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray      Mai 2010
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2010
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray Decembre 2012
C MODIFS : ALAIN PERRONNET Saint Pierre du Perray         Septembre 2020
C23456---------------------------------------------------------------012
      IMPLICIT  NONE
      include"./incl/langue.inc"
      REAL               tn1, tn, XYZPOI(3,*)
      INTEGER            NEF1, NBEF, NBNOEF, NUSOTE(NBEF,NBNOEF),
     %                   MOFACE, MXFACE, LFACES(MOFACE,MXFACE),
     %                   NTDLHB, NDDLNO(0:*),
     %                   IERR
      DOUBLE PRECISION   Coeftr, VXYZPN0(1:*), VITMXtn, Be(19)

      INTEGER            I, K, L, IK, NEF0
      DOUBLE PRECISION   RDPOIDS, D, PRCBL1(5), Xe(3,4), DELTAe,
     %                   XYZ0(3), XYZ1(3), V0(3), V1(3),
     %                   BULLE, CB1BL, CB2BL, CB3BL, CB4BL

C     POIDS ET POINTS D'INTEGRATION SUR LE TETRAEDRE UNITE P5 EXACTE
C     DOUBLE PRECISION   POTEP5( NBPTI=15 ), COTEP5( 3, NBPTI ) dans
      include"./incl/tep5poco.inc"

C     INITIALISATION DU VECTEUR ELEMENTAIRE Be pour l'EF NEF1
      IERR = 0
      DO I=1,19
         Be(I) = 0D0
      ENDDO

C     LES 3 COORDONNEES DES 4 SOMMETS DU TETRAEDRE NEF1
      DO I = 1, 4
         DO K = 1, 3
            Xe( K, I ) = DBLE( XYZPOI( K, NUSOTE(NEF1,I) ) )
         ENDDO
      ENDDO

C     CALCUL DU VECTEUR tPchapeau(bl) ( Pchapeau(X(tn;tn+1,Fe(bl)) Vitesse(NEF0) )
C     ============================================================================
C     Boucle sur les points d'integration L dans le tetraedre NEF1
      DO 100 L=1,NBPTI

C        COORDONNEES DU POINT brl D'INTEGRATION DANS EF REFERENCE I.E.
C        COORDONNEES BARYCENTRIQUES de brl
         CB2BL = COTEP5( 1, L )
         CB3BL = COTEP5( 2, L )
         CB4BL = COTEP5( 3, L )
         CB1BL = 1.D0 - CB2BL - CB3BL - CB4BL

C        COORDONNEES de XYZ1=Fe(brl) SUR LE TETRAEDRE NEF1
         XYZ1(1)=CB1BL*Xe(1,1)+CB2BL*Xe(1,2)+CB3BL*Xe(1,3)+CB4BL*Xe(1,4)
         XYZ1(2)=CB1BL*Xe(2,1)+CB2BL*Xe(2,2)+CB3BL*Xe(2,3)+CB4BL*Xe(2,4)
         XYZ1(3)=CB1BL*Xe(3,1)+CB2BL*Xe(3,2)+CB3BL*Xe(3,3)+CB4BL*Xe(3,4)

C        INTERPOLATION de la VITESSE V1(tn,XYZ1) dans le TETRAEDRE NEF1
C        LES POLYNOMES LAGRANGE DE DEGRE 1 + BULLE P4 sur TETRAEDRE UNITE
         BULLE = 64D0 * CB1BL * CB2BL * CB3BL * CB4BL
         PRCBL1(1) = CB1BL - BULLE
         PRCBL1(2) = CB2BL - BULLE
         PRCBL1(3) = CB3BL - BULLE
         PRCBL1(4) = CB4BL - BULLE
         PRCBL1(5) = 4D0 * BULLE

C        CALCUL DU POINT XYZ0=X(tn;tn+1,XYZ1) PAR INTEGRATION RETROGRADE
C        LE LONG DE LA CARACTERISTIQUE DANS DES TETRAEDRES BREZZI-FORTIN
         CALL XTN1TNBF( XYZPOI, NBEF,   NBNOEF, NUSOTE,
     %                  MOFACE, MXFACE, LFACES,
     %                  NDDLNO, NTDLHB, VXYZPN0, VITMXtn,
     %                  tn, tn1, NEF1, XYZ1, V1,
     %                  DELTAe,  NEF0, XYZ0, V0, IERR )
C        IERR:=0 V1 XYZ0 dans NEF0 et V0 CALCULES CORRECTEMENT
C             =1 VITESSE SUR NEF1 TROP GRANDE > VITMXtn  XYZ0=XYZ1 V0=V1
C             =2 V1 VITESSE TRES FAIBLE  XYZ0=XYZ1 et V0=(0,0,0)
C             =3 DELTAe<=0 ou XYZ1 NON INTERNE A NEF1 XYZ0=XYZ1 V0=V1=0

         IF( IERR .GT. 0 ) THEN
C           NON PRISE EN COMPTE DE CE POINT bl DANS LE CALCUL de be
            GOTO 100
         ENDIF

C        POIDS * JACOBIEN AU POINT L D'INTEGRATION * Coeftr
         RDPOIDS = POTEP5(L) * DELTAe * Coeftr
         IK = 0
         DO K=1,3
C           V0(k) COMPOSANTE k de la VITESSE
            D = RDPOIDS * V0(K)
            DO I=1,5
C              COMPOSANTE IK du VECTEUR
C              Coeftr Poids Jacobien tP(bl) V0k( X(tn;tn+1,Fe(bl)) )
               Be(IK+I) = Be(IK+I) + PRCBL1(I) * D
            ENDDO
            IK = IK + 5
         ENDDO

C        FIN DE L'INTEGRATION AU POINT Bl
 100  ENDDO

      RETURN
      END
