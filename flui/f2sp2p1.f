      SUBROUTINE F2SP2P1( X,
     %                    NOOBLA, NUMILI, NUMALI, LTDELI,
     %                    NOOBSF, NUMISU, NUMASU, LTDESU,
     %                    BE  )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DU SECOND MEMBRE DU TRIANGLE TAYLOR HOOD
C -----    P2 CONTINU POUR LES 2 COMPOSANTES DE LA VITESSE
C          P1 CONTINU POUR LA PRESSION (SANS INTERVENTION ICI)
C          INTEGRATION EN 7 POINTS DES MEDIANES DU TRIANGLE P5-EXACTE
C          INTEGRATION EN 3 POINTS DE GAUSS DES ARETES FRONTALIERES
C
C ENTREES:
C --------
C X      : 2 COORDONNEES DES 6 NOEUDS DU TRIANGLE (3 SOMMETS EN PREMIER)
C
C NOOBLA : NUMERO DES OBJETS LIGNES DES ARETES DE L'ELEMENT FINI
C FORCE  : FORCE IMPOSEE SUR UNE LIGNE FORCE(2) REMPLI DANS CE SP
C NUMILI : NUMERO MINIMAL DES OBJETS LIGNES
C NUMALI : NUMERO MAXIMAL DES OBJETS LIGNES
C LTDELI : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DU FLUIDE
C          DES OBJETS LIGNES
C
C NOOBSF : NUMERO DE L'OBJET SURFACE DE CET ELEMENT FINI
C FORCE  : EFFORTS DE SURFACE (2)  REMPLI DANS CE SOUS PROGRAMME
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DU FLUIDE
C          DES OBJETS SURFACES
C
C SORTIES:
C --------
C BE     : BE(15) LE SECOND MEMBRE ELEMENTAIRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : VINCENT BOYER -  GUY-FRANCOIS ZACHARIE           JANVIER 2000
C MODIFS : ALAIN PERRONNET  ANALYSE NUMERIQUE UPMC PARIS   DECEMBRE 2000
C MODIFS : ALAIN PERRONNET LJLL UPMC & Saint PIERRE du PERRAY  MARS 2010
C-----------------------------------------------------------------------
      include"./incl/donflu.inc"
      INTRINSIC         SQRT
      REAL              X(6,2)
      DOUBLE PRECISION  FORCE(2),
     %                  BE(15)
      INTEGER           NOOBLA(3)
      INTEGER           LTDELI(1:MXDOFL,NUMILI:NUMALI)
      INTEGER           LTDESU(1:MXDOFL,NUMISU:NUMASU)
      INTEGER           I,K,L,NL,KK,M
      INTEGER           NOSOAR(3)
      DOUBLE PRECISION  D, XD, YD, VN(2), XCBL, YCBL
      DOUBLE PRECISION  DELTA, S
C
C     POIDS ET POINTS D'INTEGRATION SUR LE TRIANGLE UNITE P5 EXACTE
C     VALEURS DES POLYNOMES AUX POINTS D'INTEGRATION
      DOUBLE PRECISION   POTRP5( 7 ), COTRP5( 2, 7 ), POTRBL( 6, 7 )
C
C     POIDS ET POINTS D'INTEGRATION SUR LE SEGMENT UNITE P5 EXACTE
C     3 POINTS DE GAUSS
C     VALEURS DES POLYNOMES AUX POINTS D'INTEGRATION
      DOUBLE PRECISION   POSEP5( 3 ), COSEP5( 3 ), POSEBL( 3, 3 )
C
      DATA   POTRP5 /
     % 0.62969590272413584D-01,  0.62969590272413584D-01,
     % 0.62969590272413584D-01,  0.66197076394253082D-01,
     % 0.66197076394253082D-01,  0.66197076394253082D-01,
     % 0.11250000000000000D+00 /
C
      DATA   COTRP5 /
     % 0.10128650732345633D+00,  0.10128650732345633D+00,
     % 0.79742698535308720D+00,  0.10128650732345633D+00,
     % 0.10128650732345633D+00,  0.79742698535308720D+00,
     % 0.47014206410511505D+00,  0.59715871789769809D-01,
     % 0.47014206410511505D+00,  0.47014206410511505D+00,
     % 0.59715871789769809D-01,  0.47014206410511505D+00,
     % 0.33333333333333331D+00,  0.33333333333333331D+00 /
C
      DATA   POTRBL /
     %  0.47435260858553857D+00, -0.80768594191887186D-01,
     % -0.80768594191887186D-01,  0.32307437676754874D+00,
     %  0.41035826263138293D-01,  0.32307437676754874D+00,
     % -0.80768594191887200D-01,  0.47435260858553840D+00,
     % -0.80768594191887186D-01,  0.32307437676754880D+00,
     %  0.32307437676754869D+00,  0.41035826263138349D-01,
     % -0.80768594191887047D-01, -0.80768594191887186D-01,
     %  0.47435260858553840D+00,  0.41035826263138349D-01,
     %  0.32307437676754869D+00,  0.32307437676754891D+00,
     % -0.28074943223078797D-01, -0.28074943223078852D-01,
     % -0.52583901102545356D-01,  0.88413424176407263D+00,
     %  0.11229977289231516D+00,  0.11229977289231519D+00,
     % -0.52583901102545516D-01, -0.28074943223078852D-01,
     % -0.28074943223078852D-01,  0.11229977289231541D+00,
     %  0.88413424176407240D+00,  0.11229977289231537D+00,
     % -0.28074943223078776D-01, -0.52583901102545356D-01,
     % -0.28074943223078852D-01,  0.11229977289231517D+00,
     %  0.11229977289231516D+00,  0.88413424176407263D+00,
     % -0.11111111111111111D+00, -0.11111111111111111D+00,
     % -0.11111111111111111D+00,  0.44444444444444444D+00,
     %  0.44444444444444444D+00,  0.44444444444444444D+00 /
C
      DATA   POSEP5 /
     % 0.27777777777777778D+00,  0.44444444444444444D+00,
     % 0.27777777777777778D+00 /
C
      DATA   COSEP5 /
     % 0.11270166537925830D+00,  0.5D+00, 0.88729833462074170D+00 /
C
      DATA   POSEBL /
     % 0.68729833462074175D+00, -0.87298334620741685D-01, 0.4D+00,
     % 0.D+00,                   0.D+00,                  1.0D+00,
     %-0.87298334620741685D-01,  0.68729833462074175D+00, 0.4D+00 /
C     PHASE PRELIMINAIRE
C     -------------------
C     INITIALISATION A ZERO DU VECTEUR ELEMENTAIRE
      DO I=1,15
         BE(I) = 0D0
      ENDDO
C
C     1°) CONTRIBUTION DES EFFORTS SURFACIQUES
C     ----------------------------------------
      IF( LTDESU(LPFORC,NOOBSF) .GT. 0 ) THEN
C
c        LE JACOBIEN DE Fe: e reference --> e  , P1 par composante
         DELTA = ABS( (X(2,1) - X(1,1)) * (X(3,2) - X(1,2))
     %              - (X(3,1) - X(1,1)) * (X(2,2) - X(1,2)) )
C
         DO L=1,7
C
C           COORDONNEES DU POINT BL D'INTEGRATION DANS EF REFERENCE
            XCBL = COTRP5( 1, L )
            YCBL = COTRP5( 2, L )
C
C           CALCUL DU POINT BL D'INTEGRATION SUR LE TRIANGLE COURANT
            D  = 1.D0 - XCBL - YCBL
            XD = D * X(1,1) + XCBL * X(2,1) + YCBL * X(3,1)
            YD = D * X(1,2) + XCBL * X(2,2) + YCBL * X(3,2)
C
C           RECHERCHE DE LA VALEUR DES EFFORTS SURFACIQUES AU POINT BL
            CALL REFORC(3,NOOBSF,2, XD,YD,0D0, 0D0,0D0,0D0,
     %                  LTDESU(LPFORC,NOOBSF),FORCE)
C
C           CALCUL DES COEFFICIENTS DU SECOND MEMBRE LIES
C           AUX EFFORTS SURFACIQUES SUR LES 2 COMPOSANTES DE LA VITESSE
            FORCE(1) = DELTA * POTRP5(L) * FORCE(1)
            FORCE(2) = DELTA * POTRP5(L) * FORCE(2)
C
            DO I=1,6
               S = POTRBL(I,L)
               BE(I)   = BE(I)   + S * FORCE(1)
               BE(I+6) = BE(I+6) + S * FORCE(2)
            ENDDO
C
         ENDDO
C
      ENDIF
C
C     2°) CONTRIBUTION DES EFFORTS LINEIQUES
C     --------------------------------------
      DO 50 K=1,3
C
C        NO DE LA LIGNE DE L'ARETE K
         NL = NOOBLA(K)
         IF( NL .GT. 0 ) THEN
            IF( LTDELI(LPFORC,NL) .GT. 0 ) THEN
C
C              IL EXISTE UNE FORCE DONNEE SUR CETTE LIGNE
C              LE NUMERO DES SOMMETS DU COTE K DU TRIANGLE
               IF( K .NE. 3 ) THEN
                  KK = K+1
               ELSE
                  KK = 1
               ENDIF
C
C              LE VECTEUR ORTHOGONAL AU VECTEUR TANGENT
               VN(1) = X(KK,2) - X(K ,2)
               VN(2) = X(K ,1) - X(KK,1)
C
C              LE JACOBIEN
               D = SQRT( VN(1)**2 + VN(2)**2 )
C
C              LE VECTEUR NORMAL UNITAIRE
               VN(1) = VN(1) / D
               VN(2) = VN(2) / D
C
C              LE NO DES 2 SOMMETS DE L'ARETE K
               NOSOAR(1) = K
               NOSOAR(2) = KK
C              LE NO DU MILIEU DE L'ARETE K
               NOSOAR(3) = K+3
C
C              BOUCLE SUR LES 3 POINTS DE GAUSS
               DO 40 L=1,3
C
C                 CALCUL DU POINT D'INTEGRATION L SUR L'ARETE K
                  S  = COSEP5( L )
                  XD = (1D0 - S) * X(NOSOAR(1),1) + S * X(NOSOAR(2),1)
                  YD = (1D0 - S) * X(NOSOAR(1),2) + S * X(NOSOAR(2),2)
                  CALL REFORC( 2, NL, 2,
     %                         XD,YD,0D0, VN(1),VN(2),0D0,
     %                         LTDELI(LPFORC,NL), FORCE)
C
C                 PRODUIT JACOBIEN POIDS FORCE
                  FORCE(1) = FORCE(1) * D * POSEP5(L)
                  FORCE(2) = FORCE(2) * D * POSEP5(L)
C
cccC              VALEUR DES 3 POLYNOMES DE BASE SUR LE SEGMENT UNITE
cccC              AU M-EME POINT D'INTEGRATION CALCULES UNE SEULE FOIS
ccc               POSEBL( 1, L ) = ( 1D0 - S ) * ( 1D0 - 2D0 * S )
ccc               POSEBL( 2, L ) =  S * ( 2D0 * S - 1D0 )
ccc               POSEBL( 3, L ) =  4D0 * S * ( 1D0 - S )
C
                  DO 35 M=1,3
C                    LE NUMERO DU NOEUD M DE L'ARETE K
                     NM = NOSOAR(M)
C                    CALCUL DES EFFORTS LIES AU NOEUD NM
C                    AU POINT D'INTEGRATION L
                     S = POSEBL( M, L )
                     BE(NM)   = BE(NM)   + S * FORCE(1)
                     BE(NM+6) = BE(NM+6) + S * FORCE(2)
 35               CONTINUE
C
 40            CONTINUE
            ENDIF
         ENDIF
C
 50   CONTINUE
C
      RETURN
      END
