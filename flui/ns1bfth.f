C
      DMOTSTO = NTDLHB + 1.D0 + NBNOVI + NBDLFX +
     %  MOREE2 * ( NBNOVI + MOLGAU + NDIM*NBNOVI + NTDLVP*5 + NBDLFX
     %            +MOSFOB + 2*NBCOVG )
C
      IF( NORESO .GE. 2 ) THEN
C        GRADIENT CONJUGUE STOCKAGE MORSE
C        DESTRUCTION DES TABLEAUX AUXILIAIRES
         IF( MNNDFX .GT. 0 ) CALL TNMCDS( 'ENTIER', NTDLHB,  MNNDFX )
         IF( MNAUX  .GT. 0 ) CALL TNMCDS( 'REEL2',  LOAUX,   MNAUX  )
         IF( MNBDIR .GT. 0 ) CALL TNMCDS( 'REEL2',  LODIR,   MNBDIR )
         IF( MNLPLC .GT. 0 ) CALL TNMCDS( 'ENTIER', NTDLHB+1,MNLPLC )
         IF( MNLPCC .GT. 0 ) CALL TNMCDS( 'ENTIER', LOLPCC,  MNLPCC )
         IF( MNVGC  .GT. 0 ) CALL TNMCDS( 'REEL2',  LOLPCC,  MNVGC  )
         DMOTSTO = DMOTSTO + NTDLHB+1.D0+LOLPCC
     %           + MOREE2 * (LOAUX+LODIR+LOLPCC)
      ENDIF
C
C     BILAN SUR LA PLACE MEMOIRE CENTRALE OCCUPEE PAR LES MATRICES ...
C     ================================================================
      DMOTSTO = 2D0 + NTDLVP + NTDLHB
     %        + MOREE2 * ( NBCOVG*2 + DBLE(MOLGAU) + 4*NTDLVP )

      IF( NORESO .EQ. 1 ) THEN
C
C        CROUT STOCKAGE PROFIL
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19002) DMOTSTO
         ELSE
            WRITE(IMPRIM,29002) DMOTSTO
         ENDIF
19002 FORMAT(/'STOCKAGE des MATRICES et VECTEURS=',D25.16,
     %' MOTS MEMOIRE'/)
29002 FORMAT(/'STORAGE of MATRICES and VECTORS=',  D25.16,
     %' MEMORY WORDS'/)

      ELSE IF( NORESO .GE. 2 ) THEN

C        GRADIENT CONJUGUE STOCKAGE MORSE
         MOTPRE = NTDLHB + LOLPCC * (1+MOREE2)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19004) DMOTSTO, DBLE(MOTPRE), DBLE(MOTSGC),
     %                          DMOTSTO+MOTPRE+MOTSGC
         ELSE
            WRITE(IMPRIM,29004) DMOTSTO, DBLE(MOTPRE), DBLE(MOTSGC),
     %                          DMOTSTO+MOTPRE+MOTSGC
         ENDIF
19004    FORMAT(/' MATRICES MORSE GC ',         T30,D25.16,' MOTS'/
     %           ' MATRICE PRECONDITIONNEMENT', T30,D25.16,' MOTS'/
     %           ' TABLEAUX SUPPLEMENTAIRES GC',T30,D25.16,' MOTS'/
     %           ' AU TOTAL LE GC DEMANDE',     T30,D25.16,' MOTS'/)
C
29004    FORMAT(/' GC CONDENSED MATRICES',      T30,D25.16,' WORDS'/
     %           ' PRECONDITIONED MATRIX',      T30,D25.16,' WORDS'/
     %           ' AUXILIARY ARRAYS',           T30,D25.16,' WORDS'/
     %           ' TOTAL for the CG',           T30,D25.16,' WORDS'/)
      ENDIF

C     TOTAL DES MOTS NECESSAIRES A LA RESOLUTION
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,19005) DMOTSTO
      ELSE
         WRITE(IMPRIM,29005) DMOTSTO
      ENDIF
19005 FORMAT(/'STOCKAGE TOTAL des MATRICES et VECTEURS=',G25.17,
     %' MOTS MEMOIRE'/)
29005 FORMAT(/'STORAGE TOTAL of MATRICES and VECTORS=', G25.17,
     %' MEMORY WORDS'/)

C     CHAINAGE DES FACES FRONTALIERES C-A-D APPARTENANT A UN SEUL EF
C     A PARTIR DU ZERO EN POSITION 7 DE LFACES
      IF( MNLFAC .GT. 0 ) THEN
         CALL MACFAF( MOFACE, MXFACE, MCN(MNLFAC),  L1FAFR, NBFAFR )
C        MISE A JOUR DU NUMERO DE LA PREMIERE FACE FRONTALIERE (DANS UN SEUL EF)
         MCN( MNFAOB + W1FAFR ) = L1FAFR
      ENDIF

C     COUT CALCUL DES VITESSES PRESSIONS
C     ==================================
      DVITPR = DINFO( 'DELTA CPU' )

      IF( NBPAST .GT. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
        WRITE(IMPRIM,*)'ns1bfth: Temps moyen execution 1 pas de temps='
     %, TIMEMOY1DT/NBPAST,' pour',NBPAST,' pas de temps'
         ELSE
          WRITE(IMPRIM,*)'ns1bfth: Mean Execution Time of 1 time step='
     %, TIMEMOY1DT/NBPAST,' for',NBPAST,' time steps'
         ENDIF
      ENDIF

      RETURN
      END
