      SUBROUTINE NS1BFTH( KNOMOB, NAVSTO, NTLXOB, MOREE2,RELMIN,KNOMFIC,
     %                    NDIM,   MNXYZN, NBSOM,  NBNOHB, NBNOVI,
     %                    NBTYEL, MNNPEF, NBTTEF, NUTYEL, NBDLMX,
     %                    NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                    NORESO, LPLIGN, LPCOLO, NIVOGC, NBCOVG,
     %                    NDDLNO, VXYZP0, TEMPER0,
     %                    NBVCFX, NBDLFX, MNNDLFX,MNVDLFX,
     %                    THETA,  DT,     DTSTOC,TPSINI,TPSFIN, MNTIMES,
     %                    NOVVIPR,NTDLHB, NTDLVP, NTDLTE,
     %                    VITMAX, VITMOY, MXPAST, NBPAST, VPMIMX,
     %                    DFABG,  DFACTO, DVITPR, IERR )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : CALCULER LES VITESSES PRESSION DANS UN DOMAINE 2D ou 3D
C ----- DU PROBLEME DE NAVIER-STOKES EN INSTATIONNAIRE
C       SELON LA THETA-METHODE IMPLICITE AVEC PAS DE TEMPS CONSTANT
C             LA METHODE DES EF DE TAYLOR-HOOD ou BREZZI-FORTIN pour STOKES
C             LA METHODE DES CARACTERISTIQUES RETROGRADES pour le TERME de
C             TRANSPORT du PROBLEME de NAVIER-STOKES
C
C       DENSITE DE MASSE, VISCOSITE, EPSILON SUR LA PRESSION
C       SONT INDEPENDANTS DU TEMPS
C
C       FORCE EXTERNE SUR LA FRONTIERE
C       BLOCAGE DE LA VITESSE  IMPOSEE
C       BLOCAGE DE LA PRESSION IMPOSEE   PEUVENT DEPENDRE DU TEMPS

C       RESTRICTION: TOUTES LES NDIM COMPOSANTES DE LA VITESSE EN UN NOEUD
C                    DOIVENT ETRE FIXEES avec DES VALEURS NON FORCEMENT EGALES

C*******************************************************************************
C       A L'ETAPE n+1, LE PROBLEME CONSISTE A TROUVER {u(tn+1)} SOLUTION DE
C       ( [MG(Rho)] + dt theta [KG(Mhu)] ) {u(tn+1,x)} = {Rho*u(tn,X(tn;tn+1,x))
C                   + dt * theta {fg(tn+1)} + dt * (1-theta) {fg(tn)}
C                   - dt (1-theta) [KG(Mhu)] ) {u(tn)}
C        avec [MG(Rho)] la matrice de MASSE
C             [KG(Mhu)] la matrice de VISCOSITE DYNAMIQUE
C             X(tn;tn+1,x) est la position de la molecule du fluide a tn
C             qui sera en x a tn+1
C
C      UNE SEULE FOIS LES MATRICES GLOBALES [MG+dt*theta*KG] et
C      [- dt (1-theta) KG] SONT CALCULEES  (de dimension ndim*nbnovi+nbsom)
C      et les iterations se font avec les vecteurs  Un  Un+1  Fn  Fn+1
C
C      REMARQUE: Si theta=1 cas du schema implicite LE PLUS STABLE, alors
C      UNE SEULE MATRICE peut etre stokee [MG+dt*theta*KG] puisque l'autre
C      [dt (1-theta) KG] est nulle. (Actuellement, cette particularite
C      n'est pas prise en compte et RESTE A FAIRE...
C
C      A L'ETAPE 0 IL FAUT RECUPERER U0 et CONSTRUIRE F0
C      puis, les iterations en temps peuvent demarrer
C*******************************************************************************
C
C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET A TRAITER
C NAVSTO : =1 RESOLUTION du PROBLEME DE NAVIER-STOKES PAR THETA-METHODE
C NTLXOB : NUMERO DU TMS DU LEXIQUE DE L'OBJET KNOMOB
C MOREE2 : NOMBRE DE MOTS   D'UNE VARIABLE REELLE DOUBLE PRECISION
C RELMIN : PLUS PETIT REEL SERVANT DE MARQUEUR DE NON UTILISATION
C KNOMFIC: NOM DU FICHIER SUPPORT DU VECTEUR VITESSE+PRESSION

C NDIM   : DIMENSION DES COORDONNEES DES POINTS ( 2 OU 3 )
C MNXYZN : ADRESSE MCN DU TABLEAU XYZNOEUD DE L'OBJET KNOMOB
C          XYZ des SOMMETS+BARYCENTRES    DES EF BREZZI-FORTIN
C          XYZ des SOMMETS+MILIEUX ARETES DES EF TAYLOR-HOOD
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBNOHB : NOMBRE DE NOEUDS DU MAILLAGE HORS BARYCENTRES
C NBNOVI : NOMBRE DE NOEUDS DU MAILLAGE AVEC BARYCENTRES POUR BREZZI-FORTIN
C          SOMMETS + BARYCENTRES        des TETRAEDRES POUR BREZZI-FORTIN
C          SOMMETS + MILIEUX DES ARETES des TETRAEDRES POUR TAYLOR-HOOD
C
C NBTYEL : NOMBRE DE TYPES D'EF DU MAILLAGE DE CET OBJET
C MNNPEF : ADRESSE MCN DU TABLEAU DES ADRESSES MCN DES TMS NPEF"TYPE EF
C NBTTEF : NOMBRE TOTAL D'EF DU MAILLAGE
C NBDLMX : NOMBRE MAXIMAL DE DEGRES DE LIBERTE D'UN EF
C          ( 11 si BF2, 19 si BF3, 24 si TH2, 34 si TH3 )

C NUMIOB : NUMERO MINIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C NUMAOB : NUMERO MAXIMAL DU PLSV DANS LA DEFINITION DE L'OBJET
C MNDOEL : LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C          TABLEAUX DECRIVANT LES DONNEES DU FLUIDE DE L'OBJET COMPLET
C IEBLPR : NOMBRE DE TMS BLPRESSION       DES PLS DE L'OBJET TROUVE
C
C NORESO : CODE RESOLUTION DES SYSTEMES LINEAIRES
C          1 FACTORISATION COMPLETE DE CROUT ET MATRICES PROFILS
C          LES OPTIONS CI DESSOUS NE CONVERGENT PAS GENERALEMENT
C          => ARRET AVANT LE DEBUT DU CALCUL
C          2 GRADIENT CONJUGUE PRECONDITIONNE ou NON
C          3 CGS DOUBLE GRADIENT CONJUGUE ACCELERE ou
C                CONJUGATE GRADIENT SQUARED METHOD
C          4 BICG Stab ou Bi-CONJUGATE GRADIENT STABILISED

C ATTENTION: TOUTES LES METHODES DE GRADIENT CONJUGUE PEUVENT
C            NE PAS CONVERGER, ET SOUVENT PIRE, PEUVENT DIVERGER...
C            LA FACTORISATION DE CROUT ELLE EST FIABLE

C LPLIGN : POINTEURS SUR LES COEFFICIENTS DIAGONAUX DE LA MATRIC
C LPCOLO : NUMERO DE COLONNE DES COEFFICIENTS DE LA MATRICE MORSE
C NIVOGC : NIVEAU DE FACTORISATION INCOMPLETE DE LA MATRICE DE PRECONDITIONNEMEN
C          A CHOISIR PARMI 0 1 2
C NBCOVG : NOMBRE DE COEFFICIENTS STOCKES D'UNE MATRICE GLOBALE
C NDDLNO : NUMERO DU DERNIER DL DE CHAQUE NOEUD
C NCODSM : 1 SI MATRICE DE MASSE SYMETRIQUE
C          0 SI MATRICE DE MASSE DIAGONALE
C         -1 SI MATRICE DE MASSE NON SYMETRIQUE
C MNFG0  : ADRESSE MCN DE F(t0) SECOND MEMBRE   A L'INSTANT INITIAL
C VXYZP0 : VECTEUR VITESSEPRESSION A L'INSTANT INITIAL
C NBVCFX : NOMBRE DE DEGRES DE LIBERTE DE VITESSE CONVECTEE IMPOSEE
C NBDLFX : NOMBRE DE DL VITESSE OU PRESSION FIXES
C MNNDLFX: ADRESSE MCN DU TABLEAU DU NO GLOBAL DE CHAQUE DL FIXE
C MNVDLFX: ADRESSE MCN DU TABLEAU DE LA VALEUR DE CHAQUE DL FIXE
C
C THETA  : COEFFICIENT DE LA THETA METHODE (THETA(1)=theta, THETA(0)=1-theta)
C DT     : PAS CONSTANT DU TEMPS
C DTSTOC : PAS CONSTANT DU TEMPS ENTRE 2 STOCKAGES DU VECTEUR"VITESSEPRESSION
C TPSINI : TEMPS INITIAL DU CALCUL
C TPSFIN : TEMPS FINAL   DU CALCUL
C MNTIMES: ADRESSE MCN DES TEMPS DES VECTEURS VITESSE+PRESSION
C NOVVIPR: NUMERO DU DERNIER VECTEUR VITESSEPRESSION STOCKE
C MXFLUI : NOMBRE DE VECTEUR"VITESSEPRESSION A STOCKER
C NTDLHB : NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET HORS BARYCENTRES SI BF
C NTDLVP : NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET AVEC BARYCENTRES SI BF
C VITMAX : NORME DE LA VITESSE MAXIMALE INITIALE
C VITMOY : MOYENNE INITIALE DE LA NORME DE LA VITESSE AUX NOEUDS
C MXPAST : NOMBRE DE VECTEURS VITESSE-PRESSION A CALCULER = NOMBRE DE PAS DE TEMPS
C NBPAST : NOMBRE DE PAS DE TEMPS DEJA CACULES AU TEMPS TPINI
C
C SORTIES:
C --------
C NBPAST : NOMBRE DE PAS DE TEMPS CACULES AU TEMPS TPFIN
C VPMIMX : (TEMPS, VitesseMoyenne, VitesseMax,
C           PressionMoyenne, Pression Max-Min,
C           FLUX-, FLUX+
C           No du PAS DT d'integration du Pb INSTATIONNAIRE)
C           a chaque PAS de temps de CALCUL de la VITESSE+PRESSION
C VITMAX : NORME DE LA VITESSE MAXIMALE FINALE
C VITMOY : MOYENNE FINALE DE LA NORME DE LA VITESSE AUX NOEUDS
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY     Aout 2009
C AJOUTS : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY     Mars 2010
C23456---------------------------------------------------------------012
