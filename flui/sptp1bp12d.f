      SUBROUTINE SPTP1BP12D( DT,      XYZSOM, 
     %                       NUEF,    NBEF,     NUSOTR, NDDLNO, NTDLHB,
     %                       VITMAX,  VXYZPN,
     %                       VMXNUEF, NBSSPATE, DDT )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE NOMBRE NBSSPATE DE SOUS PAS DE TEMPS DDT
C -----    DANS LE PAS DE TEMPS DT POUR LE TRIANGLE NUEF BREZZI-FORTIN

C ENTREES:
C --------
C DT     : LE PAS CONSTANT DE TEMPS ENTRE tn ET tn+1
C XYZSOM : 3 COORDONNEES DES SOMMETS DES TRIANGLES
C NUEF   : NUMERO DE L'EF A TRAITER
C NBEF   : NOMBRE DE TRIANGLES DU MAILLAGE
C NUSOTR : NUMERO DES 3 SOMMETS DES TRIANGLES
C NDDLNO : TABLEAU DES POINTEURS SUR LE DERNIER DL DE CHAQUE NOEUD FLUIDE
C          CE TABLEAU EST DIMENSIONNE A 1+NBNOEU DU MAILLAGE
C NTDLHB : NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET HORS BARYCENTRES SI BF
C          (NDIM+1) NBNOHB (SANS BARYCENTRES)
C VITMAX : NORME DE LA VITESSE MAXIMALE GLOBALE SUR LE MAILLAGE
C VXYZPN : VECTEUR GLOBAL des DL VITESSES-PRESSIONS (1:NTDL) au TEMPS tn

C SORTIES :
C ---------
C VMXNUEF : LA VITESSE MAXIMALE EN L'UN DES 3 SOMMETS DU TRIANGLE NUEF
C NBSSPATE: LE NOMBRE NBSSPATE DE SOUS PAS DE TEMPS DDT
C DDT     : LA VALEUR DU SOUS PAS DE TEMPS DANS LE PAS DE TEMPS DT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray Decembre 2012
C23456---------------------------------------------------------------012
      IMPLICIT  NONE
      REAL               DT, XYZSOM(3,*)
      DOUBLE PRECISION   VXYZPN(1:*), VITMAX, VMXNUEF, S, ARETMX, DDT
      INTEGER            NUEF, NBEF, NUSOTR(NBEF,3), NDDLNO(0:*),
     %                   NTDLHB, NBSSPATE, NDL, I, K, NS1, NS2

C     CALCUL DE LA LONGUEUR MAXIMALE DES 3 ARETES DU TRIANGLE
      ARETMX = 0D0
      NS1 = 3
      DO I=1,3
         NS2 = I
         S = (XYZSOM(1,NUSOTR(NUEF,NS2))-XYZSOM(1,NUSOTR(NUEF,NS1)))**2
     %     + (XYZSOM(2,NUSOTR(NUEF,NS2))-XYZSOM(2,NUSOTR(NUEF,NS1)))**2
         IF( S .GT. ARETMX ) ARETMX = S
         NS1 = NS2
      ENDDO
      ARETMX = SQRT( ARETMX )

C     VITESSE MAXIMALE AU TEMPS t - DT AUX 3 SOMMETS DU TRIANGLE
      VMXNUEF = 0D0
      DO I=1,3
C        NO DU DL I DANS L'EF NUEF DE LA VITESSE
         NDL = NDDLNO( NUSOTR(NUEF,I) - 1 )
         S   = VXYZPN(NDL+1)**2 + VXYZPN(NDL+2)**2
         IF( S .GT. VMXNUEF ) VMXNUEF = S
      ENDDO

C     VITESSE AU BARYCENTRE DE L'EF NUEF
      NDL = NTDLHB + 2 * NUEF
C     CARRE DE LA VITESSE AU BARYCENTRE DE L'EF NUEF
      S = VXYZPN(NDL-1)**2 + VXYZPN(NDL)**2
      IF( S .GT. VMXNUEF ) VMXNUEF = S

      VMXNUEF = SQRT( VMXNUEF )

      IF( VMXNUEF .LE. VITMAX*1D-3 ) THEN
C        SI VITESSE TROP FAIBLE RETOUR AVEC LA VITESSE NON CONVECTEE
         NBSSPATE = 0
         DDT      = 0D0
         GOTO 9999
      ENDIF

C     LE SOUS PAS DE TEMPS POUR L'INTEGRATION RETROGRADE EN TEMPS
C     -----------------------------------------------------------
C     LE NOMBRE DE SOUS PAS DE TEMPS DANS LE PAS DE TEMPS DT
      I = NINT( 8 * VMXNUEF / VITMAX )

C     5 SOUS PAS DE TEMPS POUR PARCOURIR L'ARETE MAXIMALE A
C     LA VITESSE VMXNUEF DURANT LE TEMPS DT
      K = NINT( 5 * VMXNUEF*DT / ARETMX )

      NBSSPATE = MAX( 3, I, K )
      NBSSPATE = MIN( 8, NBSSPATE )

C     SOUS PAS DE TEMPS ENTRE T-DT et T
      DDT = DT / NBSSPATE

 9999 RETURN
      END
