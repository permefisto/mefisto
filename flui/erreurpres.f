      SUBROUTINE ERREURPRES(KNOMOB, KNOMFIC, NDIM, NUTYEL, MNXYZP1,
     %                      NODDL,NUNOSO, NBSOMT,NBNOVI, NTDLVP, NTDLTE,
     %                      NCAS0,  NCAS1,  TIMES, VXYZPN, TEMPER,
     %                      NOFOPR, ERRPRES, PrErMOY,
     %                      PrErMIN, NOEMIN, NCASMIN,
     %                      PrErMAX, NOEMAX, NCASMAX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE TABLEAU DE L'ERREUR SUR LA PRESSION P1
C -----    AUX SOMMETS DU MAILLAGE C-A-D POUR LES EF de 
C          BREZZI-FORTIN ou TAYLOR-HOOD

C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET CONTENANT LE FLUIDE
C KNOMFIC: NOM DU FICHIER SUPPORT D'UN VECTEUR PRESSION+PRESSION
C NDIM   : DIMENSION 2 OU 3 DE L'ESPACE DE L'OBJET
C NUTYEL : NUMERO DU TYPE D'EF UTILISE
C          13 ou 19 : BREZZI FORTIN <=> TRIA 2P1D ou TETR 3P1D + BARYCENTRE
C          15 ou 20 : TAYLOR HOOD   <=> TRIA 2P2D ou TRIA 2P2C
C MNXYZP1: ADRESSE MCN DU TMS 'XYZSOMMET' DU MAILLAGE DE L'OBJET
C NODDL  : TABLEAU DU NUMERO DU DERNIER D.L. DE CHAQUE NOEUD
C NUNOSO : NO NOEUD => NO SOMMET POUR EF TAYLOR-HOOD
C NBSOMT : NOMBRE DE SOMMETS DU MAILLAGE  (=NBNOPR)
C NBNOVI : NOMBRE DE NOEUDS SUPPORT DE LA PRESSION
C          SOMMETS + BARYCENTRES        des TETRAEDRES POUR BREZZI-FORTIN
C          SOMMETS + MILIEUX DES ARETES des TETRAEDRES POUR TAYLOR-HOOD
C NBNOPR : NOMBRE DE NOEUDS SUPPORT DE LA PRESSION
C NTDLVP : NOMBRE TOTAL DE DEGRES DE LIBERTES EN PRESSIONS PRESSIONS
C NCAS0  : PREMIER VECTEUR PRESSION-PRESSION A TRAITER
C NCAS1  : DERNIER VECTEUR PRESSION-PRESSION A TRAITER
C TIMES  : LES NCAS0:NCAS1 TEMPS DES CALCULS DE LA PRESSION-PRESSION
C VXYZPN : UNE CARTE NCAS DES PRESSIONS-PRESSIONS AUX NOEUDS DU MAILLAGE
C NOFOPR : NUMERO DE LA FONCTION PRESSION_EXACTE(t,x,y,z,nc)
C          =0 SI ELLE N'EST PAS DONNEE PAR L'UTILISATEUR -> RETOUR

C SORTIES:
C --------
C ERRPRES: ERREUR SUR LA PRESSION EN TOUS LES NOEUDS DES CAS DE TEMPS NCAS0:NCAS1
C PrErMOY: ERREUR MOYENNE POUR TOUS LES NOEUDS ET TEMPS
C PrErMIN: ERREUR MINIMALE SUR PRESSION(Temps,Noeuds)
C NOEMIN : NUMERO DU NOEUD   DE L'ERREUR MINIMALE SUR PRESSION(Temps,Noeuds)
C NCASMIN: NUMERO DU VECTEUR DE L'ERREUR MINIMALE SUR PRESSION(Temps,Noeuds)
C PrErMAX: ERREUR MAXIMALE SUR PRESSION(Temps,Noeuds)
C NOEMAX : NUMERO DU NOEUD   DE L'ERREUR MAXIMALE SUR PRESSION(Temps,Noeuds)
C NCASMAX: NUMERO DU VECTEUR DE L'ERREUR MAXIMALE SUR PRESSION(Temps,Noeuds)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint Pierre du Perray               Mai 2021
C234567...............................................................12
      include"./incl/a___xyznoeud.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1), RMCN(1), DMCN(1))
      CHARACTER*(*)     KNOMOB
      CHARACTER*80      KNOMFIC
      INTEGER           NODDL(0:NBNOVI), NUNOSO(1:NBNOVI)
      REAL              TIMES(NCAS0:NCAS1), PrErMIN, PrErMAX, PrErMOY
      DOUBLE PRECISION  VXYZPN(1:NTDLVP), ERRPRES(1:NBSOMT,NCAS0:NCAS1),
     %                  TEMPER(*),
     %                  DPARAF(4), PrErrMOY,
     %                  PresCalc, PERROR, PrErrMIN, PrErrMAX,
     %                  PresExac
 
      INTRINSIC         SQRT

      IF( NOFOPR .LE. 0 ) RETURN

      NDIM1 = NDIM + 1

C     INITIALISATION DU MIN MAX
      NOEMIN  = 1
      NCASMIN = 1
      PrErrMIN = 1D100

      NOEMAX  = 1
      NCASMAX = 1
      PrErrMAX =-1D100

      PrErrMOY = 0D0

C     BOUCLE SUR LES TEMPS DES CALCULS
C     ================================
      MNXYZ = MNXYZP1 + WYZNOE - 3

      DO NCAS = NCAS0, NCAS1

C        LE TEMPS DU CALCUL
         TEMPS = TIMES(NCAS)

C        TRANSFERT DE LA PRESSION-PRESSION AU TEMPS NCAS PAR NOEUDS
C        FICHIER DU REPERTOIRE PROJET DANS LE TABLEAU MNVXYZP0
         CALL LIFIVIPRTE( KNOMOB, TEMPS,  NCAS,   NAVSTO, NBPASDT,
     %                    NDIM,   NBNOVI, NBNOPR, NBNOTE,
     %                    NTDLVP, VXYZPN, NTDLTE, TEMPER,
     %                    KNOMFIC, IERR )

         DO 10 I=1,NBNOVI

C           NOMBRE DE DL AU NOEUD I
            NDL = NODDL(I-1)
            ND  = NODDL(I) - NDL
            IF( ND .NE. NDIM1 ) GOTO 10

C           LA PRESSION CALCULEE au NOEUD I au TEMPS
            PresCalc = VXYZPN( NDL + NDIM1 )

C           LE NOEUD I EST LE SOMMET NST
            IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN
C              EF de TAYLOR HOOD INTERPOLATION P2 LAGRANGE
               NST = NUNOSO( I )
            ELSE
C              EF de BREZZI-FORTIN INTERPOLAION P1 LAGRANGE + BARYCENTRE
               NST = I
            ENDIF

C           XYZ DU SOMMET NST
C           ADRESSE DES COORDONNEES DU NOEUD I = COORDONNEES du SOMMET NST
            MN = MNXYZP1 + WYZNOE - 3 + 3 * NST
C           LES PARAMETRES DU CALCUL DE LA PRESSION EXACTE
            DPARAF(1) = TEMPS
            DPARAF(2) = RMCN(MN)
            DPARAF(3) = RMCN(MN+1)
            DPARAF(4) = RMCN(MN+2)

C           LA PRESSION EXACTE(temps,xp,yp,zp)
C           PRESSION EXACTE AU NOEUD I AU TEMPS NCAS
            CALL FONVAL( NOFOPR, 4, DPARAF, NCODEV, PresExac )

C           CALCUL DE L'ERREUR LOCALE SUR LA PRESSION
            PERROR = ABS( PresExac - PresCalc )

C           ERREUR SUR LA PRESSION AU NOEUD I ET AU TEMPS NCAS
            ERRPRES( NST, NCAS ) = PERROR

C           MODULE DE ERREUR MIN ET MAX AU NOEUD DE LA PRESSION
            IF( PERROR .LT. PrErrMIN ) THEN
               PrErrMIN = PERROR
               NOEMIN  = I
               NCASMIN = NCAS
            ELSE IF( PERROR .GT. PrErrMAX ) THEN
               PrErrMAX = PERROR
               NOEMAX  = I
               NCASMAX = NCAS
            ENDIF

C           AJOUT DE L'ERREUR AU NOEUD I AU TEMPS NCAS
            PrErrMOY = PrErrMOY + PERROR

 10      ENDDO

      ENDDO

C     ERREUR MOYENNE POUR TOUS LES NOEUDS ET TEMPS
      PrErrMOY = (PrErrMOY / NBNOVI) / (NCAS1-NCAS0+1)

      PRINT*,'erreurpres: ERREUR MOYENNE sur la PRESSION=',PrErrMOY

C     CONVERSION EN REEL SIMPLE
      PrErMOY = REAL( PrErrMOY )
      PrErMIN = REAL( PrErrMIN )
      PrErMAX = REAL( PrErrMAX )

      RETURN
      END
