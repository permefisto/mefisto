      SUBROUTINE LIFIVIPRTE( KNOMOB,   TEMPS,    NOVVIPR,
     %                       NAVSTO,   NBPASDT,
     %                       NDIM,     NBNOEVIT, NBNOEPRE, NBNOETEM,
     %                       NTDLVP,   VXYZPN,   NTDLTE, TEMPER,
     %                       KNOMFIC,  IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     LECTURE du VECTEUR VXYZPN + TEMPER
C -----     STOCKE dans le FICHIER NOVVIPR de
C           NTDLVP DEGRES DE LIBERTE DES VITESSES+PRESSION
C           NTDLTE DEGRES DE TEMPERATURE
C           de NUMERO de FICHIER NOVVIPR AU TEMPS TEMPS
C           dans le REPERTOIRE Unix du PROJET Mefisto

C ENTREES:
C --------
C KNOMOB  : NOM DE L'OBJET DANS LE LEXIQUE des OBJETS
C TEMPS   : TEMPS  DU VECTEUR VITESSE_XYZ+PRESSION A LIRE
C NOVVIPR : >0 NUMERO DU VECTEUR VITESSE_XYZ+PRESSION DANS LE REPERTOIRE
C              de l'OBJET FLUIDE KNOMOB

C NTDLVP  : SI =-1 ALORS
C              LECTURE SEULE DES ENTIERS DU PREMIER ENREGISTREMENT
C              du FICHIER NOVVIPR
C              AFIN de RECUPERER NAVSTO, NBPASDT, NBNOEVIT, NBNOEPRE,
C              NTDLVP, NTDLTE   SANS LIRE VXYZPN NI TEMPER

C SORTIES:
C --------
C NAVSTO :-1 SI SOLUTION DU PROBLEME DE STOKES   STATIONNAIRE
C          0 SI SOLUTION DU PROBLEME DE STOKES INSTATIONNAIRE
C          1 SI SOLUTION DU PROBLEME DE NAVIER-STOKES SOUS FORME IMPLICITE
C          2 SI SOLUTION DU PROBLEME DE NAVIER-STOKES PAR PAS FRACTIONNAIRES
C          3 SI SOLUTION DU PROBLEME DE NAVIER-STOKES + THERMIQUE BOUSSINESQ
C          4 SI SOLUTION DU PROBLEME DE NAVIER-STOKES PAR PISO+CARACTERISTIQUE
C          5 SI SOLUTION DU PROBLEME DE NAVIER-STOKES PAR PISO+(V.D) V

C NBPASDT : NOMBRE DE PAS DE TEMPS CALCULES
C           du FICHIER VITESSE+PRESSION et/ou TEMPERATURE

C NDIM    : 2 ou 3 DIMENSION DE L'ESPACE DE TRAVAIL
C NBNOEVIT: NOMBRE DE NOEUDS SUPPORTS DE LA VITESSE
C NBNOEPRE: NOMBRE DE NOEUDS SUPPORTS DE LA PRESSION
C NBNOETEM: NOMBRE DE NOEUDS SUPPORTS DE LA TEMPERATURE

C NTDLVP  : NOMBRE TOTAL DE DEGRES DE LIBERTE EN VITESSE_XYZ+PRESSION
C VXYZPN  : NTDLVP DEGRES DE LIBERTE DU  VECTEUR VITESSE_XYZ+PRESSION
C           AUX NOEUDS DU MAILLAGE POUR LA VITESSE+PRESSION

C NTDLTE  : NOMBRE TOTAL DE DEGRES DE LIBERTE EN TEMPERATURE
C TEMPER  : NTDLTE DEGRES DE LIBERTE DU VECTEUR TEMPERATURE
C           AUX NOEUDS DU MAILLAGE POUR LA TEMPERATURE

C KNOMFIC: NOM DU FICHIER SUPPORT DU VECTEUR NOVVIPR des VITESSE+PRESSION
C IERR   : =0 SI PAS D'ERREUR DETECTEE, >0 SINON
C          =2 SI LE FICHIER N'EXISTE PAS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint Pierre du Perray             Aout 2020
C MODIFS : ALAIN PERRONNET  Saint Pierre du Perray            Avril 2021
C MODIFS : ALAIN PERRONNET  Saint Pierre du Perray            Avril 2022
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      CHARACTER*(*)     KNOMOB
      CHARACTER*(*)     KNOMFIC
      LOGICAL           LEXIST, LOPEN
      DOUBLE PRECISION  VXYZPN(NTDLVP), TEMPER(NTDLTE)

C     LE NOM DU FICHIER du VECTEUR VITESSE-PRESSION
      CALL NMFIVIPRTE( KNOMOB, TEMPS, NOVVIPR,  KNOMFIC, NBK )

C     LE FICHIER KNOMFIC(1:NBK) EXISTE T IL?
      INQUIRE( FILE=KNOMFIC(1:NBK), EXIST=LEXIST, OPENED=LOPEN )
      IF( .NOT. LEXIST ) THEN

C        LE FICHIER KNOMFIC(1:NBK) N'EXISTE PAS
         IF( LANGAG .EQ. 0 ) THEN
        PRINT*,'lifiviprte: Le FICHIER ',KNOMFIC(1:NBK),' N''EXISTE PAS'
         ELSE
            PRINT*,'lifiviprte: FILE ',KNOMFIC(1:NBK),' DOES NOT EXIST'
         ENDIF
         IERR = 2
         GOTO 9999

      ENDIF

C     OUVERTURE DU FICHIER ECRIT KNOMFIC(1:NBK)
      CALL TRUNIT( NFUNIT )
      OPEN( UNIT=NFUNIT, FILE=KNOMFIC(1:NBK), STATUS='OLD',
     %      ACCESS='SEQUENTIAL', FORM='UNFORMATTED',
     %      IOSTAT=IOERR, ERR=9990 )

C     LE PREMIER ENREGISTREMENT DU FICHIER KNOMFIC(1:NBK)
C     et DERNIER ENREGISTREMENT DU FICHIER KNOMFIC(1:NBK)
C     LES NTDLVP DL DE LA VITESSE ET DE LA PRESSION AUX NOEUDS DU MAILLAGE
C     SONT ECRITS EN BINAIRE DIRECTEMENT SUR LE FICHIER KNOMFIC(1:NBK)
      IF( NTDLVP .EQ. -1 ) THEN

C        LECTURE DES 8-ERS ENTIERS
         READ(NFUNIT) NAVSTO,   NBPASDT,  NDIM,
     %                NBNOEVIT, NBNOEPRE, NBNOETEM,
     %                NTDLVP,   NTDLTE

C        AFFICHAGE
         IF( LANGAG .EQ. 0 ) THEN
            PRINT 10010, NAVSTO,   NBPASDT,  NDIM,
     %                   NBNOEVIT, NBNOEPRE, NTDLVP, NTDLTE,
     %                   KNOMFIC(1:NBK)
         ELSE
            PRINT 20010, NAVSTO,   NBPASDT,  NDIM,
     %                   NBNOEVIT, NBNOEPRE, NTDLVP, NTDLTE,
     %                   KNOMFIC(1:NBK)
         ENDIF

10010 FORMAT('lifiviprte: Lecture NAVSTO=',I2,
     %' NbPasTemps=',I8,
     %' DimensionEspace=',I1,
     %' NbNoeudsVitesse=',I9,' NbNoeudsPression=',I9,
     %' NbDLVP=',I10,' NbDLTe=',I10,' du FICHIER ',A)

20010 FORMAT('lifiviprte: READING NAVSTO=',I2,
     %' StepTimeNb=',I8,
     %' SpaceDimension=',I1,
     %' VelocityNodesNb=',I9,' PressureNodesNb=',I9,
     %' NbDFVP=',I10,' NbDFTe=',I10,' on FILE ',A)

      ELSE

C        LECTURE DES ENTIERS + NTDLVP REELS DOUBLE PRECISION
         READ(NFUNIT) NAVSTO,   NBPASDT,  NDIM,
     %                NBNOEVIT, NBNOEPRE, NBNOETEM,
     %                NTDLVP,   NTDLTE

C        ECRITURE DU VECTEUR VITESSE+PRESSION
         IF( NTDLVP .GT. 0 ) THEN
            READ(NFUNIT) (VXYZPN(I),I=1,NTDLVP)
         ENDIF

C        ECRITURE DU VECTEUR TEMPERATURE
         IF( NTDLTE .GT. 0 ) THEN
            READ(NFUNIT) (TEMPER(I),I=1,NTDLTE)
         ENDIF

C        AFFICHAGE
         IF( LANGAG .EQ. 0 ) THEN
            PRINT 10011, TEMPS, NAVSTO, NBPASDT, NDIM,
     %                   NBNOEVIT, NBNOEPRE, NTDLVP, NTDLTE,
     %                   KNOMFIC(1:NBK)
         ELSE
            PRINT 20011, TEMPS, NAVSTO, NBPASDT, NDIM,
     %                   NBNOEVIT, NBNOEPRE, NTDLVP, NTDLTE,
     %                   KNOMFIC(1:NBK)

         ENDIF

      ENDIF


10011 FORMAT('lifiviprte: TEMPS=',E15.7,' Lecture NAVSTO=',I2,
     %' PasTemps=',I8,
     %' DimensionEspace=',I1,
     %' NoeudsVitesse=',I9,' NoeudsPression=',I9,
     %' +NbDLVP=',I10,' +NbDLTE=',I10' reelsD du FICHIER ',A)

20011 FORMAT('lifiviprte: TIME=',E15.7,' READING of NAVSTO=',I2,
     %' StepTimeNb=',I8,
     %' Space Dimension=',I1,
     %' VelocityNodesNb=',I9,' PressureNodesNb=',I9,
     %' +NbDLVP=',I10' +NbDLTE=',I10,' Dreals on FILE ',A)

C     FERMETURE DU FICHIER KNOMFIC(1:NBK)
      CLOSE( NFUNIT )

      IERR = 0
      GOTO 9999


C     TRAITEMENT DES ERREURS
 9990 IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'lifiviprte: Le FICHIER ',NOMFIC,' UNITE',NFUNIT,
     %          ' NE PEUT PAS ETRE OUVERT  IOERR=',IOERR
      ELSE
         PRINT*,'lifiviprte: FILE ',NOMFIC,' UNIT',NFUNIT,
     %          ' MAY NOT BE OPENED  IOERR=',IOERR
      ENDIF
      IERR = 3

 9999 RETURN
      END
