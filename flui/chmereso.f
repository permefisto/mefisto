      SUBROUTINE CHMERESO( NAVIER, NORESO, NIVOGC, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ENTRER LA METHODE de RESOLUTION des SYSTEMES LINEAIRES
C -----    ( -> le MODE de STOCKAGE PROFIL ou CONDENSE )
C ENTREES:
C --------
C NAVIER : 0 POUR RESOUDRE LE PROBLEME DE STOKES
C          1 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES SOUS FORME IMPLICITE
C          2 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PAS FRACTIONNAIRES
C          3 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PISO+CARACTERISTIQUE
C          4 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PISO+(V.D) V

C SORTIES:
C --------
C NORESO : CODE DE RESOLUTION DES SYSTEMES LINEAIRES
C          SI NAVIER=1 ALORS
C          1 FACTORISATION DE CROUT ET MATRICES PROFILS
C          2 GRADIENT CONJUGUE PRECONDITIONNE PAR NIVEAUX ET MATRICES MORSES
C          3 CGS DOUBLE GRADIENT CONJUGUE ACCELERE ou
C                CONJUGATE GRADIENT SQUARED METHOD

C          SI NAVIER=2 ou 3 ou 4 ou 7 ou 9 ou 10 ALORS
C          1 FACTORISATION DE CROUT ET MATRICES PROFILS
C          2 GRADIENT CONJUGUE SIMPLE MATRICE MORSE
C          SI NAVIER=8 ALORS 1 FACTORISATION DE GAUSS SUR MATRICE NON SYMETRIQUE

C NIVOGC : SI NORESO=2 alors
C          =0, 1, 2 DU STOCKAGE DE LA MATRICE DE PRECONDITIONNEMENT DU GC
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint Pierre du Perray              Aout 2020
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"

C     VALEURS PAR DEFAUT
      NORESO = 2
      NIVOGC = 0
      IERR   = 0

C     CHOIX DE LA METHODE DE RESOLUTION DU SYSTEME LINEAIRE
C     =====================================================
      CALL LIMTCL( 'methrecr', NMTCL )
C     (1 : 'FACTORISATION CROUT avec stockage PROFIL',
C      2 : 'GRADIENT CONJUGUE avec CROUT INCOMPLET',
C      3 : 'DOUBLE GRADIENT ACCELERE CGS',
C      4 : 'DOUBLE GRADIENT STABILISE BiCGStab' )
      IF( NMTCL .LE. 0 ) GOTO 9999
      NORESO = NMTCL

C     VERIFICATION des COMPATIBILITES du CHOIX ET AFFICHAGES
      IF( NAVIER .GE. 2 .AND. NAVIER .LE. 4 .OR.
     %    NAVIER .EQ. 7 .OR.  NAVIER .GE. 9 ) THEN

C        CROUT PROFIL ou GC PRECONDITIONNE SEULEMENT POSSIBLE
         NORESO = MIN( 2, NORESO )
         NORESO = MAX( 1, NORESO )

      ELSE IF( NAVIER .EQ. 8 ) THEN

C        FACTORISATION GAUSS IMPOSEE POUR UN PROBLEME NON SYMETRIQUE
         NORESO = 1
         IF( LANGAG .EQ. 0 ) THEN
            PRINT *,'RESOLUTION PAR FACTORISATION DE GAUSS L U'
         ELSE
            PRINT *,'SOLUTION by L U GAUSS FACTORIZATION'
         ENDIF
         GOTO 9999

      ENDIF

C     AFFICHAGE DU CHOIX DE LA RESOLUTION RETENUE
      IF( NORESO .EQ. 1 ) THEN
         NIVOGC = 0
         IF( NAVIER .EQ. 1 ) THEN
            IF( LANGAG .EQ. 0 ) THEN
               PRINT *,'RESOLUTION PAR FACTORISATION DE CROUT L D tL'
            ELSE
               PRINT *,'SOLUTION by L D Lt CROUT FACTORIZATION'
            ENDIF
         ELSE
            IF( LANGAG .EQ. 0 ) THEN
               PRINT *,'RESOLUTION PAR FACTORISATION CROUT L D tL'
            ELSE
               PRINT *,'SOLUTION by L D Lt CROUT FACTORIZATION'
            ENDIF
         ENDIF

      ELSE IF( NORESO .EQ. 2 ) THEN

         IF( LANGAG .EQ. 0 ) THEN
            PRINT *,'RESOLUTION CHOISIE PAR GRADIENT CONJUGUE'
         ELSE
            PRINT *,'CONJUGATE GRADIENT SOLUTION CHOICE'
         ENDIF

      ELSE

         IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'Cette METHODE de RESOLUTION Ax=b N''EST PAS PROGRAMMEE'
         ELSE
            PRINT*,'THIS TYPE of SOLVER Ax=b IS NOT PROGRAMMED'
         ENDIF
         IERR = 11
         GOTO 9999

      ENDIF

C     LECTURE DU NIVEAU DE FACTORISATION (0 ou 1 ou 2)SI GRADIENT CONJUGUE
C    (NON UTILISE SI GRADIENT CONJUGUE SIMPLE MAIS DEMANDE TOUT DE MEME)
C     ====================================================================
      IF( NORESO .GE. 2 ) THEN
         CALL INVITE( 36 )
C        VALEUR PAR DEFAUT  DEMANDE DE LA VALEUR DU NIVOGC
         NIVOGC = 2
         NCVALS = 4
         CALL LIRENT( NCVALS, NIVOGC )
         IF( NCVALS .LT. 0 ) THEN
            IERR   = -1
            NIVOGC = 0
            GOTO 9999
         ENDIF

C        ECRETAGE DU NIVEAU DE FACTORISATION INCOMPLETE POUR LE GC
         IF( NIVOGC .LT. 0 ) NIVOGC = 0
         IF( NIVOGC .GT. 2 ) NIVOGC = 2

         IF( LANGAG .EQ. 0 ) THEN
            PRINT *, 'AVEC un NIVEAU',NIVOGC,' de FACTORISATION'
         ELSE
            PRINT *, 'WITH a FACTORIZATION LEVEL',NIVOGC
         ENDIF
      ENDIF

 9999 RETURN
      END
