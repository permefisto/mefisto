      SUBROUTINE AGVITTH( Rho,    DtMhu,  XYZNOE,
     %                    NUTYEL, NBNOEF, NBELEM, NUNOEF,
     %                    NORESO, NBNOE,  NBCVG,  LP2LIGN, LP2COLO, VG )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:     CONSTRUIRE ET ASSEMBLER LA MATRICE GLOBALE DU PROBLEME
C ----     VG=( Rho - dt Mhu Laplacien ) = Integrale Rho P2 P2
C                                        + DtMhu grad P2 grad P2 dX
C          avec INTEGRATION EXACTE SUR L'ELEMENT FINI
C
C ENTREES:
C --------
C Rho    : COEFFICIENT DE LA MATRICE DE MASSE      P2  P2
C DtMhu  : COEFFICIENT DE LA MATRICE DE VISCOSITE DP2 DP2
C XYZNOE : XYZNOE(3,NBNOE)  3 COORDONNEES DES NOEUDS DU MAILLAGE
C NUTYEL : NUMERO DU TYPE D'EF ( 15 TRIANGLE, 20 TETRAEDRE TAYLOR-HOOD )
C NBNOEF : NOMBRE DE NOEUDS D'UN EF DE CE TYPE
C          ( 6 POUR LE TRIANGLE, 10 POUR LE TETRAEDRE )
C NBELEM : NOMBRE D'EF DU MAILLAGE
C NUNOEF : NUNOEF(NBELEM,NBNOEF) NO DES 6 NOEUDS DES NBELEM EF

C NORESO : CODE RESOLUTION DES SYSTEMES LINEAIRES AVEC LA MATRICE VG
C          1 FACTORISATION COMPLETE DE CROUT ET MATRICE PROFIL VG
C          2 GRADIENT CONJUGUE AVEC UN STOCKAGE MORSE DE VG
C NBNOE  : NOMBRE DE NOEUDS DU MAILLAGE (SOMMETS ET MILIEUX DES ARETES)
C NBCVG  : NOMBRE DE COEFFICIENTS DES LIGNES DE VG
C          (SI NORESO=1   NBCVG=0
C          (SI NORESO=2   NBCVG=NOMBRE DE NO DES COLONNES STOCKEES)
C LP2LIGN: POINTEURS SUR LES COEFFICIENTS DIAGONAUX DE VG
C LP2COLO: SI NORESO=2 NUMERO DES COLONNES DE CHAQUE LIGNE
C                      DES COEFFICIENTS DE LA MATRICE MORSE VG
C          SI NORESO=1 LP2COLO N'EST PAS UTILISE
C
C SORTIE :
C --------
C VG     : MATRICE GLOBALE ASSEMBLEE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Octobre 2012
C23456---------------------------------------------------------------012
      IMPLICIT NONE
      include"./incl/donflu.inc"
      include"./incl/a___npef.inc"

      INTEGER           NORESO, NBNOE, NBCVG, LP2LIGN(0:NBNOE),
     %                  LP2COLO(NBCVG), NUTYEL, NBNOEF, NBELEM,
     %                  NUNOEF(NBELEM,NBNOEF)
      DOUBLE PRECISION  VG(NBCVG), Rho, DtMhu
      REAL              XYZNOE(3,NBNOE)

      INTEGER           I, NUELEM, NONOEF(10)
      DOUBLE PRECISION  AE(55)

C     MISE A ZERO DE LA MATRICE VG
      CALL AZEROD( NBCVG, VG )

      DO NUELEM = 1, NBELEM
C
C        NO DES NOEUDS DE L'ELEMENT FINI NUELEM
         DO I = 1, NBNOEF
            NONOEF(I) = NUNOEF(NUELEM,I)
         ENDDO

         IF( NUTYEL .EQ. 15 ) THEN

C           CALCUL DE LA MATRICE DE VISCOSITE Ae DE LA VITESSE P2
            CALL FAE2P2P2( Rho, DtMhu, NONOEF, NBNOE, XYZNOE, AE )

         ELSE

C           CALCUL DE LA MATRICE DE VISCOSITE Ae DE LA VITESSE P2
            CALL FAE3P2P2( Rho, DtMhu, NONOEF, NBNOE, XYZNOE, AE )
C
         ENDIF

C        ASSEMBLAGE DE LA MATRICE ELEMENTAIRE DANS LA MATRICE GLOBALE
         IF( NORESO .EQ. 1 ) THEN
C           MATRICE SYMETRIQUE PROFIL
            CALL ASMEPC( NBNOEF, NONOEF, 1, AE, AE, 1,
     %                   LP2LIGN, VG )
         ELSE
C           MATRICE SYMETRIQUE MORSE
            CALL ASMEGC( NBNOEF, NONOEF, 1, AE, AE, 1,
     %                   LP2LIGN, LP2COLO, VG )
         ENDIF
C
      ENDDO

ccc      call affvect( 'agvitth: MATRICE VITESSE VG', 20, VG )
ccc      call afl1ve(  'agvitth: MATRICE VITESSE VG', NBCVG, VG)
C     FIN DE LA BOUCLE SUR LES EF DE LA CONSTRUCTION DE VG

      RETURN
      END
