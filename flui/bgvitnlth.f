      SUBROUTINE BGVITNLTH( DT, Rho, CoGrPr, NDIM, NBSOM, NBNOVI,
     %                      NUNOSO, XYZNOE,
     %                      NUTYEL, NBNOEF, NBELEM, NUNOEF,
     %                      NBVVEF, NBSFEF, NBLAEF,
     %                      NUVVEF, NUSFEF, NULAEF,
     %                      NUMILI, NUMALI, LTDELI,
     %                      NUMISU, NUMASU, LTDESU,
     %                      NUMIVO, NUMAVO, LTDEVO,
     %                      VITtn,  VITm,   PRESSm, IEFOIN, IEFOCL,
     %                      BG )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:CONSTRUIRE ET ASSEMBLER LE SECOND MEMBRE GLOBAL BG DU PROBLEME
C ----TETRAEDRE TAYLOR-HOOD EN VITESSE et P1 EN PRESSION
C     VG Vm+1 = (Rho - dt Mhu Laplacien ) Vm+1
C             =  Rho V(tn)
C              - dt Rho (Vm . Grad) Vm
C              + dt Force(tn+1)
C              - dt CoGrPr Grad P(tn)
C     C. L.  Vm+1 = V(tn+1) SUR LA FRONTIERE

C     => CALCUL du SECOND MEMBRE DU SYSTEME LINEAIRE:
C        Rho Integrale  tP2 P2 dX V(tn,Se)
C   - dt Rho Integrale  tP2 P2 Vem(Se) . Grad P2 Vem(Se) dX
C   + dt     Integrale  tP2 P2 Force(tn+1,Se) dX
C   - dt CoGrPr Integrale  tP2 Grad P1 Pm(Se) )  dX

C ENTREES:
C --------
C DT     : PAS CONSTANT DU TEMPS
C Rho    : DENSITE DE MASSE  Integrale Rho P2 P2 dX
C CoGrPr : COEFFICIENT DU GRADIENT DE PRESSION DANS LES EDP
C NDIM   : DIMENSION DE L'ESPACE DES COORDONNEES 2 ou 3
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBNOVI : NOMBRE DE NOEUDS VITESSE SOMMETS et MILIEUX DES ARETES DES EF
C XYZNOE : XYZNOE(3,NBNOVI)  3 COORDONNEES DES NOEUDS DU MAILLAGE
C NUNOSO : NUNOSO(I) = NUMERO DU SOMMET 1 A NBSOM DU NOEUD GLOBAL I
C NUTYEL : NUMERO DU TYPE D'EF ( 15 TRIANGLE, 20 TETRAEDRE TAYLOR-HOOD )
C NBNOEF : NOMBRE DE NOEUDS D'UN EF DE CE TYPE
C          ( 6 POUR LE TRIANGLE, 10 POUR LE TETRAEDRE)
C NBELEM : NOMBRE D'EF DU MAILLAGE
C NUNOEF : NUNOEF(NBELEM,NBNOEF) NO DES NOEUDS DES NBELEM EF

C NBVVEF : NOMBRE DE VOLUME  POUR 1 EF DE CE TYPE
C NBSFEF : NOMBRE DE FACES   POUR 1 EF DE CE TYPE
C NBLAEF : NOMBRE D'ARETES   POUR 1 EF DE CE TYPE
C NUVVEF : TABLEAU DU NUMERO DE VOLUME DE CHAQUE ELEMENT FINI
C NUSFEF : TABLEAU DU NUMERO DE SURFACE DES FACES   DE CHAQUE EF
C NULAEF : TABLEAU DU NUMERO DE LIGNE   DES ARETES  DE CHAQUE EF
C
C NUMILI : NUMERO MINIMAL DES OBJETS LIGNES
C NUMALI : NUMERO MAXIMAL DES OBJETS LIGNES
C LTDELI : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DU FLUIDE
C          DES OBJETS LIGNES
C
C NUMISU : NUMERO MINIMAL DES OBJETS SURFACES
C NUMASU : NUMERO MAXIMAL DES OBJETS SURFACES
C LTDESU : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DU FLUIDE
C          DES OBJETS SURFACES
C
C NUMIVO : NUMERO MINIMAL DES OBJETS VOLUMES
C NUMAVO : NUMERO MAXIMAL DES OBJETS VOLUMES
C LTDEVO : TABLEAU DES ADRESSES DU TABLEAU DES DONNEES DU FLUIDE
C          DES OBJETS VOLUMES
C
C VITtn  : DL DES 3 COMPOSANTES DE LA VITESSE A L'INSTANT tn
C VITm   : DL DES 3 COMPOSANTES DE LA VITESSE A L'INSTANT tn+1,m
C PRESSm : DL DE LA PRESSION(tn+1,m) AUX SOMMETS DE LA TETRAEDRISATION
C IEFOCL :=0 SI PAS DE FORCES DONNEES SUR LES FRONTIERES
C         >0 NOMBRE DE TMS FORCE "AUX LIMITES" DES PLS DE L'OBJET RETROUVES
C IEFOIN :=0 SI PAS DE FORCES INTERNES AU VOLUME DONNEES
C         >0 NOMBRE DE TMS FORCE INTERNE     DES SV DE L'OBJET RETROUVES
C
C SORTIES:
C --------
C BG     : VECTEUR GLOBAL(NBNOVI,NDIM) SECOND MEMBRE ASSEMBLE
C NBCHTL : NOMBRE DE REMONTEES DE LA CARACTERISTIQUE TROP LONGUES
C IERR   : 0 PAS DE PROBLEME, >0 PROBLEME RENCONTRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray Novembre 2012
C23456---------------------------------------------------------------012
      IMPLICIT NONE
      include"./incl/donflu.inc"
      REAL              DT
      DOUBLE PRECISION  Rho, CoGrPr, BG(NBNOVI*NDIM)
      DOUBLE PRECISION  VITtn(NBNOVI,3), VITm(NBNOVI,3), PRESSm(NBSOM)
      INTEGER           NUNOEF(NBELEM,NBNOEF), NUNOSO(NBNOVI)
      INTEGER           NUMILI,NUMALI,LTDELI(1:MXDOFL,NUMILI:NUMALI)
      INTEGER           NUMISU,NUMASU,LTDESU(1:MXDOFL,NUMISU:NUMASU)
      INTEGER           NUMIVO,NUMAVO,LTDEVO(1:MXDOFL,NUMIVO:NUMAVO)
      INTEGER           NUVVEF(NBVVEF,NBELEM), NUSFEF(NBSFEF,NBELEM),
     %                  NULAEF(NBLAEF,NBELEM)
      REAL              XYZNOE(3,NBNOVI)
      INTEGER           NDIM,   NBSOM,  NBNOVI, IEFOIN, IEFOCL,
     %                  NBVVEF, NBSFEF, NBLAEF,
     %                  NUTYEL, NBELEM, NBNOEF,
     %                  NUELEM, K, M, N, MEK, NONOEF(10)
      DOUBLE PRECISION  DELTAT, BE(30), DtRho
      REAL              XYZEF(10,3)

C     LE PAS DE TEMPS EN DOUBLE PRECISION
      DELTAT = DT
      DtRho  = DT * Rho

C     MISE A ZERO DE BG
      CALL AZEROD( NDIM*NBNOVI, BG )

C     BOUCLE SUR LES EF de TAYLOR-HOOD
      DO NUELEM = 1, NBELEM

C        NO GLOBAL et XYZ DES NBNOEF NOEUDS DE L'ELEMENT FINI NUELEM
         DO M = 1, NBNOEF
            N = NUNOEF(NUELEM,M)
            NONOEF(M) = N
            XYZEF(M,1) = XYZNOE(1,N)
            XYZEF(M,2) = XYZNOE(2,N)
            XYZEF(M,3) = XYZNOE(3,N)
         ENDDO
C
         IF( NUTYEL .EQ. 15 ) THEN
C
C           TRIANGLE TAYLOR-HOOD
            CALL F2BVETH( Rho,    DELTAT, DtRho,  CoGrPr,
     %                    XYZEF,  NONOEF, NBNOVI, NBSOM, NUNOSO,
     %                 NULAEF(1,NUELEM), NUMILI, NUMALI, LTDELI, IEFOCL,
     %                 NUSFEF(1,NUELEM), NUMISU, NUMASU, LTDESU, IEFOIN,
     %                    VITtn,  VITm,   PRESSm,
     %                    BE )
         ELSE
C
C           TETRAEDRE TAYLOR-HOOD
            CALL F3BVETH( Rho,    DELTAT, DtRho,  CoGrPr,
     %                    XYZEF,  NONOEF, NBNOVI, NBSOM, NUNOSO,
     %                 NUSFEF(1,NUELEM), NUMISU, NUMASU, LTDESU, IEFOCL,
     %                 NUVVEF(1,NUELEM), NUMIVO, NUMAVO, LTDEVO, IEFOIN,
     %                    VITtn,  VITm,   PRESSm,
     %                    BE )
         ENDIF
C
         IF( NUELEM .EQ. 5 ) THEN
            call affvect( 'Sortie F23BVETH', 30, BE )
            call afl1ve(  'Sortie F23BVETH', 30, BE )
         ENDIF
C
C        ASSEMBLAGE DE BE dans V3P1 = VITX1, VITY1, VITZ1
CCC         CALL ASMEEX1( NDIM, NBNOEF, NONOEF, BE, NBNOVI, BG )

         DO M=1,NBNOEF

!           NUMERO DU M-EME NOEUD DE L'ELEMENT FINI
            N   = NONOEF(M)
            MEK = M

!           ASSEMBLAGE DU COEFFICIENT ELEMENTAIRE DANS LE COEFFICIENT GLOBAL
            DO K=1,NDIM
               BG( N ) = BG( N ) + BE( MEK )
               N   = N   + NBNOVI
               MEK = MEK + NBNOEF
            ENDDO

         ENDDO
C
C        FIN DE LA BOUCLE SUR LES EF DE LA CONSTRUCTION DE BG
      ENDDO

      call affvect( 'BGVITNLTH VECTEUR BG', 20,          BG )
      call afl1ve(  'BGVITNLTH VECTEUR BG', NBNOVI*NDIM, BG )

      RETURN
      END
