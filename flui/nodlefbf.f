      SUBROUTINE NODLEFBF( NDIM, NTDL, NUELEM, NONOEF, NOGLDL )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     TROUVER LE NUMERO GLOBAL DES DL D'UN EF BREZZI-FORTIN A
C -----     PARTIR DU NUMERO DES SOMMETS DU TRIANGLE OU TETRAEDRE
C           LES NDIM DL VITESSE DU BARYCENTRE SONT EN DERNIER
C
C ENTREES :
C ---------
C NDIM    : DIMENSION DE L'ESPACE DU FLUIDE (2 ou 3)
C NTDL    : NOMBRE DE DL PORTES PAR LES SOMMETS (NDIM*NB SOMMETS)
C NUELEM  : NUMERO DE L'ELEMENT TRAITE
C NONOEF  : NUMERO GLOBAL DES "NOEUDS" DE L'EF (AU PLUS 6 NOEUDS)
C
C SORTIE  :
C ---------
C NOGLDL  : TABLEAU DU NUMERO GLOBAL DE CHAQUE DL D'UN EF BREZZI-FORTIN
C           Y COMPRIS LES DL DU BARYCENTRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire J-L.LIONS UPMC Paris Janvier 2009
C23456---------------------------------------------------------------012
      INTEGER  NONOEF(5), NOGLDL(19)
C
C     TRIANGLE ou TETRAEDRE de BREZZI FORTIN
C     NOMBRE DE SOMMETS DE L'EF = NOMBRE DE DL D'UN SOMMET
      NBSTEF = NDIM + 1
C
C     NO DU DL DU BARYCENTRE DANS L'INTERPOLATION BREZZI-FORTIN
      NODLBA = NBSTEF + 1
C
C     LES MULTIPLES POUR VY VZ
      NODLBA2 = NODLBA * 2
      NODLBA3 = NODLBA * 3
C
      DO 10 I=1,NBSTEF
C
C        NUMERO DU DERNIER DDL DU NOEUD NONOEF(I)-1
         NDDL = (NONOEF(I)-1) * NBSTEF
C
C        NO GLOBAL DES NBSTEF DL DU SOMMET I DE L'EF
         NOGLDL(I        ) = NDDL + 1
         NOGLDL(I+NODLBA ) = NDDL + 2
         NOGLDL(I+NODLBA2) = NDDL + 3
         IF( NDIM .EQ. 3 ) THEN
            NOGLDL(I+NODLBA3) = NDDL + 4
         ENDIF
C
 10   CONTINUE
C
C     NUMERO DU DERNIER DDL DE L'EF NUELEM-1 DU MAILLAGE
      NDDL = NTDL + (NUELEM-1) * NDIM
      DO 20 I = 1, NDIM
C        NO DU DL I DU BARYCENTRE
         NOGLDL( NODLBA * I ) = NDDL + I
 20   CONTINUE
C
      RETURN
      END
