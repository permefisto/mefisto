      SUBROUTINE GS2P1BP1( NTDLHB, NUEF, BE, BG )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    STOCKAGE DES DEGRES DE LIBERTE VITESSE 4 et 8 DANS BG
C -----    (VITESSE1 et 2 au BARYCENTRE de l'EF)
C          INTERNES ATTACHES AU TRIANGLE BREZZI-FORTIN
C          COMPRESSION DU SECOND MEMBRE ELEMENTAIRE BE
C
C ENTREES:
C --------
C NTDLHB : NOMBRE DE DEGRES DE LIBERTE SANS LES BARYCENTRES DES EF
C NUEF   : NUMERO DE L'EF BREZZI-FORTIN A TRAITER
C
C MODIFIE:
C --------
C BE     : EN ENTREE SECOND MEMBRE ELEMENTAIRE (11)
C          EN SORTIE SECOND MEMBRE ELEMENTAIRE (9) REDUIT
C BG     : VECTEUR GLOBAL SECOND MEMBRE APRES ASSEMBLAGE DES DL BARYCENTRES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC Saint Pierre du Perray Decembre 2008
C23456---------------------------------------------------------------012
      DOUBLE PRECISION  BE(11), BG(*)
C
C     LE DEGRE DE LIBERTE 4 DE BE ELIMINE ENSUITE PAR GAUSS SUR [AG]
C     EST ASSEMBLE DANS BG
      N = NTDLHB + 2*NUEF
      BG( N - 1 ) = BE(4)
C
C     LE DEGRE DE LIBERTE 8 DE BE ELIMINE ENSUITE PAR GAUSS SUR [AG]
C     EST ASSEMBLE DANS BG
      BG( N ) = BE(8)
C
C     RECONSTRUCTION DANS BE AVEC SEULEMENT 9 DEGRES DE LIBERTE
C     REDUCTION DES DL VITESSE 4 8
      BE(4) = BE( 5)
      BE(5) = BE( 6)
      BE(6) = BE( 7)
C
      BE(7) = BE( 9)
      BE(8) = BE(10)
      BE(9) = BE(11)
C
      RETURN
      END
