      SUBROUTINE ECFIVIPR( KNOMOB, TEMPS,  NOVVIPR,
     %                     NAVSTO, NORESO, NIVOGC,  NBPASDT,
     %                     NBNOEVIT, NBNOEPRE,
     %                     NTDLVP,   VXYZPN,
     %                     KNOMFIC,  IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CREATION et ECRITURE du VECTEUR VXYZPN DANS UN FICHIER
C -----     DE NTDLVP DEGRES DE LIBERTE de NUMERO NOVVIPR AU TEMPS TEMPS
C           dans le REPERTOIRE Unix du PROJET Mefisto

C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET DANS LE LEXIQUE des OBJETS
C TEMPS  : TEMPS  DU VECTEUR VITESSE_XYZ+PRESSION A STOCKER
C NOVVIPR: >0 NO DU VECTEUR VITESSE_XYZ+PRESSION ECRIT dans le REPERTOIRE
C          UNIX du PROJET Mefisto
C NAVSTO :-1 POUR RESOUDRE LE PROBLEME DE STOKES   STATIONNAIRE
C          0 POUR RESOUDRE LE PROBLEME DE STOKES INSTATIONNAIRE
C          1 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES SOUS FORME IMPLICITE
C          2 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PAS FRACTIONNAIRES
C          3 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PISO+CARACTERISTIQUE
C          4 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PISO+(V.D) V
C NORESO : CODE DE RESOLUTION DES SYSTEMES LINEAIRES
C          1 FACTORISATION CROUT avec stockage PROFIL
C          2 GRADIENT CONJUGUE avec CROUT INCOMPLET => NIVOGC
C NIVOGC : SI NORESO=2 alors NIVEAU 0 ou 1 ou 2 du
C          STOCKAGE de la MATRICE de PRECONDITIONNEMENT du GC
C          SINON VALEUR NON EXPLOITEE
C NBPASDT:  NOMBRE DE PAS DE TEMPS CALCULES EN CAS DE LECTURE
C           D'UN FICHIER VITESSE+PRESSION
C NBNOEVIT: NOMBRE DE NOEUDS SUPPORTS DE LA VITESSE
C NBNOEPRE: NOMBRE DE NOEUDS SUPPORTS DE LA PRESSION
C NTDLVP  : NOMBRE TOTAL DE DEGRES DE LIBERTES EN VITESSE_XYZ+PRESSION
C VXYZPN  : NTDLVP DEGRES DE LIBERTE DU  VECTEUR VITESSE_XYZ+PRESSION
C           AUX NOEUDS DU MAILLAGE

C SORTIES:
C --------
C KNOMFIC : NOM du FICHIER SUPPORT du VECTEUR NOVVIPR des VITESSE_XYZ+PRESSION
C IERR    : =0 SI PAS D'ERREUR DETECTEE, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint Pierre du Perray             Aout 2020
C MODIFS : ALAIN PERRONNET  Saint Pierre du Perray            Avril 2021
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      CHARACTER*(*)     KNOMOB
      CHARACTER*(*)     KNOMFIC
      LOGICAL           LEXIST, LOPEN
      DOUBLE PRECISION  VXYZPN(NTDLVP)

C     LE NOM DU FICHIER du VECTEUR VITESSE-PRESSION
      CALL NMFIVIPR( KNOMOB, TEMPS, NOVVIPR,  KNOMFIC, NBK )

ccc      PRINT*,'ecfivipr: TEMPS=',TEMPS,' Creation  du fichier ',
ccc     %        KNOMFIC(1:NBK),' de 4 ENTIERS +',NTDLVP,
ccc     %       ' REELS double precision'

C     SI LE FICHIER KNOMFIC(1:NBK) EXISTE ALORS IL EST DETRUIT PUIS RECONSTRUIT
      INQUIRE( FILE=KNOMFIC(1:NBK), EXIST=LEXIST, OPENED=LOPEN )
      IF( LEXIST ) THEN
C        LE FICHIER KNOMFIC(1:NBK) EXISTE
         IF( .NOT. LOPEN ) THEN
C           OUVERTURE DU FICHIER KNOMFIC(1:NBK)
            CALL TRUNIT( NFUNIT )
            OPEN( FILE=KNOMFIC(1:NBK), UNIT=NFUNIT, STATUS='OLD' )
         ENDIF
C        DESTRUCTION DU FICHIER
         CLOSE( NFUNIT, STATUS='DELETE' )
      ENDIF

C     CREATION DU NOUVEAU FICHIER KNOMFIC(1:NBK)
      CALL TRUNIT( NFUNIT )
      OPEN( UNIT=NFUNIT, FILE=KNOMFIC(1:NBK), STATUS='NEW',
     %      ACCESS='SEQUENTIAL', FORM='UNFORMATTED',
     %      IOSTAT=IOERR, ERR=9998 )

C     LE PREMIER ENREGISTREMENT DU FICHIER KNOMFIC(1:NBK)
C     et DERNIER ENREGISTREMENT DU FICHIER KNOMFIC(1:NBK)
C     LES NTDLVP DL DE LA VITESSE ET DE LA PRESSION AUX NOEUDS DU MAILLAGE
C     SONT ECRITS EN BINAIRE DIRECTEMENT SUR LE FICHIER KNOMFIC(1:NBK)
      NCRESO = 10 * NORESO + NIVOGC
      WRITE(NFUNIT) NAVSTO, NCRESO, NBPASDT, NBNOEVIT, NBNOEPRE, NTDLVP,
     %             (VXYZPN(I),I=1,NTDLVP)

C     FERMETURE DU FICHIER KNOMFIC(1:NBK)
      CLOSE( NFUNIT )

C     AFFICHAGE
      IF( LANGAG .EQ. 0 ) THEN
      PRINT 10011, NAVSTO, NORESO, NIVOGC, NBPASDT,
     %             NBNOEVIT, NBNOEPRE, NTDLVP, KNOMFIC(1:NBK)
10011 FORMAT('ecfivipr: Ecriture de NAVSTO=',I2,
     %' NORESO=',I1,' NivoGC=',I1,' Nb Pas Temps=',I8,
     %' Nb NOEUDS Vitesse=',I9,' Nb Noeuds Pression=',I9,
     %' + NTDLVP=',I10,' double reels sur le FICHIER ',A)

      ELSE
      PRINT 20011, NAVSTO, NORESO, NIVOGC, NBPASDT,
     %             NBNOEVIT, NBNOEPRE, NTDLVP, KNOMFIC(1:NBK)
20011 FORMAT('ecfivipr: WRITING of NAVSTO=',I1,
     %' NORESO=',I1,' NIVOGC=',I1,' Step Time Nb=',I8,
     %' Velocity Nodes Nb=',I9,' Pressure Nodes Nb=',I9,
     %' + NTDLVP=',I10,' double REALS on FILE ',A)

      ENDIF

      IERR = 0
      GOTO 9999

C     TRAITEMENT DES ERREURS
 9998 IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'ecfivipr: Le FICHIER ',KNOMFIC,' UNITE',NFUNIT,
     %       ' NE PEUT PAS ETRE DECLARE  IOSTAT=',IOERR
      ELSE
         PRINT*,'ecfivipr: FILE ',KNOMFIC,' UNIT',NFUNIT,
     %       ' MAY NOT BE DECLARED  IOSTAT=',IOERR
      ENDIF
      IERR = 1

 9999 RETURN
      END
