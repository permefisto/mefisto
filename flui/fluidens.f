                  CLOSE( LPLECT(LHL) )
               ENDDO

C              RETOUR AU LECTEUR INITIAL
               LHLECT    = 1
               LECTEU    = IINFO('LECTEUR INITIAL')
               LPLECT(1) = LECTEU
               INTERA    = IINFO('INTERACTIVITE INITIALE')
               INTERB(1) = INTERA

            ENDIF

         ENDIF

      ENDIF

