      SUBROUTINE fluideNS( KNOMOB, NAVSTO, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES VITESSES et PRESSIONS et TEMPERATURE D'UN FLUIDE
C -----    MODELISES PAR L'EQUATION INSTATIONNAIRE de NAVIER-STOKES
C          METHODE DES EF de TAYLOR-HOOD ou de BREZZI-FORTIN
C          METHODE DES CARACTERISTIQUES RETROGRADES de 0. PIRONNEAU
C          pour le TERME de TRANSPORT DU PROBLEME DE NAVIER-STOKES

C ENTREES:
C --------
C KNOMOB : NOM DE L'OBJET FLUIDE INSTATIONNAIRE A TRAITER
C NAVSTO :(-1POUR RESOUDRE LE PROBLEME DE STOKES   STATIONNAIRE Cf stokesta.f)
C          0 POUR RESOUDRE LE PROBLEME DE STOKES INSTATIONNAIRE Cf stokesins.f
C          1 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES SOUS FORME IMPLICITE
C          2 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PAS FRACTIONNAIRES
C          3 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES + THERMIQUE
Cf           https://en.wikipedia.org/wiki/Boussinesq_approximation_(buoyancy)

Cccc       4 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PISO+CARACTERISTIQUE
Cccc       5 POUR RESOUDRE LE PROBLEME DE NAVIER-STOKES PAR PISO+(V.D) V)

C SORTIES:
C --------
C IERR   : 0 SI PAS D'ERREUR D'EXECUTION, NON NUL SINON
C          1 SI OBJET INCONNU
C         10 SI EF NON TAYLOR-HOOD POUR RESOUDRE NAVIER-STOKES + CHALEUR
C         20 SI PAS ASSEZ DE PLACE MEMOIRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire J-L.LIONS UPMC Paris     Mai 2007
C MODIFS : ALAIN PERRONNET Laboratoire J-L.LIONS UPMC Paris Octobre 2007
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray     Aout 2009
C MODIFS : ALAIN PERRONNET TIMS NTU TAIPEI TAIWAN          Decembre 2009
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray    Avril 2010
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray     Juin 2011
C MODIFS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Janvier 2012
C AJOUTS : ALAIN PERRONNET TEXAS A & M UNIVERSITY at QATAR     Mars 2012
C AJOUTS : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Fevrier 2013
C MODIFS : ALAIN PERRONNET Veulettes & St Pierre du Perray     Aout 2020
C MODIFS : ALAIN PERRONNET Veulettes & St Pierre du Perray  Octobre 2020
C MODIFS : ALAIN PERRONNET             St Pierre du Perray  Fevrier 2021
C MODIFS : ALAIN PERRONNET             St Pierre du Perray  Fevrier 2022
C MODIFS : ALAIN PERRONNET             St Pierre du Perray    Avril 2022
CC23456--------------------------------------------------------------012

C     RUSE POUR CALCULER 2**31-1 PLUS GRAND ENTIER STOCKABLE SUR 32 BITS
C                                        -2 pour la FIN de BOUCLE
      PARAMETER         ( MAXENTIER=2**30-2+2**30 )
 
      PARAMETER (MXTYEL=7, MXVPFILE=2048)
      include"./incl/nmproj.inc"
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/lu.inc"
      include"./incl/pilect.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/donflu.inc"
      include"./incl/donele.inc"
      include"./incl/donthe.inc"
      include"./incl/cnonlin.inc"
      include"./incl/ctemps.inc"
      include"./incl/cthet.inc"
      include"./incl/xyzext.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"

      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(MOTMCN)
      DOUBLE PRECISION  DMCN(MOTMCN/2)
      EQUIVALENCE      (DMCN(1),RMCN(1),MCN(1))

      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)

      CHARACTER*(*)     KNOMOB
      CHARACTER*128     NMVPFILE(MXVPFILE)
      CHARACTER*80      KNOMFIC

      EXTERNAL          ETTAEL
      REAL              T, DT, DTSTOC, TPSINI, TPSFIN

      INTEGER           NUMIOB(4),NUMAOB(4),
     %                  MNDOEL(4),MXDOEL(4),
     %                  MNDTEL(4),MXDTEL(4)
      INTEGER           NONOEF(27)
      INTEGER, allocatable, dimension(:) :: NDDLNO, LPTDVOI, LISTVOI,
     %                                      LPLIGN, LPCOLO,
     %                                      LPLIDL, LPCODL

      DOUBLE PRECISION, allocatable, dimension(:) :: VXYZP0, TEMPER0
      DOUBLE PRECISION  DINFO, DCPU, DFABG, DFACTO, DVITPR,
     %                  VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY,
     %                  TEMMIN, TEMMAX, TEMMOY,
     %                  FLUNEG, FLUPOS, THETA(0:1)
      DOUBLE PRECISION  RELMIN
      DATA              RELMIN/-1D28/

C     INITIALISATION DU TEMPS CALCUL INITIAL
      DCPU = DINFO( 'CPU' )

      PRINT*,'fluideNS: DEBUT'

C     LES ADRESSES MCN SUIVANTES SONT STOCKEES DANS LE cthet.inc
      MNTHDL = 0
      MNNODL = 0

C     INSTANT ACTUEL DU CALCUL
      TEMPS  = 0.0
      MNTIME = 0
      MNTIMES= 0

C     NOMBRE DE VECTEURS VITESSES-PRESSIONS ECRITS SUR UN FICHIER
      NOVVIPR = 0
      NBVPFILE= 0
      NBPAST0= 0
      NBPAST = 0
      MXPAST = 0

C     PAR DEFAUT, CODE DE STOCKAGE DES MATRICES EF
      NORESO = 2
      NIVOGC = 1

C     LE NOMBRE DE MOTS D'UNE VARIABLE REEL2
      MOREE2 = MOTVAR(6)

      NBTYEL = 0
      MNNPEF = 0
      MNX    = 0
      MNTAUZ = 0
      NBDLMX = 0
      MOLPLI = 0
      NTDLHB = 0
      NTDLVP = 0
      NTDLTE = 0
      NTDLTE0= 0
      NBNOTE = 0
      DO I=1,4
         MNDOEL(I) = 0
         MXDOEL(I) = 0
         MNDTEL(I) = 0
         MXDTEL(I) = 0
      ENDDO
      NTVECT = 0
      MNVECT = 0
      MNLPCV = 0
      MOAUX  = 0
      MNTAUX = 0

      NBVCFX = 0
      NBVPFX = 0
      MNNVPFX= 0
      MNVVPFX= 0
      NBTEFX = 0
      MONTEFX= 0
      MNNTEFX= 0
      MNVTEFX= 0

      MNFVSF = 0
      MNVPMIX= 0
      MOLPCO = 0

      DFABG  = 0D0
      DFACTO = 0D0
      DVITPR = 0D0

      IERANDDLNO  = 1
      IERALPTDVOI = 1
      IERALISTVOI = 1
      IERALPLIGN  = 1
      IERALPCOLO  = 1
      IERAVXYZP0  = 1
      IERATEMPER0 = 1

C     ICI SEUL LE SECOND MEMBRE PEUT DEPENDRE DU TEMPS
      TESTNL = 0

C     L'ANCIEN HISTORIQUE EST EFFACE
      CALL RECTEF( NRHIST )
      NBLGRC(NRHIST) = 1
      IF( LANGAG .EQ. 0 ) THEN
         KHIST(1) = 'OBJET: ' // KNOMOB
      ELSE
         KHIST(1) = 'OBJECT: ' // KNOMOB
      ENDIF
      CALL LHISTO
C
      IF( NAVSTO .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10000) KNOMOB
         ELSE
            WRITE(IMPRIM,20000) KNOMOB
         ENDIF
      ELSE IF( NAVSTO .EQ. 1 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10001) KNOMOB
         ELSE
            WRITE(IMPRIM,20001) KNOMOB
         ENDIF
      ELSE IF( NAVSTO .EQ. 2 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10002) KNOMOB
         ELSE
            WRITE(IMPRIM,20002) KNOMOB
         ENDIF
      ELSE IF( NAVSTO .EQ. 3 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10003) KNOMOB
         ELSE
            WRITE(IMPRIM,20003) KNOMOB
         ENDIF
      ELSE IF( NAVSTO .EQ. 4 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10004) KNOMOB
         ELSE
            WRITE(IMPRIM,20004) KNOMOB
         ENDIF
      ELSE IF( NAVSTO .EQ. 5 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10005) KNOMOB
         ELSE
            WRITE(IMPRIM,20005) KNOMOB
         ENDIF
      ENDIF
C
10000 FORMAT(/,1X,80('=')/,
     %' ECOULEMENT INSTATIONNAIRE d''un FLUIDE STOKES de l''OBJET ',A,/,
     %1X,80('='))
20000 FORMAT(/,1X,80('=')/,
     %' UNSTEADY STOKES FLUID FLOW of the OBJECT ',A,/,1X,80('='))
C
10001 FORMAT(/,1X,80('=')/,
     %' ECOULEMENT INSTATIONNAIRE d''un FLUIDE NAVIER-STOKES de l''OBJET
     % ',A,/' INTEGRATION IMPLICITE EN TEMPS'/1X,80('='))
C
20001 FORMAT(/,1X,80('=')/,
     %' UNSTEADY NAVIER-STOKES FLUID FLOW of the OBJECT ',A,/
     %' IMPLICIT TIME INTEGRATION'/1X,80('='))
C
10002 FORMAT(/,1X,80('=')/,
     %' ECOULEMENT INSTATIONNAIRE d''un FLUIDE NAVIER-STOKES de l''OBJET
     % ',A,/' INTEGRATION FRACTIONNAIRE EN TEMPS'/1X,80('='))
20002 FORMAT(/,1X,80('=')/,
     %' UNSTEADY NAVIER-STOKES FLUID FLOW of the OBJECT ',A,/
     %' FRACTIONAL TIME INTEGRATION'/1X,80('='))
C
10003 FORMAT(/,1X,80('=')/,
     %' ECOULEMENT INSTATIONNAIRE d''un FLUIDE NAVIER-STOKES de l''OBJET
     % ',A,/' AVEC CONVECTION NATURELLE: INTEGRATION EN TEMPS'/
     %1X,80('='))
20003 FORMAT(/,1X,80('=')/,
     %' UNSTEADY NAVIER-STOKES FLUID FLOW of the OBJECT ',A,/
     %' with BOUSSINESQ HEAT NATURAL CONVECTION: TIME INTEGRATION'/
     %1X,80('='))
C
10004 FORMAT(/,1X,80('=')/,
     %' ECOULEMENT INSTATIONNAIRE d''un FLUIDE NAVIER-STOKES de l''OBJET
     % ',A,/' INTEGRATION PISO+CARACTERISTIQUES EN TEMPS'/1X,80('='))
20004 FORMAT(/,1X,80('=')/,
     %' UNSTEADY NAVIER-STOKES FLUID FLOW of the OBJECT ',A,/
     %' TIME PISO+CHARACTERISTICS INTEGRATION'/1X,80('='))
C
10005 FORMAT(/,1X,80('=')/,
     %' ECOULEMENT INSTATIONNAIRE d''un FLUIDE NAVIER-STOKES de l''OBJET
     % ',A,/' INTEGRATION PISO+(V.D)V  EN TEMPS'/1X,80('='))
20005 FORMAT(/,1X,80('=')/,
     %' UNSTEADY NAVIER-STOKES FLUID FLOW of the OBJECT ',A,/
     %' TIME PISO+(V.D)V  INTEGRATION'/1X,80('='))

C     RECHERCHE DU NOM DE L'OBJET DANS LE LEXIQUE DES OBJETS
      CALL LXLXOU( NTOBJE, KNOMOB, NTLXOB, MNLXOB )
C     S'IL N'EXISTE PAS RETOUR A LA DEMANDE DU NOM DE L'OBJET
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'fluideNS: OBJET INCONNU ' // KNOMOB
         ELSE
            KERR(1) = 'fluideNS: UNKNOWN OBJECT ' // KNOMOB
         ENDIF
         CALL LEREUR
         CALL LXIM( NTOBJE )
         IERR = 1
         GOTO 9999
      ENDIF

C     RECHERCHE DU TABLEAU DEFINITION DE L'OBJET
      CALL LXTSOU( NTLXOB, 'DEFINITION', NTDFOB, MNDFOB )
      IF( NTDFOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) ='ERREUR: DEFINITION INCONNUE de l''OBJET ' //KNOMOB
         ELSE
            KERR(1) ='ERROR: UNKNOWN DEFINITION for the OBJECT '//KNOMOB
         ENDIF
         CALL LEREUR
         IERR = 2
         GOTO 9999
      ENDIF


C     LECTURE des CHOIX et des DONNEES du FLUIDE de l'UTILISATEUR
C     ===========================================================
C     ENTREE du CHOIX de la METHODE de RESOLUTION des SYSTEMES Ax=b
C     ( => le MODE de STOCKAGE de la MATRICE GLOBALE )
      CALL CHMERESO( NAVSTO, NORESO, NIVOGC, IERR )
C     NORESO : CODE DE RESOLUTION DES SYSTEMES LINEAIRES
C              1 FACTORISATION CROUT avec stockage PROFIL
C              2 GRADIENT CONJUGUE SIMPLE ou avec CROUT INCOMPLET
C     NIVOGC : SI NORESO=2 alors NIVEAU de 
C              =0, 1, 2 STOCKAGE MATRICE de PRECONDITIONNEMENT du GC

C     DONNEE des TEMPS INITIAL et FINAL pour la RESOLUTION du PROBLEME
C     INSTATIONNAIRE D'ORDRE 1 en TEMPS par la THETA (IMPOSE) METHODE
      CALL DFT0T1DT( TPSINI, TPSFIN, DT, NBVPSTK, IERR )
C     MISE A JOUR de ctemps.inc
      TEMPS    = TPSINI
      TEMPSINI = TPSINI
      TEMPSFIN = TPSFIN
      PasTemps = DT

C     RECUPERATION EVENTUELLE des TEMPS deja CALCULES a partir du nom
C     du FICHIER des VECTEURS VITESSE+PRESSION+TEMPERATURE SAUVEGARDES
C     SUR DISQUE dans le REPERTOIRE du PROJET
      CALL LINMFIVPT( KNOMOB,  MXVPFILE,
     %                MNTIMES, NMVPFILE, NBVPFILE, NCAST0, NCAST1 )

C     RECHERCHE DU TEMPS INITIAL TPSINI COMME PLUS PROCHE TEMPS
C     PARMI LES TEMPS DES VECTEURS VITESSE+PRESSION STOCKES DANS DES FICHIERS
C     NOVVIPR NO DU VECTEUR STOCKE AU TEMPS LE PLUS PROCHE DE TPSINI
      IF( NBVPFILE .LE. 0 ) THEN

C        PAS DE FICHIER VITESSE+PRESSION(+TEMPERATURE)
C        *********************************************
         NOVVIPR = 0
         TEMPS   = TPSINI

      ELSE

C        IL EXISTE DES FICHIERS VITESSE+PRESSION(+TEMPERATURE)
C        *****************************************************
C        RECHERCHE DU TEMPS FICHIER LE PLUS PROCHE DE TPSINI TEMPS INITIAL
         TPROCH = RINFO( 'GRAND' )
         DO I=1,NBVPFILE
            TTT = ABS( RMCN(MNTIMES-1+I) - TPSINI )
            IF( TTT .LT. TPROCH ) THEN
               NOVVIPR = I
               TPROCH  = TTT
            ENDIF
         ENDDO

         IF( NOVVIPR .EQ. 1 ) THEN
C           *************************************************
C           TEMPSINI EST LE TEMPS INITIAL DU CALCUL PRECEDENT
C           REDEPART A ZERO SANS UTILISER LE FICHIER 1
C           ET TOUS LES FICHIERS SONT DETRUITS
C           *************************************************
            NOVVIPR = 0
            TEMPS   = TPSINI
         ELSE
C           LE TEMPS DU TRAITEMENT EST CELUI DU PLUS PROCHE TEMPS DE TPSINI
            TEMPS   = RMCN( MNTIMES-1 + NOVVIPR )
            TPSINI  = TEMPS
            TEMPSINI= TPSINI
         ENDIF

C        DESTRUCTION DES FICHIERS DE TEMPS SUPERIEUR A TPSINI
         CALL TRUNIT( NFUNIT )
         DO 4 NVP = NOVVIPR+1, NBVPFILE
            OPEN( UNIT=NFUNIT, FILE=NMVPFILE(NVP), STATUS='OLD',
     %      ACCESS='SEQUENTIAL', FORM='UNFORMATTED',
     %      IOSTAT=IOERR, ERR=4 )
            CLOSE( NFUNIT, STATUS='DELETE' )
 4       ENDDO
C        IL RESTE LES NOVVIPR FICHIERS AVANT TPSINI et TPSINI
         NBVPFILE = NOVVIPR

      ENDIF


C     RECUPERATION DU MAILLAGE DE L'OBJET FLUIDE
C     RECHERCHE DES TABLEAUX XYZSOMMET XYZNOEUD XYZPOINT NPEF"xPxd
C     et DES TABLEAUX de la PHYSIQUE DE L'OBJET FLUIDE
C     ============================================================
C     1 SEUL JEU DE CALCUL A CHAQUE TEMPS
      NBJEUX = 1
      CALL MIMAOB( NBJEUX, NTLXOB, MXDOFL, NTTOPO, MNTOPO,
     %             NTXYZP, MNXYZP, NTXYZN, MNXYZN,
     %             MXTYEL, NBTYEL, MTNPEF, MNNPEF,
     %             NUMIOB, NUMAOB,
     %             NDPGST, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %             MXDOEL, MNDOEL, IERR )
      IF( IERR .NE. 0 ) GOTO 9990
C     ICI NUMIOB CONTIENT LES 4 NUMEROS MINIMA DES PLSV
C         NUMAOB          LES 4 NUMEROS MAXIMA DES PLSV
C     MNDOEL LES 4 ADRESSES MCN DES TABLEAUX DES ADRESSES DES
C     TABLEAUX DECRIVANT LA PHYSIQUE DE L'OBJET COMPLET

C     UN SEUL TYPE D'EF PERMIS  BREZZI-FORTIN P1+BULLE ou TAYLOR YOUNG P2
C       (13:TRIA 2P1D ou 19:TETR 3P1D pour EF de BREZZI-FORTIN
C     ou 15:TRIA 2P2C ou 20:TETR 3P2C pour EF de TAYLOR-HOOD)
      IF( NBTYEL .LE. 0 .OR. NBTYEL .GT. 1 ) THEN
         NBLGRC(NRERR) = 2
         WRITE(KERR(MXLGER)(1:4),'(I4)') NBTYEL
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'fluideNS:' // KERR(MXLGER)(1:4)
     %             //' TYPES d''ELEMENTS FINIS'
            KERR(2) = '1 SEUL TYPE D''ELEMENT FINI PERMIS'
         ELSE
            KERR(1) = 'fluideNS:' // KERR(MXLGER)(1:4)
     %             //' TYPES of FINITE ELEMENTS'
            KERR(2) = 'ONLY ONE TYPE OF FINITE ELEMENT ALLOWED'
         ENDIF
         CALL LEREUR
         IERR = 3
         GOTO 9990
      ENDIF

C     NOMBRE DE COORDONNEES 3 ou 6
      NBCOOR = MCN( MNXYZP + WBCOOP )

C     LE NOMBRE DE POINTS DU MAILLAGE DE L'OBJET
      NBPOI  = MCN( MNXYZP + WNBPOI )
      NBNOE0 = 0

C     NDIM LA DIMENSION 2 OU 3 DE L'ESPACE DES COORDONNEES
      CALL DIMCOO( NBPOI, MCN(MNXYZP+WYZPOI), NDIM )

C     VISEE DE L'ENSEMBLE DU FLUIDE
      INIEXT = 0
      CALL MAJEXT( MNXYZP )

C     MNELE : ADRESSE DU TABLEAU NPEF"TYPE EF
      MNELE = MCN( MNNPEF )

C     NOMBRE D'ELEMENTS FINIS DE CE TYPE
      NBELEM = MCN( MNELE + WBELEM )

C     LE NUMERO DU TYPE DE L'ELEMENT FINI
      NUTYEL = MCN( MNELE + WUTYEL )

      IF( NAVSTO .EQ. 3 .AND. NUTYEL .NE. 15 .AND. NUTYEL .NE. 20 ) THEN
C        NAVIER-STOKES + CHALEUR SEULEMENT PROGRAMME AVEC L'EF TAYLOR-YOUNG
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluidens: NAVIER-STOKES + CHALEUR SEULEMENT PROGRAMM
     %E AVEC EF TAYLOR-YOUNG'
         ELSE
            PRINT*,'fluidens: NAVIER-STOKES + HEAT ONLY PROGRAMED WITH T
     %AYLOR-YOUNG FINITE ELEMENT'
         ENDIF
C        SORTIE
         IERR = 10
         GOTO 9999
      ENDIF

C     EXTENSION POUR MAILLAGE BREZZI-FORTIN DES XYZ DES BARYCENTRES DES EF
      IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C        TRIANGLE ou TETRAEDRE BREZZI-FORTIN. SI CE N'EST PAS FAIT,
C        CONSTRUCTION DU TMS XYZNOEUD des XYZ des SOMMETS+BARYCENTRES DES EF
         CALL XYZNOEBF( NTLXOB, MNNPEF, NTXYZN, MNXYZN )
      ENDIF

C     LE TYPE D'EF DE FLUIDE
      IF( NUTYEL .EQ. 13 ) THEN

C        TRIANGLE 2P1D  =>  EF BREZZI-FORTIN 2D
C        --------------------------------------
C        NOMBRE DE SOMMETS DU MAILLAGE ET NOMBRE DE NOEUDS VITESSE
         NBSOM = NBPOI
C        NOMBRE DE NOEUDS DU MAILLAGE HORS BARYCENTRES
         NBNOHB = NBSOM
C        NOMBRE DE NOEUDS SUPPORT DE LA VITESSE => BARYCENTRES EN PLUS
         NBNOVI = NBNOHB + NBELEM
C        NOMBRE TOTAL DE DEGRES DE LIBERTE EF TYPE BREZZI-FORTIN
C        CHAQUE SOMMET SUPPORTE V1 V2 P SANS LES VITESSES AUX BARYCENTRES
C        DES TRIANGLES ET SUPPRIMEES PAR GAUSS
         NTDLHB = 3 * NBNOHB
C        ET AVEC LES VITESSES AU BARYCENTRE DU TRIANGLE
         NTDLVP = NTDLHB + 2 * NBELEM
C        LES DL SONT NUMEROTES DE 1 A NTDLHB=NDIM*NBNOHB
C        PUIS LES NDIM DL DE CHAQUE BARYCENTRE DE NTDLHB+1 A NTDLVP
C        NOMBRE DE DEGRES DE LIBERTE ELEMENTAIRES DE L'EF (P1*(V+P)+BL*V)
         NBDLMX = 11
C        NOMBRE DE COEFFICIENTS STOCKES POUR ELIMINER LE DL 1 VITESSE
C        AU BARYCENTRE
         IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN
            NBDLGA = 4
         ELSE
            NBDLGA = NBDLMX
         ENDIF

      ELSE IF( NUTYEL .EQ. 19 ) THEN

C        TETRAEDRE 3P1D =>  EF BREZZI-FORTIN 3D
C        --------------------------------------
C        NOMBRE DE SOMMETS DU MAILLAGE ET NOMBRE DE NOEUDS VITESSE
         NBSOM = NBPOI
C        NOMBRE DE NOEUDS DU MAILLAGE HORS BARYCENTRES
         NBNOHB = NBSOM
C        NOMBRE DE NOEUDS SUPPORT DE LA VITESSE => BARYCENTRES EN PLUS
         NBNOVI = NBNOHB + NBELEM
C        CHAQUE NOEUD=SOMMET SUPPORTE V1 V2 V3 P
C        SANS LES VITESSES AUX BARYCENTRES DES TETRAEDRES SUPPRIMEES PAR GAUSS
         NTDLHB = 4 * NBNOHB
C        ET AVEC LES VITESSES AUX BARYCENTRES DES TETRAEDRES
         NTDLVP = NTDLHB + 3 * NBELEM
C        LES DL SONT NUMEROTES DE 1 A NTDLHB=NDIM*NBNOHB
C        PUIS LES NDIM DL DE CHAQUE BARYCENTRE DE NTDLHB+1 A NTDLVP
C        NOMBRE DE DEGRES DE LIBERTE ELEMENTAIRES DE L'EF (P1*(V+P)+BL*V)
         NBDLMX = 19
C        NOMBRE DE COEFFICIENTS STOCKES POUR ELIMINER PAR GAUSS
C        LE DL 1 VITESSE AU BARYCENTRE
         IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN
            NBDLGA = 5
         ELSE
            NBDLGA = NBDLMX
         ENDIF

      ELSE IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN

C        TRIANGLE  TAYLOR-HOOD avec TRIA 2P2C ou
C        TETRAEDRE TAYLOR-HOOD avec TETR 3P2C
C        ---------------------------------------
C        NOMBRE DE NOEUDS DU MAILLAGE P2 = SOMMETS+MILIEUX
         NBNOHB = MCN( MNXYZN + WNBNOE )
C        NOMBRE DE NOEUDS SUPPORT DE LA VITESSE = SOMMETS+MILIEUX
         NBNOVI = NBNOHB

C        DETERMINATION DU NOMBRE NBSOM DE SOMMETS DU MAILLAGE P2
         CALL TNMCDC( 'ENTIER', NBNOHB, MNTAUZ )
         IF( MNTAUZ .LE. 0 ) GOTO 9990
         CALL AZEROI( NBNOHB, MCN(MNTAUZ) )
         DO NUELEM=1,NBELEM
            CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOEF )
C           BOUCLE SUR LES SEULS SOMMETS
            DO I=1,NDIM+1
               MCN(MNTAUZ+NONOEF(I)-1) = 1
            ENDDO
         ENDDO
         NBSOM = 0
         DO I=1,NBNOHB
            IF( MCN(MNTAUZ+I-1) .GT. 0 ) THEN
               NBSOM = NBSOM + 1
            ENDIF
         ENDDO
         CALL TNMCDS( 'ENTIER', NBNOHB, MNTAUZ )

C        LE NOMBRE TOTAL DE DEGRES DE LIBERTE EF TYPE TAYLOR-HOOD
C        CHAQUE SOMMET SUPPORTE V123 P et le MILIEU DES ARETES SUPPORTE V123
         NTDLHB = NBNOHB * NDIM + NBSOM
         NTDLVP = NTDLHB

C        RECUPERATION DES TABLEAUX POBA NECESSAIRES A LA
C        CONSTRUCTION DES TABLEAUX ELEMENTAIRES DU TETRAEDRE P2 TAYLOR-HOOD
         IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN
            CALL TAPOBA( NBTYEL, MNNPEF, ETTAEL,
     %                   MNTPOB, NBDLMX, MOAUX, NBTTEF, NOAXIS, I, IERR)
            IF( IERR .NE. 0 ) GOTO 9990
C           NOMBRE DE DEGRES DE LIBERTE ELEMENTAIRES DE L'EF (EN 3D)
            NBDLMX = 10 * 3 + 4
         ELSE
C           NOMBRE DE DEGRES DE LIBERTE ELEMENTAIRES DE L'EF (EN 2D)
            NBDLMX = 6 * 2 + 3
         ENDIF
C        NOMBRE DE COEFFICIENTS STOCKES POUR ELIMINER LE DL 1 VITESSE
C        AU BARYCENTRE DES EF BREZZI-FORTIN
         NBDLGA = 0

      ELSE

C        EF NI BREZZI-FORTIN, NI TAYLOR-HOOD => ERREUR
         WRITE(KERR(3)(1:4),'(I4)') NUTYEL
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'EF ni BREZZI-FORTIN ni TAYLOR-HOOD'
            KERR(2) = 'NUMERO du TYPE d''EF =' // KERR(3)(1:4)
         ELSE
            KERR(1) = 'FE neither BREZZI-FORTIN nor TAYLOR-HOOD'
            KERR(2) = 'FE TYPE NUMBER =' // KERR(3)(1:4)
         ENDIF
         CALL LEREUR
         IERR = 1
         GOTO 9990

      ENDIF

C     NOMBRE DE DL DU TABLEAU DES TEMPERATURES PRESENT SEULEMENT EN
C     CAS DE NAVIER-STOKES + THERMIQUE dans l'APPROXIMATION de BOUSSINESQ
      IF( NAVSTO .EQ. 3 ) THEN
C        TAYLOR-HOOD SEULEMENT PROGRAMME AVEC TEMPERATURE P2
         NBNOTE = NBNOVI
         NTDLTE = NBNOVI
C        POUR LA DECLARATION DU TABLEAU TEMPERATURE ICI UTILISE
         NTDLTE0 = NBNOVI
      ELSE
         NBNOTE = 0
         NTDLTE = 0
C        POUR LA DECLARATION DU TABLEAU NON UTILISE
         NTDLTE0 = 1
      ENDIF

      IF( LANGAG .EQ. 0 ) THEN
        WRITE(IMPRIM,10020) NDIM,NUTYEL,NBSOM,NBPOI,NBNOHB,NBELEM,
     %                      NBSOM,NBNOVI*NDIM,NTDLVP,NTDLTE
      ELSE
        WRITE(IMPRIM,20020) NDIM,NUTYEL,NBSOM,NBPOI,NBNOHB,NBELEM,
     %                      NBSOM,NBNOVI*NDIM,NTDLVP,NTDLTE
      ENDIF
10020 FORMAT(/' ESPACE du PROBLEME R',I1,/,
     %        ' TYPE de L''EF' ,T25,I9,/,
     %        ' Nb de SOMMETS ',T25,I9,/,
     %        ' Nb de POINTS ', T25,I9,/,
     %        ' Nb de NOEUDS ', T25,I9,/,
     %        ' Nb d''EF ',     T25,I9,/,
     %        ' Nb DL PRESSION',T25,I9,/,
     %        ' Nb DL VITESSE', T25,I9,/,
     %        ' Nb DL VITESSE+PRESSION',T25,I9,/,
     %        ' Nb DL TEMPERATURE',T25,I9,/)

20020 FORMAT(/' SPACE DIMENSION of PB ',T30,I9,/
     %        ' TYPE of FE',     T30,I9,/,
     %        ' Nb of VERTICES ',T30,I9,/,
     %        ' Nb of POINTS ',  T30,I9,/,
     %        ' Nb of NODES ',   T30,I9,/,
     %        ' Nb of FE ',      T30,I9,/,
     %        ' Nb of DoF PRESSURE',T30,I9,/,
     %        ' Nb of DoF VELOCITY',T30,I9,/,
     %        ' Nb of DoF VELOCITY+PRESSURE',T30,I9,/,
     %        ' Nb of DoF TEMPERATURE',T30,I9,/)

C     CONSTRUCTION DU TABLEAU NDDLNO(0:NBNOVI)
C     POINTEUR SUR LE NO DU DERNIER DL DE CHAQUE NOEUD VITESSE DU FLUIDE
C     ------------------------------------------------------------------
C     MNELE : ADRESSE DU TABLEAU NPEF"TYPE EF
      MNELE = MCN( MNNPEF )
      IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'fluideNS: DEMANDE  ALLOCATION  NDDLNO(0:',NBNOVI,
     %          ') ENTIERS'
         ALLOCATE ( NDDLNO( 0:NBNOVI ), STAT=IERANDDLNO )
         IF( IERANDDLNO .NE. 0 ) THEN
            PRINT*,'fluideNS: ERREUR ALLOCATION  NDDLNO(0:',NBNOVI,
     %             ') ENTIERS'
            IERR = IERANDDLNO
            GOTO 9990
         ENDIF
         PRINT*,'fluideNS: CORRECTE ALLOCATION  NDDLNO(0:',NBNOVI,
     %          ') ENTIERS'
      ELSE
         PRINT*,'fluideNS: ALLOCATION DEMAND   NDDLNO(0:',NBNOVI,
     %          ') INTEGERS'
         ALLOCATE ( NDDLNO( 0:NBNOVI ), STAT=IERANDDLNO )
         IF( IERANDDLNO .NE. 0 ) THEN
            PRINT*,'fluideNS: ALLOCATION ERROR NDDLNO(0:',NBNOVI,
     %             ') INTEGERS'
            IERR = IERANDDLNO
            GOTO 9990
         ENDIF
         PRINT*,'fluideNS: CORRECT ALLOCATION  NDDLNO(0:',NBNOVI,
     %          ') INTEGERS'
      ENDIF

      CALL AZEROI( 1+NBNOVI, NDDLNO )
      CALL PTDLFLUI( NDIM,   NBSOM, NBNOVI, NBELEM, MNELE, NUTYEL, 
     %               NDDLNO, IERR )
      NTDLHB = NDDLNO( NBNOHB )
      IF( IERR .NE. 0 ) THEN
         PRINT*,'Exit ptdlflui: IERR=',IERR,' NBSOM=',NBSOM,
     %          ' NBNOVI=',NBNOVI,' NTDLHB=',NTDLHB,
     %          ' NBELEM=',NBELEM,' NUTYEL=',NUTYEL
         IERR=12
         GOTO 9990
      ENDIF


C     CREATION DES MATRICES et VECTEURS NECESSAIRES a la RESOLUTION
C     =============================================================
C     NOMBRE DE MATRICES et VECTEURS STOCKES des SYSTEMES LINEAIRES
      IF( NAVSTO .EQ. 0 ) THEN

C        PROBLEME DE STOKES IMPLICITE
C        LA MATRICE DES COEFFICIENTS NON NULS EST SYMETRIQUE
         NCODSA = 1
C        2 MATRICES GLOBALES SYMETRIQUES UTILISEES
         NBMAGL = 2
C        NOMBRE DE VECTEURS POUR MENER LES ITERATIONS EN TEMPS
         NBVEGL = 4

      ELSE IF( NAVSTO .EQ. 1 ) THEN

C        PROBLEME DE NAVIER-STOKES IMPLICITE
C        LA MATRICE des DL U et P DE COEFFICIENTS NON NULS EST SYMETRIQUE
         NCODSA = 1
C        2 MATRICES GLOBALES SYMETRIQUES UTILISEES V1 ... Vndim Pr
         NBMAGL = 2
C        NOMBRE DE VECTEURS POUR MENER LES ITERATIONS EN TEMPS
         NBVEGL = 5

      ELSE IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN

C        PROBLEME DE NAVIER-STOKES PAR PAS FRACTIONNAIRES
C        LA MATRICE DE PRESSION de COEFFICIENTS NON NULS EST SYMETRIQUE
         NCODSA = 1
C        2 MATRICES GLOBALES SYMETRIQUES UTILISEES VGi et PG PRESSION
         NBMAGL = 2
C        NOMBRE DE VECTEURS POUR MENER LES ITERATIONS EN TEMPS
         NBVEGL = 6

      ELSE

C        METHODE NON PROGRAMMEE
         IERR = 1
         GOTO 9990

      ENDIF

C     NOMBRE de MOTS du TABLEAU LPLIGN des POINTEURS SUR LE DERNIER COEFFICIENT
C     STOCKE DE CHAQUE LIGNE DE LA MATRICE GLOBALE VG SELON SON MODE DE STOCKAGE
C     --------------------------------------------------------------------------
      IF( NORESO .EQ. 1 ) THEN

C        CALCUL DU PROFIL SKYLINE DE LA MATRICE VG
C        -----------------------------------------
C        CREATION DU TABLEAU POINTEUR SUR DIAGONALE POUR LE FLUIDE
         IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN
C           MATRICE DE LA VITESSE AUX NOEUDS POUR UNE COMPOSANTE SEULEMENT
            MOLPLI = NBNOVI
         ELSE
C           MATRICE DES VITESSES-PRESSION AUX NOEUDS SAUF BARYCENTRES
            MOLPLI = NTDLHB
         ENDIF

      ELSE IF( NORESO .GE. 2 ) THEN

C        METHODE DU GRADIENT CONJUGUE STOCKAGE CONDENSE VG
C        -------------------------------------------------
         IF( NAVSTO .LE. 1 ) THEN
C           ADRESSAGE DU TABLEAU POINTEUR SUR LES LIGNES DES DL
C           NOMBRE TOTAL DE DEGRES DE LIBERTE DES NOEUDS DU MAILLAGE
C           (Pour BREZZI-FORTIN SANS LES DL DES BARYCENTRES)
            MOLPLI = NDDLNO( NBNOHB )
         ELSE
C           METHODE FRACTIONNAIRE IMPLICITE EN TEMPS
            IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C              EF BREZZI-FORTIN
C              NOMBRE DE SOMMETS DE L'OBJET
               MOLPLI = NBSOM
            ELSE
C              EF TAYLOR-HOOD
C              NOMBRE DE NOEUDS DE L'OBJET
               MOLPLI = NBNOVI
            ENDIF
         ENDIF
      ENDIF

C     DECLARATION du TABLEAU LPLIGN des POINTEURS SUR LES COEFFICIENTS
C     DIAGONAUX DE LA MATRICE GLOBALE VG SELON SON MODE DE STOCKAGE
C     ----------------------------------------------------------------
      IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'fluideNS: DEMANDE  ALLOCATION  LPLIGN(0:',MOLPLI,
     %          ') ENTIERS'
         ALLOCATE ( LPLIGN( 0:MOLPLI ), STAT=IERALPLIGN )
         IF( IERALPLIGN .NE. 0 ) THEN
            PRINT*,'fluideNS: ERREUR ALLOCATION  LPLIGN(0:',MOLPLI,
     %             ') ENTIERS'
            IERR = IERALPLIGN
            GOTO 9990
         ENDIF
         PRINT*,'fluideNS: CORRECTE ALLOCATION  LPLIGN(0:',MOLPLI,
     %          ') ENTIERS'
      ELSE
         PRINT*,'fluideNS: ALLOCATION DEMAND   LPLIGN(0:',MOLPLI,
     %          ') INTEGERS'
         ALLOCATE ( LPLIGN( 0:MOLPLI ), STAT=IERALPLIGN )
         IF( IERALPLIGN .NE. 0 ) THEN
            PRINT*,'fluideNS: ALLOCATION ERROR LPLIGN(0:',MOLPLI,
     %             ') INTEGERS'
            IERR = IERALPLIGN
            GOTO 9990
         ENDIF
         PRINT*,'fluideNS: CORRECT ALLOCATION  LPLIGN(0:',MOLPLI,
     %          ') INTEGERS'
      ENDIF
      CALL AZEROI( 1+MOLPLI, LPLIGN )


C     CONSTRUCTION DES POINTEURS LIGNES ET COLONNES DE LA MATRICE GLOBALE
C     -------------------------------------------------------------------
      IF( NORESO .EQ. 1 ) THEN

C        CALCUL DU PROFIL SKYLINE DE LA MATRICE EF
C        -----------------------------------------
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN

C           BREZZI-FORTIN TRIANGLE 2D ou TETRAEDRE 3D
C           .........................................
C           CALCUL DU POINTEUR SUR CHAQUE COEFFICIENT DIAGONAL DE LA
C           MATRICE PROFIL GLOBALE (SANS LES BARYCENTRES DES EF)
C           MNELE   : ADRESSE DU TABLEAU NPEF"TYPE EF
            MNELE   = MCN( MNNPEF )
C           MNUNDEL : ADRESSE DU TABLEAU NUNDEL DES EF DE BREZZI-FORTIN
            MNUNDEL = MNELE + WUNDEL
C           NBSTEF  = NOMBRE DE SOMMETS DE L'EF = NB DE DL DE CHAQUE SOMMET
            NBSTEF  = NDIM + 1
C           LE NOMBRE DE DL EN CHAQUE SOMMET
            IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN
C              1 DL = LA PRESSION ou UNE COMPOSANTE DE LA VITESSE AUX SOMMETS
               NBDLNO = 1
            ELSE
C              NDIM COMPOSANTES DE LA VITESSE ET LA PRESSION AUX SOMMETS
               NBDLNO = NDIM + 1
            ENDIF

C           CONSTRUCTION DU TABLEAU POINTEUR LPLIGN SUR LES COEFFICIENTS
C           DE LA DIAGONALE
            CALL DIAGBF( NBDLNO, NBSOM, NBELEM, NBSTEF, MCN(MNUNDEL),
     %                   NCODSA, LPLIGN, IERR )
            IF( IERR .GT. 0 ) GOTO 9990

C           NBCMVG DONNE LE NOMBRE DE COEFFICIENTS STOCKES DE LA MATRICE
C           GLOBALE POUR LES DL ATTACHES AUX SOMMETS
C           MAIS SANS LES LIGNES DES DL ATTACHES AUX BARYCENTRES
            IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN
C              NOMBRE DE COEFFICIENTS STOCKES DE LA MATRICE DES VITESSES
               NBCMVG = LPLIGN( NBSOM )
            ELSE
C              NOMBRE DE COEFFICIENTS STOCKES DE LA MATRICE
               NBCMVG = LPLIGN( NTDLHB )
            ENDIF

C           NOMBRE DE VARIABLES POUR STOCKER LES LIGNES ELIMINEES PAR GAUSS
            MXMOGA = NDIM * NBELEM * NBDLGA

         ELSE

C           TAYLOR-HOOD TRIANGLE 2D ou TETRAEDRE 3D
C           .......................................
C           CREATION DU TABLEAU AUXILIAIRE POUR CALCULER LE POINTEUR
C           SUR DIAGONALE "CLASSIQUE " A UTILISER POUR LE POINTEUR
C           SUR LA DIAGONALE (POUR L'ELEMENT FINI TAYLOR-HOOD P2 P1)
            IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN

C              METHODE IMPLICITE PAS FRACTIONNAIRE EN TEMPS
C              POINTEUR SUR LA DIAGONALE POUR 1 DL PAR NOEUD AUX NBNOVI NOEUDS
               CALL PRPRMC( MNTOPO, MCN(MNNPEF), MNXYZN, 1, NCODSA,
     %                      LPLIGN, IERR )
               IF( IERR .NE. 0 ) GOTO 9990

C              NBCMVG DONNE LE NOMBRE DE COEFFICIENTS STOCKES DE LA MATRICE
               NBCMVG = LPLIGN( NBNOHB )

            ELSE

C              METHODE IMPLICITE EN TEMPS
C              POINTEUR SUR LA DIAGONALE POUR TOUS LES DL VITESSE et PRESSION
C              RESERVATION D'UN TABLEAU AUXILIAIRE
               CALL TNMCDC( 'ENTIER', NBNOHB+1, MNTAUX )
               IF( MNTAUX .LE. 0 ) GOTO 9990
               CALL AZEROI( NBNOHB+1, MCN(MNTAUX) )

C              CALCUL DU POINTEUR SUR CHAQUE COEFFICIENT DIAGONAL DE LA
C              MATRICE PROFIL GLOBALE AVEC UN SEUL DL PAR NOEUD DES EF
               CALL PRPRMC( MNTOPO, MCN(MNNPEF), MNXYZN, 1, NCODSA,
     %                      MCN(MNTAUX), IERR )
C              NOMBRE DE VARIABLES DE LA MATRICE POUR 1 SEUL DEGRE DE LIBERTE
               NBCMVG = MCN(MNTAUX+NBNOHB)
               IF( IERR .GT. 0 ) GOTO 30

C              A PARTIR DU POINTEUR POUR EF AVEC 1 DL PAR NOEUD
C              CONSTRUIRE LE POINTEUR SUR LA DIAGONALE DES DL NDDLNO
C              DES EF TAYLOR-HOOD
               CALL LPLINODL( NBNOHB, NTDLHB, NDDLNO,NCODSA,MCN(MNTAUX),
     %                        LPLIGN, NBCMVG, IERR )

C              DESTRUCTION DU TABLEAU AUXILIAIRE
 30            CALL TNMCDS( 'ENTIER', NBNOHB+1, MNTAUX )
               IF( IERR .NE. 0 ) GOTO 9990

            ENDIF

C           NOMBRE DE VARIABLES POUR STOCKER LES LIGNES ELIMINEES PAR GAUSS
            MXMOGA = 0

         ENDIF

C        AFFICHAGE DU NOMBRE DE COEFFICIENTS DE LA MATRICE PROFIL
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10030) NBCMVG
         ELSE
            WRITE(IMPRIM,20030) NBCMVG
         ENDIF

10030  FORMAT(' fluideNS: MATRICE PROFIL VG de',I15,
     %        ' REELS DOUBLE PRECISION')
20030  FORMAT(' fluideNS: VG SKYLINE MATRIX STORAGE of',I15,
     %        ' DOUBLE PRECISION REAL')


      ELSE IF( NORESO .GE. 2 ) THEN

C        CONSTRUCTION DES POINTEURS LIGNES ET COLONNES DE LA MATRICE
C        GLOBALE VG CONDENSEE POUR LA METHODE DU GRADIENT CONJUGUE
C        -----------------------------------------------------------
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C           EF BREZZI-FORTIN
C           MATRICE GLOBALE DES DL DE LA PRESSION AUX SOMMETS
C           LES BARYCENTRES NE SONT PAS PRIS EN COMPTE CAR
C           ELIMINES PAR GAUSS EF PAR EF
C           MOMENTANEMENT LE NOMBRE DE NOEUDS => NB SOMMETS
            NBNOE0 = MCN( MNXYZN + WNBNOE )
            MCN( MNXYZN + WNBNOE ) = NBSOM
         ENDIF

C        EF BREZZI-FORTIN ou TAYLOR-YOUNG
C        NOMBRE DE NOEUDS DE L'OBJET
         NBNOE = MCN( MNXYZN + WNBNOE )

C        NOMBRE TOTAL DE DEGRES DE LIBERTE DE L'OBJET POUR 1DL PAR NOEUD
         NTDL = NBNOE

C        ALLOCATION DU TABLEAU LPTDVOI DES POINTEURS SUR LE DERNIER VOISIN
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluideNS: DEMANDE  ALLOCATION LPTDVOI(0:',NBNOE,
     %             ') ENTIERS'
            ALLOCATE ( LPTDVOI( 0:NBNOE ), STAT=IERALPTDVOI )
            IF( IERALPTDVOI .NE. 0 ) THEN
               PRINT*,'fluideNS: ERREUR ALLOCATION LPTDVOI(0:',NBNOE,
     %                ') ENTIERS'
               IERR = IERALPTDVOI
               GOTO 9990
            ENDIF
            PRINT*,'fluideNS: CORRECTE ALLOCATION LPTDVOI(0:',NBNOE,
     %             ') ENTIERS'
         ELSE
            PRINT*,'fluideNS: ALLOCATION DEMAND  LPTDVOI(0:',NBNOE,
     %             ') INTEGERS'
            ALLOCATE ( LPTDVOI( 0:NBNOE ), STAT=IERALPTDVOI )
            IF( IERALPTDVOI .NE. 0 ) THEN
               PRINT*,'fluideNS: ALLOCATION ERROR LPTDVOI(0:',NBNOE,
     %                ') INTEGERS'
               IERR = IERALPTDVOI
               GOTO 9990
            ENDIF
            PRINT*,'fluideNS: CORRECT ALLOCATION LPTDVOI(0:',NBNOE,
     %             ') INTEGERS'
         ENDIF

         LPTDVOI(0) = 0

C        CREER LA LISTE DU NOMBRE D'ELEMENTS FINIS DE CHAQUE NOEUD
         CALL NBEFNOEUD( MNTOPO, MCN(MNNPEF),  NBNOE,
     %                   LPTDVOI(1), MXNOE1EF, IERR )

C        ESTIMATION DU NOMBRE MOYEN DE NOEUDS VOISINS DANS CHAQUE EF
         NBNOVO = MXNOE1EF/2

C        ALLOCATION DU TABLEAU LISTVOI DES NOEUDS VOISINS DE CHAQUE NOEUD
         NBAGMX = 0

 50      NBAGMX = NBAGMX + 1
         IF( NBAGMX .GT. 6 ) THEN
C           TROP D'AUGMENTATIONS DE LA VALEUR DE NBNOVO
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1)='fluideNS: TROP d''AUGMENTATIONS de NBNOVO'
               KERR(2)='POUR DECLARER LE TABLEAU DES VOISINS DES NOEUDS'
               KERR(3)='REDUIRE le MAILLAGE'
            ELSE
               KERR(1)='fluideNS: TOO AUGMENTATIONS of NBNOVO'
               KERR(2)='to DECLARE the ARRAY of NODE NEIGHBORS of NODES'
               KERR(3)='REDUCE the MESH'
            ENDIF
            CALL LEREUR
            IERR = 2
            GOTO 9990
         ELSE
            NBNOVO = NBNOVO + 1
            PRINT*,'fluideNS: NB NOEUDS VOISINS 1 EF NBNOVO=',NBNOVO,
     %             ' MAX NOEUDS 1EF=',MXNOE1EF
         ENDIF

         IF( NBAGMX .GT. 1 ) THEN
C           RECREER LA LISTE DU NOMBRE D'ELEMENTS FINIS DE CHAQUE NOEUD
            CALL NBEFNOEUD( MNTOPO, MCN(MNNPEF),  NBNOE,
     %                      LPTDVOI(1), MXNOE1EF, IERR )
         ENDIF

C        ESTIMATION DU NOMBRE DE VOISINS DE CHAQUE NOEUD A PARTIR DU
C        NOMBRE D'EF DE CHAQUE NOEUD ET LE NOMBRE MOYEN DE VOISINS
         LPTDVOI(0) = 0
         DO K = 1, NBNOE
C           NOMBRE D'EF DU NOEUD K
            NBEFK = LPTDVOI(K)
C           NOMBRE MOYEN DE VOISINS PAR EF
            IF( NBEFK .LE. 6 ) THEN
               NBNV = MXNOE1EF - 1
            ELSE
               NBNV = NBNOVO
            ENDIF
C           ESTIMATION DU NOMBRE DE NOEUDS VOISINS DU NOEUD K
            LPTDVOI(K) = LPTDVOI(K-1) + NBNV * NBEFK
         ENDDO
         MXLISTV = LPTDVOI(NBNOE)

         IF( MXLISTV .LE. 0 ) THEN
C           MXLISTV > 2**31-1
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1)='fluideNS: SATURATION DU MAX DES ENTIERS'
               KERR(2)='POUR DECLARER LE TABLEAU DES VOISINS DES NOEUDS'
               KERR(3)='REDUIRE le MAILLAGE'
            ELSE
               KERR(1)='fluideNS: SATURATION of INTEGER MAXIMUM'
               KERR(2)='to DECLARE the ARRAY of NODE NEIGHBORS of NODES'
               KERR(3)='REDUCE the MESH'
            ENDIF
            CALL LEREUR
            IERR = 2
            GOTO 9990
         ENDIF

C        DECLARATION DU TABLEAU LISTVOI(MXLISTV) DES NOEUDS VOISINS
C        SI TAILLE<MAXENTIER=2**31-1
         IF( MXLISTV .LE. 0 .OR. MXLISTV .GE. MAXENTIER ) THEN
C           MXLISTV EST TROP GRAND POUR ETRE DECLARE
            PRINT*,'fluideNS: declaration de LISTVOI(',MXLISTV,')'
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'ERREUR: TAILLE DE LA LISTE DES VOISINS avec'
               KERR(2) = 'DEPASSEMENT DU MAX DES ENTIERS 2**31-1'
               KERR(3) = 'REDUIRE LA TAILLE DU MAILLAGE'
            ELSE
               KERR(1) = 'ERROR: NEIGHBORS LIST SIZE'
               KERR(2) = 'OVERFLOW of INTEGER MAX 2**31-1'
               KERR(3) = 'REDUCE THE SIZE OF THE MESH'
            ENDIF
            CALL LERESU
            IERR = 2
            GOTO 9990
         ENDIF

         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'DEMANDE  ALLOCATION LISTVOI(',MXLISTV,') ENTIERS'
            ALLOCATE ( LISTVOI( 1:MXLISTV ), STAT=IERALISTVOI )
            IF( IERALISTVOI .NE. 0 ) THEN
               PRINT*,'ERREUR ALLOCATION LISTVOI(',MXLISTV,') ENTIERS'
               IERR = IERALISTVOI
               GOTO 9990
            ENDIF
            PRINT*,'CORRECTE ALLOCATION LISTVOI(',MXLISTV,') ENTIERS'
         ELSE
            PRINT*,'ALLOCATION DEMAND  LISTVOI(',MXLISTV,') INTEGERS'
            ALLOCATE ( LISTVOI( 1:MXLISTV ), STAT=IERALISTVOI )
            IF( IERALISTVOI .NE. 0 ) THEN
               PRINT*,'ALLOCATION ERROR LISTVOI(',MXLISTV,') INTEGERS'
               IERR = IERALISTVOI
               GOTO 9990
            ENDIF
            PRINT*,'CORRECT ALLOCATION LISTVOI(',MXLISTV,') INTEGERS'
         ENDIF

C        GENERER LA LISTE CROISSANTE DES VOISINS DE CHACUN DES NOEUDS
C        ( LE NOEUD N'EST PAS PRIS EN COMPTE DANS SA LIGNE )
C        ------------------------------------------------------------
         CALL LISTNOVO( MNTOPO,  MCN(MNNPEF), NBNOE, MXLISTV,
     %                  LPTDVOI, LISTVOI,    MXVOIS, IERR )

         IF( IERR .EQ. 5 ) THEN
C           REALLOCATION DE LISTVOI DEMANDEE
            IF( IERALISTVOI .EQ. 0 ) then
               DEALLOCATE( LISTVOI )
               IERALISTVOI = 1
               IF( LANGAG .EQ. 0 ) THEN
                  PRINT*,'LISTVOI est DESALLOUE'
               ELSE
                  PRINT*,'LISTVOI is DESALLOCATED'
               ENDIF
            ENDIF
            GOTO 50
         ENDIF

         IF( IERR .NE. 0 ) GOTO 9990

C        ESTIMATION DU TABLEAU LPCOLO (NUMERO DES COLONNES DE
C        CHAQUE LIGNE) DE LA MATRICE GLOBALE DE TYPE CONDENSEE
C        -----------------------------------------------------
C        MOLPCO est une MAJORATION DU NOMBRE DE COLONNES A STOCKER
C        (EGAL AU NOMBRE DE COEFFICIENTS NON NUL DE LA MATRICE)
         IF( NCODSA .GT. 0 ) THEN
C           MATRICE SYMETRIQUE
            MOLPCO = LPTDVOI(NBNOE)/2 + NBNOE + MXVOIS
         ELSE
C           MATRICE NON SYMETRIQUE
            MOLPCO = LPTDVOI(NBNOE) + NBNOE
         ENDIF

         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C           REMISE EN ETAT DU NOMBRE DE NOEUDS DE L'EF BREZZI-FORTIN
            MCN( MNXYZN + WNBNOE ) = NBNOE0
         ENDIF

C        CONSTRUCTION DU TABLEAU LPLIGN et LPCOLO POUR NBDLNO DL PAR NOEUDS
C        ------------------------------------------------------------------
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C           BREZZI-FORTIN TRIANGLE 2D ou TETRAEDRE 3D
C           LE NOMBRE DE DL EN CHAQUE SOMMET
            IF( NAVSTO .GE. 2 .AND. NAVSTO .LE. 5 ) THEN
C              1 DL = LA PRESSION ou UNE COMPOSANTE DE LA VITESSE AUX SOMMETS
               NBDLNO = 1
            ELSE
C              NDIM COMPOSANTES DE LA VITESSE ET LA PRESSION AUX SOMMETS
               NBDLNO = NDIM + 1
               MOLPCO = MOLPCO * NBDLNO**2 - NBSOM * NDIM * (NDIM+1) / 2
            ENDIF
         ELSE
C           TAYLOR-HOOD TRIANGLE 2D ou TETRAEDRE 3D
C           MOMENTANEMENT 1 DL PAR NOEUD et AJUSTE PLUS LOIN SI NAVSTO<2
            NBDLNO = 1
         ENDIF

         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluideNS: DEMANDE  ALLOCATION LPCOLO(',MOLPCO,
     %             ') ENTIERS'
            ALLOCATE ( LPCOLO( 1:MOLPCO ), STAT=IERALPCOLO )
            IF( IERALPCOLO .NE. 0 ) THEN
               PRINT*,'fluideNS: ERREUR ALLOCATION LPCOLO(',MOLPCO,
     %                ') ENTIERS'
               IERR = IERALPCOLO
               GOTO 9990
            ENDIF
            PRINT*,'fluideNS: CORRECTE ALLOCATION LPCOLO(',MOLPCO,
     %             ') ENTIERS'
         ELSE
            PRINT*,'fluideNS: ALLOCATION DEMAND  LPCOLO(',MOLPCO,
     %             ') INTEGERS'
            ALLOCATE ( LPCOLO( 1:MOLPCO ), STAT=IERALPCOLO )
            IF( IERALPCOLO .NE. 0 ) THEN
               PRINT*,'fluideNS: ALLOCATION ERROR LPCOLO(',MOLPCO,
     %                ') INTEGERS'
               IERR = IERALPCOLO
               GOTO 9990
            ENDIF
            PRINT*,'fluideNS: CORRECT ALLOCATION LPCOLO(',MOLPCO,
     %             ') INTEGERS'
         ENDIF

C        CONSTRUCTION DE LPLIGN et LPCOLO de la MATRICE CONDENSEE pour GC
         CALL PRGCLC( NBNOE,  NBDLNO, NCODSA, LPTDVOI, LISTVOI,
     %                LPLIGN, MOLPCO, LPCOLO, MOLPCO1 )

C        LE NOMBRE DE COEFFICIENTS DU TABLEAU LPCOLO ETAIT IL SUFFISANT?
         IF( MOLPCO1 .GT. MOLPCO ) THEN
C           NON: MOLPCO A AUGMENTER
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'fluideNS: VALEUR DE MOLPCO A AUGMENTER'
            ELSE
               KERR(1) = 'fluideNS: AUGMENT the MOLPCO VALUE'
            ENDIF
            CALL LEREUR
            IERR = 1
            GOTO 9990
         ENDIF

         IF( MOLPCO1 .LT. MOLPCO ) THEN
C           LE TABLEAU LPCOLO POUVAIT ETRE REDUIT A SA DECLARATION
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: MOLPCO=',MOLPCO,
     %                ' POUVAIT ETRE REDUIT A MOPLCO1=',MOLPCO1
            ELSE
               PRINT*,'fluideNS: MOLPCO=',MOLPCO,
     %                ' MAY BE REDUCED TO MOPLCO1=',MOLPCO1 
            ENDIF
         ENDIF

C        DESTRUCTION DES TABLEAUX AUXILIAIRES POUR LES VOISINS
         IF( IERALISTVOI .EQ. 0 ) THEN
            DEALLOCATE( LISTVOI )
            IERALISTVOI = 1
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: LISTVOI est DESALLOUE'
            ELSE
               PRINT*,'fluideNS: LISTVOI is DESALLOCATED'
            ENDIF
         ENDIF

         IF( IERALPTDVOI .EQ. 0 ) THEN
            DEALLOCATE( LPTDVOI )
            IERALPTDVOI = 1
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: LPTDVOI est DESALLOUE'
            ELSE
               PRINT*,'fluideNS: LPTDVOI is DESALLOCATED'
            ENDIF
         ENDIF

C        NOMBRE DE COEFFICIENTS NON NULS DE LA MATRICE GLOBALE CONDENSEE
         NBCMVG = MOLPCO1
 
C        TAILLE DE LA MATRICE CONDENSEE SYMETRIQUE
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10060) NBCMVG
         ELSE
            WRITE(IMPRIM,20060) NBCMVG
         ENDIF
10060    FORMAT(' MATRICE CONDENSEE ',I15,' VARIABLES DOUBLE PRECISION')
20060    FORMAT(' CONDENSED MATRIX of',I15,' DOUBLE PRECISION REALS')

C        NOMBRE DE VARIABLES POUR STOCKER LES LIGNES ELIMINEES PAR GAUSS
C        PAS DE LIGNES ELIMINEES PAR GAUSS
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C           NOMBRE DE VARIABLES POUR STOCKER LES LIGNES ELIMINEES PAR GAUSS
            MXMOGA = NDIM * NBELEM * NBDLGA
         ENDIF

         IF( (NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20) .AND.
     %       NAVSTO .LE. 1 ) THEN

C           TAYLOR-HOOD AVEC MATRICE GLOBALE D'ORDRE NDDLNO(NBNOVI)
C           PASSAGE DES LPLIGN+LPCOLO PAR NOEUDS A
C                       LPLIDL+LPCODL PAR DL A PARTIR DE NDDLNO
            MOLIDL = NDDLNO( NBNOVI )
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: DEMANDE  ALLOCATION LPLIDL(0:',MOLIDL,
     %          ') ENTIERS'
               ALLOCATE ( LPLIDL( 0:MOLIDL ), STAT=IERALPLIDL )
               IF( IERALPLIDL .NE. 0 ) THEN
                  PRINT*,'fluideNS: ERREUR ALLOCATION LPLIDL(0:',MOLIDL,
     %             ') ENTIERS'
                  IERR = IERALPLIDL
                  GOTO 9990
               ENDIF
               PRINT*,'fluideNS: CORRECTE ALLOCATION LPLIDL(0:',MOLIDL,
     %          ') ENTIERS'
            ELSE
               PRINT*,'fluideNS: ALLOCATION DEMAND  LPLIDL(0:',MOLIDL,
     %          ') INTEGERS'
               ALLOCATE ( LPLIDL( 0:MOLIDL ), STAT=IERALPLIDL )
               IF( IERALPLIDL .NE. 0 ) THEN
                  PRINT*,'fluideNS: ALLOCATION ERROR LPLIDL(0:',MOLIDL,
     %             ') INTEGERS'
                  IERR = IERALPLIDL
                  GOTO 9990
               ENDIF
               PRINT*,'fluideNS: CORRECT ALLOCATION LPLIDL(0:',MOLIDL,
     %          ') INTEGERS'
            ENDIF
ccc            CALL AZEROI( 1+MOLIDL, LPLIDL )

C           NOMBRE DE NO DE COLONNES PAR DL
            MOCODL = LPLIGN( NBNOVI ) * (NDIM+1)**2
     %             - NBSOM * (NDIM*(NDIM+1))/2
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: DEMANDE  ALLOCATION LPCODL(',MOCODL,
     %          ') ENTIERS'
               ALLOCATE ( LPCODL( MOCODL ), STAT=IERALPCODL )
               IF( IERALPCODL .NE. 0 ) THEN
                  PRINT*,'fluideNS: ERREUR ALLOCATION LPCODL(',MOCODL,
     %             ') ENTIERS'
                  IERR = IERALPCODL
                  GOTO 9990
               ENDIF
               PRINT*,'fluideNS: CORRECTE ALLOCATION LPCODL(',MOCODL,
     %          ') ENTIERS'
            ELSE
               PRINT*,'fluideNS: ALLOCATION DEMAND  LPCODL(',MOCODL,
     %          ') INTEGERS'
               ALLOCATE ( LPCODL( MOCODL ), STAT=IERALPCODL )
               IF( IERALPCODL .NE. 0 ) THEN
                  PRINT*,'fluideNS: ALLOCATION ERROR LPCODL(',MOCODL,
     %             ') INTEGERS'
                  IERR = IERALPCODL
                  GOTO 9990
               ENDIF
               PRINT*,'fluideNS: CORRECT ALLOCATION LPCODL(',MOCODL,
     %          ') INTEGERS'
            ENDIF

C           A PARTIR DU POINTEUR SUR LA DIAGONALE et NO COLONNE PAR NOEUDS
C           GENERER  LE POINTEUR SUR LA DIAGONALE et NO COLONNE PAR DL
            NTDLHB = NDDLNO(NBNOVI)
            CALL LICONODL( NBNOVI, NTDLHB, NDDLNO, NCODSA,
     %                     LPLIGN, LPCOLO,
     %                     LPLIDL, LPCODL, NBCMVG, IERR )

C           LPLIDL(0:NTDLHB) --> LPLIGN(0:NTDLHB)
            CALL TRTATA( LPLIDL, LPLIGN, 1+NTDLHB )
            DEALLOCATE( LPLIDL )
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: LPLIDL est DESALLOUE'
            ELSE
               PRINT*,'fluideNS: LPLIDL is DESALLOCATED'
            ENDIF

C           LPCODL --> LPCOLO
            DEALLOCATE( LPCOLO )
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: LPCOLO est DESALLOUE'
            ELSE
               PRINT*,'fluideNS: LPCOLO is DESALLOCATED'
            ENDIF

C           NOMBRE DE NO DE COLONNES PAR DL DE LA MATRICE GLOBALE
            MOCOLO = LPLIGN( NTDLHB )

            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'fluideNS: DEMANDE  ALLOCATION LPCOLO(',MOCOLO,
     %          ') ENTIERS'
               ALLOCATE ( LPCOLO( MOCOLO ), STAT=IERALPCOLO )
               IF( IERALPCOLO .NE. 0 ) THEN
                  PRINT*,'fluideNS: ERREUR ALLOCATION LPCOLO(',MOCOLO,
     %             ') ENTIERS'
                  IERR = IERALPCOLO
                  GOTO 9990
               ENDIF
               PRINT*,'fluideNS: CORRECTE ALLOCATION LPCOLO(',MOCOLO,
     %          ') ENTIERS'
            ELSE
               PRINT*,'fluideNS: ALLOCATION DEMAND  LPCOLO(',MOCOLO,
     %          ') INTEGERS'
               ALLOCATE ( LPCOLO( MOCOLO ), STAT=IERALPCOLO )
               IF( IERALPCOLO .NE. 0 ) THEN
                  PRINT*,'fluideNS: ALLOCATION ERROR LPCOLO(',MOCOLO,
     %             ') INTEGERS'
                  IERR = IERALPCOLO
                  GOTO 9990
               ENDIF
               PRINT*,'fluideNS: CORRECT ALLOCATION LPCOLO(',MOCOLO,
     %          ') INTEGERS'
            ENDIF

            CALL TRTATA( LPCODL, LPCOLO, MOCOLO )
            DEALLOCATE( LPCODL )

         ENDIF

      ENDIF


C     LE VECTEUR GLOBAL VITESSEPRESSION INITIAL PAR NOEUDS
C     EST ALLOUE ET EST INITIALISE A ZERO
C     ----------------------------------------------------
      IF( LANGAG .EQ. 0 ) THEN
        PRINT*,'fluideNS: DEMANDE  ALLOCATION VXYZP0(',NTDLVP,') DREELS'
         ALLOCATE ( VXYZP0( 1:NTDLVP ), STAT=IERAVXYZP0 )
         IF( IERAVXYZP0 .NE. 0 ) THEN
          PRINT*,'fluideNS: ERREUR ALLOCATION VXYZP0(',NTDLVP,') DREELS'
            IERR = IERAVXYZP0
            GOTO 9990
         ENDIF
        PRINT*,'fluideNS: CORRECTE ALLOCATION VXYZP0(',NTDLVP,') DREELS'
      ELSE
         PRINT*,'fluideNS: ALLOCATION DEMAND  VXYZP0(',NTDLVP,') DREALS'
         ALLOCATE ( VXYZP0( 1:NTDLVP ), STAT=IERAVXYZP0 )
         IF( IERAVXYZP0 .NE. 0 ) THEN
           PRINT*,'fluideNS: ALLOCATION ERROR VXYZP0(',NTDLVP,') DREALS'
            IERR = IERAVXYZP0
            GOTO 9990
         ENDIF
         PRINT*,'fluideNS: CORRECT ALLOCATION VXYZP0(',NTDLVP,') DREALS'
      ENDIF
      CALL AZEROD( NTDLVP, VXYZP0 )


C     LE VECTEUR GLOBAL TEMPERATURE INITIALE PAR NOEUDS
C     EST ALLOUE ET EST INITIALISE A ZERO
C     -------------------------------------------------
      IF( LANGAG .EQ. 0 ) THEN
      PRINT*,'fluideNS: DEMANDE  ALLOCATION TEMPER0(',NTDLTE0,') DREELS'
         ALLOCATE ( TEMPER0( 1:NTDLTE0 ), STAT=IERATEMPER0 )
         IF( IERATEMPER0 .NE. 0 ) THEN
        PRINT*,'fluideNS: ERREUR ALLOCATION TEMPER0(',NTDLTE0,') DREELS'
            IERR = IERATEMPER0
            GOTO 9990
         ENDIF
      PRINT*,'fluideNS: CORRECTE ALLOCATION TEMPER0(',NTDLTE0,') DREELS'
      ELSE
       PRINT*,'fluideNS: ALLOCATION DEMAND  TEMPER0(',NTDLTE0,') DREALS'
         ALLOCATE ( TEMPER0( 1:NTDLTE0 ), STAT=IERATEMPER0 )
         IF( IERATEMPER0 .NE. 0 ) THEN
         PRINT*,'fluideNS: ALLOCATION ERROR TEMPER0(',NTDLTE0,') DREALS'
            IERR = IERATEMPER0
            GOTO 9990
         ENDIF
       PRINT*,'fluideNS: CORRECT ALLOCATION TEMPER0(',NTDLTE0,') DREALS'
      ENDIF
      CALL AZEROD( NTDLTE0, TEMPER0 )

C     RETROUVER LES ADRESSES MCN DES DONNEES "FLUIDE" du FLUIDE
C     DES  V   ou S  "OBJETS INTERNES"    DE L'OBJET selon NDIM=3 ou 2
C     DES  SLP ou LP "OBJETS AUX LIMITES" DE L'OBJET
C     ----------------------------------------------------------------
      CALL FLUDON( NUMIOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL, MNDOEL,
     %             IEMASS, IEVISC, IECOPR, IEFOIN, IEVTIN, IEPRIN,
     %             IEFOCL, IEFOPO, IEBLVI, IEBLPR, IEVIAN, IECOBO,
     %             IERR )
C     IEMASS : NOMBRE DE TMS MASSE            DES SV DE L'OBJET RETROUVES
C     IEVISC : NOMBRE DE TMS VISCOSITE        DES SV DE L'OBJET RETROUVES
C     IECOPR : NOMBRE DE TMS COEFPRESSION     DES SV DE L'OBJET RETROUVES
C     IEFOIN : NOMBRE DE TMS FORCE INTERNE    DES SV DE L'OBJET RETROUVES
C     IEVTIN : NOMBRE DE TMS VITESSE INITIALE DES SV DE L'OBJET RETROUVES
C     IEPRIN : NOMBRE DE TMS PRESSION INITIALEDES SV DE L'OBJET RETROUVES
C     IEFOCL : NOMBRE DE TMS FORCE "AUX LIMITES" DES PLS DE L'OBJET RETROUVES
C     IEFOPO : NOMBRE DE TMS FORCE               DES P   DE L'OBJET RETROUVES
C     IEBLVI : NOMBRE DE TMS BLVITESSE        DES PLS DE L'OBJET TROUVE
C     IEBLPR : NOMBRE DE TMS BLPRESSION       DES PLS DE L'OBJET TROUVE
C     IEVIAN : NOMBRE DE TMS VITESSE ANGULAIRE DES SV DE L'OBJET RETROUVES
C     IECOBO : NOMBRE DE TMS COEFBOUSSINESQ   DES SV DE L'OBJET RETROUVES

      IF( IEVISC .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             KERR(1) = 'Une SURFACE ou VOLUME SANS VISCOSITE'
             KERR(2) = '=> ECOULEMENT DE FLUIDE SANS SOLUTION'
          ELSE
             KERR(1) = '1 SURFACE or VOLUME WITHOUT VISCOSITY'
             KERR(2) = '=> FLUID FLOW PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 10
      ENDIF

C     ON N'AUTORISE PAS UN PB SANS CL SUR LA VITESSE
      IF ( IEBLVI .LE. 0 ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             KERR(1) = 'OBJET SANS CONDITION sur la VITESSE'
             KERR(2) = '=> PROBLEME SANS SOLUTION'
          ELSE
             KERR(1) = 'OBJECT WITHOUT BOUNDARY CONDITION on VELOCITY'
             KERR(2) = '=> PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 2
      ENDIF

C     SIMPLE AVERTISSEMENT POUR UN PB SANS CL SUR LA PRESSION
      IF ( IEBLPR .LE. 0 ) THEN
         WRITE(IMPRIM,*)
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'PAS DE CONDITION AUX LIMITES sur la PRESSION'
ccc             KERR(2) = '=> PRESSION DEFINIE A UNE CONSTANTE PRES'
         ELSE
            KERR(1) = 'FLUID WITHOUT BOUNDARY CONDITION on PRESSURE'
ccc             KERR(2) = '=> PRESSURE DEFINED MODULO A TRANSLATION'
         ENDIF
         CALL LERESU
ccc          IERR = 2
      ENDIF

      IF( IERR .NE. 0 ) GOTO 9990


C     CREATION du VECTEUR VITESSE-PRESSION VXYZP0 et TEMPER0 AU TEMPS TPSINI
C     ======================================================================
C     MODE d'INITIALISATION DU VECTEUR VITESSES et PRESSIONS INITIALES
      NTCALCUL = 1

ccc      2/2022 par MODIF de vxyzpt0.f
ccc      CALL LIMTCL( 'vp0flui', NTCALCUL )
ccc      IF( NTCALCUL .LE. 0 ) THEN
ccc         IERR = 13
ccc         GOTO 9990
ccc      ENDIF

      IF( NAVSTO .EQ. 3 ) THEN
C        RECUPERATION DU MAILLAGE DE L'OBJET FLUIDE THERMIQUE
C        RECHERCHE DES TABLEAUX XYZSOMMET XYZNOEUD XYZPOINT NPEF"xPxd
C        ET DES TABLEAUX DE DONNEES THERMIQUES SUR LES PLSV DE L'OBJET FLUIDE
C        --------------------------------------------------------------------
         CALL MIMAOB( NBJEUX, NTLXOB, MXDOTH, NTTOPO, MNTOPO,
     %                NTXYZP, MNXYZP, NTXYZN, MNXYZN,
     %                MXTYEL, NBTYEL, MTNPEF, MNNPEF,
     %                NUMIOB, NUMAOB,
     %                NDPGST, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %                MXDTEL, MNDTEL, IERR )
         IF( IERR .NE. 0 ) GOTO 9990

C        CALCUL DES VITESSES PRESSIONS TEMPERATURES AUX DIFFERENTS PAS DE TEMPS
C        RETROUVER LES ADRESSES MCN DES DONNEES "TEMPERATURE" du FLUIDE
C        DES  V   ou S  "OBJETS INTERNES"    DE L'OBJET selon NDIM=3 ou 2
C        DES  SLP ou LP "OBJETS AUX LIMITES" DE L'OBJET
C        ----------------------------------------------------------------------
         CALL THEDON( NUMIOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %                NBJEUX, MNDTEL,
     %                IEMAST, IECHMA, IECOND, IEDILA, IEVIFL, IECOET,
     %                IESOIN, IECONT, IEECHA, IESOCL, IESOPO,
     %                IETEIN, IEVIIN, IEVIANT,IECOBO, IERR )
C
         IF( IEMAST .NE. NBOBIN ) THEN
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               IF( NDIM .EQ. 3 ) THEN
                  KERR(1)='Au MOINS UN VOLUME SANS DENSITE de MASSE'
               ELSE IF( NDIM .EQ. 2 ) THEN
                  KERR(1)='Au MOINS UNE SURFACE SANS DENSITE de MASSE'
               ELSE IF( NDIM .EQ. 1 ) THEN
                  KERR(1)='Au MOINS UNE LIGNE SANS DENSITE de MASSE'
               ENDIF
               KERR(2)='=> PROBLEME THERMIQUE SANS SOLUTION'
            ELSE
               IF( NDIM .EQ. 3 ) THEN
                  KERR(1)='At LEAST 1 VOLUME WITHOUT MASS DENSITY'
               ELSE IF( NDIM .EQ. 2 ) THEN
                  KERR(1)='At LEAST 1 SURFACE WITHOUT MASS DENSITY'
               ELSE IF( NDIM .EQ. 1 ) THEN
                  KERR(1)='At LEAST 1 LINE WITHOUT MASS DENSITY'
               ENDIF
               KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
            ENDIF
            CALL LEREUR
            IERR = 10
         ENDIF

         IF( IECHMA .NE. NBOBIN .AND. TESTNL .LT. 5 ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='Au MOINS UN VOLUME SANS CHALEUR MASSIQUE'
             ELSE IF( NDIM .EQ. 2 ) THEN
           KERR(1)='Au MOINS UNE SURFACE SANS CHALEUR MASSIQUE'
             ELSE IF( NDIM .EQ. 1 ) THEN
           KERR(1)='Au MOINS UNE LIGNE SANS CHALEUR MASSIQUE'
             ENDIF
             KERR(2)='=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='At LEAST 1 VOLUME WITHOUT MASSIC HEAT'
             ELSE IF( NDIM .EQ. 2 ) THEN
            KERR(1)='At LEAST 1 SURFACE WITHOUT MASSIC HEAT'
             ELSE IF( NDIM .EQ. 1 ) THEN
            KERR(1)='At LEAST 1 LINE WITHOUT MASSIC HEAT'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 10
         ENDIF
C
         IF( IECOND .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='AU MOINS UN VOLUME SANS CONDUCTIVITE'
             ELSE IF( NDIM .EQ. 2 ) THEN
                KERR(1)='AU MOINS UNE SURFACE SANS CONDUCTIVITE'
             ELSE IF( NDIM .EQ. 1 ) THEN
                KERR(1)='AU MOINS UNE LIGNE SANS CONDUCTIVITE'
             ENDIF
             KERR(2) = '=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='At LEAST 1 VOLUME WITHOUT CONDUCTIVITY'
             ELSE IF( NDIM .EQ. 2 ) THEN
                KERR(1)='At LEAST 1 SURFACE WITHOUT CONDUCTIVITY'
             ELSE IF( NDIM .EQ. 1 ) THEN
                KERR(1)='At LEAST 1 LINE WITHOUT CONDUCTIVITY'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 11
         ENDIF
         IF( IERR .NE. 0 ) GOTO 9999

      ENDIF

      CALL VXYZPT0( NAVSTO, NTCALCUL, KNOMOB, RELMIN, KNOMFIC,
     %              NTLXOB, NDIM,   MNXYZN, MNNPEF, NUMIOB,
     %              NBTYEL, NUTYEL, MNDOEL, MNDTEL, NDPGST,
     %              NBELEM, NBSOM,  NBNOHB, NBNOVI, NBNOTE,
     %              NTDLHB, NTDLVP, NTDLTE,
     %              IEVTIN, IEPRIN, IETEIN,
     %              NBVCFX, NBVPFX,  MNNVPFX, MNVVPFX,
     %              NBTEFX, MONTEFX, MNNTEFX, MNVTEFX,
     %              MNTIMES,TPSINI,  NOVVIPR, NDDLNO, NBPAST0,
     %              VXYZP0, TEMPER0, IERR )
      IF( IERR .NE. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
        PRINT*,'fluideNS: Fin entree des DONNEES du FLUIDE avec ERREUR='
     %          ,IERR
         ELSE
            PRINT*,'fluideNS: Exit FLUID DATA INPUT with ERROR=',IERR
         ENDIF
         GOTO 9990
      ENDIF

      IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'fluideNS: au TEMPS INITIAL=',TPSINI,
     %          ' Nb PAS de TEMPS=',NBPAST0,' deja CALCULES'
      ELSE
         PRINT*,'fluideNS: At INITIAL TIME=',TPSINI,
     %          ' Previous Time Steps Number=',NBPAST0,' COMPUTED'
      ENDIF

C     LE PAS DE TEMPS POUR LES STOCKAGES DES VECTEURS VITESSE PRESSION
      DTSTOC = ( TPSFIN - TPSINI ) / NBVPSTK

C     NOMBRE DE VECTEURS VITESSES+PRESSION a STOKER DANS DES FICHIERS
      IF( NOVVIPR .EQ. 0 ) THEN
C        STOCKAGE DU VECTEUR INITIAL => 1 DE PLUS
         MXVVIPR = 1 + NBVPSTK
C        PREMIER VECTEUR VITESSE+PRESSION STOCKE SUR FICHIER
         NOVVIPR = 1
      ELSE
         MXVVIPR = NOVVIPR + NBVPSTK
      ENDIF

C     AFFICHAGE DU VECTEUR"VITESSEPRESSION NOVVIPR AU TEMPS INITIAL
      TEMPS = RMCN( MNTIMES -1 + NOVVIPR )
      IF( NAVSTO .EQ. 3 ) THEN
         CALL AFVIPRTE( NUTYEL, NDIM,   NBNOVI, MNXYZN, NDDLNO,
     %                  TEMPS,  NTDLVP, MIN(5,NBNOVI),  VXYZP0, TEMPER0,
     %                  VITMIN, NOVMIN, VITMAX, NOVMAX, VITMOY,
     %                  PREMIN, NOPMIN, PREMAX, NOPMAX, PREMOY,
     %                  TEMMIN, NOTMIN, TEMMAX, NOTMAX, TEMMOY )
      ELSE
         CALL AFVIPR( NUTYEL, NDIM,   NBNOVI, MNXYZN, NDDLNO,
     %                TEMPS,  NTDLVP, MIN(5,NBNOVI),  VXYZP0,
     %                VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )
      ENDIF

C     MISE SUR FICHIER DU VECTEUR NOVVIPR VITESSE+PRESSION+TEMPERATURE
C     AU TEMPS INITIAL TPSINI
      IF( NOVVIPR .EQ. 1 ) THEN
         CALL ECFIVIPRTE( KNOMOB,  TEMPS,  NOVVIPR, NAVSTO,  NBPAST0,
     %                    NDIM,    NBNOVI, NBSOM,   NBNOTE,
     %                    NTDLVP,  VXYZP0, NTDLTE,  TEMPER0,
     %                    KNOMFIC, IERR )
      ENDIF

      IF( NUTYEL .EQ. 19 .OR. NUTYEL .EQ. 20 ) THEN
