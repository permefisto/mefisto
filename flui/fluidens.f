      ELSE IF( NAVSTO .EQ. 4 ) THEN
C
C        ******************************************************************
C        RESOLUTION PISO EN TEMPS DU PROBLEME DE NAVIER-STOKES
C        METHODE PISO (Pressure Implicit with Split Operator)
C        AVEC REMONTEE DES CARACTERISTIQUES POUR TRAITER (U.Grad)U
C        LE TERME NON LINEAIRE DU TRANSPORT
C        ******************************************************************
C        A L'ETAPE n=0, il faut RECUPERER U(t0)=U0 P(t0)=P0 et CONSTRUIRE F(t0)
C        puis, les iterations n par pas de temps dt peuvent demarrer
C
C        A L'ETAPE n+1, LE PROBLEME CONSISTE A TROUVER
C        {U(tn+1), P(tn+1)} = lim {Un+1,m+1, Pn+1,m+1} quand m->Infini
C        SOLUTION de
C
C (1)    m=0; Un+1,m=Un;  Pn+1,m=Pn;
C
C (2)   (Rho - dt Mhu Laplacien) {Ui,n+1,m+1(X)} =
C        Rho {Ui,n(X(tn;tn+1,X))} -dt CoGrPr Gradi Pn+1,m(X) + dt {Fgi(tn+1)})
C       ou X(tn;tn+1,X) est la position de la molecule du fluide a tn
C          qui sera en X a tn+1 (methode des caracteristiques)
C          Ui,n+1,m+1(tn+1,X) = Ui(tn+1,X)   avec X sur Gamma
C          pour  i=1,...,NDIM
C
C        - CoGrPr Laplacien Pn+1,m+1(X) =
C        Div { -Fg(tn+1) + Rho (Un+1,m+1(X) . Grad) Un+1,m+1(X) } X dans Omega
C        Pn+1,m+1(Gamma) = P(tn+1,Gamma) pour tout Gamma sur la frontiere
C
C        Si ||Ui,n+1,m+1 - Ui,n+1,m|| > Eps ||Ui,n+1,m+1||
C        ou ||P  n+1,m+1 - P  n+1,m|| > Eps ||P  n+1,m+1|| aller en (2)
C        Sinon
C          Si T0 + n dt < Tfinal alors n=n+1; goto (1)
C
C        ***************************************************************
C        CALCUL DES VITESSES PRESSIONS AUX DIFFERENTS PAS DE TEMPS
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C
C           PISO SUR BF NON ENCORE PROGRAMME   ... A FAIRE ...
C
C           TRIANGLE ou TETRAEDRE DE BREZZI-FORTIN
            CALL NS2BF( KNOMOB, NAVSTO, NTLXOB,MOREE2,RELMIN,KNOMFIC,
     %                     NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                     NBTYEL, MNNPEF, NUTYEL,
     %                     NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                     NORESO, NIVOGC, LPLIGN, LPCOLO, NBCMVG,
     %                     DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
     %                     NBPAST, NOVVIPR,NTDLHB, NTDLVP, NTDLTE,
     %                     NDDLNO, VXYZP0, VITMAX, VITMOY, TEMPER0,
     %                     MXPAST, MCN(MNVPMIX),
     %                     DFABG,  DFACTO,  DVITPR, IERR )

         ELSE IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN

C           METHODE PISO (Pressure Implicit with Split Operator)
C           POUR DES EF de TAYLOR-HOOD + REMONTEE DES CARACTERISTIQUES
            CALL FPISOCATH( KNOMOB, NTLXOB,MNTOPO,MOREE2,RELMIN,KNOMFIC,
     %                      NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                      NBTYEL, MNNPEF, NBTTEF, NUTYEL,
     %                      NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                      NORESO, NIVOGC, LPLIGN, LPCOLO, NBCMVG,
     %                      NBVPFX, MNNVPFX,MNVVPFX,
     %                      DT,     DTSTOC, TPSINI, TPSFIN,
     %                      NOVVIPR,MXVVIPR,NTDLVP, NDDLNO, VXYZP0,
     %                      NTVECT, MNVECT, VITMAX, VITMOY,
     %                      MXPAST, MCN(MNVPMIX),
     %                      DFABG,  DFACTO, DVITPR, IERR )

         ELSE

C           MAILLAGE D'EF NON PROGRAMME
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'TYPE D''ELEMENT FINI NON PROGRAMME'
            ELSE
               KERR(1) = 'FINITE ELEMENT TYPE NOT PROGRAMMED'
            ENDIF
            CALL LEREUR
            GOTO 9990

         ENDIF


      ELSE IF( NAVSTO .EQ. 5 ) THEN
C
C        ******************************************************
C        RESOLUTION PISO EN TEMPS DU PROBLEME DE NAVIER-STOKES
C        METHODE PISO (Pressure Implicit with Split Operator)
C        AVEC PRISE EN COMPTE DIRECTE DU TERME NON LINEAIRE DE
C        TRANSPORT (U.Grad) U  SUR OMEGA et GAMMA en PRESSION
C        ******************************************************
C       A L'ETAPE n=0,  U0 = U(t0),  P0 = P(t0)=P0
C
C       A L'ETAPE n+1, LE PROBLEME CONSISTE A TROUVER
C       {U(tn+1), P(tn+1)} = lim {Um+1, Pm+1} quand m->Infini
C       SOLUTION de
C
C (0)    m=0; Um=U(tn);  Pm=P(tn);  (calculees juste avant)
C
C (1)   (1/dt - Mhu/Rho Laplacien) {Ui,m+1(X)} =
C         + 1/dt {U(tn)} - (Um.Grad) {Ui,m(X)}
C         - 1/Rho Gradi Pm(X) + 1/Rho {Fgi(tn+1)}
C        Condition aux limites
C         Ui,m+1(tn+1,X) = Ui(tn+1,X)  avec X sur Gamma
C        pour  i=1,...,NDIM
C
C (2)   - 1/Rho Laplacien Pm+1(X) =
C       Div { -Fg(tn+1)/Rho + (Un+1,m+1(X).Grad) Un+1,m+1(X)} X dans le domaine
C
C        Condition aux limites
C       - 1/Rho dPm+1(Gamma)/dn =
C       n . { -Fg(tn+1)/Rho + (Un+1,m+1(gamma).Grad) Un+1,m+1(gamma) }
C       or Pn+1,m+1(Gamma) = P(tn+1,Gamma) pour Gamma sur la frontiere
C
C       Si ||Ui,n+1,m+1 - Ui,n+1,m|| > Eps ||Ui,n+1,m+1||
C       ou ||P  n+1,m+1 - P  n+1,m|| > Eps ||P  n+1,m+1|| aller en (1)
C       Sinon
C          Si T0 + n dt < Tfinal alors n=n+1; tn+1=tn+dt;  goto (0)
C
C        ***************************************************************
C        CALCUL DES VITESSES PRESSIONS AUX DIFFERENTS PAS DE TEMPS
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C
C           PISO SUR BF NON ENCORE PROGRAMME   ... A FAIRE ...
C
C           TRIANGLE ou TETRAEDRE DE BREZZI-FORTIN
            CALL NS2BF( KNOMOB, NAVSTO, NTLXOB,MOREE2,RELMIN,KNOMFIC,
     %                  NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                  NBTYEL, MNNPEF, NUTYEL,
     %                  NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                  NORESO, NIVOGC, LPLIGN, LPCOLO, NBCMVG,
     %                  DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
     %                  NBPAST, NOVVIPR,NTDLHB, NTDLVP, NTDLTE,
     %                  NDDLNO, VXYZP0, VITMAX, VITMOY, TEMPER0,
     %                  MXPAST, MCN(MNVPMIX),
     %                  DFABG,  DFACTO,  DVITPR, IERR )
C
         ELSE IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN
C
C           METHODE PISO (Pressure Implicit with Split Operator)
C           POUR DES EF de TAYLOR-HOOD + TERME NON LINEAIRE (U.Grad) U
            CALL FPISONLTH( KNOMOB, NTLXOB, MOREE2, RELMIN, KNOMFIC,
     %                      NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                      NBTYEL, MNNPEF, NBTTEF, NUTYEL,
     %                      NUMIOB, NUMAOB, MNDOEL,
     %                      IEFOIN, IEFOCL, IEBLPR,
     %                      NORESO, LPLIGN, LPCOLO, NIVOGC, NBCMVG,
     %                      NBVPFX, MNNVPFX,MNVVPFX,
     %                      DT,     DTSTOC, TPSINI, TPSFIN,
     %                      NOVVIPR,MXVVIPR,NTDLVP, NDDLNO, VXYZP0,
     %                      NTVECT, MNVECT, VITMAX, VITMOY,
     %                      MXPAST, MCN(MNVPMIX),
     %                      DFABG,  DFACTO, DVITPR, IERR )
C
         ELSE
C
C           MAILLAGE D'EF NON PROGRAMME
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'TYPE D''ELEMENT FINI NON PROGRAMME'
            ELSE
               KERR(1) = 'FINITE ELEMENT TYPE NOT PROGRAMMED'
            ENDIF
            CALL LEREUR
            GOTO 9990
C
         ENDIF

      ENDIF

      IF( IERR .EQ. 0 ) THEN

C        AFFICHAGE DU DERNIER VECTEUR (NOVVIPR) VITESSE PRESSION STOCKE
C        --------------------------------------------------------------
         PRINT*
         IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'fluideNS: AFFICHAGE DU DERNIER VECTEUR VITESSE PRESSION
     % CALCULE  -----------------'
         ELSE
         PRINT*,'fluideNS: PRINTING of the LAST COMPUTED VELOCITY+PRESSU
     %RE VECTOR -----------------'
         ENDIF
         TEMPS = RMCN( MNTIMES -1 + NOVVIPR )
         CALL AFVIPR( NUTYEL, NDIM, NBNOVI, MNXYZN, NDDLNO,
     %                TEMPS, NTDLVP, MIN(10,NBNOVI), VXYZP0,
     %                VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )

cccC        POUR LES TEMPS STOCKES AFFICHAGE DE
cccC        LA NORME L2 DE LA VITESSE et PRESSION et
cccC        SI VITESSE_EXACTE(t,x,y,z,nc) et/ou PRESSION_EXACTE(t,x,y,z)
cccC        EXISTENT alors AFFICHAGE DE LA NORME L2 DE L'ERREUR
cccC        ------------------------------------------------------------
ccc         CALL FL2NORMES( KNOMOB, IERR, DCPUL2 )

      ENDIF

C     MISE A JOUR TMS VECTEUR"MAXVITESSEPRESS AUX PAS DE TEMPS CALCULES
C     =================================================================
      IF( NBPAST .LE. 0 ) THEN
C        DESTRUCTION DU TMS
         CALL LXTSDS( NTLXOB, 'VECTEUR"MAXVITESSEPRESS' )
         GOTO 9990
      ENDIF

C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR = N1VPMIX
      MCN(MNVPMIMX+WBCOVE) = N1VPMIX
C     NOMBRE DE VECTEURS MAX VITESSE PRESSION = NOMBRE DE PAS DE TEMPS CALCULES
      MCN(MNVPMIMX+WBVECT) = 1+NBPAST
C     NOMBRE DE TEMPS A LA FIN DES VECTEURS
      MCN(MNVPMIMX+WBCPIN) = 0

C     LA DATE DU TMS VECTEUR"MAXVITESSEPRESS
      CALL ECDATE( MCN(MNVPMIMX) )

C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNVPMIMX + MOREE2 ) = NONMTD( '~>>>VECTEUR' )

ccc      MOVPMIMX1 = WECTEU + N1VPMIX * (1+NBPAST) * MOREE2
ccc      IF( MOVPMIMX1 .LT. MOVPMIMX ) CALL TAMSRA( NTVPMIMX, MOVPMIMX1 )

C     AFFICHAGE DU TABLEAU DES MIN MAX DES VECTEURS VITESSE+PRESSION
C     --------------------------------------------------------------
      CALL AFVPMIMX( N1VPMIX, NBPAST, MCN(MNVPMIMX+WECTEU) )

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,19000) NOVVIPR
19000    FORMAT(I7,' FICHIERS ECRITS du VECTEUR VITESSES-PRESSIONS')
      ELSE
         WRITE(IMPRIM,29000) NOVVIPR
29000    FORMAT(I7,' WRITTEN VELOCITY-PRESSURE VECTOR FILES')
      ENDIF

C     DESTRUCTION DES TABLEAUX DEVENUS INUTILES
C     =========================================
 9990 IF( IERATEMPER0 .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluideNS: TEMPER0 est DESALLOUE'
         ELSE
            PRINT*,'fluideNS: TEMPER0 is DESALLOCATED'
         ENDIF
         DEALLOCATE( TEMPER0 )
         IERATEMPER0 = 1
      ENDIF

      IF( IERAVXYZP0 .EQ. 0 ) THEN
