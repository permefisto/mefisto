         IF( MNFVSF .LE. 0 ) THEN
            GOTO 70
         ENDIF
         IF( NDIM .EQ. 3 ) THEN
            CALL FLUVITSF( MNXYZP, MCN(MNNPEF),
     %                     NDDLNO, VXYZP0,
     %                     NUMIOB(3), NUMAOB(3),
     %                     FLUNEG, FLUPOS, MCN(MNFVSF) )
         ELSE
            CALL FLUVITLG( MNXYZP, MCN(MNNPEF),
     %                     NDDLNO, VXYZP0,
     %                     NUMIOB(2), NUMAOB(2),
     %                     FLUNEG, FLUPOS, MCN(MNFVSF) )
         ENDIF
         CALL TNMCDS( 'REEL2', MOSFOB, MNFVSF )
      ENDIF

C     ICI SEUL LE SECOND MEMBRE PEUT DEPENDRE DU TEMPS
 70   TESTNL = 0

C     LE PARAMETRE THETA COMPRIS ENTRE 1/2 ET 1 (COMPROMIS=2/3)
C     ---------------------------------------------------------
c     SCHEMA TOTALEMENT IMPLICIT
      THETA(1) = 1D0
      THETA(0) = 0D0

cccc     SCHEMA COMPROMIS EN STABILITE PRECISION
ccc      THETA(1) = 2D0 / 3D0
ccc      THETA(0) = 1D0 - THETA(1)

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10070) THETA(1)
      ELSE
         WRITE(IMPRIM,20070) THETA(1)
      ENDIF
10070 FORMAT(/' COEFFICIENT THETA DU SCHEMA EN TEMPS =',G15.7)
20070 FORMAT(/' THETA COEFFICIENT of the TIME SCHEME =',G15.7)


C     CREATION OU AUGMENTATION DU TMS 'VECTEUR"MAXVITESSEPRESS'
C     Temps, |VITESSE|Moyen, |VITESSE|Max, PressionMoyen, Max-MinPression,
C     FluxNegatif, FluxPositif  SONT STOCKES
C     A CHAQUE PAS DE TEMPS CALCULE pour TRACER ces COURBES(Temps)
C     --------------------------------------------------------------------
C     CALCUL DU NOMBRE de VECTEURS VITESSES PRESSIONS AUX PAS DE TEMPS
C     NOMBRE MAXIMAL DE PAS DE TEMPS A CALCULER ICI
      NEWPASDT = NINT( ( TPSFIN - TPSINI ) / DT ) + 1

C     EXISTE T IL UN TMS 'VECTEUR"MAXVITESSEPRESS'
      CALL LXTSOU( NTLXOB,'VECTEUR"MAXVITESSEPRESS', NTVPMIMX,MNVPMIMX )

      IF( NTVPMIMX .GT. 0 ) THEN

C        LE TMS 'VECTEUR"MAXVITESSEPRESS' EXISTE
C        IL EST SAUVEGARDE DANS UN NOUVEAU TMC
C        NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR = N1VPMIX
         N1VPMIX = MCN(MNVPMIMX+WBCOVE)
C        NOMBRE VECTEURS MAX VITESSE PRESSION=NOMBRE PAS DE TEMPS CALCULES
C        ATTENTION: L'INDICE COMMENCE A ZERO ET COMPTE POUR 1 DANS NBVECT
         NBVECT = MCN(MNVPMIMX+WBVECT)

C        RECHERCHE DU DERNIER PAS DE TEMPS CALCULE AU TEMPS < TPSINI
         NBPT = 0
         MND = ( MNVPMIMX+WECTEU +1 ) / MOREE2
         DO K = 1, NBVECT
C           LE TEMPS DU PAS DE TEMPS K
            T = REAL( DMCN( MND ) )
            IF( T .GE. TPSINI ) GOTO 77
            NBPT = NBPT + 1
            MND  = MND + N1VPMIX
         ENDDO

 77      MOVPIX = WECTEU + MOREE2 * N1VPMIX * NBPT
         CALL TNMCDC( 'ENTIER', MOVPIX, MNVPIX )
         CALL TRTATA( MCN(MNVPMIMX), MCN(MNVPIX), MOVPIX )
C        DESTRUCTION DU TMS 'VECTEUR"MAXVITESSEPRESS'
         CALL LXTSDS( NTLXOB, 'VECTEUR"MAXVITESSEPRESS' )
         NTVPMIMX = 0
         MNVPMIMX = 0
         NBPAST0  = NBPT -1
         MXPAST   = NBPAST0 + NEWPASDT

      ELSE

         MOVPIX  = 0
         MNVPIX  = 0
         N1VPMIX = 0
         NBPAST0 = 0
         MXPAST  = NEWPASDT

      ENDIF

C     CONSTRUCTION DU TMS 'VECTEUR"MAXVITESSEPRESS'
      IF( NAVSTO .EQ. 3 ) THEN
         N1VPMIX = 11
      ELSE
         N1VPMIX = 8
      ENDIF

      PRINT*,'DECLARATION du TMS VECTEUR"MAXVITESSEPRESS de ',KNOMOB
      MOVPMIMX = WECTEU + MOREE2 * N1VPMIX * (1+MXPAST)
      CALL LXTNDC( NTLXOB,'VECTEUR"MAXVITESSEPRESS', 'REEL',  MOVPMIMX )
      CALL LXTSOU( NTLXOB,'VECTEUR"MAXVITESSEPRESS', NTVPMIMX,MNVPMIMX )

      IF( MOVPIX .GT. 0 .AND. MNVPIX .GT. 0 ) THEN
C        RECUPERATION DU PRECEDENT 'VECTEUR"MAXVITESSEPRESS'
         CALL TRTATA( MCN(MNVPIX), MCN(MNVPMIMX), MOVPIX )
         CALL TNMCDS( 'ENTIER', MOVPIX, MNVPIX )
      ENDIF

C     NOMBRE DE COMPOSANTES DE CHAQUE VECTEUR = N1VPMIX
C     Temps, |VITESSE|Moyen, |VITESSE|Max, PressionMoyen, Max-MinPression,
C     FluxNegatif, FluxPositif, NO PAS de TEMPS  SONT STOCKES
      MCN(MNVPMIMX+WBCOVE) = N1VPMIX
C     NOMBRE DE VECTEURS = NOMBRE DE PAS DE TEMPS A CALCULER
      MCN(MNVPMIMX+WBVECT) = 1+MXPAST
C     NOMBRE DE TEMPS A LA FIN DES VECTEURS
      MCN(MNVPMIMX+WBCPIN) = 0

C     ADRESSE MCN DU DEBUT DES NOUVEAUX VP MIN MAX ...
      MNVPMIX = MNVPMIMX + WECTEU

C     NOMBRE DE PAS DE TEMPS CALCULES AVANT LA NOUVELLE RESOLUTION
      NBPAST = NBPAST0

      IF( NAVSTO .EQ. 0 ) THEN

C        ***************************************************************
C        RESOLUTION DU PROBLEME DE STOKES INSTATIONNAIRE
C        ***************************************************************
C        INTEGRATION EN TEMPS PAR LA THETA  METHODE IMPLICITE
C        [MG(Rho)] (u(tn+1) - u(tn)) = dt * (Somme i=0,1 de
C        ( theta i ( {fg(tn+i)} - [VG] {u(tn+i)} )
C        avec [MG] la matrice de MASSE
C             [VG] la matrice de VISCOSITE
C***********************************************************************
C        ATTENTION: LE GRADIENT CONJUGUE NE CONVERGE PAS
C        CAR LA MATRICE N'EST PAS DEFINIE POSITIVE
C        PAR CONTRE LA FACTORISATION DE CROUT L D tL
C        CONVIENT ET EST IMPOSEE
         IF( NORESO .NE. 1 ) THEN
         NORESO = 1
         IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'stokeins: Le GRADIENT CONJUGUE NE CONVERGE PAS'
         PRINT*,'stokeins: La FACTORISATION L D tL de CROUT est IMPOSEE'
         ELSE
            PRINT*,'stokeins: The CONJUGATE GRADIENT DOES NOT CONVERGE'
            PRINT*,'stokeins: The CROUT FACTORISATION L D tL IS IMP0SED'
         ENDIF
         ENDIF
C***********************************************************************
C        CALCUL DES VITESSES PRESSIONS AUX DIFFERENTS PAS DE TEMPS
         CALL STOKEINS( KNOMOB, NTLXOB, MOREE2, NAVSTO, RELMIN, KNOMFIC,
     %                  NDIM,   MNXYZN, NBNOHB, NBNOVI,
     %                  NBTYEL, MNNPEF, NBTTEF, NUTYEL, NBDLMX,
     %                  NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                  NORESO, LPLIGN, LPCOLO, NIVOGC, NBCMVG,
     %                  NDDLNO, VXYZP0, TEMPER0,
     %                  NBVPFX, MNNVPFX, MNVVPFX,
     %                  THETA,  DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
     %                  MXPAST, NBPAST, MCN(MNVPMIX),
     %                  NOVVIPR,NTDLHB, NTDLVP, NTDLTE,
     %                  VITMAX, VITMOY,
     %                  DFABG,  DFACTO, DVITPR, IERR )


      ELSE IF( NAVSTO .EQ. 1 ) THEN

C        ****************************************************************
C        RESOLUTION EULER IMPLICITE EN TEMPS DU PROBLEME DE NAVIER-STOKES
C        ****************************************************************
C        FORMULE 1: INTEGRATION EN TEMPS PAR LA THETA  METHODE IMPLICITE
C        Rho (u(tn+1) - u(X(tn;tn+1,x))) = dt * (Somme i=0,1 de
C        ( theta i ( {fg(tn+i)} - [VG] {u(tn+i)} )
C        avec [MG] la matrice de MASSE
C             [VG] la matrice de VISCOSITE-PRESSION
C             X(tn;tn+1,x) est la position de la molecule du fluide a tn
C             qui sera en x a tn+1
C        soit encore
C        ( [MG] + dt theta [VG] ) {u(tn+1)} = {Rho*u(X(tn;tn+1,x))}
C                     + dt * theta {fg(tn+1)} + dt * (1-theta) {fg(tn)}
C                     - dt (1-theta) [VG] ) {u(tn)}
C        ***************************************************************
C        CALCUL DES VITESSES PRESSIONS AUX DIFFERENTS PAS DE TEMPS
         CALL NS1BFTH( KNOMOB, NAVSTO, NTLXOB, MOREE2, RELMIN, KNOMFIC,
     %                 NDIM,   MNXYZN, NBSOM,  NBNOHB, NBNOVI,
     %                 NBTYEL, MNNPEF, NBTTEF, NUTYEL, NBDLMX,
     %                 NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                 NORESO, LPLIGN, LPCOLO, NIVOGC, NBCMVG,
     %                 NDDLNO, VXYZP0, TEMPER0,
     %                 NBVCFX, NBVPFX, MNNVPFX,MNVVPFX,
     %                 THETA,  DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
     %                 NOVVIPR,NTDLHB, NTDLVP, NTDLTE,
     %                 VITMAX, VITMOY, MXPAST, NBPAST, MCN(MNVPMIX),
     %                 DFABG,  DFACTO, DVITPR, IERR )


      ELSE IF( NAVSTO .EQ. 2 ) THEN

C        ********************************************************************
C        RESOLUTION FRACTIONNAIRE DU PROBLEME DE NAVIER-STOKES
C        ********************************************************************
C        FORMULE 2: INTEGRATION EN TEMPS PAR LA METHODE SEMI-IMPLICITE
C      + ( Rho - dt Mhu Laplacien ) U*(tn+1,x) =
C          Rho {U(tn,X(tn;tn+1,x))} - dt CoGrPr grad P(tn,x) + dt {fg(tn+1)})
C        avec X(tn;tn+1,x) est la position de la molecule du fluide a tn
C             qui sera en x a tn+1 (methode des caracteristiques)
C
C      - dt CoGrPr Laplacien {P(tn+1)-P(tn)} = - Rho Div {U*(tn+1,x)}
C
C      + Rho {U(tn+1,x)-U*(tn+1,x)} = - dt CoGrPr {grad(p(tn+1,x)-p(tn,x)}

C        U(tn+1,Noeuds) = U(tn,Noeuds) + (U(tn+1) - U(tn))(Noeuds)

C        P(tn+1,Sommets) = P(tn,Sommets) + ( P(tn+1,Sommets)-P(tn,Sommets) )
C        ********************************************************************

C        CALCUL DES VITESSES PRESSIONS AUX DIFFERENTS PAS DE TEMPS
         IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN

C           TRIANGLE ou TETRAEDRE DE BREZZI-FORTIN
            CALL NS2BF( KNOMOB, NAVSTO, NTLXOB,MOREE2,RELMIN,KNOMFIC,
     %                  NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                  NBTYEL, MNNPEF, NUTYEL,
     %                  NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                  NORESO, NIVOGC, LPLIGN, LPCOLO, NBCMVG,
     %                  DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
     %                  NBPAST, NOVVIPR,NTDLHB, NTDLVP, NTDLTE,
     %                  NDDLNO, VXYZP0, VITMAX, VITMOY, TEMPER0,
     %                  MXPAST, MCN(MNVPMIX),
     %                  DFABG,  DFACTO,  DVITPR, IERR )

         ELSE IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN

C           TRIANGLE ou TETRAEDRE DE TAYLOR-HOOD
C           SEMI-IMPLICITE SCHEMA AVEC DECOMPOSITION DES COMPOSANTES
C           DE L'OPERATEUR ET PRESSION INTERPOLEE P1
            CALL NS2TH( KNOMOB, NAVSTO, NTLXOB,MOREE2,RELMIN,KNOMFIC,
     %                  NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                  NBTYEL, MNNPEF, NUTYEL,
     %                  NUMIOB, NUMAOB, MNDOEL, IEBLPR,
     %                  NORESO, LPLIGN, LPCOLO, NBCMVG,
     %                  DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
     %                  NBPAST, NOVVIPR,NTDLVP, NTDLTE,
     %                  NDDLNO, VXYZP0, VITMAX, VITMOY, TEMPER0,
     %                  MXPAST, MCN(MNVPMIX),
     %                  DFABG,  DFACTO, DVITPR, IERR )

         ENDIF


      ELSE IF( NAVSTO .EQ. 3 ) THEN

C        ********************************************************************
C        RESOLUTION DU PROBLEME DE NAVIER-STOKES + THERMIQUE INSTATIONNAIRE
C        SELON L'APPROXIMATION DE BOUSSINEQ POUR DES EF TAYLOR-HOOD SEULEMENT
C        ********************************************************************

C        Discretisation avec la methode des caracteristiques retro
C        A L'ETAPE n+1, LE PROBLEME CONSISTE A TROUVER
C                      {u(tn+1), p(tn+1), T(tn+1)=te(tn+1)} SOLUTION de

C      1. Calcul de la temperature {te(tn+1,x)} :
C      ( Rho Cp - dt Conduc Laplacien ) {te(tn+1,x)} =
C      soit
C       = Rho Cp {te(X(tn;tn+1,x)} + dt {fgte(tn+1)})
C       ou X(tn;tn+1,x) est la position de la molecule du fluide a tn
C            qui sera en x a tn+1 (methode des caracteristiques en tn+1)
C      soit  (NON PROGRAMME)
C       = Rho Cp {te(tn,x) - Rho Cp dt {Som  Ui(tn,x) . d/dxi te(tn,x)}
c                                     i=1,NDIM   (MOINS IMPLICITE car en tn)

C       + dt {fgte(tn+1)})

C      Condition aux limites sur la temperature te
C       te(tn+1,X) = teG(tn+1,X)  avec X sur GammaD
C       - Conduc d te(tn+1,x)/dn = CoefEch ( te(tn+1,x) - teExt(tn+1,x) )
C       condition de Fourier-Robin avec le coefficient d'echange CoefEch
C       teExt(t,x) la temperature exterieure a l'instant t et en x
C                  sur la frontiere GammaF
C     ...................................................................

C      2. Calcul des NDIM composantes de la vitesse intermediaire {U*i(tn+1,x)}
C      ( Rho - dt Mhu Laplacien ) {U*i(tn+1,x)} =
C            Rho {Ui(tn,X(tn;tn+1,x))}
C           - dt CoGrPr gradi p(tn,x)
C           - dt Rho CoBous g (-Z) {te(tn+1)-te(t0)}
C           + dt {fgui(tn+1)

C       ou X(tn;tn+1,x) est la position de la molecule du fluide a tn
C                       qui sera en x a tn+1 (methode des caracteristiques)
C          g l'acceleration de la pesanteur est ici prise en compte
C            dans la direction opposee a l'axe Z car c'est la force
C            qui fait apparaitre la CONVECTION NATURELLE du fluide
C          CoBous est le coefficient de DILATATION THERMIQUE
C                     du fluide (THERMAL EXPANSION COEFFICIENT)
C          Cf https://www.comsol.fr/multiphysics/boussinesq-approximation

C      Condition aux limites sur U*i(tn+1)
C       U*i(tn+1,X) = Ui(tn+1,X)        avec X sur GammaU
C       U*i(tn+1,X) = Ui(X(tn;tn+1,x))  avec X sur GammaU de sortie de fluide
C       -dt Mhu dU*i(tn+1,X)/dn = 0     avec X sur Frontiere-GammaU 
C                               pour i=1,...,NDIM
C     .......................................................................

C      3. Calcul de la difference des pressions {P(tn+1)-P(tn)}
C     -dt CoGrPr Laplacien {P(tn+1)-P(tn)} =
C       - (Rho -dt Mhu Laplacien) (Div{U*(tn+1)} -Integrale Div U*(tn+1) /Vol) }
C     Condition aux limites
C       Pn+1,m+1(Gamma) = P(tn+1,Gamma) pour Gamma sur la frontiere GammaP
C      -dt CoGrPr dPn+1,m+1/dn = -n . (Rho -dt Mhu Laplacien) {U*(tn+1)}
C     Si dt Mhu est petit devant Rho, le terme dt Mhu Laplacien est neglige
C     .......................................................................

C      4. Calcul des NDIM composantes de la difference des vitesses {U(tn+1)-U*}
C     ( Rho - dt Mhu Laplacien ) {U(tn+1)-U*}i =
C                                 - dt CoGrPr d/dxi (P(tn+1)-P(tn))
C       Condition aux limites
C       Ui(tn+1,X)-U* = 0           avec X sur GammaU
C       -dt Mhu dUi(tn+1,X)/dn = 0  avec X sur Frontiere-Gamma 
C                              pour i=1,...,NDIM
C     .......................................................................

C      5. Calcul des NDIM composantes de la vitesse {U(tn+1)}
C      U(tn+1,Noeuds) = U(tn,Noeuds) + (U(tn+1) - U(tn))(Noeuds)
C     .......................................................................

C      6. Calcul de la pression {P(tn+1)}
C      P(tn+1,Sommets) = P(tn,Sommets) + ( P(tn+1,Sommets)-P(tn,Sommets) )
C     .......................................................................


C       A L'ETAPE t0 initiale IL FAUT RECUPERER U(t0) P(t0) Te(t0)
C       puis, les iterations en temps peuvent demarrer

C        ********************************************************************

C        CALCUL DES VITESSES PRESSIONS TEMPERATURES AUX DIFFERENTS PAS DE TEMPS
C        RETROUVER LES ADRESSES MCN DES DONNEES "TEMPERATURE" du FLUIDE
C        DES  V   ou S  "OBJETS INTERNES"    DE L'OBJET selon NDIM=3 ou 2
C        DES  SLP ou LP "OBJETS AUX LIMITES" DE L'OBJET
C        ----------------------------------------------------------------
         CALL THEDON( NUMIOB, NBOBIN, MNOBIN, NBOBCL, MNOBCL,
     %                NBJEUX, MNDTEL,
     %                IEMAST, IECHMA, IECOND, IEDILA, IEVIFL, IECOET,
     %                IESOIN, IECONT, IEECHA, IESOCL, IESOPO,
     %                IETEIN, IEVIIN, IEVIANT,IECOBO, IERR )
C
         IF( IEMAST .NE. NBOBIN ) THEN
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               IF( NDIM .EQ. 3 ) THEN
                  KERR(1)='Au MOINS UN VOLUME SANS DENSITE de MASSE'
               ELSE IF( NDIM .EQ. 2 ) THEN
                  KERR(1)='Au MOINS UNE SURFACE SANS DENSITE de MASSE'
               ELSE IF( NDIM .EQ. 1 ) THEN
                  KERR(1)='Au MOINS UNE LIGNE SANS DENSITE de MASSE'
               ENDIF
               KERR(2)='=> PROBLEME THERMIQUE SANS SOLUTION'
            ELSE
               IF( NDIM .EQ. 3 ) THEN
                  KERR(1)='At LEAST 1 VOLUME WITHOUT MASS DENSITY'
               ELSE IF( NDIM .EQ. 2 ) THEN
                  KERR(1)='At LEAST 1 SURFACE WITHOUT MASS DENSITY'
               ELSE IF( NDIM .EQ. 1 ) THEN
                  KERR(1)='At LEAST 1 LINE WITHOUT MASS DENSITY'
               ENDIF
               KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
            ENDIF
            CALL LEREUR
            IERR = 10
         ENDIF

         IF( IECHMA .NE. NBOBIN .AND. TESTNL .LT. 5 ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='Au MOINS UN VOLUME SANS CHALEUR MASSIQUE'
             ELSE IF( NDIM .EQ. 2 ) THEN
           KERR(1)='Au MOINS UNE SURFACE SANS CHALEUR MASSIQUE'
             ELSE IF( NDIM .EQ. 1 ) THEN
           KERR(1)='Au MOINS UNE LIGNE SANS CHALEUR MASSIQUE'
             ENDIF
             KERR(2)='=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
             KERR(1)='At LEAST 1 VOLUME WITHOUT MASSIC HEAT'
             ELSE IF( NDIM .EQ. 2 ) THEN
            KERR(1)='At LEAST 1 SURFACE WITHOUT MASSIC HEAT'
             ELSE IF( NDIM .EQ. 1 ) THEN
            KERR(1)='At LEAST 1 LINE WITHOUT MASSIC HEAT'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 10
         ENDIF
C
         IF( IECOND .NE. NBOBIN ) THEN
          NBLGRC(NRERR) = 2
          IF( LANGAG .EQ. 0 ) THEN
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='AU MOINS UN VOLUME SANS CONDUCTIVITE'
             ELSE IF( NDIM .EQ. 2 ) THEN
                KERR(1)='AU MOINS UNE SURFACE SANS CONDUCTIVITE'
             ELSE IF( NDIM .EQ. 1 ) THEN
                KERR(1)='AU MOINS UNE LIGNE SANS CONDUCTIVITE'
             ENDIF
             KERR(2) = '=> PROBLEME THERMIQUE SANS SOLUTION'
          ELSE
             IF( NDIM .EQ. 3 ) THEN
                KERR(1)='At LEAST 1 VOLUME WITHOUT CONDUCTIVITY'
             ELSE IF( NDIM .EQ. 2 ) THEN
                KERR(1)='At LEAST 1 SURFACE WITHOUT CONDUCTIVITY'
             ELSE IF( NDIM .EQ. 1 ) THEN
                KERR(1)='At LEAST 1 LINE WITHOUT CONDUCTIVITY'
             ENDIF
             KERR(2)='=> HEAT TRANSFER PROBLEM WITHOUT SOLUTION'
          ENDIF
          CALL LEREUR
          IERR = 11
         ENDIF
         IF( IERR .NE. 0 ) GOTO 9999

C        CALCUL DES ITERATIONS AVEC UN PAS DE TEMPS DT CONSTANT
C        TRIANGLE ou TETRAEDRE DE TAYLOR-HOOD AVEC LES INTERPOLATIONS
C        VITESSES X Y Z P2 , PRESSION P1, TEMPERATURE P2
Cf       https://en.wikipedia.org/wiki/Boussinesq_approximation_(buoyancy)
         CALL NST3TH( KNOMOB, NAVSTO, NTLXOB, MOREE2, RELMIN, KNOMFIC,
     %                NDIM,   MNXYZN, NBSOM,  NBNOVI,
     %                NBTYEL, MNNPEF, NUTYEL, MNTPOB, NBDLMX, MOAUX,
     %                NUMIOB, NUMAOB, MNDOEL, MNDTEL, NDPGST, IEBLPR,
     %                NBTEFX, MONTEFX, MNNTEFX, MNVTEFX,
     %                NORESO, LPLIGN,  LPCOLO, NBCMVG,
     %                DT,     DTSTOC, TPSINI, TPSFIN, MNTIMES,
