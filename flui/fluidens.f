            PRINT*,'fluideNS: VXYZP0 est DESALLOUE'
         ELSE
            PRINT*,'fluideNS: VXYZP0 is DESALLOCATED'
         ENDIF
         DEALLOCATE( VXYZP0 )
         IERAVXYZP0 = 1
      ENDIF


      IF( IERALPCOLO .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluideNS: LPCOLO est DESALLOUE'
         ELSE
            PRINT*,'fluideNS: LPCOLO is DESALLOCATED'
         ENDIF
         DEALLOCATE( LPCOLO )
         IERALPCOLO = 1
      ENDIF

      IF( IERALPLIGN .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluideNS: LPLIGN est DESALLOUE'
         ELSE
            PRINT*,'fluideNS: LPLIGN is DESALLOCATED'
         ENDIF
         DEALLOCATE( LPLIGN )
         IERALPLIGN = 1
      ENDIF

      IF( IERANDDLNO .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'fluideNS: NDDLNO est DESALLOUE'
         ELSE
            PRINT*,'fluideNS: NDDLNO is DESALLOCATED'
         ENDIF
         DEALLOCATE( NDDLNO )
         IERANDDLNO = 1
      ENDIF

      DO I=1,4
         IF( MNDOEL(I) .GT. 0 .AND. MXDOEL(I) .GT. 0 ) THEN
