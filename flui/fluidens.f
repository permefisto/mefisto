         ENDIF
         IF( MNDTEL(I) .GT. 0 .AND. MXDTEL(I) .GT. 0 ) THEN
            CALL TNMCDS( 'ENTIER', MXDTEL(I), MNDTEL(I) )
         ENDIF
      ENDDO
      IF( MNNPEF  .GT. 0 ) CALL TNMCDS( 'ENTIER', 2*MXTYEL, MNNPEF )
      IF( MNTIMES .GT. 0 ) CALL TNMCDS( 'REEL',   MXVPFILE, MNTIMES)
      IF( MNTHDL  .GT. 0 ) CALL TNMCDS( 'REEL2',  NBDLMX,   MNTHDL )
      IF( MNX     .GT. 0 ) CALL TNMCDS( 'REEL' ,  NBDLMX*3, MNX    )

      IF( MNNTEFX .GT. 0 ) CALL TNMCDS( 'ENTIER', NBTEFX, MNNTEFX)
      IF( MNVTEFX .GT. 0 ) CALL TNMCDS( 'REEL2',  NBTEFX, MNVTEFX)
      IF( MNNVPFX .GT. 0 ) CALL TNMCDS( 'ENTIER', NBVPFX, MNNVPFX)
      IF( MNVVPFX .GT. 0 ) CALL TNMCDS( 'REEL2',  NBVPFX, MNVVPFX)

C     BILAN SUR LE TEMPS CALCUL UTILISE
C     =================================
      IF( IERR .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,12001) DFABG,DFACTO,DVITPR,
     %                          DFABG+DFACTO+DVITPR
         ELSE
            WRITE(IMPRIM,22001) DFABG,DFACTO,DVITPR,
     %                          DFABG+DFACTO+DVITPR
         ENDIF
      ELSE

C        SUITE A UNE ERREUR DETECTEE SELON LA VALEUR DE
C        INTERA=0:BATCH      PAS d' ECRAN GRAPHIQUE PAS de CLAVIER PAS de SOURIS
C               1:INTERACTIF AVEC   ECRAN GRAPHIQUE PAS de CLAVIER PAS de SOURIS
C               3:INTERACTIF AVEC   ECRAN GRAPHIQUE AVEC   CLAVIER AVEC   SOURIS

         IF( INTERA .LE. 1 ) THEN

c           BATCH: SUITE A UNE ERREUR DETECTEE, ARRET DU LOGICIEL Mefisto
C           AVEC ESSAI DE SAUVER LES DONNEES DANS LES FICHIERS Mefisto
            CALL ARRET( 100 )
C
         ELSE

C           EXECUTION INTERACTIVE:
C           RETOUR AU LECTEUR UTILISATEUR DANS LA PILE DES FICHIERS DE LECTURE
            IF( LHLECT .LE. 0 .OR. LHLECT .GT. MXLECT ) THEN

               NBLGRC(NRERR) = 2
               IF( LANGAG .EQ. 0 ) THEN
