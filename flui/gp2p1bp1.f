      SUBROUTINE GP2P1BP1( NTDL, NUELEM, NONOEF, AE, U, V )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DU VECTEUR V PRODUIT MATRICE AE PAR LE VECTEUR U
C -----    POUR LES 2 DL VITESSE DU BARYCENTRE DU TRIANGLE NUELEM
C          DE TYPE BREZZI-FORTIN
C
C ENTREES:
C --------
C NTDL   : NOMBRE DE DEGRES DE LIBERTE SANS LES DL DU BARYCENTRE DES EF
C NUELEM : NUMERO DU TRIANGLE BREZZI-FORTIN A TRAITER ICI
C NONOEF : NO DES NOEUDS DU TRIANGLE C'EST A DIRE DES 3 SOMMETS
C AE     : LES 2 LIGNES DE AE DES DEGRES DE LIBERTE 4 ET 8 DU BARYCENTRE
C U      : VECTEUR A MULTIPLIER PAR AE
C
C SORTIE :
C --------
C V      : VECTEUR PRODUIT DE AE * U
C          EN FAIT, SEULEMENT LES 2 COMPOSANTES ASSOCIEES AU BARYCENTRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC Saint Pierre du Perray Decembre 2008
C23456---------------------------------------------------------------012
      DOUBLE PRECISION  AE(11,2), U(*), V(*), S
      INTEGER           NONOEF(3), NOGLDL(11)
C
C     NO GLOBAL DES DL DE L'ELEMENT FINI BREZZI-FORTIN
      CALL NODLEFBF( 2, NTDL, NUELEM, NONOEF, NOGLDL )
C
C     NUMERO DU DERNIER DL AVANT CEUX DU BARYCENTRE
      NBADL = NTDL + 2*NUELEM - 2
C
C     PRODUIT D'UNE LIGNE 4 et 8 PAR LE VECTEUR U
      DO 30 K=1,2
C
C        PRODUIT DE LA LIGNE DU DL BARYCENTRE PAR LE VECTEUR U
         S = 0D0
         DO 10 I=1,11
            S = S + AE(I,K) * U( NOGLDL(I) )
 10      CONTINUE
         V( NBADL + K ) = S
C
C        BLOC AU DESSUS DE LA DIAGONALE OBTENU PAR TRANSPOSITION
         S = U( NBADL + K )
         DO 20 I=1,11
            IF( I .NE. 4 .AND. I .NE. 8  ) THEN
               NP = NOGLDL( I )
               V(NP) = V(NP) + AE(I,K) * S
            ENDIF
 20      CONTINUE
C
 30   CONTINUE
C
      RETURN
      END
