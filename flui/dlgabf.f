      SUBROUTINE DLGABF( NDIM,  NBELEM, NBNOEF, NUNOEF, NBDLAG, AGGAUSS,
     %                   NBSOM, NBNOVI, VITESSE )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  CALCUL PAR REMONTEE DE GAUSS DES DEGRES DE LIBERTE VITESSE
C -----  INTERNES AU TRIANGLE ou TETRAEDRE BREZZI-FORTIN
C        A PARTIR DU SECOND MEMBRE STOCKE DANS VITESSE AU DELA DE NBSOM
C
C ENTREES:
C --------
C NDIM   : 2 ou 3 DIMENSION DE L'ESPACE DU FLUIDE
C NBELEM : NOMBRE DE TETRAEDRES DU MAILLAGE
C NBNOEF : NOMBRE DE NOEUDS D'UN TETRAEDRE (P1B 2d=>4, P1B 3d=>5)
C NUNOEF : NO DES NBNOEF NOEUDS DES NBELEM TRIANGLES ou TETRAEDRES
C NBDLAG : NOMBRE DE COLONNES DE AGGAUSS STOCKEES LORS DE L'ELIMINATION
C          DE GAUSS OU NOMBRE DE DL DE L'INTERPOLATION DE BREZZI-FORTIN
C          NOMBRE DE SOMMETS DE L'EF + 1
C NBELEM : NOMBRE D'EF DU MAILLAGE DU FLUIDE
C AGGAUSS: LIGNE DE AE D'UNE COMPOSANTE DE LA VITESSE ELIMINEE PAR GAUSS
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NBNOVI : NOMBRE DE NOEUDS VITESSE (NBSOM+NBELEM)
C
C MODIFIES:
C --------
C VITESSE: EN ENTREE NDIM VITESSES SOLUTIONS AUX SOMMETS DES EF
C          EN SORTIE IDEM + NDIM VITESSES SOLUTIONS AU BARYCENTRE DES EF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC Saint Pierre du Perray Decembre 2010
C MODIFS: ALAIN PERRONNET LJLL UPMC Saint Pierre du Perray Decembre 2012
C23456---------------------------------------------------------------012
      IMPLICIT NONE
      DOUBLE PRECISION  AGGAUSS( NBDLAG, NBELEM )
      DOUBLE PRECISION  VITESSE( NBNOVI, NDIM ), S
      INTEGER           NDIM,   NBELEM, NBNOEF, NUNOEF( NBELEM, NBNOEF),
     %                  NBDLAG, NBSOM,  NBNOVI
      INTEGER           NUELEM, NUBARY, K, I, NS
C
C     LA BOUCLE SUR LES ELEMENTS FINIS
C     =================================
      DO NUELEM = 1, NBELEM
C
C        LE NO GLOBAL DU DL A CALCULER ASSOCIE AU BARYCENTRE
         NUBARY = NBSOM + NUELEM
C
C        REMONTEE DES DL ELIMINES PAR GAUSS
         DO K = NDIM, 1, -1
C
C           LE SECOND MEMBRE ASSOCIE AU BARYCENTRE COMPOSANTE K
            S = VITESSE( NUBARY, K )
C
C           REMONTEE DES COEFFICIENTS DE LA LIGNE DE AGGAUSS
            DO I = 1, NBDLAG-1
               NS = NUNOEF( NUELEM, I )
               S = S - AGGAUSS( I, NUELEM ) * VITESSE( NS, K )
            ENDDO
C
C           LE DL K VITESSE AU BARYCENTRE DE CET EF NUELEM
            VITESSE( NUBARY, K ) = S / AGGAUSS( NBDLAG, NUELEM )
C
         ENDDO
C
      ENDDO
C
ccc      call affvect('Fin dlgabf.f VITESSE=', 20, VITESSE )
ccc      call afl1ve( 'Fin dlgabf.f VITESSE=', NBNOVI*NDIM, VITESSE )
C
      RETURN
      END
