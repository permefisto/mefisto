      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1), RMCN(1), DMCN(1))

      EXTERNAL          ETTAEL
      CHARACTER*(*)     KNOMOB, KNOMFIC
      CHARACTER*4       NOMELE(2)
      LOGICAL           COMP

      REAL              COORDP(30)
      REAL              DT, DTSTOC, TPSINI, TPSFIN

      INTEGER           NUMIOB(4), NUMAOB(4), MNDOEL(4)
      INTEGER           NOOBVC,    NOOBSF(6), NOOBLA(12), NOOBPS(8)
      INTEGER           NDDLNO(0:*), LPLIGN(0:NTDLHB), LPCOLO(*)
      INTEGER           NONOEF(10), NOGLDL(34)

ccc      DOUBLE PRECISION  DIAMIN, DIAMAX
      DOUBLE PRECISION  THETA(0:1)
      DOUBLE PRECISION  VXYZP0(NTDLVP), VPMIMX(8,0:MXPAST)
      DOUBLE PRECISION  TEMPER0(*)
      DOUBLE PRECISION  RELMIN
      DOUBLE PRECISION  DVAL, DINFO, DFABG, DFACTO, DVITPR
      DOUBLE PRECISION  VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY,
     %                  FLUNEG, FLUPOS, DMOTSTO, SEMINVIT, SEMAXVIT
      DOUBLE PRECISION, allocatable, dimension(:) :: MG
      DOUBLE PRECISION, allocatable, dimension(:) :: VG
      INTRINSIC         ALLOCATED
      INTRINSIC         DBLE

C     VALEUR A METTRE SUR LA DIAGONALE POUR TRAITER LES DL FIXES
      DOUBLE PRECISION  TGV
ccc      TGV = 1D10   donne une pression incorrecte!
      TGV = 1D30

      PRINT*
      PRINT 10000
      IF( LANGAG .EQ. 0 ) THEN
      PRINT*,'stokeins: RESOUDRE l''EQUATION de STOKES INSTATIONNAIRE du
     % TEMPS',TPSINI, ' a',TPSFIN
      ELSE
      PRINT*,'stokeins: SOLVE the UNSTEADY STOKES EQUATION FROM TIME',
     %        TPSINI,' to',TPSFIN
      ENDIF
      PRINT 10000
10000 FORMAT(120('='))

      TEMPS = TPSINI
      NIVMAX = 10
      MNTPOB = 0
      MNLPLC = 0
      MNLPCC = 0
      MNLPLU = 0
      IERMGALLOC = 1
      IERVGALLOC = 1
      MNVGC  = 0
      MNAUX  = 0
      MNLGAU = 0
      MOLGAU = 0
      MNLGMM = 0
      MNDPDP = 0
      MNTAEL = 0
      MNVXYZP0=0
      MNVXYZP1=0
      MNFG1  = 0
      MNFG0  = 0
      MNBDIR = 0
      LODIR  = 0
      MNNDFX = 0
      MNFVSF = 0
      MNPRPR = 0
      MNAUX1 = 0
      MNAUX2 = 0
      MNAUX3 = 0
      MNAUX4 = 0
      MNAUX5 = 0
      MNAUX6 = 0
      MNAUX7 = 0
      MNDIR  = 0
      MNDAD  = 0
      MNBET  = 0
      MNADIR = 0
      MOTSGC = 0
      MNVXVYVZP1=0
      MNNOSO = 0
      MOARET = 0
      MXARET = 0
      MNLARE = 0
      MOFACE = 0
      MXFACE = 0
      MNLFAC = 0
      NBNOTE = 0
      NTDLTE = 0

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10010)
      ELSE
         WRITE(IMPRIM,20010)
      ENDIF
10010 FORMAT(/' MASSE VOLUMIQUE ET VISCOSITE NE DEPENDENT NI DU TEMPS NI
     % DE LA VITESSE')
20010 FORMAT(/' DENSITY of MASS and VISCOSITY ARE INDEPENDENT of TIME an
     %d VELOCITY')
C
C     DECLARATION DES TABLEAUX ELEMENTAIRES: VE, ME, SE
C     =================================================
C     DIMENSION MAXIMALE 2 MATRICES ELEMENTAIRES
C     ( MASSE ET VISCOSITE ) SYMETRIQUES + 1 SECOND MEMBRE
C     ELEMENTAIRES POUR LES METTRE L'UN DERRIERE L'AUTRE
C     MOTAEL : NOMBRE DE MOTS DECLARES DU TABLEAU DES TABLEAUX ELEMENTAIRES
C     MNTAEL : ADRESSE MCN DES TABLEAUX ELEMENTAIRES
      MOTAEL = NBDLMX * (NBDLMX+1) + NBDLMX
      CALL TNMCDC( 'REEL2', MOTAEL, MNTAEL )
C     ADRESSE MATRICE MASSE ELEMENTAIRE
      MNMAEL =   MNTAEL
      MDMAEL = ( MNTAEL + 1 ) / MOREE2
C     ADRESSE MATRICE VISCOSITE ELEMENTAIRE
      MNVIEL =   MNMAEL + NBDLMX * (NBDLMX+1) / 2  * MOREE2
      MDVIEL = ( MNVIEL + 1 ) / MOREE2
C     ADRESSE SECOND MEMBRE ELEMENTAIRE
      MNSE   = MNVIEL + NBDLMX * (NBDLMX+1) / 2  * MOREE2
C
C     FLUX DE LA VITESSE SUR LES SURFACES DE L'OBJET POUR UN TEMPS DONNE
      MOSFOB = MAX( NUMAOB(2) - NUMIOB(2), NUMAOB(3) - NUMIOB(3) ) +1
      CALL TNMCDC( 'REEL2', MOSFOB, MNFVSF )
C
C     LES 2 VECTEURS GLOBAUX: VITESSEPRESSION  ET SECONDS MEMBRES
C     A L'INSTANT 0 et 1 SONT DECLARES
      CALL TNMCDC( 'REEL2', NTDLVP, MNVXYZP0 )
      IF( MNVXYZP0 .LE. 0 ) GOTO 9993
      CALL TNMCDC( 'REEL2', NTDLVP, MNVXYZP1 )
      IF( MNVXYZP1 .LE. 0 ) GOTO 9993
      CALL TNMCDC( 'REEL2', NTDLVP, MNFG0 )
      IF( MNFG0 .LE. 0 ) GOTO 9993
      CALL TNMCDC( 'REEL2', NTDLVP, MNFG1 )
      IF( MNFG1 .LE. 0 ) GOTO 9993

      CALL TNMCDC( 'REEL2', NTDLVP, MNVXVYVZP1 )
      IF( MNVXVYVZP1 .LE. 0 ) GOTO 9993
      MNVITX1 = MNVXVYVZP1
      MNVITY1 = MNVITX1 + MOREE2 * NBNOVI
      MNVITZ1 = MNVITY1 + MOREE2 * NBNOVI
      MNPRES1 = MNVITX1 + MOREE2 * NBNOVI * NDIM

C     COPIE DE VXYZP0(1:NTDLVP) dans MCN(MNVXYZP0...)
      MNVP0 = ( MNVXYZP0+1 ) / MOREE2
      CALL TRTATD( VXYZP0, DMCN(MNVP0), NTDLVP )

C     L'ADRESSE DE LA VITESSEPRESSION A L'ITERATION 0 DU POINT FIXE
C     NON UTILISE ICI PUISQUE PROBLEME LINEAIRE
ccc      MNVITI = MNVXYZP0
C
C     DECLARATION DE LA MATRICE GLOBALE DE MASSE [MG] ET VISCOSITE [VG]
C     =================================================================
C     A PRIORI MATRICE DE MASSE SYMETRIQUE NON DIAGONALE
      NCODSM = 1
C     MATRICE DE VISCOSITE SYMETRIQUE NON DIAGONALE
      NCODSV = 1

C     VERSION ALLOCATION des MATRICES MG et VG en LANGAGE FORTRAN 90
C     NBCOVG : NOMBRE DE REELS DOUBLE PRECISION DES MATRICES MG ET KG
C     ---------------------------------------------------------------
      WRITE(IMPRIM,*)
      WRITE(IMPRIM,*) 'ALLOCATION DEMAND  of',NBCOVG,
     %                ' DOUBLE PRECISION of the [MG] and [VG] MATRICES'
      ALLOCATE ( MG(1:NBCOVG), STAT=IERMGALLOC )
      IF( IERMGALLOC .NE. 0 ) THEN
       WRITE(IMPRIM,*)'ALLOCATION ERROR   of',NBCOVG,
     %                ' DOUBLE PRECISION of the [MG] MATRIX'
         IERR = IERMGALLOC
         GOTO 9993
      ENDIF
C
      ALLOCATE ( VG(1:NBCOVG), STAT=IERVGALLOC )
      IF( IERVGALLOC .NE. 0 ) THEN
       WRITE(IMPRIM,*)'ALLOCATION ERROR   of',NBCOVG,
     %                ' DOUBLE PRECISION of the [VG] MATRIX'
         IERR = IERVGALLOC
         GOTO 9993
      ENDIF
      WRITE(IMPRIM,*) 'ALLOCATION CORRECT of',NBCOVG,
     %                ' DOUBLE PRECISION of the [MG] and [VG] MATRICES'
      WRITE(IMPRIM,*)
C
C     CONSTRUCTION DES MATRICES [MG] [VG] ET DU SECOND MEMBRE {BG}
C     ============================================================
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10001)
         WRITE(IMPRIM,10002)
         WRITE(IMPRIM,10003)
      ELSE
         WRITE(IMPRIM,20001)
         WRITE(IMPRIM,20002)
         WRITE(IMPRIM,20003)
      ENDIF
10001 FORMAT(' CONSTRUCTION de la MATRICE [MG] de MASSE')
10002 FORMAT(' CONSTRUCTION de la MATRICE [VG] de VISCOSITE')
10003 FORMAT(' CONSTRUCTION DU VECTEUR {F0} SECOND MEMBRE')
20001 FORMAT(' CONSTRUCTION of the MASS      MATRIX [MG]')
20002 FORMAT(' CONSTRUCTION of the VISCOSITY MATRIX [VG]')
20003 FORMAT(' CONSTRUCTION of the SECOND MEMBER VECTOR {F0}')
C
C     MISE A ZERO DES MATRICES GLOBALES
      CALL AZEROD( NBCOVG, MG )
      CALL AZEROD( NBCOVG, VG )
C
C     LA GENERATION DES TABLEAUX ELEMENTAIRES ET LES MATRICES
C     DE MASSE ET VISCOSITE SONT STOCKEES SOUS FORME PROFIL
C     LE VECTEUR FG0 A L'INSTANT INITIAL EST AUSSI ASSEMBLE
C     =======================================================
C     MNELE : ADRESSE DU TABLEAU NPEF"TYPE EF (TRIA 2P1D ou 2P2C)
      MNELE  = MCN( MNNPEF )
C
C     NOMBRE D'ELEMENTS FINIS DE CE TYPE
      NBELEM = MCN( MNELE + WBELEM )
C
C     LE NUMERO DU TYPE DE L'ELEMENT FINI (TRIA 2P1D ou 2P2C)
      NUTYEL = MCN( MNELE + WUTYEL )
C
C     MNPGEL ADRESSE MCN DES NUMEROS NOEUDS ET POINTS GEOMETRIQUES DES EF
      MNNDEL = MNELE + WUNDEL
      MNPGEL = MNNDEL
C
C     LES CARACTERISTIQUES DE L'ELEMENT FINI
      CALL ELNUNM( NUTYEL, NOMELE )
      CALL ELTYCA( NUTYEL )
C
      IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN

C        TRIANGLE OU TETRAEDRE DE BREZZI-FORTIN
C        ======================================
C        CONSTRUCTION DES TABLEAUX PERMETTANT APRES RESOLUTION DE CALCULER
C        LES DL VITESSES MOYENNES SUR LES TRIANGLES ou TETRAEDRES BREZZI-FORTIN
C        STOCKAGE DES ndim LIGNES DE Me et Ve DES ndim DL ELIMINES PAR GAUSS
C        ----------------------------------------------------------------------
         MOLGAU = NDIM * NBELEM * NBDLMX * 2
         CALL TNMCDC( 'REEL2', MOLGAU, MNLGAU )
         IF( MNLGAU .LE. 0 ) GOTO 9993
         MNLGMM = MNLGAU + NDIM * NBELEM * NBDLMX * MOREE2

      ELSE IF( NUTYEL .EQ. 20 ) THEN

C        TAYLOR-HOOD 3D TETRAEDRE 3P2C  CALCUL des INTEGRALES DP2 DP2 dX
C        ===============================================================
C        RECUPERATION DES TABLEAUX POBA NECESSAIRES A LA
C        CONSTRUCTION DES TABLEAUX ELEMENTAIRES
C        RETRIEVE the ARRAYS of BASIS POLYNOMIAL VALUES at
C        NUMERICAL FORMULA POINTS
         CALL TAPOBA( NBTYEL, MNNPEF, ETTAEL,
     %                MNTPOB, NBDLM0, MOAUX, NBTTEF, NOAXIS, I, IERR )
         IF( IERR .NE. 0 ) GOTO 9999

C        RECHERCHE DU TABLEAU DE POBA ET PARTAGE EN P ET DP
         L = MNTPOB

C        SAUT DU TABLEAU POUR LES EF 2D
C        LES VALEURS DES POLYNOMES DE L'EF DE DIMENSION TOTALE
C        EN 3D sur le TETRAEDRE DE REFERENCE
         L  = L + 1
         IA = MCN( L )
C        DIMENSION DE L ESPACE
C        NDIM   = MCN( IA )
C        NOMBRE DE POLYNOMES D INTERPOLATION
         NBPOLY = MCN( IA + 1 )
C        NOMBRE DE POINTS D INTEGRATION NUMERIQUE
         NPI    = MCN( IA + 2 )
C        ADRESSE DES POIDS DES POINTS DE LA FORMULE D INTEGRATION
         MNPOID = IA + 8
C        ADRESSE DES VALEURS DES POLYNOMES AUX POINTS D INTEGRATION
         MNPOL  = MNPOID + MCN( IA + 3 ) * MOREE2 * NPI
C        ADRESSE DES VALEURS DES DERIVEES DES POLYNOMES AUX POINTS D'INTEGRATION
         MNDPOL = MNPOL + MCN( IA + 4 ) * MOREE2 * NBPOLY * NPI

C        CALCUL DE L'INTEGRALE DP2 DP2 dx dy dz sur LE TETRAEDRE
C        POUR L'EF de TAYLOR-HOOD en FLUIDE
         CALL TNMCDC( 'REEL2', 3*10*3*10, MNDPDP )
         CALL INTDPDP( NPI, MCN(MNPOID), NDIM, NBPOLY, MCN(MNDPOL),
     %                 MCN(MNDPDP))
      ENDIF

C     LE STOCKAGE DES LIGNES ELIMINEES PAR GAUSS POUR LES EF BREZZI-FORTIN
C     POUR LES 2 MATRICES GLOBALES
      MNLGA = MNLGAU
      MNLGM = MNLGMM

C     LA BOUCLE SUR LES ELEMENTS FINIS POUR CONSTRUIRE [MG] [VG] et {FG0}
C     ===================================================================
      CALL AZEROD( NTDLVP, DMCN((MNFG0+1)/MOREE2) )
      DO 22 NUELEM = 1, NBELEM

C        NO DES NOEUDS DE L'ELEMENT FINI NUELEM
         CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOEF )

C        NO DES POINTS LIGNES SURFACES VOLUMES DES SOMMETS ARETES FACES VOLUME
         CALL EFPLSV( MNELE , NUELEM,
     %                NOVCEL, NOSFEL, NOLAEL, NOPSEL,
     %                NOOBVC, NOOBSF, NOOBLA, NOOBPS )

C        INTERPOLATION DU FLUIDE avec F: echapeau->e  P1 ndim
C        COORDONNEES DES POINTS DE L'ELEMENT FINI
         CALL EFXYZP( NDIM, MNXYZN, NBELEM, NUELEM, MNPGEL, NBNOEF,
     %                COORDP )
C
C        NOGLDL: NUMERO GLOBAL DES DEGRES DE LIBERTE DE L'ELEMENT FINI
         CALL NUGDLE( NDIM,   NUTYEL, NBDLMX, NBNOEU,
     %                NONOEF, NDDLNO,
     %                NBDLEF, NOGLDL, IERR )

         IF( NUTYEL .EQ. 13 ) THEN

C           TRIANGLE BREZZI-FORTIN P1BULLEP3 EN VITESSE et P1 EN PRESSION
C           -------------------------------------------------------------
C           MATRICE ELEMENTAIRE DE MASSE
            CALL F2MP1BP1( COORDP,
     %                     NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                     DMCN(MDMAEL) )
C
C           MATRICE ELEMENTAIRE DE VISCOSITE
            CALL F2RP1BP1( COORDP,
     %                     NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                     DMCN(MDVIEL) )

C           CONSTRUCTION DES MATRICES ELEMENTAIRES A PARTIR DE ME ET VE
C           MDVIEL => [VE] = [ MASSE + DT THETA VISCOSITE ]
            CALL CL2VED ( 66, 1D0,         DMCN(MDMAEL),
     %                        DT*THETA(1), DMCN(MDVIEL),
     %                        DMCN(MDVIEL) )

C           MDMAEL => [ME] = [ MASSE - DT (1-THETA) VISCOSITE ]
            CALL CL2VED ( 66, 1D0+THETA(0)/THETA(1),
     %                                          DMCN(MDMAEL),
     %                      -THETA(0)/THETA(1), DMCN(MDVIEL),
     %                       DMCN(MDMAEL))

C           STOCKAGE DES LIGNES DES DL DU BARYCENTRE DE LA MATRICE
C           [ME]=[ MASSE - DT (1-THETA) VISCOSITE ]
C           DANS LE TABLEAU D'ADRESSE MNLGMM AVEC LE DECALAGE PAR EF
            CALL GM2P1BP1( DMCN(MDMAEL), MCN(MNLGM) )
            MNLGM = MNLGM + MOREE2 * 11 * 2
C
C           GAUSS SUR [VE]=[M+theta delta V] DES DL DU BARYCENTRE
C           (ELIMINATION DES 2 DL 4 et 8 INTERNES au TRIANGLE)
            CALL GV2P1BP1( DMCN(MDVIEL), MCN(MNLGA) )
C           AVANT      4 + 4 + 3 = 11 DEGRES DE LIBERTE
C           MAINTENANT 3 + 3 + 3 =  9 DEGRES DE LIBERTE
            MNLGA = MNLGA + MOREE2 * 11 * 2

C           SECOND MEMBRE ELEMENTAIRE AVEC 11 DEGRES DE LIBERTE
            CALL F2SP1BP1( COORDP,
     %                     NOOBLA, NUMIOB(2), NUMAOB(2), MCN(MNDOEL(2)),
     %                     NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                     MCN(MNSE) )

C           ASSEMBLAGE DES DEGRES DE LIBERTE 4 ET 8 DE SE DANS MCN(MNFG0)
            CALL GS2P1BP1( NTDLHB, NUELEM, MCN(MNSE),  MCN(MNFG0) )

         ELSE IF( NUTYEL .EQ. 19 ) THEN

C           TETRAEDRE BREZZI-FORTIN P1BULLEP4 EN VITESSE et P1 EN PRESSION
C           --------------------------------------------------------------
C           MATRICE ELEMENTAIRE DE MASSE
            CALL F3MP1BP1( COORDP,
     %                     NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                     DMCN(MDMAEL) )
C
C           MATRICE ELEMENTAIRE DE VISCOSITE
            CALL F3RP1BP1( COORDP,
     %                     NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                     DMCN(MDVIEL) )

C           COMBINAISONS LINEAIRES DES MATRICES ELEMENTAIRES
C           CONSTRUCTION DES MATRICES ELEMENTAIRES A PARTIR DE ME ET VE
C           MNVIEL => [VE] = [ MASSE + DT THETA VISCOSITE ]
            CALL CL2VED ( 190, 1D0,         DMCN(MDMAEL),
     %                         DT*THETA(1), DMCN(MDVIEL),
     %                         DMCN(MDVIEL) )

C           MNMAEL => [ME] = [ MASSE - DT (1-THETA) VISCOSITE ]
            CALL CL2VED ( 190, 1D0+THETA(0)/THETA(1),
     %                                               DMCN(MDMAEL),
     %                        -THETA(0)/THETA(1),    DMCN(MDVIEL),
     %                         DMCN(MDMAEL))

C           STOCKAGE DES LIGNES DE LA MATRICE [ME] DES DL DU BARYCENTRE
C           DANS LE TABLEAU D'ADRESSE MNLGMM AVEC LE DECALAGE PAR EF
            CALL GM3P1BP1( DMCN(MDMAEL), MCN(MNLGM) )
            MNLGM = MNLGM + MOREE2 * 19 * 3

C           GAUSS SUR [M+theta delta V] DES DEGRES DE LIBERTE 5 DES 3 VITESSES
C           (ELIMINATION DES 3 DL 5 10 15 INTERNES au TETRAEDRE)
            CALL GV3P1BP1( DMCN(MDVIEL), MCN(MNLGA) )
C           AVANT      5 + 5 + 5 + 4 = 19 DEGRES DE LIBERTE
C           MAINTENANT 4 + 4 + 4 + 4 = 16 DEGRES DE LIBERTE
            MNLGA = MNLGA + MOREE2 * 19 * 3

C           SECOND MEMBRE ELEMENTAIRE
            CALL F3SP1BP1( COORDP,
     %                     NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                     NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                     MCN(MNSE) )
C
C           ASSEMBLAGE DES DEGRES DE LIBERTE 5 10 15 DE SE DANS MCN(MNFG0)
            CALL GS3P1BP1( NTDLHB, NUELEM, MCN(MNSE),  MCN(MNFG0) )

         ELSE IF( NUTYEL .EQ. 15 ) THEN

C           TRIANGLE TAYLOR-HOOD P2 EN VITESSE et P1 EN PRESSION
C           ----------------------------------------------------
C           MATRICE ELEMENTAIRE DE MASSE
            CALL F2MP2P1( COORDP,
     %                    NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                    DMCN(MDMAEL) )

C           MATRICE ELEMENTAIRE DE VISCOSITE AVEC Integrale Eps P1 P1 dX
            CALL F2RP2P1( COORDP,
     %                    NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                    DMCN(MDVIEL) )
cccC           MATRICE ELEMENTAIRE DE VISCOSITE AVEC Integrale Eps Grad P1 Grad
ccc            CALL F2RP2P1GRAD( COORDP,
ccc     %                    NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
ccc     %                    DMCN(MDVIEL) )

C           SECOND MEMBRE ELEMENTAIRE
            CALL F2SP2P1( COORDP,
     %                    NOOBLA, NUMIOB(2), NUMAOB(2), MCN(MNDOEL(2)),
     %                    NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                    MCN(MNSE) )

         ELSE IF( NUTYEL .EQ. 20 ) THEN

C           TETRAEDRE TAYLOR-HOOD P2 EN VITESSE et P1 EN PRESSION
C           -----------------------------------------------------
C           MATRICE ELEMENTAIRE DE MASSE
            CALL F3MP2P1( COORDP,
     %                    NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                    DMCN(MDMAEL) )

C           MATRICE ELEMENTAIRE DE VISCOSITE
            CALL F3RP2P1( COORDP, MCN(MNDPDP),
     %                    NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                    DMCN(MDVIEL) )

C           SECOND MEMBRE ELEMENTAIRE
            CALL F3SP2P1( COORDP,
     %                    NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                    NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                    MCN(MNSE) )
C
         ENDIF

C        ASSEMBLAGE DES TABLEAUX ELEMENTAIRES DANS LES TABLEAUX GLOBAUX
C        ==============================================================
         IF( NORESO .EQ. 1 ) THEN

C           ASSEMBLAGE MATRICE PROFIL DE MASSE
C           ----------------------------------
            CALL ASMEPC( NBDLEF, NOGLDL,
     %                   NCODSM, DMCN(MDMAEL), DMCN(MDMAEL),
     %                   NCODSM, LPLIGN, MG  )

C           ASSEMBLAGE EN STOCKAGE PROFIL DE LA MATRICE DE VISCOSITE
C           --------------------------------------------------------
            CALL ASMEPC( NBDLEF, NOGLDL,
     %                   NCODSV, DMCN(MDVIEL), DMCN(MDVIEL),
     %                   NCODSV, LPLIGN, VG  )

         ELSE IF( NORESO .GE. 2 ) THEN

C           ASSEMBLAGE MATRICE MORSE DE MASSE
C           ---------------------------------
            CALL ASMEGC( NBDLEF, NOGLDL,
     %                   NCODSM, DMCN(MDMAEL), DMCN(MDMAEL),
     %                   NCODSM, LPLIGN, LPCOLO, MG )

C          ASSEMBLAGE EN STOCKAGE MORSE DE LA MATRICE DE VISCOSITE
C          -------------------------------------------------------
            CALL ASMEGC( NBDLEF, NOGLDL,
     %                   NCODSV, DMCN(MDVIEL), DMCN(MDVIEL),
     %                   NCODSV, LPLIGN, LPCOLO, VG )
         ENDIF

C        ASSEMBLAGE DU SECOND MEMBRE ELEMENTAIRE
C        ---------------------------------------
         CALL AS1BEBG( NBDLEF, NOGLDL, MCN(MNSE),  MCN(MNFG0) )

 22   CONTINUE

C     FIN DE LA BOUCLE SUR LES EF DE CONSTRUCTION DE [MG] [VG] et {FG0}
      IF( MNDPDP .GT. 0 ) CALL TNMCDS( 'REEL2', 3*10*3*10, MNDPDP )

      IF( NUTYEL .EQ. 15 .OR. NUTYEL .EQ. 20 ) THEN
C
C        EF TAYLOR-HOOD: COMBINAISONS LINEAIRES DES MATRICES GLOBALES
C        CONSTRUCTION DES MATRICES GLOBALES A PARTIR DE MG ET VG
C        [VG] = [ MASSE + DT THETA(1) VISCOSITE ]
C        [MG] = [ MASSE - DT THETA(0) VISCOSITE ]
C        =======================================================
C        VG = > MASSE + DT THETA(1) VISCOSITE
         CALL CL2VED ( NBCOVG, 1D0,         MG,
     %                         DT*THETA(1), VG,
     %                         VG )
C
C        MG => MASSE - DT THETA(0) VISCOSITE
         IF( THETA(0) .NE. 0D0 ) THEN
            CALL CL2VED ( NBCOVG, 1D0+THETA(0)/THETA(1), MG,
     %                            -THETA(0)/THETA(1),    VG,
     %                    MG)
ccc            call affvect( 'ME - DT (1-THETA) VE =', 100, MG )
ccc            call affvect( 'ME + DT THETA VE =', 100, VG )
         ENDIF

      ENDIF

C     CONSTRUCTION DES TABLEAUX DES NO ET VALEURS DES DEGRES DE LIBERTE
C     VITESSES-PRESSIONS FIXES AU TEMPS ACTUEL
C     ICI LES CONDITIONS AUX LIMITES PEUVENT DEPENDRE DU TEMPS
C     =================================================================
      CALL VIPRFX( RELMIN, NTDLHB, NDIM,    MNXYZN, NDDLNO,
     %             NBTYEL, MNNPEF, NUMIOB,  MNDOEL,
     %             NBVCFX, NBDLFX, MNNDLFX, MNVDLFX )

C     PRISE EN COMPTE DES CONDITIONS AUX LIMITES. DL FIXES POUR VG UNIQUEMENT
C     LE COEFFICIENT DIAGONAL D'UN DL FIXE EST MIS A LA VALEUR TGV
C     ATTENTION LA POSITION DES DL FIXES NE DOIT PAS CHANGER
C     PAR CONTRE LA VALEUR DE FIXATION PEUT DEPENDRE DU TEMPS ET CHANGER
C     =======================================================================
      IF( NORESO .EQ. 1 ) THEN
C
C        MATRICE PROFIL
         DO I=1, NBDLFX
C           LE NO GLOBAL DU DL FIXE
            NDL = MCN( MNNDLFX - 1 + I )
C           LE NO DU COEFFICIENT DIAGONAL NDL
            NDIAG = LPLIGN( NDL )
C           MODIFICATION DU COEFFICIENT DIAGONAL
C           CETTE VALEUR PEUT SERVIR DE MARQUEUR D'UN DL FIXE POUR VG
            VG( NDIAG ) = TGV
         ENDDO
C
      ELSE
C
C        MATRICE CONDENSEE
C        CONSTRUCTION DU TABLEAU DL FIXE OU NON?
         CALL TNMCDC( 'ENTIER', NTDLHB, MNNDFX )
C        A PRIORI TOUS LES DL SONT LIBRES => 0
         CALL AZEROI( NTDLHB, MCN(MNNDFX) )
C        MISE A 1 DES DL FIXES
         DO I=1, NBDLFX
C           LE NO GLOBAL DU DL FIXE
            NDL = MCN( MNNDLFX - 1 + I )
C           MISE A 1 DU DL FIXE NDL
            MCN( MNNDFX-1 + NDL ) = 1
         ENDDO
C
      ENDIF
C
cccc     verification: nombre de coefficients diagonaux<0 de VG
ccc      call diagne( ntdlhb, LPLIGN, VG, NBDINE, DIAMIN, DIAMAX )
C
C     TEMPS CPU DE FABRICATION DES TABLEAUX GLOBAUX
      DFABG = DINFO( 'DELTA CPU' )
C
      IF( NORESO .EQ. 1 ) THEN
C
C        FACTORISATION COMPLETE DE CROUT DE [VG] STOCKAGE PROFIL
C        =======================================================
         EPSFLU = 0.0
         CALL CRMC1D( LPLIGN, VG, NTDLHB, EPSFLU, 1,
     %                VG, IERR )
C
C        TEMPS CALCUL DE FORMATION DU SYSTEME LINEAIRE
         DFACTO = DINFO( 'DELTA CPU' )
C
         IF( IERR .NE. 0 ) THEN
C            MATRICE NON INVERSIBLE
             NBLGRC(NRERR) = 2
             IF( LANGAG .EQ. 0 ) THEN
                KERR(1) = 'ERREUR: MATRICE NON INVERSIBLE'
                KERR(2) = 'REVOYEZ LES CONDITIONS AUX LIMITES'
             ELSE
                KERR(1) = 'ERROR NOT INVERSIBLE MATRIX'
                KERR(2) = 'SEE AGAIN the BOUNDARY CONDITIONS'
             ENDIF
             CALL LEREUR
             IERR = 7
             GOTO 9999
         ENDIF
C
      ELSE
C
C        GRADIENT CONJUGUE PRECONDITIONNE DE [VG] STOCKAGE MORSE
C        =======================================================
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10016)
         ELSE
            WRITE(IMPRIM,20016)
         ENDIF
10016    FORMAT(/' RESOLUTION par GRADIENT CONJUGUE MATRICE CONDENSEE')
20016    FORMAT(/' SOLUTION by CONDENSED MATRIX CONJUGATE GRADIENT')
C
C        CALCUL DU SQUELETTE (POINTEURS DIAGONALE ET COLONNES)
C        DE LA MATRICE DE PRECONDITIONNEMENT DU GRADIENT CONJUGUE
C        --------------------------------------------------------
         IERR = 0
         PRINT*,'stokeins: LPLIGN(',NTDLHB,')=',LPLIGN(NTDLHB)
         CALL CALPRE( NTDLHB, NIVOGC, NCODSV, LPLIGN, LPCOLO,
     %                MNLPLC, MNLPCC, MNLPLU, COMP, IERR )
C
C        VERIFICATION DE LA FACTORISATION
         LOLPCC = MCN( MNLPLC + NTDLHB )
         IF( IERR .NE. 0 ) THEN
            WRITE(KERR(MXLGER)(1:2),'(I2)') NIVOGC
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'PROBLEME lors de la FACTORISATION de NIVEAU '
     %              // KERR(MXLGER)(1:2)
               KERR(2) = 'ABANDON de la METHODE du GRADIENT CONJUGUE'
            ELSE
               KERR(1)='PROBLEM of INCOMPLETE FACTORIZATION of LEVEL '
     %               // KERR(MXLGER)(1:2)
               KERR(2) = 'EXIT of the CONJUGATE GRADIENT METHOD'
            ENDIF
            CALL LEREUR
            IERR = 2
            GOTO 9999
         ENDIF
C
C        DECLARATION INITIALISATION DE LA MATRICE MORSE DE CONDITIONNEMENT
C        TEST DE LA PLACE MEMOIRE DISPONIBLE
         CALL TNMCMX( 'REEL2', MAXVAR )
         IF( MAXVAR .LT. LOLPCC ) THEN
C           PLACE MEMOIRE INSUFFISANTE
            NBLGRC(NRERR) = 2
            WRITE(KERR(MXLGER)(1:2),'(I2)') NIVOGC
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'stokeins: PLACE MEMOIRE MCN INSUFFISANTE'
               KERR(2) = 'DIMINUEZ la VALEUR du NIVEAU'
     %                 // KERR(MXLGER)(1:2)
            ELSE
               KERR(1) = 'stokeins: NOT ENOUGH MCN MEMORY'
               KERR(2) = 'DIMINISH the LEVEL VALUE'
     %                 // KERR(MXLGER)(1:2)
            ENDIF
            CALL LEREUR
            IERR = 2
            GOTO 9999
         ENDIF
C
C        DECLARATION DE LA MATRICE DE PRECONDITIONNEMENT
         CALL TNMCDC( 'REEL2', LOLPCC, MNVGC )
         IF( MNVGC .LE. 0 ) GOTO 9999
C
C        AFFICHAGE
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'CROUT FACTORISATION INCOMPLETE PAR NIVEAUX'
         ELSE
            KERR(1) = 'INCOMPLETE CROUT FACTORIZATION by LEVELS'
         ENDIF
         CALL LERESU
C
C        CONSTRUCTION DE LA MATRICE DE PRECONDITIONNEMENT
         CALL INCRCO( NTDLHB, MCN(MNNDFX), LPLIGN, LPCOLO, VG,
     &                MCN(MNLPLC), MCN(MNLPCC), MCN(MNVGC),
     &                IERR )
C
cccC        ESSAI INFRUCTUEUX
cccC        CONSTRUCTION DE LA MATRICE DE PRECONDITIONNEMENT HORS DL PRESSION
ccc         CALL INCRSP( NTDLHB, MCN(MNNDFX), NDIM, NBNOVI, NDDLNO,
ccc     &                LPLIGN, LPCOLO, VG,
ccc     &                MCN(MNLPLC), MCN(MNLPCC), MCN(MNVGC),
ccc     &                IERR )   => DIVERGE
C
C        FACTORISATION INCOMPLETE DE CROUT L D tL
         CALL INCRFA(NTDLHB, MCN(MNLPLC), MCN(MNLPCC), MCN(MNVGC), IERR)
C
C        TEMPS CALCUL DE FORMATION DU SYSTEME LINEAIRE
         DFACTO = DINFO( 'DELTA CPU' )
C
C        VERIFICATION DE LA STABILITE DE LA FACTORISATION INCOMPLETE
         IF( IERR .GT. 0 ) THEN
C           LA FACTORISATION EST INSTABLE : ABANDON DES CALCULS
            WRITE(KERR(MXLGER)(1:2),'(I2)') NIVOGC
            NBLGRC(NRERR) = 2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'FACTORISATION INSTABLE AU NIVEAU '
     &              // KERR(MXLGER)(1:2)
               KERR(2) = 'ABANDON DE LA METHODE DU GRADIENT CONJUGUE'
            ELSE
               KERR(1)='UNSTABLE INCOMPLETE FACTORIZATION at LEVEL '
     %              // KERR(MXLGER)(1:2)
               KERR(2) = 'EXIT of the CONJUGATE GRADIENT METHOD'
            ENDIF
            CALL LEREUR
            IERR = 2
            GO TO 9999
         ENDIF
      ENDIF

      IF( NORESO .EQ. 2 ) THEN

C        LES TABLEAUX AUXILIAIRES DE GCPRCR
         LOAUX = NTDLHB * 3
         CALL TNMCDC( 'REEL2', LOAUX, MNAUX )
         IF( MNAUX .LE. 0 ) GOTO 9999
         MNAUX2 = MNAUX
         MNAUX3 = MNAUX2 + NTDLHB * MOREE2
         MNAUX4 = MNAUX3 + NTDLHB * MOREE2
C
C        DECLARATION DE TABLEAUX AUXILIAIRES
         NBDIR = 3
C        NOMBRE DE DIRECTIONS >=1 POUR STABILISER LE GC
         LODIR = (NTDLHB+1) * NBDIR * 2
         CALL TNMCDC( 'REEL2', LODIR, MNBDIR )
         IF( MNBDIR .LE. 0 ) GOTO 9999
         MNDIR  = MNBDIR
         MNADIR = MNDIR  + NTDLHB * NBDIR * MOREE2
         MNDAD  = MNADIR + NTDLHB * NBDIR * MOREE2
         MNBET  = MNDAD  + NBDIR * MOREE2
C
         MOTSGC = NTDLHB + NBCOVG + (LOAUX+LODIR) * MOREE2 + MOTAEL
C
      ELSE IF( NORESO .GE. 3 ) THEN
C
C        LES TABLEAUX AUXILIAIRES DE CGSQSY ou BICG Stab
         LOAUX = NTDLHB * 7
         CALL TNMCDC( 'REEL2', LOAUX, MNAUX )
         IF( MNAUX .LE. 0 ) GOTO 9999
         MNAUX1 = MNAUX
         MNAUX2 = MNAUX1 + NTDLHB * MOREE2
         MNAUX3 = MNAUX2 + NTDLHB * MOREE2
         MNAUX4 = MNAUX3 + NTDLHB * MOREE2
         MNAUX5 = MNAUX4 + NTDLHB * MOREE2
         MNAUX6 = MNAUX5 + NTDLHB * MOREE2
         MNAUX7 = MNAUX6 + NTDLHB * MOREE2
C
         MOTSGC = NTDLHB + NBCOVG + LOAUX * MOREE2 + MOTAEL
C
      ENDIF


C     CONSTRUCTION DU TABLEAU NO NOEUD  => NO SOMMET
C     ==============================================
      CALL TNMCDC( 'ENTIER', NBNOVI, MNNOSO )
      CALL AZEROI( NBNOVI,  MCN(MNNOSO) )

      MN0 = MNELE+WUNDEL-1
      NBS = NDIM + 1
C     LA BOUCLE SUR LES ELEMENTS FINIS
      DO NUELEM = 1, NBELEM
         MN = MN0 + NUELEM
         DO I = 1, NBS
C           N NUMERO DU NOEUD SOMMET I
            N = MCN(MN)
            MCN( MNNOSO+N-1 ) = 1
            MN = MN + NBELEM
         ENDDO
      ENDDO

C     LE NUMERO DE SOMMET DE CHAQUE NOEUD (0 SI MILIEU D'ARETE ou BARYCENTRE)
      NBSOM = 0
      DO I = 1, NBNOVI
         IF( MCN(MNNOSO-1+I ) .GT. 0 ) THEN
            NBSOM = NBSOM + 1
            MCN( MNNOSO-1+I ) = NBSOM
         ENDIF
      ENDDO

      IF( NDIM .EQ. 2 ) THEN

C        TRIANGLES: RECUPERATION DU TMS DES ARETES DE L'OBJET 2D
c        construction imposee du tms arete
C        -------------------------------------------------------
         CALL LXTSOU( NTLXOB, 'ARETE', NTAROB, MNAROB )
         IF( NTAROB .GT. 0 ) CALL LXTSDS( NTLXOB, 'ARETE' )

C        LE TABLEAU N'EXISTE PAS => IL EST CREE
C        CALCUL PAR HACHAGE DES ARETES DE L'OBJET A PARTIR DE TOPO+NPEF"...
C        NECESSAIRE POUR CONNAITRE LES EF ADJACENTS PAR ARETE
         CALL HAC2AF( KNOMOB, 3, NTAROB, MNAROB, IERR )
         IF( IERR .NE. 0 .OR. NTAROB .LE. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'OBJET SANS TABLEAU DES ARETES'
            ELSE
               KERR(1) = 'OBJECT WITHOUT THE ARRAY OF EDGES'
            ENDIF
            CALL LEREUR
            GOTO 9999
         ENDIF

C        LE NOMBRE D'ENTIERS PAR ARETE
         MOARET = MCN( MNAROB + WOARET )
C        LA MAJORATION DU NOMBRE D'ARETES
         MXARET = MCN( MNAROB + WXARET )
C        LE NOMBRE D'ARETES FRONTALIERES NON SUR LIGNES UTILISATEUR
C        NBARFB = MCN( MNAROB + WBARFB )
C        LE NOMBRE D'ARETES INTERFACES   NON SUR LIGNES UTILISATEUR
C        NBARIN = MCN( MNAROB + WBARIN )
C        LE NUMERO MINIMAL DE LIGNE DE L'OBJET
         NUMILF = MCN( MNAROB + WUMILF )
C        LE NUMERO MAXIMAL DE LIGNE DE L'OBJET
         NUMXLF = MCN( MNAROB + WUMXLF )
C        LE NUMERO DE LA PREMIERE ARETE FRONTALIERE NON SUR LIGNES DE L'OBJET
C        L1ARFB = MCN( MNAROB + W1ARFB )
C        LE NUMERO DE LA PREMIERE ARETE INTERFACE NON SUR LIGNES DE L'OBJET
C        L1ARIN = MCN( MNAROB + W1ARIN )

C        ADRESSE MCN DU 1-ER MOT DU TABLEAU LARETE
         MNLARE = MNAROB + W1LGFR + NUMXLF - NUMILF + 1
C        LARETE : TABLEAU DES ARETES DU MAILLAGE
C        LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE (0 SI PAS D'ARETE)
C        LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C        LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C        LARETE(4,I)= NUMERO DU 1-ER TRIANGLE CONTENANT CETTE ARETE
C                   + NO TYPE EF (1 a NBTYEF) * 100 000 000
C                     0 SI PAS DE 1-ER  TRIANGLE
C        LARETE(5,I)= NUMERO DU 2-EME TRIANGLE CONTENANT CETTE ARETE
C                   + NO TYPE EF (1 a NBTYEF) * 100 000 000
C                     0 SI PAS DE 2-EME TRIANGLE
C        LARETE(6,I)= NUMERO DANS LARETE DE L'ARETE SUIVANTE
C                 SOIT DANS LE CHAINAGE D'UNE LIGNE J ENTRE NUMILF ET NUMX
C                 SOIT DANS LE CHAINAGE DES ARETES FRONTALIERES
C                 0 SI C'EST LA DERNIERE

      ELSE

C        TETRAEDRES: RECUPERATION DU TMS DES FACES DE L'OBJET 3D
C        CONSTRUCTION IMPOSEE DU TMS FACE
C        -------------------------------------------------------
         CALL LXTSOU( NTLXOB, 'FACE', NTFAOB, MNFAOB )
         IF( NTFAOB .GT. 0 ) CALL LXTSDS( NTLXOB, 'FACE' )

C        LE TABLEAU N'EXISTE PAS => IL EST CREE
C        CALCUL PAR HACHAGE DES FACES DE L'OBJET A PARTIR DE TOPO+NPEF"...
C        NECESSAIRE POUR CONNAITRE LES EF ADJACENTS PAR FACE
         CALL HACHOB( KNOMOB, 4, NTFAOB, MNFAOB, IERR )
         IF( IERR .NE. 0 .OR. NTFAOB .LE. 0 ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'OBJET SANS TABLEAU DES FACES'
            ELSE
               KERR(1) = 'OBJECT WITHOUT THE ARRAY OF FACES'
            ENDIF
            CALL LEREUR
            GOTO 9999
         ENDIF

C        LE NOMBRE D'ENTIERS PAR FACE
         MOFACE = MCN( MNFAOB + WOFACE )
C        LA MAJORATION DU NOMBRE DE FACES DU TABLEAU LFACES
         MXFACE = MCN( MNFAOB + WXFACE )
C        LE NUMERO DE LA PREMIERE FACE FRONTALIERE (DANS UN SEUL EF)
         L1FAFR = MCN( MNFAOB + W1FAFR )
C        LE NOMBRE DE FACES FRONTALIERES
         NBFAFR = MCN( MNFAOB + WBFAFR )
C        ADRESSE MCN DU 1-ER MOT DU TABLEAU LFACES
         MNLFAC = MNFAOB + WFACES

C        CREATION DU HACHAGE DES ARETES DES FACES FRONTALIERES DE L'OBJET
         CALL HACHAF( KNOMOB, 0, NTFAOB, MNFAOB,
     %                NTAFOB, MNAFOB, I )

C        LE NOMBRE D'ENTIERS PAR ARETE FRONTALIERE
         MOARFR = MCN( MNAFOB + WOARFR )
C        LA MAJORATION DU NOMBRE DES ARETES FRONTALIERES
         MXARFR = MCN( MNAFOB + WXARFR )
C        LE NUMERO DANS LAREFR DE LA PREMIERE ARETE FRONTALIERE
         L1ARFR = MCN( MNAFOB + W1ARFR )
C        LE NOMBRE D'ARETES FRONTALIERES DANS LE CHAINAGE
         NBARFR = MCN( MNAFOB + WBARFR )

C        RESERVATION DES TABLEAUX NO XYZ POUR LE TRI
         CALL TNMCDC( 'ENTIER',  NBARFR, MNNBFR )
         CALL TNMCDC( 'REEL'  ,3*NBARFR, MNBAFR )

C        ATTENTION: MODIFICATION TEMPORAIRE DU TABLEAU LFACES
C                   NECESSAIRE POUR IDENTIFIER VITE UNE FACE FRONTALIERE
C        LES FACES FRONTALIERES RECUPERENT ZERO COMME SECOND EF
C        AU LIEU DU CHAINAGE DES FACES FRONTALIERES DANS LE TABLEAU LFACES
C        LFACES(1,I)= NO DU 1-ER  SOMMET DE LA FACE
C        LFACES(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C        LFACES(3,I)= NO DU 3-EME SOMMET DE LA FACE
C        LFACES(4,I)= NO DU 4-EME SOMMET DE LA FACE
C                     0 SI TRIANGLE
C        LFACES(5,I)= CHAINAGE HACHAGE SUR FACE SUIVANTE
C        LFACES(6,I)= NUMERO DU 1-ER  CUBE CONTENANT CETTE FACE
C                     0 SI PAS DE 1-ER  CUBE
C        LFACES(7,I)= NUMERO DU 2-EME CUBE CONTENANT CETTE FACE
C                     ou CHAINAGE SUR LA FACE FRONTALIERE SUIVANTE
C        Cette valeur est remise a zero dans ce sous programme
C        puis remise au lien sur la face frontaliere suivante
C        LFACES(8,I)= NUMERO DE FACE A TANGENTES
         CALL MAZFAF( MOFACE, MXFACE, MCN(MNLFAC), L1FAFR )

C        DEFINITION DE LA VISEE AXONOMETRIQUE
C        ------------------------------------
C        POINT VU LE CENTRE DE L'HEXAEDRE ENGLOBANT
         AXOPTV(1) = ( COOEXT(1,1) + COOEXT(1,2) ) * 0.5
         AXOPTV(2) = ( COOEXT(2,1) + COOEXT(2,2) ) * 0.5
         AXOPTV(3) = ( COOEXT(3,1) + COOEXT(3,2) ) * 0.5
C        POSITION DE L'OEIL
         AXOEIL(1) = AXOPTV(1)
         AXOEIL(2) = AXOPTV(2) - 0.52 * ( COOEXT(2,2) - COOEXT(2,1) )
         AXOEIL(3) = AXOPTV(3)
C        LARGEUR/2 et HAUTEUR/2 de la FENETRE
         AXOLAR = ( COOEXT(1,2) - COOEXT(1,1) ) * 0.5
         AXOHAU = ( COOEXT(3,2) - COOEXT(3,1) ) * 0.5
C        PAS DE PLAN ARRIERE ET AVANT
         AXOARR = 0
         AXOAVA = 0
         IF( INTERA .GE. 1 )
     %   CALL AXONOMETRIE(AXOPTV, AXOEIL, AXOLAR, AXOHAU, AXOARR,AXOAVA)

      ENDIF

C     LES CARACTERISTIQUES VFAS DE L'ELEMENT FINI TRIANGLE ou TETRAEDRE
      IF( NUTYEL .EQ. 13 ) THEN

C        TRIANGLE DE BREZZI-FORTIN
         NDIM   = 2
C        NOMBRE DE NO DE SOMMETS D'UN EF DANS NPEF
         NBS1EF = 3
C        NOMBRE DE SOMMETS DE L'EF
         NBPSEF = 3
C        NOMBRE D'ARETES D'UN EF
         NBLAEF = 3
C        NOMBRE DE FACES D'UN EF
         NBSFEF = 1
C        NOMBRE DE VOLUME D'UN EF
         NBVVEF = 0

      ELSE IF( NUTYEL .EQ. 19 ) THEN

C        TETRAEDRE DE BREZZI-FORTIN
         NDIM   = 3
C        NOMBRE DE NO DE SOMMETS D'UN EF DANS NPEF
         NBS1EF = 4
C        NOMBRE DE SOMMETS DE L'EF
         NBPSEF = 4
C        NOMBRE D'ARETES D'UN EF
         NBLAEF = 6
C        NOMBRE DE FACES D'UN EF
         NBSFEF = 4
C        NOMBRE DE VOLUME D'UN EF
         NBVVEF = 1

      ELSE IF( NUTYEL .EQ. 15 ) THEN

C        TRIANGLE DE TAYLOR-HOOD
         NDIM   = 2
C        NOMBRE DE NO DE NOEUDS D'UN EF DANS NPEF
         NBNOEF = 6
C        NOMBRE DE SOMMETS DE L'EF
         NBPSEF = 3
C        NOMBRE D'ARETES D'UN EF
         NBLAEF = 3
C        NOMBRE DE FACES D'UN EF
         NBSFEF = 1
C        NOMBRE DE VOLUME D'UN EF
         NBVVEF = 0

      ELSE IF( NUTYEL .EQ. 20 ) THEN

C        TETRAEDRE DE TAYLOR-HOOD
         NDIM   = 3
C        NOMBRE DE NO DE NOEUDS D'UN EF DANS NPEF
         NBNOEF = 10
C        NOMBRE DE SOMMETS DE L'EF
         NBPSEF = 4
C        NOMBRE D'ARETES D'UN EF
         NBLAEF = 6
C        NOMBRE DE FACES D'UN EF
         NBSFEF = 4
C        NOMBRE DE VOLUME D'UN EF
         NBVVEF = 1

      ENDIF

C     CONSTRUCTION DES TABLEAUX VFAS SUR LE MAILLAGE
C     en 3D: NUVVEF(NBELEM), NUSFEF(NBSFEF,NBELEM), 
C            NULAEF(NBLAEF,NBELEM), NUPSEF(NBPSEF,NBELEM)
C     en 2D: NUSFEF(NBELEM), NULAEF(NBLAEF,NBELEM), NUPSEF(NBPSEF,NBELEM)
      CALL EFVFAS( MNELE, NBVVEF, NBSFEF, NBLAEF, NBPSEF,
     %                    MNVVEF, MNSFEF, MNLAEF, MNPSEF, IERR )
      IF( IERR .NE. 0 ) GOTO 9993


C     AFFICHAGE DU VECTEUR"VITESSEPRESSION NOVVIPR AU TEMPS INITIAL
      CALL AFVIPR( NUTYEL, NDIM, NBNOVI, MNXYZN, NDDLNO,
     %             TEMPS,  NTDLVP, MIN(10,NBNOVI), MCN(MNVXYZP0),
     %             VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )

C     STOCKAGE DES RESULTATS VP MAX MIN AU TEMPS INITIAL DU CALCUL
C     ------------------------------------------------------------
      VPMIMX(1,NBPAST) = TEMPS
      VPMIMX(2,NBPAST) = VITMOY
      VPMIMX(3,NBPAST) = VITMAX
      VPMIMX(4,NBPAST) = PREMOY
      VPMIMX(5,NBPAST) = PREMAX - PREMIN
      FLUNEG = 0D0
      FLUPOS = 0D0
      VPMIMX(6,NBPAST) = FLUNEG
      VPMIMX(7,NBPAST) = FLUPOS
      VPMIMX(8,NBPAST) = NBPAST

C     BILAN DE L'INSTANT INITIAL
C     LE PROCHAIN TEMPS POUR STOCKER LE VECTEUR VITESSEPRESSION
      TSTOC = TPSINI + DTSTOC

C     ############################################################
C     ##                                                        ##
C     ##  LA BOUCLE EN TEMPS AVEC UN PAS DE TEMPS CONSTANT = DT ##
C     ##                                                        ##
C     ############################################################

C     MNFG0 CONTIENT  F(t0) SECOND MEMBRE    A L'INSTANT INITIAL
C     MNVXYZP0 CONTIENT  U0    VITESSEPRESSION  A L'INSTANT INITIAL
C     VG       CONTIENT  MASSE + DT THETA(1) VISCOSITE FACTORISEE
C     MG       CONTIENT  MASSE - DT THETA(0) VISCOSITE

C     LE NOUVEAU TEMPS OU SE FAIT LE CALCUL
 100  TEMPS = TEMPS + DT
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10100) TEMPS
      ELSE
         WRITE(IMPRIM,20100) TEMPS
      ENDIF

10100 FORMAT(/' Au TEMPS',G14.6,': CALCUL des VITESSES-PRESSIONS '/
     %120('='))
20100 FORMAT(/' At TIME',G14.6,': COMPUTATION of VELOCITY-PRESSURE '/
     %120('='))

C     ============================================================
C     LA GENERATION DU SECOND MEMBRE ELEMENTAIRE A L'INSTANT TEMPS
C     EN 2 PARTIES DISTINCTES : FG0(tn) + DT THETA FG1(tn+1)
C     FG0 = DT (1-THETA) Fn + [MASSE - (1-THETA) DT VISCOSITE] U0
C     FG1 = Fn+1 A CALCULER EF PAR EF
C     ============================================================
C     UG1 = [MASSE - (1-THETA) DT VISCOSITE] UG0
      IF( NORESO .EQ. 1 ) THEN
         CALL MAPRVE( 0,      1D0,         NTDLHB,
     %                NCODSV, LPLIGN, MG, MCN(MNVXYZP0),
     %                MCN(MNVXYZP1) )
      ELSE
         CALL MAGCVX( NTDLHB, MCN(MNNDFX), LPLIGN, LPCOLO, MG,
     %                MCN(MNVXYZP0), MCN(MNVXYZP1) )
      ENDIF

C     LA GENERATION DU SECOND MEMBRE ELEMENTAIRE FG1=Fn+1 A L'INSTANT TEMPS
C     =====================================================================
      CALL AZEROD( NTDLVP, DMCN((MNFG1+1)/MOREE2) )
C
C     LA BOUCLE SUR LES ELEMENTS FINIS
      MNLGM = MNLGMM
      DO 122 NUELEM = 1, NBELEM
C
C        NO DES NOEUDS DE L'ELEMENT FINI NUELEM
         CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOEF )
C
C        NO DES POINTS LIGNES SURFACES VOLUMES DES SOMMETS ARETES FACES VOLUME
         CALL EFPLSV( MNELE , NUELEM,
     %                NOVCEL, NOSFEL, NOLAEL, NOPSEL,
     %                NOOBVC, NOOBSF, NOOBLA, NOOBPS )
C
C        INTERPOLATION DU FLUIDE avec F: echapeau->e  P1 ndim
C        COORDONNEES DES POINTS DE L'ELEMENT FINI
         CALL EFXYZP( NDIM, MNXYZN, NBELEM, NUELEM, MNPGEL, NBNOEF,
     %                COORDP )
C
C        NOGLDL: NUMERO GLOBAL DES DEGRES DE LIBERTE DE L'ELEMENT FINI
         CALL NUGDLE( NDIM,   NUTYEL, NBDLMX, NBNOEU,
     %                NONOEF, NDDLNO,
     %                NBDLEF, NOGLDL, IERR )
C
         IF( NUTYEL .EQ. 13 ) THEN
C
C           TRIANGLE BREZZI-FORTIN P1BULLEP3 EN VITESSE et P1 EN PRESSION
C           -------------------------------------------------------------
C           UG1 = [MASSE - (1-THETA) DT VISCOSITE] UG0 SUR LES DL BARYCENTRES
            CALL GP2P1BP1(NTDLHB,NUELEM,NONOEF,MCN(MNLGM),MCN(MNVXYZP0),
     %                    MCN(MNVXYZP1) )
            MNLGM = MNLGM + MOREE2 * 11 * 2
C
C           SECOND MEMBRE ELEMENTAIRE AVEC 11 DEGRES DE LIBERTE
            CALL F2SP1BP1( COORDP,
     %                     NOOBLA, NUMIOB(2), NUMAOB(2), MCN(MNDOEL(2)),
     %                     NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                     MCN(MNSE) )
C
C           ASSEMBLAGE DES DEGRES DE LIBERTE 4 ET 8 DE SE DANS MCN(MNFG1)
C           COMPRESSION DE 11 A 9 DEGRES DE LIBERTE DE SE
            CALL GS2P1BP1( NTDLHB, NUELEM, MCN(MNSE),  MCN(MNFG1) )
C
         ELSE IF( NUTYEL .EQ. 19 ) THEN
C
C           TETRAEDRE BREZZI-FORTIN P1BULLEP4 EN VITESSE et P1 EN PRESSION
C           --------------------------------------------------------------
C           UG1 = [MASSE - (1-THETA) DT VISCOSITE] UG0 SUR LES DL BARYCENTRES
            CALL GP3P1BP1(NTDLHB,NUELEM,NONOEF,MCN(MNLGM),MCN(MNVXYZP0),
     %                    MCN(MNVXYZP1) )
            MNLGM = MNLGM + MOREE2 * 19 * 3
C
C           SECOND MEMBRE ELEMENTAIRE
            CALL F3SP1BP1( COORDP,
     %                     NOOBSF, NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                     NOOBVC, NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                     MCN(MNSE) )
C
C           ASSEMBLAGE DES DEGRES DE LIBERTE 5 10 15 DE SE DANS MCN(MNFG1)
C           COMPRESSION DE 19 A 16 DEGRES DE LIBERTE DE SE
            CALL GS3P1BP1( NTDLHB, NUELEM, MCN(MNSE),  MCN(MNFG1) )
C
         ELSE IF( NUTYEL .EQ. 15 ) THEN
C
C           TRIANGLE TAYLOR-HOOD P2 EN VITESSE et P1 EN PRESSION
C           ----------------------------------------------------
C           SECOND MEMBRE ELEMENTAIRE
            CALL F2SP2P1( COORDP,
     %                    NOOBLA,NUMIOB(2),NUMAOB(2),MCN(MNDOEL(2)),
     %                    NOOBSF,NUMIOB(3),NUMAOB(3),MCN(MNDOEL(3)),
     %                    MCN(MNSE) )
C
         ELSE IF( NUTYEL .EQ. 20 ) THEN
C
C           TETRAEDRE TAYLOR-HOOD P2 EN VITESSE et P1 EN PRESSION
C           -----------------------------------------------------
C           SECOND MEMBRE ELEMENTAIRE
            CALL F3SP2P1( COORDP,
     %                    NOOBSF,NUMIOB(3), NUMAOB(3), MCN(MNDOEL(3)),
     %                    NOOBVC,NUMIOB(4), NUMAOB(4), MCN(MNDOEL(4)),
     %                    MCN(MNSE) )
C
         ENDIF
C
C        ASSEMBLAGE DU VECTEUR ELEMENTAIRE DANS LE VECTEUR GLOBAL FG1
C        ============================================================
         CALL AS1BEBG( NBDLEF, NOGLDL, MCN(MNSE),  MCN(MNFG1) )
C
 122  CONTINUE
C
C     FIN DE LA BOUCLE SUR LES EF POUR LA CONSTRUCTION DE FG1
C
C     COMBINAISON LINEAIRE DES 2 VECTEURS GLOBAUX POUR LE SECOND MEMBRE
C     =================================================================
C     FG0 = DT (1-THETA) Fn + [MASSE - DT (1-THETA) VISCOSITE] UG0
      MDFG0 = ( MNFG0 + 1 ) / MOREE2
      MDVXYZP1 = ( MNVXYZP1 + 1 ) / MOREE2
      CALL CL2VED( NTDLVP, DT*THETA(0), DMCN(MDFG0),
     %                     1D0,         DMCN(MDVXYZP1), DMCN(MDFG0) )
C     FG1=Fn+1
C     => UG1 = DT THETA FG1 + FG0
      MDFG1 = (MNFG1+1) / MOREE2
      CALL CL2VED( NTDLVP, DT*THETA(1), DMCN(MDFG1),
     %                     1D0,         DMCN(MDFG0), DMCN(MDVXYZP1) )
C
C     ICI UG1 = DT THETA FG1 + DT (1-THETA) FG0 +
C             + [MASSE - DT (1-THETA) VISCOSITE] UG0
C
      IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN
C
C        LA BOUCLE SUR LES ELEMENTS FINIS BREZZI-FORTIN SEULEMENT
C        POUR FAIRE GAUSS SUR LE SECOND MEMBRE DU SYSTEME LINEAIRE A RESOUDRE
C        ====================================================================
         MNLGA = MNLGAU
         DO 132 NUELEM = 1, NBELEM
C
C           NO DES NOEUDS DE L'ELEMENT FINI NUELEM
            CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOEF )
C
            IF( NUTYEL .EQ. 13 ) THEN
C
C              TRIANGLE BREZZI-FORTIN P1BULLEP3 EN VITESSE et P1 EN PRESSION
C              -------------------------------------------------------------
C              METHODE DE GAUSS SUR LES DL 4 8 DU BARYCENTRE SUR LE SECOND
C              MEMBRE DU SYSTEME A RESOUDRE
               CALL GR2P1BP1( NTDLHB, NUELEM, NONOEF, MCN(MNLGA),
     %                        MCN(MNVXYZP1) )
               MNLGA = MNLGA + MOREE2 * 11 * 2
            ELSE IF( NUTYEL .EQ. 19 ) THEN
C
C              TETRAEDRE BREZZI-FORTIN P1BULLEP4 EN VITESSE et P1 EN PRESSION
C              --------------------------------------------------------------
C              METHODE DE GAUSS SUR LES DL 5 10 15 DU BARYCENTRE SUR LE SECOND
C              MEMBRE DU SYSTEME A RESOUDRE
               CALL GR3P1BP1( NTDLHB, NUELEM, NONOEF, MCN(MNLGA),
     %                        MCN(MNVXYZP1) )
               MNLGA = MNLGA + MOREE2 * 19 * 3
C
            ENDIF
C
 132     CONTINUE
C
      ENDIF
C
C     CONSTRUCTION DES TABLEAUX DES NO ET VALEURS DES DEGRES DE LIBERTE
C     VITESSES-PRESSIONS FIXES AU TEMPS ACTUEL
C     ICI LES CONDITIONS AUX LIMITES PEUVENT DEPENDRE DU TEMPS
C     =================================================================
      CALL VIPRFX( RELMIN, NTDLHB, NDIM,    MNXYZN, NDDLNO,
     %             NBTYEL, MNNPEF, NUMIOB,  MNDOEL,
     %             NBVCFX, NBDLFX, MNNDLFX, MNVDLFX )
C
C     PRISE EN COMPTE DES CONDITIONS AUX LIMITES SUR LE SECOND MEMBRE
C     ===============================================================
      DO 300 I=1, NBDLFX
C        LE NO DU DL FIXE
         NDL = MCN( MNNDLFX - 1 + I )
C        LA VALEUR A FIXER
         DVAL = DMCN( (MNVDLFX-1)/2 + I )
C        MODIFICATION DU SECOND MEMBRE
         IF( NORESO .EQ. 1 ) THEN
C           CROUT SUR LE SYSTEME AVEC TGV SUR LA DIAGONALE DES DL FIXES
            DMCN( (MNVXYZP1-1)/2 + NDL ) = TGV * DVAL
         ELSE
C           TRAITEMENT COMME L'IDENTITE SUR LA LIGNE DE LA MATRICE
            DMCN( (MNVXYZP1-1)/2 + NDL ) = DVAL
         ENDIF
 300  CONTINUE
C
      IF( NORESO .EQ. 1 ) THEN
C
C        --------------------------------------------------------
C        RESOLUTION DU SYSTEME PROFIL FACTORISE PAR CROUT COMPLET
C        --------------------------------------------------------
C
C        DESCENTE DIVISION DIAGONALE et REMONTEE DU SYSTEME DE MATRICE
C        FACTORISEE COMPLETEMENT
         CALL DRCRPR( NTDLHB, NCODSV, LPLIGN, VG, MCN(MNVXYZP1), 3,
     %                MCN(MNVXYZP1), IERR )
C
      ELSE IF( NORESO .EQ. 2 ) THEN
C
C        ----------------------------------------------------------------
C        RESOLUTION DU SYSTEME PAR GRADIENT CONJUGUE AVEC CROUT INCOMPLET
C        ----------------------------------------------------------------
C
C        LE VECTEUR INITIAL DU GC EST LE DERNIER VECTEUR CALCULE
C        MISE A ZERO DU TABLEAU AUXILIAIRE
         CALL AZEROD( LOAUX, DMCN((MNAUX+1)/MOREE2) )
C        MISE A ZERO DU TABLEAU DES DIRECTIONS
         CALL AZEROD( LODIR, DMCN((MNBDIR+1)/MOREE2) )
C        RESOLUTION GC PRECONDITIONNE PAR CROUT INCOMPLET
         CALL GCPRCR( NTDLHB,      1, NBDIR, MCN(MNNDFX),
     %                LPLIGN, LPCOLO, VG,  MCN(MNVXYZP1),
     %                MCN(MNLPLC), MCN(MNLPCC), MCN(MNVGC),
     %                MCN(MNVXYZP0),MCN(MNAUX2),MCN(MNAUX3),MCN(MNAUX4),
     %                MCN(MNDIR),  MCN(MNADIR), MCN(MNDAD), MCN(MNBET),
     %                MCN(MNVXYZP1),  IERR )
ccc      call affvect( 'Apres GCPRCR UG1=', NTDLHB, MCN(MNVXYZP1) )
C
C        VERIFICATION DE LA CONVERGENCE DU GC
         IF( IERR .NE. 0 ) THEN
C           IL N'Y A PAS EU CONVERGENCE DU GC
C           LA METHODE DU GC NE CONVERGE PAS => ABANDON DES CALCULS
            WRITE(KERR(MXLGER)(1:8),'(I8)') NIVOGC
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'NON CONVERGENCE AU NIVEAU '//KERR(MXLGER)(1:8)
               KERR(2) = 'ABANDON DE LA METHODE DU GRADIENT CONJUGUE'
        KERR(3)='ESSAYER FACTORISATION COMPLETE DE CROUT MATRICE PROFIL'
            ELSE
               KERR(1) = 'NO CONVERGENCE at LEVEL '// KERR(MXLGER)(1:8)
               KERR(2) = 'EXIT of the CONJUGATE GRADIENT METHOD'
             KERR(3)='TRY FULL CROUT FACTORIZATION with SKYLINE STORAGE'
            ENDIF
            CALL LEREUR
            IERR = 9
            GOTO 9999
         ENDIF
C
      ELSE IF( NORESO .EQ. 3 ) THEN
C
C        ---------------------------------------------------------------
C        RESOLUTION DU SYSTEME PAR DOUBLE GRADIENT CONJUGUE ACCELERE CGS
C        ---------------------------------------------------------------
C
C        LE VECTEUR INITIAL DU GC EST LE DERNIER VECTEUR CALCULE
C        MISE A ZERO DU TABLEAU AUXILIAIRE
         CALL AZEROD( LOAUX, DMCN((MNAUX+1)/MOREE2) )
C
C        RESOLUTION PAR ''CONJUGATE GRADIENTS SQUARED'' SUR MATRICE SYMETRIQUE
         CALL CGSQSY( NTDLHB, MCN(MNNDFX),
     &                LPLIGN, LPCOLO, VG, MCN(MNVXYZP1),
     &                MCN(MNLPLC), MCN(MNLPCC), MCN(MNVGC),
     &                MCN(MNVXYZP0),  MCN(MNAUX),
     &                MCN(MNVXYZP1),  IERR )
C
C        VERIFICATION DE LA CONVERGENCE DU GC
         IF( IERR .NE. 0 ) THEN
C           IL N'Y A PAS EU CONVERGENCE DU GC
C           LA METHODE DU GC NE CONVERGE PAS => ABANDON DES CALCULS
            WRITE(KERR(MXLGER)(1:8),'(I8)') NIVOGC
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'NON CONVERGENCE AU NIVEAU '//KERR(MXLGER)(1:8)
               KERR(2) = 'ABANDON DE LA METHODE CGS'
        KERR(3)='ESSAYER FACTORISATION COMPLETE DE CROUT MATRICE PROFIL'
            ELSE
               KERR(1) = 'NO CONVERGENCE at LEVEL '//KERR(MXLGER)(1:8)
               KERR(2) = 'EXIT of the CGS METHOD'
             KERR(3)='TRY FULL CROUT FACTORIZATION with SKYLINE STORAGE'
          ENDIF
          IERR = 9
          GOTO 9999
       ENDIF

      ELSE IF( NORESO .EQ. 4 ) THEN

C        ---------------------------------------------------------------------
C        RESOLUTION DU SYSTEME PAR DOUBLE GRADIENT CONJUGUE STABILISE BiCGStab
C        ---------------------------------------------------------------------

C        LE VECTEUR INITIAL DU GC EST LE DERNIER VECTEUR CALCULE
C        MISE A ZERO DU TABLEAU AUXILIAIRE
         CALL AZEROD( LOAUX, DMCN((MNAUX+1)/MOREE2) )

C           RESOLUTION ''BI-CONJUGATE GRADIENT STABILISED'' SUR MATRICE SYMETRIQ
            CALL BICGST( NTDLHB, MCN(MNNDFX),
     &           LPLIGN, LPCOLO, VG, MCN(MNVXYZP1),
     &           MCN(MNLPLC), MCN(MNLPCC), MCN(MNVGC),  MCN(MNVXYZP0),
     &           MCN(MNAUX1), MCN(MNAUX2), MCN(MNAUX3), MCN(MNAUX4),
     &           MCN(MNAUX5), MCN(MNAUX6), MCN(MNAUX7),
     &           MCN(MNVXYZP1),  IERR )

C        VERIFICATION DE LA CONVERGENCE DU BIGCStab
         IF( IERR .NE. 0 ) THEN
C           IL N'Y A PAS EU CONVERGENCE DU GC
C           LA METHODE DU GC NE CONVERGE PAS => ABANDON DES CALCULS
            WRITE(KERR(MXLGER)(1:8),'(I8)') NIVOGC
            NBLGRC(NRERR) = 3
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'NON CONVERGENCE AU NIVEAU '//KERR(MXLGER)(1:8)
               KERR(2) = 'ABANDON DE LA METHODE BiCGStab'
        KERR(3)='ESSAYER FACTORISATION COMPLETE DE CROUT MATRICE PROFIL'
            ELSE
               KERR(1) = 'NO CONVERGENCE at LEVEL '//KERR(MXLGER)(1:8)
               KERR(2) = 'EXIT of the BiCGStab METHOD'
             KERR(3)='TRY FULL CROUT FACTORIZATION with SKYLINE STORAGE'
          ENDIF
          IERR = 9
          GOTO 9999
       ENDIF

      ENDIF

      IF( NUTYEL .EQ. 13 .OR. NUTYEL .EQ. 19 ) THEN

C        TRIANGLE ou TETRAEDRE BREZZI-FORTIN
C        CALCUL DES DEGRES DE LIBERTE ELIMINES DANS LES EF PAR LA
C        METHODE DE GAUSS POUR TOUS LES ELEMENTS FINIS DU MAILLAGE
C        =========================================================
         CALL DLVBBF( NDIM, MNNPEF, NBDLMX, NBELEM, MCN(MNLGAU),
     %                NTDLHB, NTDLVP, MCN(MNVXYZP1) )

      ENDIF

      IF ( IEBLPR .LE. 0 ) THEN

C        PAS DE CONDITION AUX LIMITES SUR LA PRESSION
C        => ELLE EST DEFINIE A UNE CONSTANTE PRES
C        RECHERCHE DE LA PLUS BASSE PRESSION POUR SERVIR DE TRANSLATION
C        ELLE EST SOUSTRAITE DES AUTRES PRESSIONS ET DEVIENT ZERO
         MN     = (MNVXYZP1-1)/2
         PREMIN = 1D100
         NDL1   = NDDLNO(0)
         DO N=1,NBNOEU
            NDL2 = NDDLNO(N)
            IF( NDL2 - NDL1 .EQ. NDIM+1 ) THEN
               IF( DMCN(MN+NDL2) .LT. PREMIN ) THEN
                  PREMIN = DMCN(MN+NDL2)
               ENDIF
            ENDIF
            NDL1 = NDL2
         ENDDO
C
C        TRANSLATION DE PREMIN A ZERO
         NDL1 = NDDLNO(0)
         DO N=1,NBNOEU
            NDL2 = NDDLNO(N)
            IF( NDL2 - NDL1 .EQ. NDIM+1 ) THEN
               DMCN(MN+NDL2) = DMCN(MN+NDL2) - PREMIN
            ENDIF
            NDL1 = NDL2
         ENDDO
C
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*) 'Au TEMPS',TEMPS,' PRESSION MIN',PREMIN,
     %                      ' est TRANSLATEE a ZERO'
         ELSE
            WRITE(IMPRIM,*) 'At TIME',TEMPS,' PRESSURE MIN',PREMIN,
     %                      ' is TRANSLATED to ZERO'
         ENDIF
C
      ENDIF
C
C     AFFICHAGE DU VECTEUR"VITESSEPRESSION NOVVIPR AU TEMPS
C     -----------------------------------------------------
      CALL AFVIPR( NUTYEL, NDIM, NBNOVI, MNXYZN, NDDLNO,
     %             TEMPS,  NTDLVP, MIN(32,NBNOVI), MCN(MNVXYZP1),
     %             VITMIN, VITMAX, VITMOY, PREMIN, PREMAX, PREMOY )
C
      IF( NUTYEL .EQ. 19 .OR. NUTYEL .EQ. 20 ) THEN

C        FLUX DE LA VITESSE A TRAVERS LES FACES DES SURFACES DE L'OBJET
C        --------------------------------------------------------------
C        MNELE=MCN( MNNPEF ): ADRESSE DU TABLEAU NPEF"TYPE EF
         CALL FLUVITSF( MNXYZN, MCN(MNNPEF), NDDLNO, MCN(MNVXYZP1),
     %                  NUMIOB(3), NUMAOB(3),

     %                  FLUNEG, FLUPOS, MCN(MNFVSF) )

      ELSE

C        FLUX DE LA VITESSE A TRAVERS LES ARETES DES LIGNES DE L'OBJET
C        -------------------------------------------------------------
C        MNELE=MCN( MNNPEF ): ADRESSE DU TABLEAU NPEF"TYPE EF
         CALL FLUVITLG( MNXYZN, MCN(MNNPEF), NDDLNO, MCN(MNVXYZP1),
     %                  NUMIOB(2), NUMAOB(2),
     %                  FLUNEG, FLUPOS, MCN(MNFVSF) )

      ENDIF

C     STOCKAGE DES RESULTATS DU PAS DE TEMPS NBPAST
      NBPAST = NBPAST + 1
      VPMIMX(1,NBPAST) = TEMPS
      VPMIMX(2,NBPAST) = VITMOY
      VPMIMX(3,NBPAST) = VITMAX
      VPMIMX(4,NBPAST) = PREMOY
      VPMIMX(5,NBPAST) = PREMAX - PREMIN
      VPMIMX(6,NBPAST) = FLUNEG
      VPMIMX(7,NBPAST) = FLUPOS
      VPMIMX(8,NBPAST) = NBPAST


C     STOCKAGE OU PAS DU VECTEUR VITESSE+PRESSION A CE TEMPS?
C     =======================================================
      IF( TEMPS .GE. TSTOC*0.9999 ) THEN

C        LE NOMBRE DE VECTEURS VITESSEPRESSION STOCKES
         NOVVIPR = NOVVIPR + 1

C        STOCKAGE DE LA VITESSEPRESSION NOVVIPR A CET INSTANT TEMPS
C        SUR FICHIER DU REPERTOIRE DU PROJET
         CALL ECFIVIPRTE( KNOMOB,  TEMPS,  NOVVIPR, NAVSTO, NBPAST,
     %                    NDIM,    NBNOVI, NBSOM, NBNOTE,
     %                    NTDLVP,  MCN(MNVXYZP1), NTDLTE, TEMPER0,
     %                    KNOMFIC, IERR )

C        LE TEMPS DU STOCKAGE
         RMCN(MNTIMES-1+NOVVIPR) = TEMPS

         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10150) TEMPS, NTDLVP, NOVVIPR, NMPROJ
         ELSE
            WRITE(IMPRIM,20150) TEMPS, NTDLVP, NOVVIPR, NMPROJ
         ENDIF

10150 FORMAT(' stokeins: Au TEMPS',G14.6,' STOCKAGE de',I9,
     %       ' D.L. VITESSE+PRESSION sur FICHIER',I5,
     %       ' du REPERTOIRE du PROJET ',A)
20150 FORMAT(' stokeins: At TIME',G14.6,' STORAGE of',I9,
     %       ' VELOCITY PRESSURE DoF on FILE',I5,
     %       ' of PROJECT DIRECTORY ',A)

C        LE PROCHAIN TEMPS DE STOCKAGE
         TSTOC = TSTOC + DTSTOC

      ENDIF

C     TRACE DES VITESSES EN TOUS LES POINTS AVEC SEUIL MIN A TRACER
C     =============================================================
C     CONSTRUCTION DU VECTEUR VITESSE EN X, Y, Z et VECTEUR PRESSION
      CALL VXVYVZPR1( NDIM,   NUTYEL, NDDLNO,
     %                NTDLVP, 1,      MCN(MNVXYZP1),  MCN(MNNOSO),
     %                NBNOVI, MCN(MNVITX1),MCN(MNVITY1),MCN(MNVITZ1),
     %                NBSOM,  MCN(MNPRES1) )

      IF( INTERA .GE. 1 ) THEN

C        MODE GRAPHIQUE AVEC X11
C        LA MEMOIRE PIXELS EST EFFACEE
         CALL EFFACEMEMPX
         NOTYVI   = 0
         LORBITE  = 0
C        SEUIL DE TRACE DES FLECHES DE LA VITESSE
         SEMAXVIT = VITMAX
         CMFLEC   = 1.8
         CMVITE   = REAL( CMFLEC / VITMAX )
C
         IF( NDIM .EQ. 2 ) THEN
C
C           TRACE DES AXES 2D
            CALL VISEE0
            CALL TRAXE2
C
C           TRACE DES FLECHES VITESSES
            SEMINVIT = VITMOY * 0.7D0
            CALL TRVITE2D( NBNOVI, MCN(MNXYZN+WYZNOE),
     %                     MOARET, MXARET, MCN(MNLARE),
     %                     SEMINVIT, SEMAXVIT, CMVITE, 1,
     %                     MCN(MNVITX1), MCN(MNVITY1), VITMAX )
C
         ELSE

ccc            CALL LONLAT( -89.0, 0. )
C           LONGITUDE et LATITUDE
            CALL LONLAT( -80.0, 0. )

C           LOUPE GROSSISSANTE
ccc            GROSSI = 1.2
ccc            GROSSI = 1.15
            GROSSI = 0.9
            AXOLAR = AXOLAR / GROSSI
            AXOHAU = AXOHAU / GROSSI

C           TRACE DES AXES 3D
            CALL TRAXE3

C           TRACE DES FLECHES VITESSES
            SEMINVIT = VITMOY
            CALL TRVITE3D( NBNOVI, MCN(MNXYZN+WYZNOE),
     %                     MOARFR, MXARFR, L1ARFR,  MCN(MNAFOB+WAREFR),
     %                     SEMINVIT, SEMAXVIT, CMVITE, 1,
     %                     MCN(MNVITX1), MCN(MNVITY1), MCN(MNVITZ1),
     %                     VITMAX )

         ENDIF

C        LE TRACE DU TITRE FINAL
         CALL LEGVIT( KNOMOB, 1, VITMOY, VITMIN, VITMAX, CMVITE )

      ENDIF


C     MISE A JOUR MNVXYZP0 et MNFG0 (EVITE des TRANSFERTS de TABLEAUX)
C     =============================
      MN       = MNVXYZP0
      MNVXYZP0 = MNVXYZP1
      MNVXYZP1 = MN

      MN    = MNFG0
      MNFG0 = MNFG1
      MNFG1 = MN

C     UN PAS DE TEMPS DE PLUS A CALCULER?
      IF( TEMPS + DT .LT. TPSFIN * 1.00001 ) GOTO 100

C    ##############################################################
C    ##                                                          ##
C    ##                FIN DE LA BOUCLE EN TEMPS                 ##
C    ##                                                          ##
C    ##############################################################

C     COPIE DU DERNIER VECTEUR VP MCN(MNVXYZP0...) dans VXYZP0(1:NTDLVP)
      CALL TRTATD( DMCN(MNVP0), VXYZP0, NTDLVP )

C     STOCKAGE OU PAS DU VECTEUR VITESSE+PRESSION A CE TEMPS?
C     =======================================================
      IF( RMCN(MNTIMES-1+NOVVIPR) .NE. TEMPS ) THEN

C        LE NOMBRE DE VECTEURS VITESSEPRESSION STOCKES
         NOVVIPR = NOVVIPR + 1

C        STOCKAGE DE LA VITESSEPRESSION NOVVIPR A CET INSTANT TEMPS
C        SUR FICHIER DU REPERTOIRE DU PROJET 
         CALL ECFIVIPRTE( KNOMOB,  TEMPS,  NOVVIPR, NAVSTO, NBPAST,
     %                    NDIM,    NBNOVI, NBSOM, NBNOTE,
     %                    NTDLVP,  MCN(MNVXYZP0), NTDLTE, TEMPER0,
     %                    KNOMFIC, IERR )

C        LE TEMPS DU STOCKAGE
         RMCN(MNTIMES-1+NOVVIPR) = TEMPS

         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10150) TEMPS, NTDLVP, NOVVIPR, NMPROJ
         ELSE
            WRITE(IMPRIM,20150) TEMPS, NTDLVP, NOVVIPR, NMPROJ
         ENDIF

      ENDIF

      GOTO 9999


C     PLACE MEMOIRE INSUFFISANTE
 9993 NBLGRC(NRERR) = 2
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1) = 'ERREUR PLACE MEMOIRE INSUFFISANTE'
         KERR(2) = 'REDUIRE LE MAILLAGE'
      ELSE
         KERR(1) = 'ERROR: NOT ENOUGH LARGE MEMORY'
         KERR(2) = 'REDUCE the MESH'
      ENDIF
      CALL LEREUR
      IERR = 20


C     DESTRUCTION DES TMC DEVENUS INUTILES
C     ====================================
C     FREE MEMORY USED BY ARRAY MG and VG
 9999 IF( IERMGALLOC .EQ. 0 ) DEALLOCATE( MG )
      IF( IERVGALLOC .EQ. 0 ) DEALLOCATE( VG )

      IF( NORESO .GE. 2 ) THEN
C        GRADIENT CONJUGUE STOCKAGE CONDENSE MORSE
C        DESTRUCTION DES TABLEAUX AUXILIAIRES
         IF( MNNDFX .GT. 0 ) CALL TNMCDS( 'ENTIER', NTDLHB, MNNDFX )
         IF( MNAUX  .GT. 0 ) CALL TNMCDS( 'REEL2',  LOAUX,  MNAUX  )
         IF( MNBDIR .GT. 0 ) CALL TNMCDS( 'REEL2',  LODIR,  MNBDIR )
         IF( MNLPLC .GT. 0 ) CALL TNMCDS( 'ENTIER', NTDLHB+1, MNLPLC )
         IF( MNLPCC .GT. 0 ) CALL TNMCDS( 'ENTIER', LOLPCC, MNLPCC )
         IF( MNVGC  .GT. 0 ) CALL TNMCDS( 'REEL2',  LOLPCC, MNVGC  )
      ENDIF

      IF( MNVXYZP0.GT. 0  ) CALL TNMCDS( 'REEL2',  NTDLVP, MNVXYZP0 )
      IF( MNVXYZP1.GT. 0  ) CALL TNMCDS( 'REEL2',  NTDLVP, MNVXYZP1 )
      IF( MNVXVYVZP1.GT.0 ) CALL TNMCDS( 'REEL2',  NTDLVP, MNVXVYVZP1)
      IF( MNFG1  .GT. 0   ) CALL TNMCDS( 'REEL2',  NTDLVP, MNFG1   )
      IF( MNFG0  .GT. 0   ) CALL TNMCDS( 'REEL2',  NTDLVP, MNFG0   )
      IF( MNNOSO .GT. 0   ) CALL TNMCDS( 'ENTIER', NBNOVI, MNNOSO  )
      IF( MNNDLFX.GT. 0   ) CALL TNMCDS( 'ENTIER', NBDLFX, MNNDLFX )
      IF( MNVDLFX.GT. 0   ) CALL TNMCDS( 'REEL2',  NBDLFX, MNVDLFX )
      IF( MNLGAU .GT. 0   ) CALL TNMCDS( 'REEL2',  MOLGAU, MNLGAU  )
      IF( MNTAEL .GT. 0   ) CALL TNMCDS( 'REEL2',  MOTAEL, MNTAEL  )
      IF( MNFVSF .GT. 0   ) CALL TNMCDS( 'REEL2',  MOSFOB, MNFVSF  )

C     BILAN SUR LA PLACE MEMOIRE CENTRALE OCCUPEE PAR LES MATRICES ...
C     ================================================================
      DMOTSTO = 2D0 + NTDLVP + NTDLHB
     %        + MOREE2 * (NBCOVG*2 + DBLE(MOLGAU) + 5*NTDLVP)
      IF( NORESO .EQ. 1 ) THEN

C        CROUT STOCKAGE PROFIL
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19002) DMOTSTO
         ELSE
            WRITE(IMPRIM,29002) DMOTSTO
         ENDIF
19002 FORMAT(/' STOCKAGE des MATRICES et VECTEURS=',D25.16,
     %' MOTS MEMOIRE'/)
29002 FORMAT(/' STORAGE of MATRICES and VECTORS=',  D25.16,
     %' MEMORY WORDS'/)

      ELSE IF( NORESO .GE. 2 ) THEN

C        GRADIENT CONJUGUE STOCKAGE CONDENSE MORSE
         MOTPRE = NTDLHB + LOLPCC * (1+MOREE2)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,19004) DMOTSTO, DBLE(MOTPRE), DBLE(MOTSGC),
     %                          DMOTSTO+MOTPRE+MOTSGC
         ELSE
            WRITE(IMPRIM,29004) DMOTSTO, DBLE(MOTPRE), DBLE(MOTSGC),
     %                          DMOTSTO+MOTPRE+MOTSGC
         ENDIF
19004    FORMAT(/' MATRICES CONDENSEE GC ',     T30,D25.16,' MOTS'/
     %           ' MATRICE PRECONDITIONNEMENT', T30,D25.16,' MOTS'/
     %           ' TABLEAUX SUPPLEMENTAIRES GC',T30,D25.16,' MOTS'/
     %           ' AU TOTAL LE GC DEMANDE',     T30,D25.16,' MOTS'/)
C
29004    FORMAT(/' GC CONDENSED MATRICES',      T30,D25.16,' WORDS'/
     %           ' PRECONDITIONED MATRIX',      T30,D25.16,' WORDS'/
     %           ' AUXILIARY ARRAYS',           T30,D25.16,' WORDS'/
     %           ' TOTAL for the CG',           T30,D25.16,' WORDS'/)
      ENDIF
C
C     COUT CALCUL DES VITESSES PRESSIONS
C     ==================================
      DVITPR = DINFO( 'DELTA CPU' )
C
      RETURN
      END
