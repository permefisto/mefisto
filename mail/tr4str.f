      SUBROUTINE TR4STR( NPT,    NTR,    NOARPT, N1ADEL,
     %                   MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                   MOARTR, MXARTR, N1ARTR, NOARTR,
     %                   NOARST,
     %                   NUTR,   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    FORMER LES 4 SOUS-TRIANGLES A PARTIR DU POINT NPT DE PXYD
C -----    ET DU TRIANGLE NTR ET DU TRIANGLE ADJACENT PAR LE COTE NATROP
C          DU TRIANGLE NTR
C
C ENTREES:
C --------
C NPT    : NUMERO DANS LE TABLEAU PXYD DU POINT
C NTR    : NUMERO DANS LE TABLEAU NOARTR DU TRIANGLE A TRIANGULER
C NOARPT : NUMERO DE 1 A 3 DE L'ARETE DE TRIANGLE OPPOSE A TRAITER
C N1ADEL : DEBUT DU CHAINAGE DES ARETES DU TRIANGLE NTR A RENDRE DELAUNAY
C
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE DU TABLEAU NOSOAR
C MXSOAR : NOMBRE MAXIMAL D'ARETES STOCKABLES DANS LE TABLEAU NOSOAR
C MOARTR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE DU TABLEAU NOARTR
C MXARTR : NOMBRE MAXIMAL DE TRIANGLES STOCKABLES DANS LE TABLEAU NOARTR
C
C MODIFIES:
C ---------
C N1SOAR : NUMERO DE LA PREMIERE ARETE VIDE DANS LE TABLEAU NOSOAR
C          UNE ARETE I DE NOSOAR EST VIDE  <=>  NOSOAR(1,I)=0
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES, CHAINAGES
C          SOMMET 1 = 0 SI ARETE VIDE => SOMMET 2 = ARETE VIDE SUIVANTE
C          HACHAGE DES ARETES = (NOSOAR(1)+NOSOAR(2)) MODULO MXSOAR
C N1ARTR : NUMERO DU PREMIER TRIANGLE VIDE DANS LE TABLEAU NOARTR
C          LE CHAINAGE DES TRIANGLES VIDES SE FAIT SUR NOARTR(2,.)
C NOARTR : LES 3 ARETES DES TRIANGLES +-ARETE1, +-ARETE2, +-ARETE3
C          ARETE1 = 0 SI TRIANGLE VIDE => ARETE2 = TRIANGLE VIDE SUIVANT
C NOARST : NOARST(I) NUMERO D'UNE ARETE DE SOMMET I
C
C SORTIES:
C --------
C NUTR   : LE NUMERO DES 3 SOUS-TRIANGLES DU TRIANGLE NT
C NTR    : EN SORTIE LE TRIANGLE INITIAL N'EST PLUS ACTIF DANS NOARTR
C          C'EST EN FAIT LE PREMIER TRIANGLE VIDE DE NOARTR
C N1ADEL : DEBUT DU CHAINAGE DES ARETES DU TRIANGLE NTR A RENDRE DELAUNAY
C          LES 4 ARETES PERIPHERIQUES SONT CHAINEES
C IERR   : = 0 SI PAS D'ERREUR
C          =51 SI LE TABLEAU NOSOAR EST SATURE
C          =52 SI LE TABLEAU NOARTR EST SATURE
C          = 5 SI LE TRIANGLE OPPOSE A L'ARETE N'EXISTE PAS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & ST PIERRE DU PERRAY     MARS 2008
C2345X7..............................................................012
      PARAMETER        (LCHAIN=6)
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      INTEGER    NOSOAR(MOSOAR,MXSOAR),
     %           NOARTR(MOARTR,MXARTR),
     %           NOARST(*),
     %           NUTR(4)
C
      INTEGER    NOSOTR(3), NUARCO(4), NOSOQU(4)
C
C     LES NUMEROS DES 3 SOMMETS DU TRIANGLE NTR
      CALL NUSOTR( NTR, MOSOAR, NOSOAR, MOARTR, NOARTR, NOSOTR )
C
C     NPT EST SUPPOSE SUR OU PROCHE DE L'ARETE DE NUMERO NOARPT DE NTR
      NOARPT1 = MOD(NOARPT,  3) + 1
      NOARPT2 = MOD(NOARPT1, 3) + 1
C
C     NP EST SUR L'ARETE NOARPT DU TRIANGLE NTR
C     LE TRIANGLE NTR+ADJACENT SONT SUBDIVISES EN 4 SOUS-TRIANGLES
C
C     L'ARETE SUPPORT DU POINT NPT
      NAOP = ABS( NOARTR(NOARPT,NTR) )
C     LE TRIANGLE OPPOSE ADJACENT A L'ARETE NOARPT
      IF( NOSOAR(4,NAOP) .EQ. NTR ) THEN
         NTROP = NOSOAR(5,NAOP)
      ELSE
         NTROP = NOSOAR(4,NAOP)
      ENDIF
      IF( NTROP .LE. 0 ) THEN
C        PAS DE TRIANGLE OPPOSE
         IERR = 5
         RETURN
      ENDIF
C
C     VERIFICATION QUE CE TRIANGLE EST ACTIF
      IF( NOARTR(1,NTROP) .EQ. 0 ) THEN
C        PAS DE TRIANGLE OPPOSE
         print *,'TR4STR: Pt',NPT,' Triangle',NTROP,
     %        ' INACTIF  bizarre!'
         IERR = 5
      ENDIF
C
C     LE TRIANGLE NTROP EST ACTIF
      DO 50 NOAROP=1,3
         NA = NOARTR(NOAROP,NTROP)
         IF( NA .GT. 0 ) THEN
C           ARETE NOARPT DE NTR DANS NTROP
            IF( NOSOAR(1,NA) .EQ. NOSOTR(NOARPT1) .AND.
     %          NOSOAR(2,NA) .EQ. NOSOTR(NOARPT)) GOTO 52
         ELSE
C           ARETE NOARPT DE NTR DANS NTROP
            IF( NOSOAR(1,-NA) .EQ. NOSOTR(NOARPT) .AND.
     %          NOSOAR(2,-NA) .EQ. NOSOTR(NOARPT1)) GOTO 52
         ENDIF
 50   CONTINUE
      print *,'Pt',NPT,' Triangle',NTROP,' SANS ST',
     %        NOSOTR(NOARPT), NOSOTR(NOARPT1)
C
C     LES 2 ARETES DE NOTROP SUIVANT L'ARETE NOAROP
 52   NOAROP1 = MOD(NOAROP,3) + 1
      NOAROP2 = MOD(NOAROP1,3) + 1
C
C     LE 4-EME SOMMET DES 2 TRIANGLES NTR + NTROP
      NA = NOARTR(NOAROP1,NTROP)
      IF( NA .GT. 0 ) THEN
         NS4 = NOSOAR( 2, NA )
      ELSE
         NS4 = NOSOAR( 1, -NA )
      ENDIF
C
C     RESERVATION DES 4 SOUS-TRIANGLES DANS NOARTR
      DO 60 N=1,4
         IF( N1ARTR .LE. 0 ) THEN
C           TABLEAU NOARTR SATURE
            IERR = 52
            RETURN
         ENDIF
         NUTR(N) = N1ARTR
C        LE NOUVEAU PREMIER TRIANGLE LIBRE DANS NOARTR
         N1ARTR = NOARTR(2,N1ARTR)
 60   CONTINUE
C
C     FORMATION DES 4 ARETES S1 PT, S2 PT, S3 PT, S4 PT
      NOSOQU(1) = NOSOTR(NOARPT1)
      NOSOQU(2) = NOSOTR(NOARPT2)
      NOSOQU(3) = NOSOTR(NOARPT )
      NOSOQU(4) = NS4
      DO 70 I=1,4
C        AJOUT EVENTUEL DE L'ARETE DANS NOSOAR
         IF( I .EQ. 1 ) THEN
            I2 = 4
         ELSE
            I2 = I-1
         ENDIF
         CALL FASOAR( NOSOQU(I), NPT,
     %                NUTR(I), NUTR(I2), 0,
     %                MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                NOARST,
     %                NUARCO(I), IERR )
         IF( IERR .NE. 0 ) RETURN
 70   CONTINUE
C
C     LES ARETES DES 4 SOUS-TRIANGLES DU QUADRANGLE
C
C     SOUS-TRIANGLE 1: NOSOTR(NOARPT1) NOSOTR(NOARPT) NPT
C     ARETE NOSOTR(NOARPT1)-NOSOTR(NOARPT2) DANS NOSOAR
C     EST CELLE DU COTE NOARPT1 DE NTR
      NA = NOARTR( NOARPT1, NTR )
      NOARTR( 1, NUTR(1) ) = NA
C     MISE A JOUR DU TRIANGLE DE L'ARETE 1 DU SOUS-TRIANGLE
      NA = ABS(NA)
      IF( NOSOAR(4,NA) .EQ. NTR ) THEN
         NOSOAR(4,NA) = NUTR(1)
      ELSE
         NOSOAR(5,NA) = NUTR(1)
      ENDIF
C     ARETE NOSOTR(NOARPT2)-NPT
      IF( NOSOTR(NOARPT2) .EQ. NOSOAR(1,NUARCO(2)) ) THEN
         LESIGN = 1
      ELSE
         LESIGN = -1
      ENDIF
      NOARTR( 2, NUTR(1) ) = LESIGN * NUARCO(2)
C     ARETE NPT-NOSOTR(NOARPT1)
      IF( NOSOTR(NOARPT1) .EQ. NOSOAR(1,NUARCO(1)) ) THEN
         LESIGN = -1
      ELSE
         LESIGN =  1
      ENDIF
      NOARTR( 3, NUTR(1) ) = LESIGN * NUARCO(1)
C     CHAINAGE DE L'ARETE 1 A RENDRE DELAUNAY EVENTUELLEMENT
      N = ABS( NOARTR( NOARPT1, NTR ) )
      NOSOAR(LCHAIN,N) = N1ADEL
      N1ADEL = N
C
C     SOUS-TRIANGLE 2: NOSOTR(NOARPT2) NOSOTR(NOARPT) NPT
C     ARETE NOSOTR(NOARPT2)-NOSOTR(NOARPT) DANS NOSOAR
C     EST CELLE DU COTE NOARPT2 DE NTR
      NA = NOARTR( NOARPT2, NTR )
      NOARTR( 1, NUTR(2) ) = NA
C     MISE A JOUR DU TRIANGLE DE L'ARETE 1 DU SOUS-TRIANGLE
      NA = ABS(NA)
      IF( NOSOAR(4,NA) .EQ. NTR ) THEN
         NOSOAR(4,NA) = NUTR(2)
      ELSE
         NOSOAR(5,NA) = NUTR(2)
      ENDIF
C     ARETE NOSOTR(NOARPT)-NPT
      IF( NOSOTR(NOARPT) .EQ. NOSOAR(1,NUARCO(3)) ) THEN
         LESIGN = 1
      ELSE
         LESIGN = -1
      ENDIF
      NOARTR( 2, NUTR(2) ) = LESIGN * NUARCO(3)
C     ARETE NPT-NOSOTR(NOARPT2)
      IF( NOSOTR(NOARPT2) .EQ. NOSOAR(1,NUARCO(2)) ) THEN
         LESIGN = -1
      ELSE
         LESIGN =  1
      ENDIF
      NOARTR( 3, NUTR(2) ) = LESIGN * NUARCO(2)
C     CHAINAGE DE L'ARETE 1 A RENDRE DELAUNAY EVENTUELLEMENT
      N = ABS( NOARTR( NOARPT2, NTR ) )
      NOSOAR(LCHAIN,N) = N1ADEL
      N1ADEL = N
C
C     SOUS-TRIANGLE 3: NOSOTR(NOARPT) NS4 NPT
C     ARETE NOSOTR(NOARPT)-NS4 DANS NOSOAR EST CELLE DU COTE NOAROP1 DE NTROP
      NA = NOARTR( NOAROP1, NTROP )
      NOARTR( 1, NUTR(3) ) = NA
C     MISE A JOUR DU TRIANGLE DE L'ARETE 1 DU SOUS-TRIANGLE
      NA = ABS(NA)
      IF( NOSOAR(4,NA) .EQ. NTROP ) THEN
         NOSOAR(4,NA) = NUTR(3)
      ELSE
         NOSOAR(5,NA) = NUTR(3)
      ENDIF
C     ARETE NS4-NPT
      IF( NS4 .EQ. NOSOAR(1,NUARCO(4)) ) THEN
         LESIGN = 1
      ELSE
         LESIGN = -1
      ENDIF
      NOARTR( 2, NUTR(3) ) = LESIGN * NUARCO(4)
C     ARETE NPT-NOSOTR(NOARPT)
      IF( NOSOTR(NOARPT) .EQ. NOSOAR(1,NUARCO(3)) ) THEN
         LESIGN = -1
      ELSE
         LESIGN =  1
      ENDIF
      NOARTR( 3, NUTR(3) ) = LESIGN * NUARCO(3)
C     CHAINAGE DE L'ARETE 1 A RENDRE DELAUNAY EVENTUELLEMENT
      N = ABS( NOARTR( NOAROP1, NTROP ) )
      NOSOAR(LCHAIN,N) = N1ADEL
      N1ADEL = N
C
C     SOUS-TRIANGLE 4: NS4 NOSOTR(NOARPT1) NPT
C     ARETE NS4-NOSOTR(NOARPT1) DANS NOSOAR EST CELLE DU COTE NOAROP2 DE NTROP
      NA = NOARTR( NOAROP2, NTROP )
      NOARTR( 1, NUTR(4) ) = NA
C     MISE A JOUR DU TRIANGLE DE L'ARETE 1 DU SOUS-TRIANGLE
      NA = ABS(NA)
      IF( NOSOAR(4,NA) .EQ. NTROP ) THEN
         NOSOAR(4,NA) = NUTR(4)
      ELSE
         NOSOAR(5,NA) = NUTR(4)
      ENDIF
C     ARETE NOSOTR(NOARPT1)-NPT
      IF( NOSOTR(NOARPT1) .EQ. NOSOAR(1,NUARCO(1)) ) THEN
         LESIGN = 1
      ELSE
         LESIGN = -1
      ENDIF
      NOARTR( 2, NUTR(4) ) = LESIGN * NUARCO(1)
C     ARETE NPT-NS4
      IF( NS4 .EQ. NOSOAR(1,NUARCO(4)) ) THEN
         LESIGN = -1
      ELSE
         LESIGN =  1
      ENDIF
      NOARTR( 3, NUTR(4) ) = LESIGN * NUARCO(4)
C     CHAINAGE DE L'ARETE 1 A RENDRE DELAUNAY EVENTUELLEMENT
      N = ABS( NOARTR( NOAROP2, NTROP ) )
      NOSOAR(LCHAIN,N) = N1ADEL
      N1ADEL = N
C
C     SUPPRESSION DE L'ARETE NOSOTR(NOARPT)-NOSOTR(NOARPT1) DANS NOSOAR
      CALL SASOAR( NAOP,   MOSOAR, MXSOAR, N1SOAR,
     %             NOSOAR, NOARST )
C
C     SUPPRESSION DU TRIANGLE NTR DANS NOARTR
      NOARTR(1,NTR) = 0
      NOARTR(2,NTR) = N1ARTR
C     LE NOUVEAU PREMIER TRIANGLE LIBRE DANS NOARTR
      N1ARTR = NTR
C
C     SUPPRESSION DU TRIANGLE NTROP DANS NOARTR
      NOARTR(1,NTROP) = 0
      NOARTR(2,NTROP) = N1ARTR
C     LE NOUVEAU PREMIER TRIANGLE LIBRE DANS NOARTR
      N1ARTR = NTROP
C
      RETURN
      END
