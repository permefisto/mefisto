      SUBROUTINE PTPRCT( U,     NBS,  ABCULI, MNXYSL, MNXYTL, MNNTGL,
     %                   XYZPT, XYZTG )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CALCULER LES 3 COORDONNEES ET LES 3 COMPOSANTES DE LA
C -----     TANGENTE DU POINT DE COORDONNEE U SUR LA LIGNE
C           DEFINIE PAR SES SOMMETS ET TANGENTES
C
C ENTREES:
C --------
C U      : ABSCISSE DU POINT COMPRISE ENTRE 0 ET 1
C NBS    : NOMBRE DE SOMMETS DE LA LIGNE
C ABCULI : ABSCISSES CURVILIGNES HOMOGENES DES NBS   SOMMETS   DE LA LIGNE
C MNXYSL : ADRESSE MCN DE LA PREMIERE COORDONNEE DES SOMMETS   DE LA LIGNE
C MNXYTL : ADRESSE MCN DE LA PREMIERE COMPOSANTE DES TANGENTES DE LA LIGNE
C MNNTGL : ADRESSE MCN DES 2 NUMEROS DES TANGENTES DES ARETES  DE LA LIGNE
C
C SORTIES:
C --------
C XYZPT  : 3 COORDONNEES DU POINT D'ABSCISSE CURVILIGNE HOMOGENE U
C XYZTG  : 3 COMPOSANTES DE LA TANGENTE EN CE POINT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     OCTOBRE 1997
C23456---------------------------------------------------------------012
      COMMON / EPSSSS / EPZERO,EPSXYZ
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
C
      REAL              ABCULI(1:NBS)
      REAL              XYZPT(3), XYZTG(3)
C
      REAL              TG2AR(3,2), FB(4), DFB(4)
C
C     RECHERCHE DE L'INTERVALLE DU PARAMETRE CONTENANT U
      N2=1
      DO 10 I1=2,NBS
         IF( U .LE. ABCULI(I1)+EPSXYZ ) THEN
            N2=I1
            GOTO 11
         ENDIF
 10   CONTINUE
C
C     LES NUMEROS DES SOMMETS DE L'ARETE ENCADRANT LE POINT PROJETE
 11   N1 = N2 - 1
C
C     RECHERCHE DES 2 TANGENTES DE L'ARETE N1 DE LA LIGNE
C     ARETE P3 HERMITE => C1
      MN2 = MNXYSL + 3 * N1
      MN1 = MN2 - 3
      CALL TG2ARL( N1, RMCN(MN1), RMCN(MN2), MNXYTL, MNNTGL, TG2AR )
C
C     LA VALEUR DES POLYNOMES DE BASE P3 HERMITE AU POINT S1 DE [0,1]
      U1 = ABCULI(N1)
      U2 = ABCULI(N2)
      H  = U2 - U1
      S1 = ( U - U1 ) / H
      CALL VFBP3H( S1, FB )
C
C     LA VALEUR DE LA DERIVEE DES POLYNOMES DE BASE
C     P3 HERMITE AU POINT S1 DE [0,1]
      CALL DFBP3H( S1, DFB )
C
C     CALCUL DES 3 COORDONNEES DU POINT ET DE LA TANGENTE
      MN1 = MN1 - 1
      MN2 = MN2 - 1
      DO 20 K=1,3
C
C        LA COORDONNEE K DU POINT SUR LA LIGNE P3 PAR MORCEAUX
         XYZPT(K) = FB(1) * RMCN(MN1+K)
     %            + FB(2) * RMCN(MN2+K)
     %            + FB(3) * TG2AR(K,1)
     %            + FB(4) * TG2AR(K,2)
C
C        LA COMPOSANTE K DE LA DERIVEE TANGENTIELLE
C        AU POINT SUR LA LIGNE P3 PAR MORCEAUX
         XYZTG(K) = ( DFB(1) * RMCN(MN1+K)
     %              + DFB(2) * RMCN(MN2+K)
     %              + DFB(3) * TG2AR(K,1)
     %              + DFB(4) * TG2AR(K,2) ) / H
C
 20   CONTINUE
      END
