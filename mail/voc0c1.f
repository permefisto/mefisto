      SUBROUTINE VOC0C1( NMVOLU, NTLXVL,
     %                   NTNSEF, MNNSEF, NTXYZS, MNXYZS, IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    POUR LE MAILLAGE D'UN VOLUME, TRANSFORMER LES FACES C0-C1
C -----    EN FACES C1 ET MODIFIER EN CONSEQUENCE LES EF VOLUMIQUES
C          DU MAILLAGE
C
C ENTREES:
C --------
C NMVOLU : NOM DU VOLUME
C NTLXVL : NO TMS DU LEXIQUE DU VOLUME DE MAILLAGE A TRAITER
C
C SORTIES:
C --------
C NTNSEF : NO TMS 'NSEF'      DU MAILLAGE DU VOLUME
C MNNSEF : ADRESSE MCN DU TMS 'NSEF'
C NTXYZS : NO TMS 'XYZSOMMET' DU MAILLAGE DU VOLUME
C MNXYZS : ADRESSE MCN DU TMS 'XYZSOMMET'
C IERR   : =0 SI PAS D'ERREUR RENCONTREE
C          >0 SINON
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS    DECEMBRE 1996
C ...................................................................012
      IMPLICIT INTEGER (W)
      include"./incl/gsmenu.inc"
      include"./incl/a___face.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/sotgfc.inc"
C
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      CHARACTER*(*)     NMVOLU
C
      INTEGER           NOSOEL(64), NO8TGF(8)
C
C     LE TABLEAU 'NSEF' DE CE VOLUME
      CALL LXTSOU( NTLXVL, 'NSEF', NTNSEF, MNNSEF )
      IF( NTNSEF .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'VOC0C1: VOLUME SANS NSEF'
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C
C     LES PARAMETRES DES NO SOMMET DU MAILLAGE DU VOLUME
      CALL NSEFPA( MCN(MNNSEF),
     %             NUTYMA, NBSOEL, NBSOEF, NBTGEF,
     %             LDAPEF, LDNGEF, LDTGEF, NBEFOB,
     %             NX    , NY    , NZ    ,
     %             IERR   )
      IF( IERR .NE. 0 ) RETURN
C
C     LE NOMBRE D'EF A TG DU VOLUME
      NBEFTG = MCN( MNNSEF + WBEFTG )
      IF( NBEFTG .LE. 0 ) RETURN
C
C     LE TABLEAU 'XYZSOMMET' DE CE VOLUME
      CALL LXTSOU( NTLXVL, 'XYZSOMMET', NTXYZS, MNXYZS )
      IF( NTXYZS .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'VOC0C1: VOLUME SANS XYZSOMMET'
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
C     LE NOMBRE DE TANGENTES DU MAILLAGE DU VOLUME
      NBTGVL = MCN( MNXYZS + WNBTGS )
      IF( NBTGVL .LE. 0 ) RETURN
C
C     LE NOMBRE DE SOMMETS DU MAILLAGE DU VOLUME
      NBSOVL = MCN( MNXYZS + WNBSOM )
C
C     CONSTRUCTION DU TABLEAU DES FACES DU VOLUME
      CALL HAFAVO( NMVOLU, 4, NTFAVO, MNFAVO, IERR )
      IF( IERR .NE. 0 ) RETURN
C
C     LE NOMBRE D'ENTIERS PAR FACE
      MOFACE = MCN( MNFAVO + WOFACE )
C     LA MAJORATION DU NOMBRE DE FACES
      MXFACE = MCN( MNFAVO + WXFACE )
C     LE NOMBRE DE FACES A TG
      NBFATG = MCN( MNFAVO + WBFATG )
      IF( NBFATG .LE. 0 ) RETURN
C
C     ADRESSE MCN DU TABLEAU LFACES
      MNFACE = MNFAVO + WFACES
C
C     ADRESSE MCN DU TABLEAU NUTGFA DU TMS A___FACE - DECALAGE DE 8
      MNTGFA = MNFACE + MOFACE*MXFACE - 9
C
C     RESERVATION D'UN TABLEAU DES NOUVEAUX EF VOLUMIQUES A TG
      MNNETG = 0
      CALL TNMCDC( 'ENTIER', NBFATG*NBTGEF, MNNETG )
      NBNETG = 0
C
C     NECESSAIRE POUR RETROUVER LE NUMERO DE LA FACE DANS UN CUBE
C     A PARTIR DES SOMMETS DE LA FACE
      DO 10 NCOGEL=5,9
         IF( NCOGEL .EQ. 9 ) GOTO 10
C        LE NUMERO DES SOMMETS DES FACES DU TETRAEDRE, PENTAEDRE, HEXAEDRE
         CALL SOFACU( NCOGEL, NBSTFA(1,NCOGEL), NOSTFA(1,1,NCOGEL) )
C        LE NUMERO DES TANGENTES DES FACES DU TETRAEDRE, PENTAEDRE, HEXAEDRE
         CALL TGFACU( NCOGEL, NBTGFA(1,NCOGEL), NOTGFA(1,1,NCOGEL) )
 10   CONTINUE
C
C     PARCOURS DU TABLEAU LFACES
C     ==========================
      MNF = MNFACE - MOFACE - 1
      DO 100 NUFA = 1,MXFACE
C
C        ADRESSE MCN DU DEBUT DE LA FACE
         MNF = MNF + MOFACE
C
C        L'EVENTUEL NUMERO DE FACE A TG
         NUFATG = MCN( MNF + 8 )
         IF( NUFATG .GT. 0 ) THEN
C
C           FACE A TG : NUMERO DES 8 TANGENTES DE LA FACE
C                       UNE TANGENTE AU MOINS EST DIFFERENTE DE ZERO
C           NOMBRE DE SOMMETS DE LA FACE
            IF( MCN(MNF+4) .EQ. 0 ) THEN
C              FACE TRIANGULAIRE
               NBSF = 3
            ELSE
C              FACE QUADRANGULAIRE
               NBSF = 4
            ENDIF
C
            MN8TG = MNTGFA + 8 * NUFATG
            IF( MCN(MNF+7) .EQ. 0 ) GOTO 100
C
C           FACE ADJACENTE A 2 EF EN COMMUN
C           LE NUMERO DES AU PLUS 4 SOMMETS DE LA FACE NUFA DEBUTENT A l'ADRESSE
C
            DO 80 K=1,2
C
C              NUMERO DE L'EF VOLUMIQUE PARTAGEANT CETTE FACE
               NEF = MCN( MNF + 5 + K )
C
C              LE NUMERO DES SOMMETS ET TANGENTES DE L'EF NEF DU VOLUME
               CALL NSEFNS( ABS(NEF), NUTYMA, NBSOEF, NBTGEF,
     %                      LDAPEF, LDNGEF, LDTGEF,
     %                      MNNSEF, NX, NY, NZ,
     %                      NCOGEL, NUGEEF, NUEFTG, NOSOEL, IERR )
C              PAS DE TEST SUR IERR CAR DEJA FAIT DANS CALL HAFAVO
C
C              RECHERCHE DU NO DE LA FACE NUFA DANS L'EF VOLUMIQUE NEF
               CALL NOFACU( NBSF, NCOGEL, NOSOEL, I1, LESENS, MINNUS )
C              I1 EST LE NUMERO DE LA FACE DANS L'EF VOLUMIQUE NEF A TG
C              MINNUS EST LE NUMERO DANS LA FACE DU PLUS PETIT NUMERO DE SOMMET
C
C              NUMERO DES 8 TANGENTES DE LA FACE I1 AVEC MINNUS ROTATIONS
               CALL NU8TGF( I1, NCOGEL, MINNUS, LESENS, NBT, NO8TGF )
C
C              CALCUL DE L'ADRESSE DANS NUTGEF DES TG DE L'EF VOLUMIQUE
               IF( NUEFTG .GT. 0 ) THEN
C
C                 EF VOLUMIQUE DEJA A TG
                  MN = MNNSEF + LDTGEF + NBTGEF * (NUEFTG-1)
C                 LES 8 TANGENTES DE LA FACE SONT IMPOSEES A LA FACE DE L'EF
C                 PUISQUE LES 2 TG DE LA FACE SONT LA SYNTHESE DE CELLES
C                 DES 2 EF ADJACENTS PAR CETTE FACE
C
               ELSE
C
C                 EF VOLUMIQUE SANS TG AU DEPART
                  MNNS = MNNSEF + LDAPEF + ABS(NEF)-1
                  IF( MCN(MNNS) .EQ. 0 ) THEN
C                    EF ENCORE SANS TG => IL DEVIENT UN EF A TG
                     NBNETG = NBNETG + 1
C                    LE SIGNE - POUR INDIQUER UN NOUVEL EF A TG
                     MCN( MNNS ) = -(NBEFTG+NBNETG)
C                    TOUS LES NUMEROS DE TANGENTES SONT MIS A ZERO
                     MN = MNNETG + NBTGEF * (NBNETG-1)
                     CALL AZEROI( NBTGEF, MCN(MN) )
                  ELSE
C                    EF CREE AVEC DEJA AU MOINS UNE FACE A TG
                     MN = MNNETG + NBTGEF * (-MCN(MNNS)-NBEFTG-1)
                  ENDIF
C
               ENDIF
C
C              LES 8 TANGENTES DE LA FACE SONT IMPOSEES A L'EF VOLUMIQUE
               DO 40 L=1,8
                  IF( MCN( MN8TG + L ) .NE. 0 ) THEN
C                    NUMERO DE LA TANGENTE DANS L'EF VOLUMIQUE
                     MCN( MN + NO8TGF(L) ) = MCN( MN8TG + L )
                  ENDIF
 40            CONTINUE
 80         CONTINUE
         ENDIF
 100  CONTINUE
C
C     ALLONGEMENT DU TMS 'NSEF' ET MISE A JOUR
C     ========================================
      IF( NBNETG .GT. 0 ) THEN
         NBEFAP = MCN(MNNSEF+WBEFAP)
         L      = LDAPEF + NBEFAP + (NBEFTG+NBNETG) * (1+NBTGEF)
         CALL TAMSAU( NTNSEF, L )
         CALL TAMSOU( NTNSEF, MNNSEF )
C
C        LE POINTEUR SUR LES EF A TG REDEVIENT POSITIF
         MN = MNNSEF + LDAPEF
         DO 190 I = MN, MN+NBEFAP-1
            MCN(I) = ABS( MCN(I) )
 190     CONTINUE
C
C        DECALAGE DE NBNETG DU TABLEAU DES NUMEROS DE TG DES EF A TG
         MN = MN + NBEFAP + NBEFTG
         DO 200 I = MN+NBTGEF*NBEFTG-1, MN, -1
            MCN(I+NBNETG) = MCN(I)
 200     CONTINUE
C
C        MISE A ZERO DU CODE GEOMETRIQUE (ANCIEN EF C0)
         DO 210 I = MN, MN+NBNETG-1
            MCN(I) = 0
 210     CONTINUE
C
C        COPIE DES TG DES NOUVEAUX EF A TG A LA SUITE DES ANCIENS
         MN = MN + NBNETG + NBEFTG * NBTGEF
         CALL TRTATA( MCN(MNNETG), MCN(MN), NBNETG*NBTGEF )
C
C        LE NOMBRE FINAL D'EF A TG
         MCN( MNNSEF + WBEFTG ) = NBEFTG + NBNETG
      ENDIF
C
C     DESTRUCTION DU TABLEAU NETG
      CALL TNMCDS( 'ENTIER', NBFATG*NBTGEF, MNNETG )
      END
