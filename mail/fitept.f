       SUBROUTINE FITEPT( PT, PTXYZD, LARBRT, NUFILS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TROUVER LE NUMERO DU PREMIER FILS DE L'OCTAEDRE LARBRT
C -----    CONTENANT LE POINT PT
C
C    ATTENTION : LE POINT PT EST SUPPOSE INTERNE AU TETRAEDRE !!!
C
C ENTREES:
C --------
C PT     : X  Y  Z DU POINT + UN REEL DOUBLE DECLARE MAIS INUTILE
C PTXYZD : PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE
C LARBRT : ARBRE-14 DES OCTAEDRES ( FOND DE LA TETRAEDRISATION )
C      LARBRT(-1) : NO DU PERE DE L'OCTAEDRE DANS UN DES 2 ARBRES
C                     >0 => DANS LARBRO
C                     <0 => DANS LARBRT
C      LARBRT(0)  : 1 A 14 NO DE FILS DE L'OCTAEDRE POUR SON PERE
C                   + 100 * NO TYPE DE L'OT
C                   NO TYPE DE L'OT : 0 SI OCTAEDRE
C                                     1 SI TETRAEDRE T RONDE (T1)
C                                     2 SI TETRAEDRE T       (T2)
C   SI LARBRT(0)>0 ALORS J EST UN TETRAEDRE OCCUPE
C      SI LARBRT(1)>0 ALORS
C         LARBRT(1:5): NO (>0) LARBRT DES 5 SOUS-OCTA-TETRAEDRES
C      SINON
C         LARBRT(1:5):-NO PTXYZD DES 0 A 5 POINTS INTERNES DU TETRA
C                       0  SI PAS DE POINT
C                      ( L'OT EST ALORS UNE FEUILLE DE L'ARBRE )
C
C      LARBRT(6:9) : NO PTXYZD DES 4 SOMMETS DU TETRAEDRE
C   SINON
C      LARBRT(0): -ADRESSE DANS LARBRT DU TETRAEDRE VIDE SUIVANT
C
C SORTIES:
C --------
C NUFILS : NUMERO DU PREMIER FILS DE L'OCTAEDRE CONTENANT LE POINT PT
C          =0 SI LE POINT N'EST PAS DANS LE TETRAEDRE LARBRT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    Novembre 1992
C MODIFS : ALAIN PERRONNET Saint Pierre du Perray          Novembre 2019
C2345X7..............................................................012
      INTEGER           LARBRT(-1:9)
      DOUBLE PRECISION  PT(1:4)
      DOUBLE PRECISION  PTXYZD(4,*)
      DOUBLE PRECISION  CB(4)

C     CALCUL DES COORDONNEES BARYCENTRIQUES DU POINT DANS LE TETRAEDRE
C     LE POINT PT EST SUPPOSE INTERNE AU TETRAEDRE LARBRT
      CALL CBDATE( PT, PTXYZD, LARBRT(6), CB, IERR )

      DO NUFILS=2,5
         IF( CB(NUFILS-1) .GE. 0.5D0 ) THEN
C           LE POINT EST INTERNE AU TETRAEDRE FILS NUFILS
            RETURN
         ENDIF
      ENDDO

C     L'OCTAEDRE CENTRAL FILS CONTIENT LE POINT
      NUFILS = 1

      RETURN
      END
