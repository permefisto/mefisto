      SUBROUTINE QUA1SB( GRAND,  NS,     XYZSOM, NPSOFR,
     %                   NBSOTE, NSTETR, VOLUMT, QUALIT,
     %                   NO1TSO, NOTESO, NBBARF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   AMELIORER LA QUALITE DE LA TETRAEDRISATION AUTOUR DU SOMMET NS
C -----   INTERNE A UN SEUL MATERIAU EN LE REMPLACANT PAR LE BARYCENTRE
C         DES SOMMETS DE SES TETRAEDRES
C
C ENTREES:
C --------
C GRAND  : PLUS GRAND REEL SIMPLE PRECISION STOCKABLE
C NS     : NUMERO DU SOMMET DE QUALITE A AMELIORER
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C NSTETR : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NPSOFR : NUMERO 0 SI SOMMET INTERNE (=> DANS UN SEUL MATERIAU)
C                 1 SI SOMMET SUR LA FRONTIERE
C                 2 SI SOMMET SUR L'INTERFACE ENTRE 2 MATERIAUX
C                -1 SI SOMMET SUPPRIME LORS DE L'AMELIORATION
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C NOTESO : NOTESO(1,*) NUMERO DANS NSTETR DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER

C MODIFIES:
C ---------
C XYZSOM : COORDONNEES X Y Z DES NBSOMM SOMMETS DES TETRAEDRES
C QUALIT : QUALITE DES TETRAEDRES DE LA TETRAEDRISATION
C VOLUMT : VOLUME  DES TETRAEDRES DE LA TETRAEDRISATION
C NBBARF : NOMBRE DE BARYCENTRAGES FAITS AVEC UNE MEILLEURE QUALITE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC et St Pierre du Perray    Mars 2016
C2345X7..............................................................012
      include"./incl/langue.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      INTEGER           NPSOFR(*),
     %                  NSTETR(NBSOTE,*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*)
      REAL              XYZSOM(3,*),
     %                  VOLUMT(*),
     %                  QUALIT(*)

      REAL              XYZBAR(3), XYZNS(3), ARMIN, ARMAX, SURFTR(4),
     %                  V, VOLNS0, VOLNS1, Q, QUANS0, QUANS1

C     LE SOMMET NS EST IL INTERNE A UN SEUL MATERIAU?
      IF( NPSOFR( NS ) .NE. 0 ) RETURN

C     OUI: NS EST UN SOMMET INTERNE APPARTENANT A UN SEUL MATERIAU
      DO K=1,3
         XYZBAR(K) = 0
      ENDDO
      NB = 0

      VOLNS0 = 0
      VOLNS1 = 0
      QUANS0 = GRAND
      QUANS1 = GRAND

C     POSITION DANS NOTESO DU 1-ER TETRAEDRE DE SOMMET NS
      NDT = NO1TSO( NS )

C     TANT QU'IL EXISTE UN TETRAEDRE DE SOMMET NS FAIRE
 10   IF( NDT .GT. 0 ) THEN

C        LE NUMERO DU TETRAEDRE DANS NSTETR
         NT = NOTESO(1,NDT)

C        CONTRIBUTION DES SOMMETS NS1 NON NS AU BARYCENTRE
         DO N=1,4
            NS1 = NSTETR( N, NT )
            IF( NS1 .NE. NS ) THEN
               DO K=1,3
                  XYZBAR(K) = XYZBAR(K) + XYZSOM(K,NS1)
               ENDDO
               NB = NB + 1
            ENDIF
         ENDDO

C        CALCUL DU VOLUME ET QUALITE MINIMALE DES TETRAEDRES DE SOMMET NS
         CALL QUATET( XYZSOM(1,NSTETR(1,NT)),
     %                XYZSOM(1,NSTETR(2,NT)),
     %                XYZSOM(1,NSTETR(3,NT)),
     %                XYZSOM(1,NSTETR(4,NT)),
     %                ARMIN, ARMAX, SURFTR, V, Q )

         VOLNS0 = VOLNS0 + ABS( V )
         IF( QUANS0 .GT. Q ) QUANS0 = Q

C        LE TETRAEDRE SUIVANT
         NDT = NOTESO(2,NDT)
         GOTO 10

      ENDIF

C     LE BARYCENTRE DES SOMMETS DES TETRAEDRES DE SOMMET NS
      DO K=1,3
C        SAUVEGARDE DU SOMMET NS
         XYZNS(K)  = XYZSOM(K,NS)

C        COORDONNEE K DU BARYCENTRE
         XYZBAR(K) = XYZBAR(K) / NB

C        NS EST PLACE AU BARYCENTRE
         XYZSOM(K,NS) = XYZBAR(K)
      ENDDO

C     POSITION DANS NOTESO DU 1-ER TETRAEDRE DE SOMMET NS
      NDT = NO1TSO( NS )

C     TANT QU'IL EXISTE UN TETRAEDRE DE SOMMET NS FAIRE
 20   IF( NDT .GT. 0 ) THEN

C        LE NUMERO DU TETRAEDRE DANS NSTETR
         NT = NOTESO(1,NDT)

C        CALCUL DU VOLUME ET QUALITE MINIMALE DES TETRAEDRES DE SOMMET NS
         CALL QUATET( XYZSOM(1,NSTETR(1,NT)),
     %                XYZSOM(1,NSTETR(2,NT)),
     %                XYZSOM(1,NSTETR(3,NT)),
     %                XYZSOM(1,NSTETR(4,NT)),
     %                ARMIN, ARMAX, SURFTR, V, Q )

         VOLUMT( NT ) = V
         QUALIT( NT ) = Q

         VOLNS1 = VOLNS1 + ABS( V )
         IF( QUANS1 .GT. Q ) THEN
            QUANS1 = Q

            IF( QUANS0 .GT. QUANS1 ) THEN
C              LA QUALITE AVEC LE BARYCENTRE EST INFERIEURE
C              => RESTAURATION DU SOMMET NS
               GOTO 30
            ENDIF

         ENDIF

C        LE TETRAEDRE SUIVANT
         NDT = NOTESO(2,NDT)
         GOTO 20

      ENDIF

C     ICI, LE BARYCENTRE DONNE T IL UNE MEILLEURE QUALITE?
      IF( ABS(VOLNS1-VOLNS0) .LT. VOLNS0*0.001 ) THEN

C        OUI. NS DEVIENT LE BARYCENTRE
ccc         WRITE(IMPRIM,*)
ccc         IF( LANGAG .EQ. 0 ) THEN
ccc            WRITE(IMPRIM,*) 'qua1sb: le SOMMET',NS,' EST REMPLACE PAR SO
ccc     %N BARYCENTRE'
ccc         ELSE
ccc            WRITE(IMPRIM,*) 'qua1sb: the VERTEX',NS,' IS REPLACED BY ITS
ccc     % BARYCENTER'
ccc         ENDIF
ccc         WRITE(IMPRIM,*) 'XYZ0=',(XYZNS(K),K=1,3),    'VOLNS0=',VOLNS0,
ccc     %' QUANS0=',QUANS0
ccc         WRITE(IMPRIM,*) 'XYZ1=',(XYZSOM(K,NS),K=1,3),'VOLNS1=',VOLNS1,
ccc     %' QUANS1=',QUANS1

         NBBARF = NBBARF + 1
         GOTO 9000

      ENDIF

C     ICI, LE BARYCENTRE DONNE UNE PLUS MAUVAISE QUALITE
C     RESTAURATION DES ANCIENNES COORDONNEES DE NS
 30   DO K=1,3
C        RESTAURATION DU SOMMET NS
         XYZSOM(K,NS) = XYZNS(K)
      ENDDO

C     POSITION DANS NOTESO DU 1-ER TETRAEDRE DE SOMMET NS
      NDT = NO1TSO( NS )

C     TANT QU'IL EXISTE UN TETRAEDRE DE SOMMET NS FAIRE
 40   IF( NDT .GT. 0 ) THEN

C        LE NUMERO DU TETRAEDRE DANS NSTETR
         NT = NOTESO(1,NDT)

C        CALCUL DU VOLUME ET QUALITE MINIMALE DES TETRAEDRES DE SOMMET NS
         CALL QUATET( XYZSOM(1,NSTETR(1,NT)),
     %                XYZSOM(1,NSTETR(2,NT)),
     %                XYZSOM(1,NSTETR(3,NT)),
     %                XYZSOM(1,NSTETR(4,NT)),
     %                ARMIN, ARMAX, SURFTR, VOLUMT(NT), QUALIT(NT) )

C        LE TETRAEDRE SUIVANT
         NDT = NOTESO(2,NDT)
         GOTO 40

      ENDIF

 9000 RETURN
      END
