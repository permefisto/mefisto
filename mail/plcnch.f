      SUBROUTINE PLCNCH( MXGENE, XYZGPL,
     %                   PAX1CN, RAY1CN, PAX2CN, RAY2CN, HAUTCN, RPCONE,
     %                   XYZCPL, VN,
     %                   NPSUIV, RPINT,
     %                   NBP,    N1COUR, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DE LA LIGNE D'INTERSECTION ENTRE 1 TRONC DE CONE
C -----    ET UN PLAN DEFINI PAR 3 POINTS
C          LE PLAN NE DOIT PAS ETRE TANGENT AU TRONC DE CONE
C
C ENTREES:
C --------
C MXGENE : NOMBRE DE GENERATRICES DU CONE 1 A INTERSECTER AVEC LE CONE 2
C XYZGPL : 3 COORDONNEES GLOBALES DES 3 POINTS DE DEFINITION DU PLAN
C
C PAX1CN : POINT DE BASE DE L'AXE DU TRONC DE CONE 1
C RAY1CN : RAYON DU CERCLE AU POINT PAX1CN (PLAN ORTHOGONAL A L'AXE DU CONE )
C PAX2CN : POINT DE L'AXE DU TRONC DE CONE 1
C RAY2CN : RAYON DU CERCLE AU POINT PAX2CN (PLAN ORTHOGONAL A L'AXE DU CONE )
C
C SORTIES:
C --------
C HAUTCN : HAUTEUR DU TRONC DE CONE 1
C RPCONE : REPERE PROPRE ORTHONORME DU CONE 1 D'ORIGINE AU POINT PAX1CN
C          ORIGINE AU POINT PAX1CN
C NPSUIV : NUMERO DANS RPINT DU POINT D'INTERSECTION SUIVANT
C RPINT  : RPINT(1,N) ANGLE DE LA GENERATRICE DU CONE 1 DE CE POINT N
C          RPINT(2,N) PARAMETRE D'INTERSECTION ENTRE LES 2 POINTS DE LA
C          GENERATRICE N
C NBIP   : NUMERO DANS N1COUR DU DERNIER INTERVALLE DES POINTS D'INTERSECTION
C N1COUR : DEBUT DES CHAINAGES DES COURBES, PUIS DES INTERVALLES DE GENERATRICES
C          PUIS DES INTERVALLES DE POINTS D'INTERSECTION
C          N1COUR(0) POINTE SUR LE PREMIER VIDE
C IERR   :=0 PAS D'ERREUR DETECTEE
C         =1 LES 3 POINTS DU PLAN SONT ALIGNES
C         =2 LE PLAN EST TANGENT AU CONE => UTILISER SEGMENT DE DROITE
C         =3 SI AUCUN POINT D'INTERSECTION
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET UPMC ANALYSE NUMERIQUE  PARIS    JANVIER 1998
C2345X7..............................................................012
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION  XYZGPL(3,3), XYZCPL(3,3)
      DOUBLE PRECISION  PAX1CN(3), PAX2CN(3)
      DOUBLE PRECISION  RAY1CN, RAY2CN, HAUTCN, PARAME,
     %                  ANGLE, DANGLE, ANGLE1, ANGLE2
      DOUBLE PRECISION  RPCONE(3,3)
      DOUBLE PRECISION  RPINT(1:2, 1:MXGENE+1)
      INTEGER           NPSUIV(MXGENE+1)
      INTEGER           N1COUR(0:6)
C
      DOUBLE PRECISION  P(3), Q(3), VN(3)
C
      DEUXPI = ATAN( 1D0 ) * 8D0
C
C     CALCUL DU REPERE PROPRE DU CONE
      CALL CONAXE( PAX1CN, PAX2CN, RPCONE, HAUTCN )
C
C     LES 3 COORDONNEES DES 3 POINTS DU PLAN DANS LE REPERE PROPRE DU CONE
      DO 5 N=1,3
C        PASSAGE AUX COORDONNEES PROPRES DU CONE 1 DU POINT DE TANGENCE MG
         CALL NC3D3D( PAX1CN, RPCONE, XYZGPL(1,N), XYZCPL(1,N) )
 5    CONTINUE
C
C     LA NORMALE VN AU PLAN P1 P2 P3 DANS LE REPERE PROPRE AU CONE
      P(1) = XYZCPL(1,2) - XYZCPL(1,1)
      P(2) = XYZCPL(2,2) - XYZCPL(2,1)
      P(3) = XYZCPL(3,2) - XYZCPL(3,1)
      Q(1) = XYZCPL(1,3) - XYZCPL(1,1)
      Q(2) = XYZCPL(2,3) - XYZCPL(2,1)
      Q(3) = XYZCPL(3,3) - XYZCPL(3,1)
      CALL PROVEC( P, Q, VN )
      CALL NORME1( 3, VN, IERR )
      IF( IERR .NE. 0 ) RETURN
C
C     CHAINAGE DES VIDES PAR LE SUIVANT
      N1COUR( 0 ) = 1
      DO 8 N=1,MXGENE
         NPSUIV( N ) = N+1
 8    CONTINUE
      NPSUIV(MXGENE+1) = 0
C
C     PAS ANGULAIRE
      DANGLE = DEUXPI / MXGENE
C     PAS DE POINT D'INTERSECTION
      N0 = 0
C
C     BOUCLE SUR LES GENERATRICES DU CONE
      DO 10 N = 0, MXGENE-1
C
C        LES 2 POINTS AUX EXTREMITES DE LA GENERATRICE N DU CONE
         ANGLE = N * DANGLE
         CALL PLCNAN( ANGLE,  RAY1CN, RAY2CN, HAUTCN, XYZCPL, VN,
     %                PARAME, P, Q,   IERR )
C        IERR=0 PAS D'ERREUR DETECTEE
C            =2 LE PLAN EST TANGENT AU CONE
         IF( IERR .NE. 0 ) RETURN
C
C        LE NUMERO DU POINT AJOUTE
         NUPINT = N1COUR(0)
C        LE NOUVEAU VIDE PREMIER POINT D'INTERSECTION
         N1COUR(0) = NPSUIV( NUPINT )
C
C        ANGLE DU POINT D'INTERSECTION
         RPINT( 1, NUPINT ) = ANGLE
C
C        LE PARAMETRE
         RPINT( 2, NUPINT ) = PARAME
C
         IF( N0 .EQ. 0 ) THEN
C
C           C'EST LE PREMIER POINT D'UNE PARTIE DE LA LIGNE
            N1COUR(1) = NUPINT
C           PAS DE SUIVANT
            NPSUIV( NUPINT ) = 0
C
         ELSE
C
C           IL EXISTE UN PRECEDENT N0 => SON CHAINAGE
            NPSUIV( N0 ) = NUPINT
C
         ENDIF
C
C        LE DERNIER POINT ACTUEL DE LA LIGNE
         N0 = NUPINT
C
 10   CONTINUE
C
C     EXISTE-T-IL DES POINTS D'INTERSECTION?
      IF( N0 .LE. 0 ) THEN
C        NON
         IERR = 3
         RETURN
      ENDIF
C
C     FERMETURE DU CHAINAGE CIRCULAIRE DE LA LIGNE
      NPSUIV( N0 ) = N1COUR( 1 )
      N1COUR(  2 ) = N1COUR( 1 )
C
C     TRONCATURE SELON LES PLANS ORTHOGONAUX A L'AXE DU CONE
C     ======================================================
C     PAS DE POINT D'INTERSECTION
      N0  = 0
      NBP = 0
C
C     DEBUT DU CHAINAGE
      N1 = 0
      N2 = N1COUR( 1 )
C     LA FIN DU CHAINAGE DE L'INTERVALLE DE POINTS D'INTERSECTION
      NF = N1COUR( 2 )
C     LE NOMBRE DE PARTIES DE LA LIGNE
      NBP = 0
C
C     RECHERCHE DU PREMIER POINT DANS LE TRONC DE CONE
C     ------------------------------------------------
C     LES 2 POINTS AUX EXTREMITES DE LA GENERATRICE N2 DU CONE
 30   PARAME = RPINT( 2, N2 )
      IF( PARAME .LT. 0D0 .OR. PARAME .GT. 1D0 ) THEN
C
C        POINT EXTERNE AU TRONC DE CONE => PASSAGE AU POINT SUIVANT
         N1 = N2
         N2 = NPSUIV(N2)
         IF( N2 .NE. NF ) GOTO 30
C
C        FIN DU CHAINAGE
         GOTO 90
      ENDIF
C
C     INTERSECTION DANS LE TRONC DE CONE
      N0 = 1
C
C     POINT N2 SUR LE TRONC DE CONE AVEC SON PREDECESSEUR N1 EXTERIEUR
      NBP = NBP + 1
      N1COUR(NBP) = N2
      IF( N1 .EQ. 0 ) GOTO 40
C
C     AFFINAGE DE L'ANGLE PAR DICHOTOMIE POUR QUE LE POINT
C     SOIT EXACTEMENT DANS LE PLAN DE TRONCATURE DU CONE
      ANGLE1 = RPINT( 1, N1 )
      ANGLE2 = RPINT( 1, N2 )
      CALL PLCNDI( PARAME, ANGLE1, ANGLE2,
     %             RAY1CN, RAY2CN, HAUTCN, XYZCPL, VN,
     %             ANGLE,  P, Q,   IERR )
      IF( IERR .NE. 0 ) RETURN
C     LE NOUVEL ANGLE OBTENU
      RPINT( 1, N2 ) = ANGLE
C     LE NOUVEAU PARAMETRE OBTENU
      RPINT( 2, N2 ) = PARAME
C
C     RECHERCHE DU DERNIER POINT SUR LE TRONC DE CONE
C     -----------------------------------------------
 40   N1 = N2
      N2 = NPSUIV(N2)
      IF( N2 .EQ. NF ) GOTO 90
      PARAME = RPINT( 2, N2 )
      IF( 0D0 .LE. PARAME .AND. PARAME .LE. 1D0 ) THEN
C        LE POINT N2 EST SUR LE TRONC DE CONE. PASSAGE AU SUIVANT
         GOTO 40
      ENDIF
C
C     LE POINT N2 EST EXTERIEUR ET SON PREDECESSEUR N1 EST INTERNE
C     AFFINAGE DE L'ANGLE PAR DICHOTOMIE POUR QUE LE POINT
C     SOIT EXACTEMENT DANS LE PLAN DE TRONCATURE DU CONE
      ANGLE2 = RPINT( 1, N1 )
      ANGLE1 = RPINT( 1, N2 )
      CALL PLCNDI( PARAME, ANGLE1, ANGLE2,
     %             RAY1CN, RAY2CN, HAUTCN, XYZCPL, VN,
     %             ANGLE,  P, Q,   IERR )
      IF( IERR .NE. 0 ) RETURN
C     LE NOUVEL ANGLE OBTENU
      RPINT( 1, N2 ) = ANGLE
C     LE NOUVEAU PARAMETRE OBTENU
      RPINT( 2, N2 ) = PARAME
C
C     N2 EST LE DERNIER DU CHAINAGE DE CETTE PARTIE DE LIGNE
      NBP = NBP + 1
      N1COUR(NBP) = N2
C     PAS D'INTERSECTION
      N0 = 0
C     PASSAGE AU SUIVANT
      N1 = N2
      N2 = NPSUIV(N2)
C     ATTENTION A NE PAS MODIFIER CET ORDRE
C     N1=ANCIEN N2 EST LE DERNIER DU CHAINAGE DE CETTE PARTIE DE LIGNE
      NPSUIV(N1) = 0
      IF( N2 .NE. NF ) GOTO 30
C
C     DERNIER POINT ATTEINT N2=NF
C     ===========================
 90   IF( N0 .NE. 0 ) THEN
C        FERMETURE DE CETTE PARTIE DE LA LIGNE
         NBP = NBP + 1
         N1COUR(NBP) = N2
      ENDIF
C
C     EXISTE-T-IL DES POINTS D'INTERSECTION?
      IF( NBP .LE. 0 ) THEN
C        NON
         IERR = 3
         RETURN
      ENDIF
C
C     LA LIGNE SE REFERME T ELLE ?
C     ============================
      IF( N1COUR(NBP) .EQ. N1COUR(1) .OR.
     %   (RPINT(1,N1COUR(1)) .EQ. 0D0 .AND.
     %    ABS(DEUXPI-RPINT(1,N1COUR(NBP))) .LE. 1D-14) ) THEN
C
C        OUI => CHAINAGE CIRCULAIRE
         NPSUIV( N1 ) = N1COUR(1)
         IF( NBP .EQ. 2 ) THEN
            N1COUR( 2 ) = N1COUR(1)
C
         ELSE
C
C           POUR OBTENIR UN ANGLE CONTINU, LA DERNIERE PARTIE
C           DEMANDE UN RETRAIT DE 2 PI
            N2 = N1COUR(NBP-1)
 100        IF( N2 .NE. N1COUR(NBP) ) THEN
               RPINT(1,N2) = RPINT(1,N2) - DEUXPI
               N2 = NPSUIV(N2)
               GOTO 100
            ENDIF
C           SUPPRESSION DU DERNIER COUPLE CAR LA PREMIERE PARTIE
C           A ETE SCINDEE EN 2 PARTIES POURTANT CONTINUES
            N1COUR( 1 ) = N1COUR( NBP-1 )
            NBP = NBP - 2
         ENDIF
      ENDIF
C
      RETURN
      END
