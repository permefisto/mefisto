      SUBROUTINE VD3F5F( NOTRCF, N1ARCF, NOARCF,
     %                   MXFACO, LEFACO, NBFACO,
     %                   N1FASC, NBFAPE, NOFAPE,
     %                   NBSOMM, MXSOMM, PTXYZD, NPSOFR, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    DECOUPER EN 5 FACES LE PENTAGONE FORME DE 3 TRIANGLES
C -----    PAR AJOUT DU BARYCENTRE DES SOMMETS
C
C ENTREES:
C --------
C NOTRCF : TABLEAU DU NUMERO DANS LEFACO DES 3 TRIANGLES DU PENTAGONE
C N1ARCF : TABLEAU (0:MXTRCF) AUXILIAIRE
C NOARCF : NUMERO DES ARETES DE LA LIGNE DU CONTOUR FERME SELON UN SENS
C
C MODIFIES :
C ----------
C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO
C LEFACO : LES 3 SOMMETS, 2 MATERIAUX, 3 FACES VOISINES ET CHAINAGE
C          DES FACES TRIANGULAIRES DU CONTOUR ET INTERFACES
C NBFACO ; NOMBRE DE FACES ACTIVES DE LEFACO
C N1FASC : N1FASC(NS)=NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS
C NBFAPE : NOMBRE DE FACES PERDUES
C NOFAPE : NUMERO DES FACES PERDUES
C NBSOMM : NUMERO DU DERNIER SOMMET AJOUTE A LA TETRAEDRISATION
C          MIS A JOUR EN SORTIE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TETRAEDRISATION
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NPSOFR : NPSOFR(I)=1 SI POINT AJOUTE AU MILIEU D'UNE ARETE LEFACO
C                   =2 SI POINT AJOUTE BARYCENTRE DE SOMMETS LEFACO
C
C SORTIE :
C --------
C IERR   : 0 SI PAS D'ERREUR
C          1 SI SATURATION DES SOMMETS DE PTXYZD
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    OCTOBRE 1991
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/gsmenu.inc"
      DOUBLE PRECISION  PTXYZD(1:4,1:MXSOMM)
      INTEGER           NOTRCF(3),
     %                  N1ARCF(0:*),
     %                  NOARCF(1:3,1:*),
     %                  LEFACO(11,0:MXFACO),
     %                  NPSOFR(MXSOMM),
     %                  N1FASC(1:*),
     %                  NOFAPE(1:NBFAPE)
      DOUBLE PRECISION  D
C
C     LES 2 VOLUMES DES FACES
      NV1 = LEFACO( 4 , NOTRCF(1) )
      NV2 = LEFACO( 5 , NOTRCF(1) )
C
C     TRACE ET DESTRUCTION DES 3 FACES INITIALES
      DO 2 I=1,3
C        LA FACE I
         NF = NOTRCF(I)
CCC         CALL TRFATR( NCBLAN, NCNOIR, LEFACO(1,NF), PTXYZD )
C        LA FACE NF EST MARQUEE RETROUVEE DANS NOFAPE
         CALL VDNFRE( NF, NBFAPE, NOFAPE, LEFACO )
C        DESTRUCTION DE LA FACE NF DE LEFACO AVEC
C        MISE A JOUR DES ADJACENCES
         CALL VDDSFA( NF,  MXFACO, LEFACO, NBFACO, N1FASC )
 2    CONTINUE
C
C     CREATION DES COORDONNEES DU POINT BARYCENTRE DU PENTAGONE
      IF( NBSOMM .GE. MXSOMM ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:10),'(I10)') MXSOMM
         KERR(1) = 'MAXIMUM DE SOMMETS ' // KERR(MXLGER)(1:10) //
     %             ' ATTEINT'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
      NBSOMM = NBSOMM + 1
      DO 5 I=1,4
C        LA PREMIERE ARETE
         N1 = N1ARCF(1)
         D  = 0D0
         DO 4 J=1,5
            D = D + PTXYZD(I,NOARCF(1,N1))
            N1 = NOARCF(2,N1)
 4       CONTINUE
         PTXYZD(I,NBSOMM) = D * 0.2D0
 5    CONTINUE
C
C     BARYCENTRE DE 5 SOMMETS LEFACO
      NPSOFR(NBSOMM) = 2
C
C     LES FACES OPPOSEES AU CF
      NF0 = 0
      NF3 = -1
      N1  = N1ARCF(1)
      DO 10 I=1,5
C
C        LE NUMERO DES 2 SOMMETS DE L'ARETE ET DES 3 SOMMETS DE LA FACE
         NS1 = NOARCF(1,N1)
         N2  = NOARCF(2,N1)
         NS2 = NOARCF(1,N2)
C
C        LA FACE OPPOSEE AU CF
         NF1 = NOARCF(3,N1)
C
C        LA FACE NF1 EST SUPPOSEE RETROUVEE
         CALL VDNFRE( NF1, NBFAPE, NOFAPE, LEFACO )
C
C        AJOUT DU TRIANGLE I A PARTIR DE L'ARETE I ET DU BARYCENTRE
         CALL VDAJFA( NS1, NS2, NBSOMM, NV1, NV2, NF1, -1, NF3,
     %                MXFACO, LEFACO, NBFACO, N1FASC, NF )
         IF( NF .LE. 0 ) GOTO 9000
C
CCCC        TRACE DE LA NOUVELLE FACE
CCC         CALL TRFATR( NCCYAN, NCBLEU, LEFACO(1,NF), PTXYZD )
C
C        SAUVEGARDE DU NUMERO DE LA PREMIERE FACE
         IF( I .EQ. 1 ) NF0 = NF
C
C        MISE A JOUR DE N1FASC
         N1FASC( NS1 ) = NF
C
C        MISE A JOUR DE LA FACE OPPOSEE CONNUE
         NF3 = NF
C
C        ARETE SUIVANTE
         N1 = N2
 10   CONTINUE
C
C     MISE A JOUR DE L'ADJACENCE DE NF ET DE NF0
      DO 20 I=6,8
         IF( LEFACO(I,NF) .EQ. -1 ) THEN
            LEFACO(I,NF) = NF0
            GOTO 30
         ENDIF
 20   CONTINUE
C
C     MISE A JOUR DE L'ADJACENCE DE NF0 ET NF
 30   DO 40 I=6,8
         IF( LEFACO(I,NF0) .EQ. -1 ) THEN
            LEFACO(I,NF0) = NF
            GOTO 50
         ENDIF
 40   CONTINUE
 50   RETURN
C
C     SATURATION LEFACO
 9000 IERR = 2
      END
