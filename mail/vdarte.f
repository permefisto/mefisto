      SUBROUTINE VDARTE( NS1, NS2, N1TETS, NOTETR, MXTNS1, NOTNS1, NTE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER UN TETRAEDRE DE NOTETR CONTENANT L'ARETE
C -----    DE SOMMETS NS1 NS2

C ENTREES:
C --------
C NS1,NS2: NUMERO DES 2 SOMMETS DE L'ARETE A RECHERCHER DANS LA
C          TETRAEDRISATION ACTUELLE
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C MXTNS1 : MAX DE MOTS DU TABLEAU NOTNS1
C NOTNS1 : TABLEAU AUXILIAIRE DES TETRAEDRES CONTENANT LE SOMMET NS1

C SORTIES:
C ---------
C NTE    : >0 NUMERO D'UN TETRAEDRE DE NOTETR AYANT CETTE ARETE NS1-NS2
C          =0 SI AUCUN TETRAEDRE DE NOTETR NE CONTIENT CETTE ARETE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    OCTOBRE 1991
C2345X7..............................................................012
      include"./incl/trvari.inc"
      INTEGER        NOTETR(8,1:*), N1TETS(1:*), NOTNS1(MXTNS1)

C     RECHERCHE DES NBTE TETRAEDRES DE NOTETR CONTENANT LE SOMMET NS1
      CALL TETR1S( NS1, N1TETS, NOTETR, NBTE, MXTNS1, NOTNS1, IERR )
C     SI NBTE<=0 ALORS NTE=0 EN SORTIE SANS FAIRE UN TEST DE PLUS

C     BOUCLE SUR LES TETRAEDRES DE SOMMET NS1 ET CONTENANT LE SOMMET NS2
      DO N=1,NBTE
C        LE TETRAEDRE
         NTE = NOTNS1( N )
         DO I=1,4
            IF( NOTETR(I,NTE) .EQ. NS2 ) GOTO 9000
         ENDDO
      ENDDO

C     ICI PAS DE TETRAEDRE D'ARETE DE SOMMETS NS1-NS2
      NTE = 0

 9000 RETURN
      END
