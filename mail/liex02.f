      SUBROUTINE LIEX02( NTLXLI, LADEFI, RADEFI,
     %                   NTNSEF, MNNSEF, NTXYZS, MNXYZS, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES ARETES DE LA LIGNE SEGMENT DE DROITE
C -----     ( LIGNE DE TYPE 2 )
C
C ENTREES:
C --------
C NTLXLI : NUMERO DU TABLEAU TMS DU LEXIQUE DE LA LIGNE
C LADEFI : TABLEAU ENTIER DES DONNEES DE LA DEFINITION DE LA LIGNE
C          CF ~/td/d/a_ligne__definition
C RADEFI : TABLEAU REEL   DES DONNEES DE LA DEFINITION DE LA LIGNE
C
C ATTENTION: CES 2 TABLEAUX LADEFI ET RADEFI DOIVENT AVOIR
C            LA MEME ADRESSE MCN LORS DE L'APPEL
C
C SORTIES:
C --------
C NTNSEF : NUMERO      DU TMS 'NSEF' DES NUMEROS DES ARETES DE LA LIGNE
C MNNSEF : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES ARETES DE LA LIGNE
C          CF ~/td/d/a___nsef
C NTXYZS : NUMERO      DU TMS 'XYZSOMMET' DE LA LIGNE
C MNXYZS : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA LIGNE
C          CF ~/td/d/a___xyzsommet
C IERR   : 0 SI PAS D'ERREUR
C          1 SI NOMBRE DE SOMMETS INCORRECT <2
C          2 POINT INITIAL OU FINAL NON INITIALISE
C          3 POINT INITIAL ET FINAL CONFONDUS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC  PARIS  SEPTEMBRE 1996
C2345X7..............................................................012
      IMPLICIT INTEGER (W)
      include"./incl/gsmenu.inc"
      include"./incl/a_ligne__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/ntmnlt.inc"
C
C     LE SUPER-TABLEAU OU TOUS LES TMC ET TMS OUVERTS SONT STOCKES
C     ICI LE TABLEAU DMCN REEL DOUBLE PRECISION N'EST PAS UTILE
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE     ( MCN(1), RMCN(1) )
C
C     LE NUMERO D'UNITE DU CLAVIER, FENETRE DES AFFICHAGES ET
C     LE PARAMETRE DE NIVEAU D'INTERACTIVITE
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
C
C     LE TABLEAU DES DONNEES DE LA DEFINITION DE LA LIGNE
C     VU SOUS FORME D'UN TABLEAU D'ENTIERS
      INTEGER           LADEFI(0:*)
C     VU SOUS FORME D'UN TABLEAU DE REELS
      REAL              RADEFI(0:*)
C
      DOUBLE PRECISION  RAIGEO, XYZ1(3), XYZ2(3)
C
C     RECUPERATION DU NOMBRE D'ARETES ET DE SOMMETS DE LA LIGNE
      NBARLI = LADEFI(WBARLI)
C
C     RECUPERATION DU NUMERO DANS LE LX POINT DES 2 POINTS
C     EXTREMITES DE LA LIGNE SEGMENT DE DROITE
      NUPTIN = LADEFI( WUPTIN )
      NUPTFI = LADEFI( WUPTFI )
C
C     PROGRESSION GEOMETRIQUE OU NON DE LA LONGUEUR DES ARETES?
C     VALEUR NON UTILISEE SI LA FONCTION TAILLE_IDEALE EXISTE
      RAIGEO = RADEFI( WAIGEO )
C
C     EXISTENCE OU NON DE LA FONCTION 'TAILLE_IDEALE' DES ARETES AUTOUR DU POINT
C     ==========================================================================
C     ICI LA CARTE EST SUPPOSEE ISOTROPE
      NOFOTI = NOFOTIEL()
C     NOFOTI>0 SI CETTE FONCTION 'TAILLE_IDEALE' ou 'EDGE_LENGTH' EXISTE
      IF( NOFOTI .GT. 0 ) THEN
C        LA FONCTION 'TAILLE_IDEALE(X,Y,Z)' EXISTE
         IF( NBARLI .LT. 1 ) THEN
            NBARLI = 1
         ENDIF
      ENDIF
C
C     VERIFICATION DE LA COHERENCE DES DONNEES
      IF( NBARLI .LT. 1 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:8),'(I8)') NBARLI
         KERR(1) = 'NOMBRE INCORRECT D''ARETES'//KERR(MXLGER)(1:8)
         CALL LEREUR
         IERR = 1
         GOTO 9999
      ELSE IF( NUPTIN .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LE POINT INITIAL EST INCONNU'
         CALL LEREUR
         IERR = 2
         GOTO 9999
      ELSE IF( NUPTFI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LE POINT FINAL EST INCONNU'
         CALL LEREUR
         IERR = 2
         GOTO 9999
      ENDIF
C
C     RECUPERATION DES 3 COORDONNEES DU POINT INITIAL
      CALL LXNLOU( NTPOIN, NUPTIN, NTPOI, MN )
      CALL LXTSOU( NTPOI , 'XYZSOMMET',  NTSOM, MNSOM )
      MN = MNSOM + WYZSOM
      XYZ1( 1 ) = RMCN( MN )
      XYZ1( 2 ) = RMCN( MN + 1 )
      XYZ1( 3 ) = RMCN( MN + 2 )
C
C     RECUPERATION DES 3 COORDONNEES DU POINT FINAL
      CALL LXNLOU( NTPOIN, NUPTFI, NTPOI, MN )
      CALL LXTSOU( NTPOI , 'XYZSOMMET',  NTSOM, MNSOM )
      MN = MNSOM + WYZSOM
      XYZ2( 1 ) = RMCN( MN )
      XYZ2( 2 ) = RMCN( MN + 1 )
      XYZ2( 3 ) = RMCN( MN + 2 )
C
C     CALCUL DES ARETES ET SOMMETS DE CE SEGMENT DE DROITE
C     ====================================================
      CALL SEGDRO( NTLXLI, XYZ1,   XYZ2,   NBARLI, RAIGEO,
     %             NTNSEF, MNNSEF, NTXYZS, MNXYZS, IERR )
      MCN( MNXYZS + WBCOOR ) = 3
C
 9999 RETURN
      END
