      SUBROUTINE CRTGCT( NBCOTE, NUCOTE, MNSOCT, MNXYST, NBARLI, MNNTGL)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : GENERER LES TABLEAUX DES COORDONNEES DES TANGENTES AUX ARETES
C ----- DES NBCOTE COTES DU POLYGONE (TRIANGLE OU QUADRANGLE) COURBE
C
C ENTREES:
C --------
C NBCOTE : NOMBRE DE COTE DU TRIANGLE OU QUADRANGLE
C NUCOTE : NUCOTE(3) NUMERO DE 1 A 4 DU COTE 3 PARMI LES 4 LIGNES...
C          SI NUCOTE(3)=-4 LE COTE 3 EST LA LIGNE 4 A PARCOURIR
C                          EN SENS INVERSE DE SON RANGEMENT...
C MNSOCT : ADRESSE MCN DU TABLEAU XYZSOMMET DES 4 LIGNES  COTES
C          DU QUADRANGLE COURBE DANS L'ODRE DONN'E PAR L'UTILISATEUR
C MNARLI : ADRESSE MCN DU TABLEAU 'nsef'      DE LA LIGNE
C MNSOLI : ADRESSE MCN DU TABLEAU 'xyzsommet' DE LA LIGNE
C
C SORTIES:
C --------
C MNXYST : ADRESSE MCN DU TABLEAU DES 3 COORDONNEES DES SOMMETS DES LIGNES
C NBARLI : NOMBRE D'ARETES DE LA LIGNE  (0 SI PROBLEME RENCONTRE)
C MNXYTG : ADRESSE MCN DU TABLEAU DES 3 COMPOSANTES DES TANGENTES
C MNNTGL : ADRESSE MCN DU TABLEAU DES 2 NUMEROS DES TANGENTES DES
C          NBARLI ARETES DE LA LIGNE    (0 SI PROBLEME RENCONTRE)
C          TABLEAU A DETRUIRE EN FIN D'UTILISATION
C MNCGEF : ADRESSE MCN DU TABLEAU DU CODE GEOMETRIQUE DES NBARLI ARETES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS         OCTOBRE 1996
C234567..............................................................012
      IMPLICIT INTEGER (W)
      include"./incl/a___xyzsommet.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL             RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      INTEGER           NUCOTE(NBCOTE), MNSOCT(NBCOTE),
     %                  MNXYST(NBCOTE),
     %                  NBARLI(NBCOTE),
     %                  MNNTGL(NBCOTE)
C
C     L'ADRESSE DES SOMMETS DES NBCOTE LIGNES=COTES DU QUADRANGLE COURBE
      DO 20 K=1,NBCOTE
C
C        LE NUMERO DE LIGNE DU COTE K
         N = ABS( NUCOTE(K) )
C
C        LE NOMBRE DE SOMMETS DE LA LIGNE
         NBS = MCN( MNSOCT(N) + WNBSOM )
C
C        RESERVATION DES COORDONNEES DES SOMMETS DE LA LIGNE N
         CALL TNMCDC( 'REEL', 3*NBS, MNXYST(N) )
C
         IF( NUCOTE(K) .GT. 0 ) THEN
C           SOMMETS DANS LE BON SENS
            CALL TRTATA( RMCN(MNSOCT(N)+WYZSOM), RMCN(MNXYST(N)),
     %                   3*NBS )
         ELSE
C           SOMMETS DANS LE MAUVAIS SENS => ILS SONT INVERSES
            MNL1 = MNXYST(N)
            MNL0 = MNSOCT(N) + WYZSOM + 3 * NBS - 3
            DO 10 J=1,NBS
               RMCN(MNL1  ) = RMCN(MNL0  )
               RMCN(MNL1+1) = RMCN(MNL0+1)
               RMCN(MNL1+2) = RMCN(MNL0+2)
               MNL1 = MNL1 + 3
               MNL0 = MNL0 - 3
 10         CONTINUE
C           LES DERNIERS NUMEROS DEVIENNENT LES PREMIERS
            IF(MNNTGL(N).GT.0)CALL RENVER(2*NBARLI(N),MCN(MNNTGL(N)))
CCCC        LES DERNIERS NUMEROS CODES GEOMETRIQUES DEVIENNENT LES PREMIERS
CCC         CALL RENVER( NBARLI(N), MCN(MNCGEF(N)) )
         ENDIF
 20   CONTINUE
      END
