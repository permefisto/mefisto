      SUBROUTINE CH2T3T( INFACO, MXFACO, LEFACO, MXQUAL, IVOLTE, NVOLTE,
     %                   NT,     NF,     NTOP,
     %                   PTXYZD, N1TETS, NOTETR, N1TEVI, NUDTETR,
     %                   NTEN23, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    EVENTUEL CHANGEMENT DE 2 TETRAEDRES AYANT UNE FACE COMMUNE
C -----    EN 3 TETRAEDRES SI LE VOLUME EST CONSERVE
C          => UNE FACE EST ECHANGEE EN UNE ARETE
C          SI MXQUAL=1 ALORS CHANGEMENT 2T -> 3T SEULEMENT SI
C             MIN( QUALITE 2T INITIAUX ) < MIN( QUALITE DES 3T FINAUX )

C ENTREES:
C --------
C INFACO : =1 PAS DE 2T->3T SI LA FACE COMMUNE AUX 2T APPARTIENT A LEFACO
C          =0 SI PAS DE CONTROLE SUR LA FACE COMMUNE ET SON APPARTENANCE
C             A LEFACO
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC

C MXQUAL : =1 CHANGEMENT 2T -> 3T  SI et SEULEMENT SI
C             MIN( QUALITE 2T INITIAUX ) < MIN( QUALITE DES 3T )
C          =0 CHANGEMENT IMPOSE 2T -> 3T SI VOLUMES 2T et 3T EGAUX

C IVOLTE : =0 PAS       DE TABLEAU NVOLTE A L'APPEL de ch2t3t
C          =1 EXISTENCE DU TABLEAU NVOLTE A L'APPEL de ch2t3t
C NVOLTE :    NUMERO DU VOLUME (1 A NBVOPA) DE CHAQUE TETRAEDRE
C          =-1 SI VOLUME INCONNU

C NT     : NUMERO NOTETR DU PREMIER TETRAEDRE
C NF     : NUMERO DE 1 A 4 DE LA FACE COMMUNE DES 2 TETRAEDRES DANS NT
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS

C MODIFIES:
C ---------
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C N1TEVI : NUMERO DU 1 PREMIER TETRAEDRE VIDE DANS LE TABLEAU NOTETR
C          LE CHAINAGE DES TETRAEDRES VIDES SE FAIT SUR NOTETR(5,.)
C NUDTETR: PLUS GRAND NUMERO DANS NOTETR DE TETRAEDRE OCCUPE

C SORTIE :
C --------
C NTOP   : NUMERO DU TETRAEDRE OPPOSE AVANT SA DESTRUCTION
C NTEN23 : NUMERO NOTETR DES 3 TETRAEDRES QUI REMPLACENT NT NTOP

C IERR   : 0 SI PAS D'ERREUR ET 2T -> 3T EST REALISE
C          1 SI PAS DE TETRAEDRE DE L'AUTRE COTE DE LA FACE NF DE NT
C          2 SI SATURATION DU TABLEAU NOTETR
C          3 SI LE VOLUME NT+NTOP EST DIFFERENT DU VOLUME DE NTEN23(1:3)
C            OU SI UN DES 3 TETRAEDRES EST DE VOLUME NUL OU NEGATIF
C            => PAS DE TRANSFORMATION 2T=>3T
C          4 SI MIN( QUALITE 2T INITIAUX ) >= MIN( QUALITE DES 3T )
C          5 SI LA FACE EST DANS LEFACO ET POUR LA CONSERVER PAS D'ECHANGE
C          6 SI NOTETR(1,NT)<=0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       JUIN 1993
C....................................................................012
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      DOUBLE PRECISION  PTXYZD(1:4,1:*),VOLUM1,VOLUM2,VOLTET,V
      DOUBLE PRECISION  ARMIN, ARMAX, SURFTR(4)
      REAL              Q5TE(5)
      INTEGER           INFACO, LEFACO(1:11,0:MXFACO), NVOLTE(*),
     %                  NOTETR(8,*),
     %                  N1TETS(*),
     %                  NTEN23(3)
      INTEGER           NSF(3),
     %                  NOSOTR(3),
     %                  NOSOFATE(3,4)
C     NO DES SOMMETS DES FACES POUR QUE VU DU SOMMET MANQUANT
C     LES SOMMETS SOIENT VUS DANS LE SENS DIRECT
C     C-A-D AVEC UN VECTEUR NORMAL VERS L'INTERIEUR DU TETRAEDRE
      DATA              NOSOFATE/ 1,2,3, 2,4,3, 3,4,1, 4,2,1 /

      IF( NOTETR(1,NT) .LE. 0 ) THEN
         IERR = 6
         RETURN
      ENDIF
      IERR = 0

C     L'ARETE NS1-NS2 PERFORE LA FACE COMMUNE AUX 2 TETRAEDRES
C     2 TETRAEDRES => 3 TETRAEDRES D'ARETE COMMUNE NS1-NS2

C     LE NUMERO DE FACE COMMUNE DANS LE TETRAEDRE OPPOSE
      CALL NOFAOP( NF, NT, NOTETR, NFOP, NTOP )
      IF( NFOP .EQ. 0 ) THEN
C        PAS DE TETRAEDRE OPPOSE
ccc         NBLGRC(NRERR) = 1
ccc         KERR(1) = 'ch2t3t: PAS DE TETRAEDRE OPPOSE'
ccc         CALL LEREUR
         IERR = 1
         GOTO 9000
      ENDIF

C     LA FACE NF DE NT EST ELLE DANS LEFACO?
      IF( INFACO .NE. 0 ) THEN
         CALL NULEFT( NF, NT, NOTETR, MXFACO, LEFACO, NFLE )
         IF( NFLE .GT. 0 ) THEN
C           OUI: LA FACE EST DANS LEFACO
C           POUR LA CONSERVER PAS D'ECHANGE
            IERR = 5
            GOTO 9000
         ENDIF
      ENDIF

      IF( IVOLTE .NE. 0 ) THEN
         NOVOLU = NVOLTE( NT )
         IF( NOVOLU .NE. NVOLTE( NTOP ) ) THEN
C           LES 2 TETRAEDRES NE SONT PAS DANS LE MEME VOLUME
            IERR = 5
            GOTO 9000
         ENDIF
      ELSE
         NOVOLU = 0
      ENDIF

C     NUMERO DES 3 SOMMETS DE LA FACE COMMUNE
      NSF(1) = NOTETR( NOSOFATE(1,NF), NT )
      NSF(2) = NOTETR( NOSOFATE(2,NF), NT )
      NSF(3) = NOTETR( NOSOFATE(3,NF), NT )

C     NS1 LE SOMMET DE NT N'APPARTENANT PAS A LA FACE COMMUNE
      IF( NF .NE. 1 ) THEN
         NS1 = NF - 1
      ELSE
         NS1 = 4
      ENDIF
      NS1 = NOTETR( NS1, NT )

C     NS2 LE SOMMET DE NTOP N'APPARTENANT PAS A LA FACE COMMUNE
      IF( NFOP .NE. 1 ) THEN
         NS2 = NFOP - 1
      ELSE
         NS2 = 4
      ENDIF
      NS2 = NOTETR( NS2, NTOP )

C     VERIFICATION : COMPARAISON DES 2 VOLUMES 2T ET 3T
      VOLUM1 = ABS( VOLTET( PTXYZD(1,NOTETR(1,NT)),
     %                      PTXYZD(1,NOTETR(2,NT)),
     %                      PTXYZD(1,NOTETR(3,NT)),
     %                      PTXYZD(1,NOTETR(4,NT)) ) )
     %       + ABS( VOLTET( PTXYZD(1,NOTETR(1,NTOP)),
     %                      PTXYZD(1,NOTETR(2,NTOP)),
     %                      PTXYZD(1,NOTETR(3,NTOP)),
     %                      PTXYZD(1,NOTETR(4,NTOP)) ) )

      VOLUM2 = VOLTET( PTXYZD(1,NSF(1)), PTXYZD(1,NSF(2)),
     %                 PTXYZD(1,NS2),    PTXYZD(1,NS1) )
      IF( VOLUM2 .LE. 0D0 ) THEN
         IERR = 3
C        RETOUR SANS MODIFICATION
         GOTO 9000
      ENDIF

      V = VOLTET( PTXYZD(1,NSF(2)), PTXYZD(1,NSF(3)),
     %            PTXYZD(1,NS2),    PTXYZD(1,NS1) )
      IF( V .LE. 0D0 ) THEN
         IERR = 3
C        RETOUR SANS MODIFICATION
         GOTO 9000
      ENDIF
      VOLUM2 = VOLUM2 + V
C
      V = VOLTET( PTXYZD(1,NSF(3)), PTXYZD(1,NSF(1)),
     %            PTXYZD(1,NS2),    PTXYZD(1,NS1) )
      IF( V .LE. 0D0 ) THEN
         IERR = 3
C        RETOUR SANS MODIFICATION
         GOTO 9000
      ENDIF
      VOLUM2 = VOLUM2 + V

C     COMPARAISON DU VOLUME DE L'ETOILE AVANT ET APRES
      IF( ABS(VOLUM1-VOLUM2) .GT. VOLUM1*1D-8 ) THEN
CCC         WRITE(IMPRIM,10010) VOLUM1,VOLUM2
CCC10010 FORMAT(' ch2t3t: VOLUME 2T AVANT',G25.16,' NON EGAL A 3T',G25.16)
         IERR = 3
C        RETOUR SANS MODIFICATION
         GOTO 9000
      ENDIF

C     RECHERCHE ON NON DU MAXIMUM DES QUALITES 2T ou 3T?
      IF( MXQUAL .GT. 0 ) THEN

C        OUI: CALCUL DES QUALITES DES TETRAEDRES NT et NTOP
         CALL QUATETD( PTXYZD(1,NOTETR(1,NT)),
     %                 PTXYZD(1,NOTETR(2,NT)),
     %                 PTXYZD(1,NOTETR(3,NT)),
     %                 PTXYZD(1,NOTETR(4,NT)),
     %                 ARMIN, ARMAX, SURFTR, V, Q5TE(1) )

         CALL QUATETD( PTXYZD(1,NOTETR(1,NTOP)),
     %                 PTXYZD(1,NOTETR(2,NTOP)),
     %                 PTXYZD(1,NOTETR(3,NTOP)),
     %                 PTXYZD(1,NOTETR(4,NTOP)),
     %                 ARMIN, ARMAX, SURFTR, V, Q5TE(2) )

C        OUI: CALCUL DES QUALITES DES 3 TETRAEDRES POSSIBLES
         CALL QUATETD( PTXYZD(1,NSF(1)),
     %                 PTXYZD(1,NSF(2)),
     %                 PTXYZD(1,NS2),
     %                 PTXYZD(1,NS1),
     %                 ARMIN, ARMAX, SURFTR, V, Q5TE(3) )

         CALL QUATETD( PTXYZD(1,NSF(2)), 
     %                 PTXYZD(1,NSF(3)),
     %                 PTXYZD(1,NS2),
     %                 PTXYZD(1,NS1),
     %                 ARMIN, ARMAX, SURFTR, V, Q5TE(4) )

         CALL QUATETD( PTXYZD(1,NSF(3)),
     %                 PTXYZD(1,NSF(1)),
     %                 PTXYZD(1,NS2),
     %                 PTXYZD(1,NS1),
     %                 ARMIN, ARMAX, SURFTR, V, Q5TE(5) )

         IF( MIN(Q5TE(1),Q5TE(2)) .GE. MIN(Q5TE(3),Q5TE(4),Q5TE(5)))THEN

            IERR = 4
C           RETOUR SANS MODIFICATION
            GOTO 9000

         ENDIF

      ENDIF

C     CHANGEMENT 2T => 3T : RECHERCHE DE 3 TETRAEDRES VIDES
C     ---------------------
      DO LL=1,3

         IF( N1TEVI .LE. 0 ) THEN
C           SATURATION DU TABLEAU NOTETR DES TETRAEDRES
            NBLGRC(NRERR) = 1
            KERR(1) = 'SATURATION DES TETRAEDRES NOTETR N1TEVI='
            L = NUDCNB( KERR(1) )
            WRITE(KERR(1)(L+1:L+10),'(I10)') N1TEVI
            CALL LEREUR
            IERR = 2
            GOTO 9000
         ENDIF
C        MISE A JOUR DU DERNIER TETRAEDRE OCCUPE DANS NOTETR
         NUDTETR = MAX( NUDTETR, N1TEVI )
         NTEN23( LL ) = N1TEVI
         IF( IVOLTE .NE. 0 ) THEN
C           LE NUMERO DE VOLUME DU NOUVEAU TETRAEDRE
            NVOLTE( N1TEVI ) = NOVOLU
         ENDIF

C        LE NOUVEAU PREMIER TETRAEDRE VIDE
         N1TEVI = NOTETR( 5, N1TEVI )

      ENDDO

C     RECHERCHE DES COUPLES DE FACES DE NT ET NTOP AYANT UNE ARETE COMMUNE
      DO LL=1,3

C        LE NUMERO DES 2 SOMMETS DE L'ARETE LL DE LA FACE COMMUNE
         NS3 = NSF(LL)
         NS4 = NSF(MOD(LL,3)+1)

C        LE NUMERO NF1 DE FACE DE NS1 - NS3 - NS4 DANS NT
         NOSOTR(1) = NS1
         NOSOTR(2) = NS3
         NOSOTR(3) = NS4
         CALL TRI3NO( NOSOTR, NOSOTR )
         CALL NO1F1T( NOSOTR, NOTETR(1,NT), NF1 )

C        LE NUMERO NF2 DE FACE DE NS2 - NS3 - NS4 DANS NTOP
         NOSOTR(1) = NS2
         NOSOTR(2) = NS3
         NOSOTR(3) = NS4
         CALL TRI3NO( NOSOTR, NOSOTR )
         CALL NO1F1T( NOSOTR, NOTETR(1,NTOP), NF2 )

C        LE TETRAEDRE LL EST CONSTRUIT
         NTN = NTEN23(LL)
         NOTETR(1,NTN) = NS3
         NOTETR(2,NTN) = NS4
         NOTETR(3,NTN) = NS2
         NOTETR(4,NTN) = NS1

C        LE CHAINAGE DES TETRAEDRES PAR LES FACES
C        LE TETRAEDRE EXTERIEUR A LA FACE AU DELA DE LA FACE
C        ATTENTION: IL PEUT NE PAS EXISTER UN TEL TETRAEDRE
         NOTETR(5,NTN) = NOTETR(4+NF2,NTOP)
         NOTETR(6,NTN) = NTEN23(MOD(LL,3)+1)
         IF( LL .GT. 1 ) THEN
            NFF = LL-1
         ELSE
            NFF = 3
         ENDIF
         NOTETR(7,NTN) = NTEN23(NFF)
         NOTETR(8,NTN) = NOTETR(4+NF1,NT)

C        MISE A JOUR DES TETRAEDRES OPPOSES
         NT1 = NOTETR(4+NF2,NTOP)
         IF( NT1 .GT. 0 ) THEN
            NOSOTR(1) = NS3
            NOSOTR(2) = NS4
            NOSOTR(3) = NS2
            CALL TRI3NO( NOSOTR, NOSOTR )
            CALL NO1F1T( NOSOTR, NOTETR(1,NT1), NFF )
            NOTETR(4+NFF,NT1) = NTN
         ENDIF

         NT1 = NOTETR(4+NF1,NT)
         IF( NT1 .GT. 0 ) THEN
            NOSOTR(1) = NS3
            NOSOTR(2) = NS4
            NOSOTR(3) = NS1
            CALL TRI3NO( NOSOTR, NOSOTR )
            CALL NO1F1T( NOSOTR, NOTETR(1,NT1), NFF )
            NOTETR(4+NFF,NT1) = NTN
         ENDIF

C        MISE A JOUR D'UN TETRAEDRE CONTENANT CHAQUE SOMMET
         N1TETS(NS1) = NTN
         N1TETS(NS2) = NTN
         N1TETS(NS3) = NTN
         N1TETS(NS4) = NTN

C        AJOUTER EVENTUELLEMENT LES 4 FACES DU TETRAEDRE NTN
C        DANS LE TABLEAU LEFACO
         CALL AJTELEFA( NTN, NOTETR, INFACO, MXFACO, LEFACO )

      ENDDO

C     SUPPRIMER EVENTUELLEMENT LES 4 FACES DU TETRAEDRE NT
C     DANS LE TABLEAU LEFACO
      CALL SUTELEFA( NT, NOTETR, INFACO, MXFACO, LEFACO )

C     SUPPRIMER EVENTUELLEMENT LES 4 FACES DU TETRAEDRE NTOP
C     DANS LE TABLEAU LEFACO
      CALL SUTELEFA( NTOP, NOTETR, INFACO, MXFACO, LEFACO )

C     DESTRUCTION DES TETRAEDRES NT ET NTOP
C     ILS REDEVIENNENT VIDES DANS NOTETR
      NOTETR(1,NT) = 0
      NOTETR(5,NT) = N1TEVI
      N1TEVI       = NT

      NOTETR(1,NTOP) = 0
      NOTETR(5,NTOP) = N1TEVI
      N1TEVI         = NTOP

      IF( IVOLTE .NE. 0 ) THEN
C        LEUR VOLUME EST INCONNU
         NVOLTE( NT   ) = -1
         NVOLTE( NTOP ) = -1
      ENDIF

      IERR = 0

 9000 RETURN
      END
