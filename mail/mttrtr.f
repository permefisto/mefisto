      SUBROUTINE MTTRTR( PXYD,   NUMTRI, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                   NCTRIA, NCARET )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LE TRIANGLE NUMTRI
C -----
C ENTREES:
C --------
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C NUMTRI : NUMERO DANS NOARTR DU TRIANGLE
C MOARTR : NOMBRE MAXIMAL D'ENTIERS PAR TRIANGLE
C NOARTR : LES 3 ARETES DES TRIANGLES +-ARETE1, +-ARETE2, +-ARETE3
C          ARETE1=0 SI TRIANGLE VIDE => ARETE2=TRIANGLE VIDE SUIVANT
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES, CHAINAGES EN +
C NCTRIA : NUMERO DE LA COULEUR DU TRIANGLE A TRACER
C NCARET : NUMERO DE LA COULEUR DES ARETES DU TRIANGLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JUILLET 1995
C....................................................................012
      include"./incl/trvari.inc"
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
C     TRACE OU NON DES TRIANGLES GENERES DANS LA TRIANGULATION(CF DVTR2D)
      INTEGER           NOARTR(MOARTR,*),
     %                  NOSOAR(MOSOAR,*)
      DOUBLE PRECISION  PXYD(3,*)
      REAL              X(3),Y(3)
C
C     TEST DE VALIDITE
      IF( TRATRI ) THEN
C
C        LE TRIANGLE DOIT ETRE TRACE
         IF( NUMTRI .LE. 0 ) RETURN
         IF( NOARTR(1,NUMTRI) .EQ. 0 ) RETURN
C
C        LE NUMERO DES 3 SOMMETS DU TRIANGLE NUMTRI
         CALL MT3STR( NUMTRI, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                NS1, NS2, NS3 )
C
C        LES XY DES 3 SOMMETS POUR LE TRACE DU TRIANGLE
         X(1) = REAL( PXYD(1,NS1) )
         Y(1) = REAL( PXYD(2,NS1) )
         X(2) = REAL( PXYD(1,NS2) )
         Y(2) = REAL( PXYD(2,NS2) )
         X(3) = REAL( PXYD(1,NS3) )
         Y(3) = REAL( PXYD(2,NS3) )
C
C        LE TRACE DE LA FACE ET DES ARETES
         CALL FACE2D( NCTRIA, NCARET, 3 , X , Y )
C
C        TRACE DU NUMERO DES SOMMETS DU TRIANGLE
         CALL ENTIER2D( NCNOIR, X(1), Y(1), NS1 )
         CALL ENTIER2D( NCNOIR, X(2), Y(2), NS2 )
         CALL ENTIER2D( NCNOIR, X(3), Y(3), NS3 )
C
C        COPIE DE MEMPX DANS FENETRE
         CALL MEMPXFENETRE
C
      ENDIF
C
      RETURN
      END
