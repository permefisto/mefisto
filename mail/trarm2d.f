      SUBROUTINE TRARM2D( NOCOUL, MOARET, MXARET, LARETE, XYZPOI )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER TOUTES LES ARETES DU TABLEAU LARETE
C -----    SELON LA COULEUR NOCOUL
C
C ENTREES:
C --------
C NOCOUL : NUMERO DE LA COULEUR DE TRACE DES ARETES
C          <0 => PAS DE TRACE
C MOARET : NOMBRE DE MOTS PAR ARETE CHAINEE DU TABLEAU LARETE
C MXARET : NOMBRE MAXIMAL D'ARETES DU TABLEAU LARETE
C LARETE : TABLEAU NUMERO DES 2 SOMMETS, LIEN, EF1, EF2, CHAINAGE
C          LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE CHAINEE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= 0 OU NUMERO DE L'ARETE CHAINEE SUIVANTE
C          LARETE(4,I)= NUMERO DU 1-ER TRIANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 1-ER  TRIANGLE
C          LARETE(5,I)= NUMERO DU 2-EME TRIANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 2-EME TRIANGLE
C          LARETE(6,I)= NUMERO DANS LARETE DE L'ARETE SUIVANTE
C            SOIT DANS LE CHAINAGE D'UNE LIGNE J ENTRE NUMILF ET NUMXLF
C            SOIT DANS LE CHAINAGE DES ARETES CHAINEES
C            0 SI C'EST LA DERNIERE
C XYZPOI : 3 COORDONNEES DES SOMMETS DES ARETES
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Fevrier 2010
C23456---------------------------------------------------------------012
      INTEGER  LARETE(1:MOARET,1:*)
      REAL     XYZPOI(1:3,1:*)
C
      IF( NOCOUL .LT. 0 ) RETURN
C
C     BOUCLE SUR LES ARETES
      DO 10 N = 1, MXARET
C
C        TRACE DE L'ARETE CHAINEE N
         NS1 = LARETE(1,N)
         IF( NS1 .LE. 0 ) GOTO 10
         NS2 = LARETE(2,N)
         IF( NS2 .LE. NS1 ) GOTO 10
         CALL TRAIT2D( NOCOUL,
     %                 XYZPOI(1,NS1), XYZPOI(2,NS1),
     %                 XYZPOI(1,NS2), XYZPOI(2,NS2) )
C
C        PASSAGE A L'ARETE SUIVANTE
C
 10   CONTINUE
C
      RETURN
      END
