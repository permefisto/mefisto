      SUBROUTINE SUEX40( NTLXSU, LADEFI,
     %                   NTFASU, MNFASU, NTSOSU, MNSOSU, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    EXTRAIRE D'UNE SURFACE1 LES TRIANGLES ET QUADRANGLES QUI NE
C -----    SONT PAS DANS LA SURFACE2
C          SURFACE EXTRAITE = SURFACE1 - SURFACE2
C          ATTENTION: LE MAILLAGE DE SURFACE 2 DOIT ETRE TOTALEMENT
C                     INCLUS DANS CELUI DE SURFACE1
C
C ENTREES:
C --------
C NTLXSU : NUMERO DU TABLEAU TS DU LEXIQUE DE LA SURFACE
C LADEFI : TABLEAU ENTIER DE DEFINITION DE LA SURFACE
C          CF ~/td/d/a_surface__definition
C
C SORTIES:
C --------
C NTFASU : NUMERO      DU TMS 'NSEF' DE LA SURFACE EXTRAITE
C MNFASU : ADRESSE MCN DU TMS 'NSEF' DE LA SURFACE EXTRAITE
C          CF ~/td/d/a___nsef
C NTSOSU : NUMERO      DU TMS 'XYZSOMMET' DE LA SURFACE EXTRAITE
C MNSOSU : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE EXTRAITE
C          CF ~/td/d/a___xyzsommet
C IERR   : 0 SI PAS D'ERREUR
C          1 SI SURFACE INITIALE INCONNUE
C          2 SI SURFACE INITIALE SANS NSEF
C          3 SI SURFACE INITIALE SANS XYZSOMMET
C          4 SI NB SOMMETS SURFACE2 > NB SOMMETS SURFACE1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Septembre 2010
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/a_surface__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/trvari.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      INTEGER           LADEFI(0:*)
      INTEGER           NOSOEL(12), NOSOEL2(4)
      CHARACTER*24      NMSUIN1, NMSUIN2
      DOUBLE PRECISION  DISMIN, D
C
      IERR   = 0
      NTFASU = 0
      MNFASU = 0
      NTSOSU = 0
      MNSOSU = 0
      MNSTID = 0
      MNFALV = 0
C
C     LA SURFACE INITIALE
C     ===================
C     LE NOM DE CETTE SURFACE
      NUSUIN1 = LADEFI( WUSUIN )
C     LE TABLEAU LEXIQUE DE CETTE SURFACE
      CALL LXNLOU( NTSURF, NUSUIN1, NTLXSF1, MN )
      IF( NTLXSF1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE1 INITIALE INCONNUE'
         ELSE
            KERR(1) = 'UNKNOWN INITIAL SURFACE1'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     LE NOM DE LA SURFACE INITIALE
      CALL NMOBNU( 'SURFACE', NUSUIN1, NMSUIN1 )
      IF( NDIMLI .GE. 3 ) THEN
C        ASSURER L'ORIENTATION DU MAILLAGE DE LA SURFACE
C        PAR PARCOURS DES EF A TRAVERS LES ARETES COMMUNES
C        ET PERMUTATION DES SOMMETS 2-NBSTEF DU SECOND EF
C        D'UNE ORIENTATION DIFFERENTE DU PREMIER EF
         CALL SUORIENT( NMSUIN1, NTFASF1, MNFASF1,
     %                  NTSOSF1, MNSOSF1, IERR )
      ENDIF
C
C     LE TABLEAU 'NSEF' DE CETTE SURFACE
      CALL LXTSOU( NTLXSF1, 'NSEF', NTFASF1, MNFASF1 )
      IF( NTFASF1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE1 SANS TMS NSEF'
         ELSE
            KERR(1) = 'SURFACE1 WITHOUT NSEF TMS'
         ENDIF
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C     NOMBRE D'EF FACES DE LA SURFACE1
      NBEFOB1 = MCN( MNFASF1 + WBEFOB )
C
C     LE TABLEAU 'XYZSOMMET' DE CETTE SURFACE
      CALL LXTSOU( NTLXSF1, 'XYZSOMMET', NTSOSF1, MNSOSF1 )
      IF( NTSOSF1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE1 SANS TMS XYZSOMMET'
         ELSE
            KERR(1) = 'SURFACE1 WITHOUT XYZSOMMET TMS'
         ENDIF
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
      NBSOM1 = MCN( MNSOSF1 + WNBSOM )
      NBTGS1 = MCN( MNSOSF1 + WNBTGS )
C
C     LA SURFACE2 INITIALE A RETIRER DE LA SURFACE INITIALE
C     =====================================================
C     LE NOM DE CETTE SURFACE
      NUSUIN2 = LADEFI( WUSUI2 )
C     LE TABLEAU LEXIQUE DE CETTE SURFACE
      CALL LXNLOU( NTSURF, NUSUIN2, NTLXSF2, MN )
      IF( NTLXSF2 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE2 A RETIRER INCONNUE'
         ELSE
            KERR(1) = 'UNKNOWN TAKE OFF SURFACE2'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     LE NOM DE LA SURFACE A RETIRER DE LA SURFACE INITIALE
      CALL NMOBNU( 'SURFACE', NUSUIN2, NMSUIN2 )
      IF( NDIMLI .GE. 3 ) THEN
C        ASSURER L'ORIENTATION DU MAILLAGE DE LA SURFACE
C        PAR PARCOURS DES EF A TRAVERS LES ARETES COMMUNES
C        ET PERMUTATION DES SOMMETS 2-NBSTEF DU SECOND EF
C        D'UNE ORIENTATION DIFFERENTE DU PREMIER EF
         CALL SUORIENT( NMSUIN2, NTFASF2, MNFASF2,
     %                           NTSOSF2, MNSOSF2, IERR )
      ENDIF
C
C     LE TABLEAU 'NSEF' DE CETTE SURFACE
      CALL LXTSOU( NTLXSF2, 'NSEF', NTFASF2, MNFASF2 )
      IF( NTFASF2 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE SANS TMS NSEF'
         ELSE
            KERR(1) = 'SURFACE WITHOUT NSEF TMS'
         ENDIF
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C     NOMBRE D'EF FACES DE LA SURFACE2
      NBEFOB2 = MCN( MNFASF2 + WBEFOB )
C
C     LE TABLEAU 'XYZSOMMET' DE CETTE SURFACE
      CALL LXTSOU( NTLXSF2, 'XYZSOMMET', NTSOSF2, MNSOSF2 )
      IF( NTSOSF2 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE SANS TMS XYZSOMMET'
         ELSE
            KERR(1) = 'SURFACE WITHOUT XYZSOMMET TMS'
         ENDIF
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
      NBSOM2 = MCN( MNSOSF2 + WNBSOM )
      NBTGS2 = MCN( MNSOSF2 + WNBTGS )
C
      IF( NBSOM2 .GE. NBSOM1 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE1 AVEC MOINS DE SOMMETS QUE SURFACE2'
         ELSE
            KERR(1) = 'SURFACE1 WITH LOWER VERTICES THAN SURFACE2'
         ENDIF
         CALL LEREUR
         IERR = 4
         RETURN
      ENDIF
C
C     IDENTIFICATION DES SOMMETS DE SURFACE2 DANS SURFACE1
C     ----------------------------------------------------
C     TOUS DOIVENT ETRE RETROUVES
      NBSTID = 0
      NBNID2 = 0
      CALL TNMCDC( 'ENTIER', NBSOM1, MNSTID )
      CALL AZEROI(  NBSOM1, MCN(MNSTID) )
C
      MN1 = MNSOSF1 + WYZSOM -3
      MN2 = MNSOSF2 + WYZSOM -3
C
      DO 22 I2=1,NBSOM2
         MNS2 = MN2 + 3 * I2
         MNS1 = MN1
         DO 11 I1=1,NBSOM1
            MNS1 = MNS1 + 3
            CALL XYZIDE( RMCN(MNS1), RMCN(MNS2), ID )
            IF( ID .EQ. 1 ) THEN
C              LE SOMMET I1 DE SURFACE1 EST LE SOMMET I2 DE SURFACE2
               MCN( MNSTID - 1 + I1 ) = I2
               NBSTID = NBSTID + 1
               GOTO 22
            ENDIF
 11      CONTINUE
C
C        LE SOMMET I2 DE SURFACE2 N'EST PAS UN SOMMET DE SURFACE1
         WRITE(IMPRIM,*)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*) 'SOMMET',I2,
     %                     ' DE SURFACE 2 NON IDENTIFIE DANS SURFACE1'
         ELSE
            WRITE(IMPRIM,*) 'VERTEX',I2,
     %                     ' of SURFACE 2 IS NOT IDENTIFIED IN SURFACE1'
         ENDIF
         WRITE(IMPRIM,*) 'XYZ',I2,' S2 =',(RMCN(MNS2+K),K=0,2)
         NBNID2 = NBNID2 + 1
C
C        RECHERCHE DU PLUS PROCHE SOMMET DE SURFACE1
         DISMIN = 1D100
         MNS1   = MN1
         DO 15 I1=1,NBSOM1
            MNS1 = MNS1 + 3
            D = (RMCN(MNS1  )-RMCN(MNS2  )) ** 2
     %        + (RMCN(MNS1+1)-RMCN(MNS2+1)) ** 2
     %        + (RMCN(MNS1+2)-RMCN(MNS2+2)) ** 2
            IF( D .LT. DISMIN ) THEN
               DISMIN = D
               I1MIN  = I1
            ENDIF
            IF( LANGAG .EQ. 0 ) THEN
          WRITE(IMPRIM,*)I1MIN,' EST SON PLUS PROCHE SOMMET DE SURFACE1'
            ELSE
          WRITE(IMPRIM,*)I1MIN,' is its NEAREST VERTEX of SURFACE1'
            ENDIF
            WRITE(IMPRIM,*) 'XYZ',I1,' S1 =',(RMCN(MNS2+K),K=0,2)
C
 15      CONTINUE
C
 22   CONTINUE
C
      IF( LANGAG .EQ. 0 ) THEN
      WRITE(IMPRIM,*)NBSOM1,' SOMMETS DANS SURFACE1'
      WRITE(IMPRIM,*)NBSOM2,' SOMMETS DANS SURFACE2'
      WRITE(IMPRIM,*)NBSTID,' SOMMETS DE SURFACE2 DANS SURFACE1'
      WRITE(IMPRIM,*)NBNID2,' SOMMETS DE SURFACE2 NON IDENTIFIES DANS SU
     %RFACE1'
      ELSE
      WRITE(IMPRIM,*)NBSOM1,' VERTICES in SURFACE1'
      WRITE(IMPRIM,*)NBSOM2,' VERTICES in SURFACE2'
      WRITE(IMPRIM,*)NBSTID,' VERTICES of SURFACE2 in SURFACE1'
      WRITE(IMPRIM,*)NBNID2,' VERTICES of SURFACE2 NOT IDENTIFIED in SUR
     %FACE1'
      ENDIF
C
      IF( NBNID2 .GT. 0 ) THEN
         NBLGRC(NRERR) = 2
         WRITE(KERR(5)(1:6),'(I6)') NBNID2
         IF( LANGAG .EQ. 0 ) THEN
      KERR(1) = KERR(5)(1:6) //
     %' SOMMETS DE SURFACE2 NE SONT PAS DES SOMMETS DE SURFACE1'
      KERR(2) = 'LE MAILLAGE DE SURFACE2 DOIT ETRE INCLUS DANS LE MAILLA
     %GE DE SURFACE1'
         ELSE
      KERR(1) = KERR(5)(1:6) //
     %' SURFACE2 VERTICES ARE NOT SURFACE1 VERTICES'
      KERR(2) = 'THE SURFACE2 MESH HAS TO BE INCLUDED IN SURFACE1 MESH'
         ENDIF
         CALL LEREUR
         IERR = 5
         RETURN
      ENDIF
C
C     EXTRACTION SURFACE= SURFACE1 - SURFACE2
C     UNE FACE DE SURFACE1 EST EXTRAITE SI
C     UN AU MOINS DE SES 3 SOMMETS N'EST PAS IDENTIFIE A UN SOMMET DE S2
C     OU SES 3 SOMMETS SONT IDENTIFIES MAIS LA FACE N'EST PAS IDENTIFIEE
C     A UNE FACE DE SURFACE 2
C     ----------------------------------------------------------------
C     LE NOMBRE DE FACES DE LA SURFACE EXTRAITE
      NBFASU = 0
      NBSOSU = 0
C
C     LES PARAMETRES DES NO SOMMET DU MAILLAGE DE SURFACE1
      CALL NSEFPA( MCN(MNFASF1),
     %             NUTYMA, NBSOEL, NBSOEF, NBTGEF,
     %             LDAPEF, LDNGEF, LDTGEF,
     %             NBEFOB1,NX, NY, NZ,
     %             IERR   )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     RESERVATION DE LA PLACE NECESSAIRE AU TABLEAU TEMPORAIRE
C     DE STOCKAGE DES NBSOEF SOMMETS DE CHAQUE EF EXTRAIT
C                 DES NBTGEF TANGENTES ET DU CODE GEOMETRIQUE
      MXFALV = NBEFOB1 * ( NBSOEF + NBTGEF + 1 )
      CALL TNMCDC( 'ENTIER', MXFALV, MNFALV )
      MNAVS = MNFALV - 1
      MNAVT = MNAVS + NBEFOB1 * NBSOEF
      MNAVG = MNAVT + NBEFOB1 * NBTGEF
C
C     LA BOUCLE SUR LES EF=FACES DU MAILLAGE DE SURFACE1
C     --------------------------------------------------
      DO 100 N = 1, NBEFOB1
C
C        LE NUMERO DES NBSOEF SOMMETS DE L'EF N
         CALL NSEFNS( N     , NUTYMA, NBSOEF, NBTGEF,
     %                LDAPEF, LDNGEF, LDTGEF,
     %                MNFASF1, NX, NY, NZ,
     %                NCOGEL, NUGEEF, NUEFTG, NOSOEL, IERR )
C        LE NOMBRE DE SOMMETS DE CET ELEMENT FINI = NCOGEL
C
C        TOUS LES SOMMETS DE L'EF DE SURFACE1 SONT ILS IDENTIFIES
C        DANS SURFACE2 ?
C        --------------------------------------------------------
         DO I=1,NCOGEL
C           LE SOMMET EST IL IDENTIFIE DANS SURFACE2 ?
            IF( MCN( MNSTID-1+NOSOEL(I) ) .EQ. 0 ) GOTO 20
         ENDDO
C
C        LES NCOGEL SOMMETS SONT IDENTIFIES COMME SOMMETS DE SURFACE2
C        LA FACE NOSOEL DE SURFACE1 EST ELLE DANS SURFACE2?
C        ------------------------------------------------------------
         DO I=1,NCOGEL
C           LES NO DE SOMMETS DE LA FACE S1 DEVIENNENT
C           LES NO DE SOMMETS DE LA FACE S2
            NOSOEL2(I) = MCN( MNSTID-1+NOSOEL(I) )
         ENDDO
         CALL FACSURF( NCOGEL, NOSOEL2, MNFASF2,  NOFASF2 )
         IF( NOFASF2 .LT. 0 ) GOTO 9999
         IF( NOFASF2 .GT. 0 ) GOTO 100
C
C        LA FACE N DE SURFACE1 N'EST PAS DANS SURFACE2: ELLE EST EXTRAITE
C        ----------------------------------------------
 20      NBFASU = NBFASU + 1
C
C        LES NUMEROS DES SOMMETS SONT STOCKES
         DO 30 I=1,NCOGEL
            MCN( MNAVS + I ) = NOSOEL( I )
 30      CONTINUE
C        COMPLETION EVENTUELLE AVEC DES ZEROS
         DO 40 I=NCOGEL+1,NBSOEF
            MCN( MNAVS + I ) = 0
 40      CONTINUE
         MNAVS = MNAVS + NBSOEF
C
C        LES NUMEROS DES NBTGEF TANGENTES OU 0 TANGENTE
         IF( NBTGEF .EQ. 0 .OR. NUEFTG .EQ. 0 ) THEN
C           LES NBTGEF TANGENTES SONT NULLES
            DO 50 I=1,NBTGEF
               MCN( MNAVT + I ) = 0
 50         CONTINUE
C           CODE GEOMETRIQUE
            MCN( MNAVG + 1 ) = 0
         ELSE
            DO 60 I=1,NBTGEF
               MCN( MNAVT + I ) = NOSOEL( NBSOEF + I )
 60         CONTINUE
C           CODE GEOMETRIQUE
            MCN( MNAVG + 1 ) = NUGEEF
         ENDIF
         MNAVT = MNAVT + NBTGEF
         MNAVG = MNAVG + 1
C
 100  CONTINUE
C
      IF( NBFASU .EQ. 0 ) THEN
C        PAS DE FACES EXTRAITES
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'PAS DE FACE DE SURFACE 2 DANS SURFACE1'
         ELSE
           KERR(1) = 'NO FACE of SURFACE2 IN SURFACE1'
         ENDIF
         CALL LEREUR
         IERR   = 6
         GOTO 9999
      ELSE IF( NBFASU .NE. NBEFOB1-NBEFOB2 ) THEN
C        TOUTES LES FACES DE SURFACE2 N'ONT PAS ETE OTEES
C        DES FACES DE SURFACE2 CE QUI EST LE CAS SI
C        SURFACE2 N'EST PAS TOTALEMENT INCLUSE DANS SURFACE1
         IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) NBEFOB1-NBFASU,' FACES de SURFACE2 EXTRAITES de
     % SURFACE1 PARMI LES',NBEFOB2,' FACES de SURFACE2'
         ELSE
         WRITE(IMPRIM,*) NBEFOB1-NBFASU,' FACES of SURFACE2 EXTRACTED of
     % SURFACE1 AMONG THE',NBEFOB2,' FACES of SURFACE2'
         ENDIF
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
           KERR(1) = 'SURFACE2 N''EST PAS INCLUSE DANS SURFACE1'
         ELSE
           KERR(1) = 'SURFACE2 IS NOT INCLUDED IN SURFACE1'
         ENDIF
         CALL LERESU
      ENDIF
C
C     RENUMEROTATION DES SOMMETS ET TANGENTES DES EF EXTRAITS
C     -------------------------------------------------------
      CALL REEFEX( 3,      NTLXSU, NBSOEF, NBTGEF,
     %             NBSOM1, NBTGS1, MNSOSF1,
     %             NBFASU, MNFALV, MNFALV+NBEFOB1*NBSOEF,
     %             MNFALV+NBEFOB1*(NBSOEF+NBTGEF),
     %             NTFASU, MNFASU, NTSOSU, MNSOSU, IERR )
C
C     DESTRUCTION DES TABLEAUX AUXILIAIRES
 9999 IF( MNFALV .GT. 0 ) CALL TNMCDS( 'ENTIER', MXFALV, MNFALV )
      IF( MNSTID .GT. 0 ) CALL TNMCDS( 'ENTIER', NBSOM1, MNSTID )
C
      RETURN
      END
