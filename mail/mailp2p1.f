      SUBROUTINE MAILP2P1( NDIM,    MNXYZN2, MNNPEF2,
     %                     NDPGST1, MNXYZN1, MNNPEF1, MNNOSO )
C
C     CREER UN MAILLAGE P1 A PARTIR DES SOMMETS D'UN MAILLAGE P2
C     POUR TRACER LA PRESSION P1 SANS PRENDRE LA DEMI SOMME
C     AUX EXTREMITES DE L'ARETE
C
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___npef.inc"
      INTEGER           MCN
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL             RMCN(1)
      DOUBLE PRECISION DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
C
      MNXYZN1 = 0
      MNNPEF1 = 0
C     NOMBRE DE NOEUDS P2
      NBNOEU = MCN(MNXYZN2+WNBNOE)
C     NOMBRE DE TANGENTES P2
      NBTGN  = MCN(MNXYZN2+WNBTGN)
C
C     NOMBRE DE COORDONNEES 3
      NBCOOR = MCN(MNXYZN2+WBCOON)
C
C     ADRESSE DEBUT DES XYZ DES NOEUDS P2
      MNXYZ2 = MNXYZN2 + WYZNOE
C
C     MNELE : ADRESSE DU TABLEAU NPEF"TYPE EF (P1D ou P2C)
      MNELE  = MCN( MNNPEF2 )
C
C     NOMBRE D'ELEMENTS FINIS DE CE TYPE
      NBELEM = MCN( MNELE + WBELEM )
C
C     LE NUMERO DU TYPE DE L'ELEMENT FINI
C     (13:TRIA 2P1D ou 15:TRIA 2P2C ou 19:TETR 3P1D ou 20:TETR 3P2C)
      NUTYEL = MCN( MNELE + WUTYEL )
C
C     NDPGST : CODE TRAITEMENT DES XYZ DES SOMMETS POINTS NOEUDS DU MAILLAGE
C              0 : NOEUDS=POINTS=SOMMETS
C              1 : NOEUDS=POINTS#SOMMETS
C              2 : NOEUDS#POINTS=SOMMETS
C              3 : NOEUDS#POINTS#SOMMETS
      NDPGST1 = 0
C
C     LES CARACTERISTIQUES DE L'ELEMENT FINI
      CALL ELNUNM( NUTYEL, NOMELE )
C
C     LES CARACTERISTIQUES DE L'ELEMENT FINI
      CALL ELTYCA( NUTYEL )
C
C     CONSTRUCTION DU TABLEAU NOSO: NO NOEUD  => NO SOMMET
      IF( MNNOSO .GT. 0 ) CALL TNMCDS('ENTIER', MONOSO, MNNOSO )
      MONOSO = NBNOEU
      CALL TNMCDC( 'ENTIER', MONOSO, MNNOSO )
C
C     NOMBRE DE SOMMETS DE L'EF TRIANGLE OU TETRAEDRE
      NBS = NDIM + 1
C
C     MARQUAGE DES SOMMETS
      CALL AZEROI( NBNOEU,  MCN(MNNOSO) )
      DO NUELEM = 1, NBELEM
         CALL EFNOEU( MNELE, NUELEM, NBNOEF, NONOEF )
         DO I = 1, NBS
C           TEMOIN DE SOMMET
            MCN( MNNOSO-1+NONOEF(I) ) = 1
         ENDDO
      ENDDO
C
C     NUMEROTATION CROISSANTE DES SOMMETS
      NBSOMT = 0
      DO I = 1, NBNOEU
         IF( MCN(MNNOSO-1+I ) .GT. 0 ) THEN
            NBSOMT = NBSOMT + 1
            MCN( MNNOSO-1+I ) = NBSOMT
         ENDIF
      ENDDO
C
C     DECLARATION DU TABLEAU XYZN P1
      CALL TNMCDC( 'REEL', 3*NBSOMT, MNXYZN1 )
C
C     NOMBRE DE MOTS DU TABLEAU NPEF P2
      NBNDEL = MCN(MNNPEF2+WBNDEL)
      NBPGEL = MCN(MNNPEF2+WBPGEL)
      LNPEF2 = WUNDEL + NBELEM*NBNDEL + NBELEM*NBPGEL
C
C     DECLARATION DU TABLEAU NPEF P1
      LNPEF1 = WUNDEL + NBELEM
      CALL TNMCDC( 'REEL', 3*NBSOMT, MNXYZN1 )
C     NON FINI ....
      RETURN
      END
