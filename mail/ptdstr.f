      SUBROUTINE PTDSTR( P, PTXYZD, NOSOTR, COBARY, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LE POINT P EST IL DANS LE TRIANGLE DE SOMMETS NOSOTR ?
C -----
C
C ENTREES:
C --------
C P      : LES 3 COORDONNEES DU POINT
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NOSOTR : LE NUMERO DES 3 SOMMETS DU TRIANGLE
C
C SORTIES:
C --------
C COBARY : LES 3 COORDONNEES BARYCENTRIQUES (/S3 /S1 /S2) SI TRIANGLE
C          NON DEGENERE ET NON INITIALISE SINON
C NONOUI : 1 SI LE POINT EST DANS OU SUR LE TRIANGLE
C          0 SI LE POINT P EST EXTERIEUR AU TRIANGLE
C         -1 SI LE TRIANGLE EST DEGENERE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1996
C....................................................................012
      DOUBLE PRECISION  EPSBAR
      PARAMETER        (EPSBAR=0D0)
      INTEGER           NOSOTR(3)
      DOUBLE PRECISION  PTXYZD(4,*), P(3), S, COBARY(3)
      DOUBLE PRECISION  SURTRD
C
C     LE PRODUIT MIXTE P1 P2 P3  AVEC RESULTAT >=0
      S         = SURTRD( PTXYZD(1,NOSOTR(1)),
     %                    PTXYZD(1,NOSOTR(2)),
     %                    PTXYZD(1,NOSOTR(3)) )
      IF( S .EQ. 0D0 ) GOTO 9900
C
C     LE PRODUIT MIXTE P1 P2 P ET LA COORDONNEE BARYCENTRIQUE DE S3
      COBARY(1) = SURTRD( PTXYZD(1,NOSOTR(1)),
     %                    PTXYZD(1,NOSOTR(2)),
     %                    P ) / S
      IF( COBARY(1) .LT. EPSBAR ) GOTO 9000
C
C     LE PRODUIT MIXTE P2 P3 P ET LA COORDONNEE BARYCENTRIQUE DE S1
      COBARY(2) = SURTRD( PTXYZD(1,NOSOTR(2)),
     %                    PTXYZD(1,NOSOTR(3)),
     %                    P ) / S
      IF( COBARY(2) .LT. EPSBAR ) GOTO 9000
C
C     LE PRODUIT MIXTE P2 P3 P ET LA COORDONNEE BARYCENTRIQUE DE S2
      COBARY(3) = SURTRD( PTXYZD(1,NOSOTR(3)),
     %                    PTXYZD(1,NOSOTR(1)),
     %                    P ) / S
      IF( COBARY(3) .LT. EPSBAR ) GOTO 9000
C
C     BILAN GENERAL
      IF( COBARY(1)+COBARY(2)+COBARY(3) .GT. 1.000001D0 ) THEN
C         LE POINT P N'EST PAS DANS LE TRIANGLE
          GOTO 9000
      ENDIF
C
C     LE POINT P EST DANS OU SUR LE TRIANGLE
      NONOUI = 1
      RETURN
C
C    LE POINT P N'EST PAS DANS LE TRIANGLE
 9000 NONOUI = 0
      RETURN
C
C     TRIANGLE DEGENERE
 9900 NONOUI = -1
C
      RETURN
      END
