      SUBROUTINE IDSTFAPE( MXSOMM, PTXYZD, NPSOFR,
     %                     NFLPER, MXFACO, LEFACO,
     %                     MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                     MXTE1S, NOTE1S,
     %                     MXFETO, NFETOI, NBIDST, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RECUPERATION DE FACES PERDUES DE LA FRONTIERE PAR
C -----    IDENTIFICATION DE SOMMETS PROCHES NON FRONTALIERS
C          A DES SOMMETS FRONTALIERS

C ENTREES:
C --------
C NBSOMM : NOMBRE DE SOMMETS INITIAUX
C MXSOMM : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C              LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE PT OU FACE
C          = -1 SI LE POINT EST SOMMET TROP PROCHE PT OU FACE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT DEPLACE SUR LA SURFACE
C            DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE

C NFLPER : NUMERO LEFACO DE LA FACE PERDUE A TRAITER
C MXFACO : MAX DE FACES DECLARABLES DANS LEFACO
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...

C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON

cccC          12: = NO FACEOC DE 1 A NBFACES D'OC

C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C MXTE1S : NOMBRE D'ENTIERS DU TABLEAU NOTE1S AUXILIAIRE
C NOTE1S : TABLEAU AUXILIAIRE DE MXTE1S ENTIERS

C MXFETO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LE CHAINAGE NFETOI
C NFETOI : AU DEBUT VERSION1  FACES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR AYANT CETTE FACE
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DE CETTE FACE DANS LEFACO, 0 SI PAS DANS LEFACO
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C          ENSUITE VERSION2
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST DIRIGE VERS L'INTERIEUR DE L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C SORTIES :
C ---------
C NUDTETR : NUMERO NOTETR DU DERNIER TETRAEDRE ACTIF
C NBIDST  : NOMBRE D'IDENTIFICATIONS DE SOMMET INTERNE A UN SOMMET FRONTALIER
C IERR    : 0 SI PAS D'ERREUR
C          1 ETOILE AVEC MOINS DE 4 FACES
C          2 SATURATION D'UN TABLEAU
C          3 3 SOMMETS NON DANS UN TETRAEDRE
C          9 VOLUME DE L'ETOILE AVANT et APRES DIFFERENTS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & VEULETTES SUR MER   Decembre 2016
C2345X7..............................................................012
      PARAMETER       ( MXETOI=4096, MXPILE=4096, MXSSET=1024 )
      include"./incl/gsmenu.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)

      COMMON / TRTETR / STOPTE,TRACTE
      LOGICAL           STOPTE,TRACTE,TRACTE0

      DOUBLE PRECISION  PTXYZD(4,MXSOMM)
      INTEGER           NOTETR(8,MXTETR),
     %                  N1TETS(MXSOMM),
     %                  LEFACO(11,0:MXFACO),
     %                  NPSOFR(1:MXSOMM),
     %                  NVOLTE(1),
     %                  NOTE1S(MXTE1S),
     %                  NFETOI(5,MXFETO)

      CHARACTER*200     KTITRE
      DOUBLE PRECISION  D, DMIN, DMOY

      INTEGER           NOSOAR(2,6), NOSOAROP(2,6)
      DATA              NOSOAR   / 1,2,  2,3,  3,1,  1,4,  2,4,  3,4 /
      DATA              NOSOAROP / 2,3,  1,4,  2,4,  2,3,  3,1,  1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

C     TRACTE = FAUX ==> PAS DE TRACE DES TETRAEDRES
C     VRAI ==> TRACE DES TETRAEDRES DE L'ETOILE
      TRACTE0 = TRACTE

C     ==========================================================
C     BOUCLE SUR LES FACES LEFACO PERDUES DANS LA TETREDRISATION
C     ==========================================================
      IERR    = 0
      INFACO  = 1
      IVOLTE  = 0
      NBIDST  = 0
      NBEC2T3T= 0
      NBECMTNT= 0

C     NO NOTETR D'UN TETRAEDRE CONTENANT LA FACE LEFACO NFLPER
      NTE = LEFACO( 11, NFLPER )

ccc         PRINT*
ccc         PRINT*,'idstfape0: Traitement   FACE PERDUE',NFP,
ccc     %          ' LEFACO(',NFLPER,')=',(LEFACO(K,NFLPER),K=1,3),NTE,
ccc     %' NOTETR(1,',NTEB,')=',NOTETR(1,NTEB)


C     LA FACE LEFACO NFLPER EST ELLE DANS LA TETRAEDRISATION?
      IF( NTE .GT. 0 ) GOTO 9999

C     =========================================================
C     METHODE : DEPLACER UN SOMMET NON SOMMET FRONTALIER PROCHE
C               EN L'UN DES 3 SOMMETS DU TRIANGLE PERDU
C     =========================================================
      NBTE1S = 0
      DO NN = 1, 3

C        LE SOMMET NN DE LA FACE NFLPER
         NS = LEFACO( NN, NFLPER )

         IF( NS .GT. 0 ) THEN

            CALL TETR1S( NS,    N1TETS, NOTETR,
     %                   NBTNS, MXTE1S-NBTE1S, NOTE1S(NBTE1S+1), IERR )
            IF( IERR .NE. 0 ) GOTO 9000
C           IERR   : = 3 SI SATURATION DU TABLEAU NOTE1S
C                    = 2 SI LE TETRAEDRE N1TETS(NS) N'EST PAS ACTIF
C                    = 1 SI LE SOMMET NS N'EST PAS UN SOMMET DE N1TETS(NS)

            NBTE1S = NBTE1S + NBTNS
C           NBTE1S : EN ENTREE NOMBRE DE TETRAEDRES DEJA RANGES DANS NOTE1S
C                    EN SORTIE AJOUT DES TETRAEDRES AYANT NS COMME SOMMET
            IF( NBTE1S .GT. MXTE1S ) THEN
               PRINT*,'idstfape: SATURATION du TABLEAU NOTE1S MXTE1S=',
     %                  MXTE1S
C              ABANDON DU TRAITEMENT DE LA FACE PERDUE
               GOTO 9000
            ENDIF


C           UNICITE DES NO DES TETRAEDRES DU TABLEAU NOTE1S DES 3 SOMMETS
C           DE LA FACE NFLPER
            CALL UNITABL( NOTE1S, NBTE1S )

            IF( IERR .NE. 0 .OR. NBTE1S .LE. 0 ) THEN
               PRINT*,'idstfape: Probleme dans call tetr1s.f No St=',
     %                 NS,' NBTE1S=',NBTE1S,' MXTE1S=',MXTE1S,
     %                ' IERR=',IERR
C              ABANDON DU TRAITEMENT DE LA FACE PERDUE
               GOTO 9000
            ENDIF

         ENDIF

      ENDDO

C     VERIFICATION DES TETRAEDRES OPPOSES
      CALL VEOPTE( NBTE1S, NOTE1S, NOTETR, PTXYZD, NBFANR )
      IF( NBFANR .NE. 0 ) THEN
         PRINT*,'idstfape: ProbleME dans call veopte NBFANR=',NBFANR
         GOTO 9000
      ENDIF

C     TRACE DES TETRAEDRES, TRIANGLES, ARETES DE LA FRONTIERE
      IF( NBTE1S .GT. 0 ) THEN
        KTITRE='idstfape:           TETRAEDRES PROCHES DE LA FACE PERDUE
     %            '
        WRITE(KTITRE(11:18),'(I8)') NBTE1S
        WRITE(KTITRE(58:65),'(I8)') NFLPER
        NS = LEFACO( 1, NFLPER )
        CALL TRACEETOILE( KTITRE, PTXYZD, NOTETR, NS, NBTE1S, NOTE1S )
      ENDIF

C     TROUVER LE SOMMET NSNOFR NON FRONTALIER LE PLUS PROCHE
C     DE L'UN DES 3 SOMMETS DE LA FACE LEFACO PERDUE NFLPER
C     ------------------------------------------------------
      NSNOFR = 0
      NSDLFR = 0
      DMIN   = 1D111
      DMOY   = 0D0
      NB     = 0
      DO 28 NS = 1, 3

C        LE SOMMET NS DE LA FACE LEFACO NFLPER
         NSDFR = LEFACO( NS, NFLPER )
         IF( NSDFR .LE. 0 ) GOTO 28

C        RECHERCHE DU SOMMET NSNFR NON FRONTALIER SOMMET DE
C        L'UN DES NBTE1S TETRAEDRES NOTE1S

         DO N = 1, NBTE1S

C           NO NOTETR DU TETRAEDRE
            NTE = NOTE1S( N )

            DO 25 K=1,4

C              SOMMET K DU TETRAEDRE NTE
               NSNFR = NOTETR( K, NTE )

               IF( NSNFR .EQ. NSDFR ) GOTO 25

C              LE SOMMET EST IL UN SOMMET FRONTALIER?
               IF( NPSOFR(NSNFR) .GT. 0 ) GOTO 25

C              CARRE DE LA DISTANCE NSDFR-NSNFR
               D = ( PTXYZD(1,NSDFR) - PTXYZD(1,NSNFR) ) ** 2
     %           + ( PTXYZD(2,NSDFR) - PTXYZD(2,NSNFR) ) ** 2
     %           + ( PTXYZD(3,NSDFR) - PTXYZD(3,NSNFR) ) ** 2
               D = SQRT( D )

               NB   = NB + 1
               DMOY = DMOY + D

               IF( D .LT. DMIN ) THEN
                  NSDLFR = NSDFR
                  NSNOFR = NSNFR
                  DMIN   = D
               ENDIF

 25         ENDDO

         ENDDO

 28   ENDDO
      DMOY = DMOY / NB

C     LE SOMMET NSNOFR EST IL PROCHE DU SOMMET NSDLFR FRONTALIER?
C     -----------------------------------------------------------
ccc      IF( NSNOFR .EQ. 0 .OR. DMIN .GE. DMOY*0.16D0 ) THEN
ccc      IF( NSNOFR .EQ. 0 .OR. DMIN .GE. DMOY*0.2D0 ) THEN

      IF( NSNOFR .EQ. 0 .OR. DMIN .GE. DMOY*0.25D0 ) THEN
ccc         PRINT*,'idstfape: PAS DE SOMMET NON FRONTIERE SUFFISAMMENT P
ccc     %ROCHE DES SOMMETS FRONTERE DMIN=',DMIN,' DMOY=',DMOY
         GOTO 9000
      ENDIF

C     OUI: LES SOMMETS NSNOFR et NSDLFR SONT SUFFISAMMENT PROCHES
C          SIMULATION DU DEPLACEMENT DU SOMMET NSNOFR NON FRONTALIER
C          AU SOMMET NSDLFR FRONTALIER
C     --------------------------------------------------------------
      CALL SISTST( NSNOFR, NSDLFR, MXSOMM, PTXYZD, NPSOFR,
     %             MXTETR, N1TETS, NOTETR, 0,      NVOLTE,
     %             MXETOI, N1FEOC, NFETOI,
     %             MXTE1S, NBTE1S, NOTE1S, IERR )

      IF( IERR .NE. 0 ) THEN
C        IDENTIFICATION REFUSEE
ccc         PRINT*,'idstfape: PB dans call sistst IERR=',IERR
         GOTO 9000
      ENDIF

C     DEPLACEMENT EFFECTIF DU SOMMET NSNOFR NON FRONTALIER AU
C     SOMMET NSDLFR FRONTALIER NSNOFR -> NSDLFR
C     -------------------------------------------------------
      CALL IDSTST( NSNOFR, NSDLFR, MXSOMM, PTXYZD, NPSOFR,
     %             MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %             0,      NVOLTE,
     %             MXETOI, N1FEOC, NFETOI,
     %             MXTE1S, NBTE1S, NOTE1S, QUAMIN, IERR )
C     NBTE1S: NB DE  TETRAEDRES DE NOTETR CONTENANT LE SOMMET NSDLFR
C     NOTE1S: NO DES TETRAEDRES DE NOTETR CONTENANT LE SOMMET NSDLFR

      IF( IERR .NE. 0 ) THEN
C        TENTATIVE ABANDONNEE
ccc            PRINT*,'idstfape: PB dans call idstst IERR=',IERR,
ccc     %    ' NSNOFR=',NSNOFR,' NON IDENTIFIE au St NSDLFR=', NSDLFR
         GOTO 9000
      ENDIF

C     IDENTIFICATION REALISEE  LE SOMMET NSNOFR -> NSDLFR
      NBIDST = NBIDST + 1

      PRINT*,'idstfape: IDENTIFICATION du SOMMET NON FRONTALIER',
     %        NSNOFR,' au SOMMET FRONTALIER',NSDLFR

C     VERIFICATION DES TETRAEDRES OPPOSES
      CALL VEOPTE( NBTE1S, NOTE1S, NOTETR, PTXYZD, NBFANR )
      IF( NBFANR .NE. 0 ) THEN
         PRINT*,'idstfape: PRObleME dans call veopte NBFANR=',NBFANR
         GOTO 9000
      ENDIF

C     MISE A JOUR DES FACES LEFACO DES FACES DES TETRAEDRES DE NOTE1S
      CALL MJLEFACO( NBTE1S, NOTE1S, NOTETR, MXFACO, LEFACO )


C     ESSAI D'AMELIORER LES TETRAEDRES DE L'ETOILE PAR 2T -> 3T ou mT->2m-4T
C     ----------------------------------------------------------------------
      CALL EC2TMTNT( NSDLFR, PTXYZD, INFACO,  MXFACO, LEFACO,
     %               0,      NVOLTE, MXTE1S,  NBTE1S, NOTE1S,
     %               NOTETR, N1TEVI, NUDTETR, N1TETS,
     %               NBEC2T3T, NBECMTNT )

      IF( NBEC2T3T .GT. 0 .OR. NBECMTNT .GT. 0 ) THEN
         PRINT *,'idstfape: NSDLFR=',NSDLFR, ' SOMMET de',
     %            NBTE1S,  ' TETRAEDRES.',
     %            NBEC2T3T,' echanges 2T->3T et',
     %            NBECMTNT,' mT->2m-4T'
C        VERIFICATION DES TETRAEDRES OPPOSES
         CALL VEOPTE( NBTE1S, NOTE1S, NOTETR, PTXYZD, NBFANR )
         IF( NBFANR .NE. 0 ) THEN
            PRINT*,'idstfape: Probleme dans call veopte NbFANR=',NBFANR
            GOTO 9000
         ENDIF
      ENDIF

      KTITRE='idstfape:           TETRAEDRES de SOMMETS        ->      '
      WRITE(KTITRE(12:18),'(I7)') NBTE1S
      WRITE(KTITRE(42:49),'(I8)') NSNOFR
      WRITE(KTITRE(52:59),'(I8)') NSDLFR
      CALL TRACEETOILE( KTITRE, PTXYZD, NOTETR, NSDLFR, NBTE1S, NOTE1S )

C     MISE A JOUR DU TABLEAU N1TETS
C     -----------------------------
      CALL MJN1TETS( MXTETR, NOTETR, MXSOMM, N1TETS, NUDTETR, NBSOMM )

C     BILAN FINAL
 9000 PRINT*,'idstfape:',NBIDST,' IDENTIFICATIONS REUSSIES',
     % NBEC2T3T,' Echanges 2T->3T et',NBECMTNT,' mT->2m-4T'

 9999 TRACTE = TRACTE0
      RETURN
      END
