      SUBROUTINE C6X123000( NOPROJ,    KNOMOB,    NTLXOB,
     %                      NTEF6Q1C,  MNEF6Q1C,  MNTE6Q1C,
     %                      NTEF3Q1C,  MNEF3Q1C,  NTTE3Q1C, MNTE3Q1C,
     %                      MOXYZ3Q1C, MNXYZ3Q1C, NBTYEL,   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CONSTRUIRE A PARTIR DE LA TOPOLOGIE D'UN MAILLAGE EN 6-CUBES
C -----    LES EF 3Q1C CONTENANT LE POINT (X4=0,X5=0,X6=0) ET
C          CONSTRUCTION DU TABLEAU DES TEMPERATURES(X1,X2,X3,0,0,0)
C          EN FAIT LA COORDONNEE 0 EST LA COORDONNEE MEDIANE ENTRE MIN ET MAX
C
C ENTREES:
C --------
C NOPROJ  : TYPE DE PROJECTION 0 CI-DESSOUS FIXE LA COORDONNEE A ZERO
C           1 : 'X Y Z 0 0 0'
C           2 : 'X Y 0 U 0 0'
C           3 : 'X 0 0 U V 0'
C           4 : '0 0 0 U V W'
C KNOMOB  : NOM DE L'OBJET
C NTLXOB  : LEXIQUE DE L'OBJET
C MNTE6Q1C: ADRESSE DU TMS DES TEMPERATURES DU MAILLAGE DE 6CUBES
C
C SORTIES:
C --------
C NTEF6Q1C: NUMERO      DU TMS NPEF"6Q1C
C MNEF6Q1C: ADRESSE MCN DU TMS NPEF"6Q1C
C NTEF3Q1C: NUMERO      DU TMS NPEF"3Q1C
C MNEF3Q1C: ADRESSE MCN DU TMS NPEF"3Q1C
C MNXYZ3Q1C: ADRESSE MCN DU TMC XYZNOEUD DES 3Q1C
C NBTYEL  : 2 NOMBRE DE TYPES D'EF (6Q1C et 3Q1C)
C IERR    : 0 SI PAS D'ERREUR, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET Laboratoire J-L LIONS UPMC PARIS Novembre 2006
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a_objet__topologie.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___xyznoeud.inc"
      include"./incl/a___xyzpoint.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___vecteur.inc"
C
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL             RMCN(1)
      DOUBLE PRECISION DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      PARAMETER        (MXTYEL=7)
      CHARACTER*(*)     KNOMOB
      CHARACTER *24     KNOM
C
      DOUBLE PRECISION  POLY6C(64,64), POLY3C(8,8), DPOLY3C(8,8,3), D
C
ccc      include"./incl/nusc3c6.inc"
C
C     OUVERTURE DE L'OBJET
      CALL LXLXOU( NTOBJE, KNOMOB, NTLXOB, MNLXOB )
      IF( NTLXOB .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET INCONNU ' // KNOMOB
         ELSE
            KERR(1) = 'ERROR: UNKNOWN OBJECT  ' // KNOMOB
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     RECUPERATION DU TABLEAU XYZSOMMET = XYZPOINT = XYZNOEUD
      CALL LXTSOU( NTLXOB, 'XYZSOMMET', NTXYZS, MNXYZS )
C
C     NBCOOR : NOMBRE DE COORDONNEES DES NOEUDS=POINTS (3 ou 6)
      NBCOOR = MCN( MNXYZS + WBCOOR )
      IF( NBCOOR .NE. 6 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET NON DE DIMENSION 6 ' // KNOMOB
         ELSE
            KERR(1) = 'ERROR: OBJECT NOT IN 6D ' // KNOMOB
         ENDIF
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C
C     NOMBRE DE SOMMETS DU MAILLAGE 6-CUBES
      NBST6C = MCN( MNXYZS + WNBSOM )
C
C     CALCULER LES 64 COEFFICIENTS DES 64 POLYNOMES DE BASE DE L'EF 6CUBE 6Q1C
      CALL CF6Q1C( POLY6C )
C
C     CALCULER LES 8 COEFFICIENTS DES 8 POLYNOMES DE BASE DE L'EF 3CUBE 3Q1C
      CALL CF3Q1C( POLY3C )
C     LES 8 COEFFICIENTS DES 8 POLYNOMES DERIVES DANS LES 3 DIRECTIONS
      DO 3 I=1,8
         DO 2 K=1,3
            CALL PN3DDE( K, 2, POLY3C(1,I), DPOLY3C(1,I,K) )
 2       CONTINUE
 3    CONTINUE
C
C     LE TABLEAU TOPOLOGIE DU MAILLAGE 6CUBES
      IERR = 0
      CALL LXTSOU( NTLXOB, 'TOPOLOGIE', NTTOPO, MNTOPO )
      IF( NTTOPO .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: OBJET SANS INTERPOLATION'
            KERR(2) = 'RE-EXECUTER MAILLER POUR LE FAIRE'
         ELSE
            KERR(1) = 'ERROR: OBJECT WITHOUT INTERPOLATION'
            KERR(2) = 'EXECUTE AGAIN MAILLER'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     LES TYPES D'ELEMENTS FINIS SONT RETROUVES
      NBTYEL = MCN( MNTOPO + WBTYEL )
      IF( NBTYEL .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:4),'(I4)') NBTYEL
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR:' // KERR(MXLGER)(1:4)
     %             //' TYPES D''ELEMENTS FINIS'
         ELSE
            KERR(1) = 'ERROR:' // KERR(MXLGER)(1:4)
     %             //' TYPES OF FINITE ELEMENTS'
         ENDIF
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
C     LES NO DE POINTS DES 6CUBES
      KNOM = 'NPEF"6Q1C'
      CALL LXTSOU( NTLXOB, KNOM, NTEF6Q1C, MNEF6Q1C )
      IF( NTEF6Q1C .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: NPEF"6Q1C ABSENT'
         ELSE
            KERR(1) = 'ERROR: NPEF"6Q1C DO NOT EXIST'
         ENDIF
         CALL LEREUR
         IERR = 4
         RETURN
      ENDIF
C
C     AJOUT EVENTUEL A LA TOPOLOGIE DE L'EF '3Q1C' DES 3-CUBES DU 6-CUBE
C     LES TYPES D'EF SONT DANS L'ORDRE  1:6Q1C  2:3Q1C
      IF( NBTYEL .EQ. 1 ) THEN
C        AJOUT DES EF 3Q1C POUR LES 3-CUBES DU 6-CUBE
         NBOBIN = MCN(MNTOPO+WBOBIN)
         NBOBCL = MCN(MNTOPO+WBOBCL)
C        NOMBRE DE MOTS DU TYPEOBJET => 2
         MOTTO = MOTVAR(13 )
         MOT = WMTYEL + NBTYEL + 1 + MOTTO * (NBOBIN + NBOBCL)
         CALL TAMSAU( NTTOPO, MOT )
         CALL TAMSOU( NTTOPO, MNTOPO )
         MN = MNTOPO+WMTYEL+NBTYEL-1
         DO 30 I=(NBOBIN+NBOBCL)*MOTTO,1,-1
            MCN(MN+I+1) = MCN(MN+I)
 30      CONTINUE
         MCN(MN+1) = ICHARX( '3Q1C' )
         NBTYEL = NBTYEL + 1
         MCN(MNTOPO+WBTYEL) = NBTYEL
      ENDIF
C
C     LA DATE DE LA TOPOLOGIE EST REMISE A JOUR
C     POUR FORCER LE RECALCUL DES FACES DES 3-CUBES
      CALL ECDATE( MCN(MNTOPO) )
C
C     LE NOMBRE D'EF 6Q1C
      NBEF6C = MCN(MNEF6Q1C+WBELEM )
C
C     TABLEAU AUXILIAIRE DES 3Q1C EXTRAITS
      D = NBEF6C
      D = SQRT( D )
      MXEF3C = 8 * NINT( D )
C
C     DECLARATION DES TMC XYZ3C et TEMPE3C
 40   CALL TNMCDC( 'REEL', 3*8*MXEF3C, MNXYZ3C )
      NBVECT = MCN( MNTE6Q1C + WBVECT )
      MNTEM3C = 0
      CALL TNMCDC( 'REEL2', 8*MXEF3C*NBVECT, MNTEM3C )
      IF( MNXYZ3C .EQ. 0 .OR. MNTEM3C .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'ERREUR: MEMOIRE SATUREE POUR DECLARER 3Q1C'
         ELSE
            KERR(1) = 'ERROR: SATURATED MEMORY TO DECLARE 3Q1C'
         ENDIF
         CALL LEREUR
         IERR = 5
         RETURN
      ENDIF
C
C     CALCULER les 3 COORDONNEES  XYZ3C des NBEF3C 3Q1C
C     INTERSECTION DES 6CUBES AVEC U=V=W=0 et LA TEMPERATURE
C     EN LES SOMMETS DES 3CUBES.
      CALL C6I123000( NOPROJ, NBEF6C, MCN(MNEF6Q1C+WUNDEL),
     %                NBST6C, RMCN(MNXYZS+WYZSOM),
     %                NBVECT, RMCN(MNTE6Q1C+WECTEU),
     %                POLY6C, POLY3C, DPOLY3C,
     %                MXEF3C, NBEF3C, RMCN(MNXYZ3C), RMCN(MNTEM3C),
     %                IERR )
C
C     LE TABLEAU XYZNOEUD DES SOMMETS DES 3Q1C EXTRAITS
      IF( MNXYZ3Q1C .GT. 0 ) CALL TNMCDS( 'MOTS', MOXYZ3Q1C, MNXYZ3Q1C )
      IF( NBEF3C .LE. 0 .OR. IERR .EQ. 3 ) THEN
         CALL TNMCDS( 'REEL',  3*8*MXEF3C,      MNXYZ3C )
         CALL TNMCDS( 'REEL2', 8*MXEF3C*NBVECT, MNTEM3C )
         MXEF3C = 4 * MXEF3C
         IERR = 0
         GOTO 40
      ENDIF
C
      NBST3C = 8 * NBEF3C
      MOXYZ3Q1C = WYZNOE+3*NBST3C
      CALL TNMCDC( 'MOTS', MOXYZ3Q1C, MNXYZ3Q1C )
      MCN(MNXYZ3Q1C+WBCOON) = 3
      MCN(MNXYZ3Q1C+WNBNOE) = NBST3C
      MCN(MNXYZ3Q1C+WNBTGN) = 0
      MN = MNXYZ3Q1C+WYZNOE-1
      DO 50 I=0,3*NBST3C-1
         RMCN( MNXYZ3Q1C+WYZNOE+I ) = RMCN(MNXYZ3C+I)
 50   CONTINUE
C
C     LA DATE
      CALL ECDATE( MCN(MNXYZ3Q1C) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNXYZ3Q1C + MOTVAR(6) ) = NONMTD( '~>>>XYZNOEUD' )
      CALL TNMCDS( 'REEL', 3*8*MXEF3C, MNXYZ3C )
C
C     SI LE TMS 'NPEF"3Q1C' EXISTE ALORS IL EST DETRUIT
      KNOM = 'NPEF"3Q1C'
      CALL LXTSOU( NTLXOB, KNOM, NTEF3Q1C, MNEF3Q1C )
      IF( MNEF3Q1C .GT. 0 ) THEN
         CALL LXTSDS( NTLXOB, KNOM )
         MNEF3Q1C = 0
      ENDIF
C
C     CONSTRUCTION DU TABLEAU NPEF"3Q1C
C     STOCKAGE DES NO DES 8 SOMMETS DES NBEF3C 3Q1C UNIQUEMENT
      CALL LXTNDC( NTLXOB, KNOM,'MOTS', WUNDEL + NBEF3C * 8 )
      CALL LXTSOU( NTLXOB, KNOM, NTEF3Q1C, MNEF3Q1C )
C
C     REMPLISSAGE DES VARIABLES DU TABLEAU NPEF"3Q1C
C     NOM DU TYPE DES ELEMENTS FINIS
      NMNPEF = ICHARX( '3Q1C' )
C     NUMERO 23 DU TYPE DE L'ELEMENT FINI 3Q1C
      NUTYEL = 23
      MCN( MNEF3Q1C + WUTYEL ) = NUTYEL
C     NBELEM 'NOMBRE D'EF DE CE TYPE
      MCN( MNEF3Q1C + WBELEM ) = NBEF3C
C     NBNDEL 'NOMBRE DE NOEUDS D''UN ELEMENT FINI DE CE TYPE'
      MCN( MNEF3Q1C + WBNDEL ) = 8
C     NBPGEL 'NOMBRE DE POINTS GEOMETRIQUES D''UN ELEMENT'
      MCN( MNEF3Q1C + WBPGEL ) = 0
C     NBPSEL 'NOMBRE DE POINTS UTILISATEUR SOMMET D''UN ELEMENT'
      MCN( MNEF3Q1C + WBPSEL ) = 0
C     MOPSEL 'NOMBRE DE VARIABLES DES TABLEAUX NLPSEL ET NEPSEL'
      MCN( MNEF3Q1C + WOPSEL ) = 0
C     NBLAEL 'NOMBRE DE LIGNES UTILISATEUR ARETE D''UN ELEMENT'
      MCN( MNEF3Q1C + WBLAEL ) = 0
C     MOLAEL 'NOMBRE DE VARIABLES DES TABLEAUX NLLAEL ET NELAEL'
      MCN( MNEF3Q1C + WOLAEL ) = 0
C     NBSFEL 'NOMBRE DE SURFACES UTILISATEUR FACE D''UN ELEMENT'
      MCN( MNEF3Q1C + WBSFEL ) = 0
C     MOSFEL 'NOMBRE DE VARIABLES DES TABLEAUX NLSFEL ET NESFEL'
      MCN( MNEF3Q1C + WOSFEL ) = 0
C     NBVCEL 'NOMBRE DE VOLUMES UTILISATEUR CUBE D''UN ELEMENT'
C     PAS DE VOLUME CAR EN PROJECTION QUEL EST LE BON VOLUME?
      MCN( MNEF3Q1C + WBVCEL ) = 0
C     MOVCEL 'NOMBRE DE VARIABLES DES TABLEAUX NLVCEL ET NEVCEL'
      MCN( MNEF3Q1C + WOVCEL ) = 0
C
C     LES TG DANS  NPEF"3Q1C  N'EXISTENT PAS
C     NOMBRE D'EF AVEC POINTEUR SUR EF A TG
      MCN( MNEF3Q1C + WBELAP ) = 0
C     NOMBRE D'EF A TG
      MCN( MNEF3Q1C + WBELTG ) = 0
C     NOMBRE DE TANGENTES D'UN EF
      MCN( MNEF3Q1C + WBTGEL ) = 0
C
C     NUMERO DES 8 SOMMETS DES NBEF3C 3Q1C
      NS3C = 0
      MNE3 = MNEF3Q1C + WUNDEL - NBEF3C
      DO 150 NEF=1,NBEF3C
         DO 140 N=1,8
            NS3C = NS3C + 1
            MCN( MNE3 + N*NBEF3C ) = NS3C
 140     CONTINUE
C        PERMUTATION DES NO DES SOMETS 3-4 et 7-8
C        POUR RETROUVER L'ORDRE DES 3Q1C STANDARDS
         L = MCN( MNE3 + 3*NBEF3C )
         MCN( MNE3 + 3*NBEF3C ) = MCN( MNE3 + 4*NBEF3C )
         MCN( MNE3 + 4*NBEF3C ) = L
         L = MCN( MNE3 + 7*NBEF3C )
         MCN( MNE3 + 7*NBEF3C ) = MCN( MNE3 + 8*NBEF3C )
         MCN( MNE3 + 8*NBEF3C ) = L
C        PASSAGE A L'EF SUIVANT
         MNE3 = MNE3 + 1
 150  CONTINUE
C
C     LA DATE
      CALL ECDATE( MCN(MNEF3Q1C) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNEF3Q1C + MOTVAR(6) ) = NONMTD( '~>>>NPEF' )
C
      WRITE(IMPRIM,*)'CREATION du TMS NPEF"3Q1C des ',NBEF3C,
     %' 3-CUBES projection des',NBEF6C,' 6-CUBES'
C
C
C     CONSTRUCTION DE 'VECTEUR"TEMP3Q1C' TEMPERATURES DES 3-CUBES
      CALL LXTSOU( NTLXOB, 'VECTEUR"TEMP3Q1C', NTTE3Q1C, MNTE3Q1C )
      IF( MNTE3Q1C .GT. 0 ) THEN
        CALL LXTSDS( NTLXOB, 'VECTEUR"TEMP3Q1C' )
        MNTE3Q1C = 0
      ENDIF
      NBCPIN = MCN( MNTE6Q1C + WBCPIN )
      MOREE2 = MOTVAR(6)
      L      = WECTEU + MOREE2 * NBST3C * NBVECT + NBCPIN
      CALL LXTNDC( NTLXOB, 'VECTEUR"TEMP3Q1C', 'MOTS', L )
      CALL LXTSOU( NTLXOB, 'VECTEUR"TEMP3Q1C', NTTE3Q1C, MNTE3Q1C )
      MCN( MNTE3Q1C + WBCOVE ) = NBST3C
      MCN( MNTE3Q1C + WBVECT ) = NBVECT
      MCN( MNTE3Q1C + WBCPIN ) = NBCPIN
C
      MNT6 = MNTEM3C
      MNT3 = MNTE3Q1C + WECTEU
      DO 170 N=1,NBVECT
         CALL TRTATA( MCN(MNT6), MCN(MNT3), MOREE2*NBST3C )
         MNT6 = MNT6 + MOREE2 * 8 * MXEF3C
         MNT3 = MNT3 + MOREE2 * NBST3C
 170  CONTINUE
C
C     LES COMPLEMENTS TEMPS, VALEURS PROPRES, ...
      MNT3 = MNTE3Q1C + WECTEU + MOREE2 * NBST3C * NBVECT
      MNT6 = MNTE6Q1C + WECTEU + MOREE2 * MCN(MNTE6Q1C+WBCOVE) * NBVECT
      CALL TRTATA( MCN(MNT6), MCN(MNT3), NBCPIN )
C
C     LA DATE
      CALL ECDATE( MCN(MNTE3Q1C) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNTE3Q1C + MOREE2 ) = NONMTD( '~>>>VECTEUR' )
      WRITE(IMPRIM,*)'CREATION TMS VECTEUR"TEMP3Q1C'
C
C     DESTRUCTION DES TEMPERATURES INUTILES
      CALL TNMCDS( 'REEL2', 8*MXEF3C*NBVECT, MNTEM3C )
C
      RETURN
      END
