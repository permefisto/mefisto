      SUBROUTINE DVTR2D( NUTYSU, ARETMX, NBITAQ, MXSOMM, MXTRIA,
     %                   NBLFTR, NDARLF,
     %                   NDST1L, NBARFR, MXSOAR, NOSOAR,
     %                   NBSOIN, NOSOIN, DISOIN,
     %                   N1TRVI, NOTRIA, CETRIA,
     %                   PXYD  , NOTRSO, NLSOFR, NSLIGN,
     %                   NBSOMM, NBSOM1, NBTRIA, NDTRIA,
     %                   ORIGIN, D2D3, XMIN, YMIN, XYMXMI, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRIANGULATION DELAUNAY 2D A PARTIR DES ARETES DU CONTOUR ET
C -----    DE POINTS INTERNES IMPOSES, RESPECT DE LA FRONTIERE ET
C          AJOUT DE POINTS INTERNES
C
C ENTREES:
C --------
C NUTYSU : NUMERO D'OPTION DE GENERATION DU MAILLAGE DE CETTE SURFACE
C          9 APPEL DIRECT
C          1 APPEL PAR LE SP SUEX01-QUNSAL (QUADRANGLE ALGEBRIQUE)
C          6 APPEL PAR LE SP SUEX06-TRNSAL (TRIANGLE   ALGEBRIQUE)
C ARETMX : LONGUEUR MAXIMALE DES ARETES DES TRIANGLES EQUILATERAUX
C NBITAQ : NOMBRE D'ITERATIONS POUR AMELIORER LA QUALITE DU MAILLAGE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TRIANGULATION
C MXTRIA : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES DANS NOTRIA
C NBLFTR : NOMBRE DE LIGNES FERMEES LIMITANT LA SURFACE A TRIANGULER
C NDARLF : NUMERO DE LA PREMIERE ARETE DE CHAQUE LIGNE FERMEE DANS
C          LE TABLEAU NOSOAR
C NDST1L : NUMERO DU DERNIER SOMMET DE LA LIGNE ENVELOPPE
C          = NOMBRE D'ARETES DE LA LIGNE-ENVELOPPE
C NBARFR : NOMBRE D'ARETES  DE LA FRONTIERE OU CONTOUR DU DOMAINE =
C          NOMBRE DE POINTS DE LA FRONTIERE OU CONTOUR DU DOMAINE
C MXSOAR : NOMBRE MAXIMAL D'ARETES FRONTALIERES DECLARABLES
C
C ENTREES ET SORTIES :
C --------------------
C NOSOAR : NUMERO DES 2 SOMMETS DE CHAQUE ARETE ET
C          POINTEUR SUR L'ARETE SUIVANTE
C
C    ATTENTION: UNE ARETE DOIT ETRE LA SUIVANTE DE LA PRECEDENTE
C    ---------- OU LA PREMIERE D'UNE COMPOSANTE CONNEXE DE LA
C               FRONTIERE  OU LA DERNIERE
C
C NBSOIN : NOMBRE DE POINTS INTERNES A LA SURFACE FUTUR SOMMET DE LA
C          TRIANGULATION (REMIS A JOUR SI POINTS DOUBLES)
C NOSOIN : NOSOIN(I) NUMERO DANS LE LEXIQUE POINTS DU I-EME POINT
C          INTERNE
C DISOIN : DISOIN(I) DISTANCE SOUHAITEE AVEC LES VOISINS DU POINT
C
C SORTIES:
C --------
C N1TRVI : NUMERO DU PREMIER TRIANGLE VIDE DANS LE TABLEAU NOTRIA
C          LE CHAINAGE DES TRIANGLES VIDES SE FAIT SUR NOTRIA(4,.)
C NOTRIA : LISTE DES TRIANGLES
C  PAR TRIANGLE : SOMMET1 SOMMET2 SOMMET3 TR_VOIS1 TR_VOIS2 TR_VOIS3
C                 SOMMET    EST LE NUMERO DU SOMMET
C                 TR_VOIS i EST LE NUMERO DANS NOTRIA DU TRIANGLE
C                           ADJACENT PAR L'ARETE i
C          TABLEAU ENTIER(6,2*MXSOMM)
C
C CETRIA : COORDONNEES DU CENTRE DU CERCLE CIRCONSCRIT ET
C          CARRE DU RAYON
C          PAR TRIANGLE : XCENTRE YCENTRE RAYON**2
C          TABLEAU REEL2(3,MXTRIA)
C
C PXYD   : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C          TABLEAU REEL2(3,MXSOMM)
C
C NOTRSO : NOTRSO(I) NUMERO D'UN TRIANGLE AYANT POUR SOMMET I
C
C NLSOFR : TABLEAU DU NUMERO DE LIGNE(1 A NBLFTR) DES SOMMETS
C          TABLEAU ENTIER(MXSOMM)
C
C NSLIGN : TABLEAU DU NUMERO DE SOMMET DANS SA LIGNE POUR CHAQUE
C          SOMMET FRONTALIER
C          NUMERO DU POINT DANS LE LEXIQUE POINT SI INTERNE
C          0 SI LE POINT EST INTERNE NON IMPOSE PAR L'UTILISATEUR
C
C NBSOMM : NOMBRE DE SOMMETS INTERNES + EXTERNES APRES TRIANGULATION
C NBSOM1 : NOMBRE DE SOMMETS INTERNES APRES TRIANGULATION
C NBTRIA : NOMBRE DE TRIANGLES DE LA TRIANGULATION
C NDTRIA : NUMERO MAXIMAL DANS NOTRIA D'UN TRIANGLE INTERNE
C ORIGIN : POINT ORIGINE DANS LE PLAN DE MAILLAGE
C D2D3   : MATRICE DE PASSAGE DES COORDONNEES 3D EN COORDONNEES 2D
C XMIN   : ABSCISSE MINIMALE APRES TRANSFORMATION 3D->2D
C YMIN   : ORDONNEE MINIMALE APRES TRANSFORMATION 3D->2D
C XYMXMI : ECART MAXIMAL ENTRE XMAX-XMIN ET YMAX-YMIN
C IERR   : 0 SI PAS D'ERREUR
C          1 SATURATION DES SOMMETS
C          2 SATURATION DES ARETES
C          3 SATURATION DES TRIANGLES
C          4 PILE SATUREE DES ARETES FRONTALIERES NON RETROUVEES
C          5 PILE SATUREE DES ARETES EXTERIEURES AU DOMAINE
C          6 EXISTENCE D'ARETES PERDUES STOCKEES DANS NTETOI (CF LETREE)
C          7 TOUS LES POINTS SONT ALIGNES
C          8 AUCUN CERCLE CIRCONSCRIT AUX TRIANGLES ACTUELS NE CONTIENT
C            LE POINT COURANT
C          9 TABLEAU NOSOAR DE TAILLE INSUFFISANTE CAR TROP D'ARETES
C            PERDUES
C         10 LIGNE ENVELOPPE INCORRECTE
C         11 2 EXTREMITES CONFONDUES POUR UNE ARETE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1991
C....................................................................012
      IMPLICIT INTEGER (W)
      include"./incl/a___xyzsommet.inc"
      include"./incl/gsmenu.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
C     TRACE OU NON DES TRIANGLES GENERES DANS LA TRIANGULATION
C     LES PARAMETRES D'ENTREE ET SORTIE
      INTEGER           NOSOAR(3,MXSOAR),NLSOFR(MXSOMM),NSLIGN(MXSOMM),
     %                  NDARLF(1:NBLFTR),
     %                  NOSOIN(NBSOIN),NOTRSO(MXSOMM)
      REAL              DISOIN(NBSOIN)
      DOUBLE PRECISION  DINFO,TCPU,D
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
C
C     LES PARAMETRES RESULTATS
      INTEGER           NOTRIA(6,MXTRIA)
      DOUBLE PRECISION  CETRIA(3,MXTRIA),
     %                  PXYD(3,MXSOMM)
      REAL              COMXMI(3,2),
     %                  ORIGIN(3)
      DOUBLE PRECISION  D2D3(3,3)
C
C     ZEMEPS :  -EPSILON POUR LA PRECISION A ZERO
C     UNPEPS : 1+EPSILON POUR LA PRECISION A 1
      DOUBLE PRECISION  ZEMEPS, UNPEPS
C
C     LES SEUILS DE PRECISION POUR SAVOIR SI UN POINT EST DANS
C     UN TRIANGLE OU UN CERCLE
C     ========================================================
      ZEMEPS = -1000.D0 * DINFO( 'PRECISION' )
      UNPEPS = 1.D0 - ZEMEPS
C
C     TRACE OU NON DES TRIANGLES GENERES
      TRATRI = .FALSE.
C
C     NOMBRE D'ITERATIONS SUR LA CREATION DES POINTS INTERNES
      NBITPI = 1
C
C     AU DEBUT PAS D'ERREUR
      IERR   = 0
      MNSTEC = 0
      WRITE(IMPRIM,10000)
10000 FORMAT(' TRIANGULATION DELAUNAY DANS UN PLAN DE R3')
C
C     INITIALISATIONS DES POINTS ET DES ARETES FRONTALIERES
C     RENUMEROTATION GLOBALE POUR LA TRIANGULATION
C     =====================================================
      CALL DVINIT( MXSOMM, NBSOIN, NOSOIN, DISOIN,
     %             NDST1L, NBARFR, MXSOAR, NOSOAR,
     %             NBSOMM, PXYD  , NLSOFR, NSLIGN,
     %             ORIGIN, D2D3  , COMXMI, AREMIN, AREMAX,
     %             XMIN  , YMIN  , XYMXMI, IERR )
      IF( IERR .NE. 0 ) GOTO 9999
C     ICI LE TABLEAU PXYD N'A PAS DE SOMMETS IDENTIQUES
C
      IF( TRATRI ) THEN
C        LES TRACES SONT DEMANDES
         CALL EFFACE
C        LE CADRE OBJET GLOBAL EN UNITES UTILISATEUR
         XE = COMXMI(1,2) - COMXMI(1,1)
         YE = COMXMI(2,2) - COMXMI(2,1)
         CALL ISOFENETRE( COMXMI(1,1)-XE/20, COMXMI(1,2)+XE/20,
     %                    COMXMI(2,1)-YE/20, COMXMI(2,2)+YE/20 )
      ENDIF
      D    = DINFO('DELTA CPU')
      TCPU = D
      WRITE(IMPRIM,19900) TCPU
19900 FORMAT(/' TEMPS COHERENCE DES DONNEES :',T60,F10.1,' SECONDES')
C     PROTECTION CONTRE UNE ARETE MAX DESIREE TROP GRANDE OU TROP PETITE
      ARETMX = MISECH( ARETMX, 0.0, XYMXMI )
      ARETMX = MIN( ARETMX, AREMAX )
      ARETMX = MAX( ARETMX, AREMIN )
C
C     LISTE DES SOMMETS DE PXYD DE L'ENVELOPPE CONVEXE DE LA 1-ERE LIGNE
C     ==================================================================
      MN1 = 0
      NDSTLL = NDST1L * 2
      CALL TNMCDC( 'REEL2', NDSTLL, MN1 )
      MN2 = 0
      CALL TNMCDC( 'ENTIER', NDSTLL, MN2 )
      CALL TNMCDC( 'ENTIER', NDSTLL*2, MNSTEC )
      NBSTVE = NBSOMM
      CALL DVEC1L( NDST1L,   NBSOMM, MXSOMM, PXYD, AREMAX,
     %             MCN(MN1), NOTRSO, MCN(MN2),
     %             NDSTLL,   NBSTEC, MCN(MNSTEC), N1ST, IERR )
      NBSTPE = NBSOMM
      CALL TNMCDS( 'REEL2',  NDSTLL, MN1 )
      CALL TNMCDS( 'ENTIER', NDSTLL, MN2 )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19911) D
19911 FORMAT(' TEMPS SOMMETS ENVELOPPE CONVEXE :',T60,F10.1,' SECONDES')
      IF( IERR .NE. 0 ) GOTO 9999
C
C     AJOUT DANS PXYD DU BARYCENTRE DES SOMMETS DE L'ENVELOPPE CONVEXE
C     ================================================================
      CALL DVAJBA( ARETMX, NBSTEC, MCN(MNSTEC), NBSOMM, PXYD )
C     LE NUMERO DU SOMMET BARYCENTRE DE L'ENVELOPPE CONVEXE
      NOSTBA = NBSOMM
C
C     CREATION DE L'ARBRE LETREE ET AJOUT DES SOMMETS DANS LETREE
C     ===========================================================
C     PREMIERE ESTIMATION DE MXTREE
      MXTREE = 2 * MXSOMM
C
 11   MNTREE = 0
C     REINITIALISATION EN CAS D'AUGMENTATION DE MXTREE
      NBSOMM = NOSTBA
      CALL TNMCDC( 'ENTIER', 9*(1+MXTREE), MNTREE )
C     NO DE TE DE CHAQUE SOMMET
      MNLESO = 0
      CALL TNMCDC( 'ENTIER', MXSOMM, MNLESO )
C     LE NUMERO DU PREMIER SOMMET DU TE RACINE DE LETREE
      NOSTE1 = NBSOMM + 1
      CALL DVAJTE( MXSOMM, NBSOMM, PXYD, COMXMI,
     %             ARETMX, MXTREE, MCN(MNTREE), MCN(MNLESO),
     %             IERR )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19907) D
19907 FORMAT(' TEMPS AJOUT ARBRE 4 TRIANGLES EQUILATERAUX:',
     %         T60,F10.1,' SECONDES' )
C
      IF( IERR .EQ. 1 ) THEN
C        SATURATION DE LETREE => SA TAILLE EST AUGMENTEE
C        DESTRUCTION DES 2 TABLEAUX AUXILIAIRES
         CALL TNMCDS( 'ENTIER', MXSOMM, MNLESO )
         CALL TNMCDS( 'ENTIER', 9*(1+MXTREE), MNTREE )
         MXTREE = MXTREE * 2
         IERR = 0
         WRITE(IMPRIM,*) 'NOUVELLE VALEUR DE MXTREE =',MXTREE
         GOTO 11
      ENDIF
      IF( IERR .NE. 0 ) GOTO 9999
C     ICI LE TABLEAU PXYD N'A PAS DE SOMMETS IDENTIQUES
C     ET CONTIENT LES SOMMETS DES TE ET LES POINTS FRONTALIERS ET INTERNES
C
C     HOMOGENEISATION DE L'ARBRE DES TE A UN SAUT DE TAILLES AU PLUS
C     ==============================================================
C     ATTENTION : LA QUEUE (TABLEAU LAQUEU) ET NOTRIA EN EQUIVALENCE A L'APPEL
      MNNPXY = 0
      CALL TNMCDC( 'ENTIER', MXSOMM+1, MNNPXY )
      CALL DVHOTE( NUTYSU, NBSTEC, MCN(MNSTEC), NOSTE1,
     %             MXSOMM, NBSOMM, PXYD,
     %             ORIGIN, D2D3, XMIN, YMIN, XYMXMI,
     %             COMXMI, ARETMX,
     %             MCN(MNTREE), MCN(MNLESO), 6*MXTRIA, NOTRIA,
     %             NBS,    MCN(MNNPXY), IERR )
      CALL TNMCDS( 'ENTIER', MXSOMM, MNLESO )
      CALL TNMCDS( 'ENTIER', 9*(1+MXTREE), MNTREE )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19910) D
19910 FORMAT(' TEMPS ADAPTATION ET HOMOGENEISATION ARBRE 4 :',
     %         T60,F10.1,' SECONDES' )
      IF( IERR .EQ. 1 ) THEN
C        LETREE SATURE
         MXTREE = MXTREE * 2
         WRITE(IMPRIM,*) 'NOUVELLE VALEUR DE MXTREE =',MXTREE
         CALL TNMCDS( 'ENTIER', MXSOMM+1, MNNPXY )
         IERR = 0
         GOTO 11
      ENDIF
      IF( IERR .NE. 0 ) THEN
         CALL TNMCDS( 'ENTIER', MXSOMM+1, MNNPXY )
         GOTO 9999
      ENDIF
C
C     TRIANGULATION DES SOMMETS DE L'ENVELOPPE CONVEXE ET DE LEUR BARYCENTRE
C     ======================================================================
      WRITE(IMPRIM,10020) 0, NBSTEC+1
10020 FORMAT(/' TRIANGULATION INITIALE DE L''ENVELOPPE CONVEXE'/
     % ' NOMBRE SOMMETS ACTUELS  ' ,T30,I8/
     % ' NOMBRE SOMMETS A TRIANGULER' ,T30,I8)
      CALL DVTRI1( NBSTEC, MCN(MNSTEC), NOSTBA, MXSOMM, PXYD,
     %             MXTRIA, N1TRVI, NOTRIA, NOTRSO, CETRIA )
      CALL TNMCDS( 'ENTIER', NDSTLL*2, MNSTEC )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19905) D
19905 FORMAT(' TEMPS TRIANGULATION ENVELOPPE CONVEXE :',T60,F10.1,
     %        ' SECONDES' )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     TRIANGULATION CONTRAINTE DES SOMMETS DES TE QUI APPARTIENNENT A UN
C     TRIANGLE DE LA TRIANGULATION ACTUELLE PUIS CEUX DES ARETES FRONTALIERES
C     =======================================================================
      WRITE(IMPRIM,10021) NBS
10021 FORMAT(/' TRIANGULATION DES SOMMETS TE ET FRONTALIERS'/
     %        ' NOMBRE SOMMETS A TRIANGULER' ,T30,I8)
      MXETOI = MXSOMM
      MNETOI = 0
      CALL TNMCDC( 'ENTIER', MXETOI*5, MNETOI )
      CALL DVAJPC( ZEMEPS, UNPEPS,
     %             NBS,    MCN(MNNPXY), MXTRIA, PXYD,
     %             N1TRVI, NOTRIA, CETRIA, NOTRSO,
     %             NBLFTR, NDARLF, MXSOAR, NOSOAR, NLSOFR,
     %             MXETOI, MCN(MNETOI), MCN(MNETOI+4*MXETOI),
     %             IERR )
      CALL TNMCDS( 'ENTIER', MXSOMM+1, MNNPXY )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19920) D
19920 FORMAT(' TEMPS TRIANGULATION SOMMETS TE ET FRONTALIERS :',
     %        T60,F10.1,' SECONDES' )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     DESTRUCTION DES SOMMETS AJOUTES SUR L'ENVELOPPE CONVEXE
C     =======================================================
      MXARCF = MXSOMM/5
      MNARCF = 0
      CALL TNMCDC( 'ENTIER', MXARCF*5+1, MNARCF )
CCC      DO 22 NS=NBSTVE+1,NBSTPE
CCC         CALL ADSUPO( NS,     PXYD,
CCC     %                N1TRVI, NOTRIA, NOTRSO,
CCC     %                0     , MCN(MNETOI),
CCC     %                MXARCF, MCN(MNETOI), MCN(MNETOI+4*MXETOI),
CCC     %                MCN(MNARCF), MCN(MNARCF+1+MXARCF),
CCC     %                NBTRCF, MCN(MNARCF+1+MXARCF*4), IERR )
CCC 22   CONTINUE
C
C     DETECTION DES ARETES FRONTALIERES INITIALES PERDUES
C     TRIANGULATION FRONTALE POUR LES RETROUVER
C     ===================================================
      WRITE(IMPRIM,10022)
10022 FORMAT(/' REDECOUVERTE DES ARETES PERDUES DE LA FRONTIERE')
      CALL DVREFR( NBLFTR, NDARLF, MXSOAR, NOSOAR,
     %             PXYD  , NLSOFR, N1TRVI, NOTRIA, NOTRSO,
     %             MXETOI, MCN(MNETOI), MCN(MNETOI+4*MXETOI),
     %             MXARCF, MCN(MNARCF), MCN(MNARCF+1+MXARCF),
     %             MCN(MNARCF+1+MXARCF*4),
     %             NBARPE, IERR )
      CALL TNMCDS( 'ENTIER', MXARCF*5+1, MNARCF)
      CALL TNMCDS( 'ENTIER', MXETOI*5, MNETOI )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19930) D
19930 FORMAT(' TEMPS POUR RECUPERER LES ARETES DE LA FRONTIERE :',
     %         T60,F10.1,' SECONDES')
      IF( IERR .NE. 0 ) GOTO 9999
C     FIN DE LA TRIANGULATION AVEC RESPECT DES ARETES INITIALES FRONTALIERES
C
C     SUPPRESSION DES TRIANGLES EXTERNES
C     NOTRSO(I)=NUMERO DU SOMMET INTERNE 0 SI EXTERNE
C     ===============================================
      WRITE(IMPRIM,10023)
10023 FORMAT(/' SUPPRESSION DES TRIANGLES EXTERNES')
C     RECHERCHE DU DERNIER TRIANGLE UTILISE
      DO 40 NDTRI0=MXTRIA,1,-1
         IF( NOTRIA(1,NDTRI0) .GT. 0 ) GOTO 45
 40   CONTINUE
 45   MNTRSU = 0
      CALL TNMCDC( 'ENTIER', NDTRI0,   MNTRSU )
      MNPILE = 0
      CALL TNMCDC( 'ENTIER', NDTRI0*2, MNPILE )
      CALL DVSUEX( MXSOMM, NBSOMM, N1ST, PXYD,
     %             MXTRIA, NBLFTR, NDARLF,
     %             MXSOAR, NOSOAR, NLSOFR,
     %             N1TRVI, NOTRIA, NOTRSO,
     %             NBTRIA, NDTRIA,
     %             NDTRI0, MCN(MNPILE), MCN(MNTRSU), IERR   )
      CALL TNMCDS( 'ENTIER', NDTRI0*2, MNPILE )
      CALL TNMCDS( 'ENTIER', NDTRI0,   MNTRSU )
      WRITE(IMPRIM,19958) NBTRIA
19958 FORMAT(' NOMBRE DE TRIANGLES INTERNES =',I8)
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19960) D
19960 FORMAT(' TEMPS SUPPRESSION DES TRIANGLES EXTERNES :',
     %         T60,F10.1,' SECONDES')
CCC      WRITE(IMPRIM,19965) TCPU
CCC19965 FORMAT(/' TEMPS TOTAL TRIANGULATION AVANT AMELIORATION QUALITE :'
CCC     %        ,T60,F10.1,' SECONDES')
      IF( IERR .NE. 0 ) GOTO 9999
C
C     CALCUL DE LA QUALITE DE LA TRIANGULATION
C     ========================================
      QUAMOY = 0
      QUAMIN = 2.0
      DO 80 NT=1,NDTRIA
         IF( NOTRIA(1,NT) .GT. 0 ) THEN
            CALL QUTR2D( PXYD(1,NOTRIA(1,NT)), PXYD(1,NOTRIA(2,NT)),
     %                   PXYD(1,NOTRIA(3,NT)), QUALIT )
C           LA QUALITE MOYENNE
            QUAMOY = QUAMOY + QUALIT
C           LA QUALITE MINIMALE
            QUAMIN = MIN( QUAMIN, QUALIT )
         ENDIF
 80   CONTINUE
      QUAMOY = QUAMOY / NBTRIA
      WRITE(IMPRIM,10070) 0,QUAMOY,QUAMIN
10070 FORMAT(/' AMELIORATION',I3,' QUALITE MOYENNE=',F6.3,
     %       ' MINIMALE=',F6.3,I5 )
C
C     TENTATIVE D'AMELIORATION DE LA QUALITE DE LA TRIANGULATION
C     ==========================================================
      CALL DVAMQU( NBITAQ, MXSOMM, NBSOMM, PXYD,
     %             MXTRIA, NDTRIA, N1TRVI, NOTRIA, CETRIA,
     %             NOTRSO, NLSOFR,
     %             NBTRIA, QUAMOY, QUAMIN )
C
C     RENUMEROTATION DES SOMMETS INTERNES
C     ===================================
      NBSOM1 = 0
      DO 90 NS=1,NBSOMM
         IF( NOTRSO(NS) .GT. 0 ) THEN
            NBSOM1 = NBSOM1 + 1
            NOTRSO(NS) = NBSOM1
         ELSE
            NOTRSO(NS) = 0
         ENDIF
 90   CONTINUE
C
C     IMPRESSION DU NOMBRE DE SOMMETS ET TRIANGLES
C     ============================================
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19993) D,TCPU
19993 FORMAT(/' TEMPS AMELIORATION QUALITE TRIANGULATION :',T60,F10.1,
     %       ' SECONDES'//
     %       ' TEMPS TOTAL DE LA TRIANGULATION + QUALITE :',T60,F10.1,
     %       ' SECONDES')
C
 9999 IF( MNSTEC .GT. 0 ) THEN
         CALL TNMCDS( 'ENTIER', NDSTLL*2, MNSTEC )
      ENDIF
      END
