      SUBROUTINE CRSTCF( NBCF,   N1ARCF, NOARCF, NBARCF,
     %                   MXSTCF, NBSTCF, NOSTCF, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  CONSTRUCTION DU TABLEAU DES NBSTCF NUMEROS DES SOMMETS
C -----  DES NBCF CONTOURS FERMES DEFINIS PAR LEURS ARETES PERIPHERIQUES

C ENTREES:
C --------
C NBCF   : NOMBRE DE LIGNES FERMEES PERIPHERIQUES DES TRIANGLES DU CF
C N1ARCF : POINTEUR DANS NOARCF SUR LA PREMIERE ARETE DES NBCF
C NOARCF : NUMERO DES ARETES DE LA LIGNE DU CONTOUR FERME SELON UN SENS
C MXSTCF : NOMBRE MAXIMAL DE SOMMETS DU CF DECLARABLES DANS NOSTCF

C SORTIES:
C --------
C NBARCF : NOMBRE D'ARETES DES NBCF LIGNES FERMEES
C NBSTCF : NOMBRE DE SOMMETS DU CF
C NOSTCF : NUMERO PTXYZD DES NBSTCF SOMMETS CONSECUTIFS PERIPHERIQUES DU CF
C IERR   : =5 NBSTCF>MXSTCF => TROP DE SOMMETS DU CF
C          =0 SI PAS D'ERREUR RENCONTREE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE DU PERRAY      Mai 2015
C MODIFS : ALAIN PERRONNET LJLL UPMC & St PIERRE DU PERRAY  Octobre 2017
C2345X7..............................................................012
      INTEGER  N1ARCF(0:NBCF), NOARCF(1:3,1:*), NOSTCF(MXSTCF)

      IERR   = 0
      NBSTCF = 0
      NBARCF = 0

      DO NCF = 1, NBCF

C        PREMIERE ARETE
         NA0 = N1ARCF( NCF )
         NA1 = NA0

C        UNE ARETE DU CF DE PLUS
 10      NBARCF = NBARCF + 1

C        NUMERO DU SOMMET1 DE L'ARETE NA1 DU CF
         NS1 = NOARCF(1,NA1)

C        NS1 EST IL DEJA STOCKE DANS NOSTCF?
         DO K=1,NBSTCF
            IF( NS1 .EQ. NOSTCF(K) ) GOTO 20
         ENDDO

C        NON : IL EST AJOUTE
         IF( NBSTCF .GE. MXSTCF ) THEN
            PRINT*,'crstcf: TABLEAU NOSTCF SATURE. AUGMENTER MXSTCF='
     %            ,MXSTCF
            IERR = 5
            RETURN
         ENDIF

         NBSTCF = NBSTCF + 1
         NOSTCF( NBSTCF ) = NS1

C        PASSAGE A L'ARETE SUIVANTE DU CF
 20      NA1 = NOARCF(2,NA1)
         IF( NA1 .NE. NA0 ) GOTO 10

      ENDDO

      RETURN
      END
