      SUBROUTINE PTDSTE( Pt, PTXYZD, NOSOTE,  NONOUI, VTE, COBARY )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LE POINT Pt EST IL DANS LE TETRAEDRE DES 4 SOMMETS NOSOTE?
C -----

C ENTREES:
C --------
C Pt     : LES 3 COORDONNEES DU POINT
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NOSOTE : LE NUMERO DES 4 SOMMETS DU TETRAEDRE

C SORTIES:
C --------
C NONOUI : 1  Pt   EST     DANS LE TETRAEDRE
C          0  Pt N'EST PAS DANS LE TETRAEDRE
C         -1  LE TETRAEDRE NOSOTE EST DE VOLUME NUL
C VTE    : LE VOLUME DU TETRAEDRE NOSOTE
C COBARY : LA VALEUR DES 4 COORDONNEES BARYCENTRIQUES DE Pt
C          MAIS ATTENTION A L'ORDRE:
C          COBARY(I) EST ICI LA COORDONNEE BARYCENTRIQUE OPPOSEE
C                  A LA FACE I DE SOMMETS I, I+1, I+2 MODULO 4
C          EN PARTICULIER ELLE VAUT 0 SUR LA FACE I ET 1 AU SOMMET I-1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC St PIERRE DU PERRAY  SEPTEMBRE 2014
C....................................................................012
      INTEGER           NOSOTE(4), NONOUI
      DOUBLE PRECISION  PTXYZD(4,*), Pt(3), COBARY(4), VTE
ccc     %                 ,CB1234

C     CALCUL DES 4 COORDONNEES BARYCENTRIQUES DU POINT Pt DANS LE
C     TETRAEDRE DE SOMMETS NOSOTE
      CALL COBATE( Pt, PTXYZD, NOSOTE,  VTE, COBARY, IERR )

      IF( IERR .NE. 0 ) THEN

C        LE TETRAEDRE NOSOTE A UN VOLUME VTE NUL
         NONOUI = -1

      ELSE

ccc         CB1234 = ABS( COBARY(1) ) + ABS( COBARY(2) )
ccc     %          + ABS( COBARY(3) ) + ABS( COBARY(4) )
ccc         IF( CB1234 .LT. 1.0001D0 ) THEN 20/12/2018
ccc         IF( CB1234 .LT. 1.0005D0 ) THEN 05/10/2020

         IF( COBARY(1) .GE. -0.001D0 .AND. COBARY(1) .LE. 1.001D0 .AND.
     %       COBARY(2) .GE. -0.001D0 .AND. COBARY(2) .LE. 1.001D0 .AND.
     %       COBARY(3) .GE. -0.001D0 .AND. COBARY(3) .LE. 1.001D0 .AND.
     %       COBARY(4) .GE. -0.001D0 .AND. COBARY(4) .LE. 1.001D0 ) THEN

C           LE POINT Pt EST DANS LE TETRAEDRE NOSOTE
            NONOUI = 1

         ELSE

C           LE POINT Pt N'EST PAS DANS LE TETRAEDRE NOSOTE
            NONOUI = 0

         ENDIF

      ENDIF

      RETURN
      END
