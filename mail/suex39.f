      SUBROUTINE SUEX39( NTLXVO, LADEFI,
     %                   NTNSSF, MNNSSF, NTSOSF, MNSOSF, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES 2**2=4 SOUS EF DES TRIANGLES et QUADRANGLES
C -----    DU MAILLAGE D'UNE SURFACE
C
C ENTREES:
C --------
C NTLXVO : NUMERO DU TABLEAU TS DE LA SURFACE DES EF A DECOUPER
C LADEFI : TABLEAU DE DEFINITION DE LA SURFACE PARTITIONNE
C          CF '~td/d/a_volume__definition'
C
C SORTIES:
C --------
C NTNSSF : NUMERO      DU TMS 'NSEF' DES NUMEROS DES EF 2D
C MNNSSF : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES EF 2D
C          CF '~td/d/a___nsef'
C NTSOSF : NUMERO      DU TMS 'XYZSOMMET' DE LA SURFACE
C MNSOSF : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE
C          CF '~td/d/a___xyzsommet'
C IERR   : 0 SI PAS D'ERREUR
C        > 0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN LJLL UPMC Paris et VEULETTES/mer Juillet 2008
C.......................................................................
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a_surface__definition.inc"
      include"./incl/a___face.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      INTEGER           LADEFI(0:*)
      INTEGER           NOSOEF(64)
C
      IERR   = 0
      MNSOFI = 0
      MNSTSF = 0
      MNUEFI = 0
C
C     LE NUMERO DE LA SURFACE INITIALE
      NUSUIN = LADEFI( WUSU4E )
C
C     OUVERTURE DE LA SURFACE
      CALL LXNLOU( NTSURF, NUSUIN, NTLXSU, MNLXSU )
      IF( NTLXSU .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE INEXISTANTE'
         ELSE
            KERR(1) = 'UNKNOWN SURFACE'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     TRANSFORMATION DU MAILLAGE STRUCTURE OU NON EN NON STRUCTURE
C     ============================================================
C     LES SOMMETS COPIE DES SOMMETS DU MAILLAGE INITIAL
      CALL LXTSOU( NTLXSU, 'XYZSOMMET', NTSOSI, MNSOSI )
      IF( NTSOSI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE SANS SOMMETS'
         ELSE
            KERR(1) = 'SURFACE WITHOUT VERTICES'
         ENDIF
         CALL LEREUR
         IERR = 4
         RETURN
      ENDIF
C
C     LE TABLEAU 'NSEF'
      CALL LXTSOU( NTLXSU, 'NSEF', NTNSSI, MNNSSI )
      IF( NTNSSI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SURFACE SANS NO DES SOMMETS DES EF'
         ELSE
            KERR(1) = 'SURFACE WITHOUT FINITE ELEMENT VERTEX NUMBERS'
         ENDIF
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
C     LES CARACTERISTIQUES DES EF DE CE MAILLAGE INITIAL
      CALL NSEFPA( MCN(MNNSSI),
     %             NUTYMA, NBSOEL, NBSOEF, NBTGEF,
     %             LDAPEF, LDNGEF, LDTGEF, NBEFSI,
     %             NX    , NY    , NZ    ,
     %             IERR   )
C     NUTYMA : 'NUMERO DE TYPE DU MAILLAGE'    ENTIER
C              0 : 'NON STRUCTURE'      , 2 : 'SEGMENT    STRUCTURE',
C              3 : 'TRIANGLE  STRUCTURE', 4 : 'QUADRANGLE STRUCTURE',
C              5 : 'TETRAEDRE STRUCTURE', 6 : 'PENTAEDRE  STRUCTURE',
C              7 : 'HEXAEDRE  STRUCTURE'
C     NBSOEL : NOMBRE DE SOMMETS DES EF
C              0 SI MAILLAGE NON STRUCTURE
C     NBSOEF : NOMBRE DE SOMMETS DE STOCKAGE DES EF
C              ( TETRAEDRE NBSOEL=4  NBSOEF=8 )
C     NBEFSI : NOMBRE DE EF DU MAILLAGE INITIAL
C     NX, NY, NZ : LE NOMBRE D'ARETES DANS LES DIRECTION X Y Z
C                CF LE TMS ~td/d/a___nsef
C
      MXEFSF = 0
      IF( NUTYMA.NE.0 .AND. NUTYMA.NE.3 .AND. NUTYMA.NE.4 ) THEN
         NBLGRC(NRERR) = 3
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'OBJET IMPOSSIBLE A SUBDIVISER EN 4 SOUS EF'
            KERR(2) = 'TYPE DE MAILLAGE INCOMPATIBLE'
            KERR(3) = 'NON TRIANGLES OU QUADRANGLES'
         ELSE
            KERR(1) = 'IMPOSSIBLE TO SUBDIVIDE into 4 SUB-FE'
            KERR(2) = 'INCOMPATIBLE MESH TYPE'
            KERR(3) = 'NOT TRIANGLES or QUADRANGLES'
         ENDIF
         CALL LEREUR
         IERR = 2
         RETURN
C
      ELSE IF( NUTYMA .EQ. 0 ) THEN
C
C        MAILLAGE NON STRUCTURE
         MNUEFI = MNNSSI + WUSOEF
         DO 90 NUELEM = 1, NBEFSI
C           LE NUMERO DES 4 SOMMETS DE L'ELEMENT FINI NUELEM
            CALL NSEFNS( NUELEM, NUTYMA, NBSOEF, NBTGEF,
     %                   LDAPEF, LDNGEF, LDTGEF,
     %                   MNNSSI, NX, NY, NZ,
     %                   NCOGEL, NUGEEF, NUEFTG, NOSOEF, IERR )
            IF( IERR .NE. 0 ) RETURN
 90      CONTINUE
cccC        VERIFICATION DE LA SURFACE POSITIVE DES TETRAEDRES PENTAEDRES ET HEX
ccc         CALL VOLPLUS( MNSOSI, MNNSSI )
C
      ELSE IF ( NUTYMA.EQ.3 .OR. NUTYMA.EQ.4 ) THEN
C
C        MAILLAGE STRUCTURE ici LA SURFACE DES EF EST SUPPOSEE>0
         MXEFSF = 4*NBEFSI
         CALL TNMCDC( 'ENTIER', MXEFSF, MNUEFI )
         MN = MNUEFI
         DO 100 NUELEM = 1, NBEFSI
C           LE NUMERO DES 4 SOMMETS DE L'ELEMENT NUELEM
            CALL NSEFNS( NUELEM, NUTYMA, NBSOEF, NBTGEF,
     %                   LDAPEF, LDNGEF, LDTGEF,
     %                   MNNSSI, NX, NY, NZ,
     %                   NCOGEL, NUGEEF, NUEFTG, MCN(MN), IERR )
            MN = MN + 4
            IF( IERR .NE. 0 ) RETURN
 100     CONTINUE
      ENDIF
C
C     LES NUMEROS DES SOMMETS DES SOUS-EF DE CETTE SURFACE
C     MAXIMUM D'ARETES DE LA SURFACE INITIALE
      NBSOSI = MCN(MNSOSI+WNBSOM)
      MXARSI = NBSOSI + NBEFSI + 1024
      NBEFSF = 4 * NBEFSI
      MOTS   = WUSOEF +  4 * NBEFSF
      CALL LXTNDC( NTLXVO, 'NSEF', 'ENTIER', MOTS )
      CALL LXTSOU( NTLXVO, 'NSEF',  NTNSSF , MNNSSF )
C     LES PREMIERES VARIABLES DU TABLEAU 'NSEF'
C     VARIABLE NUTYOB 'NUMERO DE TYPE DE L''OBJET
      MCN( MNNSSF + WUTYOB ) = 3
C     LE TYPE INCONNU DE FERMETURE DU MAILLAGE
      MCN( MNNSSF + WUTFMA ) = -1
C     VARIABLE NBSOEF 'NOMBRE DE SOMMETS PAR EF
      MCN( MNNSSF + WBSOEF ) = 4
C     PAS DE TANGENTES STOCKEES
      MCN( MNNSSF + WBTGEF ) = 0
C     VARIABLE NBEFOB 'NOMBRE DE EF DE L''OBJET
      MCN( MNNSSF + WBEFOB ) = NBEFSF
C     PAS DE TANGENTES STOCKEES
      MCN( MNNSSF + WBEFTG ) = 0
      MCN( MNNSSF + WBEFAP ) = 0
C     VARIABLE NUTYMA 'NUMERO DE TYPE DU MAILLAGE
      MCN( MNNSSF + WUTYMA ) = 0
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNNSSF) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNNSSF + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
C
C     TABLEAU AUXILIAIRE DES XYZ DES SOMMETS DU MAILLAGE FINAL
C     AVANT IDENTIFICATION DES MILIEUX DES ARETES
      MXSOSF = NBSOSI + 5 * NBEFSI
      CALL TNMCDC( 'REEL', 3*MXSOSF, MNSTSF )
C
C     DECOUPAGE DES EF 2D EN 4 SOUS EF 3D
      CALL EF4SEF( NBSOSI, MCN(MNSOSI+WYZSOM),
     %             NBEFSI, MCN(MNUEFI),
     %             MXSOSF, NBSOSF, MCN(MNSTSF),
     %             NBEFSF, MCN(MNNSSF+WUSOEF), IERR )

      IF( IERR .GT. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'SUEX39: MXSOSF SOUS-ESTIME'
         ELSE
            KERR(1) = 'SUEX39: MXSOSF UNDER-ESTIMATED'
         ENDIF
         CALL LEREUR
         IERR = 5
         GOTO 9000
      ENDIF
C
C     IDENTIFICATION DES MILIEUX DES ARETES
      CALL TNMCDC( 'REEL', 3*NBSOSF, MNSOFI )
      CALL IDSTMA( 4, NBEFSF, MCN(MNNSSF+WUSOEF),
     %             NBSOSF, MCN(MNSTSF),
     %             NBSOFI, MCN(MNSOFI) )
C
C     CONSTRUCTION DU TMS XYZSOMMET
C     LES SOMMETS COPIE DES SOMMETS DU MAILLAGE INITIAL
      MOTS = WYZSOM + 3 * NBSOFI
      CALL LXTNDC( NTLXVO, 'XYZSOMMET', 'ENTIER', MOTS )
      CALL LXTSOU( NTLXVO, 'XYZSOMMET',  NTSOSF, MNSOSF )
      MCN(MNSOSF+WNBSOM) = NBSOFI
      MCN(MNSOSF+WNBTGS) = 0
      MCN(MNSOSF+WBCOOR) = 3
      CALL TRTATA( MCN(MNSOFI), MCN(MNSOSF+WYZSOM), 3 * NBSOFI )
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNSOSF) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNSOSF + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
C
C     FIN DU TRAITEMENT
 9000 IF ( NUTYMA .EQ. 3 .OR. NUTYMA .EQ. 4 ) THEN
C        MAILLAGE INITIAL STRUCTURE
         IF( MNUEFI .GT. 0 ) CALL TNMCDS( 'ENTIER', MXEFSF, MNUEFI )
      ENDIF
      IF( MNSOFI .GT. 0 ) CALL TNMCDS( 'REEL', 3*NBSOSF, MNSOFI )
      IF( MNSTSF .GT. 0 ) CALL TNMCDS( 'REEL', 3*MXSOSF, MNSTSF )
C
      RETURN
      END
