      SUBROUTINE TET1AJBA( NOFOTI, NBSOMM, MXSOMM, PTXYZD, NPSOFR,
     %                     QTEAME, NTE, MXTETR, N1TEVI, NOTETR, NUDTETR,
     %                     N1TETS, MXFACO, LEFACO, MXPILE, LAPILE,
     %                     NBEC2T3T, NBEC3T2T, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   TETRAEDRISATION DU BARYCENTRE DES TETRAEDRES JUGES TROP GRANDS
C -----
C ENTREES:
C --------
C NOFOTI : NUMERO DE LA FONCTION 'TAILLE_IDEALE' DES ARETES SINON 0
C          C'EST LE DIZIEME DE CELUI DU PLUS MAUVAIS TRIANGLE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DECLARABLES
C MXTETR : NOMBRE MAXIMAL DE TETRAEDRES DECLARABLES
C QTEAME : QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C          AMELIORATION DE LA QUALITE DES TETRAEDRES EST DEMANDEE
C NTE    : NUMERO NOTETR DU TETRAEDRE DE BARYCENTRE A AJOUTER
C MXPILE : NOMBRE MAXIMAL D'ELEMENTS DU TABLEAU LAPILE

C ENTREE ET SORTIE :
C ------------------
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NPSOFR : NUMERO DE POSITION PAR RAPPORT A LA FRONTIERE DE CHAQUE POINT
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET D'OT A ETE REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
C N1TEVI : NUMERO DU PREMIER TETRAEDRE VIDE DANS LE CHAINAGE NOTETR(5,*)
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: PLUS GRAND NUMERO DANS NOTETR DE TETRAEDRE OCCUPE
C N1TETS : NUMERO DANS NOTETR D'UN TETRAEDRE DE CHAQUE SOMMET

C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...

C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I
C ECHPAV  : ECHELLE DANS LA DIRECTION I
C N1SPAVE : NO DU 1-ER SOMMET DANS PTXYZD DU PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES
C NBSOMM : NOMBRE DES (FUTURS) SOMMETS (DE LA TETRAEDRISATION
C          EN ENTREE ET EN SORTIE APRES AJOUT DES BARYCENTRES
C LAPILE : LAPILE(1,*) NUMERO DANS NOTETR DES LHPILE TETRAEDRES A TRAITER
C          LAPILE(2,*) NUMERO LOCAL 1 A 4 DE LA FACE A ESSAYER D'ECHANGER

C SORTIES:
C --------
C NBEC2T3T: NOMBRE D'ECHANGES DES TETRAEDRES 2T->3T
C NBEC3T2T: NOMBRE D'ECHANGES DES TETRAEDRES 3T->2T
C IERR    : 0 SI PAS D'ERREUR
C           1 SATURATION DES SOMMETS
C           2 BARYCENTRE NON AJOUTE POUR DES RAISONS DE QUALITE MEDIOCRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       Juin  1991
C MODIFS: ALAIN PERRONNET LJLL UPMC PARIS St PIERRE du PERRAY Avril 2016
C MODIFS: ALAIN PERRONNET LJLL UPMC PARIS St PIERRE du PERRAY Mai   2017
C23456...............................................................012
      PARAMETER        (MXTECR = 1024)
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      DOUBLE PRECISION  PTXYZD(4,MXSOMM),CENTRE(4), S, VTE
      INTEGER           NPSOFR(MXSOMM), LEFACO(1:11,0:MXFACO)
      INTEGER           NOTETR(8,MXTETR), N1TETS(MXSOMM), NOSOTE(4),
     %                  LAPILE(2,MXPILE), NVOLTE(1)
C     LES SOMMETS SONT VUS A PARTIR DU BARYCENTRE DANS LE SENS DIRECT
      INTEGER           NOSOFATE(3,4), NOTECR(MXTECR)
      DATA              NOSOFATE/ 1,2,3, 2,4,3, 3,4,1, 4,2,1 /

C     LE BARYCENTRE EST AJOUTE
C     ------------------------
C     SI SATURATION DES SOMMETS ALORS ERREUR ET RETOUR
      IF( NBSOMM .GE. MXSOMM ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'tet1ajba: SATURATION DES SOMMETS. Augmenter MXSOMM'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF

C     LES 3 COORDONNEES ET DISTANCE SOUHAITEE DU BARYCENTRE
      NBSOMM = NBSOMM + 1

      IF( MOD(NBSOMM,10000) .EQ. 0 ) THEN
         IF( LANGAG .EQ. 0 ) THEN
        WRITE(IMPRIM,*) 'tet1ajba: TETRAEDRISATION du BARYCENTRE',NBSOMM
         ELSE
       WRITE(IMPRIM,*) 'tet1ajba: TETRAHEDRIZATION of BARYCENTRE',NBSOMM
         ENDIF
      ENDIF

      DO I=1,4
         PTXYZD(I,NBSOMM) = ( PTXYZD(I,NOTETR(1,NTE))
     %                      + PTXYZD(I,NOTETR(2,NTE))
     %                      + PTXYZD(I,NOTETR(3,NTE))
     %                      + PTXYZD(I,NOTETR(4,NTE)) )  * 0.25D0
      ENDDO

C     SIMULATION DES 4 TETRAEDRES DE SOMMET LE BARYCENTRE NBSOMM
      DO NF = 1, 4

C        3-ERS SOMMETS DE LA FACE DANS LE SENS DIRECT DU TETRAEDRE NTE
         NOSOTE( 1 ) = NOTETR( NOSOFATE(1,NF), NTE )
         NOSOTE( 2 ) = NOTETR( NOSOFATE(2,NF), NTE )
         NOSOTE( 3 ) = NOTETR( NOSOFATE(3,NF), NTE )
         NOSOTE( 4 ) = NBSOMM

C        LES COORDONNEES DU CENTRE DE LA BOULE CIRCONSCRITE
C        AU TETRAEDRE NOSOTE ET CARRE DE SON RAYON
         CALL CEBOQU( 0.0, NOSOTE, PTXYZD, CENTRE,  VTE, QTE )

         IF( QTE .LT. QTEAME ) THEN
C           TETRAEDRE DE TROP MAUVAISE QUALITE -> PAS D'AJOUT DU BARYCENTRE
            NBSOMM = NBSOMM - 1
            IERR   = 2
            GOTO 9999
         ENDIF

      ENDDO

      IF( NOFOTI .GT. 0 ) THEN
C        EXISTENCE DE LA FONCTION UTILISATEUR ARETEIDEALE(X,Y,Z)
C        LONGUEUR IDEALE DES ARETES AUTOUR DU POINT XYZ
         CALL FONVAL( NOFOTI, 3, PTXYZD(1,NBSOMM),  NCODEV, S )
         IF( NCODEV .NE. 0 ) PTXYZD(4,NBSOMM) = S
      ENDIF

C     BARYCENTRE DE 4 SOMMETS QUELCONQUES NON SUR LA SURFACE
      NPSOFR( NBSOMM ) = 0


C     GENERATION DES 4 TETRAEDRES DE SOMMET LE BARYCENTRE NBSOMM
      DO NF = 1, 4

C        LE TETRAEDRE A CREER NT EST LE PREMIER TETRAEDRE VIDE
         NT = N1TEVI
         NOTECR( NF ) = N1TEVI
C        MISE A JOUR DU 1-ER TETRAEDRE VIDE
         N1TEVI = NOTETR( 5, N1TEVI )
C        NUMERO MAX DES TETRAEDRES UTILISES DANS NOTETR
         NUDTETR = MAX( NUDTETR, NT )

C        3-ERS SOMMETS DE LA FACE DANS LE SENS DIRECT DU TETRAEDRE NTE
         NOTETR( 1, NT ) = NOTETR( NOSOFATE(1,NF), NTE )
         NOTETR( 2, NT ) = NOTETR( NOSOFATE(2,NF), NTE )
         NOTETR( 3, NT ) = NOTETR( NOSOFATE(3,NF), NTE )
         NOTETR( 4, NT ) = NBSOMM

C        LE CHAINAGE DES TETRAEDRES ADJACENTS PAR LES FACES
C        LE TETRAEDRE OPPOSE A LA FACE AU DELA DE LA FACE
C        ATTENTION: IL PEUT NE PAS EXISTER DE TEL TETRAEDRE
C        NTOP=0 POUR UNE FACE FRONTIERE SANS TETRAEDRE OPPOSE
C            -1 POUR UN TETRAEDRE OPPOSE INCONNU
         NTOP = NOTETR( 4+NF, NTE )

         NOTETR( 5, NT ) = NTOP
         NOTETR( 6, NT ) = -1
         NOTETR( 7, NT ) = -1
         NOTETR( 8, NT ) = -1

C        MISE A JOUR D'UN TETRAEDRE CONTENANT CHAQUE SOMMET
C        TOUS LES TETRAEDRES ONT PU DISPARAITRE
C        CAS DE L'ETOILE REDUITE AUX 2 TETRAEDRES INITIAUX
         DO N=1,4
            N1TETS( NOTETR(N,NT) ) = NT
         ENDDO

      ENDDO
               
C     SUPPRESSION DU TETRAEDRE NTE
      NOTETR( 1, NTE ) = 0
      NOTETR( 5, NTE ) = N1TEVI
      N1TEVI = NTE

C     COMPLETION DES TETRAEDRES OPPOSES CREES DANS L'ETOILE
      NBTECR = 4
      CALL MJOPTE( NBTECR, NOTECR, N1TETS, NOTETR, NUDTETR,
     %             N1TEVI, PTXYZD, NBFANR )

C     ESSAI D'AMELIORER LES 4 TETRAEDRES OPPOSES AUX 4 FACES
C     PAR 2T->3T ou 3T->2T
      LHPILE = 0
      DO N=1,4
C        RECHERCHE DU TETRAEDRE OPPOSE NTOP ET DE SA FACE NFOP
C        A LA FACE 1 DU TETRAEDRE NOTECR(N)
         CALL NOFAOP( 1, NOTECR(N), NOTETR,  NFOP, NTOP )
         IF( NFOP .GT. 0 ) THEN
            LHPILE = LHPILE + 1
            LAPILE(1,LHPILE) = NTOP
            LAPILE(2,LHPILE) = NFOP
         ENDIF
      ENDDO

      CALL BA2T3T2T( PTXYZD, MXFACO, LEFACO,  0, NVOLTE,
     %               NOTETR, N1TEVI, NUDTETR, N1TETS,
     %               MXPILE, LHPILE, LAPILE,
     %               NBEC2T3T, NBEC3T2T )

      IERR = 0

 9999 RETURN
      END
