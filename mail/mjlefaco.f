      SUBROUTINE MJLEFACO( NBTECF, NOTECF, NOTETR, MXFACO, LEFACO )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    MISE A JOUR DES FACES PERDUES RETROUVEES DANS LES
C -----    NBTECF TETRAEDRES DE NOTECF

C ENTREES:
C --------
C NBTECF : NOMBRE DE TETRAEDRES DE LA LISTE NOTECF
C NOTECF : NUMERO NOTETR DES TETRAEDRES DE LA LISTE
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C MXFACO : MAX DE FACES DECLARABLES DANS LEFACO

C MODIFIES:
C ---------
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...

C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12 : LEFACO(12,.) = NO FACEOC DE 1 A NBFACES D'OC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Decembre 2016
C2345X7..............................................................012
      INTEGER  NOTECF(NBTECF), NOTETR(8,*), LEFACO(1:11,0:MXFACO)

      NBFARE = 0
      DO NT=1,NBTECF

C        TETRAEDRE DE SOMMET NSNOCF=NSDUCF
         NTE = NOTECF( NT )

         DO NF=1,4

C           LA FACE NF DE NTE EST ELLE DANS LEFACO?
            CALL NULEFT( NF, NTE, NOTETR, MXFACO, LEFACO,  NFLEFA )
C           NFLEFA: LE NUMERO DANS LEFACO DE LA FACE NF DE NTE
C                   >0 SI ELLE EST RETROUVEE
C                   =0 SI ELLE N'EST PAS RETROUVEE

            IF( NFLEFA .GT. 0 ) THEN
               NTELEF = LEFACO( 11, NFLEFA )
               IF( NTELEF .NE. NTE ) THEN
C                 UNE FACE DE LEFACO RETROUVEE
                  NBFARE = NBFARE + 1
                  LEFACO( 11, NFLEFA ) = NTE
               ENDIF
            ENDIF

         ENDDO

      ENDDO

ccc      PRINT*,'mjlefaco:',NBFARE,' FACES PERDUES DE LEFACO RETROUVEES'

      RETURN
      END
