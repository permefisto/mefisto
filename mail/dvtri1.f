      SUBROUTINE DVTRI1( NBSTEC, NOSTEC, NOSTBA, MXSOMM, PXYD,
     %                   MXTRIA, N1TRVI, NOTRIA, NOTRSO, CETRIA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRIANGULER LES SOMMETS DE L'ENVELOPPE CONVEXE DE LA
C -----    PREMIERE LIGNE ENVELOPPE DU DOMAINE A TRIANGULER
C
C ENTREES:
C --------
C NBSTEC : NOMBRE DE SOMMETS DE L'ENVELOPPE CONVEXE
C NOSTEC : NOSTEC(1,NA) NUMERO DU SOMMET DE L'ARETE NA DE L'ENVELOPPE
C          NOSTEC(2,NA) NUMERO DE L'ARETE SUIVANTE DANS L'ENVELOPPE
C          NA=1 EST LA PREMIERE ARETE DE L'ENVELOPPE PARCOURUE SELON
C          LE SENS DES AIGUILLES D'UNE MONTRE
C          LE CHAINAGE EST CIRCULAIRE
C NOSTBA : NUMERO PXYD DU BARYCENTRE DE L'ENVELOPPE CONVEXE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TRIANGULATION
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C
C SORTIES:
C --------
C NOTRSO : NOTRSO(I) NUMERO D'UN TRIANGLE AYANT POUR SOMMET I DANS PXYD
C MXTRIA : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES DANS NOTRIA
C N1TRVI : NUMERO DU PREMIER TRIANGLE VIDE DANS LE TABLEAU NOTRIA
C          LE CHAINAGE DES TRIANGLES VIDES SE FAIT SUR NOTRIA(4,.)
C NOTRIA : LISTE DES TRIANGLES
C  PAR TRIANGLE : SOMMET1 SOMMET2 SOMMET3 TR_VOIS1 TR_VOIS2 TR_VOIS3
C                 SOMMET    EST LE NUMERO DU SOMMET
C                 TR_VOIS i EST LE NUMERO DANS NOTRIA DU TRIANGLE
C                           ADJACENT PAR L'ARETE i
C          TABLEAU ENTIER(6,2*MXSOMM)
C CETRIA : COORDONNEES DU CENTRE DU CERCLE CIRCONSCRIT ET
C          CARRE DU RAYON PAR TRIANGLE : XCENTRE YCENTRE RAYON**2
C IERR   : 0 SI PAS D'ERREUR
C          5 SATURATION DES ARETES DU TABLEAU NOSTEC
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       JUIN 1994
C....................................................................012
      include"./incl/trvari.inc"
      INTEGER          NOSTEC(1:2,1:*), NOTRIA(6,MXTRIA), NOTRSO(MXSOMM)
      DOUBLE PRECISION PXYD(3,*), CETRIA(3,MXTRIA)
C
C     MISE A ZERO DU TABLEAU NOTRSO
      DO 10 I = 1 , MXSOMM
         NOTRSO(I) = 0
 10   CONTINUE
C
C     INITIALISATION DES NBSTEC TRIANGLES
      N1 = 1
      DO 20 I = 1, NBSTEC
C        LES 2 SOMMETS NS1 NS2 DE L'ARETE DE L'ENVELOPPE CONVEXE
         NS1 = NOSTEC(1,N1)
         N2  = NOSTEC(2,N1)
         NS2 = NOSTEC(1,N2)
C
         NOTRIA(1,I) = NS1
         NOTRIA(2,I) = NOSTBA
         NOTRIA(3,I) = NS2
         NOTRIA(4,I) = I-1
         NOTRIA(5,I) = I+1
         NOTRIA(6,I) = 0
C
C        TRACE DU TRIANGLE I
         CALL DVTRTR( PXYD, NOTRIA, I, NCBLEU, NCBLAN )
C
C        LE NUMERO D'UN TRIANGLE DE SOMMET NS1
         NOTRSO(NS1) = I
C
C        LES COORDONNEES DU CENTRE DE LA BOULE CIRCONSCRITE
C        DU TRIANGLE I ET LE CARRE DE SON RAYON
         IER = 1
         CALL CENCED( PXYD(1,NS1), PXYD(1,NOSTBA),
     %                PXYD(1,NS2), CETRIA(1,I), IER )
         N1 = N2
 20   CONTINUE
C
C     RECTIFICATION DU PREMIER ET DERNIER TRIANGLE
      NOTRIA(4,1     ) = NBSTEC
      NOTRIA(5,NBSTEC) = 1
C
C     LE NUMERO D'UN TRIANGLE DE SOMMET NS1
      NOTRSO( NOSTBA ) = 1
C
C     DEBUT DES CHAINAGES DES TRIANGLES VIDES
      N1TRVI = NBSTEC + 1
C
C     CHAINAGE DES TRIANGLES VIDES SUIVANTS
      DO 30 I = N1TRVI , MXTRIA
C        LE NUMERO DU SOMMET 1 = 0 => TRIANGLE VIDE
         NOTRIA(1,I) = 0
C        LE TRIANGLE VIDE SUIVANT
         NOTRIA(4,I) = I + 1
 30   CONTINUE
C
C     LA FIN DU CHAINAGE DES SUIVANTS VIDES
      NOTRIA(4,MXTRIA) = 0
      END
