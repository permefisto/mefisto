      SUBROUTINE VD2TPL( COANPL, NF1, NF2, LEFACO, PTXYZD, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LES FACES NF1 NF2 DE LEFACO SONT-ELLES COPLANAIRES ?
C -----    C-A-D LA VALEUR ABSOLUE DU COSINUS DE L'ANGLE ENTRE
C          LES NORMALES EST SUPERIEUR OU EGAL A COANPL

C ENTREES:
C --------
C COANPL : SEUIL DE LA VALEUR ABSOLUE DU COSINUS DE L'ANGLE FORME
C          PAR LES NORMALES AUX 2 FACES ET AU DESSUS DUQUEL LES 2 FACES
C          SONT CONSIDEREES COPLANAIRES
C NF1 NF2: NUMERO DANS LEFACO DES 2 FACES PEUT ETRE PLANES
C LEFACO : LES 3 SOMMETS, 2 MATERIAUX, 3 FACES VOISINES ET CHAINAGE
C          DES FACES TRIANGULAIRES DU CONTOUR ET INTERFACES
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE

C SORTIE :
C --------
C NONOUI : =1 SI LES 2 FACES SONT DANS LE MEME PLAN
C          =0 SINON ou LA NORMALE a UNE FACE REDUITE A UNE ARETE
C                                                   ou UN  SOMMET
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC  SEPTEMBRE 1991
C....................................................................012
      DOUBLE PRECISION PTXYZD(1:4,1:*), COS2TRD
      INTEGER          LEFACO(11,0:*)
      INTRINSIC        REAL

C     LE COSINUS DE L'ANGLE DES NORMALES AUX TRIANGLES NF1 NF2
      CALL COS2TD( PTXYZD(1,LEFACO(1,NF1)),
     %             PTXYZD(1,LEFACO(2,NF1)),
     %             PTXYZD(1,LEFACO(3,NF1)),
     %             PTXYZD(1,LEFACO(1,NF2)),
     %             PTXYZD(1,LEFACO(2,NF2)),
     %             PTXYZD(1,LEFACO(3,NF2)),
     %             COS2TRD, IERR1, IERR2 )

      IF( IERR1.NE.0 .OR. IERR2.NE.0 ) THEN
C        UNE FACE est REDUITE a UNE ARETE ou un SOMMET
C        => UNE NORMALE N'EST PAS CALCULABLE
ccc         COSN12 = 9. 26/9/2019
         NONOUI = 0
         GOTO 9999
      ENDIF

C     LES FACES SONT COPLANAIRES SI LA VALEUR ABSOLUE DU COSINUS
C     DE L'ANGLE ENTRE LES NORMALES EST SUPERIEUR OU EGAL A COANPL
C              ( 0.99985  => 1 DEGRE  )
C              ( 0.9994   => 2 DEGRES )
C              ( 0.99905  => 2.5 DEGRES )
C              ( 0.99863  => 3 DEGRES )
C              ( 0.9976   => 4 DEGRES )
C              ( 0.9962   => 5 DEGRES )
      IF( ABS( REAL( COS2TRD ) ) .GE. COANPL ) THEN
         NONOUI = 1
      ELSE
         NONOUI = 0
      ENDIF

 9999 RETURN
      END
