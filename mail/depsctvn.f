      SUBROUTINE DEPSCTVN( NTEQM, NOTETR, N1TETS, PTXYZD, NPSOFR,INFACO,
     %                     VTEQM, QTEQM, MODIF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:  SI NTEQM EST UN TETRAEDRE DE VOLUME NEGATIF ALORS
C ----  TENTATIVE DE DEPLACER LE SOMMET CENTRAL DU TETRAEDRE ENFONCE
C       DE L'AUTRE COTE DE LA FACE POUR RENDRE LE VOLUME POSITIF

C ENTREES:
C --------
C NTEQM  : NUMERO NOTETR DU TETRAEDRE SUPPOSE DE VOLUME NEGATIF
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : NUMERO DE POSITION PAR RAPPORT A LA FRONTIERE DE CHAQUE POINT
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI LE POINT I EST DEPLACE SUR LA SURFACE FRONTIERE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
C INFACO : = 0 PAS DE TABLEAU LEFACO NI CONTROLE DE FACE FRONTIERE
C          = 1 EXISTENCE DU TABLEAU LEFACO ET CONTROLE DE FACE FRONTIERE

C SORTIES:
C --------
C VTEQM  : VOLUME  DU TETRAEDRE NTEQM
C QTEQM  : QUALITE DU TETRAEDRE NTEQM
C MODIF  : = 1 LES XYZ D'UN SOMMET DU TETRAEDRE NTEQM ONT ETE MODIFIES
C              POUR RENDRE LE VOLUME ET LA QUALITE POSITIFS
C          = 0 PAS DE MODIFICATION DU TETRAEDRE NTEQM
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Veulettes sur mer                  Juin 2020
C2345X7..............................................................012
      PARAMETER        (MXTE1S=1024)
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE, TRACTE0
      INTEGER           NOTETR(8,*), NPSOFR(*), N1TETS(*)
      DOUBLE PRECISION  ARMIN, ARMAX, SURFTR(4), SURF(4), SFMAX, VTEQM
      DOUBLE PRECISION  PTXYZD(4,*), PTPROJ(3), XYZ(3), COOR, VTENS1
      INTEGER           NOSOFATE(3,4), NOTE1S(MXTE1S)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
      CHARACTER*120     KTITRE

      MODIF = 0
      TRACTE0 = TRACTE

C     VOLUME et QUALITE DU TETRAEDRE NTEQM
      CALL QUATETD( PTXYZD( 1, NOTETR(1,NTEQM) ),
     %              PTXYZD( 1, NOTETR(2,NTEQM) ),
     %              PTXYZD( 1, NOTETR(3,NTEQM) ),
     %              PTXYZD( 1, NOTETR(4,NTEQM) ),
     %              ARMIN, ARMAX, SURFTR, VTEQM, QTEQM )
      IF( QTEQM .GE. 0 ) GOTO 9999

C     RECHERCHE DE LA PLUS GRANDE DES 4 FACES DU TETRAEDRE NTEQM
      NFSFMAX = 1
      SFMAX   = SURFTR(1)
      DO I=2,4
         IF( SURFTR(I) .GT. SFMAX ) THEN
            SFMAX   = SURFTR(I)
            NFSFMAX = I
         ENDIF
      ENDDO

C     LE SOMMET OPPOSE A LA PLUS GRANDE FACE NFSFMAX DE NTEQM
      IF( NFSFMAX .GT. 1 ) THEN
         IS = NFSFMAX - 1
      ELSE
         IS = 4
      ENDIF

C     NS1 LE NUMERO DU SOMMET OPPOSE A LA PLUS GRANDE FACE NFSFMAX DE NTEQM
      NS1 = NOTETR(IS,NTEQM)

C     LES 3 SOMMETS DE LA PLUS GRANDE FACE NFSFMAX DE NTEQM
C     DE NORMALE DIRIGEE VERS L'INTERIEUR DU TETRAEDRE NTEQM
C     SONT NS2 NS3 NS4 => VOLUME (NS1234) >=0
      NS2 = NOSOFATE( 1, NFSFMAX )
      NS2 = NOTETR( NS2, NTEQM )

      NS3 = NOSOFATE( 3, NFSFMAX )
      NS3 = NOTETR( NS3, NTEQM )

      NS4 = NOSOFATE( 2, NFSFMAX )
      NS4 = NOTETR( NS4, NTEQM )

C     PTPROJ EST LE POINT PROJETE DU SOMMET OPPOSE NS1 SUR LA FACE NFSFMAX
      CALL PRPTPLD( PTXYZD(1,NS1),
     %              PTXYZD(1,NS2), PTXYZD(1,NS3), PTXYZD(1,NS4),
     %              PTPROJ, IERR )
      IF( IERR .NE. 0 ) THEN
C        3 POINTS ALIGNES NE DEFINISSANT PAS UN TRIANGLE
         GOTO 9999
      ENDIF

C     AFFICHAGE DES TETRAEDRES DE SOMMET NS1, VOLUME et QUALITE INITIAUX
      CALL TETR1S( NS1, N1TETS, NOTETR, NBTE1S, MXTE1S, NOTE1S, IERR )

      PRINT*,'depsctvn: TETRAEDRE(',NTEQM,'): St',
     %       (NOTETR(K,NTEQM),K=1,4),' TeOp',(NOTETR(K,NTEQM),K=5,8),
     %       ' V=',VTEQM,' Q=',QTEQM,' NPSOFR(',NS1,')=', NPSOFR(NS1)
      QMIN0 = QTEQM
      DO I = 1, NBTE1S
         NTENS1 = NOTE1S( I )
         IF( NTENS1 .GT. 0 .AND. NOTETR(1,NTENS1) .GT. 0 ) THEN
            CALL QUATETD( PTXYZD( 1, NOTETR(1,NTENS1) ),
     %                    PTXYZD( 1, NOTETR(2,NTENS1) ),
     %                    PTXYZD( 1, NOTETR(3,NTENS1) ),
     %                    PTXYZD( 1, NOTETR(4,NTENS1) ),
     %                    ARMIN, ARMAX, SURF, VTENS1, QTENS1 )
            IF( QTENS1 .LT. QMIN0 ) QMIN0 = QTENS1
            PRINT*,'depsctvn: TETRAEDRE(',NTENS1,'): St',
     %             (NOTETR(K,NTENS1),K=1,4),' Tens1',
     %             (NOTETR(K,NTENS1),K=5,8),' V=',VTENS1,' Q=',QTENS1
         ENDIF
      ENDDO

C     TRACE DES ARETES DU TETRAEDRE NTE, DE SES 4 TETRAEDRES
C     VOISINS, ET DES TETRAEDRES VOISINS DES TETRAEDRES VOISINS
      KTITRE='depsctvn: DEPLACER LE SOMMET CENTRAL DU TETRAEDRE      
     % ENFONCE de VOLUME                    EST TRAITE ENSUITE'
      WRITE(KTITRE(51:58),'(I8)') NTEQM
      WRITE(KTITRE(78:91),'(G14.6)') VTEQM
      CALL SANSDBL( KTITRE, L )
      CALL TRFETO9( KTITRE(1:L), PTXYZD, NTEQM, NOTETR )

C     DEPLACEMENT DU SOMMET NS1 DE L'AUTRE COTE DE LA PLUS GRANDE FACE
C     PTPROJ EST LE POINT PROJETE DU SOMMET OPPOSE SUR LA FACE NFSFMAX
C     ----------------------------------------------------------------
C     SAUVEGARDE DANS XYZ DES COORDONNEES INITIALES DU SOMMET NS1
      DO I = 1, 3
         COOR = PTXYZD( I, NS1 )
         XYZ( I ) = COOR
         PTXYZD( I, NS1 ) = COOR + 1.8D0 * ( PTPROJ( I ) - COOR )
      ENDDO

C     VOLUME>0 et QUALITE AMELIORES DU TETRAEDRE NTEQM?
      CALL QUATETD( PTXYZD( 1, NOTETR(1,NTEQM) ),
     %              PTXYZD( 1, NOTETR(2,NTEQM) ),
     %              PTXYZD( 1, NOTETR(3,NTEQM) ),
     %              PTXYZD( 1, NOTETR(4,NTEQM) ),
     %              ARMIN, ARMAX, SURFTR, VTEQM, QTEQM )

      PRINT*
      PRINT*,'depsctvn: TETRAEDRE(',NTEQM,'): St',
     %       (NOTETR(K,NTEQM),K=1,4),' TeOp',(NOTETR(K,NTEQM),K=5,8),
     %       ' V=',VTEQM,' Q=',QTEQM

C     VOLUME et QUALITE des TETRAEDRES DE SOMMET NS1 DEPLACE
      QMIN1 = QTEQM
      DO I = 1, NBTE1S
         NTENS1 = NOTE1S( I )
         IF( NTENS1 .GT. 0 .AND. NOTETR(1,NTENS1) .GT. 0 ) THEN
            CALL QUATETD( PTXYZD( 1, NOTETR(1,NTENS1) ),
     %                    PTXYZD( 1, NOTETR(2,NTENS1) ),
     %                    PTXYZD( 1, NOTETR(3,NTENS1) ),
     %                    PTXYZD( 1, NOTETR(4,NTENS1) ),
     %                    ARMIN, ARMAX, SURF, VTENS1, QTENS1 )
            IF( QTENS1 .LT. QMIN1 ) QMIN1 = QTENS1
            PRINT*,'depsctvn: TETRAEDRE(',NTENS1,'): St',
     %       (NOTETR(K,NTENS1),K=1,4),' Tens1',(NOTETR(K,NTENS1),K=5,8),
     %         ' V=',VTENS1,' Q=',QTENS1
         ENDIF
      ENDDO


      IF( QMIN0 .LT. QMIN1 ) THEN

C        MODIFICATION ACCEPTEE DES XYZ DU SOMMET NS1
C        -------------------------------------------
         MODIF = 1

         IF( INFACO. NE. 0 .AND. NPSOFR(NS1) .GT. 2 ) THEN
C           NS1 POINT DEPLACE SUR LA SURFACE
C           DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
            NPSOFR(NS1) = -NPSOFR(NS1)
         ENDIF

         PRINT*,'depsctvn: le SOMMET', NS1,' est DEPLACE pour RENDRE le 
     %VOLUME',VTEQM,' >0 et sa QUALITE=', QTEQM, '>=0'
         PRINT*,'depsctvn: NS=',NS1,' XYZ AVANT',XYZ,' QMIN0=',QMIN0
         PRINT*,'depsctvn: NS=',NS1,' XYZ APRES',(PTXYZD(I,NS1),I=1,3),
     %          ' QMIN1=',QMIN1

C        TRACE DES ARETES DU TETRAEDRE NTE, DE SES 4 TETRAEDRES
C        VOISINS, ET DES TETRAEDRES VOISINS DES TETRAEDRES VOISINS
ccc         tracte = .true.
         KTITRE='depsctvn: DEPLACE LE SOMMET CENTRAL DU TETRAEDRE         
     %    ENFONCE de VOLUME                  Q=             est TRAITE'
         WRITE(KTITRE(51:58),'(I8)') NTEQM
         WRITE(KTITRE(78:91), '(G14.6)') VTEQM
         WRITE(KTITRE(95:108),'(G14.6)') QMIN1
         CALL SANSDBL( KTITRE, L )
         CALL TRFETO9( KTITRE(1:L), PTXYZD, NTEQM, NOTETR )

      ELSE

C        PAS DE MODIFICATION DES XYZ DU SOMMET NS1
C        -----------------------------------------
         MODIF = 0
         DO I = 1, 3
            PTXYZD( I, NS1 ) = XYZ( I )
         ENDDO
         PRINT*,'depsctvn: le SOMMET', NS1,' N''EST PAS DEPLACE QMIN0=',
     %           QMIN0

      ENDIF
      PRINT*

 9999 tracte = tracte0
      RETURN
      END
