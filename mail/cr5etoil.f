      SUBROUTINE CR5ETOIL( NFLPER, QTEAME, KTITRE, MXSOMM, PTXYZD,
     %                     NPSOFR,
     %                     MXTETR, N1TEVI, NOTETR, N1TETS, NUDTETR,
     %                     MXFACO, LEFACO, NO0FAR, NBTRCF, NOTRCF,
     %                     MXTECF, NBTECF, NOTECF, MODIF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CONSTRUCTION DE L'ETOILE DES NBTRCF FACES PERDUES
C -----    DE LA FRONTIERE PAR TRAITEMENT DES TETRAEDRES DE MEDIOCRE
C          QUALITE PAR

C          DECOMPOSITION DU TETRAEDRE EN 2 TETRAEDRES S'IL
C          EST PLAT AVEC UN SOMMET PROCHE DU MILIEU D'UNE ARETE

C          SI LE TYPE DU TETRAEDRE PLAT EST UN QUADRANGLE
C          ALORS SUPPRESSION D'UNE DE SES DIAGONALES EN LA SECONDE

C          SINON AJOUT DES TETRAEDRES OPPOSES AUX FACES DES TETRAEDRES
C                DE QUALITE MEDIOCRE

C ENTREES:
C --------
C NFLPER : NUMERO LEFACO DE LA FACE PERDUE A TRAITER
C QTEAME : QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C          AMELIORATION DES TETRAEDRES EST DEMANDEE
C KTITRE : TITRE D'UN TRACE
C MXSOMM : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : NUMERO DE POSITION PAR RAPPORT A LA FRONTIERE DE CHAQUE POINT
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE

C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C NUDTETR: NO NOTETR DU DERNIER TETRAEDRE ACTIF

C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C NO0FAR : NUMERO DES 3 SOMMETS DES FACES AJOUTEES AU CF

C NBTRCF : NOMBRE DE FACES DE NOTRCF
C NOTRCF : >0 NUMERO DANS LEFACO DES TRIANGLES PERDUS  DU CF
C          <0 NUMERO DANS NO0FAR DES TRIANGLES AJOUTES AU CF
C NBSTCF : NOMBRE DE SOMMETS DES ARETES PERIPHERIQUES DU CF
C NOSTCF : NUMERO PTXYZD DES NBSTCF SOMMETS DES ARETES PERIPHERIQUES

C SORTIES:
C --------
C NBTECF : NOMBRE DE TETRAEDRES DE L'ETOILE
C NOTECF : NUMERO NOTETR DES NBTECF TETRAEDRES DE L'ETOILE
C NUDTETR: NO NOTETR DU DERNIER TETRAEDRE ACTIF
C MODIF  : =0 PAS DE MODIFICATION DES TETRAEDRES DE QUALITE MEDIOCRE
C          =1        MODIFICATION DES TETRAEDRES DE QUALITE MEDIOCRE
C                    PAR SUBDIVISION DE CERTAINS D'ENTRE EUX
C          =2 AJOUT DES TETRAEDRES OPPOSES AUX FACES DES TETRAEDRES
C             DE QUALITE MEDIOCRE AU TABLEAU NOTECF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint PIERRE du PERRAY              Mai 2018
C2345X7..............................................................012
      CHARACTER*(*)     KTITRE
      DOUBLE PRECISION  PTXYZD(4,MXSOMM), VOLUTE,
     %                  ARMIN, ARMAX, SURFTR(4)
      INTEGER           NPSOFR(MXSOMM), NOTETR(8,*), N1TETS(MXSOMM),
     %                  LEFACO(11,0:MXFACO), NO0FAR(3,*),
     %                  NOTRCF(NBTRCF), NOTECF(MXTECF)
      INTEGER           NFETOIL(5), NVOLTE(2)

      MODIF = 0
      IF( NBTECF .LE. 0 ) RETURN
      NBTECF0 = NBTECF

C     QTEMIN : QUALITE MINIMALE EXIGEE POUR CREER UN TETRAEDRE
      QTEMIN = QTEAME / 10
      INFACO = 1
      IVOLTE = 0

C     CALCUL DU VOLUME ET QUALITE DES NBTECF TETRAEDRES DU TABLEAU NOTECF
      NBTECF0 = NBTECF
      DO N = 1, NBTECF0

C        NUMERO NOTETR DU N-EME TETRAEDRE DE L'ETOILE ACTUELLE
         NTE = NOTECF( N )

         IF( NTE .GT. 0 .AND. NOTETR(1,NTE) .GT. 0 ) THEN

C           LES COORDONNEES DU CENTRE DE LA BOULE CIRCONSCRITE
C           AU TETRAEDRE NTE ET CARRE DE SON RAYON, VOLUME ET QUALITE
            CALL QUATETD( PTXYZD( 1, NOTETR(1,NTE) ),
     %                    PTXYZD( 1, NOTETR(2,NTE) ),
     %                    PTXYZD( 1, NOTETR(3,NTE) ),
     %                    PTXYZD( 1, NOTETR(4,NTE) ),
     %                    ARMIN, ARMAX, SURFTR, VOLUTE, QUALTE )

            IF( QUALTE .LE. QTEMIN ) THEN

C              NTE TETRAEDRE DE MEDIOCRE QUALITE
               PRINT*,'cr5etoil: FACE PERDUE',NFLPER,
     %                ': Essai a1teqm du TETRAEDRE INITIAL',
     %                 NTE,' St:',(NOTETR(L,NTE),L=1,4),
     %                ' V=',VOLUTE,' Q=',QUALTE

C              AMELIORER LA QUALITE DES TETRAEDRES AUTOUR DU TETRAEDRE NTE
C              DE MEDIOCRE QUALITE SELON SON TYPE QUADRANGLE ou TRIANGLE
C              ou LA CONFIGURATION DES TETRAEDRES OPPOSES AUX FACES
               CALL A1TEQM( QTEMIN, NTE,    MXSOMM, PTXYZD, NPSOFR,
     %                      INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %                      MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                      MODIF,  IERR )

cccC              TENTATIVE DE MODIFIER XYZ D'UN SOMMET DU TETRAEDRE
cccC              NTE, NON SOMMET DU CF POUR AMELIORER LA QUALITE DE NTE
ccc               CALL MDXYZ1ST( QTEMIN, NTE, NOTETR, PTXYZD, NPSOFR, 
ccc     %                        NBSTCF, NOSTCF, NSDEPL  )
cccC              NOUVEAUX VOLUME ET QUALITE DE NTE
ccc               CALL QUATETD( PTXYZD( 1, NOTETR(1,NTE) ),
ccc     %                       PTXYZD( 1, NOTETR(2,NTE) ),
ccc     %                       PTXYZD( 1, NOTETR(3,NTE) ),
ccc     %                       PTXYZD( 1, NOTETR(4,NTE) ),
ccc     %                       ARMIN, ARMAX, SURFTR, VOLUTE, QUALTE )
ccc               IF( NSDEPL .GT. 0 ) THEN
cccC                 APRES MODIFICATION DES XYZ DU SOMMET NSDEPL
cccC                 RETOUR SUR L'ETOILE GOTO ?
ccc               ENDIF

               IF( MODIF .GT. 0 ) THEN
C                 DES TETRAEDRES ONT ETE MODIFIES
                  GOTO 9999
               ENDIF

            ENDIF
         ENDIF

      ENDDO


C     AUCUNE MODIFICATION AUPARAVANT =>
C     AJOUT DES 4 TETRAEDRES OPPOSES AUX 4 FACES DES TETRAEDRES MEDIOCRES
C     -------------------------------------------------------------------
ccc      PRINT*,'cr5etoil: FACE PERDUE',NFLPER,
ccc     %       ' AJOUT des TETRAEDRES OPPOSES aux 4 FACES des',
ccc     %        NBTECF0,' TETRAEDRES de l''ETOILE'

      DO N = 1, NBTECF0

C        NUMERO NOTETR DU N-EME TETRAEDRE DE L'ETOILE
         NTE = NOTECF( N )

C        PROTECTION EN CAS DE PROBLEME LORS DE LA TETRAEDRISATION
C        DE L'ETOILE POUR REVENIR EN ARRIERE
         IF( NTE .GT. 0 .AND. NOTETR(1,NTE) .GT. 0 ) THEN

C           LES COORDONNEES DU CENTRE DE LA BOULE CIRCONSCRITE
C           AU TETRAEDRE NTE ET CARRE DE SON RAYON, VOLUME ET QUALITE
            CALL QUATETD( PTXYZD( 1, NOTETR(1,NTE) ),
     %                    PTXYZD( 1, NOTETR(2,NTE) ),
     %                    PTXYZD( 1, NOTETR(3,NTE) ),
     %                    PTXYZD( 1, NOTETR(4,NTE) ),
     %                    ARMIN, ARMAX, SURFTR, VOLUTE, QUALTE )

            IF( QUALTE .LE. QTEMIN ) THEN

               DO 20 NF=1,4

                  NTEOP = NOTETR( 4+NF, NTE )
                  IF( NTEOP.GT.0 .AND. NOTETR(1,NTEOP).GT.0 ) THEN

C                    NTEOP EST IL DEJA DANS L'ETOILE?
                     DO K=1,NBTECF
                        IF( NOTECF(K) .EQ. NTEOP ) GOTO 20
                     ENDDO

C                    NON: IL EST AJOUTE A NOTECF
                     IF( NBTECF .GE. MXTECF ) THEN
                        PRINT*,'cr5etoil: AUGMENTER MXTECF=',MXTECF
C                       ABANDON
                        GOTO 9999
                     ENDIF

                     PRINT*,'cr5etoil: FACE PERDUE',NFLPER,
     %                  ' NOTETR(',NTEOP,'):',(NOTETR(K,NTEOP),K=1,8),
     %                  ' est le TETRAEDRE OPPOSE a NTE AJOUTE a NOTECF'
                     NBTECF = NBTECF + 1
                     NOTECF( NBTECF ) = NTEOP
                     MODIF = 2

                  ENDIF

 20            ENDDO

            ENDIF
         ENDIF

      ENDDO

C     COMPRESSION DU TABLEAU NOTECF DES TETRAEDRES DE L'ETOILE
C     --------------------------------------------------------
      NBTEC0 = NBTECF
      NBTECF  = 0
      DO K=1,NBTEC0
         NTEK = NOTECF( K )
         IF( NTEK .GT. 0 .AND. NOTETR(1,NTEK) .GT. 0 ) THEN
            NBTECF = NBTECF + 1
            NOTECF( NBTECF ) = NTEK
         ENDIF
      ENDDO

C     TRACE DES NBTECF TETRAEDRES DE NOTECF
C     -------------------------------------
      IF( NBTECF0 .LT. NBTECF ) THEN
      KTITRE='cr5etoil: FACE PERDUE             TETRAEDRES NOTECF a la F
     %IN'
         WRITE(KTITRE(23:29),'(I7)') NFLPER
         WRITE(KTITRE(29:33),'(I5)') NBTECF
         CALL SANSDBL( KTITRE, L )
ccc         PRINT*,KTITRE(1:L)
         CALL TRFETO4( KTITRE(1:L), PTXYZD, 0, NFETOIL,
     %                 NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                 NBTECF, NOTECF, NOTETR )
      ENDIF

 9999 RETURN
      END
