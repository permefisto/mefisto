      SUBROUTINE DVINIT( MXSOMM, NBSOIN, NOSOIN, DISOIN,
     %                   NBARF1, NBARFR, MXSOAR, NOSOAR,
     %                   NBSOMM, PXYD  , NLSOFR, NSLIGN,
     %                   ORIGIN, D2D3  , COMXMI, AREMIN, AREMAX,
     %                   XMIN  , YMIN  , XYMXMI, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    INITIALISATION DES TABLEAUX NDARLF NOSOAR PXYD ORIGIN D2D3
C -----
C ENTREES:
C --------
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TRIANGULATION
C MXTRIA : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES
C NBLFTR : NOMBRE DE LIGNES FERMEES LIMITANT LA SURFACE A TRIANGULER
C NDARLF : NUMERO DE LA PREMIERE ARETE DE CHAQUE LIGNE FERMEE DANS
C          LE TABLEAU NOSOAR
C MNSOLF : ADRESSE MCN DU TABLEAU SOMMETS DE CHAQUE LIGNE FERMEE
C NOSOIN : NOSOIN(I) NUMERO DANS LE LEXIQUE POINTS DU I-EME POINT INTERNE
C DISOIN : DISOIN(I) DISTANCE SOUHAITEE AVEC LES VOISINS DU POINT
C NBARF1 : NOMBRE D'ARETES DE LA LIGNE ENVELOPPE
C MXSOAR : NOMBRE MAXIMAL D'ARETES FRONTIERES DECLARABLES
C
C ENTREES ET SORTIES :
C --------------------
C NBSOIN : NOMBRE DE POINTS INTERNES A LA SURFACE FUTUR SOMMET DE LA
C          TRIANGULATION (REMIS A JOUR SI POINTS DOUBLES)
C NBARFR : NOMBRE D'ARETES  DE LA FRONTIERE OU CONTOUR DU DOMAINE =
C          NOMBRE DE POINTS DE LA FRONTIERE OU CONTOUR DU DOMAINE
C NOSOAR : NUMERO DES 2 SOMMETS DE CHAQUE ARETE ET
C          POINTEUR SUR L'ARETE SUIVANTE
C
C  ATTENTION: UNE ARETE DOIT ETRE LA SUIVANTE DE LA PRECEDENTE
C  ---------- OU LA PREMIERE D'UNE COMPOSANTE CONNEXE DE LA
C             FRONTIERE  OU LA DERNIERE
C             DE PLUS LE DOMAINE DOIT ETRE A GAUCHE POUR LE
C             LE SENS NS1 => NS2
C
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C          TABLEAU REEL2(3,MXSOMM)
C
C SORTIES:
C --------
C NBSOMM : NOMBRE DE SOMMETS APRES IDENTIFICATION
C NLSOFR : NUMERO DE LA LIGNE FERMEE DE CHAQUE POINT
C         -NUMERO DE POINT INTERNE UTILISATEUR IMPOSE
C          0 SI LE POINT EST INTERNE OU EXTERNE NON IMPOSE
C NSLIGN : TABLEAU DU NUMERO DE SOMMET DANS SA LIGNE POUR CHAQUE
C          SOMMET FRONTALIER
C          NUMERO DU POINT DANS LE LEXIQUE POINT SI INTERNE IMPOSE
C          0 SI LE POINT EST INTERNE NON IMPOSE PAR L'UTILISATEUR
C ORIGIN : POINT ORIGINE DANS LE PLAN DE MAILLAGE
C D2D3   : MATRICE DE PASSAGE DES COORDONNEES 3D EN COORDONNEES 2D
C COMXMI : COORDONNEES MINIMALES ET MAXIMALES DES POINTS FRONTALIERS 2D
C AREMIN : LONGUEUR MINIMALE DES ARETES DES LIGNES FERMEES
C AREMAX : LONGUEUR MAXIMALE DES ARETES DES LIGNES FERMEES
C XMIN   : ABSCISSE MINIMALE APRES TRANSFORMATION 3D->2D
C YMIN   : ORDONNEE MINIMALE APRES TRANSFORMATION 3D->2D
C XYMXMI : ECART MAXIMAL ENTRE XMAX-XMIN ET YMAX-YMIN
C IERR   : 0 SI PAS D'ERREUR
C          7 TOUS LES POINTS SONT ALIGNES
C         10 LIGNE ENVELOPPE INCORRECTE
C         11 2 EXTREMITES CONFONDUES POUR UNE ARETE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1994
C....................................................................012
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
C
      INTEGER           NOSOAR(3,MXSOAR),
     %                  NOSOIN(NBSOIN),
     %                  NLSOFR(MXSOMM),
     %                  NSLIGN(MXSOMM)
C
      REAL              DISOIN(NBSOIN),
     %                  ORIGIN(3),
     %                  XYZ(3)
C
      DOUBLE PRECISION  PXYD(3,MXSOMM)
      DOUBLE PRECISION  D2D3(3,3)
C
C     LES TABLEAUX AUXILIAIRES
      REAL              COMXMI(3,2),XYZP(3,2),A(2),S
C
C     RECHERCHE DES POINTS EXTREMES EN X ET Y DU DOMAINE A TRIANGULER
C     ===============================================================
      RMAX = RINFO( 'GRAND' )
      DO 5 K=1,2
         COMXMI(K,1) =  RMAX
         COMXMI(K,2) = -RMAX
 5    CONTINUE
      COMXMI(3,1) = 0
      COMXMI(3,2) = 0
      XYZP(3,1) = 0
      XYZP(3,2) = 0
C
C     BOUCLE SUR LES SOMMETS DE LA LIGNE ENVELOPPE
C     LES COORDONNEES MIN ET MAX DES SOMMETS DE LA LIGNE ENVELOPPE
      DO 15 K=1,NBARF1
         DO 10 I=1,2
            COMXMI(I,1) = REAL( MIN( PXYD(I,K), DBLE(COMXMI(I,1)) ) )
            COMXMI(I,2) = REAL( MAX( PXYD(I,K), DBLE(COMXMI(I,2)) ) )
 10      CONTINUE
 15   CONTINUE
C
      XYZP(1,1) =  RMAX
      XYZP(2,1) =  RMAX
      XYZP(1,2) = -RMAX
      XYZP(2,2) = -RMAX
      DO 30 K=NBARF1,NBARFR
C        LES COORDONNEES MIN ET MAX DES SOMMETS DE LA FRONTIERE
         DO 20 J=1,2
            XYZP(J,1) = REAL( MIN( PXYD(J,K), DBLE(XYZP(J,1)) ) )
            XYZP(J,2) = REAL( MAX( PXYD(J,K), DBLE(XYZP(J,2)) ) )
            IF( XYZP(J,1) .LT. COMXMI(J,1) .OR.
     %          XYZP(J,2) .GT. COMXMI(J,2) ) THEN
                NBLGRC(NRERR) = 3
                KERR(1) = 'UNE LIGNE FERMEE N''EST PAS'
                KERR(2) = 'DANS L''ENVELOPPE DE LA SURFACE'
                KERR(3) = '(1-ERE LIGNE FERMEE DEMANDEE)'
                CALL LEREUR
                IERR = 10
                GOTO 9999
            ENDIF
 20      CONTINUE
 30   CONTINUE
C
C     VERIFICATION : SURFACE REDUITE A UNE LIGNE?
C     ===========================================
      XMIN = COMXMI(1,1)
      YMIN = COMXMI(2,1)
      A(1) = COMXMI(1,2) - COMXMI(1,1)
      A(2) = COMXMI(2,2) - COMXMI(2,1)
C     COEFFICIENT DE MISE A L'ECHELLE
      XYMXMI = MAX( A(1), A(2) )
C     LA LONGUEUR DE LA DIAGONALE
      S = SQRT( A(1)**2 + A(2)**2 )
      DO 60 K=1,2
         IF( A(K) .LT. 1E-4 * S ) THEN
            NBLGRC(NRERR) = 1
            KERR(1) = 'TOUS LES POINTS SONT ALIGNES'
            CALL LEREUR
            IERR = 7
            GOTO 9999
         ENDIF
 60   CONTINUE
C
C     MISE AU MINIMUM POSSIBLE DE LA DISTANCE SOUHAITEE
      DO 70 I=1,MXSOMM
         PXYD(3,I) = 0D0
 70   CONTINUE
C
C     MISE A L'ECHELLE DANS LE PLAN DES COORDONNEES DES SOMMETS
C     =========================================================
      DO 72 I=1,NBARFR
         NS1 = NOSOAR(1,I)
         R   = REAL( PXYD(1,NS1) )
C        MISE A l'ECHELLE DANS LE PLAN
         PXYD(1,I) = MISECH( R, XMIN, XYMXMI )
         R  = REAL( PXYD(2,NS1) )
         PXYD(2,I) = MISECH( R, YMIN, XYMXMI )
 72   CONTINUE
C
C     RENUMEROTATION  DES SOMMETS FRONTALIERS DE 1 A NBARFR
C     INITIALISATION  DES SOMMETS DES ARETES DANS PXYD
C     =====================================================
      AREMIN = RMAX
      AREMAX = 0
      NS0    = 0
      NBSAVA = 0
      DO 100 I=1,NBARFR
C
C        LE NUMERO DES 2 SOMMETS DE L'ARETE I
         NS1 = NOSOAR(1,I)
         NS2 = NOSOAR(2,I)
C
C        LONGUEUR DE L'ARETE I
         S = REAL( SQRT( (PXYD(1,NS2)-PXYD(1,NS1))**2 +
     %                   (PXYD(2,NS2)-PXYD(2,NS1))**2 ) )
         IF( S .LE. 0.0 ) THEN
            NBLGRC(NRERR) = 2
            WRITE(KERR(MXLGER)(1:10),'(I10)') NS1
            WRITE(KERR(MXLGER)(11:20),'(I10)') NS2
            KERR(1) = 'ARETE DE SOMMETS ' //
     %            KERR(MXLGER)(1:10) // KERR(MXLGER)(11:20)
            KERR(2) = 'EST DE LONGUEUR NULLE'
            CALL LEREUR
            IERR = 11
            GOTO 9999
         ENDIF
C        CALCUL DE LA LONGUEUR MAX ET MIN D'UNE ARETE
         IF( S .GT. AREMAX ) THEN
            AREMAX = S
         ELSE IF( S .LT. AREMIN ) THEN
            AREMIN = S
         ENDIF
C
C        LA DISTANCE SOUHAITEE EST LE MINIMUM DES DISTANCES AUX VOISINS
         IF( PXYD(3,NS1) .LE. 0D0 ) THEN
            PXYD(3,NS1) = S
         ELSE
            PXYD(3,NS1) = MIN( PXYD(3,NS1), DBLE(S) )
         ENDIF
         IF( PXYD(3,NS2) .LE. 0D0 ) THEN
            PXYD(3,NS2) = S
         ELSE
            PXYD(3,NS2) = MIN( PXYD(3,NS2), DBLE(S) )
         ENDIF
C
 100  CONTINUE
C
C     LE NOMBRE REEL DE SOMMETS FRONTALIERS
      NBSOMM = NBARFR
      NBSOFR = NBSOMM
      WRITE(IMPRIM,*) NBARFR,' SOMMETS DIFFERENTS SUR LA FRONTIERE'
CCC      WRITE(IMPRIM,*) AREMIN,' LONGUEUR MINIMALE D''UNE ARETE FRONTIERE'
CCC      WRITE(IMPRIM,*) AREMAX,' LONGUEUR MAXIMALE D''UNE ARETE FRONTIERE'
C
C     AJOUT DES SOMMETS INTERNES AUX POINTS INITIAUX
C     ==============================================
      DO 150 I=1,NBSOIN
C        LE NUMERO DU POINT INTERNE
         NS1 = NOSOIN(I)
C        MNX L'ADRESSE DE L'ABCISSE DU POINT
         CALL XYZPOI( NS1, MNX, IERR )
         IF( IERR .NE. 0 ) GOTO 9999
C        LES 2 COORDONNEES DU POINT
         CALL CH3D2D( ORIGIN, D2D3, RMCN(MNX), XYZP(1,1) )
C        MISE A l'ECHELLE
         XYZP(1,1) = MISECH( XYZP(1,1), XMIN, XYMXMI )
         XYZP(2,1) = MISECH( XYZP(2,1), YMIN, XYMXMI )
C        CE POINT NS1 EST IL DEJA STOCKE ?
         DO 140 NS = 1 ,NBSOMM
            XYZ(1) = REAL( PXYD(1,NS) )
            XYZ(2) = REAL( PXYD(2,NS) )
            XYZ(3) = 0
            CALL XYIDE( XYZP(1,1), XYZ, K )
            IF( K .NE. 0 ) THEN
C              POINT INTERNE DEJA RECENSE => AVERTISSEMENT
               NBLGRC(NRERR) = 1
               KERR(1) = 'UN POINT DOUBLE IDENTIFIE'
               CALL LEREUR
               WRITE(IMPRIM,*) 'LE POINT',NS1,' X=',XYZP(1,1),
     %        ' Y=',XYZP(2,1),' A DEJA ETE DONNE.IL EST SUPPRIME'
               GOTO 145
            ENDIF
 140     CONTINUE
C
C        NON . IL EST AJOUTE
         NBSOMM = NBSOMM + 1
C        LES COORDONNEES DU POINT
C        MISE A l'ECHELLE
         PXYD(1,NBSOMM) = XYZP(1,1)
         PXYD(2,NBSOMM) = XYZP(2,1)
         NLSOFR(NBSOMM) = -NS1
         NSLIGN(NBSOMM) =  NS1
         NS = NBSOMM
C
C        MISE A L'ECHELLE DE LA DISTANCE SOUHAITEE AUTOUR DE NS
 145     S = DISOIN(I)
         S = MISECH( S, 0.0, XYMXMI )
         IF( PXYD(3,NS) .LE. 0D0 ) THEN
            PXYD(3,NS) = S
         ELSE
            PXYD(3,NS) = MIN( DBLE(S), PXYD(3,NS) )
         ENDIF
C
 150  CONTINUE
C
C     LE NOMBRE REEL DE SOMMETS INTERNES
      NBSOIN = NBSOMM - NBSOFR
      WRITE(IMPRIM,*) NBSOIN ,' SOMMETS INTERNES'
C
C     MISE A l'ECHELLE DES XY MIN ET MAX
      DO 160 I=1,2
         COMXMI(1,I) = MISECH( COMXMI(1,I), XMIN, XYMXMI )
         COMXMI(2,I) = MISECH( COMXMI(2,I), YMIN, XYMXMI )
 160  CONTINUE
C
C     ICI LE TABLEAU PXYD N'A PAS DE SOMMETS IDENTIQUES
 9999 RETURN
      END
