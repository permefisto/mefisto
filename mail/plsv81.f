        SUBROUTINE PLSV81( NUTYOB , NUPLSV , NTPLSV , LADEFI ,
     %                     NTNSOB , MNNSOB , NTSTOB , MNSTOB , IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RENOMMER UN POINT, LIGNE, SURFACE, VOLUME
C -----
C ENTREES:
C --------
C NUTYOB : NUMERO DU TYPE DU P L S V   1:POINT, 2:LIGNE, ...
C NUPLSV : NUMERO DU P L S V DANS SON LEXIQUE
C LADEFI : TABLEAU DE DEFINITION DU P L S V
C          CF '~TD/D/A_..._DEFINITION'
C
C MODIFIE :
C ---------
C NTPLSV : NUMERO DU TABLEAU TS DU LEXIQUE DU P L S V
C          EN ENTREE LEXIQUE DU NOUVEAU P L S V
C          EN SORTIE LEXIQUE DE L'ANCIEN P L S V  RENOMME
C SORTIES:
C --------
C NTNSOB : NUMERO      TMS 'SOUS-P L S VS' DES NUMEROS DES SOMMETS DES EF
C MNNSOB : ADRESSE MCN TMS 'SOUS-P L S VS' DES NUMEROS DES SOMMETS DES EF
C          CF '~TD/D/A___SOUS-P L S VS'
C NTSTOB : NUMERO      DU TMS 'XYZSOMMET' DU P L S V
C MNSTOB : ADRESSE MCN DU TMS 'XYZSOMMET' DU P L S V
C          CF '~TD/D/A___SOMMETS'
C IERR   : 0 SI PAS D'ERREUR
C        > 0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : A.PERRONNET LABORATOIRE ANALYSE NUMERIQUE PARIS DECEMBRE 1990
C23456...............................................................012
      IMPLICIT INTEGER (W)
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a_point__definition.inc"
C
C     ATTENTION: LE NOM DE L'ANCIEN P L S V A RENOMMER DOIT ETRE
C                AU MEME ENDROIT DANS LE TABLEAU DEFINITION
C
      INTEGER           LADEFI(0:*)
      CHARACTER*24      KNOM0,KNOM1
      CHARACTER*10      NMTOBJ,NMTYOB
C
      IERR = 0
C
C     LE NUMERO DE TMS DU LEXIQUE DE TYPE NUTYOB
      NTLXLX = NTMN( NUTYOB )
C
C     LE NOM DU TYPE DU P L S V
      NMTOBJ = NMTYOB( NUTYOB )
C
C     VERIFICATION DU TYPE DU P L S V
      IF( LADEFI(WUTYPO) .NE. 81 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'PLSV81:TYPE DE P L S V DIFFERENT DE 81'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     LE  P L S V  ANCIEN EXISTE-T-IL ?
      CALL LXNLOU( NTLXLX , LADEFI(WPAVAN) , NT0 , MN0 )
      IF( NT0 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IERR = NUDCNB( NMTOBJ )
         IF( NUTYOB .EQ. 2 .OR. NUTYOB .EQ. 3 ) THEN
            KERR(1) = NMTOBJ(1:IERR-1) // ' ANCIENNE INCONNUE'
         ELSE
            KERR(1) = NMTOBJ(1:IERR-1) // ' ANCIEN INCONNU'
         ENDIF
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C
C     L'ANCIEN NOM DU POINT
      CALL NMOBNU( NMTOBJ , LADEFI(WPAVAN) , KNOM0 )
C
C     LES NOMS SONT ILS LES MEMES ?
      IF( NUPLSV .EQ. LADEFI(WPAVAN) ) THEN
         NBLGRC(NRERR) = 3
         KERR(1) = 'NOMS ANCIEN ET NOUVEAU IDENTIQUES'
         KERR(2) = 'LA DEFINITION A ETE DETRUITE'
         IERR = NUDCNB( NMTOBJ )
         KERR(3) = 'REDEFINIR '// NMTOBJ(1:IERR-1) // ' '// KNOM0
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
C     LE NOUVEAU NOM DU POINT
      CALL NMOBNU( NMTOBJ , NUPLSV , KNOM1 )
C
C     DESTRUCTION DU POINT NOUVEAU
      CALL LXLXDS( NTLXLX , KNOM1 )
C
C     L'ANCIEN POINT EST RENOMME AVEC LE NOUVEAU NOM
      CALL LXNMNM( NTLXLX , KNOM0 , KNOM1 )
C
C     LE LEXIQUE DU P L S V EST CELUI DE L'ANCIEN MAIS RENOMME
      NTPLSV = NT0
C
C     LE TABLEAU 'XYZSOMMET'
      CALL LXTSOU( NT0 , 'XYZSOMMET' , NTSTOB , MNSTOB )
C
C     LE TABLEAU 'NSEF'
      CALL LXTSOU( NT0 , 'NSEF' , NTNSOB , MNNSOB )
      END
