      SUBROUTINE NTED1FAC( NBEF,   NBNOEF, NUNOTE,
     %                     MOFACE, MXFACE, LFACES,
     %                     NF0,    NTE0,   NTE1 )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT: TROUVER LE NO NTE1 DU TETRAEDRE DERRIERE LA FACE NF0 DU TETRAEDRE NTE0
C ----

C ENTREES:	
C --------
C NBEF   : NOMBRE DE TETRAEDRES DU MAILLAGE
C NBNOEF : NOMBRE DE NOEUDS D'UN TETRAEDRE
C          (SEULS LES 4 SOMMETS SONT UTILES ICI et ONT POUR No 1 2 3 4)
C NUNOTE : NUMERO DES NBNOEF NOEUDS DES NBEF TETRAEDRES

C MOFACE : NOMBRE DE MOTS DE CHAQUE FACE DU TABLEAU LFACES
C MXFACE : NOMBRE MAXIMAL de FACES DU TABLEAU LFACES
C LFACES : LFACES(1,I)= NO DU 1-ER  SOMMET DE LA FACE
C          LFACES(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LFACES(3,I)= NO DU 3-EME SOMMET DE LA FACE
C          LFACES(4,I)= NO DU 4-EME SOMMET DE LA FACE
C                       0 SI TRIANGLE
C          LFACES(5,I)= CHAINAGE HACHAGE SUR FACE SUIVANTE
C          SI SOMMET 2 < DERNIER SOMMET  => FACE   DIRECTE DANS L'ELEMENT FINI
C                      >                 => FACE INDIRECTE
C          UNE FACE DIRECTE EST VUE DE L EXTERIEUR DE L'ELEMENT FINI
C          SOUS LA FORME DIRECTE
C          LFACES(6,I)= NUMERO DU 1-ER ELEMENT FINI CONTENANT CETTE FACE
C                       >0 SI FACE   DIRECTE DANS CET ELEMENT FINI
C                       <0 SI FACE INDIRECTE DANS CET ELEMENT FINI

C          SI LA FACE APPARTIENT A 2 ELEMENTS FINIS ALORS
C          LFACES(7,I)= NUMERO DU 2-EME ELEMENT FINI CONTENANT CETTE FACE
C                       >0 SI FACE   DIRECTE DANS CET ELEMENT FINI
C                       <0 SI FACE INDIRECTE DANS CET ELEMENT FINI
C          SINON
C          LFACES(7,I)= NUMERO DE LA FACE FRONTALIERE SUIVANTE
C                       0 SI C'EST LA DERNIERE
C          L1FAFR = NUMERO DE LA PREMIERE FACE FRONTALIERE DANS LFACES
C                   CHAINAGE SUIVANT DANS LFACES(7,I)
C          L1FA2M = NUMERO DE LA PREMIERE FACE INTERFACE 2 MATERIAUX DANS LFACES
C                   CHAINAGE SUIVANT DANS LFACES(8,I)
C          SI VOLUME MULTI-MATERIAUX
C          LFACES(8,I)= NO DE LA FACE INTERFACE ENTRE 2 MATERIAUX SUIVANTE
C                       0 SI FACE SANS INFORMATION SUPPLEMENTAIRE
C          SINON VOLUME MONO-MATERIAU
C          LFACES(8,I)= NUMERO DE LA FACE A TANGENTE
C                       0 SI FACE SANS TANGENTE

C NF0   : NO DE 1 A 4 DE LA FACE DE NTE0
C NTE0  : NO NUNOTE DU TETRAEDRE A TROUVER LE TETRAEDRE DERRIERE SA FACE NF0

C SORTIE :
C --------
C NTE1   : >0 NO NUNOTE DU TETRAEDRE DERRIERE LA FACE NF0 DE NTE0
C          =0 PAS       DE TETRAEDRE DERRIERE LA FACE NF0 DE NTE0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint Pierre du Perray           Octobre 2020
C23456---------------------------------------------------------------012
      include"./incl/langue.inc"
      INTEGER         NTE0, NTE1, NBEF, NBNOEF, NUNOTE(NBEF,NBNOEF),
     %                MOFACE, MXFACE, LFACES(MOFACE,MXFACE),
     %                NSTFAC(3), NOFA, NTED1, NTED2

      INTEGER         NOSOFATE(3,4)
      DATA            NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /

C     LE NUMERO DES SOMMETS DE LA FACE NF0 DE NTE0
      NSTFAC(1) = NUNOTE( NTE0, NOSOFATE(1,NF0) )
      NSTFAC(2) = NUNOTE( NTE0, NOSOFATE(2,NF0) )
      NSTFAC(3) = NUNOTE( NTE0, NOSOFATE(3,NF0) )

C     RECHERCHE DU TETRAEDRE DERRIERE LA FACE NF0 DE NTE0
C     TRI CROISSANT DES NUMEROS DES 3 SOMMETS DE LA FACE
      CALL TRIENT( 3, NSTFAC )

C     HACHAGE POUR RETROUVER LA FACE DANS LES FACES DE LA TETRAEDRISATION
      LIBREF = MXFACE
      CALL HACHAG( 3, NSTFAC, MOFACE, MXFACE, LFACES, 5,
     %             LIBREF, NOFA )

      IF( NOFA .LE. 0 ) THEN
C        ERREUR: FACE NF0 DE NTE0 NON RETROUVEE PARMI
C                toutes les FACES des TETRAEDRES => PROBLEME!
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'nted1fac: NTE0=',NTE0,' sa FACE',NF0,
     %             ' st:',NSTFAC,
     %             ' NON FACE des',NBEF,' TETRAEDRES'
         ELSE
            PRINT*,'nted1fac: NTE0=',NTE0,' its FACE',NF0,
     %             ' vt:',NSTFAC,
     %             ' is NOT a FACE of',NBEF,' TETRAHEDRA'
         ENDIF
C        POINT DANS le TETRAEDRE NTE0 de FACE NON 1 FACE des TETRAEDRES
         NTE1 = 0
         GOTO 9999
      ENDIF

C     LE NUMERO DES EF QUI SE PARTAGENT LA FACE NOFA
      NTED1 = ABS( LFACES( 6, NOFA ) )
      NTED2 = ABS( LFACES( 7, NOFA ) )

      IF( NTED1 .EQ. NTE0 ) THEN
C        NTED2 EST LE TETRAEDRE DERRIERE LA FACE NF0 DE NTE0
         NTE1 = NTED2
      ELSE IF( NTED2 .EQ. NTE0 ) THEN
C        NTED1 EST LE TETRAEDRE DERRIERE LA FACE NF0 DE NTE0
         NTE1 = NTED1
      ELSE
C        PAS DE TETRAEDRE DERRIERE CAR LA FACE APPARTIENT A 3 TETRAEDRES...
         NTE1 = 0
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'nted1fac: FACE',NSTFAC,' COMMUNE a 3 TETRAEDRES',
     %              NTE0,NTED1,NTED2
         ELSE
            PRINT*,'nted1fac: COMMON FACE',NSTFAC,'to 3 TETRAHEDRA',
     %              NTE0,NTED1,NTED2
         ENDIF
        PRINT*,'nted1fac:TETRAEDRE',NTE0, ': St',(NUNOTE(NTE0, K),K=1,4)
        PRINT*,'nted1fac:TETRAEDRE',NTED1,': St',(NUNOTE(NTED1,K),K=1,4)
        PRINT*,'nted1fac:TETRAEDRE',NTED2,': St',(NUNOTE(NTED2,K),K=1,4)

      ENDIF

 9999 RETURN
      END
