      SUBROUTINE TETRMA( NCTRIA, NCARET,
     %                   COMXMI, PXYD,
     %                   MOSOAR, MXSOAR, NOSOAR,
     %                   MOARTR, MXARTR, NOARTR,
     %                   NBTRIA, QUAMOY, QUAMIN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LA TRIANGULATION ACTUELLE DEFINIE PAR LES TABLEAUX
C -----    NOSOAR ET NOARTR
C          CALCULER LA QUALITE MOYENNE ET MINIMALE DES TRIANGLES
C
C ENTREES:
C --------
C NCTRIA : NUMERO DE LA COULEUR DE TRACE DES TRIANGLES
C NCARET : NUMERO DE LA COULEUR DE TRACE DES ARETES DES TRIANGLES
C COMXMI : 3 COORDONNEES MINIMALES ET MAXIMALES
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE ET
C          INDICE DANS NOSOAR DE L'ARETE SUIVANTE DANS LE HACHAGE
C MXSOAR : NOMBRE MAXIMAL D'ARETES STOCKABLES DANS LE TABLEAU NOSOAR
C          ATTENTION: MXSOAR>3*MXSOMM OBLIGATOIRE!
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES DE L'ARETE,
C          CHAINAGE DES ARETES FRONTALIERES, CHAINAGE DU HACHAGE DES ARETES
C          HACHAGE DES ARETES = NOSOAR(1)+NOSOAR(2)*2
C          AVEC MXSOAR>=3*MXSOMM
C          UNE ARETE I DE NOSOAR EST VIDE <=> NOSOAR(1,I)=0 ET
C          NOSOAR(2,ARETE VIDE)=L'ARETE VIDE QUI PRECEDE
C          NOSOAR(3,ARETE VIDE)=L'ARETE VIDE QUI SUIT
C MOARTR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE DU TABLEAU NOARTR
C MXARTR : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES
C NOARTR : LES 3 ARETES DES TRIANGLES +-ARETE1, +-ARETE2, +-ARETE3
C          ARETE1 = 0 SI TRIANGLE VIDE => ARETE2 = TRIANGLE VIDE SUIVANT
C
C SORTIES:
C --------
C NBTRIA : NOMBRE DE TRIANGLES INTERNES AU DOMAINE
C QUAMOY : QUALITE MOYENNE  DES TRIANGLES ACTUELS
C QUAMIN : QUALITE MINIMALE DES TRIANGLES ACTUELS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       MARS 2007
C....................................................................012
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
C     TRACE OU NON DES TRIANGLES GENERES DANS LA TRIANGULATION
      DOUBLE PRECISION  PXYD(3,*), D, SURTD2
      DOUBLE PRECISION  AIRE
      REAL              COMXMI(3,2)
      INTEGER           NOSOAR(MOSOAR,MXSOAR)
      INTEGER           NOARTR(MOARTR,MXARTR)
      INTEGER           NOSOTR(3)
      EQUIVALENCE      (NOSOTR(1),NS1), (NOSOTR(2),NS2),
     %                 (NOSOTR(3),NS3)
C
      IF( TRATRI ) THEN
C
C        LES TRACES SONT DEMANDES
         CALL EFFACE
C
C        LE CADRE OBJET GLOBAL EN UNITES UTILISATEUR
         XE = COMXMI(1,2) - COMXMI(1,1)
         YE = COMXMI(2,2) - COMXMI(2,1)
         CALL ISOFENETRE( COMXMI(1,1)-XE/40, COMXMI(1,2)+XE/40,
     %                    COMXMI(2,1)-YE/40, COMXMI(2,2)+YE/40 )
C
      ENDIF
C
      QUAMOY = 0
      QUAMIN = 2.0
      NBTRAN = 0
      NBTRIA = 0
      AIRE   = 0D0
C
      DO 20 NT=1,MXARTR
         IF( NOARTR(1,NT) .NE. 0 ) THEN
C
C           UN TRIANGLE OCCUPE DE PLUS
            NBTRIA = NBTRIA + 1
C
C           LE NUMERO DES 3 SOMMETS DU TRIANGLE NT
            CALL NUSOTR( NT, MOSOAR, NOSOAR, MOARTR, NOARTR,
     %                   NOSOTR )
C           NOSOTR(1:3) EST EN EQUIVALENCE AVEC NS1, NS2, NS3
C
C           LA QUALITE DU TRIANGLE NS1 NS2 NS3
            CALL QUTR2D( PXYD(1,NS1), PXYD(1,NS2), PXYD(1,NS3), QUALIT )
C
C           LA QUALITE MOYENNE
            QUAMOY = QUAMOY + QUALIT
C
C           LA QUALITE MINIMALE
            QUAMIN = MIN( QUAMIN, QUALIT )
C
C           TRACE DU TRIANGLE NT
            CALL MTTRTR( PXYD, NT, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                   NCTRIA, NCARET )
C
C           AIRE SIGNEE DU TRIANGLE NT
            D = SURTD2( PXYD(1,NS1), PXYD(1,NS2), PXYD(1,NS3) )
C
            IF( D .LE. 0 ) THEN
C
C              UN TRIANGLE D'AIRE NEGATIVE DE PLUS
               NBTRAN = NBTRAN + 1
               WRITE(IMPRIM,*) 'SP TETRMA: TRIANGLE ',NT,' ST:',
     %                          NS1,NS2,NS3,' D AIRE ',D,'<=0'
C              TRACE DU TRIANGLE NT EN ROUGE BORDE D'ARETES NOIRES
               CALL XVEPAISSEUR( 3 )
               CALL MTTRTR( PXYD, NT, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                      NCROUG, NCNOIR )
               CALL XVEPAISSEUR( 1 )
C
            ENDIF
C
C           AIRE DES TRIANGLES ACTUELS
            AIRE = AIRE + ABS( D )
         ENDIF
C
 20   CONTINUE
C
C     LES AFFICHAGES
      QUAMOY = QUAMOY / NBTRIA
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10020) QUAMOY, QUAMIN, NBTRIA
      ELSE
         WRITE(IMPRIM,20020) QUAMOY, QUAMIN, NBTRIA
      ENDIF
10020 FORMAT(' TETRMA: QUALITE MOYENNE=',F6.3,
     %       ' MINIMALE=',F6.3,' pour',I8,' TRIANGLES')
20020 FORMAT(' TETRMA: QUALITY AVERAGE=',F6.3,
     %       ' MINIMUM=',F6.3,' for',I8,' TRIANGLES')
C
      IF( NBTRAN .GT. 0 ) THEN
      IF( LANGAG .EQ. 0 ) THEN
      WRITE(IMPRIM,*) 'PROBLEME: NOMBRE DE TRIANGLES D''AIRE NEGATIVE='
     %,NBTRAN
      WRITE(IMPRIM,*) 'SURFACE TOTALE DES TRIANGLES =',AIRE
      ELSE
      WRITE(IMPRIM,*) 'PROBLEM: NUMBER of TRIANGLES with NEGATIVE AREA='
     %,NBTRAN
      WRITE(IMPRIM,*) 'TOTAL SURFACE of TRIANGLES =',AIRE
      ENDIF
      ENDIF
C
      IF( TRATRI ) THEN
         CALL XVEPAISSEUR( 1 )
         CALL CLICSO
      ENDIF
C
      RETURN
      END
