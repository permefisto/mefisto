      SUBROUTINE OTVOSO( NUOTIN, NOSMMT, LARBRO, LARBRT, NUOTSO, NBOTSO)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER L'OT OPPOSE PAR LE SOMMET NOSMMT DE L'OT NUOTIN
C -----
C
C ENTREES:
C --------
C NUOT   : NUMERO DE L'OT, >0 SI DANS LARBRO,  <0 SI DANS LARBRT
C NOSMMT : 1 A 4 POUR UN TETRAEDRE ( NUOT<0 )
C          1 A 6 POUR UN OCTAEDRE  ( NUOT>0 )
C LARBRO : ARBRE-14 DES OCTAEDRES ( FOND DE LA TETRAEDRISATION )
C      LARBRO(-1:20,1) : RACINE DE L'ARBRE (OCTAEDRE SANS PERE)
C      LARBRO(-1,J) : NO DU PERE DE L'OCTAEDRE J DANS UN DES 2 ARBRES
C                     >0 => DANS LARBRO
C                     <0 => DANS LARBRT
C      LARBRO(0,J)  : 1 A 14 NO DE FILS DE L'OCTAEDRE J POUR SON PERE
C                     + 100 * NO TYPE DE L'OT J
C                     NO TYPE DE L'OT : 0 SI OCTAEDRE
C                                       1 SI TETRAEDRE T RONDE (T1)
C                                       2 SI TETRAEDRE T       (T2)
C   SI LARBRO(0,J)>0 ALORS J EST UN OCTAEDRE OCCUPE
C      SI LARBRO(1,.)>0 ALORS
C         LARBRO(1:14,J): NO (>0 OU <0) LARBRO DES 14 SOUS-OCTA-TETRAEDRES
C         REMARQUE : TOUT FILS 1 EST UN OCTAEDRE => NO OT >0 DANS LARBRO
C                    DONC <0 => UNE FEUILLE
C      SINON
C         LARBRO(1:14,J):-NO PTXYZD DES 0 A 14 POINTS INTERNES DE L'OCTA J
C                         0  SI PAS DE POINT
C                        ( J EST ALORS UNE FEUILLE DE L'ARBRE )
C
C      LARBRO(15:20,J) : NO PTXYZD DES 6 SOMMETS DE L'OCTAEDRE J
C   SINON
C      LARBRO(0,J): -ADRESSE DANS LARBRO DE L'OCTAEDRE VIDE SUIVANT
C
C LARBRT : ARBRE-5 DES TETRAEDRES ( FOND DE LA TETRAEDRISATION )
C      LARBRT(0,0) : NO DU 1-ER TETRAEDRE VIDE DANS LARBRT
C      LARBRT(1,0) : MAXIMUM DU 1-ER INDICE DE LARBRT (ICI -1:9)
C      LARBRT(2,0) : MAXIMUM DECLARE DU 2-EME INDICE DE LARBRT
C                     (ICI = MXARBT)
C
C      LARBRT(-1,J) : NO DU PERE DU TETRAEDRE J DANS UN DES 2 ARBRES
C                     >0 => DANS LARBRO
C                     <0 => DANS LARBRT
C      LARBRT(0,J) : 1 A 5 NO DE FILS DU TETRAEDRE J POUR SON PERE
C                    + 100 * NO TYPE DE L'OT J
C                     NO TYPE DE L'OT : 0 SI OCTAEDRE
C                                       1 SI TETRAEDRE T RONDE (T1)
C                                       2 SI TETRAEDRE T       (T2)
C
C   SI LARBRT(0,J)>0 ALORS J EST UN TETRAEDRE OCCUPE
C      SI LARBRT(1,J)>0 ALORS
C         LARBRT(1:5,J): NO (>0 OU <0) LARBRT DES 5 SOUS-OCTA-TETRAEDRES
C         REMARQUE : TOUT FILS 1 EST UN OCTAEDRE => NO OT >0 DANS LARBRO
C                    DONC <0 => UNE FEUILLE
C      SINON
C         LARBRT(1:5,J):-NO PTXYZD DES 0 A 5 POINTS INTERNES AU TETRA J
C                         0  SI PAS DE POINT
C                        ( J EST ALORS UNE FEUILLE DU ARBRE )
C
C      LARBRT(6:9,J) : NO PTXYZD DES 4 SOMMETS DU TETRAEDRE J
C   SINON
C      LARBRT(0,J): ADRESSE DANS LARBRT DU TETRAEDRE VIDE SUIVANT
C
C NUOTPT : NUMERO D'OT (>0 SI LARBRO, <0 SI LARBRT) DE CHAQUE POINT PTXYZD
C          SI SOMMET D'OT, C'EST LE NUMERO D'OT DU PERE
C          SI POINT INTERNE OU FRONTALIER, C'EST LE NUMERO D'OT LE CONTENANT
C
C SORTIES:
C --------
C NUOTSO : NUMERO DE L'OT OPPOSE PAR LE SOMMET NOSMMT
C          =0 SI PAS D'OT OPPOSE (RACINE , ... )
C NBOTSO : =0 SI NUOT ET NUOTSO ONT MEME TAILLE
C          >0 SI NUOT EST 8**NBOTSO FOIS PLUS PETIT QUE NUOTSO
C          -1 SI SATURATION DE LA PILE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1992
C2345X7..............................................................012
      PARAMETER        (MXPILE=64)
      COMMON / UNITES / LECTEU , IMPRIM , INTERA , NUNITE(29)
      INTEGER           LARBRO(-1:20,0:*),
     &                  LARBRT(-1:9 ,0:*),
     &                  LAPILE(MXPILE)
      include"./incl/otvoso.inc"
C
C     INITIALISATION DE LA PILE POUR LA DESCENTE SUR L'OT OPPOSE
      NBOTSO = 0
      NUOT   = NUOTIN
C
C     LE NOMBRE D'OT VOISIN PAR UNE ARETE
      NBOTVO = 0
C
C     TANT QU'IL EXISTE UN OT PERE
 10   IF( NUOT .EQ. 1 ) THEN
C        RACINE ATTEINTE => PAS D'OT OPPOSE
         NUOTSO = 0
         NBOTSO = NBOTSO - 1
         RETURN
      ENDIF
C
      IF( NUOT .GT. 0 ) THEN
C        OCTAEDRE
C        SON NUMERO DE FILS POUR SON PERE
         NUFIOT = LARBRO(0,NUOT)
C        L'EVENTUEL OT DU PERE
         NUOTPE = LARBRO(-1,NUOT)
      ELSE
C        TETRAEDRE
C        SON NUMERO DE FILS  POUR SON PERE
         NUFIOT = MOD( LARBRT(0,-NUOT) , 100 )
C        L'EVENTUEL OT DU PERE
         NUOTPE = LARBRT(-1,-NUOT)
      ENDIF
C
C     LE TYPE DE L'OT DU PERE
      IF( NUOTPE .GT. 0 ) THEN
         NUTYPE = 0
      ELSE
         NUTYPE = LARBRT(0,-NUOTPE) / 100
      ENDIF
C
      GOTO( 90, 100, 200 ) , NUTYPE+1
C
C     PERE OCTAEDRE
C     =============
 90   IF( NUFIOT .LE. 6 ) THEN
C        UN FILS OCTAEDRE  DANS UN PERE OCTAEDRE
C        LE NUMERO DU FILS DANS LE PERE OPPOSE
         NVFIPE = LSOCOC( NOSMMT, NUFIOT )
C        L'EVENTUEL NUMERO D'ARETE DU PERE POUR LEQUEL
C        LE SOMMET NOSMMT EN EST LE MILIEU
         NOARET = LAOCOC( NOSMMT, NUFIOT )
      ELSE
C        UN FILS TETRAEDRE DANS UN PERE OCTAEDRE
C        LE NUMERO DU FILS DANS LE PERE OPPOSE
         NVFIPE = LSOCTE( NOSMMT, NUFIOT )
C        L'EVENTUEL NUMERO D'ARETE DU PERE POUR LEQUEL
C        LE SOMMET NOSMMT EN EST LE MILIEU
         NOARET = LAOCTE( NOSMMT, NUFIOT )
      ENDIF
      GOTO 500
C
C     PERE TETRAEDRE DE TYPE 1
C     ========================
 100  IF( NUFIOT .EQ. 1 ) THEN
C        UN FILS OCTAEDRE  DANS UN PERE TETRAEDRE 1
C        LE NUMERO DU FILS DANS LE PERE OPPOSE
         NVFIPE = LST1OC( NOSMMT )
C        L'EVENTUEL NUMERO D'ARETE DU PERE POUR LEQUEL
C        LE SOMMET NOSMMT EN EST LE MILIEU
         NOARET = LAT1OC( NOSMMT )
      ELSE
C        UN FILS TETRAEDRE DANS UN PERE TETRAEDRE 1
C        LE NUMERO DU FILS DANS LE PERE OPPOSE
         NVFIPE = LST1TE( NOSMMT, NUFIOT )
C        L'EVENTUEL NUMERO D'ARETE DU PERE POUR LEQUEL
C        LE SOMMET NOSMMT EN EST LE MILIEU
         NOARET = LATETE( NOSMMT, NUFIOT )
      ENDIF
      GOTO 500
C
C     PERE TETRAEDRE DE TYPE 2
C     ========================
 200  IF( NUFIOT .EQ. 1 ) THEN
C        UN FILS OCTAEDRE  DANS UN PERE TETRAEDRE 2
C        LE NUMERO DU FILS DANS LE PERE OPPOSE
         NVFIPE = LST2OC( NOSMMT )
C        L'EVENTUEL NUMERO D'ARETE DU PERE POUR LEQUEL
C        LE SOMMET NOSMMT EN EST LE MILIEU
         NOARET = LAT2OC( NOSMMT )
      ELSE
C        UN FILS TETRAEDRE DANS UN PERE TETRAEDRE 2
C        LE NUMERO DU FILS DANS LE PERE OPPOSE
         NVFIPE = LST2TE( NOSMMT, NUFIOT )
C        L'EVENTUEL NUMERO D'ARETE DU PERE POUR LEQUEL
C        LE SOMMET NOSMMT EN EST LE MILIEU
         NOARET = LATETE( NOSMMT, NUFIOT )
      ENDIF
C
C     TRAITEMENT DE L'OT OPPOSE
C     =========================
 500  IF( NVFIPE .GT. 0 ) THEN
C
C        L'OT OPPOSE N'EST PAS UN DE SES FRERES
C        CE NUMERO NVFIPE EST EMPILE
         IF( NBOTSO .GE. MXPILE ) THEN
C           PILE SATUREE
            WRITE(IMPRIM,*) 'SP OTVOSO: PILE SATUREE AUGMENTER MXPILE'
            CALL XVPAUSE
            NUOTSO = 0
            NBOTSO = -1
            RETURN
         ENDIF
         NBOTSO = NBOTSO + 1
         LAPILE( NBOTSO ) = NVFIPE
C
C        TRAITEMENT DU PERE SELON UNE ARETE OU LE MEME SOMMET ?
         IF( NOARET .EQ. 0 ) THEN
C           SELON LE MEME SOMMET DU PERE
            NUOT = NUOTPE
            GOTO 10
         ELSE
C           SELON L'ARETE NOARET DANS LE PERE
            CALL OTVOAR( NUOTPE, NOARET, LARBRO, LARBRT, NUOT, NBOTVO )
            IF( NUOT .EQ. 0  ) THEN
C              PAS DE VOISIN
               NUOTSO = 0
               NBOTSO = 0
               RETURN
            ELSE
               GOTO 700
            ENDIF
         ENDIF
      ENDIF
C
C     IL EXISTE UN OT FRERE DE NUOT PAR LE SOMMET NOSMMT
C     ================================================
C     LE NUMERO D'OT DU PERE DE L'OT EST NUOTPE
C     LE NUMERO D'OT DU FRERE -NVFIPE EST
      NVFIPE = -NVFIPE
      IF( NUOTPE .GT. 0 ) THEN
C        PERE OCTAEDRE
         NUOT = LARBRO( NVFIPE, NUOTPE )
      ELSE
C        PERE TETRAEDRE
         NUOT = LARBRT( MOD(NVFIPE,100), -NUOTPE )
      ENDIF
C
C     DESCENTE DE LA PILE DES OPPOSES JUSQU'A LA FEUILLE
C     ==================================================
C     LE FILS OPPOSE PAR LE SOMMET NOSMMT
 700  IF( NUOT .GT. 0 ) THEN
C        OCTAEDRE
         IF( LARBRO(1,NUOT) .LE. 0 ) THEN
C           NUOT EST UNE FEUILLE ET L'OT OPPOSE
            GOTO 9000
         ELSE
C           NUOT A DES FILS
            IF( NBOTSO .GT. 0 ) THEN
C              LA PILE N'EST PAS VIDE
               NUFILS = LAPILE( NBOTSO )
               NBOTSO = NBOTSO - 1
               NUOT   = LARBRO( NUFILS, NUOT )
               GOTO 700
            ENDIF
C           LA PILE EST VIDE => NUOT EST L'OT OPPOSE
            GOTO 9000
         ENDIF
      ELSE
C        TETRAEDRE
         IF( LARBRT(1,-NUOT) .LE. 0 ) THEN
C           NUOT EST UNE FEUILLE ET L'OT OPPOSE
            GOTO 9000
         ELSE
C           NUOT A DES FILS
            IF( NBOTSO .GT. 0 ) THEN
C              LA PILE N'EST PAS VIDE
               NUFILS = MOD( LAPILE(NBOTSO), 100 )
               NBOTSO = NBOTSO - 1
               NUOT   = LARBRT( NUFILS, -NUOT )
               GOTO 700
            ENDIF
C           LA PILE EST VIDE => NUOT EST L'OT OPPOSE
            GOTO 9000
         ENDIF
      ENDIF
C
C     NUOT EST L'OT OPPOSE PAR LE SOMMET
 9000 NUOTSO = NUOT
      NBOTSO = NBOTSO + NBOTVO
      END
