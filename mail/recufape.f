      SUBROUTINE RECUFAPE( NUVOLU, KNMVOL, COANPL, QUAMINEX, QTEAME,
     %                     MXSOMM, NBSOMM, PTXYZD, NPSOFR,   LISPOI,
     %                     HEXAPAVE,NBIPAV,ECHPAV, N1SPAVE,  NOPTSUIV,
     %                     MXTETR, N1TEVI, N1TETS, NOTETR,   NUDTETR,
     %                     MXTE1S, MNTE1S, NOTE1S,
     %                     MXTRCF, NOTRCF, NOSTCF, NOSTIS,
     %                     MXARCF, N1ARCF, NOARCF,
     %                     MXETOI, NFETOI, NAETOI, DISTMIN,
     %                     INFACO, MXFACO, NBFACO, LEFACO, NO0FAR,
     %                     N1FASC, IVOLTE, NVOLTE,
     %                     MXFAPE, NBFAPE, NOFAPE,
     %                     MXPILE, MNPILE, LAPILE,
     %                     NBABAN, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RECUPERER DANS LA TETRAEDRISATION LES FACES PERDUES
C -----    DE LA FRONTIERE ET DES INTERFACES DE LA PARTITION DES VOLUMES

C ENTREES:
C --------
C NUVOLU : NUMERO DU VOLUME PARTITION DANS LE LEXIQUE DES VOLUMES
C KNMVOL : NOM DU VOLUME A TETRAEDRISER
C COANPL : SEUIL DU COSINUS DE L'ANGLE FORME PAR LES NORMALES AUX
C          2 FACES ET AU DESSUS DUQUEL LES FACES
C          SONT CONSIDEREES COPLANAIRES
C          ( 0.9848   => 10   DEGRES )
C          ( 0.99     => 8.11 DEGRES )
C          ( 0.9962   => 5    DEGRES )
C          ( 0.99756  => 4    DEGRES )
C          ( 0.99863  => 3    DEGRES )
C          ( 0.999    => 2.56 DEGRES )
C          ( 0.9999   => 0.8  DEGRES )
C QUAMINEX: QUALITE MINIMALE EXIGEE POUR CREER UN TETRAEDRE
C QTEAME  : QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C           AMELIORATION DE LA QUALITE DES TETRAEDRES EST DEMANDEE

C ARETGR  : LONGUEUR DE L'ARETE SOUHAITEE POUR L'ENSEMBLE DES TETRAEDRES
C NOFOTI  : NUMERO DE LA FONCTION UTILISATEUR 'TAILLE_IDEALE', 0 SINON

C MXSOMM  : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C NBSOMM  : NOMBRE DE SOMMETS INITIAUX
C PTXYZD  : TABLEAU DES COORDONNEES DES POINTS
C           PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR  : =  0 SI LE POINT EST AJOUTE NON SUR LA SURFACE DE L'OBJET
C           =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C           =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C           =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C           = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                          LA NUMEROTATION DES SOMMETS DE LA SURFACE
C           = -4 SI LE POINT EST SOMMET D'OT NON TROP PROCHE PT OU FACE
C           = -1 SI LE POINT EST SOMMET D'OT TROP PROCHE PT OU FACE
C           = -3 SI LE POINT EST SOMMET D'OT REFUSE DANS LA TETRAEDRISATION
C           = -NPSOFR(I) SI POINT DEPLACE SUR LA SURFACE
C              DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE
C LISPOI  : LISPOI(I)=I ou 0 EN ENTREE POUR PERMETTRE LA PERMUTATION
C           DES POINTS LORSQUE DES TETRAEDRES DEGENERES SONT RENCONTRES

C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I
C ECHPAV  : ECHELLE DANS LA DIRECTION I
C N1SPAVE : NO DU 1-ER SOMMET DANS PTXYZD DU PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES

C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: NU NOTETR DU DERNIER TETRAEDRE ACTIF
C MXTE1S : NOMBRE MAXIMAL D'ENTIERS DU TABLEAU NOTE1S
C MNTE1S : ADRESSE MCN DU TABLEAU NOTE1S
C NOTE1S : TABLEAU 1:MXTE1S AUXILIAIRE D'ENTIERS
C          GENERALEMENT LES NO DES TETRAEDRES D'UN SOMMET

C MXTRCF : NOMBRE MAXIMAL DECLARABLE D'ARETES OU TRIANGLES
C          OU SOMMETS DANS L'ETOILE
C NOTRCF : TABLEAU DU NUMERO DANS LEFACO DES TRIANGLES PERDUS DU CF
C NOSTCF : TABLEAU DU NUMERO DANS PTXYZD DES SOMMETS DU CF
C NOSTIS : NO DES SOMMETS ISOLES N'APPARTENANT PAS AU CONTOUR FERME
C          TABLEAU 1:MXSOMM AUXILIAIRE D'ENTIERS

C MXARCF : MAXIMUM D'ARETES DECLARABLES DANS N1ARCF et NOARCF
C N1ARCF : TABLEAU (0:MXARCF) AUXILIAIRE
C          POINTE SUR LE DEBUT DES ARETES DE CHAQUE LIGNE FERMEE DU CF
C          0 POUR LES PLACES VIDES
C NOARCF : 1:NUMERO DU SOMMET DE L'ARETE SIMPLE PERIPHERIQUE DU CF
C          2:NUMERO DANS NOARCF DE L'ARETE SIMPLE SUIVANTE DU CF
C          3:NUMERO DANS LEFACO DU TRIANGLE ADJACENT OPPOSE AU
C            TRIANGLE NOTRCF DE CETTE ARETE COMMUNE

C MXETOI : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LE CHAINAGE NFETOI
C NFETOI : au debut VERSION1 FACES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR AYANT CETTE FACE
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DE CETTE FACE DANS LEFACO, 0 SI PAS DANS LEFACO
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C          ensuite VERSION2
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST DIRIGE VERS L'INTERIEUR DE L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C DISTMIN: TABLEAU 1:MXETOI DOUBLE PRECISION

C INFACO : = 0 PAS DE TABLEAU LEFACO NI DE CONSERVATION DES
C              FACES FRONTIERE ( NO DE VOLUME CONNU PAR NVOLTE )
C          = 1 EXISTENCE DU TABLEAU LEFACO ET CONSERVATION DES
C              FACES DE LA FRONTIERE
C MXFACO : MAX DE FACES DECLARABLES DANS LEFACO
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1 < SOMMET 2 < SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON C-A-D AUCUN TETRAEDRE CONTIENT CETTE FACE
C              <0  SINON C-A-D AUCUN TETRAEDRE CONTIENT CETTE FACE
C                  MAIS EN PLUS ELLE EST MARQUEE ABANDONNEE MOMENTANEMENT
cccC          12: = NO FACEOC DE 1 A NBFACES D'OpenCascade
C N1FASC : N1FASC(NS)=NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS

C IVOLTE : 0 PAS       DE TABLEAU NVOLTE A L'APPEL
C          1 EXISTENCE DU TABLEAU NVOLTE A L'APPEL
C NVOLTE : >0 NUMERO DU VOLUME DE 1 A NBVOPA DE CHAQUE TETRAEDRE
C          -1 SI TETRAEDRE INACTIF ou SUPPRIME

C MXFAPE : NOMBRE MAXIMAL DE FACES PERDUES DE LEFACO
C NBFAPE : NOMBRE DE FACES PERDUES DE LEFACO
C NOFAPE : >0  NUMERO LEFACO DES FACES DANS AUCUN TETRAEDRE NOTETR
C          <0 -NUMERO LEFACO DES FACES DANS AUCUN TETRAEDRE NOTETR
C              MAIS EN PLUS ELLE EST MARQUEE TRAITEE

C MXPILE : NOMBRE MAXIMAL D'ELEMENTS DU TABLEAU AUXILIAIRE LAPILE
C MNPILE : ADRESSE MCN DU TABLEAU LAPILE
C LAPILE : LAPILE(2,MXPILE) PILE D'ENTIERS TABLEAU AUXILIAIRE

C SORTIES:
C --------
C NBSOMM : NOMBRE DE SOMMETS FINAUX DANS PTXYZD
C NBABAN : NOMBRE D'ABANDONS DE TRAITEMENT DES FACES PERDUES
C IERR   : =0 SI PAS D'ERREUR
C          =1 ETOILE AVEC MOINS DE 4 FACES
C          =2 SATURATION D'UN TABLEAU
C          =3 3 SOMMETS NON DANS UN TETRAEDRE
C          =9 VOLUME DE L'ETOILE AVANT et APRES DIFFERENTS
C          =11 UNE FACE PERDUE NON RECUPEREE. ABANDON DE LA RECUPERATION
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint PIERRE du PERRAY         Decembre 2019
C2345X7..............................................................012
      PARAMETER        (MXPTIN=4096, MXTECF=16384, MXSSET=1024 )

      include"./incl/gsmenu.inc"

      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE, TRACTE0
      CHARACTER*24      KNMVOL
      CHARACTER*192     KTITRE
      DOUBLE PRECISION  PTXYZD(4,MXSOMM), DISTMIN(MXETOI)
      DOUBLE PRECISION  HEXAPAVE(3,2), ECHPAV(3)
      INTEGER           NBIPAV(3), N1SPAVE(0:*), NOPTSUIV(1:*)
      DOUBLE PRECISION  PTINTERS(3,MXPTIN),
     %                  SUMIMX, VOLET0, VOLET1, VMOYEN,
     %                  DATAN, PIS4
      DOUBLE PRECISION  ARMIN, ARMAX, SURFTR(4), VOLUTE
      INTEGER           NOTETR(8,MXTETR), N1TETS(MXSOMM),
     %                  LEFACO(11,0:MXFACO), NVOLTE(MXTETR),
     %                  NPSOFR(1:MXSOMM), N1FASC(1:MXSOMM),
     %                  NOFAPE(1:MXFAPE),
     %                  N1ARCF(0:MXARCF), NOARCF(1:3,1:MXARCF),
     %                  NOTE1S(MXTE1S),
     %                  NOTRCF(MXTRCF), NOSTCF(MXTRCF), NOSTIS(MXSOMM),
     %                  NOTECF(MXTECF), NOTECFPR(MXTECF),
     %                  NAETOI(4,MXETOI), NFETOI(5,MXETOI),
     %                  NO0FAR(3,MXETOI), LAPILE(2,MXPILE),
     %                  N1SSET(MXSSET), LISPOI(0:MXSOMM)

      INTEGER           NOSOFATE(3,4), NOSOTR(3), NOSOTR2(3)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

C     TRACTE = FAUX ==> PAS DE TRACE
C     TRACTE = VRAI ==> TRACE DE L'ETOILE
      TRACTE0 = TRACTE
      LORBITE = 1

C     NOMBRE DE SOMMETS INITIAUX
      NBSOM0 = NBSOMM
      NOSOM1 = NBSOMM
      NOSOM2 = NBSOMM

C     NOMBRE D'ABANDONS DU TRAITEMENT D'UNE FACE PERDUE
      NBABAN = 0
C     PAS DE TABLEAU NVOLTE
      IVOLTE = 0

C     CONSTANTE de CONVERSION RADIANS<->DEGRES Pi/4
      PIS4 = DATAN( 1D0 )

      NBITERFP = 0

C     UN PAS DE PLUS POUR RETROUVER LES FACES PERDUES DE LEFACO
C     ---------------------------------------------------------
 1    NBITERFP = NBITERFP + 1

      IF( NBITERFP .GE. 2 ) THEN

C        BARYCENTRAGE/2 DES SOMMETS D'UNE TETRAEDRISATION
C        EXCEPTES LES SOMMETS DE NPSOFR NON NUL AVEC
C        RECHERCHE DE LA QUALITE MINIMALE MAXIMISEE
C        ================================================
         NBITERBA = 3
         CALL BASTTETR( NBITERBA, NBSOMM, PTXYZD, NPSOFR,
     %                  MXTETR,   NOTETR, N1TETS, MXTE1S, NOTE1S )

      ENDIF


C     POUR TOUTES LES FACES PERDUES de LEFACO: 
C     TENTATIVE d'ECHANGER 2Faces Perdues adjacentes -> 2Faces Recuperees?
C     POUR CELA EXISTE T IL UNE AUTRE FACE ADJACENTE PERDUE DE LA FACE
C     PERDUE TELLE QUE LE QUADRILATERE AINSI FORME A SA SECONDE DIAGONALE
C     COMME ARETE DE LA TETRAEDRISATION
C     SI OUI: ECHANGE DES 2 DIAGONALES ET RECUPERATION DE 2 FACES PERDUES
C     ====================================================================
      CALL VD2F2F( COANPL, PTXYZD, N1TETS, NOTETR,
     %             MXFACO, LEFACO, N1FASC, MXTE1S, NOTE1S, NB2F2F )


C     RECHERCHE DES NBFAPE FACES PERDUES I.E. NON FACE D'UN TETRAEDRE
C     MISE A JOUR DU NO NOTETR DE TETRAEDRE CONTENANT UNE FACE LEFACO
C     TRACE DES NBPAPE FACES LEFACO PERDUES
C     ===============================================================
ccc      tracte = .true.
      CALL NTLEFACO( MXSOMM, NBSOMM, PTXYZD, MXTETR, NOTETR, N1TETS,
     %               N1FASC, MXFACO, LEFACO,
     %               MXFAPE, NBFAPE, NOFAPE, N1TEVI, NUDTETR )
ccc      tracte = tracte0
      IF( NBFAPE .LE. 0 ) GOTO 9990


C     TRACE DES FACES PERDUES ET TETRAEDRES des SOMMETS des FACES PERDUES
C     -------------------------------------------------------------------
      KTITRE='         FACES PERDUES INITIALES et TETRAEDRES de SOMMETS 
     %des FACES PERDUES du VOLUME ' // KNMVOL
      WRITE( KTITRE(1:8), '(I8)' ) NBFAPE
      CALL TRFPTEST( KTITRE, PTXYZD, NOSOM1, NOSOM2,
     %               NBFAPE, NOFAPE, MXFACO, LEFACO,
     %               MXTE1S, NOTE1S, NOTETR, N1TETS  )


C     RECHERCHE DE NFLPER LA PREMIERE FACE PERDUE LEFACO POUR RECUPERER
C     LES TRIANGLES DES SURFACES DE LA FRONTIERE
C     -----------------------------------------------------------------
      MIL2NO  =-1
      NUFP    = 0
 2    NUESSAI = 0

 4    NBPMIL  = 0
      NB2NOR  = 0
      NFLPER  = 0
      NBSOMM0 = NBSOMM
      NBCF    = 0
      NBTECF0 = 0
      NBTECF  = 0
      NBTECFPR= 0
      NBTECR  = 0
      NBPTIN  = 0
      NBSTIS  = 0
      NFTRAV  = 0
      NCTRAV  = 0
      NBSSET  = 0
      IERR    = 0

ccc      PRINT*
ccc      PRINT*,'recufape: ',KNMVOL,NBFAPE,' Faces Perdues No ESSAI=',
ccc     %        NUESSAI

C     LA FACE PERDUE LEFACO A TRAITER EST NFLPER APPARTENANT
C     A AUCUN TETRAEDRE
 5    IF( NUFP .GE. NBFAPE ) THEN
C        IL N'Y A PLUS DE FACES PERDUES NON ABANDONNEES
         GOTO 9990
      ENDIF
      NUFP   = NUFP + 1
      NFLPER = NOFAPE( NUFP )
      IF( NFLPER .LE. 0 ) GOTO 5

C     LA FACE PERDUE EST ELLE VIDE?
      IF( LEFACO( 1, NFLPER ) .EQ. 0 ) GOTO 5

C     NON: LA FACE NFLPER DE LEFACO EST ELLE VRAIMENT PERDUE?
C     LE NO NOTETR DU TETRAEDRE DE FACE NFLPER DANS LEFACO
      NTE = LEFACO( 11, NFLPER )
      IF( NTE .GT. 0 ) THEN
C        LE TETRAEDRE NTE A T IL VRAIMENT POUR FACE LA FACE NFLPER?
         CALL NO1F1T( LEFACO(1,NFLPER), NOTETR(1,NTE), NF )
         IF( NF .GT. 0 ) THEN
C           NON: LA FACE NFLPER APPARTIENT A NTE et
C                CETTE FACE N'EST PAS PERDUE
            NOFAPE( NUFP ) = 0
            GOTO 2

ccc         ELSE
cccC           NTE N'A PAS LA FACE NFLPER DE LEFACO
cccC           -> NFLPER SANS TETRAEDRE EST CONSIDEREE PERDUE
ccc            LEFACO( 11, NFLPER ) = 0    ce PEUT ETRE -1 ...

         ENDIF
      ENDIF

C     ==========================================================
C     AIGUILLAGE DES ESSAIS POUR RETROUVER LA FACE PERDUE NFLPER
C     ==========================================================
 10   NUESSAI = NUESSAI + 1

C     FORMATION DU PREMIER CONTOUR FERME CF FORME DES TRIANGLES
C     LEFACO PERDUS ADJACENTS A PARTIR DE LA FACE PERDUE NFLPER
C     ---------------------------------------------------------
C     COANPL = -1.1  POUR EVITER LA RECHERCHE DE COPLANEARITE
      CALL FADUCF1( -1.1,   PTXYZD, N1TETS, NOTETR,
     %              NFLPER, NBFAPE, NOFAPE, MXFACO, LEFACO, NO0FAR,
     %              MXTE1S, NOTE1S,
     %              MXTRCF, NBTRCF, NOTRCF, IERR )

C     A PARTIR DES NBTRCF FACES (LEFACO ou NO0FAR) DU CF
C     CONSTRUCTION DES SOMMETS DE LA FRONTIERE ET INTERNES AU CF
C     DES FACES PERDUES ADJACENTES
      CALL ARDUCF( PTXYZD, NBFAPE, NOFAPE, MXFACO, LEFACO, NO0FAR,
     %             MXETOI, NAETOI, MXTRCF, NBTRCF, NOTRCF,
     %             MXARCF, NBCF,   N1ARCF, NOARCF, NBARCF,
     %             MXTRCF, NBSTCF, NOSTCF,
     %             MXSOMM, NBSTIS, NOSTIS, IERR )

      IF( NBTRCF .GT. 8 .OR. NBSTIS .GT. 1 ) THEN

C        FACE PERDUE AVEC TROP DE FACES PERDUES ADJACENTES
C        -------------------------------------------------
C        POUR VOIR LA CONFIGURATION DES FACES PERDUES
C        TRACE DES FACES TRIANGULAIRES DU CF
 7       KTITRE='         FACES PERDUES:NBTRCF=       NBSTCF=       NBST
     %IS=       '  // KNMVOL
         WRITE(KTITRE(1:8),  '(I8)') NBFAPE
         WRITE(KTITRE(31:36),'(I6)') NBTRCF
         WRITE(KTITRE(45:50),'(I6)') NBSTCF
         WRITE(KTITRE(59:64),'(I6)') NBSTIS
         CALL SANSDBL( KTITRE, NC )
         KTITRE = KTITRE(1:NC)//' --> Ajout du MILIEU de LONGUES ARETES'
         CALL SANSDBL( KTITRE, NC )
         WRITE( KTITRE(NC+1:NC+8),'(I8)') NFLPER
         CALL SANSDBL( KTITRE, NC )
         PRINT*, KTITRE(1:NC)
         CALL TRDUCF( KTITRE(1:NC), PTXYZD, NBTRCF,NOTRCF,NBSTIS,NOSTIS,
     %                LEFACO, NO0FAR )

ccc         CALL TRFPTEST( KTITRE(1:NC), PTXYZD, NOSOM1, NOSOM2,
ccc     %                  NBFAPE, NOFAPE, MXFACO, LEFACO,
ccc     %                  MXTE1S, NOTE1S, NOTETR, N1TETS  )

ccc  7       
         MIL2NO = -MIL2NO
         IF( MIL2NO .GT. 0 ) THEN

C           AJOUT DU MILIEU DE CERTAINES GRANDES ARETES PERDUES
C           AJOUT DU BARYCENTRE DES FACES PERDUES D'ARETES NON PERDUES
C           ----------------------------------------------------------
            NBSOMM0 = NBSOMM
            CALL AJOMILFP( KNMVOL, COANPL, NFLPER,
     %                     MXFACO, LEFACO, NBFACO, N1FASC,
     %                     NBFAPE, MXFAPE, NOFAPE,
     %                     NBSOMM, MXSOMM, PTXYZD, NPSOFR,
     %                     MXTRCF, NBTRCF, NOTRCF, NBSTIS, NOSTIS,
     %                     MXARCF, N1ARCF, NOARCF,
     %                     4*MXETOI, NAETOI, MXTE1S, MNTE1S, IERR )
            IF( NBSOMM0 .LT. NBSOMM ) THEN
C              LA FACE PERDUE NFLPER EST NON TRAITEE
               NUFP = NUFP - 1
               GOTO 150
            ELSE
               GOTO 7
            ENDIF

C           LES FACES PERDUES ADJACENTES SONT MARQUEES
C           ABANDONNEES MOMENTANEMENT
            NOFAPE( NUFP ) = -NFLPER
            DO 8 K = 1, NBTRCF
               NTR = ABS( NOTRCF( K ) )
               DO N = 1, NBFAPE
                  IF( ABS( NOFAPE(N) ) .EQ. NTR ) THEN
C                 LA FACE PERDUE N DE NOFAPE EST MARQUEE ABANDONNEE
                     NOFAPE( N ) = -NTR
C                 LA FACE NTR DE LEFACO EST MARQUEE SANS TETRAEDRE
C                 ET ABANDONNEE
                     LEFACO(11,NTR) = -1
                     GOTO 8
                  ENDIF
               ENDDO
 8          ENDDO
            GOTO 5

         ELSE

C           AJOUT A LA LISTE DES POINTS A TETRAEDRISER 2 POINTS SITUES
C           SUR LA NORMALE AUX FACES DU CONTOUR FERME DE LA FACE PERDUE
C           NFLPER DE LEFACO
C           -----------------------------------------------------------
            NB2NOR  = NB2NOR + 1
            IF( NB2NOR .GE. 3 ) GOTO 117
            NBSOMM0 = NBSOMM
            CALL AJ2NORFP( NB2NOR, MXSOMM, NBSOMM, PTXYZD, NPSOFR,
     %                     HEXAPAVE, NBIPAV,ECHPAV, N1SPAVE, NOPTSUIV,
     %                     NFLPER, MXFACO, LEFACO,
     %                     MXTRCF, NBTRCF, NOTRCF,
     %                     MXETOI, NAETOI, NFETOI, DISTMIN, IERR )
            IF( NBSOMM0 .LT. NBSOMM ) THEN
               GOTO 150
            ENDIF

         ENDIF
         tracte = tracte0

      ENDIF


C     ESSAIS DE RECUPERATION DE LA FACE LEFACO PERDUE NFLPER
C     ======================================================
C     LA FACE PERDUE NUFP est MARQUEE TRAITEE dans NOFAPE
      NOFAPE( NUFP ) = -NFLPER

ccc      PRINT 10009
ccc10009 FORMAT(200('+'))
      PRINT 10010, KNMVOL,NFLPER,(LEFACO(N,NFLPER),N=1,3),
     %             NBSOMM, NBTRCF, NBSTCF, NBSTIS,
     %             NUESSAI, IERR, NBABAN
10010 FORMAT(' recufape: Volume ',A24,' Retrouver la FACE LEFACO(',
     %   I9,') St:',3I8,' NBSOMM=',I8,' NBTRCF=',I4,
     %   ' NBSTCF=',I4,' NBSTIS=',I3,' No ESSAI=',I2,
     %   ' IERR=',I2,' Nb Abandon->=',I6)
ccc      DO K=1,NBTRCF
ccc         NTR = NOTRCF( K )
ccc         PRINT *,'recufape: FACE PERDUE LEFACO(',NTR,')=',
ccc     %           (LEFACO(N,NTR),N=1,11)
ccc      ENDDO
ccc      PRINT 10009
      IF( IERR .NE. 0 ) GOTO 9000

      IF( NUESSAI .LE. 4 .AND. NBTRCF .LE. 5 ) THEN
C        POUR PEU DE FACES PERDUES, L'AJOUT DE POINTS FRONTIERE EST EVITE
         GOTO 15
      ENDIF

