10260    FORMAT(' recufape: FACE RECUPEREE LEFACO(',I7,'):',
     %           I5,' tetraedres REMPLACES par',
     %           I5,' tetraedres de Qmoy=',
     %           G13.6,' Qmin=',G13.6,' et',I3,' SOMMETS AJOUTES,'
     %           I5,' FACES du CF, Reprise=',I1,
     %           ' NbSSET=',I1)

C        BILAN SUR LES FACES PERDUES DU CF RECUPEREES ou NON?
         DO 270 K = 1, NBTRCF

C           NO FACE PERDUE DU CF DANS LEFACO
            NTR = NOTRCF( K )
            IF( NTR .LE. 0 ) GOTO 270
            
C           LE TETRAEDRE CONTENANT LA FACE
            NTE = LEFACO( 11, NTR )

            IF( NTE .GT. 0 ) THEN
               IF( NOTETR(1,NTE) .GT. 0 ) THEN
C                 LA FACE NTR DE LEFACO EST RECUPEREE
                  PRINT *,'recufape: FACE     RECUPEREE LEFACO(',NTR,
     %                    ')=',(LEFACO(L,NTR),L=1,11)
                  GOTO 270
               ENDIF
            ENDIF

C           LA FACE NTR DE LEFACO N'EST PAS RECUPEREE
            LEFACO( 11, NTR ) = 0
            PRINT *,'recufape: FACE NON RECUPEREE LEFACO(',NTR,
     %              ')=',(LEFACO(L,NTR),L=1,11)

 270     ENDDO
         PRINT 10261
         PRINT *

C        TRACE DES NBTECR TETRAEDRES CREES POUR CETTE FACE NFLPER
         KTITRE='Fin recufape       TETRAEDRES CREES dans ETOILE FINALE 
     %QUALITE MOY=                QUALITE MIN=                '
         WRITE(KTITRE(14:19), '(I6)'   ) NBTECR
         WRITE(KTITRE(68:81) ,'(G14.6)') QUAMOY
         WRITE(KTITRE(99:112),'(G14.6)') QUAMIN
         CALL SANSDBL( KTITRE, L )
         CALL TRFETO4( KTITRE(1:L), PTXYZD, 0, NFETOI,
     %                 NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                 NBTECR, NOTECF, NOTETR )

      ENDIF

C     ---------------------------------------------------------------
C     FACE PERDUE RECUPEREE: PASSAGE A LA FACE LEFACO PERDUE SUIVANTE
C     ---------------------------------------------------------------
      TRACTE = TRACTE0
      GOTO 2


C     =============================================================
C     ECHEC DE LA METHODE => ABANDON FINAL DE LA FACE PERDUE NFLPER
C     =============================================================
 9000 PRINT*
      PRINT 19000
19000 FORMAT(10('_ABANDON_FACE_PERDUE__'))
      PRINT 19001,NFLPER,NBFAPE,NBTRCF,NBSOMM-NBSOMM0,NUESSAI,
     %            NBTECFPR,NBTECR,IERR
19001 FORMAT(' recufape: 9000) ABANDON DEFINITIF de la FACE PERDUE',I6,
     %       '/',I6,
     %       ' avec NBTRCF=',I3,I7,' SOMMETS AJOUTES. NUESSAI=',I2,
     %       ' NBTECFPR=',I5,' NBTECR=',I5,' IERR=',I2)
      PRINT*,'recufape: 9000) FACE PERDUE LEFACO(',NFLPER,')=',
     %       (LEFACO(L,NFLPER),L=1,11)

      IF( NBTECFPR .GT. 0 .OR. NBTECR .GT. 0 ) THEN

C        TRACE DES TETRAEDRES INITIAUX et CREES AVANT ABANDON
         KTITRE='recufape: ABANDON de la FACE         REPRISE=    TETRAE
     %DRES BLEUS INITIAUX         et       CREES VERTS. NBTRCF=        '
         WRITE(KTITRE(30:36),'(I7)') NFLPER
         WRITE(KTITRE(46:47),'(I2)') NUESSAI
         WRITE(KTITRE(76:80),'(I5)') NBTECFPR
         WRITE(KTITRE(87:91),'(I5)') NBTECR
         WRITE(KTITRE(113:117),'(I5)') NBTRCF
         CALL SANSDBL( KTITRE, L )
         PRINT*,KTITRE(1:L)
         CALL TRFETO8( KTITRE(1:L), PTXYZD,
     %                 NBTRCF,   NOTRCF,  LEFACO, NO0FAR,
     %                 NBTECFPR, NOTECFPR,
     %                 NBTECR,   NOTECF, NOTETR )

C        DESTRUCTION  DES NBTECR   TETRAEDRES NOTECF,
C        RESTAURATION DES NBTECFPR TETRAEDRES NOTECFPR
C        MISE A JOUR  DE N1TEVI, NOTETR, N1TETS
         CALL RSTETO( PTXYZD, NBTECFPR, NOTECFPR, NBTECR,  NOTECF,
     %                MXTETR, N1TEVI,   NOTETR,   NUDTETR, N1TETS,
     %                NBFANR )
C        PLUS AUCUN TETRAEDRE CREE APRES leur DESTRUCTION
         NBTECR = 0
         IF( NBFANR .GT. 0 ) THEN
            PRINT*, 'recufape: 9000) PB RESTAURATION TETRAEDRES INITIAUX 
     %avec mjopte => NBFANR=',NBFANR,' ?'
         ENDIF

         CALL TRFETO8( KTITRE(1:L), PTXYZD,
     %                 NBTRCF,   NOTRCF,  LEFACO, NO0FAR,
     %                 NBTECFPR, NOTECFPR,
     %                 NBTECR,   NOTECF,  NOTETR )

      ENDIF

C     FINALEMENT LE NOMBRE ACTUEL DE TETRAEDRES CREES EST NUL
      NBTECR = 0

C     RECUPERATION DES POINTS AJOUTES POUR RECUPERER CETTE FACE PERDUE
      NBSOMM = NBSOMM0

C     NOMBRE TOTAL D'ABANDON DE FACES PERDUES
      NBABAN = NBABAN + NBTRCF

      PRINT 19000
      PRINT*


C     SUPPRESSION DANS NOFAPE DES FACES PERDUES A EVITER
C     --------------------------------------------------
      DO 9050 K = 1, NBTRCF

C        LA FACE RECUPEREE EST EFFACEE DANS NOFAPE
         NTR = ABS( NOTRCF( K ) )
         IF( NTR .EQ. 0 ) GOTO 9050

         DO J = 1, NBFAPE
            IF( NTR .EQ. ABS( NOFAPE(J) ) ) THEN
               NOFAPE( J ) = -NTR
               GOTO 9040
            ENDIF
         ENDDO

C        UN TETRAEDRE NOTETR CONTENANT LA FACE NTR DE LEFACO
 9040    NTE = LEFACO(11,NTR)
         IF( NTE .LE. 0 ) THEN
            LEFACO(11,NTR) = 0
            GOTO 9050
         ENDIF

         IF( NOTETR(1,NTE) .LE. 0 ) THEN
            LEFACO(11,NTR) = 0
            GOTO 9050
         ENDIF

 9050 ENDDO

C     UNE FACE PERDUE NON RECUPEREE. ABANDON DE LA RECUPERATION
      IERR = 11

C     ------------------------------------------
C     FIN DU TRAITEMENT DES NBFAPE FACES PERDUES
C     ------------------------------------------
 9990 TRACTE = TRACTE0

      PRINT 19992
19992 FORMAT(200('@'))
      NBC = NUDCNB(KNMVOL)
      PRINT*,'recufape: Volume: ',KNMVOL(1:NBC),'  IERR=',IERR,
     %' Iteration',NBITERFP,
     %NBFAPE,' FACES sont PERDUES',
     %NBABAN,' TRAITEES mais ABANDONNEES',
     %NBSOM0,' Sommets INITIAUX',
     %NBSOMM,' FINAUX',
     %NUDTETR,' No Dernier TETRAEDRE'
      PRINT 19992
      PRINT*
      IF( NBFAPE .GT. 0 ) THEN
         GOTO 1
      ENDIF

 9999 RETURN
      END
