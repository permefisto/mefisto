     %                    PTXYZD(1,NOTETR(3,NTE)),
     %                    PTXYZD(1,NOTETR(4,NTE)),
     %                    ARMIN, ARMAX, SURFTR, VOLUTE, QUALTE )
            QUAMOY = QUAMOY + QUALTE
            IF( QUALTE .LT. QUAMIN ) THEN
               QUAMIN = QUALTE
            ENDIF

