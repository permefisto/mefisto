      SUBROUTINE NOFAOP( NF, NT, NOTETR,  NFOP, NTOP )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER LE NUMERO LOCAL DE LA FACE DANS LE TETRAEDRE
C -----    OPPOSE A LA FACE NF DU TETRAEDRE NT

C ENTREES:
C --------
C NF     : NUMERO DE 1 A 4 DE LA FACE DE NT A TRAITER
C NT     : NUMERO DANS NOTETR DU TETRAEDRE
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C SORTIES:
C --------
C NFOP   : NUMERO DE 1 A 4 DANS LE TETRAEDRE OPPOSE DE LA FACE NF DE NT
C          0 SI PAS DE TETRAEDRE OPPOSE
C NTOP   : NUMERO DANS NOTETR DU TETRAEDRE OPPOSE A LA FACE NF DE NT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC        MAI 1991
C....................................................................012
      INTEGER  NOTETR(8,*)

C     LE NUMERO DU TETRAEDRE OPPOSE
      NTOP = NOTETR( 4+NF, NT )
      IF( NTOP .LE. 0 ) GOTO 10

C     IL EXISTE UN TETRAEDRE OPPOSE . SON TETRAEDRE OPPOSE EST NT
      DO NFOP = 1, 4
         IF( NOTETR( 4+NFOP, NTOP ) .EQ. NT ) GOTO 90
      ENDDO

C     PAS DE TETRAEDRE OPPOSE A LA FACE NF DE NT
 10   NFOP = 0

 90   RETURN
      END
