      SUBROUTINE SUTEQM( QUAMINEX, MXTETR, N1TEVI, NOTETR, N1TETS,
     %                   PTXYZD, MXFACO, LEFACO,
     %                   NUDTETR,  NBTESU ) 
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TENTATIVE DE SUPPRIMER LES TETRAEDRES DE MEDIOCRE QUALITE
C -----    PAR 3TETRAEDRES -> 2TETRAEDRES (sp ch3t2t.f)

C ENTREES:
C --------
C QUAMINEX:QUALITE AU DESSOUS DE LAQUELLE UN TETRAEDRE N'EST PAS CREE
C MXTETR : NOMBRE MAXIMAL DE TETRAEDRES DU TABLEAU NOTETR
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NBSTCF : NOMBRE DE SOMMETS DU CF DES FACES PERDUES CONTIGUES
C NOSTCF : NUMERO PTXYZD DES NBSTCF SOMMETS DU CF

C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C NUDTETR: NUMERO DU DERNIER TETRAEDRE ACTIF DANS NOTETR

C SORTIES:
C --------
C NUDTETR: NUMERO DU DERNIER TETRAEDRE ACTIF DANS NOTETR
C NBTESU : NOMBRE DE TETRAEDRES MEDIOCRES SUPPRIMES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET VEULETTES & St PIERRE du PERRAY  Janvier 2018
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,*)
      INTEGER           NOTETR(8,MXTETR), N1TETS(*),
     %                  LEFACO(1:11,0:MXFACO)

      INTEGER           NTEOLD(3), NTENEW(2), NVOLTE(1)
      DOUBLE PRECISION  ARMIN, ARMAX, SURFTR(4), VOLUTE
      REAL              Q5TE(5)

      NUDTET0 = NUDTETR
      NBTESU  = 0
      DO 10 NTE = 1, MIN( NUDTET0+100, MXTETR )

         IF( NOTETR(1,NTE) .GT. 0 ) THEN

C           NO DU DERNIER TETRAEDRE ACTIF DANS NOTETR
            IF( NUDTETR .LT. NTE ) NUDTETR = NTE

C           SON VOLUME ET SA QUALITE
            CALL QUATETD( PTXYZD(1,NOTETR(1,NTE)),
     %                    PTXYZD(1,NOTETR(2,NTE)),
     %                    PTXYZD(1,NOTETR(3,NTE)),
     %                    PTXYZD(1,NOTETR(4,NTE)),
     %                    ARMIN, ARMAX, SURFTR, VOLUTE, Q )

            IF( Q .LE. QUAMINEX ) THEN

C              NTE EST DE QUALITE MEDIOCRE. TENTATIVE DE LE SUPPRIMER
               DO NA = 1, 6

C                 EVENTUEL CHANGEMENT DE 3 TETRAEDRES AYANT UNE ARETE COMMUNE
C                 EN 2 TETRAEDRES => UNE ARETE EST ECHANGEE EN UNE FACE
                  CALL CH3T2T( 1,      MXFACO, LEFACO,  1,  0, NVOLTE,
     %                         NTE,    NA,     PTXYZD, 
     %                         N1TETS, NOTETR, N1TEVI, NUDTETR,
     %                         NTEOLD, NTENEW, Q5TE,   IERR )
                  IF( IERR .EQ. 0 ) THEN
C                    NTE A ETE SUPPRIME
                     NBTESU = NBTESU + 1
                     GOTO 10
                  ENDIF

               ENDDO

            ENDIF

         ENDIF

 10   ENDDO

      PRINT*,'suteqm  :',NBTESU,' TETRAEDRES SUPPRIMES',
     %       ' NO DERNIER TETRAEDRE=',NUDTETR

      RETURN
      END
