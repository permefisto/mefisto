      SUBROUTINE COCOTG( PAX1C1, PAX1C2, RAY1C2, RAY2C2,
     %                   RPCON1, RPCON2, HAUTC2,
     %                   RACINE, M2,     MG,     P, Q,
     %                   TG,     IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DU VECTEUR UNITAIRE DE LA TG AUX 2 TRONCS DE CONES
C -----    AU POINT M2=MG DE L'INTERSECTION ENTRE LES 2 TRONCS DE CONES
C
C ENTREES:
C --------
C PAX1C1 : POINT DE BASE DE L'AXE DU TRONC DE CONE 1
C PAX1C2 : POINT DE BASE DE L'AXE DU TRONC DE CONE 2
C RAY1C2 : RAYON DU CERCLE AU POINT PAX1C2 (PLAN ORTHOGONAL A L'AXE DU CONE )
C PAX2C2 : POINT DE L'AXE DU TRONC DE CONE 2
C RAY2C2 : RAYON DU CERCLE AU POINT PAX2C2 (PLAN ORTHOGONAL A L'AXE DU CONE )
C
C RPCON1 : REPERE PROPRE ORTHONORME DU CONE 1 D'ORIGINE AU POINT PAX1C1
C          ORIGINE AU POINT PAX1C1
C RPCON2 : REPERE PROPRE ORTHONORME DU CONE 2 D'ORIGINE AU POINT PAX1C2
C          ORIGINE AU POINT PAX1C2
C HAUTC2 : HAUTEUR DU TRONC DE CONE 2
C RACINE : VALEUR DE LA RACINE DU POINT D'INTERSECTION M2
C M2     : POINT D'INTERSECTION DANS LE REPERE PROPRE AU CONE 2
C MG     : MEME POINT MAIS DANS LE REPERE GLOBAL
C P, Q   : 2 POINTS DE LA GENERATRICE DU CONE 1 ET PASSANT PAR M2
C          DANS LE REPERE PROPRE AU CONE 2
C
C SORTIES:
C --------
C TG     : VECTEUR UNITAIRE DANS LE REPERE GLOBAL DE LA TG AUX 2 TRONCS
C          DE CONES AU POINT GLOBAL MG
C IERR   : 0 SI AUCUNE ERREUR DETECTEE
C          1 SI VECTEUR TG NUL
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS     JANVIER 1998
C2345X7..............................................................012
      DOUBLE PRECISION  PAX1C1(3),   PAX1C2(3)
      DOUBLE PRECISION  RPCON1(3,3), RPCON2(3,3)
      DOUBLE PRECISION  RAY1C2, RAY2C2, HAUTC2, RACINE
      DOUBLE PRECISION  P(3),   Q(3),    M2(3),  MG(3),
     %                  P1(3),  PG1(3),  P2(3),  PG2(3),
     %                  V1(3),  V2(3),   V3(3),  VN1(3),  VN2(3),
     %                  AX1(3), AXG1(3), AX2(3), AXG2(3), TG(3)
      DOUBLE PRECISION  ANGLED, ANGLE
C
C     CALCUL DU VECTEUR UNITAIRE DE LA DROITE TANGENTE AUX 2 CONES
C     AU POINT MG (XYZ GLOBALES) OU M DANS LE REPERE PROPRE AU CONE 2
C     ---------------------------------------------------------------
C     AX2 EST LE POINT DE L'AXE DU CONE 2 DE MEME COTE Z QUE M
C     LES COORDONNEES SONT CELLES DU REPERE PROPRE AU CONE 2
      AX2(1) = 0D0
      AX2(2) = 0D0
      AX2(3) = M2(3)
C     PASSAGE AUX COORDONNEES GLOBALES
      CALL AC3D3D( PAX1C2, RPCON2, AX2, AXG2 )
C
C     CALCUL D'UN POINT P2 DE LA GENERATRICE DU CONE 2 PASSANT PAR M2
C     ANGLE DE LA GENERATRICE DU CONE 2 PASSANT PAR M2
      P2(1) = 1D0
      P2(2) = 0D0
      ANGLE = ANGLED( AX2, P2, M2 )
      IF( ABS(M2(3)) .LE. 1D-12*HAUTC2 ) THEN
C        LE POINT CHOISI DE LA GENERATRICE DU CONE 2 A POUR COTE Z HAUTC2
         P2(1) = RAY2C2 * COS( ANGLE )
         P2(2) = RAY2C2 * SIN( ANGLE )
         P2(3) = HAUTC2
      ELSE
C        LE POINT CHOISI DE LA GENERATRICE DU CONE 2 A POUR COTE Z 0D0
         P2(1) = RAY1C2 * COS( ANGLE )
         P2(2) = RAY1C2 * SIN( ANGLE )
         P2(3) = 0D0
      ENDIF
C     PASSAGE AUX COORDONNEES GLOBALES
      CALL AC3D3D( PAX1C2, RPCON2, P2, PG2 )
C
C     VECTEUR NORMAL QUI AVEC M2 P2 DEFINIT LE PLAN TG AU CONE 2 AU POINT M2=MG
      V1(1) = AXG2(1) - MG(1)
      V1(2) = AXG2(2) - MG(2)
      V1(3) = AXG2(3) - MG(3)
      CALL NORME1( 3, V1, IERR )
      V2(1) = PG2(1)  - MG(1)
      V2(2) = PG2(2)  - MG(2)
      V2(3) = PG2(3)  - MG(3)
      CALL NORME1( 3, V2, IERR )
      CALL PROVEC( V1, V2, V3 )
C     LE VECTEUR NORMAL VN2 AU PLAN TG DU CONE 2 AU POINT MG
      CALL PROVEC( V3, V2, VN2 )
      CALL NORME1( 3, VN2, IERR )
C
C     LES POINTS P Q DEFINISSENT LA GENERATRICE DU CONE 1
C     AVEC DES COORDONNEES QUI SONT CELLES DU REPERE PROPRE AU CONE 2
C     CALCUL D'UN POINT P1 DE LA GENERATRICE DU CONE 1 PASSANT PAR MG
      IF( ABS(RACINE) .LE. 1D-12 ) THEN
C        LE POINT CHOISI DE LA GENERATRICE DU CONE 1 EST Q
         P1(1) = Q(1)
         P1(2) = Q(2)
         P1(3) = Q(3)
      ELSE
C        LE POINT CHOISI DE LA GENERATRICE DU CONE 1 EST P
         P1(1) = P(1)
         P1(2) = P(2)
         P1(3) = P(3)
      ENDIF
C     PASSAGE AUX COORDONNEES GLOBALES PG1 DE P1
      CALL AC3D3D( PAX1C2, RPCON2, P1, PG1 )
C
C     PASSAGE AUX COORDONNEES PROPRES DU CONE 1 DU POINT DE TANGENCE MG
      CALL NC3D3D( PAX1C1, RPCON1, MG, V3 )
C     LE POINT AX1
      AX1(1) = 0D0
      AX1(2) = 0D0
      AX1(3) = V3(3)
C     PASSAGE AUX COORDONNEES GLOBALES
      CALL AC3D3D( PAX1C1, RPCON1, AX1, AXG1 )
C
C     VECTEUR NORMAL QUI AVEC MG PG1 DEFINIT LE PLAN TG AU CONE 1 AU POINT MG
      V1(1) = AXG1(1) - MG(1)
      V1(2) = AXG1(2) - MG(2)
      V1(3) = AXG1(3) - MG(3)
      CALL NORME1( 3, V1, IERR )
      V2(1) = PG1(1)  - MG(1)
      V2(2) = PG1(2)  - MG(2)
      V2(3) = PG1(3)  - MG(3)
      CALL NORME1( 3, V2, IERR )
      CALL PROVEC( V1, V2, V3 )
C     LE VECTEUR NORMAL VN2 AU PLAN TG DU CONE 1 AU POINT MG
      CALL PROVEC( V3, V2, VN1 )
      CALL NORME1( 3, VN1, IERR )
C
C     LE VECTEUR UNITAIRE TANGENT AU POINT MG AUX 2 TRONCS DE CONES
      CALL PROVEC( VN1, VN2, TG )
C
C     NORME DU VECTEUR TANGENT AUX 2 TRONCS DE CONES
      ANGLE = SQRT( TG(1)**2 + TG(2)**2 + TG(3)**2 )
      IF( ANGLE .LT. 1D-3 ) THEN
C        LES 2 CONES ONT MEME PLAN TANGENT
         IERR = 1
      ELSE
C        NORMALISATION DE LA TG
         TG(1) = TG(1) / ANGLE
         TG(2) = TG(2) / ANGLE
         TG(3) = TG(3) / ANGLE
      ENDIF
C
      RETURN
      END
