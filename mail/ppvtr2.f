      SUBROUTINE PPVTR2( P, H, PXYD, LETREE, NOLESO, NS, NT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE PLUS PROCHE POINT NS DE P DE DISTANCE < H
C -----    RENDRE NEGATIF NOLESO(N) DE TOUT SOMMET N DE TE
C          TROP PROCHE DU POINT P ( DISTANCE < H )
C
C ATTENTION : P N'EST PAS DANS LETREE NI AJOUTE DANS CE SP (CF AJPTR2)
C
C ENTREES:
C --------
C P      : 2 COORDONNEES DU POINT P
C H      : DISTANCE AU DESSOUS DE LAQUELLE CHERCHER LES SOMMETS DE TE
C PXYD   : X Y DISTANCE DES POINTS
C LETREE : ARBRE-4 DES TRIANGLES EQUILATERAUX (TE) FOND DE LA TRIANGULATION
C      LETREE(0,0)  NO DU 1-ER TE VIDE DANS LETREE
C      LETREE(0,1) : MAXIMUM DU 1-ER INDICE DE LETREE (ICI 8)
C      LETREE(0,2) : MAXIMUM DECLARE DU 2-EME INDICE DE LETREE (ICI MXTREE)
C      LETREE(0:8,1) : RACINE DE L'ARBRE  (TRIANGLE SANS SUR TRIANGLE)
C      SI LETREE(0,.)>0 ALORS
C         LETREE(0:3,J) : NO (>0) LETREE DES 4 SOUS-TRIANGLES DU TRIANGLE J
C      SINON
C         LETREE(0:3,J) :-NO PXYD DES 1 … 4 POINTS INTERNES AU TRIANGLE J
C                         0  SI PAS DE POINT
C                        ( J EST ALORS UNE FEUILLE DE L'ARBRE )
C      LETREE(4,J) : NO LETREE DU SUR-TRIANGLE DU TRIANGLE J
C      LETREE(5,J) : 0 1 2 3 NO DU SOUS-TRIANGLE J POUR SON SUR-TRIANGLE
C      LETREE(6:8,J) : NO PXYD DES 3 SOMMETS DU TRIANGLE J
C NOLESO : TABLEAU DU NUMERO DANS LETREE D'UN SOMMET DE TE
C      >0 +NO LETREE DU TRIANGLE DU SOMMET ET SOMMET DE LA TRIANGULATION
C      <0 -NO LETREE DU TRAINGLE DU SOMMET ELIMINE   DE LA TRIANGULATION
C      =0 SI SOMMET N'APPARTENANT PAS AUX SOMMETS DE LETREE
C
C SORTIES :
C ---------
C NS      : NUMERO PXYD DU PLUS PROCHE SOMMET DE LA TRIANGULATION ACTUELLE
C           ET DISTANT DE MOINS DE H
C NT      : NUMERO LETREE DU TE MINIMAL CONTENANT LE POINT P
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1992
C2345X7..............................................................012
      DOUBLE PRECISION  P(2), PXYD(3,*)
      INTEGER           LETREE(0:8,0:*),NOLESO(*)
      INTEGER           N12TRV(13)
C
C     RECHERCHE DU SOUS-TRIANGLE MINIMAL CONTENANT LE POINT P
      NT = NOTRPT( P, PXYD, 1, LETREE )
C
C     RECHERCHE DES 12 TRIANGLES VOISINS DU TE NT CONTENANT LE POINT P
C     DE MEMES NIVEAUX
      CALL  N12TVT( NT, LETREE, N12TRV )
C     ATTENTION : LE 13-EME EST A DECLARER CAR CALCULE ICI
C
C     INITIALISATION DU SOMMET LE PLUS PROCHE
      NS   = 0
      DMIN = 1E28
C
C     CARRE DE LA DISTANCE MAXIMALE PERMISE
      HH = H * H
C
C     LE POINT LE PLUS PROCHE ET MISE A JOUR NOLESO
      CALL PPVTR1( P, HH, PXYD, NT, LETREE, NOLESO, NS, DMIN )
C
C     RECHERCHE DU POINT LE PLUS PROCHE DANS CHACUN DES 12 TRIANGLES
C     SUPPRESSION DES TRIANGLES VOISINS 2 FOIS
      CALL COMPRT( 12, N12TRV, N1, N12TRV )
      DO 10 I=1,N1
         IF( N12TRV(I) .GT. 0 ) THEN
            CALL PPVTR1( P, HH, PXYD, N12TRV(I), LETREE, NOLESO,
     %                   NS, DMIN )
         ENDIF
 10   CONTINUE
      END
