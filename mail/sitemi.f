      SUBROUTINE SITEMI( IAR,    NOTET0, XYZSOM,
     %                   NBSOTE, NSTETR, NO1TSO, NOTESO,
     %                   N1FEOC, N1FEVI, NFETOI, VOETOI, QUETOI,
     %                   NBTEDS, MXTETR, NOTEDS,
     %                   VOLUMT, QUALIT, VOLET0, QUAET0, VOLET1, QUAET1,
     %                   P  )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    SIMULATION DE LA TETRAEDRISATION ETOILEE PAR LE MILIEU
C -----    DE L'ARETE IAR DU TETRAEDRE NOTET0
C
C ENTREES:
C --------
C IAR    : NUMERO DANS LE TETRAEDRE NOTET0 DE L'ARETE A TRAITER
C NOTET0 : NUMERO DANS NSTETR DU TETRAEDRE CONTENANT LE POINT P
C XYZSOM : LES 3 COORDONNEES DES SOMMETS
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C NSTETR : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C NOTESO : NOTESO(1,*) NUMERO DANS NSTETR DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER
C VOLUMT : VOLUME  DES TETRAEDRES DE LA TETRAEDRISATION
C QUALIT : QUALITE DES TETRAEDRES DE LA TETRAEDRISATION
C
C MODIFIES :
C ----------
C N1FEVI : NUMERO DANS NFETOI DE LA PREMIERE FACE VIDE
C N1FEOC : NUMERO DANS NFETOI DE LA PREMIERE FACE OCCUPEE
C NFETOI : NUMERO DES 3 SOMMETS, TETRAEDRE ET SUIVANTE DES FACES
C          VUES UNE FOIS DE L'ETOILE CHAINEES N1FEOC PUIS NFETOI(5,*)
C VOETOI : VOLUME  DU TETRAEDRE INITIAL ASSOCIE A LA FACE
C QUETOI : QUALITE DU TETRAEDRE INITIAL ASSOCIE A LA FACE
C
C SORTIES:
C --------
C NBTEDS : NOMBRE DE TETRAEDRES A DETRUIRE
C NOTEDS : NUMERO DANS NSTETR DES TETRAEDRES A DETRUIRE
C VOLET0 : VOLUME  INITIAL  DE LA TETRAEDRISATION ETOILEE PAR P
C QUAET0 : QUALITE INITIALE DE LA TETRAEDRISATION ETOILEE PAR P
C VOLET1 : VOLUME  FINAL    DE LA TETRAEDRISATION ETOILEE PAR P
C QUAET1 : QUALITE FINALE   DE LA TETRAEDRISATION ETOILEE PAR P
C          0 INDIQUE UNE IMPOSSIBLITE D'OBTENIR UNE ETOILE POUR P
C P      : 3 COORDONNEES DU POINT MILIEU DE L'ARETE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   DECEMBRE 1991
C....................................................................012
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      REAL              P(3), XYZSOM(3,*)
      INTEGER           NSTETR(NBSOTE,*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*),
     %                  NFETOI(5,*),
     %                  NOTEDS(MXTETR)
      REAL              VOETOI(*), QUETOI(*), VOLUMT(*), QUALIT(*)
      REAL              ARMIN, ARMAX, SURFTR(4),
     %                  VOLET0, VOLET1, QUAET0, QUAET1
      INTEGER           NOSOARTE(2,6)
      DATA              NOSOARTE / 1,2, 2,3, 3,1, 4,1, 4,2, 4,3 /

      GRAND  = RINFO( 'GRAND' )
      VOLET0 = 0
      VOLET1 = 0
      QUAET0 = GRAND
      QUAET1 = GRAND

C     REINITIALISATION A VIDE DES FACES DE NFETOI
C     -------------------------------------------
      CALL ETOIOCVI( N1FEOC, N1FEVI, NFETOI )

C     LES 2 SOMMETS EXTREMITES DE CETTE ARETE IAR
C     -------------------------------------------
      NS1 = NSTETR( NOSOARTE(1,IAR), NOTET0 )
      IF( NS1 .LE. 0 )  GOTO 9000

      NS2 = NSTETR( NOSOARTE(2,IAR), NOTET0 )
      IF( NS2 .LE. 0 )  GOTO 9000

C     LE POINT MILIEU DE CETTE ARETE
      DO J=1,3
         P(J) = ( XYZSOM(J,NS1) + XYZSOM(J,NS2) ) * 0.5
      ENDDO

C     RECHERCHE DES TETRAEDRES D'ARETE NS1 ET NS2
C     -------------------------------------------
      CALL NOTEAR( NS1,    NS2,
     %             NBSOTE, NSTETR, NO1TSO, NOTESO,
     %             NBTEDS, NOTEDS )
      IF( NBTEDS .GT. MXTETR ) GOTO 9000

C     L'ETOILE DES FACES SIMPLES DES TETRAEDRES D'ARETE NS1 NS2
C     ---------------------------------------------------------
      DO 50 J=1,NBTEDS

C        CALCUL DU VOLUME ET QUALITE MIN INITIAL DE CES TETRAEDRES
         NT = NOTEDS(J)

C        CALCUL DU VOLUME ET QUALITE DU TETRAEDRE DU A LA FACE
         CALL QUATET( XYZSOM(1, NSTETR(1,NT) ),
     %                XYZSOM(1, NSTETR(2,NT) ),
     %                XYZSOM(1, NSTETR(3,NT) ),
     %                XYZSOM(1, NSTETR(4,NT) ),
     %                ARMIN, ARMAX, SURFTR, VOLUMT(NT), QUALIT(NT) )
         VOLET0 = VOLET0 + ABS( VOLUMT(NT) )
         QUAET0 = MIN( QUAET0, QUALIT(NT) )

         DO 40 I=1,4
C           SI    ( LA FACE I DU TETRAEDRE NOTET0 AJOUTE
C                   N'APPARTIENT PAS AUX FACES DE L'ETOILE NFETOI )
C           ALORS ELLE EST AJOUTEE A L'ETOILE DANS NFETOI
C           SINON ELLE EST EMPILEE DANS NPILE POUR ETRE DETRUITE ENSUITE
C                 ELLE EST SUPPRIMEE DE L'ETOILE NFETOI
            CALL AJFACE( 1, NT, I, NBSOTE, NSTETR,
     %                   N1FEOC, N1FEVI, NFETOI,
     %                   NF  )
            IF( NF .LT. 0 ) THEN
C              SATURATION DES FACES DE L'ETOILE
               GOTO 9000
            ENDIF
 40      CONTINUE

 50   CONTINUE
C
C     ICI, L'ETOILE EST COMPLETE CALCUL DE SA QUALITE
C     -----------------------------------------------
      NF = N1FEOC
 90   IF( NF .GT. 0 ) THEN

C        CALCUL DU VOLUME ET QUALITE DU TETRAEDRE DU A LA FACE
         CALL QUATET( XYZSOM( 1, NFETOI(1,NF) ),
     %                XYZSOM( 1, NFETOI(2,NF) ),
     %                XYZSOM( 1, NFETOI(3,NF) ),
     %                P,
     %                ARMIN,ARMAX,SURFTR, VOETOI(NF), QUETOI(NF) )

C        VOLUME DE L'ETOILE DE CENTRE P
         VOLET1 = VOLET1 + ABS( VOETOI(NF) )

C        QUALITE MINIMALE DE L'ETOILE DE CENTRE P
         IF( QUETOI(NF) .LT. QUAET1 ) THEN
            QUAET1 = QUETOI(NF)
         ENDIF

C        PASSAGE A LA FACE SUIVANTE
         NF = NFETOI(5,NF)
         GOTO 90

      ENDIF

      IF( ABS( VOLET1-VOLET0 ) .GT. 1D-5 * VOLET0 ) GOTO 9000
      IF( QUAET1 .EQ. GRAND ) GOTO 9000

      GOTO 9999
C
C     PROBLEME RENCONTRE
 9000 NBTEDS = 0
      QUAET1 = 0
C
 9999 RETURN
      END
