      SUBROUTINE ETOIL01( NOPOINT, POINTN,  NOTETI,
     %                    PTXYZD,  NUDTETR, NOTETR,
     %                    NOTETS,  COBARY )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TROUVER UN TETRAEDRE CONTENANT LE POINT NOPOINT
C -----    A PARTIR DU TETRADRE NOTETI

C ENTREES :
C ---------
C NOPOINT: NUMERO DU POINT DANS PTXYZD
C POINTN : 3 COORDONNEES DU POINT D'ETOILE DES TETRAEDRES A CONSTRUIRE
C NOTETI : NUMERO DU TETRAEDRE DE DEBUT DE RECHERCHE
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NUDTETR: PLUS GRAND NUMERO DANS NOTETR DE TETRAEDRE OCCUPE
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C
C SORTIES :
C ---------
C NOTETS : NUMERO DANS NOTETR DU TETRAEDRE CONTENANT LE POINTN
C COBARY : LES 4 COORDONNEES BARYCENTRIQUES DE POINTN DANS
C          LE TETRAEDRE NOTETS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC PARIS & VEULETTES SUR MER AOUT 2014
C23456...............................................................012
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      INTEGER           NOTETR(8,NUDTETR), NTETOI(1)
      DOUBLE PRECISION  PTXYZD(1:4,1:*), POINTN(3), COBARY(4),
     %                  CBMIN, CBMINMAX, V
      CHARACTER*80      KTITRE
C
C     SAUVEGARDE POUR AFFICHAGE EN CAS DE NON CHEMIN
      NUTET1  = 1
      NOTET0  = 0
      NOTET00 = 0
      NOTETS  = NOTETI
      NBTEVU  = 0

ccc      if( nopoint .eq. 257 ) then
ccc         print *,'etoil01: point n=',nopoint, pointn
ccc      endif

C
C     RECHERCHE D'UN TETRAEDRE NOTETS CONTENANT LE POINTN
C     ===================================================
C     CALCUL DE LA COORDONNEE BARYCENTRIQUE MINIMALE DU POINTN DANS NOTETS
 10   CALL COBATE( POINTN, PTXYZD, NOTETR(1,NOTETS), V, COBARY, IERR )
C     COBARY(I) EST ICI LA COORDONNEE BARYCENTRIQUE OPPOSEE A LA FACE I
      IF( IERR .NE. 0 ) THEN
C        TETRAEDRE NOTETS DE VOLUME NUL
ccc         WRITE(IMPRIM,*) 'ETOIL01: POINT ',NOPOINT,' XYZ=',POINTN,
ccc     %                   '  TETRAEDRE(',NOTETS,')=',
ccc     %                   (NOTETR(I,NOTETS),I=1,4),' VOLUME=',V,'<=0'
         GOTO 30
      ENDIF

C     SI LES COORDONNEES BARYCENTRIQUES SONT SUPERIEURES OU EGALES A 0D0
C     => LE POINTN EST CONSIDERE DANS LE TETRAEDRE NOTETS
      CBMIN = MIN( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
      IF( CBMIN .GE. 0D0 ) GOTO 50
C
C     LE TETRAEDRE NOTETS NE CONTIENT PAS LE POINTN
C     RECHERCHE DE LA FACE NOFMAX DE CE TETRAEDRE QUI REGARDE
C     DIRECTEMENT LE POINTN
C     =======================================================
      CALL FAVRPT( POINTN, NOTETS, NOTETR, PTXYZD,  NOFMAX )
C     LE TETRAEDRE DE L'AUTRE COTE DE LA FACE POUR PROGRESSER VERS POINTN
      NBTEVU = NBTEVU + 1
      IF( NBTEVU .GT. NUDTETR ) THEN
C        DETECTION D'UNE BOUCLE INFINIE
         WRITE(IMPRIM,*) 'ETOIL01: POINT ',NOPOINT,' XYZ=',POINTN,
     %                   ' BOUCLE INFINIE SUR FAVRPT'
         GOTO 30
      ENDIF
      NOTET00 = NOTET0
      NOTET0  = NOTETS
      NOTET2  = NOTETR(4+NOFMAX,NOTETS)
      IF( NOTET00 .NE. NOTET2 ) THEN
C        PAS DE BOUCLE SUR 2 TETRAEDRES SUCCESSIFS
         IF( NOTET2 .GT. 0 ) THEN
            NOTETS = NOTET2
            GOTO 10
         ENDIF
      ENDIF
C
C     BOUCLE SUR 2 TRIANGLES ou SORTIE PAR FACE SANS FACE OPPOSEE
C     => RECHERCHE D'UN TETRAEDRE DE DEPART PARMI TOUS LES TETRAEDRES
C     ===============================================================
 30   CBMINMAX = -1D0
      NTMAX = 0

ccc      WRITE(IMPRIM,*) 'ETOIL01: RECHERCHE PARMI LES TETRAEDRES',
ccc     %NUDTETR,' a 1 du POINT ',NOPOINT, POINTN

      DO NOTETS = NUDTETR, 1, -1
         IF( NOTETR(1,NOTETS) .GT. 0 ) THEN
            CALL COBATE( POINTN,PTXYZD,NOTETR(1,NOTETS),V,COBARY,IERR )
            IF( IERR .EQ. 0 ) THEN
               CBMIN=MIN( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
               IF( CBMIN .GE. -1D-2 ) THEN
ccc                  WRITE(IMPRIM,*)'ETOIL01: TETRAEDRE',NOTETS,
ccc     %                  ' CONTIENT le POINT',NOPOINT,POINTN
                  GOTO 50
               ENDIF
               IF( CBMIN .GT. -1D0 .AND. CBMIN .GT. CBMINMAX ) THEN
                  CBMINMAX = CBMIN
                  NTMAX = NOTETS
               ENDIF
            ENDIF
         ENDIF
      ENDDO

      WRITE(IMPRIM,*) 'ETOIL01: POINT ', NOPOINT, POINTN,
     %             ' EXTERIEUR AUX', NUDTETR,' TETRAEDRES. NTMAX=',NTMAX
      IF( NTMAX .LE. 0 ) THEN
         NOTETS = 0
         RETURN
      ENDIF

      NOTETS = NTMAX
      CALL COBATE( POINTN, PTXYZD, NOTETR(1,NOTETS), V, COBARY, IERR )
      WRITE(IMPRIM,*) 'ETOIL01: DEPART avec le TETRAEDRE',NOTETS,': ST',
     %                (NOTETR(I,NOTETS),I=1,4)
      WRITE(IMPRIM,*) 'ETOIL01: VOLUME=',V,' COBARY=',COBARY,
     %                'CBMINMAX=',CBMINMAX,' NTMAX=',NTMAX
C
C     LE TETRAEDRE NOTETS CONTIENT LE POINTN
C     ======================================
 50   NTETOI(1) = NOTETS
C     TRACE FILAIRE DES ARETES DU TETRAEDRE CONTENANT POINTN
      KTITRE='etoil01: POINT            dans le TETRAEDRE              '
      WRITE(KTITRE(17:24),'(I8)') NOPOINT
      WRITE(KTITRE(45:52),'(I8)') NOTETS
      CALL TRACEETOILE( KTITRE, PTXYZD, NOTETR, NOPOINT, 1, NTETOI )

      RETURN
      END
