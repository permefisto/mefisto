      SUBROUTINE TRITEMV0( NOMTCLTR, KNMSURF,  NUSURF, MNXYZS, MNNSEF,
     %                     L1ARFA,   L2ARFA,   MNARFA, NBAR1F, MNAR1F,
     %                     NBA2FCOL, MNA2FCOL, NBAR2SF,MNAR2SF,
     %                     NBAR2F, MNAR2F, NBAR3F, MNAR3F,
     %                     MXITDI, MNITDI, MXITAN, MNITAN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : TRACER LES FACES 3D DU MAILLAGE NSEF + XYZSOMMET + ARETES + FACES
C ----- CONSTRUCTION DES ITEMS SOMMETS et ARETES ET FACES VISIBLES
C       dans la FENETRE

C ENTREES:
C --------
C NOMTCLTR: TRACE SELON LA VALEUR DU MOTCLE NOMTCLTR
C          =1 DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) ARETES du MAILLAGE
C          =2 DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) FACES OMBREES du MAILLAGE
C          =3 DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) FACES QUALITE du MAILLAGE
C          =4 DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) FACES MEDIOCRE QUALITE
C             TRACER LA FACE TRIANGLE DE PLUS MAUVAISE QUALITE

C KNMSURF: NOM DE LA SURFACE FINALE    DANS LE LEXIQUE DES SURFACES
C NUSURF : NUMERO DE LA SURFACE FINALE DANS LE LEXIQUE DES SURFACES
C MNXYZS : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE FINALE
C           CF $MEFISTO/td/d/a___xyzsommet
C MNNSEF : ADRESSE MCN DU TMS 'NSEF' DE LA SURFACE FINALE
C           CF $MEFISTO/td/d/a___nsef

C L1ARFA : NOMBRE DE MOTS PAR ARFA DU TABLEAU NARFA
C L2ARFA : NOMBRE DE QTANGLES DU TABLEAU NARFA
C MNARFA : ADRESSE DANS MCN DU TABLEAU NARFA DES QTANGLES DU MAILLAGE
C          EN SORTIE NARFA(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C                    NARFA(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C                    NARFA(3,I)= CHAINAGE HACHAGE SUR LE QTANGLE SUIVANT
C                    NARFA(4:L1ARFA,I)= NO NOSTQT DU QTANGLE
C                                       CONTENANT L'ARETE
C          SI UNE ARETE APPARTIENT A PLUS DE L1ARFA-3 QTANGLES, 
C          LE DERNIER NUMERO DE QTANGLE EST RENDU NEGATIF POUR INDIQUER
C          QUE LA LISTE DES QTANGLES EST INCOMPLETE

C NBAR1F : NOMBRE D'ARETES APPARTENANT A UNE SEULE FACE
C MNAR1F : >0 ADRESSE MCN DU TMC NAR1F NO NARFA DES ARETES TRIPLES
C          =0 SI NBAR1F=0

C NBAR2SF : NOMBRE D'ARETES DE NO LARETE DANS LE TMC AR2SF
C MNAR2SF : >0 ADRESSE MCN DU TMC NAR2SF NO LARETE DES ARETES DE RATIO FAIBLE
C           =0 SI NBAR2SF=0

C NBA2FCOL: NOMBRE D'ARETES DE NO LARETE DANS LE TMC A2FCOL
C MNA2FCOL: >0 ADRESSE MCN DU TMC NA2FCOL NO LARETE DE L'ARETE DES
C               COUPLES DE TRIANGLES ADJACENTS COLLES
C           =0 SI NBA2FCOL=0

C NBAR2F : NOMBRE D'ARETES APPARTENANT A AU MOINS 2 FACES
C MNAR2F : >0 ADRESSE MCN DU TMC NAR2F NO NARFA DES ARETES TRIPLES
C          =0 SI NBAR2F=0

C NBAR3F : NOMBRE D'ARETES APPARTENANT A AU MOINS 3 FACES
C MNAR3F : >0 ADRESSE MCN DU TMC NAR3F NO NARFA DES ARETES TRIPLES
C          =0 SI NBAR3F=0

C MODIFIES:
C ---------
C MXITDI : NOMBRE MAXIMAL D'ITEMS VISIBLES DU TABLEAU MNITDI
C MNITDI : ADRESSE MCN DU TABLEAU DE LA DISTANCES DE L'ITEM A L'OEIL
C MXITAN : NOMBRE MAXIMAL D'ITEMS VISIBLES DU TABLEAU MNITAN
C MNITAN : ADRESSE MCN DU TABLEAU DU NUMERO ANCIEN DES ITEMS APRES
C          LEUR TRI CROISSANT SELON LA DISTANCE A L'OEIL
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : Alain PERRONNET  Saint PIERRE du PERRAY            Avril 2020
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/darete.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/pp.inc"
      COMMON          MCN(MOTMCN)
      REAL           RMCN(MOTMCN)
      EQUIVALENCE   (RMCN(1),MCN(1))

      CHARACTER*24   KNMSURF
      CHARACTER*160  KTITRE
      REAL           VECNOR(3), BARY(3), TAIARE(3)

      IF( MNNSEF .LE. 0 .OR. MNXYZS .LE. 0 ) GOTO 9999

      PRINT*,'tritemv0; ITEMS de la SURFACE ',KNMSURF,
     %       ' de NUMERO de SURFACE=',NUSURF
C     EFFACEMENT DE LA MEMOIRE MEMPX
      CALL EFFACEMEMPX

      IF( NOMTCLTR .GT. 4 ) NOMTCLTR = 3
      GOTO( 10, 20, 30, 40, 9000 ), NOMTCLTR

C     1: DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) ARETES du MAILLAGE
C        TRACER EN VERT  TOUTES LES ARETES APPARTENANT A  1 FACE
C               EN NOIR  TOUTES LES ARETES APPARTENANT A  2 FACES
C               EN ROUGE TOUTES LES ARETES APPARTENANT A >2 FACES
C     ------------------------------------------------------------
C     MAILLAGE FIL DE FER
 10   LCRITR = 0
      PREDUF = 5.0
C     PAS DE TRACE DU REMPLISSAGE DE LA FACE
      IAVFAC = 0
      CALL PALCDE( 12 )
      GOTO 300

C     2: DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) FACES OMBREES du MAILLAGE
C        TRACER EN VERT  TOUTES LES ARETES APPARTENANT A  1 FACE
C               EN ROUGE TOUTES LES ARETES APPARTENANT A >2 FACES
C     ---------------------------------------------------------------------
C     MAILLAGE OMBRE SANS LA QUALITE
 20   LCRITR = 0
      PREDUF = 3.0
C     TRACE AVEC REMPLISSAGE DE LA FACE
      IAVFAC = 1
C     PALETTE DES GRIS
      CALL PALCDE( 10 )
      GOTO 300

C     3: DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) FACES QUALITE du MAILLAGE
C        TRACER EN VERT  TOUTES LES ARETES APPARTENANT A  1 FACE
C               EN ROUGE TOUTES LES ARETES APPARTENANT A >2 FACES
C     ---------------------------------------------------------------------
C     MAILLAGE AVEC LA QUALITE
 30   LCRITR = 1
      PREDUF = 5.0
C     TRACE AVEC REMPLISSAGE DE LA FACE
      IAVFAC = 1
C     PALETTE DES QUALITES
      CALL PALCDE( 12 )
      GOTO 300

C     4: DEPLACER(Bouton1) ORBITER(B2) ZOOMER(B3) FACES MEDIOCRE QUALITE
C        TRACER LA FACE TRIANGLE DE QUALITE LA PLUS MAUVAISE
C     ------------------------------------------------------------------
C     RECHERCHE DU TRIANGLE DE PLUS MAUVAISE QUALITE = QUALITE MINIMALE
 40   CALL QUALTTRI( RMCN(MNXYZS+WYZSOM),
     %               4, MCN(MNNSEF+WBEFOB), MCN(MNNSEF+WUSOEF),
     %               QTRMOY, QTRMIN, NOTRQMIN )
      IF( NOTRQMIN .LE. 0 ) GOTO 9000
      IF( LANGAG .EQ. 0 ) THEN
         PRINT*,'tritemv0: TRIANGLE',NOTRQMIN,' QUALITE MIN=',QTRMIN
      ELSE
         PRINT*,'tritemv0: TRIANGLE',NOTRQMIN,' MINIMUM QUALITY=',QTRMIN
      ENDIF

C     CONSTRUCTION DU VECTEUR NORMAL UNITE DU TRIANGLE NOTRQMIN
      MNS1 = MNNSEF + WUSOEF + 4 * NOTRQMIN - 4
C     NO DES 3 SOMMETS DU TRIANGLE NOTRQMIN
      NS1  = MCN( MNS1 )
      NS2  = MCN( MNS1 + 1 )
      NS3  = MCN( MNS1 + 2 )
C     ADRESSE MCN DES XYZ DES 3 SOMMETS
      MNXYZ1 = MNXYZS + WYZSOM + 3 * NS1 - 3
      MNXYZ2 = MNXYZS + WYZSOM + 3 * NS2 - 3
      MNXYZ3 = MNXYZS + WYZSOM + 3 * NS3 - 3
      CALL NORFA3( RMCN(MNXYZ1), RMCN(MNXYZ2), RMCN(MNXYZ3),
     %             VECNOR, IERR )
      IF( IERR .NE. 0 ) GOTO 9000

C     CALCUL DE LA TAILLE DES 3 ARETES DU TRIANGLE NOTRQMIN
      TAIARE(1) = SQRT( ( RMCN(MNXYZ2)   - RMCN(MNXYZ1)   ) ** 2
     %                + ( RMCN(MNXYZ2+1) - RMCN(MNXYZ1+1) ) ** 2
     %                + ( RMCN(MNXYZ2+2) - RMCN(MNXYZ1+2) ) ** 2 )

      TAIARE(2) = SQRT( ( RMCN(MNXYZ3)   - RMCN(MNXYZ2)   ) ** 2
     %                + ( RMCN(MNXYZ3+1) - RMCN(MNXYZ2+1) ) ** 2
     %                + ( RMCN(MNXYZ3+2) - RMCN(MNXYZ2+2) ) ** 2 )

      TAIARE(3) = SQRT( ( RMCN(MNXYZ1)   - RMCN(MNXYZ3)   ) ** 2
     %                + ( RMCN(MNXYZ1+1) - RMCN(MNXYZ3+1) ) ** 2
     %                + ( RMCN(MNXYZ1+2) - RMCN(MNXYZ3+2) ) ** 2 )

      TAIMAX = MAX( TAIARE(1), TAIARE(2), TAIARE(3) )

C     CALCUL DES 3 COORDONNEES DU BARYCENTRE DU TRIANGLE NOTRQMIN
      CALL BARYEF( 3, MCN(MNS1), RMCN(MNXYZS+WYZSOM), BARY )

C     CALCUL DE LA VISEE DU TRACE
C     LE POINT VISE
      AXOPTV(1) = BARY(1)
      AXOPTV(2) = BARY(2)
      AXOPTV(3) = BARY(3)

C     LA POSITION DE L'OEIL SUR LE VECTEUR NORMAL DU TRIANGLE NOTRQMIN
      TAIMAX = 2 * REAL( TAIMAX + DARETE )
      AXOEIL(1) = AXOPTV(1) + TAIMAX * VECNOR(1)
      AXOEIL(2) = AXOPTV(2) + TAIMAX * VECNOR(2)
      AXOEIL(3) = AXOPTV(3) + TAIMAX * VECNOR(3)

C     DEMI-LARGEUR de la FENETRE
      AXOLAR = TAIMAX
C     DEMI-HAUTEUR de la FENETRE
      AXOHAU = AXOLAR * 0.75

C     PAS DE PLAN ARRIERE ET AVANT
      AXOARR = 0
      AXOAVA = 0
      CALL AXONOMETRIE( AXOPTV, AXOEIL, AXOLAR, AXOHAU, AXOARR, AXOAVA )

      LCRITR = 1
      PREDUF = 5.0
C     TRACE AVEC REMPLISSAGE DE LA FACE
      IAVFAC = 1
C     PALETTE DES QUALITES
      CALL PALCDE( 12 )


C     CONSTRUCTION DES ITEMS VISIBLES SOMMETS et FACES + TRI DISTANCE a l'OEIL
C     TRACER LES FACES DU MAILLAGE EN FIL de FER ou QUALITE ou EN GRIS OMBRE
C     ------------------------------------------------------------------------
 300  IF( LORBITE .LE. 0 ) GOTO 330

C     INITIALISATION DE L'ORBITE ZOOM DEPLACEMENT
      CALL ORBITE0( NOTYEV )
      GOTO 330

C     ORBITE OU ZOOM OU TRANSLATION ACTIFS
 320  CALL ORBITE1( NOTYEV )
      IF( NOTYEV .EQ. 0 ) GOTO 9000

C     LE TRACE EFFECTIF
 330  CALL TRAXE3

C     CONSTRUIRE LES ITEMS VISIBLES SOMMETS et FACES de la SURFACE et
C     TRACER EN 3D LES ITEMS VISIBLES SELON L'ALGORITHME DU PEINTRE
C     DANS LA MEMOIRE PIXELS DE LA FENETRE GRAPHIQUE
C     ---------------------------------------------------------------
      CALL TRITEMV1( MNXYZS, MNNSEF,
     %               L1ARFA, L2ARFA, MCN(MNARFA),  NBAR1F, MCN(MNAR1F),
     %               NBAR2F, MCN(MNAR2F),  NBAR3F, MCN(MNAR3F),
     %               NBAR2SF,MCN(MNAR2SF), NBA2FCOL, MCN(MNA2FCOL),
     %               MXITDI, MNITDI, MXITAN, MNITAN )

C     MISE SUR L'ECRAN DE LA FENETRE GRAPHIQUE DES ITEMS VISIBLES
C     -----------------------------------------------------------
      IF( LANGAG .EQ. 0 ) THEN
      KTITRE='ARETES a PROBLEME :         AretesRouge1Tria &       Arete
     %sMagenta2SurfDiff &         AretesVerte2TriaColles &         Arete
     %sSaumon3Tria'
      ELSE
      KTITRE='EDGES with PROBLEM:         EdgesRed1Tria    &       Edges
     %Magenta2SurfDiff  &         EdgesGreen2GluedTria   &         Edges
     %Salmon3Tria'
      ENDIF
      WRITE( KTITRE(22:28),  '(I7)') NBAR1F
      WRITE( KTITRE(47:53),  '(I7)') NBAR2SF
      WRITE( KTITRE(80:86),  '(I7)') NBA2FCOL
      WRITE( KTITRE(113:119),'(I7)') NBAR3F

      CALL SANSDBL( KTITRE, NBC )
      CALL TRFINS(  KTITRE(1:NBC) )

C     REPRISE EVENTUELLE DE L'ORBITE
C     ------------------------------
      IF( LORBITE .GT. 0 ) GOTO 320

C     SAUVEGARDE DES PIXELS DE LA FENETRE PIXELS MEFISTO
 9000 CALL SAUVEMEMPX

 9999 RETURN
      END
