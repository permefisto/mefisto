      SUBROUTINE SIPTGRAR( KTITRE, PTXYZD, N1FEOC, NFETOI,
     %                     NBFETO, XYZMAX, MXVPSI, NBVPSI, NFVPSI,
     %                     QUAMIMX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    SIMULER LA TETRAEDRISATION DES FACES SIMPLES DE L'ETOILE
C -----    NFETOI ISSUES DE N1FEOC A PARTIR D'UN POINT XYZ CALCULE
C          SUR UNE GRILLE 3D CENTRE AU MILIEU DE LA PLUS GRANDE ARETE
C          DES FACES DE L'ETOILE POUR MAXIMISER LE NOMBRE DE TETRAEDRES
C          FACE+XYZ DE VOLUMES POSITIFs ET SANS INTERSECTION AVEC LES FACES

C ENTREES:
C --------
C KTITRE : TITRE DU TRACE
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : LES FACES TRIANGULAIRES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR
C             DE L'ETOILE
C          5: CHAINAGE SUR LA FACE SUIVANTE OCCUPEE OU VIDE
C MXVPSI : NOMBRE MAXIMAL DE FACES DU TABLEAU NFVPSI et NFVPSI0

C SORTIES:
C --------
C NBFETO : NOMBRE ACTUEL  DE FACES DE L'ETOILE ISSUE DE N1FEOC
C XYZMAX : POINT CHOISI POUR FORMER AVEC DES FACES DE L'ETOILE DES TETRAEDRES
C          MAXIMISANT LE NOMBRE DE TETRAEDRES FACE+XYZMAx A V>0
C NBVPSI : NOMBRE DE FACES DES TETRAEDRES DE VOLUME>0 SANS INTERSECTION
C          AVEC LES FACES
C NFVPSI : NUMERO NFETOI DES NBVPSI FACES DONNANT AVEC XYZMAX UN VOLUME>0
C          ET SANS INTERSECTION AVEC LES FACES DE L'ETOILE
C QUAMIMX: QUALITE MINIMALE DES NBVPSI FUTURS TETRAEDRES A VOLUME>0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint PIERRE du PERRAY          Janvier 2020
C2345X7..............................................................012
ccc      include"./incl/darete.inc"
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE, TRACTE0

      DOUBLE PRECISION  PTXYZD(4,*), XYZ(3), XYZMIL(3), XYZPAS(3),
     %                  XYZMAX(3), XYZMIX(3,2), D, XYZBE(3),
     %                  AREMAX, VECNOR1(3), VECNOR2(3)

      INTEGER           NFETOI(5,*), NFVPSI(MXVPSI)
      REAL              QUAMIN, QUAMIMX
      CHARACTER*(*)     KTITRE

      TRACTE0 = TRACTE
      LORBITE = 1

cccC     TRACE DES FACES DE L'ETOILE ET DU POINT XYZ
ccc      KTITRE='Entree siptgrar:        FACES de NFETOI'
ccc      WRITE(KTITRE(17:21),'(I5)') NBFETO
ccc      CALL SANSDBL( KTITRE, L )
ccc      PRINT*, KTITRE(1:L)
ccc      CALL TRFETO3( KTITRE(1:L), XYZ, XYZ, PTXYZD, N1FEOC, NFETOI,
ccc     %              0, NFVPSI )

      NFVPSIMAX = 0
      QUAMIMX   = -2.0

C     RECHERCHE DE L'ARETE DES FACES DE L'ETOILE DE LONGUEUR MAXIMALE
      CALL ARMXFAET( PTXYZD, N1FEOC, NFETOI,
     %               NBFETO, AREMAX, NS1MAX, NS2MAX, NF1MAX )

C     RECHERCHE DE L'AUTRE FACE NFETOI D'ARETE NS1MAX-NS2MAX
      NF1 = N1FEOC
 10   IF( NF1 .GT. 0 ) THEN

         IF( NF1 .EQ. NF1MAX ) GOTO 15

C        PARCOURS DES ARETES DE LA FACE NFETOI
         NS1 = NFETOI(4,NF1)
         DO K = 1, 3
            NS2 = ABS( NFETOI(1+K,NF1) )
            IF( NS2 .EQ. NS1MAX .AND. NS1 .EQ. NS2MAX ) THEN
               NF2MAX = NF1
               GOTO 20
            ENDIF
            NS1 = NS2
         ENDDO

C        LA FACE SUIVANTE
 15      NF1 = NFETOI(5,NF1)
         GOTO 10

      ENDIF

 20   IF( NF1MAX .GT. 0 ) GOTO 25
C     SAUT DU CALCUL INUTILE DES VECTEURS NORMAUX


C     NF1MAX et NF2MAX LES 2 FACES NFETOI DE L'ARETE DE LONGUEUR MAX
C     CALCUL DU VECTEUR NORMAL UNITE A LA FACE NF1MAX DE NFETOI
      CALL VECNOR3D( PTXYZD(1,ABS(NFETOI(2,NF1MAX))),
     %               PTXYZD(1,    NFETOI(3,NF1MAX) ),
     %               PTXYZD(1,    NFETOI(4,NF1MAX) ),
     %               VECNOR1 )
C     CARRE DE LA LONGUEUR DU VECTEUR NORMAL
      D = VECNOR1(1)**2 + VECNOR1(2)**2 + VECNOR1(3)**2
      IF( D .LE. 0D0 ) THEN
C        LE TRIANGLE NFETOI EST DEGENERE
         PRINT*,'siptgrar: FACE NFETOI(',NF1MAX,'):',
     %          (NFETOI(K,NF1MAX),K=1,5),' DEGENEREE'
         NBVPSI = 0
         GOTO 9999
      ENDIF
C     LE VECTEUR NORMAL UNITAIRE (NORME 1) A LA FACE NF1MAX DE NFETOI
      D = SQRT( D )
      DO K=1,3
         VECNOR1(K) = VECNOR1(K) / D
      ENDDO

C     CALCUL DU VECTEUR NORMAL UNITE A LA FACE NF2MAX DE NFETOI
      CALL VECNOR3D( PTXYZD(1,ABS(NFETOI(2,NF2MAX))),
     %               PTXYZD(1,    NFETOI(3,NF2MAX) ),
     %               PTXYZD(1,    NFETOI(4,NF2MAX) ),
     %               VECNOR2 )
C     CARRE DE LA LONGUEUR DU VECTEUR NORMAL
      D = VECNOR2(1)**2 + VECNOR2(2)**2 + VECNOR2(3)**2
      IF( D .LE. 0D0 ) THEN
C        LE TRIANGLE NFETOI EST DEGENERE
         PRINT*,'siptgrar: FACE NFETOI(',NF2MAX,'):',
     %          (NFETOI(K,NF2MAX),K=1,5),' DEGENEREE'
         NBVPSI = 0
         GOTO 9999
      ENDIF
C     LE VECTEUR NORMAL UNITAIRE (NORME 1) A LA FACE NF2MAX DE NFETOI
      D = SQRT( D )
      DO K=1,3
         VECNOR2(K) = VECNOR2(K) / D
      ENDDO

C     CALCUL DU VECTEUR NORMAL MOYEN AUX 2 FACES
      DO K=1,3
         VECNOR1(K) = ( VECNOR1(K) + VECNOR2(K) ) / 2
      ENDDO


C     CHOIX: XYZ UN SOMMET D'UNE GRILLE CUBIQUE AUTOUR DU
C            MILIEU DE LA PLUS LONGUE ARETE DE L'ETOILE NFETOI
C     --------------------------------------------------------
 25   DO K=1,3
         XYZMIL(K) = ( PTXYZD(K,NS1MAX) + PTXYZD(K,NS2MAX) ) / 2
      ENDDO

C     CALCUL DES COORDONNEES EXTREMES ET DU POINT XYZBE DU BARYCENTRE
C     DES FACES N1FEOC A 0 DE L'ETOILE DEFINIE DANS NFETOI
C     ---------------------------------------------------------------
      CALL XYZBF10( PTXYZD, N1FEOC, 0, NFETOI, NBFETO, XYZMIX, XYZBE )


C     GRILLE DE POINTS AUTOUR DU BARYCENTRE DES NBFETO FACES NFETOI
C     =============================================================
C     LES PAS XYZPAS ENTRE SOMMETS DE LA GRILLE
      NBSUB = 3
      DO K=1,3
         XYZPAS(K) = AREMAX / 20 / NBSUB
      ENDDO

C     METHODE DU MAXIMUM DE FACE+XYZ DE VOLUME>0 SANS INTERSECTION ET DE
C     MAXIMUM DU MINIMUM DES QUALITES DES TETRAEDRES NF+XYZ
C     ------------------------------------------------------------------
      MXFPVP = 0
      DO N = -NBSUB, NBSUB, 1
         XYZ(3) = XYZMIL(3) + N * XYZPAS(3)
         DO M = -NBSUB, NBSUB, 1
            XYZ(2) = XYZMIL(2) + M * XYZPAS(2)
            DO 30 L = -NBSUB, NBSUB, 1

C              EVITER LE POINT MILIEU DE L'ARETE MAX
               IF( L.EQ.0 .AND. M.EQ.0 .AND. N.EQ.0 ) GOTO 30

               XYZ(1) = XYZMIL(1) + L * XYZPAS(1)

C              DETERMINER LE NOMBRE MAXIMAL DE TETRAEDRES XYZ+FACES
C              SIMPLES DE VOLUME POSITIF et SANS INTERSECTION AVEC
C              LES AUTRES FACES SIMPLES DE L'ETOILE
               CALL FAPTVPSI( XYZ,    PTXYZD, N1FEOC, NFETOI,
     %                        MXVPSI, NBVPSI, NFVPSI,
     %                        NBFETO, NBVPIN, QUAMIN )

               IF( NBVPSI .GT. MXFPVP ) THEN
                  MXFPVP    = NBVPSI
                  QUAMIMX   = QUAMIN
                  XYZMAX(1) = XYZ(1)
                  XYZMAX(2) = XYZ(2)
                  XYZMAX(3) = XYZ(3)
                  NFVPSIMAX = NFVPSI(1)
               ELSE IF( NBVPSI.EQ.MXFPVP .AND. QUAMIN.GT.QUAMIMX ) THEN
                  QUAMIMX   = QUAMIN
                  XYZMAX(1) = XYZ(1)
                  XYZMAX(2) = XYZ(2)
                  XYZMAX(3) = XYZ(3)
                  NFVPSIMAX = NFVPSI(1)
               ENDIF

 30         ENDDO
         ENDDO
      ENDDO

C     LE MEILLEUR RESULTAT AVEC LE MAXIMUM DE FACES+XYZMAX A V>0
      NBVPSI = MXFPVP

C     RECALCULER LE TABLEAU NFVPSI POUR LE POINT XYZMAX CAR LE
C     TABLEAU A PEUT ETRE ETE ECRASE PAR LES POINTS SUIVANTS de XYZMAX
      CALL FAPTVPSI( XYZMAX, PTXYZD, N1FEOC, NFETOI,
     %               MXVPSI, NBVPSI, NFVPSI,
     %               NBFETO, NBVPIN, QUAMIMX )

      IF( NBVPSI .EQ. NBFETO ) THEN

C        XYZMAX TETRAEDRISE TOUTES LES FACES DE NFETOI
C        =============================================
       KTITRE='siptgrar: TOUTES LES        FACES+XYZMAX->TETRAEDRES V>0'
         WRITE(KTITRE(22:26), '(I5)' ) NBFETO

      ELSE

       KTITRE='siptgrar: PARMI les       FACES seules       FACES+XYZMAX
     % -> TETRAEDRES V>0'
         WRITE(KTITRE(20:24), '(I5)' ) NBFETO
         WRITE(KTITRE(39:43), '(I5)' ) NBVPSI

      ENDIF

C     TRACE DES FACES DE L'ETOILE ET DU POINT XYZ
      CALL SANSDBL( KTITRE, L )
      PRINT*, KTITRE(1:L)
      CALL TRFETO3( KTITRE(1:L), XYZMAX, XYZMAX, PTXYZD,
     %              N1FEOC, NFETOI, NBVPSI, NFVPSI )

 9999 TRACTE = TRACTE0
      RETURN
      END
