      SUBROUTINE ETOIPT21( N,        QTEDEG, NOTET1,  PTXYZD,  NPSOFR,
     %                     HEXAPAVE, NBIPAV, ECHPAV,  N1SPAVE, NOPTSUIV,
     %                     MXTETR,   NOTETR, NUDTETR, N1TETS,  NOVOLT,
     %                     MXFACO,   LEFACO,
     %                     MXETOI,   N1FEOC, N1FEVI,  NFETOI,
     %                     NBTEET,   NTETOI, VOLET0 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CONSTRUCTION DE L'ETOILE DES TETRAEDRES CONTENANT LE POINT N
C -----    UN PAVAGE DES SOMMETS DES TETRAEDRES EST DISPONIBLE
C          DE PLUS LE POINT N PEUT ETRE SUPPRIME

C ENTREES :
C ---------
C N      : NUMERO DU POINT A TETRAEDRISER DE COORDONNEES DANS PTXYZD
C QTEDEG : QUALITE MINIMALE POUR DIRE QU'UN TETRAEDRE EST SUPPOSE DEGENERE
C          INITIALEMENT, C'EST LE DIZIEME DE CELUI DU PLUS MAUVAIS TRIANGLE
C NOTET1 : NUMERO DU TETRAEDRE DE DEBUT DE RECHERCHE
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NPSOFR : NUMERO DES POINTS INITIAUX
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR UNE SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -3 SI LE POINT EST SOMMET NON SUPPRIMABLE REFUSE DANS TETRAEDRISATION
C          = -4 SI LE POINT EST SOMMET DE LA GRILLE REGULIERE SUPPRIMABLE
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I
C ECHPAV  : ECHELLE DANS LA DIRECTION I
C N1SPAVE : NO DU 1-ER SOMMET DANS PTXYZD DU PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: PLUS GRAND NUMERO DANS NOTETR DE TETRAEDRE OCCUPE
C N1TETS : NUMERO DANS NOTETR D'UN TETRAEDRE DE CHAQUE SOMMET
C NOVOLT : NO DU VOLUME DU TETRAEDRE, 0 SI EXTERIEUR OU INCONNU
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C MXETOI : MAXIMUM DECLARE DANS LES TABLEAUX NTETOI ET NFETOI
C
C ENTREES ET SORTIES :
C --------------------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : 1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3  NON UTILISE ICI
C          4: NUMERO DE CETTE FACE DANS LEFACO, 0 SI PAS DANS LEFACO
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C SORTIES :
C ---------
C NBTEET : NOMBRE DE TETRAEDRES DE L'ETOILE DU POINT N
C NTETOI : NUMERO DANS NOTETR DES NBTEET TETRAEDRES DE L'ETOILE
C VOLET0 : LE VOLUME DE L'ETOILE FINALE DU POINT N
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC PARIS & VEULETTES SUR MER AOUT 2014
C23456...............................................................012
      PARAMETER       ( MXSMIN=1000 )
      DOUBLE PRECISION  EPSDEG
      PARAMETER        (EPSDEG=1D-10, MXITER=16)
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      INTEGER           NOSMIN(MXSMIN), NODSNO(MXSMIN), MINTET(MXSMIN)
C
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE
C     STOPTE = FAUX ==> PAS D'ARRET APRES LE TRACE DE CHAQUE TETRAEDRE
C              VRAI ==> DEMANDE D'UN CARACTERE POUR REDEMARRER
C     TRACTE = FAUX ==> PAS DE TRACE DES TETRAEDRES
C              VRAI ==> TRACE DES TETRAEDRES DE L'ETOILE

      DOUBLE PRECISION  DISTMIN(MXSMIN)
      INTEGER           NOTETR(8,MXTETR),N1TETS(*),NTETOI(MXETOI),
     %                  NFETOI(5,MXETOI),NPSOFR(*),NOVOLT(*)
      INTEGER           LEFACO(1:11,0:MXFACO)
      INTEGER           NBIPAV(3), N1SPAVE(0:*), NOPTSUIV(1:*)
      DOUBLE PRECISION  HEXAPAVE(3,2), ECHPAV(3)
      DOUBLE PRECISION  PTXYZD(4,*)
      DOUBLE PRECISION  V0, V, VOLTET, VOLET0, VOLET1,
     %                  COBARY(4), CBMAX, CBMIN, DMIN, D
      CHARACTER*80      KTITRE
      INTEGER           NS(4)
      EQUIVALENCE      (NS(1),NS1),(NS(2),NS2),
     %                 (NS(3),NS3),(NS(4),NS4)
      INTEGER           NOSOFATE(3,4)
C     NO DES SOMMETS DES FACES POUR QUE VU DU SOMMET MANQUANT
C     LES SOMMETS SOIENT VUS DANS LE SENS DIRECT
      DATA              NOSOFATE/ 1,2,3, 2,4,3, 3,4,1, 4,2,1 /

ccc      if( N .EQ. 462 ) then
ccc         print *,'etoipt21: POINT',N
ccc         TRACTE = .TRUE.
ccc      endif
C
C     PERMETTRE DES TETRAEDRES ECRASES : QUITTE A LES TRAITER ENSUITE
      QTEDEG = 0
      NBTEET = 0
      NBREPA = -1
C
C     REINITIALISATION A VIDE DES FACES OCCUPEES DE NFETOI
C     ====================================================
      CALL ETOIOCVI( N1FEOC, N1FEVI, NFETOI )
      LOCTAE = 0
C
C     RETROUVER LES PLUS PROCHES TETRAEDRES DU POINT N RANGES DANS LES PAVES
C     ======================================================================
      CALL DMINTETRAPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE,NOPTSUIV,
     %                   PTXYZD(1,N), PTXYZD,
     %                   MXSMIN, NBSMIN, NOSMIN, NODSNO, DISTMIN,
     %                   N1TETS, NBTEET, MINTET )
      IF( NBTEET .LE. 0 ) GOTO 15

      NUTDMIN = 0
C
C     RETROUVER UN TETRAEDRE CONTENANT LE POINT N
C     ===========================================
 10   IF( NUTDMIN .LT. NBTEET ) THEN
         NUTDMIN = NUTDMIN + 1
         NOTET1 = MINTET( NUTDMIN )

CCC         WRITE(IMPRIM,*) 'ETOIPT21: POINT ',N,
CCC     %                   ' REPRISE ETOIL2 AVEC NOTET1=',NOTET1

C        LE POINT N EST IL DANS LE TETRAEDRE NOTET1?

C        CALCUL DE LA COORDONNEE BARYCENTRIQUE MINIMALE DU POINTN DANS NOTET1
         CALL PTDSTE( PTXYZD(1,N), PTXYZD, NOTETR(1,NOTET1),
     %                NONOUI, V0, COBARY )

C        LE VOLUME EST IL NUL OU NEGATIF?
         IF( NONOUI .LE. 0 ) THEN
C           LE VOLUME DE NOTET1 EST NUL OU
C           LE POINTN N'EST PAS DANS LE TETRAEDRE NOTET1
            GOTO 10
         ENDIF

C        LE TETRAEDRE NOTET1 A-T-IL UN SOMMET TROP PROCHE DU POINT N?
         CBMAX = MAX( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
         IF( ABS( 1D0 - CBMAX ) .LE. 1D-3 ) THEN
            NSMIN = 0
            DMIN  = 1D111
            DO I = 1, 4
               NOS = NOTETR(I,NOTET1)
               IF( NOS .LE. 0 ) THEN
                  GOTO 10
               ENDIF
               D = ( PTXYZD(1,N) - PTXYZD(1,NOS) )**2
     %           + ( PTXYZD(2,N) - PTXYZD(2,NOS) )**2
     %           + ( PTXYZD(3,N) - PTXYZD(3,NOS) )**2
               IF( D .LT. DMIN ) THEN
                  DMIN  = D
                  NSMIN = NOS
               ENDIF
            ENDDO
C
C           LE POINT NSMIN EST LE PLUS PROCHE DE PTXYZD(NOTETR(IMIN,NOTET1))
            IF( DMIN .LT. (PTXYZD(4,NSMIN)*0.001D0)**2 ) THEN

C              POINT N IDENTIFIE AU SOMMET NSMIN : A ABANDONNER SI POSSIBLE
               IF( NPSOFR(N) .GT. 0 ) THEN
               WRITE(IMPRIM,*)
               WRITE(IMPRIM,*) 'ETOIPT21: POINT',N,' NPSOFR=',NPSOFR(N),
     %                   ' TROP PROCHE D''UN SOMMET DU TETRAEDRE',NOTET1
               WRITE(IMPRIM,*) 'COOR BARYCENTRIQUES=',COBARY
               WRITE(IMPRIM,*) 'POINT PTXYZD',N,'=',PTXYZD(1,N),
     %                      PTXYZD(2,N),PTXYZD(3,N),PTXYZD(4,N),
     %                    ' NPSOFR=',NPSOFR(N)
               NNS = NOTETR(1,NOTET1)
               WRITE(IMPRIM,*) 'ST1: PTXYZD',NNS,'=',PTXYZD(1,NNS),
     %                    PTXYZD(2,NNS),PTXYZD(3,NNS),PTXYZD(4,NNS)
               NNS = NOTETR(2,NOTET1)
               WRITE(IMPRIM,*) 'ST2: PTXYZD',NNS,'=',PTXYZD(1,NNS),
     %                    PTXYZD(2,NNS),PTXYZD(3,NNS),PTXYZD(4,NNS)
               NNS = NOTETR(3,NOTET1)
               WRITE(IMPRIM,*) 'ST3: PTXYZD',NNS,'=',PTXYZD(1,NNS),
     %                    PTXYZD(2,NNS),PTXYZD(3,NNS),PTXYZD(4,NNS)
               NNS = NOTETR(4,NOTET1)
               WRITE(IMPRIM,*) 'ST4: PTXYZD',NNS,'=',PTXYZD(1,NNS),
     %                    PTXYZD(2,NNS),PTXYZD(3,NNS),PTXYZD(4,NNS)
               ENDIF
C              CODE DE POINT TROP PROCHE D'UN SOMMET. A SUPPRIMER
               NBTEET = -100
               GOTO 20
            ENDIF
         ENDIF

C        LE TETRAEDRE NOTET1 CONTIENT LE POINT N
C        ET CE POINT EST ASSEZ ELOIGNE DE SES 4 SOMMETS
         NBTEET = 1
         NTETOI(1) = NOTET1
         GOTO 18

      ENDIF

C     AUCUN DES TETRAEDRES PROCHES DU POINT CONTIENT LE POINT N
C     RECHERCHE PARMI LES TETRAEDRES NUDTETR A 1 UN TETRAEDRE NOTET1
C     CONTENANT LE POINT NOPOINT
C     ==============================================================
 15   NBREPA = NBREPA + 1
      CALL ETOIL1TET( N, PTXYZD(1,N), PTXYZD, NUDTETR, NOTETR,
     %                NOTET1, COBARY )

 18   IF( NOTET1 .GT. 0 ) THEN

C        LE POINT N EST IL SUR UNE ARETE DU TETRAEDRE NOTET1?
C        NOTAMMENT CE POURRAIT ETRE UN MILIEU D'ARETE AJOUTE
         NBL0 = 0
         DO I=1,4
            IF( ABS( COBARY(I) ) .LE. 1D-5 ) THEN
               NBL0 = NBL0 + 1
C              LE NUMERO DE LA COORDONNEE BARYCENTRIQUE NULLE
               NTETOI(NBL0) = I
            ELSE
               IMIN = I
            ENDIF
         ENDDO

         IF( NBL0 .EQ. 2 ) THEN

C           POINT MILIEU D'ARETE: LES TETRAEDRES QUI PARTAGENT CETTE
C                                 ARETE SONT AJOUTES A L'ETOILE
C           --------------------------------------------------------
C           NTETOI(3:4) LES NUMEROS DES 2 SOMMETS DE L'ARETE
            IF( NTETOI(1) .EQ. 1 ) THEN
               IF( NTETOI(2) .EQ. 2 ) THEN
                  NTETOI(3) = 2
                  NTETOI(4) = 3
               ELSE IF( NTETOI(2) .EQ. 3 ) THEN
                  NTETOI(3) = 1
                  NTETOI(4) = 3
               ELSE IF( NTETOI(2) .EQ. 4 ) THEN
                  NTETOI(3) = 1
                  NTETOI(4) = 2
               ENDIF
            ELSE IF( NTETOI(1) .EQ. 2 ) THEN
               IF( NTETOI(2) .EQ. 3 ) THEN
                  NTETOI(3) = 3
                  NTETOI(4) = 4
               ELSE IF( NTETOI(2) .EQ. 4 ) THEN
                  NTETOI(3) = 2
                  NTETOI(4) = 4
               ENDIF
            ELSE
               NTETOI(3) = 1
               NTETOI(4) = 4
            ENDIF

            CALL TETR1A( NOTETR(NTETOI(3),NOTET1),
     %                   NOTETR(NTETOI(4),NOTET1),
     %                   N1TETS, NOTETR, NBTEET, MXETOI, NTETOI, IERR )

         ELSE IF( NBL0 .EQ. 3 ) THEN

C           POINT N EST UN SOMMET
C           ---------------------
C           IL EST SUPPRIME SI POSSIBLE
            NBTEET = -100
            GOTO 20

         ELSE

C           POINT N INTERNE OU SUR UNE FACE
C           -------------------------------
            NBTEET = 1
            NTETOI(1) = NOTET1

         ENDIF

      ELSE
         NBTEET = -1
      ENDIF

C     LE NOMBRE DE TETRAEDRES CONTENANT N EST IL CORRECT?
C     ---------------------------------------------------
 20   IF( NBTEET .LE. 0 ) THEN
C
C        LE POINT N A UN PROBLEME: PEUT IL ETRE SUPPRIME?
         IF( NPSOFR(N) .EQ. -4 .OR. NPSOFR(N) .EQ. -3 .OR.
     %       NPSOFR(N) .EQ. 0 ) THEN
C           OUI: IL EST SUPPRIME
            NBTEET = -1
         ELSE
C           NON: LE POINT N NE PEUT ETRE SUPPRIME
            IF( NBTEET .EQ. -100 ) THEN
C              POINT N TROP PROCHE D'UN SOMMET DE LA TETRAEDRISATION
               IF( NBREPA .LE. 0 ) GOTO 15
               WRITE(IMPRIM,*) 'ETOIPT21: POINT ',N,' SANS ETOILE',
     %          ' NPSOFR(N)=',NPSOFR(N),' PTXYZD=',(PTXYZD(I,N),I=1,4)
               WRITE(IMPRIM,*) 'dans TETRAEDRE',NOTET1,':',
     %                         (NOTETR(kk,NOTET1),kk=1,4),
     %                        ' COBARY=',COBARY
               WRITE(IMPRIM,*) 'ETOIPT21: LE POINT ',N,
     %      ' NON SUPPRIMABLE EST TROP PROCHE D''UN SOMMET DE TETRAEDRE'
            ENDIF
         ENDIF
         RETURN
      ENDIF

C     TOUS LES TETRAEDRES DE L'ETOILE DOIVENT ETRE DANS UN MEME VOLUME
C     SAUF POUR L'ETOILE INITIALE DES TETRAEDRES CONTENANT LE POINT N
C     SINON IL Y AURAIT DISCONTINUITE DU MAILLAGE NOTAMMENT SI LE POINT N
C           SE TROUVE SUR UNE ARETE OU UNE FACE DU 1-ER TETRAEDRE
      NUVOL = NOVOLT( NTETOI(1) )

C     FORMATION DE L'ETOILE INITIALE CONSTITUEE DES 4 FACES DES NBTEET
C     TETRAEDRES CONTENANT LE POINT N
C     TETRAEDRE UNIQUE ou 2 TETRAEDRES AVEC LE POINT N SUR LA FACE 
C     ou NBTEET TETRAEDRES D'ARETE COMMUNE CONTENANT LE POINT N
C     CONSTRUCTION DES FACES SIMPLES DE L'ETOILE INITIALE DU POINT N
C     ================================================================
      VOLET0 = 0D0

      DO 30 I = 1, NBTEET

C        LE TETRAEDRE A AJOUTER
         NT = NTETOI( I )
         IF( NT .LE. 0 ) GOTO 30
         IF( NOTETR( 1, NT ) .LE. 0 ) THEN
            PRINT *,'ETOIPT21: ERREUR POINT',N,' TETRAEDRE',NT,':',
     %              (NOTETR(MM,NT),MM=1,4),' INCORRECT'
            NBTEET = 0
            RETURN
         ENDIF

         IF( NOVOLT( NT ) .NE. NUVOL ) GOTO 30

C        CALCUL DES COORDONNEES BARYCENTRIQUES DU POINT N DANS NT
         CALL COBATE( PTXYZD(1,N), PTXYZD, NOTETR(1,NT),
     %                V0, COBARY, IERR )

C        VOLUME DU TETRAEDRE NT
         IF( V0 .LT. 0D0 ) THEN
            IF( I.EQ.1 .AND. NBTEET.EQ.1 ) THEN
               IF( NBREPA .LE. 0 ) THEN
                  GOTO 15
               ENDIF
            ENDIF
            PRINT *,'ETOIPT21: POINT',N,' NBTEET=',NBTEET,
     %              ' ETOILE INITIALE avec le TETRAEDRE',NT,':',
     %              (NOTETR(MM,NT),MM=1,4),' de VOLUME=',V0,
     %              '<=0  MAIS ON CONTINUE...'
            NTETOI(I) = -NTETOI(I)
            GOTO 30
         ENDIF

C        VOLUME ABSOLU DE L'ETOILE
         VOLET0 = VOLET0 + ABS( V0 )

C        LE TETRAEDRE NT A-T-IL UN SOMMET TROP PROCHE DU POINT N?
         CBMIN = MIN( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
         CBMAX = MAX( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
         IF( CBMIN .GE. 0D0 .AND. ABS( 1D0 - CBMAX ) .LE. 1D-3 ) THEN
            NOTET1 = NT
            NBTEET = -100
            GOTO 20
         ENDIF

C        AJOUT DES 4 FACES DU TETRAEDRE NT DE L'ETOILE INITIALE
C        SANS TEST SUR LE VOLUME DES FUTURS TETRAEDRES DE SOMMET N
         DO J=1,4
C           AJOUT DE LA FACE J DU TETRAEDRE NT
            CALL AJFAET( NT,  J, NOTETR, MXFACO, LEFACO,
     %                   N1FEOC, N1FEVI, NFETOI, NFA )
         ENDDO

 30   CONTINUE

      NBTEVV = 0
      DO I = 1, NBTEET
         NT = NTETOI(I)
         IF( NT .GT. 0 ) THEN
            NBTEVV = NBTEVV + 1
            NTETOI( NBTEVV ) = NT
         ENDIF
      ENDDO

      IF( NBTEVV .LE. 0 ) THEN
         PRINT *,'ETOIPT21: POINT N=',N,' NBTEET=',NBTEET,
     %   ' NOMBRE DE TETRAEDRES VIVANTS DE L''ETOILE INITIALE=',NBTEVV
         IF( NBREPA .LE. 1 ) THEN
            GOTO 15
         ELSE
            NBTEET = 0
            RETURN
         ENDIF
      ENDIF
      NBTEET = NBTEVV
C     TRACER LES ARETES DES NBTEET TETRAEDRES DU TABLEAU NTETOI
      KTITRE='etoipt21: POINT           ETOILE de          TETRAEDRES'
      WRITE(KTITRE(17:29),'(I8)') N
      WRITE(KTITRE(37:44),'(I8)') NBTEET
      CALL TRACEETOILE( KTITRE, PTXYZD, NOTETR, N, NBTEET, NTETOI ) 

C     EXPANSION DE L'ETOILE PAR LE TETRAEDRE OPPOSE AUX FACES SIMPLES
C     SI LE POINT N VOIT SES 3 AUTRES FACES ET FORME DES TETRAEDRES
C     DE VOLUME POSITIF
C     ===============================================================
 40   NF1 = N1FEOC
      GOTO 48

C     NO FACE SIMPLE SUIVANTE DANS NFETOI
 44   NF1 = NFETOI(5,NF1)

 48   IF( NF1 .GT. 0 ) THEN

C        SON NO DE FACE DANS LE TETRAEDRE INTERNE A L'ETOILE
C        UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE DANS L'ETOILE
         NFI = NFETOI(2,NF1)
         IF( NFI .LE. 0 ) GOTO 44

C        LA FACE EST TRAITEE CI APRES ET DONC MARQUEE POUR LA SUITE
         NFETOI(2,NF1) = -NFI
C
C        NUMERO NOTETR DU TETRAEDRE INTERNE DE LA FACE SIMPLE
         NT = NFETOI(1,NF1)
         IF( NT .LE. 0 ) GOTO 44
         IF( NOTETR(1,NT) .LE. 0 ) THEN
          print *,'etoipt21 1: Point N=',N,' XYZ=',(PTXYZD(MM,N),MM=1,3)
          print *,'ERREUR: NOTETR(',NT,')=',(NOTETR(MM,NT),MM=1,8)
            GOTO 44
         ENDIF

C        NTOP LE TETRAEDRE OPPOSE PAR LA FACE NFI
         NTOP = NOTETR( 4+NFI, NT )
         IF( NTOP .LE. 0 ) GOTO 44
         IF( NOTETR(1,NTOP) .LE. 0 ) THEN
          print *
          print *,'etoipt21 2: Point N=',N,' XYZ=',(PTXYZD(MM,N),MM=1,3)
          print *,'Face',J,' du NOTETR(',NT,')=',(NOTETR(MM,NT),MM=1,8)
          print *,'ERREUR NOTETR(',NTOP,')=',(NOTETR(MM,NTOP),MM=1,8)
            GOTO 44
         ENDIF

C        TOUS LES TETRAEDRES DE L'EXPANSION DE L'ETOILE DOIVENT ETRE
C        DANS UN MEME VOLUME POUR EVITER LA PERTE DE FACES FRONTALIERES
         IF( NOVOLT(NTOP) .NE. NUVOL ) GOTO 44

C        NO DANS LEFACO DE LA FACE SIMPLE, =0 SI NON DANS LEFACO
         NFLE = NFETOI(4,NF1)
         IF( NFLE .NE. 0 ) GOTO 44

C        NFOP NUMERO LOCAL DE LA FACE DANS LE TETRAEDRE OPPOSE
         IF( NOTETR(5,NTOP) .EQ. NT ) THEN
            NFOP = 1
         ELSE IF( NOTETR(6,NTOP) .EQ. NT ) THEN
            NFOP = 2
         ELSE IF( NOTETR(7,NTOP) .EQ. NT ) THEN
            NFOP = 3
         ELSE
            NFOP = 4
         ENDIF

C        CALCUL DES COORDONNEES BARYCENTRIQUES DU POINT N DANS NTOP
         CALL COBATE( PTXYZD(1,N), PTXYZD, NOTETR(1,NTOP),
     %                V0, COBARY, IERR )

C        V0 VOLUME DU TETRAEDRE NTOP OPPOSE
         IF( V0 .LE. 0D0 ) GOTO 44

C        LE TETRAEDRE NTOP A-T-IL UN SOMMET TROP PROCHE DU POINT N?
         CBMIN = MIN( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
         CBMAX = MAX( COBARY(1), COBARY(2), COBARY(3), COBARY(4) )
         IF( CBMIN .GE. 0D0 .AND. ABS( 1D0 - CBMAX ) .LE. 1D-3 ) THEN
            NOTET1 = NTOP
            NBTEET = -100
            GOTO 20
         ENDIF

C        VOLUME DE LA NOUVELLE ETOILE AVEC LE TETRAEDRE NTOP
         VOLET1 = VOLET0

C        LES 4 FACES DU TETRAEDRE NTOP SONT RELIEES AU POINT N
C        RETRAIT DU VOLUME DU A LA FACE SIMPLE
C        AJOUT DU VOLUME DU AUX 3 NOUVELLES FACES
         DO J=1,4

C           AJOUT DU VOLUME DU TETRAEDRE FACE J-POINT N DU TETRAEDRE NTOP
C           NUMERO DES 3 SOMMETS DE LA FACE J
            NS1 = NOTETR( NOSOFATE(1,J), NTOP )
            NS2 = NOTETR( NOSOFATE(2,J), NTOP )
            NS3 = NOTETR( NOSOFATE(3,J), NTOP )
            NS(4) = N
            V = VOLTET( PTXYZD(1,NS1), PTXYZD(1,NS2),
     %                  PTXYZD(1,NS3), PTXYZD(1,N) )

            IF( J .NE. NFOP ) THEN

C              UNE DES 3 FACES DIFFERENTES DE CELLE DU TETRAEDRE OPPOSE
               IF( V .LE. 0D0 ) THEN
C                 LA FACE J-POINT N SERAIT UN TETRAEDRE DEGENERE
C                 QUI CREERAIT UNE FACE DE PLAN CONTENANT LE POINT N
                  GOTO 44
               ENDIF
C              ON AJOUTE LE VOLUME V QUI EST NEGATIF SI FACE INCORRECTE
               VOLET1 = VOLET1 + ABS( V )

            ELSE

C              LA FACE DU TETRAEDRE OPPOSE
C              SI VOLUME=0 ON AJOUTE LE TETRAEDRE OPPOSE
C              POUR SUPPRIMER CETTE FACE
               IF( V .EQ. 0D0 ) THEN
C                 LE POINT N EST SUR LE PLAN DE LA FACE
C                 IL NE FAUT PAS AJOUTER LE TETRAEDRE OPPOSE
                  GOTO 44
               ENDIF
C              ON RETIRE LE VOLUME V QUI NORMALEMENT DOIT ETRE <0
               VOLET1 = VOLET1 - ABS( V )

            ENDIF

         ENDDO

C        NOUVEAU VOLUME DE L'ETOILE ANCIENNE + TETRAEDRE NTOP
         V      = VOLET0
         VOLET0 = VOLET0 + ABS( V0 )

C        LES 2 VOLUMES DE L'ETOILE AVANT APRES DE L'AJOUT NTOP SONT ILS EGAUX?
C        ---------------------------------------------------------------------
         IF( ABS(VOLET1-VOLET0) .GT. ABS(VOLET0)*1D-6 ) THEN

ccc            WRITE(IMPRIM,*)'ETOIPT21: DIFFERENCE DE VOLUMES AU POINT',N,
ccc     %                  ' VOLET0=',VOLET0,' VOLET1=',VOLET1

C           LE TETRAEDRE NTOP N'EST PAS AJOUTE A L'ETOILE DU POINT N
            VOLET0 = V
            GOTO 44
         ENDIF

C        LES 4 FACES DU TETRAEDRE NTOP SONT AJOUTEES A L'ETOILE DU POINT N
C        -----------------------------------------------------------------
         DO J=1,4
C           AJOUT DE LA FACE J DU TETRAEDRE NTOP
            CALL AJFAET( NTOP,   J,      NOTETR, MXFACO, LEFACO,
     %                   N1FEOC, N1FEVI, NFETOI, NFA )
         ENDDO

C        LE TETRAEDRE NTOP EST AJOUTE A L'ETOILE DU POINT N
C        --------------------------------------------------
         NBTEET = NBTEET + 1
         NTETOI( NBTEET ) = NTOP

C        RETOUR AU DEBUT DE LA LISTE DES FACES CAR NF1 A PU DISPARAITRE
         GOTO 40
      ENDIF

C     TRACE FILAIRE DES ARETES DES TETRAEDRES DE L'ETOILE
      KTITRE='etoipt21: POINT           ETOILE FINALE de          TETRAE
     %DRES'
      WRITE(KTITRE(17:29),'(I8)') N
      WRITE(KTITRE(37:44),'(I8)') NBTEET
      CALL TRACEETOILE( KTITRE, PTXYZD, NOTETR, N, NBTEET, NTETOI ) 

      RETURN
      END
