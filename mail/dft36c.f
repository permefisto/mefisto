      SUBROUTINE DFT36C( NTLXOB, NBELEM, MNELEM,
     %                   NBOBPR, MNMNSO,
     %                   NDSCOU, NUSOUN,
     %                   NTNPEF, MNNPEF, NMNPEF,
     %                   NUOBIN, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   AJOUTER LES NO DES SOMMETS ET OBJET PREMIER DES EF 6-CUBES
C -----   DES OBJETS PREMIERS DE L'OBJET COMPLET
C         STOCKAGE DES 64 NO DE SOMMETS ET DU NO DE VOLUME UNIQUEMENT
C
C ENTREES:
C --------
C NTLXOB : NUMERO DU TMS LEXIQUE DE L'OBJET
C MOELEM : NOMBRE DE MOTS DE CHAQUE TYPE D'ELEMENT FINI
C NBELEM : NOMBRE TOTAL D'EF POUR L'OBJET COMPLET (UNION DE PLUSIEURS)
C          NBELEM(1)=NOMBRE DE NOEUDSOMMETS
C          NBELEM(2)=SEGMENTS
C          NBELEM(3)=TRIANGLES
C          NBELEM(4)=QUADRANGLES
C          NBELEM(5)=TETRAEDRES
C          NBELEM(6)=PENTAEDRES
C          NBELEM(7)=HEXAEDRES
C          NBELEM(8)=6-CUBES
C          NBELEM(9)=PYRAMIDES
C MNELEM : ADRESSE MCN DES EVENTUELS 9 TABLEAUX (MOELEM(i)*NBELEM(i))
C          0 SI PAS DE D'ELEMENTS FINIS DE CE TYPE
C NBOBPR : NOMBRE D'OBJETS PREMIERS
C NUOBPR : LE TYPEOBJET DE CHAQUE OBJET PREMIER
C MNMNSO : ADRESSE MCN DU TABLEAU 'NSEF' DE CHAQUE OBJET PREMIER
C NDSCOU : NDSCOU(0)=0 NDSCOU(I)=POINTE DANS NUSOUN SUR LE DERNIER
C                                SOMMET DE L'OBJET PREMIER I
C NUSOUN : NUMERO DU SOMMET DANS L'UNION DES SOMMETS
C
C SORTIES :
C ---------
C NTNPEF : NUMERO      DU TMS NPEF"6Q1C EN POSITION 8
C MNNPEF : ADRESSE MCN DU TMS NPEF"6Q1C EN POSITION 8
C NMNPEF : SUFFIXE     DU TMS NPEF"6Q1C EN POSITION 8
C NUOBIN : NUMEROS DES OBJETS INTERNES
C NUOBCL : NUMEROS DES OBJETS AUX LIMITES
C IERR   : 0 SI PAS D'ERREUR, NON NUL SINON
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  TEXAS A & M UNIVERSITY             JULY 2005
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a___npef.inc"
      include"./incl/a___nsef.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      INTEGER           NBELEM(9), MNELEM(9)
      INTEGER           MNMNSO(NBOBPR),
     %                  NDSCOU(0:NBOBPR), NUSOUN(1:*),
     %                  NTNPEF(9), MNNPEF(9), NMNPEF(9)
      INTEGER           NUOBIN(NBOBPR)
      INTEGER           NOSOEL(64)
      CHARACTER*13      KNOM
C
C     LE NOMBRE DE NOEUDS DU 6-CUBE "6Q1C" = NOMBRE DE SES SOMMETS
      NBNDEL = 64
C
C     BOUCLE SUR LES OBJETS PREMIERS
C     ==============================
C     ADRESSE MCN -1 DU NO DU 1-ER NOEUD DU 1-ER EF DE L'UNION
      MNSOEF = MNELEM(8) - 1
C     ADRESSE MCN -1 DU NO DE VOLUME DU 1-ER EF DE L'UNION
      MNVOEF = MNSOEF + NBELEM(8) * NBNDEL
C
C     NO DE L'EF COURANT DE L'UNION
      NOEF = 0
C
      DO 100 I=1,NBOBPR
C
C        C'EST UN MATERIAU CAD OBJET INTERNE
         NUOBIN(I) = 1
C
C        LE TABLEAU NSEF DE L'OBJET PREMIER I
         MNSOOB = MNMNSO(I)
C
C        LES NO DES SOMMETS DU MAILLAGE DE L'OBJET PREMIER I
         CALL NSEFPA( MCN(MNSOOB),
     %                NUTYMA, NBSOEL, NBNDEL, NBTGEF,
     %                LDAPEF, LDNGEF, LDTGEF, NBEFOB,
     %                NX    , NY    , NZ    ,
     %                IERR  )
         IF( IERR .NE. 0 ) GOTO 100
C
C        LA BOUCLE SUR LE TABLEAU NSEF DU MAILLAGE DE CET OBJET PREMIER I
C        ----------------------------------------------------------------
C        LE DEBUT DU TABLEAU DES NUMEROS DES NOEUDS DU MAILLAGE
         DO 50 N=1,NBEFOB
C
C           NO DE L'EF DANS LA TOTALITE DES OBJETS PREMIERS
            NOEF = NOEF + 1
C
C           LE NUMERO DES NBNDEL NOEUDS=SOMMETS DE L'EF N DE L'OBJET PREMIER I
            CALL NSEFNS( N     , NUTYMA, NBNDEL, NBTGEF,
     %                   LDAPEF, LDNGEF, LDTGEF,
     %                   MNSOOB, NX, NY, NZ,
     %                   NCOGEL, NUGEEF, NUEFTG, NOSOEL, IERR )
C
C           TRANSFORMATION DU NUMERO DES SOMMETS DE CET OBJET PREMIER I
C           EN LES NUMEROS DE SOMMETS DE L'UNION
C           ATTENTION:  Tableau NUNDEL( 1..NBELEM(8), 1..NBNDEL )
C                      'Numero des noeuds de chaque EF' entier
C                       LE NO D'EF VARIE LE PLUS VITE
            MNN = MNSOEF + NOEF
            DO 20 J=1,NBNDEL
C              LE NUMERO DU SOMMET DANS L'UNION APRES IDENTIFICATION
               MCN( MNN ) = NUSOUN( NDSCOU(I-1) + NOSOEL(J) )
               MNN = MNN + NBELEM(8)
 20         CONTINUE
C
C           STOCKAGE DU NO OBJET PREMIER DE L'EF
            MCN( MNVOEF + NOEF ) = I
 50      CONTINUE
 100  CONTINUE
C
C     CONSTRUCTION DU TMS NPEF"6Q1C DE L'OBJET COMPLET
C     ================================================
C     S'IL EXISTE ALORS IL EST DETRUIT
      KNOM = 'NPEF"6Q1C'
      CALL LXTSOU( NTLXOB, KNOM,  NTNPEF(8), MNNPEF(8) )
      IF( NTNPEF(8) .GT. 0 ) THEN
C        DESTRUCTION DE L'ANCIEN TABLEAU
         CALL LXTSDS( NTLXOB, KNOM )
      ENDIF
C
C     LE NOMBRE DE MOTS DU TABLEAU NPEF"6Q1C EN POSITION 8
C     STOCKAGE DES 64 NO DE SOMMETS ET DU NO DE VOLUME UNIQUEMENT
      K = WUNDEL + NBELEM(8) * ( NBNDEL + 1 )
      CALL LXTNDC( NTLXOB, KNOM, 'MOTS', K )
      CALL LXTSOU( NTLXOB, KNOM,  NTNPEF(8), MNNPEF(8) )
C
C     REMPLISSAGE DES VARIABLES DU TABLEAU NPEF"6Q1C
C     NOM DU TYPE DES ELEMENTS FINIS
      NMNPEF( 8 ) = ICHARX( '6Q1C' )
C     NUMERO 30 DU TYPE DE L'ELEMENT FINI 6CUB 6Q1C
      NUTYEL = 30
      MCN( MNNPEF(8) + WUTYEL ) = NUTYEL
C     NBELEM 'NOMBRE D'EF DE CE TYPE
      MCN( MNNPEF(8) + WBELEM ) = NBELEM(8)
C     NBNDEL 'NOMBRE DE NOEUDS D''UN ELEMENT FINI DE CE TYPE'
      MCN( MNNPEF(8) + WBNDEL ) = NBNDEL
C     NBPGEL 'NOMBRE DE POINTS GEOMETRIQUES D''UN ELEMENT'
      MCN( MNNPEF(8) + WBPGEL ) = 0
C     NBPSEL 'NOMBRE DE POINTS UTILISATEUR SOMMET D''UN ELEMENT'
      MCN( MNNPEF(8) + WBPSEL ) = 0
C     MOPSEL 'NOMBRE DE VARIABLES DES TABLEAUX NLPSEL ET NEPSEL'
      MCN( MNNPEF(8) + WOPSEL ) = 0
C     NBLAEL 'NOMBRE DE LIGNES UTILISATEUR ARETE D''UN ELEMENT'
      MCN( MNNPEF(8) + WBLAEL ) = 0
C     MOLAEL 'NOMBRE DE VARIABLES DES TABLEAUX NLLAEL ET NELAEL'
      MCN( MNNPEF(8) + WOLAEL ) = 0
C     NBSFEL 'NOMBRE DE SURFACES UTILISATEUR FACE D''UN ELEMENT'
      MCN( MNNPEF(8) + WBSFEL ) = 0
C     MOSFEL 'NOMBRE DE VARIABLES DES TABLEAUX NLSFEL ET NESFEL'
      MCN( MNNPEF(8) + WOSFEL ) = 0
C     NBVCEL 'NOMBRE DE VOLUMES UTILISATEUR CUBE D''UN ELEMENT'
      MCN( MNNPEF(8) + WBVCEL ) = NBELEM(8)
C     MOVCEL 'NOMBRE DE VARIABLES DES TABLEAUX NLVCEL ET NEVCEL'
      MCN( MNNPEF(8) + WOVCEL ) = 0
C
C     LES TG DANS  NPEF"6Q1C  N'EXISTENT PAS
C     NOMBRE D'EF AVEC POINTEUR SUR EF A TG
      MCN( MNNPEF(8) + WBELAP ) = 0
C     NOMBRE D'EF A TG
      MCN( MNNPEF(8) + WBELTG ) = 0
C     NOMBRE DE TANGENTES D'UN EF
      MCN( MNNPEF(8) + WBTGEL ) = 0
C
C     COPIE DES NO DE SOMMETS DES 6-CUBES ET DU NO DE VOLUME DES 6-CUBES
      CALL TRTATA( MCN(MNELEM(8)), MCN(MNNPEF(8)+WUNDEL),
     %             NBELEM(8)*(NBNDEL+1) )
C
C     LA DATE
      CALL ECDATE( MCN(MNNPEF(8)) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNNPEF(8) + MOTVAR(6) ) = NONMTD( '~>>>NPEF' )
C
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'FIN DE CREATION DU TMS: NPEF"6Q1C'
      ELSE
         WRITE(IMPRIM,*) 'END of CREATION of TMS: NPEF"6Q1C'
      ENDIF
C
      RETURN
      END
