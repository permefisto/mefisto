      SUBROUTINE AJFAET( NOTET1, I,      NOTETR, MXFACO, LEFACO,
     %                   N1FEOC, N1FEVI, NFETOI, NF2 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  SI LA FACE I DU TETRAEDRE NOTET1 N'EST PAS DANS NFETOI
C -----  ALORS ELLE EST AJOUTEE ET NF2 EST SA PLACE DANS NFETOI
C              SI ELLE EST DANS LEFACO ALORS ELLE EST MARQUEE NEGATIVE
C        SINON
C              SI ELLE N'EST PAS DANS LEFACO ALORS ELLE EST SUPPRIMEE
C                                                  NF2=0
C                                            SINON NF2=-POSITION LEFACO
C                                                  RETOUR
C ENTREES :
C ---------
C NOTET1 : NUMERO DU TETRAEDRE
C I      : NUMERO DE LA FACE DU TETRAEDRE NOTET1
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C
C ENTREES ET SORTIES :
C --------------------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI :    VERSION 1
C          1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DANS LEFACO DE LA FACE, 0 SINON
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C
C SORTIE :
C --------
C NF2    : >0 NUMERO DANS NFETOI DE LA FACE AJOUTEE
C          =0 SUPPRESSION DE LA FACE N'APPARTENANT PAS A LEFACO
C          <0 -NUMERO LEFACO DE LA FACE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     FEVRIER 1993
C23456...............................................................012
      INTEGER  NOTETR(8,*), NFETOI(5,*), LEFACO(1:11,0:MXFACO)
C
C     LE NUMERO NTOP DU TETRAEDRE DE L'AUTRE COTE DE LA FACE I
      NTOP = NOTETR(I+4,NOTET1)
      IF( NTOP .LE. 0 ) THEN
C        LE TETRAEDRE DE L'AUTRE COTE N'EXISTE PAS
         GOTO 90
      ENDIF
C
C     NFOP LE NUMERO LOCAL DE LA FACE CONCERNEE DANS LE TETRAEDRE OPPOSE
      IF( NOTETR(5,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 1
      ELSE IF( NOTETR(6,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 2
      ELSE IF( NOTETR(7,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 3
      ELSE
         NFOP = 4
      ENDIF
C
C     LE DEBUT DU CHAINAGE DES FACES DE L'ETOILE
C     ET BOUCLE SUR LES FACES DE L'ETOILE
C     ==========================================
      NF1 = 0
      NF2 = N1FEOC
C
 10   IF( NF2 .GT. 0 ) THEN

         IF(     NFETOI(1,NF2)  .EQ. NTOP .AND.
     %       ABS(NFETOI(2,NF2)) .NE. NFOP ) THEN

C            LA FACE NFOP DU TETRAEDRE NTOP EST IDENTIQUE A NF2
C            LA FACE EST ELLE DANS LEFACO?
             IF( NFETOI(4,NF2) .EQ. 0 ) THEN

C               NON : DESTRUCTION DE LA FACE DE L'ETOILE
                IF( NF1 .NE. 0 ) THEN
C                  LA FACE NF2,PRECEDEE DE NF1,N'EST PAS LA
C                  PREMIERE DE L'ETOILE
                   NFETOI(5,NF1) = NFETOI(5,NF2)
C                  LA FACE NF2 DEVIENT LA PREMIERE VIDE DE L'ETOILE
                   NFETOI(5,NF2) = N1FEVI
                   N1FEVI = NF2
                ELSE
C                  LA FACE NF2=N1FEOC EST LA 1-ERE DE L'ETOILE
                   NF1    = NFETOI(5,N1FEOC)
                   NFETOI(5,N1FEOC) = N1FEVI
                   N1FEVI = N1FEOC
                   N1FEOC = NF1
                ENDIF
                NF2 = 0

             ELSE

C               OUI: LA FACE NF2 EST DANS LEFACO: RETOUR AVEC NF2=-NFLE
                NF2 = -NFETOI(4,NF2)

             ENDIF

             GOTO 9999

         ELSE

C            LA FACE EST DIFFERENTE.PASSAGE A LA SUIVANTE
             NF1 = NF2
             NF2 = NFETOI(5,NF2)
             GOTO 10

         ENDIF

      ENDIF
C
C     LA FACE NFOP N'EST PAS RETROUVEE
C     ELLE EST AJOUTEE A L'ETOILE A LA FIN DES FACES OCCUPEES
C     CECI AFIN DE CHEMINER DU CENTRE VERS L'EXTERIEUR
C     =======================================================
 90   NF1 = 0
      NF2 = N1FEOC
 95   IF( NF2 .GT. 0 ) THEN
         NF1 = NF2
         NF2 = NFETOI(5,NF2)
         GOTO 95
      ENDIF
C     NF1 EST LA DERNIERE FACE DE L'ETOILE
C
C     RECHERCHE D'UNE FACE VIDE
      NF2    = N1FEVI
      N1FEVI = NFETOI(5,N1FEVI)
C
C     MISE A JOUR DU CHAINAGE : NF2 VIENT APRES NF1
      IF( NF1 .GT. 0 ) THEN
C        IL EXISTE UNE FACE DANS L'ETOILE
         NFETOI( 5, NF1 ) = NF2
      ELSE
C        NF2 EST LA SEULE FACE DE L'ETOILE
         N1FEOC = NF2
      ENDIF
C
C     INTIALISATION DE LA FACE SIMPLE EN DERNIERE POSITION DE L'ETOILE
C     ----------------------------------------------------------------
      NFETOI(1,NF2) = NOTET1
      NFETOI(2,NF2) = I
C
C     LA FACE EST ELLE DANS LEFACO?
      CALL NULEFT( I, NOTET1, NOTETR, MXFACO, LEFACO, NFLE )
      NFETOI(4,NF2) = NFLE
      NFETOI(5,NF2) = 0
C
C     MISE A JOUR DU NUMERO DE TETRAEDRE AYANT LA FACE NFLE DE LEFACO
      IF( NFLE .GT. 0 ) THEN
         LEFACO(11,NFLE) = NOTET1
      ENDIF

 9999 RETURN
      END
