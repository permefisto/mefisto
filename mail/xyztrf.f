      SUBROUTINE XYZTRF( R, S, XYZ, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   A PARTIR DES 3 APPLICATIONS :
C -----   Q1  : CARRE => Q   Q1(U,V)=(R,S)
C         T   : CARRE => QUADRANGLE TRANSFINI  T(U,V)=(X,Y,Z)
C         F   : Q     => QUADRANGLE TRANSFINI  F(R,S)=(X,Y,Z)=T(Q1**(-1))
C         CALCULER F(R,S)=(X,Y,Z)
C         ICI LES ARETES DES 4 COTES DU QUADRANGLE SONT SUPPOSEES DROITES (P1)
C         SEUL LE SP QUNSAL PEUT EXECUTER CE SP CAR DE NOMBREUX
C         PARAMETRES SONT CACHES DANS LE COMMON /S09S01/!
C
C ENTREES:
C --------
C R,S    : LES 2 COORDONNEES DU POINT DU QUADRANGLE Q DEFINI PAR SES
C          4 SOMMETS SELON LES  LONGUEURS DES 4 COTES DU QUADRANGLE
C          TRANSFINI
C
C SORTIES:
C --------
C XYZ    : LES 3 COORDONNEES DU POINT IMAGE SUR LE QUADRANGLE TRANSFINI
C IERR   : 0 SI PAS D'ERREUR, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS         FEVRIER 1997
C234567..............................................................012
C     ATTENTION: LE COMMON SUIVANT DOIT ETRE INITIALISE!
C                CF LE SP QUNSAL
      COMMON /S09S01/  MNCOCU, NBSOCQ(4), NUCOTQ(4), MNSOCQ(4),
     %                 XYZ4ST(3,4), XYZL(3,4), XY(4), ST(2,4), UNMOT
      REAL             XYZ(3)
C
C     (U,V) = Q1**(-1) (R,S)
C     PASSAGE DES COORDONNEES DU POINT DU QUADRANGLE DE SOMMETS ST
C     AUX COORDONNEES DU POINT DU CARRE UNITE
C     INVERSION D'UNE TRANSFORMATION Q1**2
      CALL FQ1INV( R, S, ST, U, V, IERR )
C     IERR>0 EST LE NOMBRE MAXIMAL D'ITERATIONS SANS CONVERGENCE
C     DE L'ALGORITHME DE NEWTON POUR INVERSER L'APPLICATION Q1**3
      IF( IERR .NE. 0 ) RETURN
C
C     ( X, Y, Z ) = T( U, V )
C     PASSAGE DES COORDONNEES DU POINT DU CARRE UNITE
C     AUX COORDONNEES DU POINT DU QUADRANGLE TRANSFINI DANS R**3
      CALL XYZTRT( U, V, XYZ )
      END
