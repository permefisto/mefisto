      SUBROUTINE OTRACINE( KNMVOL, ARETGR, MXSOMM, HEXAVOLU,
     %                     NBSOMM, PTXYZD, ARETOCG, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES 6 SOMMETS DE L'OCTAEDRE ENGLOBANT TOUS LES POINTS
C -----    FRONTALIERS OU INTERNES AU DOMAINE A TETRAEDRISER

C ENTREES:
C --------
C KNMVOL  : NOM DU VOLUME A TETRAEDRISER
C ARETGR  : LONGUEUR SOUHAITEE DE L'ARETE DE LA GRILLE REGULIERE
C MXSOMM  : NOMBRE MAXIMAL DE SOMMETS DECLARABLES
C HEXAVOLU: LES 3 COORDONNEES MINIMALES ET MAXIMALES DES 2 SOMMETS
C           EXTREMES DE L'HEXAEDRE ENGLOBANT LE VOLUME DE TYPE 9
C           HEXAVOLU(I,J) = I-EME COORDONNEE MINIMALE SI J=1
C                                            MAXIMALE SI J=2

C SORTIES:
C --------
C NBSOMM  : =6 NOMBRE DE SOMMETS DE L'OCTAEDRE RACINE
C PTXYZD  : TABLEAU DES COORDONNEES DES POINTS
C           PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE
C ARETOCG : TAILLE SOUHAITEE AUTOUR des SOMMETS DE L'OCTAEDRE ENGLOBANT
C IERR    : 0 SI PAS D'ERREUR
C           1 SATURATION DES POINTS DU TABLEAU PTXYZD
C           2 TOUS LES POINTS SONT COPLANAIRES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1992
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      CHARACTER*24      KNMVOL
      REAL              ARETGR, HEXAVOLU(6,2), ARETOCG

      DOUBLE PRECISION  PTXYZD(4,MXSOMM), DIS2ST,
     %                  X0, Y0, Z0, RAC2, ARETOT, ARETOTS2

C     LES POINTS SONT ILS COPLANAIRES ?
C     =================================
      DO K=1,3
C        L'AMPLITUDE DE L'OBJET DANS LA DIRECTION K
         H = HEXAVOLU(K,2) - HEXAVOLU(K,1)
         IF( H .LT. 1E-3 * (ABS(HEXAVOLU(K,1))+ABS(HEXAVOLU(K,2)))) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1)='otracine: TOUS LES POINTS SONT COPLANAIRES'
            ELSE
               KERR(1)='otracine: ALL POINTS ARE ON A SAME PLANE'
            ENDIF
            CALL LEREUR
            IERR = 2
            GOTO 9999
         ENDIF
      ENDDO

C     RACINE CARREE DE 2
      RAC2 = SQRT( 2D0 )

C     ECART EN X Y Z DU VOLUME TOTAL
      X0 = HEXAVOLU(1,2) - HEXAVOLU(1,1)
      Y0 = HEXAVOLU(2,2) - HEXAVOLU(2,1)
      Z0 = HEXAVOLU(3,2) - HEXAVOLU(3,1)

C     LONGUEUR DE L'ARETE DE L'OCTAEDRE ENGLOBANT
C     ===========================================
      ARETOT = MAX( X0, Y0 ) + Z0/RAC2 + 4.01D0 * ARETGR

C     ARETOCG = DISTANCE SOUHAITEE des SOMMETS DE L'OCTAEDRE ENGLOBANT
      ARETOCG = REAL( ARETOT * 3D0 )

C     LE CENTRE DE L'OCTAEDRE
C     =======================
      X0 = ( HEXAVOLU(1,2) + HEXAVOLU(1,1) ) * 0.5D0
      Y0 = ( HEXAVOLU(2,2) + HEXAVOLU(2,1) ) * 0.5D0
      Z0 = ( HEXAVOLU(3,2) + HEXAVOLU(3,1) ) * 0.5D0

C     GENERATION DES 6 SOMMETS DE L'OCTAEDRE RACINE
C     =============================================
C     LE POINT 1
      PTXYZD(1,1) = X0
      PTXYZD(2,1) = Y0
      PTXYZD(3,1) = Z0 + ARETOT / RAC2
      PTXYZD(4,1) = ARETOCG

C     LE POINT 6
      PTXYZD(1,6) = X0
      PTXYZD(2,6) = Y0
      PTXYZD(3,6) = Z0 - ARETOT / RAC2
      PTXYZD(4,6) = ARETOCG

C     LA DEMI-LARGEUR DE L'OCTAEDRE
      ARETOTS2 = ARETOT * 0.5D0

C     LE POINT 2
      PTXYZD(1,2) = X0 - ARETOTS2
      PTXYZD(2,2) = Y0 - ARETOTS2
      PTXYZD(3,2) = Z0
      PTXYZD(4,2) = ARETOCG

C     LE POINT 3
      PTXYZD(1,3) = X0 + ARETOTS2
      PTXYZD(2,3) = Y0 - ARETOTS2
      PTXYZD(3,3) = Z0
      PTXYZD(4,3) = ARETOCG

C     LE POINT 4
      PTXYZD(1,4) = X0 + ARETOTS2
      PTXYZD(2,4) = Y0 + ARETOTS2
      PTXYZD(3,4) = Z0
      PTXYZD(4,4) = ARETOCG

C     LE POINT 5
      PTXYZD(1,5) = X0 - ARETOTS2
      PTXYZD(2,5) = Y0 + ARETOTS2
      PTXYZD(3,5) = Z0
      PTXYZD(4,5) = ARETOCG

C     IMPRESSIONS
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) DIS2ST( PTXYZD(1,2), PTXYZD(1,3) )
         DO K = 1,6
            PRINT*, 'OCTAEDRE GLOBAL de SOMMET',K,
     %              ' x:',PTXYZD(1,K), ' y:',PTXYZD(2,K),
     %              ' z:',PTXYZD(3,K), ' d:',PTXYZD(4,K)
         ENDDO
      ELSE
         WRITE(IMPRIM,20000) DIS2ST( PTXYZD(1,2), PTXYZD(1,3) )
         DO K = 1,6
            PRINT*, 'GLOBAL OCTAHEDRON: Vertex',K,
     %              ' x:',PTXYZD(1,K), ' y:',PTXYZD(2,K),
     %              ' z:',PTXYZD(3,K), ' d:',PTXYZD(4,K)
         ENDDO
      ENDIF

10000 FORMAT(/' LONGUEUR DES ARETES DE L''OCTAEDRE ENGLOBANT=',G15.6)
20000 FORMAT(/' EDGE LENGTH of GLOBAL OCTAHEDRON =',G15.6)


      IF( LANGAG .EQ. 0 ) THEN
         PRINT 11000, KNMVOL, ((HEXAVOLU(I,J),I=1,3),J=1,2)
      ELSE
         PRINT 21000, KNMVOL, ((HEXAVOLU(I,J),I=1,3),J=1,2)
      ENDIF

11000 FORMAT(/
     % ' COORDONNEES MIN-MAX des SOMMETS du VOLUME ',A/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 )

21000 FORMAT(/
     % ' MIN-MAX VERTICES COORDINATES of VOLUME ',A/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 )

CCCC
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,2), PTXYZD(1,3) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,3), PTXYZD(1,4) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,4), PTXYZD(1,5) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,5), PTXYZD(1,2) )
CCCC
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,2), PTXYZD(1,1) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,3), PTXYZD(1,1) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,4), PTXYZD(1,1) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,5), PTXYZD(1,1) )
CCCC
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,2), PTXYZD(1,6) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,3), PTXYZD(1,6) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,4), PTXYZD(1,6) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,5), PTXYZD(1,6) )

C     6 SOMMETS POUR L'OCTAEDRE ENGLOBANT
      NBSOMM = 6

C     LES DISTANCES SOUHAITEES
C     ========================
      DO K = NBSOMM+1, MXSOMM
C        DISTANCE INITIALISEE A ZERO POUR FAIRE ENSUITE UNE MAJORATION
         PTXYZD(4,K) = 0D0
      ENDDO
C
 9999 RETURN
      END
