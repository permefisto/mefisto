      SUBROUTINE COMBRE( NS1, NS2, NOSOTR, PTXYZD, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LE SOMMET NS2 EST IL DANS LE CONE D'OMBRE DE LA FACE DE
C -----    SOMMETS NOSOTR DANS PTXYZD ET VUE DU SOMMET NS1 ?
C
C ENTREES :
C ---------
C NS1    : LE POINT REGARDANT LA FACE NF DU TETRAEDRE NT
C NS2    : CE POINT EST IL DANS LE CONE D'OMBRE ?
C NOSOTR : NUMERO PTXYZD DES 3 SOMMETS DE LA FACE
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C SORTIES :
C ---------
C NONOUI : 0 SI NS2 N'EST PAS DANS LE CONE D'OMBRE
C            OU SI NS2 EST UN SOMMET DE NF
C          1 SI NS2 EST DANS LE CONE D'OMBRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS        MARS 1993
C23456...............................................................012
      DOUBLE PRECISION  PTXYZD(1:4,1:*), PT(3), COBARY(3),
     %                  A, X21, Y21, Z21
      INTEGER           NOSOTR(3)
C
      DO 10 I=1,3
         IF( NOSOTR(I) .EQ. NS2 ) THEN
C           NS2 EST SOMMET DE NOSOTR
            GOTO 9000
         ENDIF
 10   CONTINUE
C
C     CALCUL DU POINT D'INTERSECTION DE NS1-NS2 ET DU PLAN DE LA FACE NF
      CALL INDRPL( PTXYZD(1,NS1), PTXYZD(1,NS2), PTXYZD(1,NOSOTR(1)),
     %             PTXYZD(1,NOSOTR(2)), PTXYZD(1,NOSOTR(3)), PT, IERR )
C
      IF( IERR .NE. 0 ) THEN
C        S1=S2 OU S1-S2 PARALLELE A LA FACE
         GOTO 9000
      ENDIF
C
C     IL EXISTE UN POINT D'INTERSECTION S1-S2 AVEC LA FACE
C     EST IL DANS LE TRIANGLE ?
      CALL PTDSTR( PT, PTXYZD, NOSOTR, COBARY, NONOUI )
      IF( NONOUI .GT. 0 ) THEN
C
C        PT EST DANS OU SUR LE TRIANGLE: PT EST IL ENTRE NS1 ET NS2 ?
         X21 = PTXYZD(1,NS2) - PTXYZD(1,NS1)
         Y21 = PTXYZD(2,NS2) - PTXYZD(2,NS1)
         Z21 = PTXYZD(3,NS2) - PTXYZD(3,NS1)
C
         A = ( (PT(1)-PTXYZD(1,NS1)) * X21
     %       + (PT(2)-PTXYZD(2,NS1)) * Y21
     %       + (PT(3)-PTXYZD(3,NS1)) * Z21 )
     %     / ( X21 * X21 + Y21 * Y21 + Z21 * Z21 )
         IF( 0.0D0 .LT. A .AND. A .LT. 1.0D0 ) THEN
C           PT EST ENTRE NS1 ET NS2 => NS2 DERRIERE LE TRIANGLE
C           NS2 EST DANS LE CONE D'OMBRE DE LA FACE VUE DE NS1
            NONOUI = 1
            RETURN
         ENDIF
      ENDIF
C
C     NS2 NON DANS LE CONE D'OMBRE DE LA FACE VUE DE NS1
 9000 NONOUI = 0
      RETURN
      END
