      SUBROUTINE NFAQVXYZ( NBEF,   NBNOEF, NUNOTE,
     %                     MOFACE, MXFACE, LFACES,
     %                     XYZPOI, XYZ, NTE0,   XYZBFMX, NTEDER )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:     TROUVER A PARTIR DE LA FACE DU TETRAEDRE NTE0 QUI VOIT
C ----     LE MIEUX LE POINT XYZ A ATTEINDRE, LE TETRAEDRE NTEDER
C          QUI EST DERRIERE CETTE FACE DE NTE0

C ENTREES:	
C --------
C NBEF   : NOMBRE DE TETRAEDRES DU MAILLAGE
C NBNOEF : NOMBRE DE NOEUDS D'UN TETRAEDRE
C          (SEULS LES 4 SOMMETS SONT UTILES ICI et ONT POUR No 1 2 3 4)
C NUNOTE : NUMERO DES NBNOEF NOEUDS DES NBEF TETRAEDRES

C MOFACE : NOMBRE DE MOTS DE CHAQUE FACE DU TABLEAU LFACES
C MXFACE : NOMBRE MAXIMAL de FACES DU TABLEAU LFACES
C LFACES : LFACES(1,I)= NO DU 1-ER  SOMMET DE LA FACE
C          LFACES(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LFACES(3,I)= NO DU 3-EME SOMMET DE LA FACE
C          LFACES(4,I)= NO DU 4-EME SOMMET DE LA FACE
C                       0 SI TRIANGLE
C          LFACES(5,I)= CHAINAGE HACHAGE SUR FACE SUIVANTE
C          SI SOMMET 2 < DERNIER SOMMET  => FACE   DIRECTE DANS L'ELEMENT FINI
C                      >                 => FACE INDIRECTE
C          UNE FACE DIRECTE EST VUE DE L EXTERIEUR DE L'ELEMENT FINI
C          SOUS LA FORME DIRECTE
C          LFACES(6,I)= NUMERO DU 1-ER ELEMENT FINI CONTENANT CETTE FACE
C                       >0 SI FACE   DIRECTE DANS CET ELEMENT FINI
C                       <0 SI FACE INDIRECTE DANS CET ELEMENT FINI

C          SI LA FACE APPARTIENT A 2 ELEMENTS FINIS ALORS
C          LFACES(7,I)= NUMERO DU 2-EME ELEMENT FINI CONTENANT CETTE FACE
C                       >0 SI FACE   DIRECTE DANS CET ELEMENT FINI
C                       <0 SI FACE INDIRECTE DANS CET ELEMENT FINI
C          SINON
C          LFACES(7,I)= NUMERO DE LA FACE FRONTALIERE SUIVANTE
C                       0 SI C'EST LA DERNIERE
C          L1FAFR = NUMERO DE LA PREMIERE FACE FRONTALIERE DANS LFACES
C                   CHAINAGE SUIVANT DANS LFACES(7,I)
C          L1FA2M = NUMERO DE LA PREMIERE FACE INTERFACE 2 MATERIAUX DANS LFACES
C                   CHAINAGE SUIVANT DANS LFACES(8,I)
C          SI VOLUME MULTI-MATERIAUX
C          LFACES(8,I)= NO DE LA FACE INTERFACE ENTRE 2 MATERIAUX SUIVANTE
C                       0 SI FACE SANS INFORMATION SUPPLEMENTAIRE
C          SINON VOLUME MONO-MATERIAU
C          LFACES(8,I)= NUMERO DE LA FACE A TANGENTE
C                       0 SI FACE SANS TANGENTE

C XYZPOI : 3 COORDONNEES DES SOMMETS DES TETRAEDRES
C XYZ    : 3 COORDONNEES DU POINT A ATTEINDRE
C NTE0   : NO NUNOTE DU TETRAEDRE DES FACES A ANALYSER

C SORTIE :
C --------
C XYZBFMX: XYZ DU BARYCENTRE DE LA MEILLEURE FACE VOYANT XYZ DE NTE0
C NTEDER : >0 NO NUNOTE DU TETRAEDRE DERRIERE LA MEILLEURE FACE DE NTE0
C             QUI VOIT LE MIEUX LE POINT XYZ
C          =0 PAS DE TETRAEDRE DERRIERE LA MEILLEURE FACE DE NTE0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint Pierre du Perray           Octobre 2020
C23456---------------------------------------------------------------012
      IMPLICIT NONE
      include"./incl/langue.inc"
      INTEGER           NTE0, NTEDER, NBEF, NBNOEF, NUNOTE(NBEF,NBNOEF),
     %                  MOFACE, MXFACE, LFACES(MOFACE,MXFACE)

      REAL              XYZPOI(3,*)
      DOUBLE PRECISION  XYZ(3), XYZBFMX(3),X1,X2,X3, Y1,Y2,Y3, Z1,Z2,Z3,
     %                  XBAR,YBAR,ZBAR, XB,YB,ZB, XN,YN,ZN, COSMAX,
     %                  D, DBPT, DN
      INTEGER           K, N1, N2, N3, NFMAX

      INTEGER           NOSOFATE(3,4)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /

C     LE POINT XYZ EST EXTERIEUR AU TETRAEDRE NTE0
C     RECHERCHE DE LA FACE DE L'EF NTE0 QUI LE REGARDE
C     ------------------------------------------------
      COSMAX = -2D0
      NFMAX  = 0

C     BOUCLE SUR LES 4 FACES DU TETRAEDRE NTE0
      DO 20 K=1,4

C        FACE K DE NTE0
         N1 = NUNOTE( NTE0, NOSOFATE(1,K) )
         N2 = NUNOTE( NTE0, NOSOFATE(2,K) )
         N3 = NUNOTE( NTE0, NOSOFATE(3,K) )

C        LES COORDONNEES DU BARYCENTRE DES 3 SOMMETS DE LA FACE K
         X1 = XYZPOI( 1, N1 )
         Y1 = XYZPOI( 2, N1 )
         Z1 = XYZPOI( 3, N1 )

         X2 = XYZPOI( 1, N2 )
         Y2 = XYZPOI( 2, N2 )
         Z2 = XYZPOI( 3, N2 )

         X3 = XYZPOI( 1, N3 )
         Y3 = XYZPOI( 2, N3 )
         Z3 = XYZPOI( 3, N3 )

         XBAR = ( X1 + X2 + X3 ) / 3D0
         YBAR = ( Y1 + Y2 + Y3 ) / 3D0
         ZBAR = ( Z1 + Z2 + Z3 ) / 3D0

C        LES COMPOSANTES DU VECTEUR: BARYCENTRE DE LA FACE K -> PT
         XB = XYZ(1) - XBAR
         YB = XYZ(2) - YBAR
         ZB = XYZ(3) - ZBAR

C        CARRE DE LA NORME DU VECTEUR (BARYCENTRE - XYZ)
         DBPT = XB**2 + YB**2 + ZB**2
         IF( DBPT .LE. 0D0 ) THEN
C           XYZ EST LE BARYCENTRE DE LA FACE K
            NFMAX = K
            XYZBFMX(1) = XBAR
            XYZBFMX(2) = YBAR
            XYZBFMX(3) = ZBAR
            GOTO 30
         ENDIF

C        LES COMPOSANTES DU VECTEUR NORMAL A LA FACE K
         XN = (Y2-Y1) * (Z3-Z1) - (Z2-Z1) * (Y3-Y1)
         YN = (Z2-Z1) * (X3-X1) - (X2-X1) * (Z3-Z1)
         ZN = (X2-X1) * (Y3-Y1) - (Y2-Y1) * (X3-X1)
C        CARRE DE LA NORME DU VECTEUR NORMAL (ou SURFACE DE LA FACE*2)
         DN = XN**2 + YN**2 + ZN**2

C        COSINUS ANGLE (NORMALE A LA FACE,BARYCENTRE-PT)
         D = ( XN * XB + YN * YB + ZN * ZB ) / SQRT( DN * DBPT )
         IF( D .GT. COSMAX ) THEN
            NFMAX  = K
            COSMAX = D
            XYZBFMX(1) = XBAR
            XYZBFMX(2) = YBAR
            XYZBFMX(3) = ZBAR
         ENDIF

 20   ENDDO

 30   IF( NFMAX .LE. 0 ) THEN
C        A PRIORI CAS IMPOSSIBLE
         IF( LANGAG .EQ. 0 ) THEN
            PRINT*,'nfaqvxyz: TETRAEDRE',NTE0,' NoFaceMX=',NFMAX,
     %             ' SANS FACE REGARDANT le POINT',XYZ
         ELSE
            PRINT*,'nfaqvxyz: TETRAHEDRON',NTE0,' NbFaceMX=',NFMAX,
     %             ' FACE Number SEEING the POINT',XYZ
         ENDIF
C        POINT DANS UN TETRAEDRE NTE0 SANS FACE REGARDANT LE POINT XYZ
         NTEDER = 0
         GOTO 9999
      ENDIF

C     RECHERCHE DU TETRAEDRE NTEDER DE L'AUTRE COTE DE LA FACE NFMAX de NTE0
      CALL NTED1FAC( NBEF,   NBNOEF, NUNOTE,
     %               MOFACE, MXFACE, LFACES,
     %               NFMAX,  NTE0,   NTEDER )
C     NTEDER: >0 NO NUNOTE DU TETRAEDRE DERRIERE LA FACE NFMAX DE NTE0
C             =0 PAS       DE TETRAEDRE DERRIERE LA FACE NFMAX DE NTE0

 9999 RETURN
      END

