      SUBROUTINE GSXYEF( KNUADP, NUSURF,
     %                   MXYZST, NBXYZ,  XYZST,
     %                   MXTRIA, NBTRIA, NOTRIA, NOSUTR,
     %                   NUAVAP,
     %                   NTXYZS, MNXYZS, NTNSEF, MNNSEF, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES TABLEAUX XYZSOMMET ET NSEF DE LA SURFACE NUSURF
C -----    A PARTIR DES TTABLEAUX XYZST ET NOTRIA ISSUS DE L'ADAPTATION
C          L'ANCIEN NOM DE NSEF         ET XYZSOMMET        DEVIENNENT
C                          NSEF.Anuiter ET XYZSOMMET.Anuiter
C ENTREES :
C ---------
C KNUADP : SUFFIXE DE L'ITERATION D'ADAPTATION
C          '.A NO ITERATION'
C NUSURF : NUMERO DANS LE LEXIQUE SURFACE DE LA SURFACE A TRAITER
C MXYZST : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS XYZST
C NBXYZ  : NOMBRE DE SOMMETS ACTUELS DE LA TRIANGULATION
C XYZST  : 3 COORDONNEES DES NBXYZ SOMMETS DE LA TRIANGULATION
C MXTRIA : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES DANS NOTRIA
C NBTRIA : NOMBRE ACTUEL DE TRIANGLES DANS NOTRIA
C NOTRIA : NUMERO DES 3 SOMMETS NO SURFACE DES NBTRIA TRIANGLES ACTUELS
C NOSUTR : NUMERO DE SURFACE DE CHAQUE TRIANGLE
C
C SORTIES :
C ---------
C NUAVAP : NUMERO DE SOMMET AVANT PUIS APRES RENUMEROTATION
C NTXYZS : NUMERO      DU TMS XYZSOMMET DE LA SURFACE
C NMXYZS : ADRESSE MCN DU TMS XYZSOMMET DE LA SURFACE
C NTNSEF : NUMERO      DU TMS NSEF DE LA SURFACE
C NMNSEF : ADRESSE MCN DU TMS NSEF DE LA SURFACE
C IERR   : =0 SI PAS D'ERREUR DETECTEE
C          >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     JANVIER 1995
C23456---------------------------------------------------------------012
      include"./incl/ntmnlt.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a___xyzsommet.inc"
C
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE       (MCN(1),RMCN(1))
C
      INTEGER           NOTRIA(6,MXTRIA),
     %                  NOSUTR(MXTRIA),
     %                  NUAVAP(MXYZST)
      DOUBLE PRECISION  XYZST(3,MXYZST)
C
      CHARACTER*4       KNUADP
      CHARACTER*24      KNOM
C
C     RECHERCHE DU LEXIQUE DE CETTE SURFACE
      CALL LXNLOU( NTSURF, NUSURF, NTLXSU, MNLXSU )
      IF( NTLXSU .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:8),'(I8)') NUSURF
         KERR(1) = 'SURFACE ' // KERR(MXLGER)(1:8) //' SANS LEXIQUE'
         CALL LEREUR
         IERR = 12
         RETURN
      ENDIF
C
C     MISE A ZERO DU NUMERO APRES DES SOMMETS DE CETTE SURFACE
      DO 10 I=1,NBXYZ
         NUAVAP( I ) = 0
 10   CONTINUE
C
C     MARQUAGE DES SOMMETS DE LA SURFACE
      NBT = 0
      DO 30 I=1,NBTRIA
         IF( NOTRIA(1,I) .GT. 0 ) THEN
            IF( NOSUTR(I) .EQ. NUSURF ) THEN
               NBT = NBT + 1
               DO 20 J=1,3
                  NUAVAP( NOTRIA(J,I) ) = 1
 20            CONTINUE
            ENDIF
         ENDIF
 30   CONTINUE
C
C     LES NOUVEAUX NUMEROS DES SOMMETS
      N = 0
      DO 40 I=1,NBXYZ
         IF( NUAVAP(I) .GT. 0 ) THEN
            N = N + 1
            NUAVAP(I) = N
         ENDIF
 40   CONTINUE
C
C     GENERATION DU TABLEAU XYZSOMMET DE CETTE SURFACE
C     ================================================
      CALL LXTSOU( NTLXSU, 'XYZSOMMET', NTXYZS, MNXYZS )
      IF( NTXYZS .GT. 0 ) THEN
C        CHANGEMENT DU NOM DU TABLEAU ACTUEL XYZSOMMET EN XYZSOMMET.Aiter
         KNOM = 'XYZSOMMET' // KNUADP
         CALL LXNMNM( NTLXSU, 'XYZSOMMET', KNOM )
      ENDIF
C
C     DECLARATION DU NOUVEAU TABLEAU
      CALL LXTNDC( NTLXSU, 'XYZSOMMET', 'MOTS', WYZSOM+3*N )
      CALL LXTSOU( NTLXSU, 'XYZSOMMET', NTXYZS, MNXYZS )
C
C     LE NOMBRE DE SOMMETS
      MCN( MNXYZS + WNBSOM ) = N
C
C     LES COORDONNEES DES SOMMETS
      MN = MNXYZS + WYZSOM - 1
      DO 80 I=1,NBXYZ
         NS = NUAVAP(I)
         IF( NS .GT. 0 ) THEN
            DO 70 J=1,3
               RMCN(MN+J) = REAL( XYZST(J,I) )
 70         CONTINUE
            MN = MN + 3
         ENDIF
 80   CONTINUE
C
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNXYZS) )
C
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNXYZS + WNBTGS ) = 0
      MCN( MNXYZS + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
C
C     GENERATION DU TABLEAU NSEF DE CETTE SURFACE
C     ===========================================
      CALL LXTSOU( NTLXSU, 'NSEF', NTNSEF, MNNSEF )
      IF( NTNSEF .GT. 0 ) THEN
C        CHANGEMENT DU NOM DU TABLEAU ACTUEL NSEF EN NSEF.Aiter
         KNOM = 'NSEF' // KNUADP
         CALL LXNMNM( NTLXSU, 'NSEF', KNOM )
      ENDIF
C
      CALL LXTNDC( NTLXSU, 'NSEF', 'MOTS', WUSOEF+4*NBT )
      CALL LXTSOU( NTLXSU, 'NSEF', NTNSEF, MNNSEF )
C
C     MISE A JOUR DU TABLEAU 'NSEF' DE CETTE SURFACE
C     TYPE DE L'OBJET : SURFACE
      MCN( MNNSEF + WUTYOB ) = 3
C     NUMERO DU TYPE DU MAILLAGE : MAILLAGE NON STRUCTURE
      MCN( MNNSEF + WUTYMA ) = 0
C     NOMBRE DE SOMMETS PAR NSEF
      MCN( MNNSEF + WBSOEF ) = 4
C     NOMBRE DE NSEF DU MAILLAGE
      MCN( MNNSEF + WBEFOB ) = NBT
C
C     LE NUMERO DES 3 SOMMETS DES TRIANGLES
      MN = MNNSEF + WUSOEF - 1
      DO 90 I=1,NBTRIA
         IF( NOTRIA(1,I) .GT. 0 ) THEN
            IF( NOSUTR(I) .EQ. NUSURF ) THEN
C              UN NOUVEAU TRIANGLE POUR CETTE SURFACE
               DO 85 J=1,3
                  MCN(MN+J) = NUAVAP( NOTRIA(J,I) )
 85            CONTINUE
               MN = MN + 4
               MCN(MN) = 0
            ENDIF
         ENDIF
 90   CONTINUE
C
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNNSEF) )
C
C     LE TYPE INCONNU DE FERMETURE DU MAILLAGE
      MCN( MNNSEF + WUTFMA ) = -1
C
C     PAS DE TANGENTES STOCKEES
      MCN( MNNSEF + WBTGEF ) =  0
      MCN( MNNSEF + WBEFTG ) =  0
      MCN( MNNSEF + WBEFAP ) =  0
C
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNNSEF + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
      END
