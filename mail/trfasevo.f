      SUBROUTINE TRFASEVO( NMVOLU, NOAXE, VNORMAL, IERR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES EF D'UN VOLUME INTERSECTES PAR UN PLAN
C -----    DEFINI PAR SON VECTEUR NORMAL
C
C ENTREES:
C --------
C NMVOLU : NOM DU VOLUME
C NOAXE  : =1 AXE X
C          =2 AXE Y
C          =3 AXE Z
C          =4 AXE AVEC VNORMAL=NORMALE AU PLAN
C VNORMAL: VECTEUR NORMAL AU PLAN POUR NOAXE=4
C
C SORTIE :
C --------
C IERR   : =0 SI PAS D'ERREUR RENCONTREE, >0 SINON
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire JL LIONS UPMC Paris Decembre 2006
C....................................................................012
      include"./incl/langue.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/gsmenu.inc"
      include"./incl/xyzext.inc"
      include"./incl/mecoit.inc"
      include"./incl/trvari.inc"
      include"./incl/a___face.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      CHARACTER*(*)     NMVOLU
C
      REAL              VNORMAL(3)
      REAL              PTPLAN(3), APTPLAN(3)
      REAL              XYZ0(3), XYZ(3)
      REAL              COPLAN, COPLANMIN, COPLANMAX
      REAL              XYZPLAN(3,4)
C
      MNNOFF = 0
      MNBAFF = 0
      MNNSFS = 0
      MNEFFS = 0
      MNXYBS = 0
      MNNOFT = 0
      MNDIFT = 0
C
      NBFAFR = 0
      NBFASE = 0
C
C     RECHERCHE OU GENERATION DU MAILLAGE DU VOLUME
C     ----------------------------------------------
      NBLGRC(NRERR) = 0
      CALL MAILEX( 'VOLUME', NMVOLU, NTNSEF, MNNSEF,
     %             NTXYZS, MNXYZS, IERR )
C     SI SORTIE DE LA DESTRUCTION DU VOLUME (IERR=82)
      IF( IERR .EQ. 82 ) GOTO 9000
      IF( IERR .NE. 0  ) THEN
         NBLGRC(NRERR) = NBLGRC(NRERR) + 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(NBLGRC(NRERR)) = 'VOLUME SANS MAILLAGE: ' // NMVOLU
         ELSE
            KERR(NBLGRC(NRERR)) = 'VOLUME WITHOUT MESH: ' // NMVOLU
         ENDIF
         CALL LEREUR
C        DESTRUCTION DU VOLUME NON MAILLE
         CALL LXLXDS( NTMN(4), NMVOLU )
         GOTO 9000
      ENDIF
      IF( MNXYZS .LE. 0 .OR. MNNSEF .LE. 0 ) GOTO 9000
C
C     MIN ET MAX DES COORDONNEES DES POINTS DU MAILLAGE DE L'OBJET
C     COOEXT(*,1)=MIN XYZ    COOEXT(*,2)=MAX XYZ
      NBCOOR = MCN(MNXYZS+WBCOOR)
      NBSOM  = MCN(MNXYZS+WNBSOM )
      CALL MIMXPT( NBCOOR, NBSOM, RMCN(MNXYZS+WYZSOM), COOEXT )
C
      IF( COOEXT(3,1) .EQ. COOEXT(3,2) ) THEN
C        HEXAEDRE REDUIT A UN RECTANGLE
C        INCORRECT POUR TRACER UN VOLUME!
         GOTO 9000
      ENDIF
C
C     NOMBRE D'ELEMENTS FINIS
      NBEFOB = MCN(MNNSEF + WBEFOB)
C
C     GENERATION PAR HACHAGE DES FACES DES CUBES
C     CHAINAGE DES FACES FRONTALIERES EN POSITION 7
C     AVEC UN LIEN NEGATIF POUR LES FACES FRONTALIERES
C     ------------------------------------------------
      CALL HAFAVO( NMVOLU, 3, NTFAVO, MNFAVO, NBSOM, IERR )
      IF( IERR .NE. 0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'VOLUME: ' // NMVOLU
         KERR(2) = 'IMPOSSIBLE DE CREER SES FACES'
         CALL LEREUR
         GOTO 9000
      ENDIF
C
C     LE NOMBRE D'ENTIERS PAR FACE
      MOFACE = MCN( MNFAVO + WOFACE )
C     LA MAJORATION DU NOMBRE DE FACES
      MXFACE = MCN( MNFAVO + WXFACE )
C     LE NOMBRE DE FACES FRONTALIERES
      NBFAFR = MCN( MNFAVO + WBFAFR )
C     LE NUMERO DE LA PREMIERE FACE FRONTALIERE
      L1FAFR = MCN( MNFAVO + W1FAFR )
C     LE NOMBRE DE FACES INTERFACES
      NBFA2M = MCN( MNFAVO + WBFA2M )
C     LE NUMERO DE LA PREMIERE FACE INTERFACE
      L1FA2M = MCN( MNFAVO + W1FA2M )
C
C     RESERVATION DES TABLEAUX DE CONSTRUCTION DES FACES
      NBFAFI = NBFAFR + NBFA2M
      CALL TNMCDC( 'ENTIER',   NBFAFI, MNNOFF )
      CALL TNMCDC( 'REEL',   3*NBFAFI, MNBAFF )
C
C     BARYCENTRE DES FACES FRONTALIERES
      CALL BAFAFR( MOFACE,MXFACE,MCN(MNFAVO+WFACES),RMCN(MNXYZS+WYZSOM),
     %             0, L1FAFR, NBFAFR, L1FA2M, NBFA2M,
     %             MCN(MNNOFF), RMCN(MNBAFF) )
C
C     LES FACES DES EF SECTIONNES PAR LE PLAN
ccc      MXFASE = NBEFOB * 4 + 1024
      MXFASE = MXFACE / 2 + 4096
      CALL TNMCDC( 'ENTIER', 4*MXFASE, MNNSFS )
      CALL TNMCDC( 'ENTIER',   MXFASE, MNEFFS )
      CALL TNMCDC( 'REEL',   3*MXFASE, MNXYBS )
C
C     MAX DE FACES A TRACER (FRONTALIERES ET SECTIONNEES)
      MXFATR = NBFAFI + MXFASE + 64
      CALL TNMCDC( 'ENTIER',  MXFATR, MNNOFT )
      CALL TNMCDC( 'REEL'  ,  MXFATR, MNDIFT )
C
C     COULEUR ET EPAISSEUR DES ARETES DES FACES
      CALL XVCOULEUR( NCOUAF )
      CALL XVEPAISSEUR( NEPARF )
C
C     DEFINITION DE LA VISEE AXONOMETRIQUE POUR DEFINIR LE PLAN DE SECTION
C     --------------------------------------------------------------------
C     POINT VU LE CENTRE DE L'HEXAEDRE ENGLOBANT
      AXOPTV(1) = ( COOEXT(1,1) + COOEXT(1,2) ) * 0.5
      AXOPTV(2) = ( COOEXT(2,1) + COOEXT(2,2) ) * 0.5
      AXOPTV(3) = ( COOEXT(3,1) + COOEXT(3,2) ) * 0.5
C     POSITION DE L'OEIL
      AXOEIL(1) = AXOPTV(1) + 4.0 * ( COOEXT(1,2) - COOEXT(1,1) )
      AXOEIL(2) = AXOPTV(2) + 3.0 * ( COOEXT(2,2) - COOEXT(2,1) )
      AXOEIL(3) = AXOPTV(3) + 3.5 * ( COOEXT(3,2) - COOEXT(3,1) )
C     LARGEUR/2 et HAUTEUR/2 de la FENETRE
      AXOLAR = (COOEXT(1,2)-COOEXT(1,1) + COOEXT(2,2)-COOEXT(2,1))*0.45
      AXOHAU = ( COOEXT(3,2) - COOEXT(3,1) ) * 0.85
C     PAS DE PLAN ARRIERE ET AVANT
      AXOARR = 0
      AXOAVA = 0
      CALL AXONOMETRIE( AXOPTV, AXOEIL, AXOLAR, AXOHAU, AXOARR, AXOAVA )

C     TRACE DE LA QUALITE DES ELEMENTS FINIS
      LCRITR = 1
C
C     INITIALISATION DE L'ORBITE ZOOM DEPLACEMENT
C     -------------------------------------------
C     LE BARYCENTRE DE L'HEXAEDRE ENGLOBANT SERT DE POINT DE DEPART
C     DU PLAN DE SECTION
      PTPLAN(1) = AXOPTV(1)
      PTPLAN(2) = AXOPTV(2)
      PTPLAN(3) = AXOPTV(3)
C
C     LES COORDONNEES AXONOMETRIQUES DU POINT DU PLAN
      CALL XYZAXO( PTPLAN, APTPLAN )
C
      IF ( NOAXE .LE. 3 ) THEN
C        COORDONNEE SELON L'AXE NOAXE
         COPLANMIN = COOEXT(NOAXE,1)
         COPLANMAX = COOEXT(NOAXE,2)
      ELSE
C        LA COORDONNEE SELON LA DIRECTION NORMALE
         COPLAN0   = PROSCR( PTPLAN,      VNORMAL, 3 )
         COPLANMIN = PROSCR( COOEXT(1,1), VNORMAL, 3 )
         COPLANMAX = PROSCR( COOEXT(1,2), VNORMAL, 3 )
      ENDIF
      IF( COPLANMIN .GT. COPLANMAX ) THEN
         C         = COPLANMIN
         COPLANMIN = COPLANMAX
         COPLANMAX = C
      ENDIF
C
C     ECART MAXIMAL MIN-MAX = DIAGONALE DE L'HEXAEDRE
      ECMX = SQRT( (COOEXT(1,2)-COOEXT(1,1))**2 +
     %             (COOEXT(2,2)-COOEXT(2,1))**2 +
     %             (COOEXT(3,2)-COOEXT(3,1))**2 )
C
C     INITIALISATION POUR UN TRACE 3D AVEC TRANSLATION ORBITE ZOOM
      CALL ORBITE0( NOTYEV )
      IF( NOTYEV .EQ. 0 ) GOTO 9000
C
      NOPX0 = LAPXFE / 2
      NOPY0 = LHPXFE / 2
C     LE POINTEUR DE LA SOURIS EST CENTRE DANS LA FENETRE
      CALL DEPLSOURIS( NOPX0, NOPY0 )
C
C     MISE A JOUR DU TRACE 3D AVEC TRANSLATION ORBITE TRANSLATION
C     -----------------------------------------------------------
 15   CALL ORBITE2( NOTYEV )
      IF( NOTYEV .EQ.  0 ) GOTO 9000
      IF( NOTYEV .EQ. -3 ) THEN
C
C        DEPLACEMENT EN COORDONNEE AXONOMETRIQUE
C        PAR RAPPORT A CELUI EN PIXELS
         APTPLAN(1) = APTPLAN(1) + ECMX*2 * FLOAT( NOPX1 - NOPX0 )
     %                           / FLOAT(LAPXFE)
         APTPLAN(2) = APTPLAN(2) + ECMX*2 * FLOAT( NOPY0 - NOPY1 )
     %                           / FLOAT(LHPXFE)
C        PASSAGE AUX COORDONNEES CARTESIENNES
         CALL AXOXYZ( APTPLAN, PTPLAN )
C
C        SELON NOAXE CALCUL DE COPLAN SELON LA NORMALE AU PLAN
C        ET DES 4 SOMMETS DU CARRE DE VISUALISATION DU PLAN
         CALL SCTPLN( NOAXE,  VNORMAL,
     %                PTPLAN, COPLAN0, COPLANMIN, COPLANMAX,
     %                COPLAN, XYZPLAN )
C
C        MISE A JOUR DU PIXEL INITIAL
         NOPX0 = NOPX1
         NOPY0 = NOPY1
      ENDIF
C
C     LISTE DES NO DES SOMMETS DES FACES DES EF 3D SECTIONNES PAR
C     LE PLAN DE COORDONNEE COPLAN DANS LA DIRECTION DE LA NORMALE AU PLAN
C     --------------------------------------------------------------------
      CALL TRFASE1( NOAXE,  VNORMAL, COPLAN,
     %              NBSOM,  RMCN(MNXYZS+WYZSOM), MNNSEF,
     %              MXFASE, NBFASE, MCN(MNNSFS), MCN(MNEFFS),
     %              RMCN(MNXYBS) )
C
C     CALCUL DE LA DISTANCE DU BARYCENTRE DES FACES A L'OEIL
C     1. LES FACES FRONTALIERES
      MNBA = MNBAFF
      DO 20 NF = 0, NBFAFI-1
C        COTE Z AXONOMETRIQUE DANS LA DIRECTION DE VISEE
         CALL XYZAXO( RMCN(MNBA), XYZ )
         RMCN(MNDIFT+NF) = XYZ(3)
C        NO POSITIF POUR SAVOIR QUE C'EST UNE FACE FRONTALIERE
C        NO DE LA FACE DANS LE TABLEAU LFACES DES FACES DES EF
         MCN(MNNOFT+NF) = MCN(MNNOFF+NF)
         MNBA = MNBA + 3
 20   CONTINUE
C
C     2. LES FACES DES EF SECTIONNES
      MNBA = MNXYBS
      DO 30 NF = 0, NBFASE-1
C        COTE Z AXONOMETRIQUE DANS LA DIRECTION DE VISEE
         CALL XYZAXO( RMCN(MNBA), XYZ )
         RMCN(MNDIFT+NBFAFI+NF) = XYZ(3)
C        NO NEGATIF POUR SAVOIR QUE C'EST UNE FACE DE SECTION
          MCN(MNNOFT+NBFAFI+NF) = -(1+NF)
         MNBA = MNBA + 3
 30   CONTINUE
C
C     3. LES 4 ARETES DU RECTANGLE DU PLAN DE SECTION
      NBFACT = NBFAFI + NBFASE
      NF0 = 4
      DO 40 NF = 1, 4
C        COTE Z AXONOMETRIQUE DANS LA DIRECTION DE VISEE
         XYZ0(1) = (XYZPLAN(1,NF0) + XYZPLAN(1,NF)) * 0.5
         XYZ0(2) = (XYZPLAN(2,NF0) + XYZPLAN(2,NF)) * 0.5
         XYZ0(3) = (XYZPLAN(3,NF0) + XYZPLAN(3,NF)) * 0.5
         CALL XYZAXO( XYZ0, XYZ )
         RMCN(MNDIFT+NBFACT+NF-1) = XYZ(3)
C        NO>NBFACT POUR SAVOIR QUE C'EST UNE ARETE DU RECTANGLE DU PLAN
         MCN(MNNOFT+NBFACT+NF-1) = -(NBFACT + NF)
         NF0 = NF
 40   CONTINUE
C
C     NOMBRE DE TRACES A FAIRE ET A TRIER
      NBFATR = NBFACT + 4
C
C     LE TRI PAR TAS DE CETTE DISTANCE DE LA FACE A L'OEIL
C     ----------------------------------------------------
      CALL TRITRP( NBFATR, RMCN(MNDIFT), MCN(MNNOFT) )
C     ICI MCN(MNNOFT) = NUMERO DE LA FACE LA PLUS PROCHE DE L'OEIL
C
C     LA MEMOIRE PIXELS EST EFFACEE
      CALL EFFACEMEMPX
C
C     TRACE DES AXES
      CALL TRAXE3
C
C     TRACE FIL DE FER DES FACES FRONTALIERES
C     TRACE COULEUR    DES FACES DES EF SECTIONNES PAR LE PLAN
C     --------------------------------------------------------
      CALL TRFASE2( NBSOM, RMCN(MNXYZS+WYZSOM), MNNSEF,
     %              NBFATR, MCN(MNNOFT),
     %              NBFASE, MCN(MNNSFS), MCN(MNEFFS), RMCN(MNDIFT),
     %              MOFACE, MCN(MNFAVO+WFACES),
     %              NBFACT, XYZPLAN )
C
C     TRACE DU NOM DU VOLUME
      CALL TRFINS( NMVOLU )
C
C     REPRISE DE L'ORBITE
      GOTO 15
C
C     DESTRUCTION DES TABLEAUX SERVANT AU TRI
 9000 IF( MNNSFS .GT. 0 ) CALL TNMCDS( 'ENTIER', 4*MXFASE, MNNSFS )
      IF( MNEFFS .GT. 0 ) CALL TNMCDS( 'ENTIER',   MXFASE, MNEFFS )
      IF( MNXYBS .GT. 0 ) CALL TNMCDS( 'REEL',   3*MXFASE, MNXYBS )
      IF( MNNOFF .GT. 0 ) CALL TNMCDS( 'ENTIER',   NBFAFI, MNNOFF )
      IF( MNBAFF .GT. 0 ) CALL TNMCDS( 'REEL',   3*NBFAFI, MNBAFF )
      IF( MNNOFT .GT. 0 ) CALL TNMCDS( 'ENTIER',   MXFATR, MNNOFT )
      IF( MNDIFT .GT. 0 ) CALL TNMCDS( 'REEL'  ,   MXFATR, MNDIFT )
C
      RETURN
      END
