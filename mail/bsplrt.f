      SUBROUTINE BSPLRT( DEGREX, LRX, RX,
     %                   DEGREY, LRY, RY, SPLINE,
     %                   NBSOM,  XYZSOM,
     %                   NBTGS , XYZTGS, XYZTST,
     %                   NBTRIA, NOSTTR, NOTGTR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRANSFORMATION DES SOMMETS DE LA TRIANGULATION DU RECTANGLE
C -----    PLAN AUX SOMMETS DE LA TRIANGULATION DE LA SURFACE B-SPLINE
C          CALCUL DU GRADIENT DE LA TRANSFORMATION EN CHAQUE SOMMET
C
C ENTREES:
C --------
C DEGREX : DEGRE DES POLYNOMES DE LA B-SPLINE
C LRX    : NOMBRE DE POINTS-1 DE CONTROLE (R) DE LA LIGNE
C RX     : LES EXTREMITES DES INTERVALLES DU PARAMETRE EN X
C DEGREY : DEGRE DES POLYNOMES DE LA B-SPLINE
C LRY    : NOMBRE DE POINTS-1 DE CONTROLE (R) DE LA LIGNE
C RY     : LES EXTREMITES DES INTERVALLES DU PARAMETRE EN Y
C SPLINE : LES COEFFICIENTS DES POLYNOMES SUR CHAQUE INTERVALLE
C NBSOM  : NOMBRE DES SOMMETS DE LA TRIANGULATION
C NBTGS  : NOMBRE DES TANGENTES A CALCULER (VALEUR MISE A JOUR)
C NBTRIA : NOMBRE DE TRIANGLES DE LA TRIANGULATION
C NOSTTR : NUMERO DES 3 SOMMETS + 0 DES NBTRIA TRIANGLES DE LA B-SPLINE
C NOTGTR : NUMERO DES 6 TANGENTES +0+0 DES NBTRIA TRIANGLES
C
C SORTIES:
C --------
C XYZSOM : 3 COORDONNEES DES SOMMETS DES TRIANGLES SUR LA B-SPLINE
C XYZTGS : 3 COORDONNEES DES 6 TANGENTES DES TRIANGLES SUR LA B-SPLINE
C
C TABLEAU AUXILIAIRE:
C -------------------
C XYZTST : 3 COORDONNEES DES 2 COMPOSANTES DU GRADIENT
C          EN CHACUN DES NBSOM SOMMETS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS         MAI 1996
C2345X7..............................................................012
      INTEGER      DEGREX,DEGREY
      REAL         RX(0:LRX),RY(0:LRY),
     %             SPLINE(0:DEGREX,0:DEGREY,0:LRX-1,0:LRY-1,1:3),
     %             XYZSOM(1:3,1:NBSOM)
      REAL         XYZTGS(1:3,1:6,1:NBTRIA),
     %             XYZTST(1:3,1:2,1:NBSOM)
      INTEGER      NOSTTR(1:4,1:NBTRIA), NOTGTR(1:8,1:NBTRIA)
C
      REAL         DIRTG(2)
      INTEGER      NOTGAR(2), NS(2)
      EQUIVALENCE (NS1,NS(1)),(NS2,NS(2))
C
C     LA TRANSFORMATION DES SOMMETS DE LA TRIANGULATION DU RECTANGLE PLAN
C     AUX SOMMETS DE LA TRIANGULATION DE LA SURFACE B-SPLINE
C     EST FAITE APRES CALCUL DES TANGENTES CAR LES 3 NOUVELLES
C     COORDONNEES DES NBSOM SOMMETS ECRASENT LES ANCIENNES
C     REMARQUE : LE SP XYZBSP CALCULE A LA FOIS LE SOMMET ET SON GRADIENT
      IF( NBTGS .LE. 0 ) GOTO 90
C
C     LE GRADIENT AUX SOMMETS DE LA TRIANGULATION
C     ===========================================
      DO 5 I = 1, NBSOM
C        LES 2 COORDONNEES DANS LE RECTANGLE PLAN
         X = XYZSOM(1,I)
         Y = XYZSOM(2,I)
C        LES 3 COORDONNEES DES 2 COMPOSANTES DU GRADIENT AU SOMMET N
C        SUR LA SURFACE B-SPLINE
         CALL XYZBTG( X, Y,
     %                DEGREX, LRX, RX,
     %                DEGREY, LRY, RY, SPLINE,
     %                XYZTST(1,1,I) )
 5    CONTINUE
C
C     CALCUL DES 6 TANGENTES DANS CHAQUE TRIANGLE A PARTIR
C     DU GRADIENT DEJA CALCULE EN CHAQUE SOMMET DE LA TRIANGULATION
C     =============================================================
C     LE NOMBRE ACTUEL DE TANGENTES CALCULEES
      NBTGS  = 0
      DO 40 NBT = 1, NBTRIA
C
C        LA BOUCLE SUR LES ARETES DU TRIANGLE NBT
         NBTG = 0
         DO 20 I=1,3
C
            IF( I .LT. 3 ) THEN
               II = I + 1
            ELSE
               II = 1
            ENDIF
C
C           LE NUMERO DANS XYZSOMMET DES 2 SOMMETS DE L'ARETE I
            NS1 = NOSTTR(  I, NBT )
            NS2 = NOSTTR( II, NBT )
C
C           DERIVEE DE DIRECTION S1 VERS S2
            DIRTG(1) = XYZSOM(1,NS2) - XYZSOM(1,NS1)
            DIRTG(2) = XYZSOM(2,NS2) - XYZSOM(2,NS1)
C
C           LE NUMERO DES 2 TANGENTES DE L'ARETE I DU TRIANGLE
            CALL TGAREF( 3, I, NOTGAR )
C
C           LES COMPOSANTES DE LA DIRECTION DES 2 TANGENTES DANS LE RECTANGLE
            DO 10 K=1,2
C
C              CALCUL DE LA TANGENTE SELON CETTE DIRECTION
C              EN FAISANT LE PRODUIT DU [GRADIENT] {DIRECTION}
C              UNE TANGENTE DE PLUS
               NBTGS = NBTGS + 1
               NBTG  = NBTG  + 1
C              LE NUMERO DE LA TG K DE CETTE ARETE
               NTG = NOTGAR(K)
               NOTGTR( NTG, NBT ) = NBTGS
C
C              LES 3 COORDONNEES DE LA TANGENTE
               XYZTGS(1,NBTG,NBT) = XYZTST(1,1,NS(K)) * DIRTG(1)
     %                            + XYZTST(1,2,NS(K)) * DIRTG(2)
C
               XYZTGS(2,NBTG,NBT) = XYZTST(2,1,NS(K)) * DIRTG(1)
     %                            + XYZTST(2,2,NS(K)) * DIRTG(2)
C
               XYZTGS(3,NBTG,NBT) = XYZTST(3,1,NS(K)) * DIRTG(1)
     %                            + XYZTST(3,2,NS(K)) * DIRTG(2)
C
C              CHANGEMENT DU SENS DE LA DIRECTION DE LA TANGENTE
               DIRTG(1) = -DIRTG(1)
               DIRTG(2) = -DIRTG(2)
 10         CONTINUE
 20      CONTINUE
C
C        LES 2 TANGENTES NULLES SONT AJOUTEES
         NOTGTR(7,NBT) = 0
         NOTGTR(8,NBT) = 0
 40   CONTINUE
C
C     TRANSFORMATION DES SOMMETS DE LA TRIANGULATION DU RECTANGLE PLAN
C     AUX SOMMETS DE LA TRIANGULATION DE LA SURFACE B-SPLINE
C     ================================================================
 90   DO 100 I = 1, NBSOM
C        LES 2 COORDONNEES DANS LE RECTANGLE PLAN
         X = XYZSOM(1,I)
         Y = XYZSOM(2,I)
C        LES 3 COORDONNEES DU SOMMET N SUR LA SURFACE B-SPLINE
         CALL XYZBST( X, Y,
     %                DEGREX, LRX, RX,
     %                DEGREY, LRY, RY, SPLINE,
     %                XYZSOM(1,I) )
 100  CONTINUE
      END
