      SUBROUTINE TRIARFR( MOARFR, L1ARFR, LAREFR, XYZPOI,
     %                    NBARFR, NOARFR, XYZBAR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LISTER LES ARETES FRONTALIERES DEFINIES DANS LAREFR
C -----    POUR LES TRIER SELON L'ALGORITHME DU PEINTRE
C
C ENTREES:
C --------
C MOARFR : NOMBRE DE MOTS PAR ARETE FRONTALIERE DU TABLEAU LAREFR
C L1ARFR : NUMERO DE LA PREMIERE ARETE FRONTALIERE DANS LAREFR
C LAREFR : TABLEAU NUMERO DES 2 SOMMETS ET LIEN
C          LAREFR(1,I)= NO DU 1-ER  SOMMET DE L'ARETE FRONTALIERE
C          LAREFR(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LAREFR(3,I)= 0 OU NUMERO DE L'ARETE FRONTALIERE SUIVANTE
C
C SORTIES:
C --------
C NBARFR : NOMBRE D'ARETES FRONTALIERES
C NOARFR : NO DE L'ARETE FRONTALIERE DANS LAREFR
C XYZBAR : XYZ DU BARYCENTRE DES ARETES FRONTALIERES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire JL LIONS UPMC Paris Decembre 2006
C23456...............................................................012
      INTEGER  LAREFR(1:MOARFR,1:*)
      INTEGER  NOARFR(NBARFR)
      REAL     XYZPOI(1:3,1:*)
      REAL     XYZBAR(3,NBARFR)
C
C     BOUCLE SUR LES ARETES FRONTALIERES
      NBARFR = 0
      NA     = L1ARFR
C
 10   IF( NA .GT. 0 ) THEN
C
C        NO DE L'ARETE FRONTALIERE DANS LAREFR
         NBARFR = NBARFR + 1
         NOARFR( NBARFR ) = NA
C
C        LE BARYCENTRE DE L'ARETE
         N1 = LAREFR(1,NA)
         N2 = LAREFR(2,NA)
         XYZBAR( 1, NBARFR ) =  ( XYZPOI(1,N1) + XYZPOI(1,N2) ) * 0.5
         XYZBAR( 2, NBARFR ) =  ( XYZPOI(2,N1) + XYZPOI(2,N2) ) * 0.5
         XYZBAR( 3, NBARFR ) =  ( XYZPOI(3,N1) + XYZPOI(3,N2) ) * 0.5
C
C        PASSAGE A L'ARETE SUIVANTE
         NA = LAREFR(3,NA)
         GOTO 10
C
      ENDIF
      END
