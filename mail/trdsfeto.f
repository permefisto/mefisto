      SUBROUTINE TRDSFETO( NOSOTR, N1FEOC, NFETOI, NF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LE TRIANGLE DE SOMMET NOSOTR EST IL UNE FACE DE NFETOI?
C -----
C ENTREES:
C --------
C NOSOTR : NUMERO DES 3 SOMMETS DU TRIANGLE
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : LES FACES TRIANGULAIRES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C SORTIES:
C --------
C NF     : =0 SI TRIANGLE NON RETROUVE DANS NFETOI
C          >0 NUMERO DANS NFETOI DE NOSOTR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & VEULETTES SUR MER   NOVEMBRE 2014
C2345X7..............................................................012
      INTEGER  NFETOI(5,*), NOSOTR(3), NOSOTR2(3), NOSOTR3(3)

C     TRI CROISSANT DES 3 SOMMETS
      CALL TRI3NO( NOSOTR, NOSOTR2 )

C     PARCOURS DES FACES DE L'ETOILE
      NF = N1FEOC
 10   IF( NF .GT. 0 ) THEN
         CALL TRI3NO( NFETOI(2,NF), NOSOTR3 )
         IF( NOSOTR2(1) .EQ. NOSOTR3(1) .AND.
     %       NOSOTR2(2) .EQ. NOSOTR3(2) .AND.
     %       NOSOTR2(3) .EQ. NOSOTR3(3) ) THEN
            GOTO 9999
         ENDIF
         NF = NFETOI(5,NF)
         GOTO 10
      ENDIF
C
C     ICI NF=0 TRIANGLE NON RETROUVE
 9999 RETURN
      END
