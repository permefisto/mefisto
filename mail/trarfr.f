      SUBROUTINE TRARFR( NOCOUL, MOARFR, L1ARFR, LAREFR, XYZPOI )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES ARETES FRONTALIERES DEFINIES DANS LAREFR
C -----    SELON LA COULEUR NOCOUL
C
C ENTREES:
C --------
C NOCOUL : NUMERO DE LA COULEUR DE TRACE DES ARETES
C MOARFR : NOMBRE DE MOTS PAR ARETE FRONTALIERE DU TABLEAU LAREFR
C L1ARFR : NUMERO DE LA PREMIERE ARETE FRONTALIERE DANS LAREFR
C LAREFR : TABLEAU NUMERO DES 2 SOMMETS ET LIEN
C          LAREFR(1,I)= NO DU 1-ER  SOMMET DE L'ARETE FRONTALIERE
C          LAREFR(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LAREFR(3,I)= 0 OU NUMERO DE L'ARETE FRONTALIERE SUIVANTE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1994
C ...................................................................012
      INTEGER  LAREFR(1:MOARFR,1:*)
      REAL     XYZPOI(1:3,1:*)
C
C     BOUCLE SUR LES ARETES FRONTALIERES
      N = L1ARFR
C
 10   IF( N .GT. 0 ) THEN
C
C        TRACE DE L'ARETE FRONTALIERE N
         CALL TRAIT3D( NOCOUL, XYZPOI(1,LAREFR(1,N)),
     %                         XYZPOI(1,LAREFR(2,N)) )
C
C        PASSAGE A L'ARETE SUIVANTE
         N = LAREFR(3,N)
         GOTO 10
C
      ENDIF
C
      RETURN
      END
