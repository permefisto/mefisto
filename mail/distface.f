      SUBROUTINE DISTFACE( NBSOM,  XYZSOM, NBEFOB, NOSOEF,
     %                     DICRFA, NOFACT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  CONSTRUIRE LES TABLEAUX DES DISTANCES A L'OEIL ET
C -----  NUMERO DES FACES TRIEES SELON LEUR ELOIGNEMENT A L'OEIL DE VISEE
C        LA DIRECTION DE VISEE PTV-OEIL: DIREVI est SUPPOSEE INITIALISEE

C ENTREES:
C --------
C NBSOM  : NOMBRE DE FACES DU MAILLAGE
C XYZSOM : XYZ 3 COORDONNEES CARTESIENNES DES FACES DU MAILLAGE
C NBEFOB : NOMBRE DE FACES:TRIANGLES OU QUADRANGLES DU MAILLAGE
C NOSOEF : NO DES 4 FACES DES TRIANGLES ET/OU QUADRANGLES DU MAILLAGE

C SORTIES:
C --------
C DICRFA : DISTANCE CROISSANTE DU BARYCENTRE DES FACES A L'OEIL DE VISEE
C NOFACT : NUMERO DANS NOSOEF DES FACES TRIEES SELON LA DISTANCE
C          CROISSANTE A L'OEIL. (LES PREMIERES SONT LES PLUS PROCHES)
C          NOFACT(n) = INDICE DANS NOSOEF DE LA n-eme FACE TRIEE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : Alain PERRONNET  Saint PIERRE du PERRAY            Avril 2020
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      INTEGER        NOSOEF(4,NBEFOB), NOFACT(NBEFOB)
      REAL           XYZSOM(3,NBSOM),  DICRFA(NBEFOB)
      REAL           BARYCENT(3)

C     LA DIRECTION DE VISEE PTV-OEIL: DIREVI est SUPPOSEE INITIALISEE
C     CALCUL DE LA DISTANCE DU BARYCENTRE DES FACES A L'OEIL selon DIREVI
C     -------------------------------------------------------------------
      DO NOFACE = 1, NBEFOB

C        CALCUL du BARYCENTRE de la FACE NOFACE
         CALL BARYFACE( NOFACE, NOSOEF, XYZSOM, BARYCENT )
C
C        ELOIGNEMENT DU POINT PAR RAPPORT A L'OEIL PASSAGE EN AXONOMETRIE
         CALL XYZAXO( BARYCENT, BARYCENT )

C        LE SIGNE - POUR METTRE EN PREMIER LES PLUS ELOIGNEES
         DICRFA( NOFACE ) = -BARYCENT(3)
C
C        NUMERO INITIAL DE LA FACE NOFACE AVANT LE TRI DANS NOSOEF
         NOFACT( NOFACE ) = NOFACE

      ENDDO

C     LE TRI CROISSANT DES DISTANCES (COTE -Z AXONOMETRIQUE) A L'OEIL
C     ---------------------------------------------------------------
      CALL TRITRP( NBEFOB, DICRFA, NOFACT )
C     NOFACT(N): NUMERO INITIAL DANS NOSOEF DE LA FACE N APRES LE TRI 

      RETURN
      END
