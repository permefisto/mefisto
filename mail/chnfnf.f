      SUBROUTINE CHNFNF( NBARCF, N1ARCF, NOARCF,
     %                   NBARINCF, NOSTARINCF,
     %                   NBTRCF, NOTRCF0, NOSTTR,
ccc     %                   PTXYZD,
     %                   MXFACO, LEFACO, N1FASC,
     %                   NOTRCF1, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     ESSAI D'ECHANGER UNE FILE DE TRIANGLES PERDUS
C -----     PAR UNE SUITE DE TRIANGLES APPARTENANT A LA TETRAEDRISATION
C
C ENTREES:
C --------
C NBARCF : NOMBRE D'ARETES DU CF DES TRIANGLES PERDUS
C N1ARCF : NO NOARCF DE LA PREMIERE ARETE DU CF DES TRIANGLES PERDUS
C NOARCF : NO SOMMET, NO ARETE SUIVANTE, NO TRIANGLE ADJACENT A L'ARETE
C NBARINCF: NOMBRE D'ARETES INTERNES AU CF (2 SOMMETS DU CF) ET
C           APPARTENANT A LA TETRAEDRISATION
C NOSTARINCF: NO DES 2 SOMMETS DE L'ARETE INTERNE AU CF
C NBTRCF : NOMBRE DE TRIANGLES PERDUS DU CF
C NOTRCF0: NUMERO LEFACO DES NBTRCF FACES PERDUES
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C
C AUXILIAIRES:
C ------------
C NOSTTR : NO DES 3 SOMMETS ET TRIANGLES LEFACO ADJACENTS
C
C MODIFIES:
C ---------
C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO
C LEFACO : LES 3 SOMMETS, 2 MATERIAUX, 3 FACES VOISINES ET CHAINAGE
C          DES FACES TRIANGULAIRES DU CONTOUR ET INTERFACES
C N1FASC : N1FASC(NS) = NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS
C
C SORTIES:
C --------
C NOTRCF1: NO LEFACO DES NOUVEAUX TRIANGLES DU CF
C NONOUI : 1 SI CHANGEMENT DE LA TRIANGULATION, 0 PAS DE CHANGEMENT
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   DECEMBRE 2005
C....................................................................012
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
ccc      DOUBLE PRECISION  PTXYZD(1:4,1:*)
      INTEGER           LEFACO(11,0:MXFACO),
     %                  N1FASC(*)
      INTEGER           NOTRCF0(NBTRCF), NOTRCF1(NBTRCF),
     %                  NOSTTR(6,NBTRCF)
      INTEGER           NOARCF(3,*),
     %                  NOSTARINCF(2,NBARINCF)
C
      NONOUI = 0
C
CCC      DO 2 N=1,NBTRCF
CCCC     TRACE DE LA FACE INITIALE DE LEFACO
CCC      CALL TRFATR( NCBLAN, NCVERT, LEFACO(1,NOTRCF0(I)), PTXYZD )
CCC 2    CONTINUE
C
C     LE PREMIER TRIANGLE DU CF
      NF0 = NOTRCF0(1)
C
C     LES VOLUMES DE LA FACE NF0
      NV1 = LEFACO(4,NF0)
      NV2 = LEFACO(5,NF0)
C
      DO 10 N = 2, NBTRCF
         NF1 = NOTRCF0( N )
         NW1 = LEFACO(4,NF1)
         NW2 = LEFACO(5,NF1)
         IF( (NV1 .NE. NW1 .AND. NV1 .NE. NW2) .OR.
     %       (NV2 .NE. NW1 .AND. NV2 .NE. NW2) ) RETURN
 10   CONTINUE
C
C     1-ERE ETAPE: LA TRIANGULATION ENVISAGEE EST ELLE COHERENTE?
C     ===========================================================
C     NB TRIANGLES LEFACO GENERES
      NBTRCF1 = 0
C
C     LES TRIANGLES FORMES D'AUCUNE ARETE DU CF
C     ET DE 3 ARETES INTERNES AU CF (MAIS DE LA TETRAEDRISATION)
C     BOUCLE SUR LES ARETES EXTERNES DU CF
      DO 90 I3=1,NBARINCF-2
C
C        L'ARETE N INTERNE A POUR SOMMETS
         NS1 = NOSTARINCF(1,I3)
         NS2 = NOSTARINCF(2,I3)
C
C        RECHERCHE DES 2 ARETES INTERNES AU CF ET DE LA TETRAEDRISATION
C        DE SOMMETS INITIAUX NS1 ET NS2 ET DE SOMMET FINAL NS3 COMMUN
         DO 40 I1 = I3+1, NBARINCF
            IF( NS1 .EQ. NOSTARINCF(1,I1) ) GOTO 42
            IF( NS1 .EQ. NOSTARINCF(2,I1) ) GOTO 45
 40      CONTINUE
         GOTO 90
C        LE SOMMET FINAL DE L'ARETE INTERNE ISSUE DE NS1
 42      NS3 = NOSTARINCF(2,I1)
         GOTO 50
C        LE SOMMET FINAL DE L'ARETE INTERNE ISSUE DE NS1
 45      NS3 = NOSTARINCF(1,I1)
C
C        RECHERCHE DE L'ARETE INTERNE ISSUE DE NS2 ET DE SOMMET NS3
 50      DO 60 I2 = I3+1, NBARINCF
            IF( I2 .EQ. I1 ) GOTO 60
            IF( NS2 .EQ. NOSTARINCF(1,I2) ) THEN
               IF( NS3 .EQ. NOSTARINCF(2,I2) ) GOTO 80
            ELSE
               IF( NS2 .EQ. NOSTARINCF(2,I2) ) THEN
                  IF( NS3 .EQ. NOSTARINCF(1,I2) ) GOTO 80
               ENDIF
            ENDIF
 60      CONTINUE
         GOTO 90
C
C        CONSTRUCTION DU TRIANGLE NS1-NS2-NS3 DANS LEFACO
 80      NBTRCF1 = NBTRCF1 + 1
         NOSTTR(1,NBTRCF1) = NS1
         NOSTTR(2,NBTRCF1) = NS2
         NOSTTR(3,NBTRCF1) = NS3
         NOSTTR(4,NBTRCF1) = -1
         NOSTTR(5,NBTRCF1) = -1
         NOSTTR(6,NBTRCF1) = -1
CCC         PRINT *,'CHNFNF: TRIANGLE3I ',NBTRCF1,' ST',NS1,NS2,NS3
 90   CONTINUE
      NBTR3I = NBTRCF1
C
C     LES TRIANGLES FORMES D'UNE ARETE DU CF
C     ET DE 2 ARETES INTERNES AU CF (MAIS DE LA TETRAEDRISATION)
      NA1 = N1ARCF
C     BOUCLE SUR LES ARETES EXTERNES DU CF
      DO 195 N=1,NBARCF
C
C        L'ARETE N DU CF EST NA1
C        LES 2 SOMMETS DE L'ARETE DU CF
         NS1 = NOARCF(1,NA1)
C        L'ARETE SUIVANTE
         NA2 = NOARCF(2,NA1)
         NS2 = NOARCF(1,NA2)
C
C        RECHERCHE DES 2 ARETES INTERNES AU CF ET DE LA TETRAEDRISATION
         DO 170 I1 = 1, NBARINCF
            IF( NS1 .EQ. NOSTARINCF(1,I1) ) THEN
C              LE SOMMET FINAL NS3 DE L'ARETE INTERNE ISSUE DE NS1
               NS3 = NOSTARINCF(2,I1)
            ELSE
               IF( NS1 .NE. NOSTARINCF(2,I1) ) GOTO 170
C              LE SOMMET FINAL NS3 DE L'ARETE INTERNE ISSUE DE NS1
               NS3 = NOSTARINCF(1,I1)
            ENDIF
C
C           RECHERCHE DE L'AUTRE ARETE INTERNE DE SOMMETS NS2-NS3
            DO 160 I2 = 1, NBARINCF
               IF( NS2 .EQ. NOSTARINCF(1,I2) ) THEN
                  IF( NS3 .EQ. NOSTARINCF(2,I2) ) GOTO 180
               ELSE
                  IF( NS2 .EQ. NOSTARINCF(2,I2) ) THEN
                     IF( NS3 .EQ. NOSTARINCF(1,I2) ) GOTO 180
                  ENDIF
               ENDIF
 160        CONTINUE
 170     CONTINUE
         GOTO 190
C
C        CONSTRUCTION DU TRIANGLE NS1-NS2-NS3 DANS LEFACO
 180     NBTRCF1 = NBTRCF1 + 1
         NOSTTR(1,NBTRCF1) = NS1
         NOSTTR(2,NBTRCF1) = NS2
         NOSTTR(3,NBTRCF1) = NS3
         NOSTTR(4,NBTRCF1) = NOARCF(3,NA1)
         NOSTTR(5,NBTRCF1) = -1
         NOSTTR(6,NBTRCF1) = -1
CCC         PRINT *,'CHNFNF: TRIANGLE2I ',NBTRCF1,' ST',NS1,NS2,NS3
C
C        PASSAGE A L'ARETE SUIVANTE DU CF
 190     NA1 = NA2
 195  CONTINUE
      NBTR2I = NBTRCF1 - NBTR3I
C
C     LES TRIANGLES AVEC 2 ARETES CONSECUTIVES CF ET 1 ARETE INTERNE
      NA1 = N1ARCF
      DO 250 N=1,NBARCF
C
C        LE TRIANGLE CONTIENT L'ARETE NA1 ET SA SUIVANTE DU CF
         NS1 = NOARCF(1,NA1)
         NA2 = NOARCF(2,NA1)
         NS2 = NOARCF(1,NA2)
         NA3 = NOARCF(2,NA2)
         NS3 = NOARCF(1,NA3)
C
C        RECHERCHE DE L'ARETE NS3-NS1 DANS LES ARETES INTERNES
         DO 220 M=1,NBARINCF
            NS4 = NOSTARINCF(1,M)
            NS5 = NOSTARINCF(2,M)
            IF( (NS3 .EQ. NS4 .AND. NS1 .EQ. NS5) .OR.
     %          (NS3 .EQ. NS5 .AND. NS1 .EQ. NS4) ) GOTO 230
 220     CONTINUE
         GOTO 240
C
C        CONSTRUCTION DU TRIANGLE NS1-NS2-NS3 DANS LEFACO
 230     NBTRCF1 = NBTRCF1 + 1
         NOSTTR(1,NBTRCF1) = NS1
         NOSTTR(2,NBTRCF1) = NS2
         NOSTTR(3,NBTRCF1) = NS3
         NOSTTR(4,NBTRCF1) = NOARCF(3,NA1)
         NOSTTR(5,NBTRCF1) = NOARCF(3,NA2)
         NOSTTR(6,NBTRCF1) = -1
CCC         PRINT *,'CHNFNF: TRIANGLE1I ',NBTRCF1,' ST',NS1,NS2,NS3
C
C        PASSAGE A L'ARETE SUIVANTE
 240     NA1 = NA2
 250  CONTINUE
      NBTR1I = NBTRCF1-NBTR3I-NBTR2I
C
C     BILAN COHERENT?
CCC      PRINT *,'CHNFNF: NBTRCF=',NBTRCF,' NBTR3I=',NBTR3I,
CCC     %        ' NBTR2I=',NBTR2I,' NBTR1I=',NBTR1I
      IF( NBTRCF .NE. NBTRCF1 ) THEN
C        NON: PAS DE MODIFICATION DE LA TRIANGULATION
         RETURN
      ENDIF
C
C     2-EME PARTIE: GENERATION EFFECTIVE DE LA NOUVELLE TRIANGULATION
C     ===============================================================
      DO 310 N=1,NBTRCF
C        DESTRUCTION DE LA FACE INITIALE PERDUE DE LEFACO
         CALL VDDSFA( NOTRCF0(N), MXFACO, LEFACO, NBFACO, N1FASC )
 310  CONTINUE
C
      DO 320 N=1,NBTRCF
C        CONSTRUCTION DU TRIANGLE NOSTTR(1:3,N) DANS LEFACO
         CALL VDAJFA( NOSTTR(1,N),NOSTTR(2,N),NOSTTR(3,N), NV1,NV2,
     %                NOSTTR(4,N),NOSTTR(5,N),NOSTTR(6,N),
     %                MXFACO, LEFACO, NBFACO, N1FASC, NOTRCF1(N) )
CCC         PRINT *,'CHNFNF: CONSTRUCTION TRIANGLE',N,' ',NOTRCF1(N),
CCC     %           ': SOMMETS ',(NOSTTR(I,N),I=1,3)
 320  CONTINUE
C
C     MISE A JOUR DES TRIANGLES LEFACO OPPOSES MARQUES -1 CAR INCONNUS AVANT
      DO 395 M=1,NBTRCF-1
         NOTR = NOTRCF1(M)
         DO 390 I1=1,3
            IF( LEFACO(5+I1,NOTR) .EQ. -1 ) THEN
C              LE NO DES 2 SOMMETS DE L'ARETE A METTRE A JOUR
               NS1 = LEFACO(I1,NOTR)
               IF( I1 .EQ. 3 ) THEN
                  I2 = 1
               ELSE
                  I2 = I1 + 1
               ENDIF
               NS2 = LEFACO(I2,NOTR)
C
C              RECHERCHE DU TRIANGLE LEFACO OPPOSE A CETTE ARETE
               DO 370 MM=M+1,NBTRCF
                  NOTROP = NOTRCF1(MM)
                  DO 360 J1=1,3
                     NS3 = LEFACO(J1,NOTROP)
                     IF( J1 .EQ. 3 ) THEN
                        J2 = 1
                     ELSE
                        J2 = J1 + 1
                     ENDIF
                     NS4 = LEFACO(J2,NOTROP)
                     IF( ( NS1 .EQ. NS3 .AND. NS2 .EQ. NS4 ) .OR.
     %                   ( NS2 .EQ. NS3 .AND. NS1 .EQ. NS4 ) ) THEN
C                       LES 2 TRIANGLES LEFACO OPPOSES
                        LEFACO( 5+I1, NOTR  ) = NOTROP
                        LEFACO( 5+J1, NOTROP) = NOTR
                     ENDIF
                     GOTO 390
 360              CONTINUE
 370           CONTINUE
               PRINT *,'CHNFNF: ANOMALIE ETIQUETTE 370'
               CALL CLICSO
            ENDIF
 390     CONTINUE
 395  CONTINUE
C
C     CHANGEMENT CORRECT
      NONOUI = 1
      RETURN
      END
