      SUBROUTINE DFTOPA( NUELEM , NLDSEF , NUPLSV , MXAPRO ,
     %                   NB , MX , MN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  EMPILER LE NUMERO DE L'ELEMENT FINI GLOBAL NUELEM
C -----          LE NUMERO LOCAL NLDSEF DE LA FACE OU ARETE OU SOMMET
C                DANS L'ELEMENT FINI GLOBAL NUELEM
C                LE NUMERO DU PLSV SUPPORT DE LA FACE OU ARETE OU SOMMET
C ENTREES :
C ---------
C NUELEM : NUMERO DE L'ELEMENT FINI GLOBAL DANS LE MAILLAGE DES EF DE CE TYPE
C NLDSEF : NUMERO LOCAL DE LA FACE OU ARETE OU SOMMET DANS L'EF NUELEM
C NUPLSV : NUMERO DU PLSV SUPPORT DE LA FACE OU ARETE OU SOMMET
C MXAPRO : APPROXIMATION DU MAXIMUM DE LA LONGUEUR DU TABLEAU
C
C SORTIES :
C ---------
C NB     : NOMBRE DE TRIPLETS EMPILES
C MX     : NOMBRE MAXIMUM DE TRIPLETS DECLARES
C MN     : ADRESSE MCN DU TABLEAU PILE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE UPMC PARIS      AVRIL 1989
C2345X7..............................................................012
      COMMON / UNITES / LECTEU , IMPRIM , INTERA , NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
C
C     SI LE TABLEAU EST TROP COURT IL EST ALLONGE
      IF( NB .GE. MX ) THEN
         IF( MX .EQ. 0 ) THEN
            MXP = MAX( MXAPRO , 256 )
         ELSE
            MXP = MX + MX
         ENDIF
         CALL TNMCAU( 'ENTIER' , MX*3 , MXP*3 , NB*3 , MN )
         MX = MXP
      ENDIF
C
C     AJOUT DES 3 INFORMATIONS
      MNN = MN + 3 * NB
C     NUMERO DU PLSV SUPPORT DE LA FACE OU ARETE OU SOMMET
      MCN( MNN     ) = NUPLSV
C     NUMERO LOCAL DE LA FACE OU ARETE OU SOMMET DANS L'EF NUELEM
      MCN( MNN + 1 ) = NLDSEF
C     NUMERO DE L'ELEMENT FINI GLOBAL DANS LE MAILLAGE DES EF DE CE TYPE
      MCN( MNN + 2 ) = NUELEM
      NB = NB + 1
      END
