      SUBROUTINE CREERPAVAGE( ARETEMAX, HEXAPAVE, NBIPAV, ECHPAV )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CREATION DU PAVAGE DE CHAINAGE DES POINTS
C -----     POUR ACCELERER LA RECHERCHE DU POINT LE PLUS PROCHE

C ENTREE :
C --------
C ARETEMAX: LONGUEUR DE L'ARETE MAXIMALE DU MAILLAGE

C SORTIES :
C ---------
C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I DU PAVAGE
C ECHPAV  : ECHELLE DANS LA DIRECTION I DU PAVAGE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     Octobre 2005
C MODIFS : ALAIN PERRONNET LJLL UPMC & St PIERRE DU PERRAY     Mars 2016
C2345X7..............................................................012
      include"./incl/langue.inc"
      COMMON / EPSSSS / EPZERO, EPSXYZ
C PRECISON ABSOLUE  POUR IDENTIFIER 2 POINTS DE R3 DANS LE VOISINAGE DE ZERO
C PRECISON RELATIVE POUR IDENTIFIER 2 POINTS DE R3 HORS LE VOISINAGE DE ZERO

      DOUBLE PRECISION  HEXAPAVE(3,2), ECHPAV(3)
      INTEGER           NBIPAV(3)
      REAL              ARETEMAX
      DOUBLE PRECISION  EPS, S, D, DMAX

C     LONGUEUR D'UN INTERVALLE DANS UNE DIRECTION DU PAVAGE
ccc      DMAX = 1.3D0 * ARETEMAX   22/3/2016
      DMAX = ARETEMAX

C     MAXIMUM DES EPSILON
      EPS = EPSXYZ
      IF( EPS .LT. EPZERO ) EPS=EPZERO
      EPS = EPS * DMAX

C     NOMBRE D'INTERVALLES DU PAVAGE DANS LES 3 DIRECTIONS
C     CALCUL DES 'ECHELLES' DANS LES 3 DIRECTIONS
      DO I=1,3

C        NOMBRE D'INTERVALLES D'ARETEMAX DANS LA DIRECTION I
C        +3 POUR AVOIR EN PLUS ARETMAX*1.5 AUX 2 EXTREMITES
         NBIPAV(I) = NINT((HEXAPAVE(I,2)-HEXAPAVE(I,1) +2*EPS) / DMAX)+3

C        DEMI DISTANCE MIN-MAX DANS LA DIRECTION I
         D = NBIPAV(I) * DMAX * 0.5D0

C        MILIEU DANS LA DIRECTION I
         S = ( HEXAPAVE(I,1) + HEXAPAVE(I,2) ) / 2D0

C        SOMMETS EXTREMES DE L'HEXAEDRE
         HEXAPAVE(I,1) = S - D
         HEXAPAVE(I,2) = S + D

C        1/DISTANCE ENTRE 2 SOMMETS DANS LA DIRECTION I
C        TRES PROCHE DE 1/ARETEMAX = ECHELLE DANS LA DIRECTION I
         ECHPAV(I) = NBIPAV(I) / (HEXAPAVE(I,2) - HEXAPAVE(I,1))

      ENDDO

C     NOMBRE DE SOMMETS ET CUBES DU MAILLAGE
      NBSOM = ( NBIPAV(1) + 1 ) * ( NBIPAV(2) + 1 ) * ( NBIPAV(3) + 1 )
      NBCUBE = NBIPAV(1) *  NBIPAV(2) * NBIPAV(3)

      IF( LANGAG .EQ. 0 ) THEN
         PRINT 10000, ((HEXAPAVE(I,J),I=1,3),J=1,2),
     %                  NBSOM, NBCUBE, ARETEMAX,
     %         1.D0/ECHPAV(1), 1.D0/ECHPAV(2), 1.D0/ECHPAV(3)
      ELSE
         PRINT 20000, ((HEXAPAVE(I,J),I=1,3),J=1,2),
     %                       NBSOM, NBCUBE, ARETEMAX,
     %         1.D0/ECHPAV(1), 1.D0/ECHPAV(2), 1.D0/ECHPAV(3)
      ENDIF

10000 FORMAT(/' creerpavage:'/
     % ' PAVAGE de l''HEXAEDRE: COORDONNEES MIN-MAX'/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 /
     % I9,' SOMMETS des',I9,' CUBES de LONGUEUR d''ARETE',G15.7/
     % ' LONGUEUR D''ARETE dans les DIRECTIONS XYZ=',3G15.7)

20000 FORMAT(/' creerpavage:'/
     % ' HEXAHEDRON PAVEMENT: MIN-MAX COORDINATES'/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 /
     % I9,' VERTICES of',I9,' CUBES of EDGE LENGTH',G15.7/
     % ' EDGE LENGTH in DIRECTION XYZ=',3G15.7)

      RETURN
      END
