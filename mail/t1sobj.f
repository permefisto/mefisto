      SUBROUTINE T1SOBJ( NUTYOB, NMOBJT, NUOBJT,
     %                   NTNSEF, MNNSEF, NTXYZS, MNXYZS )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LE MAILLAGE D'UN OBJET DEFINI PAR SON TABLEAU MS
C------
C ENTREES:
C --------
C NUTYOB : NUMERO DU TYPE DU MAILLAGE A TRACER (1:SOMMETS 2:ARETES...)
C NMOBJT : NOM DE L'OBJET
C NUOBJT : NUMERO DE L'OBJET DANS SON LEXIQUE
C NTNSEF : NUMERO      DU TABLEAU 'NSEF' DU MAILLAGE
C MNNSEF : ADRESSE MCN DU TABLEAU 'NSEF' DU MAILLAGE
C NTXYZS : NUMERO      DU TABLEAU 'XYZSOMMET' DES XYZ DES SOMMETS
C MNXYZS : ADRESSE MCN DU TABLEAU 'XYZSOMMET' DES XYZ DES SOMMETS
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS   SEPTEMBRE 1994
C....................................................................012
      include"./incl/gsmenu.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/xyzext.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/pp.inc"
      COMMON    MCN(MOTMCN)
C
C     LE NUMERO D'UNITE DU CLAVIER, FENETRE DES AFFICHAGES ET
C     LE PARAMETRE DE NIVEAU D'INTERACTIVITE
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
C
C     LE PARAMETRE D'ENTREE DU SP
      CHARACTER*(*)     NMOBJT
C
      IF( NTXYZS .LE. 0 .OR. MNXYZS .LE. 0 ) RETURN
C
      IF( NUOBJT .EQ. 1 .AND. INIEXT .EQ. 0 ) THEN
C        MISE A JOUR DES COORDONNEES EXTREMES 9/9/2010
         NOTYVI = 0
         INIEXT = 0
         CALL MAJEXT( MNXYZS )
      ENDIF
C
C     SI LE TABLEAU TRACE DE CET OBJET EXISTE ALORS LES OPTIONS DE TRACE
C     SONT INITIALISEES AVEC SES VALEURS
      CALL COUOBJ( NUTYOB, NMOBJT, MNTRAC )

cccC     SI LA SOURIS EST DISPONIBLE EN INTERACTIF ELLE EST ACTIVEE POUR MODIFIER
cccC     LE CADRE DES TRACES
ccc      IF( INTERA .GE. 2 )THEN
ccc         LORBITE=1
ccc      ELSE
ccc         LORBITE = 0
ccc      ENDIF
C     LORBITE EST PRISE TELLE QUE SELON L'INITIALISATION PAR MENU

C     LA VISEE DE L'OBJET SELON LES PARAMETRES DU COMMON / TRVARI /
      CALL VISEE1
C
C     ***************************************
C     TRACE SELON LE NUMERO DE TYPE DES PLSVO
C     ***************************************
      GOTO( 100, 200, 300, 400, 500 ), NUTYOB
C
C     SOMMETS TRACE DE POINTS
C     =======
 100  IF( NDIMLI .LE. 2 ) THEN
         CALL T21SOM( NMOBJT, NUOBJT, MNXYZS )
      ELSE
         CALL T31SOM( NMOBJT, NUOBJT, MNXYZS )
      ENDIF
      GOTO 800
C
C     ARETES TRACE DE LIGNES
C     ======
 200  IF( MNNSEF .LE. 0 ) RETURN
      IF( NDIMLI .LE. 2 ) THEN
         CALL T21ARE( NMOBJT, NUOBJT, MNNSEF, MNXYZS )
      ELSE
         CALL T31ARE( NMOBJT, NUOBJT, MNNSEF, MNXYZS )
      ENDIF
      GOTO 800
C
C     FACES TRACE DE SURFACES
C     =====
 300  IF( MNNSEF .LE. 0 ) RETURN
      CALL TRAFAC( NMOBJT, NUOBJT, MNNSEF, MNXYZS )
      GOTO 800
C
C     CUBES TRACE DE VOLUMES
C     =====
 400  IF( NTNSEF .LE. 0 .OR. MNNSEF .LE. 0 ) RETURN
      CALL TRACUB( NMOBJT, NUOBJT, MNNSEF, MNXYZS )
      GOTO 800
C
C     OBJETS TRACE D'OBJETS
C     ======
 500  NBLGRC(NRERR) = 1
      KERR(1) = 'OBJET NON TRACE: '//NMOBJT
      CALL LEREUR
C
 800  RETURN
      END
