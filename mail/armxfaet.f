      SUBROUTINE ARMXFAET( PTXYZD, N1FEOC, NFETOI,
     %                     NBFETO, AREMAX, NS1MAX, NS2MAX, NF1MAX )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   CALCUL DE LA LONGUEUR DE L'ARETE MAXIMALE DES FACES DE
C -----   L'ETOILE DEFINIE PAR LE TABLEAU NFETOI

C ENTREES:
C --------
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C XYZ    : POINT 4-EME SOMMET POUR LES TETRAEDRES FACE+XYZ
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : LES FACES TRIANGULAIRES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST DIRIGE VERS L'INTERIEUR DE L'ETOILE
C          5: CHAINAGE SUR LA FACE SUIVANTE OCCUPEE OU VIDE

C SORTIES:
C --------
C NBFETO : NOMBRE DE FACES NFETOI CHAINEES A PARTIR DE NFETOI
C AREMAX : LONGUEUR MOYENNE DES ARETES DES FACES DE L'ETOILE
C NS1MAX : NUMERO PTXYZD DU SOMMET 1 DE L'ARETE DE LONGUEUR MAXIMALE
C NS2MAX : NUMERO PTXYZD DU SOMMET 2 DE L'ARETE DE LONGUEUR MAXIMALE
C NF1MAX : NUMERO NFETOI DE LA FACE D'ARETE DE LONGUEUR MAXIMALE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint PIERRE du PERRAY           Janvier 2020
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,*), AREMAX, ARETE
      INTEGER           NFETOI(5,*)

      AREMAX = -1D0
      NBFETO = 0

      NF1 = N1FEOC

 10   IF( NF1 .GT. 0 ) THEN

C        UNE FACE DE PLUS
         NBFETO = NBFETO + 1

C        PARCOURS DES ARETES DE LA FACE NFETOI
         NS1 = NFETOI(4,NF1)
         DO K = 1, 3

            NS2 = ABS( NFETOI(1+K,NF1) )
            ARETE = SQRT( ( PTXYZD(1,NS2) - PTXYZD(1,NS1) ) **2
     %                  + ( PTXYZD(2,NS2) - PTXYZD(2,NS1) ) **2
     %                  + ( PTXYZD(3,NS2) - PTXYZD(3,NS1) ) **2 )

            IF( ARETE .GT. AREMAX ) THEN
               AREMAX = ARETE
               NS1MAX = NS1
               NS2MAX = NS2
               NF1MAX = NF1
            ENDIF

            NS1 = NS2

         ENDDO

C        LA FACE SUIVANTE
         NF1 = NFETOI(5,NF1)
         GOTO 10

      ENDIF

      RETURN
      END
