      SUBROUTINE PTNORTR( PTXYZD, NOSOTR, COTMOY, XYZBAR, VN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES COORDONNEES VN DE LA NORMALE AU TRIANGLE NOSOTR
C -----    ET LES COORDONNEES XYZBAR DE SON BARYCENTRE
 
C ENTREES:
C --------
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NOSOTR : NUMERO PTXYZD DES 3 SOMMETS S1 S2 S3 DU TRIANGLE

C SORTIES:
C --------
C COTMOY : LONGUEUR DU COTE MOYEN DU TRIANGLE
C XYZBAR : 3 COORDONNEES DU BARYCENTRE DU TRIANGLE DE SOMMETS NOSOTR(1:3)
C VN     : 3 COORDONNEES DE LA NORMALE AU TRIANGLE DE SOMMETS NOSOTR(1:3)
C          S1S2 x S1S3 NORMALISEE A 1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & VEULETTES SUR MER   NOVEMBRE 2014
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,*), PROSCD, VN(3), D, XYZBAR(3), COTMOY
      INTEGER           NOSOTR(3)
      INTRINSIC         SQRT

C     VECTEUR NORMAL AU TRIANGLE
      CALL VECNOR3D( PTXYZD(1,NOSOTR(1)),
     %               PTXYZD(1,NOSOTR(2)),
     %               PTXYZD(1,NOSOTR(3)), VN )

C     LONGUEUR DE LA NORMALE
      D = SQRT( PROSCD( VN, VN, 3 ) )

C     LE VECTEUR NORMAL DE NORME 1
      DO I=1,3
         VN(I) = VN(I)  / D
         XYZBAR(I) = 0D0
      ENDDO

C     BARYCENTRE ET PERIMETRE DU TRIANGLE
      COTMOY = 0D0
      NS0    = NOSOTR(3)
      DO K=1,3
C        SOMMET ET ARETE K DU TRIANGLE
         D  = 0D0
         NS = NOSOTR(K)
         DO I=1,3
            XYZBAR(I) = XYZBAR(I) + PTXYZD(I,NS)
            D = D + ( PTXYZD(I,NS) - PTXYZD(I,NS0) ) ** 2
         ENDDO
         COTMOY = COTMOY + SQRT(D)
         NS0 = NS
      ENDDO

C     LES 3 COORDONNEES DU BARYCENTRE DU TRIANGLE
      DO I=1,3
         XYZBAR(I) = XYZBAR(I) / 3D0
      ENDDO

C     LA LONGUEUR MOYENNE DES COTES DU TRIANGLE
      COTMOY = COTMOY / 3D0

      RETURN
      END

