      SUBROUTINE VDDSFA( NF, MXFACO, LEFACO, NBFACO, N1FASC )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    DETRUIRE LA FACE NF DU TABLEAU LEFACO des FACES
C -----    de la FRONTIERE du VOLUME

C ENTREES:
C --------
C NF     : NUMERO DANS LEFACO DE LA FACE A DETRUIRE
C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO

C MODIFIES :
C ----------
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...

C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
CCCC       LEFACO(12,*) NO DE FACE OC

C NBFACO : NOMBRE DE FACES ACTIVES DU TABLEAU LEFACO
C N1FASC : N1FASC(I) NUMERO D'UN TRIANGLE DE LEFACO DE SOMMET I
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC  SEPTEMBRE 1991
C....................................................................012
      INTEGER  LEFACO(11,0:*), N1FASC(*)

C     MISE A JOUR DU CHAINAGE POUR LE HACHAGE
C     HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
      NF0 = 0
      LF  = MOD( LEFACO(1,NF)+LEFACO(2,NF)+LEFACO(3,NF) , MXFACO ) + 1
      NF1 = LEFACO(10,LF)

C     CETTE FACE EST ELLE NF?
 10   IF( NF1 .GT. 0 ) THEN

          IF( NF1 .NE. NF ) THEN
C            LA FACE N'EST PAS RETROUVEE.PASSAGE A LA SUIVANTE
             NF0 = NF1
             NF1 = LEFACO(9,NF1)
             GOTO 10
          ENDIF

C         LA FACE NF=NF1 EST RETROUVEE . CHAINAGE
ccc      print*,'vddsfa: Suppression LEFACO(',NF,'):',(LEFACO(I,NF),I=1,11)
          IF( NF0 .NE. 0 ) THEN
C            NF N'EST PAS LA PREMIERE FACE DU CHAINAGE
             LEFACO(9,NF0) = LEFACO(9,NF)
          ELSE
C            NF EST LA PREMIERE FACE DU HACHAGE
             LEFACO(10,LF) = LEFACO(9,NF)
          ENDIF

C         LA FACE NF EST VIDE ET MISE EN DEBUT DE CHAINAGE DES VIDES
          LEFACO(9,NF) = LEFACO(10,0)
          LEFACO(10,0) = NF

C         LES FACES ADJACENTES SONT MISES A JOUR
          DO I=1,3
C            RECHERCHE DE LA FACE NF0 QUI PRECEDE NF DANS LE CHAINAGE
C            CIRCULAIRE DES FACES ADJACENTES PAR L'ARETE I
             CALL NOFQPF( I, NF, LEFACO,  NF0, NA0 )
             IF( NF0 .GT. 0 ) THEN
C               SUPPRESSION DE LA FACE NF DU CHAINAGE
                LEFACO( 5+NA0, NF0 ) = LEFACO( 5+I, NF )
C               REMARQUE: SI CHAINAGE CIRCULAIRE A 2 FACES
C                         APRES SUPPRESSION DE LA FACE
C               NF0 POINTE SUR LUI MEME CE QUI RESTE CIRCULAIRE

C               MISE A JOUR DE N1FASC
                N1FASC( LEFACO(I,NF)          ) = NF0
                N1FASC( LEFACO(MOD(I,3)+1,NF) ) = NF0
             ENDIF
          ENDDO

C         MISE A ZERO DE LEFACO(*,NF)
          CALL AZEROI( 8, LEFACO(1,NF) )
C
C         UNE FACE DE MOINS DANS LEFACO
          NBFACO = NBFACO - 1
          GOTO 9999

      ENDIF

      PRINT*, 'ERREUR vddsfa: FACE LEFACO ',NF,' INEXISTANTE'

 9999 RETURN
      END
