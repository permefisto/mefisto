      SUBROUTINE MINTRI( ARETMX, NBITAQ, MXSOMM, NBLFTR,
     %                   NBARF1, NBARFR,
     %                   MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                   MOARTR, MXARTR, N1ARTR, NOARTR,
     %                   PXYD  , NOARST, NLSOFR,
     %                   NBSOMM, NBSOM1, NBTRIA, NDTRIA,
     %                   ORIGIN, D2D3, XMIN, YMIN, XYMXMI, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRIANGULATION PAR MINIMISATION DES SURFACES DES TRIANGLES
C -----
C ENTREES:
C --------
C ARETMX : LONGUEUR MAXIMALE DES ARETES DES TRIANGLES EQUILATERAUX
C NBITAQ : NOMBRE D'ITERATIONS POUR AMELIORER LA QUALITE DU MAILLAGE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TRIANGULATION
C NBLFTR : NOMBRE DE LIGNES FERMEES LIMITANT LA SURFACE A TRIANGULER
C NDARLF : NUMERO DE LA PREMIERE ARETE DE CHAQUE LIGNE FERMEE DANS
C          LE TABLEAU NOSOAR
C MNSOLF : ADRESSE MCN DU TABLEAU SOMMETS DE CHAQUE LIGNE FERMEE
C NBARF1 : NUMERO DU DERNIER SOMMET DE LA LIGNE ENVELOPPE
C          = NOMBRE D'ARETES DE LA LIGNE-ENVELOPPE
C NBARFR : NOMBRE D'ARETES  DE LA FRONTIERE OU CONTOUR DU DOMAINE =
C          NOMBRE DE POINTS DE LA FRONTIERE OU CONTOUR DU DOMAINE
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE
C MXSOAR : NOMBRE MAXIMAL D'ARETES FRONTALIERES DECLARABLES
C
C ENTREES ET SORTIES :
C --------------------
C NOSOAR : NO SOMMET 1 2 , NO LIGNE DE 1 A NBLFTR, TRIANGLE 1 2, CHAINAGES EN PL
C          POUR CHAQUE ARETE DE LA TRIANGULATION
C SORTIES:
C --------
C N1TRVI : NUMERO DU PREMIER TRIANGLE VIDE DANS LE TABLEAU NOTRIA
C          LE CHAINAGE DES TRIANGLES VIDES SE FAIT SUR NOTRIA(4,.)
C NOTRIA : LISTE DES TRIANGLES
C          PAR TRIANGLE : ARETE1, ARETE2, ARETE3
C
C PXYD   : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C          TABLEAU REEL2(3,MXSOMM)
C
C NOARST : NOARST(I) NUMERO D'UNE ARETE DE SOMMET I
C
C NLSOFR : TABLEAU DU NUMERO DE LIGNE(1 A NBLFTR) DES SOMMETS
C          TABLEAU ENTIER(MXSOMM)
C
C NSLIGN : TABLEAU DU NUMERO DE SOMMET DANS SA LIGNE POUR CHAQUE
C          SOMMET FRONTALIER
C          NUMERO DU POINT DANS LE LEXIQUE POINT SI INTERNE
C          0 SI LE POINT EST INTERNE NON IMPOSE PAR L'UTILISATEUR
C
C NBSOMM : NOMBRE DE SOMMETS INTERNES + EXTERNES APRES TRIANGULATION
C NBSOM1 : NOMBRE DE SOMMETS INTERNES APRES TRIANGULATION
C NBTRIA : NOMBRE DE TRIANGLES DE LA TRIANGULATION
C NDTRIA : NUMERO MAXIMAL DANS NOTRIA D'UN TRIANGLE INTERNE
C ORIGIN : POINT ORIGINE DANS LE PLAN DE MAILLAGE
C D2D3   : MATRICE DE PASSAGE DES COORDONNEES 3D EN COORDONNEES 2D
C XMIN   : ABSCISSE MINIMALE APRES TRANSFORMATION 3D->2D
C YMIN   : ORDONNEE MINIMALE APRES TRANSFORMATION 3D->2D
C XYMXMI : ECART MAXIMAL ENTRE XMAX-XMIN ET YMAX-YMIN
C IERR   : 0 SI PAS D'ERREUR
C          1 SATURATION DES SOMMETS
C          2 SATURATION DES ARETES
C          3 SATURATION DES TRIANGLES
C          4 PILE SATUREE DES ARETES FRONTALIERES NON RETROUVEES
C          5 PILE SATUREE DES ARETES EXTERIEURES AU DOMAINE
C          6 EXISTENCE D'ARETES PERDUES STOCKEES DANS NTETOI (CF LETREE)
C          7 TOUS LES POINTS SONT ALIGNES
C          8 AUCUN CERCLE CIRCONSCRIT AUX TRIANGLES ACTUELS NE CONTIENT
C            LE POINT COURANT
C          9 TABLEAU NOSOAR DE TAILLE INSUFFISANTE CAR TROP D'ARETES
C            PERDUES
C         10 LIGNE ENVELOPPE INCORRECTE
C         11 2 EXTREMITES CONFONDUES POUR UNE ARETE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JUILLET 1995
C....................................................................012
      include"./incl/a___xyzsommet.inc"
      include"./incl/gsmenu.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
C     TRACE OU NON DES TRIANGLES GENERES DANS LA TRIANGULATION
C     LES PARAMETRES D'ENTREE ET SORTIE
      INTEGER           NOSOAR(MOSOAR,MXSOAR),
     %                  NLSOFR(MXSOMM),
     %                  NOARST(MXSOMM)
      DOUBLE PRECISION  DINFO,TCPU,D
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
C
C     LES PARAMETRES RESULTATS
      INTEGER           NOARTR(MOARTR,MXARTR)
      DOUBLE PRECISION  PXYD(3,MXSOMM)
      DOUBLE PRECISION  SURTD2, AIRE
      REAL              COMXMI(3,2),
     %                  ORIGIN(3)
      DOUBLE PRECISION  D2D3(3,3), AIREXA
C
C     ZEMEPS :  -EPSILON POUR LA PRECISION A ZERO
C     UNPEPS : 1+EPSILON POUR LA PRECISION A 1
      DOUBLE PRECISION  ZEMEPS, UNPEPS
C
C     LES SEUILS DE PRECISION POUR SAVOIR SI UN POINT EST DANS
C     UN TRIANGLE OU UN CERCLE
C     ========================================================
      ZEMEPS = -1000.D0 * DINFO( 'PRECISION' )
      UNPEPS = 1.D0 - ZEMEPS
C
C     TRACE OU NON DES TRIANGLES GENERES
CCC      TRATRI = .TRUE.
      TRATRI = .FALSE.
C
C     NOMBRE D'ITERATIONS SUR LA CREATION DES POINTS INTERNES
      NBITPI = 1
C
C     AU DEBUT PAS D'ERREUR
      IERR   = 0
      WRITE(IMPRIM,10000)
10000 FORMAT(' TRIANGULATION MINIMISATION D''AIRES DANS UN PLAN DE R3')
C
C     INITIALISATIONS DES POINTS ET DES ARETES FRONTALIERES
C     RENUMEROTATION GLOBALE POUR LA TRIANGULATION
C     =====================================================
      CALL MTINIT( MXSOMM, MXARTR, NBLFTR,
     %             NBARF1, NBARFR, MOSOAR, MXSOAR, NOSOAR,
     %             PXYD  ,
     %             ORIGIN, D2D3  , COMXMI, AREMIN, AREMAX,
     %             XMIN  , YMIN  , XYMXMI, AIREXA, IERR )
      IF( IERR .NE. 0 ) GOTO 9999
      NBSOMM = NBARFR
C     ICI LE TABLEAU PXYD N'A PAS DE SOMMETS IDENTIQUES
C
      IF( TRATRI ) THEN
C        LES TRACES SONT DEMANDES
         CALL EFFACE
C        LE CADRE OBJET GLOBAL EN UNITES UTILISATEUR
         XE = COMXMI(1,2) - COMXMI(1,1)
         YE = COMXMI(2,2) - COMXMI(2,1)
         CALL ISOFENETRE( COMXMI(1,1)-XE/20, COMXMI(1,2)+XE/20,
     %                    COMXMI(2,1)-YE/20, COMXMI(2,2)+YE/20 )
      ENDIF
      D    = DINFO('DELTA CPU')
      TCPU = D
      WRITE(IMPRIM,19900) TCPU
19900 FORMAT(/' TEMPS COHERENCE DES DONNEES :',T60,F10.2,' SECONDES')
C     PROTECTION CONTRE UNE ARETE MAX DESIREE TROP GRANDE OU TROP PETITE
      ARETMX = MISECH( ARETMX, 0.0, XYMXMI )
      ARETMX = MIN( ARETMX, AREMAX )
      ARETMX = MAX( ARETMX, AREMIN )
C
C     INITIALISATION DE LA PREMIERE TRIANGULATION EN JOIGNANT LE SOMMET 1
C     AUX ARETES DES LIGNES FERMEES
C     ===================================================================
      CALL MT1TRI( MXSOMM, PXYD,   NBARFR,
     %             MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %             MOARTR, MXARTR, N1ARTR, NOARTR,
     %             NOARST, NBARET, NBTRIA, AIRE  )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19905) D
19905 FORMAT(' TEMPS TRIANGULATION INITIALE :',T60,F10.2,' SECONDES' )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     BOUCLE SUR LES ARETES AVEC ECHANGE DES 2 DIAGONALES AFIN DE RENDRE
C     LA TRIANGULATION DELAUNAY
C     ===================================================================
      CALL MTDELA( MXSOMM, PXYD,   NOARST,
     %             NBARFR, NBARET, MOSOAR, MXSOAR, NOSOAR,
     %             MOARTR, MXARTR, NOARTR, AIRE,   MODIFS )
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19907) D
19907 FORMAT(' TEMPS BOUCLE SUR DELAUNAY :',T60,F10.2,' SECONDES' )
      IF( IERR .NE. 0 ) GOTO 9999
      ITER = 0
C
 20      ITER = ITER + 1
C        BOUCLE SUR LES ARETES AVEC ECHANGE DES 2 DIAGONALES AFIN DE MINIMISER
C        LA SOMME DES VALEURS ABSOLUES DES SURFACES DES 2 TRIANGLES AYANT EN
C        COMMUN UNE ARETE
C        ===================================================================
         CALL MT2T2A( MXSOMM, PXYD,   NOARST,
     %                NBARFR, NBARET, MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                MOARTR, MXARTR, NOARTR, AIRE,   MODIFS )
         WRITE(IMPRIM,*) 'ITERATION ',ITER,MODIFS,' MODIFS MIN AIRES'
         D    = DINFO('DELTA CPU')
         TCPU = TCPU + D
         WRITE(IMPRIM,19906) D
19906 FORMAT(' TEMPS BOUCLE SUR LES ARETES :',T60,F10.2,' SECONDES' )
         IF( IERR .NE. 0 ) GOTO 9999
         IF( MODIFS .EQ. 0 ) GOTO 30
C
C        BOUCLE SUR LES ARETES AVEC ECHANGE DES 2 DIAGONALES AFIN DE RENDRE
C        LA TRIANGULATION DELAUNAY
C        ===================================================================
         CALL MTDELA( MXSOMM, PXYD,   NOARST,
     %                NBARFR, NBARET, MOSOAR, MXSOAR, NOSOAR,
     %                MOARTR, MXARTR, NOARTR, AIRE,   MODIFS )
         WRITE(IMPRIM,*) 'ITERATION ',ITER,MODIFS,' MODIFS DELAUNAY'
         D    = DINFO('DELTA CPU')
         TCPU = TCPU + D
         WRITE(IMPRIM,19907) D
         IF( IERR .NE. 0 ) GOTO 9999
         IF( MODIFS .GT. 0 ) GOTO 20
C
C     CALCUL DE LA QUALITE DE LA TRIANGULATION
C     ========================================
 30   CALL TNMCDC( 'ENTIER', MXARTR, MNEGAT )
      WRITE(IMPRIM,*) 'AIRE EXACTE ET CALCULEE :',AIREXA,AIRE
      CALL EFFACE
      QUAMOY = 0
      QUAMIN = 2.0
      NBTRAN = 0
      NBTRIA = 0
      AIRE   = 0D0
      DO 80 NT=1,MXARTR
         IF( NOARTR(1,NT) .NE. 0 ) THEN
            NBTRIA = NBTRIA + 1
C           LE NUMERO DES 3 SOMMETS DU TRIANGLE NT
            CALL MT3STR( NT, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                   NS1, NS2, NS3 )
C           LA QUALITE DU TRIANGLE NS1 NS2 NS3
            CALL QUTR2D( PXYD(1,NS1), PXYD(1,NS2), PXYD(1,NS3), QUALIT )
C           LA QUALITE MOYENNE
            QUAMOY = QUAMOY + QUALIT
C           LA QUALITE MINIMALE
            QUAMIN = MIN( QUAMIN, QUALIT )
C           TRACE DU TRIANGLE NT
            CALL MTTRTR( PXYD, NT, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                   NCBLAN, NCNOIR )
C           AIRE SIGNEE DU TRIANGLE NT
            D = SURTD2( PXYD(1,NS1), PXYD(1,NS2), PXYD(1,NS3) )
            IF( D .LE. 0 ) THEN
C              UN TRIANGLE D'AIRE NEGATIVE DE PLUS
               MCN(MNEGAT+NBTRAN) = NT
               NBTRAN = NBTRAN + 1
               WRITE(IMPRIM,*) 'TRIANGLE ',NT,' ST:',NS1,NS2,NS3,
     %                         ' D AIRE ',D,'<=0'
C              TRACE DU TRIANGLE NT EN ROUGE
               CALL MTTRTR( PXYD, NT, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                      NCROUG, NCMAGE )
            ENDIF
            AIRE = AIRE + ABS( D )
         ENDIF
 80   CONTINUE
      QUAMOY = QUAMOY / NBTRIA
      WRITE(IMPRIM,*) NBTRAN,' TRIANGLES D AIRE <=0'
      WRITE(IMPRIM,*)'AIRE TOTALE EXACTE  =',AIREXA
      WRITE(IMPRIM,*)'AIRE TOTALE CALCULEE=',AIRE
      WRITE(IMPRIM,*)'AIRE PERDUE   EN %  =',ABS(AIREXA-AIRE)/AIREXA*100
      WRITE(IMPRIM,10070) 0,QUAMOY,QUAMIN,NBTRIA
10070 FORMAT(/' AMELIORATION',I3,' QUALITE MOYENNE=',F6.3,
     %       ' MINIMALE=',F6.3,' POUR',I6,' TRIANGLES'/)
C
C     TRACE DES TRIANGLES D'AIRE NEGATIVE
      DO 87 NS = 0,NBTRAN-1
C        TRACE DU TRIANGLE NT EN ROUGE
         CALL MTTRTR( PXYD, MCN(MNEGAT+NS),
     %                MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                NCROUG, NCMAGE )
 87   CONTINUE
      CALL TNMCDS( 'ENTIER', MXARTR, MNEGAT )
C
C     TENTATIVE D'AMELIORATION DE LA QUALITE DE LA TRIANGULATION
C     ==========================================================
      CALL XVPAUSE
      CALL DVAMQU( NBITAQ, MXSOMM, NBSOMM, PXYD,
     %             MXARTR, NDTRIA, N1TRVI, NOTRIA,
     %             NOARST, NLSOFR,
     %             NBTRIA, QUAMOY, QUAMIN )
C
C     RENUMEROTATION DES SOMMETS INTERNES
C     ===================================
      NBSOM1 = 0
      DO 90 NS=1,NBSOMM
         IF( NOARST(NS) .GT. 0 ) THEN
            NBSOM1 = NBSOM1 + 1
            NOARST(NS) = NBSOM1
         ELSE
            NOARST(NS) = 0
         ENDIF
 90   CONTINUE
C
C     IMPRESSION DU NOMBRE DE SOMMETS ET TRIANGLES
C     ============================================
      D    = DINFO('DELTA CPU')
      TCPU = TCPU + D
      WRITE(IMPRIM,19993) D,TCPU
19993 FORMAT(/' TEMPS AMELIORATION QUALITE TRIANGULATION :',T60,F10.2,
     %       ' SECONDES'//
     %       ' TEMPS TOTAL DE LA TRIANGULATION + QUALITE :',T60,F10.2,
     %       ' SECONDES')
 9999 RETURN
      END
