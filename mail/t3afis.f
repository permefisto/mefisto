      SUBROUTINE T3AFIS( NBISO,  NBARFR, NBFISO, NOFAFR,
     %                   MOARFR, MXARFR, LAREFR,
     %                   NBCOOR, XYZPOI,
     %                   TRIANG, XYZISO, NUMISO )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES ARETES FRONTALIERES ET LES FACES ISOVALEURS
C ----     SELON L'ALGORITHME DU PEINTRE
C
C ENTREES:
C --------
C NBISO  : NOMBRE DE SURFACES ISOVALEURS A TRACER  (DOIT ETRE <=10)
C NBARFR : NOMBRE DES ARETES FRONTALIERES
C NBFISO : NOMBRE DE FACES ISOVALEURS
C NOFAFR : NUMERO DES FACES SELON LEUR DISTANCE DECROISSANTE A L'OEIL
C MOARFR : NOMBRE DE MOTS PAR ARETE FRONTALIERE DU TABLEAU LAREFR
C MXARFR : NOMBRE DE FACES DU TABLEAU LAREFR
C LAREFR : TABLEAU NUMERO DES 2 SOMMETS ET LIEN
C          LAREFR(1,I)= NO DU 1-ER  SOMMET DE L'ARETE FRONTALIERE
C          LAREFR(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LAREFR(3,I)= 0 OU NUMERO DE L'ARETE FRONTALIERE SUIVANTE
C                            DANS LE HACHAGE
C NBCOOR : NOMBRE DE COORDONNES PAR POINT
C XYZPOI : COORDONNEES DES SOMMETS DES FACES FRONTALIERES (ET INTERNES)
C TRIANG : REEL VALEUR TEMOIN DE TRIANGLE DANS XYZISO(3,4,.)
C XYZISO : XYZ DES 4 SOMMETS DES FACES DES ISOVALEURS
C NUMISO : NUMERO DE L'ISOVALEUR DE CHAQUE FACE ISOVALEUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS     OCTOBRE 1994
C2345X7..............................................................012
      PARAMETER   ( LIGCON=0, LIGTIR=1 )
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/gsmenu.inc"
      INTEGER        NUMISO(NBFISO)
      INTEGER        NOFAFR(1:NBFISO+NBARFR)
      INTEGER        LAREFR(1:MOARFR,1:MXARFR)
      REAL           XYZPOI(1:NBCOOR,1:*)
      REAL           XYZISO(1:3,1:4,1:NBFISO)
      REAL           XYZI(1:3,1:4), XYZB(3)
      REAL           CNORFA(1:3)
C
C     LA DIRECTION DE VISEE PTV-OEIL
      DIREVI(1) = AXOEIL(1) - AXOPTV(1)
      DIREVI(2) = AXOEIL(2) - AXOPTV(2)
      DIREVI(3) = AXOEIL(3) - AXOPTV(3)
C
C     LA DIRECTION DE VISEE NORMALISEE A 1.
      CALL NORMER( 3, DIREVI, IERR )
      IF( IERR .NE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'OEIL ET POINT VU IDENTIQUES'
         CALL LEREUR
         RETURN
      ENDIF
C
C     LE NOMBRE DE COULEURS DISPONIBLES
      NBCOUL = NDCOUL - N1COUL + 1
C
C     NOMBRE-1 DE COULEURS DISPONIBLES POUR UNE ISOVALEUR (NBISO<=10)
      NBCPIS = NBCOUL / 10 - 1
      IF( NBCPIS .LT. 0 ) NBCPIS = 0
C
C     LA PALETTE ARC EN CIEL DU TYPE QUALITES
      CALL PALCDE(13)
C
C     LES ARETES SONT TRACEES AVEC UNE EPAISSEUR MINIMALE
      CALL XVEPAISSEUR( 0 )
C
C     LE TRACE DES FACES EN COMMENCANT PAR LES PLUS ELOIGNEES
C     =======================================================
      DO 200 NF = 1, NBARFR+NBFISO
C
C        LE NUMERO DE LA FACE LA PLUS ELOIGNEE NON TRACEE
         NAOF = NOFAFR( NF )
C
         IF( NAOF .GT. 0 ) THEN
C
C           TRACE DE LA FACE ISOVALEUR
C           --------------------------
C           LE NOMBRE DE SOMMETS DE LA FACE
            IF( XYZISO(3,4,NAOF) .EQ. TRIANG ) THEN
               NAF = 3
            ELSE
               NAF = 4
            ENDIF
C
C           CNORFA LES COORDONNEES DE LA NORMALE A LA FACE (NORME=1)
            CALL NORMTQ( NAF, XYZISO(1,1,NAOF),  CNORFA, NC )
CCC            IF( NC .NE. 0 ) GOTO 200 SUPPRIMER CAR QUADRANGLES NON TRACES
C
C           LE PRODUIT SCALAIRE DIRECTION VISEE ET NORMALE
            R = PROSCR( DIREVI , CNORFA , 3 )
C
C           LE NUMERO DE L'ISOVALEUR DE LA FACE
            NUISO = NUMISO(NAOF)
C
C           LA COULEUR PRIMAIRE VISUALISE L'ISOVALEUR  (NBISO<=10)
            NC = NBISO + 1 - NUISO
            NC = NC - 1 + N1COUL
C
C           LA COULEUR PONDEREE PAR LA DIRECTION DANS LA COULEUR DE L'ISO
            NUDC = NINT( ABS(R) * NBCPIS )
C
C           LA COULEUR DANS LA PALETTE
            NC = NC + 10 * NUDC
C
C           LE TRACE DE LA FACE
            CALL XVTYPETRAIT( NTLAPL )
C
            IF( PREDUF .NE. 0 ) THEN
C
C               REDUCTION DE LA FACE
                REDUCF = PREDUF * 0.01
                REDUC1 = 1.0 - REDUCF
C               CALCUL DES COORDONNEES XYZP DU BARYCENTRE DE LA FACE
                CALL COBAPO( NAF, XYZISO(1,1,NAOF), XYZB )
C               L'HOMOTHETIE DE CENTRE LE BARYCENTRE DE LA FACE
                DO 100 J=1,NAF
                   XYZI(1,J) = XYZISO(1,J,NAOF)*REDUC1 + XYZB(1)*REDUCF
                   XYZI(2,J) = XYZISO(2,J,NAOF)*REDUC1 + XYZB(2)*REDUCF
                   XYZI(3,J) = XYZISO(3,J,NAOF)*REDUC1 + XYZB(3)*REDUCF
 100            CONTINUE
C
               IF( NAF .EQ. 4 ) THEN
C                 TRACE DU QUADRANGLE
                  CALL QUAD3D( NC, NCOAPL, XYZI )
               ELSE
C                 TRACE DU TRIANGLE
                  CALL FACE3D( NC, NCOAPL, NAF, XYZI )
               ENDIF
C
            ELSE
C
C              PAS DE REDUCTION DE LA FACE
               IF( NAF .EQ. 4 ) THEN
C                 TRACE DU QUADRANGLE
                  CALL QUAD3D( NC, NCOAPL, XYZISO(1,1,NAOF) )
               ELSE
C                 TRACE DU TRIANGLE
                  CALL FACE3D( NC, NCOAPL, NAF, XYZISO(1,1,NAOF) )
               ENDIF
            ENDIF
C
         ELSE
C
C           TRACE DE L' ARETE FRONTALIERE
C           -----------------------------
            CALL XVTYPETRAIT( NTLAFR )
C           LE NUMERO DE L'ARETE DANS LE TABLEAU LAREFR
            NAOF = -NAOF
            CALL TRAIT3D( NCOUAF, XYZPOI(1,LAREFR(1,NAOF)),
     %                            XYZPOI(1,LAREFR(2,NAOF)) )
C
         ENDIF
 200  CONTINUE
C
C     RETOUR AU TRACE CONTINU DES LIGNES
      CALL XVTYPETRAIT( LIGCON )
      RETURN
      END
