      SUBROUTINE TR2ACF( NBET,   NA1,    NF3,
     %                   N1TRVI, NOTRIA, NOTRSO,
     %                   N1ARCF, NOARCF, NF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    MODIFICATION DE LA TRIANGULATION DU CONTOUR FERME NBET
C -----    PAR AJOUT D'UN TRIANGLE AYANT 2 ARETES SUR LE CONTOUR
C          ET MODIFICATION DU CONTOUR PAR AJOUT DE LA 3-EME ARETE
C
C ENTREES:
C --------
C NBET    : NUMERO DANS N1ARCF DU CF TRAITE ICI
C NA1     : NUMERO NOARCF DE LA PREMIERE ARETE SUR LE CONTOUR
C           IMPLICITEMENT SA SUIVANTE EST SUR LE CONTOUR
C           LA SUIVANTE DE LA SUIVANTE N'EST PAS SUR LE CONTOUR
C NF3     : NUMERO DU TRIANGLE NOTRIA EXTERIEUR A LA 3-EME ARETE
C N1TRVI  : NOMBRE MAXIMAL DE TRIANGLES PERMIS POUR LA TRIANGULATION
C
C ENTREES ET SORTIES :
C --------------------
C NOTRIA : LES 3 SOMMETS, 2 MATERIAUX, 3 FACES VOISINES ET CHAINAGE
C          DES FACES TRIANGULAIRES DU CONTOUR ET INTERFACES
C NOTRSO : NOTRSO(I) NUMERO D'UN TRIANGLE DE NOTRIA DE SOMMET I
C N1ARCF : NUMERO D'UNE ARETE DE CHAQUE CONTOUR
C NOARCF : NUMERO DES ARETES DE LA LIGNE DU CONTOUR FERME
C          ATTENTION : CHAINAGE CIRCULAIRE DES ARETES
C
C SORTIE :
C --------
C NF     : NUMERO DU TRIANGLE AJOUTE DANS NOTRIA
C          0 SI SATURATION DU TABLEAU NOTRIA
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1992
C2345X7..............................................................012
      include"./incl/gsmenu.inc"
      INTEGER           NOTRSO(*),
     %                  NOTRIA(6,*),
     %                  N1ARCF(0:*),
     %                  NOARCF(3,*)
C
C     LES ARETES SUIVANTES
      NA2 = NOARCF( 2, NA1 )
C
C     LE TRIANGLE A SES ARETES NA1 NA2 SUR LE CONTOUR FERME
C     AJOUT DANS NOTRIA DE CE TRIANGLE NF
      CALL TRAJFA( NOARCF(1,NA1), NOARCF(1,NA2),NOARCF(1,NOARCF(2,NA2)),
     %             NOARCF(3,NA1), NOARCF(3,NA2), NF3,
     %             N1TRVI, NOTRIA, NOTRSO, NF )
      IF( NF .LE. 0 ) RETURN
C
C     SUPPRESSION DES 2 ARETES (NA1 NA2) DU CF
C     CES 2 ARETES SE SUIVENT DANS LE CHAINAGE DU CF
C     AJOUT DE LA 3-EME ARETE  (NAF) DANS LE CF
C     L'ARETE SUIVANTE DE NA1 DEVIENT LA SUIVANTE DE NA2
      NOARCF(2,NA1) = NOARCF(2,NA2)
      NOARCF(3,NA1) = NF
C
C     L'ARETE NA2 DEVIENT VIDE DANS NOARCF
      NOARCF(2,NA2) = N1ARCF( 0 )
      N1ARCF( 0 )   = NA2
C
C     SI L'ARETE SUPPRIMEE EST CELLE POINTEE ALORS MISE A JOUR
      IF( N1ARCF(NBET) .EQ. NA2 )  N1ARCF(NBET) = NA1
      END
