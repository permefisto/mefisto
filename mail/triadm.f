      SUBROUTINE TRIADM( NS1, NS2, NS3, NBSTIS, NOSTIS, PTXYZD,
     %                   NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    SAVOIR SI LE TRIANGLE NS1 NS2 NS3 NE CONTIENT PAS DE
C -----    POINTS ISOLES
C
C ENTREES:
C --------
C NS1    : LE NUMERO PTXYZD DU PREMIER   SOMMET DU TRIANGLE
C NS2    : LE NUMERO PTXYZD DU SECOND    SOMMET DU TRIANGLE
C NS3    : LE NUMERO PTXYZD DU TROISIEME SOMMET DU TRIANGLE
C NBSTIS : NOMBRE DE POINTS ISOLES
C NOSTIS : NUMERO PTXYZD DES POINTS ISOLES
C PTXYZD : TABLEAU DES COORDONNEES DES SOMMETS
C
C SORTIE :
C --------
C NONOUI : 1 SI LE TRIANGLE NE CONTIENT PAS DE POINTS ISOLES
C         -1 SI LE TRIANGLE EST DEGENERE
C          0 SI LE TRIANGLE CONTIENT AU MOINS UN POINT ISOLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1993
C2345X7..............................................................012
      INTEGER           NOSTIS(1:NBSTIS)
      DOUBLE PRECISION  PTXYZD(4,*)
      DOUBLE PRECISION  D2D3(3,3),PT(2),SOMMET(1:2,2:3)
      DOUBLE PRECISION  X2,X3,Y2,Y3,D,CB1,CB2,CB3
      EQUIVALENCE      (SOMMET(1,2),X2),(SOMMET(2,2),Y2),
     %                 (SOMMET(1,3),X3),(SOMMET(2,3),Y3)
C
C     PASSAGE DANS LE PLAN P1DR P2DR PT AVEC P1DR COMME ORIGINE
C     LA MATRICE DE PASSAGE D2D3
      CALL DI3D2D( PTXYZD(1,NS1), PTXYZD(1,NS2), PTXYZD(1,NS3),
     %             D2D3, IERR )
      IF( IERR .GT. 0 ) GOTO 9999
C
C     LES COORDONNEES DANS LE PLAN
C   ( LE POINT NS1 EST L'ORIGINE SOMMET(1)=(0,0) CE QUI SIMPLIFIE LES FORMULES )
      CALL CI3D2D( PTXYZD(1,NS1), D2D3 , PTXYZD(1,NS2) , SOMMET(1,2) )
      CALL CI3D2D( PTXYZD(1,NS1), D2D3 , PTXYZD(1,NS3) , SOMMET(1,3) )
C
      DO 20 I=1,NBSTIS
         NS = NOSTIS(I)
         IF( NS .EQ. NS1 .OR. NS .EQ. NS2 .OR. NS .EQ. NS3 ) GOTO 20
C
C        LES COORDONNEES 2D DU POINT
         CALL CI3D2D( PTXYZD(1,NS1), D2D3 , PTXYZD(1,NS) , PT )
C
C        LE POINT PT EST IL DANS LE TRIANGLE ?
C        LES 3 COORDONNEES BARYCENTRIQUES DU POINT PT(1) PT(2) DANS LE TRIANGLE
         D   =  X2 * Y3 - X3 * Y2
C
         IF( ABS(D) .LE. 0D0 ) GOTO 9999
C
C        TRIANGLE NON DEGENERE
C        =====================
         CB1 = (( X3    - X2    ) * PT(2) +
     %          ( PT(1) - X3    ) * Y2 +
     %          ( X2    - PT(1) ) * Y3 ) / D
         CB2 = (  PT(1) * Y3 - X3 * PT(2) ) / D
         CB3 = 1D0 - CB1 - CB2
C
         IF( CB1 .GE. -0.00005D0 .AND. CB1 .LE. 1.00005D0 .AND.
     %       CB2 .GE. -0.00005D0 .AND. CB2 .LE. 1.00005D0 .AND.
     %       CB3 .GE. -0.00005D0 .AND. CB3 .LE. 1.00005D0 ) THEN
C            LE TRIANGLE CONTIENT LE POINT
             NONOUI = 0
             RETURN
         ENDIF
 20   CONTINUE
C
C     PAS DE POINTS ISOLES DANS LE TRIANGLE
      NONOUI = 1
      RETURN
C
C     TRIANGLE DEGENERE
 9999 NONOUI = -1
      RETURN
      END
