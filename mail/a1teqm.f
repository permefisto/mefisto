      SUBROUTINE A1TEQM( QTEAME, NTEQM0, MXSOMM, PTXYZD, NPSOFR,
     %                   INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %                   MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                   MODIFT, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    AMELIORER LA QUALITE DES TETRAEDRES AUTOUR DU TETRAEDRE NTEQM0
C -----    DE MEDIOCRE QUALITE SELON SON TYPE DE DEGENERESCENCE
C          QUADRANGLE ou TRIANGLE ou LA CONFIGURATION DES TETRAEDRES
C          OPPOSES AUX FACES

C ENTREES:
C --------
C QTEAME : QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C          AMELIORATION DES TETRAEDRES EST DEMANDEE
C NTEQM0 : NUMERO NOTETR DU TETRAEDRE DE QUALITE MEDIOCRE A TRAITER
C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C MXSOMM : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : NUMERO DE POSITION PAR RAPPORT A LA FRONTIERE DE CHAQUE POINT
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI LE POINT EST AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
C INFACO : = 0 PAS DE TABLEAU LEFACO NI CONTROLE DE FACE FRONTIERE
C          = 1 EXISTENCE DU TABLEAU LEFACO ET CONTROLE DE FACE FRONTIERE
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C IVOLTE : =0 TABLEAU NVOLTE NON PRESENT (No VOLUME DE CHAQUE TETRAEDRE)
C          =1 TABLEAU NVOLTE     PRESENT

C MODIFIES:
C ---------
C NVOLTE : NUMERO DE VOLUME (1 a NBVOPA) DE CHAQUE TETRAEDRE
C          -1 SI VOLUME INCONNU (Exemple: les TETRAEDRES VIDES DE NOTETR)
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: NUMERO NOTETR DU DERNIER TETRAEDRE ACTIF DANS NOTETR

C SORTIES:
C --------
C MODIFT : = 0 AUCUNE MODIFICATION DE TETRAEDRES
C          = 1 DES TETRAEDRES ONT ETE MODIFIES
C IERR   : = 0 PAS D'ERREUR
C          = 1 SATURATION DES SOMMETS    du TABLEAU PTXYZD
C          = 3 SATURATION DES TETRAEDRES du TABLEAU NOTETR
C          =11 RENCONTRE D'UN TETRAEDRE NTEQM AVEC 0 POUR PREMIER SOMMET
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint PIERRE du PERRAY           Fevrier 2017
C MODIFS : ALAIN PERRONNET Saint PIERRE du PERRAY           Janvier 2019
C2345X7..............................................................012
      PARAMETER        (MXFETO=1024, MXTEAU=3072 )
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE
      LOGICAL                          TRACTE0
      CHARACTER*120     KTITRE
      DOUBLE PRECISION  PTXYZD(4,MXSOMM)
      DOUBLE PRECISION  ARMIN, ARMAX, SURFTR(4), S, ST, SFMIN, SFMAX
      DOUBLE PRECISION  VTEQM, TID, VOLTID
      INTEGER           NOTETR(8,MXTETR), N1TETS(MXSOMM),NPSOFR(MXSOMM),
     %                  LEFACO(1:11,0:MXFACO), NVOLTE(*),
     %                  NFETOI(5,MXFETO), NOTEAU(MXTEAU)

ccc      PARAMETER    ( MXT2M4=1024 )
ccc      INTEGER        NOT2M4(MXT2M4)

      INTEGER           NOSOFATE(3,4)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

      INTEGER           NOSOARTE(2,6)
      DATA              NOSOARTE / 1,2, 2,3, 3,1, 4,1, 4,2, 4,3 /

      INTEGER           NOSOARTEOP(2,6)
      DATA              NOSOARTEOP/ 4,3, 4,1, 4,2, 2,3, 3,1, 1,2 /

      INTEGER           NOAROP(6)
      DATA              NOAROP/ 6, 4, 5, 2, 3, 1 /

      TRACTE0 = TRACTE
      IERR    = 0
      MODIFT  = 0
      NBTEAU  = 0
      NTEQM   = NTEQM0

C     CARACTERISTIQUES DU TETRAEDRE NTEQM DE QUALITE MEDIOCRE
C     -------------------------------------------------------
      IF( NTEQM .LE. 0 ) GOTO 9900
      IF( NOTETR(1,NTEQM) .LE. 0 ) GOTO 9900

C     VOLUME et QUALITE DU TETRAEDRE NTEQM
      CALL QUATETD( PTXYZD( 1, NOTETR(1,NTEQM) ),
     %              PTXYZD( 1, NOTETR(2,NTEQM) ),
     %              PTXYZD( 1, NOTETR(3,NTEQM) ),
     %              PTXYZD( 1, NOTETR(4,NTEQM) ),
     %              ARMIN, ARMAX, SURFTR, VTEQM, QTEQM )

ccc      PRINT *,'a1teqm: Debut QTEAME=',QTEAME,
ccc     %' Tetraedre',NTEQM,' de St:',(NOTETR(I,NTEQM),I=1,8),
ccc     %' Q=',QTEQM,' V=',VTEQM

      IF( QTEQM .GE. QTEAME ) THEN
ccc         PRINT*,'a1teqm: TETRAEDRE',NTEQM,' DE QUALITE',
ccc     %          QTEQM,' de VOLUME',VTEQM,' RETOUR SANS TRAITEMENT'
         GOTO 9900
      ENDIF

C     NUMERO DE VOLUME DU TETRAEDRE NTEQM
      IF( IVOLTE .NE. 0 ) THEN
C        PRESENCE DU TABLEAU NVOLTE C-A-D DE TETRAEDRES FRONTIERE
         NOVOLU = NVOLTE( NTEQM )
      ELSE
C        NUMERO PAR DEFAUT DE VOLUME DU TETRAEDRE NTEQM
         NOVOLU = 0
      ENDIF

C     TAILLE SOUHAITEE MOYENNE AUTOUR DES 4 SOMMETS DU TETRAEDRE NTEQM
      TID = ( PTXYZD(4,NOTETR(1,NTEQM)) + PTXYZD(4,NOTETR(2,NTEQM))
     %      + PTXYZD(4,NOTETR(3,NTEQM)) + PTXYZD(4,NOTETR(4,NTEQM)) )/ 4

C     VOLUME IDEAL du TETRAEDRE PAR RAPPORT A LA TAILLE SOUHAITEE DES ARETES
      VOLTID = ( TID * TID * TID ) / 6D0


C     TRAITEMENT DU TETRAEDRE NTEQM DE QUALITE A AMELIORER
C     ====================================================
      KTITRE='a1teqm: TETRAEDRE          de QUALITE=          VOLUME=    
     %                debut du traitement'
      WRITE( KTITRE(19:26),'(I8)'   ) NTEQM
      WRITE( KTITRE(39:47),'(F9.6)' ) QTEQM
      WRITE( KTITRE(56:70),'(G15.6)') VTEQM
      CALL SANSDBL( KTITRE, L )
      CALL TRFETO9( KTITRE(1:L), PTXYZD, NTEQM, NOTETR )


C     SOMME DES 4 SURFACES TRIANGULAIRES DU TETRAEDRE NTEQM
      ST = SURFTR(1) + SURFTR(2) + SURFTR(3) + SURFTR(4)

C     RECHERCHE DE LA FACE DE SURFACE MINIMALE ET MAXIMALE
      NFMAX = 0
      SFMIN = 1D100
      SFMAX =-1D100
      DO NF=1,4
         S = SURFTR(NF)
         IF( S .LT. SFMIN ) THEN
            SFMIN = S
            NFMIN = NF
         ENDIF
         IF( S .GT. SFMAX ) THEN
            SFMAX = S
            NFMAX = NF
         ENDIF
      ENDDO

C     LE TETRAEDRE NTEQM S'ECRASE T IL EN UN TRIANGLE?
      IF( ST-SFMAX .GT. SFMAX*1.05D0 ) THEN
C        NON: PEUT ETRE ECRASEMENT EN UN QUADRANGLE
         GOTO 50
      ENDIF

C     OUI: LE TETRAEDRE NTEQM S'ECRASE EN UN TRIANGLE FRONTALIER
C     ===  =====================================================
      IF( IVOLTE .NE. 0 ) THEN

C        SI LE TETRAEDRE NTEQM A SA FACE MAXIMALE FRONTIERE et
C        SI LA SOMME DE SES 3 FACES NON MAXIMALES EST PROCHE DE SFMAX
C        SI SON VOLUME est FAIBLE PAR RAPPORT A SON VOLUME IDEAL
C        ALORS IL EST SUPPRIME
C        ------------------------------------------------------------
         NTOP = NOTETR( 4+NFMAX, NTEQM )
         IF( NTOP  .EQ. 0   .AND.
     %       VTEQM .LT. VOLTID*1D-4 ) THEN

C           ATTENTION: ICI RISQUE DE FAIRE UN TROU SUR LA FRONTIERE DE
C                      VOLUME FAIBLE VTEQM!... PERTE DE VOLUME

C           LES FACES OPPOSEES A LA FACE FRONTIERE DEVIENNENT FRONTIERE
            DO NF = 1, 4
               NTOP = NOTETR( 4+NF, NTEQM )
               IF( NTOP .GT. 0 ) THEN
                  CALL NOFAOP( NF, NTEQM, NOTETR,  NFOP, NTOP )
                  IF( NFOP .GT. 0 ) THEN

C                    LA FACE NFOP DE NTOP DEVIENT FRONTIERE
                     NOTETR( 4+NFOP, NTOP ) = 0

C                    LES SOMMETS DE NTOP APPARTIENNENT A NTOP
C                    ET NON PLUS A NTEQM
                     DO K=1,4
                        N1TETS( NOTETR(K,NTOP) ) = NTOP
                     ENDDO

                  ENDIF
               ENDIF
            ENDDO

C           SUPPRESSION DE NOTETR DU TETRAEDRE NTEQM
ccc            PRINT*,'a1teqm: SUPPRESSION de NOTETR(',NTEQM,')=',
ccc     %             (NOTETR(K,NTEQM),K=1,8),' V Ideal=',VOLTID,
ccc     %             ' V=',VTEQM,' Q=',QTEQM

            DO K=1,8
               NOTETR( K, NTEQM ) = 0
            ENDDO
            NOTETR( 5, NTEQM ) = N1TEVI
            N1TEVI =  NTEQM

C           TETRAEDRE INACTIF DONC DE VOLUME INCONNU
            IF( IVOLTE .NE. 0 ) THEN
               NVOLTE( NTEQM ) = -1
            ENDIF

            MODIFT = 1
            IERR   = 0
            GOTO 9999

         ENDIF

      ENDIF


C     RECHERCHE D'UNE ARETE TRES PETITE PAR RAPPORT AUX 5 AUTRES
C     TENTATIVE DE SUPPRIMER TOUS LES TETRAEDRES DE CETTE ARETE
C     EN IDENTIFIANT LES 2 SOMMETS DE L'ARETE
C     ----------------------------------------------------------
      CALL TEQMTYFA( MXSOMM, PTXYZD, NPSOFR, NTEQM,
     %               MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %               IVOLTE, NVOLTE,
     %               MXFETO, N1FEOC, NFETOI,
     %               MXTEAU, NBTEAU, NOTEAU, MODIFT )

      IF( MODIFT .NE. 0 ) THEN
C        NTEQM A ETE SUPPRIME ET CEUX ADJACENTS A SA TOUTE PETITE ARETE
         GOTO 9999
      ENDIF


C     SI LE TETRAEDRE NTEQM EST ECRASE DE TYPE TRIANGLE:
C     SI LE TETRAEDRE NTEQM A 3 SOMMETS SUR UNE ARETE ALORS
C     DECOMPOSITION DE TETRAEDRES EN 2 TETRAEDRES AUTOUR DE CETTE ARETE
C     -----------------------------------------------------------------
      CALL TEQMTYTR( QTEAME, PTXYZD, NTEQM,  N1TEVI, NOTETR, N1TETS,
     %               NUDTETR,
     %               INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %               MXTEAU, NBTEAU, NOTEAU, MODIFT )
      IF( MODIFT .GT. 0 ) THEN
C        NTEQM A ETE RESORBE
         IERR = 0
         GOTO 9999
      ENDIF

      GOTO 100


C     SI LE TETRAEDRE NTEQM EST ECRASE DE TYPE QUADRANGLE:
C     DECOMPOSITION DE TETRAEDRES PAR SUPPRESSION D'UNE DIAGONALE
C     AUTOUR DE LAQUELLE S'ENROULE 4, 5, ... TETRAEDRES
C     -----------------------------------------------------------
 50   CALL TEQMTYQU( PTXYZD,  NTEQM,  N1TEVI, NOTETR, N1TETS,
     %               NUDTETR, IVOLTE, NVOLTE,
     %               MXTEAU,  NBTEAU, NOTEAU, MODIFT )
      IF( MODIFT .GT. 0 ) THEN
C        NTEQM A ETE RESORBE
         IERR = 0
         GOTO 9999
      ENDIF


C     RECHERCHE DE CONFIGURATIONS SIMPLES DES TETRAEDRES OPPOSES
C     AUX 4 FACES DE NTEQM, NOTAMMENT AVEC UN SOMMET COMMUN
C     AMELIORER LA QUALITE DES TETRAEDRES AUTOUR DU TETRAEDRE NTEQM
C     DE MEDIOCRE QUALITE EN CHERCHANT DES TETRAEDRES OPPOSES AUX
C     FACES ET AYANT 1 SOMMET COMMUN
C     EN CAS DE SUCCES: 4t->1t ou 3t->2t
C     -------------------------------------------------------------
      CALL TEQMTYTQ( QTEAME, NTEQM,  PTXYZD, NPSOFR,
     %               INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %               MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %               MXTEAU, NBTEAU, NOTEAU, MODIFT, IERR )

cccC     VISUALISATION FINALE DES NBTEAU TETRAEDRES MODIFIES
ccc      IF( NBTEAU .GT. 0 ) THEN
ccc      TRACTE = .TRUE.
ccc      KTITRE='a1teqm: NPt=                 NOUVEAUX TETRAEDRES AUTOUR du
ccc     % TETRAEDRE de QUALITE<       et FIN'
ccc      WRITE(KTITRE(14:20),'(I7)') NPt
ccc      WRITE(KTITRE(22:28),'(I7)') NBTEAU
ccc      WRITE(KTITRE(81:86),'(F6.3)') QTEAME
ccc      CALL SANSDBL( KTITRE, NBC )
ccc      PRINT*,KTITRE(1:NBC)
ccc      CALL TRACEETOILE( KTITRE, PTXYZD, NOTETR, NPt, NBTEAU, NOTEAU )
ccc      ENDIF

      IF( MODIFT .GT. 0 ) THEN
C        NTEQM A ETE RESORBE
         IERR = 0
         GOTO 9999
      ENDIF


C     ESSAI DE CHANGER 2T->3T SUR LES 4 FACES DU TETRAEDRE NTEQM
C     et SINON m TETRAEDRES AYANT UNE ARETE COMMUNE EN 2m-4 TETRAEDRES
C     => UNE ARETE EST ECHANGEE EN m-2 FACES TRIANGULAIRES
C     ================================================================
 100  NPt = NOTETR( 1, NTEQM )
      NBTEAU = 1
      NOTEAU( 1 ) = NTEQM
      CALL EC2TMTNT( NPt,    PTXYZD, INFACO,  MXFACO, LEFACO,
     %               IVOLTE, NVOLTE, MXTEAU,  NBTEAU, NOTEAU,
     %               NOTETR, N1TEVI, NUDTETR, N1TETS,
     %               NBEC2T3T, NBECMTNT )
      IF( NBEC2T3T + NBECMTNT .GT. 0 ) THEN
C        NTEQM A ETE TRAITE
         MODIFT = 1
         IERR   = 0
         GOTO 9999
      ENDIF

C     PAS DE MODIFICATION DE TETRAEDRES
C     ---------------------------------
 9900 MODIFT = 0

 9999 TRACTE = TRACTE0
      RETURN
      END
