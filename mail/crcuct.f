      SUBROUTINE CRCUCT( NBCOTE, NUCOTE, MNSOCT, RLONGC, MNCUCT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES COORDONNEES CURVILIGNES HOMOGENES DES LIGNES
C -----    DES NBCOTE COTES DU POLYGONE (TRIANGLE OU QUADRANGLE) COURBE
C
C ENTREES:
C --------
C NBCOTE : NOMBRE DE COTES (3 POUR LE TRIANGLE, NBCOTE POUR LE QUADRANGLE)
C NUCOTE : NUCOTE(3) NUMERO DE 1 A NBCOTE DU COTE 3 PARMI LES NBCOTE LIGNES...
C          SI NUCOTE(3)=-NBCOTE LE COTE 3 EST LA LIGNE NBCOTE A PARCOURIR
C                          EN SENS INVERSE DE SON RANGEMENT...
C          L'ORDRE EST CELUI IMPOSE PAR  LA DONNEE DE NUCOTE(123)
C          ET LES SIGNES DE NUCOTE(I) POUR CE SENS IMPOSE
C MNSOCT : ADRESSE MCN DU TABLEAU XYZSOMMET DES NBCOTE LIGNES  COTES
C          DU POLYGONE COURBE DANS L'ODRE DONN'E PAR L'UTILISATEUR
C
C SORTIES:
C --------
C RLONGC : LONGUEUR DES NBCOTE COTES DANS L'ORDRE DES COTES
C MNCUCT : MNCUCT(J) L'ADRESSE DES COORDONNEES CURVILIGNES HOMOGENES
C          DU COTE J
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS         OCTOBRE 1996
C234567..............................................................012
      IMPLICIT INTEGER (W)
      include"./incl/a___xyzsommet.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL             RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      INTEGER           NUCOTE(NBCOTE)
      INTEGER           MNSOCT(NBCOTE)
      REAL              RLONGC(NBCOTE)
      INTEGER           MNCUCT(NBCOTE)
C
C     SOMME DES SOMMETS DES NBCOTE LIGNES DES COTES
      NBST = 0
      DO 5 J=1,NBCOTE
         NBST = NBST + MCN( MNSOCT(J)+WNBSOM )
 5    CONTINUE
C
C     DECLARATION DU TABLEAU DES COORDONNEES CURVILIGNES HOMOGENES
C     DES NBCOTE LIGNES
      CALL TNMCDC( 'REEL', NBST, MNCUCT(1) )
C
C     LES COORDONNEES CURVILIGNES HOMOGENES DES NBCOTE LIGNES COTES
C     DU POLYGONE COURBE DANS LE SENS
      MNC = MNCUCT(1) - 1
      DO 50 J=1,NBCOTE
C
C        LE NUMERO DE LA LIGNE DU COTE J
         N    = ABS( NUCOTE(J) )
C        L'ABSCISSE CURVILIGNE DU PREMIER SOMMET DU COTE J
         MNC0 = MNC
         MNC  = MNC + 1
         R    = 0.0
         RMCN(MNC) = R
C
C        L'ADRESSE DU DEBUT DES COORDONNEES DES SOMMETS DE LA LIGNE DU COTE J
         MN0 = MNSOCT(N) + WYZSOM
C
C        LE NOMBRE DE SOMMETS DE LA LIGNE DU COTE J
         NBS = MCN( MNSOCT(N) + WNBSOM )
C
         IF( NUCOTE(J) .GT. 0 ) THEN
C           SENS CORRECT
            DO 10 I = 2, NBS
               MN1 = MN0 + 3
               R   = R + DIST2P( RMCN(MN0), RMCN(MN1) )
               MNC = MNC + 1
               RMCN(MNC) = R
               MN0 = MN1
 10         CONTINUE
         ELSE
C           SENS A INVERSER
            MN0 = MN0 + 3 * NBS - 3
            DO 20 I = 2, NBS
               MN1 = MN0 - 3
               R   = R + DIST2P( RMCN(MN0), RMCN(MN1) )
               MNC = MNC + 1
               RMCN(MNC) = R
               MN0 = MN1
 20         CONTINUE
         ENDIF
C
C        LA LONGUEUR DU COTE J
         RLONGC(J) = R
C
C        NORMALISATION A 1.0 DE L'ABSCISSE CURVILIGNE
         DO 30 I=2,NBS-1
            RMCN(MNC0+I) = RMCN(MNC0+I) / R
 30      CONTINUE
C
C        LE DERNIER SOMMET DU COTE J
         RMCN(MNC) = 1.0
C
C        L'ADRESSE DE DEBUT DES COORDONNEES CURVILIGNES HOMOGENES DES COTES
         IF( J. NE. NBCOTE ) MNCUCT(J+1) = MNC+1
C
 50   CONTINUE
      END
