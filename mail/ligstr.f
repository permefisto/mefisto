      SUBROUTINE LIGSTR( NTLXOB, NTNSEF, MNNSEF, NTXYZS, MNXYZS, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TENTATIVE DE STRUCTURER UNE LIGNE DEFINIE PAR
C -----    SES 2 TMS XYZSOMMET ET NSEF
C          SI LA LIGNE EST FERMEE DONC NON STRUCTURABLE SES TMS SONT
C          NEANMOINS REMIS DANS UN SENS DE PARCOURS FAVORABLE
C
C ENTREES:
C --------
C NTLXOB : NUMERO DE TMS DU LEXIQUE DE LA LIGNE A RESTRUCTURER
C NTNSEF : NUMERO      DU TMS 'NSEF' DES NUMEROS DES ARETES DE LA LIGNE
C MNNSEF : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES ARETES
C          CF ~/td/d/a___nsef'
C NTXYZS : NUMERO      DU TMS 'XYZSOMMET' DES SOMMETS DES ARETES DE LA LIGNE
C MNXYZS : ADRESSE MCN DU TMS 'XYZSOMMET' DES SOMMETS DES ARETES DE LA LIGNE
C          CF '~/td/d/a___xyzsommet'
C
C SORTIE :
C --------
C IERR   : 0 LA LIGNE A ETE RESTRUCTUREE OU ETAIT STRUCTUREE
C          1 RESTRUCTURATION IMPOSSIBLE LIGNE NON MODIFIEE
C          2 RESTRUCTURATION IMPOSSIBLE LIGNE FERMEE (TABLEAUX MODIFIES)
C          5 ERREUR NTNSEF<=0
C          3 ERREUR NTXYZS<=0
C          4 CE N'EST PAS UNE LIGNE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS    NOVEMBRE 1995
C2345X7..............................................................012
      include"./incl/gsmenu.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a_ligne__definition.inc"
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL             RMCN(1)
      EQUIVALENCE     (RMCN(1),MCN(1))
C
      IF( NTNSEF .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGSTR: ERREUR TMS NSEF <0'
         CALL LEREUR
         IERR = 5
         RETURN
      ENDIF
C
      IF( MNNSEF .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGSTR: ERREUR ADRESSE TMS NSEF <0'
         CALL LEREUR
         IERR = 5
         RETURN
      ENDIF
C
      IF( NTXYZS .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGSTR: ERREUR TMS XYZS <0'
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
      IF( MNXYZS .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGSTR: ERREUR ADRESSE TMS XYZS <0'
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
      IF( MCN(MNNSEF+WUTYOB) .NE. 2 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGSTR: ERREUR OBJET DIFFERENT D''UNE LIGNE'
         CALL LEREUR
         IERR = 4
         RETURN
      ENDIF
C
      NUTYMA = MCN( MNNSEF + WUTYMA )
      IF( NUTYMA .EQ. 2 ) THEN
C        LIGNE DEJA STRUCTUREE
         IERR = 0
         RETURN
      ENDIF
C
      IF( NUTYMA .NE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGSTR: ERREUR LIGNE DE TYPE NON 0 OU 2'
         CALL LEREUR
         IERR = 4
         RETURN
      ENDIF
C
C     ICI NUTYMA=0 C-A-D LIGNE NON STRUCTUREE AVEC OU SANS TG
      NBSOM  = MCN( MNXYZS + WNBSOM )
      NBCOOR = MCN( MNXYZS + WBCOOR )
      NBTGS  = MCN( MNXYZS + WNBTGS )
      NBSOEF = MCN( MNNSEF + WBSOEF )
      NBTGEF = MCN( MNNSEF + WBTGEF )
      NBEFOB = MCN( MNNSEF + WBEFOB )
      NBEFTG = MCN( MNNSEF + WBEFTG )
      NBEFAP = MCN( MNNSEF + WBEFAP )
      MNLPEF = MNNSEF + WUSOEF + NBSOEF * NBEFOB
      MNLGEF = MNLPEF + NBEFAP
      MNLTEF = MNLGEF + NBEFTG
C
C     DECLARATIONS DE TABLEAUX AUXILIAIRES
      MOTIND = NBCOOR * MAX( NBSOM, NBEFOB ) + NBCOOR
      CALL TNMCDC( 'ENTIER', MOTIND, MNINDX )
      CALL TNMCDC( 'REEL'  , MOTIND, MNSOME )
      MNXYTG = 0
      IF( NBTGS .GT. 0 ) THEN
C        LA COPIE DES NBCOOR COORDONNEES DES NBTGS TANGENTES
         CALL TNMCDC( 'REEL', NBCOOR*NBTGS, MNXYTG )
         CALL TRTATA( MCN(MNXYZS+WYZSOM+NBCOOR*NBSOM),
     %                MCN(MNXYTG), NBCOOR*NBTGS )
         CALL TNMCDC( 'ENTIER', 2*NBEFTG, MNTGFI )
         CALL TNMCDC( 'ENTIER',   NBEFAP, MNLPEN )
      ENDIF
C
C     TENTATIVE DE RESTRUCTURATION
C     ============================
C     PROTECTION DE NBSOM POUR LES PRECEDENTES DECLARATIONS DE TMC
      NBSOMF = NBSOM
      CALL LIEXW4( NBEFOB, NBSOMF, NBEFTG,
     %             MCN(MNNSEF+WUSOEF),  MCN(MNLTEF),
     %             RMCN(MNXYZS+WYZSOM), MCN(MNLPEF),
     %             MCN(MNINDX),  MCN(MNTGFI),
     %             RMCN(MNSOME), MCN(MNLPEN), IERR )
C
      IF( IERR .EQ. 0 ) THEN
C
C        LA LIGNE EST EN FAIT STRUCTUREE POUR SES SOMMETS
C        ------------------------------------------------
C        MISE A JOUR DU TABLEAU  'NSEF'
         CALL LXTSDS( NTLXOB, 'NSEF' )
         MOTS = WBARSE+1
         IF( NBTGS .GT. 0 ) MOTS = MOTS + NBEFAP + NBCOOR * NBEFTG
         CALL LXTNDC( NTLXOB, 'NSEF', 'MOTS', MOTS )
         CALL LXTSOU( NTLXOB, 'NSEF', NTNSEF, MNNSEF )
C        LE TYPE DE L'OBJET UNION ICI LIGNE
         MCN( MNNSEF + WUTYOB ) = 2
C        LE TYPE DE FERMETURE DU MAILLAGE
C        LA LIGNE EST NON FERMEE
         MCN( MNNSEF + WUTFMA ) = 0
C        LE NOMBRE DE SOMMETS PAR EF
         MCN( MNNSEF + WBSOEF ) = NBSOEF
C        LE NOMBRE DE TG PAR EF
         MCN( MNNSEF + WBTGEF ) = NBTGEF
C        LE NOMBRE DE POINTEURS
         MCN( MNNSEF + WBEFAP ) = NBEFAP
C        LE NOMBRE D'EF A TG
         MCN( MNNSEF + WBEFTG ) = NBEFTG
C        LE NOMBRE DES EF DE LA LIGNE
         MCN( MNNSEF + WBEFOB ) = NBEFOB
C        LE TYPE DU MAILLAGE : ICI LIGNE STRUCTUREE
         MCN( MNNSEF + WUTYMA ) = 2
C        LE NOMBRE D'ARETES DE LA LIGNE STRUCTUREE
         MCN( MNNSEF + WBARSE ) = NBEFOB
         IF( NBTGS .GT. 0 ) THEN
C           COPIE DU TABLEAU DES POINTEURS SUR LES EF A TG
            MN = MNNSEF+WBARSE+1
            CALL TRTATA( MCN(MNLPEN), MCN(MN), NBEFAP )
C           COPIE DU TABLEAU DES NUMEROS GEOMETRIQUES DES EF A TG
            MN = MN + NBEFAP
            CALL TRTATA( MCN(MNLGEF), MCN(MN), NBEFTG )
C           COPIE DU TABLEAU NUTGEF FINAL
            MN = MN + NBEFTG
            CALL TRTATA( MCN(MNTGFI), MCN(MN), 2*NBEFTG )
         ENDIF
      ENDIF
C
      IF( IERR .EQ. 2 ) THEN
C
C        LA LIGNE EST EN FAIT FERMEE MAIS NON STRUCTUREE
C        -----------------------------------------------
C        LES TANGENTES ONT ETE REMISES A JOUR EN DIRECTION
C        MISE A JOUR DU TABLEAU  'NSEF'
         CALL LXTSDS( NTLXOB, 'NSEF' )
         MOTS = WUSOEF + 2 * NBEFOB
         IF( NBTGS .GT. 0 ) MOTS = MOTS + NBEFOB + NBCOOR*NBEFTG
         CALL LXTNDC( NTLXOB, 'NSEF', 'MOTS', MOTS )
         CALL LXTSOU( NTLXOB, 'NSEF', NTNSEF, MNNSEF )
C        LE TYPE DE L'OBJET UNION ICI LIGNE
         MCN( MNNSEF + WUTYOB ) = 2
C        LE TYPE DE FERMETURE DU MAILLAGE
C        LA LIGNE EST FERMEE
         MCN( MNNSEF + WUTFMA ) = 1
C        LE NOMBRE DE SOMMETS PAR EF
         MCN( MNNSEF + WBSOEF ) = NBSOEF
C        LE NOMBRE DE TG PAR EF
         MCN( MNNSEF + WBTGEF ) = NBTGEF
C        LE NOMBRE DE POINTEURS
         MCN( MNNSEF + WBEFAP ) = NBEFAP
C        LE NOMBRE D'EF A TG
         MCN( MNNSEF + WBEFTG ) = NBEFTG
C        LE NOMBRE DES EF DE LA LIGNE
         MCN( MNNSEF + WBEFOB ) = NBEFOB
C        LE TYPE DU MAILLAGE : ICI NON STRUCTURE
         MCN( MNNSEF + WUTYMA ) = 0
         MN = MNNSEF + WUSOEF
         DO 10 I=1,NBEFOB
C           LE NUMERO DES 2 SOMMETS DE L'ARETE I DE LA LIGNE
            MCN(MN  ) = I
            MCN(MN+1) = I+1
            MN = MN + 2
 10      CONTINUE
C        LE DERNIER SOMMET EST EN FAIT LE PREMIER
         MCN(MN-1) = 1
         IF( NBTGS .GT. 0 ) THEN
C           COPIE DU TABLEAU DES POINTEURS SUR LES EF A TG
            CALL TRTATA( MCN(MNLPEN), MCN(MN), NBEFAP )
C           COPIE DU TABLEAU DES NUMEROS GEOMETRIQUES DES EF A TG
            MN = MN + NBEFAP
            CALL TRTATA( MCN(MNLGEF), MCN(MN), NBEFTG )
C           COPIE DU TABLEAU NUTGEF FINAL
            MN = MN + NBEFTG
            CALL TRTATA( MCN(MNTGFI), MCN(MN), 2*NBEFTG )
         ENDIF
      ENDIF
C
      IF( IERR .EQ. 0 .OR. IERR .EQ. 2 ) THEN
C
C        LA DATE ET LE NUMERO DE TD
         CALL ECDATE( MCN(MNNSEF) )
         MCN( MNNSEF + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
C
C        MISE A JOUR DU TABLEAU 'XYZSOMMET' DE LA LIGNE STRUCTUREE OU FERMEE
         CALL LXTSDS( NTLXOB, 'XYZSOMMET' )
         CALL LXTNDC( NTLXOB, 'XYZSOMMET', 'MOTS',
     %                WYZSOM+NBCOOR*(NBSOMF+NBTGS) )
         CALL LXTSOU( NTLXOB, 'XYZSOMMET', NTXYZS, MNXYZS )
C        LE NOMBRE DE COORDONNEES DES SOMMETS DE LA LIGNE
         MCN( MNXYZS + WBCOOR ) = NBCOOR
C        LE NOMBRE DE SOMMETS DE LA LIGNE
         MCN( MNXYZS + WNBSOM ) = NBSOMF
C        LE NOMBRE DE TANGENTES DE LA LIGNE
         MCN( MNXYZS + WNBTGS ) = NBTGS
C        LA COPIE DES NBCOOR COORDONNEES DES NBSOMF SOMMETS
         MN = MNXYZS + WYZSOM
         CALL TRTATA( MCN(MNSOME), MCN(MN), NBCOOR*NBSOMF )
         IF( NBTGS .GT. 0 ) THEN
C           LES NBCOOR COORDONNEES DES NBTGS TANGENTES SONT RECOPIEES
            MN = MN + NBCOOR*NBSOMF
            CALL TRTATA( MCN(MNXYTG), MCN(MN), NBCOOR*NBTGS )
         ENDIF
C        LA DATE ET LE NUMERO DE TD
         CALL ECDATE( MCN(MNXYZS) )
         MCN( MNXYZS + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
      ENDIF
C
C     DESTRUCTION DES TABLEAUX AUXILIAIRES
      IF( NBTGS .GT. 0 ) THEN
         CALL TNMCDS( 'ENTIER',     NBEFAP, MNLPEN )
         CALL TNMCDS( 'ENTIER',   2*NBEFTG, MNTGFI )
         CALL TNMCDS( 'REEL', NBCOOR*NBTGS, MNXYTG )
      ENDIF
      CALL TNMCDS( 'REEL'  , MOTIND, MNSOME )
      CALL TNMCDS( 'ENTIER', MOTIND, MNINDX )

      RETURN
      END
