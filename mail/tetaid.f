      SUBROUTINE TETAID( NUTYSU, NOFOTI, RAP2P3, DX, DY,
     %                   ORIGIN, D2D3,   XMIN, YMIN, XYMXMI,
     %                   XYZ,    DTAILL, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CALCUL DES 3 COORDONNEES DANS LE REPERE INITIAL DE (DX,DY)
C -----     CALCULER LA TAILLE SOUHAITEE DANS L'ESPACE INITIAL 3D
C           AUTOUR DU SOMMET I DE PXYD ET COMPTE TENU DE L'APPEL
C           PAR SUEX01 03 06 09 19 ET LA PRESENCE OU NON DE LA
C           FONCTION TAILLE_IDEALE(X,Y,Z)
C           EFFECTUER TOUTES LES TRANSFORMATIONS POUR RAMENER CETTE
C           TAILLE DANS LE PLAN ET A L'ECHELLE 2**20 POUR LE SP SUEX09
C
C ENTREES:
C --------
C NUTYSU : NUMERO D'OPTION DE GENERATION DU MAILLAGE DE CETTE SURFACE
C          9 APPEL PAR LE SP SUEX09        (TRIANGULATION DES TE PLANS)
C         19 APPEL PAR LE SP SUEX19        (TRIANGULATION DES LIGNES PLANS)
C          1 APPEL PAR LE SP SUEX01-QUNSAL (QUADRANGLE ALGEBRIQUE)
C          3 APPEL PAR LE SP SUEX03        (B-SPLINE D'INTERPOLATION)
C          6 APPEL PAR LE SP SUEX06-TRNSAL (TRIANGLE   ALGEBRIQUE)
C NOFOTI : NUMERO DE LA FONCTION TAILLE_IDEALE(X,Y,Z)
C          >0 SI ELLE EXISTE, 0 SINON
C RAP2P3 : RAPPORT DU PERIMETRE DE L'ENVELOPPE DU DOMAINE PLAN SUR
C                  LE PERIMETRE DE L'ENVELOPPE DU DOMAINE DE R**3
C          APPROXIMATION POUR CALCULER RAPIDEMENT LA TAILLE DE L'ARETE
C          DANS LE PLAN CONNAISSANT SA TAILLE DANS R**3
C DX, DY : ABSCISSE ET ORDONNEE DANS LE PLAN DU POINT (REEL2!)
C ORIGIN : POINT ORIGINE DANS LE PLAN DE MAILLAGE
C D2D3   : MATRICE DE PASSAGE DES COORDONNEES 3D EN COORDONNEES 2D
C XMIN   : ABSCISSE MINIMALE APRES TRANSFORMATION 3D->2D
C YMIN   : ORDONNEE MINIMALE APRES TRANSFORMATION 3D->2D
C XYMXMI : ECART MAXIMAL ENTRE XMAX-XMIN ET YMAX-YMIN
C
C SORTIES:
C --------
C XYZ    : LES 3 COORDONNEES DU POINT DANS L'ESPACE R**3 INITIAL
C DTAILL : LA TAILLE_IDEALE AUTOUR DU POINT XYZ RAMENEE DANS LE PLAN
C          DU DOMAINE OU S'EFFECTUE LA TRIANGULATION
C IERR   : 0 SI PAS D'ERREUR, <>0 SINON
C          1 CALCUL INCORRECT DE TAILLE_IDEALE(X,Y,Z)
C          2 TAILLE CALCULEE NULLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       MARS 1997
C2345X7..............................................................012
C     SI SUEX03 A APPELE SUEX09 (NUTYSU=3) ALORS
C     LES VARIABLES DU COMMON QUI SUIT DOIVENT ETRE INITIALISEES
      COMMON / S09S03 / LDEXSB, LRX, MNRX,
     %                  LDEYSB, LRY, MNRY, MNSPQB
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      include"./incl/pp.inc"
      COMMON            RMCN(MOTMCN)
C
      DOUBLE PRECISION  RAP2P3, DX, DY, D2D3(3,3), DTAILL
      REAL              ORIGIN(3), XYZ(3)
C
      REAL              XY(2)
      DOUBLE PRECISION  DPARAF(3), D0
C
      IF( NOFOTI .GT. 0 ) THEN
C
C        EXISTENCE DE LA FONCTION TAILLE_IDEALE( X, Y, Z )
C        =================================================
C        CALCUL DES 3 COORDONNEES DANS LE REPERE INITIAL DE (DX,DY)
         XY(1) = REAL( DX )
         XY(2) = REAL( DY )
         CALL DV2D3D( XY, ORIGIN, D2D3, XMIN, YMIN, XYMXMI, XYZ )
         R = XYZ(1)
         S = XYZ(2)
C
         IF( NUTYSU .EQ. 1 ) THEN
C
C           QUADRANGLE ALGEBRIQUE TRANSFINI
C           ...............................
            CALL XYZTRF( R, S, XYZ, IERR )
            IF( IERR .NE. 0 ) RETURN
C
         ELSE IF( NUTYSU .EQ. 3 ) THEN
C
C           SURFACE B-SPLINE D'INTERPOLATION
C           ................................
C           ATTENTION: LES VARIABLES DE L'APPEL APPARTENANT AU
C           COMMON /S09S03/ DOIVENT ETRE INITIALISEES
            CALL XYZBST( R, S,
     %                   LDEXSB, LRX, RMCN(MNRX),
     %                   LDEYSB, LRY, RMCN(MNRY),
     %                   RMCN(MNSPQB),  XYZ )
C
         ELSE IF( NUTYSU .EQ. 6 ) THEN
C
C           TRIANGLE ALGEBRIQUE
C           ...................
            CALL XYZTTF( R, S, XYZ )
C
         ELSE IF( NUTYSU .EQ. 23 ) THEN
C
C           TORE
C           ....
            CALL XYZTORE( R, S, XYZ )
C
         ENDIF
C
C        LES 3 PARAMETRES D'APPEL DE LA FONCTION 'TAILLE_IDEALE'
C        EN CE SOMMET XYZ DU REPERE R**3 "NATUREL"
         D0 = DTAILL
         DPARAF(1) = XYZ(1)
         DPARAF(2) = XYZ(2)
         DPARAF(3) = XYZ(3)
         CALL FONVAL( NOFOTI, 3, DPARAF, NCODEV, DTAILL )
         IF( NCODEV .LE. 0 ) THEN
            IERR   = 1
            DTAILL = D0
            RETURN
         ENDIF
         IF( DTAILL .LT. 0D0 ) THEN
            WRITE(IMPRIM,10000) DPARAF
10000       FORMAT('ATTENTION: TAILLE_IDEALE(',
     %              G14.6,',',G14.6,',',G14.6,')<=0! => RENDUE >0' )
            DTAILL = -DTAILL
         ENDIF
         IF( DTAILL .EQ. 0D0 ) THEN
            WRITE(IMPRIM,10001) DPARAF
10001       FORMAT('ERREUR TAILLE_IDEALE(',
     %              G14.6,',',G14.6,',',G14.6,')=0!' )
            IERR = 2
            DTAILL = D0
         ENDIF
C
C        DTAILL CALCULE DANS R**3 EST RAMENE DANS LE PLAN DE TRIANGULATION
         DTAILL = DTAILL * RAP2P3
C
C        DTAILL EST LA TAILLE IDEALE DE L'ARETE DANS R**3
C        RAMENEE DANS LE PLAN. IL RESTE ENCORE A LA
C        METTRE A L'ECHELLE 2**20
         R      = REAL( XMIN + DTAILL )
         DTAILL = MISECH( R, XMIN, XYMXMI )
      ENDIF
C
      RETURN
      END
