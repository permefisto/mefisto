      SUBROUTINE EC2DFORC( NTR, NA, NBTRIA, NOTRIA, NTRADJ )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  ECHANGE FORCE DE L'ARETE NA DU TRIANGLE NTR ADJACENT AU
C -----  TRIANGLE NTRADJ POUR L'ARETE DES AUTRES SOMMETS DE NTR et NTRADJ
C        ou ECHANGE FORCE des DIAGONALES SANS AUCUNE PROTECTION

C ENTREES:
C --------
C NTR    : NUMERO NOTRIA DU TRIANGLE
C NA     : NUMERO DE L'ARETE A ECHANGER DU TRIANGLE NTR
C NBTRIA : NOMBRE DE TRIANGLES DU TABLEAU NOTRIA
C NOTRIA : NUMERO DE 1 A NBSOMM DES SOMMETS DES TRIANGLES DE LA TRIANGULATION
C          1 2 3 LE NO XYZSOM DES 3 SOMMETS DU TRIANGLE
C          4 5 6 LE NO DU TRIANGLE ADJACENT PAR L'ARETE 1 2 3

C SORTIE :
C --------
C NTRADJ : NUMERO NOTRIA DU TRIANGLE ADJACENT A NTR PAR SON ARETE NA
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  Saint Pierre du Perray             Mars 2020
C23456...............................................................012
      INTEGER  NOTRIA(6,NBTRIA)

C     LES 2 SOMMETS DE L'ARETE NA DE NTR
      IF( NA .EQ. 3 ) THEN
         N1 = 1
      ELSE
         N1 = NA+1
      ENDIF

      IF( N1 .EQ. 3 ) THEN
         N2 = 1
      ELSE
         N2 = N1+1
      ENDIF

C     LE NUMERO DES 3 SOMMETS DE NTR
      NS1 = NOTRIA( NA, NTR )
      NS2 = NOTRIA( N1, NTR )
      NS3 = NOTRIA( N2, NTR )

C     NUMERO DU TRIANGLE ADJACENT A NTR PAR L'ARETE NA
      NTRADJ = NOTRIA( 3+NA, NTR )

C     LE 3-EME SOMMET DU TRIANGLE ADJACENT NTRADJ PAR L'ARETE NA DE NTR
      IF( NTRADJ .EQ. 0 ) GOTO 9999
      IF( NOTRIA(1,NTRADJ) .EQ. 0 ) GOTO 9999

C     NS4 NUMERO DU SOMMET DE NTRADJ NON NS1 et NON NS2
      DO K=1,3
         NS4 = NOTRIA( K, NTRADJ )
         IF( NS4 .NE. NS1 .AND. NS4 .NE. NS2 ) GOTO 10
      ENDDO

C     PROTECTION DES TRIANGLES ADJACENTS DE NTR
 10   NTRA23 = NOTRIA( 3+N1, NTR )
      NTRA31 = NOTRIA( 3+N2, NTR )

C     PROTECTION DES TRIANGLES ADJACENTS DE NTRADJ
      CALL NUAR2STR( NS1, NS4, NOTRIA(1,NTRADJ), N4 )
      NTRA14 = NOTRIA( 3+N4, NTRADJ )
      CALL NUAR2STR( NS4, NS2, NOTRIA(1,NTRADJ), N5 )
      NTRA42 = NOTRIA( 3+N5, NTRADJ ) 

C     LE TRIANGLE NTR: NS1 NS2 NS3 et LE TRIANGLE NTRADJ: NS2 NS1 NS4
C     DEVIENNENT
C     LE TRIANGLE NTR: NS3 NS1 NS4 et LE TRIANGLE NTRADJ: NS3 NS4 NS2
      NOTRIA( 1, NTR ) = NS3
      NOTRIA( 2, NTR ) = NS1
      NOTRIA( 3, NTR ) = NS4

      NOTRIA( 4, NTR ) = NTRA31
      NOTRIA( 5, NTR ) = NTRA14
      NOTRIA( 6, NTR ) = NTRADJ

      NOTRIA( 1, NTRADJ ) = NS3
      NOTRIA( 2, NTRADJ ) = NS4
      NOTRIA( 3, NTRADJ ) = NS2

      NOTRIA( 4, NTRADJ ) = NTR
      NOTRIA( 5, NTRADJ ) = NTRA42
      NOTRIA( 6, NTRADJ ) = NTRA23

 9999 RETURN
      END
