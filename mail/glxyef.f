      SUBROUTINE GLXYEF( KNUADP, NULIGN,
     %                   MXYZST, NBXYZ,  XYZST,
     %                   NUMILF, NUMXLF, L1LGFR,
     %                   MXARCH, NSARCH,
     %                   NUAVAP,
     %                   NTXYZS, MNXYZS, NTNSEF, MNNSEF, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES TABLEAUX XYZSOMMET ET NSEF DE LA LIGNE NULIGN
C -----    A PARTIR DES TABLEAUX XYZST ET NSNVTR ISSUS DE L'ADAPTATION
C          L'ANCIEN NOM DE NSEF         ET XYZSOMMET        DEVIENNENT
C                          NSEF.Anuiter ET XYZSOMMET.Anuiter
C ENTREES :
C ---------
C KNUADP : SUFFIXE DE L'ITERATION D'ADAPTATION
C          '.A NO ITERATION'
C NULIGN : NUMERO DANS LE LEXIQUE LIGNE DE LA LIGNE A TRAITER
C
C MXYZST : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS XYZST
C NBXYZ  : NOMBRE DE SOMMETS ACTUELS DE LA TRIANGULATION
C XYZST  : 3 COORDONNEES DES NBXYZ SOMMETS DE LA TRIANGULATION
C
C NUMILF : NUMERO MINIMAL DES LIGNES FRONTIERES DE L'OBJET
C NUMXLF : NUMERO MAXIMAL DES LIGNES FRONTIERES DE L'OBJET
C L1LGFR : POUR CHAQUE LIGNE DE NUMILF A NUMXLF POINTE SUR LA PREMIERE
C          ARETE CHAINEE DE LA LIGNE
C
C MXARCH : NOMBRE MAXIMAL D'ARETES DECLARABLES DANS NSARCH
C NBARCH : NOMBRE D'ARETES CHAINEES DANS NSARCH
C NSARCH : NUMERO DES 2 SOMMETS DE L'ARETE, ARETE PRECEDENTE ET SUIVANTE
C
C
C SORTIES :
C ---------
C NUAVAP : NUMERO DU SOMMET AVANT PUIS APRES LA RENUMEROTATION
C NTXYZS : NUMERO      DU TMS XYZSOMMET DE LA LIGNE
C NMXYZS : ADRESSE MCN DU TMS XYZSOMMET DE LA LIGNE
C NTNSEF : NUMERO      DU TMS NSEF DE LA LIGNE
C NMNSEF : ADRESSE MCN DU TMS NSEF DE LA LIGNE
C IERR   : =0 SI PAS D'ERREUR DETECTEE
C          >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS     JANVIER 1995
C23456---------------------------------------------------------------012
      include"./incl/ntmnlt.inc"
      include"./incl/gsmenu.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a___xyzsommet.inc"
C
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE       (MCN(1),RMCN(1))
C
      INTEGER           L1LGFR(NUMILF:NUMXLF),
     %                  NSARCH(4,MXARCH),
     %                  NUAVAP(MXYZST)
      DOUBLE PRECISION  XYZST(3,MXYZST)
C
      CHARACTER*4       KNUADP
      CHARACTER*24      KNOM
C
C     RECHERCHE DU LEXIQUE DE CETTE LIGNE
      CALL LXNLOU( NTLIGN, NULIGN, NTLXLI, MNLXLI )
      IF( NTLXLI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:8),'(I8)') NULIGN
         KERR(1) = 'LIGNE ' // KERR(MXLGER)(1:8) //' SANS LEXIQUE'
         CALL LEREUR
         IERR = 12
         RETURN
      ENDIF
C
C     MISE A ZERO DU NUMERO APRES DES SOMMETS DU MAILLAGE
      DO 10 I=1,NBXYZ
         NUAVAP( I ) = 0
 10   CONTINUE
C
C     CALCUL DU NOMBRE D'ARETES DE LA LIGNE ADAPTEE
C     ---------------------------------------------
      NBA = 0
C     LA PREMIERE ARETE DE LA LIGNE ADAPTEE
      I   = L1LGFR( NULIGN )
C
 15   IF( I .GT. 0 ) THEN
C        PASSAGE A LA SUIVANTE
         I   = NSARCH(4,I)
         NBA = NBA + 1
         GOTO 15
      ENDIF
      IF( NBA .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:8),'(I8)') NULIGN
         KERR(1) = 'LIGNE ' // KERR(MXLGER)(1:8) //' SANS ARETE'
         CALL LEREUR
         IERR = 13
         RETURN
      ENDIF
C
C     MARQUAGE DES SOMMETS DES ARETES DE LA LIGNE
C     LA PREMIERE ARETE DE LA LIGNE ADAPTEE
      I   = L1LGFR( NULIGN )
 25   IF( I .GT. 0 ) THEN
         NUAVAP( NSARCH(1,I) ) = 1
         NUAVAP( NSARCH(2,I) ) = 1
C        PASSAGE A LA SUIVANTE
         I   = NSARCH(4,I)
         GOTO 25
      ENDIF
C
C     LE NOUVEAU NUMERO DES SOMMETS DE LA LIGNE
      NBS = 0
      DO 40 I=1,NBXYZ
         IF( NUAVAP(I) .GT. 0 ) THEN
            NBS = NBS + 1
            NUAVAP(I) = NBS
         ENDIF
 40   CONTINUE
C
C     GENERATION DU TABLEAU XYZSOMMET DE CETTE LIGNE
C     ==============================================
      CALL LXTSOU( NTLXLI, 'XYZSOMMET', NTXYZS, MNXYZS )
      IF( NTXYZS .GT. 0 ) THEN
C        CHANGEMENT DU NOM DU TABLEAU ACTUEL XYZSOMMET EN XYZSOMMET.Aiter
         KNOM = 'XYZSOMMET' // KNUADP
         CALL LXNMNM( NTLXLI, 'XYZSOMMET', KNOM )
      ENDIF
C
C     DECLARATION DU NOUVEAU TABLEAU
      CALL LXTNDC( NTLXLI, 'XYZSOMMET', 'MOTS', WYZSOM+3*NBS )
      CALL LXTSOU( NTLXLI, 'XYZSOMMET', NTXYZS, MNXYZS )
C
C     LE NOMBRE DE SOMMETS
      MCN( MNXYZS + WNBSOM ) = NBS
C
C     LES COORDONNEES DES SOMMETS
      MN = MNXYZS + WYZSOM - 1
      DO 80 I=1,NBXYZ
         NS = NUAVAP(I)
         IF( NS .GT. 0 ) THEN
            DO 70 J=1,3
               RMCN(MN+J) = REAL( XYZST(J,I) )
 70         CONTINUE
            MN = MN + 3
         ENDIF
 80   CONTINUE
C
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNXYZS) )
C
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNXYZS + WNBTGS ) = 0
      MCN( MNXYZS + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
C
C     GENERATION DU TABLEAU NSEF DE CETTE LIGNE
C     =========================================
      CALL LXTSOU( NTLXLI, 'NSEF', NTNSEF, MNNSEF )
      IF( NTNSEF .GT. 0 ) THEN
C        CHANGEMENT DU NOM DU TABLEAU ACTUEL NSEF EN NSEF.Aiter
         KNOM = 'NSEF' // KNUADP
         CALL LXNMNM( NTLXLI, 'NSEF', KNOM )
      ENDIF
C
      CALL LXTNDC( NTLXLI, 'NSEF', 'MOTS', WUSOEF+4*NBA )
      CALL LXTSOU( NTLXLI, 'NSEF', NTNSEF, MNNSEF )
C
C     MISE A JOUR DU TABLEAU 'NSEF' DE CETTE LIGNE
C     TYPE DE L'OBJET : LIGNE
      MCN( MNNSEF + WUTYOB ) = 2
C     NUMERO DU TYPE DU MAILLAGE : MAILLAGE NON STRUCTURE
      MCN( MNNSEF + WUTYMA ) = 0
C     NOMBRE DE SOMMETS PAR NSEF
      MCN( MNNSEF + WBSOEF ) = 2
C     NOMBRE DE NSEF DU MAILLAGE
      MCN( MNNSEF + WBEFOB ) = NBA
C
C     LE NUMERO DES 2 SOMMETS DES ARETES
      MN = MNNSEF + WUSOEF - 1
C     LA PREMIERE ARETE DE LA LIGNE ADAPTEE
      I   = L1LGFR( NULIGN )
 50   IF( I .GT. 0 ) THEN
C        UNE NOUVELLE ARETE POUR CETTE LIGNE
         DO 85 J=1,2
            MCN(MN+J) = NUAVAP( NSARCH(J,I) )
 85      CONTINUE
         MN = MN + 2
C        PASSAGE A L'ARETE SUIVANTE
         I  = NSARCH(4,I)
         GOTO 50
      ENDIF
C
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNNSEF) )
C
C     LE TYPE INCONNU DE FERMETURE DU MAILLAGE
      MCN( MNNSEF + WUTFMA ) = -1
C
C     PAS DE TANGENTES STOCKEES
      MCN( MNNSEF + WBTGEF ) = 0
      MCN( MNNSEF + WBEFTG ) = 0
      MCN( MNNSEF + WBEFAP ) = 0
C
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNNSEF + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
      END
