      SUBROUTINE NBFAARET( KTITRE, NFP,  PTXYZD, NOTETR, NBTECF, NOTECF,
     %                     LEFACO, NO0FAR, NBTRCF, NOTRCF,
     %                     NBCF,   N1ARCF, NOARCF,
     %                     N1FEOC, NFETOI,
     %                     MXFAN2, NBFAN2, NOFAN2,
     %                     ANGMIN, NF1MIN, NF2MIN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     CONSTRUIRE LE TABLEAU NOFAN2 DES ARETES DE L'ETOILE QUI
C -----     N'APPARTIENNENT PAS A 2 ET SEULEMENT 2 FACES

C ENTREES:
C --------
C KTITRE : TITRE DU TRACE COMPLETE PAR LE NOMBRE DE TETRAEDRES
C PTXYZD : PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : VERSION 1
C          1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3  =0
C          4: NUMERO DE CETTE FACE DANS LEFACO, 0 SI PAS DANS LEFACO
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C          EN VERSION 2 (NFETOI(3,.)>0)
C          1: NUMERO NOTETR DU TETRAEDRE AU DELA DE LA FACE
C             =0 SI INCONNU
C          2: NUMERO PTXYZD DU 1-ER SOMMET DE LA FACE
C          3: NUMERO PTXYZD DU 2-ME SOMMET DE LA FACE
C          4: NUMERO PTXYZD DU 3-ME SOMMET DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR DE
C             L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C NO0FAR : NUMERO DES 3 SOMMETS DE LA FACE AJOUTEE AU CF
C          NORMALE VERS L'INTERIEUR DU TETRAEDRE LA CONTENANT

C NBTRCF : NOMBRE DE TRIANGLES DU TABLEAU NOTRCF
C          C-A-D DU POLYGONE ENCORE DIT ENSUITE ETOILE
C NOTRCF : SI NOTRCF(*)>0 NUMERO LEFACO DU TRIANGLE
C                      <0 NUMERO NO0FAR DU TRIANGLE AJOUTE AU CF

C NBCF   : NOMBRE DE CIRCUITS DU CONTOUR FERME
C N1ARCF : TABLEAU (0:MXARCF) AUXILIAIRE
C          POINTE SUR LE DEBUT DES ARETES DE CHAQUE LIGNE FERMEE DU CF
C          0 POUR LES PLACES VIDES
C NOARCF : 1:NUMERO DU SOMMET DE L'ARETE DE LA LIGNE DU CONTOUR FERME
C          2:NUMERO DANS NOARCF DE L'ARETE SUIVANTE DU CF
C          3:NUMERO DANS LEFACO DU TRIANGLE ADJACENT OPPOSE A L'ARETE

C NBTECF : NOMBRE DE TETRAEDRES DE LA LISTE DES TETRAEDRES A TRACER
C NOTECF : NUMERO DANS NOTETR DES TETRAEDRES A TRACER
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C MXFAN2 : NOMBRE MAXIMAL D'ENTIERS DU TABLEAU NOFAN2

C SORTIES:
C --------
C NBFAN2 : NOMBRE D'ARETES DU CF N'APPARTENANT PAS A 2 ET SEULEMENT 2 FACES
C NOFAN2 : NOFAN2(NDFAN2+1)=NOMBRE NBF DE FACES NFETOI DE L'ARETE
C          NOFAN2(NDFAN2+2)=NUMERO PTXYZD DU SOMMET 1  DE L'ARETE
C          NOFAN2(NDFAN2+3)=NUMERO PTXYZD DU SOMMET 2  DE L'ARETE
C          NOFAN2(NDFAN2+4)=NUMERO DANS N1ARCF DU CIRCUIT DU CF
C          NOFAN2(NDFAN2+4+1...NBF)=NUMERO NF DANS NFETOI DE LA FACE
C ANGMIN : ANGLE MINIMAL ENTRE LES 2 FACES NF1MIN-NF2MIN DES ARETES DU CF
C NF1MIN : NUMERO NFETOI VERSION1 DE LA FACE 1
C NF2MIN : NUMERO NFETOI VERSION1 DE LA FACE 2
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Novembre 2017
C2345X7..............................................................012
      CHARACTER*(*)     KTITRE
      DOUBLE PRECISION  PTXYZD(4,*), ANGLE, ANGMIN, PIS4
      INTEGER           NOTETR(8,*), NOTECF(NBTECF),
     %                  LEFACO(11,0:*), NO0FAR(3,*),
     %                  NOTRCF(NBTECF), N1ARCF(0:*), NOARCF(1:3,*),
     %                  NFETOI(5,*),    NOFAN2(MXFAN2)
      INTEGER           NOSOTR1(3), NOSOTR2(3), NOSOFATE(3,4)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

      PIS4   = ATAN( 1D0 )
      ANGMIN = 10D0
      NF1MIN = 0
      NF2MIN = 0

C     RECHERCHE DES ARETES DU CF QUI N'APPARTIENNENT PAS
C     A 2 ET SEULEMENT 2 FACES SIMPLES DES TETRAEDRES DE L'ETOILE
C     -----------------------------------------------------------
      NDFAN2 = 0
      NBFAN2 = 0

      KTITRE='nbfaaret: FACE PERDUE        DEBUT DES ARETES A PB?'
      WRITE( KTITRE(23:27), '(I5)' ) NFP
      CALL SANSDBL( KTITRE, L )
      PRINT *, KTITRE(1:L)
      CALL TRFETO11( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %               NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %               NBTECF, NOTECF, NOTETR,
     %               NBFAN2, NOFAN2 )

      DO NCF = 1, NBCF

C        NOARCF:1: NO DU SOMMET DE L'ARETE DE LA LIGNE DU CONTOUR FERME
C               2: NO DANS NOARCF DE L'ARETE SUIVANTE DU CF
C               3: NO DANS LEFACO DU TRIANGLE ADJACENT OPPOSE A L'ARETE   
C        ARETE INITIALE DU CF NCF
         NA0  = 0
         NA00 = N1ARCF( NCF )
         NA1  = NA00

C        ARETE NA1-NA2 DU CF. RECHERCHE DU NOMBRE NBFA12 DE SES FACES NFETOI
 10      NA2 = NOARCF(2,NA1)

C        LES 2 SOMMETS DE L'ARETE NA1 DU CF
         NS1 = NOARCF(1,NA1)
         NS2 = NOARCF(1,NA2)

C        COMBIEN DE FACES NFETOI DE L'ETOILE CONTIENNENT CETTE ARETE DU CF?
         NBFA12 = 0

C        NOMBRE DE FACES AYANT CETTE ARETE NS1-NS2 DANS LE SENS INVERSE
C        C'EST A DIRE DANS L'AUTRE SENS DE NS1-NS2
         NBFA1AI = 0
C        NOMBRE DE FACES AYANT CETTE ARETE NS1-NS2 DANS LE MEME SENS
         NBFA1AM = 0

         NF1 = N1FEOC
 20      IF( NF1 .GT. 0 ) THEN

            IF( NFETOI(3,NF1) .EQ. 0 ) THEN
C              SI NFETOI EN VERSION 1
C              NUMERO NOTETR DU TETRAEDRE DE LA FACE SIMPLE
               NTE = ABS( NFETOI(1,NF1) )
C              NUMERO DE LA FACE SIMPLE DANS LE TETRAEDRE NTE
               I = ABS( NFETOI(2,NF1) )
C              LA FACE EST REINITIALISEE NON TRAITEE
               NFETOI(2,NF1) = I

C              LES 3 SOMMETS DE LA FACE SIMPLE DE L'ETOILE
C              NORMALE VERS L'INTERIEUR DU TETRAEDRE NTE (DONC DE L'ETOILE)
C              CAR PERMUTATION DES SOMMETS 2 ET 3 DE NOSOFATE
               NOSOTR1(1) = NOTETR( NOSOFATE(1,I), NTE )
               NOSOTR1(2) = NOTETR( NOSOFATE(3,I), NTE )
               NOSOTR1(3) = NOTETR( NOSOFATE(2,I), NTE )
            ELSE
C              NFETOI EN VERSION 2
               NOSOTR1(1) = NFETOI(2,NF1)
               NOSOTR1(2) = NFETOI(3,NF1)
               NOSOTR1(3) = NFETOI(4,NF1)
            ENDIF

C           PARCOURS DES 3 ARETES DE LA FACE NF1
            NAS1 = NOSOTR1(3)
            DO K=1,3

C              NAS1-NAS2 ARETE K-1 DE LA FACE I DU TETRAEDRE NTE
               NAS2 = NOSOTR1(K)
               IF( NAS1 .EQ. NS1 .AND. NAS2 .EQ. NS2 ) THEN
C                 L'ARETE EST RETROUVEE UNE FOIS DE PLUS DANS LE MEME SENS
                  NBFA1AM = NBFA1AM + 1
                  GOTO 30
               ELSE IF( NAS2 .EQ. NS1 .AND. NAS1 .EQ. NS2 ) THEN
C                 L'ARETE EST RETROUVEE UNE FOIS DE PLUS DANS LE SENS INVERSE
                  NBFA1AI = NBFA1AI + 1
                  GOTO 30
               ENDIF

C              ARETE SUIVANTE DE LA FACE NF1
               NAS1 = NAS2

            ENDDO
C           NS1-NS2 N'EST PAS UNE ARETE DE NF1
            GOTO 40

C           L'ARETE K DE LA FACE I DU TETRAEDRE NTE
C           EST L'ARETE NS1-NS2 NA1 DU CF DANS NFETOI
 30         NBFA12 = NBFA12 + 1
            NOFAN2( NDFAN2 + 4 + NBFA12 ) = NF1

C           LA FACE SUIVANTE DE L'ETOILE
 40         NF1 = NFETOI(5,NF1)
            GOTO 20

         ENDIF

         IF( NBFA1AI .NE. NBFA1AM ) THEN
            PRINT *,'nbfaaret: FACE PERDUE',NFP,' ARETE CF NS1=',
     %      NS1,'->NS2=',NS2,' dans NBFA1AMeme=',NBFA1AM,' NBFA1AInvrs='
     %     ,NBFA1AI,' soit',NBFA12,' FACES SIMPLES'
         ENDIF

         IF( NBFA12 .EQ. 2 ) THEN

C           ARETE NS1-NS2 DANS 2 ET SEULEMENT 2 FACES
C           -----------------------------------------
            NF1 = NOFAN2( NDFAN2 + 4 + 1 )
            NF2 = NOFAN2( NDFAN2 + 4 + 2 )

            IF( NFETOI(3,NF1) .EQ. 0 ) THEN
C              SI NFETOI EN VERSION 1
C              NUMERO NOTETR DU TETRAEDRE DE LA FACE SIMPLE
               NTE1 = ABS( NFETOI(1,NF1) )
C              NUMERO DE LA FACE SIMPLE DANS LE TETRAEDRE NTE1
               I = ABS( NFETOI(2,NF1) )
C              LA FACE EST REINITIALISEE NON TRAITEE
               NFETOI(2,NF1) = I

C              LES 3 SOMMETS DE LA FACE SIMPLE DE L'ETOILE
C              NORMALE VERS L'INTERIEUR DU TETRAEDRE NTE1 (DONC DE L'ETOILE)
C              CAR PERMUTATION DES SOMMETS 2 ET 3 DE NOSOFATE
               NOSOTR1(1) = NOTETR( NOSOFATE(1,I), NTE1 )
               NOSOTR1(2) = NOTETR( NOSOFATE(3,I), NTE1 )
               NOSOTR1(3) = NOTETR( NOSOFATE(2,I), NTE1 )
            ELSE
C              NFETOI EN VERSION 2
               NOSOTR1(1) = NFETOI(2,NF1)
               NOSOTR1(2) = NFETOI(3,NF1)
               NOSOTR1(3) = NFETOI(4,NF1)
            ENDIF

            IF( NFETOI(3,NF2) .EQ. 0 ) THEN
C              SI NFETOI EN VERSION 1
C              NUMERO NOTETR DU TETRAEDRE DE LA FACE SIMPLE
               NTE2 = ABS( NFETOI(1,NF2) )
C              NUMERO DE LA FACE SIMPLE DANS LE TETRAEDRE NTE2
               I = ABS( NFETOI(2,NF2) )
C              LA FACE EST REINITIALISEE NON TRAITEE
               NFETOI(2,NF2) = I

C              LES 3 SOMMETS DE LA FACE SIMPLE DE L'ETOILE
C              NORMALE VERS L'INTERIEUR DU TETRAEDRE NTE2 (DONC DE L'ETOILE)
C              CAR PERMUTATION DES SOMMETS 2 ET 3 DE NOSOFATE
               NOSOTR2(1) = NOTETR( NOSOFATE(1,I), NTE2 )
               NOSOTR2(2) = NOTETR( NOSOFATE(3,I), NTE2 )
               NOSOTR2(3) = NOTETR( NOSOFATE(2,I), NTE2 )
            ELSE
C              NFETOI EN VERSION 2
               NOSOTR2(1) = NFETOI(2,NF2)
               NOSOTR2(2) = NFETOI(3,NF2)
               NOSOTR2(3) = NFETOI(4,NF2)
            ENDIF

C           RECHERCHE NS3 SOMMET DE NF1 NON SOMMET NS1 et NS2
            DO K=1,3
               NS3 = NOSOTR1(K)
               IF( NS3 .NE. NS1 .AND. NS3 .NE. NS2 ) GOTO 43
            ENDDO

C           RECHERCHE NS4 SOMMET DE NF2 NON SOMMET NS1 et NS2
 43         DO K=1,3
               NS4 = NOSOTR2(K)
               IF( NS4 .NE. NS1 .AND. NS4 .NE. NS2 ) GOTO 45
            ENDDO

C           CALCUL DE L'ANGLE AUTOUR DE L'ARETE NS1-NS2 DE CES 2 FACES
C           ANGLE: ANGLE ENTRE LES PLANS DES 2 TRIANGLES P1P2P3 et P1P2P4
C                  DANS L'INTERVALLE [0, 2Pi] RADIANS
 45         CALL ANG2TR3D( PTXYZD(1,NS1),
     %                     PTXYZD(1,NS2),
     %                     PTXYZD(1,NS3),
     %                     PTXYZD(1,NS4),
     %                     ANGLE, IERR )
            PRINT*,'nbfaaret: ARETE',NS1,NS2,' ANGLE=',ANGLE*45D0/PIS4,
     %             ' degres',
     %             ' NF1=',NF1,' :',NOSOTR1,
     %             ' NF2=',NF2,' :',NOSOTR2

            IF( ANGLE .LT. ANGMIN ) THEN
               ANGMIN = ANGLE
               NF1MIN = NF1
               NF2MIN = NF2
            ENDIF

         ELSE

C           CAS OU L'ARETE NS1-NS2 DU CF N'APPARTIENT PAS
C           A 2 ET SEULEMENT 2 FACES DE L'ETOILE
C           STOCKAGE DANS NOFAN2 ET AFFICHAGE DE L'ARETE
C           ET DES FACES QUI CONTIENNENT CETTE ARETE
C           ---------------------------------------------
C           L'ARETE NS1-NS2 EST STOCKEE SI CE N'EST PAS ENCORE FAIT
            NDFA2 = 0
            DO K=1,NBFAN2
               NBF  = NOFAN2( NDFA2 + 1 )
               NAS1 = NOFAN2( NDFA2 + 2 )
               NAS2 = NOFAN2( NDFA2 + 3 )
               IF( ( NAS1.EQ.NS1 .AND. NAS2.EQ.NS2 ) .OR.
     %             ( NAS1.EQ.NS2 .AND. NAS2.EQ.NS1 ) ) GOTO 50
               NDFA2 = NDFA2 + 4 + NBF
            ENDDO

C           AJOUT DE L'ARETE NA1 NON ENCORE STOCKEE
            NBFAN2 = NBFAN2 + 1
C           ARETE NA1 DU CF APPARTIENT A NBFA12 FACES
            NOFAN2( NDFAN2 + 1 ) = NBFA12
C           SOMMET 1 DE L'ARETE SANS FACE ADJACENTE
            NOFAN2( NDFAN2 + 2 ) = NS1
C           SOMMET 2 DE L'ARETE SANS FACE ADJACENTE
            NOFAN2( NDFAN2 + 3 ) = NS2
C           NUMERO DU CIRCUIT DU CF (DANS N1ARCF)
            NOFAN2( NDFAN2 + 4 ) = NCF

            PRINT *,'nbfaaret: FACE PERDUE',NFP,' ARETE CF',
     %      NS1,NS2,' dans',NBFA12,' FACES SIMPLES et NBFA1AMeme=',
     %      NBFA1AM,' NBFA1AInvrs=',NBFA1AI

C           POINTEUR SUR LA DERNIERE FACE NFETOI DE CETTE ARETE
            NDFAN2 = NDFAN2 + 4 + NBFA12
            IF( NDFAN2 .GT. MXFAN2 ) THEN
               PRINT*,'nbfaaret: TABLEAU NOFAN2 SATURE. MXFAN2=',MXFAN2
               RETURN
            ENDIF

C           TRACE DES ARETES N'APPARTENANT PAS A SEULEMENT 2 FACES
            KTITRE='      ARETES N''APPARTENANT PAS A SEULEMENT 2 FACES'
            WRITE(KTITRE(1:5),'(I5)') NBFAN2
            CALL SANSDBL( KTITRE, L )
            CALL TRFETO11( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %                     NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                     NBTECF, NOTECF, NOTETR,
     %                     NBFAN2, NOFAN2 )
         ENDIF


C        PASSAGE A L'ARETE SUIVANTE DU CF
 50      IF( NA2 .NE. NA00 ) THEN
            NA0 = NA1
            NA1 = NA2
            GOTO 10
         ENDIF

      ENDDO


C     BILAN FINAL DES ARETES DU CF
      IF( NF1MIN .NE. 0 ) THEN
         PRINT*,'nbfaaret: ANGMIN=',ANGMIN/PIS4*45D0,' degres'
         PRINT*,'NF1MIN=',NF1MIN,' NFETOI:',(NFETOI(K,NF1MIN),K=1,5)
         PRINT*,'NF2MIN=',NF2MIN,' NFETOI:',(NFETOI(K,NF2MIN),K=1,5)
      ENDIF

      KTITRE='nbfaaret: FACE PERDUE        + les ARETES du CF Fin nbfaar
     %et'
      WRITE( KTITRE(23:27), '(I5)' ) NFP
      CALL SANSDBL( KTITRE, L )
      PRINT *, KTITRE(1:L)
      CALL TRFETO11( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %               NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %               NBTECF, NOTECF, NOTETR,
     %               NBFAN2, NOFAN2 )

      RETURN
      END
