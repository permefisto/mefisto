      SUBROUTINE Q05S2T( NS1, NS2, NS3, NS4, NS5, NUMATE, NUDMEF,
     %                   XYZSOM, NBSOTE, N1TETR, NSTETR,
     %                   NO1TSO, N1TESO, NOTESO,
     %                   MXTETA, VOLUMT, QUALIT, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER 2 TETRAEDRES A PARTIR DE 5 SOMMETS
C -----    TETRAEDRES DE SOMMETS 1453 5423
C
C ENTREES :
C ---------
C NS1,...NS5 : LES NUMEROS DES 5 SOMMETS DE COORDONNEES DANS XYZSOM
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C XYZSOM : COORDONNEES X Y Z DES NBSOMM SOMMETS DES TETRAEDRES
C NUMATE : NUMERO DE MATERIAU DES TETRAEDRES
C
C MODIFIES :
C ----------
C N1TETR : NUMERO DU PREMIER TETRAEDRE VIDE DANS NSTETR
C NSTETR : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NUDMEF : NUMERO DE MATERIAU DE CHAQUE TETRAEDRE DU MAILLAGE
C          ATTENTION: CE TABLEAU EXISTE SEULEMENT SI NBDM>0
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C N1TESO : NUMERO DU PREMIER TETRAEDRE NON UTILISE DANS NOTESO
C          CHAINAGE DES PLACES VIDES DANS NOTESO(2,*)
C NOTESO : NOTESO(1,*) NUMERO DANS NSTETR DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER
C MXTETA : NUMERO MAXIMAL DES TETRAEDRES ACTUELS
C VOLUMT : VOLUME  DES TETRAEDRES DE LA TETRAEDRISATION
C QUALIT : QUALITE DES TETRAEDRES DE LA TETRAEDRISATION
C IERR   : =0 SI PAS D'ERREUR
C          >0 EN CAS DE SATURATION D'UN TABLEAU
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1992
C....................................................................012
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      INTEGER           NSTETR(NBSOTE,*),
     %                  NUDMEF(*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*)
      REAL              XYZSOM(3,*),VOLUMT(*),QUALIT(*)
      REAL              ARMIN,ARMAX,SURFTR(4)
C
      DO 10 I=1,2
C
C        NT EST LE NOUVEAU TETRAEDRE
         IF( N1TETR .LE. 0 ) THEN
C           SATURATION DES TETRAEDRES
            NBLGRC(NRERR) = 1
            KERR(1) = 'SATURATION DES TETRAEDRES'
            CALL LEREUR
            IERR = 1
            RETURN
         ENDIF
C
C        MISE A JOUR DU 1-ER TETRAEDRE VIDE
         NT     = N1TETR
         N1TETR = NSTETR(2,N1TETR)
C
         IF( I .EQ. 1 ) THEN
C           LE NOUVEAU TETRAEDRE NT : 1453
            NSTETR(1,NT) = NS1
            NSTETR(2,NT) = NS4
            NSTETR(3,NT) = NS5
            NSTETR(4,NT) = NS3
         ELSE
C           LE NOUVEAU TETRAEDRE NT : 5423
            NSTETR(1,NT) = NS5
            NSTETR(2,NT) = NS4
            NSTETR(3,NT) = NS2
            NSTETR(4,NT) = NS3
         ENDIF
C
C        AJOUT DU TETRAEDRE NT DANS LE CHAINAGE DES TETRAEDRES
C        DE SES 4 SOMMETS
         CALL CHTESO( NT,     NBSOTE, NSTETR,
     %                NO1TSO, N1TESO, NOTESO, IERR )
C
C        QUALITE DU TETRAEDRE NT
         CALL QUATET( XYZSOM(1,NSTETR(1,NT)),
     %                XYZSOM(1,NSTETR(2,NT)),
     %                XYZSOM(1,NSTETR(3,NT)),
     %                XYZSOM(1,NSTETR(4,NT)),
     %                ARMIN, ARMAX, SURFTR, VOLUMT(NT), QUALIT(NT) )
C
C        EVENTUEL NO DE MATERIAU DU TETRAEDRE
         IF( NUMATE .GT. 0 ) NUDMEF( NT ) = NUMATE
C
C        MISE A JOUR DU NUMERO MAXIMAL
         MXTETA = MAX( MXTETA, NT )
 10   CONTINUE
C
      RETURN
      END
