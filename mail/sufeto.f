      SUBROUTINE SUFETO( NOTET1, I,      NOTETR,
     %                   N1FEOC, N1FEVI, NFETOI, NFE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     SI LA FACE I DU TETRAEDRE NOTET1 EST DANS NFETOI
C -----     ALORS ELLE EST SUPPRIMEE DE NFETOI et NFE=0
C           SINON ELLE EST AJOUTEE A NFETOI EN POSITION NFE>0

C ENTREES :
C ---------
C NOTET1 : NUMERO DU TETRAEDRE
C I      : NUMERO DE LA FACE DU TETRAEDRE NOTET1
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C ENTREES ET SORTIES :
C --------------------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : VERSION 1
C          1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3 A 4 NON UTILISES ICI
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C SORTIE :
C --------
C NFE    : > 0 NUMERO DANS NFETOI DE LA FACE AJOUTEE
C          = 0 SUPPRESSION DE LA FACE DE L'ETOILE
C          =-1 TABLEAU NFETOI SATURE. N1FEVI<=0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY   Janvier 2017
C23456...............................................................012
      INTEGER  NOTETR(8,*), NFETOI(5,*)

C     RECHERCHE DE LA FACE I DU TETRAEDRE NOTET1 DANS L'ETOILE NFETOI
      NF0 = 0
      NFE = N1FEOC

 10   IF( NFE .GT. 0 ) THEN

         IF( NFETOI(1,NFE) .EQ. NOTET1 .AND.
     %       ABS( NFETOI(2,NFE) ) .EQ. I ) THEN

C            LA FACE I DU TETRAEDRE NOTET1 EST IDENTIQUE A NFE
C            DESTRUCTION DE LA FACE NFE DE L'ETOILE NFETOI
             IF( NF0 .NE. 0 ) THEN

C               LA FACE NFE, PRECEDEE DE NF0, N'EST PAS LA
C               PREMIERE DE L'ETOILE
                NFETOI(5,NF0) = NFETOI(5,NFE)
C               LA FACE NFE DEVIENT LA PREMIERE VIDE DE L'ETOILE
                NFETOI(5,NFE) = N1FEVI
                N1FEVI = NFE

             ELSE

C               LA FACE NFE=N1FEOC EST LA 1-ERE DE L'ETOILE
                NF0    = NFETOI(5,N1FEOC)
                NFETOI(5,N1FEOC) = N1FEVI
                N1FEVI = N1FEOC
                N1FEOC = NF0

             ENDIF

             NFE = 0
             RETURN

         ELSE

C            LA FACE EST DIFFERENTE.PASSAGE A LA SUIVANTE
             NF0 = NFE
             NFE = NFETOI(5,NFE)
             GOTO 10

         ENDIF

      ENDIF

C     LA FACE I DE NOTET1 N'EST PAS RETROUVEE DANS NFETOI
C     ELLE EST AJOUTEE A L'ETOILE A LA FIN DES FACES OCCUPEES DE NFETOI
C     CECI AFIN DE CHEMINER DU CENTRE VERS L'EXTERIEUR
C     -------------------------------------------------------

C     LE NUMERO NTOP DU TETRAEDRE DE L'AUTRE COTE DE LA FACE I
      NTOP = NOTETR( I+4, NOTET1 )
      IF( NTOP .LE. 0 ) THEN
C        LE TETRAEDRE DE L'AUTRE COTE N'EXISTE PAS  FACE FRONTALIERE
C        LA FACE N'EST PAS AJOUTEE A NFETOI
         NFE = 0
         RETURN
      ENDIF

C     PARCOURS DES FACES OCCUPEES DE NFETOI POUR TROUVER LA DERNIERE
      NF0 = 0
      NFE = N1FEOC
 20   IF( NFE .GT. 0 ) THEN
         NF0 = NFE
         NFE = NFETOI(5,NFE)
         GOTO 20
      ENDIF
C     NF0 EST LA DERNIERE FACE DE L'ETOILE

C     RECHERCHE D'UNE FACE VIDE DANS NFETOI
      IF( N1FEVI .LE. 0 ) THEN
         PRINT *,'sufeto: NFETOI est SATURE. N1FEVI=',N1FEVI
         NFE = -1
         GOTO 9999
      ENDIF

C     NFE EST LA PREMIERE PLACE VIDE DANS NFETOI
      NFE    = N1FEVI
      N1FEVI = NFETOI(5,N1FEVI)
C
C     MISE A JOUR DU CHAINAGE : NFE VIENT APRES NF0
      IF( NF0 .GT. 0 ) THEN
C        IL EXISTE AU MOINS UNE FACE DANS L'ETOILE
         NFETOI( 5, NF0 ) = NFE
      ELSE
C        NFE EST LA SEULE FACE DE L'ETOILE
         N1FEOC = NFE
      ENDIF
C
C     NFOP LE NUMERO LOCAL DE LA FACE CONCERNEE DANS LE TETRAEDRE OPPOSE
      IF( NOTETR(5,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 1
      ELSE IF( NOTETR(6,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 2
      ELSE IF( NOTETR(7,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 3
      ELSE IF( NOTETR(8,NTOP) .EQ. NOTET1 ) THEN
         NFOP = 4
      ELSE
         PRINT *,'sufeto: TETRAEDRE NOTET1=',NOTET1,
     %   ' NON OPPOSE PAR SA FACE',I,' AU TETRAEDRE NTOP=',NTOP
         PRINT *,'NOTETR(',NOTET1,')=',(NOTETR(J,NOTET1),J=1,8)
         PRINT *,'NOTETR(',NTOP,  ')=',(NOTETR(J,NTOP),  J=1,8)
         NFOP = 0
      ENDIF
C
C     INTIALISATION DE LA FACE SIMPLE EN DERNIERE POSITION DE L'ETOILE
      NFETOI(1,NFE) = NTOP
      NFETOI(2,NFE) = NFOP
      NFETOI(3,NFE) = 0
      NFETOI(4,NFE) = 0
      NFETOI(5,NFE) = 0

 9999 RETURN
      END
