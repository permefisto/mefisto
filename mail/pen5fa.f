         SUBROUTINE  PEN5FA( XYZ, I0, J0, K0, M, N,
     %                       XYSTR1, XYSTR5, XYSCA1, COSO,
     %                       XYZF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LES COORDONNEES DES POINTS SUR LES FACES
C -----    DU PENTAEDRE RECTANGLE UNITE DE LA FORMULE DE
C          L'INTERPOLATION TRANSFINIE DEGRE 1 (cf CRAS A.PERRONNET)
C
C ENTREES:
C --------
C XYZ     : LES 3 COORDONNEES DU POINT DANS LE PENTAEDRE UNITE
C I0,J0,K0: LES 3 INDICES DANS LE PENTAEDRE DU POINT INTERNE A TRAITER
C M       : LE NOMBRE DE SOMMETS PAR ARETE DES FACES TRIANGULAIRES
C N       : LE NOMBRE DE SOMMETS PAR ARETE EN Z DES FACES QUADRILATERES
C XYSTR1  : LES 2 COORDONNEES DES POINTS SUR LE TRIANGLE UNITE
C           DE LA FACE 1 DU PENTAEDRE
C XYSTR5  : LES 2 COORDONNEES DES POINTS SUR LE TRIANGLE UNITE
C           DE LA FACE 5 DU PENTAEDRE
C XYSCA1  : LES 2 COORDONNEES DES POINTS SUR LE CARRE UNITE
C           DES FACES 2 3 4 DU PENTAEDRE
C COSO    : LES COORDONNEES DES SOMMETS SUR LES FACES DU PENTAEDRE COURBE
C
C SORTIES:
C --------
C XYZF   : LES 3 COORDONNEES DES SOMMETS DES FACES DU PENTAEDRE COURBE
C          NECESSAIRES DANS LA FORMULE DE L'INTERPOLATION TRANSFINIE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN ANALYSE NUMERIQUE UPMC PARIS    NOVEMBRE 1997
C23456---------------------------------------------------------------012
      REAL     XYZ(3)
      REAL     XYSTR1(2,*), XYSTR5(2,*)
      REAL     XYSCA1(2,M,N,2:4)
      REAL     COSO(3,*)
      REAL     XYZF(3,8)
      REAL     CB(3)
C
C     LES FONCTIONS FORMULES DES NUMEROS DES SOMMETS DU TRIANGLE ET PENTAEDRE
      NUSOTR(I,J)   = ( I * I - I ) / 2 + J
      NUSOPE(I,J,K) = J + ( I * I - I + (K-1) * ( M * M + M ) ) / 2
C
C     LES COORDONNEES BARYCENTRIQUES DU TRIANGLE DE SECTION
      CB2 = XYZ(1)
      CB3 = XYZ(2)
      CB1 = 1 - CB2 - CB3
      Z   = XYZ(3)
C
C     SUR LA FACE 1 TRIANGULAIRE INFERIEURE DU PENTAEDRE UNITE
      CALL REXYT1( CB2, CB3, M, XYSTR1,
     %             NTY, I, J, CB )
C     LE POINT EST DANS LE SOUS-TRIANGLE DE SOMMETS NS1,NS2,NS3
C     AVEC LES COORDONNEES BARYCENTRIQUES CB(1), CB(2), CB(3)
C     CALCUL DE L'INTERPOLATION P1 DU POINT DANS LE SOUS-TRIANGLE
      IF( NTY .GT. 0 ) THEN
C        TRIANGLE DE SOMMET 1 "VERS L'ORIGINE"
         NS1 = NUSOTR(I  ,J  )
         NS2 = NUSOTR(I+1,J  )
         NS3 = NUSOTR(I+1,J+1)
      ELSE
C        TRIANGLE DE SOMMET 1 "NON VERS L'ORIGINE"
         NS1 = NUSOTR(I+1,J+1)
         NS2 = NUSOTR(I  ,J+1)
         NS3 = NUSOTR(I  ,J  )
      ENDIF
      DO 10 K=1,3
         XYZF(K,1) = CB(1) * COSO(K,NS1)
     %             + CB(2) * COSO(K,NS2)
     %             + CB(3) * COSO(K,NS3)
 10   CONTINUE
C
C     SUR LA FACE 5 TRIANGULAIRE SUPERIEURE DU PENTAEDRE UNITE
      CALL REXYT1( CB2, CB3, M, XYSTR5,
     %             NTY, I, J, CB )
C     LE POINT EST DANS LE SOUS-TRIANGLE DE SOMMETS NS1,NS2,NS3
C     AVEC LES COORDONNEES BARYCENTRIQUES CB(1), CB(2), CB(3)
C     CALCUL DE L'INTERPOLATION P1 DU POINT DANS LE SOUS-TRIANGLE
      IF( NTY .GT. 0 ) THEN
C        TRIANGLE DE SOMMET 1 "VERS L'ORIGINE"
         NS1 = NUSOTR(I  ,J  )
         NS2 = NUSOTR(I+1,J  )
         NS3 = NUSOTR(I+1,J+1)
      ELSE
C        TRIANGLE DE SOMMET 1 "NON VERS L'ORIGINE"
         NS1 = NUSOTR(I+1,J+1)
         NS2 = NUSOTR(I  ,J+1)
         NS3 = NUSOTR(I  ,J  )
      ENDIF
C     DECALAGE POUR ATTEINDRE LE 1-ER SOMMET DE CETTE FACE DANS COSO
      L = NUSOPE( 1, 1, N ) - 1
      DO 20 K=1,3
         XYZF(K,2) = CB(1) * COSO(K,L+NS1)
     %             + CB(2) * COSO(K,L+NS2)
     %             + CB(3) * COSO(K,L+NS3)
 20   CONTINUE
C
C     SUR LA FACE 2 QUADRANGULAIRE DU PENTAEDRE UNITE
C     S(1-L2,L2,0,Z)
      CALL REXYQU( 2, I0, K0, CB2, Z, M, N, XYSCA1(1,1,1,2),
     %             COSO, XYZF(1,3) )
C     S(L1,1-L1,0,Z)
      CALL REXYQU( 2, I0, K0, 1-CB1, Z, M, N, XYSCA1(1,1,1,2),
     %             COSO, XYZF(1,4) )
C
C     SUR LA FACE 3 QUADRANGULAIRE DU PENTAEDRE UNITE
C     S(0,1-L3,L3,Z)
      CALL REXYQU( 3, J0, K0, CB3, Z, M, N, XYSCA1(1,1,1,3),
     %             COSO, XYZF(1,5) )
C     S(0,L2,1-L2,Z)
      CALL REXYQU( 3, J0, K0, 1-CB2, Z, M, N, XYSCA1(1,1,1,3),
     %             COSO, XYZF(1,6) )
C
C     SUR LA FACE 4 QUADRANGULAIRE DU PENTAEDRE UNITE
C     S(L1,0,1-L1,Z) ATTENTION Y DECROISSANT => 1-(1-L1)=L1
      CALL REXYQU( 4, M+1-I0, K0, CB1, Z, M, N, XYSCA1(1,1,1,4),
     %             COSO, XYZF(1,7) )
C     S(1-L3,0,L3,Z) ATTENTION Y DECROISSANT => 1-L3
      CALL REXYQU( 4, M+1-I0, K0, 1-CB3, Z, M, N, XYSCA1(1,1,1,4),
     %             COSO, XYZF(1,8) )
C
      RETURN
      END
