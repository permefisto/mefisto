      SUBROUTINE PLCNAN( ANGLE,  RAY1CN, RAY2CN, HAUTCN, XYZ1PL, VN,
     %                   PARAME, P, Q,   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DU PARAMETRE DU POINT D'INTERSECTION DE LA GENERATRICE
C -----    DU CONE AVEC LE PLAN DEFINI PAR LES 3 POINTS XYZCPL
C
C ENTREES:
C --------
C ANGLE  : DE LA GENERATRICE AVEC OX DANS LE REPERE PROPRE AU CONE
C RAY1CN : RAYON DU CERCLE AU POINT PAX1CN (PLAN ORTHOGONAL A L'AXE DU CONE )
C RAY2CN : RAYON DU CERCLE AU POINT PAX2CN (PLAN ORTHOGONAL A L'AXE DU CONE )
C HAUTCN : HAUTEUR DU TRONC DE CONE
C XYZ1PL : 3 COORDONNEES DANS LE REPERE DU CONE DES 1-ER POINT DE DEFINITION DU
C VN     : VECTEUR NORMAL UNITAIRE AU PLAN DES 3 POINTS
C
C SORTIES:
C --------
C PARAME : PARAMETRE A PARTIR DE P SUR LA DROITE PQ DU POINT D'INTERSECTION
C          AVEC LE PLAN
C P,Q    : 3 COORDONNEES DANS LE REPERE PROPRE DU CONE DES 2 EXTREMITES DE
C          DE LA GENERATRICE D'ANGLE AVEC OX
C IERR   :=0 PAS D'ERREUR DETECTEE
C         =2 LE PLAN EST TANGENT AU CONE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET UPMC ANALYSE NUMERIQUE  PARIS    JANVIER 1998
C2345X7..............................................................012
      DOUBLE PRECISION ANGLE,  RAY1CN, RAY2CN, HAUTCN, XYZ1PL(3), VN(3),
     %                 PARAME, P(3), Q(3)
      DOUBLE PRECISION C, S, D
C
C     LES 2 POINTS AUX EXTREMITES DE LA GENERATRICE N DU CONE
      C = COS( ANGLE )
      S = SIN( ANGLE )
C
      P(1) = RAY1CN * C
      P(2) = RAY1CN * S
      P(3) = 0D0
C
      Q(1) = RAY2CN * C
      Q(2) = RAY2CN * S
      Q(3) = HAUTCN
C
C     LE POINT D'INTERSECTION DU PLAN AVEC LA GENERATRICE N DU CONE
      D =   ( Q(1) - P(1) ) * VN(1)
     %    + ( Q(2) - P(2) ) * VN(2)
     %    + ( Q(3) - P(3) ) * VN(3)
C
      IF( D .EQ. 0D0 ) THEN
C        PLAN TG AU CONE
         IERR = 2
         RETURN
      ENDIF
C
      PARAME = ( ( XYZ1PL(1) - P(1) ) * VN(1)
     %         + ( XYZ1PL(2) - P(2) ) * VN(2)
     %         + ( XYZ1PL(3) - P(3) ) * VN(3) ) / D
      IERR = 0
C
      RETURN
      END
