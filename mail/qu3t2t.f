      SUBROUTINE QU3T2T( NT,     IAR,
     %                   XYZSOM, NBSOTE, N1TEVI, NSTETR, NBDM, NUDMEF,
     %                   NO1TSO, N1TESO, NOTESO,
     %                   NBTEDS, NOTEDS,
     %                   MXTETA, VOLUMT, QUALIT, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   TRANSFORMER LES EVENTUELS TETRAEDRES AYANT EN COMMUN
C -----   L'ARETE IAR DU TETRAEDRE NT (EN PARTICULIER TRAITE 3T=>2T)
C
C ENTREES :
C ---------
C NT     : NUMERO DU TETRAEDRE
C IAR    : NUMERO DE L'ARETE A TRAITER
C XYZSOM : COORDONNEES X Y Z DES NBSOMM SOMMETS DES TETRAEDRES
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C NBDM   : 0 SI 1 MATERIAU=VOLUME, SINON NOMBRE DE MATERIAUX DU VOLUME
C
C MODIFIES :
C ----------
C N1TEVI : NUMERO DU PREMIER TETRAEDRE VIDE DANS NSTETR
C NSTETR : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NUDMEF : NUMERO DE MATERIAU DE CHAQUE TETRAEDRE DU MAILLAGE
C          ATTENTION: CE TABLEAU EXISTE SEULEMENT SI NBDM>0
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C N1TESO : NUMERO DU PREMIER TETRAEDRE NON UTILISE DANS NOTESO
C          CHAINAGE DES PLACES VIDES DANS NOTESO(2,*)
C NOTESO : NOTESO(1,*) NUMERO DANS NSTETR DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER
C NBTEDS : NOMBRE DE  TETRAEDRES D'ARETE IAR
C NOTEDS : NUMERO DES TETRAEDRES D'ARETE IAR
C MXTETA : NUMERO MAXIMAL DES TETRAEDRES ACTUELS
C VOLUMT : VOLUME  DES TETRAEDRES DE LA TETRAEDRISATION
C QUALIT : QUALITE DES TETRAEDRES DE LA TETRAEDRISATION
C IERR   : =-1 SI ECHANGE FAIT SANS ERREUR
C          = 0 SI PAS D'ERREUR MAIS PAS D'ECHANGE
C          > 0 EN CAS DE SATURATION D'UN TABLEAU
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1992
C....................................................................012
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      INTEGER           NSTETR(NBSOTE,*),
     %                  NUDMEF(*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*),
     %                  NOTEDS(*)
      REAL              XYZSOM(3,*),VOLUMT(*),QUALIT(*)
      REAL              ARMIN,ARMAX,SURFTR(4)
      INTEGER           NSF(3)
C
      GRAND = RINFO( 'GRAND' )
C
C     LES 2 SOMMETS NS4 NS5 DE L'ARETE IAR DU TETRAEDRE NT
      IF( IAR .LE. 3 ) THEN
C        NUMEROTATION SELON LE SP LON6AR DE UTIL
         NS4 = IAR
         IF( IAR .EQ. 3 ) THEN
            NS5 = 1
         ELSE
            NS5 = IAR + 1
         ENDIF
      ELSE
         NS4 = 4
         NS5 = IAR - 3
      ENDIF
      NS4 = NSTETR(NS4,NT)
      NS5 = NSTETR(NS5,NT)
C
C     NBTEDS TETRAEDRES ONT L'ARETE NS4-NS5 COMMUNE
      CALL NOTEAR( NS4,    NS5,
     %             NBSOTE, NSTETR, NO1TSO, NOTESO,
     %             NBTEDS, NOTEDS )
      IF( NBTEDS .NE. 3 ) RETURN
C
      NUMATE = 0
      IF( NBDM .GT. 0 ) THEN
C        PAS DE MODIFICATION SI LES 2 TRIANGLES NE SONT PAS
C        DANS LE MEME VOLUME
         NUMATE = NUDMEF(NOTEDS(1))
         IF( NUMATE .NE. NUDMEF(NOTEDS(2)) .OR.
     %       NUMATE .NE. NUDMEF(NOTEDS(3)) ) RETURN
      ENDIF
C
C     LES SOMMETS DES 3 ARETES NON COMMUNES
      L = 0
      DO 20 I=1,NBTEDS
         NT1 = NOTEDS( I )
         DO 10 J=1,4
            NS1 = NSTETR(J,NT1)
            IF( NS1 .EQ. NS4 .OR. NS1 .EQ. NS5 ) GOTO 10
C           SOMMET DIFFERENT DE NS4 NS5
            DO 5 K=1,L
               IF( NS1 .EQ. NSF(K) ) GOTO 10
 5          CONTINUE
C           UN SOMMET DES ARETES NON COMMUNES
            L = L + 1
            NSF( L ) = NS1
 10      CONTINUE
 20   CONTINUE
C
C     REMISE EN VOLUME POSITIF DU TETRAEDRE NSF - NS4
      CALL QUATET( XYZSOM(1,NSF(1)), XYZSOM(1,NSF(2)),
     %             XYZSOM(1,NSF(3)), XYZSOM(1,NS4),
     %             ARMIN, ARMAX, SURFTR, V, Q2 )
      IF( V .LT. 0 ) THEN
         K      = NSF(2)
         NSF(2) = NSF(3)
         NSF(3) = K
      ENDIF
C
C     QUALITE DE NOTEDS
      Q1 = MIN( QUALIT(NOTEDS(1)), QUALIT(NOTEDS(2)), QUALIT(NOTEDS(3)))
      V1 = ABS( VOLUMT( NOTEDS(1) ) )
     %   + ABS( VOLUMT( NOTEDS(2) ) )
     %   + ABS( VOLUMT( NOTEDS(3) ) )
C
C     QUALITE ET VOLUME DES TETRAEDRES 1234 ET 1325
      CALL QUATET( XYZSOM(1,NSF(1)), XYZSOM(1,NSF(2)),
     %             XYZSOM(1,NSF(3)), XYZSOM(1,NS4),
     %             ARMIN, ARMAX, SURFTR, V2, Q2 )
      CALL QUATET( XYZSOM(1,NSF(1)), XYZSOM(1,NSF(3)),
     %             XYZSOM(1,NSF(2)), XYZSOM(1,NS5),
     %             ARMIN, ARMAX, SURFTR, V, Q3 )
C
C     COMPARAISON DES VOLUMES
      V2 = ABS(V2) + ABS(V)
      IF( ABS( V1 - V2 ) .LE. 1E-4*V1 ) THEN
C
C        VOLUMES CONSIDERES EGAUX. COMPARAISON DES QUALITES
         IF( MIN(Q2,Q3) .GT. Q1 ) THEN
C
C           QUALITE AMELIOREE PAR 3T =>2T
            DO 50 I=1,NBTEDS
C              SUPPRESSION DU TETRAEDRE NT1
               NT1 = NOTEDS(I)
               CALL DS1TET( NT1,    NBSOTE, N1TEVI, NSTETR,
     %                      NO1TSO, N1TESO, NOTESO )
               QUALIT( NT1 ) = GRAND
               IF( NBDM .GT. 0 ) NUDMEF( NT1 ) = 0
 50         CONTINUE
C
C           CREATION DES TETRAEDRES 5231 3241
            CALL Q05S2T( NS5, NS4, NSF(1), NSF(2), NSF(3),NUMATE,NUDMEF,
     %                   XYZSOM, NBSOTE, N1TEVI, NSTETR,
     %                   NO1TSO, N1TESO, NOTESO,
     %                   MXTETA, VOLUMT, QUALIT, IERR )
            IF( IERR .EQ. 0 ) IERR = -1
         ENDIF
      ENDIF

      RETURN
      END
