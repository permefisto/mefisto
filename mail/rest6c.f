      SUBROUTINE REST6C( NBSOM,  NB6CUB, NSEF6C,
     %                   L16CSU, MX6CSU, LE6CSU, NONUST, NANUST, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RENUMEROTER LES SOMMETS DES 6CUBES POUR REDUIRE LA DEMI
C -----    LARGEUR DE BANDE DU MAILLAGE
C
C ENTREES:
C --------
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C NB6CUB : NOMBRE DE SOUS 6CUBES
C NSEF6C : NUMERO DES 64 SOMMETS DES NB6CUB SOUS 6CUBES
C L16CSU : NUMERO DU PREMIER 6CUBE DU CHAINAGE DES 6CUBES DE CHAQUE SOMMET
C MX6CSU : MAXIMUM DU SECOND INDICE POUR STOCKER LE NO DE 6CUBE SUIVANT
C          MX6CSU = 64 * NB6CUB
C LE6CSU : LE6CSU(1,N) NUMERO DU 6CUBE CONTENANT LE SOMMET
C          LE6CSU(2,N) NUMERO DU 6CUBE SUIVANT DU CHAINAGE DES 6CUBES D'UN SOMME
C                      0 POUR INDIQUER L'ABSENCE DE SUIVANT
C
C SORTIES:
C --------
C NONUST : NOUVEAU NUMERO DE CHACUN DES NBSOM SOMMETS
C NANUST : ANCIEN  NUMERO DE CHACUN DES NBSOM SOMMETS
C IERR   : >0 SI RENUMEROTATION INCORRECTE, 0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Fevrier 2009
C23456................................................................72
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      INTEGER  NSEF6C( 64, NB6CUB )
      INTEGER  L16CSU( NBSOM ),
     %         LE6CSU( 2, MX6CSU ),
     %         NONUST( NBSOM ),
     %         NANUST( NBSOM )
C
C     MISE A ZERO DE LA NOUVELLE NUMEROTATION ET DU PREMIER 6CUBE DU CHAINAGE
      DO 10 NS=1,NBSOM
         NONUST( NS ) = 0
         L16CSU( NS ) = 0
 10   CONTINUE
C
C     INVERSION DU TABLEAU NSEF6C EN TERMES DE CHAINAGES
      NUCHAI = 0
      DO 30 NE=1,NB6CUB
         DO 20 NS=1,64
C
C           NUMERO DU SOMMET NS DU 6CUBE NE
            NST = NSEF6C( NS, NE )
C
C           AJOUT DANS LE CHAINAGE DU 6CUBE NE CONTENANT LE SOMMET NST
            NUCHAI = NUCHAI + 1
C           NUMERO DU 6CUBE A CHAINER
            LE6CSU( 1, NUCHAI ) = NE
C
C           NUMERO DU 6CUBE SUIVANT DANS LE6CSU
            LE6CSU( 2, NUCHAI ) =  L16CSU( NST )
C           LE PREMIER CHAINAGE SUR LES 6CUBES CONTENANT LE SOMMET NST
            L16CSU( NST ) = NUCHAI
C
 20      CONTINUE
 30   CONTINUE
C
C     PARCOURS POUR CHAQUE SOMMET DES 6CUBES LE CONTENANT ET RENUMERATION
C     DE SES SOMMETS SI CE N'EST PAS DEJA FAIT
C     LE DERNIER SOMMET EST SUR LA FRONTIERE D'OU L'ORDRE INVERSE
C     LE NUMERO ANCIEN D'UN SOMMET RENUMEROTE EST STOCKE POUR CONSTITUER
C     LE FRONT DE RENUMEROTATION DES SOMMETS
C     LE PREMIER SOMMET RENUMEROTE EST L'ACTUEL DERNIER
      NEWST = 1
      NONUST( NBSOM ) = 1
      NANUST(   1   ) = NBSOM
C
C     BOUCLE SUR LES SOMMETS RENUMEROTES
      DO 60 NST = 1, NBSOM
C
C        ANCIEN NUMERO DU SOMMET RENUMEROTE NST
         NAST = NANUST( NST )
C
C        PARCOURS DE SES 6CUBES CHAINES A PARTIR DU PREMIER
         N6CSU = L16CSU( NAST )
C
 40      IF( N6CSU .GT. 0 ) THEN
C
C           PARCOURS DES SOMMETS NON RENUMEROTES DE CE 6CUBE NE
            NE = LE6CSU( 1, N6CSU )
C
            DO 50 NS=1,64
C              LE NUMERO DU SOMMET NS DU 6CUBE NE
               NAST6C = NSEF6C( NS, NE )
C              LE NOUVEAU NUMERO DU SOMMET?
               NUST6C = NONUST( NAST6C )
               IF( NUST6C .EQ. 0 ) THEN
C                 SOMMET NON RENUMEROTE: SON NOUVEAU NUMERO
                  NEWST = NEWST + 1
                  NONUST( NAST6C ) = NEWST
                  NANUST( NEWST  ) = NAST6C
               ENDIF
 50         CONTINUE
C
C           PASSAGE AU 6CUBE CHAINE SUIVANT DU SOMMET NAST
            N6CSU = LE6CSU( 2, N6CSU )
            GOTO 40
C
         ENDIF
C
 60   CONTINUE
C
ccc      WRITE(IMPRIM,10060) (NST,NONUST(NST),NST=1,NBSOM)
ccc10060 FORMAT(5('  NONUST(',I7,')=',I7))
C
C     VERIFICATION DE LA BIJECTIVITE
      DO 70 NST=1, NBSOM
         IF( NONUST(NST) .LE. 0 ) THEN
            WRITE(IMPRIM,*) 'ERREUR: NONUST(',NST,')=',NONUST(NST)
            IERR = IERR + 1
         ENDIF
 70   CONTINUE
C
      RETURN
      END
