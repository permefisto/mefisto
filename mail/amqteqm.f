      SUBROUTINE AMQTEQM( ARETGR, QTEAME, NOSOM1, NOSOM2,
     %                    MXSOMM, PTXYZD, NPSOFR,
     %                    MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                    MXTENS, NOTENS, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    AMELIORATION DE LA QUALITE DES TETRAEDRES NOTETR
C -----    DE QUALITE <= QTEAME ET DE SOMMET NOSOM1 A NOSOM2

C ENTREES:
C --------
C ARETGR : LONGUEUR DE L'ARETE DE LA GRILLE REGULIERE
C QTEAME : QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C          AMELIORATION DES TETRAEDRES EST DEMANDEE
C NOSOM1 : NUMERO PTXYZD DU PREMIER POINT DE TETRAEDRES A AMELIORER LA QUALITE
C NOSOM2 : NUMERO PTXYZD DU DERNIER POINT DE TETRAEDRES A AMELIORER LA QUALITE
C MXSOMM : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : NUMERO DE POSITION PAR RAPPORT A LA FRONTIERE DE CHAQUE POINT
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C MXTENS : NOMBRE MAXIMAL DE TETRAEDRES DECLARABLES DANS NOTENS

C MODIFIES:
C ---------
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C N1TETS : N1TETS(NST) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NST
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: NUMERO NOTETR DU DERNIER TETRAEDRE ACTIF DANS NOTETR

C SORTIES :
C ---------
C NOTENS : NUMERO DES TETRAEDRES DE NOTETR CONTENANT AU MOINS
C          1 DES 4 SOMMETS DU TETRAEDRE PLAT
C IERR   : = 0 PAS D'ERREUR
C          = 1 SATURATION DU TABLEAU NOTETR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY  Fevrier 2017
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,MXSOMM), CENTRT(4), VTE, VOLMOY
      INTEGER           NOTETR(8,MXTETR), N1TETS(MXSOMM),NPSOFR(MXSOMM),
     %                  NOTENS(MXTENS),
     %                  NVOLTE(1), LEFACO(1)

C     INFACO = 0 PAS DE TABLEAU LEFACO NI DE CONSERVATION DES FACES FRONTIERE
      INFACO = 0
      MXFACO = 1

C     IVOLTE = 0 PAS DE PRESENCE DU TABLEAU NVOLTE DU NO VOLUME DES TETRAEDRES
      IVOLTE = 0

      IERR     = 0
      VOLMOY   = ARETGR * ARETGR * ARETGR / 6D0
      MODIFTET = 0

      DO 100 NST = NOSOM1, NOSOM2

         NTE = N1TETS( NST )
         IF( NTE .EQ. 0 ) GOTO 100
         IF( NOTETR(1,NTE) .EQ. 0 ) GOTO 100

C        LISTE DES TETRAEDRES DE SOMMET NST
         CALL TETR1S(NST, N1TETS, NOTETR, NBTENS, MXTENS, NOTENS, IERR)
         IF( IERR .NE. 0 ) GOTO 100

         DO N = 1, NBTENS

C           UN TETRAEDRE DE SOMMET NST
            NTE = NOTENS( N )

            IF( NOTETR(1,NTE) .LE. 0 ) GOTO 100

C           SON VOLUME ET SA QUALITE
            CALL CEBOQU( 0., NOTETR(1,NTE), PTXYZD, CENTRT, VTE, QTE )

            IF( QTE .LE. QTEAME ) THEN

C              AMELIORER LA QUALITE DE CE TETRAEDRE NTE
               CALL A1TEQM( QTEAME, NTE,    MXSOMM, PTXYZD, NPSOFR,
     %                      INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %                      MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                      MODIFT, IERR )

C              S'IL Y A EU MODIFICATION DES TETRAEDRES ALORS MODIFTET+1
C              NECESSAIRE POUR TERMINER LES ITERATIONS SUR amqteqm.f
               MODIFTET = MODIFTET + MODIFT

            ENDIF

         ENDDO

 100  ENDDO

      RETURN
      END
