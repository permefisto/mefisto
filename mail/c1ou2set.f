      SUBROUTINE C1OU2SET( PTXYZD, NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                     NBCF,   N1ARCF, NOARCF,
     %                     NBTECF, NOTECF, NOTETR,
     %                     MXFETO, N1FEVI, NFETOI,
     %                     MXPILE, LAPILE,
     %                     N1FEOC, N2FEOC, NFTRAV,NCTRAV, NS1ACF,NS2ACF)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  L'ETOILE NFETOI DE FACES SIMPLES EST ELLE DECOUPABLE EN
C -----  2/2 ETOILES DE PREMIERE FACE N1FEOC et N2FEOC?

C ENTREES:
C --------
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NBTRCF : NOMBRE DE FACES DE NOTRCF
C NOTRCF : >0 NUMERO DANS LEFACO DES TRIANGLES PERDUS  DU CF
C          <0 NUMERO DANS NO0FAR DES TRIANGLES AJOUTES AU CF
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C NO0FAR : NUMERO DES 3 SOMMETS DES FACES AJOUTEES AU CF

C NBCF   : NOMBRE DE LIGNES FERMEES PERIPHERIQUES DES FACES PERDUES
C N1ARCF : POINTE SUR LE DEBUT DES ARETES DE CHAQUE LIGNE FERMEE DU CF
C          0 POUR LES PLACES VIDES
C NOARCF : 1:NUMERO DU SOMMET DE L'ARETE DE LA LIGNE DU CONTOUR FERME
C          2:NUMERO DANS NOARCF DE L'ARETE SUIVANTE DU CF
C          3:NUMERO DANS LEFACO DU TRIANGLE ADJACENT OPPOSE A L'ARETE

C NBTECF : NOMBRE DE TETRAEDRES DE L'ETOILE
C NOTECF : NUMERO NOTETR DES NBTECF TETRAEDRES DE L'ETOILE
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE

C MXFETO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LE CHAINAGE NFETOI
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE NFETOI
C NFETOI : VERSION1  FACES SIMPLES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR AYANT CETTE FACE
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DE CETTE FACE DANS LEFACO, 0 SI PAS DANS LEFACO
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C          ou VERSION2   PAS DANS CE SOUS-PROGRAMME
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST DIRIGE VERS L'INTERIEUR DE L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C MXPILE : NOMBRE MAXIMAL DE FACES DANS LA PILE
C LAPILE : NUMERO NFETOI DE LA FACE
C          NUMERO DE L'ARETE 1:1-2, 2:2-3 3:3-1 DE TRAVERSEE DU CF
C N1FEOC : NUMERO NFETOI DE LA PREMIERE FACE SIMPLE DE L'ETOILE A DECOUPER

C SORTIES:
C --------
C N2FEOC : =0 UNE SEULE ETOILE. PAS DE SECONDE DEMI-ETOILE
C          >0 NUMERO NFETOI DE LA PREMIERE FACE SIMPLE DE LA SECONDE/2  ETOILE
C N1FEOC : >0 NUMERO NFETOI DE LA PREMIERE FACE SIMPLE DE LA PREMIERE/2 ETOILE
C             OU DE LA SEULE ETOILE
C NFTRAV : =0 UNE ARETE D'UN TRIANGLE DU CF INTERSECTE UNE FACE SIMPLE DE L'ETOILE
C             ou AUCUNE ARETE DE DEPART DU CF COMME ARETE D'UNE FACE
C             SIMPLE DE L'ETOILE N1FEOC (0 ARETE DU CF DANS L'ETOILE)
C          >0 NUMERO DE LA FACE NFETOI D'ARETE NCTRAV DU CF
C NCTRAV : =0 AUCUNE ARETE DE DEPART DU CF
C          >0 NUMERO DU COTE DE LA FACE NFETOI DE TRAVERSEE DU CF
C NS1ACF : =0 AUCUNE ARETE DE DEPART DU CF
C          >0 NUMERO PTXYZD DU 1-ER SOMMET DE L'ARETE DE TRAVERSEE DU CF
C NS2ACF : =0 AUCUNE ARETE DE DEPART DU CF
C          >0 NUMERO PTXYZD DU 2-ND SOMMET DE L'ARETE DE TRAVERSEE DU CF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  St PIERRE du PERRAY             Janvier 2018
C MODIFS : ALAIN PERRONNET  St PIERRE du PERRAY            Novembre 2019
C2345X7..............................................................012
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE, TRACTE0
      CHARACTER*80      KTITRE
      DOUBLE PRECISION  PTXYZD(4,*), XYZ(3),CBTR(3)
      INTEGER           NOTRCF(NBTRCF), NOTETR(8,*), LAPILE(2,MXPILE),
     %                  LEFACO(11,0:*), NO0FAR(3,*),
     %                  N1ARCF(0:*), NOARCF(1:3,1:*), NFETOI(5,MXFETO)
      INTEGER           NOSOTR(3)
      INTEGER           NOSOFATE(3,4)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

      TRACTE0 = TRACTE
      NFTRAV = 0
      NCTRAV = 0

C     PREMIERE FACE DE LA SECONDE DEMI-ETOILE
      N2FEOC = 0
      NS1ACF = 0
      NS2ACF = 0

      IF( NBTECF .LE. 0 ) THEN
C        AUCUN TETRAEDRE DANS L'ETOILE
        PRINT*,'c1ou2set: AUCUN TETRAEDRE dans l''ETOILE NBTECF=',NBTECF
         GOTO 9999
      ENDIF

C     CONSTRUCTION NFETOI VERSION 1 DES TRIANGLES FACES SIMPLES DES
C     TETRAEDRES ENVELOPPANTS. LES FACES VUES 2 FOIS SONT ELIMINEES
C     -------------------------------------------------------------
      CALL CRFETOI1( NBTECF, NOTECF, NOTETR,
     %               MXFETO, N1FEOC, N1FEVI, NFETOI )

C     NOMBRE DE FACES N'APPARTENANT PAS A 2 FACES SIMPLES DE L'ETOILE
      KTITRE = 'c1ou2set:        TETRAEDRES DECOUPABLES en 2/2 ETOILES?'
      WRITE(KTITRE(11:16),'(I6)') NBTECF
      CALL SANSDBL( KTITRE, L )
      CALL TRFETO4( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %              NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %              NBTECF, NOTECF, NOTETR )

C     UNE ARETE DES NBTRCF TRIANGLES DU CF INTERSECTE T ELLE UNE FACE
C     SIMPLE DES NBTECF TETRAEDRES NOTECF?
C     ---------------------------------------------------------------
      NFS = N1FEOC
 10   IF( NFS .GT. 0 ) THEN

C        NUMERO NOTETR DU TETRAEDRE DE LA FACE SIMPLE
         NTE = NFETOI(1,NFS)

C        NUMERO DE LA FACE SIMPLE DANS LE TETRAEDRE NTE
         NFTE = ABS( NFETOI(2,NFS) )

C        LES 3 SOMMETS DE LA FACE SIMPLE NFS 
C        AVEC UNE NORMALE VERS L'INTERIEUR DE L'ETOILE
         NOSOTR(1) = NOTETR( NOSOFATE(1,NFTE), NTE )
         NOSOTR(2) = NOTETR( NOSOFATE(3,NFTE), NTE )
         NOSOTR(3) = NOTETR( NOSOFATE(2,NFTE), NTE )

C        CETTE FACE SIMPLE EST ELLE INTERSECTEE PAR UNE ARETE
C        DES NBTRCF FACES PERDUES? Si OUI, L'ETOILE EST INCORRECTE
         DO N=1,NBTRCF

C           LA FACE PERDUE N
            NTR = NOTRCF( N )

C           LE NO DES 2 SOMMETS D'UNE ARETE DE NTR
            IF( NTR .GT. 0 ) THEN
               NS1 = LEFACO(3, NTR)
            ELSE
               NS1 = NO0FAR(3,-NTR)
            ENDIF

            DO K=1,3
               IF( NTR .GT. 0 ) THEN
                  NS2 = LEFACO(K, NTR)
               ELSE
                  NS2 = NO0FAR(K,-NTR)
               ENDIF

C              LA FACE NOSOTR SIMPLE DE L'ETOILE EST ELLE INTERSECTEE
C              PAR L'ARETE NS1-NS2 DE LA FACE PERDUE NTR?
               CALL INARTR( PTXYZD( 1, NS1 ), PTXYZD( 1, NS2 ),
     %                      PTXYZD( 1, NOSOTR(1) ),
     %                      PTXYZD( 1, NOSOTR(2) ),
     %                      PTXYZD( 1, NOSOTR(3) ),
     %                      LINTER, XYZ, CBTR )
C              LINTER : -2 SI S1=S2
C                       -1 SI S1-S2 PARALLELE AU PLAN DU TRIANGLE
C                        0 SI S1-S2 N'INTERSECTE PAS LE TRIANGLE
C                                   ENTRE CES 3 SOMMETS
C                        1 SI S1-S2   INTERSECTE     LE TRIANGLE
C                                   ENTRE CES 3 SOMMETS
C              XYZ : 3 COORDONNEES DU POINT D'INTERSECTION SI LINTER=1
C              CBTR: 3 COORDONNEES BARYCENTRIQUES DE PT DANS LE TRIANGLE
               IF(  LINTER .EQ. 1
     %             .AND. CBTR(1) .LT. 0.999999D0
     %             .AND. CBTR(2) .LT. 0.999999D0
     %             .AND. CBTR(3) .LT. 0.999999D0 ) THEN

C                 OUI: UN POINT D'INTERSECTION XYZ INTERIEUR A NTR
C                      => L'ETOILE EST INCORRECTE
                  PRINT*,'c1ou2set: l''ARETE',NS1,NS2,
     %                   ' du TRIANGLE PERDU',NTR,
     %                   ' INTERSECTE la FACE SIMPLE',NOSOTR,
     %                   ' de l''ETOILE des',NBTECF,' TETRAEDRES'
                  PRINT*,'c1ou2set: CBTR=',CBTR

ccc                  tracte = .true.
      KTITRE='c1ou2set: PB ARETE       -        INTERSECTE le TRIANGLE     
     %                      '
                  WRITE(KTITRE(20:25),'(I6)') NS1
                  WRITE(KTITRE(28:33),'(I6)') NS2
                  WRITE(KTITRE(58:63),'(I6)') NOSOTR(1)
                  WRITE(KTITRE(65:70),'(I6)') NOSOTR(2)
                  WRITE(KTITRE(72:77),'(I6)') NOSOTR(3)
                  CALL SANSDBL( KTITRE, L )
                  CALL TRFETO4( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %                          NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                          NBTECF, NOTECF, NOTETR )

ccc                  NFTRAV = 0  deja initialise a zero
ccc                  NCTRAV = 0
ccc                  N2FEOC = 0

C                 ABANDON DE CETTE ETOILE
                  GOTO 9999
               ENDIF

               NS1 = NS2
            ENDDO
         ENDDO

C        LA FACE SUIVANTE DE L'ETOILE
         NFS = NFETOI(5,NFS)
         GOTO  10

      ENDIF


C     RECHERCHE D'UNE ARETE DE DEPART DE LA PERIPHERIE DU CF
C     COMME ARETE DE L'UN DES TRIANGLES DE L'ETOILE N1FEOC
C     ------------------------------------------------------
ccc      IF( NFTRAV .GT. 0 .AND. NCTRAV .GT. 0 ) THEN
cccC        NCTRAV COTE DE LA FACE NFTRAV DE TRAVERSEE
cccC        ONT ETE DONNES EN PARAMETRES de c1ou2set
ccc         GOTO 20
ccc      ENDIF

C     LA FACE DE TRAVERSEE NFTRAV EST A TROUVER
      NFTRAV = N1FEOC
 15   IF( NFTRAV .GT. 0 ) THEN

C        NUMERO NOTETR DU TETRAEDRE DE LA FACE SIMPLE
         NTE = NFETOI(1,NFTRAV)

C        NUMERO DE LA FACE SIMPLE DANS LE TETRAEDRE NTE
         NFTE = ABS( NFETOI(2,NFTRAV) )

C        LES 3 SOMMETS DE LA FACE SIMPLE NFTRAV 
C        AVEC UNE NORMALE VERS L'INTERIEUR DE L'ETOILE
         NOSOTR(1) = NOTETR( NOSOFATE(1,NFTE), NTE )
         NOSOTR(2) = NOTETR( NOSOFATE(3,NFTE), NTE )
         NOSOTR(3) = NOTETR( NOSOFATE(2,NFTE), NTE )

C        LES 3 ARETES DE LA FACE TRIANGULAIRE SIMPLE DE L'ETOILE
         DO NCTRAV = 1, 3

C           NS1 NS2 SOMMETS DE L'ARETE NCTRAV DE LA FACE NFTE DU TETRAEDRE NTE
            NS1 = NOSOTR(NCTRAV)
            IF( NCTRAV .EQ. 3 ) THEN
               NS2 = 1
            ELSE
               NS2 = NCTRAV + 1
            ENDIF
            NS2 = NOSOTR(NS2)

C           NS1-NS2 EST ELLE UNE ARETE DU CF?
            CALL ARARCF( NS1, NS2, NBCF, N1ARCF, NOARCF,
     %                   NLFCF, NAVANT, NA1 )
            IF( NA1 .NE. 0 ) THEN
C              OUI: L'ARETE NA1 DE NOARCF EST L'ARETE NCTRAV DE LA FACE NFTE
C                   DU TETRAEDRE NTE
               NS1ACF = NS1
               NS2ACF = NS2
               GOTO 20
            ENDIF

         ENDDO

C        LA FACE SUIVANTE DE L'ETOILE
         NFTRAV = NFETOI(5,NFTRAV)
         GOTO  15

      ENDIF

C     PROBLEME: AUCUNE ARETE DE DEPART DU CF COMME ARETE D'UNE FACE
C     SIMPLE DE L'ETOILE N1FEOC
      PRINT*,'c1ou2set: PAS d''ARETE DE DEPART DU CF COMME ARETE DE L''U
     %N DES TRIANGLES DE L''ETOILE N1FEOC'
      PRINT*,'c1ou2set: TOUTES LES ARETES DU CF APPARTIENNENT A 0 FACES
     % SIMPLES de l''ETOILE'
      GOTO 9999


C     L'ARETE NCTRAV DE LA FACE NFTRAV DE NFETOI CONTIENT L'ARETE NA1 DU CF
C     DEBUT DE CONSTRUCTION DE LA SECONDE DEMI-ETOILE DE DEBUT N2FEOC
C     EN RETIRANT (CHAINANT) DE L'ETOILE1 TOUS LES TRIANGLES DE
C     L'ETOILE LIES PAR UNE ARETE ADJACENTE A CETTE FACE NFTRAV
C     ---------------------------------------------------------------------
ccc 20   PRINT*,'c1ou2set: Recherche 2 demi etoiles a partir de la face NFT
ccc     %RAV=',NFTRAV,' NCTRAV=',NCTRAV
C     LA PILE DES ARETES DES FACES DE L'ETOILE A TRAITER
 20   LHPILE = 1
C     LA FACE NFETOI INITIALE
      LAPILE( 1, 1 ) = NFTRAV
C     LE NUMERO DE L'ARETE 1:1-2, 2:2-3 3:3-1 DE TRAVERSEE
      LAPILE( 2, 1 ) = NCTRAV

ccc      CALL AFNFETOI( N1FEOC, NFETOI, NOTETR )

C     PARCOURS DES FACES ADJACENTES A LA FACE NF2 SANS TRAVERSER LE CF
C     TANT QUE LA PILE DES FACES EST NON VIDE, TRAITER LE HAUT DE PILE
 30   IF( LHPILE .GT. 0 ) THEN

C        LA FACE NFETOI A TRAITER
         NF2 = LAPILE( 1, LHPILE )
C        LE NUMERO DE L'ARETE 1:1-2, 2:2-3 3:3-1 DE TRAVERSEE
         NC2 = LAPILE( 2, LHPILE )

C        LA FACE EST DEPILEE
         LHPILE = LHPILE - 1

C        NF2 EST RETIREE DU CHAINAGE DE L'ETOILE1 NFETOI
C        -----------------------------------------------
         IF( NF2 .NE. N1FEOC ) THEN

C           LA FACE NF2 A RETIRER DE L'ETOILE N1FEOC N'EST PAS LA PREMIERE
C           RECHERCHE DE LA FACE PRECEDENTE NF0 DE NF2 DANS ETOILE N1FEOC
            NF0 = 0
            NF1 = N1FEOC
 40         IF( NF1 .NE. NF2 ) THEN
C              LA FACE SUIVANTE DE L'ETOILE
               NF0 = NF1
               NF1 = NFETOI(5,NF1)
               IF( NF1 .GT. 0 ) THEN
C                 IL EXISTE UNE FACE NF1
                  GOTO  40
               ELSE
C                 NF2 N'EST PAS UNE FACE DE ETOILE1
                  GOTO  30
               ENDIF
            ENDIF
c           LE SUIVANT DE NF0 EST MAINTENANT LE SUIVANT DE NF2
            NFETOI(5,NF0) = NFETOI(5,NF2)

         ELSE

C           LA FACE NF2 EST LA PREMIERE DE L'ETOILE N1FEOC
            N1FEOC = NFETOI(5,N1FEOC)

            IF( N1FEOC .LE. 0 ) THEN

C              -------------------------------------------------------
C              UNE SEULE DEMI-ETOILE :
C              NF2 EST PREMIERE ET DERNIERE DE L'ETOILE RESTANTE
C              LES FACES DE L'ETOILE ONT TOUTES ETE RETROUVEES
C              DANS LA PREMIERE DEMI-ETOILE
C              (IL EXISTE UNE ARETE DU CF NON ARETE DES FACES SIMPLES)
C              -------------------------------------------------------
C              FACE INITIALE DE L'ETOILE
               N1FEOC = N2FEOC
               NFETOI(5,NF2) = N1FEOC
               N1FEOC = NF2
C              FACE INITIALE DE LA SECONDE DEMI-ETOILE (ICI VIDE)
               N2FEOC = 0

               PRINT*,'c1ou2set: 1 SEULE DEMI-ETOILE',
     %                 NBTECF,' TETRAEDRES N1FEOC=',N1FEOC,
     %                ' NBTRCF=',NBTRCF,' FACES du CF'
ccc               CALL AFNFETOI( N1FEOC, NFETOI, NOTETR ) 
ccc               TRACTE0 = TRACTE
ccc               tracte  = .true.
               KTITRE='       TETRAEDRES FORMENT une SEULE ETOILE'
               WRITE(KTITRE(1:6),'(I6)') NBTECF
               CALL SANSDBL( KTITRE, L )
               CALL TRFETO4( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %                       NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                       NBTECF, NOTECF, NOTETR )

               GOTO 9000

            ENDIF

         ENDIF


C        LA FACE NF2 EST AJOUTEE A LA DEMI-ETOILE2 EN PREMIERE POSITION
C        --------------------------------------------------------------
         NFETOI(5,NF2) = N2FEOC
         N2FEOC = NF2

C        TRAITEMENT DES 2 AUTRES ARETES DE LA FACE SIMPLE NF2
C        QUI NE FAIT PLUS PARTIE DE L'ETOILE INITIALE
C        ----------------------------------------------------
C        NUMERO NOTETR DU TETRAEDRE DE LA FACE SIMPLE
         NTE = NFETOI(1,NF2)
C        NUMERO DE LA FACE DANS LE TETRAEDRE NTE
         NFTE = ABS( NFETOI(2,NF2) )

C        LES 3 SOMMETS DE LA FACE NFTE
         NOSOTR(1) = NOTETR( NOSOFATE(1,NFTE), NTE )
         NOSOTR(2) = NOTETR( NOSOFATE(3,NFTE), NTE )
         NOSOTR(3) = NOTETR( NOSOFATE(2,NFTE), NTE )

         DO 50 NC=1,3

C           LES 2 SOMMETS DE L'ARETE NC DE NF2 A TRAITER
            NS1 = NOSOTR( NC )
            IF( NC .EQ. 3 ) THEN
               NS2 = 1
            ELSE
               NS2 = NC + 1
            ENDIF
            NS2 = NOSOTR( NS2 )

            IF( NC .NE. NC2 ) THEN

C              LES 2 SOMMETS NS1 NS2 DE L'ARETE NC DE NF2 A TRAITER
C              EST ELLE UNE ARETE DU CONTOUR NOARCF DU CF?
C              CF AVEC NOARCF CALCULE
               CALL ARARCF( NS1, NS2, NBCF, N1ARCF, NOARCF,
     %                      NLFCF, NAVANT, NA1 )
               IF( NA1 .NE. 0 ) THEN
C                 OUI: PAS DE TRAVERSEE DE L'ARETE DE L'AUTRE COTE DU CF
C                      LA FACE SIMPLE OPPOSEE N'EST PAS EMPILEE
                  GOTO 50
               ENDIF


ccc               ELSE
cccC                 LE CF INITIAL A ETE COMPLETE POUR ATTEINDRE L'ETOILE
cccC                 DES FACES SIMPLES
ccc                  CALL ARARCF1( NS1,    NS2,    NBTRCF, NOTRCF,
ccc     %                          LEFACO, NO0FAR, NFLEFA, NAR )
ccc                  IF( NFLEFA .NE. 0 ) THEN
cccC                       PAS DE TRAVERSEE DE L'ARETE DE L'AUTRE COTE DU CF
ccc                     GOTO 50
ccc                  ENDIF
ccc               ENDIF


C              RECHERCHE DANS ETOILE1 DE LA FACE SIMPLE NF1 DIFFERENTE
C              DE NF2 OPPOSEE A L'ARETE NC
               CALL ARETOI( NS1, NS2, NOTETR, N1FEOC, NFETOI,
     %                      NF1, NC1 )
               IF( NF1 .LE. 0 ) THEN
C                 LA FACE A DEJA ETE TRAITEE ET MISE DANS ETOILE2
                  GOTO 50
               ENDIF
   
C              CETTE FACE NF1 OPPOSEE A L'ARETE NC DE NF2 EST EMPILEE
               IF( LHPILE .GE. MXPILE ) THEN
                  PRINT *,'c1ou2set: PILE SATUREE. AUGMENTER MXPILE=',
     %                     MXPILE
                  GOTO 50
               ENDIF
               LHPILE = LHPILE + 1
C              LA FACE NFETOI
               LAPILE( 1, LHPILE ) = NF1
C              LE NUMERO DE L'ARETE 1:1-2, 2:2-3 3:3-1 DE TRAVERSEE
               LAPILE( 2, LHPILE ) = NC1

            ENDIF

 50      ENDDO

C        ALLER EN HAUT DE PILE
         GOTO 30

      ENDIF

C     TRACE DES FACES SIMPLES DES DEMI-ETOILES
 9000 IF( N2FEOC .GT. 0 ) THEN
         KTITRE='c1ou2set: Les FACES SIMPLES de la PREMIERE DEMI-ETOILE'
      ELSE
         KTITRE='c1ou2set: Les FACES SIMPLES de la SEULE DEMI-ETOILE'
      ENDIF
      CALL SANSDBL( KTITRE, L )
      CALL TRFETO4( KTITRE(1:L), PTXYZD, N1FEOC, NFETOI,
     %              NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %              NBTECF, NOTECF, NOTETR )
ccc      CALL AFNFETOI( N1FEOC, NFETOI, NOTETR ) 

      IF( N2FEOC .GT. 0 ) THEN
         KTITRE='c1ou2set: Les FACES SIMPLES de la SECONDE DEMI-ETOILE'
         CALL SANSDBL( KTITRE, L )
         CALL TRFETO4( KTITRE(1:L), PTXYZD, N2FEOC, NFETOI,
     %                 NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                 NBTECF, NOTECF, NOTETR )
ccc         CALL AFNFETOI( N2FEOC, NFETOI, NOTETR ) 
      ENDIF

 9999 TRACTE = TRACTE0
      RETURN
      END
