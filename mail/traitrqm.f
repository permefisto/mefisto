      SUBROUTINE TRAITRQM( COSMAXPL, QUALMN, NUSURF, KNMSURF,
     %                     NOFERM0,  MXSOM,  NBSOM,  MNSOTA,
     %                     MXTRIA,   M1TRIA, NBTRIA, MNFATA,
     %                     MXTRAR,   L1ARET, L2ARET, MNARET,
     %                     MXTRQM,   NBTRQM, NOTRQM,
     %                     MXTRPV,   NOTRPV, QTRMOY, QTRMIN, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CONSTRUCTION DU TABLEAU LARETE DES ARETES DU MAILLAGE
C -----    A PARTIR DU TABLEAU MCN(MNTRIA)=NOTRIA(M1TRIA,NBTRIA) et
C          TRAITEMENT DES NBTRQM TRIANGLES DE QUALITE INFERIEURE A QUALMN
C          TRACE DES TRIANGLES NOTRPV DE NOTRIA, LEURS VOISINS et
C          LEURS VOISINS DE VOISINS POUR VOIR LEUR ENVIRONNEMENT

C ENTREES:
C --------
C COSMAXPL:COSINUS DE L'ANGLE DIEDRE ENTRE LES 2 PLANS DES TRIANGLES
C          AU DESSOUS DUQUEL LES 2 TRIANGLES SONT CONSIDERES NON-COPLANAIRES
C QUALMN : QUALITE AU DESSOUS DE LAQUELLE LA QUALITE D'UN TRIANGLE
C          EST MAUVAISE ET EST A AMELIORER
C NUSURF : NUMERO DE LA SURFACE A AMELIORER DANS LE LEXIQUE DES SURFACES
C KNMSURF: NOM    DE LA SURFACE A AMELIORER DANS LE LEXIQUE DES SURFACES
C NOFERM0: CODE DE FERMETURE OU NON DU MAILLAGE DE LA SURFACE
C          =61 INCONNU, =0 NON FERME, =1 FERME

C MXSOM  : NOMBRE MAXIMUM DE SOMMETS DU TABLEAU XYZSOM
C MNSOTA : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE
C NBSOM  : NOMBRE ACTUEL  DE SOMMETS DU TABLEAU XYZSOM
C          MNXYZS = MNSOTA + WYZSOM
C          XYZSOM = RMCN(MNXYZS) X  Y  Z DES NBSOM SOMMETS DU MAILLAGE

C MNFATA : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES TRIANGLES AMELIORES
C          CF '~td/d/a___nsef'
C MXTRIA : NOMBRE MAXIMUM DE TRIANGLES DU TABLEAU NOTRIA
C M1TRIA : NOMBRE DE MOTS PAR TRIANGLE DU TABLEAU NOTRIA
C NBTRIA : NOMBRE ACTUEL  DE TRIANGLES DU TABLEAU NOTRIA
C          MNTRIA = MNFATA + WUSOEF
C NOTRIA : =MCN(MNTRIA) TABLEAU DU NUMERO DES 3 SOMMETS ET 0
C          SANS NUMERO NOTRIA des 3 TRIANGLES ADJACENTS

C MXTRAR : NOMBRE MAXIMAL DE TRIANGLES ADJACENTS A UNE ARETE
C L1ARET : NOMBRE DE MOTS PAR ARETE DU TABLEAU NARET
C L2ARET : NOMBRE DE ARETES DU TABLEAU NARET
C MNARET : ADRESSE MCN DU TABLEAU LARETE DES ARETES DU MAILLAGE NOTRIA
C          TABLEAU DES ARETES DU MAILLAGE
C          LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR ARETE SUIVANTE
C          LARETE(4,I)= NUMERO DU 1-ER TRIANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 1-ER  TRIANGLE
C          LARETE(5,I)= NUMERO DU 2-EME TRIANGLE CONTENANT CETTE ARETE
C                       0 SI PAS DE 2-EME TRIANGLE

C MXTRQM : NOMBRE MAXIMUM DE TRIANGLES DU TABLEAU NOTRQM
C NBTRQM : NOMBRE ACTUEL  DE TRIANGLES DU TABLEAU NOTRQM
C NOTRQM : LISTE DES NBTRQM NUMEROS DANS NOTRIA DES TRIANGLES A TRACER
C          ( 1:NBTRQM ) TRIANGLE DE QUALITE < QUALMN   AUGMENTES de
C          (NBTRQM+1:NBT2) TRIANGLES OPPOSES AJOUTES DANS LE TRACE
C          (NBT2+1:NBT3) TRIANGLES OPPOSES OPPOSES AJOUTES DANS LE TRACE

C MXTRPV : NOMBRE MAXIMUM DE TRIANGLES DU TABLEAU NOTRPV
C NOTRPV : LISTE DES NBTRPV NUMEROS DANS NOTRIA DES TRIANGLES A TRACER
C          ( 1:NBTRPV )    TRIANGLE INITIAL   AUGMENTES de
C          (NBTRPV+1:NBT2) TRIANGLES OPPOSES AJOUTES DANS LE TRACE
C          (NBT2+1:NBT3) TRIANGLES OPPOSES OPPOSES AJOUTES DANS LE TRACE

C SORTIES:
C --------
C QTRMOY : QUALITE MOYENNE DES NBTRQM TRIANGLES
C QTRMIN : QUALITE MINIMUM DES NBTRQM TRIANGLES
C IERR   : =9 SI LA FERMETURE DE LA SURFACE A CHANGE LORS DES CALCULS
C             EFFECTUES AUPARAVANT
C          =3 SI UNE ARETE APPARTIENT A PLUS DE MXTRAR TRIANGLES
C          =4 SI M1TRIA DIFFERENT de NBSOEF c-a-d
C             du NOMBRE DE MOTS PAR TRIANGLE DU TABLEAU NOTRIA
C          =0 SI PAS D'ERREUR DETECTEE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint Pierre du Perray             Mars 2020
C2345X7..............................................................012
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/xyzext.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(MOTMCN)
      EQUIVALENCE      (RMCN(1),MCN(1))

      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE, TRACTE0

      CHARACTER*24      KNMSURF
      CHARACTER*96      KTITRE
      INTEGER           NOTRQM(MXTRQM), NOTRPV(MXTRPV)

      IERR    = 0
      TRACTE0 = TRACTE

C     MISE A JOUR du TMS xyzsom et nsef de la SURFACE NUSURF
C     ------------------------------------------------------
      MNXYZS = MNSOTA + WYZSOM

      NBSOEF = MCN( MNFATA + WBSOEF ) 
      IF( M1TRIA .NE. NBSOEF ) THEN
         PRINT*,'traitrqm: M1TRIA=',M1TRIA,' NBSOEF=',NBSOEF,
     %          ' DEVRAIENT ETRE EGAUX. A CORRIGER...'
         IERR = 4
         GOTO 9999
      ENDIF 

      MNTRIA = MNFATA + WUSOEF
C     MISE A JOUR DU NOMBRE DE TRIANGLES DE LA TRIANGULATION
      NBSOM0 = NBSOM
      MCN( MNSOTA + WNBSOM ) = NBSOM
C     MISE A JOUR DU NOMBRE DE TRIANGLES DE LA TRIANGULATION
      NBTRIA0 = NBTRIA
      MCN( MNFATA + WBEFOB ) = NBTRIA
C     FERMETURE IMPOSEE INCONNUE EN ENTREE POUR LA RECALCULER
      MCN( MNFATA + WUTFMA ) = -1

C     La TRIANGULATION de la SURFACE NUSURF EST ELLE FERMEE?
C     ------------------------------------------------------
      LORBITE = 0
      CALL OBJFER( 3, NUSURF, 1, NOFERM1 )
C     NOFERM1: =1 SI L'OBJET EST FERME
C              =0 SI L'OBJET N'EST PAS FERME
C              <0 SI SATURATION DU TABLEAU SERVANT AU HACHAGE
C                 SI LIGNE OU SURFACE ALORS NUTFMA DU TMS 'NSEF'
C                 EST MIS A JOUR
      LORBITE = 1

      IF( NOFERM0 .EQ. 1 .AND. NOFERM1 .NE. 1 ) THEN
         PRINT*,' PROBLEME CHANGEMENT de FERMETURE de la TRIANGULATION d
     %e la SURFACE ',KNMSURF
         TRACTE = .TRUE.
         CALL TRAFAC( KNMSURF, NUSURF, MNFATA, MNSOTA )
         IERR   = 9
         QTRMIN = 0
         QTRMOY = 0
         GOTO 9999
      ENDIF

C     LISTE DES TRIANGLES DE QUALITE < QUALMN
C     ---------------------------------------
      KTITRE='traitrqm 1:          TRIANGLES de QUALITE<               '
      WRITE(KTITRE(43:52),'(F10.7)') QUALMN
      CALL LISTTRQM( QUALMN, M1TRIA, NBTRIA, MCN(MNTRIA),
     %               MXTRQM, NBTRQM, NOTRQM,
     %               NBSOM,  RMCN(MNXYZS), QTRMOY, QTRMIN )

C     TRAITEMENT DES NBTRQM TRIANGLES DE QUALITE < QUALMN
C     ---------------------------------------------------
      IF( NBTRQM .GT. 0 ) THEN

C        RE-CONSTRUCTION DU TABLEAU LARETE DES ARETES DU MAILLAGE
C        ET TRACE DES NBTRQM TRIANGLES NOTRQM
         CALL TRTRIAR( 'traitrqm 1', RMCN(MNXYZS),
     %                  MXTRAR, L1ARET, L2ARET, MNARET,
     %                  NBTRIA, MCN(MNTRIA),
     %                  MXTRQM, NBTRQM, NBTRQMV, NBTRQMVV, NOTRQM )

C        TRAITEMENT DES TRIANGLES DE QUALITE INFERIEURE A QUALMN
         CALL MODGTRQM( COSMAXPL, QUALMN, MXSOM,  NBSOM, RMCN(MNXYZS),
     %                  MXTRAR,   L1ARET, L2ARET, MNARET,
     %                  M1TRIA,   MXTRIA, NBTRIA, MCN(MNTRIA),
     %                  MXTRQM,   NBTRQM, NOTRQM,
     %                  MXTRPV,   NOTRPV, NBRATR )

C        TRACE DES TRIANGLES DE QUALITE < QUALMN
         KTITRE='traitrqm 2:          TRIANGLES de QUALITE<            '
         WRITE(KTITRE(43:52),'(F10.7)') QUALMN
         CALL LISTTRQM( QUALMN, M1TRIA, NBTRIA, MCN(MNTRIA),
     %                  MXTRQM, NBTRQM, NOTRQM,
     %                  NBSOM,  RMCN(MNXYZS), QTRMOY, QTRMIN )

C        RE-CONSTRUCTION DU TABLEAU LARETE DES ARETES DU MAILLAGE
C        ET TRACE DES NBTRQM TRIANGLES NOTRQM
         CALL TRTRIAR( 'traitrqm 2', RMCN(MNXYZS),
     %                  MXTRAR, L1ARET, L2ARET, MNARET,
     %                  NBTRIA, MCN(MNTRIA),
     %                  MXTRQM, NBTRQM, NBTRQMV, NBTRQMVV, NOTRQM )

      ENDIF

      IF( NBSOM0 .NE. NBSOM .OR. NBTRIA0 .NE. NBTRIA ) THEN

C        MISE A JOUR du TMS xyzsom de la SURFACE NUSURF
C        MISE A JOUR DU NOMBRE DE SOMMETS DE LA TRIANGULATION
         MCN( MNSOTA + WNBSOM ) = NBSOM

C        MISE A JOUR du TMS nsef de la SURFACE NUSURF
C        MISE A JOUR DU NOMBRE DE TRIANGLES DE LA TRIANGULATION
         MCN( MNFATA + WBEFOB ) = NBTRIA
C        FERMETURE IMPOSEE INCONNUE EN ENTREE POUR LA RECALCULER
         MCN( MNFATA + WUTFMA ) = -1

C        La TRIANGULATION de la SURFACE NUSURF EST ELLE FERMEE?
         LORBITE = 0
         CALL OBJFER( 3, NUSURF, 1, NOFERM1 )
C        NOFERM1: =1 SI L'OBJET EST FERME
C                 =0 SI L'OBJET N'EST PAS FERME
C                 <0 SI SATURATION DU TABLEAU SERVANT AU HACHAGE
C                    SI LIGNE OU SURFACE ALORS NUTFMA DU TMS 'NSEF'
C                 EST MIS A JOUR
         LORBITE = 1
         IF( NOFERM0 .EQ. 1 .AND. NOFERM1 .NE. 1 ) THEN
            PRINT*,' PROBLEME CHANGEMENT de FERMETURE de la TRIANGULATIO
     %N de la SURFACE ',KNMSURF
            TRACTE = .TRUE.
            CALL TRAFAC( KNMSURF, NUSURF, MNFATA, MNSOTA )
            IERR   = 9
            QTRMIN = 0
            QTRMOY = 0
         ENDIF

      ENDIF

 9999 TRACTE = TRACTE0
      RETURN
      END
