      SUBROUTINE TRAJGR1( XYSGR,  NTR1,   NTR2,
     %                    ARETMX, NOFOTI, NUTYSU, RAP2P3,
     %                    ORIGIN, D2D3,   XMIN,   YMIN,   XYMXMI,
     %                    MXSOMM, NBSOMM, PXYD,
     %                    MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                    MOARTR, MXARTR, N1ARTR, NOARTR, NOARST,
     %                    IERR  )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRIANGULER UN SOMMET XYSGR D'UNE GRILLE DE TRIANGLES EQUILATERAUX
C -----    DE COTE EN FONCTION DE LA TAILLE SOUHAITEE DES ARETES
C          ET MISE EN DELAUNAY DES TRIANGLES
C
C ENTREES:
C --------
C XYSGR  : X Y DU SOMMET A AJOUTER S'IL APPARTIENT A UN TRIANGLE
C ARETMX : LONGUEUR MAXIMALE DES ARETES DES TRIANGLES
C NOFOTI : NO DANS LE LX DES FONCTIONS DE LA FONCTION TAILLE_IDEALE(X,Y,Z)
C          >0 SI ELLE EXISTE ET 0 SINON
C NUTYSU : NUMERO D'OPTION DE GENERATION DU MAILLAGE DE CETTE SURFACE
C          19 APPEL DIRECT A PARTIR DU SP SUEX19
C          1 APPEL PAR LE SP SUEX01-QUNSAL (QUADRANGLE ALGEBRIQUE)
C          3 APPEL PAR LE SP SUEX03        (B-SPLINE POLYNOMIALE )
C          6 APPEL PAR LE SP SUEX06-TRNSAL (TRIANGLE   ALGEBRIQUE)
C RAP2P3 : RAPPORT DU PERIMETRE DE L'ENVELOPPE DU DOMAINE PLAN SUR
C                  LE PERIMETRE DE L'ENVELOPPE DU DOMAINE DE R**3
C          APPROXIMATION POUR CALCULER RAPIDEMENT LA TAILLE DE L'ARETE
C          DANS LE PLAN CONNAISSANT SA TAILLE DANS R**3
C ORIGIN : POINT ORIGINE DANS LE PLAN DE MAILLAGE
C D2D3   : MATRICE DE PASSAGE DES COORDONNEES 3D EN COORDONNEES 2D
C XMIN   : ABSCISSE MINIMALE APRES TRANSFORMATION 3D->2D
C YMIN   : ORDONNEE MINIMALE APRES TRANSFORMATION 3D->2D
C XYMXMI : ECART MAXIMAL ENTRE XMAX-XMIN ET YMAX-YMIN
C
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS PXYD
C NBSOMM : NOMBRE DE SOMMETS
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE DU TABLEAU NOSOAR
C MXSOAR : NOMBRE MAXIMAL D'ARETES STOCKABLES DANS LE TABLEAU NOSOAR
C MOARTR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE DU TABLEAU NOARTR
C MXARTR : NOMBRE MAXIMAL DE TRIANGLES STOCKABLES DANS LE TABLEAU NOARTR
C
C MODIFIES:
C ---------
C NTR1   : NO DU TRIANGLE INITIAL POUR LA RECHERCHE DU TRIANGLE CONTENANT
C          LE POINT XYSGR
C          EN SORTIE NO DE L'UN DES 3 SOUS-TRIANGLES DU TRIANGLE SI LE
C          POINT APPARTIENT A L'UN DES TRIANGLES
C NTR2   : NO DU TRIANGLE SECOND POUR LA RECHERCHE DU TRIANGLE CONTENANT
C          LE POINT XYSGR SI LE CHEMIN ISSU DE NTR1 DEBOUCHE SUR L'EXTERIEUR
C N1SOAR : NUMERO DE LA PREMIERE ARETE VIDE DANS LE TABLEAU NOSOAR
C          UNE ARETE I DE NOSOAR EST VIDE  <=>  NOSOAR(1,I)=0
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES DE L'ARETE,
C          CHAINAGE DES ARETES FRONTALIERES, CHAINAGE DU HACHAGE DES ARETES
C          HACHAGE DES ARETES = H(NS1,NS2)=MIN(NS1,NS2)
C NOARST : NOARST(I) NUMERO DANS NOSOAR D'UNE ARETE DE SOMMET I
C
C SORTIES:
C --------
C N1ARTR : NUMERO DU PREMIER TRIANGLE VIDE DANS LE TABLEAU NOARTR
C          LE CHAINAGE DES TRIANGLES VIDES SE FAIT SUR NOARTR(2,.)
C NOARTR : LES 3 ARETES DES TRIANGLES +-ARETE1, +-ARETE2, +-ARETE3
C          ARETE1 = 0 SI TRIANGLE VIDE => ARETE2 = TRIANGLE VIDE SUIVANT
C IERR   : =- 1 SI LE POINT XYSGR N'EST DANS AUCUN TRIANGLE
C          =-NS SI LE POINT XYSGR EST TROP PROCHE DU SOMMET NS DE PXYD
C          =  0 SI LE POINT XYSGR = NBSOMM EST DANS UN TRIANGLE
C          = 51 SI LE TABLEAU NOSOAR EST SATURE
C          = 52 SI LE TABLEAU NOARTR EST SATURE
C          = 53 SI LE TABLEAU PXYD   EST SATURE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & ST PIERRE DU PERRAY    AVRIL 2008
C2345X7..............................................................012
      DOUBLE PRECISION  EPSARETE
      PARAMETER        (EPSARETE=0.025D0)
      PARAMETER        (LCHAIN=6)
C
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
C
      DOUBLE PRECISION  XYSGR(2)
      DOUBLE PRECISION  PXYD(3,MXSOMM), COBARY(3)
      DOUBLE PRECISION  RAP2P3
      DOUBLE PRECISION  D2D3(3,3)
      REAL              ORIGIN(3), XYZ(3)
C
      INTEGER           NOSOAR(MOSOAR,MXSOAR),
     %                  NOARTR(MOARTR,MXARTR),
     %                  NOARST(MXSOMM)
C
      INTEGER           NUTR(4)
C
C     RECHERCHE DU TRIANGLE CONTENANT CE SOMMET A PARTIR DU TRIANGLE NTR0
      IPAS = 0
      NTR0 = NTR1
 10   CALL LETRPT( XYSGR, PXYD, MOSOAR, NOSOAR, MOARTR, NOARTR,
     %             NTR0,  NTR, COBARY )
      IF( NTR .LE. 0 ) THEN
         IF( IPAS .EQ. 0 ) THEN
            NTR0 = NTR2
            IPAS = 1
            GOTO 10
         ENDIF
         IERR = -1
         GOTO 9000
      ENDIF
C
C     ICI LE POINT XYSGR EST DANS LE TRIANGLE OU SUR UNE ARETE OU UN SOMMET
      N1ADEL = 0
      NBSTR  = 0
      IF( COBARY(1) .GE. 0.95D0 .OR.
     %    COBARY(2) .GE. 0.95D0 .OR.
     %    COBARY(3) .GE. 0.95D0 ) THEN
C        REJET D'UN SOMMET DE LA GRILLE TROP PROCHE D'UN SOMMET ACTUEL
         NBCB0 = 2
         GOTO 25
      ENDIF
C
C     NBC0 NOMBRE DE COORDONNEES BARYCENTRIQUES TRES FAIBLES
      NBCB0 = 0
      DO 20 I=1,3
         IF( COBARY(I) .LE. EPSARETE ) THEN
            NBCB0 = NBCB0 + 1
            I1    = I
         ENDIF
 20   CONTINUE
C
 25   IF( NBCB0 .GE. 2 ) THEN
C        SOMMET DE LA GRILLE TROP PROCHE D'UN SOMMET ACTUEL => REJET
C        LE NO DES 3 SOMMETS DU TRIANGLE NTR
         CALL NUSOTR(NTR, MOSOAR, NOSOAR, MOARTR, NOARTR, NUTR )
         DO 28 I=1,3
            IF( COBARY(I) .GE. 0.95D0 ) THEN
C              LE NO DU SOMMET EST RETOURNE NEGATIF DANS IERR
               IERR = -NUTR(I)
               GOTO 9000
            ENDIF
 28      CONTINUE
      ENDIF
C
      IF( NBCB0 .EQ. 1 ) THEN
C        NO DE L'ARETE OPPOSEE AU SOMMET I
         IF( I1 .EQ. 3 ) THEN
            I1 = 1
         ELSE
            I1 = I1 + 1
         ENDIF
C        NO NOSOAR DE L'ARETE I1 OPPOSE AU SOMMET I
         N = ABS( NOARTR(I1,NTR) )
C        XYSGR EST PROCHE DE L'ARETE I1 DU TRIANGLE NTR
         IF( NOSOAR(3,N) .GT. 0 ) THEN
C           SOMMET DE LA GRILLE TROP PROCHE D'UNE ARETE FRONTIERE
            IERR = -1
            GOTO 9000
         ENDIF
C        IL EXISTE UN TRIANGLE OPPOSE A CETTE ARETE I1 PROCHE DE XYSGR
C        LE TRIANGLE NTR ET OPPOSE SONT SUBDIVISES EN 4 SOUS-TRIANGLES
C        AVEC POUR SOMMET COMMUN LE POINT XYSGR
C        -------------------------------------------------------------
         NBSTR = 4
C
      ELSE
C
C        LE TRIANGLE NTR CONTIENT LE SOMMET NBSOMM ASSEZ LOIN DES ARETES
C        LE TRIANGLE NTR EST SUBDIVISE EN 3 SOUS-TRIANGLES DE SOMMET NBSOMM
C        ------------------------------------------------------------------
         NBSTR = 3
C        CHAINAGE DES 3 ARETES DU TRIANGLE NTR A RENDRE DELAUNAY EVENTUELLEMENT
         DO 30 I=1,3
            N = ABS( NOARTR(I,NTR) )
            NOSOAR(LCHAIN,N) = N1ADEL
            N1ADEL = N
 30      CONTINUE
C
      ENDIF
C
C     NO DANS PXYD DU POINT BARYCENTRE ou XYSGR
      IF( NBSOMM .GE. MXSOMM ) THEN
C        TABLEAU PXYD SATURE
         WRITE(IMPRIM,*)'TRAJGR1: PXYD SATURE.AUGMENTER MXSOMM=',MXSOMM
         IERR = 53
         RETURN
      ENDIF
C
C     LE SOMMET XYSGR DE LA GRILLE EST AJOUTE DANS PXYD
C     -------------------------------------------------
      NBSOMM = NBSOMM + 1
C     COORDONNEES DU POINT NBSOMM A AJOUTER
      PXYD(1,NBSOMM) = XYSGR(1)
      PXYD(2,NBSOMM) = XYSGR(2)
C
C     MISE A JOUR DE LA DISTANCE SOUHAITEE AUTOUR DE CE POINT NBSOMM
      IF( NOFOTI .GT. 0 ) THEN
C        LA FONCTION TAILLE_IDEALE(X,Y,Z) EXISTE
C        CALCUL DE PXYZD(3,NBSOMM) DANS LE REPERE INITIAL => XYZ(1:3)
         CALL TETAID( NUTYSU, NOFOTI, RAP2P3,
     %                PXYD(1,NBSOMM), PXYD(2,NBSOMM),
     %                ORIGIN, D2D3, XMIN, YMIN, XYMXMI,
     %                XYZ,    PXYD(3,NBSOMM), IER )
      ELSE
         PXYD(3,NBSOMM) = ARETMX
      ENDIF
C
      IF( NBSTR .EQ. 3 ) THEN
C
C        LE TRIANGLE NTR EST SUBDIVISE EN 3 SOUS-TRIANGLES DE SOMMET NBSOMM
C        ------------------------------------------------------------------
         CALL TR3STR( NBSOMM, NTR,
     %                MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                MOARTR, MXARTR, N1ARTR, NOARTR,
     %                NOARST,
     %                NUTR,   IERR )
         IF( IERR .NE. 0 ) RETURN
C
C        PLUS GRAND NUMERO DE TRIANGLE TRAITE SERT DE DEPART
C        POUR LE POINT SUIVANT
         NTR1 = MAX( NUTR(1), NUTR(2), NUTR(3) )
C
      ELSE IF( NBSTR .EQ. 4 ) THEN
C
C        IL EXISTE UN TRIANGLE OPPOSE A CETTE ARETE I+1 PROCHE DE XYSGR
C        LE TRIANGLE NTR ET OPPOSE SONT SUBDIVISES EN 4 SOUS-TRIANGLES
C        AVEC LE SOMMET COMMUN LE POINT NBSOMM
C        -------------------------------------------------------------
         CALL TR4STR( NBSOMM, NTR,    I1,     N1ADEL,
     %                MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                MOARTR, MXARTR, N1ARTR, NOARTR,
     %                NOARST,
     %                NUTR,   IERR )
         IF( IERR .EQ. 5 ) THEN
C           LE TRIANGLE OPPOSE A L'ARETE I1 N'EXISTE PAS
            IERR = -1
            GOTO 9000
         ELSE IF( IERR .NE. 0 ) THEN
            RETURN
         ENDIF
C
C        PLUS GRAND NUMERO DE QUADRANGLE TRAITE SERT DE DEPART
C        POUR LE POINT SUIVANT
         NTR1 = MAX( NUTR(1), NUTR(2), NUTR(3), NUTR(4) )
      ENDIF
C
      IF( TRATRI ) THEN
C        LES TRACES DES TRIANGLES AJOUTES SONT DEMANDES
         DO 50 I=1,NBSTR
C           TRACE DU TRIANGLE NUTR(I)
            CALL MTTRTR( PXYD, NUTR(I), MOARTR, NOARTR,
     %                   MOSOAR, NOSOAR, I, NCBLAN )
 50      CONTINUE
      ENDIF
C
C     MISE EN DELAUNAY DES ARETES PERIPHERIQUES DES SOUS TRIANGLES CREES
      CALL TEDELA( PXYD,   NOARST,
     %             MOSOAR, MXSOAR, N1SOAR, NOSOAR, N1ADEL,
     %             MOARTR, MXARTR, NOARTR, MODIFS )
      IERR = 0
C
 9000 RETURN
      END
