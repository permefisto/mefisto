      SUBROUTINE VDAJFA( NS1,NS2,NS3,  NV1,NV2,  NF1,NF2,NF3,
     %                   MXFACO, LEFACO, NBFACO, N1FASC, NF )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    AJOUTER LA FACE DE SOMMET NS123 , DE VOLUMES NV12
C -----    ET DE FACES ADJACENTES NF123 DANS LE TABLEAU LEFACO
C          des FACES de la FRONTIERE du VOLUME

C ENTREES:
C --------
C NS1,NS2,NS3 : LES 3 NUMEROS DES SOMMETS DE LA FACE
C NV1,NV2     : LES NUMEROS DES 2 VOLUMES DE LA FACE
C NF1,NF2,NF3 : LES 3 NUMEROS DES FACES ADJACENTES PAR LES ARETES

ccc  Dans NEF: apres NF3, vient le parametre
cccc nufaOC  : le numero de faceOC de 1 a NbFaces des FACES D'OpenCascade

C MODIFIES :
C ----------
C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1 < SOMMET 2 < SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...

C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
CCCC       LEFACO(12,*) NO DE FACE OpenCascade

C NBFACO : NOMBRE DE FACES DU TABLEAU LEFACO
C N1FASC : N1FASC(NS)=NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS

C SORTIES:
C --------
C NF     : NUMERO DANS LEFACO DE LA FACE AJOUTEE
C          0 SI PLUS DE FACE LIBRE DANS LEFACO
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC  SEPTEMBRE 1991
C....................................................................012
      INTEGER   LEFACO(11,0:*), N1FASC(*),  NSF(3), NFA(3)

      IF( NV1 .LE. 0 .AND. NV2 .LE. 0 ) THEN
         PRINT*,'vdajfa: ANOMALIE les 2 VOLUMES sont NULS'
      ENDIF

C     MISE EN TABLEAU DES 3 SOMMETS
      NSF(1) = NS1
      NSF(2) = NS2
      NSF(3) = NS3

C     MISE EN TABLEAU DES 3 FACES LEFACO ADJACENTES
      NFA(1) = NF1
      NFA(2) = NF2
      NFA(3) = NF3

C     TRI CROISSANT DES 3 SOMMETS ET EN COORDINATION LES 3 ARETES
      CALL TRI3SAR( NSF, NFA, NSF, NFA )

C     RECHERCHE D'UNE FACE LIBRE DANS LEFACO
C     HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
      LF  = MOD( NS1+NS2+NS3, MXFACO ) + 1
      NF0 = LEFACO( 10, LF )

C     RECHERCHE D'UNE FACE VIDE DANS LEFACO
      NF = LEFACO(10,0)
      IF( NF .LE. 0 ) THEN
C        PLUS DE FACE LIBRE DANS LEFACO
         PRINT*,'vdajfa: SATURATION DES FACES LEFACO MXFACO=',MXFACO
         NF = 0
         RETURN
      ENDIF
      LEFACO(10,0) = LEFACO(9,NF)

C     CHAINAGE DE CETTE FACE NF EN DEBUT DE HACHAGE
      LEFACO(9,NF ) = NF0
      LEFACO(10,LF) = NF

C     LA FACE NF EST VIDE. AJOUT DES DONNEES
C     LE NUMERO DES SOMMETS
      LEFACO(1,NF) = NSF(1)
      LEFACO(2,NF) = NSF(2)
      LEFACO(3,NF) = NSF(3)
C     LE NUMERO DES VOLUMES
      LEFACO(4,NF) = NV1
      LEFACO(5,NF) = NV2
C     LE NUMERO DES FACES ADJACENTES
      LEFACO(6,NF) = NFA(1)
      LEFACO(7,NF) = NFA(2)
      LEFACO(8,NF) = NFA(3)
C     PAS DE TETRAEDRE CONTENANT LA FACE
      LEFACO(11,NF) = 0

C     LE NUMERO DE FACE NF DANS LES FACES ADJACENTES
      DO 80 I=1,3

         NF0 = NFA(I)
         IF( NF0 .LE. 0 ) GOTO 80
         IF( I .NE. 3 ) THEN
            I1 = I + 1
         ELSE
            I1 = 1
         ENDIF
C        RECHERCHE DU NUMERO D'ARETE DE NFA(I) ADJACENTE A NF
         DO J=1,3
            IF( J .NE. 3 ) THEN
               J1 = J + 1
            ELSE
               J1 = 1
            ENDIF
            IF( (NSF(I).EQ.LEFACO(J,NF0).AND.NSF(I1).EQ.LEFACO(J1,NF0))
     %      .OR.(NSF(I).EQ.LEFACO(J1,NF0).AND.NSF(I1).EQ.LEFACO(J,NF0)))
     %      THEN
C              L'ARETE I DE NF ET J DE NF0 SONT COMMUNES
C              LE CHAINAGE CIRCULAIRE EST ASSURE EN AJOUTANT NF
C              AU DELA DE NF0
               IF( LEFACO(5+J,NF0).GT.0) LEFACO(5+I,NF)=LEFACO(5+J,NF0)
               LEFACO(5+J,NF0) = NF
               GOTO 80
            ENDIF
         ENDDO

 80   ENDDO

C     LE CHAINAGE A ETE MIS A JOUR

C     MISE A JOUR DU TABLEAU NO D'UNE FACE AYANT CES SOMMETS
      N1FASC( NSF(1) ) = NF
      N1FASC( NSF(2) ) = NF
      N1FASC( NSF(3) ) = NF

C     UNE FACE DU CONTOUR DE PLUS
      NBFACO = NBFACO + 1

ccc      print*,'vdajfa: Ajout LEFACO(',NF,'):',(LEFACO(I,NF),I=1,11)
      RETURN
      END
