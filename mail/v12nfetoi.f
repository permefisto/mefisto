      SUBROUTINE V12NFETOI( N1FEOC, NFETOI, NOTETR, NBFETO )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    MODIFIER LES VALEURS DU TABLEAU NFETOI DE L'ETOILE de la
C -----    Version1 en la Version2

C ENTREES:
C --------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE NFETOI
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C MODIFIES:
C ---------
C NFETOI : EN ENTREE VERSION 1
C          1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DANS LEFACO DE LA FACE, 0 SINON
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C             =0 SI DERNIER DU CHAINAGE

C          EN SORTIE VERSION 2
C          1: > 0 NUMERO NOTETR DU TETRAEDRE AU DELA DE LA FACE
C             = 0 PAS DE TETRAEDRE AU DELA DE LA FACE (FRONTIERE)
C             =-1 TETRAEDRE OPPOSE INCONNU A RETROUVER (NON FRONTIERE)
C          2: NUMERO PTXYZD DU 1-ER SOMMET DE LA FACE
C          3: NUMERO PTXYZD DU 2-ME SOMMET DE LA FACE
C          4: NUMERO PTXYZD DU 3-ME SOMMET DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR DE
C             L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C             =0 SI DERNIER DU CHAINAGE

C SORTIE :
C --------
C NBFETO : NOMBRE DE FACES DE L'ETOILE NFETOI
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint PIERRE du PERRAY           Janvier 2018
C2345X7..............................................................012
      INTEGER  NFETOI(5,*), NOTETR(8,*)

      INTEGER  NOSOFATE(3,4)
      DATA     NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

C     NOMBRE DE FACES DE L'ETOILE
      NBFETO = 0

C     BOUCLE SUR LES FACES CHAINEES DE L'ETOILE N1FEOC
C     CODAGE de la VERSION 1 de NFETOI en la VERSION 2 DE NFETOI
C     ----------------------------------------------------------
      NF = N1FEOC

 5    IF( NF .GT. 0 ) THEN

C        NO DU TETRAEDRE NTE INTERNE A L'ETOILE ET NO NFTE LOCAL DE LA FACE
         NTE  = NFETOI(1,NF)
         NFTE = NFETOI(2,NF)

C        LE NO DU TETRAEDRE NTE INTERNE EST ECRASE PAR 
C        LE NO DU TETRAEDRE NTEOP AU DELA DE LA FACE NFTE DE NTE
         NTEOP = NOTETR( 4+ABS(NFTE), NTE )
         NFETOI(1,NF) = NTEOP

C        NUMERO DES 3 SOMMETS DE LA FACE COMMUNE DANS NFETOI(2:4,NF)
C        POUR ASSURER QUE LA NORMALE A LA FACE NFETOI SOIT DIRIGEE
C        VERS L'INTERIEUR DE L'ETOILE
C        S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR DE L'ETOILE
         IF( NFTE .GE. 0 ) THEN
C           POUR OBTENIR UNE NORMALE A LA FACE ORIENTEE VERS
C           L'INTERIEUR DU TETRAEDRE NTE
            NFETOI(2,NF) = NOTETR( NOSOFATE(1,NFTE), NTE )
            NFETOI(3,NF) = NOTETR( NOSOFATE(3,NFTE), NTE )
            NFETOI(4,NF) = NOTETR( NOSOFATE(2,NFTE), NTE )
         ELSE
C           POUR OBTENIR UNE NORMALE A LA FACE ORIENTEE VERS
C           L'EXTERIEUR DU TETRAEDRE NTE
            NFTE = -NFTE
            NFETOI(2,NF) = NOTETR( NOSOFATE(1,NFTE), NTE )
            NFETOI(3,NF) = NOTETR( NOSOFATE(2,NFTE), NTE )
            NFETOI(4,NF) = NOTETR( NOSOFATE(3,NFTE), NTE )
         ENDIF

C        UNE FACE DE L'ETOILE DE PLUS
         NBFETO = NBFETO + 1

C        PASSAGE A LA FACE SUIVANTE
         NF = NFETOI(5,NF)
         GOTO 5

      ENDIF

      RETURN
      END
