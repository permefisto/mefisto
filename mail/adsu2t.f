      SUBROUTINE ADSU2T( NA1,    NT1,    PXYD,
     %                   N1TRVI, NOTRIA, NOTRSO, NUPLIS, NUISOP,
     %                   NUMILF, NUMXLF, L1LGFR,
     %                   NBARCH, N1ARCH, NSARCH,
     %                   MXTRCF, NOTRCF, NS2, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     SUPPRIMER LES 2 TRIANGLES DE PETITE ARETE COMMUNE
C -----
C ATTENTION : L'UN DES 2 SOMMETS DE L'ARETE EST SUPPOSE SUPPRIMABLE !
C
C ENTREES:
C --------
C NA1    : NUMERO DE 1 A 3 DE L'ARETE A SUPPRIMER DANS LE TRIANGLE NT1
C NT1    : NUMERO DANS NOTRIA DU TRIANGLE D'ARETE NA1 A SUPPRIMER
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C
C MODIFIES :
C ----------
C N1TRVI : NUMERO DU PREMIER TRIANGLE VIDE DANS NOTRIA
C          (CHAINAGE DANS NOTRIA(4,*))
C NOTRIA : LISTE DES TRIANGLES ST1 ST2 ST3 TR1 TR2 TR3
C          ST NUMERO DANS PXYD DU SOMMET
C NOTRSO : NUMERO D'UN TRIANGLE DANS NOTRIA POUR CHAQUE SOMMET
C          0 SI LE SOMMET APPARTIENT A AUCUN TRIANGLE DE NOTRIA
C NUPLIS : NUMERO DE POINT OU LIGNE DE CHACUN DES SOMMETS
C          -2 000 000 - NUPLIS() ANCIEN SI LE SOMMET A DEJA ETE SUPPRIME
C          -NP SI NP EST LE NUMERO DU POINT UTILISATEUR DE CE SOMMET
C          -1 000 000 - NL1 -1000 NL2 SI LE SOMMET APPARTIENT A 2 LIGNES
C           TELLES QUE NL1 > NL2 (SOMMET INITIAL)
C          NU LIGNE SI LE SOMMET EST SUR UNE LIGNE UTILISATEUR
C          0        SINON
C NUISOP : NUMERO DE L'ISO DE CHAQUE POINT ET 0 SINONC
C
C NUMILF : NUMERO MINIMAL DES LIGNES FRONTIERES DE L'OBJET
C NUMXLF : NUMERO MAXIMAL DES LIGNES FRONTIERES DE L'OBJET
C L1LGFR : POUR CHAQUE LIGNE DE NUMILF A NUMXLF POINTE SUR LA PREMIERE
C          ARETE CHAINEE DE LA LIGNE
C
C NBARCH : NOMBRE D'ARETES CHAINEES DANS NSARCH
C N1ARCH : NUMERO DANS NSARCH DE LA PREMIERE ARETE VIDE
C NSARCH : NUMERO DES 2 SOMMETS DE L'ARETE, ARETE PRECEDENTE ET SUIVANTE
C
C
C TABLEAUX AUXILIAIRE :
C ---------------------
C MXTRCF : NOMBRE DE VARIABLES DU TABLEAU NOTRCF
C NOTRCF : NUMERO DES TRIANGLES DE SOMMETS NS1 OU NS2
C
C SORTIES:
C --------
C NS2    : NUMERO DU SOMMET NON SUPPRIME DE L'ARETE SUPPRIMEE
C IERR   : 0 SI PAS D'ERREUR DETECTEE, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1995
C....................................................................012
      include"./incl/gsmenu.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      INTEGER           NOTRIA(6,*),
     %                  NOTRSO(*),
     %                  NUPLIS(*),
     %                  NUISOP(*),
     %                  L1LGFR(NUMILF:NUMXLF),
     %                  NSARCH(4,*),
     %                  NOTRCF(MXTRCF)
      DOUBLE PRECISION  PXYD(3,*)
C
C     L'ARETE NA1 A POUR SOMMETS
      NS1 = NOTRIA( NA1, NT1 )
      IF( NA1 .LT. 3 ) THEN
         NA2 = NA1 + 1
      ELSE
         NA2 = 1
      ENDIF
      NS2 = NOTRIA( NA2, NT1 )
C
C     ATTENTION : L'UN DES 2 SOMMETS EST SUPPOSE SUPPRIMABLE !
C     ========================================================
C     LES NBTRCF TRIANGLES ACTUELS DE SOMMET NS1
      CALL TRC1ST(NS1, NOTRSO(NS1), NOTRIA, MXTRCF, NBTRCF, NOTRCF(1) )
      NBTRCF = ABS( NBTRCF )
C     LES NBTRCF TRIANGLES ACTUELS DE SOMMET NS2
      CALL TRC1ST( NS2, NOTRSO(NS2), NOTRIA, MXTRCF-NBTRCF,
     %             I,   NOTRCF(NBTRCF+1) )
      NBTRCF = NBTRCF + ABS(I)
C
      IF( NA2 .LT. 3 ) THEN
         NA3 = NA2 + 1
      ELSE
         NA3 = 1
      ENDIF
C     LE TROISIEME SOMMET DU TRIANGLE NT1
      NS3 = NOTRIA( NA3, NT1 )
C
C     LES  TRIANGLES ACTUELS DE SOMMET NS3
      CALL TRC1ST( NS3, NOTRSO(NS3), NOTRIA, MXTRCF-NBTRCF,
     %             I,   NOTRCF(NBTRCF+1) )
      IF( I .EQ. 3 ) THEN
C         CE SOMMET EST "BARYCENTRE" DE 3 TRIANGLES
C         => IMPOSSIBLE DE REDUIRE LE TRIANGLE NT1 A UNE ARETE
          RETURN
      ENDIF
C
C     LE TRIANGLE OPPOSE A NT1 D'ARETE NA1
      NT2 = NOTRIA(3+NA1,NT1)
      IF( NT2 .LE. 0 ) GOTO 50
      IF( NOTRIA(1,NT2) .LE. 0 ) THEN
         NBLGRC( NRERR ) = 2
         KERR(1) = 'ERREUR ADSU2T: TRIANGLE NT2 DETRUIT'
         KERR(2) = 'MAIS AVEC POINTEUR DESSUS'
         CALL LEREUR
         IERR = 1
         CALL XVPAUSE
         RETURN
      ENDIF
C
      DO 10 NAA1=1,3
         IF( NOTRIA(3+NAA1,NT2) .EQ. NT1 ) GOTO 15
 10   CONTINUE
C
 15   IF( NAA1 .LT. 3 ) THEN
         NAA2 = NAA1 + 1
      ELSE
         NAA2 = 1
      ENDIF
      IF( NAA2 .LT. 3 ) THEN
         NAA3 = NAA2 + 1
      ELSE
         NAA3 = 1
      ENDIF
C     LE QUATRIEME SOMMET DU QUADRANGLE
      NS4 = NOTRIA( NAA3, NT2 )
C
C     LES  TRIANGLES ACTUELS DE SOMMET NS4
      CALL TRC1ST( NS4, NOTRSO(NS4), NOTRIA, MXTRCF-NBTRCF,
     %             I,   NOTRCF(NBTRCF+1) )
      IF( I .EQ. 3 ) THEN
C         CE SOMMET EST "BARYCENTRE" DE 3 TRIANGLES
C         => IMPOSSIBLE DE REDUIRE LE TRIANGLE NT2 A UNE ARETE
          RETURN
      ENDIF
C
C     RECHERCHE DES TRIANGLES OPPOSES AUX 2 ARETES DU TRIANGLE NT2
      NTT2 = NOTRIA(3+NAA2,NT2)
      IF( NTT2 .LE. 0 ) THEN
         K2 = 0
         GOTO 35
      ENDIF
      IF( NOTRIA(1,NTT2) .LE. 0 ) THEN
         NBLGRC( NRERR ) = 2
         KERR(1) = 'ERREUR ADSU2T: TRIANGLE NTT2 DETRUIT'
         KERR(2) = 'MAIS AVEC POINTEUR DESSUS'
         CALL LEREUR
         IERR = 2
         CALL XVPAUSE
         RETURN
      ENDIF
      DO 30 K2=4,6
         IF( NOTRIA(K2,NTT2) .EQ. NT2 ) GOTO 35
 30   CONTINUE
C
 35   NTT3 = NOTRIA(3+NAA3,NT2)
      IF( NTT3 .LE. 0 ) THEN
         K3 = 0
         GOTO 45
      ENDIF
      IF( NOTRIA(1,NTT3) .LE. 0 ) THEN
         NBLGRC( NRERR ) = 2
         KERR(1) = 'ERREUR ADSU2T: TRIANGLE NTT3 DETRUIT'
         KERR(2) = 'MAIS AVEC POINTEUR DESSUS'
         CALL LEREUR
         IERR = 3
         CALL XVPAUSE
         RETURN
      ENDIF
      DO 40 K3=4,6
         IF( NOTRIA(K3,NTT3) .EQ. NT2 ) GOTO 45
 40   CONTINUE
C
C     LES NOUVEAUX TRIANGLES VOISINS
 45   IF( NTT2 .GT. 0 ) THEN
         NOTRIA(K2,NTT2) = NTT3
C        MISE A JOUR DE NOTRSO DU 3-EME SOMMET DE NT2 CAR NT2 DISPARAIT
         NOTRSO( NS4 ) = NTT2
      ENDIF
      IF( NTT3 .GT. 0 ) THEN
         NOTRIA(K3,NTT3) = NTT2
C        MISE A JOUR DE NOTRSO DU 3-EME SOMMET DE NT2 CAR NT2 DISPARAIT
         NOTRSO( NS4 ) = NTT3
      ENDIF
C
C     LE TRIANGLE NT2 REDEVIENT LIBRE
      NOTRIA(4,NT2) = N1TRVI
      N1TRVI = NT2
      NOTRIA(1,NT2) = 0
C
C     RECHERCHE DES TRIANGLES AU DELA DES 3 ARETES DU TRIANGLE NT1
C     ============================================================
 50   NTT2 = NOTRIA(3+NA2,NT1)
      IF( NTT2 .LE. 0 ) THEN
         K2 = 0
         GOTO 55
      ENDIF
      IF( NOTRIA(1,NTT2) .LE. 0 )THEN
         NBLGRC( NRERR ) = 2
         KERR(1) = 'ERREUR ADSU2T: TRIANGLE NTT2 DETRUIT'
         KERR(2) = 'MAIS AVEC POINTEUR DESSUS CF 50'
         CALL LEREUR
         IERR = 4
         CALL XVPAUSE
         RETURN
      ENDIF
      DO 52 K2=4,6
         IF( NOTRIA(K2,NTT2) .EQ. NT1 ) GOTO 55
 52   CONTINUE
C
 55   NTT3 = NOTRIA(3+NA3,NT1)
      IF( NTT3 .LE. 0 ) THEN
         K3 = 0
         GOTO 65
      ENDIF
      IF( NOTRIA(1,NTT3) .LE. 0 ) THEN
         NBLGRC( NRERR ) = 2
         KERR(1) = 'ERREUR ADSU2T: TRIANGLE NTT3 DETRUIT'
         KERR(2) = 'MAIS AVEC POINTEUR DESSUS CF 55'
         CALL LEREUR
         IERR = 5
         CALL XVPAUSE
         RETURN
      ENDIF
      DO 60 K3=4,6
         IF( NOTRIA(K3,NTT3) .EQ. NT1 ) GOTO 65
 60   CONTINUE
C
C     LES NOUVEAUX TRIANGLES VOISINS
 65   IF( NTT2 .GT. 0 ) THEN
C        LE NOUVEAU TRIANGLE VOISIN
         NOTRIA(K2,NTT2) = NTT3
C        MISE A JOUR DE NOTRSO DU 3-EME SOMMET DE NT1 CAR NT1 DISPARAIT
         NOTRSO( NS3 ) = NTT2
C        MISE A JOUR DE NOTRSO DU SOMMET NS1 ET NS2
         NOTRSO( NS1 ) = NTT2
         NOTRSO( NS2 ) = NTT2
      ENDIF
      IF( NTT3 .GT. 0 ) THEN
C        LE NOUVEAU TRIANGLE VOISIN
         NOTRIA(K3,NTT3) = NTT2
C        MISE A JOUR DE NOTRSO DU 3-EME SOMMET DE NT1 CAR NT1 DISPARAIT
         NOTRSO( NS3 ) = NTT3
C        MISE A JOUR DE NOTRSO DU SOMMET NS1 ET NS2
         NOTRSO( NS1 ) = NTT3
         NOTRSO( NS2 ) = NTT3
      ENDIF
C
C     RECHERCHE DU SOMMET NS1 A REMPLACER PAR NS2
C     ===========================================
      IF( NUPLIS(NS1) .LT. 0 ) THEN
C        PERMUTATION DE NS1 ET NS2
         K2  = NS2
         NS2 = NS1
         NS1 = K2
         GOTO 100
      ENDIF
      IF( NUPLIS(NS2) .LT. 0 ) GOTO 100
C
C     NS1 ET NS2 SONT SUPPRIMABLES => DE PREFERENCE SI LE SOMMET
C     EST SUR UNE LIGNE ALORS IL EST CONSERVE
      IF( NUPLIS(NS2) .GT. 0 ) GOTO 100
C
      IF( NUPLIS(NS1) .GT. 0 ) THEN
C        NS1 EST UN SOMMET D'UNE LIGNE => PERMUTATION DE NS1 ET NS2
         K2  = NS2
         NS2 = NS1
         NS1 = K2
         GOTO 100
      ENDIF
C
C     NS1 ET NS2 SONT SUPPRIMABLES => DE PREFERENCE SI LE SOMMET
C     EST SUR UNE ISOVALEUR ALORS IL EST CONSERVE
      IF( NUISOP(NS2) .GT. 0 ) GOTO 100
C
      IF( NUISOP(NS1) .GT. 0 ) THEN
C        NS1 EST UN SOMMET D'UNE LIGNE ISOVALEUR => PERMUTATION DE NS1 ET NS2
         K2  = NS2
         NS2 = NS1
         NS1 = K2
      ENDIF
C
C     ICI NS1 EST SUPPRIMABLE  => NS1 DEVIENT NS2 => NS1 DISPARAIT
C     ============================================================
 100  NUL1 = NUPLIS( NS1 )
C     LE NUMERO DE LA LIGNE DU SOMMET NS1
      IF( NUL1 .GT. 0 ) THEN
C        LE POINT EST SUPPRIME DU CHAINAGE DES ARETES DE LA LIGNE
         CALL SSARCH( NS1,    L1LGFR(NUL1),
     %                NBARCH, N1ARCH, NSARCH, IERR )
      ENDIF
C
C     REMPLACEMENT DE NS1 PAR NS2 DANS LES TRIANGLES DE SOMMET NS1 OU NS2
      DO 130 K3=1,ABS(NBTRCF)
         NTT2 = NOTRCF( K3 )
         DO 120 K2=1,3
            IF( NOTRIA(K2,NTT2) .EQ. NS1 ) THEN
               NOTRIA(K2,NTT2) = NS2
               GOTO 125
            ENDIF
 120     CONTINUE
 125     CALL DVTRTR( PXYD, NOTRIA, NTT2, NCROUG, NCNOIR )
 130  CONTINUE
C
C     LE SOMMET NS EST SUPPRIME DANS NUPLIS
C     =====================================
      NUPLIS( NS1 ) = - 2 000 000 - NUPLIS( NS1 )
C
C     LE TRIANGLE NT1 REDEVIENT LIBRE DANS LE TABLEAU NOTRIA
C     ======================================================
      NOTRIA(4,NT1) = N1TRVI
      N1TRVI = NT1
      NOTRIA(1,NT1) = 0
C
CCC      WRITE(IMPRIM,*) 'ADSU2T: SUPPRESSION DU SOMMET ',NS1
      END
