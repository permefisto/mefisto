ccc     %          ' LEFACO(11,',NT,')=',NTE
ccc      ENDDO

      NFP = 0

C     ======================================================================
C     BOUCLE SUR LES NBFAPE FACES LEFACO PERDUES NON DANS LA TETRAEDRISATION
C     ======================================================================
 1    IERR   = 0
      NUREPR = 0

C     NO NOFAPE DE LA FACE PERDUE A TRAITER
      NFP = NFP + 1
      IF( NFP .GT. NBFAPE ) GOTO 9990
      NPTE2P = 0

C     NFLPER NUMERO LEFACO DE LA FACE PERDUE NFP A TRAITER
