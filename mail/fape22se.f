ccc         tracte = .true.
ccc      endif


C     PROTECTION DU NOMBRE ACTUEL DE SOMMETS PTXYZD
      NBSOMM0 = NBSOMM

C     RETOUR EN CAS DE REPRISE
      NBSSET  = 0
      NBTECFPR= 0
      NBTECF0 = 0
      NBTECF  = 0
      NBTECR  = 0
      NBPTIN  = 0
      NBTRCF  = 0
      NBSTIS  = 0
      NFTRAV  = 0
      NCTRAV  = 0

C     ------------------------------------------------------------
C     0) FORMATION DU PREMIER CONTOUR FERME CF FORME DES TRIANGLES
C        LEFACO PERDUS ADJACENTS A PARTIR DE LA FACE NFLPER
C     ------------------------------------------------------------
C     COSMAX = -1.1  POUR EVITER LA RECHERCHE DE COPLANEARITE
      CALL FADUCF1( -1.1,   PTXYZD, N1TETS, NOTETR,
     %              NFLPER, NBFAPE, NOFAPE, MXFACO, LEFACO, NO0FAR,
     %              MXTAUX*4, NAETOI,
     %              MXTRCF, NBTRCF, NOTRCF, IERR )

ccc      IF( NBTRCF .GT. 8 ) THEN
      IF( NBTRCF .GT. 10 ) THEN

C        TROP DE TRIANGLES DANS LE CF DES FACES PERDUES
         TRACTE = .TRUE.
         PRINT*
         PRINT*,'FAPE22SE: ATTENTION VOLUME ',KNMVOL,
     %          ' FACE PERDUE(',NFP,')=',NFLPER,' NBTRCF=',NBTRCF,
     %          ' NBSOMM=',NBSOMM,' Nb Abandons=',NBABAN

C        TRACE DES NBTRCF FACES TRIANGULAIRES DU CF
         KTITRE='         FACES PERDUES du CF'
         WRITE(KTITRE(1:6),'(I6)') NBTRCF
         CALL TRDUCF( KTITRE, PTXYZD, NBTRCF, NOTRCF, 0, NOTECF,
     %                LEFACO, NO0FAR )

CCCC        TRACE DES ARETES DES FACES PERDUES ET DES TETRAEDRES
CCCC        DE LEURS SOMMETS, FACE PERDUE PAR FACE PERDUE
CCC         IF( INFACO .NE. 0 ) THEN
