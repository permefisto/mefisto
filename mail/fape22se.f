            NOFAPE( NFP ) = -NFLPER
            GOTO 1
         ELSE
C           NTE N'A PAS LA FACE NFLPER DE LEFACO
C           -> NFLPER SANS TETRAEDRE EST PERDUE
            LEFACO(11,NFLPER) = 0
         ENDIF
      ENDIF


ccc      if( nuvolu .eq. 45 .and. nfp .eq. 16 ) then
