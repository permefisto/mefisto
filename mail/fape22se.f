         GOTO 9000

      ENDIF

      NBTRCF00 = NBTRCF

C     A PARTIR DES NBTRCF FACES (LEFACO ou NO0FAR) DU CF
C     CONSTRUCTION DES SOMMETS DE LA FRONTIERE ET INTERNES AU CF
C     DES FACES PERDUES (COPLANAIRES SI COSMAX>-1) ADJACENTES
C     ----------------------------------------------------------
      CALL ARDUCF( PTXYZD, NBFAPE, NOFAPE, MXFACO, LEFACO, NO0FAR,
     %             MXTAUX, NAETOI, MXTRCF, NBTRCF, NOTRCF,
     %             MXARCF, NBCF,   N1ARCF, NOARCF, NBARCF,
     %             MXTRCF, NBSTCF, NOSTCF,
     %             MXSOMM, NBSTIS, NOSTIS, IERR )

ccc      CALL VDR1CF( -1.0,   NFLPER, NBSOMM, PTXYZD, N1TETS, NOTETR,
ccc     %             NBFAPE, NOFAPE, MXFACO, LEFACO, N1FASC, NO0FAR,
ccc     %             MXTAUX, NAETOI,
ccc     %             MXTRCF, NBTRCF, NOTRCF,
ccc     %             MXARCF, NBCF,   N1ARCF, NOARCF,
ccc     %             MXTRCF, NBSTCF, NOSTCF,
ccc     %             MXSOMM, NBSTIS, NOSTIS,
ccc     %             IERR )

ccc      IF( NFP.EQ.16 ) THEN
