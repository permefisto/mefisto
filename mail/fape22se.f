      CALL VOLTETRA( PTXYZD, MXTETR, NOTETR, NBTETR, NUDTETR, VOLMOY )
      VOLMOY = VOLMOY / NBTETR

C     TRACE EVENTUEL DES FACES PERDUES DANS LEFACO
      CALL TRFAPE( NBFAPE, NOFAPE, MXFACO, LEFACO, PTXYZD )
      MNTEFA = 0
      MXTEFA = 1024
      NBABAN = 0

C     TRI SELON LES SURFACES DECROISSANTES DES FACES PERDUES
      CALL TRIFAP( NBFAPE, NOFAPE, LEFACO, PTXYZD )

cccC     BILAN SUR LES FACES PERDUES
ccc      DO N=1,NBFAPE
