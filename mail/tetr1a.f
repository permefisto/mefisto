      SUBROUTINE TETR1A( NS1,    NS2,    N1TETS, NOTETR,
     %                   NBTE1A, MXTE1A, NOTE1A, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LISTER DANS NOTE1A LE NUMERO NOTETR DES NBTE1A TETRAEDRES
C -----    D'UNE ARETE COMMUNE DE SOMMETS NS1-NS2
C          i.e. TETRAEDRES DE SOMMETS NS1 et NS2
C
C ENTREES:
C --------
C NS1,NS2: NUMERO DES 2 SOMMETS DE L'ARETE COMMUNE A TOUS LES TETRAEDRES
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE DE NUMEROTATION
C          1: 123      2: 234      3: 341      4: 412
C MXTE1A : NOMBRE D'ENTIERS DU TABLEAU NOTE1A
C
C SORTIES:
C --------
C NBTE1A : >0 NOMBRE DES TETRAEDRES AYANT NS1 et NS2 COMME SOMMETS si IERR=0
C          =0 PAS DE TETRAEDRE D'ARETE NS1-NS2 ou IERR>0
C NOTE1A : NOTE1A(I) = NUMERO DU TETRAEDRE I DE SOMMETS NS1 et NS2
C IERR   : = 0 PAS D'ERREUR
C          = 1 SI L'ARETE NS1-NS2 N'A PAS DE TETRAEDRES ENROULANTS
C          = 2 SI SATURATION DU TABLEAU NOTE1A
C          = VALEUR de IERR DU CALL tetra1s POUR NS1 ou NS2 si NBTE1A=0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   DECEMBRE 2005
C....................................................................012
      INTEGER  NOTETR(8,*), N1TETS(*), NOTE1A(MXTE1A)

      IERR   = 0
      NBTE1A = 0

      IF( NS1 .LE. 0 ) THEN
         PRINT*,'tetr1a: ERREUR NS1=',NS1,'<=0 INTERDIT'
         IERR = 1
         GOTO 9999
      ENDIF

      IF( NS2 .LE. 0 ) THEN
         PRINT*,'tetr1a: ERREUR NS2=',NS2,'<=0 INTERDIT'
         IERR = 1
         GOTO 9999
      ENDIF

      IF( N1TETS(NS1) .LE. 0 .OR. N1TETS(NS2) .LE. 0 ) THEN
C        UN DES 2 SOMMETS DE L'ARETE APPARTIENT A AUCUN TETRAEDRE
C        => L'ARETE APPARTIENT A AUCUN TETRAEDRE
         GOTO 9999
      ENDIF

C     POUR REDUIRE LES RECHERCHES LE SOMMET DE PLUS GRAND NUMERO EST
C     UTILISE EN PREMIER
      IF( NS1 .GT. NS2 ) THEN
         NSS1 = NS1
         NSS2 = NS2
      ELSE
         NSS1 = NS2
         NSS2 = NS1
      ENDIF

C     LES NBTE TETRAEDRES DE SOMMET NSS1 SONT RETROUVES DANS NOTE1A
 5    CALL TETR1S( NSS1, N1TETS, NOTETR,
     %             NBTE, MXTE1A, NOTE1A, IERR )

      IF( IERR .EQ. 3 ) THEN
C        SATURATION du TABLEAU NOTE1A pour le SOMMET NSS1
         IF( NSS1 .GT. NSS2 ) THEN
            PRINT*,'tetr1a: SECOND ESSAI de CALCUL des TETRAEDRES d''ARE
     %TE',NSS1,NSS2
            N    = NSS1
            NSS1 = NSS2
            NSS2 = N
            GOTO 5
         ENDIF
C        SATURATION NOTE1A pour les SOMMETS NSS1 et NSS2
         IERR = 2
      ENDIF

      IF( IERR .NE. 0 ) THEN
         NBTE1A = 0
         GOTO 9999
      ENDIF

C     RETRAIT DES TETRAEDRES NON DE SOMMET NSS2
      DO 50 N=1,NBTE

C        LE TETRAEDRE NTE A T IL LE SOMMET NSS2?
         NTE = NOTE1A( N )
         DO K=1,4
            IF( NOTETR(K,NTE) .EQ. NSS2 ) GOTO 30
         ENDDO
         GOTO 50

C        AJOUT DU TETRAEDRE DE SOMMET NSS2 S'IL N'EST PAS DEJA STOCKE
 30      DO K=1,NBTE1A
            IF( NTE .EQ. NOTE1A(K) ) GOTO 50
         ENDDO
         NBTE1A = NBTE1A + 1
         NOTE1A( NBTE1A ) = NTE

 50   ENDDO

CCC      PRINT *
CCC      PRINT *,'TETR1A: +',NBTE1A,' TETRAEDRES DE SOMMETS',NS1,NS2
CCC      DO 600 K=1,NBTE1A
CCC         PRINT *,'TETRA',K,NOTE1A(K),': ST',(NOTETR(N,NOTE1A(K)),N=1,4)
CCC 600  CONTINUE

 9999 RETURN
      END
