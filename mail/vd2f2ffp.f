      SUBROUTINE VD2F2FFP( COANPL, PTXYZD, N1TETS, NOTETR,
     %                     NFLPER, MXFACO, LEFACO, N1FASC,
     %                     MXTE1S, NOTE1S, NONOUIEC )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ECHANGE DU QUADRILATERE COPLANAIRE FORME PAR 2 TRIANGLES
C -----    PERDUS AVEC LES 2 AUTRES TRIANGLES SI L'AUTRE DIAGONALE
C          SE TROUVE DANS LA TETRAEDRISATION

C ENTREES:
C --------
C COANPL : SEUIL DU COSINUS DE L'ANGLE FORME PAR LES NORMALES AUX
C          2 FACES ET AU DESSUS DUQUEL LES FACES
C          SONT CONSIDEREES COPLANAIRES
C          ( 0.9848   => 10   DEGRES )
C          ( 0.99     => 8.11 DEGRES )
C          ( 0.9962   => 5    DEGRES )
C          ( 0.99756  => 4    DEGRES )
C          ( 0.99863  => 3    DEGRES )
C          ( 0.999    => 2.56 DEGRES )
C          ( 0.9999   => 0.8  DEGRES )
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C NFLPER : NUMERO LEFACO DE LA FACE PERDUE A ECHANGER SI POSSIBLE
C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...

C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
CCCC       LEFACO(12,*) NO DE FACE OC
C N1FASC : N1FASC(NS)=NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS

C MXTE1S : MAX DE MOTS DU TABLEAU NOTE1S
C NOTE1S : TABLEAU AUXILIAIRE

C SORTIE :
C --------
C NONOUIEC : =1 SI IL Y A EU ECHANGE 2F -> 2F DES DIAGONALES
C            =0 SI PAS D'ECHANGE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       JUIN 2002
C MODIFS : ALAIN PERRONNET  Saint PIERRE du PERRAY         Decembre 2019
C....................................................................012
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      DOUBLE PRECISION  PTXYZD(1:4,1:*)
      INTEGER           NOTETR(8,*),
     %                  N1TETS(*),
     %                  LEFACO(11,0:MXFACO),
     %                  N1FASC(*),
     %                  NOTE1S(MXTE1S)

C     ===================================================================
C     2Faces Perdues -> 2Faces Retrouvees?
C     EXISTE T IL UNE AUTRE FACE ADJACENTE PERDUE DE LA FACE NFLPER
C     TELLE QUE LE QUADRILATERE AINSI FORME A SA SECONDE DIAGONALE
C     COMME ARETE DE LA TETRAEDRISATION
C     SI OUI: ECHANGE DES 2 DIAGONALES ET RECUPERATION DE 2 FACES PERDUES
C     ===================================================================
      NONOUIEC = 0

C     LE NO NOTETR DU TETRAEDRE DE FACE NFLPER DANS LEFACO
      NTE = LEFACO( 11, NFLPER )
      IF( NTE .GT. 0 ) GOTO 9999

C     LES VOLUMES DE LA FACE PERDUE NFLPER
      NV1 = LEFACO( 4, NFLPER )
      NV2 = LEFACO( 5, NFLPER )

C     RECHERCHE D'UNE AUTRE FACE PERDUE D'ARETE COMMUNE AVEC NFLPER
C     BOUCLE SUR LES 3 ARETES DE LA FACE NFLPER
      DO 20 NA=1,3

C        LA FACE DANS LEFACO DE L'AUTRE COTE DE L'ARETE NA
         NFLPERAD = LEFACO( 5+NA, NFLPER )
         IF( NFLPERAD .LE. 0 ) GOTO 20

C        CETTE FACE EST ELLE PERDUE?
         IF( LEFACO( 11, NFLPERAD ) .GT. 0 ) THEN
C           LA FACE NFLPERAD N'EST PAS PERDUE
            GOTO 20
         ENDIF

C        LES VOLUMES DE CES 2 FACES SONT ILS LES MEMES ?
         NW1 = LEFACO( 4, NFLPERAD )
         NW2 = LEFACO( 5, NFLPERAD )
         IF( (NV1 .NE. NW1 .AND. NV1 .NE. NW2) .OR.
     %       (NV2 .NE. NW1 .AND. NV2 .NE. NW2) ) GOTO 20

C        OUI: LES 2 VOLUMES DES 2 FACES SONT IDENTIQUES
C             LES FACES NFLPER ET NFLPERAD SONT ELLES COPLANAIRES ?
         CALL VD2TPL( COANPL, NFLPER, NFLPERAD, LEFACO, PTXYZD, NONOUI )
         IF( NONOUI .EQ. 0 ) GOTO 20

C        NFLPER NFLPERAD FORMENT UN QUADRANGLE DE DIAGONALE NS1-NS2
C        L'AUTRE DIAGONALE NS3-NS4 DU QUADRANGLE AINSI FORME
C        EXISTE ELLE DANS LA TETRAEDRISATION ACTUELLE ?
         IF( NA .EQ. 3 ) THEN
            NA1 = 1
         ELSE
            NA1 = NA + 1
         ENDIF
         NS1 = LEFACO( NA,  NFLPER )
         NS2 = LEFACO( NA1, NFLPER )

C        NS3 LE SOMMET RESTANT DE NFLPER
         IF( NA1 .EQ. 3 ) THEN
            NA2 = 1
         ELSE
            NA2 = NA1 + 1
         ENDIF
         NS3 = LEFACO( NA2, NFLPER )

C        NF4 EST LA FACE DE L'AUTRE COTE DE NS2-NS3
         NF4 = LEFACO( 5+NA1, NFLPER )
C        NF5 EST LA FACE DE L'AUTRE COTE DE NS1-NS3
         NF5 = LEFACO( 5+NA2, NFLPER )

C        NS4 LE SOMMET RESTANT DE LA FACE ADJACENTE NFLPERAD
         DO K=1,3
            NS4 = LEFACO( K, NFLPERAD )
            IF( NS4 .NE. NS1 .AND. NS4 .NE. NS2 ) GOTO 10
         ENDDO

C        LES 2 FACES DIAGONALES EXISTENT DANS LA TETRAEDRISATION
C        LE QUADRANGLE EST IL CONVEXE <=> LES SURFACES SONT ELLES EGALES ?
 10      CALL QUADCXD( PTXYZD(1,NS1), PTXYZD(1,NS3),
     %                 PTXYZD(1,NS2), PTXYZD(1,NS4), NONOUI )
         IF( NONOUI .EQ. 0 ) GOTO 20

C        LA FACE NF2=NS1 NS4 NS3 EST ELLE DANS LA TETRAEDRISATION ?
         CALL TETR11F( NS1,NS4,NS3, N1TETS, NOTETR,
     %                 MXTE1S, NOTE1S, NTE2 )
         IF( NTE2 .LE. 0 ) GOTO 20

C        LA FACE NF3=NS4 NS2 NS3 EST ELLE DANS LA TETRAEDRISATION ?
         CALL TETR11F( NS4,NS2,NS3, N1TETS, NOTETR,
     %                 MXTE1S, NOTE1S, NTE3 )
         IF( NTE3 .LE. 0 ) GOTO 20

C        CES 2 FACES NFLPER NS123 ET NFLPERAD NS214 DE LEFACO
C        SONT REMPLACEES DANS LEFACO PAR LES FACES NS143 et NS423
C        QUI APPARTIENNENT AUX TETRAEDRES NTE2 et NTE3
C        --------------------------------------------------------
C        SAUVEGARDE DES FACES ADJACENTES AUX 2 FACES NFLPER et NFLPERAD
C        NS4 EST LE SOMMET K DE LA FACE NFLPERAD
         IF( K .EQ. 1 ) THEN
            L = 3
         ELSE
            L = K - 1
         ENDIF
         IF( NS1 .EQ. LEFACO(L,NFLPERAD) ) THEN
C           FACE DE L'AUTRE COTE DE NS1 NS4
            NF6 = LEFACO(5+L,NFLPERAD)
C           FACE DE L'AUTRE COTE DE NS2 NS4
            NF7 = LEFACO(5+K,NFLPERAD)
         ELSE
C           FACE DE L'AUTRE COTE DE NS1 NS4
            NF6 = LEFACO(5+K,NFLPERAD)
C           FACE DE L'AUTRE COTE DE NS2 NS4
            NF7 = LEFACO(5+L,NFLPERAD)
         ENDIF

CCCC        TRACE DES 2 FACES INITIALES DE LEFACO
CCC         CALL TRFATR( NCBLAN, NCNOIR, LEFACO(1,NFLPER),   PTXYZD )
CCC         CALL TRFATR( NCBLAN, NCNOIR, LEFACO(1,NFLPERAD), PTXYZD )

C        DESTRUCTION DES 2 FACES INITIALES DE LEFACO
         CALL VDDSFA( NFLPER,   MXFACO, LEFACO, NBFACO, N1FASC )
         CALL VDDSFA( NFLPERAD, MXFACO, LEFACO, NBFACO, N1FASC )

C        AJOUT DE LA FACE NF2 DANS LEFACO
         CALL VDAJFA( NS1,NS4,NS3, NV1,NV2, NF6,-1,NF5,
     %                MXFACO, LEFACO, NBFACO, N1FASC, NF2 )
C        LA FACE NF2 DE LEFACO EST UNE FACE DU TETRAEDRE NTE2
         LEFACO(11,NF2) = NTE2

C        AJOUT DE LA FACE NF3 DANS LEFACO
         CALL VDAJFA( NS4,NS2,NS3, NV1,NV2, NF7,NF4,NF2,
     %                MXFACO, LEFACO, NBFACO, N1FASC, NF3 )
C        LA FACE NF3 DE LEFACO EST UNE FACE DU TETRAEDRE NTE3
         LEFACO(11,NF3) = NTE3

CCCC        TRACE DES 2 FACES FINALES DE LEFACO
CCC         CALL TRFATR( NCNOIR, NCBLAN, LEFACO(1,NF2), PTXYZD )
CCC         CALL TRFATR( NCNOIR, NCBLAN, LEFACO(1,NF3), PTXYZD )

C        MISE A JOUR DE NF3 DANS LA FACE NF2 DE LEFACO
         DO L=6,8
            IF( LEFACO(L,NF2) .EQ. -1 ) THEN
               LEFACO(L,NF2) = NF3
               GOTO 20
            ENDIF
         ENDDO

cccC        AFFICHAGES
ccc         IF( LANGAG .EQ. 0 ) THEN
ccc            PRINT *,'vd2f2ffp: Les FACES PERDUES',
ccc     %               NS1,NS2,NS3,' +',NS1,NS2,NS4,
ccc     %              ' REMPLACEES par', (LEFACO(L,NF2),L=1,3),
ccc     %              ' +', (LEFACO(L,NF3),L=1,3),
ccc     %              ' par ECHANGE des DIAGONALES'
ccc         ELSE
ccc            PRINT *,'vd2f2ffp: The LOST FACES',
ccc     %               NS1,NS2,NS3,' +',NS1,NS2,NS4,
ccc     %              ' are REPLACED to', (LEFACO(L,NF2),L=1,3),
ccc     %              ' +', (LEFACO(L,NF3),L=1,3),
ccc     %              ' by DIAGONAL EXCHANGES'
ccc         ENDIF

         NONOUIEC = 1
         GOTO 9999

 20   ENDDO

 9999 RETURN
      END
