      SUBROUTINE VOEX19( NUVOLU, NTLXVL, LADEFI, RADEFI,
     %                   NTCUPA, MNCUPA, NTSOMM, MNSOMM, IERR )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT : GENERER LES TETRAEDRES DU VOLUME NUVOLU (CODE DEFINITION TYPE 19)
C ---   PARTITION DE VOLUMES DE TYPE 8 A TETRAEDRISER EN UNE FOIS
C       AVEC EMPLOI D'UNE GRILLE REGULIERE DE PAVES POUR LOCALISER

C ENTREES:
C --------
C NUVOLU : NUMERO DU VOLUME PARTITION DANS LE LEXIQUE DES VOLUMES
C NTLXVL : NUMERO DU TABLEAU TS DU LEXIQUE DU VOLUME PARTITION
C LADEFI : TABLEAU DE DEFINITION DU VOLUME PARTITIONNE
C          CF '~/td/d/a_volume__definition'
C RADEFI : MEME TABLEAU DECLARE REEL

C SORTIES:
C --------
C NTCUPA : NUMERO DU TS DES NUMEROS DES CUBES DU VOLUME PARTITION
C MNCUPA : ADRESSE MCN DU TABLEAU DES NUMEROS DES NO SOMMET DU VOLUME
C          CF '~/td/d/a___nsef'
C NTSOMM : NUMERO DU TABLEAU TS DES SOMMETS DE LA PARTITION
C MNSOMM : ADRESSE MCN   DU TABLEAU SOMMETS DE LA PARTITION
C          CF '~/td/d/a___xyzsommet'
C IERR   : 0  SI PAS D'ERREUR,  >0 SI ERREUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC et St PIERRE DU PERRAY    Mai  2008
C MODIFS : ALAIN PERRONNET LJLL UPMC et St PIERRE DU PERRAY    Mars 2016
C2345X7..............................................................012
      DOUBLE PRECISION  EPSTET
      PARAMETER        (EPSTET=-1D-8)
C     QTEAME: QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C             AMELIORATION DE LA QUALITE DES TETRAEDRES EST DEMANDEE
ccc      PARAMETER         (QTEAME=0.001)  9/9/17
ccc      PARAMETER         (QTEAME=0.01 ) 15/9/17
ccc      PARAMETER         (QTEAME=0.01 ) 13/10/17
ccc      PARAMETER         (QTEAME=0.02 )  3/12/18
ccc      PARAMETER        (QTEAME=0.05)
      PARAMETER        (QTEAME=0.08)

C     QUAMINEX: QUALITE MINIMALE EXIGEE POUR CREER UN TETRAEDRE
C               C-A-D AU DESSOUS DE LAQUELLE UN TETRAEDRE N'EST PAS CREE
      PARAMETER        (QUAMINEX=0.005)

      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      COMMON / EPSSSS / EPZERO, EPSXYZ

      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a_surface__definition.inc"
      include"./incl/a_volume__definition.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___materiaux.inc"
      include"./incl/xyzext.inc"
      include"./incl/trvari.inc"

      COMMON / TRTETR / STOPTE,TRACTE
      LOGICAL           STOPTE,TRACTE

      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))

      DOUBLE PRECISION, allocatable, dimension(:,:) :: PTXYZD
      INTEGER, allocatable, dimension(:,:) ::  NOTETR
      INTEGER, allocatable, dimension(:)   ::  CHATVO
      INTRINSIC         ALLOCATED

C     FORMULES : TETRAEDRES + ARETES + NB_FRONTIERES = FACES + SOMMETS
C     ========== 4*TETRAEDRES = FACES_FRONTALIERES + 2*FACES_INTERNES
C                ASYMPTOTIQUEMENT  FACES = 2 * TETRAEDRES

      CHARACTER*80      KINFO
      CHARACTER*24      KNMVOL
      INTEGER           LADEFI(0:*)
      REAL              RADEFI(0:*)

      REAL              QUAMIN, QUAMOY
      REAL              HEXA(6,2)
      REAL              COINOB(6,2), COMIMX(6,2)
      DOUBLE PRECISION  HEXAGL(3,2), HEXAPAVE(3,2), ECHPAV(3)
      INTEGER           NBIPAV(3)
      INTEGER           NUFACE(16), NOSOCU(1:8)
      EQUIVALENCE     ( NUFACE(1),  NOSOCU(1) )
      DOUBLE PRECISION  DINFO, VOLUMT, VOLMOY, CPU, CPUT

C     INITIALISATION DU TEMPS CPU
C     ---------------------------
      CPU  = DINFO( 'DELTA CPU' )
      CPUT = 0D0

C     NOM DU VOLUME DE NUMERO NUVOLU DANS LE LEXIQUE DES VOLUMES
      CALL NMOBNU( 'VOLUME', NUVOLU, KNMVOL )
      PRINT*
      PRINT*,'voex19: Debut de la TETRAEDRISATION du VOLUME ',KNMVOL

C     STOPTE = FAUX ==> PAS D'ARRET APRES LE TRACE DE CHAQUE TETRAEDRE
C              VRAI ==> DEMANDE D'UN CARACTERE POUR REDEMARRER
C     TRACTE = FAUX ==> PAS DE TRACE DES TETRAEDRES
C              VRAI ==> TRACE DES TETRAEDRES DE L'ETOILE
      STOPTE = .FALSE.
      TRACTE = .FALSE.

C     AU DEPART PAS DE TABLEAU NVOLTE
      IVOLTE = 0

      IERAOALLOC = 1
      IERATALLOC = 1
      IERZDALLOC = 1
      IERTEALLOC = 1
      IERCHALLOC = 1
      IERARELLOC = 1
C
C     EXISTENCE OU NON DE LA FONCTION 'TAILLE_IDEALE'
C     -----------------------------------------------
      NOFOTI = NOFOTIEL()
      IF( LANGAG .EQ. 0 ) THEN
         IF( NOFOTI .GT. 0 ) THEN
C           LA FONCTION TAILLE_IDEALE(X,Y,Z) EXISTE
            WRITE(IMPRIM,10000) 'AVEC'
         ELSE
C           LA FONCTION TAILLE_IDEALE(X,Y,Z) N'EXISTE PAS
            WRITE(IMPRIM,10000) 'SANS'
         ENDIF
      ELSE
         IF( NOFOTI .GT. 0 ) THEN
C           LA FONCTION TAILLE_IDEALE(X,Y,Z) EXISTE
            WRITE(IMPRIM,20000) '  WITH '
         ELSE
C           LA FONCTION TAILLE_IDEALE(X,Y,Z) N'EXISTE PAS
            WRITE(IMPRIM,20000) 'WITHOUT'
         ENDIF
      ENDIF
10000 FORMAT('voex19: TETRAEDRISATION ',A4,
     %       ' FONCTION TAILLE_IDEALE(X,Y,Z) (ou EDGE_LENGTH(X,Y,Z))' )
20000 FORMAT('voex19: TETRAHEDRISATION ',A7,
     %       ' FUNCTION EDGE_LENGTH(X,Y,Z) (or TAILLE_IDEALE(X,Y,Z))' )

C     ADRESSE DU LEXIQUE DES SURFACES
      CALL TAMSOU( NTSURF, MNSURF )

C     ADRESSE DU LEXIQUE DES VOLUMES
      CALL TAMSOU( NTVOLU, MNVOLU )

C     AU DEBUT PAS D'ERREUR
      IERR   = 0

C     LA PARTITION EST FORMEE DE NBVOPA VOLUMES
      NBVOPA = LADEFI( WBVOPA )

C     LE MAXIMUM DE SOMMETS A GENERER
      MXSOMM = LADEFI( WXSOVO )

C     LONGUEUR DE L'ARETE DE LA GRILLE REGULIERE
C     ------------------------------------------
      ARETGR = ABS( RADEFI( WRETGR ) )

C     ANGLE DE COPLANEARITE ENTRE FACES DE TETRAEDRES
C     -----------------------------------------------
      ANEN2P = RADEFI( WNEN2P )
      ANEN2P = MAX( ANEN2P,  0.0 )
      ANEN2P = MIN( ANEN2P, 30.0 )
      COANPL = COS( ANEN2P / 45.0 * ATAN(1.0) )
C
C     COANPL : SEUIL DU COSINUS DE L'ANGLE FORME PAR LES NORMALES AUX
C              2 FACES ET AU DESSUS DUQUEL LES FACES SONT CONSIDEREES COPLANAIRE
C              ( 0.95     => 18.2  DEGRES )
C              ( 0.96     => 16.3  DEGRES )
C              ( 0.97     => 14.1  DEGRES )
C              ( 0.98     => 11.5  DEGRES )
C              ( 0.99     =>  8.11 DEGRES )
C              ( 0.9962   =>  5    DEGRES )
C              ( 0.99756  =>  4    DEGRES )
C              ( 0.99863  =>  3    DEGRES )
C              ( 0.999    =>  2.56 DEGRES )
C              ( 0.9999   =>  0.8  DEGRES )

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10001) ANEN2P, ARETGR
      ELSE
         WRITE(IMPRIM,20001) ANEN2P, ARETGR
      ENDIF
10001 FORMAT( ' ANGLE en DEGRES de COPLANEARITE de 2 FACES =',F5.2 /
     %        ' LONGUEUR de l''ARETE de la GRILLE REGULIERE =',G14.6)
20001 FORMAT( ' DEGREES ANGLE of COPLANEARITY of 2 FACES   =',F5.2 /
     %        ' EDGE LENGTH of the REGULAR GRID            =',G14.6)

C     LE NOMBRE DE POINTS INTERNES AU VOLUME DEFINIS PAR L'UTILISATEUR
      NBPTIV = LADEFI( WBPTIV )

C     NOMBRE D'ITERATIONS DE TETRAEDRISATION
      NBVDWH = 0
      NBTETR = 0

C     LE NOMBRE DE FACES PERDUES DES INTERFACES ET DE LA FRONTIERE
      NBFAPE = 0
      MXFAPE = 0

C     NOMBRE DE SOMMETS ET POINTS INTERNES DES SURFACES DU VOLUME
C     PARTITIONE AVANT IDENTIFICATION
      NBSOCO = NBPTIV

C     PROTECTION POUR LES DESTRUCTIONS DE TABLEAUX APRES ERREUR
      MNTETS = 0
      MNSOFR = 0
      MNFACO = 0
      MNFASC = 0
      MNLIPO = 0
      MNTETO = 0
      MNARCF = 0
      MNFETO = 0
      MNOPTSUIV=0
      MN1SPAVE=0

      MNF1VO = 0
      MNFRIN = 0

      MX3T2T = 0
      MN3T2T = 0

      MNPILL = 0

      MNFAPE = 0
      MNTAST = 0
      MNTE1S = 0
      MNTRCF = 0
      MNSTSV = 0

C     SAUVEGARDE DES COORDONNEES EXTREMES DE L'OBJET DANS COMIMX
      CALL TRTATA( COOEXT, COMIMX, 6*2 )

C     RECHERCHE DES COORDONNEES COINOB DE L'HEXAEDRE ENGLOBANT
C     NOMBRE DE SOMMETS ET FACES DES SURFACES CONTOUR DU VOLUME
C     LES POINTS SONT ILS COPLANAIRES DANS X=Cte ou Y=Cte ou Z=Cte ?
C     ==============================================================
      CALL OBVOMIMX( KNMVOL, LADEFI, COINOB, NBSOCO, NBFACO, IERR )
      IF( IERR .NE. 0 ) RETURN

      IF( TRACTE ) THEN
C        SI LES TRACES SONT DEMANDES => INITIALISATION AXONOMETRIE
         CALL EFFACE
         DISTMX=MAX( COINOB(1,2) - COINOB(1,1),
     %               COINOB(2,2) - COINOB(2,1),
     %               COINOB(3,2) - COINOB(3,1) )
         AXOAVA = 0
         AXOARR = 0
         AXOLAR = DISTMX * 0.6
         AXOHAU = DISTMX * 0.6
         AXOPTV(1) = ( COINOB(1,1) + COINOB(1,2) ) / 2
         AXOPTV(2) = ( COINOB(2,1) + COINOB(2,2) ) / 2
         AXOPTV(3) = ( COINOB(3,1) + COINOB(3,2) ) / 2
         AXOEIL(1) =   COINOB(1,2)
         AXOEIL(2) =   COINOB(2,2)
         AXOEIL(3) =   COINOB(3,2)
         CALL AXONOMETRIE( AXOPTV,AXOEIL, AXOLAR,AXOHAU, AXOARR,AXOAVA )
      ENDIF

C     L'HEXAEDRE DU PAVAGE DE L'HEXAEDRE ENGLOBANT
C     POUR ACCELERER LA RECHERCHE DU PLUS PROCHE SOMMET D'UN POINT
C     ============================================================
      DO K=1,3
         HEXAPAVE(K,1) = COINOB(K,1) - ARETGR
         HEXAPAVE(K,2) = COINOB(K,2) + ARETGR
      ENDDO

C     CREATION DU PAVAGE DE L'HEXAEDRE ENGLOBANT
      CALL CREERPAVAGE( ARETGR, HEXAPAVE, NBIPAV, ECHPAV )

C     NOMBRE DE PAVES DE L'HEXAEDRE
      NBPAVE = (NBIPAV(1)+1) * (NBIPAV(2)+1) * (NBIPAV(3)+1)

C     CREATION ET INITIALISATION DU TABLEAU DU PAVAGE
C     POINTEUR SUR UN SOMMET PTXYZD DE CHAQUE PAVE
      CALL TNMCDC( 'ENTIER', NBPAVE, MN1SPAVE )
      CALL AZEROI(  NBPAVE,  MCN(MN1SPAVE) )

C     NOMBRE DE SOMMETS DES CUBES DE L'HEXAEDRE ENGLOBANT
      NBSTPV = (NBIPAV(1)+2) * (NBIPAV(2)+2) * (NBIPAV(3)+2)

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10002) ((HEXAPAVE(I,J),I=1,3),J=1,2),
     %                       NBSTPV, NBPAVE, ARETGR
      ELSE
         WRITE(IMPRIM,20002) ((HEXAPAVE(I,J),I=1,3),J=1,2),
     %                       NBSTPV, NBPAVE, ARETGR
      ENDIF

10002 FORMAT(/
     % ' COORDONNEES MIN-MAX de l''HEXAEDRE PAVE'/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 /
     % I9,' SOMMETS des',I9,' PAVES de LONGUEUR d''ARETE',G15.7)

20002 FORMAT(/
     % ' PAVAGE de l''HEXAEDRE: COORDONNEES MIN-MAX'/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 /
     % I9,' VERTICES of',I9,' CUBES of EDGE LENGTH',G15.7)

C     MAJORATION DU NOMBRE DE SOMMETS FACES TETRAEDRES
C     ================================================
C     LE NOMBRE MAXIMAL DE SOMMETS DECLARABLES
      MXSOMM = MAX( MXSOMM, NBSOCO*3, 10 000 ) + NBSTPV

C     NO DU SOMMET SUIVANT DE CHAQUE SOMMET DANS SON PAVE
      CALL TNMCDC( 'ENTIER', MXSOMM, MNOPTSUIV )
      CALL AZEROI(  MXSOMM,  MCN(MNOPTSUIV) )

C     MAJORATION DU NOMBRE DE TETRAEDRES SELON LE MAXIMUM DE SOMMETS
      MXTETR = 5 * MXSOMM

C     MAJORATION DU NOMBRE DE FACES DES SURFACES CONTOUR DU VOLUME
      IF( NBFACO .LT. 1000 ) THEN
         MXFACO = NBFACO * 4 + 128
      ELSE IF( NBFACO .LT. 5000 ) THEN
         MXFACO = NBFACO * 2
      ELSE
         MXFACO = ( NBFACO * 3 ) / 2
      ENDIF

C     IMPRESSIONS DE CES MAJORATIONS
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10010) MXSOMM, MXTETR, NBFACO
      ELSE
         WRITE(IMPRIM,20010) MXSOMM, MXTETR, NBFACO
      ENDIF
10010 FORMAT(
     %' MAX SOMMETS        =',I9/
     %' MAX TETRAEDRES     =',I9/
     %' TRIANGLES INITIAUX =',I9)
20010 FORMAT(
     %' VERTICES   MAXIMUM =',I9/
     %' TETRAHEDRA MAXIMUM =',I9/
     %' INITIAL TRIANGLES  =',I9)

C     ALLOCATION FORTRAN 90 du tableau PTXYZD(4,MXSOMM)
      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'DEMANDE ALLOCATION de',4*MXSOMM,
     %                ' DOUBLE PRECISION pour PtXYZd'
         ALLOCATE ( PTXYZD( 1:4, 1:MXSOMM ), STAT=IERZDALLOC )
         IF( IERZDALLOC .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ERREUR ALLOCATION des',4*MXSOMM,
     %                ' DOUBLE PRECISION pour PtXYZd'
            IERR = IERZDALLOC
            GOTO 9900
         ENDIF
         WRITE(IMPRIM,*) 'CORRECTE ALLOCATION de PtXYZD(1:4,1:',MXSOMM,
     %                ') DOUBLE PRECISION'
      ELSE
         WRITE(IMPRIM,*) 'ALLOCATION DEMAND  of',4*MXSOMM,
     %                ' DOUBLE PRECISION of PtXYZd'
         ALLOCATE ( PTXYZD( 1:4, 1:MXSOMM ), STAT=IERZDALLOC )
         IF( IERZDALLOC .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ALLOCATION ERROR   of',4*MXSOMM,
     %                ' DOUBLE PRECISION of PtXYZd'
            IERR = IERZDALLOC
            GOTO 9900
         ENDIF
         WRITE(IMPRIM,*) 'ALLOCATION CORRECT of PtXYZD(1:4,1:',MXSOMM,
     %                ') DOUBLE PRECISION'
      ENDIF

C     DECLARATION DES TABLEAUX DES TETRAEDRES
C     ALLOCATION FORTRAN 90 de NOTETR(8,MXTETR)
C     no des 4 SOMMETS DES TETRAEDRES et des 4 TETRAEDRES VOISINS
C     -----------------------------------------------------------
      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'DEMANDE ALLOCATION de',8*MXTETR,
     %                ' ENTIERS No DES SOMMETS des TETRAEDRES'
         ALLOCATE ( NOTETR( 1:8, 1:MXTETR ), STAT=IERTEALLOC )
         IF( IERTEALLOC .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ERREUR ALLOCATION de',8*MXTETR,
     %                ' ENTIERS No DES SOMMETS des TETRAEDRES'
            IERR = IERTEALLOC
            GOTO 9900
         ENDIF
         WRITE(IMPRIM,*) 'CORRECTE ALLOCATION de NOTETR(1:8,1:',MXTETR,
     %                ') ENTIERS No DES SOMMETS des TETRAEDRES'
      ELSE
         WRITE(IMPRIM,*) 'ALLOCATION DEMAND  of',8*MXTETR,
     %                ' INTEGER of TETRAHEDRA VERTICES NUMBER'
         ALLOCATE ( NOTETR( 1:8, 1:MXTETR ), STAT=IERTEALLOC )
         IF( IERTEALLOC .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ALLOCATION ERROR   of',8*MXTETR,
     %                ' INTEGER of TETRAHEDRA VERTICES NUMBER'
            IERR = IERTEALLOC
            GOTO 9900
         ENDIF
         WRITE(IMPRIM,*) 'ALLOCATION CORRECT of NOTETR(1:8,1:',MXTETR,
     %                ') INTEGER of TETRAHEDRA VERTICES NUMBER'
      ENDIF

C     ALLOCATION FORTRAN 90 du tableau CHATVO
C     LE CHAINAGE DES TETRAEDRES DE CHACUN DES VOLUMES
C     ------------------------------------------------
      WRITE(IMPRIM,*)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'DEMANDE ALLOCATION de',MXTETR,
     %                ' ENTIERS pour CHATVO'
         ALLOCATE ( CHATVO( 1:MXTETR ), STAT=IERCHALLOC )
         IF( IERCHALLOC .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ERREUR ALLOCATION de',MXTETR,
     %                ' ENTIERS pour CHATVO'
            IERR = IERCHALLOC
            GOTO 9900
         ENDIF
         WRITE(IMPRIM,*) 'CORRECTE ALLOCATION de CHATVO(1:',MXTETR,
     %                ') ENTIERS pour CHATVO'
      ELSE
         WRITE(IMPRIM,*) 'ALLOCATION DEMAND  of',MXTETR,
     %                ' INTEGER of CHATVO'
         ALLOCATE ( CHATVO( 1:MXTETR ), STAT=IERCHALLOC )
         IF( IERCHALLOC .NE. 0 ) THEN
            WRITE(IMPRIM,*)'ALLOCATION ERROR   of',MXTETR,
     %                ' INTEGER of CHATVO'
            IERR = IERCHALLOC
            GOTO 9900
         ENDIF
         WRITE(IMPRIM,*) 'ALLOCATION CORRECT of CHATVO(1:',MXTETR,
     %                ') INTEGER'
      ENDIF
C     -1 TEMOIN DE NON UTILISATION
C      0 POUR INDIQUER LA FIN DU CHAINAGE
      DO N = 1, MXTETR
         CHATVO( N ) = -1
      ENDDO

C     MCN(MNTETS) ( MXSOMM )  NO 1 TETRAEDRE DE CHAQUE SOMMET
      MNTETS = -1
      CALL TNMCDC( 'ENTIER', MXSOMM, MNTETS )
      IF( MNTETS .LE. 0 ) GOTO 9998
      CALL AZEROI( MXSOMM, MCN(MNTETS) )


C     CREATION DE L'HEXAEDRE ENGLOBANT A DISTANCE ARETGR
C     CONSTRUCTION DE L'HEXAEDRISATION D'ARETE ARETGR
C     TETRAEDRISATION DES CUBES DE L'HEXAEDRE ENGLOBANT
C     ==================================================
      CALL TETRHEXA( NUVOLU,   NTLXVL, ARETGR, COINOB,
     %               HEXAPAVE, NBIPAV, ECHPAV,
     %               MCN(MN1SPAVE), MCN(MNOPTSUIV),
     %               HEXAGL, MXSOMM, NBSOMM, PTXYZD,
     %               MXTETR, N1TEVI, NBTETR, NOTETR, MCN(MNTETS),
     %               NUDTETR, VOLUMT, QUAMIN, QUAMOY, IERR )
      IF( IERR .GT. 0 ) GOTO 9998

      PRINT *
      PRINT *,'tetrhexa:'
      PRINT *,'tetra 1hexa: Nombre de  SOMMETS   =', NBSOMM
      PRINT *,'tetra 1hexa: Nombre MAX SOMMETS   =', MXSOMM
      PRINT *,'tetra 1hexa: Nombre de  TETRAEDRES=', NBTETR
      PRINT *,'tetra 1hexa: Nombre MAX TETRAEDRES=', MXTETR
      PRINT *,'tetra 1hexa: No DERNIER TETRAEDRE =', NUDTETR
      PRINT *,'tetra 1hexa: Volume des TETRAEDRES=', VOLUMT
      PRINT *,'tetra 1hexa: Qualite Minimale     =', QUAMIN
      PRINT *,'tetra 1hexa: Qualite Moyenne      =', QUAMOY

C     NOMBRE DE SOMMETS DES TETRAEDRES DE L'HEXAEDRE ENGLOBANT
      NBSTHE = NBSOMM

      DO K = 1, 3
         HEXA(K,1) = REAL( HEXAGL(K,1) )
         HEXA(K,2) = REAL( HEXAGL(K,2) )
      ENDDO

C     BILAN EN TEMPS CPU
      CPU  = DINFO( 'DELTA CPU' )
      CPUT = CPUT + CPU
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,11001) CPU
      ELSE
         WRITE(IMPRIM,21001) CPU
      ENDIF
11001 FORMAT(/' TEMPS CPU TETRAEDRISATION de l''HEXAEDRE ENGLOBANT=',
     %F11.2,' SECONDES')
21001 FORMAT(/' CPU TIME of INCLUDING HEXAHEDRON TETRAHEDRIZATION=',
     %F11.2,' SECONDS')


C     RESERVATION DES TABLEAUX LES PETITS D'ABORD ET DANS UN ORDRE
C     FONCTION DES DESTRUCTIONS POUR EVITER L'EMIETTEMENT
C     ============================================================
C     RESERVATION DU TABLEAU NPSOFR POUR RETOURNER AUX COORDONNEES INITIALES
C     NPSOFR(I)= 0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C              = 1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C              = 2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C              = 1 000 000  + NUMERO DU POINT INTERNE UTILISATEUR
C              =(1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                             LA NUMEROTATION DES SOMMETS DE LA SURFACE
C              =-4 SI LE POINT EST SOMMET D'OT NON TROP PROCHE PT OU FACE
C              =-1 SI LE POINT EST SOMMET D'OT TROP PROCHE PT OU FACE
C              =-3 SI LE POINT EST SOMMET D'OT REFUSE DANS LA TETRAEDRISATION
C              =-NPSOFR(I) SI POINT DEPLACE SUR LA SURFACE
C     DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE
      MNSOFR = -1
      CALL TNMCDC( 'ENTIER', MXSOMM, MNSOFR )
      IF( MNSOFR .LE. 0 ) GOTO 9998
C     NPSOFR(I)= 0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
      CALL AZEROI( MXSOMM, MCN(MNSOFR) )

C     MCN(MNLIPO) ( 1+MXSOMM ) NO DU POINT AVANT ET APRES TRI
      MNLIPO = -1
      CALL TNMCDC( 'ENTIER', 1+MXSOMM, MNLIPO )
      IF( MNLIPO .LE. 0 ) GOTO 9998
      DO I=0,MXSOMM
         MCN(MNLIPO+I) = I
      ENDDO

C     RESERVATION DU TABLEAU LEFACO :
C     3 SOMMETS, 2 VOLUMES D'APPARTENANCE ET FACES VOISINES PAR LES 3 ARETES
      MNFACO = -1
      CALL TNMCDC( 'ENTIER', 11*MXFACO+11, MNFACO )
      IF( MNFACO .LE. 0 ) GOTO 9998

C     RESERVATION DU TABLEAU :  NO 1 FACE DANS LEFACO D'UN SOMMET
      MNFASC = -1
