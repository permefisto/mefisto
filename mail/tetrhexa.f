      SUBROUTINE TETRHEXA( NUVOLU,   NTLXVL, ARETGR, COINOB,
     %                     HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                     HEXAGL,   MXSOMM, NBSOMM, PTXYZD,
     %                     MXTETR,   N1TEVI, NBTETR, NOTETR, N1TETS,
     %                     NUDTETR,  VOLUMT, QUAMIN, QUAMOY, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES SOMMETS DES CUBES DE L'HEXAEDRE ENGLOBANT
C -----    A DISTANCE ARETGR ET LES TETRAEDRISER

C ENTREES:
C --------
C NUVOLU : NUMERO DU VOLUME PARTITION DANS LE LEXIQUE DES VOLUMES
C NTLXVL : NUMERO DU TABLEAU TS DU LEXIQUE DU VOLUME PARTITION
C ARETGR : LONGUEUR SOUHAITEE DE L'ARETE DE LA GRILLE REGULIERE
C          ET MAXIMALE DU MAILLAGE
C COINOB : LES 3 COORDONNEES MINIMALES ET MAXIMALES STRICTES
C          DES 8 SOMMETS DE L'HEXAEDRE ENGLOBANT L'OBJET
C          COINOB(I,J) = I-EME COORDONNEE MINIMALE SI J=1
C                                         MAXIMALE SI J=2

C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I DU PAVAGE
C ECHPAV  : ECHELLE DANS LA DIRECTION I DU PAVAGE
C N1SPAVE : NO DANS PTXYZD DU 1-ER SOMMET DE CHAQUE PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS PTXYZD
C MXTETR : NOMBRE MAXIMAL DE TETRAEDRES DECLARABLES DANS NOTETR

C SORTIES:
C --------
C HEXAGL : MIN ET MAX DES COORDONNEES DE L'HEXAEDRE ENGLOBANT
C          ARETGD/2 PLUS PETIT DE HEXAPAVE
C NBSOMM : NOMBRE DE SOMMETS DES CUBES DE L'HEXAEDRE ENGLOBANT
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE
C NOTETR : SOMMET1   < SOMMET2    SOMMET3     SOMMET4
C          ORDRE CLASSIQUE 1 2 3 DIRECT EN BAS ET 4 EN HAUT
C          LE SOMMET 1 EST LE PLUS PETIT
C          LE VOLUME EST POSITIF => UN ORDRE
C
C          TETRAEDRE1  TETREDRE2  TETRAEDRE3  TETREDRE4  VOISINS
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C N1TETS : N1TETS(NS) = NO NOTETR D'1 TETRAEDRE DE SOMMET NS
C N1TEVI : NUMERO NOTETR DU PREMIER TETRAEDRE VIDE CHAINE EN POSITION 5
C NUDTETR: NUMERO DU DERNIER TETRAEDRE ACTIF DANS NOTETR DU MAILLAGE
C VOLUMT : VOLUME           DES NBTETR TETRAEDRES DU MAILLAGE
C QUAMIN : QUALITE MINIMALE DES NBTETR TETRAEDRES DU MAILLAGE
C QUAMOY : QUALITE MOYENNE  DES NBTETR TETRAEDRES DU MAILLAGE
C IERR   : 0 SI PAS D'ERREUR
C          1 SATURATION DES POINTS DANS PTXYZD
C          2 TOUS LES POINTS SONT COPLANAIRES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC et St PIERRE DU PERRAY    Mars 2016
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a_volume__definition.inc"
      include"./incl/a___face.inc"
      include"./incl/a___materiaux.inc"

      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)

      REAL              ARETGR, COINOB(6,2), QUAMIN, QUAMOY, QUALTE
      DOUBLE PRECISION  ARETGD, ARETXYZ(3), VOLUMT, VOLUTE, S, D,
     %                  DEPLCT, ECARHE(3), XYZD(3),
     %                  PTXYZD(1:4,1:MXSOMM),
     %                  HEXAGL(3,2), HEXAPAVE(3,2), ECHPAV(3)
      INTEGER           NBIPAV(3), N1SPAVE(*), NOPTSUIV(MXSOMM),
     %                  NOTETR(8,MXTETR), N1TETS(MXSOMM), NBINHE(3)
      CHARACTER*24      NMVOLU, NMVOLT

      MNLFAC = 0
C     PASSAGE SIMPLE PRECISION EN DOUBLE PRECISION DE TAILLE D'ARETE
C     DE LA GRILLE
      ARETGD = ARETGR

cccC     TAILLE DE L'ARETE SELON LES AXES POUR AVOIR UNE GRILLE FINALE
cccC     D'OCTAEDRES EQUILATERAUX EN AJOUTANT ENSUITE LE BARYCENTRE DES CUBES
ccc      ARETXYZ(1) = ARETGD * 2
ccc      ARETXYZ(2) = ARETGD * 2
cccC     EN Z IL SERA AJOUTE UN BARYCENTRE QUI SERA A HAUTEUR ARETGD/SQRT(2)
ccc      ARETXYZ(3) = ARETGD * SQRT( 2D0 )
ccc      ARETXYZ(3) = ARETGD * 2


C     CONSTRUCTION DE L'HEXA ENGLOBANT A DISTANCE ARETGD DES MIN MAX
C     ==============================================================
ccc      ARETXYZ(1) = ARETGD * 3.5D0
ccc      ARETXYZ(2) = ARETGD * 3.5D0
ccc      ARETXYZ(3) = ARETGD * 3.5D0  10/2/2018

ccc      ARETXYZ(1) = ARETGD * 0.99D0
ccc      ARETXYZ(2) = ARETGD * 0.99D0
ccc      ARETXYZ(3) = ARETGD * 0.99D0 28/2/2018

ccc      ARETXYZ(1) = ARETGD * 1.666666666666D0
ccc      ARETXYZ(2) = ARETGD * 1.666666666666D0
ccc      ARETXYZ(3) = ARETGD * 1.666666666666D0 1/3/2018

ccc      ARETXYZ(1) = ARETGD * 1.333D0
ccc      ARETXYZ(2) = ARETGD * 1.333D0
ccc      ARETXYZ(3) = ARETGD * 1.333D0  18/4/2018

      ARETXYZ(1) = ARETGD * 1.6D0
      ARETXYZ(2) = ARETGD * 1.6D0
      ARETXYZ(3) = ARETGD * 1.6D0

ccc      CALL CREERPAVAGE( ARETGR, HEXAGL, NBINHE, ECARHE )
ccc      DO I=1,3
cccC        DISTANCE ENTRE 2 SOMMETS DANS LA DIRECTION I
cccC        TRES PROCHE DE ARETXYZ(I)
ccc         ECARHE(I) = 1D0 / ECARHE(I) 
ccc      ENDDO

C     NOMBRE D'INTERVALLES D'ARETGD DANS LES 3 DIRECTIONS
C     CALCUL DES 'ECHELLES' DANS LES 3 DIRECTIONS
      DO I=1,3

C        NOMBRE D'INTERVALLES D'ARETXYZ(I) DANS LA DIRECTION I
C        +2 POUR AVOIR EN PLUS ARETMAX AUX 2 EXTREMITES
         NBINHE(I) = NINT( (COINOB(I,2) - COINOB(I,1)) / ARETXYZ(I) ) +2

C        DEMI DISTANCE MIN-MAX DANS LA DIRECTION I
         D = NBINHE(I) * ARETXYZ(I) / 2D0

C        MILIEU
         S = ( COINOB(I,1) + COINOB(I,2) ) / 2D0

C        COORDONNEE I DU SOMMET MIN ET MAX DE L'HEXAEDRE
         HEXAGL(I,1) = S - D
         HEXAGL(I,2) = S + D

C        DISTANCE ENTRE 2 SOMMETS DANS LA DIRECTION I
C        TRES PROCHE DE ARETXYZ(I)
         ECARHE(I) = ( HEXAGL(I,2) - HEXAGL(I,1) ) / NBINHE(I)

      ENDDO

C     NOMBRE DE SOMMETS ET CUBES DU MAILLAGE
      NBSOM = ( NBINHE(1) + 1 ) * ( NBINHE(2) + 1 ) * ( NBINHE(3) + 1 )
      NBCUBE = NBINHE(1) *  NBINHE(2) * NBINHE(3)
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) ((HEXAGL(I,J),I=1,3),J=1,2),
     %                       NBSOM, NBCUBE, ARETGD, ECARHE,NBINHE
      ELSE
         WRITE(IMPRIM,20000) ((HEXAGL(I,J),I=1,3),J=1,2),
     %                       NBSOM, NBCUBE, ARETGD, ECARHE,NBINHE
      ENDIF

10000 FORMAT(
     % ' COORDONNEES MIN-MAX de l''HEXAEDRE ENGLOBANT les VOLUMES'/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 /
     % I9,' SOMMETS des',I9,' CUBES de LONGUEUR d''ARETE',G15.7/
     % ' LONGUEUR D''ARETE DANS LES DIRECTIONS X=',G15.7,' Y=',G15.7,
     % ' Z=',G15.7/
     % ' NOMBRE ARETES DANS LES DIRECTIONS X=',I15,' Y=',I15,' Z=',I15 )

20000 FORMAT(
     % ' MIN-MAX COORDINATES of the INCLUDING HEXAHEDRON of VOLUMES'/
     % ' XMIN=',G15.7,'  YMIN=',G15.7,'  ZMIN=',G15.7 /
     % ' XMAX=',G15.7,'  YMAX=',G15.7,'  ZMAX=',G15.7 /
     % I9,' VERTICES of',I9,' CUBES of EDGE LENGTH',G15.7/
     % ' EDGE LENGTH in DIRECTION X=',G15.7,' Y=',G15.7,' Z=',G15.7/
     % ' EDGE NUMBER in DIRECTION X=',I15,  ' Y=',I15,  ' Z=',I15 )

C     GENERATION DES SOMMETS DES CUBES DE L'HEXAEDRE STRUCTURE ENGLOBANT
C     ==================================================================
C     CONSTRUCTION DU TABLEAU 'XYZSOMMET' DE CE VOLUME
      CALL LXTNDC( NTLXVL, 'XYZSOMMET', 'MOTS',  WYZSOM + 3 * NBSOM )
      CALL LXTSOU( NTLXVL, 'XYZSOMMET',  NTSOCU, MNSOCU )
C     LE NOMBRE DE SOMMETS
      MCN( MNSOCU + WNBSOM) = NBSOM
C     CALCUL DES COORDONNEES DES SOMMETS
      MN = MNSOCU + WYZSOM

ccc      DEPLCT = ARETGD / 30D0 / MAX( NBINHE(1), NBINHE(2), NBINHE(3) )
ccc      DEPLCT = ARETGD / 15D0 / MAX( NBINHE(1), NBINHE(2), NBINHE(3) )
ccc      DEPLCT = ARETGD / 22D0 / MAX( NBINHE(1), NBINHE(2), NBINHE(3) )
ccc      DEPLCT = 0D0

      DEPLCT = ARETGD / 20D0 / MAX( NBINHE(1), NBINHE(2), NBINHE(3) ) *2

C     DEPLACEMENT DANS LES 3 DIRECTIONS DU SOMMET POUR EVITER DES FACES PLANES

      DO K=0,NBINHE(3)
         DO J=0,NBINHE(2)
            DO I=0,NBINHE(1)
               DEPLCT = -DEPLCT
               RMCN( MN   )=REAL( HEXAGL(1,1) + I * (ECARHE(1)+DEPLCT) )
               RMCN( MN+1 )=REAL( HEXAGL(2,1) + J * (ECARHE(2)+DEPLCT) )
               RMCN( MN+2 )=REAL( HEXAGL(3,1) + K * (ECARHE(3)+DEPLCT) )
               MN  = MN + 3
            ENDDO
            IF( MOD( 1+NBINHE(1), 2 ) .EQ. 0 ) THEN
               DEPLCT = -DEPLCT
            ENDIF
         ENDDO
         IF( MOD( 1+NBINHE(2), 2 ) .EQ. 0 ) THEN
            DEPLCT = -DEPLCT
         ENDIF
      ENDDO
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNSOCU) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNSOCU + WNBTGS ) = 0
C     LE NOMBRE DE COORDONNEES PAR SOMMET
      MCN( MNSOCU + WBCOOR ) = 3
      MCN( MNSOCU + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )

C     GENERATION DES CUBES EF DE L'HEXAEDRE STRUCTURE ENGLOBANT
C     =========================================================
C     CONSTRUCTION DU TABLEAU 'NSEF' DE CE VOLUME
      CALL LXTNDC( NTLXVL, 'NSEF', 'ENTIER', WBARZH+1)
      CALL LXTSOU( NTLXVL, 'NSEF',  NTCUVO,  MNCUVO )
C     MISE A JOUR DU TABLEAU 'NSEF' DE CE VOLUME
C     TYPE DE L'OBJET : VOLUME
      MCN( MNCUVO + WUTYOB ) = 4
C     LE TYPE INCONNU DE FERMETURE DU MAILLAGE
      MCN( MNCUVO + WUTFMA ) = -1
C     NUMERO DU TYPE DU MAILLAGE : HEXAEDRE STRUCTURE
      MCN( MNCUVO + WUTYMA ) = 7
C     NBARXQ NOMBRE D'ARETES EN X
      MCN( MNCUVO + WBARXH ) = NBINHE(1)
C     NBARYQ NOMBRE D'ARETES EN Y
      MCN( MNCUVO + WBARYH ) = NBINHE(2)
C     NBARYQ NOMBRE D'ARETES EN Z
      MCN( MNCUVO + WBARZH ) = NBINHE(3)
C     LE NOMBRE DE SOMMETS PAR CUBE
      MCN( MNCUVO + WBSOEF ) = 8
C     LE NOMBRE DE TANGENTES STOCKEES PAR EF : VOLUME C0
      MCN( MNCUVO + WBTGEF ) = 0
      MCN( MNCUVO + WBEFAP ) = 0
      MCN( MNCUVO + WBEFTG ) = 0
C     LE NOMBRE D'EF DE LA SURFACE
      MCN( MNCUVO + WBEFOB ) = NBINHE(1) * NBINHE(2) * NBINHE(3)
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNCUVO) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNCUVO + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )


C     GENERER UN VOLUME TETRAEDRISATION DE L'HEXAEDRISATION
C     =====================================================
C     CREATION D'UN VOLUME INTERMEDIAIRE POUR LA TETRAEDRISATION
C     NOM DU VOLUME DE NUMERO NUVOLU
      CALL NMOBNU( 'VOLUME', NUVOLU, NMVOLU )
C     NOM DU VOLUME TETRAEDRISE
      L = NUDCNB( NMVOLU )
      IF( L .LE. 20 ) THEN
         NMVOLT = NMVOLU(1:L) // '$TET'
      ELSE
         NMVOLT = NMVOLU(1:20) // '$TET'
      ENDIF

      CALL LXLXOU( NTVOLU, NMVOLT, NTLXVT, MNLXVT )
C     S'IL EXISTE IL EST DETRUIT
      IF( NTLXVT .GT. 0 ) THEN
         CALL LXLXDS( NTVOLU, NMVOLT )
      ENDIF

C     PUIS IL EST CREE et OUVERT
      CALL LXLXDC( NTVOLU, NMVOLT, 24, 8 )
      CALL LXLXOU( NTVOLU, NMVOLT, NTLXVT, MNLXVT )

C     CREATION DU TMS DE DEFINITION DU VOLUME TETRAEDRISE
      CALL LXTNDC( NTLXVT, 'DEFINITION', 'ENTIER', WUVOAS+1 )

C     OUVERTURE DU TABLEAU  'DEFINITION'
      CALL LXTSOU( NTLXVT, 'DEFINITION', NTDFVT, MNDFVT )
      IF( MNDFVT .LE. 0 ) THEN
         IERR = 1
         GOTO 9900
      ENDIF

C     REMPLISSAGE DU TMS 'DEFINITION'
      MCN( MNDFVT+WTYTRV ) = 1
C     TYPE 31 : TETRAEDRISATION d''une PYRA-PENTA-HEXAEDRISATION
      MCN( MNDFVT+WUTYVO ) = 31
C     NUMERO du volume de tetra-penta-hexaedres a tetraedriser
      MCN( MNDFVT+WUVOAS ) = NUVOLU
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNDFVT) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNDFVT + MOTVAR(6) ) = NONMTD( '~>VOLUME>>DEFINITION' )

C     TETREDRISATION DES CUBES DE L'HEXAEDRE ENGLOBANT
C     ------------------------------------------------
      CALL VOEX31( NUVOLT, NTLXVT, MCN(MNDFVT),
     %             NTTEHE, MNTEHE, NTSOHE, MNSOHE, IERR )
      IF( IERR .NE. 0 ) GOTO 9900

C     RECUPERATION DU TMS XYZSOMMET DE L'HEXAEDRE TETRAEDRISE
C     -------------------------------------------------------
      CALL LXTSOU( NTLXVT, 'XYZSOMMET', NTXYZT, MNXYZT )

C     TRANSFERT DES XYZ DES NBSOMM SOMMETS DANS PTXYZD
      NBSOMM = 0
      NBSOM  = MCN( MNXYZT + WNBSOM )
      MN     = MNXYZT + WYZSOM - 1
      DO K = 1, NBSOM

C        XYZ DU SOMMET K
         DO L = 1, 3
            XYZD(L) = RMCN(MN+L)
         ENDDO
         MN = MN + 3

C        INTEGRATION DU SOMMET K XYZD AU PAVAGE
C        NORMALEMENT AUCUNE IDENTIFICATION A UN AUTRE SOMMET DOIT ETRE FAIT
         CALL REAJSTPV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                  XYZD,     ARETGD, NBSOMM, MXSOMM,  PTXYZD,
     %                  NOSOMM )
C        SI SOMMET AJOUTE PTXYZD(4,NOSOMM)=ARETGD

CCC         DECLARATION DE PTXYZD SUFFISANTE POUR EVITER LE TEST
CCC         IF( NOSOMM .LE. 0 ) THEN
CCC            IERR = 1
CCC            GOTO 9900
CCC         ENDIF

      ENDDO

C     LES DISTANCES SOUHAITEES AUTOUR DES FUTURS SOMMETS
      DO K = NBSOMM+1, MXSOMM
C        DISTANCE INITIALISEE A ARETGD
         PTXYZD(4,K) = ARETGD
      ENDDO

C     RECUPERATION DU TMS NSEF DE L'HEXAEDRE TETRAEDRISE
C     --------------------------------------------------
      CALL LXTSOU( NTLXVT, 'NSEF', NTNSEFT, MNNSEFT )

C     NOMBRE DE SOMMETS PAR EF (ICI =8 AVEC 5678=0)
      NBSOEF = MCN( MNNSEFT + WBSOEF )

C     NOMBRE DE TETRAEDRES
      NBTETR = MCN( MNNSEFT + WBEFOB )
      NBSOEF = MCN( MNNSEFT + WBSOEF )
      NBTETR = MCN( MNNSEFT + WBEFOB )

      VOLUMT = 0D0
      QUAMIN = 2
      QUAMOY = 0

C     TRANSFERT DU NO DES 4 SOMMETS DES NBTETR TETRAEDRES DANS NOTETR
C     CALCUL DU TABLEAU N1TETS(NS)=NO NOTETR D'1 TETRAEDRE DE SOMMET NS
C     ADRESSE DU DEBUT DES NO DES SOMMETS DES TETRAEDRES
      MN = MNNSEFT + WUSOEF - 1

      DO NTE = 1, NBTETR

         DO K = 1, NBSOEF
            NOTETR( K, NTE ) = MCN( MN+K )
         ENDDO

         DO K = 1, 4
C           SOMMET K DU TETRAEDRE NTE
C           N1TETS(NS)=NO NOTETR D'1 TETRAEDRE DE SOMMET NS
            N1TETS( NOTETR( K, NTE ) ) = NTE
         ENDDO

C        CALCUL DU CENTRE DE LA BOULE CIRCONSCRITE ET DU CARRE DU RAYON
         CALL QV1TET( PTXYZD, NOTETR(1,NTE), VOLUTE, QUALTE )

         QUAMIN = MIN( QUAMIN, QUALTE )
         QUAMOY = QUAMOY + QUALTE

C        VOLUMT : VOLUME DES NBTETR TETRAEDRES DU MAILLAGE
         VOLUMT = VOLUMT + ABS( VOLUTE )
         IF( VOLUTE .LT. 0D0 ) THEN
            IERR=1
         ENDIF

         MN = MN + NBSOEF

      ENDDO

C     QUAMOY : QUALITE MOYENNE DES NBTETR TETRAEDRES DU MAILLAGE
      QUAMOY = QUAMOY / NBTETR

C     CHAINAGE DES TETRAEDRES VIDES SUIVANTS
      NUDTETR = NBTETR
      N1TEVI  = NBTETR + 1
      DO NTE = N1TEVI, MXTETR
C        LE NUMERO DU PREMIER SOMMET EST MARQUE TETRAEDRE VIDE
         NOTETR( 1, NTE ) = 0
C        LE TETRAEDRE VIDE SUIVANT
         NOTETR( 5, NTE ) = NTE + 1
      ENDDO
C     LA FIN DU CHAINAGE DES TETRAEDRES SUIVANTS VIDES
      NOTETR(5,MXTETR) = 0

C     TABLEAU DES FACES DES TETRAEDRES DU VOLUME
      MXFACE = 6 * NBTETR
      MOLFAC = 6 * MXFACE
      CALL TNMCDC( 'ENTIER', MOLFAC, MNLFAC )
      IF( MNLFAC .LE. 0 ) THEN
         IERR = 1
         GOTO 9900
      ENDIF

C     CONSTRUCTION PAR HACHAGE DES FACES DES TETRAEDRES
C     MISE A JOUR DU NO DE TETRAEDRE OPPOSE AUX FACES DANS NOTETR
C     -----------------------------------------------------------
      CALL HAFTTE( NBTETR, NOTETR, MXFACE,  MCN(MNLFAC), IERR )

C     LFACES(1,I)= NO DU 1-ER  SOMMET DE LA FACE
C     LFACES(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C     LFACES(3,I)= NO DU 3-EME SOMMET DE LA FACE
C     LFACES(4,I)= CHAINAGE HACHAGE SUR FACE SUIVANTE
C     LFACES(5,I)= NUMERO DU 1-ER  TETRAEDRE CONTENANT CETTE FACE
C                  0 SI PAS DE 1-ER  TETRAEDRE
C     LFACES(6,I)= NUMERO DU 2-EME TETRAEDRE CONTENANT CETTE FACE
C                  0 SI PAS DE 2-EME TETRAEDRE

      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,19000) NBSOMM, NBTETR
      ELSE
         WRITE(IMPRIM,29000) NBSOMM, NBTETR
      ENDIF
19000 FORMAT(I9,' SOMMETS des',I9,' TETRAEDRES de l''HEXAEDRE ENGLOBANT
     %les VOLUMES' )
29000 FORMAT(I9,' VERTICES of',I9,' TETRAHEDRA of the INCLUDING HEXAHEDR
     %ON of VOLUMES')

C     DESTRUCTION DU VOLUME TETRAEDRISE INTERMEDIAIRE
 9900 CALL LXLXDS( NTVOLU, NMVOLT )

      IF( MNLFAC .GT. 0 ) CALL TNMCDS( 'ENTIER', MOLFAC, MNLFAC )

      RETURN
      END
