      SUBROUTINE TRIANGCF( VNPLAN, NBCF,  LC1ARET, MXARET, LCHARET,
     %                     MXST,   XYZST, MXTRA,   NBTRA,  NUSTRA,
     %                     IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRIANGULER UN CONTOUR FERME D'ARETES CHAINEES D'UN MEME PLAN
C -----
C ENTREES :
C ---------
C VNPLAN : VECTEUR NORMAL AU PLAN DU CF QUI ORIENTE LES TRIANGLES
C NBCF   : NOMBRE DE CF CONTOURS FERMES D'ARETES CHAINEES DANS LCHARET
C LC1ARET: INDICE DANS LCHARET DE LA PREMIERE ARETE DE CHACUN DES NBCF
C MXARET : NOMBRE MAXIMAL D'ARETES DECLARABLES DANS LCHARET
C MXST   : NOMBRE MAXIMAL DE POINTS AJOUTES DECLARABLES DANS XYZST
C XYZST  : 3 XYZ  DES POINTS AJOUTES, SOMMET DES ARETES D'INTERSECTION
C MXTRA  : NOMBRE MAXIMAL DE TRIANGLES AJOUTES DECLARABLES DANS NUSTRA
C
C MODIFIES:
C ---------
C LCHARET: (3,MXARET) NUMERO DANS XYZST DU SOMMET 1 ET 2 DU SEGMENT,
C          CHAINAGE SUR L'ARETE SUIVANTE DANS LCHARET
C NBTRA  : NOMBRE DE TRIANGLES AJOUTES
C NUSTRA : (3,NBTRA) NO DANS XYZST DES 3 SOMMETS DES NBTRA TRIANGLES
C IERR   : =0 SI PAS D'ERREUR RENCONTREE, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET Alain LJLL UPMC & St Pierre du Perray  Octobre 2011
C2345X7..............................................................012
      include"./incl/gsmenu.inc"
      include"./incl/langue.inc"
C
      DOUBLE PRECISION VNPLAN(3), XYZST(3,MXST)
      INTEGER          LC1ARET(NBCF), LCHARET(3,MXARET), NUSTRA(3,MXTRA)
      DOUBLE PRECISION XYZPT(3), CBP1P2, SURTRD, S, QUALIT, CBPTTRA(3)
C
C     DERNIER TRIANGLE CREE AVANT CETTE TRIANGULATION
      NBTRA0 = NBTRA
      DO 1000 NUCF = 1, NBCF
C
C     LA PREMIERE ARETE DU CF
      LCH    = LC1ARET(NUCF)
      LCHDER = 0
C
C     RECHERCHE DE LA DERNIERE ARETE DANS LE CHAINAGE DU CF
      NBSG = 0
 10   IF( LCH .GT. 0 ) THEN
         NBSG   = NBSG + 1
         LCHDER = LCH
         LCH    = LCHARET( 3, LCH )
         GOTO 10
      ENDIF
C
C     LE CHAINAGE EST RENDU CYCLIQUE => TEST: ARRET SI SUIVANT=PREMIER
      LCHARET( 3, LCHDER ) = LC1ARET(NUCF)
C
C     RECHERCHE DE 2 ARETES CONSECUTIVES FORMANT UN TRIANGLE POSITIF
C     SANS INTERSECTION AVEC LES ARETES DU CF EN LEUR INTERIEUR
C     --------------------------------------------------------------
      LCH1 = LC1ARET( NUCF )
cccC     L'ARETE SUIVANTE POUR EVITER QUE LES SGI TOUS ALIGNES POSENT PROBLEME
ccc      LCH1 = LCHARET( 3, LCH1 )
C
 20   LCH2 = LCHARET( 3, LCH1 )
C
C     LES 3 SOMMETS DU TRIANGLE FORME PAR LES 2 ARETES SUCCESSIVES
      NS1  = LCHARET( 1, LCH1 )
      NS2  = LCHARET( 2, LCH1 )
      NS3  = LCHARET( 2, LCH2 )
      IF( NS1 .LE. 0 .OR. NS2 .LE. 0 .OR. NS3 .LE. 0 ) GOTO 9600
C
      IF( NBSG .EQ. 3 ) THEN
C
C        LE CF EST REDUIT A UN TRIANGLE QUI EST LE DERNIER GENERE
         IF( NBTRA .GE. MXTRA ) GOTO 9400
         NBTRA = NBTRA + 1
         NUSTRA(1,NBTRA) = NS1
         NUSTRA(2,NBTRA) = NS2
         NUSTRA(3,NBTRA) = NS3
ccc         print *,'0NUSTRA(',NBTRA,')=',(NUSTRA(M,NBTRA),M=1,3)
C
C        QUALITE DU TRIANGLE
         CALL QUATRID( NUSTRA(1,NBTRA), XYZST,  QUALIT )
         IF( QUALIT .LT. 1D-4 ) THEN
C           AFFICHAGE DU TRIANGLE NUSTRA DE MAUVAISE QUALITE
            S = SURTRD( XYZST(1,NUSTRA(1,NBTRA)),
     %                  XYZST(1,NUSTRA(2,NBTRA)),
     %                  XYZST(1,NUSTRA(3,NBTRA)) )
            print *,'3ST NUSTRA(',NBTRA,')=',(NUSTRA(M,NBTRA),M=1,3),
     %      ' SURFACE=',S,' QUALITE=',QUALIT,' est AJOUTE'
ccc            NBTRA = NBTRA - 1
         ENDIF
C
C        LE CF EST LAISSE EN L'ETAT
         GOTO 1000
C
      ENDIF
C
C     LA NORMALE A CE TRIANGLE EST ELLE DANS LA MEME DIRECTION
C     QUE CELLE DU TRIANGLE NT1?
      INV = INVTRIAD( XYZST(1,NS1), XYZST(1,NS2), XYZST(1,NS3), VNPLAN )
      IF( INV .LT. 0D0 ) THEN
C        NON: ORIENTATION OPPOSEE A CELLE DU PLAN => REJET
C        PASSAGE A L'ARETE SUIVANTE DU CF
         LCH1 = LCH2
         GOTO 20
      ENDIF
C
C     L'ARETE NS1-NS3 COUPE T ELLE UNE ARETE DU CF EN LEURS INTERIEURS?
C     L'ARETE SUIVANTE DE L'ARETE 2 DE SOMMETS  NSA1 - NSA2
      LCHA = LCHARET( 3, LCH2 )
C
 30   NSA1 = LCHARET( 1, LCHA )
      NSA2 = LCHARET( 2, LCHA )
      CALL INTARAR3( XYZST(1,NS1),  XYZST(1,NS3),
     %               XYZST(1,NSA1), XYZST(1,NSA2),
     %               NBPT, XYZPT, CBP1P2 )
C     NBPT: LE NOMBRE DE POINT D'INTERSECTION DES ARETES S1S3 ET SA1SA2
C           =1 SI LE POINT D'INTERSECTION EXISTE ET EST INTERNE AUX 2
C              ARETES (EXTREMITES INCLUSES)
C           =0 SI LE POINT D'INTERSECTION N'EXISTE PAS OU EST EXTERNE
C              STRICTEMENT A L'UNE DES 2 ARETES
      IF( NBPT .EQ. 0 .OR.
     %    CBP1P2 .LE. 1D-4 .OR. CBP1P2 .GE. 0.9999D0 ) THEN
C
C        PAS D'INTERSECTION INTERNE DE L'ARETE NSA1-NSA2
C        LE SOMMET NSA2 EST IL INTERNE AU TRIANGLE NS1 NS2 NS3?
C        LES COORDONNEES BARYCENTRIQUES DU POINT NSA2
         CALL CBPTTR( XYZST(1,NS1), XYZST(1,NS2),
     %                XYZST(1,NS3), XYZST(1,NSA2),
     %                CBPTTRA )
         IF(   CBPTTRA(1) .GT. 1D-4 .AND. CBPTTRA(1) .LT. 0.9999D0
     %   .AND. CBPTTRA(2) .GT. 1D-4 .AND. CBPTTRA(2) .LT. 0.9999D0
     %   .AND. CBPTTRA(3) .GT. 1D-4 .AND. CBPTTRA(3) .LT. 0.9999D0 )
     %   THEN
C           LE SOMMET NSA2 EST STRICTEMENT INTERNE AU TRIANGLE NS1-NS2-NS3
C           LE TRIANGLE N'EST PAS FORME
C           PASSAGE A l'ARETE DU CF SUIVANTE DU CF
            LCH1 = LCH2
            GOTO 20
         ENDIF
C
C        NSA2 EST IL STRICTEMENT INTERNE A UNE ARETE DE NS1-NS2-NS3?
         DO K=1,3
            IF( K .NE. 1 ) THEN
               K1 = K-1
            ELSE
               K1=3
            ENDIF
            IF( ABS(CBPTTRA(K)) .LT. 1D-4   .AND.
     %          CBPTTRA(K1).GT.1D-4 .AND. CBPTTRA(K1).LT.0.9999D0 )THEN
C              OUI: NSA2 EST SUR L'ARETE K+1 ET ENTRE STK+1 et STK-1
C              LE TRIANGLE N'EST PAS FORME
C              PASSAGE A l'ARETE DU CF SUIVANTE DU CF
               LCH1 = LCH2
               GOTO 20
            ENDIF
         ENDDO
C
C        NSA2 NON INTERNE AU TRIANGLE NS1-NS2-NS3
C        => PASSAGE A L'ARETE SUIVANTE DU CF
         LCHA = LCHARET( 3, LCHA )
         IF( LCHA .NE. LCH1 ) THEN
C
C           PASSAGE A L'ARETE SUIVANTE
            GOTO 30
C
         ELSE
C
C           PAS D'INTERSECTION DES ARETES ET PAS DE SOMMET INTERNE
C           => GENERATION DU TRIANGLE NS1-NS2-NS3
            IF( NBTRA .GE. MXTRA ) GOTO 9400
            NBTRA = NBTRA + 1
C           SOMMET NS1 DANS XYZST
            NUSTRA(1,NBTRA) = NS1
            NUSTRA(2,NBTRA) = NS2
            NUSTRA(3,NBTRA) = NS3
ccc            print *,'1NUSTRA(',NBTRA,')=',(NUSTRA(M,NBTRA),M=1,3)
C
C           QUALITE DU TRIANGLE
            CALL QUATRID( NUSTRA(1,NBTRA), XYZST,  QUALIT )
            IF( QUALIT .LT. 1D-4 ) THEN
C              SUPPRESSION DU TRIANGLE DANS LE TABLEAU NUSTRA
               S = SURTRD( XYZST(1,NUSTRA(1,NBTRA)),
     %                     XYZST(1,NUSTRA(2,NBTRA)),
     %                     XYZST(1,NUSTRA(3,NBTRA)) )
               print *,'-NUSTRA(',NBTRA,')=',(NUSTRA(M,NBTRA),M=1,3),
     %       ' SURFACE=',S,' QUALITE=',QUALIT,' N''EST PAS AJOUTE'
               IF( NUSTRA(1,NBTRA) .EQ. NUSTRA(2,NBTRA) .OR.
     %             NUSTRA(2,NBTRA) .EQ. NUSTRA(3,NBTRA) .OR.
     %             NUSTRA(3,NBTRA) .EQ. NUSTRA(1,NBTRA) ) GOTO 9500
               NBTRA = NBTRA - 1
C
cccc              pour arreter le calcul
ccc               print *,'PB TRIANGLE PLAT NON AJOUTE'
ccc               read  *,s
C
C              PASSAGE A L'ARETE SUIVANTE
               LCH1 = LCHARET( 3, LCH2 )
               GOTO 20
            ENDIF
C
C           REMPLACEMENT DES 2 ARETES  NS1-NS2 NS2-NS3
C           PAR LA NOUVELLE ARETE NS1-NS3
            LCHARET( 2, LCH1 ) = NS3
            LCHARET( 3, LCH1 ) = LCHARET( 3, LCH2 )
            NBSG = NBSG - 1
C           REMARQUE L'ARETE LCH2 EST PERDUE ... SANS PB
C
C           RECHERCHE D'UN NOUVEAU TRIANGLE A PARTIR DE LCH1
C           QUI EST LA NOUVELLE ARETE TRAITEE
            GOTO 20
C
         ENDIF
C
      ENDIF
C
C     INTERSECTION INTERNE
C     PASSAGE AU TRIANGLE POTENTIEL SUIVANT
      LCH1 = LCH2
      GOTO 20
C
 1000 CONTINUE
C
C     FIN DE GENERATION DES TRIANGLES
C
C     MAXIMISATION DU MININUM DES QUALITES DES TRIANGLES
C     CREES DE NBTRA0+1 A NBTRA
C     --------------------------------------------------
      CALL TRIAMAXQ( NBTRA0+1, NBTRA, NUSTRA, XYZST )
      GOTO 9990
C
C     PAS ASSEZ DE TRIANGLES AJOUTABLES
 9400 NBLGRC(NRERR) = 1
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1) = 'TRIANGCF: PAS ASSEZ DE TRIANGLES AJOUTES DECLARES'
         KERR(2) = 'A AUGMENTER dans oplor3.f'
      ELSE
         KERR(1) = 'TRIANGCF: NOT ENOUGH DECLARED ADDING TRIANGLES'
         KERR(2) = 'AUGMENT IT IN oplor3.f'
      ENDIF
      CALL LEREUR
      IERR = 1
      GOTO 9990
C
C     ERREUR DECTECTEE: NUMERO INCORRECT D'UN SOMMET DU TRIANGLE NBTRA
 9500 NBLGRC(NRERR) = 2
      WRITE(KERR(2)( 1:10),'(I10)') NUSTRA(1,NBTRA)
      WRITE(KERR(2)(11:20),'(I10)') NUSTRA(2,NBTRA)
      WRITE(KERR(2)(21:30),'(I10)') NUSTRA(3,NBTRA)
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1)='TRIANGCF: NUMEROS INCORRECTS des SOMMETS'
      ELSE
         KERR(1)='TRIANGCF: INCORRECT NUMBERS of VERTICES'
      ENDIF
      CALL LEREUR
      IERR = 2
C
C     ERREUR DECTECTEE: NUMERO INCORRECT D'UN SOMMET DU CF
C     AFFICHAGE DU CF AVEC PROBLEME
 9600 LCH = LC1ARET(NUCF)
      N   = 0
 97   IF( LCH .GT. 0 ) THEN
         print *,'CF LCHARET(',LCH,'): St1=',LCHARET(1,LCH),
     %           ' St2=',LCHARET(2,LCH),' Suivant=',LCHARET(3,LCH)
         N = N + 1
         IF( N .GE. 100 ) GOTO 99
         LCH = LCHARET( 3, LCH )
         GOTO 97
      ENDIF
C
 99   NBLGRC(NRERR) = 2
      IF( LANGAG .EQ. 0 ) THEN
         KERR(1)='TRIANGCF: NUMERO INCORRECT de SOMMETS du CF'
      ELSE
      KERR(1)='TRIANGCF: UNCORRECT NUMBER of VERTICES of CLOSED CONTOUR'
      ENDIF
      CALL LEREUR
      IERR = 2
      GOTO 9999
C
cccC     AFFICHAGE DES TRIANGLES CREES
ccc 9990 DO K = NBTRA0+1, NBTRA
ccc         S = SURTRD( XYZST(1,NUSTRA(1,K)),
ccc     %               XYZST(1,NUSTRA(2,K)),
ccc     %               XYZST(1,NUSTRA(3,K)) )
ccc         CALL QUATRID( NUSTRA(1,K), XYZST,  QUALIT )
ccc         print *,'Sous-Triangle NUSTRA(',K,')=',(NUSTRA(M,K),M=1,3),
ccc     %   ' Surface=',S,' Qualite=',QUALIT
ccc      ENDDO
C
 9990 CONTINUE
 9999 RETURN
      END
