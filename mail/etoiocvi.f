      SUBROUTINE ETOIOCVI( N1FEOC, N1FEVI, NFETOI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RENDRE VIDES LES FACES OCCUPEES DU TABLEAU NFETOI
C -----    DES FACES DE L'ETOILE => TOUTES LES FACES SONT VIDES

C ENTREES ET SORTIES :
C --------------------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE OCCUPEE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : 1 A 4: NON UTILISES ICI
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS        MARS 1993
C23456...............................................................012
      INTEGER  NFETOI(5,*)

C     REINITIALISATION A VIDE DES FACES OCCUPEES DE NFETOI
      NF0 = 0
      NF1 = N1FEOC
 10   IF( NF1 .GT. 0 ) THEN

C        POUR INDIQUER LA VERSION 1 DE NFETOI
         NFETOI(3,NF1) = 0
C        NUMERO DE LA FACE DANS LEFACO
         NFETOI(4,NF1) = 0
C        PASSAGE A LA FACE SUIVANTE
         NF0 = NF1
         NF1 = NFETOI( 5, NF1 )
         GOTO 10

      ENDIF

C     FIN DU CHAINAGE DES FACES VIDES
      IF( NF0 .GT. 0 ) THEN
C        LA DERNIERE FACE OCCUPEE EST CHAINEE SUR LA 1-ERE VIDE
         NFETOI( 5, NF0 ) = N1FEVI
C        LA 1-ERE OCCUPEE DEVIENT LA 1-ERE VIDE
         N1FEVI = N1FEOC
      ENDIF

C     IL N'Y A PAS DE FACES OCCUPEES DANS L'ETOILE
      N1FEOC = 0

      RETURN
      END
