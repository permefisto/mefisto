      SUBROUTINE COPOBJ( NMTOBJ , NUOBJ1 , NUOBJ2 ,
     %                   NTSUS2 , MNSUS2 , NTSOM2 , MNSOM2 , IERR )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    COPIER LES TABLEAUX 'XYZSOMMET' ET EVENTUELLEMENT 'NSEF'
C -----    D'UN OBJET DE TYPE NMTOBJ DE NUMERO NUOBJ1 DANS L'OBJET NUOBJ2
C
C ENTREES:
C --------
C NMTOBJ : NOM DU TYPE DE L'OBJET
C NUOBJ1 : NUMERO DE L'OBJET INITIAL DANS LE LEXIQUE DES OBJETS
C NUOBJ2 : NUMERO DE L'OBJET FINAL   DANS LE LEXIQUE DES OBJETS
C          LE TABLEAU DE DEFINITION D'UNE TRANSFORMATION EST DECRIT DANS
C          LE FICHIER  ~TD/DAT/A_TRANSFO__DEFINITION
C SORTIES:
C --------
C NTSUS2 : NUMERO      DU TABLEAU 'NSEF' DE L'OBJET , 0 SI POINT
C MNSUS2 : ADRESSE MCN DU TABLEAU 'NSEF' DE L'OBJET , 0 SI POINT
C NTSOM2 : NUMERO      DU TABLEAU 'XYZSOMMET' DE L'OBJET
C MNSOM2 : ADRESSE MCN DU TABLEAU 'XYZSOMMET' DE L'OBJET
C IERR   : 0   PAS D'ERREUR  , >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE UPMC PARIS  OCTOBRE 1988
C23456---------------------------------------------------------------012
      IMPLICIT          INTEGER (W)
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU , IMPRIM , INTERA , NUNITE(29)
      include"./incl/ntmnlt.inc"
      include"./incl/a_point__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a_transfo__definition.inc"
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (DMCN(1),RMCN(1),MCN(1))
C
      CHARACTER*(*)     NMTOBJ
      CHARACTER*9       KTYPE
C
C     NUMERO TS DU LEXIQUE DES OBJETS DE CE TYPE D'OBJETS
      NUTYOB = NTYOBJ( NMTOBJ )
      NTLX   = NTMN  ( NUTYOB )
C
C     LE NUMERO DE TMS DU LEXIQUE DE L'OBJET INITIAL
      CALL LXNLOU( NTLX , NUOBJ1 , NTLXO1 , MNLXO1)
      IF( NTLXO1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'COPOBJ:OBJET INITIAL INCONNU'
         CALL LEREUR
         IERR = 1
         GOTO 9999
      ENDIF
C
C     LE NUMERO DE TMS DU LEXIQUE DE L'OBJET
      CALL LXNLOU( NTLX , NUOBJ2 , NTLXO2 , MNLXO2 )
      IF( NTLXO2 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'COPOBJ:OBJET FINAL INCONNU'
         CALL LEREUR
         IERR = 1
         GOTO 9999
      ENDIF
C
C     LE TABLEAU 'XYZSOMMET' DE CET OBJET EST LA COPIE DE
C     CELUI DE L'OBJET INITIAL
C     =================================================
      CALL LXTSOU( NTLXO1 , 'XYZSOMMET' , NTSOM1 , MNSOM1 )
      IF( NTSOM1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'COPOBJ:TABLEAU ''XYZSOMMET'' INCONNU'
         CALL LEREUR
         IERR = 2
         GOTO 9999
      ENDIF
C     LE NOMBRE DE SOMMETS DE L'OBJET INITIAL
      NBSOM = MCN( MNSOM1 + WNBSOM )
C     LE NOMBRE DE TANGENTES DE L'OBJET INITIAL
      NBTGS = MCN( MNSOM1 + WNBTGS )
C
C     CONSTRUCTION DU TABLEAU 'XYZSOMMET' DE L'OBJET FINAL
      CALL LXTNDC( NTLXO2, 'XYZSOMMET', 'MOTS' , WYZSOM+3*(NBSOM+NBTGS))
      CALL LXTSOU( NTLXO2, 'XYZSOMMET',  NTSOM2, MNSOM2 )
C
C     COPIE DES COORDONNEES DE L'OBJET INITIAL DANS L'OBJET FINAL
      CALL TRTATA( MCN(MNSOM1) , MCN(MNSOM2) , WYZSOM+3*(NBSOM+NBTGS) )
C
C     MISE A JOUR DE LA DATE
      CALL ECDATE( MCN(MNSOM2) )
C
C     LE TABLEAU 'NSEF' DE CET OBJET EST LA COPIE DE
C     CELUI DE L'OBJET INITIAL
C     ====================================================
      IF( NUTYOB .LE. 1 ) THEN
C        PAS DE NSEF
         NTSUS2 = 0
         MNSUS2 = 0
         GOTO 9000
      ENDIF
C
      CALL LXTSOU( NTLXO1 , 'NSEF' , NTSUS1 , MNSUS1 )
      IF( NTSUS1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'COPOBJ:TABLEAU ''NSEF'' INCONNU'
         CALL LEREUR
         IERR = 3
         GOTO 9999
      ENDIF
C
C     LE NOMBRE DE MOTS DE CE TABLEAU
      CALL TAMSTV( NTSUS1 , KTYPE , NBVARI )
      NBMOTS = MOTVAR( NUMTYP( KTYPE ) ) * NBVARI
C
C     CONSTRUCTION DU TABLEAU 'NSEF' DE L'OBJET FINAL
      CALL LXTNDC( NTLXO2 , 'NSEF' , 'MOTS'  , NBMOTS )
      CALL LXTSOU( NTLXO2 , 'NSEF' ,  NTSUS2 , MNSUS2 )
C
C     COPIE DES NSEF DE L'OBJET INITIAL DANS L'OBJET FINAL
      CALL TRTATA( MCN(MNSUS1) , MCN(MNSUS2) , NBMOTS )
C
C     MISE A JOUR DE LA DATE
      CALL ECDATE( MCN(MNSUS2) )
C
 9000 IERR = 0
C
C     ERREUR
C     ======
 9999 RETURN
      END
