      SUBROUTINE N5TRVS( NT, NST, LETREE, N5TRV )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LE NUMERO LETREE DES 15 TE VOISINS DU TE NT
C -----    PAR LE SOMMET NST ( 1 A 3 ) DE MEMES NIVEAUX
C
C ENTREES:
C --------
C NT     : NUMERO LETREE DU TE DE TE VOISINS A CALCULER
C NST    : NUMERO 1 A 3 DU SOMMET DU TRIANGLE NT
C LETREE : ARBRE-4 DES TRIANGLES EQUILATERAUX (TE) FOND DE LA TRIANGULATION
C      LETREE(0,0)  NO DU 1-ER TE VIDE DANS LETREE
C      LETREE(0,1) : MAXIMUM DU 1-ER INDICE DE LETREE (ICI 8)
C      LETREE(0,2) : MAXIMUM DECLARE DU 2-EME INDICE DE LETREE (ICI MXTREE)
C      LETREE(0:8,1) : RACINE DE L'ARBRE  (TRIANGLE SANS SUR TRIANGLE)
C      SI LETREE(0,.)>0 ALORS
C         LETREE(0:3,J) : NO (>0) LETREE DES 4 SOUS-TRIANGLES DU TRIANGLE J
C      SINON
C         LETREE(0:3,J) :-NO PXYD DES 1 … 4 POINTS INTERNES AU TRIANGLE J
C                         0  SI PAS DE POINT
C                        ( J EST ALORS UNE FEUILLE DE L'ARBRE )
C      LETREE(4,J) : NO LETREE DU SUR-TRIANGLE DU TRIANGLE J
C      LETREE(5,J) : 0 1 2 3 NO DU SOUS-TRIANGLE J POUR SON SUR-TRIANGLE
C      LETREE(6:8,J) : NO PXYD DES 3 SOMMETS DU TRIANGLE J
C
C SORTIES :
C ---------
C N5TRV   : NUMERO LETREE DES 5 TE VOISINS PAR LE SOMMET NST
C           DANS LE SENS DIRECT
C           0 SI PAS DE TE VOISIN (RACINE , ... )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1992
C2345X7..............................................................012
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      INTEGER           LETREE(0:8,0:*),N5TRV(5)
C
C     INITIALISATION D'UN DRAPEAU
      N5TRV(3) = -1
C
C     ESSAI DANS LE SENS DIRECT AUTOUR DU SOMMET NST DE NT
      LAR = NOPRE3( NST )
      CALL N1TRVA( NT, LAR, LETREE, N5TRV(1), NIVEAU )
      IF( N5TRV(1) .LE. 0 .OR.
     &  ( NIVEAU .GT. 0 .AND. LAR .EQ. LETREE(5,NT) ) ) THEN
C        PLUS DE VOISIN DANS CE SENS OU
C        VOISIN PLUS GRAND POUR UN SOMMET EN FAIT MILIEU D'ARETE DU SUR-TRIANGLE
         N5TRV(2) = N5TRV(1)
         N5TRV(3) = N5TRV(1)
         GOTO 20
      ENDIF
C
C     IL EXISTE UN TRIANGLE VOISIN DE MEME TAILLE => LE 2-EME TRIANGLE
      LAR = NOPRE3( LAR )
      CALL N1TRVA( N5TRV(1), LAR, LETREE, N5TRV(2), NIVEAU )
C
C     L'EVENTUEL 3-EME TRIANGLE
      IF( N5TRV(2) .GT. 0 ) THEN
         LAR = NOPRE3( LAR )
         CALL N1TRVA( N5TRV(2), LAR, LETREE, N5TRV(3), NIVEAU )
      ENDIF
C
C     ESSAI DANS LE SENS INDIRECT AUTOUR DU SOMMET NST DE NT
 20   CALL N1TRVA( NT, NST, LETREE, N5TRV(5), NIVEAU )
      IF( N5TRV(5) .LE. 0 .OR.
     &  ( NIVEAU .GT. 0 .AND. NST .NE. LETREE(5,NT) ) ) THEN
C        PLUS DE VOISIN DANS CE SENS OU
C        VOISIN PLUS GRAND POUR UN SOMMET EN FAIT MILIEU D'ARETE DU SUR-TRIANGLE
         N5TRV(4) = N5TRV(5)
         N5TRV(3) = N5TRV(5)
         GOTO 30
      ENDIF
C
C     IL EXISTE UN TRIANGLE VOISIN DE MEME TAILLE => LE 4-EME TRIANGLE
      LAR = NOSUI3( NST )
      CALL N1TRVA( N5TRV(5), LAR, LETREE, N5TRV(4), NIVEAU )
C
C     L'EVENTUEL 3-EME TRIANGLE S'IL N'A PAS ETE RETROUVE
      IF( N5TRV(3) .LT. 0 ) THEN
         IF( N5TRV(4) .GT. 0 ) THEN
            LAR = NOSUI3( LAR )
            CALL N1TRVA( N5TRV(4), LAR, LETREE, N5TRV(3), NIVEAU )
         ELSE
C           PAS DE TRIANGLE VOISIN 3
            N5TRV(3) = 0
         ENDIF
      ENDIF
 30   RETURN
      END
