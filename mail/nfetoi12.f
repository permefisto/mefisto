      SUBROUTINE NFETOI12( NOTETR, N1FEOC, NFETOI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    MODIFIER LES VALEURS DU TABLEAU NFETOI DE L'ETOILE de la
C -----    Version1 en la Version2

C ENTREES:
C --------
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)

C MODIFIES:
C ---------
C NFETOI : EN ENTREE VERSION 1
C          1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DANS LEFACO DE LA FACE, 0 SINON
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C             =0 SI DERNIER DU CHAINAGE

C          EN SORTIE VERSION 2
C          1: NUMERO NOTETR DU TETRAEDRE EXTERIEUR OPPOSE A LA FACE
C          2: NUMERO PTXYZD DU 1-ER SOMMET DE LA FACE
C          3: NUMERO PTXYZD DU 2-ME SOMMET DE LA FACE
C          4: NUMERO PTXYZD DU 3-ME SOMMET DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR DE
C             L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC ET ST PIERRE DU PERRAY Octobre 2016
C2345X7..............................................................012
      INTEGER  NOTETR(8,*), NFETOI(5,*)

      INTEGER  NOSOFA(3,4)
      DATA     NOSOFA / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C     => LA NORMALE A LA FACE EST ORIENTEE VERS L'EXTERIEUR DU TETRAEDRE

C     BOUCLE SUR LES FACES CHAINEES DE L'ETOILE N1FEOC
C     CODAGE en VERSION 2 DU TABLEAU NFETOI DES FACES DE L'ETOILE
C     -----------------------------------------------------------
      NF1 = N1FEOC

 10   IF( NF1 .GT. 0 ) THEN

C        LE NO DU TETRAEDRE NT1 INTERNE A L'ETOILE ET NO I1 LOCAL DE LA FACE
         NT1 = NFETOI(1,NF1)
         I1  = ABS( NFETOI(2,NF1) )

C        LE NUMERO DU TETRAEDRE NT2 AU DELA DE LA FACE
         NT2 = NOTETR( 4+I1, NT1 )
         NFETOI(1,NF1) = NT2

C        NUMERO DES 3 SOMMETS DE LA FACE COMMUNE EN SENS INVERSE
C        POUR OBTENIR UNE NORMALE A LA FACE DANS LE BON SENS
C        C-A-D ORIENTEE VERS L'INTERIEUR DU TETRAEDRE NT1
C        STOCKAGE DANS NFETOI(2:4,NF1)
         NFETOI(2,NF1) = NOTETR( NOSOFA(1,I1), NT1 )
         NFETOI(3,NF1) = NOTETR( NOSOFA(3,I1), NT1 )
         NFETOI(4,NF1) = NOTETR( NOSOFA(2,I1), NT1 )

C        PASSAGE A LA FACE SUIVANTE
         NF1 = NFETOI(5,NF1)
         GOTO 10

      ENDIF

      RETURN
      END
