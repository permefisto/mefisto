      SUBROUTINE RE6CUB( NTLXOB, NTEF6C, MNEF6C, MNST6C )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RENUMEROTER LES SOMMETS DES 6CUBES POUR REDUIRE LA DEMI
C -----    LARGEUR DE BANDE DU MAILLAGE
C
C ENTREES:
C --------
C NTLXOB : NUMERO DU TABLEAU TS DU LEXIQUE DU 6CUBE
C NTEF6C : NUMERO      DU TMS 'NSEF' DES NUMEROS DES 6-CUBES
C MNEF6C : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES 6-CUBES
C          CF '~td/d/a___nsef'
C MNST6C : ADRESSE MCN DU TMS 'XYZSOMMET' DU MAILLAGE DES 6-CUBES
C          CF '~td/d/a___xyzsommet'
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St Pierre du Perray  Fevrier 2009
C23456................................................................72
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
      DOUBLE PRECISION  DINFO, DCPU
      DCPU   = DINFO( 'DELTA CPU' )
      IERR = 0
C
C     NOMBRE DE SOMMETS DU MAILLAGE
      NBSOM = MCN( MNST6C + WNBSOM )
C
C     NOMBRE DE 6CUBES DU MAILLAGE
      NB6CUB = MCN( MNEF6C + WBEFOB )
C
C     MAILLAGE STRUCTURE OU NON?
      NUTYMA = MCN( MNEF6C + WUTYMA )
      IF( NUTYMA .GT. 0 ) THEN
C
C        MAILLAGE NSEF STRUCTURE DE 6CUBES => NSEF NON STRUCTURE
         CALL STNOST( NTLXOB, NTEF, MNEF, IERR )
         IF( IERR .NE. 0 ) RETURN
         NTEF6C = NTEF
         MNEF6C = MNEF
C
      ENDIF
C
C     DECLARATION DES TABLEAUX AUXILIAIRES
      MNNUST = 0
      MNNAST = 0
      MN16CS = 0
      MNE6CS = 0
      CALL TNMCDC( 'ENTIER', NBSOM, MNNUST )
      CALL TNMCDC( 'ENTIER', NBSOM, MNNAST )
      CALL TNMCDC( 'ENTIER', NBSOM, MN16CS )
      MO6CSU = 64 * NB6CUB
      CALL TNMCDC( 'ENTIER', 2*MO6CSU, MNE6CS )
C
C     LA RENUMEROTATION FRONTALE A PARTIR DU DERNIER SOMMET
C     ADRESSE MCN DES NO DE SOMMETS DES 6CUBES
      CALL REST6C( NBSOM, NB6CUB, MCN(MNEF6C+WUSOEF),
     %             MCN(MN16CS), MO6CSU, MCN(MNE6CS),
     %             MCN(MNNUST), MCN(MNNAST), IERR )
C
C     DESTRUCTION DES TABLEAUX AUXILIAIRES INUTILES
      CALL TNMCDS( 'ENTIER', 2*MO6CSU, MNE6CS )
      CALL TNMCDS( 'ENTIER', NBSOM, MN16CS )
      CALL TNMCDS( 'ENTIER', NBSOM, MNNAST )
      IF( IERR .NE. 0 ) THEN
         WRITE(IMPRIM,*) 'NON RENUMEROTATION DES SOMMETS DES 6CUBES'
         GOTO 9000
      ENDIF
C
C     COPIE AVEC RENUMEROTATION DES UVWXYZ DES SOMMETS
      CALL TNMCDC( 'REEL', 6*NBSOM, MNUVWN )
      MNUVWA = MNST6C + WYZSOM
      CALL TRTATA( MCN(MNUVWA), MCN(MNUVWN), 6*NBSOM )
C     ANCIENNE NUMEROTATION DES UVWXYZ
      MNA = MNUVWN - 1
C     NOUVELLE NUMEROTATION DES UVWXYZ
      MNW = MNUVWA - 1
      DO 20 N = 1, NBSOM
C        NOUVEAU NUMERO DU SOMMET
         NEW = MCN( MNNUST - 1 + N )
         MN =  MNW + 6*NEW - 6
         DO 10 K=1,6
            RMCN( MN + K ) = RMCN( MNA + K )
 10      CONTINUE
         MNA = MNA + 6
 20   CONTINUE
      CALL TNMCDS( 'REEL', 6*NBSOM, MNUVWN )
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNST6C) )
C
C     RENUMEROTATION DES SOMMETS DANS NSEF DIRECTEMENT
      MNA = MNEF6C + WUSOEF - 1
      DO 40 N=1,NB6CUB
         DO 30 K=1,64
C           ANCIEN NUMERO DU SOMMET K DU 6CUBE N
            MNA = MNA + 1
            NST = MCN( MNA )
C           NOUVEAU NUMERO DU SOMMET
            NEW = MCN( MNNUST - 1 + NST )
C           RENUMEROTATION
            MCN( MNA ) = NEW
 30      CONTINUE
 40   CONTINUE
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNEF6C) )
C
 9000 CALL TNMCDS( 'ENTIER', NBSOM, MNNUST )
      DCPU = DINFO( 'DELTA CPU' )
      WRITE(IMPRIM,*) DCPU,' SECONDES CPU POUR RENUMEROTER LES 6CUBES'
C
      RETURN
      END
