      SUBROUTINE TRVITE3D( NBPOI,    XYZPOI,
     %                     MOARFR,   MXARFR,   L1ARFR, LAREFR,
     %                     SEMINVIT, SEMAXVIT, CMVITE,
     %                     NCAS,     VITX, VITY, VITZ, VITMAX )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES ARETES FRONTALIERES, LES FLECHES VITESSES
C -----    EN TOUS LES POINTS DES EF 3D D'UN NCAS
C
C ENTREES:
C --------
C NBPOI  : NOMBRE DE POINTS STOCKES DANS XYZPOI
C XYZPOI : XYZ DES POINTS DES EF DU MAILLAGE
C MOARFR : NOMBRE DE MOTS PAR ARETE FRONTALIERE DU TABLEAU LAREFR
C MXARFR : NOMBRE DE FACES DU TABLEAU LAREFR
C L1ARFR : NUMERO DANS LAREFR DE LA PREMIERE ARETE FRONTALIERE
C LAREFR : TABLEAU NUMERO DES 2 SOMMETS ET LIEN
C          LAREFR(1,I)= NO DU 1-ER  SOMMET DE L'ARETE FRONTALIERE
C          LAREFR(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LAREFR(3,I)= 0 OU NUMERO DE L'ARETE FRONTALIERE SUIVANTE
C SEMINVIT: MIN DE LA NORME DE LA VITESSE AU DESSOUS DUQUEL LA
C           FLECHE NE DOIT PAS ETRE TRACEE
C SEMAXVIT: MAX DE LA NORME DE LA VITESSE AU DESSUS DUQUEL LA
C           FLECHE NE DOIT PAS ETRE TRACEE
C CMVITE : NOMBRE DE CM POUR L'UNITE DE VITESSE
C NCAS   : NUMERO DU CAS A TRAITER
C VITX   : COMPOSANTE X DE LA VITESSE EN CHAQUE NOEUD DES TETRAEDRES
C VITY   : COMPOSANTE Y DE LA VITESSE EN CHAQUE NOEUD DES TETRAEDRES
C VITZ   : COMPOSANTE Z DE LA VITESSE EN CHAQUE NOEUD DES TETRAEDRES
C VITMAX : VITESSE MAXIMALE POUR DEFINIR LA COULEUR DANS LA PALETTE
C
C REMARQUE:
C          LA FONCTION UTILISATEUR Region(t,x,y,z) PEUT LIMITER LA
C          REGION OU TRACER LA FLECHE DE LA VITESSE
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire JL LIONS UPMC Paris  Fevrier 2007
C MODIFS : ALAIN PERRONNET Veulettes sur mer                Aout    2020
C MODIFS : ALAIN PERRONNET Saint Pierre du Perray           Mars    2021
C ...................................................................012
      PARAMETER     (LIGCON=0, LIGTIR=1)
      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/ctemps.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      DOUBLE PRECISION  VITX(NBPOI),
     %                  VITY(NBPOI),
     %                  VITZ(NBPOI)
      REAL              XYZPOI(3,NBPOI)
      INTEGER           LAREFR(1:MOARFR,1:MXARFR)
      REAL              DIRFLE(3), DISTCM
      DOUBLE PRECISION  SEMINVIT, SEMAXVIT, VITNORM, VITMAX,
     %                  DPARAF(4), DSREGI

C     EXISTENCE OU NON DE LA FONCTION 'REGION'
C     ========================================
      CALL LXNMNO( NTFONC, 'REGION', NOFORE, I )
C     NOFORE>0 SI CETTE FONCTION EXISTE

C     LA PALETTE 2 : LES COULEURS VARIENT RAPIDEMENT ET S'ASSOMBRISSENT
C     LA PALETTE 11: ARC EN CIEL
      CALL PALCDE(11)

C     LE NOMBRE-1 DE COULEURS DISPONIBLES
      NBCOUL = NDCOUL - N1COUL

C     LES ARETES SONT TRACEES EN CONTINU
      CALL XVTYPETRAIT( LIGCON )

C     LA DIRECTION DE VISEE PTV-OEIL
      DIREVI(1) = AXOEIL(1) - AXOPTV(1)
      DIREVI(2) = AXOEIL(2) - AXOPTV(2)
      DIREVI(3) = AXOEIL(3) - AXOPTV(3)

      CALL T3PLAV
      CALL TRAXE3
C     AUCUN ITEM SUR L'ECRAN
      CALL ITEMS0

      IF( NOFORE .GT. 0 ) THEN
C        LIMITATION du TRACE PAR LA FONCTION 'REGION(t,x,y,z)'
C        -----------------------------------------------------
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10011) NCAS
         ELSE
            WRITE(IMPRIM,20021) NCAS
         ENDIF
10011    FORMAT(' CAS',I6,
     %   ': VITESSES dans la REGION DEFINIE par la FONCTION REGION(t,x,y
     %,z)')
20021    FORMAT(' CASE',I6,
     %   ': VELOCITIES in the REGION DEFINED by the FUNCTION REGION(t,x,
     %y,z)')
      ENDIF

      N = L1ARFR
 10   IF( N .GT. 0 ) THEN

C        TRACE DE L' ARETE FRONTALIERE DES VOLUMES DE L'OBJET
C        ----------------------------------------------------
C        LE NUMERO DE L'ARETE DANS LE TABLEAU LAREFR
         CALL XVEPAISSEUR( 0 )
         CALL TRAIT3D( NCOUAF, XYZPOI(1,LAREFR(1,N)),
     %                         XYZPOI(1,LAREFR(2,N)) )

C        PASSAGE A L'ARETE SUIVANTE
         N = LAREFR(3,N)
         GOTO 10

      ENDIF

C     FLECHE VITESSE EN TOUS LES NOEUDS SAUF NON INCLUS DANS LA REGION
C     ----------------------------------------------------------------
      IF( NPAFLE .LE. 0 ) NPAFLE = 1
      CALL XVEPAISSEUR( NEPFLE )
ccc      CALL XVEPAISSEUR( 2 )

      DO 20 N=1,NBPOI

         IF( MOD(N,NPAFLE) .NE. 0 ) GOTO 20

ccc         print 10020,N,(XYZPOI(K,N),K=1,3),VITX(N),
ccc     %                  VITY(N),VITZ(N)
ccc10020    FORMAT('XYZ',I3,'=',3G13.4,'  VXYZ=',3G15.6)

         IF( NOFORE .GT. 0 ) THEN
C           LES 4 PARAMETRES D'APPEL DE LA FONCTION 'REGION'
C           LE TEMPS EN 1-ER PARAMETRE
            DPARAF(1) = TEMPS
C           PUIS LES 3 COORDONNEES X Y Z DU NOEUD N
            DO K=1,3
               DPARAF(1+K) = XYZPOI(K,N)
            ENDDO
C           FONCTION REGION(TEMPS,X,Y,Z)
            CALL FONVAL( NOFORE, 4, DPARAF, NCODEV, DSREGI )
            IF( NCODEV .EQ. 0 .OR. DSREGI .EQ. 0D0 ) GOTO 20
C           ICI TRACE DE LA FLECHE
         ENDIF

C        MODULE DE LA FLECHE VITESSE AU POINT
         VITNORM = SQRT( VITX(N)**2 + VITY(N)**2 + VITZ(N)**2 )

C        AU DESSOUS DU SEUIL MIN PAS DE TRACE DE LA FLECHE
         IF( VITNORM .LT. SEMINVIT ) GOTO 20
C        AU DESSUS DU SEUIL MAX PAS DE TRACE DE LA FLECHE
         IF( VITNORM .GT. SEMAXVIT ) GOTO 20

C        LONGUEUR DE LA FLECHE
         DIRFLE(1) = REAL( VITX(N) * CMVITE )
         DIRFLE(2) = REAL( VITY(N) * CMVITE )
         DIRFLE(3) = REAL( VITZ(N) * CMVITE )
         DISTCM    = REAL( VITNORM * CMVITE )

C        COULEUR DU BOIS DE LA FLECHE
         NOCOUL = NINT( VITNORM / VITMAX * NBCOUL + N1COUL )
         IF( NOCOUL .GT. NDCOUL ) NOCOUL = NDCOUL
         CALL T3FLEC( NOCOUL, XYZPOI(1,N), DISTCM, DIRFLE )

 20   ENDDO

C     FINITION DU TRACE DES 3 PLANS PAR TRACE DES 3 ARETES VUES
      CALL T3PLAP
      CALL XVEPAISSEUR( 0 )

      RETURN
      END
