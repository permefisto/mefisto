      SUBROUTINE NOTEFO( NS1, NS2, NS3, NT0,
     %                   NBSOTE, NOSOTE, NO1TSO, NOTESO,
     %                   NF, NT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   RETROUVER LE NUMERO NT DANS NOSOTE DU TETRAEDRE OPPOSE PAR LA
C -----   FACE DE SOMMETS NS1 NS2 NS3 DU TETRAEDRE NT0
C
C ENTREES:
C --------
C NS1 NS2 NS3 : LES 3 SOMMETS D'UNE FACE DE NT0
C NT0    : NUMERO DU TETRAEDRE DE FACE A TRAITER
C          SI NT0=0 ALORS CE SP DONNE LE PREMIER TETRAEDRE DE FACE NS123
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NOSOTE(>3)
C NOSOTE : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C NOTESO : NOTESO(1,*) NUMERO DANS NOSOTE DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER
C SORTIES :
C ---------
C NF     : NUMERO LOCAL DE LA FACE DANS NT, 0 SI PAS DE TETRAEDRE OPPOSE
C NT     : NUMERO DANS NOSOTE DU TETRAEDRE OPPOSE
C          0 S'IL N'EN EXISTE PAS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   DECEMBRE 1991
C....................................................................012
      INTEGER           NOSOTE(NBSOTE,*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*)
      INTEGER           NSF(3)
C
C     LES 3 SOMMETS CROISSANTS DE LA FACE DE NT0
      NSF( 1 ) = NS1
      NSF( 2 ) = NS2
      NSF( 3 ) = NS3
      CALL TRI3NO( NSF, NSF )
C
C     PARCOURS DES TETRAEDRES DE SOMMET NS1
      NDT = NO1TSO( NS1 )
C
C     TANT QU'IL EXISTE UN TETRAEDRE DE SOMMET NS1 FAIRE
 10   IF( NDT .GT. 0 ) THEN
C
C        LE NUMERO DU TETRAEDRE DANS NOSOTE
         NT = NOTESO(1,NDT)
         IF( NT .NE. NT0 ) THEN
C           NUMERO DE LA FACE NF DANS NT EVENTUELLEMENT
            CALL NO1F1T( NSF, NOSOTE(1,NT), NF )
            IF( NF .GT. 0 ) RETURN
         ENDIF
C
C        LE TETRAEDRE SUIVANT
         NDT = NOTESO(2,NDT)
         GOTO 10
      ENDIF
C
C     PAS DE TEL TETRAEDRE OPPOSE
      NT = 0
      NF = 0
C
      RETURN
      END
