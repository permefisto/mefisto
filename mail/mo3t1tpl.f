      SUBROUTINE MO3T1TPL( NOTRIA, NT, NS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  REMPLACER LES 3 TRIANGLES COPLANAIRES, AYANT LE SOMMET NS DE NT
C -----  COMME CENTRE D'UN GRAND TRIANGLE, PAR CE GRAND TRIANGLE
C        ET DETRUIRE LES 2 AUTRES TRIANGLES

C ENTREES:
C --------
C NOTRIA : NUMERO DES 3 SOMMETS ET 3 TRIANGLES ADJACENTS PAR LES ARETES

C MODIFIE:
C --------
C NT     : NUMERO DANS NOTRIA DU TRIANGLE A TRAITER PUIS
C          NUMERO DU GRAND TRIANGLE UNION DES 3 TRIANGLES DE SOMMET NS
C NS     : >0 EN ENTREE LE SOMMET NS DU TRIANGLE NT EST LE CENTRE
C             DE 3 TRIANGLES FORMANT UN GRAND TRIANGLE
C          =0 EN SORTIE SI LES 3 TRIANGLES N'ONT PAS ETE REMPLACES
C             PAR LE GRAND TRIANGLE, SINON GARDE SA VALEUR>0
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC et St PIERRE DU PERRAY Janvier 2016
C2345X7..............................................................012
      INTEGER  NOTRIA(6,*)

      IF( NT .EQ. 0 ) GOTO 8000

C     SOMMET NS DU TRIANGLE NT
      NST1 = NOTRIA( NS, NT )

C     LE TRIANGLE ADJACENT PAR L'ARETE NS
      NT12 = NOTRIA( 3+NS, NT )
      IF( NT12 .EQ. 0 ) GOTO 8000

C     NUMERO NS1 DE L'ARETE QUI SUIT L'ARETE NS DANS LE TRIANGLE NT
      IF( NS .EQ. 3 ) THEN
         NS1 = 1
      ELSE
         NS1 = NS+1
      ENDIF
C     NUMERO DU SOMMET QUI SUIT LE SOMMET NS
      NST2 = NOTRIA( NS1, NT )

C     NUMERO NS0 DE L'ARETE QUI PRECEDE L'ARETE NS DANS LE TRIANGLE NT
      IF( NS .EQ. 1 ) THEN
         NS0 = 3
      ELSE
         NS0 = NS-1
      ENDIF

C     LE TRIANGLE ADJACENT PAR L'ARETE NS0
      NT31 = NOTRIA( 3+NS0, NT )
      IF( NT31 .EQ. 0 ) GOTO 8000

C     NUMERO DU SOMMET QUI PRECEDE LE SOMMET NS
      NST3 = NOTRIA( NS0, NT )

C     NS4 SOMMET DE NT12 DIFFERENT DE NS1 ET NS2
      DO L=1,3
         NST4 = NOTRIA( L, NT12 )
         IF( NST4 .NE. NST1 .AND. NST4 .NE. NST2 ) GOTO 10
      ENDDO
      GOTO 8000

C     LE GRAND TRIANGLE RESTANT NT A POUR SOMMET NST2 NST3 NST4
C     ------------------------------------------------------
C     TRAITEMENT DU TRIANGLE ADJACENT A NT12
 10   NT12AD = 0
      DO L=1,3
         NSTT1 = NOTRIA( L, NT12 )
         IF( L .EQ. 3 ) THEN
            L1 = 1
         ELSE
            L1 = L+1
         ENDIF
         NSTT2 = NOTRIA( L1, NT12 )
         IF( NSTT1.EQ.NST2 .AND. NSTT2.EQ.NST4   .OR. 
     %       NSTT1.EQ.NST4 .AND. NSTT2.EQ.NST2 ) THEN
C           TRIANGLE ADJACENT PAR L'ARETE L NST2-NST4 DU TRIANGLE NT12
            NT12AD = NOTRIA( 3+L, NT12 )
            GOTO 15
         ENDIF
      ENDDO

C     QUEL EST LE NUMERO D'ARETE DE NST2-NST4 DANS NT12AD?
 15   IF( NT12AD .GT. 0 ) THEN
         DO L=4,6
            IF( NOTRIA( L, NT12AD ) .EQ. NT12 ) THEN
C              NT NOUVEAU NUMERO DE TRIANGLE ADJACENT
               NOTRIA( L, NT12AD ) = NT
               GOTO 20
            ENDIF
         ENDDO
      ENDIF


C     TRAITEMENT DU TRIANGLE ADJACENT A NT31
 20   NT31AD = 0
      DO L=1,3
         NSTT1 = NOTRIA( L, NT31 )
         IF( L .EQ. 3 ) THEN
            L1 = 1
         ELSE
            L1 = L+1
         ENDIF
         NSTT2 = NOTRIA( L1, NT31 )
         IF( NSTT1.EQ.NST3 .AND. NSTT2.EQ.NST4   .OR. 
     %       NSTT1.EQ.NST4 .AND. NSTT2.EQ.NST3 ) THEN
C           TRIANGLE ADJACENT PAR L'ARETE L DU TRIANGLE NT31
            NT31AD = NOTRIA( 3+L, NT31 )
            GOTO 25
         ENDIF
      ENDDO

C     QUEL EST LE NUMERO D'ARETE DE NST3-NST4 DANS NT31AD?
 25   IF( NT31AD .GT. 0 ) THEN
         DO L=4,6
            IF( NOTRIA( L, NT31AD ) .EQ. NT31 ) THEN
C              NT NOUVEAU NUMERO DE TRIANGLE ADJACENT
               NOTRIA( L, NT31AD ) = NT
               GOTO 30
            ENDIF
         ENDDO
      ENDIF


C     LE TRIANGLE NT : NST1-NST2-NST3 DEVIENT NT : NST4-NST2-NST3
 30   PRINT*,'mo3t1tpl: les 3 TRIANGLES',NT,NT12,NT31,
     %       ' de SOMMET COMMUN',NST1,' FORMENT UN GRAND TRIANGLE',NT
      PRINT*,'mo3t1tpl: TRIANGLE',NT  ,'St:',(NOTRIA(kl,NT  ),kl=1,3)
      PRINT*,'mo3t1tpl: TRIANGLE',NT12,'St:',(NOTRIA(kl,NT12),kl=1,3)
      PRINT*,'mo3t1tpl: TRIANGLE',NT31,'St:',(NOTRIA(kl,NT31),kl=1,3)

      NOTRIA( NS,    NT ) = NST4
      NOTRIA( 3+NS,  NT ) = NT12AD
      NOTRIA( 3+NS0, NT ) = NT31AD

C     DESTRUCTION DU TRIANGLE NT12
      NOTRIA( 1, NT12 ) = 0

C     DESTRUCTION DU TRIANGLE NT31
      NOTRIA( 1, NT31 ) = 0

C     LE GRAND TRIANGLE NT EST MAINTENANT L'UNION DE L'ANCIEN NT+NT12+NT31
      PRINT*,'mo3t1tpl: GRAND TRIANGLE',NT,'St:',(NOTRIA(kl,NT),kl=1,3)
      GOTO 9000


C     LE SOUS-TRIANGLE NT N'A PAS ETE MODIFIE
 8000 NS = 0

 9000 RETURN
      END
