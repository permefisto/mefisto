      SUBROUTINE ETOIVIDE( MXFETO, N1FEOC, N1FEVI, NFETOI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    REMETTRE A VIDE TOUT LE TABLEAU NFETOI DES FACES D'UNE ETOILE
C -----
C ENTREE :
C --------
C MXFETO : NOMBRE DE FACES DU TABLEAU NFETOI

C SORTIES:
C --------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : 1 A 4 NON INITIALISEES ICI
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE DU PERRAY Novembre 2014
C23456...............................................................012
      INTEGER  NFETOI(5,MXFETO)
C
C     REINITIALISATION A VIDE DE TOUTES LES FACES DE NFETOI
      N1FEVI = 1

      DO I=1,MXFETO
C        POUR INDIQUER LA VERSION 1 DE NFETOI
         NFETOI(3,I) = 0
C        NUMERO DE LA FACE DANS LEFACO
         NFETOI(4,I) = 0
C        NUMERO DANS NFETOI DE LA FACE SUIVANTE
         NFETOI(5,I) = I+1
      ENDDO

C     PAS DE FACE SUIVANTE
      NFETOI(5,MXFETO) = 0

C     AUCUNE FACE OCCUPEE DANS NFETOI
      N1FEOC = 0

      RETURN
      END
