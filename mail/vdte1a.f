      SUBROUTINE VDTE1A( NS1, NS2, N1TETS, NOTETR,
     %                   NBT, MXTEAR, MNTEAR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER LE NUMERO DE TOUS LES TETRAEDRES D'ARETE DE
C -----    SOMMETS NS1 NS2
C
C ENTREES:
C --------
C NS1,NS2: NUMERO DES SOMMETS COMMUNS A TOUTES LES FACES DES TETRAEDRES
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C
C MODIFIES:
C ---------
C MXTEAR : NOMBRE D'ENTIERS DU TABLEAU TEAR
C          =0 EN ENTREE DEMANDE UNE DECLARATION MNTEAR EST CALCULE
C          >0 EN ENTREE DEMANDE L'EMPLOI DU TABLEAU D'ADRESSE MNTEAR
C             ATTENTION CETTE ADRESSE PEUT ETRE CHANGEE PAR LA SUBROUTINE
C
C SORTIES:
C --------
C NBT    : NOMBRE DE TETRAEDRES DE SOMMETS NS1 NS2 DANS LE TABLEAU TEAR
C MNTEAR : ADRESSE MCN DU TABLEAU TEAR
C          TEAR(I) = NUMERO DU TETRAEDRE DE SOMMETS NS1 NS2
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   NOVEMBRE 1991
C2345X7..............................................................012
      include"./incl/pp.inc"
      COMMON    MCN(MOTMCN)
      INTEGER   NOTETR(8,1:*),N1TETS(1:*)
C
C     RECHERCHE DES FACES DES TETRAEDRES DE NOTETR CONTENANT LE SOMMET NS1
      CALL VDTE1S( NS1, N1TETS, NOTETR, NBF, MXTEAR, MNTEAR )
C
C     BOUCLE SUR LES TETRAEDRES DE SOMMET NS1 ET CONTENANT LE SOMMET NS2
      NBT = 0
      DO 50 N=1,NBF
C        LE TETRAEDRE
         MN = MNTEAR - 2 + N + N
         NT = MCN( MN )
C
C        LE TETRAEDRE NT A T IL L'ARETE NS1-NS2 ?
         DO 10 I=1,4
            IF( NOTETR(I,NT) .EQ. NS2 ) THEN
C              TETRAEDRE DE SOMMET NS1 NS2
               DO 5 K=0,NBT-1
C                 CE TETRAEDRE EST IL DEJA RECENSE?
                  IF( MCN(MNTEAR + K ) .EQ. NT ) GOTO 20
 5             CONTINUE
C              UN NOUVEAU TETRAEDRE
               MCN( MNTEAR + NBT ) = NT
               NBT = NBT + 1
            ENDIF
 10      CONTINUE
C
C        LE TETRAEDRE OPPOSE A NT PAR LA FACE A T IL L'ARETE NS1-NS2 ?
C        LE NUMERO LOCAL DE LA FACE
 20      I = MCN( MN + 1 )
C        LE TETRAEDRE OPPOSE A LA FACE NF
         NT = NOTETR( I+4, NT )
         IF( NT .LE. 0 ) GOTO 50
         DO 40 I=1,4
            IF( NOTETR(I,NT) .EQ. NS2 ) THEN
C              TETRAEDRE DE SOMMET NS1 NS2
               DO 35 K=0,NBT-1
C                 CE TETRAEDRE EST IL DEJA RECENSE?
                  IF( MCN(MNTEAR + K ) .EQ. NT ) GOTO 50
 35            CONTINUE
C              UN NOUVEAU TETRAEDRE
               MCN( MNTEAR + NBT ) = NT
               NBT = NBT + 1
            ENDIF
 40      CONTINUE
 50   CONTINUE

      RETURN
      END
