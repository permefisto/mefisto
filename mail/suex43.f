      SUBROUTINE SUEX43( NTLXSU, LADEFI,
     %                   NTFASU, MNFASU, NTSOFA, MNSOFA, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TENTATIVE DE RESTRUCTURATION D'UNE QUADRANGULATION
C -----    NON STRUCTUREE EN UN QUADRANGLE STRUCTURE
C
C ENTREES:
C --------
C NTLXSU : NUMERO DU TABLEAU TS DU LEXIQUE DE LA SURFACE
C LADEFI : TABLEAU ENTIER DE DEFINITION DE LA SURFACE
C          CF $MEFISTO/td/d/a_surface__definition
C
C SORTIES:
C --------
C NTFASU : NUMERO      DU TMS 'NSEF' DES NUMEROS DES FACES DE LA SURFACE
C MNFASU : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES FACES DE LA SURFACE
C          CF $MEFISTO/td/d/a___nsef
C NTSOFA : NUMERO      DU TMS 'XYZSOMMET' DE LA SURFACE
C MNSOFA : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE
C          CF $MEFISTO/td/d/a___xyzsommet
C IERR   : 0 SI PAS D'ERREUR ET FINALEMENT QUADRANGULATION STRUCTUREE
C          > 0 SINON (EXISTENCE DE TRIANGLES, ...)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : CHRISTOPHE DOURSAT ANALYSE NUMERIQUE UPMC PARIS JUILLET  1989
C MODIFS : ALAIN    PERRONNET ANALYSE NUMERIQUE UPMC PARIS DECEMBRE 1999
C2345+7...............................................................72
      IMPLICIT    INTEGER(W)
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
      include"./incl/a_surface__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (RMCN(1),MCN(1))
      INTEGER           LADEFI(0:*)
      CHARACTER*24      KNOM
C
      IERR = 0
C
C     LA SURFACE A RESTRUCTURER
C     =========================
C     LE NOM DE CETTE SURFACE
      NUSUIN = LADEFI(WUSUIN)
C
C     LE TABLEAU LEXIQUE DE CETTE SURFACE
      CALL LXNLOU( NTSURF, NUSUIN, NTLXSF, MNLXSF )
      IF( NTLXSF .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  'SURFACE INITIALE INCONNUE'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
      CALL NMOBNU( 'SURFACE', NUSUIN, KNOM )
C
C     LE TABLEAU 'NSEF' DE CETTE SURFACE
      CALL LXTSOU( NTLXSF, 'NSEF', NTNSEF, MNNSEF )
      IF( NTNSEF .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'SURFACE ' // KNOM
         KERR(2) = 'SURFACE SANS NSEF'
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C
C     LE TABLEAU 'XYZSOMMET' DE CETTE SURFACE
      CALL LXTSOU( NTLXSF, 'XYZSOMMET', NTXYZ0, MNXYZ0 )
      IF( NTXYZ0 .LE. 0 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'SURFACE ' // KNOM
         KERR(2) = 'SURFACE SANS SOMMETS'
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
C
C     LA SURFACE EST ELLE DEJA STRUCTUREE ?
      NUTYSU = MCN( MNNSEF + WUTYMA )
      IF( NUTYSU .EQ. 4 ) THEN
         NBLGRC(NRERR) = 2
         KERR(1) = 'SURFACE ' // KNOM
         KERR(2) = 'SURFACE DEJA STRUCTUREE'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     TEST SUR LE NOMBRE DE SOMMETS
      NBSOEF = MCN( MNNSEF + WBSOEF )
      IF( NBSOEF .NE. 4 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'NOMBRE DE SOMMETS PAR EF NON EGAL A 4'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C     LE NOMBRE DE SUPOOSES QUADRANGLES
      NBEFOB = MCN( MNNSEF + WBEFOB )
C     LE NOMBRE DE SOMMETS DE LA SUPPOSEE QUADRANGULATION
      NBSOMM = MCN( MNXYZ0 + WNBSOM )
      IF( NBSOMM .LE. 3 ) THEN
         NBLGRC(NRERR) = 1
         WRITE(KERR(MXLGER)(1:4),'(I4)')  NBSOMM
         KERR(1) = 'NOMBRE DE SOMMETS=' // KERR(MXLGER)(1:4)
     %          // ' AU LIEU D''ETRE > 3'
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
c     CREATION DES TABLEAUX AUXILIAIRES NECESSAIRES
      CALL TNMCDC( 'ENTIER', NBEFOB  , MNINDX )
      CALL TNMCDC( 'REEL'  , 3*NBSOMM, MNSOME )
C
C     LA TENTATIVE DE RESTRUCTURATION DE LA QUADRANGULATION
      CALL SUEY40( NBSOEF, NBEFOB, MCN(MNNSEF+WUSOEF),
     %             RMCN(MNXYZ0+WYZSOM),
     %             NBSOMM, MCN(MNINDX), RMCN(MNSOME),
     %             NBARX,  NBARY,       IERR )
C
      IF( IERR .EQ. 0 ) THEN
C
C        LA SURFACE EST MAINTENANT STRUCTUREE
C
C        CONSTRUCTION DU TABLEAU 'NSEF'
C        ------------------------------------
         CALL LXTNDC( NTLXSU, 'NSEF', 'ENTIER',
     %                WUSOEF + NBEFOB * 4 )
         CALL LXTSOU( NTLXSU, 'NSEF',  NTFASU , MNFASU )
C
C        LE TYPE DE L'OBJET : ICI SURFACE
         MCN( MNFASU + WUTYOB ) = 3
C        LE TYPE DU MAILLAGE : ICI STRUCTURE
         MCN( MNFASU + WUTYMA ) = 4
C        LE NOMBRE DE SOMMETS PAR FACE
         MCN( MNFASU + WBSOEF ) = 4
C        LE NOMBRE DE TANGENTES STOCKEES PAR EF: SURFACE C0 PAS DE TG
         MCN( MNFASU + WBTGEF ) = 0
         MCN( MNFASU + WBEFAP ) = 0
         MCN( MNFASU + WBEFTG ) = 0
C        LE NOMBRE D'EF DE LA SURFACE
         MCN( MNFASU + WBEFOB ) = NBARX * NBARY
C        LE NOMBRE DE SOMMETS PAR EF
         MCN( MNFASU + WBSOEF ) = 4
C        LE NOMBRE D'ARETES DANS LES DEUX DIRECTIONS
         MCN( MNFASU + WBARXQ ) = NBARX
         MCN( MNFASU + WBARYQ ) = NBARY
C        LE TYPE NON-FERME DE FERMETURE DU MAILLAGE
         MCN( MNFASU + WUTFMA ) = 0
C
C        AJOUT DE LA DATE
         CALL ECDATE( MCN(MNFASU) )
C
C        AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MNFASU + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
C
C        CONSTRUCTION DU TABLEAU 'XYZSOMMET'
C        -----------------------------------
         L = WYZSOM + 3 * NBSOMM
         CALL LXTNDC( NTLXSU, 'XYZSOMMET', 'MOTS', L )
         CALL LXTSOU( NTLXSU, 'XYZSOMMET',  NTSOFA , MNSOFA )
C        COPIE DU TABLEAU 'XYZSOMMET' STRUCTURE DANS CELUI NON STRUCTURE
         CALL TRTATA( MCN(MNSOME), MCN(MNSOFA), L )
C        LE NOMBRE DE SOMMETS DE L'UNION
         MCN( MNSOFA + WNBSOM ) = NBSOMM
         CALL TRTATA( MCN(MNSOME), MCN(MNSOFA+WYZSOM), 3*NBSOMM )
C        LA DATE ET LE NUMERO DE TD
         CALL ECDATE( MCN(MNSOFA) )
         MCN( MNSOFA + WNBTGS ) = 0
C        LE NOMBRE DE COORDONNEES PAR SOMMET
         MCN( MNSOFA + WBCOOR ) = 3
         MCN( MNSOFA + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
C
      ELSE
C
         IF ( IERR .EQ. 1 ) THEN
            NBLGRC(NRERR) = 2
            KERR(1) = 'SURFACE: ' // KNOM
            KERR(2) = 'DESOLE : SURFACE NON STRUCTURABLE'
            CALL LEREUR
         ELSE IF( IERR .EQ. 2 ) THEN
            NBLGRC(NRERR) = 2
            KERR(1) = 'SURFACE: ' // KNOM
            KERR(2) = 'NE CONTENANT PAS QUE DES QUADRANGLES'
            CALL LEREUR
         ELSE IF( IERR .EQ. 3 ) THEN
            NBLGRC(NRERR) = 2
            KERR(1) = 'SURFACE: ' // KNOM
            KERR(2) = 'NOMBRE DE SOMMETS ET QUADRANGLES NON COHERENTS'
            CALL LEREUR
         ENDIF
      ENDIF
C
C     DESTRUCTION DES TABLEAUX AUXILIAIRES
      CALL TNMCDS( 'ENTIER', NBEFOB,   MNINDX )
      CALL TNMCDS( 'REEL',   3*NBSOMM, MNSOME )
C
      RETURN
      END
