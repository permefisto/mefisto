      SUBROUTINE AJBATRCF( MXSOMM, NBSOMM, PTXYZD, NPSOFR,
     %                     HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                     MXFACO, LEFACO, NO0FAR, NBTRCF, NOTRCF,
     %                     MXSMIN, NOSMIN, NODSNO, DISTMIN, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   AJOUT A PTXYZD DU BARYCENTRE DES NBTRCF TRIANGLES NOTRCF DU CF
C -----   S'IL N'EST PAS TROP PROCHE D'UN DES TRIANGLES

C ENTREES:
C --------
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TETRAEDRISATION
C NBSOMM : NOMBRE DE SOMMETS INITIAUX
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NPSOFR : =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C              LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -4 SI LE POINT EST SOMMET D'OT NON TROP PROCHE PT OU FACE
C          = -1 SI LE POINT EST SOMMET D'OT TROP PROCHE PT OU FACE
C          = -3 SI LE POINT EST SOMMET D'OT REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE

C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I
C ECHPAV  : ECHELLE DANS LA DIRECTION I
C N1SPAVE : NO DU 1-ER SOMMET DANS PTXYZD DU PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES

C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C NO0FAR : NUMERO DES 3 SOMMETS DES FACES AJOUTEES AU CF

C NBTRCF : NOMBRE DE FACES PERDUES DU CF
C NOTRCF : TABLEAU DU NUMERO DANS LEFACO ou NO0FAR DES FACES DU CF

C MXSMIN : NOMBRE D'ENTIERS DES TABLEAUX AUXILIAIRES NOSMIN, NODSNO
C NOSMIN : TABLEAU 1:MXSMIN D'ENTIERS
C NODSNO : TABLEAU 1:MXSMIN D'ENTIERS
C DISTMIN: TABLEAU 1:MXSMIN DOUBLE PRECISION

C SORTIES:
C --------
C NBSOMM : NOMBRE DE SOMMETS FINAUX
C IERR   : 0 SI PAS D'ERREUR
C          1 SATURATION DES SOMMETS
C          2 PROBLEME LORS DU CALCUL DE LA FONCTION TAILLE_IDEALE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET St Pierre du Perray              Fevrier 2020
C....................................................................012
      INTEGER           NPSOFR(MXSOMM),
     %                  LEFACO(11,0:MXFACO), NO0FAR(3,*),
     %                  NOTRCF(NBTRCF)
      INTEGER           NBIPAV(3), N1SPAVE(0:*), NOPTSUIV(1:*),
     %                  NOSMIN(MXSMIN), NODSNO(MXSMIN), NOSOTR(3)
      DOUBLE PRECISION  PTXYZD(1:4,1:MXSOMM)
      DOUBLE PRECISION  HEXAPAVE(3,2), ECHPAV(3), DISTMIN(MXSMIN)
      DOUBLE PRECISION  XYZDBACF(4), D, CBTR(3), PTPROJ(3), ARLG
      INTRINSIC         SQRT

      NBSOM0 = NBSOMM

C     EXISTENCE OU NON DE LA FONCTION TAILLE_IDEALE(X,Y,Z)
      NOFOTI = NOFOTIEL()

C     CALCUL DES XYZD DU BARYCENTRE DES NBTRCF TRIANGLES DU CF
C     --------------------------------------------------------
      DO I=1,4
         XYZDBACF(I) = 0D0
      ENDDO

C     NOMBRE DE SOMMETS DU CALCUL DU BARYCENTRE DES TRIANGLES DU CF
      NBS = 0
      DO N = 1, NBTRCF
         NTR = NOTRCF( N )
         DO K = 1, 3
C           NO DU SOMMET K DU TRIANGLE NTR
            NBS = NBS + 1
            IF( NTR .GT. 0 ) THEN
C              FACE LEFACO
               NS = LEFACO( K, NTR )
            ELSE
C              FACE NO0FAR
               NS = NO0FAR( K, -NTR )
            ENDIF
            DO I=1,4
               XYZDBACF( I ) = XYZDBACF( I ) + PTXYZD( I, NS )
            ENDDO
         ENDDO
      ENDDO
      DO I=1,4
         XYZDBACF( I ) = XYZDBACF( I ) / NBS
      ENDDO

      IF( NOFOTI .GT. 0 ) THEN
C        CALCUL DE LA DISTANCE SOUHAITEE AU BARYCENTRE
C        PAR APPEL DE LA FONCTION 'TAILLE_IDEALE'
         CALL FONVAL( NOFOTI, 3, XYZDBACF, NCODEV, XYZDBACF(4) )
         IF( NCODEV .LE. 0 ) THEN
            IERR = 2
            GOTO 9900
         ENDIF
      ENDIF

C     EXISTE-T-IL UN SOMMET PTXYZD TRES PROCHE DU BARYCENTRE?
C     RETROUVER LES PLUS PROCHES SOMMETS TETRAEDRISES DU POINT XYZDBACF
C     A UNE DISTANCE D'UN PAVE RANGES DANS LES PAVES DE L'HEXAEDRE
C     -----------------------------------------------------------------
      CALL DMINSTPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                XYZDBACF, PTXYZD,
     %                MXSMIN, NBSMIN, NOSMIN, NODSNO, DISTMIN )

C     DISTANCE DU SOMMET LE PLUS PROCHE DU BARYCENTRE
      NSDMIN = NOSMIN( NODSNO(1) )
      D      = SQRT( DISTMIN( 1 ) )

      IF( D .LT. PTXYZD(4,NSDMIN) * 1D-2 ) THEN
C        LE BARYCENTRE EST TROP PROCHE DU POINT NSDMIN. IL EST ABANDONNE
         PRINT*,'ajbatrcf: ABANDON du BARYCENTRE',XYZDBACG,
     %             ' du TETRAEDRE',NTR
         PRINT*,'ajbatrcf: TROP PROCHE de',(PTXYZD(K,NSDMIN),K=1,4)
         GOTO 9900
      ENDIF

C     LE BARYCENTRE EST IL INTERNE A UN TRIANGLE NOTRCF?
C     --------------------------------------------------
      DO 20 L=1,NBTRCF

         NTR = NOTRCF( L )
         IF( NTR .GT. 0 ) THEN
            NOSOTR(1) = LEFACO(1,NTR)
            NOSOTR(2) = LEFACO(2,NTR)
            NOSOTR(3) = LEFACO(3,NTR)
         ELSE
            NOSOTR(1) = NO0FAR(1,-NTR)
            NOSOTR(2) = NO0FAR(2,-NTR)
            NOSOTR(3) = NO0FAR(3,-NTR)
         ENDIF

C        POINT DE PROJECTION DU BARYCENTRE SUR LE PLAN DE LA FACE PERDUE NTR
         CALL PRPTPLD( XYZDBACF,
     %                 PTXYZD(1,NOSOTR(1)),
     %                 PTXYZD(1,NOSOTR(2)),
     %                 PTXYZD(1,NOSOTR(3)),
     %                 PTPROJ, IER )
C        PTPROJ(3) : XYZ DU POINT PROJETE SUR LE PLAN NOSOTR(1:3)
         IF( IER .NE. 0 ) GOTO 20

C        3 COORDONNEES BARYCENTRIQUES DU POINT PT DANS LE TRIANGLE NTR
         CALL CBPTTR( PTXYZD(1,NOSOTR(1)),
     %                PTXYZD(1,NOSOTR(2)),
     %                PTXYZD(1,NOSOTR(3)),
     %                PTPROJ,  CBTR )

C        PTPROJ EST IL INTERNE OU PROCHE DU TRIANGLE?
         IF( ABS(CBTR(1))+ABS(CBTR(2))+ABS(CBTR(3)) .LE. 1.1D0 ) THEN

C           OUI: CALCUL DE LA DISTANCE BARYCENTRE-PTPROJ
            D = SQRT( ( XYZDBACF(1) - PTPROJ(1) ) **2
     %              + ( XYZDBACF(2) - PTPROJ(2) ) **2
     %              + ( XYZDBACF(3) - PTPROJ(3) ) **2 )

C           PERIMETRE DES 3 ARETES DU TRIANGLE NTR
            ARLG = 0D0
            NS1  = NOSOTR(3)
            DO M=1,3
               NS2 = NOSOTR(M)
               ARLG = ARLG + SQRT(
     %                           ( PTXYZD(1,NS2) - PTXYZD(1,NS1) ) ** 2
     %                         + ( PTXYZD(2,NS2) - PTXYZD(2,NS1) ) ** 2
     %                         + ( PTXYZD(3,NS2) - PTXYZD(3,NS1) ) ** 2)
               NS1 = NS2
            ENDDO

            IF( D .LT. ARLG * 0.01D0 ) THEN

C              LE BARYCENTRE EST TROP PROCHE DE LA FACE NTR
C              IL EST ABANDONNE
               GOTO 9900

            ENDIF
         ENDIF

 20   ENDDO

C     CREATION DU POINT NBSOMM BARYCENTRE DU CF
C     -----------------------------------------
      IF( NBSOMM .GE. MXSOMM ) THEN
         PRINT*,'ajbatrcf: TABLEAU PTXYZD SATURE MXSOMM=',MXSOMM
         IERR = 1
         GOTO 9900
      ENDIF
      NBSOMM = NBSOMM + 1
C     POINT INTERNE AU VOLUME NON SUR LA FRONTIERE
      NPSOFR( NBSOMM ) = 0
      DO I=1,4
         PTXYZD( I, NBSOMM ) = XYZDBACF( I )
      ENDDO
      IERR = 0
      PRINT*,'ajbatrcf: Ajout du BARYCENTRE',NBSOMM,
     %       ' des',NBTRCF,' FACES du CF'
      GOTO 9999

C     BARYCENTRE ABANDONNE
 9900 PRINT*,'ajbatrcf: BARYCENTRE du CF TROP PROCHE. ABANDONNE IERR=',
     %        IERR

 9999 RETURN
      END
