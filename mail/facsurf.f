      SUBROUTINE FACSURF( NBSOFA, NOSOFA, MNNSEF, NOFASF )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER UNE FACE DE SOMMETS NOSOEL DANS LES FACES D'UNE
C -----    SURFACE DEFINIE PAR XYZSOMMET ET NSEF
C
C ENTREES:
C --------
C NBSOFA : NOMBRE DE SOMMETS DE LA FACE
C MNNSEF : L'ADRESSE MCN DU TABLEAU NSEF DE LA SURFACE
C
C MODIFIE:
C---------
C NOSOFA : NUMERO DES NBSOFA SOMMETS DANS LA NUMEROTATION DES SOMMETS
C          DE LA SURFACE ( NUMEROS RENUMEROTES CROISSANTS ICI )
C
C SORTIE :
C --------
C NOFASF : NUMERO DE LA FACE DE LA SURFACE SI ELLE EST IDENTIFIEE
C          0 SI NON IDENTIFIEE
C         -1 SI PROBLEME DANS LE TABLEAU NSEF DE LA SURFACE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET TEXAS A & M UNIVERSITY at QATAR      MARS 2011
C2345X7..............................................................012
      include"./incl/pp.inc"
      COMMON       MCN(MOTMCN)
      INTEGER      NOSOFA(4), NOSOSF(4)
C
C     RENUMEROTATION CROISSANTE DES NUMEROS DE SOMMETS DE LA FACE
      CALL TRIENT( NBSOFA, NOSOFA )
C
C     LES PARAMETRES DES NO SOMMET DU MAILLAGE DE SURFACE1
      CALL NSEFPA( MCN(MNNSEF),
     %             NUTYMA, NBSOEL, NBSOEF, NBTGEF,
     %             LDAPEF, LDNGEF, LDTGEF,
     %             NBEFOB, NX, NY, NZ,
     %             IERR   )
      IF( IERR .NE. 0 ) THEN
         NOFASF = -1
         GOTO 9999
      ENDIF
C
C     LA BOUCLE SUR LES EF DU MAILLAGE DE LA SURFACE
      DO 100 NUELEM = 1, NBEFOB
C
C        LE NUMERO DES NBSOEF SOMMETS DE L'EF N
         CALL NSEFNS( NUELEM, NUTYMA, NBSOEF, NBTGEF,
     %                LDAPEF, LDNGEF, LDTGEF,
     %                MNNSEF, NX, NY, NZ,
     %                NCOGEL, NUGEEF, NUEFTG, NOSOSF, IERR )
C        LE NOMBRE DE SOMMETS DE CET ELEMENT FINI = NCOGEL
         IF( NCOGEL .NE. NBSOFA ) GOTO 100
C
C        RENUMEROTATION CROISSANTE DES NUMEROS DE SOMMETS DE LA FACE
         CALL TRIENT( NBSOFA, NOSOSF )
C
C        TEST D'IDENTICITE SOMMET PAR SOMMET
         DO K=1,NBSOFA
            IF( NOSOFA(K) .NE. NOSOSF(K) ) GOTO 100
         ENDDO
C
C        FACE RETROUVEE
         NOFASF = NUELEM
         GOTO 9999
C
 100  CONTINUE
C
C     FACE NON RETROUVEE
      NOFASF = 0
C
 9999 RETURN
      END
