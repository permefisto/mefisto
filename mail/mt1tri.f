      SUBROUTINE MT1TRI( MXSOMM, PXYD,   NBARFR,
     %                   MOSOAR, MXSOAR, N1SOAR, NOSOAR,
     %                   MOARTR, MXARTR, N1ARTR, NOARTR,
     %                   NOARST, NBARET, NBTRIA, AIRE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    FORMER LES TRIANGLES SOMMET 1 - ARETES DE LA FRONTIERE
C -----
C          ATTENTION : CHEVAUCHEMENTS ASSURES DES TRIANGLES !
C
C ENTREES:
C --------
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS PXYD
C PXYD   : TABLEAU DES X  Y  DISTANCE_SOUHAITEE DE CHAQUE SOMMET
C NBARFR : NOMBRE TOTAL D'ARETES DES LIGNES FERMEES DE LA FRONTIERE
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE
C MXARTR : NOMBRE MAXIMAL D'ENTIERS PAR TRIANGLE
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES, CHAINAGES
C          SOMMET 1 = 0 SI ARETE VIDE => SOMMET 2 = ARETE VIDE SUIVANTE
C MXARTR : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES DANS NOARTR
C
C SORTIES:
C --------
C N1SOAR : NUMERO DE LA PREMIERE ARETE VIDE DANS LE TABLEAU NOSOAR
C N1ARTR : NUMERO DU PREMIER TRIANGLE VIDE DANS LE TABLEAU NOARTR
C          LE CHAINAGE DES TRIANGLES VIDES SE FAIT SUR NOARTR(2,.)
C NOARTR : LES 3 ARETES DES TRIANGLES +-ARETE1, +-ARETE2, +-ARETE3
C          ARETE1 = 0 SI TRIANGLE VIDE => ARETE2 = TRIANGLE VIDE SUIVANT
C NOARST : NOARST(I) NUMERO D'UNE ARETE DE SOMMET I
C NBARET : NUMERO DE LA DERNIERE ARETE ACTUELLE DANS NOSOAR
C NBTRIA : NOMBRE DE TRIANGLES ACTUELS
C AIRE   : SOMME DES VALEURS ABSOLUES DES AIRES DES TRIANGLES ACTUELS
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JUILLET 1995
C....................................................................012
      include"./incl/trvari.inc"
      DOUBLE PRECISION  PXYD(3,*), AIRE, SURTD2
      INTEGER           NOSOAR(MOSOAR,MXSOAR),
     %                  NOARTR(MOARTR,MXARTR),
     %                  NOARST(MXSOMM)
C
C     MISE A ZERO DU TABLEAU NOARST
      DO 10 I = 1 , MXSOMM
         NOARST(I) = 0
 10   CONTINUE
C
C     INITIALISATION DES NBTRIA TRIANGLES A PATIR DU SOMMET 1 ET DES
C     NBARFR ARETES DES LIGNES FERMEES DE LA FRONTIERE
      AIRE   = 0D0
      NA1    = 1
      NBARET = NBARFR
      NBTRIA = NBARFR - 2
C
      DO 20 NA2 = 2, NBTRIA+1
C
C        LES 2 SOMMETS NS1 NS2 DE L'ARETE DU BORD FRONTIERE DU DOMAINE
         NS2 = NOSOAR(1,NA2)
         NS3 = NOSOAR(2,NA2)
C
C        CONSTRUCTION DE L'ARETE NS3-NS1
         NBARET = NBARET + 1
C        LE SOMMET INITIAL
         NOSOAR(1,NBARET) = NS3
C        LE SOMMET FINAL
         NOSOAR(2,NBARET) = 1
C        LE NUMERO DE LA LIGNE DE L'ARETE
         NOSOAR(3,NBARET) = 0
         NT = NA2 - 1
C        LE TRIANGLE AVANT L'ARETE
         NOSOAR(4,NBARET) = NT
C        LE TRIANGLE APRES L'ARETE
         NOSOAR(5,NBARET) = NA2
C        LE CHAINAGE N'EXISTE PAS
         NOSOAR(6,NBARET) = -1
C
C        MISE A JOUR DE L'ARETE FRONTALIERE
         NOSOAR(4,NA2) = NT
         NOSOAR(5,NA2) = 0
C
C        LE TRIANGLE NT
         NOARTR(1,NT) = NA1
         NOARTR(2,NT) = NA2
         NOARTR(3,NT) = NBARET
C
C        TRACE DU TRIANGLE NT
         CALL MTTRTR( PXYD, NT, MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                NCROUG, NCBLAN )
C
C        AIRE ABSOLUE DU TRIANGLE NT
         AIRE = AIRE + ABS( SURTD2(PXYD(1,1),PXYD(1,NS2),PXYD(1,NS3)) )
C
C        LE NUMERO D'UNE ARETE DE SOMMET NS3
         NOARST(NS3) = NBARET
C
C        LA DERNIERE ARETE FORMEE A LIRE DANS L'AUTRE SENS
         NA1 = -NBARET
 20   CONTINUE
C
C     UNE ARETE INTERNE DE TROP A ETE CREEE = ARETE NBARFR
      NBARET = NBARET - 1
C
C     RECTIFICATION DU DERNIER TRIANGLE
      NOARTR(3,NBTRIA) = NBARFR
      NOSOAR(4,NBARFR) = NBTRIA
      NOSOAR(5,NBARFR) = 0
C
C     RECTIFICATION DE LA PREMIERE ARETE
C     LE TRIANGLE AVANT L'ARETE
      NOSOAR(4,1) = 1
C     LE TRIANGLE APRES L'ARETE
      NOSOAR(5,1) = 0
C
C     RECTIFICATION DE LA SECONDE ARETE
C     LE TRIANGLE AVANT L'ARETE
      NOSOAR(4,2) = 1
C     LE TRIANGLE APRES L'ARETE
      NOSOAR(5,2) = 0
C
C     LE NUMERO D'UN TRIANGLE DE SOMMET 1
      NOARST(1) = 1
C
C     LE NUMERO D'UN TRIANGLE DE SOMMET 2
      NOARST(2) = 1
C
C     DEBUT DES CHAINAGES DES TRIANGLES VIDES
C     =======================================
      N1ARTR = NBTRIA + 1
C
C     CHAINAGE DES TRIANGLES VIDES
      DO 30 I = N1ARTR , MXARTR
C        LE NUMERO DU SOMMET 1 = 0 => TRIANGLE VIDE
         NOARTR(1,I) = 0
C        LE TRIANGLE VIDE SUIVANT
         NOARTR(2,I) = I + 1
 30   CONTINUE
C
C     LA FIN DU CHAINAGE DES TRIANGLES VIDES
      NOARTR(2,MXARTR) = 0
C
C     DEBUT DES CHAINAGES DES ARETES VIDES
C     ====================================
      N1SOAR = NBARET + 1
C
C     CHAINAGE DES ARETES VIDES
      DO 40 I = N1SOAR , MXSOAR
C        LE NUMERO DU SOMMET 1 = 0 => TRIANGLE VIDE
         NOSOAR(1,I) = 0
C        L'ARETE VIDE SUIVANTE
         NOSOAR(2,I) = I + 1
 40   CONTINUE
C
C     LA FIN DU CHAINAGE DES ARETES VIDES
      NOSOAR(2,MXSOAR) = 0
      END
