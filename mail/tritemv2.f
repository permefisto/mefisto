      SUBROUTINE TRITEMV2( MNXYZS, MNNSEF,
     %                     L1ARET,  L2ARET,   LARETE, NBITEMV,
ccc     %                     L1ITEMS, NBITEMS,  NUITEMS,
     %                     L1ITEMA, NBITEMA,  NUITEMA,
     %                     L1ITEMF, NBITEMF,  NUITEMF,
     %                     MXITDI,  DIITOEIL, MXITAN, MNITAN )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  TRACER EN 3D LES ITEMS VISIBLES SELON L'ALGORITHME DU PEINTRE
C -----  ATTENTION: LES TRACES SONT FAITS DANS LA MEMOIRE PIXELS
C        POUR LES FAIRE APPARAITRE SUR L'ECRAN: CALL TRFINS(TITRE)

C ENTREES:
C --------
C KNMSURF: NOM DE LA SURFACE FINALE    DANS LE LEXIQUE DES SURFACES
C NUSURF : NUMERO DE LA SURFACE FINALE DANS LE LEXIQUE DES SURFACES
C MNXYZS : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE FINALE
C           CF $MEFISTO/td/d/a___xyzsommet
C MNNSEF : ADRESSE MCN DU TMS 'NSEF' DE LA SURFACE FINALE
C           CF $MEFISTO/td/d/a___nsef

C L1ARET : NOMBRE DE MOTS PAR ARETE DU TABLEAU LARETE
C L2ARET : NOMBRE MAXIMAL DE FACES QTANGLES DU TABLEAU LARETE
C LARETE : LARETE(1,I)= NO DU 1-ER  SOMMET DE L'ARETE
C          LARETE(2,I)= NO DU 2-EME SOMMET > 1-ER  SOMMET
C          LARETE(3,I)= CHAINAGE HACHAGE SUR L'ARETE SUIVANTE
C          LARETE(4:L1ARET,I)= NO NOSTQT DE LA FACE CONTENANT L'ARETE
C          SI UNE ARETE APPARTIENT A PLUS DE L1ARET-3 FACES,
C          LE DERNIER NUMERO DE FACE EST RENDU NEGATIF POUR INDIQUER
C          QUE LA LISTE DES FACES DE L'ARETE EST INCOMPLETE

C NBITEMV: (1)=DERNIER NO XYZSOM DES SOMMETS VISIBLES
C          (2)=DERNIER NO LARETE DES ARETES APPARTENANT A UNE SEULE FACE
C          (3)=DERNIER NO LARETE DES ARETES APPARTENANT A 2FACES
C          (4)=DERNIER NO LARETE DES ARETES APPARTENANT A 3FACES
C          (5)=DERNIER NO LARETE DES ARETES DE RATIO DES FACES TROP FAIBLE
C          (6)=DERNIER NO LARETE DES ARETES DES COUPLES DE FACES COLLEES
C          (7)=DERNIER NO NOSOEF DES FACES VISIBLES

C N1TEMS : NOMBRE DE MOTS PAR ITEM SOMMET VISIBLE (cf incl/mecoit.inc)
C NBITEMS: NOMBRE D'ITEMS SOMMET VISIBLE
C NUITEMS: NUITEMS(1,I)= X DU SOMMET VISIBLE I
C          NUITEMS(2,I)= Y DU SOMMET VISIBLE I
C          NUITEMS(3,I)= Z DU SOMMET VISIBLE I
C          NUITEMS(4,I)= NUMERO DU SOMMET DANS XYZSOM DU SOMMET VISIBLE I

C N1TEMA : NOMBRE DE MOTS PAR ITEM ARETE VISIBLE (cf incl/mecoit.inc)
C NBITEMA: NOMBRE D'ITEMS ARETE VISIBLE
C NUITEMA: NUITEMA(1,I)= X DU BARYCENTRE DE L'ARETE VISIBLE I
C          NUITEMA(2,I)= Y DU BARYCENTRE DE L'ARETE VISIBLE I
C          NUITEMA(3,I)= Z DU BARYCENTRE DE L'ARETE VISIBLE I
C          NUITEMA(4,I)= NUMERO DE L'ARETE DANS LE TABLEAU LARETE

C N1TEMF : NOMBRE DE MOTS PAR ITEM FACE VISIBLE (cf incl/mecoit.inc)
C NBITEMF: NOMBRE D'ITEMS FACE VISIBLE
C NUITEMF: NUITEMF(1,I)= X DU BARYCENTRE DE LA FACE VISIBLE I
C          NUITEMF(2,I)= Y DU BARYCENTRE DE LA FACE VISIBLE I
C          NUITEMF(3,I)= Z DU BARYCENTRE DE LA FACE VISIBLE I
C          NUITEMF(4,I)= NUMERO DE LA FACE DANS LE TABLEAU NUSOEF

C MXITDI : NOMBRE MAXIMAL DECLARE d'ITEMS VISIBLES DE TABLEAU DIITOEIL
C MXITAN : NOMBRE MAXIMAL DECLARE d'ITEMS VISIBLES DU TABLEAU MCN(MNITAN)

C MODIFIES:
C ---------
C DIITOEIL:TABLEAU DES DISTANCES A L'OEIL DU BARYCENTRE DES ITEMS VISIBLES
C          AVANT et APRES le TRI CROISSANT des DISTANCES
C MNITAN : TABLEAU DU NUMERO ANCIEN DES ITEMS APRES LEUR TRI CROISSANT
C          SELON LA DISTANCE A L'OEIL AVANT et APRES le TRI
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: PERRONNET ALAIN Saint PIERRE du PERRAY              Avril 2020
C2345X...............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/xyzext.inc"
      include"./incl/trvari.inc"
      include"./incl/mecoit.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"

C     DECLARATION DU SUPER-TABLEAU NUMERIQUE MCN
      include"./incl/pp.inc"
      COMMON             MCN(MOTMCN)
      REAL              RMCN(MOTMCN)
      EQUIVALENCE      (RMCN(1),MCN(1))

C     LES PARAMETRES
ccc      CHARACTER*(*)     KNMSURF
      INTEGER           LARETE(L1ARET,L2ARET),
ccc     %                  NUITEMS(L1ITEMS,0:NBITEMS),
     %                  NUITEMA(L1ITEMA,0:NBITEMA),
     %                  NUITEMF(L1ITEMF,0:NBITEMF)
      REAL              DIITOEIL(MXITDI)

      CHARACTER*8       KNMSOM
      INTEGER           NBITEMV(0:7), NOSOEL(12)
      REAL              XYZP(3), COUL(5), XYZ(3,5), XYZNORFA(3),
     %                  X(12), Y(12), Z(12)

C     SAUVEGARDE DES COULEURS DES FACES et ARETES DES FACES
      NCOUAF0 = NCOUAF
      NCOUFA0 = NCOUFA

C     LE NOMBRE DE SOMMETS DES FACES DU MAILLAGE DE LA SURFACE
      NBSOM = MCN( MNXYZS + WNBSOM )

C     L'ADRESSE RMCN DU TABLEAU XYZSOM DES XYZ DES NBSOM SOMMETS
      MNXYZST = MNXYZS + WYZSOM

C     LES PARAMETRES DES NO SOMMET DES EF DU MAILLAGE
C     -----------------------------------------------
      CALL NSEFPA( MCN(MNNSEF),
     %             NUTYMA, NBSOEL, NBSOEF, NBTGEF,
     %             LDAPEF, LDNGEF, LDTGEF, NBEFOB,
     %             NX,     NY,     NZ,
     %             IERR   )
      IF( IERR .NE. 0 ) GOTO 9999
C     NBEFOB EST LE NOMBRE DE FACES DU MAILLAGE DE LA SURFACE
C     ADRESSE MCN DU DEBUT DES NO DES SOMMETS DES EF
      MNSOEF = MNNSEF + WUSOEF

C     NOMBRE TOTAL D'ITEMS VISIBLES A TRACER
      NBITEM = NBITEMV( 7 )
      IF( NBITEM .LE. 0 ) GOTO 9900


C     LE TRI CROISSANT DES NBITEM DISTANCES (COTE -Z AXONOMETRIQUE) A L'OEIL
C     DES ITEMS SOMMETS ARETES 1F, 2F, 3F, 2SF, 2FCOL et FACES
C     ======================================================================
C     INITIALISATION DU NUMERO d'ITEM AVANT le TRI des DISTANCES a l'OEIL
      IF( MXITAN .LT. NBITEM ) THEN
         CALL TNMCAU( 'ENTIER', MXITAN, NBITEM, 0, MNITAN )
         MXITAN = NBITEM
      ENDIF
      MNITAN1 = MNITAN - 1
      DO K = 1, NBITEM
         MCN( MNITAN1 + K ) = K
      ENDDO

C     LE TRI CROISSANT DES DISTANCES A L'OEIL DES ITEMS VISIBLES
      CALL TRITRP( NBITEM, DIITOEIL, MCN(MNITAN) )
C     DIITOEIL(N): DISTANCE CROISSANTE A L'OEIL DE L'ITEM N
C     NUITAN(N)  : NUMERO INITIAL DE L'ITEM DANS LE TABLEAU DES ITEMS VISIBLES

C     DISTANCES MIN ET MAX des ITEMS VISIBLES a l'OEIL
      DISTMIN = DIITOEIL( 1 )
      DISTMAX = DIITOEIL( NBITEM )
ccc      PRINT*,'tritemv2: Distance a l''OEIL Min=',DISTMIN,' MAX=',DISTMAX

C     CALCUL DES COTES Z EXTREMES DES NBSOM SOMMETS DU MAILLAGE
C     ---------------------------------------------------------
      ZMIN = 0
      ZMAX = 1

      IF( LCRITR .EQ. -1 ) THEN

C        TRACE ARC EN CIEL DU MAILLAGE SELON LA COORDONNEE Z DES SOMMETS
C        CALCUL DES Z MIN ET MAX DES SOMMETS
         ZMIN = 1E28
         ZMAX =-1E28
         MN   = MNXYZST - 4
         DO NS = 1, NBSOM
            MN  = MN + 3
            ZNS = RMCN(MN+3)
            IF( ZNS .LT. ZMIN ) ZMIN = ZNS
            IF( ZNS .GT. ZMAX ) ZMAX = ZNS
         ENDDO

      ELSE IF( LCRITR .EQ. -2 ) THEN

C        TRACE ARC EN CIEL DU MAILLAGE SELON LA COORDONNEE Z DES SOMMETS
C        AVEC ZMIN ET ZMAX GLOBAUX POUR PLUSIEURS TRACES
         ZMIN = ZZZMIN
         ZMAX = ZZZMAX

      ENDIF

C     CALCUL DES COULEURS DES FACES AVEC PRISE EN COMPTE
C     DE LA DIRECTION DE VISEE ET L'ELOIGNEMENT
C     POID : POIDS DE LA DIRECTION DE VISEE DANS CE CALCUL
C            ATTENTION: 0 < POID < 1
C     ----------------------------------------------------
      EP1    = 0.05
      EP2    = SQRT(EP1)
      EP3    = SQRT(1+EP1) - EP2
      DIMIMX = DISTMAX - DISTMIN
      IF ( DIMIMX .EQ. 0 ) THEN
         DIMIMX = 1.0
         POID  = 1.0
      ELSE
         POID  = 0.75
      ENDIF
      POID1 = 1. - POID

C     LE NOMBRE DE COULEURS DISPONIBLES
      NBRCOU = NDCOUL - N1COUL

C     PAS DE TRACE DU NUMERO DES FACES VISIBLES
      NCONEF = -1

C     PAS DE TRACE DU NUMERO DES SOMMETS DES FACES VISIBLES
      NCONSO = -1

C     NO DE LA FACE POIGNEE DE LA SURFACE
      NOFASU = 0

C     PAS DE TRACE DU NO DE LA FACE
      NCNF = -1

C     PAS DE TRACE DU NO DES SOMMETS
      NCNS = -1

C     ========================================================================
C     LE TRACE DES ITEMS VISIBLES SELON L'ELOIGNEMENT ET LA DIRECTION DE VISEE
C     ========================================================================
C NBITEMV: (1)=DERNIER NO XYZSOM DES SOMMETS VISIBLES
C          (2)=DERNIER NO LARETE DES ARETES APPARTENANT A UNE SEULE FACE
C          (3)=DERNIER NO LARETE DES ARETES APPARTENANT A 2FACES
C          (4)=DERNIER NO LARETE DES ARETES APPARTENANT A 3FACES
C          (5)=DERNIER NO LARETE DES ARETES DE RATIO DES FACES TROP FAIBLE
C          (6)=DERNIER NO LARETE DES ARETES DES COUPLES DE FACES COLLEES
C          (7)=DERNIER NO NOSOEF DES FACES VISIBLES

      DO 200 NITE = NBITEM, 1, -1

C        TRACE DES ITEMS VISIBLES LES PLUS ELOIGNES D'ABORD
C        LE NUMERO D'ITEM DANS LE CLASSEMENT INITIAL AVANT LE TRI
C        --------------------------------------------------------
         NOITVI = MCN( MNITAN -1 + NITE )

C        SA DISTANCE A L'OEIL
         DIST = DIITOEIL( NITE )

C        CET ITEM VISIBLE EST IL UN SOMMET, UNE ARETE, UNE FACE?
         IF( NOITVI .GT. NBITEMV(6) ) THEN
C           NOITVI EST UNE FACE VISIBLE
            GOTO 160
         ENDIF

         IF( NOITVI .GT. NBITEMV(5) ) THEN
C           NOITVI EST UNE ARETE VISIBLE DES COUPLES DE FACES COLLEES
            GOTO 150
         ENDIF

         IF( NOITVI .GT. NBITEMV(4) ) THEN
C           NOITVI EST UNE ARETE VISIBLE DE RATIO DES FACES TROP FAIBLE
            GOTO 140
         ENDIF

         IF( NOITVI .GT. NBITEMV(3) ) THEN
C           NOITVI EST UNE ARETE VISIBLE APPARTENANT A 3FACES
            GOTO 130
         ENDIF

         IF( NOITVI .GT. NBITEMV(2) ) THEN
C           NOITVI EST UNE ARETE VISIBLE APPARTENANT A 2FACES
ccc            GOTO 120
            GOTO 200
         ENDIF

         IF( NOITVI .GT. NBITEMV(1) ) THEN
C           NOITVI EST UNE ARETE VISIBLE APPARTENANT A 1 FACE
            GOTO 110
         ENDIF

         IF( NOITVI .GT. NBITEMV(0) ) THEN
C           NOITVI EST UN SOMMET VISIBLE MAIS NON TRACE
ccc            GOTO 100
            GOTO 200
         ENDIF

         GOTO 200


cccC        TRACE D'UN SOMMET VISIBLE
cccC        LE NUMERO DU SOMMET DANS LE TABLEAU XYZSOM
cccC        ------------------------------------------
ccc 100      NITS = NOITVI - NBITEMV(0)
ccc         IF( NITS .LE. 0 ) GOTO 200
cccC        NUMERO DU SOMMET DANS LE MAILLAGE XYZSOM
ccc         NOST = NUITEMS( 4, NITS )
cccC        en 3D TRACE des CARACTERES '+NOST' DU SOMMET NOST de XYZSOM
ccc         CALL TRST3D( NCNOIR, NOST, RMCN(MNXYZST) )
ccc         GOTO 200


C        TRACE D'UN ITEM ARETE VISIBLE APPARTENANT A 1 FACE AVEC CETTE FACE
C        LE NUMERO LARETE DE L'ARETE DANS LE TABLEAU NAR1F
C        ------------------------------------------------------------------
 110     NITA = NOITVI - NBITEMV(1)
         IF( NITA .LE. 0 ) GOTO 200

C        NUMERO DANS LARETE DE L'ARETE VISIBLE APPARTENANT A 1 FACE
         NAR = NUITEMA( 4, NITA )

C        TRACE de la 1FACE DE L'ARETE NAR
         N1F = LARETE( 4, NAR )
         IF( N1F .LE. 0 ) GOTO 200
         NCF = NCORAN
         NCA = NCROUG
         CALL XVEPAISSEUR( 2 )
C        TRACE SANS OMBRAGE NI QUALITE
         CALL TRFACE3D( NCF, NCA, NCNF, NCNS, N1F,
     %                  MCN(MNSOEF), RMCN(MNXYZST) )

C        LE NUMERO DES 2 SOMMETS DE L'ARETE NAR de LARETE
         NS1 = LARETE( 1, NAR )
         NS2 = LARETE( 2, NAR )

C        TRACE DE L'ARETE EPAISSIE
         CALL XVEPAISSEUR( 6 )
         MNS1 = MNXYZST + 3 * NS1 - 3
         MNS2 = MNXYZST + 3 * NS2 - 3
         CALL TRAIT3D( NCA, RMCN(MNS1), RMCN(MNS2) )

cccC        TRACE du NUMERO XYZSOM DES 2 SOMMETS DE L'ARETE NAR 1FACE
ccc         CALL TRST3D( NCNOIR, NS1, RMCN(MNS1) )
ccc         CALL TRST3D( NCNOIR, NS2, RMCN(MNS2) )

         GOTO 200


cccC        TRACE D'UN ITEM ARETE VISIBLE APPARTENANT A 2 FACES
cccC        ---------------------------------------------------
cccC        LE NUMERO LARETE DE L'ARETE DANS LE TABLEAU NAR2F
ccc 120     NITA = NOITVI - NBITEMV(2)
ccc         IF( NITA .LE. 0 ) GOTO 200
cccC        NUMERO DANS LARETE DE L'ARETE VISIBLE APPARTENANT A 2 FACES
ccc         NAR = NUITEMA( 4, NITA )
cccC        LE NUMERO DES 2 SOMMETS DE L'ARETE NAR de LARETE
ccc         NS1 = LARETE( 1, NAR )
ccc         NS2 = LARETE( 2, NAR )
cccC        TRACE DE L'ARETE APPARTENANT A 2 FACES SELON LA COULEUR DE L'ITEM
ccc         CALL XVEPAISSEUR( 0 )
ccc         MNS1 = MNXYZST + 3 * NS1 - 3
ccc         MNS2 = MNXYZST + 3 * NS2 - 3
ccc         CALL TRAIT3D( NCNOIR, RMCN(MNS1), RMCN(MNS2) )
cccC        TRACE du NUMERO XYZSOM DES 2 SOMMETS DE L'ARETE NAR
ccc         CALL TRST3D( NCGRIS, NS1, RMCN(MNS1) )
ccc         CALL TRST3D( NCGRIS, NS2, RMCN(MNS2) )
ccc 120     GOTO 200


C        TRACE D'UN ITEM ARETE VISIBLE APPARTENANT A 3 FACES
C        LE NUMERO LARETE DE L'ARETE DANS LE TABLEAU NAR3F
C        ---------------------------------------------------
 130     NITA = NOITVI - NBITEMV(3)
         IF( NITA .LE. 0 ) GOTO 200

C        NUMERO DANS LARETE DE L'ARETE VISIBLE APPARTENANT A 3 FACES
         NAR = NUITEMA( 4, NITA )

C        TRACE des 3 FACES de l'ARETE NAR3F
         CALL XVEPAISSEUR( 4 )
         NCF = NCROSE
         NCA = NCSAUM

         DO 133 K= 4, L1ARET
            N3F = LARETE( K, NAR )
            IF( N3F .LE. 0 ) GOTO 133
C           TRACE SANS OMBRAGE NI QUALITE
            CALL TRFACE3D( NCF, NCA, NCNF, NCNS, N3F,
     %                     MCN(MNSOEF), RMCN(MNXYZST) )
 133     ENDDO

C        LE NUMERO DES 2 SOMMETS DE L'ARETE NAR de LARETE
         NS1 = LARETE( 1, NAR )
         NS2 = LARETE( 2, NAR )

C        TRACE DE L'ARETE EPAISSIE APPARTENANT A 3 FACES
         CALL XVEPAISSEUR( 6 )
         MNS1 = MNXYZST + 3 * NS1 - 3
         MNS2 = MNXYZST + 3 * NS2 - 3
         CALL TRAIT3D( NCA, RMCN(MNS1), RMCN(MNS2) )

cccC        TRACE du NUMERO XYZSOM DES 2 SOMMETS DE L'ARETE NAR
ccc         CALL TRST3D( NCVERT, NS1, RMCN(MNS1) )
ccc         CALL TRST3D( NCVERT, NS2, RMCN(MNS2) )

         GOTO 200


C        TRACE D'UN ITEM ARETE VISIBLE DE RATIO DES FACES TROP FAIBLE
C        LE NUMERO LARETE DE L'ARETE DANS LE TABLEAU NAR2SF
C        ------------------------------------------------------------
 140     NITA = NOITVI - NBITEMV(4)
         IF( NITA .LE. 0 ) GOTO 200

C        NUMERO DANS LARETE DE L'ARETE VISIBLE APPARTENANT A 3 FACES
         NAR = NUITEMA( 4, NITA )

         CALL XVEPAISSEUR( 2 )
         NCF  = NCCYAN
         NCA  = NCMAGE

C        TRACE de la PREMIERE FACE DE L'ARETE NAR2SF
         N2SF1 = LARETE( 4, NAR )
C        TRACE SANS OMBRAGE NI QUALITE
         CALL TRFACE3D( NCF, NCA, NCNF, NCNS, N2SF1,
     %                  MCN(MNSOEF), RMCN(MNXYZST) )

C        TRACE de la SECONDE FACE DE L'ARETE NAR2SF
         N2SF2 = LARETE( 5, NAR )
C        TRACE SANS OMBRAGE NI QUALITE
         CALL TRFACE3D( NCF, NCA, NCNF, NCNS, N2SF2,
     %                  MCN(MNSOEF), RMCN(MNXYZST) )

C        LE NUMERO DES 2 SOMMETS DE L'ARETE NAR de LARETE
         NS1 = LARETE( 1, NAR )
         NS2 = LARETE( 2, NAR )

C        TRACE DE L'ARETE EPAISSIE APPARTENANT A 3 FACES
         CALL XVEPAISSEUR( 6 )
         MNS1 = MNXYZST + 3 * NS1 - 3
         MNS2 = MNXYZST + 3 * NS2 - 3
         CALL TRAIT3D( NCA, RMCN(MNS1), RMCN(MNS2) )

cccC        TRACE du NUMERO XYZSOM DES 2 SOMMETS DE L'ARETE NAR2SF
ccc         CALL TRST3D( NCJAUN, NS1, RMCN(MNS1) )
ccc         CALL TRST3D( NCJAUN, NS2, RMCN(MNS2) )

         GOTO 200


C        TRACE D'UN ITEM ARETE VISIBLE D'UN COUPLE DE 2 FACES COLLEES
C        LE NUMERO LARETE DE L'ARETE DANS LE TABLEAU NAR2TRCOL
C        ------------------------------------------------------------
 150     NITA = NOITVI - NBITEMV(5)
         IF( NITA .LE. 0 ) GOTO 200

C        NUMERO DANS LARETE DE L'ARETE VISIBLE DU COUPLE DE FACES COLLEES
         NAR = NUITEMA( 4, NITA )

         CALL XVEPAISSEUR( 2 )
         NCF = NCTURQ
         NCA = NCVERT

C        TRACE de la PREMIERE FACE DE L'ARETE NAR2TRCOL
         N2TRCOL1 = LARETE( 4, NAR )
C        TRACE SANS OMBRAGE NI QUALITE
         CALL TRFACE3D( NCF, NCA, NCNF, NCNS, N2TRCOL1,
     %                  MCN(MNSOEF), RMCN(MNXYZST) )

C        TRACE de la SECONDE FACE DE L'ARETE NAR2TRCOL
         N2TRCOL2 = LARETE( 5, NAR )
C        TRACE SANS OMBRAGE NI QUALITE
         CALL TRFACE3D( NCF, NCA, NCNF, NCNS, N2TRCOL2,
     %                  MCN(MNSOEF), RMCN(MNXYZST) )

C        LE NUMERO DES 2 SOMMETS DE L'ARETE NAR de LARETE
         NS1 = LARETE( 1, NAR )
         NS2 = LARETE( 2, NAR )

C        TRACE DE L'ARETE EPAISSIE DES 2 TRIANGLES COLLES
         CALL XVEPAISSEUR( 6 )
         MNS1 = MNXYZST + 3 * NS1 - 3
         MNS2 = MNXYZST + 3 * NS2 - 3
         CALL TRAIT3D( NCA, RMCN(MNS1), RMCN(MNS2) )

cccC        TRACE en BLEU du NUMERO XYZSOM DES 2 SOMMETS DE L'ARETE NA2TRCOL
ccc         CALL TRST3D( NCBLEU, NS1, RMCN(MNS1) )
ccc         CALL TRST3D( NCBLEU, NS2, RMCN(MNS2) )

         GOTO 200


C        TRACE D'UN ITEM FACE VISIBLE
C        LE NUMERO NOFACE DANS LE TABLEAU NOSOEF DE LA FACE VISIBLE
C        ----------------------------------------------------------
 160     NITF = NOITVI - NBITEMV(6)
         IF( NITF .LE. 0 ) GOTO 200

C        NUMERO DE LA FACE DANS LE TABLEAU NUSOEF
         NOFACE = NUITEMF( 4, NITF )

C        LE NUMERO DES NBSOEF SOMMETS DE LA FACE NOFACE
         CALL NSEFNS( NOFACE, NUTYMA, NBSOEF, NBTGEF,
     %                LDAPEF, LDNGEF, LDTGEF,
     %                MNNSEF, NX, NY, NZ,
     %                NCOGEL, NUGEEF, NUEFTG, NOSOEL, IER )
         IF( IER .NE. 0 ) GOTO 200
         IF( NOSOEL(1) .EQ. 0 ) GOTO 200

C        POUR LA POIGNEE DE LA SURFACE
         NOFASU = NOFACE

C        NOMBRE D'EPAISSEURS DES ARETES
         CALL XVEPAISSEUR( 0 )

C        LE NOMBRE DE SOMMETS DE CET ELEMENT FINI EST NCOGEL=3 OU 4
         IF( NBRCOU .EQ. 0 ) THEN

C           ECRAN NOIR ET BLANC
C           -------------------
C           NO COULEUR DES ARETES DE LA FACE
            NCOUAF = NCBLAN
C           NO COULEUR DE REMPLISSAGE de la FACE
            NCOUFA = NCNOIR
C
         ELSE IF( LCRITR .EQ. 0 ) THEN

C           CALCUL DE LA COULEUR SELON DIRECTION DE VISEE ET ELOIGNEMENT
C           ------------------------------------------------------------
C           L'ELOIGNEMENT DE LA FACE
            R  = ( DIITOEIL(NITF) - DISTMIN ) / DIMIMX
            R2 = ( SQRT(R+EP1)    - EP2  ) / EP3

C           L'OTHOGONALITE A LA FACE
C           XYZNORFA LES COORDONNEES DE LA NORMALE A LA FACE (NORME=1)
            CALL NORF34( NCOGEL, NOSOEL, RMCN(MNXYZST), XYZNORFA, IERR )
            IF( IERR .NE. 0 ) GOTO 200
C
C           LE PRODUIT SCALAIRE DIRECTION VISEE ET NORMALE A LA FACE
            R = PROSCR( DIREVI, XYZNORFA, 3 )
            R = 1. - ABS(R)

            IF( IAVELO .NE. 0 ) THEN
C              LA COULEUR PONDEREE PAR LA DIRECTION ET L'ELOIGNEMENT
               R =  R * POID + POID1 * R2
            ENDIF

            NCOUFA = NINT( NBRCOU * R + N1COUL )

         ELSE IF( LCRITR .GT. 0 ) THEN
C
C           COULEUR DE LA FACE SELON LA QUALITE DE CET EF
C           ---------------------------------------------
C           CALCUL DE LA QUALITE DE L'ELEMENT FINI
            CALL QUALEF( NCOGEL,  NOSOEL, NBSOM, RMCN(MNXYZST),
     %                   SURFVOLU,QUALIT, IERR )
            IF ( IERR .NE. 0 ) GOTO 200

C           LA COULEUR VISUALISE LA QUALITE CROISSANTE de ROUGE a VERT a BLEU
            NCOUFA = NCOQUA( QUALIT )

         ELSE

C           COULEUR DE LA FACE SELON UN DEGRADE DES 3 COULEURS AUX SOMMETS
C           --------------------------------------------------------------
            DO J=1,NCOGEL

C              LES 3 COORDONNEES DES NCOGEL SOMMETS
