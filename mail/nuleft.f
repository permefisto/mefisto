      SUBROUTINE NULEFT( NFNTE, NTE, NOTETR, MXFACO, LEFACO,  NFLEFA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER LE NUMERO NFLEFA DANS LE TABLEAU LEFACO DE LA
C -----    FACE NFNTE DU TETRAEDRE NTE DE NOTETR

C ENTREES :
C ---------
C NFNTE  : NUMERO DE 1 A 4 DE LA FACE DU TETRAEDRE NTE
C NTE    : NUMERO DU TETRAEDRE DANS NOTETR
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1 < SOMMET 2 < SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: CHAINAGE DU HACHAGE DES FACES ISSUES DE LA FONCTION DE HACHAGE
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: NUMERO DE LA PREMIERE FACE ISSUE DE LA FONCTION DE HACHAGE
C              LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3), MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...

C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
CCCC       LEFACO(12,*) NO DE FACE OpenCascade

C SORTIES :
C ---------
C NFLEFA  : >0 NUMERO LEFACO DE LA FACE NFNTE DE NTE SI ELLE EST RETROUVEE
C           =0 SI LA FACE N'EST PAS RETROUVEE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS UPMC        JUIN 1991
C MODIFS : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY Novembre 2016
C23456...............................................................012
      INTEGER    NOTETR(8,*), LEFACO(1:11,0:MXFACO), NOSOTR(3)
      INTEGER    NOSOFATE(3,4)
      DATA       NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /
C                NORMALE VERS L'EXTERIEUR DE LA FACE

      IF( NOTETR(1,NTE) .EQ. 0 ) THEN
         NFLEFA = 0
         GOTO 9999
      ENDIF

C     LE NUMERO DES 3 SOMMETS DE LA FACE NF DU TETRAEDRE NTE
      DO N = 1, 3
         NOSOTR( N ) = NOTETR( NOSOFATE(N,NFNTE), NTE )
      ENDDO

C     LA FACE DE SOMMETS NOSOTR(1:3) EXISTE-T-ELLE DEJA DANS LEFACO?
C     HACHAGE DANS LEFACO AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
      CALL NULETR( NOSOTR, MXFACO, LEFACO,  NFLEFA )

 9999 RETURN
      END
