      SUBROUTINE F1PTVPSI( QUAMINEX, XYZPT,   PTXYZD, N1FEOC, NFETOI,
     %                     NF0,      NBVPSI0, NFVPSI0,
     %                     MXVPSI,   NBVPSI,  NFVPSI, VETXYZ, QUAMIN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C  BUT :   DETERMINER L'ENSEMBLE DES FACES CONTIGUES DE L'ETOILE FORMANT
C ------   AVEC LE POINT XYZPT DES TETRAEDRES DE VOLUMES>0
C          SANS INTERSECTION AVEC LES FACES DE L'ETOILE.
C          DE PLUS, CES FACES MAXIMISENT LE MINIMUM DES QUALITES
C          DES TETRAEDRES  XYZPT-FACES

C ENTREES:
C --------
C QUAMINEX:QUALITE MINIMALE EXIGEE POUR CREER UN TETRAEDRE
C XYZPT  : COORDONNEES XYZ DU POINT A TESTER COMME CENTRE DE TETRAEDRISATION
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : LES FACES TRIANGULAIRES DE L'ETOILE VERSION 2
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE (0 SI INCONNU)
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C          S1S2 x S1S3 VERS L'INTERIEUR DE L'ETOILE

C NF0    : FACE INITIALE POUR TROUVER SES FACES CONTIGUES PARMI NFVPSI0
C NBVPSI0: NOMBRE DE TETRAEDRES DE VOLUME>0 SANS INTERSECTION
C          AVEC LES FACES DE L'ETOILE
C NFVPSI0: NUMERO NFETOI DES NBVPSI0 FACES DONNANT AVEC XYZPT UN VOLUME>0
C          ET SANS INTERSECTION AVEC LES AUTRES FACES DE L'ETOILE

C MXVPSI : NOMBRE MAXIMAL DE FACES DU TABLEAU NFVPSI

C SORTIES:
C --------
C NBVPSI : NOMBRE DE TETRAEDRES DE VOLUME>0 SANS INTERSECTION
C          AVEC LES FACES DE L'ETOILE
C          = -MXVPSI SI NBVPSI>MXVPSI=MXPILE
C NFVPSI : NUMERO NFETOI DES NBVPSI FACES DONNANT AVEC XYZPT UN VOLUME>0
C          ET SANS INTERSECTION AVEC LES FACES DE L'ETOILE
C VETXYZ : VOLUME DE L'ETOILE DE CENTRE XYZPT DES TETRAEDRES XYZPT-FACES
C QUAMIN : QUALITE MINIMALE DES QUALITES DES TETRAEDRES XYZPT-FACES
C          SI PAS DE TETRAEDRE LA QUALITE MINIMALE EST ZERO
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY     Aout 2015
C2345X7..............................................................012
      PARAMETER        (MXPILE=2048)
      COMMON / TRTETR / STOPTE,TRACTE
      LOGICAL           STOPTE,TRACTE
      DOUBLE PRECISION  PTXYZD(4,*), XYZPT(3), V, VETXYZ,
     %                  SURFTR(4), ARMIN, ARMAX
      INTEGER           NFETOI(5,*),    NFVPSI (MXVPSI)
      INTEGER           LAPILE(MXPILE), NFVPSI0(NBVPSI0)
      REAL              QUALIT, QUAMIN

C     INITIALISATION DE LA QUALITE MINIMALE DES TETRAEDRES
      VETXYZ = 0D0
      QUAMIN = 3.
      NBVPSI = 0

C     MARQUAGE SUPPRIME DES FACES DE L'ETOILE
      NF1 = N1FEOC
 10   IF( NF1 .GT. 0 ) THEN
         NFETOI(2,NF1) = ABS( NFETOI(2,NF1) )
         NF1 = NFETOI(5,NF1)
         GOTO 10
      ENDIF

C     INITIALISATION DE LA PILE DES FACES
      LAPILE(1) = NF0
      LHPILE = 1

 20   IF( LHPILE .GT. 0 ) THEN

C           TRAITEMENT DE LA FACE NF1 EN HAUT DE PILE
            NF1 = LAPILE( LHPILE )

C           LA FACE EST DEPILEE
            LHPILE = LHPILE - 1

C           CETTE FACE EST ELLE DEJA TRAITEE?
            IF( NFETOI(2,NF1) .LE. 0 ) GOTO 20

C           ICI, LE TETRAEDRE FACE NF1+XYZPT EST V>0 ET SANS INTERSECTION
C           AVEC L'ETOILE. LA FACE GENERERA UN TETRAEDRE AVEC XYZPT
C           SI LA FACE N'EST PAS DEJA DANS NFVPSI
            DO K=1,NBVPSI
               IF( NF1 .EQ. NFVPSI(K) ) GOTO 60
            ENDDO

C           QUALITE DU TETRAEDRE NF1-XYZPT
            CALL QUATETD( PTXYZD(1,NFETOI(2,NF1) ),
     %                    PTXYZD(1,NFETOI(3,NF1) ),
     %                    PTXYZD(1,NFETOI(4,NF1) ),
     %                    XYZPT,
     %                    ARMIN, ARMAX, SURFTR, V, QUALIT )

C           LA QUALITE DU TETRAEDRE NF1-XYZPT DOIT ETRE >QUAMINEX
            IF( QUALIT .LE. QUAMINEX ) THEN
               GOTO 20
            ENDIF

C           FACE ACCEPTEE
            NBVPSI = NBVPSI + 1
            NFVPSI( NBVPSI ) = NF1

C           LA QUALITE MINIMALE DES TETRAEDRES NFVPSI
            IF( QUALIT .LT. QUAMIN ) QUAMIN = QUALIT

C           VOLUME DE L'ETOILE DE CENTRE XYZPT DES TETRAEDRES ACCEPTES
            VETXYZ = VETXYZ + ABS( V )

C           LES FACES ADJACENTES DE NF1 PAR LES ARETES SONT EMPILEES
C           POUR ETRE TRAITEES ENSUITE POUR LA CONTINUITE DES FACES
            NS1 = NFETOI(4,NF1)
            DO K=1,3

C              ARETE K DE LA FACE NF1
               NBFARK = 0
               NS2 = ABS( NFETOI(1+K,NF1) )

C              RECHERCHE DANS NFVPSI0 DES FACES D'ARETE NS1-NS2
               DO MM = 1, NBVPSI0
                  NF2 = NFVPSI0( MM )
                  IF( NF2 .NE. NF1 .AND. NFETOI(2,NF2) .GT. 0 ) THEN
C                    LA FACE NF2 N'EST PAS NF1 ET N'EST PAS TRAITEE
C                    PARCOURS DE SES 3 ARETES
                     NSS1 = NFETOI(4,NF2)
                     DO KK=1,3
                        NSS2 = NFETOI(1+KK,NF2)
                        IF( (NS1.EQ.NSS2 .AND. NS2.EQ.NSS1) .OR.
     %                      (NS1.EQ.NSS1 .AND. NS2.EQ.NSS2) ) THEN
                           NFADJ  = NF2
                           NBFARK = NBFARK + 1
                           GOTO 30
                        ENDIF
                        NSS1 = NSS2
                     ENDDO
                  ENDIF
               ENDDO

C              LA FACE ADJACENTE EST EMPILEE SI CE N'EST PAS ENCORE FAIT
 30            IF( NBFARK .EQ. 0 ) THEN
C                 PAS DE FACE ADJACENTE DE NF1 SUR SON ARETE K
                  GOTO 50
               ELSE IF( NBFARK .EQ. 1 ) THEN
C                 UNE SEULE FACE ADJACENTE NON NF1
C                 ELLE EST EMPILEE
                  LHPILE = LHPILE + 1
                  LAPILE( LHPILE ) = NFADJ
               ELSE
C                 PROBLEME: IL Y A PLUS DE 2 FACES ADJACENTES
C                           PAR L'ARETE K DE NF1
C                        => L'ETOILE ACTUELLE SE CROISE
                  PRINT *,'f1ptvpsi: ETOILE SE CROISE SUR L''ARETE',K,
     %            ' DE SOMMETS',NS1,NS2,' DE LA FACE',NF1,' A TRAITER!'
                  PRINT *,'f1ptvpsi: NFETOI(',NF1,')=',
     %            (NFETOI(MM,NF1),MM=2,4)
               ENDIF

C              ARETE K SUIVANTE DE LA FACE NF1
 50            NS1 = NS2
            ENDDO

C           LA FACE NF1 EST MARQUEE COMME ETANT TRAITEE
 60         NFETOI(2,NF1) = -ABS( NFETOI(2,NF1) )

C           RETOUR EN HAUT DE PILE
            GOTO 20

      ENDIF

C     MARQUAGE SUPPRIME DU SOMMET 1 DES FACES DE L'ETOILE DANS NFETOI
      NF1 = N1FEOC
 80   IF( NF1 .GT. 0 ) THEN
         NFETOI(2,NF1) = ABS( NFETOI(2,NF1) )
         NF1 = NFETOI(5,NF1)
         GOTO 80
      ENDIF

      IF( NBVPSI .LE. 0 ) THEN
C        SI PAS DE TETRAEDRE LA QUALITE MINIMALE EST ZERO
         QUAMIN = 0.
      ENDIF

      RETURN
      END
