      SUBROUTINE SUEX20( NUSFEX, NTLXSF, LADEFI,
     %                   NTNSEF, MNNSEF, NTXYZS, MNXYZS, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES FACES DE L'UNION DES SURFACES EXTRUDEES
C -----    D'UN POLYGONE DEFINI PAR SES SOMMETS ET LES 2 POINTS
C          DE DEFINITION DU VECTEUR TRANSLATION
C          2 SURFACES POLYGONALES ET DES SURFACES EXTRUDEES
C          DES LIGNES DU POLYGONE
C
C ENTREES:
C --------
C NUSFEX : NUMERO DE LA SURFACE DANS SON LEXIQUE
C NTLXSF : NUMERO DU TABLEAU TMS DU LEXIQUE DE LA SURFACE
C LADEFI : TABLEAU ENTIER DES DONNEES DE LA DEFINITION DE LA SURFACE
C          CF ~/td/d/a_surface__definition
C
C SORTIES:
C --------
C NTNSEF : NUMERO      DU TMS 'NSEF' DES NUMEROS DES FACES DES SURFACES
C MNNSEF : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES FACES DES SURFACES
C          CF ~/td/d/a___nsef
C NTXYZS : NUMERO      DU TMS 'XYZSOMMET' DE LA LIGNE UNION DES SURFACES
C MNXYZS : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA LIGNE UNION DES SURFACES
C          CF ~/td/d/a___xyzsommet
C IERR   : 0 SI PAS D'ERREUR, >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE DU PERRAY  FEVRIER 2010
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/a_point__definition.inc"
      include"./incl/a_ligne__definition.inc"
      include"./incl/a_surface__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/darete.inc"
C
C     LE SUPER-TABLEAU OU TOUS LES TMC ET TMS OUVERTS SONT STOCKES
C     ICI LE TABLEAU DMCN REEL DOUBLE PRECISION N'EST PAS UTILE
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL             RMCN(1)
      DOUBLE PRECISION DMCN(1)
      EQUIVALENCE   ( MCN(1), RMCN(1), DMCN(1) )
C
C     LE NUMERO D'UNITE DU CLAVIER, FENETRE DES AFFICHAGES ET
C     LE PARAMETRE DE NIVEAU D'INTERACTIVITE
      COMMON / UNITES / LECTEU, IMPRIM, INTERA, NUNITE(29)
C
      CHARACTER*24   NOMLIG, NOMSUR, NMSFEX
      CHARACTER*4    KNBRE
      CHARACTER*10   INIFIN
C
C     LE TABLEAU DES DONNEES DE LA DEFINITION DE LA SURFACE
C     VU SOUS FORME D'UN TABLEAU D'ENTIERS
      INTEGER        LADEFI(0:*)
C
      REAL           ARETMX
      INTEGER        OUVFER
C
      MOREE2 = MOTVAR(6)
      MNNULG = 0
      MNULGU = 0
      MNUSFU = 0
C
C     LE NOM DE LA SURFACE
      CALL NMOBNU( 'SURFACE', NUSFEX, NMSFEX )
C
C     RECUPERATION DES 3 COORDONNEES DES NBPTEX POINTS DU POLYGONE INITIAL
C     TRANSLATION  DES 3 COORDONNEES DES NBPTEX POINTS DU POLYGONE FINAL
C     CONSTRUCTION DES NBPTEX LIGNES DU POLYGONE INITIAL
C     POUR FERMER LE POLYGONE, LE DERNIER POINT EST LE MEME QUE LE PREMIER
C     CONSTRUCTION DES NBPTEX LIGNES DU POLYGONE FINAL
C     CONSTRUCTION DES NBPTEX LIGNES EXTRUDEES DES SOMMETS DU POLYGONE
C     --------------------------------------------------------------------
      OUVFER = LADEFI(WUFEES)
      NBPTEX = LADEFI(WBPTES)
      CALL CRLI20( NMSFEX, LADEFI(W2PTVT), OUVFER,
     %             NBPTEX, LADEFI(WUPTES),
     %             NBLG, MNNULG, IERR )
      IF( IERR .NE. 0 ) GOTO 9999
C
C     NBLGP NOMBRE DE LIGNES DU POLYGONE
      IF( OUVFER .EQ. 0 ) THEN
C        POLYGONE OUVERT
         NBLGP = NBPTEX - 1
      ELSE
C        POLYGONE FERME
         NBLGP = NBPTEX
      ENDIF
      CALL TNMCDC( 'ENTIER', 2+NBLGP, MNULGU )
      CALL TNMCDC( 'ENTIER', 2+NBLGP, MNUSFU )
C
      NBSF = 0
      IF( OUVFER .EQ. 0 ) THEN
C        POLYGONE OUVERT => PAS DE SURFACE DES 2 POLYGONES
         GOTO 20
      ENDIF
C
C     CONSTRUCTION DU MAILLAGE DE LA SURFACE DES 2 POLYGONES PLANS
C     ------------------------------------------------------------
C     ARETMX MAX LENGTH OF EDGES OF REGULAR TRIANGLES
      ARETMX = 1E25
      IF( DARETE .GT. 0D0 ) THEN
         ARETMX = REAL( DARETE )
      ENDIF
C
C     LE NOM DE LA SURFACE EST CELUI DE LA SURFACE UNION AVEC SON NUMERO
      DO L = 1, 2
         NOMLIG = NMSFEX
         N    = NUDCNB( NOMLIG )
C        UNE LIGNE DE PLUS
         NBLG = NBLG + 1
         IF( NBLG .LT. 10 ) THEN
            WRITE(KNBRE(1:1),'(I1)') NBLG
            N = MIN(N,22)
            NOMLIG = NOMLIG(1:N) // '_' // KNBRE(1:1)
         ELSE
            WRITE(KNBRE(1:2),'(I2)') NBLG
            N = MIN(N,21)
            NOMLIG = NOMLIG(1:N) // '_' // KNBRE(1:2)
         ENDIF
C
C        SI CETTE LIGNE EXISTE, ELLE EST DETRUITE
         CALL LXLXOU( NTLIGN, NOMLIG, NT1LG, MN1LG )
         IF( MN1LG .GT. 0 ) CALL LXTSDS( NTLIGN, NOMLIG )
C
C        CONSTRUCTION DE LA LIGNE UNION DES LIGNES DU POLYGONE
         CALL LXLXDC( NTLIGN, NOMLIG, 24, 8 )
         CALL LXLXOU( NTLIGN, NOMLIG, NT1LG, MN1LG )
C        NUMERO DE LA LIGNE DANS LE LX DES LIGNES
         CALL NUOBNM( 'LIGNE', NOMLIG, MCN(MNULGU-1+L) )
C
C        LA LIGNE A UN TMS DEFINITION DE LIGNE UNION DE LIGNES
         CALL LXTNDC( NT1LG, 'DEFINITION', 'MOTS', WULIUN+NBLGP )
         CALL LXTSOU( NT1LG, 'DEFINITION', NT1LDE, MN1LDE )
C        TRANSFORMATION (I pour IDENTITE)
         MCN( MN1LDE + WTYTRL ) = 1
C        TYPE DE LA LIGNE
         MCN( MN1LDE + WUTYLI ) = 51
C        NOMBRE DE LIGNES DE LA LIGNE UNION
         MCN( MN1LDE + WBLIUN ) = NBLGP
C        LES NBLGP NUMEROS DES LIGNES DANS LE LEXIQUE LIGNE
         DO K = 1, NBLGP
            MCN( MN1LDE+WULIUN-1+K )=MCN( MNNULG-1+K+(L-1)*NBLGP )
         ENDDO
C        AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MN1LDE + MOTVAR(6) ) = NONMTD( '~>LIGNE>>DEFINITION' )
C        AJOUT DE LA DATE
         CALL ECDATE( MCN(MN1LDE) )
C
C        LA LIGNE UNION DES LIGNES DU POLYGONE INITIAL
         CALL UNPLSV( 51,  2, MCN(MNULGU-1+L),
     %                NBLGP,  MCN(MNNULG+(L-1)*NBLGP),
     %                NTNSLG, MNNSLG, NTXYLG, MNXYLG,
     %                NTUNIL, MNUNIL, IERR   )
C
C        CONSTRUCTION DES TMS DEFINITION, NSEF et XYZSOMMET DE LA
C        SURFACE DU POLYGONE
         NOMSUR = NMSFEX
         N    = NUDCNB( NOMSUR )
C        UNE SURFACE DE PLUS
         NBSF = NBSF + 1
         IF( NBSF .LT. 10 ) THEN
            WRITE(KNBRE(1:1),'(I1)') NBSF
            N = MIN(N,22)
            NOMSUR = NOMSUR(1:N) // '_' // KNBRE(1:1)
         ELSE
            WRITE(KNBRE(1:2),'(I2)') NBSF
            N = MIN(N,21)
            NOMSUR = NOMSUR(1:N) // '_' // KNBRE(1:2)
         ENDIF
C
C        SI CETTE SURFACE EXISTE, ELLE EST DETRUITE
         CALL LXLXOU( NTSURF, NOMSUR, NT1SF, MN1SF )
         IF( MN1SF .GT. 0 ) CALL LXTSDS( NTSURF, NOMSUR )
C
C        CONSTRUCTION DE LA SURFACE UNION DES SURFACES DU POLYGONE
         CALL LXLXDC( NTSURF, NOMSUR, 24, 8 )
         CALL LXLXOU( NTSURF, NOMSUR, NT1SF, MN1SF )
C        NUMERO DE LA SURFACE DANS LE LX DES SURFACES
         CALL NUOBNM( 'SURFACE', NOMSUR, MCN(MNUSFU-1+L) )
C
C        LA SURFACE A UN TMS DEFINITION DE SURFACE 9:
C        TRIANGULATION ENTRE LIGNES FERMEES
         CALL LXTNDC( NT1SF, 'DEFINITION', 'MOTS', WULFTR+NBLGP )
         CALL LXTSOU( NT1SF, 'DEFINITION', NT1SDE, MN1SDE )
C        TRANSFORMATION (I POUR IDENTITE)
         MCN( MN1SDE + WTYTRS ) = 1
C        TYPE DE LA SURFACE
         MCN( MN1SDE + WUTYSU ) = 9
C        ARETMX MAX LENGTH OF EDGES OF REGULAR TRIANGLES
         RMCN( MN1SDE + WRETMX ) = ARETMX
C        NBLFTR  NUMBER OF CLOSED LINES CONTOUR OF THE SURFACE
         MCN( MN1SDE + WBLFTR ) = 1
C        NBPTIT NUMBER OF INTERNAL POINTS FUTURE VERTICES
         MCN( MN1SDE + WBPTIT ) = 0
C        TABLEAU  NULFTR(1..NBLFTR) NAME OF CLOSED LINES ^~>LIGNE
         MCN( MN1SDE + WULFTR ) = MCN(MNULGU-1+L)
C        TABLEAU NUPTIT(1..NBPTIT) NAME OF INTERNAL POINTS FUTURE VERTICES
C        INEXISTANT CAR NBPTIT=0
C        AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MN1SDE + MOTVAR(6) ) = NONMTD( '~>SURFACE>>DEFINITION' )
C        AJOUT DE LA DATE
         CALL ECDATE( MCN(MN1SDE) )
C
C        TRIANGULATION ENTRE DES LIGNES FERMEES PLANES
         CALL SUEX09( 9, NT1SF, MCN(MN1SDE), RMCN(MN1SDE),
     %                NTFASU, MNFASU, NTSOFA, MNSOFA, IERR )
         IF( IERR .GT. 0 ) GOTO 9999
C
         IF( L .EQ. 1 ) THEN
            INIFIN = 'INITIAL'
         ELSE
            IF( LANGAG .EQ. 0 ) THEN
               INIFIN = 'TRANSLATE'
            ELSE
               INIFIN = 'TRANSLATED'
            ENDIF
         ENDIF
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10010) L, INIFIN, NOMSUR
         ELSE
            WRITE(IMPRIM,20010) INIFIN, L, NOMSUR
         ENDIF
C
      ENDDO
C
10010 FORMAT('LA SURFACE',I4,' DU POLYGONE ',A10,' EST ',A)
20010 FORMAT('THE ',A10,' POLYGON SURFACE',I4,' IS ',A)
C
C
C     CONSTRUCTION DU MAILLAGE DES SURFACES EXTRUDEES DES LIGNES DU POLYGONE
C     ----------------------------------------------------------------------
 20   IF( LANGAG .EQ. 0 ) THEN
         INIFIN = 'EXTRUDE'
      ELSE
         INIFIN = 'EXTRUDED'
      ENDIF
C
C     LE NOM DE LA SURFACE EST CELUI DE LA SURFACE UNION AVEC SON NUMERO
      NBLGP2 = 2 * NBLGP
      DO L = 1, NBLGP
         NOMSUR = NMSFEX
         IF( OUVFER .EQ. 0 .AND. NBPTEX .EQ. 2 ) THEN
C           POLYGONE OUVERT A 2 POINTS
C           LE NOM DE LA SURFACE EST CELLE DE L'UNION DES SURFACES
            GOTO 30
         ENDIF
         N = NUDCNB( NOMSUR )
C        UNE SURFACE DE PLUS
         NBSF = NBSF + 1
         IF( NBSF .LT. 10 ) THEN
            WRITE(KNBRE(1:1),'(I1)') NBSF
            N = MIN(N,22)
            NOMSUR = NOMSUR(1:N) // '_' // KNBRE(1:1)
         ELSE
            WRITE(KNBRE(1:2),'(I2)') NBSF
            N = MIN(N,21)
            NOMSUR = NOMSUR(1:N) // '_' // KNBRE(1:2)
         ENDIF
C
C        CONSTRUCTION DES TMS DEFINITION, NSEF et XYZSOMMET DE LA
C        SURFACE EXTRUDEE D'UNE LIGNE DU POLYGONE
C        SI CETTE SURFACE EXISTE, ELLE EST DETRUITE
 30      CALL LXLXOU( NTSURF, NOMSUR, NT1SF, MN1SF )
         IF( MN1SF .GT. 0 ) CALL LXTSDS( NTSURF, NOMSUR )
C
C        CONSTRUCTION DE LA SURFACE UNION DES SURFACES DU POLYGONE
         CALL LXLXDC( NTSURF, NOMSUR, 24, 8 )
         CALL LXLXOU( NTSURF, NOMSUR, NT1SF, MN1SF )
C        NUMERO DE LA SURFACE DANS LE LX DES SURFACES
         CALL NUOBNM( 'SURFACE', NOMSUR, MCN(MNUSFU+1+L) )
C
C        LA SURFACE A UN TMS DEFINITION DE SURFACE 1:
C        QUADRANGLE DEFINI PAR LES LIGNES STRUCTUREES DES 4 COTES
         CALL LXTNDC( NT1SF, 'DEFINITION', 'MOTS', WU4COT+5 )
         CALL LXTSOU( NT1SF, 'DEFINITION', NT1SDE, MN1SDE )
C        TRANSFORMATION (I POUR IDENTITE)
         MCN( MN1SDE + WTYTRS ) = 1
C        TYPE DE LA SURFACE
         MCN( MN1SDE + WUTYSU ) = 1
C        MAILLAGE IRREGULIER
         MCN( MN1SDE + WAGHER ) = 2
C        NOM-NUMERO DES 4 LIGNES DES 4 COTES DU QUADRANGLE
         MCN( MN1SDE + WU4COT ) = MCN( MNNULG-1+ L )
         IF( OUVFER .NE. 0 .AND. L .EQ. NBLGP ) THEN
C           LA DERNIERE LIGNE EST LA PREMIERE EXTRUDEE
            NL = MCN( MNNULG + NBLGP2 )
         ELSE
            NL = MCN( MNNULG + NBLGP2 + L )
         ENDIF
         MCN( MN1SDE + WU4COT + 1 ) = NL
         MCN( MN1SDE + WU4COT + 2 ) = MCN( MNNULG-1+NBLGP + L )
         MCN( MN1SDE + WU4COT + 3 ) = MCN( MNNULG-1+NBLGP2+ L )
         print *,'NOLG DES 4 COTES',(MCN(MN1SDE + WU4COT+m),m=0,3)
C        AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MN1SDE + MOTVAR(6) ) = NONMTD( '~>SURFACE>>DEFINITION' )
C        AJOUT DE LA DATE
         CALL ECDATE( MCN(MN1SDE) )
C
C        QUADRANGLE DEFINI PAR LES LIGNES STRUCTUREES DES 4 COTES
         CALL SUEX01( NT1SF, MCN(MN1SDE), RMCN(MN1SDE),
     %                NTFASU, MNFASU, NTSOFA, MNSOFA, IERR )
         IF( IERR .GT. 0 ) GOTO 9999
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,10010) L, INIFIN, NOMSUR
         ELSE
            WRITE(IMPRIM,20010) INIFIN, L, NOMSUR
         ENDIF
C
      ENDDO
C
C     UNION DES NBSF SURFACES 2 POLYGONES ET EXTRUDEES
C     ------------------------------------------------
      IF( NBLGP .GT. 1 ) THEN
C
C        MODIFICATION DU TMS DEFINITION DE LA SURFACE
C        NECESSAIRE POUR LE RE-MAILLAGE AUTOMATIQUE
         CALL LXTSOU( NTLXSF, 'DEFINITION', NT1SDE, MN1SDE )
         IF( NT1SDE .GT. 0 ) CALL LXTSDS( NTLXSF, 'DEFINITION' )
C        LA SURFACE A UN TMS DEFINITION DE SURFACE UNION DE SURFACES
         CALL LXTNDC( NTLXSF, 'DEFINITION', 'MOTS', WUSUUN+NBSF )
         CALL LXTSOU( NTLXSF, 'DEFINITION', NT1SDE, MN1SDE )
C        TRANSFORMATION (I pour IDENTITE)
         MCN( MN1SDE + WTYTRS ) = 1
C        TYPE DE LA SURFACE
         MCN( MN1SDE + WUTYSU ) = 51
C        NOMBRE DE SURFACES DE LA SURFACE UNION
         MCN( MN1SDE + WBSUUN ) = NBSF
C        LES NBSF NUMEROS DES SURFACES DANS LE LEXIQUE SURFACE
         DO K = 1, NBSF
            MCN( MN1SDE+WUSUUN-1+K ) = MCN( MNUSFU-1+K )
         ENDDO
C        AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
         MCN( MN1SDE + MOTVAR(6) ) = NONMTD( '~>SURFACE>>DEFINITION' )
C        AJOUT DE LA DATE
         CALL ECDATE( MCN(MN1SDE) )
C
C        CREATION DU MAILLAGE UNION DES SURFACES
C        PERMUTATION POUR AVOIR UNE NORMALE VERS L'EXTERIEUR
         N             = MCN(MNUSFU)
         MCN(MNUSFU)   = MCN(MNUSFU+1)
         MCN(MNUSFU+1) = N
         CALL UNPLSV( 51,  3, NUSFEX, NBSF,   MCN(MNUSFU),
     %                NTNSEF, MNNSEF, NTXYZS, MNXYZS,
     %                NTUNIO, MNUNIO, IERR  )
      ELSE
C        LA SURFACE EST L'UNIQUE SURFACE EXTRUDEE
         NTNSEF = NTFASU
         MNNSEF = MNFASU
         NTXYZS = NTSOFA
         MNXYZS = MNSOFA
      ENDIF
C
C     DESTRUCTION DES TABLEAUX MC DEVENUS INUTILES
 9999 IF( MNULGU .GT. 0 ) CALL TNMCDS( 'ENTIER', 2+NBLGP,  MNULGU )
      IF( MNUSFU .GT. 0 ) CALL TNMCDS( 'ENTIER', 2+NBLGP,  MNUSFU )
      IF( MNNULG .GT. 0 ) CALL TNMCDS( 'ENTIER', 3*NBPTEX, MNNULG )
C
      RETURN
      END
