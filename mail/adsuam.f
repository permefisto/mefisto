      SUBROUTINE ADSUAM( TARMAX, MXPXYD, NBPXYD, PXYD,
     %                   MXTRIA, NBTRIA, N1TRVI, NOTRIA, NOTRSO, NOSUTR,
     %                   NUPLIS, NUISOP,
     %                   NUMILF, NUMXLF, L1LGFR,
     %                   MXARCH, NBARCH, N1ARCH, NSARCH,
     %                   IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   SUPPRIMER LES ARETES TROP PETITES ET LES SOMMETS "MILIEUX"
C -----   D'UNE TRIANGULATION ADAPTEE
C
C ENTREES:
C --------
C TARMAX : TAILLE DE L'ARETE MAXIMALE PERMISE SUR UNE FRONTIERE
C MXPXYD : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS PXYD
C NBPXYD : NOMBRE DE SOMMETS ACTUELS DU TABLEAU PXYD
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C MXTRIA : NOMBRE MAXIMAL DE TRIANGLES DECLARABLES DANS LE TABLEAU NOTRIA
C NBTRIA : NOMBRE INITIAL DE TRIANGLES OCCUPES DANS NOTRIA
C NBISO  : NOMBRE D'ISOVALEURS
C
C ENTREES ET SORTIES :
C --------------------
C N1TRVI : NUMERO DU PREMIER TRIANGLE VIDE DANS NOTRIA
C          (CHAINAGE DANS NOTRIA(4,*))
C NOTRIA : LISTE DES TRIANGLES ST1 ST2 ST3 TR1 TR2 TR3
C          ST NUMERO DANS PXYD DU SOMMET
C NOTRSO : NUMERO D'UN TRIANGLE DANS NOTRIA POUR CHAQUE SOMMET
C          0 SI LE SOMMET APPARTIENT A AUCUN TRIANGLE DE NOTRIA
C NOSUTR : NUMERO DE SURFACE DE CHAQUE TRIANGLE
C NUPLIS : NUMERO DE POINT OU LIGNE DE CHACUN DES SOMMETS
C          -2 000 000 - NUPLIS() ANCIEN SI LE SOMMET A DEJA ETE SUPPRIME
C          -NP SI NP EST LE NUMERO DU POINT UTILISATEUR DE CE SOMMET
C          -1 234 567 SI LE SOMMET APPARTIENT A 2 LIGNES (SOMMET INITIAL)
C          NU LIGNE SI LE SOMMET EST SUR UNE LIGNE UTILISATEUR
C          0        SINON
C NUISOP : NUMERO DE L'ISO DU POINT ET 0 SINON
C
C NUMILF : NUMERO MINIMAL DES LIGNES FRONTIERES DE L'OBJET
C NUMXLF : NUMERO MAXIMAL DES LIGNES FRONTIERES DE L'OBJET
C L1LGFR : POUR CHAQUE LIGNE DE NUMILF A NUMXLF POINTE SUR LA PREMIERE
C          ARETE CHAINEE DE LA LIGNE
C
C MXARCH : NOMBRE MAXIMAL D'ARETES DECLARABLES DANS NSARCH
C NBARCH : NOMBRE D'ARETES CHAINEES DANS NSARCH
C N1ARCH : NUMERO DANS NSARCH DE LA PREMIERE ARETE VIDE
C NSARCH : NUMERO DES 2 SOMMETS DE L'ARETE, ARETE PRECEDENTE ET SUIVANTE
C
C IERR   : 0 SI PAS D'ERREUR DETECTEE, NON NUL SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1995
C....................................................................012
      include"./incl/gsmenu.inc"
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      DOUBLE PRECISION  PXYD(3,MXPXYD)
      INTEGER           NOTRIA(6,MXTRIA),
     %                  NOTRSO(MXPXYD),
     %                  NOSUTR(MXTRIA),
     %                  NUPLIS(MXPXYD),
     %                  NUISOP(MXPXYD),
     %                  L1LGFR(NUMILF:NUMXLF),
     %                  NSARCH(4,MXARCH)
C
      DOUBLE PRECISION  RLONGA(3)
C
C     TRACE DU MAILLAGE ACTUEL
      CALL CORTLT( MXTRIA, NOTRIA )
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 5 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         IF( NOTRIA(1,NT) .GT. 0 ) THEN
            CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
         ENDIF
 5    CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_1', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
 6    CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             1, NS, IERR )
      IF( NS .GT. 0 ) GOTO 6
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 7 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         IF( NOTRIA(1,NT) .GT. 0 ) THEN
            CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
         ENDIF
 7    CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_2', 8 )
C
C     SUPPRESSION DES SOMMETS "MILIEUX" DES ARETES
C     ============================================
C     DECLARATION DES TABLEAUX AUXILIAIRES
      IERR   = 0
      MNAUX  = 0
      MXETRI = 128
      MXPILE = MAX( 10*MXETRI+1, 12*NBPXYD )
      MXETRI = (MXPILE-1)/10
      MXPIL2 = MXPILE / 2
      CALL TNMCDC( 'ENTIER', MXPILE, MNAUX )
      CALL TNMCDC( 'ENTIER', MXETRI, MNTRCF )
C
C     SUPPRESSION DES SOMMETS NON POINTS ET NON SUR UNE LIGNE ET NON SUR UNE ISO
C     ==========================================================================
      DO 10 NS=1,NBPXYD
C
C        LE SOMMET EST IL UN POINT OU SUR 2 LIGNES OU SUR UNE LIGNE ?
         IF( NUPLIS(NS) .GE. 0 .AND. NUISOP(NS) .EQ. 0 ) THEN
            IF( NUPLIS(NS) .GT. 0 ) THEN
C              NS EST SUR UNE LIGNE FRONTIERE
C              EST IL LE DERNIER "MILIEU" DE CETTE LIGNE?
C              PARCOURS DES ARETES CHAINEES DE LA LIGNE
               L   = L1LGFR(NUPLIS(NS))
               NAR = 0
 8             IF( L .GT. 0 ) THEN
                  NAR = NAR + 1
C                 PASSAGE A L'ARETE SUIVANTE
                  L = NSARCH(4,L)
                  GOTO 8
               ENDIF
               IF( NAR .EQ. 2 ) THEN
C                 SI CE POINT EST SUPPRIME IL PEUT ETRE IMPOSSIBLE DE
C                 RETROUVER SON NUMERO DE LIGNE => IL N'EST PAS SUPPRIME
                  GOTO 10
               ENDIF
            ENDIF
C
C           NON : SUPPRESSION DU POINT NS DE LA TRIANGULATION ADAPTEE
            MN = MNAUX + 8 * MXETRI
            CALL ADSUPO( NS,     PXYD  ,
     %                   N1TRVI, NOTRIA, NOTRSO, 1, NOSUTR,
     %                   MXETRI, MCN(MNAUX),  MCN(MNAUX+4*MXETRI),
     %                   MCN(MNAUX+9*MXETRI), MCN(MNAUX+5*MXETRI),
     %                   NBTRCF, MCN(MN), IERR )
            IF( IERR .NE. 0 ) GOTO 10
C
            IF( NUPLIS(NS) .GT. 0 ) THEN
C              SUPPRESSION DU SOMMET DANS LE CHAINAGE DES ARETES DE SA LIGNE
               CALL SSARCH( NS,     L1LGFR(NUPLIS(NS)),
     %                      NBARCH, N1ARCH, NSARCH, IERR )
            ENDIF
C
C           LE SOMMET NS EST SUPPRIME DANS NUPLIS
            NUPLIS(NS) = - 2 000 000 - ABS(NUPLIS(NS))
C
CCCC           TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY A PARTIR DU 1-ER TRIAN
CCC            NT = MCN(MN)
CCC            CALL DVRTDV( MXTRIA, PXYD, NT, NOTRIA, NOTRSO, NOSUTR,
CCC     %                   MXPIL2, MCN(MNAUX), IERR )
         ENDIF
 10   CONTINUE
C
CCCC     TRACE DU MAILLAGE ACTUEL
CCC      CALL EFFACE
CCC      CALL XVINITIERPS( 0 )
CCC      DO 11 NT=1,MXTRIA
CCCC        LE TRACE DU TRIANGLE
CCC         IF( NOTRIA(1,NT) .GT. 0 ) THEN
CCC            CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
CCC         ENDIF
CCC 11   CONTINUE
CCC      CALL XVSAUVERPS( 'ADSUAM_3', 8 )
CCCC
CCCC     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
CCC 12   CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
CCC     %             1, NS,  IERR )
CCC      IF( NS .GT. 0 ) GOTO 12
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 15 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         IF( NOTRIA(1,NT) .GT. 0 ) THEN
            CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
         ENDIF
 15   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_4', 8 )
C
C     RECHERCHE DES PETITES ARETES DES TRIANGLES SUR UNE ISOVALEUR ET SUPPRESSIO
C     ==========================================================================
      DO 50 NT=1,NBTRIA
         IF( NOTRIA(1,NT) .LE. 0 ) GOTO 50
C
C        RECHERCHE DE LA PLUS COURTE DES 3 ARETES DU TRIANGLE NT
         NS1 = NOTRIA(3,NT)
         DO 30 I=1,3
            NS = NOTRIA(I,NT)
            IF( I .LT. 3 ) THEN
               II = I + 1
            ELSE
               II = 1
            ENDIF
            NS1 = NOTRIA(II,NT)
            RLONGA(I) = SQRT( (PXYD(1,NS)-PXYD(1,NS1))**2
     %                      + (PXYD(2,NS)-PXYD(2,NS1))**2 )
 30      CONTINUE
         RMIN = REAL( RLONGA(1) )
         NA   = 1
         DO 40 I=2,3
            IF( RLONGA(I) .LT. RMIN ) THEN
               RMIN = REAL( RLONGA(I) )
               NA   = I
            ENDIF
 40      CONTINUE
C        LA SOMME DES LONGUEURS DES 2 AUTRES COTES
         P = REAL( RLONGA(1) + RLONGA(2) + RLONGA(3) - RMIN )
C
         IF( RMIN .LT. TARMAX .AND. RMIN .LT. P/5.0 ) THEN
C
C           L'ARETE EST DE TAILLE REDUITE PAR RAPPORT AUX 2 AUTRES
C           ------------------------------------------------------
            IF( NA .LT. 3 ) THEN
               II = NA + 1
            ELSE
               II = 1
            ENDIF
C           LES 2 SOMMETS DE L'ARETE NA
            NS1 = NOTRIA(NA,NT)
            NS2 = NOTRIA(II,NT)
C
            IF( NUPLIS(NS1).LT.0 .AND. NUPLIS(NS2).LT.0 ) GOTO 50
C           NS1 OU NS2 N'EST PAS UN POINT NI N'APPARTIENT A 2 LIGNES
C
            IF( NUISOP(NS1).GT.0 .AND. NUISOP(NS2).GT.0 .AND.
     %          NUISOP(NS1) .NE. NUISOP(NS2) ) GOTO 50
C           NS1 ET NS2 NE SONT PAS SUR DES LIGNES ISOVALEURS DIFFERENTES
C
C           ALORS L'UN DES 2 SOMMETS EST SUPPRIMABLE ET L'ARETE EST SUPPRIMEE
C                 => SUPPRESSION DE 1 SOMMET ET DE 2 TRIANGLES
            CALL ADSU2T( NA,     NT,     PXYD,
     %                   N1TRVI, NOTRIA, NOTRSO, NUPLIS, NUISOP,
     %                   NUMILF, NUMXLF, L1LGFR,
     %                   NBARCH, N1ARCH, NSARCH,
     %                   MXETRI, MCN(MNTRCF), NS1, IERR )
C
CCCC           TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY A PARTIR DU TRIANGLE
CCCC           DU SOMMET GARDE
CCC            CALL DVRTDV( MXTRIA, PXYD, NOTRSO(NS1), NOTRIA, NOTRSO,
CCC     %                   NOSUTR, MXPIL2, MCN(MNAUX), IERR )
         ENDIF
 50   CONTINUE
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 60 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 60   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_5', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
 70   CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             1, NS,  IERR )
      IF( NS .GT. 0 ) GOTO 70
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 80 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         IF( NOTRIA(1,NT) .GT. 0 ) THEN
            CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
         ENDIF
 80   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_6', 8 )
C
C     TENTATIVE D'AMELIORATION DE LA TOPOLOGIE DU MAILLAGE
C     ====================================================
      CALL ADAMQS( MXPXYD, NBPXYD, NUPLIS, NOSUTR, NUISOP,
     %             MXTRIA, NOTRIA, NOTRSO,
     %             MXPIL2, MCN(MNAUX), MCN(MNAUX+MXPIL2),
     %             PXYD   )
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 90 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 90   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_7', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
 110  CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             1, NS,  IERR )
      IF( NS .GT. 0 ) GOTO 110
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 120 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 120  CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_8', 8 )
C
C     TENTATIVE DE REDUCTION DES ARETES TROP GRANDES DE LA FRONTIERE
C     ==============================================================
 125  CALL ADARFR( TARMAX, MXPXYD, NBPXYD, NUPLIS, NOSUTR, NUISOP,
     %             MXTRIA, N1TRVI, NOTRIA, NOTRSO,
     %             NUMILF, NUMXLF, L1LGFR,
     %             MXARCH, NBARCH, N1ARCH, NSARCH,
     %             PXYD  , NBTRIA, NBARDE, IERR  )
      IF( IERR .EQ. 0 ) THEN
          IF( NBARDE .GT. 0 ) GOTO 125
      ENDIF
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 130 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 130   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_9', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
 140  CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             1, NS,  IERR )
      IF( NS .GT. 0 ) GOTO 140
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 150 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 150  CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_A', 8 )
C
C     TENTATIVE DE REDUCTION DES ARETES TROP GRANDES DES ISOTHERMES
C     =============================================================
 155  CALL ADARIS( TARMAX, MXPXYD, NBPXYD, NUPLIS, NOSUTR, NUISOP,
     %             MXTRIA, N1TRVI, NOTRIA, NOTRSO,
     %             PXYD  , NBTRIA, NBARDE, IERR  )
      IF( IERR .EQ. 0 ) THEN
          IF( NBARDE .GT. 0 ) GOTO 155
      ENDIF
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 160 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 160   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_B', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
 165  CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             1, NS,  IERR )
      IF( NS .GT. 0 ) GOTO 165
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 168 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 168  CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_C', 8 )
C
C     TENTATIVE D'AMELIORATION DE LA TOPOLOGIE DU MAILLAGE
C     ====================================================
      CALL ADAMQS( MXPXYD, NBPXYD, NUPLIS, NOSUTR, NUISOP,
     %             MXTRIA, NOTRIA, NOTRSO,
     %             MXPIL2, MCN(MNAUX), MCN(MNAUX+MXPIL2),
     %             PXYD   )
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 170 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 170   CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_D', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
C     ATTENTION : ICI ARETES ISO NON PROTEGEES !
 175  CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             0, NS,  IERR )
      IF( NS .GT. 0 ) GOTO 175
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 180 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 180  CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_E', 8 )
C
C     TENTATIVE DE LEGER BARYCENTRAGE
C     ===============================
      CALL ADBARY( MXSOMM, NBSOMM, NUPLIS, NOSUTR,
     %             MXTRIA, NOTRIA, NOTRSO,
     %             MXPIL2, MCN(MNAUX), MCN(MNAUX+MXPIL2),
     %             PXYD   )
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 190 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 190  CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_F', 8 )
C
C     TENTATIVE POUR RENDRE LE MAILLAGE DELAUNAY
C     ATTENTION : ICI ARETES ISO NON PROTEGEES !
 195  CALL DVDIAG( MXTRIA, PXYD, NOTRIA, NOTRSO, NOSUTR, NUISOP,
     %             0, NS,  IERR )
      IF( NS .GT. 0 ) GOTO 195
C
C     TRACE DU MAILLAGE ACTUEL
      CALL EFFACE
      CALL XVINITIERPS( 0 )
      DO 198 NT=1,MXTRIA
C        LE TRACE DU TRIANGLE
         CALL DVTRTR( PXYD, NOTRIA, NT, NCNOIR, NCBLAN )
 198  CONTINUE
      CALL XVSAUVERPS( 'ADSUAM_G', 8 )
C
C     RESULTAT CORRECT POUR NOTRIA ?
      CALL CORTLT( MXTRIA, NOTRIA )
C
C     DESTRUCTION DU TABLEAU AUXILIAIRE
      CALL TNMCDS( 'ENTIER', MXPILE, MNAUX )
      CALL TNMCDS( 'ENTIER', MXETRI, MNTRCF )
      END
