      SUBROUTINE A1TEQMMM( QTEAME, NTEQM0,
     %                     MXSOMM, NBSOMM, PTXYZD, NPSOFR,
     %                     INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %                     MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                     MXTEET, NOTEET, MXFEET, NOFEET,
     %                     MXTECR, NBTECR, NOTECR, MODIFT, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    AMELIORER LA QUALITE DES TETRAEDRES AUTOUR DU TETRAEDRE NTEQM0
C -----    DE TRES MEDIOCRE QUALITE EN RETETRAEDRISANT LES TETRAEDRES
C          DE SES SOMMETS

C ENTREES:
C --------
C QTEAME : QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C          AMELIORATION DES TETRAEDRES EST DEMANDEE
C NTEQM0 : NUMERO NOTETR DU TETRAEDRE DE QUALITE MEDIOCRE A TRAITER

C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C MXSOMM : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : NUMERO DE POSITION PAR RAPPORT A LA FRONTIERE DE CHAQUE POINT
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE OU NO DE POINT INTERNE
C INFACO : = 0 PAS DE TABLEAU LEFACO NI CONTROLE DE FACE FRONTIERE
C          = 1 EXISTENCE DU TABLEAU LEFACO ET CONTROLE DE FACE FRONTIERE
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C IVOLTE : =0 TABLEAU NVOLTE NON PRESENT (No VOLUME DE CHAQUE TETRAEDRE)
C          =1 TABLEAU NVOLTE     PRESENT
C MXTECR : NOMBRE MAXIMAL DE NO NOTETR DES TETRAEDRES CREES

C MODIFIES:
C ---------
C NVOLTE : NUMERO DE VOLUME (1 a NBVOPA) DE CHAQUE TETRAEDRE
C          -1 SI VOLUME INCONNU (Exemple: les TETRAEDRES VIDES DE NOTETR)
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: NUMERO NOTETR DU DERNIER TETRAEDRE ACTIF DANS NOTETR

C AUXILIAIRES:
C ------------
C MXTEET : NOMBRE MAXIMAL DE TETRAEDRES DECLARABLES DANS LE TABLEAU NOTEET
C NOTEET : NO NOTETR DES TETRAEDRES DES SOMMETS D'UN TETRAEDRE MEDIOCRE
C MXFEET : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LE CHAINAGE NOFEET
C NOFEET : au debut VERSION1 FACES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR AYANT CETTE FACE
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3: NON UTILISE ICI
C          4: NUMERO DE CETTE FACE DANS LEFACO, 0 SI PAS DANS LEFACO
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C          ensuite VERSION2
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST DIRIGE VERS L'INTERIEUR DE L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C SORTIES:
C --------
C NBTECR : NOMBRE     DE TETRAEDRES CREES DANS L'ETOILE DU TETRAEDRE NTEQM
C NOTECR : NO NOTETR DES TETRAEDRES CREES DANS L'ETOILE DU TETRAEDRE NTEQM
C MODIFT : = 0 AUCUNE MODIFICATION DE TETRAEDRES
C          = 1 DES TETRAEDRES ONT ETE MODIFIES
C IERR   : = 0 PAS D'ERREUR
C          = 1 SATURATION DES SOMMETS    du TABLEAU PTXYZD
C          = 3 SATURATION DES TETRAEDRES du TABLEAU NOTETR
C          =11 TETRAEDRE NTEQM AVEC 0 POUR PREMIER SOMMET
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint PIERRE du PERRAY               Mai 2020
C2345X7..............................................................012
      PARAMETER        ( MXSSET=256, QUAMED=0.005 )
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE
      LOGICAL                          TRACTE0
      CHARACTER*120     KTITRE
      DOUBLE PRECISION  PTXYZD(4,MXSOMM)
      DOUBLE PRECISION  ARMIN, ARMAX, SURFTR(4)
      DOUBLE PRECISION  VTEQM, VOLET1, VOLUMT0, VOLMOY0,
     %                  VOLUMT1, VOLMOY1, VMOYEN
      INTEGER           NOTETR(8,MXTETR), N1TETS(MXSOMM),NPSOFR(MXSOMM),
     %                  LEFACO(1:11,0:MXFACO), NVOLTE(*),NOTEET(MXTEET),
     %                  NOFEET(5,MXFEET), N1SSET(MXSSET),NOTECR(MXTECR)
      INTEGER           NOTRCF(1), NO0FAR(1), NSARP2F(3,1)
      INTEGER           NOSOFATE(3,4)
      DATA              NOSOFATE / 1,3,2,  2,3,4,  3,1,4,  4,1,2 /

      TRACTE0 = TRACTE
      IERR    = 0
      MODIFT  = 0
      NBTECR  = 0
      NTEQM   = NTEQM0

      IF( NTEQM .LE. 0 ) GOTO 9999
      IF( NOTETR(1,NTEQM) .LE. 0 ) GOTO 9999


      VMOYEN = 0D0

C     CARACTERISTIQUES DU TETRAEDRE NTEQM DE QUALITE MEDIOCRE
C     -------------------------------------------------------
C     VOLUME et QUALITE DU TETRAEDRE NTEQM
      CALL QUATETD( PTXYZD( 1, NOTETR(1,NTEQM) ),
     %              PTXYZD( 1, NOTETR(2,NTEQM) ),
     %              PTXYZD( 1, NOTETR(3,NTEQM) ),
     %              PTXYZD( 1, NOTETR(4,NTEQM) ),
     %              ARMIN, ARMAX, SURFTR, VTEQM, QTEQM )

      IF( QTEQM .GE. QTEAME ) THEN
         GOTO 9900
      ENDIF

      PRINT *
      PRINT *,'a1teqmmm: Debut QTEAME=',QTEAME,
     %' Tetraedre',NTEQM,' St:',(NOTETR(I,NTEQM),I=1,4),
     %' TeOP:',(NOTETR(I,NTEQM),I=5,8),
     %' Q=',QTEQM,' V=',VTEQM,' =============='

C     NUMERO DE VOLUME DU TETRAEDRE NTEQM
      IF( IVOLTE .NE. 0 ) THEN
C        PRESENCE DU TABLEAU NVOLTE C-A-D DE TETRAEDRES FRONTIERE
         NOVOLU = NVOLTE( NTEQM )
      ELSE
C        NUMERO PAR DEFAUT DE VOLUME DU TETRAEDRE NTEQM
         NOVOLU = 0
      ENDIF

C     TRACE DU TETRAEDRE NTEQM ET DES TETRAEDRES ADJACENTS
      KTITRE='a1teqmmm: TETRAEDRE          de QUALITE=          VOLUME=    
     %                debut du traitement'
      WRITE( KTITRE(21:28),'(I8)'   ) NTEQM
      WRITE( KTITRE(43:51),'(F9.6)' ) QTEQM
      WRITE( KTITRE(58:72),'(G15.6)') VTEQM
      CALL SANSDBL( KTITRE, L )
      CALL TRFETO9( KTITRE(1:L), PTXYZD, NTEQM, NOTETR )

      IF( VTEQM .LT. 0 ) THEN

C        TENTATIVE DE DEPLACER LE SOMMET CENTRAL DU TETRAEDRE PLAT NTEQM
C        DE L'AUTRE COTE DE LA FACE POUR RENDRE LE VOLUME POSITIF
C        ---------------------------------------------------------------
         CALL DEPSCTVN( NTEQM,  NOTETR, N1TETS, PTXYZD, NPSOFR,
     %                  INFACO, VTEQM,  QTEQM,  MODIFT )

         IF( MODIFT .EQ. 0 ) THEN
C           PAS DE MODIFICATION DU TETRAEDRE NTEQM
            GOTO 100
         ELSE
C           LES XYZ D'UN SOMMET DU TETRAEDRE NTEQM ONT ETE MODIFIES
C           POUR RENDRE LE VOLUME ET LA QUALITE POSITIFS
            MODIFT = 1
            GOTO 9999
         ENDIF

      ENDIF


cccC     CONSTRUCTION DU TABLEAU DES NO NOTETR DES TETRAEDRES
cccC     DES SOMMETS DE NTEQM
cccC     ----------------------------------------------------
ccc      NBTEET = 0
ccc      DO K = 1, 4
ccc         NS = NOTETR( K, NTEQM )
ccc         CALL TETR1S( NS,     N1TETS, NOTETR,
ccc     %                NBTENS, MXTEET-NBTEET, NOTEET(NBTEET+1), IERR )
ccc         NBTEET = NBTEET + NBTENS

cccC        UNICITE DES NUMEROS DES TETRAEDRES DE L'ETOILE DU TETRAEDRE NTEQM
ccc         CALL UNITABL( NOTEET, NBTEET )

ccc      ENDDO
ccc      IF( NBTEET .LE. 0 ) THEN
cccC        PROBLEME ETOILE SANS TETRAEDRE...
ccc         IERR = 1
ccc         GOTO 9900
ccc      ENDIF
cccC     TRACE DES NBTEET TETRAEDRES
ccc      KTITRE='a1teqmmm: les         TETRAEDRES des SOMMETS de NTEQM'
ccc      WRITE(KTITRE(15:21),'(I7)') NBTEET
ccc      CALL SANSDBL( KTITRE, L )
ccc      NPt=0
ccc      CALL TRFETO14( KTITRE(1:L), NPt, PTXYZD, NBTEET, NOTEET, NOTETR )


C     CONSTRUCTION DU TABLEAU DES NO NOTETR DES TETRAEDRES
C     OPPOSES AUX FACES DE NTEQM et OPPOSES DES TETRAEDRES OPPOSES
C     ------------------------------------------------------------
 100  NBTEET = 1
      NOTEET( 1 ) = NTEQM
      DO K = 1, 4
         NTOP = NOTETR( 4+K, NTEQM )
         IF( NTOP .GT. 0 .AND. NOTETR(1,NTOP) .GT. 0 ) THEN
            NBTEET = NBTEET + 1
            NOTEET( NBTEET ) = NTOP
         ENDIF
      ENDDO
      NBTEET1 = NBTEET

C     et OPPOSES DES TETRAEDRES OPPOSES
      DO N = 2, NBTEET1
         NTOP = NOTEET( N )
         DO K = 1, 4
            NTOPOP = NOTETR( 4+K, NTOP )
            IF( NTOPOP .GT. 0 .AND. NOTETR(1,NTOPOP) .GT. 0 ) THEN
               NBTEET = NBTEET + 1
               NOTEET( NBTEET ) = NTOPOP
            ENDIF
         ENDDO
      ENDDO

C     UNICITE DES NUMEROS DES TETRAEDRES DE L'ETOILE DU TETRAEDRE NTEQM
      CALL UNITABL( NOTEET, NBTEET )

      IF( NBTEET .LE. 0 ) THEN
C        PROBLEME ETOILE SANS TETRAEDRE...
         IERR = 1
         GOTO 9900
      ENDIF

C     VERIFIER L'ABSENCE DE TETRAEDRE  OPPOSE  NEGATIF (INCONNU)
C     VERIFIER L'ABSENCE DE TETRAEDRES OPPOSES DOUBLES
C     VERIFIER L'ABSENCE DE FACE COMMUNE A AU MOINS 3 TETRAEDRES
C     VERIFIER L'OPPOSITION DES TETRAEDRES 
C     DANS LA LISTE DES NBTEET TETRAEDRES NOTEET
      CALL VEOPTE( NBTEET, NOTEET, NOTETR, PTXYZD, NBFANR )

C     TRACE DES NBTEET TETRAEDRES
      KTITRE='a1teqmmm: les         TETRAEDRES des SOMMETS de NTEQM'
      WRITE(KTITRE(15:21),'(I7)') NBTEET
      CALL SANSDBL( KTITRE, L )
      NPt=0
      CALL TRFETO14( KTITRE(1:L), NPt, PTXYZD, NBTEET, NOTEET, NOTETR )


C     CONSTRUCTION NOFEET DES TRIANGLES FACES SIMPLES DES NBTEET TETRAEDRES
C     LES FACES VUES 2 FOIS SONT ELIMINEES
C     ---------------------------------------------------------------------
      CALL CRFETOI1( NBTEET, NOTEET, NOTETR,
     %               MXFEET, N1FEOC, N1FEVI, NOFEET )

C     PASSAGE DE NOFEET DE LA VERSION 1 A LA VERSION 2
      CALL NFETOI12( NOTETR, N1FEOC, NOFEET )

C     TRACE DE L'ETOILE NOFEET DES NBTEET TETRAEDRES
      NBTRCF = 0
      CALL TRFETO2( PTXYZD, NOTETR, N1FEOC, NOFEET,
     %              NBTRCF, NOTRCF, LEFACO, NO0FAR )


C     RECENSEMENT DES NBSSET SOUS ETOILES DE L'ETOILE NFTEET
C     ------------------------------------------------------
      NBSSET = 0
      CALL SOUSETO( PTXYZD, NOTETR, N1FEOC, NOFEET,
     %              MXSSET, NBSSET, N1SSET, IERR )
      IF( IERR .NE. 0 ) THEN
         PRINT *,'a1teqmmm: Sortie de call souseto avec IERR=',IERR
         GOTO 9999
      ENDIF

C     CALCUL DES QUALITES MIN MOYENNE et VOLUMES des NBTEET
C     TETRAEDRES NOTEET
      CALL QUALGRTE( PTXYZD, MXTETR, NOTETR,  NBTEET, NOTEET,
     %               QUAMED, NBTMED0,
     %               QUAMIN0, QUAMOY0, VOLUMT0, VOLMOY0 )

      PRINT*,'a1teqmmm:',NBTEET,
     %       ' TETRAEDRES INITIAUX de VOLUME',VOLUMT0,
     %       ' VOLUME MOYEN=',VOLMOY0,
     %       ' QUALITE MOYENNE=',QUAMOY0,
     %       ' QUALITE MIN=',QUAMIN0,' et',
     %       NBTMED0,' TETRAEDRES de QUALITE<',QUAMED

C     TRACE DES FACES DES NBSSET SOUS-ETOILES
      NBARP2F = 0
      CALL TRFETO7( PTXYZD,  N1FEOC, NFETOI, NBSSET, N1SSET,
     %              NBARP2F, NSARP2F )

C     TETRAEDRISATION DES FACES SIMPLES DES NBSSET SOUS-ETOILES
C     DE NOFEET SANS OU AVEC AJOUT DE POINTS INTERNES A L'ETOILE
C     => NBTECR TETRAEDRES DANS NOTECR
C     ----------------------------------------------------------
C     NOMBRE INITIAL DE TETRAEDRES CREES DANS L'ETOILE
      NBTECR = 0
C     VOLUME APRES TETRAEDRISATION DE L'ETOILE
      VOLET1 = 0D0
      NBSOMM0= NBSOMM
      CALL TETRETO( QUAMED, QTEAME, KTITRE,
     %              MXSOMM, NBSOMM0,NBSOMM, PTXYZD,
     %              NPSOFR, VMOYEN,
     %              MXTETR, N1TEVI, NOTETR, NUDTETR, N1TETS,
     %              NBTRCF, NOTRCF, INFACO, MXFACO, LEFACO, NO0FAR,
     %              N1FEVI, MXFEET, NOFEET,
     %              MXSSET, NBSSET, N1SSET, NBTEET, NOTEET,
     %              MXTECR, NBTECR, NOTECR, VOLET1, IERR )

      IF( IERR .EQ. 0 .AND. NBTECR .GT. 0 ) THEN

C        ETOILE TETRAEDRISEE PAR NBTECR TETRAEDRES QUI
C        REMPLACENT LES NBTEET TETRAEDRES DES SOMMETS DE NTEQM
C        -----------------------------------------------------
         MODIFT = 1

C        NUMERO DE VOLUME DU TETRAEDRE NTEQM
         IF( IVOLTE .NE. 0 ) THEN
            DO K=1,NBTECR
C              PRESENCE DU TABLEAU NVOLTE C-A-D DE TETRAEDRES FRONTIERE
               NVOLTE( NOTECR(K) ) = NOVOLU
            ENDDO
         ENDIF

C        CALCUL DES QUALITES MIN MOYENNE et VOLUMES des NBTECR
C        TETRAEDRES NOTECR CREES
         CALL QUALGRTE( PTXYZD, MXTETR, NOTETR, NBTECR, NOTECR,
     %                  QUAMED, NBTMED1,
     %                  QUAMIN1, QUAMOY1, VOLUMT1, VOLMOY1 )

         PRINT*,'a1teqmmm:',NBTECR,
     %          ' TETRAEDRES  CREES   de VOLUME',VOLUMT1,
     %          ' VOLUME MOYEN=',VOLMOY1,
     %          ' QUALITE MOYENNE=',QUAMOY1,
     %          ' QUALITE MIN=',QUAMIN1,' et',
     %          NBTMED1,' TETRAEDRES de QUALITE<',QUAMED

         GOTO 9999

      ENDIF


C     PAS DE MODIFICATION DES NBTEET TETRAEDRES DES SOMMETS DE NTEQM
C     --------------------------------------------------------------
 9900 MODIFT = 0
      PRINT*,'a1teqmmm: TETRAEDRE',NTEQM,' de QUALITE',
     %        QTEQM,' de VOLUME',VTEQM,' RETOUR SANS MODIFICATION'

 9999 TRACTE = TRACTE0
      RETURN
      END
