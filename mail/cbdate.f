      SUBROUTINE CBDATE( POINT, PTXYZD, NOSOTE, CB, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DES 4 COORDONNEES BARYCENTRIQUES DU POINT
c -----    DANS LE TETRAEDRE DE NUMEROS DE SOMMETS NOSOTE DANS PTXYZD
C          ptdste.f est une autre version de ce sous-programme
C
C ENTREES:
C --------
C POINT  : LES 3 COORDONNEES DU POINT
C         ( + UN REEL DOUBLE INUTILE MAIS DECLARE . EX: PTXYZD(1,K)  )
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NOSOTE : LE NUMERO DES 4 SOMMETS DU TETRAEDRE
C
C SORTIES:
C --------
C CB     : LES 4 COORDONNEES BARYCENTRIQUES DU POINT DANS LE TETRAEDRE
C IERR   : 1 SI LE TETRAEDRE EST DEGENERE,  0 SI PAS D'ERREUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   NOVEMBRE 1992
C....................................................................012
      INTEGER           NOSOTE(4)
      DOUBLE PRECISION  PTXYZD(4,*),
     %                  COORTE(4,5),
     %                  CB(4)
      DOUBLE PRECISION  POINT(4)
C     ATTENTION : 4 REELS DOUBLE PRECISION POUR EVITER DES TESTS !
C
C     CALCUL DES COORDONNEES BARYCENTRIQUES DU POINT DANS NT
      DO 20 J=1,4
         DO 10 I=1,3
            COORTE(I,J) = PTXYZD(I,NOSOTE(J))
  10     CONTINUE
C        LA DERNIERE LIGNE DE LA MATRICE
         COORTE(4,J) = 1D0
C        LE SECOND MEMBRE
CCC         IF( J .NE. 4 ) COORTE(J,5) = POINT(J)
CCC         PAS DE TEST SI IL EXISTE UN POINT(4) DE VALEUR INUTILE
         COORTE(J,5) = POINT(J)
  20  CONTINUE
      COORTE(4,5) = 1D0
C
C     INVERSION DU SYSTEME LINEAIRE
      CALL GAUSPT( 4 , 1 , COORTE , IERR )
      IF( IERR .NE. 0 ) RETURN
C
C     TOUTES SES COORDONNEES BARYCENTIQUES SONT MISES DANS CB
      DO 30 I=1,4
         CB(I) = COORTE(I,5)
 30   CONTINUE
      END
