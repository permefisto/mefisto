      SUBROUTINE GETEPT( P,      NBSOMM, MXSOMM, XYZSOM, NBDM, NUDMEF,
     %                   NBSOTE, N1TEVI, NSTETR,
     %                   NO1TSO, N1TESO, NOTESO,
     %                   N1FEOC, N1FEVI, NFETOI, NBTEDS, NOTEDS,
     %                   MXTETA, VOLUMT, QUALIT,
     %                   VOLET0, QUAET0, VOLET1, QUAET1, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERATION DE LA TETRAEDRISATION ETOILEE PAR LE POINT P
C -----    A PARTIR DES TRIANGLES NFETOI DE L'ETOILE

C ENTREES:
C --------
C P      : POINT ETOILANT
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DECLARABLES DANS XYZSOM
C XYZSOM : LES 3 COORDONNEES DES SOMMETS
C NBDM   : 0 SI 1 MATERIAU=VOLUME, SINON NOMBRE DE MATERIAUX DU VOLUME
C NUDMEF : NUMERO DE MATERIAU DE CHAQUE TETRAEDRE DU MAILLAGE
C          ATTENTION: CE TABLEAU EXISTE SEULEMENT SI NBDM>0
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C NSTETR : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C NOTESO : NOTESO(1,*) NUMERO DANS NSTETR DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER
C N1FEOC : NUMERO DANS NFETOI DE LA PREMIERE FACE OCCUPEE
C N1FEVI : NUMERO DANS NFETOI DE LA PREMIERE FACE VIDE
C NFETOI : NUMERO DES 3 SOMMETS, TETRAEDRE ET SUIVANTE DES FACES
C          VUES UNE FOIS DE L'ETOILE CHAINEES N1FEOC PUIS NFETOI(5,*)

C MODIFIES :
C ----------
C NBTEDS : NOMBRE DE TETRAEDRES A DETRUIRE PUIS CREES
C NOTEDS : NUMERO DANS NSTETR DES TETRAEDRES A DETRUIRE PUIS CREES
C NBSOMM : NOMBRE DE SOMMETS AVANT ET APRES
C MXTETA : NUMERO MAXIMAL D'UN TETRAEDRE ACTUEL
C VOLUMT : VOLUME  DES TETRAEDRES DE L'ETOILE
C QUALIT : QUALITE DES TETRAEDRES DE L'ETOILE

C VOLET0 : VOLUME TOTAL DE L'ETOILE INITIALE
C QUAET0 : QUALITE MINIMALE DES TETRAEDRES DE L'ETOILE INITIALE
C VOLET1 : VOLUME TOTAL DE L'ETOILE CREEE
C QUAET1 : QUALITE MINIMALE DES TETRAEDRES DE L'ETOILE CREEE
C IERR   : =0 SI PAS D'ERREUR
C          >0 SI SATURATION D'UN TABLEAU  => ERREUR DE TETRAEDRISATION
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC   DECEMBRE 1991
C MODIFS : ALAIN PERRONNET LJLL UPMC et St Pierre du Perray Fevrier 2016
C....................................................................012
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      REAL              P(3),XYZSOM(3,*),
     %                  VOLUMT(*),
     %                  QUALIT(*)
      INTEGER           NSTETR(NBSOTE,MXTETA),
     %                  NUDMEF(*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*),
     %                  NFETOI(5,*),
     %                  NOTEDS(NBTEDS)
      REAL              ARMIN, ARMAX, SURFTR(4),
     %                  VOLET0, QUAET0, VOLET1, QUAET1

C     VOLUME ET QUALITE DE L'ETOILE INITIALE ET FINALE
      VOLET0 = 0.0
      VOLET1 = 0.0
      GRAND  = RINFO( 'GRAND' )
      QUAET0 = GRAND
      QUAET1 = GRAND

C     DESTRUCTION DES NBTEDS TETRAEDRES A SUPPRIMER
C     TOUS LES TETRAEDRES DOIVENT ETRE DANS LE MEME MATERIAU NUMATE
      NUMATE = 0
      IF( NBDM .GT. 0 ) THEN
         NUMATE = NUDMEF( NOTEDS(1) )
         DO NF = 2, NBTEDS
            NT = NOTEDS(NF)
            IF( NUMATE .NE. NUDMEF( NOTEDS(NF) ) ) THEN
               GOTO 9999
            ENDIF
         ENDDO
      ENDIF
C
C     DESTRUCTION DES TETRAEDRES DE L'ETOILE
      DO NF = 1, NBTEDS

         NT = NOTEDS(NF)

         CALL QUATET( XYZSOM( 1, NSTETR(1,NT) ),
     %                XYZSOM( 1, NSTETR(2,NT) ),
     %                XYZSOM( 1, NSTETR(3,NT) ),
     %                XYZSOM( 1, NSTETR(4,NT) ),
     %                ARMIN, ARMAX, SURFTR, VTE, QTE )
         VOLET0 = VOLET0 + VTE
         QUAET0 = MIN( QUAET0, QTE )

         CALL DS1TET( NT, NBSOTE, N1TEVI, NSTETR,
     %                NO1TSO, N1TESO, NOTESO )
         QUALIT( NT ) = GRAND
         IF( NBDM .GT. 0 ) NUDMEF( NT ) = 0

      ENDDO
C
C     DECLARATION DU SOMMET
      IF( NBSOMM .GE. MXSOMM ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'AUGMENTER LE NOMBRE DE SOMMETS'
         CALL LEREUR
         IERR = 1
         GOTO 9999
      ENDIF
      NBSOMM = NBSOMM + 1
      XYZSOM(1,NBSOMM) = P(1)
      XYZSOM(2,NBSOMM) = P(2)
      XYZSOM(3,NBSOMM) = P(3)
C
C     POSITION DANS NFETOI DE LA 1-ERE FACE DE L'ETOILE
      NF = N1FEOC
      NBTEDS = 0
C
C     TANT QU'IL EXISTE UNE FACE DE L'ETOILE FAIRE
 10   IF( NF .GT. 0 ) THEN
C
C        RECHERCHE D'UN TETRAEDRE VIDE
         IF( N1TEVI .LE. 0 ) THEN
            NBLGRC(NRERR) = 1
            KERR(1) = 'SATURATION NSTETR DES TETRAEDRES'
            CALL LEREUR
            IERR = 1
            GOTO 9999
         ENDIF
C
C        LE PREMIER TETRAEDRE VIDE
         NT = N1TEVI
C        EST MIS A JOUR
         N1TEVI = NSTETR( 2, N1TEVI )

C        UN TETRAEDRE CREE DE PLUS
         NBTEDS = NBTEDS + 1
         NOTEDS( NBTEDS ) = NT

      call verifns( 'getept:', nbsomm, nbsote, nstetr, no1tso, noteso,
     %               nbtensv, ierr )
C
C        FORMATION DU TETRAEDRE DE SOMMET NBSOMM ET DE FACE OPPOSEE NF
         NFETOI(1,NF) = ABS( NFETOI(1,NF) )
         NSTETR(1,NT) = NFETOI(1,NF)
         NSTETR(2,NT) = NFETOI(2,NF)
         NSTETR(3,NT) = NFETOI(3,NF)
         NSTETR(4,NT) = NBSOMM
         IF( NBDM .GT. 0 ) NUDMEF( NT ) = NUMATE
C
C        AJOUT DE CE TETRAEDRE DANS LE CHAINAGE NOTESO DES TETRAEDRES
C        DES 4 SOMMETS DU TETRAEDRE NT
         CALL CHTESO( NT, NBSOTE, NSTETR, NO1TSO, N1TESO, NOTESO,
     %                IERR )
         IF( IERR .NE. 0 ) GOTO 9999
C
C        QUALITE ET VOLUME DU TETRAEDRE
         CALL QUATET( XYZSOM(1,NSTETR(1,NT)),
     %                XYZSOM(1,NSTETR(2,NT)),
     %                XYZSOM(1,NSTETR(3,NT)),
     %                XYZSOM(1,NSTETR(4,NT)),
     %                ARMIN, ARMAX, SURFTR, VOLUMT(NT), QUALIT(NT) )

         VOLET1 = VOLET1 + ABS( VOLUMT(NT) )
         QUAET1 = MIN( QUAET1, QUALIT(NT) )

C        LA FACE SUIVANTE
         NF = NFETOI(5,NF)
         GOTO 10
      ENDIF

      PRINT*,'getept REUSSI: VOLET0=',VOLET0,' QUAET0=',QUAET0
      PRINT*,'getept REUSSI: VOLET1=',VOLET1,' QUAET1=',QUAET1
C
C     LES FACES OCCUPEES DE NFETOI DEVIENNENT VIDES
 9999 CALL ETOIOCVI( N1FEOC, N1FEVI, NFETOI )
C
      RETURN
      END
