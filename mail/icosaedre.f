      SUBROUTINE ICOSAEDRE( ARETGR,   MXSOMM, COINOB,
     %                      HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                      NBSOMM,   PTXYZD, NUCENTR, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LES 12 SOMMETS ET LE CENTRE DE L'ICOSAEDRE ENGLOBANT
C -----    TOUS LES POINTS FRONTALIERS OU INTERNES DU DOMAINE A TETRAEDRISER
C ENTREES:
C --------
C ARETGR : LONGUEUR SOUHAITEE DE L'ARETE DE LA GRILLE REGULIERE
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS DECLARABLES
C COINOB : LES 3 COORDONNEES MINIMALES ET MAXIMALES DES 8 SOMMETS DE
C          L'HEXAEDRE ENGLOBANT L'OBJET
C          COINOB(I,J) = I-EME COORDONNEE MINIMALE SI J=1
C                                         MAXIMALE SI J=2
C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV  : NOMBRE D'ARETES DANS LA DIRECTION I DU PAVAGE
C ECHPAV  : ECHELLE DANS LA DIRECTION I DU PAVAGE
C N1SPAVE : NO DANS PTXYZD DU 1-ER SOMMET DE CHAQUE PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES
C
C SORTIE :
C --------
C NUCENTR: =13 NUMERO PTXYZD CENTRE DE L'ICOSAEDRE
C NBSOMM : NOMBRE (12) DE SOMMETS DE L'ICOSAEDRE ENGLOBANT + CENTRE => 13
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE
C IERR   : 0 SI PAS D'ERREUR
C          1 SATURATION DES POINTS DANS PTXYZD
C          2 TOUS LES POINTS SONT COPLANAIRES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC ET ST PIERRE DU PERRAY   AVRIL 2008
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      DOUBLE PRECISION  PTXYZD(1:4,1:MXSOMM)
      REAL              ARETGR, COINOB(6,2)
      DOUBLE PRECISION  HEXAPAVE(3,2), ECHPAV(3)
      INTEGER           NBIPAV(3), N1SPAVE(*), NOPTSUIV(MXSOMM)
      DOUBLE PRECISION  RAYONICOSAEDRE, CENTRE(3), DIS2ST
      DOUBLE PRECISION  PI, PISUR5, DEUXPISUR5, PHI, DBLE
      DOUBLE PRECISION  XYZ(3), RC, RS, U, ARETEMX, ARETICOS, H
C
C     LE POINT 13 EST LE CENTRE DE L'ICOSAEDRE
      NUCENTR    = 13

      PI         = ATAN(1D0) * 4
      PISUR5     = PI/5D0
      DEUXPISUR5 = 2D0 * PISUR5
C     ANGLE LATITUDE DU PLAN / EQUATEUR POUR LES 5 POINTS HORS POLES
      PHI        = ATAN(0.5D0)
      NBSOMM     = 0
C
C     LES POINTS SONT ILS COPLANAIRES ?
C     =================================
      DO 10 I=1,3
C        L'AMPLITUDE DE L'OBJET DANS LA DIRECTION I
         H = COINOB(I,2) - COINOB(I,1)
         IF( H .LT. 1D-3 * (ABS(COINOB(I,1))+ABS(COINOB(I,2)))) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) ='TOUS LES POINTS INITIAUX SONT ALIGNES'
            ELSE
               KERR(1) ='ALL POINTS ARE ON A STRAIGHT LINE'
            ENDIF
            CALL LEREUR
            IERR = 2
            GOTO 9999
         ENDIF
 10   CONTINUE
C
C     DIMENSIONS DE L'ARETE DE L'ICOSAEDRE ENGLOBANT
C     L'HEXAEDRE ENGLOBANT POUR LE PAVAGE
C     LE CENTRE DE L'HEXAEDRE ET DE L'ICOSAEDRE
C     ==============================================
      RAYONICOSAEDRE = 0D0
      DO 20 I=1,3
         H = COINOB(I,2) - COINOB(I,1)
         RAYONICOSAEDRE = RAYONICOSAEDRE + H * H
         CENTRE(I) = ( COINOB(I,1) + COINOB(I,2) ) / 2
 20   CONTINUE
ccc      RAYONICOSAEDRE = ( SQRT( RAYONICOSAEDRE ) / 2 ) * 1.3D0   6/8/2014
ccc      RAYONICOSAEDRE = SQRT( RAYONICOSAEDRE ) * 1.3D0          15/8/2014
      RAYONICOSAEDRE = SQRT( RAYONICOSAEDRE )
C
C     GENERATION DES 12 SOMMETS DE L'ICOSAEDRE RACINE
C     ===============================================
C     LA LONGUEUR D'UNE ARETE DE L'ICOSAEDRE
      ARETICOS = ( PI *  RAYONICOSAEDRE ) / ( 1 + 2 * SIN( PHI ) )
C     ARETEMX EST LA DISTANCE SOUHAITEE DES SOMMETS DE L'ICOSAEDRE
      ARETEMX = ARETICOS * 3
C
C     LE POINT 1 EST LE POLE NORD DE L'ICOSAEDRE
      XYZ(1) = CENTRE(1)
      XYZ(2) = CENTRE(2)
      XYZ(3) = CENTRE(3) + RAYONICOSAEDRE
C     LE POLE NORD EST INTEGRE AU PAVAGE
      CALL REAJSTPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                XYZ, ARETEMX, NBSOMM, MXSOMM, PTXYZD,
     %                NUCENTR,  NC )
C
      RC = RAYONICOSAEDRE * COS( PHI )
      RS = RAYONICOSAEDRE * SIN( PHI )
      U  = 0D0
      DO 12 I=0,4
C        LES 5 SOMMETS DU PLAN // EQUATEUR ET AU DESSUS
         XYZ(1) = CENTRE(1) + RC * COS( U )
         XYZ(2) = CENTRE(2) + RC * SIN( U )
         XYZ(3) = CENTRE(3) + RS
         CALL REAJSTPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                   XYZ, ARETEMX, NBSOMM, MXSOMM, PTXYZD,
     %                   NUCENTR,  NC )

         U = U + DEUXPISUR5
 12   CONTINUE
C
      U=PISUR5
      DO I=0,4
C        LES 5 SOMMETS DU PLAN // EQUATEUR ET AU DESSUS
         XYZ(1) = CENTRE(1) + RC * COS( U )
         XYZ(2) = CENTRE(2) + RC * SIN( U )
         XYZ(3) = CENTRE(3) - RS
         CALL REAJSTPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                   XYZ, ARETEMX, NBSOMM, MXSOMM, PTXYZD,
     %                   NUCENTR,  NC )

         U = U + DEUXPISUR5
      ENDDO
C
C     LE POINT 12 EST LE POLE SUD DE L'ICOSAEDRE
      XYZ(1) = CENTRE(1)
      XYZ(2) = CENTRE(2)
      XYZ(3) = CENTRE(3) - RAYONICOSAEDRE
      CALL REAJSTPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                XYZ, ARETEMX, NBSOMM, MXSOMM, PTXYZD,
     %                NUCENTR,  NC )

C
C     LE POINT 13 EST LE CENTRE DE L'ICOSAEDRE
      XYZ(1) = CENTRE(1)
      XYZ(2) = CENTRE(2)
      XYZ(3) = CENTRE(3)
      CALL REAJSTPAV( HEXAPAVE, NBIPAV, ECHPAV, N1SPAVE, NOPTSUIV,
     %                XYZ, DBLE(ARETGR), NBSOMM, MXSOMM, PTXYZD,
     %                NUCENTR,  NC )
C
C     IMPRESSIONS
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,10000) DIS2ST( PTXYZD(1,2), PTXYZD(1,3) )
      ELSE
         WRITE(IMPRIM,20000) DIS2ST( PTXYZD(1,2), PTXYZD(1,3) )
      ENDIF
10000 FORMAT(' LONGUEUR DES ARETES DE L''ICOSAEDRE =',G15.6)
20000 FORMAT(' EDGE LENGTH OF ICOSAHEDRON =',G15.6)
CCCC
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,2), PTXYZD(1,3) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,3), PTXYZD(1,4) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,4), PTXYZD(1,5) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,5), PTXYZD(1,2) )
CCCC
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,2), PTXYZD(1,1) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,3), PTXYZD(1,1) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,4), PTXYZD(1,1) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,5), PTXYZD(1,1) )
CCCC
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,2), PTXYZD(1,6) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,3), PTXYZD(1,6) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,4), PTXYZD(1,6) )
CCC      WRITE(IMPRIM,*) DIS2ST( PTXYZD(1,5), PTXYZD(1,6) )
C
C     LES DISTANCES SOUHAITEES
C     ========================
      DO 300 I=NBSOMM+1,MXSOMM
C        DISTANCE INITIALISEE A ARETGR
         PTXYZD(4,I) = ARETGR
 300  CONTINUE
C
 9999 RETURN
      END
