      SUBROUTINE PLCNTG( MXGENE, XYZ1PL, VN,
     %                   PAX1CN, RAY1CN, RAY2CN, HAUTCN, RPCONE,
     %                   NPSUIV, RPINT,  NBIP,   N1COUR,
     %                   NBARL0, NBSOM0,
     %                   NBARLI, NBSOM,  NBTGS,
     %                   XYZST,  XYZTG,
     %                   NUSOAR, LPEFTG, NGEFTG, NUTGAR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DES SOMMETS ET TANGENTES ET ARETES DE LA LIGNE
C -----    INTERSECTION ENTRE UN PLAN ET UN TRONC DE CONE
C
C ENTREES:
C --------
C MXGENE : NOMBRE DE GENERATRICES DU CONE A INTERSECTER AVEC LE PLAN
C XYZ1PL : 3 COORDONNEES DANS LE REPERE DU CONE DES 1-ER POINT DE DEFINITION DU
C VN     : VECTEUR NORMAL UNITAIRE AU PLAN DES 3 POINTS
C
C PAX1CN : POINT DE BASE DE L'AXE DU TRONC DE CONE
C RAY1CN : RAYON DU CERCLE AU POINT PAX1CN (PLAN ORTHOGONAL A L'AXE DU CONE )
C PAX2CN : POINT DE L'AXE DU TRONC DE CONE
C RAY2CN : RAYON DU CERCLE AU POINT PAX2CN (PLAN ORTHOGONAL A L'AXE DU CONE )
C
C HAUTCN : HAUTEUR DU TRONC DE CONE
C RPCONE : REPERE PROPRE ORTHONORME DU CONE D'ORIGINE AU POINT PAX1CN
C          ORIGINE AU POINT PAX1CN
C NPSUIV : NUMERO DANS RPINT DU POINT D'INTERSECTION SUIVANT
C RPINT  : RPINT(1,N) ANGLE DE LA GENERATRICE DU CONE DE CE POINT N
C          RPINT(2,N) RACINE TEL QUE PM = RACINE PC SUR LA GENERATRICE DU CONE
C          RPINT(3,N) DISTANCE AU POINT PRECEDENT
C
C NBIP   : NUMERO DANS N1COUR DU DERNIER INTERVALLE DE POINTS D'INTERSECTION
C N1COUR : DEBUT DES CHAINAGES DES COURBES, PUIS DES INTERVALLES DE GENERATRICES
C          PUIS DES INTERVALLES DE POINTS D'INTERSECTION
C          N1COUR(0) POINTE SUR LE PREMIER VIDE
C NBARL0 : NOMBRE INITIAL D'ARETES   DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NBSOM0 : NOMBRE INITIAL DE SOMMETS DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C
C SORTIES:
C --------
C NBARLI : NOMBRE FINAL D'ARETES     DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NBSOM  : NOMBRE FINAL DE SOMMETS   DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NBTGS  : NOMBRE FINAL DE TANGENTES DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NUSOAR : NUMERO DES 2 SOMMETS   DE CHAQUE ARETE
C NUTGAR : NUMERO DES 2 TANGENTES DE CHAQUE ARETE
C LPEFTG : NUMERO DE L'ARETE A TG PARMI LES ARETES (ICI IDENTITE)
C NGEFTG : NUMERO DU CODE GEOMETRIQUE DE CHAQUE ARETE (ICI 14 CAR CONE)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS     JANVIER 1998
C2345X7..............................................................012
      DOUBLE PRECISION  PAX1CN(3), RAY1CN, RAY2CN, HAUTCN
      DOUBLE PRECISION  RPCONE(3,3), XYZ1PL(3), VN(3)
      DOUBLE PRECISION  RPINT(2,1:MXGENE+1)
      INTEGER           NPSUIV(MXGENE+1)
      INTEGER           N1COUR(0:4)
C
      REAL              XYZST(3,NBSOM0), XYZTG(3,2,NBARL0)
      INTEGER           NUSOAR(2,NBARL0), NUTGAR(2,NBARL0),
     %                  LPEFTG(NBARL0), NGEFTG(NBARL0)
C
      REAL              PTSUTG(3)
      DOUBLE PRECISION  P(3), Q(3), PC(3), PT(3), TG(3)
      DOUBLE PRECISION  CORDE, SIGNE, PARAME
C
C     LE NUMERO GEOMETRIQUE DE CETTE ARETE => 13 CYLINDRE ou 14 CONE
      IF( RAY1CN .EQ. RAY2CN ) THEN
C        CYLINDRE
         NGE = 13
      ELSE
C        CONE
         NGE = 14
      ENDIF
C
C     ICI, LE CHAINAGE SUR LE SUIVANT EST NEGATIF SI LE POINT D'INTERSECTION
C     N'EST PAS PRIS COMME SOMMET, POSITIF SINON.
C     ======================================================================
      NBARLI = 0
      NBSOM  = 0
      NBTGS  = 0
      DO 100 N = 1, NBIP, 2
C
C        LE DEBUT DU CHAINAGE DE L'INTERVALLE DE POINTS D'INTERSECTION
         N1 = N1COUR( N )
C
C        LE PREMIER POINT N1 DE CETTE PARTIE DE LIGNE
         CALL PLCNAN( RPINT(1,N1), RAY1CN, RAY2CN, HAUTCN, XYZ1PL, VN,
     %                PARAME, P, Q,  IERR )
         PC(1) = P(1) + PARAME * ( Q(1) - P(1) )
         PC(2) = P(2) + PARAME * ( Q(2) - P(2) )
         PC(3) = P(3) + PARAME * ( Q(3) - P(3) )
C
C        CALCUL DE LA PREMIERE TANGENTE ET DU POINT N1 DANS LE REPERE GLOBAL
         CALL PLCN1T( PAX1CN, RAY1CN, RAY2CN,
     %                RPCONE, HAUTCN, VN, PC,
     %                PT,     TG,     IERR )
C
C        LE PREMIER POINT DE CETTE SUITE D'ARETES
C        INITIALISATION DE SES 3 COORDONNEES GLOBALES
         NBSOM = NBSOM + 1
         NBS0  = NBSOM
         XYZST(1,NBSOM) = REAL( PT(1) )
         XYZST(2,NBSOM) = REAL( PT(2) )
         XYZST(3,NBSOM) = REAL( PT(3) )
C
C        NUMERO DE CETTE PREMIERE ARETE DANS LA LIGNE
         NBARL0 = NBARLI + 1
C
C        LE POINT SUIVANT
         N2 = NPSUIV( N1 )
C
C        LA FIN DU CHAINAGE DE L'INTERVALLE DE POINTS D'INTERSECTION
         NF = N1COUR( N+1 )
C
C        RECHERCHE DE L'ARETE NBARLI DE SOMMETS N1-N2 DE LA LIGNE
C        ========================================================
 10      IF( NPSUIV(N2) .LT. 0 ) THEN
            N2 = -NPSUIV(N2)
C           POINT NON SOMMET DE L'ARETE => PASSAGE AU SUIVANT
            GOTO 10
         ENDIF
C
C        UNE ARETE NBARLI ET 2 TG DE PLUS
C        ================================
         NBARLI = NBARLI + 1
         NBTGS  = NBTGS  + 2
C
C        LE POINT D'INTERSECTION AVEC LA GENERATRICE N2
         CALL PLCNAN( RPINT(1,N2), RAY1CN, RAY2CN, HAUTCN, XYZ1PL, VN,
     %                PARAME, P, Q,  IERR )
         PC(1) = P(1) + PARAME * ( Q(1) - P(1) )
         PC(2) = P(2) + PARAME * ( Q(2) - P(2) )
         PC(3) = P(3) + PARAME * ( Q(3) - P(3) )
C        RETOUR DANS LE REPERE GLOBAL INITIAL
         CALL AC3D3D( PAX1CN, RPCONE, PC, PT )
C
C        UN SOMMET DE PLUS = LE SOMMET 2 DE L'ARETE NBARLI
         NBS1  = NBSOM
         NBSOM = NBSOM + 1
         XYZST(1,NBSOM) = REAL( PT(1) )
         XYZST(2,NBSOM) = REAL( PT(2) )
         XYZST(3,NBSOM) = REAL( PT(3) )
C
C        LA LONGUEUR DE LA CORDE POUR HOMOGENEISER LA VALEUR DE LA TG
         CORDE = SQRT( ( XYZST(1,NBSOM) - XYZST(1,NBS1) ) ** 2
     %               + ( XYZST(2,NBSOM) - XYZST(2,NBS1) ) ** 2
     %               + ( XYZST(3,NBSOM) - XYZST(3,NBS1) ) ** 2 )
C
C        LA TG 1 AU SOMMET 1 DE L'ARETE NBARLI
C        =====================================
C        CALCULEE A PARTIR DE LA TG 2 DE L'ARETE PRECEDENTE
C        LA TG1 EST EN FAIT LA TG STOCKEE DANS TG(1:3) DE L'ARETE PRECEDENTE
C        QU'IL SUFFIT DE NORMALISER ET ASSURER LE BON SENS
         SIGNE = TG(1) * ( XYZST(1,NBSOM) - XYZST(1,NBS1) )
     %         + TG(2) * ( XYZST(2,NBSOM) - XYZST(2,NBS1) )
     %         + TG(3) * ( XYZST(3,NBSOM) - XYZST(3,NBS1) )
C        SAUVEGARDE DU SIGNE
         IF( SIGNE .GE. 0D0 ) THEN
            SIGNE = CORDE
         ELSE
            SIGNE = -CORDE
         ENDIF
         XYZTG(1,1,NBARLI) = REAL( TG(1) * SIGNE )
         XYZTG(2,1,NBARLI) = REAL( TG(2) * SIGNE )
         XYZTG(3,1,NBARLI) = REAL( TG(3) * SIGNE )
C        PROJECTION DU SOMMET 2 SUR LA TG1 => LA NORME DE LA TG1
         PTSUTG(1) = XYZST(1,NBS1) + XYZTG(1,1,NBARLI)
         PTSUTG(2) = XYZST(2,NBS1) + XYZTG(2,1,NBARLI)
         PTSUTG(3) = XYZST(3,NBS1) + XYZTG(3,1,NBARLI)
         CALL PTPRDR( XYZST(1,NBSOM),XYZST(1,NBS1),PTSUTG,PTSUTG )
         XYZTG(1,1,NBARLI) = PTSUTG(1) - XYZST(1,NBS1)
         XYZTG(2,1,NBARLI) = PTSUTG(2) - XYZST(2,NBS1)
         XYZTG(3,1,NBARLI) = PTSUTG(3) - XYZST(3,NBS1)
C
C        LA TG 2 AU SOMMET 2 DE L'ARETE NBARLI
C        =====================================
         CALL PLCN1T( PAX1CN, RAY1CN, RAY2CN,
     %                RPCONE, HAUTCN, VN, PC,
     %                PT,     TG,    IERR )
         SIGNE = TG(1) * ( XYZST(1,NBS1) - XYZST(1,NBSOM) )
     %         + TG(2) * ( XYZST(2,NBS1) - XYZST(2,NBSOM) )
     %         + TG(3) * ( XYZST(3,NBS1) - XYZST(3,NBSOM) )
C        SAUVEGARDE DU SIGNE
         IF( SIGNE .GE. 0D0 ) THEN
            SIGNE = CORDE
         ELSE
            SIGNE = -CORDE
         ENDIF
         XYZTG(1,2,NBARLI) = REAL( TG(1) * SIGNE )
         XYZTG(2,2,NBARLI) = REAL( TG(2) * SIGNE )
         XYZTG(3,2,NBARLI) = REAL( TG(3) * SIGNE )
C        PROJECTION DU SOMMET 1 SUR LA TG => LA NORME DE LA TG
         PTSUTG(1) = XYZST(1,NBSOM) + XYZTG(1,2,NBARLI)
         PTSUTG(2) = XYZST(2,NBSOM) + XYZTG(2,2,NBARLI)
         PTSUTG(3) = XYZST(3,NBSOM) + XYZTG(3,2,NBARLI)
         CALL PTPRDR( XYZST(1,NBS1),XYZST(1,NBSOM),PTSUTG,PTSUTG )
         XYZTG(1,2,NBARLI) = PTSUTG(1) - XYZST(1,NBSOM)
         XYZTG(2,2,NBARLI) = PTSUTG(2) - XYZST(2,NBSOM)
         XYZTG(3,2,NBARLI) = PTSUTG(3) - XYZST(3,NBSOM)
C
C        CONSTRUCTION DE L'ARETE P3 DANS LE TMS NSEF
C        -------------------------------------------
C        LE NUMERO DES 2 SOMMETS DE L'ARETE NBARLI
         NUSOAR(1,NBARLI) = NBS1
         NUSOAR(2,NBARLI) = NBSOM
C
C        LE POINTEUR SUR L'EF A TG
         LPEFTG( NBARLI ) = NBARLI
C
C        LE NUMERO GEOMETRIQUE DE CETTE ARETE => 13 CYLINDRE ou 14 CONE
         NGEFTG( NBARLI ) = NGE
C
C        LE NUMERO DES 2 SOMMETS DE L'ARETE NBARLI
         NUTGAR(1,NBARLI) = NBTGS-1
         NUTGAR(2,NBARLI) = NBTGS
C
C        PASSAGE A L'ARETE SUIVANTE
         IF( N2 .NE. NF ) THEN
C           CE N'EST PAS LA DERNIERE ARETE DE CET INTERVALLE
            N1 = N2
            N2 = NPSUIV( N2 )
            GOTO 10
         ENDIF
C
C        DERNIERE ARETE DE CETTE SUITE D'ARETES DE LA LIGNE
C        --------------------------------------------------
C        EST IL POSSIBLE D'IDENTIFIER LES 2 EXTREMITES?
         CALL XYZIDE( XYZST(1,NBS0), XYZST(1,NBSOM), N1 )
         IF( N1 .NE. 0 ) THEN
C           LES 2 POINTS SONT IDENTIQUES => COURBE FERMEE
C           LE DERNIER SOMMET EST DONC LE PREMIER NBS0
            NBSOM = NBSOM - 1
            NUSOAR(2,NBARLI) = NBS0
         ENDIF
C
 100  CONTINUE
C
      IERR = 0
      RETURN
      END
