      SUBROUTINE FCPTVPSI( XYZPT,    PTXYZD,  N1FEOC, NFETOI, QUAMINEX,
     %                     MXVPSI,   NBVPSI,  NFVPSI, NFVPSI0,
     %                     MIARSICI, MXCIAS,  NBCIAS, N1CIAS,
     %                     MXASFVP,  NBASFVP, NSASFVP,
     %                     VETXYZ,   QUAMIMX, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C  BUT :    DETERMINER L'ENSEMBLE DES FACES CONTIGUES DE L'ETOILE FORMANT
C ------    AVEC LE POINT XYZPT DES TETRAEDRES DE VOLUMES>0
C           SANS INTERSECTION AVEC LES FACES DE L'ETOILE TELLES QUE
C           CES FACES MAXIMISENT LE MINIMUM DES QUALITES DES TETRAEDRES

C ENTREES:
C --------
C XYZPT  :  COORDONNEES XYZ DU POINT A TESTER COMME CENTRE DE TETRAEDRISATION
C PTXYZD :  TABLEAU DES COORDONNEES DES POINTS
C           PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C N1FEOC :  POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C           CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI :  LES FACES TRIANGULAIRES DE L'ETOILE
C           1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C           2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C           3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C           4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C           S1S2 x S1S3 VERS L'INTERIEUR DE L'ETOILE
C QUAMINEX: QUALITE MINIMALE EXIGEE POUR CREER UN TETRAEDRE

C MXVPSI  : NOMBRE MAXIMAL DE FACES DU TABLEAU NFVPSI et NFVPSI0
C MXCIAS  : NOMBRE MAXIMAL DE CIRCUITS
C MXFASFVP: NOMBRE MAXIMAL DE FACES SIMPLES DES CIRCUITS

C SORTIES:
C --------
C NBVPSI : NOMBRE DE TETRAEDRES DE VOLUME>0 SANS INTERSECTION
C          AVEC LES FACES DE L'ETOILE
C          = -MXVPSI SI NBVPSI>MXVPSI
C NFVPSI : NUMERO NFETOI DES NBVPSI FACES DONNANT AVEC XYZPT UN VOLUME>0
C          ET SANS INTERSECTION AVEC LES FACES DE L'ETOILE
C NFVPSI0: NUMERO NFETOI DES NBVPSI FACES DONNANT AVEC XYZPT UN VOLUME>0
C          ET SANS INTERSECTION AVEC LES FACES DE L'ETOILE
C          SANS CONTINUITE DES FACES (TABLEAU AUXILIAIRE)

C MIARSICI:NBRE MINIMAL D'ARETES SIMPLES DES NBCIAS CIRCUITS DES FACES NFVPSI

C NBCIAS : NOMBRE DE CIRCUITS DES ARETES SIMPLES DES FACES NFVPSI DANS NFETOI
C N1CIAS : NO DANS NSASFVP DE LA PREMIERE ARETE DES NBCIAS CIRCUITS

C NBASFVP: NOMBRE DE FACES DES ARETES SIMPLES DES NBCIAS CIRCUITS
C NSASFVP: 1: NUMERO DU SOMMET 1  DE L'ARETE SIMPLE
C          2: NUMERO DU SOMMET 2  DE L'ARETE SIMPLE
C          3: NUMERO DANS NSASFVP DE L'ARETE SIMPLE SUIVANTE
C          4: NUMERO DANS NFVPSI DE LA FACE DE CETTE ARETE SIMPLE

C VETXYZ : VOLUME DE L'ETOILE DE CENTRE XYZPT DES TETRAEDRES NF-XYZPT
C QUAMIMX: QUALITE MINIMALE MAXIMISEE DES QUALITES DES TETRAEDRES NF-XYZPT
C IERR   : =9 MXASFVP TROP PETIT. A AUGMENTER
C          >0 NUMERO DU CIRCUIT DE MOINS DE 3 ARETES
C          =0 PAS D'ERREUR DETECTEE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY     Aout 2015
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,*), XYZPT(3), VETXYZ
      INTEGER           NFETOI(5,*), NFVPSI(MXVPSI), NFVPSI0(MXVPSI)
      INTEGER           N1CIAS(MXCIAS), NSASFVP(4,MXASFVP)
      REAL              QUAMIMX, QUAMIN

C     MIARSICI: NOMBRE MINIMAL D'ARETES SIMPLES DES CIRCUITS DES FACES NFVPSI
      MIARSICI  = 2**30
      NBVPSIMAX = 0
      NFVPSIMAX = 0
      QUAMIMX = 0.
      NBVPSI  = 0

C     DETERMINER L'ENSEMBLE DES NBVPSI0 FACES NFVPSI0 DE L'ETOILE FORMANT
C     AVEC LE POINT XYZPT DES TETRAEDRES DE VOLUMES>0 ET QUALITE CORRECTE
C     SANS INTERSECTION AVEC LES FACES DE L'ETOILE
C     -------------------------------------------------------------------
      CALL F0PTVPSI( QUAMINEX, XYZPT, PTXYZD,  N1FEOC, NFETOI,
     %               MXVPSI, NBVPSI0, NFVPSI0, NBFETO, VETXYZ, QUAMIMX)
      IF( NBVPSI0 .EQ. NBFETO ) THEN
C        TOUTES LES FACES CONVIENNENT => ESSAI D'UN RACCOURCI
         NBVPSIMAX = NBFETO
         NFVPSIMAX = N1FEOC
C        PAS DE CIRCUIT
         MIARSICI = 0
         GOTO 50
      ENDIF

ccc      IF( NBVPSI0 .LT. MAX( 3, NBFETO/3 ) ) THEN
cccC        PAS ASSEZ DE FACES+XYZ A V>0
ccc         GOTO 9999
ccc      ENDIF

C     RECHERCHE PARMI NFVPSI0 DE LA FACE DE DEPART APPORTANT
C     LE NOMBRE MAXIMAL DE TETRAEDRES A FORMER AVEC LE POINT XYZPT
C     ET CALCUL DE LA QUALITE MINIMALE DES TETRAEDRES DE SOMMET XYZPT
C     ---------------------------------------------------------------
      DO M = 1, NBVPSI0

C        DEPART A PARTIR DE LA FACE M DE NFVPSI0
         NF0 = NFVPSI0( M )
         CALL F1PTVPSI( QUAMINEX, XYZPT,   PTXYZD,  N1FEOC, NFETOI,
     %                  NF0,      NBVPSI0, NFVPSI0,
     %                  MXVPSI,   NBVPSI,  NFVPSI, VETXYZ, QUAMIN )

C        RECHERCHE DES CIRCUITS D'ARETES SIMPLES DES NBVPSI FACES
C        NFVPSI DE NFETOI
         CALL CIASFAET( NBVPSI,  NFVPSI,  NFETOI,
     %                  NBCIAS,  MXCIAS,  N1CIAS,
     %                  MXASFVP, MIARSICI, NBASFVP, NSASFVP, IERR )
         IF( IERR .NE. 0 ) GOTO 9999

C        BILAN DE LA FACE M DE NFVPSI0. EST IL UN CAS MINIMAL?
         IF( MIARSICI .GT. NBASFVP ) THEN
            MIARSICI  = NBASFVP
            NBVPSIMAX = NBVPSI
            NFVPSIMAX = NF0
C           MAXIMISATION DU MINIMUM DES QUALITES DES TETRAEDRES DE L'ETOILE
            QUAMIMX = QUAMIN
         ELSE IF( MIARSICI.EQ.NBASFVP .AND. QUAMIN.GT.QUAMIMX ) THEN
            NBVPSIMAX = NBVPSI
            NFVPSIMAX = NF0
C           MAXIMISATION DU MINIMUM DES QUALITES DES TETRAEDRES DE L'ETOILE
            QUAMIMX = QUAMIN
         ENDIF

      ENDDO

 50   NBVPSI = NBVPSIMAX
      IF( NBVPSIMAX .GT. 0 ) THEN

C        RECUPERATION DES NBVPSIMAX FACES CORRECTES AVEC UN MAX DES MIN
C        DES QUALITES DES TETRAEDRES DE SOMMET XYZPT
C        CAR LE CAS OPTIMAL A PU ETRE EFFACE PAR UN CAS MOINS FAVORABLE
C        --------------------------------------------------------------
         CALL F1PTVPSI( QUAMINEX,  XYZPT,   PTXYZD,  N1FEOC, NFETOI,
     %                  NFVPSIMAX, NBVPSI0, NFVPSI0,
     %                  MXVPSI,    NBVPSI,  NFVPSI,  VETXYZ, QUAMIMX )

C        RECHERCHE DES CIRCUITS D'ARETES SIMPLES DES NBVPSI FACES
C        NFVPSI DE NFETOI
         CALL CIASFAET( NBVPSI,  NFVPSI,  NFETOI,
     %                  NBCIAS,  MXCIAS,  N1CIAS,
     %                  MXASFVP, MIARSICI, NBASFVP, NSASFVP, IERR )
         IF( IERR .NE. 0 ) GOTO 9999

      ENDIF

cccC     IL EXISTE DES NBVPSI TETRAEDRES DE VOLUME POSITIF A CREER
ccc      IF( NBVPSI .GT. 0 ) THEN
ccc         PRINT*,'fcptvpsi: FACE+XYZ=',XYZPT,' => NBVPSI=',NBVPSI,
ccc     % ' V>0  MAX QUALITE MIN=',QUAMIMX
ccc         PRINT*,'fcptvpsi: NFVPSI=',(NFVPSI(K),K=1,NBVPSI)
ccc         WRITE(KTITRE(11:15), '(I5)'   ) NBVPSI
ccc         KTITRE='fcptvpsi:         FACES+XYZ de VOLUME>0'
ccc         CALL TRFETO3( KTITRE, XYZPT,  XYZPT,  PTXYZD,
ccc     %                 N1FEOC, NFETOI, NBVPSI, NFVPSI )
ccc      ENDIF

 9999 RETURN
      END
