      SUBROUTINE PTBPTI( NUPT, NUAPTI, NUDPTI, PXYD, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   LE POINT NUPT EST IL DANS UNE BOULE DE POINT INTERNE IMPOSE
C -----   ET A UNE DISTANCE DE 5 FOIS CELLE SOUHAITEE?
C
C ENTREES:
C --------
C NUPT   : NUMERO DU POINT DE POSITION A ANALYSER
C NUAPTI : NUMERO DU POINT AVANT LE PREMIER POINT INTERNE IMPOSE DANS PXYD
C NUDPTI : NUMERO DU DERNIER POINT INTERNE IMPOSE DANS PXYD
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C
C SORTIE :
C --------
C NONOUI : 0 SI LE POINT NUPT N'EST PAS DANS LES BOULES CENTREES EN LES
C            POINTS INTERNES ET DE RAYON 5 FOIS LEUR DISTANCE SOUHAITEE
C          1 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET NCTS TSING HUA University  HSINCHU TAIWAN 2006
C....6...............................................................012
      DOUBLE PRECISION  PXYD(3,*), D
C
C     A PRIORI LE POINT N'EST PAS DANS UNE BOULE
      NONOUI = 0
C
      DO 10 N=NUAPTI+1, NUDPTI
         D = ( PXYD(1,N) - PXYD(1,NUPT) ) ** 2
     %     + ( PXYD(2,N) - PXYD(2,NUPT) ) ** 2
         IF( D .LT. (PXYD(3,N)*5D0)**2 ) THEN
            NONOUI = 1
            RETURN
         ENDIF
 10   CONTINUE
C
      RETURN
      END
