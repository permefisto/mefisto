      SUBROUTINE ARARCF( NS1, NS2, NBCF, N1ARCF, NOARCF,
     %                   NLFCF, NAVANT, NA1 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    L'ARETE DE SOMMET NS1-NS2 EST ELLE UNE ARETE DU CONTOUR
C -----    FERME DEFINI PAR NBCF, N1ARCF et NOARCF (DE CHAINAGE CIRCULAIRE)?

C ENTREES:
C --------
C NS1 NS2: NUMERO DES 2 SOMMETS DE L'ARETE A TESTER
C NBCF   : NOMBRE DE LIGNES FERMEES PERIPHERIQUES DES FACES PERDUES
C N1ARCF : NUMERO DU 1-ER SOMMET OU 1-ERE ARETE DES LIGNES DU CONTOUR FERME
C          N1ARCF(0) POINTEUR SUR LA PREMIERE ARETE VIDE
C NOARCF : 1:NUMERO DU SOMMET DE L'ARETE DE LA LIGNE DU CONTOUR FERME
C          2:NUMERO DANS NOARCF DE L'ARETE SUIVANTE DU CF
C          3:NUMERO DANS LEFACO DU TRIANGLE ADJACENT OPPOSE A L'ARETE

C SORTIES:
C --------
C NLFCF  : >0  NUMERO (1 A NBCF) DE LA LIGNE FERMEE DU CF D'ARETE NS1-NS2
C          =0  ARETE NS1-NS2 NON DANS NOARCF C-A-D LES ARETES SIMPLES CF
C NAVANT : >0  NUMERO NOARCF DE L'ARETE QUI PRECEDE NA1 DANS NOARCF
C          =0  ARETE NS1-NS2 NON DANS NOARCF C-A-D LES ARETES SIMPLES CF
C NA1    : >0  NUMERO NOARCF DE L'ARETE NS1->NS2 DANS NOARCF
C          <0 -NUMERO NOARCF DE L'ARETE NS2->NS1 DANS NOARCF
C          =0  ARETE NS1-NS2 NON DANS NOARCF C-A-D LES ARETES SIMPLES CF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC ET ST PIERRE DU PERRAY Octobre 2014
C2345X7..............................................................012
      INTEGER  N1ARCF(0:NBCF), NOARCF(1:3,1:*)

C     NS1-NS2 EST ELLE UNE ARETE NSA1-NSA2 DU CF?

      DO NLFCF = 1, NBCF

C        L'ARETE PRECEDENTE DE NA1
         NAVANT = 0

C        LA PREMIERE ARETE DU CF
         NA00 = N1ARCF( NLFCF )
         IF( NA00 .LE. 0 ) THEN
            print*,'ararcf: ERREUR N1ARCF(',NLFCF,')=',NA00,
     %             ' VALEUR INCORRECTE'
            GOTO 80
         ENDIF
         NA1 = NA00

C        EXTREMITE DE L'ARETE NA1 DU CF
 10      NA2 = NOARCF( 2, NA1 )

C        LES 2 SOMMETS DE L'ARETE NA1->NA2 DU CF
         NSA1 = NOARCF(1,NA1)
         NSA2 = NOARCF(1,NA2)

         IF( NS1 .EQ. NSA1 .AND. NS2 .EQ. NSA2 ) THEN
C           L'ARETE NS1-NS2 EST L'ARETE NA1 DU CF
            GOTO 90
         ELSE IF( NS1 .EQ. NSA2 .AND. NS2 .EQ. NSA1 ) THEN
C           L'ARETE NS2-NS1 EST L'ARETE NA1 DU CF
            NA1 = -NA1
            GOTO 90
         ENDIF

         IF( NA2 .NE. NA00 .AND. NA2 .NE. 0 ) THEN
C           PASSAGE A L'ARETE SUIVANTE DU CF
            NAVANT = NA1
            NA1 = NA2
            GOTO 10
         ENDIF

      ENDDO

C     ARETE NON RETROUVEE
 80   NAVANT = 0
      NA1    = 0
      NLFCF  = 0
      GOTO 9999

C     ARETE RETROUVEE
C     SI NAVANT=0  RECHERCHE DE LA DERNIERE ARETE DU CF
 90   IF( NAVANT .EQ. 0 ) THEN

C        LA PREMIERE ARETE DU CF
         NA00   = N1ARCF( NLFCF )
         NAVANT = NA00

 95      NA2 = NOARCF( 2, NAVANT )
         IF( NA2 .NE. NA00 .AND. NA2 .NE. 0 ) THEN
            NAVANT = NA2
            GOTO 95
         ENDIF

      ENDIF

 9999 RETURN
      END
