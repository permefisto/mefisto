      SUBROUTINE TRPLTR( NBTR,   NUTR,   PXYD,
     %                   MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                   NCTRIA, NCARET )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    TRACER LES NBTR TRIANGLES DE NUMEROS NUTR DANS NOARTR
C -----
C ENTREES:
C --------
C BNTR   : NOMBRE DE TRIANGLES A TRACER
C NUTR   : NUMERO DANS NOARTR DES NBTR TRIANGLES
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C MOARTR : NOMBRE MAXIMAL D'ENTIERS PAR TRIANGLE
C NOARTR : LES 3 ARETES DES TRIANGLES +-ARETE1, +-ARETE2, +-ARETE3
C          ARETE1=0 SI TRIANGLE VIDE => ARETE2=TRIANGLE VIDE SUIVANT
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES, CHAINAGES EN +
C NCTRIA : NUMERO DE LA COULEUR DU TRIANGLE A TRACER
C NCARET : NUMERO DE LA COULEUR DES ARETES DU TRIANGLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC      AVRIL 1997
C....................................................................012
      LOGICAL           TRATRI
      COMMON / DV2DCO / TRATRI
C     TRACE OU NON DES TRIANGLES GENERES DANS LA TRIANGULATION
      INTEGER           NUTR(NBTR),
     %                  NOARTR(MOARTR,*),
     %                  NOSOAR(MOSOAR,*)
      DOUBLE PRECISION  PXYD(3,*), DBLE
      REAL              COMIMX(2,2)
      INTEGER           NOSOTR(3)
C
      IF( .NOT. TRATRI ) RETURN
C
C     LES TRACES SONT DEMANDES
C     LE CADRE OBJET GLOBAL EN UNITES UTILISATEUR EST CALCULE
C     =======================================================
      NT = NUTR(1)
      NS = NOSOAR( 1, ABS(NOARTR(1,NT)) )
      COMIMX(1,1) = REAL( PXYD(1,NS) )
      COMIMX(1,2) = REAL( PXYD(1,NS) )
      COMIMX(2,1) = REAL( PXYD(2,NS) )
      COMIMX(2,2) = REAL( PXYD(2,NS) )
C
      DO 30 N=1,NBTR
C
C        LE NUMERO DU TRIANGLE N DANS LE TABLEAU NOARTR
         NT = NUTR(N)
C
C        LE NUMERO DES 3 SOMMETS DU TRIANGLE NT1 DANS LE SENS DIRECT
         CALL NUSOTR( NT, MOSOAR, NOSOAR, MOARTR, NOARTR, NOSOTR )
C
         DO 20 K=1,3
C           LE NUMERO DU SOMMET K DU TRIANGLE NT
            NS = NOSOTR(K)
            DO 10 I=1,2
               COMIMX(I,1) = REAL( MIN( DBLE(COMIMX(I,1)), PXYD(I,NS) ))
               COMIMX(I,2) = REAL( MAX( DBLE(COMIMX(I,2)), PXYD(I,NS) ))
 10         CONTINUE
 20      CONTINUE
 30   CONTINUE
C
C     AVEC UNE MARGE
      XE = COMIMX(1,2) - COMIMX(1,1)
      YE = COMIMX(2,2) - COMIMX(2,1)
      CALL ISOFENETRE( COMIMX(1,1)-XE/20, COMIMX(1,2)+XE/20,
     %                 COMIMX(2,1)-YE/20, COMIMX(2,2)+YE/20 )
C
C     L'ECRAN EST EFFACE
      CALL EFFACE
C
C     LE TRACE EFFECTIF
C     =================
      DO 50 N=1,NBTR
C        LE TRACE DU TRIANGLE NUTR(N) DANS LE TABLEAU NOARTR
         CALL MTTRTR( PXYD, NUTR(N), MOARTR, NOARTR, MOSOAR, NOSOAR,
     %                NCTRIA, NCARET )
 50   CONTINUE
C
C     COPIE DE MEMPX DANS FENETRE
      CALL MEMPXFENETRE
C
C     POUR VIDER LE BUFFER DE X11
      CALL XVVOIR
      END
