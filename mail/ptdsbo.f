      SUBROUTINE PTDSBO( Pt, PTXYZD, NOSOTE,  NONOUI, VTE, DSR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCULER LA DISTANCE RELATIVE DU POINT Pt AU CENTRE DE
C -----    LA BOULE CIRCONSCRITE AU TETRAEDRE NOSOTE
C          EN DEDUIRE SI LE POINT Pt EST INTERNE OU EXTERNE A CETTE BOULE

C ENTREES:
C --------
C Pt     : LES 3 COORDONNEES DU POINT
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NOSOTE : NUMERO PTXYZD DES 4 SOMMETS DU TETRAEDRE

C SORTIES:
C --------
C NONOUI : 1  Pt   EST     DANS LA BOULE CIRCONSCRITE
C          0  Pt N'EST PAS DANS LA BOULE CIRCONSCRITE
C         -1  LE TETRAEDRE EST DE VOLUME TRES FAIBLE ou NUL ou NEGATIF
C VTE    : LE VOLUME DU TETRAEDRE NOSOTE
C DSR    : LE CARRE DE LA DISTANCE DU POINT Pt AU CENTRE DE LA BOULE
C          DIVISE PAR LE CARRE DU RAYON DE LA BOULE
C          -1D0 SI ERREUR DANS LE CALCUL DU CENTRE DE LA BOULE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint Pierre du Perray          Novembre 2018
C....................................................................012
      DOUBLE PRECISION  Pt(3), PTXYZD(4,*), CENTRE(4), VTE, DSR
      INTEGER           NOSOTE(4), NONOUI

C     CALCUL DU CENTRE ET CARRE DU RAYON DE LA BOULE CIRCONSCRITE
      CALL CERABOCI( PTXYZD( 1, NOSOTE(1) ),
     %               PTXYZD( 1, NOSOTE(2) ),
     %               PTXYZD( 1, NOSOTE(3) ),
     %               PTXYZD( 1, NOSOTE(4) ),
     %               CENTRE, VTE, IERR )
C     IERR=0 SI CALCUL CORRECT SANS PROBLEME RENCONTRE
C         =1 SI TETRAEDRE PLAT (4 SOMMETS DANS UN PLAN)
C         =2 SI TETRAEDRE MAL ORIENTE DE VOLUME STRICTEMENT NEGATIF
C         =3 SI RAYON**2 DE LA BOULE CIRCONSCRITE PAR LA FORMULE est <0
C         =4 SI TROP GRANDE DIFFERENCE SUR LE CALCUL DU RAYON AUX 4 SOMMETS
C               SOUVENT DU A UN VOLUME TROP FAIBLE

      IF( IERR .NE. 0 ) THEN
C        LE VOLUME DU TETRAEDRE NOSOTE  EST DE VOLUME
C        TRES FAIBLE ou NUL ou NEGATIF
         DSR    = -1D0
         NONOUI = -1
         GOTO 9999
      ENDIF

C     DSR est LE CARRE DE LA DISTANCE DU POINT Pt AU CENTRE DE LA BOULE
C             DIVISE PAR LE CARRE DU RAYON DE LA BOULE
      DSR = ( ( CENTRE(1) - Pt(1) ) ** 2 +
     %        ( CENTRE(2) - Pt(2) ) ** 2 +
     %        ( CENTRE(3) - Pt(3) ) ** 2 ) / CENTRE(4)

C     LE POINT Pt EST IL DANS LA BOULE CIRCONSCRITE DU TETRAEDRE?
      IF( DSR .LE. 1.000001D0 ) THEN
C        OUI: LE POINT Pt EST INTERIEUR OU SUR LA BOULE CIRCONSCRITE
         NONOUI = 1
      ELSE
         NONOUI = 0
      ENDIF

 9999 RETURN
      END
