      SUBROUTINE CONETG( MXPINT,
     %                   PAX1C1, RAY1C1, RAY2C1,
     %                   PAX1C2, RAY1C2, RAY2C2,
     %                   HAUTC1, RPCON1, HAUTC2, RPCON2,
     %                   NPSUIV, NTPINT, RPINT,
     %                   NBIP1,  NBIP2,  N1COUR,
     %                   NBARL0, NBSOM0,
     %                   NBARLI, NBSOM,  NBTGS,
     %                   XYZST,  XYZTG,
     %                   NUSOAR, LPEFTG, NGEFTG, NUTGAR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DES SOMMETS ET TANGENTES ET ARETES DE LA LIGNE
C -----    INTERSECTION ENTRE 2 TRONCS DE CONES
C
CREMARQUE: EN SORTIE IL PEUT EXISTER JUSQU'A 2 COUPLES DE SOMMETS
C--------- IDENTIQUES (INTERSECTION DE 2 CYLINDRES DE MEME RAYON)
C          CES COUPLES NE SONT PAS IDENTIFIES POUR GARANTIR
C          LE STATUT DE LIGNE FERMEE (TOUT SOMMET APPARTIENT A 2 ARETES)
C          CE QUI NE SERAIT PLUS LE PLUS CAS APRES IDENTIFICATION
C          DE PLUS, LES 2 COURBES FERMEES FORMANT LA LIGNE
C          RESTENT ALORS FACILEMENT SEPARABLES
C
C ENTREES:
C --------
C MXGENE : NOMBRE DE GENERATRICES DU CONE 1 A INTERSECTER AVEC LE CONE 2
C MXPINT : NOMBRE MAXIMAL DE POINTS D'INTERSECTION DECLARABLES (>2*MXGENE)
C
C PAX1C1 : POINT DE BASE DE L'AXE DU TRONC DE CONE 1
C RAY1C1 : RAYON DU CERCLE AU POINT PAX1C1 (PLAN ORTHOGONAL A L'AXE DU CONE )
C PAX2C1 : POINT DE L'AXE DU TRONC DE CONE 1
C RAY2C1 : RAYON DU CERCLE AU POINT PAX2C1 (PLAN ORTHOGONAL A L'AXE DU CONE )
C
C PAX1C2 : POINT DE BASE DE L'AXE DU TRONC DE CONE 2
C RAY1C2 : RAYON DU CERCLE AU POINT PAX1C2 (PLAN ORTHOGONAL A L'AXE DU CONE )
C PAX2C2 : POINT DE L'AXE DU TRONC DE CONE 2
C RAY2C2 : RAYON DU CERCLE AU POINT PAX2C2 (PLAN ORTHOGONAL A L'AXE DU CONE )
C
C HAUTC1 : HAUTEUR DU TRONC DE CONE 1
C RPCON1 : REPERE PROPRE ORTHONORME DU CONE 1 D'ORIGINE AU POINT PAX1C1
C          ORIGINE AU POINT PAX1C1
C HAUTC2 : HAUTEUR DU TRONC DE CONE 2
C RPCON2 : REPERE PROPRE ORTHONORME DU CONE 2 D'ORIGINE AU POINT PAX1C2
C          ORIGINE AU POINT PAX1C2
C NPSUIV : NUMERO DANS NTPINT, RPINT DU POINT D'INTERSECTION SUIVANT
C NTPINT : NUMERO DE LA RACINE PARMI LES 2 POSSIBLES DU POINT D'INTERSECTION
C RPINT  : RPINT(1,N) ANGLE DE LA GENERATRICE DU CONE 1 DE CE POINT N
C          RPINT(2,N) RACINE TEL QUE PM = RACINE PQ SUR LA GENERATRICE DU CONE 1
C          RPINT(3,N) DISTANCE AU POINT PRECEDENT
C
C NBIP1  : NUMERO DANS N1COUR DU PREMIER INTERVALLE DE POINTS D'INTERSECTION
C NBIP2  : NUMERO DANS N1COUR DU DERNIER INTERVALLE DE POINTS D'INTERSECTION
C N1COUR : DEBUT DES CHAINAGES DES COURBES, PUIS DES INTERVALLES DE GENERATRICES
C          PUIS DES INTERVALLES DE POINTS D'INTERSECTION
C          N1COUR(0) POINTE SUR LE PREMIER VIDE
C NBARL0 : NOMBRE INITIAL D'ARETES   DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NBSOM0 : NOMBRE INITIAL DE SOMMETS DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C
C SORTIES:
C --------
C NBARLI : NOMBRE FINAL D'ARETES     DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NBSOM  : NOMBRE FINAL DE SOMMETS   DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NBTGS  : NOMBRE FINAL DE TANGENTES DE LA LIGNE INTERSECTION DES 2 TRONCS DE CO
C NUSOAR : NUMERO DES 2 SOMMETS   DE CHAQUE ARETE
C NUTGAR : NUMERO DES 2 TANGENTES DE CHAQUE ARETE
C LPEFTG : NUMERO DE L'ARETE A TG PARMI LES ARETES (ICI IDENTITE)
C NGEFTG : NUMERO DU CODE GEOMETRIQUE DE CHAQUE ARETE (ICI 14 CAR CONE)
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS    DECEMBRE 1997
C2345X7..............................................................012
      COMMON / UNITES / LECTEU, IMPRIM, NUNITE(30)
      DOUBLE PRECISION  PAX1C1(3)
      DOUBLE PRECISION  PAX1C2(3)
      DOUBLE PRECISION  RAY1C1, RAY2C1
      DOUBLE PRECISION  RAY1C2, RAY2C2
      DOUBLE PRECISION  HAUTC1, HAUTC2
      DOUBLE PRECISION  RPCON1(3,3), RPCON2(3,3)
      DOUBLE PRECISION  RPINT(3,1:MXPINT)
      INTEGER           NPSUIV(MXPINT), NTPINT(MXPINT)
      INTEGER           N1COUR(0:64)
C
      REAL              XYZST(3,NBSOM0), XYZTG(3,2,NBARL0)
      INTEGER           NUSOAR(2,NBARL0), NUTGAR(2,NBARL0),
     %                  LPEFTG(NBARL0), NGEFTG(NBARL0)
C
      REAL              PTSUTG(3)
      DOUBLE PRECISION  ANGLE2, DEUXPI, ARC
      DOUBLE PRECISION  P(3), Q(3), PQ(3), PT(3,2),
     %                  TG(3), TG0(3),
     %                  RACIN2(2)
C
      DEUXPI = ATAN( 1D0 ) * 8D0
C
C     LE NUMERO GEOMETRIQUE DE CETTE ARETE => 13 CYLINDRE ou 14 CONE
      IF( RAY1C1 .EQ. RAY2C1 .AND. RAY1C2 .EQ. RAY2C2 ) THEN
C        2 CYLINDRES
         NGE = 13
      ELSE
C        AU MOINS UN CONE
         NGE = 14
      ENDIF
C
C     ICI, LE CHAINAGE SUR LE SUIVANT EST POSITIF SI LE POINT EST UN SOMMET D'AR
C     NEGATIF SI LE POINT D'INTERSECTION N'EST PAS LE SOMMET SUIVANT DE L'ARETE
C     ==========================================================================
      NBS0   = 0
      NBARLI = 0
      NBSOM  = 0
      NBTGS  = 0
      DO 100 N = NBIP1, NBIP2, 2
C
C        LE DEBUT DU CHAINAGE DE L'INTERVALLE DE POINTS D'INTERSECTION
         N1 = N1COUR( N )
C        LA FIN   DU CHAINAGE DE L'INTERVALLE DE POINTS D'INTERSECTION
         NF = N1COUR( N+1 )
C
C        LE POINT SUIVANT DE N1
  5      N2 = ABS( NPSUIV(N1) )
C
C        RECHERCHE DE L'ARETE N1-N2 DE LA LIGNE
 10      IF( NPSUIV(N2) .LT. 0 ) THEN
            N2 = -NPSUIV(N2)
C           POINT NON SOMMET DE L'ARETE
            GOTO 10
         ENDIF
C
C        UNE ARETE N1-N2 DE PLUS. CALCUL DES 2 SOMMETS ET TANGENTES
         NBARLI = NBARLI + 1
         NBTGS  = NBTGS  + 2
C
         IF( N1 .EQ. N1COUR(N) ) THEN
C
C           LE SOMMET INITIAL DE LA PREMIERE ARETE N1-N2
C           --------------------------------------------
            CALL CONERA( RPINT(1,N1),
     %                   PAX1C1, RPCON1, RAY1C1, RAY2C1, HAUTC1,
     %                   PAX1C2, RPCON2, RAY1C2, RAY2C2, HAUTC2,
     %                   NBRAC2, RACIN2, P, Q  )
C           NUMERO DE LA RACINE, 1 SI RACINE DOUBLE
            NURAC = NTPINT(N1)
            IF( NURAC .EQ. 3 ) NURAC = 1
            DO 12 J=1,3
               PQ(J) = P(J) + RACIN2(NURAC) * (Q(J) - P(J))
 12         CONTINUE
C           RETOUR DANS LE REPERE GLOBAL INITIAL
            CALL AC3D3D( PAX1C2, RPCON2, PQ, PT(1,1) )
C
C           INITIALISATION DES 3 COORDONNEES XYZ DU SOMMET INITIAL
            NBSOM = NBSOM + 1
            NBS0  = NBSOM
            XYZST(1,NBSOM) = REAL( PT(1,1) )
            XYZST(2,NBSOM) = REAL( PT(2,1) )
            XYZST(3,NBSOM) = REAL( PT(3,1) )
C
C           LA TG EN CE SOMMET INITIAL
            CALL COCOTG( PAX1C1, PAX1C2, RAY1C2, RAY2C2,
     %                   RPCON1, RPCON2, HAUTC2,
     %                   RPINT(2,N1),    PQ,     PT(1,1), P, Q,
     %                   TG0,    IERR0 )
C
C           NUMERO DE L'ARETE INITIALE
            NBARL0 = NBARLI
C
         ENDIF
C
C        LE SOMMET FINAL DE L'ARETE N1-N2
C        --------------------------------
C        SI LA COURBE EST FERMEE EN RETOUR L'ANGLE 0 DOIT ALORS ETRE 2 PI
         ANGLE2 = RPINT(1,N2)
         IF( N2 .EQ. N1COUR(N) ) THEN
            IF( ANGLE2 .EQ. 0D0 ) THEN
               ANGLE2 = DEUXPI
            ENDIF
         ENDIF
         CALL CONERA( ANGLE2,
     %                PAX1C1, RPCON1, RAY1C1, RAY2C1, HAUTC1,
     %                PAX1C2, RPCON2, RAY1C2, RAY2C2, HAUTC2,
     %                NBRAC2, RACIN2, P, Q  )
         NURAC = NTPINT(N2)
         IF( NURAC .EQ. 3 ) NURAC = 1
         DO 20 J=1,3
            PQ(J) = P(J) + RACIN2(NURAC) * (Q(J) - P(J))
 20      CONTINUE
C        RETOUR DANS LE REPERE GLOBAL INITIAL
         CALL AC3D3D( PAX1C2, RPCON2, PQ, PT(1,2) )
C
C        UN SOMMET DE PLUS
         NBS1  = NBSOM
         NBSOM = NBSOM + 1
C
C        LE SECOND SOMMET DE L'ARETE
         XYZST(1,NBSOM) = REAL( PT(1,2) )
         XYZST(2,NBSOM) = REAL( PT(2,2) )
         XYZST(3,NBSOM) = REAL( PT(3,2) )
C
C        LONGUEUR DE L'ARETE DROITE
         ARC = SQRT( (XYZST(1,NBSOM)-XYZST(1,NBS1) ) ** 2
     %             + (XYZST(2,NBSOM)-XYZST(2,NBS1) ) ** 2
     %             + (XYZST(3,NBSOM)-XYZST(3,NBS1) ) ** 2 )
C
C        LA TANGENTE AU SOMMET FINAL DE L'ARETE
         CALL COCOTG( PAX1C1, PAX1C2, RAY1C2, RAY2C2,
     %                RPCON1, RPCON2, HAUTC2,
     %                ANGLE2, PQ,     PT(1,2), P, Q,
     %                TG,     IERR )
         IF( IERR .NE. 0 ) THEN
C
C           LA TG EST LA CORDE CAR LES 2 PLANS TG SONT CONFONDUS
CCC            WRITE(IMPRIM,*) 'CONETG: 2 PLANS TG IDENTIQUES'
            XYZTG(1,2,NBARLI) = XYZST(1,NBS1) - XYZST(1,NBSOM)
            XYZTG(2,2,NBARLI) = XYZST(2,NBS1) - XYZST(2,NBSOM)
            XYZTG(3,2,NBARLI) = XYZST(3,NBS1) - XYZST(3,NBSOM)
         ELSE
C
C           PROJECTION DU SOMMET INITIAL SUR LA TG FINALE => LA NORME DE LA TG1
            PTSUTG(1) = REAL( XYZST(1,NBSOM) + TG(1) * ARC )
            PTSUTG(2) = REAL( XYZST(2,NBSOM) + TG(2) * ARC )
            PTSUTG(3) = REAL( XYZST(3,NBSOM) + TG(3) * ARC )
            CALL PTPRDR( XYZST(1,NBS1),XYZST(1,NBSOM),PTSUTG, PTSUTG )
            XYZTG(1,2,NBARLI) = PTSUTG(1) - XYZST(1,NBSOM)
            XYZTG(2,2,NBARLI) = PTSUTG(2) - XYZST(2,NBSOM)
            XYZTG(3,2,NBARLI) = PTSUTG(3) - XYZST(3,NBSOM)
C
         ENDIF
C
C        LA TG AU SOMMET INITIAL
         IF( IERR0 .NE. 0 ) THEN
C
C           LA TG EST LA CORDE CAR LES 2 PLANS TG SONT CONFONDUS
CCC            WRITE(IMPRIM,*) 'CONETG: PLANS TG IDENTIQUES'
            XYZTG(1,1,NBARLI) = XYZST(1,NBSOM) - XYZST(1,NBS1)
            XYZTG(2,1,NBARLI) = XYZST(2,NBSOM) - XYZST(2,NBS1)
            XYZTG(3,1,NBARLI) = XYZST(3,NBSOM) - XYZST(3,NBS1)
         ELSE
C
C           PROJECTION DU SOMMET FINAL SUR LA TG0 => LA NORME DE LA TG0
            PTSUTG(1) = REAL( XYZST(1,NBS1) + TG0(1) * ARC )
            PTSUTG(2) = REAL( XYZST(2,NBS1) + TG0(2) * ARC )
            PTSUTG(3) = REAL( XYZST(3,NBS1) + TG0(3) * ARC )
            CALL PTPRDR( XYZST(1,NBSOM),XYZST(1,NBS1),PTSUTG, PTSUTG)
            XYZTG(1,1,NBARLI) = PTSUTG(1) - XYZST(1,NBS1)
            XYZTG(2,1,NBARLI) = PTSUTG(2) - XYZST(2,NBS1)
            XYZTG(3,1,NBARLI) = PTSUTG(3) - XYZST(3,NBS1)
C
         ENDIF
C
C        CONSTRUCTION DE L'ARETE P3 DANS LE TMS NSEF
C        -------------------------------------------
C        LE NUMERO DES 2 SOMMETS DE L'ARETE NBARLI
         NUSOAR(1,NBARLI) = NBS1
         NUSOAR(2,NBARLI) = NBSOM
C
C        LE POINTEUR SUR L'EF A TG
         LPEFTG( NBARLI ) = NBARLI
C
C        LE NUMERO GEOMETRIQUE DE CETTE ARETE => 13 CYLINDRE ou 14 CONE
         NGEFTG( NBARLI ) = NGE
C
C        LE NUMERO DES 2 SOMMETS DE L'ARETE NBARLI
         NUTGAR(1,NBARLI) = NBTGS-1
         NUTGAR(2,NBARLI) = NBTGS
C
C        PASSAGE A L'ARETE SUIVANTE
         IF( N2 .NE. NF ) THEN
C           CE N'EST PAS LA DERNIERE ARETE DE CET INTERVALLE
            IERR0  = IERR
            TG0(1) = -TG(1)
            TG0(2) = -TG(2)
            TG0(3) = -TG(3)
            N1 = N2
            GOTO 5
         ENDIF
C
C        DERNIERE ARETE DE CETTE SUITE D'ARETES DE LA LIGNE
C        --------------------------------------------------
C        EST IL POSSIBLE D'IDENTIFIER LES 2 EXTREMITES?
         CALL XYZIDE( XYZST(1,NBS0), XYZST(1,NBSOM), N1 )
         IF( N1 .NE. 0 ) THEN
C
C           LES 2 POINTS SONT IDENTIQUES => COURBE FERMEE
C           LE DERNIER SOMMET EST DONC LE PREMIER NBS0
            NBSOM = NBSOM - 1
            NUSOAR(2,NBARLI) = NBS0
C
         ENDIF
C
 100  CONTINUE
C
      IERR = 0
      RETURN
      END
