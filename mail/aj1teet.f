      SUBROUTINE AJ1TEET( XYZSOM, NBSOTE, N1TEVI, NSTETR,
     %                    NBDM,   NUDMEF, NUMATE,
     %                    NS4,    NF0,    N1FEOC, N1FEVI, NFETOI,
     %                    VOLUMT, QUALIT,
     %                    MXTETC, NBTETC, NOTETC, VOLET1, QUAET1, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:     AJOUT DU TETRAEDRE FORME DE LA FACE NF0 DE NFETOI ET
C ----     DU SOMMET NS4  et  MISE A JOUR DE L'ETOILE NFETOI

C ENTREES:
C --------
C XYZSOM : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NSTETR
C NSTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C NBDM   : =0 SI 1 MATERIAU=VOLUME, SINON =NOMBRE DE MATERIAUX DU VOLUME
C NUDMEF : NUMERO DE MATERIAU DE CHAQUE TETRAEDRE DU MAILLAGE
C          ATTENTION: CE TABLEAU EXISTE SEULEMENT SI NBDM>0
C NUMATE : NUMERO DE MATERIAU DE L'ETOILE SI NBDM>0

C NS4    : NUMERO XYZSOM DU 4-EME SOMMET DU TETRAEDRE A CREER
C NF0    : FACE DANS NFETOI DES 3 PREMIERS SOMMETS DU TETRAEDRE A CREER
C N1FEOC : POINTEUR SUR LA PREMIERE FACE OCCUPEE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : LES FACES TRIANGULAIRES DE L'ETOILE
C          1: NUMERO XYZSOM DU SOMMET 1 DE LA FACE
C          2: NUMERO XYZSOM DU SOMMET 2 DE LA FACE
C          3: NUMERO XYZSOM DU SOMMET 3 DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR
C             DE L'ETOILE
C          4: NUMERO DU TETRAEDRE DE CETTE FACE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C MXTETC : NOMBRE MAXIMAL DE TETRAEDRES CREABLES DANS NOTETC

C MODIFIES:
C ---------
C NBTETC : NOMBRE DE TETRAEDRES CREES AVANT ET APRES
C NOTETC : NUMERO NSTETR DES NBTETC TETRAEDRES CREES
C VOLET1 : VOLUME DES NBTETC TETRAEDRES CREES DANS L'ETOILE
C QUAET1 : MIN QUALITE DES NBTETC TETRAEDRES
C IERR   : 0 SI PAS D'ERREUR
C          2 SATURATION D'UN TABLEAU
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & St PIERRE du PERRAY  Fevrier 2016
C2345X7..............................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      REAL              XYZSOM(3,*), VOLUMT(*), QUALIT(*)
      INTEGER           NSTETR(NBSOTE,*), NOTETC(MXTETC), NFETOI(5,*),
     %                  NUDMEF( NBDM )

      REAL              ARMIN, ARMAX, SURFTR(4)

C     RECHERCHE D'UN TETRAEDRE VIDE DANS NSTETR
      IF( N1TEVI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'aj1teet: SATURATION NSTETR des TETRAEDRES'
         CALL LEREUR
         IERR = 2
         GOTO 9999
      ENDIF

C     LE PREMIER TETRAEDRE VIDE
      NT = N1TEVI
C     EST MIS A JOUR
      N1TEVI = NSTETR( 2, N1TEVI )

C     UN TETRAEDRE CREE DE PLUS DANS L'ETOILE
      IF( NBTETC .GE. MXTETC ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'aj1teet: SATURATION NOTETC des tetraedres CREES'
         CALL LEREUR
         IERR = 2
         GOTO 9999
      ENDIF
      NBTETC = NBTETC + 1
      NOTETC( NBTETC ) = NT

C     FORMATION DU TETRAEDRE NT DE SOMMET NS4 ET DE FACE N1FEOC DE NFETOI
      NFETOI(1,NF0) = ABS( NFETOI(1,NF0) )
      NSTETR(1,NT) = NFETOI(1,NF0)
      NSTETR(2,NT) = NFETOI(2,NF0)
      NSTETR(3,NT) = NFETOI(3,NF0)
      NSTETR(4,NT) = NS4

C     NUMERO DE MATERIAU DU TETRAEDRE NT
      IF( NBDM .GT. 0 ) NUDMEF( NT ) = NUMATE


cccC        AJOUT DE CE TETRAEDRE DANS LE CHAINAGE NOTESO DES TETRAEDRES
cccC        DES 4 SOMMETS DU TETRAEDRE NT
ccc         CALL CHTESO( NT, NBSOTE, NSTETR, NO1TSO, N1TESO, NOTESO,
ccc     %                IERR )
ccc         IF( IERR .NE. 0 ) GOTO 9999


C     QUALITE ET VOLUME DU TETRAEDRE NT
      CALL QUATET( XYZSOM(1,NSTETR(1,NT)),
     %             XYZSOM(1,NSTETR(2,NT)),
     %             XYZSOM(1,NSTETR(3,NT)),
     %             XYZSOM(1,NSTETR(4,NT)),
     %             ARMIN, ARMAX, SURFTR, VOLUMT(NT), QUALIT(NT) )

C     VOLUME DE L'ETOILE
      VOLET1 = VOLET1 + ABS( VOLUMT(NT) )

C     QUALITE MINIMALE DES TETRAEDRES DE L'ETOILE
      QUAET1 = MIN( QUAET1, QUALIT(NT) )

C     AJOUT OU RETRAIT DES 4 FACES DU TETRAEDRE NT SUPPRIME DE L'ETOILE
      DO I=1,4
C        SI    ( LA FACE I DU TETRAEDRE NT N'APPARTIENT PAS
C                AUX FACES DE L'ETOILE NFETOI )
C        ALORS ELLE EST AJOUTEE    A L'ETOILE NFETOI VERSION 2
C        SINON ELLE EST SUPPRIMEE DE L'ETOILE NFETOI
         CALL AJFACE( 0, NT, I, NBSOTE, NSTETR,
     %                N1FEOC, N1FEVI, NFETOI,  NF )
         IF( NF .LT. 0 ) THEN
C           SATURATION DES FACES DE L'ETOILE
            GOTO 9999
         ENDIF
      ENDDO

 9999 RETURN
      END
