      SUBROUTINE TRFASE1( NOAXE,  VNORMAL, COPLAN,
     %                    NBSOM,  XYZSOM,
     %                    MNNSEF,
     %                    MXFASE, NBFASE,  NOSTFA, NOEFFA, XYZBAR )
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :  LISTER LES NO DES SOMMETS DES FACES DES EF AYANT UNE
C -----  INTERSECTION AVEC LE PLAN NOAXE DE COORDONNEE COPLAN
C
C ENTREES:
C --------
C NOAXE  : =1 AXE X
C          =2 AXE Y
C          =3 AXE Z
C          =4 AXE AVEC VNORMAL=NORMALE AU PLAN
C VNORMAL: VECTEUR NORMAL AU PLAN POUR NOAXE=4
C COPLAN : COORDONNEE DU PLAN DE SECTION DANS LA DIRECTION NORMALE DU PLAN
C NBSOM  : NOMBRE DE SOMMETS DU MAILLAGE
C XYZSOM : 3 COORDONNEES DES NBSOM SOMMETS DU MAILLAGE
C MNNSEF : ADRESSE MCN DU TMS nsef DE CE VOLUME
C MXFASE : MAXIMUM DE FACES DES EF SECTIONNES PAR LE PLAN
C
C SORTIES:
C --------
C NBFASE : NOMBRE DE FACES DES EF SECTIONNES
C NOSTFA : NO DES SOMMETS DANS XYZSOM  DES FACES DES EF SECTIONNES
C NOEFFA : NO DES EF 3D DES FACES DES EF SECTIONNES
C XYZBAR : 3 COORDONNEES DU BARYCENTRE DES FACES DES EF SECTIONNES
C ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Laboratoire JL LIONS UPMC Paris Decembre 2006
C....................................................................012
      include"./incl/pp.inc"
      COMMON   MCN(MOTMCN)
C
      REAL     VNORMAL(3), XYZSOM(3,NBSOM)
      INTEGER  NOSTFA(4,MXFASE), NOEFFA(MXFASE)
      REAL     XYZBAR(3,MXFASE)
C
      INTEGER  NBSOFA(6,5:9), NOSOFA(4,6,5:9), NOSOEF(64)
C
      NBFASE = 0
C
C     LE NO DES SOMMETS DES FACES D'UN TETRAEDRE, PENTAEDRE, HEXAEDRE
      DO 10 NCOGEL = 5, 9
C        NCOGEL : CODE GEOMETRIQUE DE L ELEMENT
C                 1:POINT 2:SEGMENT 3:TRIANGLE 4:QUADRANGLE
C                 5:TETRAEDRE 6:PENTAEDRE 7:HEXAEDRE >7:ERREUR
C        NBSOFA : NOMBRE DE SOMMETS DES FACES DE L'EF DE TYPE NCOGEL
C        NOSOFA : NOSOFA(I,J) NO DU I-EME SOMMET DE LA FACE J DU "CUBE"
C        NOSOFA : NOSOFA(I,J) NO DU I-EME SOMMET DE LA FACE J
         CALL SOFACU( NCOGEL, NBSOFA(1,NCOGEL), NOSOFA(1,1,NCOGEL) )
 10   CONTINUE
C
C     LES CARACTERISTIQUES DES EF DE CE MAILLAGE INITIAL
      CALL NSEFPA( MCN(MNNSEF),
     %             NUTYMA, NBSOEL, NBSOEF, NBTGEF,
     %             LDAPEF, LDNGEF, LDTGEF, NBEFOB,
     %             NX    , NY    , NZ    ,
     %             IERR   )
C     NUTYMA : 'NUMERO DE TYPE DU MAILLAGE'    ENTIER
C              0 : 'NON STRUCTURE'      , 2 : 'SEGMENT    STRUCTURE',
C              3 : 'TRIANGLE  STRUCTURE', 4 : 'QUADRANGLE STRUCTURE',
C              5 : 'TETRAEDRE STRUCTURE', 6 : 'PENTAEDRE  STRUCTURE',
C              7 : 'HEXAEDRE  STRUCTURE'
C     NBSOEL : NOMBRE DE SOMMETS DES EF
C              0 SI MAILLAGE NON STRUCTURE
C     NBSOEF : NOMBRE DE SOMMETS DE STOCKAGE DES EF
C              ( TETRAEDRE NBSOEL=4  NBSOEF=8 )
C     NBEFOB : NOMBRE DE EF DU MAILLAGE
C     NX, NY, NZ : LE NOMBRE D'ARETES DANS LES DIRECTION X Y Z
C                CF LE TMS ~td/d/a___nsef
C     PARCOURS DES FACES DES EF VOLUMIQUES DU VOLUME
C
      DO 100 NEF=1, NBEFOB
C
         CALL NSEFNS( NEF, NUTYMA, NBSOEF, NBTGEF,
     %                LDAPEF, LDNGEF, LDTGEF,
     %                MNNSEF, NX, NY, NZ,
     %                NCOGEL, NUGEEF, NUEFTG, NOSOEF, IERR )
C
C        LE NOMBRE DE SOMMETS DE L'EF
         NBS = NBSOME( NCOGEL )
C
C        NOMBRE DE FACES DE L'EF 3D
         NBFAC = NBFACE( NCOGEL )
C
C        RECHERCHE DE LA COORDONNEE NOAXE MIN MAX DES SOMMETS DE L'EF
C        POUR SAVOIR SI LA COORDONNEE DU PLAN EST ENTRE CES VALEURS
         IF( NOAXE .LE. 3 ) THEN
C
C           PLAN A COORDONNEE NOAXE=Constante
            CMIN = XYZSOM( NOAXE, NOSOEF(1) )
            CMAX = CMIN
            DO 15 NS = 2,NBS
C              LE NO DU SOMMET NS DE L'EF EST NOSOEF(NS)
               C = XYZSOM( NOAXE, NOSOEF(NS) )
               IF( C .LT. CMIN ) CMIN = C
               IF( C .GT. CMAX ) CMAX = C
 15         CONTINUE
C
         ELSE
C
C           PLAN DEFINI PAR LE VECTEUR NORMAL
C           COORDONNEE SELON LA DIRECTION NORMALE PAR PRODUIT SCALAIRE
            CMIN = PROSCR( XYZSOM( 1, NOSOEF(1) ), VNORMAL, 3 )
            CMAX = CMIN
            DO 30 NF = 1, NBFAC
               DO 25 NS = 1,NBSOFA(NF,NCOGEL)
C                 LE NO DU SOMMET NS DE LA FACE NF DANS L'EF NEF
                  NOST = NOSOEF( NOSOFA( NS, NF, NCOGEL ) )
                  C = PROSCR( XYZSOM( 1, NOST ), VNORMAL, 3 )
                  IF( C .LT. CMIN ) CMIN = C
                  IF( C .GT. CMAX ) CMAX = C
 25            CONTINUE
 30         CONTINUE
C
         ENDIF
         IF( COPLAN .LT. CMIN .OR. COPLAN .GT. CMAX ) GOTO 100
C
C        L'EF NEF A UNE INTERSECTION AVEC LE PLAN NOAXE
C        LISTAGE DES NO DES SOMMETS DE SES FACES POUR LE TRACE
         DO 50 NF = 1, NBFAC
            IF( NBFASE .GE. MXFASE ) GOTO 9000
C           UNE FACE A TRACER DE PLUS
            NBFASE = NBFASE + 1
C           LE NO DE L'EF 3D DE LA FACE
            NOEFFA(NBFASE) = NEF
C           LES 3 COORDONNEES DU BARYCENTRE DE LA FACE
            XYZBAR(1,NBFASE) = 0
            XYZBAR(2,NBFASE) = 0
            XYZBAR(3,NBFASE) = 0
            NBSTFA = NBSOFA(NF,NCOGEL)
            DO 40 NS = 1, NBSTFA
C              LE NO DU SOMMET NS DE LA FACE NF DANS L'EF NEF
               NOST = NOSOEF( NOSOFA( NS, NF, NCOGEL ) )
               NOSTFA(NS, NBFASE) = NOST
               XYZBAR(1,NBFASE) = XYZBAR(1,NBFASE) + XYZSOM(1,NOST)
               XYZBAR(2,NBFASE) = XYZBAR(2,NBFASE) + XYZSOM(2,NOST)
               XYZBAR(3,NBFASE) = XYZBAR(3,NBFASE) + XYZSOM(3,NOST)
 40         CONTINUE
            IF( NBSTFA .EQ. 3 ) NOSTFA(4,NBFASE)=0
C           LES 3 COORDONNEES DU BARYCENTRE DE LA FACE
            XYZBAR(1,NBFASE) = XYZBAR(1,NBFASE) / NBSTFA
            XYZBAR(2,NBFASE) = XYZBAR(2,NBFASE) / NBSTFA
            XYZBAR(3,NBFASE) = XYZBAR(3,NBFASE) / NBSTFA
 50      CONTINUE
 100  CONTINUE
C
 9000 RETURN
      END
