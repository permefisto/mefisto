      SUBROUTINE TEINAR( NS1, NS2, PTXYZD, N1TETS, NOTETR,
     %                   MXTEIN, NBTEIN, NOTEIN, NBPTIN, PTIN )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :   RETROUVER LE NUMERO DES TETRAEDRES DONT UNE OU DEUX FACES
C -----   SONT INTERSECTEES PAR L'ARETE NS1 NS2 QUI N'EST PAS UNE ARETE
C         DE LA TETRAEDRISATION
C
C ENTREES:
C --------
C NS1 NS2: NUMERO PTXYZD DES 2 SOMMETS DE L'ARETE
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C N1TETS : N1TETS(I) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C MXTEIN : NOMBRE MAXIMAL DE TETRAEDRES DE NO STOCKABLE DANS NOTEIN
C
C SORTIES:
C --------
C NBTEIN : NOMBRE DE TETRAEDRES DE FACES INTERSECTEES PAR ARETE NS1 NS2
C          -1 SI PAS DE TETRAEDRE DE SOMMET NS1
C NOTEIN : NO DES NBTEIN TETRAEDRES DE FACES INTERSECTEES PAR ARETE NS1 NS2
C NBPTIN : NOMBRE DE POINTS D'INTERSECTION (=NBTEIN-1)
C PTIN   : XYZ DES NBTEIN POINTS D'INTERSECTION
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & VEULETTES SUR MER  SEPTEMBRE 2014
C....................................................................012
      DOUBLE PRECISION  PTXYZD(1:4,1:*), PTIN(3,1000)
      INTEGER           N1TETS(*), NOTETR(8,*), NOTEIN(MXTEIN)

      DOUBLE PRECISION  CBTR(3)
      INTEGER           NOSOFA(3,4), NOSTAR(2,1)
      DATA              NOSOFA/ 1,3,2,  2,3,4, 3,1,4, 4,1,2 /

C     RECHERCHE DES NBTEIN TETRAEDRES DE SOMMET NS1
      CALL TETR1S( NS1, N1TETS, NOTETR, NBTEIN, MXTEIN, NOTEIN, IERR )
C     NBTEIN : NOMBRE DE TETRAEDRES RANGES DANS NOTEIN
      IF( IERR .NE. 0 ) RETURN

C     TABLEAU NECESSAIRE POUR LES TRACES
      NOSTAR(1,1) = NS1
      NOSTAR(2,1) = NS2

      NBPTIN = 0
      DO 20 N = 1, NBTEIN

         NT1 = NOTEIN( N )
         IF( NOTETR(1,NT1) .LE. 0 ) THEN
            NBTEIN = -2
            RETURN
         ENDIF

C        RECHERCHE DE LA FACE NF DE NT1 SANS LE SOMMET NS1
         DO NS = 1, 4
            IF( NS1 .EQ. NOTETR( NS, NT1 ) ) GOTO 10
         ENDDO
C        LA FACE NF NE CONTIENT PAS NS1
 10      IF( NS .EQ. 4 ) THEN
            NF = 1
         ELSE
            NF = NS + 1
         ENDIF

C        CETTE FACE NF EST ELLE INTERSECTEE PAR L'ARETE NS1-NS2?
         CALL INARTR( PTXYZD(1,NS1), PTXYZD(1,NS2),
     %                PTXYZD( 1, NOTETR( NOSOFA(1,NF), NT1 ) ),
     %                PTXYZD( 1, NOTETR( NOSOFA(2,NF), NT1 ) ),
     %                PTXYZD( 1, NOTETR( NOSOFA(3,NF), NT1 ) ),
     %                LINTER, PTIN(1,1), CBTR )
C        LINTER : -2 SI S1=S2
C              -1 SI S1-S2 PARALLELE AU PLAN DU TRIANGLE
C               0 SI S1-S2 N'INTERSECTE PAS LE TRIANGLE ENTRE CES 3 SOMMETS
C               1 SI S1-S2   INTERSECTE     LE TRIANGLE ENTRE CES 3 SOMMETS
C        PTIN : LES 3 COORDONNEES DU POINT D'INTERSECTION SI LINTER=0
C        CBTR : LES 3 COORDONNEES BARYCENTRIQUES DE PT DANS LE TRIANGLE P1P2P3

         IF( LINTER .EQ. 1 ) THEN

C           OUI: LE TETRAEDRE DE DEPART EST STOCKE
            NBTEIN = 1
            NOTEIN( 1 ) = NT1
            print *,' tetra intersecte',NT1,' St:',(NOTETR(K,NT1),K=1,4)

C           PASSAGE AU TETRAEDRE OPPOSE PAR LA FACE INTERSECTEE
            NT2 = NOTETR( 4+NF, NT1 )
            GOTO 30

         ENDIF

 20   CONTINUE

      PRINT *,'PB TEINAR: ARETE',NS1,'-',NS2,' INTERSECTE AUCUNE FACE DE
     %S TETRAEDRES DE SOMMET',NS1
      NBTEIN = 0
      RETURN

C     LA FACE NF DU TETRAEDRE NT1 EST INTERSECTEE PAR NS1-NS2
C     LE TETRAEDRE NT2 EST IL DE SOMMET NS2?
 30   IF( NT2 .LE. 0 ) GOTO 40

      DO N = 1, 4
         IF( NOTETR(N,NT2) .EQ. NS2 ) THEN
C           AJOUT DU DERNIER TETRAEDRE INTERSECTE ET DE SOMMET NS2
            IF( NBTEIN .GE. MXTEIN ) THEN
               PRINT *,'PB TEINAR: ARETE',NS1,'-',NS2,
     %                 ' TABLEAU NOTEIN(',MXTEIN,') SATURE'
               NBTEIN = -4
               RETURN
            ENDIF
            NBTEIN = NBTEIN + 1
            NOTEIN( NBTEIN ) = NT2
            print *,' tetra intersecte',NT2,' St:',(NOTETR(K,NT2),K=1,4)
            GOTO 50
         ENDIF
      ENDDO

      DO NF2 = 1, 4
         IF( NOTETR(4+NF2,NT2) .EQ. NT1 ) GOTO 35
      ENDDO

C     NF2 EST DANS NT2 LA 1-ERE FACE INTERSECTEE PAR NS1-NS2
C     RECHERCHE DE LA SECONDE FACE INTERSECTEE
 35   DO NF = 1, 4

         IF( NF .NE. NF2 ) THEN
C           CETTE FACE NF EST ELLE INTERSECTEE PAR L'ARETE NS1-NS2?
            CALL INARTR( PTXYZD(1,NS1), PTXYZD(1,NS2),
     %                   PTXYZD( 1, NOTETR( NOSOFA(1,NF), NT2 ) ),
     %                   PTXYZD( 1, NOTETR( NOSOFA(2,NF), NT2 ) ),
     %                   PTXYZD( 1, NOTETR( NOSOFA(3,NF), NT2 ) ),
     %                   LINTER, PTIN(1,NBTEIN+1), CBTR )
            IF( LINTER .EQ. 1 ) THEN

C              OUI: LE TETRAEDRE DE DEPART EST STOCKE
               IF( NBTEIN .GE. MXTEIN ) THEN
                  PRINT *,' TEINAR: ARETE',NS1,'-',NS2,
     %            ' TABLEAU NOTEIN(',MXTEIN,') SATURE'
                  NBTEIN = -4
                  RETURN
               ENDIF
               NBTEIN = NBTEIN + 1
               NOTEIN( NBTEIN ) = NT2
            print *,'+tetra intersecte',NT2,' St:',(NOTETR(K,NT2),K=1,4)

C              PASSAGE AU TETRAEDRE OPPOSE PAR LA FACE INTERSECTEE
               NT1 = NT2
               NT2 = NOTETR( 4+NF, NT2 )
               GOTO 30

            ENDIF

         ENDIF
      ENDDO

 40   PRINT *,'PB TEINAR: ARETE',NS1,'-',NS2,
     % ' SANS INTERSECTION AVEC UNE AUTRE FACE DU TETRAEDRE',NT1
      NBTEIN = -NBTEIN

C     NOMBRE DE POINTS D'INTERSECTION
 50   NBPTIN = ABS(NBTEIN) -1

C     TRACE DES TETRAEDRES INTERSECTES PAR L'ARETE NS1-NS2
      CALL TRATEAR( PTXYZD, 1, NOSTAR, NBPTIN, PTIN,
     %               ABS(NBTEIN), NOTEIN, NOTETR )

      RETURN
      END
