      SUBROUTINE RSTETRETO( PTXYZD, 
     %                      MXTETR,   N1TEVI,   NOTETR, NUDTETR, N1TETS,
     %                      INFACO,   MXFACO,   LEFACO,
     %                      NBTEINIT, NOTEINIT, NBTECR, NOTECR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:   ABANDON de la TETRAEDRISATION DANS TETRETO =>
C ----   RESTAURATION DES NBTEINIT TETRAEDRES SAUVEGARDES
C        DESTRUCTION  DES NBTECR   TETRAEDRES NOTECR CREES DANS TETRETO

C ENTREES:
C --------
C PTXYZD  : TABLEAU DES COORDONNEES DES POINTS
C           PAR POINT : X  Y  Z DISTANCE_SOUHAITEE

C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: NU NOTETR DU DERNIER TETRAEDRE ACTIF
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS

C INFACO : = 0 PAS DE TABLEAU LEFACO NI DE CONSERVATION DES
C              FACES FRONTIERE ( NO DE VOLUME CONNU PAR NVOLTE )
C          = 1 EXISTENCE DU TABLEAU LEFACO ET CONSERVATION DES
C              FACES DE LA FRONTIERE
C MXFACO : MAX DE FACES DECLARABLES DANS LEFACO
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          1:   =0 POUR UNE FACE VIDE
C          123: NO (DANS PTXYZD) DU SOMMET 1 < SOMMET 2 < SOMMET 3
C          45:  NO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          678: NO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3

C          9: ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C             => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C             LEFACO(9,*) -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)

C          10: HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C              LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C              NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C              SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C              NF = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON C-A-D AUCUN TETRAEDRE CONTIENT CETTE FACE
C              <0  SINON C-A-D AUCUN TETRAEDRE CONTIENT CETTE FACE
C                  MAIS EN PLUS ELLE EST MARQUEE ABANDONNEE MOMENTANEMENT
cccC          12: = NO FACEOC DE 1 A NBFACES D'OpenCascade

C NBTEINIT: NOMBRE DE FACES DE L'ETOILE EN SAUVEGARDE
C NOTEINIT: NUMERO NOTETR DES NBTEINIT TETRAEDRES DE SAUVEGARDE
C NBTECR  : NOMBRE DE FACES CREEES DANS L'ETOILE PAR TETRETO
C NOTECR  : NUMERO NOTETR DES NBTECR TETRAEDRES CREES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET Saint PIERRE du PERRAY               Mai 2020
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,*)
      INTEGER           NOTETR(8,MXTETR),N1TETS(*), LEFACO(11,0:MXFACO),
     %                  NOTECR(NBTECR), NOTEINIT(NBTEINIT)

C     RESTAURATION DES NBTEINIT TETRADRES NOTEINIT
C     MISE A JOUR  DE N1TEVI, NOTETR, N1TETS, TETRAEDRES OPPOSES
      PRINT*
      PRINT*,'rstetreto: RESTAURATION des',NBTEINIT,
     %          ' TETRAEDRES INITIAUX et SUPPRESSION des',NBTECR,
     %          ' TETRAEDRES CREES par call tetreto'
      CALL RSTETO( PTXYZD, NBTEINIT, NOTEINIT, NBTECR,  NOTECR,
     %             MXTETR, N1TEVI,   NOTETR,   NUDTETR, N1TETS,
     %             NBFANR )

      IF( NBFANR .GT. 0 ) THEN
         PRINT*, 'rstetreto: PB RESTAURATION des',NBTEINIT,
     %           'TETRAEDRES INITIAUX => NBFANR=',NBFANR,' Pourquoi?...'
      ENDIF

C     MISE A JOUR DE LEFACO POUR LES NBTECR TETRAEDRES A DETRUIRE
      IF( INFACO .EQ. 0 ) GOTO 10
      DO K=1,NBTECR
         NTE = NOTECR( K )
C        SUPPRIMER EVENTUELLEMENT LES 4 FACES DU TETRAEDRE NTE
C        DANS LE TABLEAU LEFACO
         CALL SUTELEFA( NTE, NOTETR, INFACO, MXFACO, LEFACO )
      ENDDO

C     MISE A JOUR DE LEFACO POUR LES NBTEINIT TETRAEDRES A RESTAURER
      DO K=1,NBTEINIT
         NTE = NOTEINIT( K )
C        AJOUTER EVENTUELLEMENT LES 4 FACES DU TETRAEDRE NTE
C        DANS LE TABLEAU LEFACO
         CALL AJTELEFA( NTE, NOTETR, INFACO, MXFACO, LEFACO )
      ENDDO

C     PLUS AUCUN TETRAEDRE CREE APRES leur DESTRUCTION
 10   NBTECR = 0

C     RESTAURATION DES NBTEINIT TETRAEDRES INITIAUX NOTEINIT DANS NOTECR
      CALL TRTATA( NOTEINIT, NOTECR, NBTEINIT )

C     VERIFIER SI TOUT TETRAEDRE VIDE A SON 1-ER NUMERO NUL
C     VERIFIER SI LE CHAINAGE des TETRAEDRES VIDES N'EST PAS INFINI et
C     SINON CORRIGER LE CHAINAGE DES TETRAEDRES VIDES
      CALL VETEVIDE( N1TEVI, MXTETR, NOTETR, IERR )

      RETURN
      END
