      SUBROUTINE NUFANFET( NFE, NTE, N1FEOC, NFETOI, NF1 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LA FACE NFE DU TETRAEDRE NTE EST ELLE UNE FACE SIMPLE
C -----    DE L'ETOILE NFETOI en VERSION 1?

C ENTREES:
C --------
C NFE    : NUMERO DE 1 A 4 DE LA FACE DANS LE TETRAEDRE NTE
C NTE    : NUMERO NOTETR DU TETRAEDRE
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : VERSION 1
C          1: NUMERO DU TETRAEDRE DANS NOTETR
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT EFFECTUE
C          3  NON UTILISE ICI
C          4: NON UTILISE ICI
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES

C SORTIE :
C --------
C NF1    : >0 NUMERO DANS NFETOI DE LA FACE NFE DU TETRAEDRE NTE
C          =0 SI NON RETROUVEE DANS NFETOI
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC & VEULETTES SUR MER    JANVIER 2017
C2345X7..............................................................012
      INTEGER  NFETOI(5,*)

      NF1 = N1FEOC

C     BOUCLE SUR LES FACES SIMPLES DE L'ETOILE NFETOI
 10   IF( NF1 .GT. 0 ) THEN

C        LE NO DU TETRAEDRE NT INTERNE A L'ETOILE
         NT = NFETOI( 1, NF1 )

C        I NO LOCAL DE LA FACE DANS NT
         I  = NFETOI( 2, NF1 )

         IF( NT .EQ. NTE .AND. I .EQ. NFE ) RETURN

C        PASSAGE A LA FACE SIMPLE SUIVANTE
         NF1 = NFETOI( 5, NF1 )
         GOTO 10

      ENDIF

C     LA FACE NFE DU TETRAEDRE NTE N'EST PAS UNE FACE SIMPLE DE NFETOI
      NF1 = 0

      RETURN
      END
