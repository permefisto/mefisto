      SUBROUTINE AJMIARMX( NBSOMM, MXSOMM, PTXYZD, NPSOFR,
     %                     MXFACO, LEFACO, N1FASC,
     %                     NBFAPE, NOFAPE, MXTRCF, NBTRCF, NOTRCF,
     %                     MXTE1S, MNTE1S, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    AJOUT du POINT MILIEU DE LA PLUS GRANDE ARETE DES
C -----    NBTRCF TRIANGLES LEFACO PERDUS DU CF

C ENTREES:
C --------
C NBSOMM : NUMERO DU DERNIER SOMMET AJOUTE A LA TETRAEDRISATION
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TETRAEDRISATION
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NPSOFR : =  0  SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C              LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -4 SI LE POINT EST SOMMET D'OT NON TROP PROCHE PT OU FACE
C          = -1 SI LE POINT EST SOMMET D'OT TROP PROCHE PT OU FACE
C          = -3 SI LE POINT EST SOMMET D'OT REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE

C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO
C LEFACO : LES 3 SOMMETS, 2 MATERIAUX, 3 FACES VOISINES ET CHAINAGE
C          DES FACES TRIANGULAIRES DU CONTOUR ET INTERFACES
C N1FASC : N1FASC(NS)=NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS
C MXTRCF : NOMBRE MAXIMAL D'ENTIERS DU TABLEAU NOTRCF
C NBTRCF : NOMBRE DE TRIANGLES DU TABLEAU NOTRCF SUR LEFACO
C          C-A-D DU POLYGONE ENCORE DIT ENSUITE ETOILE
C NOTRCF : TABLEAU DU NUMERO DANS LEFACO DES TRIANGLES DE L'ETOILE
C NBFAPE : NOMBRE DE FACES PERDUES DE LEFACO DU CONTOUR AVANT ET APRES
C          NON PRESENTES DANS LA TETRAEDRISATION
C NOFAPE : NUMERO DANS LEFACO DE LA FACE PERDUE
C MXTE1S : NOMBRE D'ENTIERS DU TABLEAU MCN(MNTEIS)  MNTE1S,

C SORTIES:
C --------
C NBSOMM : NUMERO DU DERNIER MILIEU AJOUTE AU MILIEU D'UNE ARETE PERDUE
C          SI NBSOMM=NBSOM0 PAS DE POINT MILIEU AJOUTE A TETRAEDRISER
C IERR   : =0 SI PAS D'ERREUR
C          >0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Saint Pierre du Perray          Janvier 2020
C....................................................................012
      DOUBLE PRECISION  PTXYZD(1:4,1:MXSOMM)
      INTEGER           NPSOFR(1:MXSOMM),
     %                  LEFACO(11,0:MXFACO),N1FASC(1:MXSOMM),
     %                  NOFAPE(1:NBFAPE), NOTRCF(MXTRCF)
      DOUBLE PRECISION  D, DIS2ST, DARMAX

      PRINT*
      PRINT*,'ajmiarmx: AVANT AJOUT du MILIEU d''ARETES de FACES PERDUES
     % NBSOMM=',NBSOMM,' NBTRCF=',NBTRCF

C     AJOUT DU POINT MILIEU DE LA PLUS GRANDE DES ARETES DES TRIANGLES DU CF
C     ----------------------------------------------------------------------
      IERR   = 0
      NBSOM0 = NBSOMM
      DARMAX = -1D0
      DO N = 1, NBTRCF

C        LE TRIANGLE N DU CF
         NFLP = NOTRCF( N )

         DO NA=1,3

            IF( NA .EQ. 3 ) THEN
               NA1 = 1
            ELSE
               NA1 = NA+1
            ENDIF
C           LE NO DES 2 SOMMETS DE L'ARETE NA DU TRIANGLE NFLP
            NS1 = LEFACO( NA , NFLP )
            NS2 = LEFACO( NA1, NFLP )
            D   = DIS2ST( PTXYZD( 1, NS1 ), PTXYZD( 1, NS2 ) )

            IF( D .GT. DARMAX ) THEN
               DARMAX = D
               NFLMAX = NFLP
               NARMAX = NA
            ENDIF

         ENDDO

      ENDDO

C     AJOUT DU MILIEU DE L'ARETE NARMAX DE LA FACE LEFACO PERDUE NFLMAX
      CALL VD1F2F(      0, NARMAX, NFLMAX, MXFACO, LEFACO, NBFACO,
     %             N1FASC, NBFAPE, NOFAPE,
     %             NBSOMM, MXSOMM, PTXYZD, NPSOFR,
     %             MXTE1S, MNTE1S, IERR )
      IF( IERR .NE. 0 ) GOTO 9000

cccC     TRACE DES FACES PERDUES
ccc      CALL TRFAPE( NBFAPE, NOFAPE, MXFACO, LEFACO, PTXYZD )

 9000 PRINT*,'ajmiarmx:',NBSOMM-NBSOM0,
     %       ' MILIEU AJOUTE a la PLUS GRANDE ARETE des',NBTRCF,
     %       ' FACES PERDUES du CF'
      PRINT*

      RETURN
      END
