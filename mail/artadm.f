      SUBROUTINE ARTADM( EPSANG, NS1, NS2, N1ARCF, NOARCF, PTXYZD,
     %                   NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    SAVOIR SI L'ARETE DE SOMMETS NS1 NS2 DANS PTXYZD
C -----    EST INTERNE AU CF
C        ( NE COUPE AUCUN DES SEGMENTS DU CF NI N'EST A L'EXTERIEUR)
C
C ENTREES:
C --------
C EPSANG : SEUIL D'ADMISSIBILITE DES ANGLES EN RADIANS ENTRE UNE
C          ARETE DU CF ET LE SEGMENT NS1,NS2
C NS1    : LE NUMERO PTXYZD DU PREMIER SOMMET DE L'ARETE
C NS2    : LE NUMERO PTXYZD DU SECOND SOMMET DE L'ARETE
C N1ARCF : NUMERO DANS NOARCF DU PREMIER SOMMET DU CF
C NOARCF : NUMERO DU SOMMET , NUMERO DE L'ARETE SUIVANTE
C          NUMERO DU TRIANGLE EXTERIEUR A L'ETOILE
C PTXYZD : TABLEAU DES COORDONNEES DES SOMMETS ET DISTANCE_SOUHAITEE
C
C SORTIE :
C --------
C NONOUI : 1 SI L'ARETE EST TOTALEMENT INTERNE AU CF
C         -1 SI L'ARETE EST UNE ARETE DU CF
C          0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1993
C2345X7..............................................................012
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      DOUBLE PRECISION  PTXYZD(1:4,1:*)
      INTEGER           NOARCF(1:3,1:*)
      DOUBLE PRECISION  ANGLED,A1,A2,D,PI,COS3PD,
     %                  XY2D(3,0:2),
     %                  XY2DAR(3,2)
      DOUBLE PRECISION  D2D3(3,3)
      LOGICAL           OUI
C
C     CAS DE 2 POINTS IDENTIQUES
      IF( NS1 .EQ. NS2 ) GOTO 800
CCCC
CCCC     TRACE DE L'ARETE NS1-NS2 EN BLEU
CCC      CALL XVCOULEUR( NCBLEU )
CCC      X1 = PTXYZD(1,NS1)
CCC      Y1 = PTXYZD(2,NS1)
CCC      Z1 = PTXYZD(3,NS1)
CCC      CALL MOVE( X1, Y1, Z1 )
CCC      X2 = PTXYZD(1,NS2)
CCC      Y2 = PTXYZD(2,NS2)
CCC      Z2 = PTXYZD(3,NS2)
CCC      CALL DRAW( X2, Y2, Z2 )
CCC      CALL ECRENT( NS2 )
CCC      CALL MOVE( X1, Y1, Z1 )
CCC      CALL ECRENT( NS1 )
C
C     RECHERCHE DE 3 POINTS DU CF NON ALIGNES ET D'ANGLE PROCHE DE PI/2
      NA3 = 0
      D   = 2
      NA0 = N1ARCF
      NP0 = NOARCF( 1, NA0 )
      NA1 = NOARCF( 2, NA0 )
      NP1 = NOARCF( 1, NA1 )
      NA2 = NOARCF( 2, NA1 )
 5    NP2 = NOARCF( 1, NA2 )
      A1  = COS3PD( PTXYZD(1,NP0), PTXYZD(1,NP1), PTXYZD(1,NP2) )
      A1  = ABS( A1 )
      IF( A1 .LT. D ) THEN
         NA3 = NA2
         D   = A1
      ENDIF
      NA2 = NOARCF( 2, NA2 )
      IF( NA2 .NE. NA0 ) GOTO 5
C
C     CHANGEMENT DE REPERE
      NP2 = NOARCF( 1, NA3 )
      CALL DI3D2D(PTXYZD(1,NP0), PTXYZD(1,NP1), PTXYZD(1,NP2), D2D3, I)
      IF( I .GT. 0 ) THEN
C        LE CF EST UNE DROITE
         GOTO 850
      ENDIF
C
C     RECHERCHE DU SENS DE PARCOURS DU CF
C     SENS   DIRECT SI SOMME DES ANGLES = S * PI - 2 PI
C     SENS INDIRECT SI SOMME DES ANGLES = S * PI + 2 PI
      D = 0D0
      I = 0
      NA1 = N1ARCF
      NP1 = NOARCF( 1, NA1 )
      NA2 = NOARCF( 2, NA1 )
      NP2 = NOARCF( 1, NA2 )
      CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP1), XY2D(1,0) )
      CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP2), XY2D(1,1) )
      NA3 = NOARCF( 2, NA2 )
 10   NP3 = NOARCF( 1, NA3 )
      CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP3), XY2D(1,2) )
      D = D + ANGLED( XY2D(1,1), XY2D(1,2), XY2D(1,0) )
      I = I + 1
      IF( NA2 .NE. N1ARCF ) THEN
         NA2 = NA3
         XY2D(1,0) = XY2D(1,1)
         XY2D(2,0) = XY2D(2,1)
         XY2D(1,1) = XY2D(1,2)
         XY2D(2,1) = XY2D(2,2)
         NA3 = NOARCF( 2, NA3 )
         GOTO 10
      ENDIF
C
      PI = ATAN( 1D0 ) * 4D0
      IF( D .GT. PI * I ) THEN
C        SENS INDIRECT D = SOMME DES ANGLES = S * PI + 2 PI
         NSENS = -1
      ELSE
C        SENS   DIRECT D = SOMME DES ANGLES = S * PI - 2 PI
         NSENS = 1
      ENDIF
C
C     LES COORDONNEES DES POINTS NS1 NS2 DANS LE PLAN DU CF
      CALL CI3D2D( PTXYZD(1,NP0) , D2D3 , PTXYZD(1,NS1), XY2DAR(1,1) )
      CALL CI3D2D( PTXYZD(1,NP0) , D2D3 , PTXYZD(1,NS2), XY2DAR(1,2) )
C
C     INTERSECTION DE L'ARETE NS1-NS2 ET DES ARETES DU CF
      NA01 = N1ARCF
      NA1  = NOARCF( 2, NA01 )
      NA00 = NA1
      NA2  = NOARCF( 2, NA1  )
C
C     LE NUMERO DES 2 SOMMETS DE L'ARETE
 20   NP1  = NOARCF( 1, NA1 )
      NP2  = NOARCF( 1, NA2 )
      CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP1), XY2D(1,1) )
      CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP2), XY2D(1,2) )
CCCC
CCCC     TRACE DE L'ARETE
CCC      CALL XVCOULEUR( NCJAUN )
CCC      X1 = PTXYZD(1,NP1)
CCC      Y1 = PTXYZD(2,NP1)
CCC      Z1 = PTXYZD(3,NP1)
CCC      CALL MOVE( X1, Y1, Z1 )
CCC      X2 = PTXYZD(1,NP2)
CCC      Y2 = PTXYZD(2,NP2)
CCC      Z2 = PTXYZD(3,NP2)
CCC      CALL DRAW( X2, Y2, Z2 )
CCC      CALL ECRENT( NP2 )
CCC      CALL MOVE( X1, Y1, Z1 )
CCC      CALL ECRENT( NP1 )
C
      IF( NP1 .EQ. NS1 .OR. NP1. EQ. NS2 .OR.
     %    NP2 .EQ. NS1 .OR. NP2. EQ. NS2 ) GOTO 50
C
C     NS1 NS2 NE SONT PAS SUR LE CF  INTERSECTION DES 2 ARETES?
      CALL INT2AR( XY2DAR(1,1), XY2DAR(1,2),
     %             XY2D(1,1),   XY2D(1,2),   OUI )
      IF( OUI ) GOTO 850
      GOTO 70
C
C     LES 2 ARETES PARTAGENT AU MOINS UNE EXTREMITE
 50   IF( (NP1 .EQ. NS1 .AND. NP2. EQ. NS2) .OR.
     %    (NP1 .EQ. NS2 .AND. NP2. EQ. NS1) ) THEN
C        L'ARETE NS1-NS2 EST UNE ARETE DU CF
         NONOUI = -1
         GOTO 820
      ENDIF
C
C     LES 2 ARETES NE SONT PAS CONFONDUES. UNE SEULE EXTREMITE COMMUNE
      IF( NP1 .EQ. NS1 ) THEN
         NP3 = NOARCF(1,NA01)
         IF( NP3 .EQ. NS2 ) THEN
C           NS1 NS2 EST UNE ARETE DU CF
            NONOUI = -1
            GOTO 820
         ENDIF
C        NS1 EST UN SOMMET DU CF.  CALCUL DE L'ANGLE NP1-NP2,NP1-S(NA01)
         CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP3), XY2D(1,0) )
         A1 = ANGLED( XY2D(1,1), XY2D(1,2), XY2D(1,0) )
C        CALCUL DE L'ANGLE NP1-NP2,NP1-NS2
         A2 = ANGLED( XY2D(1,1), XY2D(1,2), XY2DAR(1,2) )
         IF( A2 .LT. EPSANG ) THEN
C           NP1=NS1 NP2 NS2 ALIGNES ET NP2 DIFFERENT DE NS2
            GOTO 850
         ENDIF
         IF( ABS(A2-A1) .LT. EPSANG ) THEN
C           NP1=NS1 NP3 NS2 ALIGNES  ET NP3 DIFFERENT DE NS2
C           NS1-NS2 ET NP3-NP1 SE CHEVAUCHENT
            GOTO 850
         ENDIF
         IF( NSENS .GT. 0 ) THEN
            IF( A1 .GE. A2 ) GOTO 70
         ELSE
            IF( A1 .LE. A2 ) GOTO 70
         ENDIF
         GOTO 850
C
      ELSE IF( NP1 .EQ. NS2 ) THEN
         NP3 = NOARCF(1,NA01)
         IF( NP3 .EQ. NS1 ) THEN
C           NS1 NS2 EST UNE ARETE DU CF
            NONOUI = -1
            GOTO 820
         ENDIF
C        NS2 EST UN SOMMET DU CF.  CALCUL DE L'ANGLE NP1-NP2,NP1-S(NA01)
         CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP3), XY2D(1,0) )
         A1 = ANGLED( XY2D(1,1), XY2D(1,2), XY2D(1,0) )
C        CALCUL DE L'ANGLE NP1-NP2,NP1-NS1
         A2 = ANGLED( XY2D(1,1), XY2D(1,2), XY2DAR(1,1) )
         IF( A2 .LT. EPSANG) THEN
C           NP1=NS2 NP2 NS1 ALIGNES ET NP2 DIFFERENT DE NS1
            GOTO 850
         ENDIF
         IF( ABS(A2-A1) .LT. EPSANG ) THEN
C           NP1=NS2 NP3 NS1 ALIGNES  ET NP3 DIFFERENT DE NS1
C           NS1-NS2 ET NP3-NP1 SE CHEVAUCHENT
            GOTO 850
         ENDIF
         IF( NSENS .GT. 0 ) THEN
            IF( A1 .GE. A2 ) GOTO 70
         ELSE
            IF( A1 .LE. A2 ) GOTO 70
         ENDIF
         GOTO 850
C
      ELSE IF( NP2 .EQ. NS1 ) THEN
C        NS1 EST UN SOMMET DU CF . NP3 EST LE SOMMET SUIVANT NP2 DANS LE CF
         NA3 = NOARCF( 2, NA2 )
         NP3 = NOARCF( 1, NA3 )
         IF( NP3 .EQ. NS2 ) THEN
C           NS1 NS2 EST UNE ARETE DU CF
            NONOUI = -1
            GOTO 820
         ENDIF
C        CALCUL DE L'ANGLE NP2-NP3,NP2-NP1
         XY2D(1,0) = XY2D(1,1)
         XY2D(2,0) = XY2D(2,1)
         XY2D(1,1) = XY2D(1,2)
         XY2D(2,1) = XY2D(2,2)
         CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP3), XY2D(1,2) )
         A1 = ANGLED( XY2D(1,1), XY2D(1,2), XY2D(1,0) )
C        CALCUL DE L'ANGLE NP2-NP3,NP2-NS2
         A2 = ANGLED( XY2D(1,1), XY2D(1,2), XY2DAR(1,2) )
         IF( A2 .LT. EPSANG) THEN
C           NP2=NS1 NP3 NS2 ALIGNES ET NP3 DIFFERENT DE NS2
            GOTO 850
         ENDIF
         IF( ABS(A2-A1) .LT. EPSANG ) THEN
C           NP2=NS1 NP1 NS2 ALIGNES  ET NP1 DIFFERENT DE NS2
C           NS1-NS2 ET NP1-NP2 SE CHEVAUCHENT
            GOTO 850
         ENDIF
         IF( NSENS .GT. 0 ) THEN
            IF( A1 .GE. A2 ) GOTO 70
         ELSE
            IF( A1 .LE. A2 ) GOTO 70
         ENDIF
         GOTO 850
C
      ELSE IF( NP2 .EQ. NS2 ) THEN
C        NS2 EST UN SOMMET DU CF . NP3 EST LE SOMMET SUIVANT NP2 DANS LE CF
         NA3 = NOARCF( 2, NA2 )
         NP3 = NOARCF( 1, NA3 )
         IF( NP3 .EQ. NS1 ) THEN
C           NS1 NS2 EST UNE ARETE DU CF
            NONOUI = -1
            GOTO 820
         ENDIF
C        CALCUL DE L'ANGLE NP2-NP3,NP2-NP1
         XY2D(1,0) = XY2D(1,1)
         XY2D(2,0) = XY2D(2,1)
         XY2D(1,1) = XY2D(1,2)
         XY2D(2,1) = XY2D(2,2)
         CALL CI3D2D( PTXYZD(1,NP0), D2D3, PTXYZD(1,NP3), XY2D(1,2) )
         A1 = ANGLED( XY2D(1,1), XY2D(1,2), XY2D(1,0) )
C        CALCUL DE L'ANGLE NP2-NP3,NP2-NS1
         A2 = ANGLED( XY2D(1,1), XY2D(1,2), XY2DAR(1,1) )
         IF( A2 .LT. EPSANG) THEN
C           NP2=NS2 NP3 NS1 ALIGNES ET NP3 DIFFERENT DE NS1
            GOTO 850
         ENDIF
         IF( ABS(A2-A1) .LT. EPSANG ) THEN
C           NP2=NS2 NP1 NS1 ALIGNES  ET NP1 DIFFERENT DE NS1
C           NS1-NS2 ET NP1-NP2 SE CHEVAUCHENT
            GOTO 850
         ENDIF
         IF( NSENS .GT. 0 ) THEN
            IF( A1 .GE. A2 ) GOTO 70
         ELSE
            IF( A1 .LE. A2 ) GOTO 70
         ENDIF
         GOTO 850
      ENDIF
C
C     PASSAGE A L'ARETE SUIVANTE
 70   NA01 = NA1
      NA1  = NA2
      IF( NA1 .NE. NA00 ) THEN
         NA2  = NOARCF( 2, NA2 )
         GOTO 20
      ENDIF
C
C     L'ARETE EST INTERNE AU CF
 800  NONOUI = 1
CCCC
CCCC     TRACE DE L'ARETE EN VERT
CCC 820  CALL XVCOULEUR( NCVERT )
 820  GOTO 900
C
C     LES 2 ARETES S'INTERSECTENT ENTRE LEURS SOMMETS
 850  NONOUI = 0
CCCC
CCCC     TRACE DE L'ARETE EN ROUGE
CCC      CALL XVCOULEUR( NCROUG )
CCCC
CCCC     TRACE EFFECTIF DU SEGMENT NS1-NS2
CCC 900  X1 = PTXYZD(1,NS1)
CCC      Y1 = PTXYZD(2,NS1)
CCC      Z1 = PTXYZD(3,NS1)
CCC      CALL MOVE( X1, Y1, Z1 )
CCC      X2 = PTXYZD(1,NS2)
CCC      Y2 = PTXYZD(2,NS2)
CCC      Z2 = PTXYZD(3,NS2)
CCC      CALL DRAW( X2, Y2, Z2 )
CCC      CALL ECRENT( NS2 )
CCC      CALL MOVE( X1, Y1, Z1 )
CCC      CALL ECRENT( NS1 )
 900  RETURN
      END
