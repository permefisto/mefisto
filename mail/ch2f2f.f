      SUBROUTINE CH2F2F( I, NF0, NF1, PTXYZD, MXFACO, LEFACO, N1FASC,
     %                   NF2, NF3 )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    ECHANGE DU QUADRILATERE COPLANAIRE FORME PAR 2 TRIANGLES
C -----    AVEC LES 2 AUTRES TRIANGLES SI LE QUADRANGLE EST CONVEXE
C
C ENTREES :
C ---------
C I      : NUMERO DE L'ARETE DE LA FACE NF0
C NF0    : NUMERO LEFACO DE LA FACE
C NF1    : NUMERO LEFACO DE LA FACE ADJACENTE A NF0 PAR SON ARETE I
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C
C MODIFIES :
C ----------
C MXFACO : NOMBRE MAXIMAL DE FACES DECLARABLES DANS LEFACO
C LEFACO : LES 3 SOMMETS, 2 MATERIAUX, 3 FACES VOISINES ET CHAINAGE
C          DES FACES TRIANGULAIRES DU CONTOUR ET INTERFACES
C N1FASC : N1FASC(NS) = NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS
C
C SORTIES :
C ---------
C NF2, NF3: LES 2 FACES LEFACO REMPLACENTES DE NF0,NF1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    FEVRIER 1992
C....................................................................012
      include"./incl/trvari.inc"
      COMMON / UNITES / LECTEU,IMPRIM,NUNITE(30)
      DOUBLE PRECISION  PTXYZD(1:4,1:*)
      INTEGER           LEFACO(11,0:MXFACO),
     %                  N1FASC(*)
C
C     NF0 NF1 FORMENT UN QUADRANGLE
C     LES 2 SOMMETS DE L'ARETE I DE NF0
      IF( I .EQ. 3 ) THEN
         K = 1
      ELSE
         K = I + 1
      ENDIF
      NS1 = LEFACO(I,NF0)
      NS2 = LEFACO(K,NF0)
C
C     NS3 LE SOMMET RESTANT DE NF0
      IF( I .EQ. 1 ) THEN
         L = 3
      ELSE
         L = I - 1
      ENDIF
      NS3 = LEFACO(L,NF0)
C
C     NF4 EST LA FACE DE L'AUTRE COTE DE NS2-NS3
      NF4 = LEFACO(5+K,NF0)
C     NF5 EST LA FACE DE L'AUTRE COTE DE NS1-NS3
      NF5 = LEFACO(5+L,NF0)
C
C     NS4 LE SOMMET RESTANT DE NF1
      DO 10 K=1,3
         NS4 = LEFACO(K,NF1)
         IF( NS4 .NE. NS1 .AND. NS4 .NE. NS2 ) GOTO 20
 10   CONTINUE
C
C     LES 2 SURFACES SONT ELLES EGALES ?
 20   CALL QUADCXD( PTXYZD(1,NS1), PTXYZD(1,NS3),
     %              PTXYZD(1,NS2), PTXYZD(1,NS4), NONOUI )
      IF( NONOUI .EQ. 0 ) GOTO 9000
C
C     LES VOLUMES DE LA FACE
      NV1  = LEFACO(4,NF0)
      NV2  = LEFACO(5,NF0)
C
C     SAUVEGARDE DES FACES ADJACENTES AUX 2 FACES INITIALES
C     DANS NF1 : NS4 EST LE POINT K
      L = K - 1
      IF( L .EQ. 0 ) L = 3
      IF( NS1 .EQ. LEFACO(L,NF1) ) THEN
C        FACE DE L'AUTRE COTE DE NS1 NS4
         NF6 = LEFACO(5+L,NF1)
C        FACE DE L'AUTRE COTE DE NS2 NS4
         NF7 = LEFACO(5+K,NF1)
      ELSE
C        FACE DE L'AUTRE COTE DE NS1 NS4
         NF6 = LEFACO(5+K,NF1)
C        FACE DE L'AUTRE COTE DE NS2 NS4
         NF7 = LEFACO(5+L,NF1)
      ENDIF
CCCC
CCCC     TRACE DES 2 FACES INITIALES DE LEFACO
CCC      CALL TRFATR( NCBLAN, NCVERT, LEFACO(1,NF0), PTXYZD )
CCC      CALL TRFATR( NCBLAN, NCVERT, LEFACO(1,NF1), PTXYZD )
C
C     DESTRUCTION DES 2 FACES INITIALES DE LEFACO
      CALL VDDSFA( NF0 , MXFACO, LEFACO, NBFACO, N1FASC )
      CALL VDDSFA( NF1 , MXFACO, LEFACO, NBFACO, N1FASC )
C
C     AJOUT DE LA FACE NF2 DANS LEFACO
      CALL VDAJFA( NS1,NS4,NS3, NV1,NV2, NF6,-7,NF5,
     %             MXFACO, LEFACO, NBFACO, N1FASC, NF2 )
C
C     AJOUT DE LA FACE NF3 DANS LEFACO
      CALL VDAJFA( NS4,NS2,NS3, NV1,NV2, NF7,NF4,NF2,
     %             MXFACO, LEFACO, NBFACO, N1FASC, NF3 )
CCCC
CCCC     TRACE DES 2 FACES FINALES DE LEFACO
CCC      CALL TRFATR( NCMAGE, NCBLEU, LEFACO(1,NF2), PTXYZD )
CCC      CALL TRFATR( NCMAGE, NCBLEU, LEFACO(1,NF3), PTXYZD )
C
C     MISE A JOUR DE NF3 DANS LA FACE NF2 DE LEFACO
      DO 70 L=6,8
         IF( LEFACO(L,NF2) .EQ. -7 ) THEN
            LEFACO(L,NF2) = NF3
            GOTO 9000
         ENDIF
 70   CONTINUE
C
 9000 RETURN
      END
