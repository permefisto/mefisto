      SUBROUTINE AJTRCF2( NBTRCF, N1FEOC, N1FEVI, MXFETO, NFETOI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    AJOUT DES TRIANGLES DU CF EN FIN DU CHAINAGE DES TRIANGLES
C -----    DE L'ETOILE DEFINIE DANS NFETOI DANS LE CAS OU LE CF
C          EST DOUBLE POUR FAIRE UNE ENCOCHE DANS L'ETOILE

C ENTREES:
C --------
C NBTRCF : NOMBRE DE TRIANGLES FORMANT LE CF

C MODIFIES:
C ---------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : VERSION 2
C          1: NUMERO NOTETR DU TETRAEDRE AU DELA DE LA FACE
C          2: NUMERO PTXYZD DU 1-ER SOMMET DE LA FACE
C          3: NUMERO PTXYZD DU 2-ME SOMMET DE LA FACE
C          4: NUMERO PTXYZD DU 3-ME SOMMET DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR DE
C             L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC et VEULETTES sur MER  Decembre 2014
C2345X7..............................................................012
      INTEGER  NFETOI(5,MXFETO)

      IF( N1FEOC .LE. 0 ) THEN
         PRINT*,'PB AJTRCF2: N1FEOC=',N1FEOC
         RETURN
      ENDIF

C     RECHERCHE DU DERNIER TRIANGLE NFD DE L'ETOILE
      NFD = 0
      NF0 = N1FEOC
 10   IF( NF0 .GT. 0 ) THEN
         NFD = NF0
C        PASSAGE A LA FACE SUIVANTE
         NF0 = NFETOI(5,NF0)
         GOTO 10
      ENDIF

C     LES NBTRCF PREMIERS TRIANGLES AJOUTES PRECEDEMMENT PAR AJTRCF
C     SONT DE NOUVEAU AJOUTES EN FIN D'ETOILE MAIS LES TRIANGLES
C     CHANGENT D'ORIENTATION
      NF0 = N1FEOC

      DO N = 1, NBTRCF

C        NF1 EST LE PREMIER TRIANGLE VIDE DE L'ETOILE
         NF1 = N1FEVI
C        MISE A JOUR DU PREMIER TRIANGLE VIDE
         N1FEVI = NFETOI(5,N1FEVI)

C        LA FACE NF1 DEVIENT LA DERNIERE OCCUPEE DE L'ETOILE
         NFETOI(5,NFD) = NF1
         NFETOI(5,NF1) = 0
         NFD = NF1

C        LE TETRAEDRE OPPOSE A CE TRIANGLE DU CF EST INCONNU
         NFETOI(1,NF1) = 0

C        LES 3 SOMMETS DE LA FACE NF1 SONT INVERSES PAR RAPPORT A NF0
         NFETOI(2,NF1) = NFETOI(2,NF0)
         NFETOI(3,NF1) = NFETOI(4,NF0)
         NFETOI(4,NF1) = NFETOI(3,NF0)

C        PASSAGE A LA FACE SUIVANTE
         NF0 = NFETOI(5,NF0)

      ENDDO

      RETURN
      END
