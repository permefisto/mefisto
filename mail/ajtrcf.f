      SUBROUTINE AJTRCF( NS1ACF, NS2ACF, PTXYZD,
     %                   NBTRCF, NOTRCF, LEFACO, NO0FAR,
     %                   NOTETR, N1FEOC, N1FEVI, MXFETO, NFETOI, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    MODIFIER LES VALEURS DU TABLEAU NFETOI DE L'ETOILE de la
C -----    Version1 en la Version2
C          AJOUT DES TRIANGLES LEFACO ET NO0FAR DU CF EN TETE DU
C          CHAINAGE DES TRIANGLES DE L'ETOILE DEFINIE DANS NFETOI
C          POUR FORMER UNE ETOILE FERMEE

C ENTREES:
C --------
C NS1ACF : NUMERO DU SOMMET 1 DE L'ARETE ORIENTEE D'INVERSE A TROUVER
C NS2ACF : NUMERO DU SOMMET 2 DE L'ARETE ORIENTEE D'INVERSE A TROUVER
C          NS1ACF->NS2ACF DONNE LE SENS DE PARCOURS DES ARETES DU CF
C PTXYZD : PAR POINT : X  Y  Z  DISTANCE_SOUHAITEE
C NBTRCF : NOMBRE DE TRIANGLES PERDUS LEFACO FORMANT LE CF
C NOTRCF : SI NOTRCF(*)>0 NUMERO LEFACO DU TRIANGLE
C                      <0 NUMERO NO0FAR DU TRIANGLE AJOUTE AU CF
C LEFACO : FACE=TRIANGLE DE LA PEAU OU DES INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1 < SOMMET 2 < SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1, VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSTFA(1)+NOSTFA(2)+NOSTFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C          LEFACO(10,*) PREMIERE FACE DANS LE HACHAGE
C          LEFACO(11,.) = NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE, 0 SINON
cccC          LEFACO(12,.) = NO FACEOC DE 1 A NBFACES D'OpenCascade

C NO0FAR : NUMERO DES 3 SOMMETS DE LA FACE AJOUTEE AU CF
C          NORMALE VERS L'INTERIEUR DU TETRAEDRE LA CONTENANT

C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412

C MODIFIES:
C ---------
C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C N1FEVI : POINTEUR SUR LA PREMIERE FACE VIDE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)

C NFETOI : EN ENTREE VERSION 1
C          1: NUMERO NOTETR DU TETRAEDRE DE LA FACE
C          2: NUMERO LOCAL AU TETRAEDRE DE LA FACE DE L'ETOILE
C             UN SIGNE NEGATIF INDIQUE UN TRAITEMENT FAIT ou A FAIRE
C          3: NON UTILISE ICI
C          4: NUMERO DANS LEFACO DE LA FACE, 0 SINON
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C             =0 SI DERNIER DU CHAINAGE

C          EN SORTIE VERSION 2
C          1: > 0 NUMERO NOTETR DU TETRAEDRE AU DELA DE LA FACE (OPPOSE)
C             = 0 PAS DE TETRAEDRE AU DELA DE LA FACE (FRONTIERE)
C             =-1 TETRAEDRE OPPOSE INCONNU A RETROUVER (NON FRONTIERE)
C          2: NUMERO PTXYZD DU 1-ER SOMMET DE LA FACE
C          3: NUMERO PTXYZD DU 2-ME SOMMET DE LA FACE
C          4: NUMERO PTXYZD DU 3-ME SOMMET DE LA FACE
C             S1S2xS1S3 EST LA NORMALE DIRIGEE VERS L'INTERIEUR DE
C             L'ETOILE
C          5: CHAINAGE SUIVANT DES FACES OCCUPEES ET VIDES
C IERR   : 0 SI PAS D'ERREUR DETECTEE
C          1 SI L'ARETE NS1ACF-NS2ACF N'EST PAS UNE ARETE DE L'ETOILE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC ET ST PIERRE DU PERRAY Octobre 2014
C MODIFS : ALAIN PERRONNET LJLL UPMC ET ST PIERRE DU PERRAYSeptembre2015
C2345X7..............................................................012
      COMMON / TRTETR / STOPTE,TRACTE
      LOGICAL           STOPTE,TRACTE

      DOUBLE PRECISION  PTXYZD(4,*)
      INTEGER  NOTRCF(NBTRCF), LEFACO(11,0:*), NO0FAR(3,*), NOTETR(8,*),
     %         NFETOI(5,MXFETO), LAPILE(2,4096)

      IERR = 0

C     TRACE DES FACES SIMPLES DE L'ETOILE DU CF NFETOI VERSION 1
C     AVEC LES TRIANGLES DU CF
      CALL TRFETO2( PTXYZD, NOTETR, N1FEOC, NFETOI,
     %              NBTRCF, NOTRCF, LEFACO, NO0FAR )

C     LE NO DE TETRAEDRE INTERNE ETOILE  => NO TETRAEDRE AU DELA DE LA FACE
C     LE NO FACE LOCAL DANS LE TETRAEDRE => NO 1-ER SOMMET DE LA FACE
C     LE NO INUTILISE 3                  => NO 2-ME SOMMET DE LA FACE
C     LE NO INUTILISE 4                  => NO 3-ME SOMMET DE LA FACE
C     LE NO NFETOI DE LA FACE SUIVANTE   => NO NFETOI DE LA FACE SUIVANTE

C     BOUCLE SUR LES FACES CHAINEES DE L'ETOILE N1FEOC
C     CODAGE en VERSION 2 DU TABLEAU NFETOI DES FACES DE L'ETOILE
C     -----------------------------------------------------------
      CALL V12NFETOI( N1FEOC, NFETOI, NOTETR, NBFETO )

C     AJOUT DES NBTRCF TRIANGLES LEFACO ou NO0FAR DE NOTRCF DU CF
C     EN SENS INVERSE POUR GARDER L'ORDRE DANS LE CHAINAGE
C     -----------------------------------------------------------
      DO N = NBTRCF, 1, -1

C        L'ETOILE EST ELLE SATUREE?
         IF( N1FEVI .LE. 0 ) THEN
            print *,'AJTRCF: AUGMENTER MXFETO=',MXFETO,' et REEXECUTER'
         ENDIF

C        NF1 EST LE PREMIER TRIANGLE NFETOI VIDE DE L'ETOILE
         NF1 = N1FEVI
C        MISE A JOUR DU PREMIER TRIANGLE VIDE
         N1FEVI = NFETOI(5,N1FEVI)

C        LA FACE NF1 DEVIENT LA PREMIERE FACE OCCUPEE DE L'ETOILE
         NFETOI(5,NF1) = N1FEOC
         N1FEOC = NF1

C        NUMERO DU TRIANGLE DANS LEFACO OU NO0FAR
         NTR = NOTRCF( N )

         IF( NTR .GT. 0 ) THEN
C           NUMERO PTXYZD DES 3 SOMMETS DU TRIANGLE NTR DANS LEFACO
C           LA DIRECTION DE LA NORMALE EST QUELCONQUE
            DO K=1,3
               NFETOI( 1+K, NF1 ) = LEFACO( K, NTR )
            ENDDO
         ELSE
C           NUMERO PTXYZD DES 3 SOMMETS DU TRIANGLE -NTR DANS NO0FAR
C           NORMALE VERS L'INTERIEUR DU TETRAEDRE LA CONTENANT
            DO K=1,3
               NFETOI( 1+K, NF1 ) = NO0FAR( K, -NTR )
            ENDDO
         ENDIF

C        ICI LA DIRECTION VERS L'INTERIEUR DE LA NORMALE A LA FACE
C        N'EST PAS ASSURE CAR NON ASSUREE DANS LEFACO et NO0FAR

C        LE TETRAEDRE OPPOSE A CE TRIANGLE DU CF EST INCONNU (=-1)
C        FRONTALIER SERAIT =0
         NFETOI( 1, NF1 ) = -1
                
C        UNE FACE DE L'ETOILE DE PLUS
         NBFETO = NBFETO + 1

      ENDDO

C     UN CHOIX D'ORIENTION DE LA FACE D'ARETE NS2ACF->NS1ACF
C     POUR ENSUITE EN DEDUIRE LA BONNE ORIENTATION DES AUTRES FACES
C     -------------------------------------------------------------
      KK  = 0
      NF1 = N1FEOC

 10   IF( NF1 .GT. 0 ) THEN

C        DANS LA FACE NF1 RECHERCHE DE NS1ACF et NS2ACF
         DO K=1,3
            IF( K .EQ. 3 ) THEN
               KK = 1
            ELSE
               KK = K+1
            ENDIF
            NS1 = NFETOI(1+ K,NF1)
            NS2 = NFETOI(1+KK,NF1)

            IF( NS1 .EQ. NS1ACF .AND. NS2 .EQ. NS2ACF ) THEN

C              NF1 EST MAL ORIENTEE
               L             = NFETOI(3,NF1)
               NFETOI(3,NF1) = NFETOI(4,NF1)
               NFETOI(4,NF1) = L

C              L'ARETE K APRES LA PERMUTATION NS1-NS2 DEVIENT
C              K=1->3, K=2->2, K=3->1 ET
C              L'ARETE SUIVANTE K+1 EST KK
               GOTO( 11, 12, 13 ) , K

 11            KK = 1
               GOTO 20

 12            KK = 3
               GOTO 20

 13            KK = 2
               GOTO 20

            ELSE IF( NS2 .EQ. NS1ACF .AND. NS1 .EQ. NS2ACF ) THEN

C              NF1 EST BIEN ORIENTEE
               GOTO 20

            ENDIF

         ENDDO

C        PASSAGE A LA FACE SUIVANTE
         NF1 = NFETOI( 5, NF1 )
         GOTO 10

      ENDIF

C     PROBLEME:
      PRINT*
      PRINT*,'PB ajtrcf: ARETE',NS1ACF,NS2ACF,' NON RETROUVEE PARMI LES' 
     %,NBFETO,' FACES DE NFETOI'
      CALL AFNFETOI( N1FEOC, NFETOI, NOTETR )
C     TRACE DES FACES SIMPLES DE L'ETOILE DU CF NFETOI
C     SANS LES TRIANGLES DU CF
      TRACTE = .TRUE.
      CALL TRFETO2( PTXYZD, NOTETR, N1FEOC, NFETOI,
     %                   0, NOTRCF, LEFACO, NO0FAR )
      IERR = 1
      GOTO 9999


C     LA FACE NF1 DE NFETOI INITIALISE L'ORIENTATION =>
C     ORIENTION DE TOUTES LES FACES POUR AVOIR DES NORMALES
C     ORIENTEES VERS L'INTERIEUR DE L'ETOILE A PARTIR DE L'ARETE
C     NS1ACF->NS2ACF QUI ORIENTE UNE FACE D'UN TETRAEDRE DU CF
C     ----------------------------------------------------------
 20   LHPILE = 1
C     ARETE 2 DE LA PREMIERE FACE NFETOI BIEN ORIENTEE
      LAPILE( 1, LHPILE ) = NF1
C     ARETE SUIVANTE BIEN ORIENTEE A TRAITER
      LAPILE( 2, LHPILE ) = KK

      LHPILE = 2
C     ARETE 3 DE LA PREMIERE FACE NFETOI BIEN ORIENTEE
      LAPILE( 1, LHPILE ) = NF1
C     ARETE SUIVANTE BIEN ORIENTEE A TRAITER
      IF( KK .EQ. 3 ) THEN
         K = 1
      ELSE
         K = KK+1
      ENDIF
      LAPILE( 2, LHPILE ) = K

C     LES 3 ARETES DE LA FACE NF1 ONT LEUR ORIENTATION TRAITEE
      NFETOI( 2, NF1 ) = - ABS( NFETOI( 2, NF1 ) )

C     TANT QUE LA PILE DES (FACE,ARETE) N'EST PAS VIDE
C     ORIENTER LE FACE POUR QUE LA NORMALE SOIT DIRIGEE VERS
C     L'INTERIEUR DE LA DEMI-ETOILE PUIS
C     EMPILER LES (FACE,ARETE) CONTIGUES NON TRAITEES
C     ------------------------------------------------------
 30   IF( LHPILE .GT. 0 ) THEN

C        FACE NFETOI BIEN ORIENTEE
         NF = LAPILE( 1, LHPILE )

C        ARETE BIEN ORIENTEE A TRAITER
         KA = LAPILE( 2, LHPILE )

C        L'ARETE A TRAITER EST DEPILEE
         LHPILE = LHPILE - 1

C        ARETE KA DE NF A POUR SOMMETS
         NS1 = ABS( NFETOI( 1+KA, NF ) )
         IF( KA .EQ. 3 ) THEN
            KK = 1
         ELSE
            KK = KA+1
         ENDIF
         NS2 = ABS( NFETOI( 1+KK, NF ) )

C        RECHERCHE DE LA FACE ADJACENTE A NF PAR L'ARETE KA
         NF1 = N1FEOC

         DO N = 1, NBTRCF
            IF( NF1 .NE. NF .AND. NFETOI(2,NF1) .GT. 0 ) THEN
               DO K=1,3
                  IF( K .EQ. 3 ) THEN
                     KK = 1
                  ELSE
                     KK = K+1
                  ENDIF
                  NS1A = NFETOI(1+ K,NF1)
                  NS2A = NFETOI(1+KK,NF1)

                  IF( NS1.EQ.NS1A .AND. NS2.EQ.NS2A ) THEN

C                    NF1 EST MAL ORIENTEE
                     L             = NFETOI(3,NF1)
                     NFETOI(3,NF1) = NFETOI(4,NF1)
                     NFETOI(4,NF1) = L
C                    ARETE SUIVANTE DE L'ARETE K APRES PERMUTATION NS1-NS2
                     IF( K .EQ. 1 ) THEN
C                       KK = 3 +1
                        KK = 1
                     ELSE IF( K .EQ. 3 ) THEN
C                       KK = 1 +1
                        KK = 2
                     ELSE
C                       KK = 2 +1
                        KK = 3
                     ENDIF
                     GOTO 40

                  ELSE IF( NS2.EQ.NS1A .AND. NS1.EQ.NS2A ) THEN

C                    NF1 EST BIEN ORIENTEE
                     GOTO 40

                  ENDIF
               ENDDO
            ENDIF

C           PASSAGE A LA FACE SUIVANTE
            NF1 = NFETOI(5,NF1)

         ENDDO

C        AUCUNE FACE DE L'AUTRE COTE DE L'ARETE
         GOTO 30

C        LES 2 AUTRES ARETES DE NF1 SONT EMPILEES
 40      LHPILE = LHPILE + 1
C        FACE NFETOI BIEN ORIENTEE
         LAPILE( 1, LHPILE ) = NF1
C        ARETE SUIVANTE BIEN ORIENTEE A TRAITER
         LAPILE( 2, LHPILE ) = KK

         LHPILE = LHPILE + 1
C        FACE NFETOI BIEN ORIENTEE
         LAPILE( 1, LHPILE ) = NF1
C        ARETE SUIVANTE BIEN ORIENTEE A TRAITER
         IF( KK .EQ. 3 ) THEN
            K = 1
         ELSE
            K = KK+1
         ENDIF
         LAPILE( 2, LHPILE ) = K

C        LES 3 ARETES DE LA FACE NF1 ONT LEUR ORIENTATION TRAITEE
         NFETOI( 2, NF1 ) = - ABS( NFETOI( 2, NF1 ) )

         GOTO 30

      ENDIF

C     MARQUAGE SUPPRIME DES FACES ISSUE DE N1FEOC APRES ORIENTATION
      NF1 = N1FEOC
 100  IF( NF1 .GT. 0 ) THEN
         NFETOI(2,NF1) = ABS( NFETOI(2,NF1) )
         NF1 = NFETOI(5,NF1)
         GOTO 100
      ENDIF

C     TRACE DES FACES SIMPLES DE L'ETOILE DU CF NFETOI VERSION 2
C     SANS LES TRIANGLES DU CF
      CALL TRFETO2( PTXYZD, NOTETR, N1FEOC, NFETOI,
     %                   0, NOTRCF, LEFACO, NO0FAR )

 9999 RETURN
      END
