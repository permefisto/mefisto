      SUBROUTINE CONED2( Z2,     ANGLE1, ANGLE2, NUMRAC,
     %                   PAX1C1, RPCON1, RAY1C1, RAY2C1, HAUTC1,
     %                   PAX1C2, RPCON2, RAY1C2, RAY2C2, HAUTC2,
     %                   NBRAC,  RACINE, P, Q, ANGLE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL PAR DICHOTOMIE DU POINT TEL QUE Z CONE2 / HAUTC2 = 0 OU 1
C -----    POINT D'INTERSECTION DE LA GENERATRICE DU CONE 1 AVEC
C          L'UN DES 2 PLANS DE LIMITATION DU CONE 2
C
C ENTREES:
C --------
C Z2     : COTE / HAUTC2 DU POINT A PROJETER SUR 0 OU 1
C ANGLE1 : ANGLE EN RADIANS DE LA 1-ERE GENERATRICE DU CONE 1 A CALCULER
C          CET ANGLE DONNE UN POINT EXTERIEUR A [0,1]
C ANGLE2 : ANGLE EN RADIANS DE LA 2-EME GENERATRICE DU CONE 1 A CALCULER
C          CET ANGLE DONNE UN POINT INTERIEUR A [0,1]
C NUMRAC : NUMERO DE LA RACINE A UTILISER PARMI LES 2 POSSIBLES
C PAX1C1 : POINT DE BASE DE L'AXE DU TRONC DE CONE 1
C RPCON1 : REPERE PROPRE ORTHONORME DU CONE 1 D'ORIGINE AU POINT PAX1C1
C          ORIGINE AU POINT PAX1C1
C RAY1C1 : RAYON DU CERCLE AU POINT PAX1C1 (PLAN ORTHOGONAL A L'AXE DU CONE)
C RAY2C1 : RAYON DU CERCLE AU POINT PAX2C1 (PLAN ORTHOGONAL A L'AXE DU CONE)
C HAUTC1 : HAUTEUR DU TRONC DE CONE 1 (DISTANCE PAX1C1 PAX2C1)
C
C PAX1C2 : POINT DE BASE DE L'AXE DU TRONC DE CONE 2
C RPCON2 : REPERE PROPRE ORTHONORME DU CONE 2 D'ORIGINE AU POINT PAX1C2
C RAY1C2 : RAYON DU CERCLE AU POINT PAX1C2 (PLAN ORTHOGONAL A L'AXE DU CONE)
C RAY2C2 : RAYON DU CERCLE AU POINT PAX2C2 (PLAN ORTHOGONAL A L'AXE DU CONE)
C HAUTC2 : HAUTEUR DU TRONC DE CONE 2 (DISTANCE PAX1C2 PAX2C2)
C
C SORTIES:
C --------
C NBRAC  : NOMBRE DE POINTS D'INTERSECTION DE LA DROITE GENERATRICE DU
C          CONE 1 AVEC LE CONE 2, LES 2 ETANT SUPPOSES ICI INFINIS
C          NBRAC=0 => RACINE NON INITIALISE
C          NBRAC=1 => RACINE(1)=L1 RACINE SIMPLE => ANOMALIE
C          NBRAC=2 => RACINE(1)=L1 RACINE(2)=L2  LES 2 RACINES SIMPLES
C          NBRAC=3 => RACINE(1)=L1 RACINE DOUBLE
C          NBRAC=4 => RACINE NON INITIALISE CAR PQ GENERATRICE DES CONES 1 2
C RACINE : LES 1 OU 2 RACINES (SIMPLES OU DOUBLE) DU PARAMETRE
C          TEL QUE PM = RACINE * PQ
C P Q    : LES 2 POINTS DE LA GENERATRICE DU CONE 1 EN COORDONNEES DU CONE 2
C ANGLE  : ANGLE EN RADIANS DE LA GENERATRICE DU CONE 1 A CALCULER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS    DECEMBRE 1997
C2345X7..............................................................012
      IMPLICIT   DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION  ANGLE1, ANGLE2, PAX1C1(3), PAX1C2(3)
      DOUBLE PRECISION  RPCON1(3,3), RPCON2(3,3)
      DOUBLE PRECISION  RAY1C1, RAY2C1, RAY1C2, RAY2C2
      DOUBLE PRECISION  P(3), Q(3), RACINE(2)
C
      IF( Z2 .LT. 1D0-Z2 ) THEN
C
C           RECHERCHE DU POINT AVEC Z2=0D0
C           ==============================
 10         ANGLE = ( ANGLE1 + ANGLE2 ) / 2
            CALL CONERA( ANGLE,
     %                   PAX1C1, RPCON1, RAY1C1, RAY2C1, HAUTC1,
     %                   PAX1C2, RPCON2, RAY1C2, RAY2C2, HAUTC2,
     %                   NBRAC,  RACINE, P, Q  )
            Z2 = ( P(3) + RACINE( NUMRAC ) * ( Q(3) - P(3) ) ) / HAUTC2
C
            IF( ABS( Z2 ) .GT. 1D-14 ) THEN
               IF( Z2 .LT. 0D0 ) THEN
C                 C'EST LA BORNE MIN DE L'INTERVALLE EXTERIEUR A [0,1]
                  ANGLE1 = ANGLE
                  GOTO 10
               ELSE
C                 C'EST LA BORNE MAX DE L'INTERVALLE INTERIEUR A [0,1]
                  ANGLE2 = ANGLE
                  GOTO 10
               ENDIF
            ENDIF
C
C           Z2 = 0 EST OBTENU
C
      ELSE
C
C           RECHERCHE DU POINT AVEC Z2=1D0
C           ==============================
 20         ANGLE = ( ANGLE1 + ANGLE2 ) / 2
            CALL CONERA( ANGLE,
     %                   PAX1C1, RPCON1, RAY1C1, RAY2C1, HAUTC1,
     %                   PAX1C2, RPCON2, RAY1C2, RAY2C2, HAUTC2,
     %                   NBRAC,  RACINE, P, Q  )
            Z2 = ( P(3) + RACINE( NUMRAC ) * ( Q(3) - P(3) ) ) / HAUTC2
C
            IF( ABS( 1D0 - Z2 ) .GT. 1D-14 ) THEN
               IF( Z2 .LT. 1D0 ) THEN
C                 C'EST LA BORNE MIN DE L'INTERVALLE INTERIEUR A [0,1]
                  ANGLE2 = ANGLE
                  GOTO 20
               ELSE
C                 C'EST LA BORNE MAX DE L'INTERVALLE EXTERIEUR A [0,1]
                  ANGLE1 = ANGLE
                  GOTO 20
               ENDIF
            ENDIF
C
C           Z2 = 1 EST OBTENU
      ENDIF
C
      RETURN
      END
