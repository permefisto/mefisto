      SUBROUTINE TEQINI( NOFOTI, MXSOMM, NBLFTR, NDARLF,
     %                   NBARFR, NSLIGN,
     %                   NBSOIN, NOSOIN, DISOIN,
     %                   MOSOAR, MXSOAR, NOSOAR,
     %                   PXYD  , NBSOMM,
     %                   ORIGIN, D2D3  , COMXMI, AREMIN, AREMAX,
     %                   XMIN  , YMIN  , XYMXMI, ARETMX, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    INITIALISATION DES TABLEAUX PXYD ORIGIN D2D3
C -----    MISE A L'ECHELLE ENTRE 0 ET MAX ENTIER (2**20) DES COORDONNEES
C          DES SOMMETS DES ARETES FRONTALIERES ET POINTS IMPOSES INTERNES
C
C ENTREES:
C --------
C NOFOTI : NUMERO DE LA FONCTION UTILISATEUR TAILLE_IDEALE(x,y,z)
C          0 SI LA FONCTION N'A PAS ETE DONNEE PAR L'UTILISATEUR
C MXSOMM : NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TRIANGULATION
C NBLFTR : NOMBRE DE LIGNES FERMEES LIMITANT LA SURFACE A TRIANGULER
C NDARLF : NUMERO DE LA PREMIERE ARETE DE CHAQUE LIGNE FERMEE DANS
C          LE TABLEAU NOSOAR
C NBSOIN : NOMBRE DE POINTS INTERNES IMPOSES COMME SOMMETS
C NOSOIN : NOSOIN(I) NUMERO DANS LE LEXIQUE POINT DU I-EME POINT INTERNE
C DISOIN : DISOIN(I) DISTANCE SOUHAITEE AVEC LES VOISINS DU POINT
C NSLIGN : >0 => NS NUMERO DU POINT DANS LE LEXIQUE POINT SI INTERNE IMPOSE
C          OU => 1 000 000 * N + NS1
C              OU N   EST LE NUMERO (1 A NBLFTR) DE LA LIGNE DE CE POINT
C                 NS1 EST LE NUMERO DU POINT DANS SA LIGNE
C          = 0 SI LE POINT EST INTERNE NON IMPOSE PAR L'UTILISATEUR
C          =-1 SI LE SOMMET EST EXTERNE AU DOMAINE
C MOSOAR : NOMBRE MAXIMAL D'ENTIERS PAR ARETE
C MXSOAR : NOMBRE MAXIMAL D'ARETES FRONTIERES DECLARABLES
C
C ENTREES ET SORTIES :
C --------------------
C NBARFR : NOMBRE D'ARETES  DE LA FRONTIERE OU CONTOUR DU DOMAINE =
C          NOMBRE DE POINTS DE LA FRONTIERE OU CONTOUR DU DOMAINE
C NOSOAR : NUMERO DES 2 SOMMETS , NO LIGNE, 2 TRIANGLES, CHAINAGES
C          SOMMET 1 = 0 SI ARETE VIDE => SOMMET 2 = ARETE VIDE SUIVANTE
C
C  ATTENTION: UNE ARETE DOIT ETRE LA SUIVANTE DE LA PRECEDENTE
C  ---------- OU LA PREMIERE D'UNE COMPOSANTE CONNEXE DE LA
C             FRONTIERE  OU LA DERNIERE
C             DE PLUS LE DOMAINE DOIT ETRE A GAUCHE POUR LE
C             LE SENS NS1 => NS2
C
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTST
C          PAR POINT : X  Y  DISTANCE_SOUHAITEE
C          TABLEAU REEL2(3,MXSOMM)
C
C ARETMX : TAILLE DE L'ARETE MAXIMALE SOUHAITEE
C          EN ENTREE DANS LES COORDONNEES 3D
C          EN SORTIE DANS LES COORDONNEES PLANES
C
C SORTIES:
C --------
C NBSOMM : NOMBRE DE SOMMETS DE LA FRONTIERE + POINTS INTERNES SIMPLES
C          NBARFR + NBSOIN
C ORIGIN : POINT ORIGINE DANS LE PLAN DE MAILLAGE
C D2D3   : MATRICE DE PASSAGE DES COORDONNEES 3D EN COORDONNEES 2D
C COMXMI : COORDONNEES MINIMALES ET MAXIMALES DES POINTS FRONTALIERS 2D
C AREMIN : LONGUEUR MINIMALE DES ARETES DES LIGNES FERMEES
C AREMAX : LONGUEUR MAXIMALE DES ARETES DES LIGNES FERMEES
C XMIN   : ABSCISSE MINIMALE APRES TRANSFORMATION 3D->2D
C YMIN   : ORDONNEE MINIMALE APRES TRANSFORMATION 3D->2D
C XYMXMI : ECART MAXIMAL ENTRE XMAX-XMIN ET YMAX-YMIN
C IERR   : 0 SI PAS D'ERREUR
C          7 TOUS LES POINTS SONT ALIGNES
C         10 LIGNE ENVELOPPE INCORRECTE
C         11 2 EXTREMITES CONFONDUES POUR UNE ARETE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JUILLET 1995
C....................................................................012
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU,IMPRIM,INTERA,NUNITE(29)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
C
      INTEGER           NDARLF(1:NBLFTR),
     %                  NSLIGN(MXSOMM),
     %                  NOSOAR(MOSOAR,MXSOAR),
     %                  NOSOIN(NBSOIN)
C
      REAL              DISOIN(NBSOIN),
     %                  ORIGIN(3)
C
      DOUBLE PRECISION  PXYD(3,MXSOMM)
      DOUBLE PRECISION  D2D3(3,3)
C
C     LES TABLEAUX AUXILIAIRES
      REAL              COMXMI(3,2), XYZP(3,2), XYZ(3), A(2)
      CHARACTER*24      KNOM
C
C     LE PLUS GRAND REEL STOCKABLE EN MC
      RMAX = RINFO( 'GRAND' )
C
C     RECHERCHE DES POINTS EXTREMES EN X ET Y DU DOMAINE A TRIANGULER
C     ===============================================================
      DO 5 K=1,2
         COMXMI(K,1) = REAL( PXYD(K,1) )
         COMXMI(K,2) = REAL( PXYD(K,1) )
 5    CONTINUE
      COMXMI(3,1) = 0
      COMXMI(3,2) = 0
C
      XYZP(1,1) =  RMAX
      XYZP(2,1) =  RMAX
      XYZP(1,2) = -RMAX
      XYZP(2,2) = -RMAX
      XYZP(3,1) = 0
      XYZP(3,2) = 0
C
C     CALCUL DES COORDONNEES MIN ET MAX DES SOMMETS DES LIGNES
      DO 15 K=1,NBARFR
         DO 10 I=1,2
            COMXMI(I,1) = REAL( MIN( PXYD(I,K), DBLE(COMXMI(I,1)) ) )
            COMXMI(I,2) = REAL( MAX( PXYD(I,K), DBLE(COMXMI(I,2)) ) )
            XYZP(I,1) = REAL( MIN( PXYD(I,K), DBLE(XYZP(I,1)) ) )
            XYZP(I,2) = REAL( MAX( PXYD(I,K), DBLE(XYZP(I,2)) ) )
 10      CONTINUE
 15   CONTINUE
C
C     VERIFICATION : LA SURFACE EST-ELLE REDUITE A UNE LIGNE?
C     =======================================================
      XMIN = COMXMI(1,1)
      YMIN = COMXMI(2,1)
      A(1) = COMXMI(1,2) - COMXMI(1,1)
      A(2) = COMXMI(2,2) - COMXMI(2,1)

C     COEFFICIENT DE MISE A L'ECHELLE
      XYMXMI = MAX( A(1), A(2) )
C
C     LA LONGUEUR DE LA DIAGONALE
      S = SQRT( A(1)**2 + A(2)**2 )
      DO 60 K=1,2
         IF( A(K) .LT. 1E-4 * S ) THEN
            NBLGRC(NRERR) = 1
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'TOUS LES SOMMETS DES LIGNES SONT ALIGNES'
            ELSE
               KERR(1) = 'ALL VERTICES ON A STRAIGHT LINE'
            ENDIF
            CALL LEREUR
            IERR = 7
            RETURN
         ENDIF
 60   CONTINUE
C
C     LA DISTANCE SOUHAITEE AUTOUR DES SOMMETS FRONTALIERS EST INCONNUE
      DO 70 I=1,NBARFR
         PXYD(3,I) = 0D0
 70   CONTINUE
C
C     MISE A L'ECHELLE ENTRE 0 ET 2**20 DANS LE PLAN DES COORDONNEES DES SOMMETS
C     ==========================================================================
      DO 72 I=1,NBARFR
         S = REAL( PXYD(1,I) )
C        MISE A l'ECHELLE DANS LE PLAN
         PXYD(1,I) = MISECH( S, XMIN, XYMXMI )
         S = REAL( PXYD(2,I) )
         PXYD(2,I) = MISECH( S, YMIN, XYMXMI )
 72   CONTINUE
C
C     INITIALISATION DES SOMMETS DES ARETES DANS PXYD
C     CALCUL DE LA LONGUEUR MAX ET MIN D'UNE ARETE
C     ===============================================
      AREMIN = RMAX
      AREMAX = 0
      NS0    = 0
      NBSAVA = 0
      NOAR   = NDARLF(1)
      DO 100 I=1,NBARFR
C
C        LE NUMERO DES 2 SOMMETS DE L'ARETE I
         NS1 = NOSOAR(1,NOAR)
         NS2 = NOSOAR(2,NOAR)
C        L'ARETE SUIVANTE
         NOAR1 = NOSOAR(6,NOAR)
C
C        LONGUEUR DE L'ARETE I
         S = REAL( SQRT( ( PXYD(1,NS2)-PXYD(1,NS1) )**2 +
     %                   ( PXYD(2,NS2)-PXYD(2,NS1) )**2 ) )
         IF( S .LE. 0.0 ) THEN
            NBLGRC(NRERR) = 2
            WRITE(KERR(MXLGER)( 1:10),'(I10)') NS1
            WRITE(KERR(MXLGER)(11:20),'(I10)') NS2
            IF( LANGAG .EQ. 0 ) THEN
               KERR(1) = 'ARETE DE SOMMETS ' //
     %                    KERR(MXLGER)(1:10) // KERR(MXLGER)(11:20)
               KERR(2) = 'EST DE LONGUEUR NULLE'
            ELSE
               KERR(1) = 'EDGE of VERTICES ' //
     %                    KERR(MXLGER)(1:10) // KERR(MXLGER)(11:20)
               KERR(2) = 'HAS a NULL LENGTH'
            ENDIF
            CALL LEREUR
            IERR = 11
            RETURN
         ENDIF
C
C        CALCUL DE LA LONGUEUR MAX ET MIN D'UNE ARETE
         IF( S .GT. AREMAX ) AREMAX = S
         IF( S .LT. AREMIN ) AREMIN = S
C
C        LA DISTANCE SOUHAITEE EST LE MINIMUM DES DISTANCES AUX VOISINS
         IF( PXYD(3,NS1) .LE. 0D0 ) THEN
            PXYD(3,NS1) = S
         ELSE
            PXYD(3,NS1) = MIN( PXYD(3,NS1), DBLE(S) )
         ENDIF
C
         IF( PXYD(3,NS2) .LE. 0D0 ) THEN
            PXYD(3,NS2) = S
         ELSE
            PXYD(3,NS2) = MIN( PXYD(3,NS2), DBLE(S) )
         ENDIF
C
C        PASSAGE A L'ARETE SUIVANTE
         NOAR = NOAR1
C
 100  CONTINUE
C
      IF( NOFOTI .GT. 0 ) THEN
C        L'ARETE SERT A LOCALISER LES TE INTERNES
C        LA CHOISIR TROP PETITE FAIT OUBLIER DES POINTS DE LA FRONTIERE
         ARETMX = AREMAX
      ELSE
C
C        MISE A L'ECHELLE ENTRE 0 ET 2**20 DE ARETMX
C        PROTECTION CONTRE UNE VALEUR TROP GRANDE DE ARETMX
         IF( ARETMX .GT. XYMXMI ) ARETMX = XYMXMI
         ARETMX = MISECH( XMIN+ARETMX, XMIN, XYMXMI )
C        PROTECTION CONTRE UNE ARETE MAX DESIREE TROP GRANDE OU TROP PETITE
         IF( ARETMX .GT. AREMAX*2.05 ) ARETMX = AREMAX
         IF( (AREMAX-AREMIN) .GT. (AREMIN+AREMAX)*0.05 .AND.
     %        ARETMX .LT. AREMIN*0.5 ) ARETMX =(AREMIN+AREMAX*2)/3.0
         if( aremin .gt. 0 .and. aretmx .lt. aremin ) aretmx = aremin
      ENDIF
C
C     LE NOMBRE REEL DE SOMMETS FRONTALIERS
      IF( LANGAG .EQ. 0 ) THEN
      WRITE(IMPRIM,*) 'NOMBRE SOMMETS(=ARETES) SUR LA FRONTIERE',NBARFR
C     AFFICHAGE DANS L'ANCIEN REPERE DES LONGUEURS DES ARETES
      WRITE(IMPRIM,*) 'LONGUEUR MINIMALE D''UNE ARETE FRONTIERE',
     %                ECHMIS( XMIN+AREMIN, XMIN, XYMXMI ) - XMIN
      WRITE(IMPRIM,*) 'LONGUEUR MAXIMALE D''UNE ARETE FRONTIERE',
     %                ECHMIS( XMIN+AREMAX, XMIN, XYMXMI ) - XMIN
      WRITE(IMPRIM,*)'LONGUEUR MAXIMALE SOUHAITEE D''UNE ARETE INTERNE',
     %                ECHMIS( XMIN+ARETMX, XMIN, XYMXMI ) - XMIN
      ELSE
      WRITE(IMPRIM,*)'NUMBER of VERTICES(=EDGES) ON BOUNDARY',NBARFR
C     AFFICHAGE DANS L'ANCIEN REPERE DES LONGUEURS DES ARETES
      WRITE(IMPRIM,*) 'LENGTH MINIMUM of BOUNDARY EDGES',
     %                ECHMIS( XMIN+AREMIN, XMIN, XYMXMI ) - XMIN
      WRITE(IMPRIM,*) 'LENGTH MAXIMUM of BOUNDARY EDGES',
     %                ECHMIS( XMIN+AREMAX, XMIN, XYMXMI ) - XMIN
      WRITE(IMPRIM,*) 'WISHED LENGTH MAXIMUM of INTERNAL EDGES',
     %                ECHMIS( XMIN+ARETMX, XMIN, XYMXMI ) - XMIN
      ENDIF
C
C     AJOUT DES SOMMETS INTERNES AUX POINTS INITIAUX
C     ==============================================
      NBSOMM = NBARFR
      DO 150 I=1,NBSOIN
C
C        LE NUMERO DU POINT INTERNE
         NS = NOSOIN(I)
C        SON NOM DANS LE LEXIQUE DES POINTS
         CALL NMOBNU( 'POINT', NS , KNOM )
C        MNX L'ADRESSE DE L'ABSCISSE DU POINT
         CALL XYZPOI( NS, MNX, IERR )
         IF( IERR .NE. 0 ) RETURN
C        LES 2 COORDONNEES DU POINT
         CALL CH3D2D( ORIGIN, D2D3, RMCN(MNX), XYZP(1,1) )
C
C        MISE A l'ECHELLE ENTRE 0 ET 2**20 DES 2 COORDONNEES DU POINT
         XYZP(1,1) = MISECH( XYZP(1,1), XMIN, XYMXMI )
         XYZP(2,1) = MISECH( XYZP(2,1), YMIN, XYMXMI )
C
C        CE POINT NS EST IL DEJA STOCKE ?
         DO 140 K = 1 ,NBSOMM
            XYZ(1) = REAL( PXYD(1,K) )
            XYZ(2) = REAL( PXYD(2,K) )
            XYZ(3) = 0
            CALL XYIDE( XYZP(1,1), XYZ, J )
            IF( J .NE. 0 ) THEN
C              POINT INTERNE DEJA RECENSE => AVERTISSEMENT
               NBLGRC(NRERR) = 3
               IF( LANGAG .EQ. 0 ) THEN
                  KERR(1) = 'UN POINT DOUBLE IDENTIFIE: '//KNOM
                  WRITE(KERR(2),10140) NS,XYZP(1,1),XYZP(2,1)
10140             FORMAT('LE POINT ',I6,' X=',G15.7,
     %            ' Y=',G15.7,' A DEJA ETE DONNE')
                  KERR(3) = 'CE DERNIER POINT EST SUPPRIME'
              ELSE
                  KERR(1) = 'AN IDENTIFIED DOUBLE POINT: '//KNOM
                  WRITE(KERR(2),20140) NS,XYZP(1,1),XYZP(2,1)
20140             FORMAT('The POINT ',I6,' X=',G15.7,
     %            ' Y=',G15.7,' HAS BEEN ALREADY GIVEN')
                  KERR(3) = 'This LAST POINT IS DELETED'
               ENDIF
               CALL LERESU
               GOTO 145
            ENDIF
 140     CONTINUE
C
C        NON . IL EST AJOUTE
         NBSOMM = NBSOMM + 1
C        LES COORDONNEES DU POINT
C        MISE A l'ECHELLE
         PXYD(1,NBSOMM) = XYZP(1,1)
         PXYD(2,NBSOMM) = XYZP(2,1)
         NSLIGN(NBSOMM) = NS
         NS = NBSOMM
C
C        MISE A L'ECHELLE ENTRE 0 ET 2**20 DE LA DISTANCE
C        SOUHAITEE AUTOUR DU SOMMET NS
C        PROTECTION CONTRE UNE VALEUR TROP GRANDE DE DISOIN(I)
 145     S = DISOIN(I)
         IF( S .GT. XYMXMI ) S = XYMXMI
         S = MISECH( XMIN+S, XMIN, XYMXMI )
         PXYD(3,NS) = MIN( S, ARETMX )
         K = NUDCNB(KNOM)
         IF( LANGAG .EQ. 0 ) THEN
            WRITE(IMPRIM,*) 'POINT INTERNE ',KNOM(1:K),
     %                      ' LONGUEUR ARETE=', DISOIN(I),
     %                      ' APRES MISE A L''ECHELLE=',S
         ELSE
            WRITE(IMPRIM,*) 'INTERNAL POINT ',KNOM(1:K),
     %                      ' EDGE LENGTH=', DISOIN(I),
     %                      ' AFTER SCALING=',S
         ENDIF
C
 150  CONTINUE
C
C     LE NOMBRE REEL DE SOMMETS INTERNES
      NBSOIN = NBSOMM - NBARFR
      IF( LANGAG .EQ. 0 ) THEN
         WRITE(IMPRIM,*) 'NOMBRE DE SOMMETS INTERNES IMPOSES',NBSOIN
      ELSE
         WRITE(IMPRIM,*) 'NUMBER of INTERNAL IMPOSED POINTS',NBSOIN
      ENDIF
C
C     MISE A L'ECHELLE ENTRE 0 ET 2**20 DES XY MIN ET MAX
C     ===================================================
      DO 160 I=1,2
         COMXMI(1,I) = MISECH( COMXMI(1,I), XMIN, XYMXMI )
         COMXMI(2,I) = MISECH( COMXMI(2,I), YMIN, XYMXMI )
 160  CONTINUE
C
C     MISE A ARETMX DE LA DISTANCE SOUHAITEE DE TOUS LES AUTRES SOMMETS
C     =================================================================
      DO 170 NS=NBSOMM+1,MXSOMM
         PXYD( 3, NS ) = ARETMX
 170  CONTINUE
C
C     ICI LE TABLEAU PXYD N'A PAS DE SOMMETS IDENTIQUES
      RETURN
      END
