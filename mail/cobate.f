      SUBROUTINE COBATE( POINT, PTXYZD, NOSOTE,  VTE, COBARY, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DES 4 COORDONNEES BARYCENTRIQUES DU POINT DANS LE
C -----    TETRAEDRE DES 4 SOMMETS NOSOTE
C          COBARY(N) = VOLUME( FACE N+POINT ) / VOLUME( TETRAEDRE )

C ENTREES:
C --------
C POINT  : LES 3 COORDONNEES DU POINT
C PTXYZD : X Y Z DISTANCE SOUHAITEE DES POINTS
C NOSOTE : LE NUMERO DES 4 SOMMETS DU TETRAEDRE

C SORTIES:
C --------
C VTE    : LE VOLUME <0 ou =0 ou >0  DU TETRAEDRE NOSOTE
C COBARY : LES 4 COORDONNEES BARYCENTRIQUES DE POINT DANS NOSOTE
C          QUELQUE SOIT LE SIGNE DU VOLUME
C          ATTENTION A L'ORDRE :
C          COBARY(I) EST ICI LA COORDONNEE BARYCENTRIQUE OPPOSEE
C                    A LA FACE I DE SOMMETS I, I+1, I+2 MODULO 4
C IERR   : 0 SI LE TETRAEDRE NOSOTE N'EST PAS DE VOLUME NUL
C          1 SI LE TETRAEDRE NOSOTE   EST     DE VOLUME NUL
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       MARS 1993
C....................................................................012
      INTEGER           NOSOTE(4)
      DOUBLE PRECISION  PTXYZD(4,*), POINT(3), COBARY(4), VTE, VOLTET

C     CALCUL DU DETERMINANT DES SOMMETS 1 2 3 4
      VTE = VOLTET( PTXYZD(1,NOSOTE(1)), PTXYZD(1,NOSOTE(2)),
     %              PTXYZD(1,NOSOTE(3)), PTXYZD(1,NOSOTE(4)) )

      IF( VTE .EQ. 0D0 ) THEN
C        VOLUME NUL
         IERR = 1
         COBARY(1) = 2D0
         COBARY(2) = 2D0
         COBARY(3) = 2D0
         COBARY(4) = 2D0
         RETURN
      ENDIF

C     VOLUME NON NUL
C     CALCUL DU DETERMINANT DE 1 2 3 POINT
      COBARY(1) = VOLTET( PTXYZD(1,NOSOTE(1)), PTXYZD(1,NOSOTE(2)),
     %                    PTXYZD(1,NOSOTE(3)), POINT ) / VTE

C     CALCUL DU DETERMINANT DE 2 4 3 POINT
      COBARY(2) = VOLTET( PTXYZD(1,NOSOTE(2)), PTXYZD(1,NOSOTE(4)),
     %                    PTXYZD(1,NOSOTE(3)), POINT ) / VTE

C     CALCUL DU DETERMINANT DE 3 4 1 POINT
      COBARY(3) = VOLTET( PTXYZD(1,NOSOTE(3)), PTXYZD(1,NOSOTE(4)),
     %                    PTXYZD(1,NOSOTE(1)), POINT ) / VTE

C     CALCUL DU DETERMINANT DE 4 2 1 POINT
      COBARY(4) = VOLTET( PTXYZD(1,NOSOTE(4)), PTXYZD(1,NOSOTE(2)),
     %                    PTXYZD(1,NOSOTE(1)), POINT ) / VTE

      IERR  = 0

      RETURN
      END
