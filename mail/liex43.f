      SUBROUTINE LIEX43( NTLXLG2, LADEFI ,
     %                   NTNSEF2, MNNSEF2, NTXYZ2, MNXYZ2, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    INVERSION DE L'ORDRE DES SOMMETS D'UNE LIGNE STRUCTUREE
C -----    LE DERNIER DEVIENT LE PREMIER, ...
C
C ENTREES:
C --------
C NTLXLG2: NUMERO DU TABLEAU TS DU LEXIQUE DE LA LIGNE
C LADEFI : TABLEAU ENTIER DE DEFINITION DE LA LIGNE
C          CF ~td/d/a_ligne__definition
C
C SORTIES:
C --------
C NTNSEF2: NUMERO      DU TMS 'NSEF' DES NUMEROS DES ARETES DE LA LIGNE
C MNNSEF2: ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES ARETES DE LA LIGNE
C          CF ~td/d/a___nsef
C NTXYZ2 : NUMERO      DU TMS 'XYZSOMMET' DE LA LIGNE
C MNXYZ2 : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA LIGNE
C IERR   : 0 SI PAS D'ERREUR
C          1 SI LIGNE INCONNUE
C          2 SI LIGNE NON STRUCTUREE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : Alain PERRONNET LJLL UPMC PARIS & St PIERRE DU PERRAY  SEPTEMBRE 2009
C2345X...............................................................012........
      IMPLICIT INTEGER (W)
      include"./incl/langue.inc"
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU , IMPRIM , INTERA , NUNITE(29)
      include"./incl/a_ligne__definition.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/ntmnlt.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1))
C
      INTEGER           LADEFI(0:*)
C
      IERR = 0
C
C     LA LIGNE D'ORDRE DES SOMMETS A INVERSER
C     =======================================
C     LE NUMERO DE CETTE LIGNE
      NULIIN = LADEFI(WULIIN)
C
C     LE TABLEAU LEXIQUE DE CETTE LIGNE
      CALL LXNLOU( NTLIGN , NULIIN , NTLXLG1 , MN )
      IF( NTLXLG1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'LIGNE INCONNUE'
         ELSE
            KERR(1) = 'UNKNOWN LINE'
         ENDIF
         CALL LEREUR
         IERR = 1
         RETURN
      ENDIF
C
C     LE TABLEAU 'NSEF' DE CETTE LIGNE INITIALE
      CALL LXTSOU( NTLXLG1, 'NSEF', NTNSEF1, MNNSEF1 )
C
C     LA LIGNE EST ELLE STRUCTUREE ?
      NUTYLI = MCN( MNNSEF1 + WUTYMA )
      IF( NUTYLI .NE. 2 ) THEN
         NBLGRC(NRERR) = 1
         IF( LANGAG .EQ. 0 ) THEN
            KERR(1) = 'LIGNE NON STRUCTUREE'
         ELSE
            KERR(1) = 'NOT A STRUCTURED LINE'
         ENDIF
         CALL LERESU
         IERR = 2
      ENDIF
      NBTGEF = MCN( MNNSEF1 + WBTGEF )
      NBEFTG = MCN( MNNSEF1 + WBEFTG )
      NBEFAP = MCN( MNNSEF1 + WBEFAP )
C
C     LE TABLEAU 'XYZSOMMET' DE CETTE LIGNE INITIALE
      CALL LXTSOU( NTLXLG1, 'XYZSOMMET',  NTXYZ1, MNXYZ1 )
      NBSOM  = MCN( MNXYZ1 + WNBSOM )
      NBTGS  = MCN( MNXYZ1 + WNBTGS )
      NBCOOR = MCN( MNXYZ1 + WBCOOR )
C
C     CONSTRUCTION DU TMS 'XYZSOMMET' DE LA LIGNE INVERSEE
      CALL LXTSOU( NTLXLG2, 'XYZSOMMET',  NTXYZ2, MNXYZ2 )
      IF( NTXYZ2 .GT. 0 ) CALL LXTSDS( NTLXLG2, 'XYZSOMMET' )
      MOXYZ2 = WYZSOM + NBCOOR * (NBSOM+NBTGS)
      CALL LXTNDC( NTLXLG2, 'XYZSOMMET', 'MOTS',  MOXYZ2 )
      CALL LXTSOU( NTLXLG2, 'XYZSOMMET',  NTXYZ2, MNXYZ2 )
C
C     COPIE DU DEBUT DU TMS XYZSOMMET
      CALL TRTATA( MCN(MNXYZ1), MCN(MNXYZ2), WBCOOR+1  )
C
C     COPIE INVERSEE DES NBCOOR DES NBSOM SOMMETS
      MN1 = MNXYZ1 + WYZSOM + NBCOOR * (NBSOM-1)
      MN2 = MNXYZ2 + WYZSOM
      DO 15 I=1,NBSOM
         DO 10 K=0,NBCOOR-1
           RMCN(MN2+K) = RMCN(MN1+K)
 10     CONTINUE
        MN1 = MN1 - NBCOOR
        MN2 = MN2 + NBCOOR
 15   CONTINUE
C
C     LES TANGENTES GARDENT LEUR NUMERO INITIAL
C     COPIE SIMPLE DES NBCOOR DES NBTGS TANGENTES
      MN1 = MNXYZ1 + WYZSOM + NBCOOR * NBSOM - 1
      MN2 = MNXYZ2 + WYZSOM + NBCOOR * NBSOM - 1
      DO 25 I=1, NBCOOR*NBTGS
         RMCN(MN2+I) = RMCN(MN1+I)
 25   CONTINUE
C
C     AJOUT DE LA DATE
      CALL ECDATE( MCN(MNXYZ2) )
C     AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNXYZ2 + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
C
C     CONSTRUCTION DU TMS 'NSEF' DE LA LIGNE INVERSEE
      CALL LXTSOU( NTLXLG2, 'NSEF',  NTNSEF2, MNNSEF2 )
      IF( NTNSEF2 .GT. 0 ) CALL LXTSDS( NTLXLG2, 'NSEF' )
      MONSEF2 = WBARSE + NBEFAP + NBEFTG + NBTGEF*NBEFTG
      CALL LXTNDC( NTLXLG2, 'NSEF', 'MOTS', MONSEF2 )
      CALL LXTSOU( NTLXLG2, 'NSEF',  NTNSEF2, MNNSEF2 )
C
C     COPIE DU DEBUT DU TMS NSEF
      CALL TRTATA( MCN(MNNSEF1), MCN(MNNSEF2), WBARSE+1 )
C
C     CONSTRUCTION POUR UNE LIGNE STRUCTUREE
C     Numero>0 de l''EF a TG sinon 0
      MN2 = MNNSEF2 + WBARSE + 1
      DO 30 I=1,NBEFAP
         MCN( MN2 ) = I
         MN2 = MN2 + 1
 30   CONTINUE
C
C     COPIE INVERSE DE NGEFTG(1..NBEFTG)
C     Numero geometrique de l''EF a TG
      MN1 = MNNSEF1 + WBARSE + NBEFAP + NBEFTG
      MN2 = MNNSEF2 + WBARSE + NBEFAP + 1
      DO 40 I=1,NBEFTG
         MCN( MN2 ) = MCN( MN1 )
         MN1 = MN1 - 1
         MN2 = MN2 + 1
 40   CONTINUE
C
C     COPIE INVERSE DE NUTGEF(1..NBTGEF,1..NBEFTG)
C     +-No des TANGENTES de l''EF a TG
      MN1 = MNNSEF1 + WBARSE + NBEFAP + NBEFTG + NBTGEF*NBEFTG
      MN2 = MNNSEF2 + WBARSE + NBEFAP + NBEFTG + 1
      DO 50 I=1, NBTGEF*NBEFTG
         MCN( MN2 ) = MCN( MN1 )
         MN1 = MN1 - 1
         MN2 = MN2 + 1
 50   CONTINUE
C
C     AJOUT DE LA DATE
      CALL ECDATE( MCN(MNNSEF2) )
C     AJOUT DU NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNNSEF2 + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
C
      RETURN
      END
