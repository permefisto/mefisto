      SUBROUTINE TRPTDC( UNPEPS, NP, PXYD, NT, CETRIA, NONOUI )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LE POINT NP DE PXYD EST IL DANS LE CERCLE CIRCONSCRIT DU
C -----    TRIANGLE NT ?
C
C ENTREES:
C --------
C UNPEPS : 1+EPSILON POUR SEUIL AU DELA DUQUEL LE POINT N"EST PAS DANS
C          LE CERCLE CIRCONSCRIT
C NP     : NUMERO DU POINT DANS PXYD
C PXYD   : TABLEAU DES COORDONNEES 2D DES POINTS
C NT     : NUMERO DU TRIANGLE POUR CETRIA
C CETRIA : COORDONNEES DU CENTRE DU CERCLE CIRCONSCRIT ET CARRE DU RAYON
C
C SORTIE :
C --------
C NONOUI : 1 SI LE POINT NP EST DANS LE CERCLE
C          0 SINON
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JUILLET 1994
C....................................................................012
      DOUBLE PRECISION  UNPEPS
      DOUBLE PRECISION  PXYD(3,*)
      DOUBLE PRECISION  CETRIA(3,*)
      DOUBLE PRECISION  X, Y
C
      X = CETRIA(1,NT) - PXYD(1,NP)
      Y = CETRIA(2,NT) - PXYD(2,NP)
C
      IF( X*X + Y*Y .GT. CETRIA(3,NT)*UNPEPS ) THEN
C        LE POINT N'EST PAS DANS LE CERCLE
         NONOUI = 0
      ELSE
C        LE POINT EST DANS LE CERCLE
         NONOUI = 1
      ENDIF
      END
