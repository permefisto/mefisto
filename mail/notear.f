      SUBROUTINE NOTEAR( NS1,    NS2,
     %                   NBSOTE, NSTETR, NO1TSO, NOTESO,
     %                   NBTEDS, NOTEDS )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LISTER LES TETRAEDRES D'ARETE COMMUNE NS1-NS2
C -----

C ENTREES:
C --------
C NS1    : NUMERO DU PREMIER SOMMET DE L'ARETE
C NS2    : NUMERO DU SECOND  SOMMET DE L'ARETE
C NBSOTE : VALEUR MAXIMALE DE DECLARATION DU PREMIER INDICE DE NSTETR(>3)
C NSTETR : NUMERO DES 4 SOMMETS DE CHAQUE TETRAEDRE
C NO1TSO : NUMERO DU 1-ER TETRAEDRE DANS NOTESO DE CHAQUE SOMMET
C NOTESO : NOTESO(1,*) NUMERO DANS NSTETR DU TETRAEDRE
C          NOTESO(2,*) NUMERO DANS NOTESO DU TETRAEDRE SUIVANT
C                      0 SI C'EST LE DERNIER

C SORTIES:
C --------
C NBTEDS : NOMBRE DE TETRAEDRES AYANT CETTE ARETE COMMUNE
C NOTEDS : NUMERO DANS NSTETR DE CES TETRAEDRES
C      ATTENTION : PAS DE TEST DE DEBORDEMENT SUR NOTEDS
C                  SUPPOSE SUFFISAMMENT LONG
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC    JANVIER 1992
C....................................................................012
      INTEGER           NSTETR(NBSOTE,*),
     %                  NO1TSO(*),
     %                  NOTESO(2,*),
     %                  NOTEDS(*)

      NBTEDS = 0

C     POSITION DANS NOTESO DU 1-ER TETRAEDRE DE SOMMET NS1
      NDT = NO1TSO( NS1 )

C     TANT QU'IL EXISTE UN TETRAEDRE DE SOMMET NS1 FAIRE
 20   IF( NDT .GT. 0 ) THEN

C        LE NUMERO DU TETRAEDRE DANS NSTETR
         NT = NOTESO(1,NDT)

C        LE SOMMET NS2 EST IL UN SOMMET DE NT ?
         DO J=1,4
            IF( NSTETR(J,NT) .EQ. NS2 ) GOTO 28
         ENDDO
         GOTO 29

C        STOCKAGE DU TETRAEDRE
 28      NBTEDS = NBTEDS + 1
         NOTEDS( NBTEDS ) = NT

C        LE TETRAEDRE SUIVANT
 29      NDT = NOTESO(2,NDT)
         GOTO 20
      ENDIF

C     POSITION DANS NOTESO DU 1-ER TETRAEDRE DE SOMMET NS2
      NDT = NO1TSO( NS2 )

C     TANT QU'IL EXISTE UN TETRAEDRE DE SOMMET NS2 FAIRE
 30   IF( NDT .GT. 0 ) THEN

C        LE NUMERO DU TETRAEDRE DANS NSTETR
         NT = NOTESO(1,NDT)

C        LE SOMMET NS1 EST IL UN SOMMET DE NT ?
         DO J=1,4
            IF( NSTETR(J,NT) .EQ. NS1 ) GOTO 36
         ENDDO
         GOTO 39

C        STOCKAGE DU TETRAEDRESI CE N'EST DEJA FAIT
 36      DO J=1,NBTEDS
           IF( NOTEDS(J) .EQ. NT ) GOTO 39
        ENDDO
         NBTEDS = NBTEDS + 1
         NOTEDS( NBTEDS ) = NT

C        LE TETRAEDRE SUIVANT
 39      NDT = NOTESO(2,NDT)
         GOTO 30
      ENDIF

      RETURN
      END
