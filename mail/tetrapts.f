      SUBROUTINE TETRAPTS( NUVOLU, KNMVOLU, QUAMINEX, QTEAME,
     %                     NOSOM1, NOSOM2, NOTEDE,
     %                     MXSOMM, NBSOMM, PTXYZD, NPSOFR,
     %                     HEXAPAVE,NBIPAV,ECHPAV, N1SPAVE, NOPTSUIV,
     %                     INFACO, MXFACO, LEFACO, N1FASC,
     %                     IVOLTE, NVOLTE,
     %                     MXTETR, N1TEVI, NOTETR, N1TETS,
     %                     MXETOI, NFETOI, NTETOI, LISPOI,
     %                     NUDTETR,NBPTSUP, IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :     TETREDRISATION DES POINTS NOSOM1 A NOSOM2
C -----     DU TABLEAU PTXYZD A PARTIR DE LEUR ETOILE

C ENTREES:
C --------
C NUVOLU :  NUMERO DANS LE LEXIQUE DES VOLUMES DU VOLUME A TETRAEDRISER
C KNMVOLU:  NOM DU VOLUME A TETRAEDRISER
C QUAMINEX: QUALITE MINIMALE EXIGEE POUR CREER UN TETRAEDRE
C           C-A-D AU DESSOUS DE LAQUELLE UN TETRAEDRE N'EST PAS CREE
C QTEAME :  QUALITE DES TETRAEDRES AU DESSOUS DE LAQUELLE UNE
C           AMELIORATION DE LA QUALITE DES TETRAEDRES EST DEMANDEE
C NOSOM1 :  NUMERO DU PREMIER SOMMET  A AJOUTER A LA TETRAEDRISATION
C NOTEDE :  NUMERO NOTETR DU TETRAEDRE SUSCEPTIBLE DE CONTENIR LE POINT NOSOM1
C MXSOMM :  NOMBRE MAXIMAL DE SOMMETS PERMIS POUR LA TETRAEDRISATION
C PTXYZD :  X Y Z DISTANCE SOUHAITEE DES POINTS
C HEXAPAVE: MIN ET MAX DES COORDONNEES DU PAVAGE
C NBIPAV :  NOMBRE D'ARETES DANS LA DIRECTION I
C ECHPAV :  ECHELLE DANS LA DIRECTION I
C N1SPAVE:  NO DU 1-ER SOMMET DANS PTXYZD DU PAVE
C NOPTSUIV: NO DU POINT SUIVANT DANS LE CHAINAGE DES POINTS DES PAVES
C MXTETR :  NOMBRE MAXIMAL DE TETRAEDRES DECLARABLES
C NUDTETR:  PLUS GRAND NUMERO DANS NOTETR DE TETRAEDRE ACTIF OCCUPE
C LISPOI :  LISPOI(I)=I ou 0 EN ENTREE POUR PERMETTRE LA PERMUTATION
C           DES POINTS LORSQUE DES TETRAEDRES DEGENERES SONT RENCONTRES
C INFACO : = 0 PAS DE TABLEAU LEFACO NI DE CONSERVATION DES
C              FACES FRONTIERE ( NO DE VOLUME CONNU PAR NVOLTE )
C          = 1 EXISTENCE DU TABLEAU LEFACO ET CONSERVATION DES
C              FACES DE LA FRONTIERE
C MXFACO : NOMBRE MAXIMAL DECLARABLE DE FACES DU CONTOUR
C LEFACO : FACE DU CONTOUR OU INTERFACES ENTRE VOLUMES
C          IL CONTIENT DANS CET ORDRE
C          NUMERO (DANS PTXYZD) DU SOMMET 1, SOMMET 2, SOMMET 3
C          NUMERO (DANS NUVOPA 0 SINON) DU VOLUME1 , VOLUME2 DE LA FACE
C          NUMERO (DANS LEFACO) DE LA FACE ADJACENTE PAR L'ARETE 1 2 3
C
C          ATTENTION: UNE ARETE PEUT APPARTENIR A PLUS DE 2 FACES
C          => CHAINAGE CIRCULAIRE DE CES FACES DANS LEFACO
C          LEFACO(9,*)  -> FACE SUIVANTE (*=0:VIDE, *<>0:NON VIDE)
C          HACHAGE AVEC LA SOMME DES 3 SOMMETS MODULO MXFACO
C          LF = MOD( NOSOFA(1)+NOSOFA(2)+NOSOFA(3) , MXFACO ) + 1
C          NF = LEFACO( 10, LF ) LE NUMERO DE LA 1-ERE FACE DANS LEFACO
C          SI LA FACE NE CONVIENT PAS. PASSAGE A LA SUIVANTE
C          NF  = LEFACO( 9, NF )  ...
C          11: >0  NO NOTETR D'UN TETRAEDRE AYANT CETTE FACE,
C              =0  SINON
cccC          12: = NO FACEOC DE 1 A NBFACES D'OC
C N1FASC : N1FASC(NS)=NUMERO (DANS LEFACO) D'UNE FACE DE SOMMET NS
C IVOLTE : 0 PAS       DE TABLEAU NVOLTE A L'APPEL
C          1 EXISTENCE DU TABLEAU NVOLTE A L'APPEL
C NVOLTE : >0 NUMERO DU VOLUME DE 1 A NBVOPA DE CHAQUE TETRAEDRE
C          -1 SI TETRAEDRE INACTIF ou SUPPRIME

C ENTREES ET SORTIES :
C --------------------
C NBSOMM : NOMBRE DE SOMMETS ACTUELS DANS PTXYZD
C NOSOM2 : NUMERO DU DERNIER SOMMET  A AJOUTER A LA TETRAEDRISATION
C          LE TRAITEMENT DES TETRAEDRES DEGENERES PEUT AJOUTER DES POINTS
C NPSOFR : NUMERO DES POINTS INITIAUX
C          LE SIGNE DEVIENT NEGATIF SI LE SOMMET EST DEPLACE
C          =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C                    LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -1 SI LE POINT EST SOMMET D'OT ET RECONNU TROP PROCHE
C          = -4 SI LE POINT EST SOMMET D'OT NON TROP PROCHE
C          = -3 SI LE POINT EST SOMMET D'OT REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT I DEPLACE SUR LA SURFACE
C             DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE
C N1TEVI : NUMERO DU 1 PREMIER TETRAEDRE VIDE DANS LE TABLEAU NOTETR
C          LE CHAINAGE DES TETRAEDRES VIDES SE FAIT SUR NOTETR(5,.)
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C N1TETS : N1TETS(I)=NUMERO D'UN TETRAEDRE AYANT POUR SOMMET I
C                    0 SI LE SOMMET N'A PAS ETE TETRAEDRISE

C TABLEAUX AUXILIAIRES :
C ----------------------
C MXETOI : NOMBRE MAXIMAL DE FACES OU TETRAEDRES DECLARABLES
C          DANS UNE ETOILE
C NFETOI : ENTIER (1:5,1:MXETOI)
C NTETOI : ENTIER (1:MXETOI)

C SORTIES:
C --------
C NBPTSUP: NOMBRE DE POINTS NON TETRAEDRISES et SUPPRIMES AVANT et APRES
C NUDTETR: NUMERO DU DERNIER TETRAEDRE OCCUPE DANS NOTETR
C IERR   : 0 SI PAS D'ERREUR
C          1 SATURATION DES SOMMETS
C          3 SATURATION DES TETRAEDRES
C          4 TETRAEDRE DEGENERE
C          8 AUCUNE SPHERE CIRCONSCRITE AUX TETRAEDRES ACTUELS
C            NE CONTIENT LE POINT COURANT
C         10 AUCUN POINT DEPLACE DANS L'ETOILE NE CONVIENT
C         11 ETOILE DU POINT N IMPOSSIBLE A FORMER
C         12 PAS DE TETRAEDRE POUR DEMARER
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  ANALYSE NUMERIQUE PARIS UPMC       Mars 1993
C MODIFS : ALAIN PERRONNET Saint Pierre du Perray          Decembre 2018
C....................................................................012
      PARAMETER        (QUAMED=0.005)
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      DOUBLE PRECISION  DMCN(1)
      EQUIVALENCE      (MCN(1),RMCN(1),DMCN(1))

      include"./incl/langue.inc"
      include"./incl/trvari.inc"
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE, TRACTE0

      CHARACTER*24      KNMVOLU
      INTEGER           LEFACO(1:11,0:MXFACO), N1FASC(1:MXSOMM),
     %                  NVOLTE(*),
     %                  NOTETR(8,MXTETR), N1TETS(MXSOMM),
     %                  LISPOI(0:MXSOMM), NPSOFR(MXSOMM),
     %                  NFETOI(5,MXETOI), NTETOI(MXETOI)
      DOUBLE PRECISION  PTXYZD(1:4,1:MXSOMM)
      DOUBLE PRECISION  HEXAPAVE(3,2), ECHPAV(3)
      INTEGER           NBIPAV(3), N1SPAVE(0:*), NOPTSUIV(1:*)
      CHARACTER*120     KTITRE
      DOUBLE PRECISION  VOLET0, VOLET1, VOLMOY, VTE, VOLTET

      IF( NOSOM2-NOSOM1 .GT. 100 ) THEN
         PRINT*
         PRINT*,'tetrapts: VOLUME: ',KNMVOLU,
     %          ' TETRAEDRISATION des POINTS',NOSOM1,' a ',NOSOM2
      ENDIF

      TRACTE0 = TRACTE

C     TOUTES LES FACES DE L'ETOILE SONT CONSIDEREES VIDES
      CALL ETOIVIDE( MXETOI, N1FEOC, N1FEVI, NFETOI )

      N1FEVI = 1
      N1FEOC = 0
      DO I=1,MXETOI
cccC        NUMERO DU TETRAEDRE DE L'AUTRE COTE DE LA FACE
cccC        NFETOI(1,I) = 0
cccC        NUMERO LOCAL AU TETRAEDRE DE LA FACE
cccC        NFETOI(2,I) = 0
C        NUMERO DE LA FACE DANS LEFACO
         NFETOI(4,I) = 0
C        NUMERO DANS NFETOI DE LA FACE SUIVANTE
         NFETOI(5,I) = I+1
      ENDDO
      NFETOI(5,MXETOI) = 0


C     DERNIER POINT A TETRAEDRISER ET POSSIBLE A PERMUTER POUR
C     TRAITER UN POINT DIFFICILEMENT TETRAEDRISABLE
      NDERPT = NOSOM2
      NBPTSU0= NBPTSUP

C     LE TETRAEDRE INITIAL DE RECHERCHE A POUR SOMMET NDERST
C     DERNIER PRECEDENT SOMMET TETRAEDRISE
      N0     = NOSOM1 -1
      NDERST = N0

C     NUMERO NOTETR DU TETRAEDRE SUSCEPTIBLE DE CONTENIR LE POINT NOSOM1
      NOTEIN = NOTEDE

 10   IF( NOTEIN .GT. 0 ) THEN
         IF( NOTETR( 1, NOTEIN ) .GT. 0 ) THEN
C           LE VOLUME ACTUEL DU TETRAEDRE NOTEIN
            VTE = VOLTET( PTXYZD(1,NOTETR(1,NOTEIN)),
     %                    PTXYZD(1,NOTETR(2,NOTEIN)),
     %                    PTXYZD(1,NOTETR(3,NOTEIN)),
     %                    PTXYZD(1,NOTETR(4,NOTEIN))  )
            IF( VTE .GT. 0D0 ) THEN
               GOTO 100
            ENDIF
         ENDIF
      ENDIF

C     ESSAI D'UN NOUVEAU SOMMET NDERST TETRAEDRISE
      NDERST = NDERST - 1
      IF( NDERST .LE. 0 ) THEN
C        PAS DE TETRAEDRE DE DEPART
         PRINT*,'tetrapts: ANOMALIE 0 PAS DE TETRAEDRE DE DEPART'
         IERR = 12
         GOTO 9999
      ENDIF

      NOTEIN = N1TETS( NDERST )
      GOTO 10

C     ==========================================
C     TETRAEDRISATION DES POINTS NOSOM1 A NOSOM2
C     ==========================================
 100  TRACTE0 = TRACTE
      N0 = N0 + 1
      IF( N0 .LE. NOSOM2 ) THEN

         IF( MOD(N0,10000) .EQ. 0 ) THEN
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'tetrapts: TETRAEDRISATION du POINT',N0
            ELSE
               PRINT*,'tetrapts: TETRAHEDRIZATION of POINT',N0
            ENDIF
         ENDIF

C        LE VERITABLE NUMERO PTXYZD DU POINT N0 A TRAITER
 110     NPt = LISPOI( N0 )
ccc         PRINT*,'TETRA POINT ', N0,':',NPt,
ccc     %   ' X=',PTXYZD(1,NPt),' Y=',PTXYZD(2,NPt),' Z=',PTXYZD(3,NPt)

         IF( NPt .LE. 6 ) THEN
C           LES 6 SOMMETS DE L'OCTAEDRE ENGLOBANT NE PEUVENT ETRE SUPPRIMES
            GOTO 100
         ENDIF

ccc         if( npt .eq. 9284 ) then
ccc            tracte = .true.
ccc         endif

C        POUR LA TETRAEDRISATION DU POINT NPt
C        LE 1-ER TETRAEDRE A CONSULTER EST LE DERNIER CONSTRUIT
 120     IF( NOTEIN .GT. 0 ) THEN
            IF( NOTETR(1,NOTEIN) .GT. 0 ) THEN
C              LE TETRAEDRE NOTEIN EST CORRECT
C              LE VOLUME ACTUEL DU TETRAEDRE NOTEIN
               VTE = VOLTET( PTXYZD(1,NOTETR(1,NOTEIN)),
     %                       PTXYZD(1,NOTETR(2,NOTEIN)),
     %                       PTXYZD(1,NOTETR(3,NOTEIN)),
     %                       PTXYZD(1,NOTETR(4,NOTEIN))  )
               IF( VTE .GT. 0D0 ) THEN
                  GOTO 150
               ENDIF
            ENDIF
         ENDIF

C        LE TETRAEDRE NOTEIN EST INCORRECT ou VIDE
C        ESSAI D'UN NOUVEAU SOMMET POUR TROUVER UN TETRAEDRE DE DEPART
C        CORRECT ET ASSEZ PROCHE DU POINT N A TETRAEDRISER
         NDERST = NDERST - 1
         IF( NDERST .LE. 0 ) THEN
C           PAS DE TETRAEDRE CORRECT DE DEPART
            PRINT*,'tetrapts: ANOMALIE1 PAS DE TETRAEDRE DE DEPART'
            PRINT*,'tetrapts: Point NPt=',NPt,
     %                      ' XYZD=',(PTXYZD(K,NPt),K=1,4)
            IERR = 12
            GOTO 9999
         ENDIF
         NOTEIN = N1TETS( NDERST )
         GOTO 120

C        *****************************************************************
C        FORMATION DE L'ETOILE des NbTNPt TETRAEDRES de BOULE CIRCONSCRITE
C        CONTENANT LE POINT NPt et des FACES SIMPLES NFETOI du POINT NPt.
C        Puis, CREATION des TETRAEDRES de SOMMET NPt
C        *****************************************************************
 150     IF( INFACO .NE. 0 ) THEN

C           PRESENCE DU TABLEAU LEFACO DES TRIANGLES FRONTIERE A RESPECTER
            CALL TETRA1PT( NUVOLU, KNMVOLU, QUAMINEX, NPt, NOTEIN,
     %                     MXSOMM, PTXYZD, NPSOFR,
     %                     HEXAPAVE,NBIPAV,ECHPAV, N1SPAVE,NOPTSUIV,
     %                     MXTETR, N1TEVI, NUDTETR,NOTETR, N1TETS,
     %                     INFACO, MXFACO, LEFACO, N1FASC,
     %                     IVOLTE, NVOLTE,
     %                     MXETOI, N1FEOC, N1FEVI, NFETOI,
     %                     NbTNPt, NTETOI, VOLET0, VOLET1 )

         ELSE

C           PRESENCE ou NON du TABLEAU NVOLTE D'UN VOLUME MULTI-MATERIAUX
C           A RESPECTER
            CALL TETRA1ST( KNMVOLU, NPt,   NOTEIN,
     %                     MXSOMM, PTXYZD, NPSOFR,
     %                     HEXAPAVE,NBIPAV,ECHPAV, N1SPAVE,NOPTSUIV,
     %                     MXTETR, N1TEVI, NUDTETR,NOTETR, N1TETS,
     %                     INFACO, MXFACO, LEFACO, N1FASC,
     %                     IVOLTE, NVOLTE,
     %                     MXETOI, N1FEOC, N1FEVI, NFETOI,
     %                     NbTNPt, NTETOI, VOLET0, VOLET1 )

         ENDIF

C        BILAN de la TETRAEDRISATION du POINT NPt
C        ========================================
         IF( NbTNPt .LE. 0 ) THEN

C           LE POINT NPt N'EST PAS TETRAEDRISE
C           ----------------------------------
            IF( NPSOFR(NPt) .EQ. -4 .OR. NPSOFR(NPt) .EQ. 0 ) THEN

C              LE POINT NPt  EST SUPPRIME
ccc            PRINT*,'tetrapts: TETRAEDRISATION ABANDONNEE du POINT',
ccc     %              NPt,' de POSITION FRONTIERE NPSOFR=',NPSOFR(NPt)
ccc            PRINT*
               N1TETS( NPt ) = 0
               IF( NPSOFR(NPt) .EQ. -4 ) NPSOFR(NPt) = -3
               NBPTSUP = NBPTSUP + 1
               GOTO 100

            ELSE

C              TENTATIVE DE PERMUTER LE POINT N0 AVEC UN AUTRE NON TRAITE
C              POUR RETARDER LA TETRAEDRISATION DU POINT NPt
 160           IF( NDERPT .GT. N0 ) THEN
                  IF( LISPOI(NDERPT) .LE. 0 ) THEN
C                    POINT DE PERMUTATION INUTILISABLE
                     NDERPT = NDERPT - 1
                     GOTO 160
                  ENDIF
                  IF( LISPOI(N0) .EQ. LISPOI(NDERPT) ) GOTO 160
C                 LE POINT N0 EST PERMUTE AVEC NDERPT POUR ETRE TRAITE PLUS TARD
                  IF( LANGAG .EQ. 0 ) THEN
                     PRINT 10160, N0,LISPOI(N0),LISPOI(NDERPT)
                  ELSE
                     PRINT 20160, N0,LISPOI(N0),LISPOI(NDERPT)
                  ENDIF
10160 FORMAT(' tetrapts: N0=',I8,' PERMUTATION des POINTS',2I8)
20160 FORMAT(' tetrapts: N0=',I8,' PERMUTATION of POINTS',2I8)
                  LISPOI(N0)     = LISPOI(NDERPT)
                  LISPOI(NDERPT) = NPt
                  NDERPT = NDERPT - 1
                  GOTO 110
               ENDIF

            ENDIF

C           PROBLEME: POINT NPt NON TETRAEDRISABLE
            IF( LANGAG .EQ. 0 ) THEN
               PRINT*,'tetrapts: PROBLEME au POINT ',NPt,
     %                ' SANS ETOILE => ABANDON de la TETRAEDRISATION'
            ELSE
               PRINT*,'tetrapts PROBLEM: POINT ',NPt,
     %                ' WITHOUT STAR => WITHDRAWAL'
            ENDIF
            PRINT*,'XYZD(',NPt,')=',(PTXYZD(I,NPt),I=1,4)
            IERR = 11
            GOTO 9999

         ENDIF

C        LE POINT NPt EST TETRAEDRISE
C        ----------------------------
C        LISTAGE DES NbTNPt TETRAEDRES DE SOMMET NPt
         CALL TETR1S( NPt,    N1TETS, NOTETR,
     %                NbTNPt, MXETOI, NTETOI, IERR )

C        VERIFICATION DES TETRAEDRES OPPOSES
         CALL VEOPTE( NbTNPt, NTETOI, NOTETR, PTXYZD, NBFANR )

         IF( NBFANR .GT. 0 ) THEN
C           IL RESTE DES FACES DE TETRAEDRE OPPOSE NON RETROUVE
            PRINT*,'tetrapts: PROBLEME NPt=',NPt,' avec',NBFANR,
     %             ' FACES de TETRAEDRE OPPOSE NON RETROUVE'
            GOTO 910
         ENDIF

C        CALCUL QUALITES MIN MOYENNE et VOLUMES des TETRAEDRES DE SOMMET NPt
         CALL QUALGRTE( PTXYZD, MXTETR, NOTETR, NbTNPt, NTETOI,
     %                  QUAMED, NBTMED, QUAMIN0,QUAMOY0, VOLET0, VOLMOY)

         IF( QUAMIN0 .GT. QTEAME ) GOTO 910

         IF( NbTNPt .GT. 0 ) GOTO 910


C      AU DESSOUS INUTILE    TEST POUR EVITER L'AMELIORATION...
C==============================================================

C        TENTATIVE D'AMELIORATION DES NbTNPt TETRAEDRES DE SOMMET NPt
C        ------------------------------------------------------------
C        TRACE DES TETRAEDRES DE L'ETOILE AVANT AMELIORATION
      KTITRE='tetrapts: NPt=          NbTNPt=         TETRAEDRES QUAMIN=
     %               QUAMOY=               AVANT AMELIORATION'
         WRITE(KTITRE(15:22),'(I8)') NPt
         WRITE(KTITRE(33:37),'(I5)') NbTNPt
         WRITE(KTITRE(59:72),'(G14.6)') QUAMIN0
         WRITE(KTITRE(81:94),'(G14.6)') QUAMOY0
         CALL SANSDBL( KTITRE, NBC )
         CALL TRACEETOILE( KTITRE(1:NBC), PTXYZD, NOTETR, NPt,
     %                     NbTNPt, NTETOI )

C        TENTATIVE d'AMELIORATION DE LA QUALITE du GROUPE DE TETRAEDRES
C        DE SOMMET NPt NOTETR en TRAITANT PAR DECOMPOSITION LES TETRAEDRES
C        DE QUALITE MEDIOCRE
         CALL AMQGRTE( QTEAME, MXSOMM, NBSOMM, PTXYZD, NPSOFR,
     %                 INFACO, MXFACO, LEFACO, IVOLTE, NVOLTE,
     %                 MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                 MXETOI, NbTNPt, NTETOI,
     %                 MODIFTET, IERR )

         IF( MODIFTET .LE. 0 ) THEN
C           PAS D'AMELIORATION NI DE MODIFICATION
            GOTO 910
         ENDIF

C        AMELIORATION: DES TETRAEDRES ONT ETE MODIFIES
C        LE POINT NPt A T IL ETE SUPPRIME?
         NT1 = N1TETS( NPt )
         IF( NT1 .LE. 0 ) THEN

C           OUI: SORTIE DIRECTE
            GOTO 910

         ELSE

C           NON: VERIFICATION DU PREMIER TETRAEDRE DE SOMMET NPt
            IF( NOTETR(1,NT1) .GT. 0 ) THEN
               GOTO 900
            ENDIF

C           PROBLEME: N1TETS DU POINT NPt POINTE SUR UN TETRAEDRE VIDE
C           EXISTE T IL DANS LE TABLEAU NTETOI UN TETRAEDRE DE SOMMET NPt
C           QUI N'AIT ETE SUPPRIME?
            CALL TETR1S( NPt,    N1TETS, NOTETR,
     %                   NbTNPt, MXETOI, NTETOI, IERR)
            DO K = 1, NbTNPt
               NT1 = NTETOI( K )
               IF( NT1 .GT. 0 .AND. NOTETR(1,NT1) .GT. 0 ) THEN
C                 OUI: NPt EST UN SOMMET DE NT1
                  N1TETS( NPt ) = NT1
                  GOTO 900
               ENDIF
            ENDDO

C           NON: AUCUN TETRAEDRE DE SOMMET NPt => PROBLEME
            PRINT*,'tetrapts: PROBLEME N1TETS(',NPt,')=',
     %              NT1,' est un TETRAEDRE VIDE'
            PRINT*,'NOTETR(',NT1,')=',(NOTETR(K,NT1),K=1,8)
            GOTO 910

         ENDIF

C        LISTAGE DES NbTNPt TETRAEDRES FINAUX DE SOMMET NPt
C        --------------------------------------------------
 900     CALL TETR1S( NPt, N1TETS, NOTETR, NbTNPt, MXETOI, NTETOI, IERR)

C        VERIFICATION DES TETRAEDRES OPPOSES
         CALL VEOPTE( NbTNPt, NTETOI, NOTETR, PTXYZD, NBFANR )

         IF( NBFANR .GT. 0 ) THEN
C           IL RESTE DES FACES DE TETRAEDRE OPPOSE AVEC PROBLEME
            PRINT*,'tetrapts: Probleme NPt=',NPt,' avec',NBFANR,
     %          ' FACES de TETRAEDRE OPPOSE AVEC PROBLEME'
         ENDIF

C        BILAN QUALITES MIN MOYENNE et VOLUMES des TETRAEDRES DE SOMMET NPt
         CALL QUALGRTE( PTXYZD, MXTETR, NOTETR, NbTNPt, NTETOI,
     %                  QUAMED, NBTMED, QUAMIN1,QUAMOY1, VOLET1, VOLMOY)

C        TRACE DES NbTNPt TETRAEDRES DE SOMMET NPt
         KTITRE='tetrapts: NPt=          NbTNPt=         TETRAEDRES QUAM
     %IN=               QUAMOY=               APRES AMELIORATION'
         WRITE(KTITRE(15:22),'(I8)') NPt
         WRITE(KTITRE(33:37),'(I5)') NbTNPt
         WRITE(KTITRE(59:72),'(G14.6)') QUAMIN1
         WRITE(KTITRE(81:94),'(G14.6)') QUAMOY1
         CALL SANSDBL( KTITRE, NBC )
         CALL TRACEETOILE( KTITRE(1:NBC), PTXYZD, NOTETR, NPt,
     %                     NbTNPt, NTETOI )


C        NO DE SOMMET-POINT DERNIER TETRAEDRISE CORRECTEMENT
 910     NDERST = NPt
         NOTEIN = N1TETS( NPt )

C        PASSAGE AU POINT NPt SUIVANT
         TRACTE = TRACTE0
         GOTO 100

      ENDIF

C=======================================================================
C     FIN DE TETRAEDRISATION DU JEU DE POINTS NOSOM1 A NOSOM2
C=======================================================================
 9999 IF( IERR .NE. 0 ) THEN
         PRINT *,'tetrapts: de',NOSOM1,' a',NOSOM2,
     %           ' et SORTIE avec IERR=',IERR,' VU AUPARAVANT'
ccc      ELSE
ccc         PRINT *,'tetrapts:',NOSOM2-NOSOM1+1-NBPTSUP+NBPTSU0,
ccc     %           ' POINTS TETRAEDRISES de',NOSOM1,' a',NOSOM2
      ENDIF

      RETURN
      END
