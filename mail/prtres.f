      SUBROUTINE PRTRES( NBTETR , NBITER , LRESUL , TCPU , VOLTOT )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    SAUVEGARDER LES STATISTIQUES D'EXECUTION DE DELAUNAY
C -----
C ENTREES:
C --------
C NBTETR : NOMBRE TOTAL DE TETRAEDRES APRES SUPPRESSION DES EXTERIEURS
C NBITER : NOMBRE D'ITERATIONS DE DELAUNAY
C LRESUL : RESULTATS DE L'ITERATION I DE L'EXECUTION DE DELAUNAY
C          LRESUL(1,I) : NOMBRE DE SOMMETS A TRAITER AU DEBUT
C          LRESUL(2,I) : NOMBRE DE SOMMETS TRAITES REELLEMENT
C          LRESUL(3,I) : NOMBRE DE BARYCENTRES CREES
C          LRESUL(4,I) : NOMBRE TOTAL DE FACES DES INTERFACES ET FRONTIERE
C          LRESUL(5,I) : NOMBRE DE FACES PERDUES DE S
C          LRESUL(6,I) : NOMBRE DE FACES PERDUES APRES 2F => 2F
C          LRESUL(7,I) : NOMBRE D'ARETES RETROUVEES PAR 2T => 3T
C          LRESUL(8,I) : NOMBRE DE FACES PERDUES APRES LIERCF
C          LRESUL(9,I) : NOMBRE DE FACES PERDUES APRES 3T => 2T
C          LRESUL(10,I): NOMBRE DE MILIEUX A AJOUTER
C TCPU   : TEMPS CPU EN SECONDES POUR L'ETAPE DE CALCUL
C          TCPU(1,I) : EXECUTION PREPARATION DE L'OTT ET ETOILE
C          TCPU(2,I) : EXECUTION CREATION DES BARYCENTRES DELONG
C          TCPU(3,I) : EXECUTION RECENSEMENT DES FACES PERDUES
C          TCPU(4,I) : EXECUTION CHANGEMENT 2F => 2F
C          TCPU(5,I) : EXECUTION CHANGEMENT 2T => 3T
C          TCPU(6,I) : EXECUTION LIERCF
C          TCPU(7,I) : EXECUTION CHANGEMENT 3T => 2T
C          TCPU(8,I) : EXECUTION AJOUT DES MILIEUX AJOMIL
C VOLTOT : VOLUME TOTAL DE L'OBJET MAILLE
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET ANALYSE NUMERIQUE PARIS  DECEMBRE 1987
C............................................................................
      COMMON / UNITES / LECTEU , IMPRIM , INTERA , NUNITE(29)
      include"./incl/nmproj.inc"
      include"./incl/homdir.inc"
      CHARACTER         KDATE*8,KHEURE*8,KCREAT*24,KINFO*80, KAUX*80
CCC      CHARACTER*72      KDELON
      INTEGER           LRESUL(10,NBITER)
      REAL              TCPU  (8,NBITER)
      DOUBLE PRECISION  VOLTOT
C
C     LE NOMBRE DE SOMMETS TETRAEDRISES
      NBSAVE = LRESUL(1,NBITER)
      IF( NBSAVE .LE. 0 ) RETURN
C
C     OUVERTURE DU FICHIER
C     RECHERCHE D'UN NUMERO LIBRE D'UNITE LOGIQUE
CCC      CALL TRUNIT( NFIC )
CCC      I = INDEX( NMPROJ , ' ' )
CCC      I = MIN( I , 17 )
CCC      KDELON = HOMDIR // '/../jobs/delon/' // NMPROJ(1:I-1)
CCC      OPEN ( FILE=KDELON,
CCC     %       UNIT=NFIC,
CCC     %       STATUS='UNKNOWN',
CCC     %       FORM='FORMATTED',ACCESS='SEQUENTIAL',ERR=9000 )

C     LA PLACE POUR LE TITRE
CCC      WRITE(NFIC  ,10000) NMPROJ
      WRITE(IMPRIM,10000) NMPROJ
10000 FORMAT(130('#')//' TITRE :',A/)

C     DATE
      KAUX = KINFO( 'DATE' )
      KDATE(1:8) = KAUX(1:8)

C     HEURE
      KAUX = KINFO( 'HEURE' )
      KHEURE(1:8) = KAUX(1:8)

C     NOM DE L'UTILISATEUR
      KAUX = KINFO( 'UTILISATEUR' )
      KCREAT(1:24) = KAUX(1:24)

10001 FORMAT(' DATE  :',A8,'   ',A2,'H ',A2,'M ',A2,'S ')
      WRITE (IMPRIM,10001)KDATE(1:8),KHEURE(1:2),KHEURE(3:4),KHEURE(5:6)
CCC      WRITE (NFIC  ,10001)KDATE(1:8),KHEURE(1:2),KHEURE(3:4),KHEURE(5:6)

10003 FORMAT(' AUTEUR:',A/)
      WRITE (IMPRIM,10003) KCREAT
CCC      WRITE (NFIC  ,10003) KCREAT
C
C     LE NOMBRE DE TETRAEDRES
      WRITE(IMPRIM,10004) NBTETR
CCC      WRITE(NFIC  ,10004) NBTETR
10004 FORMAT(' NOMBRE DE TETRAEDRES APRES SUPPRESSION DES TETRAEDRES EXT
     %ERIEURS:',2I8)
C
      IF( NBITER .LE. 0 ) RETURN
      IF( NBTETR .GT. 0 ) THEN
         WRITE(IMPRIM,10010) VOLTOT
CCC         WRITE(NFIC  ,10010) VOLTOT
10010 FORMAT(' VOLUME DES TETRAEDRES DE L''OBJET   :',G14.6)
      ENDIF
C
      WRITE(IMPRIM,10020)
CCC      WRITE(NFIC  ,10020)
10020 FORMAT(1X,155(1H-)/' I',11(12X,' I')/
     %' I NOMBRE      I SOMMETS     I SOMMETS     I BARYCENTRES I  FACES
     %      I FACES       I FACES APRES I ARETES      I FACES       I  F
     %ACES      I MILIEUX     I'/
     %' I ITERATIONS  I AVANT       I APRES       I AJOUTES     I FRONTI
     %ERE   I PERDUES     I CHANGEMENT  I RETROUVEES  I PERDUES     I PE
     %RDUES     I AJOUTES     I'/
     %' I ALGORITHME  I DELAUNAY    I DELAUNAY    I TETRAEDRES  I INTERF
     %ACES  I APRES       I DES         I APRES       I APRES       I AP
     %RES       I             I'/
     %' I DELAUNAY    I             I             I TROP GRANDS I
     %      I DELAUNAY    I DIAGONALES  I 2T => 3T    I LIERCF      I 3T
     % => 2T    I             I'/
     %' I',11(12X,' I')/1X,155(1H-))
C
      DO 100 I=1,NBITER
         WRITE(IMPRIM,10100) I,(LRESUL(J,I),J=1,10)
CCC         WRITE(NFIC  ,10100) I,(LRESUL(J,I),J=1,10)
 100  CONTINUE
10100 FORMAT(' I',11(I10,2X,' I')/1X,155(1H-))
C
      WRITE(IMPRIM,10200)  KINFO('MACHINE')
CCC      WRITE(NFIC  ,10200)  KINFO('MACHINE')
10200 FORMAT(//' LES TEMPS CPU EN SECONDES SUR LA MACHINE ',A,/
     %1X,127(1H-)/' I',9(12X,' I')/' I',
     %' ITERATION  ',' I',' DELAUNAY   ',' I',' BARYCENTRES',' I',
     %'FACES PERDUES','I',' 2F => 2F   ',' I',' 2T <-> 3T  ',' I',
     %' LIERCF     ',' I',' 3T => 2T   ',' I',' MILIEUX    ',' I'/
     %9(' I',12X),' I'/1X,127(1H-))
C
      DO 300 I=1,NBITER
         WRITE(IMPRIM,10300) I,(TCPU(J,I),J=1,8)
CCC         WRITE(NFIC  ,10300) I,(TCPU(J,I),J=1,8)
 300  CONTINUE
10300 FORMAT(' I',I12,' I',8(F12.0,' I')/1X,127(1H-))
C
C     TOTAL CPU PAR RUBRIQUE
10310 FORMAT(1X,127(1H=)/' I  TOTAUX     I',8(F12.0,' I')/1X,127(1H=))
      DO 310 I=1,8
         TCPU(I,NBITER+1) = 0
 310  CONTINUE
      DO 330 I=1,8
         DO 320 J=1,NBITER
            TCPU(I,NBITER+1) = TCPU(I,NBITER+1) + TCPU(I,J)
 320     CONTINUE
 330  CONTINUE
      WRITE(IMPRIM,10310) (TCPU(J,NBITER+1),J=1,8)
CCC      WRITE(NFIC  ,10310) (TCPU(J,NBITER+1),J=1,8)
C
      WRITE(IMPRIM,10400) TCPU(7,NBITER),TCPU(8,NBITER)
CCC      WRITE(NFIC  ,10400) TCPU(7,NBITER),TCPU(8,NBITER)
10400 FORMAT(
     %' TEMPS CPU DE LA DESTRUCTION DES TETRAEDRES EXTERNES   ',F10.0/
     %' TEMPS CPU DE LA GENERATION  DES TABLEAUX   DU MAILLAGE',F10.0/)
C
C     LE TEMPS CPU TOTAL
      TECPU = 0.
      DO 500 I=1,8
         DO 450 J=1,NBITER
            TECPU = TECPU + TCPU(I,J)
 450     CONTINUE
 500  CONTINUE
C     LE NOMBRE DE SOMMETS APRES ELIMINATION DE L'EXTERIEUR
      NBSAPE = LRESUL(2,NBITER)
      IF( TECPU .NE. 0.0 ) THEN
         I = NINT( FLOAT(NBSAPE) / TECPU )
         J = NINT( FLOAT(NBTETR) / TECPU )
         WRITE(IMPRIM,10500) TECPU,NBSAPE,I,NBTETR,J
CCC         WRITE(NFIC  ,10500) TECPU,NBSAPE,I,NBTETR,J
      ENDIF
10500 FORMAT(
     %' NOMBRE TOTAL DE SECONDES CPU',F10.0/
     %' NOMBRE FINAL DE SOMMETS     ',I10,' SOIT',I6,' ST PAR SECONDE'/
     %' NOMBRE FINAL DE TETRAEDRES  ',I10,' SOIT',I6,' EF PAR SECONDE'/
     % 155('#'))
C
CCC      CLOSE(UNIT=NFIC)
      RETURN
CCCC
CCCC     ERREUR A L'OUVERTURE
CCC 9000 WRITE(IMPRIM,*) ' ERREUR A L''OPEN DU FICHIER delon/',NMPROJ
      END
