      SUBROUTINE LIEXW4( NBARSE, NBSOLI, NBEFTG,
     S                   NARETE, NUTINI, SOMINI, LPEFAA,
     S                   INDEX,  NUTFIN, SOMFIN, LPEFAN,  IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RECHERCHE ET IDENTIFICATION DES SOMMETS COMMUNS AUX ARETES
C -----    D'UNE LIGNE NON STRUCTUREE ET RESTRUCTURATION
C          LES TANGENTES NE SONT PAS RESTRUCTUREES MAIS REMISE EN ORDRE
C          ATTENTION : SI LA LIGNE EST FERMEE (DONC NON STRUCTUREE)
C                      LES TABLEAUX XYZSOMMET ET NSEF ONT ETE MODIFIES
C
C ENTREES:
C --------
C NBARSE : NOMBRE D'ARETES   DE LA LIGNE A STRUCTURER
C NBSOLI : NOMBRE DE SOMMETS DE LA LIGNE A STRUCTURER (>=NBARSE)
C NBEFTG : NOMBRE D'EF A TG  DE LA LIGNE A STRUCTURER
C NARETE : NUMEROS DES 2 SOMMETS DES ARETES DE LA LIGNE
C NUTINI : NUMEROS INITIAUX DES 2 TANGENTES DE CHAQUE ARETE
C SOMINI : 3 COORDONNEES DES SOMMETS INITIAUX DES ARETES
C LPEFAA : ANCIEN NO POINTEUR SUR L'EF (ARETE) A TG, 0 SINON
C
C SORTIES:
C --------
C NBSOLI : NOMBRE ACTUALISE DE SOMMETS DE LA LIGNE STRUCTUREE OU FERMEE
C INDEX  : ATTENTION DECLARATION INDEX(1:3,NBSOLI>=NBARSE)
C          INDEX(1,ANCIEN  NO ARETE)=NOUVEAU NO ARETE
C          INDEX(2,NOUVEAU NO ARETE)=ANCIEN  NO ARETE
C          INDEX(3,ANCIEN  NO ARETE)=NO DE 1 A 2 DU PREMIER SOMMET
C                                    POUR LA NOUVELLE ARETE STRUCTUREE
C NUTFIN : NUMEROS FINAUX DES 2 TANGENTES DE CHAQUE ARETE
C SOMFIN : 3 COORDONNEES DES SOMMETS FINAUX DES ARETES (ORDRE STRUCTURE)
C LPEFAN : NOUVEAU NO POINTEUR SUR L'EF (ARETE) A TG, 0 SINON
C IERR   : 0 SI PAS D'ERREUR
C          1 ERREUR LIGNE EN PLUSIEURS MORCEAUX
C          2 LIGNE FERMEE MAIS NON STRUCTUREE CAR DERNIERE ARETE N->1
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PASCAL JOLY     UPMC ANALYSE NUMERIQUE PARIS    DECEMBRE 1988
C MODIFS : PERRONNET ALAIN UPMC ANALYSE NUMERIQUE PARIS    MAI      1996
C.......................................................................
      include"./incl/gsmenu.inc"
      REAL     SOMINI(3,NBSOLI), SOMFIN(3,NBSOLI)
      INTEGER  NUTINI(2,NBEFTG), NUTFIN(2,NBEFTG)
      INTEGER  INDEX(3,NBSOLI),  NARETE(2,NBARSE)
      INTEGER  LPEFAA(NBARSE),   LPEFAN(NBARSE)
      INTEGER  INV(2)
      DATA INV/2,1/
CCC
CCCC     CI-DESSOUS FAUX CAR 2 SOMMETS IDENTIQUES DE NUMEROS DIFFERENTS SONT POS
CCCC     LA LIGNE EST ELLE SUSCEPTIBLE D'ETRE OUVERTE (OU FERMEE)
CCCC     C'EST A DIRE NOMBRE DE SOMMETS = NB ARETES + 1 (OU NB ARETES)
CCC      IF( NBSOLI .NE. NBARSE+1 .AND. NBSOLI .NE. NBARSE ) THEN
CCCC        LA LIGNE NE PEUT ETRE STRUCTUREE OUVERTE OU FERMEE SANS CYCLE
CCC         IERR = 1
CCC         RETURN
CCC      ENDIF
C
C     VERIFICATION DE LA COHERENCE DES DONNEES: LIGNE SANS NOEUD!
C     RECHERCHE DE CYCLE POUR UN SOMMET (2 TRIANGLES AYANT UN SOMMET COMMUN)
C     CALCUL DU NOMBRE D'ARETES DE CHAQUE SOMMET
C     ATTENTION: INDEX(3,NBSOLI) CONTIENT LE NOMBRE D'ARETES DES SOMMETS
C                PUIS IL SERA UTILISE DIFFEREMMENT ENSUITE
      DO 200 NB=1,NBSOLI
         INDEX(1,NB) = 0
 200  CONTINUE
      DO 210 NARE=1,NBARSE
         DO 205 IND=1,2
C           LE NUMERO DU SOMMET
            N = NARETE(IND,NARE)
            INDEX(1,N) = INDEX(1,N) + 1
            IF( INDEX(1,N) .GT. 2 ) THEN
C              SOMMET COMMUN A AU MOINS 3 ARETES
               IERR = 1
               RETURN
            ENDIF
 205     CONTINUE
 210  CONTINUE
C
C     INITIALISATION DE INDEX DANS SA FORME FINALE
      IERR = 0
      DO 1 NB=1,NBARSE
C        NUMERO DANS L'ORDRE STRUCTURE DE L'ARETE ACTUELLE NB
         INDEX(1,NB)=0
 1    CONTINUE
C
C     PARCOURS DE LA LIGNE NON STRUCTUREE A PARTIR DE LA PREMIERE ARETE
C     =================================================================
      NUMARE = 1
      NB     = 1
C     NUMERO DANS L'ORDRE STRUCTURE DE L'ARETE ACTUELLE 1
      INDEX(1,NUMARE) = NB
C     IND=1 => ARETE VUE DANS LE SENS DIRECT, =2 DANS LE SENS INDIRECT
      IND = 1
      INDEX(3,NUMARE) = IND
C
C     1) EXPLORATION DANS UN SENS (VERS L'EXTREMITE DE L'ARETE DE DEPART)
C        ----------------------------------------------------------------
C     LE NUMERO DE L'EXTREMITE DE L'ARETE
 10   NUM2 = NARETE(INV(IND),NUMARE)
C
C     RECHERCHE DE CE SOMMET NUM2 PARMI LES SOMMETS DES ARETES NON TRAITEES
      DO 20 NARE=1,NBARSE
         IF( INDEX(1,NARE).EQ.0 ) THEN
C           L'ARETE N'A PAS ENCORE ETE TRAITEE ET RETROUVEE COMME SUITE D'UNE AU
            IND  = 1
            NUM1 = NARETE(IND,NARE)
            IF( NUM1 .EQ. NUM2 ) THEN
               IDENT = 1
            ELSE
               CALL XYZIDE(SOMINI(1,NUM1),SOMINI(1,NUM2),IDENT)
            ENDIF
            IF ( IDENT .EQ. 0 ) GOTO 15
C
C           LES SOMMETS NUM1 ET NUM2 SONT IDENTIQUES
            NB     = NB + 1
C           LE NUMERO DE L'ARETE RETROUVEE
            NUMARE = NARE
C           NUMERO NB DANS L'ORDRE STRUCTURE DE L'ARETE ACTUELLE NUMARE
            INDEX(1,NUMARE) = NB
C           IND=1 SI ARETE VUE DANS LE SENS DIRECT, 2 DANS LE SENS INDIRECT
            INDEX(3,NUMARE) = IND
            GO TO 10
C
 15         IND = 2
            NUM1 = NARETE(IND,NARE)
            IF( NUM1 .EQ. NUM2 ) THEN
               IDENT = 1
            ELSE
               CALL XYZIDE(SOMINI(1,NUM1),SOMINI(1,NUM2),IDENT)
            ENDIF
            IF (IDENT .EQ. 0) GOTO 20
C
C           LES SOMMETS NUM1 ET NUM2 SONT IDENTIQUES
            NB = NB + 1
C           LE NUMERO DE L'ARETE RETROUVEE
            NUMARE = NARE
C           NUMERO NB DANS L'ORDRE STRUCTURE DE L'ARETE ACTUELLE NUMARE
            INDEX(1,NUMARE) = NB
C           IND=1 SI ARETE VUE DANS LE SENS DIRECT, 2 DANS LE SENS INDIRECT
            INDEX(3,NUMARE) = IND
            GO TO 10
         END IF
 20   CONTINUE
C
C     L'EXPLORATION EST TERMINEE
      N = NBARSE - NB
C
C     ARRET DE LA RECHERCHE SI TOUTES LES ARETES SONT RETROUVEES
      IF (N.EQ.0) GO TO 100
C
C     LES PREMIERES ARETES DEVIENNENT LES DERNIERES POUR PLACER CELLES QUI VONT
      DO 25 NB=1,NBARSE
         IF( INDEX(1,NB) .NE. 0 ) THEN
C           DECALAGE DE N DES ARETES RETROUVEES
            INDEX(1,NB) = INDEX(1,NB) + N
         ENDIF
 25   CONTINUE
C
C     2) EXPLORATION DANS L'AUTRE SENS (VERS L'ORIGINE DE L'ARETE DE DEPART)
C        INV PERMET D'INDIQUER LE SENS INVERSE DU PARCOURS
C        -------------------------------------------------------------------
C     LE PREMIER SOMMET NUM1 A TRAITER DANS CE SENS EST LE PREMIER DE LA PREMIER
      NUMARE = 1
      NB     = N+1
      IND    = 2
C     LE NUMERO DE L'ORIGINE DE L'ARETE
 30   NUM1 = NARETE(INV(IND),NUMARE)
C
C     RECHERCHE DE CE SOMMET NUM1 PARMI LES SOMMETS DES ARETES NON TRAITEES
      DO 60 NARE=1,NBARSE
         IF(INDEX(1,NARE).EQ.0) THEN
C
            IND  = 2
            NUM2 = NARETE(IND,NARE)
            IF( NUM1 .EQ. NUM2 ) THEN
               IDENT = 1
            ELSE
               CALL XYZIDE(SOMINI(1,NUM1),SOMINI(1,NUM2),IDENT)
            ENDIF
            IF (IDENT.EQ.0) GOTO 40
C
C           LE SOMMET EST RETROUVE
            NB = NB - 1
C           LE NUMERO DE L'ARETE RETROUVEE ET PROCHAINE A TRAITER
            NUMARE = NARE
C           NUMERO NB DANS L'ORDRE STRUCTURE DE L'ARETE ACTUELLE NUMARE
            INDEX(1,NUMARE) = NB
C           IND=1 SI ARETE VUE DANS LE SENS DIRECT, 2 DANS LE SENS INDIRECT
            INDEX(3,NUMARE) = INV(IND)
            GO TO 30
C
 40         IND = 1
            NUM2 = NARETE(IND,NARE)
            IF( NUM1 .EQ. NUM2 ) THEN
               IDENT = 1
            ELSE
               CALL XYZIDE(SOMINI(1,NUM1),SOMINI(1,NUM2),IDENT)
            ENDIF
            IF (IDENT.EQ.0) GOTO 60
C
C           LE SOMMET EST RETROUVE
            NB = NB - 1
C           LE NUMERO DE L'ARETE RETROUVEE
            NUMARE = NARE
C           NUMERO NB DANS L'ORDRE STRUCTURE DE L'ARETE ACTUELLE NUMARE
            INDEX(1,NUMARE) = NB
C           IND=1 SI ARETE VUE DANS LE SENS DIRECT, 2 DANS LE SENS INDIRECT
            INDEX(3,NUMARE) = INV(IND)
            GO TO 30
         END IF
 60   CONTINUE
C     L'EXPLORATION EST TERMINEE
C
C     TEST SUR LE NOMBRE D'ARETES
      IF( NB .NE. 1 ) THEN
C        LIGNE AVEC PLUSIEURS COMPOSANTES
         IERR = 1
         RETURN
      END IF
C
C     ===============================================
C     ICI LA LIGNE EST STRUCTURABLE OU FERMEE
C     LES TABLEAUX FINAUX NE SONT PAS ENCORE MODIFIES
C     ===============================================
C
C     LE NOUVEAU NOMBRE DE SOMMETS DE LA LIGNE STRUCTUREE
 100  NBSOLI = NBARSE + 1
C
C     LE NUMERO ANCIEN => NOUVEAU EST INVERSE
C     =======================================
      DO 110 NARE=1,NBARSE
C        NB NOUVEAU NUMERO STRUCTURE DE L'ARETE ANCIENNE NARE
         NB = INDEX(1,NARE)
C        INVERSION NO STRUCTURE DE L'ARETE => NUMERO ANCIEN DE L'ARETE
         INDEX(2,NB) = NARE
 110  CONTINUE
C
C     BOUCLE SUR LES ARETES DE LA LIGNE DANS L'ORDRE STRUCTURE
      NBEFT = 0
      DO 140 NB=1,NBARSE
C
C        LE NUMERO NARE ANCIEN DE L'ARETE STRUCTUREE NB
         NARE = INDEX(2,NB)
C        LA POSITION DU PREMIER SOMMET STRUCTURE DANS CETTE ARETE
         IND  = INDEX(3,NARE)
C        LE NUMERO ANCIEN DU SOMMET NB STRUCTURE
         NUM1 = NARETE(IND,NARE)
C
         DO 125 J=1,3
C           LES 3 COORDONNEES DU SOMMET NB STRUCTURE
            SOMFIN(J,NB) = SOMINI(J,NUM1)
 125     CONTINUE
C
C        LES TANGENTES EVENTUELLES
         IF( NBEFTG .GT. 0 ) THEN
C           LE NUMERO ANCIEN DE L'EF A TG
            NUEFTG = LPEFAA( NARE )
            IF( NUEFTG .GT. 0 ) THEN
C              EF A TG: NUMERO ANCIEN DES 2 TANGENTES DE L'ARETE NB STRUCTUREE
               NUM1 = NUTINI( IND,      NUEFTG )
               NUM2 = NUTINI( INV(IND), NUEFTG )
C              LE NOUVEAU NUMERO DE L'EF A TG DANS L'ORDRE STRUCTURE
               NBEFT = NBEFT + 1
C              LE POINTEUR SUR L'EF (ARETE) A TG
               LPEFAN(NB) = NBEFT
C              LE NUMERO DES 2 TANGENTES
               NUTFIN(1,NBEFT) = NUM1
               NUTFIN(2,NBEFT) = NUM2
            ELSE
C              LE POINTEUR SUR L'EF (ARETE) A TG
               LPEFAN(NB) = 0
            ENDIF
         ENDIF
C
 140  CONTINUE
C
C     IL RESTE A TRAITER LE DERNIER SOMMET DE LA LIGNE
C     ================================================
C     LE NUMERO ANCIEN DU SECOND SOMMET DE LA DERNIERE ARETE
      NARE = INDEX(2,NBARSE)
      IND  = INV(INDEX(3,NARE))
      NUM1 = NARETE(IND,NARE)
      DO 150 J=1,3
         SOMFIN(J,NBSOLI) = SOMINI(J,NUM1)
 150  CONTINUE
C
C     LA LIGNE EST ELLE FERMEE ?
      CALL XYZIDE( SOMFIN(1,1), SOMFIN(1,NBSOLI), IDENT )
      IF ( IDENT .NE. 0 ) THEN
C        LE DERNIER SOMMET EST EN FAIT LE PREMIER DE LA LIGNE => LIGNE FERMEE
C        LES TABLEAUX ONT ETE RESTRUCTURES POUR FACILITER LES EVENTUELLES RECHER
         NBSOLI = NBSOLI - 1
         IERR   = 2
      ENDIF
      END
