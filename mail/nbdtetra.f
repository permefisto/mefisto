      SUBROUTINE NBDTETRA( MXTETR, NOTETR, NBTETRA, NDTETRA )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    CALCUL DU NOMBRE ACTUEL DE TETRAEDRES STOCKES DANS LE TABLEAU NOTETR
C -----    NOTETR et DU NUMERO DU DERNIER TETRAEDRE ACTIF DANS NOTETR
C
C ENTREES:
C --------
C MXTETR : NOMBRE MAXIMAL DE TETRAEDRES DECLARABLES DANS NOTETR
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C          SOMMET1 NUL => TETRAEDRE NON UTILISE
C SORTIES:
C --------
C NBTETRA : NOMBRE ACTUEL DE TETRAEDRES STOCKES DANS LE TABLEAU NOTETR
C NDTETRA : NUMERO NOTETR DU DERNIER TETRAEDRE ACTIF
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET LJLL UPMC ET ST PIERRE DU PERRAY     MAI 2008
C2345X7..............................................................012
      INTEGER  NOTETR(8,MXTETR)
C     CALCUL DU NOMBRE DE TETRAEDRES ACTIFS
      NBTETRA = 0
      DO 50 NT=1,MXTETR
         IF( NOTETR(1,NT) .GT. 0 ) THEN
            NBTETRA = NBTETRA + 1
            NDTETRA = NT
         ENDIF
 50   CONTINUE
C
      RETURN
      END
