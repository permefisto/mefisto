      SUBROUTINE EC2D1AT( XYZSOM, NT1, NAR1, N1TRIA, NBTRIA1, NBTRIA2,
     %                    NOTRIA, NT2, NAR2, MODIF,
     %                    MXPILE, LHPILE, LAPILE )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    RETROUVER PARMI LES TRIANGLES NBTRIA1 A NBTRIA2 L'ARETE
C -----    NAR1 DU TRIANGLE NT1 DANS NOTRIA
C          SI NAR2 DE NT2 EST L'ARETE COMMUNE ALORS
C          ECHANGER CETTE DIAGONALE POUR L'AUTRE SI ELLE MAXIMISE
C          LE MINIMUM DES QUALITES DES 2 COUPLES DE TRIANGLES
C ENTREES:
C --------
C NT1    : NUMERO NOTRIA DU TRIANGLE D'ARETE A RETROUVER
C NAR1   : NUMERO DE 1 A 3 DE L'ARETE DU TRIANGLE NT1 A TRAITER
C N1TRIA : NOMBRE D'ENTIERS DU PREMIER INDICE DE NOTRIA
C NBTRIA1: NUMER0 DU PREMIER TRIANGLE POSSIBLE
C NBTRIA2: NUMER0 DU DERNIER TRIANGLE POSSIBLE
C N1TRIA : NOMBRE D'ENTIERS DU PREMIER INDICE DE NOTRIA
C NOTRIA : NUMERO XYZSOM DES 3 SOMMETS ET 0 DES TRIANGLES
C MXPILE : NOMBRE MAXIMALE D'ELEMENTS DE LA PILE (TRIANGLE,ARETE)

C SORTIES:
C --------
C NT2    : >0 NUMERO NOTRIA     D'ARETE NAR1
C          =0 SI AUCUN TRIANGLE D'ARETE NAR1
C NAR2   : >0 NUMERO DE 1 A 3 DANS LE TRIANGLE NT2 DE L'ARETE NAR1 DE NT1
C          =0 SI AUCUN TRIANGLE D'ARETE NAR1
C MODIF  : =1  SI LA DIAGONALE DE NT1+NT2   A     CHANGE
C          =0  SI LA DIAGONALE DE NT1+NT2 N'A PAS CHANGE
C          =-1 SI SATURATION du TABLEAU LA PILE. AUGMENTER MXPILE
C LHPILE : HAUTEUR DE LA PILE DES (NO TRIANGLE, NO ARETE) AVANT ET APRES
C          DES ARETES A ANANLYSER POUR ECHANGE DES DIAGONALES
C LAPILE : LAPILE(1,N)=NUMERO NOTRIA DU TRIANGLE
C          LAPILE(2,N)=NUMERO DE L'ARETE DU TRIANGLE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR: ALAIN PERRONNET LJLL UPMC PARIS&St PIERRE du PERRAY Avril 2017
C....................................................................012
      REAL     XYZSOM(3,*)
      INTEGER  NOTRIA(N1TRIA,*), NOSOTR(3), LAPILE(2,MXPILE)

C     NO DES 2 SOMMETS DE L'ARETE NAR1 DU TRIANGLE NT1
      IF( NAR1 .EQ. 3 ) THEN
         NARS = 1
      ELSE
         NARS = NAR1 + 1
      ENDIF
      NS1 = NOTRIA( NAR1, NT1 )
      NS2 = NOTRIA( NARS, NT1 )

C     RECHERCHE D'UN EVENTUEL TRIANGLE ADJACENT PAR CETTE ARETE
      DO NT2 = NBTRIA2, NBTRIA1, -1

         IF( NT2 .NE. NT1 ) THEN

            DO NAR2=1,3

C              ARETE NAR2 DU TRIANGLE NT2 EST ELLE L'ARETE NS1 NS2 DE NT1?
               NSS1 = NOTRIA( NAR2, NT2 )
               IF( NAR2 .EQ. 3 ) THEN
                  NARS = 1
               ELSE
                  NARS = NAR2 + 1
               ENDIF
               NSS2 = NOTRIA( NARS, NT2 )

               IF( ( NS2 .EQ. NSS1 .AND. NS1 .EQ. NSS2 ) .OR.
     %             ( NS1 .EQ. NSS1 .AND. NS2 .EQ. NSS2 ) ) GOTO 10

            ENDDO

         ENDIF

      ENDDO

C     PAS DE TRIAGLE ADJACENT A L'ARETE NAR1 DE NT1
      NT2  = 0
      NAR2 = 0
      GOTO 9000

C     ICI, L'ARETE NAR1 DE NT1 EST L'ARETE NAR2 DE NT2

C     LE 3-EME SOMMET DU TRIANGLE NT1
C     -------------------------------
 10   IF( NAR1 .EQ. 1 ) THEN
         NS3 = 3
      ELSE
         NS3 = NAR1 - 1
      ENDIF
      NS3 = NOTRIA( NS3, NT1 )
      
C     LE 3-EME SOMMET DU TRIANGLE NT2
C     -------------------------------
      IF( NAR2 .EQ. 1 ) THEN
         NS4 = 3
      ELSE
         NS4 = NAR2 - 1
      ENDIF
      NS4 = NOTRIA( NS4, NT2 )

C     LES 2 TRIANGLES FORMENT ILS UN QUADRANGLE 1423 CONVEXE?
C     -------------------------------------------------------
      CALL QUADCXR( XYZSOM(1,NS1), XYZSOM(1,NS4),
     %              XYZSOM(1,NS2), XYZSOM(1,NS3),  NONOUI )
      IF( NONOUI .EQ. 0 ) GOTO 9000


C     OUI: QUALITE DES 2 CHOIX POSSIBLES DE TRIANGULATIONS
C     ----------------------------------------------------
C     TRIANGLE 123
      CALL QUATRI( NOTRIA(1,NT1), XYZSOM, Q12 )

C     TRIANGLE 142
      CALL QUATRI( NOTRIA(1,NT2), XYZSOM, Q   )
      Q12 = MIN( Q12, Q )

C     TRIANGLE 143
      NOSOTR(1) = NS1
      NOSOTR(2) = NS4
      NOSOTR(3) = NS3
      CALL QUATRI( NOSOTR, XYZSOM, Q )
C     COMPARAISON DES QUALITES  MIN( Q123, Q142 ) ET Q143
      IF( Q .LT. Q12 ) GOTO 9000

C     TRIANGLE 234
      NOSOTR(1) = NS2
      NOSOTR(2) = NS3
      NOSOTR(3) = NS4
      CALL QUATRI( NOSOTR, XYZSOM, Q )
C     COMPARAISON DES QUALITES  MIN( Q123, Q142 ) ET Q234
      IF( Q .LT. Q12 ) GOTO 9000

C     MIN( Q143, Q234 ) < MIN( Q123, Q142 )
C     ECHANGE EFFECTIF DE LA DIAGONALE DES 2 TRIANGLES NT1 NT2
C     --------------------------------------------------------
C     ICI NT1 NT2 SONT REMPLACES SUR EUX MEMES
      NOTRIA( 1, NT1 ) = NS1
      NOTRIA( 2, NT1 ) = NS4
      NOTRIA( 3, NT1 ) = NS3
      NOTRIA( 4, NT1 ) = 0

      NOTRIA( 1, NT2 ) = NS2
      NOTRIA( 2, NT2 ) = NS3
      NOTRIA( 3, NT2 ) = NS4
      NOTRIA( 4, NT2 ) = 0

      IF( LHPILE+4 .LE. MXPILE ) THEN

C        AINSI PAS DE RISQUE DE DEBORDEMENT DE LA PILE
C        AJOUT DES 4 ARETES FRONTALIERES SUSCEPTIBLES DE FAIRE
C        UN ECHANGE DES DIAGONALES DES TRIANGLES NT1+NT2
         LHPILE = LHPILE + 1
         LAPILE( 1, LHPILE ) = NT1
         LAPILE( 2, LHPILE ) = 1

         LHPILE = LHPILE + 1
         LAPILE( 1, LHPILE ) = NT1
         LAPILE( 2, LHPILE ) = 3

         LHPILE = LHPILE + 1
         LAPILE( 1, LHPILE ) = NT2
         LAPILE( 2, LHPILE ) = 1

         LHPILE = LHPILE + 1
         LAPILE( 1, LHPILE ) = NT2
         LAPILE( 2, LHPILE ) = 3

      ELSE

         PRINT*,'ec2d1at: SATURATION de LAPILE MXPILE=',MXPILE
         MODIF = -1
         GOTO 9999

      ENDIF

      MODIF = 1
      GOTO 9999


C     PAS D'ECHANGE DES 2 DIAGONALES
C     ------------------------------
 9000 MODIF = 0

 9999 RETURN
      END
