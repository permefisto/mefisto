      SUBROUTINE TEQMTYFA( MXSOMM, PTXYZD, NPSOFR, NTEQM,
     %                     MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %                     IVOLTE, NVOLTE,
     %                     MXFETO, N1FEOC, NFETOI,
     %                     MXTEAU, NBTEAU, NOTEAU, MODIFT )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT:     RECHERCHE D'UNE ARETE TRES PETITE PAR RAPPORT AUX 5 AUTRES
C ----     TENTATIVE DE SUPPRIMER TOUS LES TETRAEDRES DE CETTE ARETE
C          EN IDENTIFIANT LES 2 SOMMETS DE L'ARETE

C ENTREES:
C --------
C MXSOMM : NOMBRE DE SOMMETS DECLARABLES DANS PTXYZD
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NPSOFR : =  0 SI POINT AJOUTE NON SUR LA SURFACE DE L'OBJET
C          =  1 SI LE POINT EST AJOUTE SUR UNE ARETE DE LA SURFACE
C          =  2 SI LE POINT EST AJOUTE COMME BARYCENTRE SUR LA SURFACE
C          =  1 000 000  + NUMERO DU  POINT INTERNE UTILISATEUR
C          = (1 000 000) * (NO SURFACE + 1) + NUMERO DU SOMMET DANS
C              LA NUMEROTATION DES SOMMETS DE LA SURFACE
C          = -4 SI LE POINT EST SOMMET NON TROP PROCHE PT OU FACE
C          = -1 SI LE POINT EST SOMMET TROP PROCHE PT OU FACE
C          = -3 SI LE POINT EST SOMMET REFUSE DANS LA TETRAEDRISATION
C          = -NPSOFR(I) SI POINT DEPLACE SUR LA SURFACE
C            DANS LEUR SURFACE FERMEE INITIALE ou NO DE POINT INTERNE
C NTEQM  : NO NOTETR DU TETRAEDRE DE QUALITE MEDIOCRE A TRAITER
C MXTETR : NOMBRE DE TETRAEDRES DECLARABLES DANS NOTETR
C N1TEVI : NO DU PREMIER TETRAEDRE VIDE DANS NOTETR
C N1TETS : N1TETS(NS) NUMERO D'UN TETRAEDRE AYANT POUR SOMMET NS
C NOTETR : LISTE DES TETRAEDRES
C          SOMMET1,    SOMMET2,    SOMMET3,    SOMMET4,
C          TETRAEDRE1, TETRAEDRE2, TETRAEDRE3, TETRAEDRE4
C          DE L'AUTRE COTE DE LA FACE
C          1: 123      2: 234      3: 341      4: 412
C NUDTETR: NO NOTETR DU DERNIER TETRAEDRE ACTIF
C IVOLTE : =0 TABLEAU NVOLTE NON PRESENT
C          =1 TABLEAU NVOLTE     PRESENT
C MXTEAU : NOMBRE MAXIMAL DE TETRAEDRES MODIFIABLES

C SORTIES:
C --------
C NVOLTE : NUMERO DE VOLUME (1 a NBVOPA) DE CHAQUE TETRAEDRE
C          -1 SI VOLUME INCONNU (Exemple: les TETRAEDRES VIDES DE NOTETR)
C NUDTETR: NO NOTETR DU DERNIER TETRAEDRE ACTIF
C NBTEAU : NOMBRE DE SOUS-TETRAEDRES CREES DANS NOTEAU
C          =0 NE SIGNIFIE PAS QU'AUCUN TETRAEDRE N'AIT ETE MODIFIE
C NOTEAU : NO NOTETR DES NBTEAU SOUS-TETRAEDRES CREES
C MODIFT : =0 PAS DE MODIFICATION DES TETRAEDRES NTEQM ...
C          =1        MODIFICATION DES TETRAEDRES. DECOMPOSITION FAITE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET  Veulettes sur mer               janvier 2019
C2345X7..............................................................012
      COMMON / TRTETR / STOPTE, TRACTE
      LOGICAL           STOPTE, TRACTE
      LOGICAL                          TRACTE0
      DOUBLE PRECISION  PTXYZD(4,MXSOMM)
      INTEGER           NOTETR(8,MXTETR), N1TETS(MXSOMM),
     %                  NPSOFR(MXSOMM), NVOLTE(*), NOTEAU(MXTEAU),
     %                  NOTEQM(8)

      CHARACTER*120     KTITRE
      DOUBLE PRECISION  ARMIN, LONARE(6), D

      INTEGER           NOSOARTE(2,6)
      DATA              NOSOARTE / 1,2, 2,3, 3,1, 4,1, 4,2, 4,3 /

      MODIFT  = 0
      TRACTE0 = TRACTE
      NBTEAU  = 0

C     RECHERCHE D'UNE ARETE TRES PETITE PAR RAPPORT AUX 5 AUTRES
C     TENTATIVE DE SUPPRIMER TOUS LES TETRAEDRES DE CETTE ARETE
C     EN IDENTIFIANT LES 2 SOMMETS DE L'ARETE
C     ----------------------------------------------------------
      ARMIN  = 1D100
      NARMIN = 0
      DO NA = 1, 6

C        NUMERO DES 2 SOMMETS DE L'ARETE NA
         NS1 = NOTETR( NOSOARTE(1,NA), NTEQM )
         NS2 = NOTETR( NOSOARTE(2,NA), NTEQM )

         D = SQRT( ( PTXYZD(1,NS2) - PTXYZD(1,NS1) ) ** 2
     +           + ( PTXYZD(2,NS2) - PTXYZD(2,NS1) ) ** 2
     +           + ( PTXYZD(3,NS2) - PTXYZD(3,NS1) ) ** 2 )
         LONARE( NA ) = D

         IF( D .LT. ARMIN ) THEN
            ARMIN  = D
            NARMIN = NA
         ENDIF

      ENDDO

      DO NA = 1, 6
         IF( NA .NE. NARMIN ) THEN
            IF( LONARE( NA ) .LT. 8 * ARMIN ) THEN
C              L'ARETE NA EST TROP COURTE PAR RAPPORT A ARMIN
               GOTO 9900
            ENDIF
         ENDIF
      ENDDO

C     L'ARETE NARMIN EST PETITE DEVANT LES 5 AUTRES ARETES
      NSNOFR = NOTETR( NOSOARTE(1,NARMIN), NTEQM ) 
      NSDLFR = NOTETR( NOSOARTE(2,NARMIN), NTEQM ) 

      IF( NPSOFR(NSNOFR) .GT. 0 ) THEN
C        NSNOFR EST UN SOMMET FRONTALIER => PERMUTATION
         NA     = NSNOFR
         NSNOFR = NSDLFR
         NSDLFR = NA
      ENDIF

      IF( NPSOFR(NSNOFR) .GT. 0 ) THEN
C        ARETE FRONTIERE. ELLE NE PEUT ETRE DETRUITE
         GOTO 9900
      ENDIF

ccc      IF( NPSOFR(NSNOFR) .EQ. -4 .AND. NPSOFR(NSDLFR) .EQ. -4 ) THEN
cccC        2 SOMMETS DES OT NON IDENTIFIABLES
ccc         GOTO 9900
ccc      ENDIF

C     LES SOMMETS NSNOFR et NSDLFR SONT SUFFISAMMENT PROCHES
C     SIMULATION DU DEPLACEMENT DU SOMMET NSNOFR NON FRONTALIER
C     AU SOMMET NSDLFR FRONTALIER ou NON
      CALL SISTST( NSNOFR, NSDLFR, MXSOMM, PTXYZD, NPSOFR,
     %             MXTETR, N1TETS, NOTETR,
     %             IVOLTE, NVOLTE,
     %             MXFETO, N1FEOC, NFETOI,
     %             MXTEAU, NBTEAU, NOTEAU, IERR )

      IF( IERR .NE. 0 ) THEN
C        IDENTIFICATION REFUSEE
         GOTO 9900
      ENDIF

C     SAUVEGARDE DU TETRAEDRE NTEQM POUR AFFICHAGE FINAL DE SA MODIFICATION
      DO L = 1, 8
         NOTEQM( L ) = NOTETR( L, NTEQM )
      ENDDO

C     DEPLACEMENT EFFECTIF DU SOMMET NSNOFR NON FRONTALIER
C     AU SOMMET NSDLFR FRONTALIER (ou NON)
      CALL IDSTST( NSNOFR, NSDLFR, MXSOMM, PTXYZD, NPSOFR,
     %             MXTETR, N1TEVI, N1TETS, NOTETR, NUDTETR,
     %             IVOLTE, NVOLTE,
     %             MXFETO, N1FEOC, NFETOI,
     %             MXTEAU, NBTEAU, NOTEAU, QUAMIN, IERR )
C     NBTEAU: NB DE  TETRAEDRES DE NOTETR CONTENANT LE SOMMET NSDLFR
C     NOTEAU: NO DES TETRAEDRES DE NOTETR CONTENANT LE SOMMET NSDLFR

      IF( IERR .EQ. 0 ) THEN

C        NTEQM A ETE SUPPRIME ET CEUX D'ARETE NSNOFR-NSDLFR
C        --------------------------------------------------
         MODIFT = 1
         GOTO 9999

      ENDIF


C     SORTIE SANS MODIFICATION DU TETRAEDRE ET DE SES VOISINS
C     -------------------------------------------------------
 9900 NBTEAU = 0
      MODIFT = 0


 9999 IF( MODIFT .NE. 0 ) THEN
         PRINT *,'teqmtyfa: TETRAEDRE',NTEQM,
     %       ' NOTETR:',NOTEQM,' V=',VTEQM,' Q=',QTEQM,' MODIFIE et St',
     %         NSNOFR, ' ->',NSDLFR,' QuaMin=',QUAMIN

C        TRACE DES SOUS-TETRAEDRES GENERES
         KTITRE='teqmtyfa:            TETRAEDRES apres SOMMET          -
     %> Sommet          '
         WRITE( KTITRE(11:19), '(I9)' ) NBTEAU
         WRITE( KTITRE(46:54), '(I9)' ) NSNOFR
         WRITE( KTITRE(65:73), '(I9)' ) NSDLFR
         CALL SANSDBL( KTITRE, NBC )
         PRINT*, KTITRE(1:NBC)
         CALL TRFETO13( KTITRE(1:NBC), PTXYZD, NBTEAU, NOTEAU, NOTETR )

      ENDIF

      TRACTE = TRACTE0
      RETURN
      END
