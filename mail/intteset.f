      SUBROUTINE INTTESET( PTXYZD, NSTE1, NSTE2, NSTE3, NSTE4,
     %                     N1FEOC, NFETOI, LINTER )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    LE TETRAEDRE DE SOMMETS PTXYZD NSTE1 NSTE2 NSTE3,NSTE4
C -----    INTERSECTE T IL UNE DES FACES DE L'ETOILE NFETOI 
C          DE PREMIERE FACE N1FEOC?

C ENTREES:
C --------
C PTXYZD : TABLEAU DES COORDONNEES DES POINTS
C          PAR POINT : X  Y  Z DISTANCE_SOUHAITEE
C NSTi   : LES 4 NUMEROS PTXYZD DES SOMMETS DU TETRAEDRE

C N1FEOC : POINTEUR SUR LA PREMIERE FACE DE L'ETOILE
C          CHAINAGE SUIVANT DANS NFETOI(5,*)
C NFETOI : LES FACES TRIANGULAIRES DE L'ETOILE
C          1: NUMERO DU TETRAEDRE DANS NOTETR OPPOSE A CETTE FACE
C          2: NUMERO PTXYZD DU SOMMET 1 DE LA FACE
C          3: NUMERO PTXYZD DU SOMMET 2 DE LA FACE
C          4: NUMERO PTXYZD DU SOMMET 3 DE LA FACE
C          S1S2 x S1S3 VERS L'INTERIEUR DE L'ETOILE

C SORTIE :
C --------
C LINTER : 0 PAS D'INTERSECTION
C          1 SI UNE ARETE D'UNE FACE NFETOI INTERSECTE UNE FACE DU TETRAEDRE
C          2 SI UNE FACE NFETOI EST INTERSECTEE PAR UNE ARETE DU TETRAEDRE
C          3 SI UN DES SOMMETS D'UNE FACE NFETOI EST STRICTEMENT INTERNE
C            AU TETRAEDRE
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : ALAIN PERRONNET   Saint PIERRE du PERRAY        Decembre 2019
C2345X7..............................................................012
      DOUBLE PRECISION  PTXYZD(4,*)
      INTEGER           NFETOI(5,*)

      NF = N1FEOC
 10   IF( NF .GT. 0 ) THEN

C        INTERSECTION FACE TRIANGULAIRE NF-TETRAEDRE NSTE 1234 ?
         CALL INTRITET( PTXYZD(1,NFETOI(2,NF)),
     %                  PTXYZD(1,NFETOI(3,NF)),
     %                  PTXYZD(1,NFETOI(4,NF)),
     %                  PTXYZD(1,NSTE1), PTXYZD(1,NSTE2),
     %                  PTXYZD(1,NSTE3), PTXYZD(1,NSTE4),
     %                  LINTER )
         IF( LINTER  .NE. 0 ) THEN
C           OUI: UN POINT D'INTERSECTION
            GOTO 9999
         ENDIF


C        LA FACE SUIVANTE DE NF
         NF = NFETOI(5,NF)
         GOTO 10

      ENDIF

 9999 RETURN
      END
