        SUBROUTINE SUEX15( NTLXSU , LADEFI , RADEFI ,
     %                     NTFASU , MNFASU , NTSOFA , MNSOFA , IERR )
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C BUT :    GENERER LE MAILLAGE D'UNE SURFACE PAR ROTATIONS D'UNE LIGNE
c -----
C ENTREES:
C --------
C NTLXSU : NUMERO DU TABLEAU TS DU LEXIQUE DE LA SURFACE A CREER
C LADEFI : TABLEAU ENTIER DE DEFINITION DE LA SURFACE
C RADEFI : TABLEAU REEL   DE DEFINITION DE LA SURFACE
C          CF '~/td/d/a_surface__definition'
C
C SORTIES:
C --------
C NTFASU : NUMERO      DU TMS 'NSEF' DES NUMEROS DES FACES
C MNFASU : ADRESSE MCN DU TMS 'NSEF' DES NUMEROS DES FACES
C          CF '~/td/d/nsef'
C NTSOFA : NUMERO      DU TMS 'XYZSOMMET' DE LA SURFACE
C MNSOFA : ADRESSE MCN DU TMS 'XYZSOMMET' DE LA SURFACE
C          CF '~/td/d/xyzsommet'
C IERR   : = 0 SI PAS D'ERREUR
C          <>0 SI ERREUR
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C AUTEUR : PERRONNET ALAIN  ANALYSE NUMERIQUE UPMC PARIS   NOVEMBRE 1996
C.......................................................................
      IMPLICIT INTEGER (W)
      include"./incl/gsmenu.inc"
      COMMON / UNITES / LECTEU , IMPRIM , INTERA , NUNITE(29)
      include"./incl/ntmnlt.inc"
      include"./incl/a___xyzsommet.inc"
      include"./incl/a___nsef.inc"
      include"./incl/a_surface__definition.inc"
      include"./incl/pp.inc"
      COMMON            MCN(MOTMCN)
      REAL              RMCN(1)
      EQUIVALENCE       (MCN(1),RMCN(1))
      INTEGER           LADEFI(0:*)
      REAL              RADEFI(0:*)
      LOGICAL           FERME,BASFER
C
      IERR   = 0
      MNSOAX = 0
      MNANGL = 0
      MNNTGL = 0
C
C     RESTAURATION DU MAILLAGE DE LA LIGNE INITIALE
      CALL LXNLOU( NTLIGN , LADEFI(WULIAS) , NTLIAS , MN )
      IF( NTLIAS .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  'LIGNE INCONNUE'
         CALL LEREUR
         IERR = 2
         RETURN
      ENDIF
C     RESTAURATION DES TABLEAUX XYZSOMMET ET NSEF
      CALL LXTSOU( NTLIAS , 'XYZSOMMET' , NTSOLI , MNSOLI )
      IF( NTSOLI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGNE SANS SOMMETS'
         CALL LEREUR
         IERR = 3
         RETURN
      ENDIF
      CALL LXTSOU( NTLIAS , 'NSEF' , NTARLI , MNARLI )
      IF( NTARLI .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'LIGNE SANS NSEF'
         CALL LEREUR
         IERR = 4
         RETURN
      ENDIF
C
C     CONTROLE DU TABLEAU DES ANGLES
      NBANAS = LADEFI(WBANAS)
      NTYPAS = LADEFI(WTYPAS)
      IF( NTYPAS .NE. 0 ) THEN
         ANGLAS = RADEFI( WNGLAS )
         DO 10 I=1,NBANAS-1
            A = RADEFI(WNGLAS+I)
            IF( A * ANGLAS .LE. 0 ) THEN
               NBLGRC(NRERR) = 1
               KERR(1) = '2 ANGLES CONSECUTIFS DE SIGNES OPPOSES'
               CALL LEREUR
               IERR = 5
               RETURN
            ENDIF
            ANGLAS = ANGLAS + A
 10      CONTINUE
      ELSE
         ANGLAS = RADEFI( WNMXAS )
      ENDIF
      IF( ABS(ANGLAS) .GT. 360. ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) = 'SOMME DES ANGLES AU DELA DE 360 DEGRES'
         CALL LEREUR
         IERR = 6
         RETURN
      ENDIF
      FERME = ABS(ANGLAS) .EQ. 360.
C
C     RESTAURATION DES 2 POINTS DE L'AXE
      CALL LXNLOU( NTPOIN , LADEFI(WUP1AS) , NTP1 , MN )
      IF( NTP1 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  'POINT 1 DE L''AXE INCONNU'
         CALL LEREUR
         IERR = 5
         RETURN
      ENDIF
      CALL LXTSOU( NTP1 , 'XYZSOMMET' , NTP1AS , MNP1AS )
      IF( NTP1AS .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  'POINT 1 DE L''AXE INEXISTANT'
         CALL LEREUR
         IERR = 6
         RETURN
      ENDIF
C
      CALL LXNLOU( NTPOIN , LADEFI(WUP2AS) , NTP2 , MN )
      IF( NTP2 .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  'POINT 2 DE L''AXE INCONNU'
         CALL LEREUR
         IERR = 7
         RETURN
      ENDIF
      CALL LXTSOU( NTP2 , 'XYZSOMMET' , NTP2AS , MNP2AS )
      IF( NTP2AS .LE. 0 ) THEN
         NBLGRC(NRERR) = 1
         KERR(1) =  ' POINT 2 DE L''AXE INEXISTANT'
         CALL LEREUR
         IERR = 8
         RETURN
      ENDIF
C
C     LE TYPE DE MAILLAGE DE LA LIGNE
      NUTYML = MCN( MNARLI + WUTYMA )
C     LE NOMBRE D'ARETES DE LA LIGNE
      IF( NUTYML .EQ. 0 ) THEN
         NBARLI = MCN( MNARLI + WBEFOB )
      ELSE IF( NUTYML .EQ. 2 ) THEN
         NBARLI = MCN( MNARLI + WBARSE )
      ELSE
         NBLGRC(NRERR) = 1
         KERR(1) = 'TYPE INCORRECT DU MAILLAGE DE LA LIGNE'
         CALL LEREUR
         IERR = 9
         RETURN
      ENDIF
C
C     LE NOMBRE DE SOMMETS DE LA LIGNE
      NBSOLI = MCN( MNSOLI + WNBSOM )
      IF( NBSOLI .EQ. NBARLI ) THEN
C        LIGNE BASE DE LA ROTATION EST FERMEE
         BASFER = .TRUE.
      ELSE
C        LIGNE OUVERTE
         BASFER = .FALSE.
      ENDIF
C
C     LE NOMBRE DE TANGENTES DE LA LIGNE
      NBTGSL = MCN( MNSOLI + WNBTGS )
C
C     RECHERCHE DU NOMBRE DE SOMMETS DE CETTE LIGNE PLACES SUR L'AXE
      MNSOAX = 0
      CALL TNMCDC( 'ENTIER' , NBSOLI , MNSOAX )
      CALL ROTAN1( RMCN(MNP1AS+WYZSOM) , RMCN(MNP2AS+WYZSOM) ,
     %             NBSOLI , RMCN(MNSOLI+WYZSOM) ,
     %             NBSOAX , MCN(MNSOAX) , IERR )
      IF( IERR .NE. 0 ) RETURN
C
C     LES EVENTUELLES TANGENTES DE LA LIGNE
      CALL TGARLI( MNARLI, MNSOLI,
     %             NBARTG, MNXTGL, MNNTGL, MNCGEF )
C
C     CALCUL DU NOMBRE DE SOMMETS DU MAILLAGE DE LA SURFACE
      NBSOSU = NBSOLI + (NBSOLI-NBSOAX) * NBANAS
      IF( FERME ) THEN
         NBSOSU = NBSOSU - NBSOLI + NBSOAX
      ENDIF
C
C     CALCUL DU NOMBRE DE TANGENTES DE LA SURFACE
      NBTGEF = 8
      NBTGSU = NBTGEF / 2 * (NBARLI+NBARTG) * NBANAS
C
C     DECLARATION DES SOMMETS DE LA SURFACE CREEE
C     CONSTRUCTION DU TABLEAU 'XYZSOMMET' DE CETTE SURFACE
      CALL LXTNDC( NTLXSU , 'XYZSOMMET' , 'MOTS' ,
     %             WYZSOM+3*(NBSOSU+NBTGSU) )
      CALL LXTSOU( NTLXSU , 'XYZSOMMET' ,  NTSOFA , MNSOFA )
      MNXYZS = MNSOFA + WYZSOM
      MNXTGS = MNXYZS + 3 * NBSOSU
C
C     LES EVENTUELLES TANGENTES DE LA LIGNE
C
C     CONSTRUCTION DU TABLEAU 'NSEF' DE CETTE SURFACE
C     CALCUL DE LA TAILLE DU TABLEAU
      NBEFOB = NBARLI * NBANAS
      IF( FERME .OR. BASFER .OR. NBSOAX .GT. 0 ) THEN
C        SURFACE MAILLE NON STRUCTUREE
         NUTYMS = 0
         N      = WUSOEF + 4 * NBEFOB
      ELSE
C        QUADRANGLE STRUCTURE
         NUTYMS = 4
         N      = WBARYQ + 1
      ENDIF
C
C     LA PART DUE AUX TANGENTES
      NBEFTG = NBEFOB
      MOTS   = N + NBEFOB + NBEFTG * ( 1 + NBTGEF )
C
C     DECLARATION DES NO SOMMET DE LA SURFACE
      CALL LXTNDC( NTLXSU , 'NSEF' , 'ENTIER', MOTS )
      CALL LXTSOU( NTLXSU , 'NSEF' ,  NTFASU , MNFASU )
      MNEFAP = MNFASU + N
      MNNTGS = MNEFAP + NBEFOB + NBEFTG
C
C     CALCUL DES ANGLES DE ROTATION DANS TOUS LES CAS
      CALL TNMCDC( 'REEL' , NBANAS , MNANGL )
      DO 20 I=1,NBANAS
         IF( NTYPAS .EQ. 0 ) THEN
            RMCN(MNANGL-1+I) = ANGLAS / NBANAS
         ELSE
            RMCN(MNANGL-1+I) = RADEFI(WNGLAS-1+I)
         ENDIF
 20   CONTINUE
C
C     CALCUL DES COORDONNEES DES SOMMETS ET DES FACES DE LA SURFACE
      CALL ROTAN2( NBANAS, RMCN(MNANGL),
     %             RMCN(MNP1AS+WYZSOM), RMCN(MNP2AS+WYZSOM),
     %             FERME,  NBSOLI, RMCN(MNSOLI+WYZSOM),
     %             NBSOAX, MCN(MNSOAX),
     %             NUTYML, NBARLI, MCN(MNARLI+WUSOEF),
     %             NBTGSL, RMCN(MNXTGL), MCN(MNNTGL),
     %             NBSOSU,RMCN(MNXYZS),NUTYMS,MCN(MNFASU+WUSOEF),
     %             NBTGSU,RMCN(MNXTGS),RMCN(MNNTGS),
     %             IERR )
      IF( IERR .GT. 0 ) GOTO 9900
      IERR = 0
C
C     MISE A JOUR DU TABLEAU 'NSEF' DE CETTE SURFACE
C     TYPE DE L'OBJET : SURFACE
      MCN( MNFASU + WBEFOB ) = NBEFOB
      IF (NUTYMS .EQ. 0) THEN
         MCN( MNFASU + WUTYOB ) = 3
         MCN( MNFASU + WUTYMA ) = 0
      ELSE
         MCN( MNFASU + WUTYOB ) = 3
         MCN( MNFASU + WUTYMA ) = 4
         MCN( MNFASU + WBARXQ ) = NBARLI
         MCN( MNFASU + WBARYQ ) = NBANAS
      ENDIF
C     LE NOMBRE DE SOMMETS PAR EF
      MCN( MNFASU + WBSOEF ) = 4
C     LE TYPE INCONNU DE FERMETURE DU MAILLAGE
      MCN( MNFASU + WUTFMA ) = -1
C     LE NOMBRE DE TANGENTES STOCKEES PAR EF : SURFACE C1
      MCN( MNFASU + WBTGEF ) = NBTGEF
      MCN( MNFASU + WBEFAP ) = NBEFOB
      MCN( MNFASU + WBEFTG ) = NBEFTG
C
C     LE NUMERO POINTEUR SUR LES EF A TG
      DO 50 N=1,NBEFOB
         MCN(MNEFAP-1+N) = N
 50   CONTINUE
C
C     LE NUMERO GEOMETRIQUE DES EF A TG : 0 P3 STANDARD
      DO 60 N=1,NBEFTG
         MCN(MNEFAP+NBEFOB-1+N) = 0
 60   CONTINUE
C
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNFASU) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNFASU + MOTVAR(6) ) = NONMTD( '~>>>NSEF' )
C
C     MISE A JOUR DU TABLEAU 'XYZSOMMET' DE CETTE SURFACE
C     IL EST EVENTUELLEMENT RACOURCI
      CALL TAMSRA( NTSOFA , WYZSOM + 3*(NBSOSU+NBTGSU) )
C     LE NOMBRE DE SOMMETS EST MIS A JOUR
      MCN( MNSOFA + WNBSOM ) = NBSOSU
C     LE NOMBRE DE TANGENTES STOCKEES DE CETTE SURFACE
      MCN( MNSOFA + WNBTGS ) = NBTGSU
C     LE NOMBRE DE COORDONNEES PAR SOMMET
      MCN( MNSOFA + WBCOOR ) = 3
C
C     LA DATE DE CREATION
      CALL ECDATE( MCN(MNSOFA) )
C     LE NUMERO DU TABLEAU DESCRIPTEUR
      MCN( MNSOFA + MOTVAR(6) ) = NONMTD( '~>>>XYZSOMMET' )
C
C     DESTRUCTION DES TABLEAUX INUTILES
 9900 IF( MNSOAX .GT. 0 ) CALL TNMCDS( 'ENTIER' , NBSOLI , MNSOAX )
      IF( MNANGL .GT. 0 ) CALL TNMCDS( 'REEL'   , NBANAS , MNANGL )
      IF( MNNTGL .GT. 0 ) CALL TNMCDS( 'ENTIER', 3*NBARLI, MNNTGL )
      END
